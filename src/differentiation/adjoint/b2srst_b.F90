      IF (branch .EQ. 0) THEN
        CALL POPREAL8(sr%she(icv, 0), r8/8)
        t0b = t0b - switch%b2srst_rf1*srb%she(icv, 0)
        srb%she(icv, 0) = 0.D0
      ELSE
        CALL POPREAL8(sr%she(icv, 0), r8/8)
        t0b = t0b + (switch%b2srst_rf1+1.0_R8)*srb%she(icv, 0)
