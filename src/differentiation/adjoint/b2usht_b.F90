        t1b = t1b - aab(mpg%cvnvp(icv1, 1))
      END IF
      flo0b(ifc, 0) = flo0b(ifc, 0) + 0.5_R8*t2b - 0.5_R8*t1b
      con0b(ifc, 0, 0) = con0b(ifc, 0, 0) - t2b - t1b
    END DO
    CALL POPREAL8ARRAY(resht, r8*ncv/8)
!$BWD-OF II-LOOP 
    DO icv=1,ncv
      ireg = mpg%cvreg(icv)
      t0 = 1.0e60_R8
      IF (solveee(ireg)) THEN
        IF (t0 .GT. te(icv)) THEN
          t0 = te(icv)
          CALL PUSHCONTROL2B(2)
        ELSE
          CALL PUSHCONTROL2B(1)
          t0 = t0
        END IF
      ELSE
        CALL PUSHCONTROL2B(0)
      END IF
      IF (solveei(ireg)) THEN
