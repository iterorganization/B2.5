  corkt = 0.0_R8
!solve_keps - kt
  IF (switch%solve_keps .GT. 0 .AND. ANY(solvekt(0:mpg%nnreg(0)))) THEN
    CALL RESTART_MA28_FOR_US()
!   ..compute flo0, con0
    flo0 = flokt
    con0(:, :, 0) = conkt
!   ..initialize matrix to zero
    aa = 0.0_R8
!$AD II-LOOP
!   ..compute the nine-point matrix
    DO icv=1,ncv
      ireg = mpg%cvreg(icv)
      IF (solvekt(ireg)) THEN
        aa(mpg%cvnvp(icv, 1)) = -skt(icv, 1) - skt(icv, 3)*ni(icv, 1)
        IF (switch%b2mndt_style .LT. 2) THEN
          IF (reskt(icv) .GE. 0.) THEN
            abs4 = reskt(icv)
          ELSE
            abs4 = -reskt(icv)
          END IF
          aa(mpg%cvnvp(icv, 1)) = aa(mpg%cvnvp(icv, 1)) + abs4/(switch%&
&           b2npht_rxg*(kt(icv)+1.0e-5_R8*ev))
        END IF
      END IF
    END DO
!$AD II-LOOP
    DO ifc=1,nfc
! cell number of c1
      icv1 = mpg%fccv(ifc, 1)
! cell number of c2
      icv2 = mpg%fccv(ifc, 2)
!     ..contributions from flo0(:,0), con0(:,0,:) ('5-point' contributions)
      t1 = -(0.5_R8*flo0(ifc, 0)) - con0(ifc, 0, 0)
      t2 = 0.5_R8*flo0(ifc, 0) - con0(ifc, 0, 0)
      IF (solvekt(mpg%cvreg(icv1))) THEN
        aa(mpg%cvnvp(icv1, 1)) = aa(mpg%cvnvp(icv1, 1)) - t1
        mask23(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv2
        WHERE (mask23(1:mpg%cvnvp(icv1, 2))) aa(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1):&
&           mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t2
      END IF
      IF (solvekt(mpg%cvreg(icv2))) THEN
        aa(mpg%cvnvp(icv2, 1)) = aa(mpg%cvnvp(icv2, 1)) - t2
        mask24(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv1
        WHERE (mask24(1:mpg%cvnvp(icv2, 2))) aa(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1):&
&           mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) + t1
      END IF
!     ..contributions from flo0(:,1), con0(:,1,:) ('9-point' contributions)
! number of v1
      ivx1 = mpg%fcvx(ifc, 1)
! number of v2
      ivx2 = mpg%fcvx(ifc, 2)
      t1 = -(0.5_R8*flo0(ifc, 1)) - con0(ifc, 1, 0)
      t2 = 0.5_R8*flo0(ifc, 1) - con0(ifc, 1, 0)
      t11 = 0.0_R8
      DO incv=1,mpg%vxcvp(ivx1, 2)
        t11 = t11 + 1/geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)
      END DO
      t22 = 0.0_R8
      DO incv=1,mpg%vxcvp(ivx2, 2)
        t22 = t22 + 1/geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)
! contribution to equation of iCv1
      END DO
! contribution to equation of iCv2
      IF (solvekt(mpg%cvreg(icv1))) THEN
! contribution from iVx1
        DO incv=1,mpg%vxcvp(ivx1, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
          mask25(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg&
&           %cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv
          WHERE (mask25(1:mpg%cvnvp(icv1, 2))) aa(mpg%cvnvp(icv1, 1):mpg&
&           %cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1&
&             ):mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) - t3*t1
! contribution from iVx2
        END DO
        DO incv=1,mpg%vxcvp(ivx2, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
          mask26(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg&
&           %cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv
          WHERE (mask26(1:mpg%cvnvp(icv1, 2))) aa(mpg%cvnvp(icv1, 1):mpg&
&           %cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1&
&             ):mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t3*t2
        END DO
      END IF
      IF (solvekt(mpg%cvreg(icv2))) THEN
! contribution from iVx1
        DO incv=1,mpg%vxcvp(ivx1, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
          mask27(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg&
&           %cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv
          WHERE (mask27(1:mpg%cvnvp(icv2, 2))) aa(mpg%cvnvp(icv2, 1):mpg&
&           %cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1&
&             ):mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) + t3*t1
! contribution from iVx2
        END DO
        DO incv=1,mpg%vxcvp(ivx2, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
          mask28(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg&
&           %cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv
          WHERE (mask28(1:mpg%cvnvp(icv2, 2))) aa(mpg%cvnvp(icv2, 1):mpg&
&           %cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1&
&             ):mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) - t3*t2
        END DO
      END IF
    END DO
!$AD II-LOOP
!   ..check for empty matrix lines and replace with dummy equation
    DO icv=1,ncv
      lnonzero = .false.
      DO incv=1,mpg%cvnvp(icv, 2)
        lnonzero = lnonzero .OR. aa(mpg%cvnvp(icv, 1)+incv-1) .NE. &
&         0.0_R8
      END DO
      IF (.NOT.lnonzero) THEN
        aa(mpg%cvnvp(icv, 1)) = 1.0_R8
        reskt(icv) = 0.0_R8
      END IF
    END DO
!
!   ..solve the correction equation
    CALL B2UXUS(ncv, mpg, aa, itcnt, reskt, corkt, 'b2usht_kt')
  END IF
!
  IF (switch%b2usht_iout .NE. 0) THEN
    CALL MY_OUT_US(70, mpg%ncmxnv, 0, aa, 'b2usht_kt_aa')
    CALL MY_OUT_US(70, ncv, 0, reskt, 'b2usht_reskt')
    CALL MY_OUT_US(70, ncv, 0, corkt, 'b2usht_corkt')
  END IF
!
! ..turbulent enstrophy correction equation
  corzt = 0.0_R8
!solve_keps - enstrophy
  IF (switch%solve_keps .GT. 1 .AND. ANY(solvezt(0:mpg%nnreg(0)))) THEN
    CALL RESTART_MA28_FOR_US()
!   ..compute flo0, con0
    flo0 = flozt
    con0(:, :, 0) = conzt
!   ..initialize matrix to zero
    aa = 0.0_R8
!$AD II-LOOP
!   ..compute the nine-point matrix
    DO icv=1,ncv
      ireg = mpg%cvreg(icv)
      IF (solvezt(ireg)) THEN
        aa(mpg%cvnvp(icv, 1)) = -szt(icv, 1) - szt(icv, 3)*ni(icv, 1)
        IF (switch%b2mndt_style .LT. 2) THEN
          IF (reszt(icv) .GE. 0.) THEN
            abs5 = reszt(icv)
          ELSE
            abs5 = -reszt(icv)
          END IF
          aa(mpg%cvnvp(icv, 1)) = aa(mpg%cvnvp(icv, 1)) + abs5/(switch%&
&           b2npht_rxg*(zt(icv)+1.0e-5_R8*ev))
        END IF
      END IF
    END DO
!$AD II-LOOP
    DO ifc=1,nfc
! cell number of c1
      icv1 = mpg%fccv(ifc, 1)
! cell number of c2
      icv2 = mpg%fccv(ifc, 2)
!     ..contributions from flo0(:,0), con0(:,0,:) ('5-point' contributions)
      t1 = -(0.5_R8*flo0(ifc, 0)) - con0(ifc, 0, 0)
      t2 = 0.5_R8*flo0(ifc, 0) - con0(ifc, 0, 0)
      IF (solvezt(mpg%cvreg(icv1))) THEN
        aa(mpg%cvnvp(icv1, 1)) = aa(mpg%cvnvp(icv1, 1)) - t1
        mask29(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv2
        WHERE (mask29(1:mpg%cvnvp(icv1, 2))) aa(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1):&
&           mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t2
      END IF
      IF (solvezt(mpg%cvreg(icv2))) THEN
        aa(mpg%cvnvp(icv2, 1)) = aa(mpg%cvnvp(icv2, 1)) - t2
        mask30(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv1
        WHERE (mask30(1:mpg%cvnvp(icv2, 2))) aa(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1):&
&           mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) + t1
      END IF
!     ..contributions from flo0(:,1), con0(:,1,:) ('9-point' contributions)
! number of v1
      ivx1 = mpg%fcvx(ifc, 1)
! number of v2
      ivx2 = mpg%fcvx(ifc, 2)
      t1 = -(0.5_R8*flo0(ifc, 1)) - con0(ifc, 1, 0)
      t2 = 0.5_R8*flo0(ifc, 1) - con0(ifc, 1, 0)
      t11 = 0.0_R8
      DO incv=1,mpg%vxcvp(ivx1, 2)
        t11 = t11 + 1/geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)
      END DO
      t22 = 0.0_R8
      DO incv=1,mpg%vxcvp(ivx2, 2)
        t22 = t22 + 1/geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)
! contribution to equation of iCv1
      END DO
! contribution to equation of iCv2
      IF (solvezt(mpg%cvreg(icv1))) THEN
! contribution from iVx1
        DO incv=1,mpg%vxcvp(ivx1, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
          mask31(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg&
&           %cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv
          WHERE (mask31(1:mpg%cvnvp(icv1, 2))) aa(mpg%cvnvp(icv1, 1):mpg&
&           %cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1&
&             ):mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) - t3*t1
! contribution from iVx2
        END DO
        DO incv=1,mpg%vxcvp(ivx2, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
          mask32(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg&
&           %cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv
          WHERE (mask32(1:mpg%cvnvp(icv1, 2))) aa(mpg%cvnvp(icv1, 1):mpg&
&           %cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1&
&             ):mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t3*t2
        END DO
      END IF
      IF (solvezt(mpg%cvreg(icv2))) THEN
! contribution from iVx1
        DO incv=1,mpg%vxcvp(ivx1, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
          mask33(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg&
&           %cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv
          WHERE (mask33(1:mpg%cvnvp(icv2, 2))) aa(mpg%cvnvp(icv2, 1):mpg&
&           %cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1&
&             ):mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) + t3*t1
! contribution from iVx2
        END DO
        DO incv=1,mpg%vxcvp(ivx2, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
          mask34(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg&
&           %cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv
          WHERE (mask34(1:mpg%cvnvp(icv2, 2))) aa(mpg%cvnvp(icv2, 1):mpg&
&           %cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1&
&             ):mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) - t3*t2
        END DO
      END IF
    END DO
!$AD II-LOOP
!   ..check for empty matrix lines and replace with dummy equation
    DO icv=1,ncv
      lnonzero = .false.
      DO incv=1,mpg%cvnvp(icv, 2)
        lnonzero = lnonzero .OR. aa(mpg%cvnvp(icv, 1)+incv-1) .NE. &
&         0.0_R8
      END DO
      IF (.NOT.lnonzero) THEN
        aa(mpg%cvnvp(icv, 1)) = 1.0_R8
        reszt(icv) = 0.0_R8
      END IF
    END DO
!
!   ..solve the correction equation
    CALL B2UXUS(ncv, mpg, aa, itcnt, reszt, corzt, 'b2usht_zt')
  END IF
!
  IF (switch%b2usht_iout .NE. 0) THEN
    CALL MY_OUT_US(70, mpg%ncmxnv, 0, aa, 'b2usht_zt_aa')
    CALL MY_OUT_US(70, ncv, 0, reszt, 'b2usht_reszt')
    CALL MY_OUT_US(70, ncv, 0, corzt, 'b2usht_corzt')
  END IF
!
!
!
!   ..return
  ncall_b2usht = ncall_b2usht + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2usht
!
END SUBROUTINE B2USHT_NODIFF

