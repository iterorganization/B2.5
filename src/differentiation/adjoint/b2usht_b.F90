      con0b(ifc, 0, 0) = con0b(ifc, 0, 0) - t2b - t1b
    END DO
    sheb = 0.D0
    CALL POPREAL8ARRAY(reshe, r8*ncv/8)
!$BWD-OF II-LOOP 
    DO icv=1,ncv
      ireg = mpg%cvreg(icv)
      IF (solveee(ireg)) THEN
!srv 02.01.07
        arg1 = 1.5_R8*(te(icv)-ti(icv))/te(icv)
        result1 = DIM_FWD(1.0_R8, arg1)
        IF (switch%b2mndt_style .LT. 2) THEN
          IF (reshe(icv) .GE. 0.) THEN
            abs1 = reshe(icv)
            CALL PUSHCONTROL1B(0)
          ELSE
            abs1 = -reshe(icv)
            CALL PUSHCONTROL1B(1)
          END IF
          tempb2 = aab(mpg%cvnvp(icv, 1))/(switch%b2npht_rxg*te(icv))
          abs1b = tempb2
          teb(icv) = teb(icv) - abs1*tempb2/te(icv)
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            resheb(icv) = resheb(icv) + abs1b
          ELSE
            resheb(icv) = resheb(icv) - abs1b
          END IF
        END IF
        temp = SQRT(te(icv))
        temp0 = te(icv)*temp
        temp2 = ne(icv)*ni(icv, 0)
        temp3 = result1*ceqp(icv)
        tempb0 = aab(mpg%cvnvp(icv, 1))/temp0
        sheb(icv, 3) = sheb(icv, 3) - ne(icv)*aab(mpg%cvnvp(icv, 1))
        neb(icv) = neb(icv) + ni(icv, 0)*temp3*tempb0 - she(icv, 3)*aab(&
&         mpg%cvnvp(icv, 1))
        sheb(icv, 1) = sheb(icv, 1) - aab(mpg%cvnvp(icv, 1))
        aab(mpg%cvnvp(icv, 1)) = 0.D0
        result1b = ceqp(icv)*temp2*tempb0
        ceqpb(icv) = ceqpb(icv) + result1*temp2*tempb0
        nib(icv, 0) = nib(icv, 0) + ne(icv)*temp3*tempb0
        tempb1 = -(temp3*temp2*tempb0/temp0)
        CALL DIM_BWD(1.0_R8, dummydiffb, arg1, arg1b, result1b)
        tempb = 1.5_R8*arg1b/te(icv)
        IF (te(icv) .EQ. 0.D0) THEN
          teb(icv) = teb(icv) + temp*tempb1 + (1.0-(te(icv)-ti(icv))/te(&
&           icv))*tempb
        ELSE
          teb(icv) = teb(icv) + (temp+te(icv)/(2.0*temp))*tempb1 + (1.0-&
&           (te(icv)-ti(icv))/te(icv))*tempb
        END IF
        tib(icv) = tib(icv) - tempb
      END IF
    END DO
    CALL POPREAL8ARRAY(aa, r8*mpg%ncmxnv/8)
    coneb(:, :, 0) = coneb(:, :, 0) + con0b(:, :, 0)
    con0b(:, :, 0) = 0.D0
    floeb = floeb + flo0b
  ELSE
    sheb = 0.D0
  END IF
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
  aab = 0.0_R8
    CALL B2UXUS_B(ncv, mpg, aa, aab, itcnt, resht, reshtb, cortt, corttb&
&           , 'b2usht_total')
    CALL POPREAL8ARRAY(aa, r8*mpg%ncmxnv/8)
!$BWD-OF II-LOOP 
    DO icv=1,ncv
      lnonzero = .false.
      DO incv=1,mpg%cvnvp(icv, 2)
        lnonzero = lnonzero .OR. aa(mpg%cvnvp(icv, 1)+incv-1) .NE. &
&         0.0_R8
      END DO
      IF (.NOT.lnonzero) THEN
        reshtb(icv) = 0.D0
        aab(mpg%cvnvp(icv, 1)) = 0.D0
      END IF
    END DO
    flo0b = 0.D0
    CALL POPINTEGER4(ireg)
!$BWD-OF II-LOOP 
    DO ifc=1,nfc
! cell number of c1
      icv1 = mpg%fccv(ifc, 1)
! cell number of c2
      icv2 = mpg%fccv(ifc, 2)
!     ..contributions from flo0(:,0), con0(:,0,:) ('5-point' contributions)
      IF ((solveee(mpg%cvreg(icv1)) .OR. solveei(mpg%cvreg(icv1))) .OR. &
&         (solveen(ireg) .AND. switch%tn_style .EQ. 2)) THEN
        mask(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv2
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
      END IF
      IF ((solveee(mpg%cvreg(icv2)) .OR. solveei(mpg%cvreg(icv2))) .OR. &
&         (solveen(ireg) .AND. switch%tn_style .EQ. 2)) THEN
        mask0(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv1
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
      END IF
!     ..contributions from flo0(:,1), con0(:,1,:) ('9-point' contributions)
! number of v1
      ivx1 = mpg%fcvx(ifc, 1)
! number of v2
      ivx2 = mpg%fcvx(ifc, 2)
      t11 = 0.0_R8
      DO incv=1,mpg%vxcvp(ivx1, 2)
        t11 = t11 + 1/geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)
      END DO
      t22 = 0.0_R8
      DO incv=1,mpg%vxcvp(ivx2, 2)
        t22 = t22 + 1/geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)
! contribution to equation of iCv1
      END DO
! contribution to equation of iCv2
      IF ((solveee(mpg%cvreg(icv1)) .OR. solveei(mpg%cvreg(icv1))) .OR. &
&         (solveen(ireg) .AND. switch%tn_style .EQ. 2)) THEN
! contribution from iVx1
        DO incv=1,mpg%vxcvp(ivx1, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
          CALL PUSHBOOLEANARRAY(mask1(1:mpg%cvnvp(icv1, 2)), mpg%cvnvp(&
&                         icv1, 2))
          mask1(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%&
&           cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv
! contribution from iVx2
        END DO
        DO incv=1,mpg%vxcvp(ivx2, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
          CALL PUSHBOOLEANARRAY(mask2(1:mpg%cvnvp(icv1, 2)), mpg%cvnvp(&
&                         icv1, 2))
          mask2(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%&
&           cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv
        END DO
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
      END IF
      IF ((solveee(mpg%cvreg(icv2)) .OR. solveei(mpg%cvreg(icv2))) .OR. &
&         (solveen(ireg) .AND. switch%tn_style .EQ. 2)) THEN
! contribution from iVx1
        DO incv=1,mpg%vxcvp(ivx1, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
          CALL PUSHBOOLEANARRAY(mask3(1:mpg%cvnvp(icv2, 2)), mpg%cvnvp(&
&                         icv2, 2))
          mask3(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%&
&           cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv
! contribution from iVx2
        END DO
        DO incv=1,mpg%vxcvp(ivx2, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
          CALL PUSHBOOLEANARRAY(mask4(1:mpg%cvnvp(icv2, 2)), mpg%cvnvp(&
&                         icv2, 2))
          mask4(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%&
&           cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv
        END DO
