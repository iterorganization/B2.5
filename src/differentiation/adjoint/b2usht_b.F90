        DO incv=mpg%vxcvp(ivx2, 2),1,-1
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
          t2b = t2b - t3*SUM(aab(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+&
&           mpg%cvnvp(icv2, 2)-1), MASK=mask4(1:mpg%cvnvp(icv2, 2)))
          CALL POPBOOLEANARRAY(mask4(1:mpg%cvnvp(icv2, 2)), mpg%cvnvp(&
&                        icv2, 2))
        END DO
        t1b = 0.D0
        DO incv=mpg%vxcvp(ivx1, 2),1,-1
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
          t1b = t1b + t3*SUM(aab(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+&
&           mpg%cvnvp(icv2, 2)-1), MASK=mask3(1:mpg%cvnvp(icv2, 2)))
          CALL POPBOOLEANARRAY(mask3(1:mpg%cvnvp(icv2, 2)), mpg%cvnvp(&
&                        icv2, 2))
        END DO
      ELSE
        t1b = 0.D0
        t2b = 0.D0
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        DO incv=mpg%vxcvp(ivx2, 2),1,-1
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
          t2b = t2b + t3*SUM(aab(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+&
&           mpg%cvnvp(icv1, 2)-1), MASK=mask2(1:mpg%cvnvp(icv1, 2)))
          CALL POPBOOLEANARRAY(mask2(1:mpg%cvnvp(icv1, 2)), mpg%cvnvp(&
&                        icv1, 2))
        END DO
        DO incv=mpg%vxcvp(ivx1, 2),1,-1
          t3 = 1/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
          t1b = t1b - t3*SUM(aab(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+&
&           mpg%cvnvp(icv1, 2)-1), MASK=mask1(1:mpg%cvnvp(icv1, 2)))
          CALL POPBOOLEANARRAY(mask1(1:mpg%cvnvp(icv1, 2)), mpg%cvnvp(&
&                        icv1, 2))
        END DO
      END IF
      flo0b(ifc, 1) = flo0b(ifc, 1) + 0.5_R8*t2b - 0.5_R8*t1b
      con0b(ifc, 1, 0) = con0b(ifc, 1, 0) - t2b - t1b
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        t1b = SUM(aab(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%cvnvp(&
&         icv2, 2)-1), MASK=mask0(1:mpg%cvnvp(icv2, 2)))
        t2b = -aab(mpg%cvnvp(icv2, 1))
      ELSE
        t1b = 0.D0
        t2b = 0.D0
      END IF
