          t0 = ti(icv)
          CALL PUSHCONTROL2B(2)
        ELSE
          CALL PUSHCONTROL2B(1)
          t0 = t0
        END IF
      ELSE
        CALL PUSHCONTROL2B(0)
      END IF
      IF (solveen(ireg) .AND. switch%tn_style .EQ. 2) THEN
        IF (t0 .GT. tn(icv)) THEN
          t0 = tn(icv)
          CALL PUSHCONTROL2B(2)
        ELSE
          CALL PUSHCONTROL2B(1)
          t0 = t0
        END IF
      ELSE
        CALL PUSHCONTROL2B(0)
      END IF
      IF ((solveee(ireg) .OR. solveei(ireg)) .OR. (solveen(ireg) .AND. &
&         switch%tn_style .EQ. 2)) THEN
        IF (switch%b2mndt_style .LT. 2) THEN
          IF (resht(icv) .GE. 0.) THEN
            abs0 = resht(icv)
            CALL PUSHCONTROL1B(0)
          ELSE
            abs0 = -resht(icv)
            CALL PUSHCONTROL1B(1)
          END IF
          tempb = aab(mpg%cvnvp(icv, 1))/(switch%b2npht_rxg*t0)
          abs0b = tempb
          t0b = -(abs0*tempb/t0)
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            reshtb(icv) = reshtb(icv) + abs0b
          ELSE
            reshtb(icv) = reshtb(icv) - abs0b
          END IF
        ELSE
          t0b = 0.D0
        END IF
      ELSE
        t0b = 0.D0
      END IF
      CALL POPCONTROL2B(branch)
      IF (branch .NE. 0) THEN
        IF (branch .NE. 1) THEN
          tnb(icv) = tnb(icv) + t0b
          t0b = 0.D0
        END IF
        shnb(icv, 1) = shnb(icv, 1) - aab(mpg%cvnvp(icv, 1))
        shnb(icv, 3) = shnb(icv, 3) - nn(icv)*aab(mpg%cvnvp(icv, 1))
        nnb(icv) = nnb(icv) - shn(icv, 3)*aab(mpg%cvnvp(icv, 1))
      END IF
      CALL POPCONTROL2B(branch)
      IF (branch .NE. 0) THEN
        IF (branch .NE. 1) THEN
          tib(icv) = tib(icv) + t0b
          t0b = 0.D0
        END IF
        shib(icv, 1) = shib(icv, 1) - aab(mpg%cvnvp(icv, 1))
        shib(icv, 3) = shib(icv, 3) - ni(icv, 0)*aab(mpg%cvnvp(icv, 1))
        nib(icv, 0) = nib(icv, 0) - shi(icv, 3)*aab(mpg%cvnvp(icv, 1))
      END IF
      CALL POPCONTROL2B(branch)
      IF (branch .NE. 0) THEN
        IF (branch .NE. 1) teb(icv) = teb(icv) + t0b
        sheb(icv, 1) = sheb(icv, 1) - aab(mpg%cvnvp(icv, 1))
        sheb(icv, 3) = sheb(icv, 3) - ne(icv)*aab(mpg%cvnvp(icv, 1))
        neb(icv) = neb(icv) - she(icv, 3)*aab(mpg%cvnvp(icv, 1))
      END IF
    END DO
    coneb(:, :, 0) = coneb(:, :, 0) + con0b(:, :, 0)
    conib(:, :, 0) = conib(:, :, 0) + con0b(:, :, 0)
    connb = connb + con0b(:, :, 0)
    floeb = floeb + flo0b
    floib = floib + flo0b
    flonb = flonb + flo0b
  END IF
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
    CALL POPREAL8ARRAY(shn, r8*ncv*4/8)
    connb = 0.D0
    flonb = 0.D0
    shnb = 0.D0
  END IF
END SUBROUTINE B2USHT_B

