    TYPE(MAPPING), INTENT(INOUT) :: m
    TYPE(GEOMETRY), INTENT(INOUT) :: g
    INTRINSIC COUNT
    EXTERNAL XERRAB
    INTRINSIC MIN
    INTRINSIC MAX
    REAL(kind=r8) :: result1
    LOGICAL, DIMENSION(nstrat) :: mask
    LOGICAL, DIMENSION(nstrat) :: mask0
!      intrinsic huge
!
