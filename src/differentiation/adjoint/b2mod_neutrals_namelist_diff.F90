&           rcend(istra), rcpos(istra))) .GT. m%nci) rce(istra) = rce(&
&           istra) - 1
        rcl(istra) = rce(istra) - rcs(istra) + 1
        m%mxnrc = m%mxnrc + rcl(istra)
      ELSE IF (crcstra(istra) .EQ. 'S') THEN
        IF (m%imapcv(topix(rcstart(istra), rcpos(istra)), topiy(rcstart(&
&           istra), rcpos(istra))) .GT. m%nci) rcs(istra) = rcs(istra) +&
&           1
