! number of the corresponding domain face
          m%rcfc(instra) = m%cvfc(m%cvfcp(m%imapcv(rcpos(istra), rcs(&
&           istra)+istrac-1), 1))
          m%rcfcor(instra) = -1.0_R8
          IF (minlbl(istra) .GT. m%fclbl(m%rcfc(instra))) THEN
            minlbl(istra) = m%fclbl(m%rcfc(instra))
          ELSE
            minlbl(istra) = minlbl(istra)
          END IF
          IF (maxlbl(istra) .LT. m%fclbl(m%rcfc(instra))) THEN
            maxlbl(istra) = m%fclbl(m%rcfc(instra))
          ELSE
            maxlbl(istra) = maxlbl(istra)
          END IF
          lchar(istra) = 'A'
        END DO
        rcfe(istra) = 0.5_R8
        rcfi(istra) = 3.0_R8
        accel_ion(istra) = 1
      ELSE IF (crcstra(istra) .EQ. 'E') THEN
        DO istrac=1,rcl(istra)
          instra = instra + 1
! number of the corresponding domain face
          m%rcfc(instra) = m%cvfc(m%cvfcp(m%imapcv(rcpos(istra), rcs(&
&           istra)+istrac-1), 1))
          m%rcfcor(instra) = 1.0_R8
          IF (minlbl(istra) .GT. m%fclbl(m%rcfc(instra))) THEN
            minlbl(istra) = m%fclbl(m%rcfc(instra))
          ELSE
            minlbl(istra) = minlbl(istra)
          END IF
          IF (maxlbl(istra) .LT. m%fclbl(m%rcfc(instra))) THEN
            maxlbl(istra) = m%fclbl(m%rcfc(instra))
          ELSE
            maxlbl(istra) = maxlbl(istra)
          END IF
          lchar(istra) = 'A'
        END DO
        rcfe(istra) = 0.5_R8
        rcfi(istra) = 3.0_R8
        accel_ion(istra) = 1
      ELSE IF (crcstra(istra) .EQ. 'A') THEN
        minlbl(istra) = rcstart(istra)
        maxlbl(istra) = rcend(istra)
        lchar(istra) = 'A'
        rcl(istra) = 0
        noss = rcend(istra) - rcstart(istra) + 1
        DO iss=1,noss
          indss = rcstart(istra) + iss - 1
          WRITE(*, *) ' surface index ', indss
!  find all faces belonging to surface structure INDSS
          CALL FIND_FACES_NODIFF(indss, instra, rcl(istra), m%mxnrc, m%&
&                          rcfc, m%rcfcor, m, idb)
        END DO
! iss
        m%rcfcp(istra, 2) = rcl(istra)
        rcfe(istra) = 0.5_R8
        rcfi(istra) = 3.0_R8
      ELSE
        accel_ion(istra) = 0
!          call xerrab ('conv_rcparms -- wrong CRCSTRA')
      END IF
    END DO
    CLOSE(idb) 
!
    arcstra(1:nstrai) = lchar(1:nstrai)
    mask = minlbl .EQ. HUGE(1)
    WHERE (mask) minlbl = 0
    mask0 = maxlbl .EQ. -HUGE(1)
    WHERE (mask0) maxlbl = 0
    arcstart(1:nstrai) = minlbl(1:nstrai)
    arcend(1:nstrai) = maxlbl(1:nstrai)
!
    IF (crcstra(nstrai) .EQ. 'T') THEN
      arcstart(nstrai) = 0
      arcend(nstrai) = 0
    END IF
    RETURN
  END SUBROUTINE CONV_RCPARMS

