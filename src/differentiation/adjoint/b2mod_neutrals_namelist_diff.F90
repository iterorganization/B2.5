&           rcpos(istra), rcstart(istra))) .GT. m%nci) rcs(istra) = rcs(&
&           istra) + 1
        IF (m%imapcv(rightix(rcpos(istra), rcend(istra)), rightiy(rcpos(&
&           istra), rcend(istra))) .GT. m%nci) rce(istra) = rce(istra) -&
&           1
        rcl(istra) = rce(istra) - rcs(istra) + 1
        m%mxnrc = m%mxnrc + rcl(istra)
      ELSE IF ((crcstra(istra) .EQ. 'V' .OR. crcstra(istra) .EQ. 'T') &
&         .OR. crcstra(istra) .EQ. 'C') THEN
        rcl(istra) = 0
      ELSE
        CALL XERRAB('Wrong CRCSTRA or not yet available for WG')
      END IF
    END DO
!
    CALL ALLOC_MAPPING_RC(m)
    OPEN(newunit=idb, file='recyc.out') 
!
    minlbl = HUGE(1)
    result1 = HUGE(1)
    maxlbl = -result1
