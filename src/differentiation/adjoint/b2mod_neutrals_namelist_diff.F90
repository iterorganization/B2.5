      ELSE IF (crcstra(istra) .EQ. 'E') THEN
        IF (m%imapcv(leftix(rcpos(istra), rcstart(istra)), leftiy(rcpos(&
&           istra), rcstart(istra))) .GT. m%nci) rcs(istra) = rcs(istra)&
&           + 1
        IF (m%imapcv(leftix(rcpos(istra), rcend(istra)), leftiy(rcpos(&
&           istra), rcend(istra))) .GT. m%nci) rce(istra) = rce(istra) -&
&           1
        rcl(istra) = rce(istra) - rcs(istra) + 1
        m%mxnrc = m%mxnrc + rcl(istra)
      ELSE IF (crcstra(istra) .EQ. 'W') THEN
