    DO istra=1,nstrai
      rcs(istra) = rcstart(istra)
      rce(istra) = rcend(istra)
      IF (crcstra(istra) .EQ. 'A') THEN
! find the number of faces for this boundary condition
!wdk need more complex test here; present scheme double counts;
!wdk also need to correct for degenerate faces?
        rcl(istra) = COUNT(cflags(:, :, 2:5) .GE. rcstart(istra) .AND. &
