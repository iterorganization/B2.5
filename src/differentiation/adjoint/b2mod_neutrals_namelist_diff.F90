        END DO
        rcfe(istra) = 0.0_R8
        rcfi(istra) = 2.0_R8
        accel_ion(istra) = 0
      ELSE IF (crcstra(istra) .EQ. 'N') THEN
        DO istrac=1,rcl(istra)
          instra = instra + 1
! number of the corresponding domain face
          m%rcfc(instra) = m%cvfc(m%cvfcp(m%imapcv(rcs(istra)+istrac-1, &
