SUBROUTINE B2TLC0_NODIFF(ncv, nfc, nvx, ns, switch, geo, mpg, na, te, ti&
& , tn, rza, cdpa0, cdpa, cdpahz, flc)
  USE B2MOD_TYPES
  USE B2MOD_CONSTANTS
  USE B2MOD_B2CMPA_DIFF
  USE B2MOD_SWITCHES_DIFF
  USE B2US_GEO_DIFF
  USE B2US_MAP_DIFF
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_AD_DIFF, ONLY : b2tlc0_cutlo
  USE B2MOD_AD_DIFF, ONLY : my_out_folder
  USE B2MOD_SUBSYS
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER, INTENT(IN) :: ncv, nfc, nvx, ns
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  REAL(kind=r8) :: na(ncv, 0:ns-1), te(ncv), ti(ncv), tn(ncv), rza(ncv, &
& 0:ns-1), cdpa0(nfc, 0:ns-1)
!   ..input and output arguments
  REAL(kind=r8) :: cdpa(nfc, 0:1, 0:ns-1), cdpahz(nfc, 0:1, 0:ns-1)
!   ..output arguments (unspecified on entry)
  REAL(kind=r8) :: flc(nfc, 0:1, 0:ns-1)
!   ..common blocks
!     ------------------------------------------------------------------
!     B2TLC0 computes the neutral diffusion flux limit.              !srv 09.07.03
!     ------------------------------------------------------------------
!   ..local variables
  INTEGER :: ifc, is
  REAL(kind=r8) :: alpha, gamma, t0, t1, t2, t3, dpb, vbar, maxfluxlimit&
& (0:1), pb(ncv), dpbf(nfc, 0:1), pbv(nvx)
  CHARACTER :: charns*3
!   ..procedures
  INTRINSIC MAX, ABS, SQRT
  EXTERNAL XERTST
  REAL(kind=r8) :: abs0
  REAL(kind=r8) :: abs1
  INTEGER :: arg1
  CHARACTER(len=8) :: arg10
  CHARACTER(len=7) :: arg11
!     ------------------------------------------------------------------
!   ..subprogram start-up calls
  CALL SUBINI('b2tlc0')
!   ..test nCv, nFc, ns
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!
  arg1 = nfc*2*ns
  CALL SFILL_NODIFF(arg1, 1.0_R8, flc, 1)
  CALL SFILL_NODIFF(2, 1.0_R8, maxfluxlimit, 1)
!
  alpha = switch%b2tlc0_alpha
  gamma = switch%b2tlc0_gamma
!
!   ..nothing done if alpha.eq.0
  IF (alpha .NE. 0.0_R8) THEN
    IF (switch%b2tlc0_style .EQ. 0) THEN
      DO is=0,ns-1
        IF (is_neutral(is)) THEN
!    ..compute partial pressure
          CALL B2XPPB_NODIFF(ncv, rza(1, is), na(1, is), te, ti, tn, is&
&                      , pb)
!    ..compute differences of partial pressure
          CALL DIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, pb, pbv, dpbf)
          DO ifc=1,nfc
            t0 = (tn(mpg%fccv(ifc, 1))+tn(mpg%fccv(ifc, 2)))/2.0_R8
            t1 = (na(mpg%fccv(ifc, 1), is)+na(mpg%fccv(ifc, 2), is))/&
&             2.0_R8
            IF (cdpa(ifc, 0, is)*dpbf(ifc, 0) .GE. 0.) THEN
              dpb = cdpa(ifc, 0, is)*dpbf(ifc, 0)
            ELSE
              dpb = -(cdpa(ifc, 0, is)*dpbf(ifc, 0))
            END IF
            IF (geo%fcqalf(ifc, 0) .GE. 0.) THEN
              abs0 = geo%fcqalf(ifc, 0)
            ELSE
              abs0 = -geo%fcqalf(ifc, 0)
            END IF
            vbar = SQRT(8.0_R8*t0/(pi*(am(is)*mp)))*geo%fcs(ifc)*abs0
            t2 = 1.0_R8/(1.0_R8+(dpb/(alpha*vbar/4.0_R8*t1+b2tlc0_cutlo)&
&             )**gamma)**(1.0_R8/gamma)
            cdpa(ifc, 0, is) = cdpa(ifc, 0, is)*t2
            cdpahz(ifc, 0, is) = cdpahz(ifc, 0, is)*t2
            flc(ifc, 0, is) = t2
            IF (maxfluxlimit(0) .LT. t2) THEN
              maxfluxlimit(0) = t2
            ELSE
              maxfluxlimit(0) = maxfluxlimit(0)
            END IF
            IF (cdpa(ifc, 1, is)*dpbf(ifc, 1) .GE. 0.) THEN
              dpb = cdpa(ifc, 1, is)*dpbf(ifc, 1)
            ELSE
              dpb = -(cdpa(ifc, 1, is)*dpbf(ifc, 1))
            END IF
            IF (geo%fcqalf(ifc, 1) .GE. 0.) THEN
              abs1 = geo%fcqalf(ifc, 1)
            ELSE
              abs1 = -geo%fcqalf(ifc, 1)
            END IF
            vbar = SQRT(8.0_R8*t0/(pi*(am(is)*mp)))*geo%fcs(ifc)*abs1
            t2 = 1.0_R8/(1.0_R8+(dpb/(alpha*vbar/4.0_R8*t1+b2tlc0_cutlo)&
&             )**gamma)**(1.0_R8/gamma)
            cdpa(ifc, 1, is) = cdpa(ifc, 1, is)*t2
            cdpahz(ifc, 1, is) = cdpahz(ifc, 1, is)*t2
            flc(ifc, 1, is) = t2
            IF (maxfluxlimit(1) .LT. t2) THEN
              maxfluxlimit(1) = t2
            ELSE
              maxfluxlimit(1) = maxfluxlimit(1)
            END IF
          END DO
        END IF
      END DO
    ELSE
!wdk    flux limit total gradient-driven flow, not individual poloidal
!wdk    or radial contributions, since neutral transport is isotropic
      DO is=0,ns-1
        IF (is_neutral(is)) THEN
!    ..compute partial pressure
          CALL B2XPPB_NODIFF(ncv, rza(1, is), na(1, is), te, ti, tn, is&
&                      , pb)
!    ..compute differences of partial pressure
          CALL DIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, pb, pbv, dpbf)
          DO ifc=1,nfc
            t0 = (tn(mpg%fccv(ifc, 1))+tn(mpg%fccv(ifc, 2)))/2.0_R8
! lkw 30.09.2022{
            t3 = dpbf(ifc, 0)*geo%fcqalf(ifc, 0) + dpbf(ifc, 1)*geo%&
&             fcqalf(ifc, 1)
            t1 = na(mpg%fccv(ifc, 1), is)
! lkw 30.09.2022}
            IF (t3 .GT. 0) t1 = na(mpg%fccv(ifc, 2), is)
!    ..limit total gradient
            dpb = cdpa0(ifc, is)*SQRT(dpbf(ifc, 0)**2+dpbf(ifc, 1)**2)
            vbar = SQRT(8.0_R8*t0/(pi*(am(is)*mp)))*geo%fcs(ifc)
            t2 = 1.0_R8/(1.0_R8+(dpb/(alpha*vbar/4.0_R8*t1+b2tlc0_cutlo)&
&             )**gamma)**(1.0_R8/gamma)
!    ..poloidal
            cdpa(ifc, 0, is) = cdpa(ifc, 0, is)*t2
            cdpahz(ifc, 0, is) = cdpahz(ifc, 0, is)*t2
            flc(ifc, 0, is) = t2
            IF (maxfluxlimit(0) .LT. t2) THEN
              maxfluxlimit(0) = t2
            ELSE
              maxfluxlimit(0) = maxfluxlimit(0)
            END IF
!    ..radial
            cdpa(ifc, 1, is) = cdpa(ifc, 1, is)*t2
            cdpahz(ifc, 1, is) = cdpahz(ifc, 1, is)*t2
            flc(ifc, 1, is) = t2
            IF (maxfluxlimit(1) .LT. t2) THEN
              maxfluxlimit(1) = t2
            ELSE
              maxfluxlimit(1) = maxfluxlimit(1)
            END IF
          END DO
        END IF
      END DO
    END IF
  END IF
!
!      write(*,'(1x,a,1p,2e10.2)') 'b2tlc0: maxfluxlimit = ',maxfluxlimit
!
  IF (switch%b2tlc0_iout .NE. 0) THEN
    DO is=0,ns-1
      IF (is_neutral(is)) THEN
        WRITE(charns, '(i3.3)') is
        arg10 = 'fl_fn_th'//charns
        CALL MY_OUT_US(70, nfc, 1, flc(1, 0, is), arg10)
        arg11 = 'fl_fn_r'//charns
        CALL MY_OUT_US(70, nfc, 1, flc(1, 1, is), arg11)
      END IF
    END DO
  END IF
!
!   ..return
  CALL SUBEND()
  RETURN
!     ------------------------------------------------------------------
END SUBROUTINE B2TLC0_NODIFF

