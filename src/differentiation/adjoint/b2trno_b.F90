    CALL B2TLC0_B(ncv, nfc, nvx, ns, switch, switchb, geo, geob, mpg, &
&           mpgb, pl%na, plb%na, pl%te, plb%te, pl%ti, plb%ti, pl%tn, &
&           plb%tn, rt%rza, rtb%rza, cdpa0, cdpa0b, co%cdpa, cob%cdpa, &
&           co%cdpahz, cob%cdpahz, flc, flcb)
  ELSE
    cdpa0b = 0.D0
  END IF
  CALL POPREAL8ARRAY(co%csig_an, r8*SIZE(co%csig_an, 1)*SIZE(co%csig_an&
&              , 2)/8)
  cob%csig = cob%csig + cob%csig_an
  cob%csig_an = 0.D0
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
    CALL POPREAL8ARRAY(co%csig(:, 0), r8*SIZE(co%csig, 1)/8)
    cob%csig(:, 0) = switch%pol_anom_scale*cob%csig(:, 0)
    CALL POPREAL8ARRAY(co%calf(:, 0), r8*SIZE(co%calf, 1)/8)
    cob%calf(:, 0) = switch%pol_anom_scale*cob%calf(:, 0)
    CALL POPREAL8ARRAY(co%chce(:, 0), r8*SIZE(co%chce, 1)/8)
    cob%chce(:, 0) = switch%pol_anom_scale*cob%chce(:, 0)
    DO is=ns-1,0,-1
      CALL POPCONTROL1B(branch)
      IF (branch .NE. 0) THEN
        CALL POPREAL8ARRAY(co%chcb(:, 0, is), r8*SIZE(co%chcb, 1)/8)
        cob%chcb(:, 0, is) = switch%pol_anom_scale*cob%chcb(:, 0, is)
        CALL POPREAL8ARRAY(co%cvsahz(:, 0, is), r8*SIZE(co%cvsahz, 1)/8)
        cob%cvsahz(:, 0, is) = switch%pol_anom_scale*cob%cvsahz(:, 0, is&
&         )
        CALL POPREAL8ARRAY(co%cvlahz(:, 0, is), r8*SIZE(co%cvlahz, 1)/8)
        cob%cvlahz(:, 0, is) = switch%pol_anom_scale*cob%cvlahz(:, 0, is&
&         )
        CALL POPREAL8ARRAY(co%cdpahz(:, 0, is), r8*SIZE(co%cdpahz, 1)/8)
        cob%cdpahz(:, 0, is) = switch%pol_anom_scale*cob%cdpahz(:, 0, is&
&         )
        CALL POPREAL8ARRAY(co%cdnahz(:, 0, is), r8*SIZE(co%cdnahz, 1)/8)
        CALL POPREAL8ARRAY(co%cvsa(:, 0, is), r8*SIZE(co%cvsa, 1)/8)
        cob%cvsa(:, 0, is) = switch%pol_anom_scale*cob%cvsa(:, 0, is)
        CALL POPREAL8ARRAY(co%cvla(:, 0, is), r8*SIZE(co%cvla, 1)/8)
        cob%cvla(:, 0, is) = switch%pol_anom_scale*cob%cvla(:, 0, is)
        CALL POPREAL8ARRAY(co%cdpa(:, 0, is), r8*SIZE(co%cdpa, 1)/8)
        cob%cdpa(:, 0, is) = switch%pol_anom_scale*cob%cdpa(:, 0, is)
        CALL POPREAL8ARRAY(co%cdna(:, 0, is), r8*SIZE(co%cdna, 1)/8)
        cob%cdna(:, 0, is) = switch%pol_anom_scale*cob%cdna(:, 0, is)
      END IF
    END DO
  END IF
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
    CALL SFILL_BWD(nfc, 0.0_R8, dummydiffb0, co%calf(1, 0), cob%calf(1, &
&            0), 1)
    CALL SFILL_BWD(nfc, 0.0_R8, dummydiffb, co%csig(1, 0), cob%csig(1, 0&
&            ), 1)
  END IF
  CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&           hci_exb, cob%hci_exb, co%chci_exb(1:nfc, 1), cob%chci_exb(1:&
&           nfc, 1))
  CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&           hci_exb, cob%hci_exb, co%chci_exb(1:nfc, 0), cob%chci_exb(1:&
&           nfc, 0))
  CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&           hce_exb, cob%hce_exb, co%chce_exb(1:nfc, 1), cob%chce_exb(1:&
&           nfc, 1))
  CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&           hce_exb, cob%hce_exb, co%chce_exb(1:nfc, 0), cob%chce_exb(1:&
&           nfc, 0))
  CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%dzt0&
&           , cob%dzt0, co%cdzt(1:nfc, 1), cob%cdzt(1:nfc, 1))
  wrkcb = 0.D0
  CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, wrkc, &
&           wrkcb, co%cdzt(1:nfc, 0), cob%cdzt(1:nfc, 0))
  cob%dzt0 = cob%dzt0 + (switch%b2tfhi_fconzt+1.0_R8)*wrkcb
  switchb%b2tfhi_fconzt = switchb%b2tfhi_fconzt + SUM(co%dzt0*wrkcb)
  CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%dkt0&
&           , cob%dkt0, co%cdkt(1:nfc, 1), cob%cdkt(1:nfc, 1))
  wrkc = co%dkt0*(1.0_R8+switch%b2tfhi_fconkt)
  wrkcb = 0.D0
  CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, wrkc, &
&           wrkcb, co%cdkt(1:nfc, 0), cob%cdkt(1:nfc, 0))
  cob%dkt0 = cob%dkt0 + (switch%b2tfhi_fconkt+1.0_R8)*wrkcb
  switchb%b2tfhi_fconkt = switchb%b2tfhi_fconkt + SUM(co%dkt0*wrkcb)
  CALL B2TXCY_BWD(ncv, nfc, -1, geo, mpg, geo%fcvol, geo%fcs, co%alf0, &
&           cob%alf0, co%calf(1:nfc, 1), cob%calf(1:nfc, 1))
  CALL B2TXCX_BWD(ncv, nfc, -1, geo, mpg, geo%fcvol, geo%fcs, co%alf0, &
&           cob%alf0, co%calf(1:nfc, 0), cob%calf(1:nfc, 0))
  CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%sig0&
&           , cob%sig0, co%csig(1:nfc, 1), cob%csig(1:nfc, 1))
  CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%sig0&
&           , cob%sig0, co%csig(1:nfc, 0), cob%csig(1:nfc, 0))
  CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%hce0&
&           , cob%hce0, co%chce(1:nfc, 1), cob%chce(1:nfc, 1))
  CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%hce0&
&           , cob%hce0, co%chce(1:nfc, 0), cob%chce(1:nfc, 0))
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
    DO is=ns-1,0,-1
      CALL POPCONTROL1B(branch)
      IF (branch .NE. 0) THEN
        wrkc = co%dpa0(:, is)*(geo%cvbb(:, 2)/geo%cvbb(:, 3))**2
        wrkcb = 0.D0
        CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, wrk1, wrk, wrkc, wrkcb&
&                 , co%cdpahz(1:nfc, 0, is), cob%cdpahz(1:nfc, 0, is))
        CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, &
&                 wrkc, wrkcb, co%cdpa(1:nfc, 0, is), cob%cdpa(1:nfc, 0&
&                 , is))
        cob%dpa0(:, is) = cob%dpa0(:, is) + geo%cvbb(:, 2)**2*wrkcb/geo%&
&         cvbb(:, 3)**2
      END IF
    END DO
  END IF
  DO is=ns-1,0,-1
    CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, wrk1, wrk, co%vsa0(1:ncv, &
&             is), cob%vsa0(1:ncv, is), co%cvsahz(1:nfc, 1, is), cob%&
&             cvsahz(1:nfc, 1, is))
    CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, wrk1, wrk, co%vsa0(1:ncv, &
&             is), cob%vsa0(1:ncv, is), co%cvsahz(1:nfc, 0, is), cob%&
&             cvsahz(1:nfc, 0, is))
    CALL POPREAL8ARRAY(co%cvlahz(1:nfc, 1, is), r8*nfc/8)
    CALL B2TXSY_B(ncv, nfc, geo, mpg, geo%fcvol, wrk, co%vla0(1:ncv, 1, &
&           is), cob%vla0(1:ncv, 1, is), co%cvlahz(1:nfc, 1, is), cob%&
&           cvlahz(1:nfc, 1, is))
    CALL POPREAL8ARRAY(co%cvlahz(1:nfc, 0, is), r8*nfc/8)
    CALL B2TXSX_B(ncv, nfc, geo, mpg, geo%fcvol, wrk, co%vla0(1:ncv, 0, &
&           is), cob%vla0(1:ncv, 0, is), co%cvlahz(1:nfc, 0, is), cob%&
&           cvlahz(1:nfc, 0, is))
    CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, wrk1, wrk, co%dpa0(1:ncv, &
&             is), cob%dpa0(1:ncv, is), co%cdpahz(1:nfc, 1, is), cob%&
&             cdpahz(1:nfc, 1, is))
    CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, wrk1, wrk, co%dpa0(1:ncv, &
&             is), cob%dpa0(1:ncv, is), co%cdpahz(1:nfc, 0, is), cob%&
&             cdpahz(1:nfc, 0, is))
  END DO
  CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&           dna_exb, cob%dna_exb, co%cdna_exb(1, 1), cob%cdna_exb(1, 1))
  CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&           dna_exb, cob%dna_exb, co%cdna_exb(1, 0), cob%cdna_exb(1, 0))
  DO is=ns-1,0,-1
    CALL B2TXCC_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             hcib(1:ncv, is), cob%hcib(1:ncv, is), chcib(1:nfc, is), &
&             chcibb(1:nfc, is))
    CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             hcib(1:ncv, is), cob%hcib(1:ncv, is), co%chcb(1:nfc, 1, is&
&             ), cob%chcb(1:nfc, 1, is))
    CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             hcib(1:ncv, is), cob%hcib(1:ncv, is), co%chcb(1:nfc, 0, is&
&             ), cob%chcb(1:nfc, 0, is))
    CALL B2TXCC_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             vsa0(1:ncv, is), cob%vsa0(1:ncv, is), cvsa0(1:nfc, is), &
&             cvsa0b(1:nfc, is))
    CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             vsa0(1:ncv, is), cob%vsa0(1:ncv, is), co%cvsa(1:nfc, 1, is&
&             ), cob%cvsa(1:nfc, 1, is))
    CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             vsa0(1:ncv, is), cob%vsa0(1:ncv, is), co%cvsa(1:nfc, 0, is&
&             ), cob%cvsa(1:nfc, 0, is))
    CALL POPREAL8ARRAY(co%cvla(1:nfc, 1, is), r8*nfc/8)
    CALL B2TXSY_B(ncv, nfc, geo, mpg, geo%fcvol, geo%fcs, co%vla0(1:ncv&
&           , 1, is), cob%vla0(1:ncv, 1, is), co%cvla(1:nfc, 1, is), cob&
&           %cvla(1:nfc, 1, is))
    CALL POPREAL8ARRAY(co%cvla(1:nfc, 0, is), r8*nfc/8)
    CALL B2TXSX_B(ncv, nfc, geo, mpg, geo%fcvol, geo%fcs, co%vla0(1:ncv&
&           , 0, is), cob%vla0(1:ncv, 0, is), co%cvla(1:nfc, 0, is), cob&
&           %cvla(1:nfc, 0, is))
    CALL B2TXCC_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             dpa0(1:ncv, is), cob%dpa0(1:ncv, is), cdpa0(1:nfc, is), &
&             cdpa0b(1:nfc, is))
    CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             dpa0(1:ncv, is), cob%dpa0(1:ncv, is), co%cdpa(1:nfc, 1, is&
&             ), cob%cdpa(1:nfc, 1, is))
    CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             dpa0(1:ncv, is), cob%dpa0(1:ncv, is), co%cdpa(1:nfc, 0, is&
&             ), cob%cdpa(1:nfc, 0, is))
    CALL B2TXCY_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             dna0(1:ncv, is), cob%dna0(1:ncv, is), co%cdna(1:nfc, 1, is&
&             ), cob%cdna(1:nfc, 1, is))
    CALL B2TXCX_BWD(ncv, nfc, mode, geo, mpg, geo%fcvol, geo%fcs, co%&
&             dna0(1:ncv, is), cob%dna0(1:ncv, is), co%cdna(1:nfc, 0, is&
&             ), cob%cdna(1:nfc, 0, is))
  END DO
  CALL POPREAL8(switch%b2tqna_bb_ref, r8/8)
  CALL POPREAL8(switch%keps_fac, r8/8)
  CALL POPREAL8ARRAY(co%dna0, r8*SIZE(co%dna0, 1)*SIZE(co%dna0, 2)/8)
  CALL POPREAL8ARRAY(co%dpa0, r8*SIZE(co%dpa0, 1)*SIZE(co%dpa0, 2)/8)
  CALL POPREAL8ARRAY(co%vla0, r8*SIZE(co%vla0, 1)*SIZE(co%vla0, 2)*SIZE(&
&              co%vla0, 3)/8)
  CALL POPREAL8ARRAY(co%vma0, r8*SIZE(co%vma0, 1)*SIZE(co%vma0, 2)*SIZE(&
&              co%vma0, 3)/8)
  CALL POPREAL8ARRAY(co%vsa0, r8*SIZE(co%vsa0, 1)*SIZE(co%vsa0, 2)/8)
  CALL POPREAL8ARRAY(co%hci0, r8*SIZE(co%hci0, 1)/8)
  CALL POPREAL8ARRAY(co%hcn0, r8*SIZE(co%hcn0, 1)/8)
  CALL POPREAL8ARRAY(co%hcib, r8*SIZE(co%hcib, 1)*SIZE(co%hcib, 2)/8)
  CALL POPREAL8ARRAY(co%hce0, r8*SIZE(co%hce0, 1)/8)
  CALL POPREAL8ARRAY(co%sig0, r8*SIZE(co%sig0, 1)/8)
  CALL POPREAL8ARRAY(co%alf0, r8*SIZE(co%alf0, 1)/8)
  CALL POPREAL8ARRAY(co%dna_exb, r8*SIZE(co%dna_exb, 1)/8)
  CALL POPREAL8ARRAY(co%hce_exb, r8*SIZE(co%hce_exb, 1)/8)
  CALL POPREAL8ARRAY(co%hci_exb, r8*SIZE(co%hci_exb, 1)/8)
  CALL POPINTEGER4ARRAY(ndata, 2)
  CALL POPREAL8ARRAY(tdata, r8*3*nrr*nkind_coeff*(nss+1)/8)
  CALL POPINTEGER4ARRAY(addspec, nss*nkind_coeff*(nss+1))
  CALL POPBOOLEANARRAY(region_flags, cvregmax*nkind_coeff)
  CALL POPBOOLEAN(no_pflux)
  CALL POPBOOLEANARRAY(poloidal_scaling, nscale)
  CALL POPBOOLEAN(no_div)
  CALL POPINTEGER4(tr_ip_elm_count)
  CALL POPBOOLEAN(tr_ip_new_files)
  CALL POPREAL8(transport_ip_time_mod, r8/8)
  CALL POPREAL8(transport_ip_time_switch, r8/8)
  CALL POPREAL8ARRAY(cfdna, r8*nsdecl*8/8)
  CALL POPREAL8ARRAY(cfdpa, r8*nsdecl*8/8)
  CALL POPREAL8ARRAY(cfvla, r8*nsdecl*8/8)
  CALL POPREAL8ARRAY(cfvsa, r8*nsdecl*8/8)
  CALL POPREAL8ARRAY(cfhci, r8*nsdecl*8/8)
  CALL POPREAL8ARRAY(cfhce, r8*8/8)
  CALL POPREAL8ARRAY(cfsig, r8*8/8)
  CALL POPREAL8ARRAY(cfalf, r8*8/8)
  CALL POPREAL8ARRAY(cflim, r8*8/8)
  CALL POPCHARACTERARRAY(my_out_folder, 7)
  CALL POPINTEGER4(ncall_b2tqna)
  CALL POPINTEGER4(ncall_transp_keps)
  CALL POPREAL8(cutll, r8/8)
  CALL POPREAL8(cutlo, r8/8)
  CALL POPBOOLEAN(b2mod_math_initialised)
  dummyzerodiffb = 0.D0
! csc the last three arguments of the b2tqna_b call have been added 
! manually to save the sensitivity of transport coefficients in each
! cell of the domain
  CALL B2TQNA_B(ncv, nfc, nvx, ns, nscx, nscxmax, iscx, ismain, switch, &
&         switchb, geo, geob, mpg, mpgb, pl, plb, dv, dvb, rt, rtb, &
&         st_ext, co%dna0, cob%dna0, co%dpa0, cob%dpa0, co%vla0, cob%&
&         vla0, co%vma0, dummyzerodiffb, co%vsa0, cob%vsa0, co%hci0, cob&
&         %hci0, co%hcn0, cob%hcn0, co%hcib, cob%hcib, co%hce0, cob%hce0&
&         , co%sig0, cob%sig0, co%alf0, cob%alf0, hvi0, hvi0b, hve0, &
&         hve0b, co%dkt0, cob%dkt0, co%dzt0, cob%dzt0, co%dna_exb, cob%&
&         dna_exb, co%hce_exb, cob%hce_exb, co%hci_exb, cob%hci_exb)
END SUBROUTINE B2TRNO_B
