SUBROUTINE B2TRNO_NODIFF(ncv, nfc, nvx, ns, nscx, nscxmax, iscx, ismain&
& , switch, geo, mpg, pl, dv, rt, st_ext, co)
  USE B2MOD_TYPES
  USE B2MOD_CONSTANTS
  USE B2MOD_B2CMPA_DIFF
  USE B2MOD_B2CMPT_DIFF
  USE B2MOD_SWITCHES_DIFF
  USE B2US_GEO_DIFF
  USE B2US_MAP_DIFF
  USE B2US_PLASMA_DIFF
  USE B2MOD_AD_DIFF, ONLY : ncall_b2trno
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_TRANSPORT_NAMELIST_DIFF, ONLY : parm_hce, parm_hci, parm_dna&
& , parm_dpa, parm_vla, parm_vsa, parm_alf, parm_sig
  USE B2MOD_INPUT_PROFILE_DIFF, ONLY : ndata, no_pflux, tr_ip_new_files,&
& region_flags, tdata, tr_ip_elm_count, no_div, addspec, nrr, nss, &
& nkind_data, nkind_coeff, poloidal_scaling, transport_ip_filename, &
& nscale, transport_ip_time_switch, transport_ip_time_mod
  USE B2MOD_AD_DIFF, ONLY : my_out_folder, ncall_b2tqna, &
& ncall_transp_keps, nsdmax, cvregmax, ncall_b2tlh0
  USE B2MOD_MATH_DIFF, ONLY : cutlo, cutll, b2mod_math_initialised, &
& small_r4_constant
  USE B2MOD_SUBSYS
  IMPLICIT NONE
!   ..read data on first call
!   ..input arguments (unchanged on exit)
  INTEGER, INTENT(IN) :: ncv, nfc, nvx, ns, nscx, nscxmax, iscx(0:&
& nscxmax-1), ismain
  TYPE(SWITCHES), INTENT(INOUT) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(B2PLASMA), INTENT(IN) :: pl
  TYPE(B2DERIVATIVES), INTENT(INOUT) :: dv
  TYPE(B2RATES), INTENT(IN) :: rt
  TYPE(B2STATEEXT), INTENT(IN) :: st_ext
!   ..output arguments (unspecified on entry)
  TYPE(B2COEFF), INTENT(INOUT) :: co
!   ..common blocks
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2TRNO computes the anomalous transport coefficients, including
!     geometric factors such as vol/hx**2 and vol/hy**2.
!
!
!  3. description (see also routine b2cdca)
!
!     This routine computes the following discrete anomalous transport
!     coefficients.
!     - cdna: particle transport proportional to grad na.
!     - cdpa: particle transport proportional to grad pa.
!     - cvla: anomalous velocity.
!     - cvsa: viscosity.
!     - chce: electron thermal conductivity.
!     - chve: electron thermal strange velocity.
!     - chci: atom thermal conductivity.
!     - chvi: atom thermal strange velocity.
!     - csig: electrical conductivity.
!     - calf: thermo-electric coefficient.
!
!     The local differential transport coefficients, defined on cells,
!     are obtained by a calls to b2tqna. To compute from these the
!     discrete transport coefficients involves multiplication by
!     geometric factors (vol, vol/hx**2, vol/hy**2), and, usually,
!     interpolation to cell faces.
!
!     We treat the anomalous electrical conductivity separately, and
!     do not add it to the classical conductivity. See treatment in
!     b2tfch.
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: ifc, is, mode
!srv 17.06.08
  CHARACTER(len=3) :: chns
!srv 07.12.15
  INTEGER, SAVE :: flux_limit_style=2
!srv 20.09.17
  REAL(kind=r8) :: csig_stoch(nfc), chce_stoch(nfc)
  REAL(kind=r8) :: wrk(nfc), wrk1(nfc, 2), wrkc(ncv), hvi0(ncv, 0:1), &
& hve0(ncv, 0:1), flc(nfc, 0:1, 0:ns-1), flv(nfc, 0:1, 0:ns-1), cdpa0(&
& nfc, 0:ns-1), cvsa0(nfc, 0:ns-1), chcib(nfc, 0:ns-1)
  EXTERNAL XERTST, SFILL_NODIFF, smin, smax
!   ..procedures
  REAL(kind=r8) :: smin, smax
