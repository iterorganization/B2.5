      mask(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%cvnvp&
&       (icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv2
