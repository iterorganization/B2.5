    DO incv=1,mpg%vxcvp(ivx2, 2)
      t22 = t22 + 1/geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)
! contribution to equation of iCv1
    END DO
! contribution to equation of iCv2
    IF (solvereg(mpg%cvreg(icv1))) THEN
! contribution from iVx1
      DO incv=1,mpg%vxcvp(ivx1, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
        CALL PUSHBOOLEANARRAY(mask1(1:mpg%cvnvp(icv1, 2)), mpg%cvnvp(&
&                       icv1, 2))
        mask1(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv
! contribution from iVx2
      END DO
      DO incv=1,mpg%vxcvp(ivx2, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
        CALL PUSHBOOLEANARRAY(mask2(1:mpg%cvnvp(icv1, 2)), mpg%cvnvp(&
&                       icv1, 2))
        mask2(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv
      END DO
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
    END IF
    IF (solvereg(mpg%cvreg(icv2))) THEN
! contribution from iVx1
      DO incv=1,mpg%vxcvp(ivx1, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
        CALL PUSHBOOLEANARRAY(mask3(1:mpg%cvnvp(icv2, 2)), mpg%cvnvp(&
&                       icv2, 2))
        mask3(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv
! contribution from iVx2
      END DO
      DO incv=1,mpg%vxcvp(ivx2, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
        CALL PUSHBOOLEANARRAY(mask4(1:mpg%cvnvp(icv2, 2)), mpg%cvnvp(&
&                       icv2, 2))
        mask4(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv
      END DO
      t2b = 0.D0
      DO incv=mpg%vxcvp(ivx2, 2),1,-1
        t3 = 1/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
        t2b = t2b - t3*SUM(aab(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg&
&         %cvnvp(icv2, 2)-1), MASK=mask4(1:mpg%cvnvp(icv2, 2)))
        CALL POPBOOLEANARRAY(mask4(1:mpg%cvnvp(icv2, 2)), mpg%cvnvp(icv2&
&                      , 2))
      END DO
      t1b = 0.D0
      DO incv=mpg%vxcvp(ivx1, 2),1,-1
        t3 = 1/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
        t1b = t1b + t3*SUM(aab(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg&
&         %cvnvp(icv2, 2)-1), MASK=mask3(1:mpg%cvnvp(icv2, 2)))
        CALL POPBOOLEANARRAY(mask3(1:mpg%cvnvp(icv2, 2)), mpg%cvnvp(icv2&
&                      , 2))
      END DO
    ELSE
      t1b = 0.D0
      t2b = 0.D0
    END IF
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      DO incv=mpg%vxcvp(ivx2, 2),1,-1
        t3 = 1/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
        t2b = t2b + t3*SUM(aab(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg&
&         %cvnvp(icv1, 2)-1), MASK=mask2(1:mpg%cvnvp(icv1, 2)))
        CALL POPBOOLEANARRAY(mask2(1:mpg%cvnvp(icv1, 2)), mpg%cvnvp(icv1&
&                      , 2))
      END DO
      DO incv=mpg%vxcvp(ivx1, 2),1,-1
        t3 = 1/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
        t1b = t1b - t3*SUM(aab(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg&
&         %cvnvp(icv1, 2)-1), MASK=mask1(1:mpg%cvnvp(icv1, 2)))
        CALL POPBOOLEANARRAY(mask1(1:mpg%cvnvp(icv1, 2)), mpg%cvnvp(icv1&
&                      , 2))
      END DO
    END IF
    concb(ifc, 1) = concb(ifc, 1) - t2b - t1b
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      t1b = SUM(aab(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2&
&       , 2)-1), MASK=mask0(1:mpg%cvnvp(icv2, 2)))
      t2b = -aab(mpg%cvnvp(icv2, 1))
    ELSE
      t1b = 0.D0
      t2b = 0.D0
    END IF
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      t2b = t2b + SUM(aab(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%&
&       cvnvp(icv1, 2)-1), MASK=mask(1:mpg%cvnvp(icv1, 2)))
      t1b = t1b - aab(mpg%cvnvp(icv1, 1))
    END IF
    concb(ifc, 0) = concb(ifc, 0) - t2b - t1b
  END DO
!$BWD-OF II-LOOP 
  DO icv=1,ncv
    ireg = mpg%cvreg(icv)
    IF (solvereg(ireg)) THEN
      schb(icv, 1) = schb(icv, 1) - aab(mpg%cvnvp(icv, 1))
      schb(icv, 3) = schb(icv, 3) - ne(icv)*aab(mpg%cvnvp(icv, 1))
      neb(icv) = neb(icv) - sch(icv, 3)*aab(mpg%cvnvp(icv, 1))
      aab(mpg%cvnvp(icv, 1)) = 0.D0
    ELSE
      aab(mpg%cvnvp(icv, 1)) = 0.D0
    END IF
  END DO
END SUBROUTINE B2USPO_B

