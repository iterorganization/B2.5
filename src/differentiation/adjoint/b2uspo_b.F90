!$BWD-OF II-LOOP 
  DO ifc=1,nfc
! cell number of c1
    icv1 = mpg%fccv(ifc, 1)
! cell number of c2
    icv2 = mpg%fccv(ifc, 2)
!     ..contributions from conc(:,0) ('5-point' contributions)
