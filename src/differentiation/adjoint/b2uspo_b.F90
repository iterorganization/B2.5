&         name)
  CALL POPREAL8ARRAY(aa, r8*mpg%ncmxnv/8)
!$BWD-OF II-LOOP 
  DO icv=1,ncv
    lnonzero = .false.
    DO incv=1,mpg%cvnvp(icv, 2)
      lnonzero = lnonzero .OR. aa(mpg%cvnvp(icv, 1)+incv-1) .NE. 0.0_R8
    END DO
    IF (.NOT.lnonzero) THEN
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
    END IF
