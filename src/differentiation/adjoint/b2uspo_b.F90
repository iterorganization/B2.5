&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1):&
&           mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t3*t2
