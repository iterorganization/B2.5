!   ..extensive tests on first few calls
  IF (ncall_b2uspo .LT. 3) THEN
!    ..test sign of ne, te, conc, sch
    CALL B2XVSG(ncv, ne, 1, 'ne', '.gt.')
    CALL B2XVSG(ncv, te, 1, 'te', '.gt.')
    CALL B2XVSG(ncv, sch(1, 1), 1, 'sch1', '.le.')
    CALL B2XVSG(ncv, sch(1, 3), 1, 'sch3', '.le.')
  END IF
!
! ..compute the correction
  aa = 0.0_R8
!$AD II-LOOP
!   ..compute the nine-point matrix
  DO icv=1,ncv
    ireg = mpg%cvreg(icv)
    IF (solvereg(ireg)) THEN
      aa(mpg%cvnvp(icv, 1)) = -sch(icv, 1) - sch(icv, 3)*ne(icv)
    ELSE
      aa(mpg%cvnvp(icv, 1)) = 1.0_R8
    END IF
  END DO
!$AD II-LOOP
  DO ifc=1,nfc
! cell number of c1
    icv1 = mpg%fccv(ifc, 1)
! cell number of c2
    icv2 = mpg%fccv(ifc, 2)
!     ..contributions from conc(:,0) ('5-point' contributions)
    t1 = -conc(ifc, 0)
    t2 = -conc(ifc, 0)
    IF (solvereg(mpg%cvreg(icv1))) THEN
      aa(mpg%cvnvp(icv1, 1)) = aa(mpg%cvnvp(icv1, 1)) - t1
      mask(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%cvnvp&
&       (icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv2
      WHERE (mask(1:mpg%cvnvp(icv1, 2))) aa(mpg%cvnvp(icv1, 1):mpg%cvnvp&
&       (icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t2
    END IF
    IF (solvereg(mpg%cvreg(icv2))) THEN
      aa(mpg%cvnvp(icv2, 1)) = aa(mpg%cvnvp(icv2, 1)) - t2
      mask0(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%&
&       cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv1
      WHERE (mask0(1:mpg%cvnvp(icv2, 2))) aa(mpg%cvnvp(icv2, 1):mpg%&
&       cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1):mpg&
&         %cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) + t1
    END IF
!     ..contributions from conc(:,1) ('9-point' contributions)
! number of v1
    ivx1 = mpg%fcvx(ifc, 1)
! number of v2
    ivx2 = mpg%fcvx(ifc, 2)
    t1 = -conc(ifc, 1)
    t2 = -conc(ifc, 1)
    t11 = 0.0_R8
    DO incv=1,mpg%vxcvp(ivx1, 2)
      t11 = t11 + 1/geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)
    END DO
    t22 = 0.0_R8
    DO incv=1,mpg%vxcvp(ivx2, 2)
      t22 = t22 + 1/geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)
! contribution to equation of iCv1
    END DO
! contribution to equation of iCv2
    IF (solvereg(mpg%cvreg(icv1))) THEN
! contribution from iVx1
      DO incv=1,mpg%vxcvp(ivx1, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
        t3 = 1/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
        mask1(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv
        WHERE (mask1(1:mpg%cvnvp(icv1, 2))) aa(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1):&
&           mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) - t3*t1
! contribution from iVx2
      END DO
      DO incv=1,mpg%vxcvp(ivx2, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
        t3 = 1/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
        mask2(1:mpg%cvnvp(icv1, 2)) = mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) .EQ. icv
        WHERE (mask2(1:mpg%cvnvp(icv1, 2))) aa(mpg%cvnvp(icv1, 1):mpg%&
&         cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1):&
&           mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t3*t2
      END DO
    END IF
    IF (solvereg(mpg%cvreg(icv2))) THEN
! contribution from iVx1
      DO incv=1,mpg%vxcvp(ivx1, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
        t3 = 1/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
        mask3(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv
        WHERE (mask3(1:mpg%cvnvp(icv2, 2))) aa(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1):&
&           mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) + t3*t1
! contribution from iVx2
      END DO
      DO incv=1,mpg%vxcvp(ivx2, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
        t3 = 1/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
        mask4(1:mpg%cvnvp(icv2, 2)) = mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) .EQ. icv
        WHERE (mask4(1:mpg%cvnvp(icv2, 2))) aa(mpg%cvnvp(icv2, 1):mpg%&
&         cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1):&
&           mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) - t3*t2
      END DO
    END IF
  END DO
!$AD II-LOOP
!   ..correct RHS if not solving
  DO icv=1,ncv
    lnonzero = .false.
    DO incv=1,mpg%cvnvp(icv, 2)
      lnonzero = lnonzero .OR. aa(mpg%cvnvp(icv, 1)+incv-1) .NE. 0.0_R8
    END DO
    IF (.NOT.lnonzero) THEN
      aa(mpg%cvnvp(icv, 1)) = 1.0_R8
      respo(icv) = 0.0_R8
    END IF
    IF (.NOT.solvereg(mpg%cvreg(icv))) respo(icv) = 0.0_R8
  END DO
!
!   ..solve the correction equation
  WRITE(*, '(1x,a,a)') 'Calling b2uxus from '//TRIM(name)
!srv 18.03.10
  CALL B2UXUS(ncv, mpg, aa, itcnt, respo, corpo, name)
!
  IF (switch%b2uspo_iout .NE. 0) THEN
!srv 08.07.08 {
    DO k=0,3
      WRITE(chark, '(i1)') k
      arg1 = 'b2uspo_sch'//chark
      CALL MY_OUT_US(70, ncv, 0, sch(1, k), arg1)
    END DO
    CALL MY_OUT_US(70, ncv, 0, respo, 'b2uspo_respo')
    CALL MY_OUT_US(70, ncv, 0, corpo, 'b2uspo_corpo')
  END IF
!
! ..return
  ncall_b2uspo = ncall_b2uspo + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2uspo
!
END SUBROUTINE B2USPO_NODIFF

