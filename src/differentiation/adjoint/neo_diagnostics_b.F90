&           (ix, iy, 0)
        END IF
      END DO
      theta(ixref, iy) = thet
      ilpb = thet
      DO ix=-1,nx
        IF (on_closed_surface(ix, iy)) theta(ix, iy) = 2.0_R8*pi*theta(&
&           ix, iy)/ilpb
      END DO
    END IF
  END DO
!
!   ..calculate geometric parameters
!
!   ..loop over flux surfaces
  DO iy=0,ny
!
!   ..calculate neoclassical transport only for closed flux surfaces
!
!   ..calculate the connection length
    conn(iy) = 0.0_R8
    DO ix=-1,nx
      IF (on_closed_surface(ix, iy)) THEN
        IF (bb(ix, iy, 0) .GE. 0.) THEN
          abs3 = bb(ix, iy, 0)
        ELSE
          abs3 = -bb(ix, iy, 0)
        END IF
        conn(iy) = conn(iy) + hx(ix, iy)*bb(ix, iy, 3)/abs3
      END IF
    END DO
