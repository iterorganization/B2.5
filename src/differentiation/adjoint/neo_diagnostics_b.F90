    iyref = 2*topcut(1)/3
  ELSE IF (nnreg(0) .EQ. 5) THEN
    ixref = leftcut(1) + (rightcut(1)-leftcut(1))/2
    iyref = 2*topcut(1)/3
  ELSE IF (nnreg(0) .EQ. 7) THEN
    ixref = rightcut(1) - (rightcut(1)-leftcut(1))/4
    iyref = 2*topcut(1)/3
  ELSE IF (nnreg(0) .EQ. 8) THEN
    ixref = (rightcut(1)+rightcut(2))/2
