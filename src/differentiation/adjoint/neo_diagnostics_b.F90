    ixref = nx/2
    iyref = ny/2
  END IF
  CALL IPGETI('set_transport_ixref', ixref)
  CALL XERTST(-1 .LE. ixref .AND. ixref .LE. nx, &
&       'faulty argument set_transport_ixref')
  CALL IPGETI('set_transport_iyref', iyref)
  CALL XERTST(-1 .LE. iyref .AND. iyref .LE. ny, &
&       'faulty argument set_transport_iyref')
!   ..set n2
  n2 = (nx+2)*(ny+2)
!   ..test nx, ny, ns
  CALL XERTST(0 .LE. nx .AND. 0 .LE. ny, 'faulty argument nx, ny')
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!   ..extensive tests on first few calls
!   ..test bb
  CALL B2XVSG_NODIFF(n2, bb(-1, -1, 3), 1, 'bb3', '.gt.')
!   ..test state
  arg1 = n2*ns
  CALL B2XVSG_NODIFF(arg1, na, 1, 'na', '.gt.')
  CALL B2XVSG_NODIFF(n2, te, 1, 'te', '.gt.')
  CALL B2XVSG_NODIFF(n2, ti, 1, 'ti', '.gt.')
  arg1 = n2*2
  CALL B2XVSG_NODIFF(arg1, ni, 1, 'ni', '.gt.')
  CALL B2XVSG_NODIFF(n2, ne, 1, 'ne', '.gt.')
!
!   ..main
!
!   ..initialize densities and temperatures
  DO ispecies=0,ns
    t(ispecies) = 0.0_R8
    DO istate=0,nzdecl-1
      den(ispecies, istate) = 0.0_R8
      zsp(ispecies, istate) = 0.0_R8
    END DO
  END DO
!
!   ..species reordering and calculation of geometric parameters
!
!    ..loop voltage eparr (to be included in a separate routine!)
!     default value:
  eparr = 0.0_R8
!     read scalar from b2mn.dat
  CALL IPGETR('b2tqna_eparr', eparr)
  IF (eparr .NE. 0.0_R8 .OR. b2plot_debug .GE. 3) WRITE(*, *) &
&                                                'Loop voltage: eparr ='&
&                                                 , eparr
