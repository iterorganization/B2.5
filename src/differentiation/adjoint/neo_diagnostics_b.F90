!
!   ..now calculate the neoclassical transport
!
!   ..loop over flux surfaces
  DO iy=0,ny
!
!   ..calculate neoclassical transport only for closed flux surfaces
    IF (closedf(iy)) THEN
!
!   ..flux surfaces averages
!     temperatures in keV, densities in 10^19 m^-3
      DO ispecies=0,nsl-2
        result1 = FLUXAV(ti, bb, hx, vol, nx, ny, iy)
        t(ispecies) = result1/(1.0e3_R8*ev)
        result1 = FLUXAV(ti, bb, hx, vol, nx, ny, bottomiy(ixref, iy))
        tl(ispecies) = result1/(1.0e3_R8*ev)
        result1 = TFLUXAV(ti, bb, hx, vol, nx, ny, iy)
        tr(ispecies) = result1/(1.0e3_R8*ev)
        DO istate=0,ncl(ispecies)-1
          quant(:, :) = na(:, :, isort(ispecies, istate))
          result1 = FLUXAV(quant, bb, hx, vol, nx, ny, iy)
          den(ispecies, istate) = result1/1.0e19_R8
          result1 = FLUXAV(quant, bb, hx, vol, nx, ny, bottomiy(ixref, &
&           iy))
          denl(ispecies, istate) = result1/1.0e19_R8
          result1 = TFLUXAV(quant, bb, hx, vol, nx, ny, iy)
          denr(ispecies, istate) = result1/1.0e19_R8
          quant(:, :) = rza(:, :, isort(ispecies, istate))
          zsp(ispecies, istate) = FLUXAV(quant, bb, hx, vol, nx, ny, iy)
        END DO
      END DO
      result1 = FLUXAV(te, bb, hx, vol, nx, ny, iy)
      t(nsl-1) = result1/(1.0e3_R8*ev)
      result1 = FLUXAV(ne, bb, hx, vol, nx, ny, iy)
      den(nsl-1, 0) = result1/1.0e19_R8
      result1 = FLUXAV(te, bb, hx, vol, nx, ny, bottomiy(ixref, iy))
      tl(nsl-1) = result1/(1.e3_R8*ev)
      result1 = FLUXAV(ne, bb, hx, vol, nx, ny, bottomiy(ixref, iy))
      denl(nsl-1, 0) = result1/1.e19_R8
      result1 = TFLUXAV(te, bb, hx, vol, nx, ny, iy)
      tr(nsl-1) = result1/(1.0e3_R8*ev)
      result1 = TFLUXAV(ne, bb, hx, vol, nx, ny, iy)
      denr(nsl-1, 0) = result1/1.0e19_R8
!
!
!   ..thermodynamic forces
      DO ispecies=0,nsl-1
        DO istate=0,ncl(ispecies)-1
          result1 = HY1(bottomix(ixref, iy), bottomiy(ixref, iy))
          result2 = HY1(ixref, iy)
          ds(ispecies, istate, 1) = (LOG(denr(ispecies, istate)*tr(&
&           ispecies))-LOG(denl(ispecies, istate)*tl(ispecies)))/(&
&           result1+result2)
          result1 = HY1(bottomix(ixref, iy), bottomiy(ixref, iy))
          result2 = HY1(ixref, iy)
          ds(ispecies, istate, 2) = (LOG(tr(ispecies))-LOG(tl(ispecies))&
&           )/(result1+result2)
!
!
!   ..for consistency check
          densav(ispecies, istate, iy) = den(ispecies, istate)
          gradnsav(ispecies, istate, iy) = den(ispecies, istate)*(ds(&
&           ispecies, istate, 1)-ds(ispecies, istate, 2))
          tforce(ispecies, istate, 1, iy) = ds(ispecies, istate, 1)
          tforce(ispecies, istate, 2, iy) = ds(ispecies, istate, 2)
        END DO
      END DO
!
!   ..recover geometric parameters for the flux surface iy
      bav = bav_(iy)
      b2av = b2av_(iy)
      bi2a = bi2a_(iy)
      rbt = rbt_(iy)
      bgradp = bgradp_(iy)
      dpsidr = dpsidr_(iy)
      rnq = rnq_(iy)
      fc = fc_(iy)
      gclass = gclass_(iy)
      DO is2=1,mmx
        fm(is2) = fm_(is2, iy)
      END DO
      r2i = r2i_(iy)
      rho = rho_(iy)
!
!    ..calculate the diffusion coefficients
      CALL NEODV(ns, nzdecl, nsl, ncl, zsp, ic, m, t, den, ds, rho, &
&          eparr, epsn, dd(:, :, iy), vvn(:, :, iy), vtn(:, :, iy))
!
!    ..calculate the heat conduction coefficients
      CALL NEOCHI(ns, nzdecl, nsl, ncl, zsp, ic, m, t, den, ds, rho, &
&           eparr, epsn, kappa(:, :, iy), chi(:, :, iy), vw(:, :, iy))
!
!    ..calculate thermal conductivity and heat pinch from
!        q(i,j) = - kappa(i,j)*grad.T(i) + vw(i,j)*T(i)
!               = - kappa(i,j)*grad.T(i) + den(i,j)*vwt(i,j)*T(i)
      DO ispecies=0,nsl-1
        DO istate=0,ncl(ispecies)-1
          vwt(ispecies, istate, iy) = vw(ispecies, istate, iy)/(den(&
&           ispecies, istate)*1e19)
        END DO
      END DO
    ELSE
!
      DO ispecies=0,nsl-1
        DO istate=0,ncl(ispecies)-1
          dd(ispecies, istate, iy) = 0._R8
          vvn(ispecies, istate, iy) = 0._R8
          chi(ispecies, istate, iy) = 0._R8
          kappa(ispecies, istate, iy) = 0._R8
          vwt(ispecies, istate, iy) = 0._R8
          densav(ispecies, istate, iy) = 0._R8
          gradnsav(ispecies, istate, iy) = 0._R8
          tforce(ispecies, istate, 1, iy) = 0._R8
          tforce(ispecies, istate, 2, iy) = 0._R8
        END DO
      END DO
    END IF
  END DO
!
!
!    ..set transport coefficients on boundary by symmetric condition
  DO ispecies=0,nsl-1
    DO istate=0,ncl(ispecies)-1
      densav(ispecies, istate, -1) = densav(ispecies, istate, 0)
      gradnsav(ispecies, istate, -1) = gradnsav(ispecies, istate, 0)
      tforce(ispecies, istate, 1, -1) = tforce(ispecies, istate, 1, 0)
      tforce(ispecies, istate, 2, -1) = tforce(ispecies, istate, 2, 0)
      dd(ispecies, istate, -1) = dd(ispecies, istate, 0)
      vvn(ispecies, istate, -1) = vvn(ispecies, istate, 0)
      chi(ispecies, istate, -1) = chi(ispecies, istate, 0)
      kappa(ispecies, istate, -1) = kappa(ispecies, istate, 0)
      vwt(ispecies, istate, -1) = vwt(ispecies, istate, 0)
    END DO
  END DO
!
!    ..calculate the transport coefficients on cell centers
  DO iy=-1,ny
    DO ispecies=0,nsl-1
      DO istate=0,ncl(ispecies)-1
        cellface(ispecies, istate, iy, 1) = dd(ispecies, istate, iy)
        cellface(ispecies, istate, iy, 2) = vvn(ispecies, istate, iy)
        cellface(ispecies, istate, iy, 3) = chi(ispecies, istate, iy)
        cellface(ispecies, istate, iy, 4) = kappa(ispecies, istate, iy)
        cellface(ispecies, istate, iy, 5) = vwt(ispecies, istate, iy)
      END DO
    END DO
  END DO
!
  DO iy=0,ny
    IF (on_closed_surface(ixref, iy)) THEN
      IF (iy .EQ. ny) THEN
        result1 = HY1(ixref, iy)
        result2 = HY1(bottomix(ixref, iy), bottomiy(ixref, iy))
        prefactor = -(result1/(2.0_R8*result2))
        result1 = HY1(ixref, iy)
        result2 = HY1(bottomix(ixref, iy), bottomiy(ixref, iy))
        postfactor = 1.0_R8 + result1/(2.0_R8*result2)
        ishift = bottomiy(ixref, iy)
      ELSE IF (on_closed_surface(topix(ixref, iy), topiy(ixref, iy))) &
&     THEN
        prefactor = 0.5_R8
        postfactor = 0.5_R8
        ishift = iy
      ELSE
        result1 = HY1(ixref, iy)
        result2 = HY1(bottomix(ixref, iy), bottomiy(ixref, iy))
        prefactor = -(result1/(2.0_R8*result2))
        result1 = HY1(ixref, iy)
        result2 = HY1(bottomix(ixref, iy), bottomiy(ixref, iy))
        postfactor = 1.0_R8 + result1/(2.0_R8*result2)
        ishift = bottomiy(ixref, iy)
      END IF
      DO ispecies=0,nsl-1
        DO istate=0,ncl(ispecies)-1
          dd(ispecies, istate, iy) = cellface(ispecies, istate, ishift, &
&           1)*prefactor + cellface(ispecies, istate, topiy(ixref, &
&           ishift), 1)*postfactor
          vvn(ispecies, istate, iy) = cellface(ispecies, istate, ishift&
&           , 2)*prefactor + cellface(ispecies, istate, topiy(ixref, &
&           ishift), 2)*postfactor
          chi(ispecies, istate, iy) = cellface(ispecies, istate, ishift&
&           , 3)*prefactor + cellface(ispecies, istate, topiy(ixref, &
&           ishift), 3)*postfactor
          kappa(ispecies, istate, iy) = cellface(ispecies, istate, &
&           ishift, 4)*prefactor + cellface(ispecies, istate, topiy(&
&           ixref, ishift), 4)*postfactor
          vwt(ispecies, istate, iy) = cellface(ispecies, istate, ishift&
&           , 5)*prefactor + cellface(ispecies, istate, topiy(ixref, &
&           ishift), 5)*postfactor
        END DO
      END DO
    END IF
  END DO
!
!     ..calculate transport coefficients at inner boundary by
!       a mixture of extrapolation and symmetry
  result1 = HY1(ixref, -1)
  result2 = HY1(topix(ixref, -1), topiy(ixref, -1))
  result3 = HY1(topix(ixref, -1), topiy(ixref, -1))
  result4 = HY1(topix(topix(ixref, -1), topiy(ixref, -1)), topiy(topix(&
&   ixref, -1), topiy(ixref, -1)))
  prefactor = 0.5_R8*(result1+result2)/(result3+result4)
  DO ispecies=0,nsl-1
    DO istate=0,ncl(ispecies)-1
      IF (on_closed_surface(ixref, -1)) THEN
        dd(ispecies, istate, -1) = dd(ispecies, istate, topiy(ixref, -1)&
&         ) + prefactor*(dd(ispecies, istate, topiy(ixref, -1))-dd(&
&         ispecies, istate, topiy(topix(ixref, -1), topiy(ixref, -1))))
        vvn(ispecies, istate, -1) = vvn(ispecies, istate, topiy(ixref, -&
&         1)) + prefactor*(vvn(ispecies, istate, topiy(ixref, -1))-vvn(&
&         ispecies, istate, topiy(topix(ixref, -1), topiy(ixref, -1))))
        chi(ispecies, istate, -1) = chi(ispecies, istate, topiy(ixref, -&
&         1)) + prefactor*(chi(ispecies, istate, topiy(ixref, -1))-chi(&
&         ispecies, istate, topiy(topix(ixref, -1), topiy(ixref, -1))))
        kappa(ispecies, istate, -1) = kappa(ispecies, istate, topiy(&
&         ixref, -1)) + prefactor*(kappa(ispecies, istate, topiy(ixref, &
&         -1))-kappa(ispecies, istate, topiy(topix(ixref, -1), topiy(&
&         ixref, -1))))
        vwt(ispecies, istate, -1) = vwt(ispecies, istate, topiy(ixref, -&
&         1)) + prefactor*(vwt(ispecies, istate, topiy(ixref, -1))-vwt(&
&         ispecies, istate, topiy(topix(ixref, -1), topiy(ixref, -1))))
      ELSE
        dd(ispecies, istate, -1) = 0.0_R8
        vvn(ispecies, istate, -1) = 0.0_R8
        chi(ispecies, istate, -1) = 0.0_R8
        kappa(ispecies, istate, -1) = 0.0_R8
        vwt(ispecies, istate, -1) = 0.0_R8
      END IF
    END DO
  END DO
!
!    ..determine the neoclassical contribution to the transport coefficients
!       assuming the following structure for the particle and heat flux
!         gamma(i,j) = - dd(i,j)*grad.n(i,j) + vvn(i,j)*den(i,j)
!           already including the ware pinch, and
!         q(i,j) = - kappa(i,j)*grad.T(i) + den(i,j)*vwt(i,j)*T(i)
!       also including the ware pinch
!       coefficients: dn_neo, chi_neo, vn_neo, and vw_neo
!
!    ..initialize transport coefficients
  dn_neo(:, :, :) = 0.0_R8
  chi_neo(:, :, :) = 0.0_R8
  vn_neo(:, :, :) = 0.0_R8
  vw_neo(:, :, :) = 0.0_R8
!
  DO ix=-1,nx
    DO iy=-1,ny
      IF (on_closed_surface(ix, iy)) THEN
        DO ispecies=0,nsl-2
          DO istate=0,ncl(ispecies)-1
            is = isort(ispecies, istate)
            result1 = HY1(ix, iy)
            result2 = HY1(ixref, iy)
            dn_neo(ix, iy, is) = dd(ispecies, istate, iy)*(result1/&
&             result2)**2
            result1 = HY1(ix, iy)
            result2 = HY1(ixref, iy)
            vn_neo(ix, iy, is) = vvn(ispecies, istate, iy)*(result1/&
&             result2)
            result1 = HY1(ix, iy)
            result2 = HY1(ixref, iy)
            dfhcb = chi(ispecies, istate, iy)*(result1/result2)**2
            chi_neo(ix, iy, is) = dfhcb
            result1 = HY1(ix, iy)
            result2 = HY1(ixref, iy)
            vw_neo(ix, iy, is) = vwt(ispecies, istate, iy)*(result1/&
&             result2)
          END DO
        END DO
        result1 = HY1(ix, iy)
        result2 = HY1(ixref, iy)
        dfhce = chi(nsl-1, 0, iy)*(result1/result2)**2
        IF (dfhce .LT. 0) WRITE(*, *) 'dfhce(', ix, ',', iy, ')=', dfhce
        result1 = HY1(ix, iy)
        result2 = HY1(ixref, iy)
        dn_neo(ix, iy, ns) = dd(nsl-1, 0, iy)*(result1/result2)**2
        result1 = HY1(ix, iy)
        result2 = HY1(ixref, iy)
        vn_neo(ix, iy, ns) = vvn(nsl-1, 0, iy)*(result1/result2)
        chi_neo(ix, iy, ns) = dfhce
        result1 = HY1(ix, iy)
        result2 = HY1(ixref, iy)
        vw_neo(ix, iy, ns) = vwt(nsl-1, 0, iy)*(result1/result2)
!
!   ..set transport to zero in the SOL and divertor regions
      ELSE IF (.NOT.on_closed_surface(ix, iy)) THEN
        DO ispecies=0,nsl-1
          DO istate=0,ncl(ispecies)-1
            is = isort(ispecies, istate)
            dn_neo(ix, iy, is) = 0.0_R8
            vn_neo(ix, iy, is) = 0.0_R8
            chi_neo(ix, iy, is) = 0.0_R8
            vw_neo(ix, iy, is) = 0.0_R8
          END DO
        END DO
      END IF
    END DO
  END DO
!
!   ..return
  CALL SUBEND()
  RETURN
END SUBROUTINE NEO_DIAGNOSTICS_NODIFF

