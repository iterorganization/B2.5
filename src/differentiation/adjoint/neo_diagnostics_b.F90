    b2av_(iy) = 0.0_R8
    bi2a_(iy) = 0.0_R8
    rbt_(iy) = 0.0_R8
    bgradp_(iy) = 0.0_R8
    dpsidr_(iy) = 0.0_R8
    rnq_(iy) = 0.0_R8
    fc_(iy) = 0.0_R8
    gclass_(iy) = 0.0_R8
    DO is=1,mmx
      fm_(is, iy) = 0.0_R8
    END DO
    r2i_(iy) = 0.0_R8
    rho_(iy) = 0.0_R8
  END DO
!
!   ..split species into species and charge states
  DO ispecies=0,ns
    ncl(ispecies) = 0
    DO istate=0,nzdecl-1
      isort(ispecies, istate) = 0
    END DO
