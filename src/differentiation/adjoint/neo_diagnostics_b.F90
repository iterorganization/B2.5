        abs1 = amh(is) - amcomp
      ELSE
        abs1 = -(amh(is)-amcomp)
      END IF
      IF (abs1 .LT. 0.5_R8) istate = istate + 1
    END IF
  END DO
!
  ncl(ispecies-1) = istate
!
  nsl = ispecies
!
  is = 0
  DO ispecies=0,nsl-1
    DO istate=0,ncl(ispecies)-1
      DO is2=0,ns-1
        IF (am(is2) - amh(is) .GE. 0.) THEN
