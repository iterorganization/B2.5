          theta(ix, iy) = thet
          hxface = (vol(ix, iy)*hx(bottomix(ix, iy), bottomiy(ix, iy))+&
&           vol(bottomix(ix, iy), bottomiy(ix, iy))*hx(ix, iy))/(vol(&
&           bottomix(ix, iy), bottomiy(ix, iy))+vol(ix, iy))
          hxpface = (vol(leftix(ix, iy), leftiy(ix, iy))*hx(bottomix(&
&           leftix(ix, iy), leftiy(ix, iy)), bottomiy(leftix(ix, iy), &
&           leftiy(ix, iy)))+vol(bottomix(leftix(ix, iy), leftiy(ix, iy)&
&           ), bottomiy(leftix(ix, iy), leftiy(ix, iy)))*hx(leftix(ix, &
&           iy), leftiy(ix, iy)))/(vol(bottomix(leftix(ix, iy), leftiy(&
&           ix, iy)), bottomiy(leftix(ix, iy), leftiy(ix, iy)))+vol(&
&           leftix(ix, iy), leftiy(ix, iy)))
          hxnface = (vol(rightix(ix, iy), rightiy(ix, iy))*hx(bottomix(&
&           rightix(ix, iy), rightiy(ix, iy)), bottomiy(rightix(ix, iy)&
&           , rightiy(ix, iy)))+vol(bottomix(rightix(ix, iy), rightiy(ix&
&           , iy)), bottomiy(rightix(ix, iy), rightiy(ix, iy)))*hx(&
&           rightix(ix, iy), rightiy(ix, iy)))/(vol(bottomix(rightix(ix&
&           , iy), rightiy(ix, iy)), bottomiy(rightix(ix, iy), rightiy(&
&           ix, iy)))+vol(rightix(ix, iy), rightiy(ix, iy)))
