!    ..determine closed field lines
  DO iy=0,ny
    closedf(iy) = .false.
    ix = rightix(ixref, iy)
    DO WHILE (ix .NE. ixref .AND. ix .GE. -1 .AND. ix .LE. nx)
      ix = rightix(ix, iy)
    END DO
