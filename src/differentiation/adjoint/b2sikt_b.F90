      ELSE
        CALL POPREAL8(fac_diss, r8/8)
        switchb%b2sikt_fac_diss = switchb%b2sikt_fac_diss + fac_dissb
        CALL POPREAL8(fac_sheath, r8/8)
        switchb%b2sikt_fac_sheath = switchb%b2sikt_fac_sheath + &
&         fac_sheathb
      END IF
    END DO
    CALL POPCONTROL2B(branch)
    IF (branch .LT. 2) THEN
      IF (branch .EQ. 0) THEN
        csfsb = 0.D0
        GOTO 100
      ELSE
        dktcb = 0.D0
        cob%sigx_kt = cob%sigx_kt + dktc**2*geo%cvvol*wrk3b
        dktcb = 2*dktc*co%sigx_kt*geo%cvvol*wrk3b
        arg10b = 0.D0
        wrkvb = 0.D0
        CALL GRADC_P_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, arg10, &
&                  arg10b, wrkv, wrkvb, dktc, dktcb)
        WHERE (.NOT.pl%kt .EQ. 0.D0) plb%kt = plb%kt + arg10b/(2.0*SQRT(&
&           pl%kt))
      END IF
    ELSE IF (branch .EQ. 2) THEN
      tempb7 = geo%cvvol*geo%cvbb(:, 3)**2*wrk3b/(geo%cvbb(:, 0)**2*&
&       switch%b2sikt_lpar_ref**2)
      cob%sigx_kt = cob%sigx_kt + pl%kt*tempb7
      plb%kt = plb%kt + co%sigx_kt*tempb7
      wrkvb = 0.D0
    ELSE
      tempb7 = geo%cvvol*geo%cvbb(:, 3)**2*wrk3b/(geo%cvbb(:, 0)**2*geo%&
&       cvconn**2)
      cob%sigx_kt = cob%sigx_kt + pl%kt*tempb7
      plb%kt = plb%kt + co%sigx_kt*tempb7
      wrkvb = 0.D0
    END IF
    csfsb = 0.D0
    dvb%ni(:, 1) = dvb%ni(:, 1) + csfs*geo%cvvol*wrk2b/connfs
    csfsb = dv%ni(:, 1)*geo%cvvol*wrk2b/connfs
    dtecb = 0.D0
    dnacb = 0.D0
    dlbcb = 0.D0
    CALL POPREAL8ARRAY(wrk1, r8*ncv/8)
    tempb1 = geo%cvvol*wrk1b
    tempb6 = (dnac(:, 0)*dlbc(:, 0)+dnac(:, 1)*dlbc(:, 1))*tempb1
    tempb4 = pl%te*co%dna_exb*tempb1
    cob%hce_exb = cob%hce_exb + (dtec(:, 0)*dlbc(:, 0)+dtec(:, 1)*dlbc(:&
&     , 1))*switch%b2sikt_fac_aniso*2.0_R8*tempb1/3.0_R8
    tempb3 = co%hce_exb*switch%b2sikt_fac_aniso*2.0_R8*tempb1/3.0_R8
    dtecb(:, 0) = dtecb(:, 0) + dlbc(:, 0)*tempb3
    dlbcb(:, 0) = dlbcb(:, 0) + dtec(:, 0)*tempb3 + dnac(:, 0)*tempb4
    dtecb(:, 1) = dtecb(:, 1) + dlbc(:, 1)*tempb3
    dlbcb(:, 1) = dlbcb(:, 1) + dtec(:, 1)*tempb3 + dnac(:, 1)*tempb4
    dnacb(:, 0) = dnacb(:, 0) + dlbc(:, 0)*tempb4
    dnacb(:, 1) = dnacb(:, 1) + dlbc(:, 1)*tempb4
    plb%te = plb%te + co%dna_exb*tempb6
    dticb = 0.D0
    CALL POPREAL8ARRAY(wrk0, r8*ncv/8)
    tempb1 = geo%cvvol*wrk0b
    tempb5 = (dnac(:, 0)*dlbc(:, 0)+dnac(:, 1)*dlbc(:, 1))*tempb1
    cob%dna_exb = cob%dna_exb + pl%te*tempb6 + pl%ti*tempb5
    tempb4 = pl%ti*co%dna_exb*tempb1
    cob%hci_exb = cob%hci_exb + (dtic(:, 0)*dlbc(:, 0)+dtic(:, 1)*dlbc(:&
&     , 1))*switch%b2sikt_fac_aniso*2.0_R8*tempb1/3.0_R8
    tempb3 = co%hci_exb*switch%b2sikt_fac_aniso*2.0_R8*tempb1/3.0_R8
    dticb(:, 0) = dticb(:, 0) + dlbc(:, 0)*tempb3
    dlbcb(:, 0) = dlbcb(:, 0) + dtic(:, 0)*tempb3 + dnac(:, 0)*tempb4
    dticb(:, 1) = dticb(:, 1) + dlbc(:, 1)*tempb3
    dlbcb(:, 1) = dlbcb(:, 1) + dtic(:, 1)*tempb3 + dnac(:, 1)*tempb4
    dnacb(:, 0) = dnacb(:, 0) + dlbc(:, 0)*tempb4
    dnacb(:, 1) = dnacb(:, 1) + dlbc(:, 1)*tempb4
    plb%ti = plb%ti + co%dna_exb*tempb5
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      wrk2 = wrk0*dlbc(:, 0) + wrk1*dlbc(:, 1)
      tempb0 = -((am(ismain)*mp*(co%dna_exb*(switch%b2sikt_fac_vis_rs*&
&       wrk2))+pl%kt)*geo%cvvol*2.0_R8*wrksb)
      wrkg0b = 0.D0
      wrkg1b = 0.D0
      wrkg2b = 0.D0
      tempb = geo%cvvol*am(ismain)*mp*2.0_R8*wrksb
      tempb2 = (wrkg0(:, 0)**2+wrkg1(:, 1)**2+0.5_R8*(wrkg0(:, 1)**2+&
&       wrkg1(:, 0)**2+wrkg2(:, 0)**2+wrkg2(:, 1)**2)+wrkg1(:, 0)*wrkg0(&
&       :, 1))*tempb
      tempb3 = switch%b2sikt_fac_vis_rs*co%dna_exb*pl%na(:, ismain)*&
&       tempb
      wrkg0b(:, 0) = wrkg0b(:, 0) + 2*wrkg0(:, 0)*tempb3
      wrkg1b(:, 1) = wrkg1b(:, 1) + 2*wrkg1(:, 1)*tempb3
      tempb4 = 0.5_R8*tempb3
      wrkg1b(:, 0) = wrkg1b(:, 0) + wrkg0(:, 1)*tempb3 + 2*wrkg1(:, 0)*&
&       tempb4
      wrkg0b(:, 1) = wrkg0b(:, 1) + wrkg1(:, 0)*tempb3 + 2*wrkg0(:, 1)*&
&       tempb4
      wrkg2b(:, 0) = wrkg2b(:, 0) + 2*wrkg2(:, 0)*tempb4
      wrkg2b(:, 1) = wrkg2b(:, 1) + 2*wrkg2(:, 1)*tempb4
      plb%na(:, ismain) = plb%na(:, ismain) + switch%b2sikt_fac_vis_rs*&
&       co%dna_exb*tempb2 + wrk2*tempb0/3.0_R8
      wrk2b = 0.D0
      wrkfb = 0.D0
      CALL GRADC_DIV_B(ncv, nfc, nvx, 1, geo, geob, mpg, mpgb, wrk2, &
&                wrk2b, wrkf, wrkfb, wrkg2, wrkg2b)
      wrk0b = 0.D0
      wrk0b = geo%cvbb(:, 3)*wrk2b/geo%cvbb(:, 2)
      dvb%vaecrb(:, 0, ismain) = dvb%vaecrb(:, 0, ismain) + geo%fcbb(:, &
&       3)*wrkfb/geo%fcbb(:, 2)
      CALL POPREAL8ARRAY(dv%vaecrb(:, 1, ismain), r8*SIZE(dv%vaecrb, 1)/&
&                  8)
      wrk1b = 0.D0
      CALL GRADC_DIV_B(ncv, nfc, nvx, 1, geo, geob, mpg, mpgb, wrk1, &
&                wrk1b, dv%vaecrb(:, 1, ismain), dvb%vaecrb(:, 1, ismain&
&                ), wrkg1, wrkg1b)
      CALL POPREAL8ARRAY(dv%vaecrb(:, 0, ismain), r8*SIZE(dv%vaecrb, 1)/&
&                  8)
      CALL GRADC_DIV_B(ncv, nfc, nvx, 1, geo, geob, mpg, mpgb, wrk0, &
&                wrk0b, dv%vaecrb(:, 0, ismain), dvb%vaecrb(:, 0, ismain&
&                ), wrkg0, wrkg0b)
      wrk2b = 0.D0
      tempb = -(pl%na(:, ismain)*wrk2*geo%cvvol*2.0_R8*wrksb/3.0_R8)
      tempb1 = am(ismain)*mp*tempb
      switchb%b2sikt_fac_vis_rs = switchb%b2sikt_fac_vis_rs + SUM(co%&
&       dna_exb*pl%na(:, ismain)*tempb2) + SUM(wrk2*co%dna_exb*tempb1)
      cob%dna_exb = cob%dna_exb + switch%b2sikt_fac_vis_rs*pl%na(:, &
&       ismain)*tempb2 + switch%b2sikt_fac_vis_rs*wrk2*tempb1
      plb%kt = plb%kt + tempb
      wrk2b = switch%b2sikt_fac_vis_rs*co%dna_exb*tempb1 + pl%na(:, &
&       ismain)*tempb0/3.0_R8
      wrk0b = wrk0b + dlbc(:, 0)*wrk2b
      dlbcb(:, 0) = dlbcb(:, 0) + wrk0*wrk2b
      wrk1b = wrk1b + dlbc(:, 1)*wrk2b
      dlbcb(:, 1) = dlbcb(:, 1) + wrk1*wrk2b
      CALL INTCELL_BWD(nfc, ncv, mpg, mpg%intcellr, dv%vaecrb(:, 1, &
&                ismain), dvb%vaecrb(:, 1, ismain), wrk1, wrk1b)
      CALL INTCELL_BWD(nfc, ncv, mpg, mpg%intcellp, dv%vaecrb(:, 0, &
&                ismain), dvb%vaecrb(:, 0, ismain), wrk0, wrk0b)
    END IF
    CALL POPREAL8ARRAY(dtic, r8*ncv*2/8)
    CALL GRADC_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, pl%ti, plb%ti, &
&          wrkv, wrkvb, dtic, dticb)
    CALL POPREAL8ARRAY(dtec, r8*ncv*2/8)
    CALL GRADC_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, pl%te, plb%te, &
&          wrkv, wrkvb, dtec, dtecb)
    CALL POPREAL8ARRAY(dlbc, r8*ncv*2/8)
    wrksb = 0.D0
    CALL GRADC_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, wrks, wrksb, &
&          wrkv, wrkvb, dlbc, dlbcb)
    CALL POPREAL8ARRAY(dnac, r8*ncv*2/8)
    CALL GRADC_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, pl%na(:, ismain&
&          ), plb%na(:, ismain), wrkv, wrkvb, dnac, dnacb)
 100 CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      csb = 0.D0
      csb = csfsb
    ELSE
      csb = 0.D0
      csb(omp(icsepomp)) = csb(omp(icsepomp)) + SUM(csfsb)
    END IF
    rzb = 0.D0
    pzb = 0.D0
    WHERE (pz/rz .EQ. 0.D0) 
      tempb = 0.D0
    ELSEWHERE
      tempb = csb/(rz*2.0*SQRT(pz/rz))
    END WHERE
    pzb = tempb
    rzb = -(pz*tempb/rz)
    CALL B2XPPZ_B(ncv, ns, dv%ne, dvb%ne, pl%na, plb%na, pl%te, plb%te, &
&           pl%ti, plb%ti, pz, pzb, st_ext)
    CALL B2XPRZ_B(ncv, ns, mp, am, pl%na, plb%na, rz, rzb, st_ext)
  END IF
  arg1 = ncv*2
  arg1 = ncv*2
  arg1 = ncv*2
  CALL POPINTEGER4(arg1)
END SUBROUTINE B2SIKT_B

