        plb%na(:, is) = plb%na(:, is) + na0b(:, is)
        na0b(:, is) = 0.D0
      ELSE
        DO icv=ncv,1,-1
          tempb = ua0b(icv, is)/na0(icv, is)
          ua0b(icv, is) = 0.D0
          plb%ua(icv, is) = plb%ua(icv, is) + pl%na(icv, is)*tempb
          na0b(icv, is) = na0b(icv, is) - (ua_tmp*n0_tmp+pl%ua(icv, is)*&
&           pl%na(icv, is))*tempb/na0(icv, is)
          plb%na(icv, is) = plb%na(icv, is) + pl%ua(icv, is)*tempb + &
&           na0b(icv, is)
          CALL POPREAL8(na0(icv, is), r8/8)
          na0b(icv, is) = 0.D0
          CALL POPCONTROL2B(branch)
          IF (branch .LT. 2) THEN
            IF (branch .EQ. 0) THEN
              CALL POPREAL8(ua_tmp, r8/8)
            ELSE
              CALL POPREAL8(ua_tmp, r8/8)
            END IF
          ELSE IF (branch .EQ. 2) THEN
            CALL POPREAL8(ua_tmp, r8/8)
          ELSE
            CALL POPREAL8(ua_tmp, r8/8)
          END IF
          DO ia=nnatmi,1,-1
            CALL POPCONTROL1B(branch)
          END DO
          CALL POPREAL8(n0_tmp, r8/8)
        END DO
      END IF
    END DO
  END IF
  srwb%sch0 = 0.D0
  srwb%shi0 = 0.D0
  srwb%she0 = 0.D0
  srwb%smo0 = 0.D0
  srwb%sna0 = 0.D0
END SUBROUTINE B2STBM_B

