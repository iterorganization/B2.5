SUBROUTINE B2SQEL_B(ncv, ns, ismain, switch, switchb, ev, te, teb, rt, &
& rtb, rtw, rtwb)
  USE B2MOD_TYPES
  USE B2MOD_MATH_DIFF
  USE B2MOD_B2CMPA_DIFF
  USE B2US_PLASMA_DIFF
  USE B2MOD_SWITCHES_DIFF
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_AD_DIFF, ONLY : ncall_b2sqel
  USE B2MOD_SUBSYS
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, ns, ismain
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(SWITCHES) :: switchb
  REAL(kind=r8) :: ev, te(ncv)
  REAL(kind=r8) :: teb(ncv)
  TYPE(B2RATES), INTENT(INOUT) :: rt
  TYPE(B2RATES_DIFF), INTENT(INOUT) :: rtb
!   ..output arguments (unspecified on entry)
  TYPE(B2RATESWORK), INTENT(INOUT) :: rtw
  TYPE(B2RATESWORK), INTENT(INOUT) :: rtwb
!
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2SQEL computes on all cells of the mesh the rate coefficients
!     for ionisation, recombination, electron heat loss and radiation.
!
!
!  3. description (see also routine b2cdca)
!
!     This routine employs the log-log linear representation provided
!     in the arrays rlsa, rlra, rlqa, rlrd and rlbr to evaluate on all cells 
!     of the mesh the rate coefficients for ionisation, recombination, 
!     electron heat loss, and radiation.
!
!
!  8. further comments
!
!     In case a rate coefficient vanishes, the representation of the
!     log of the rate coefficient will provide a large negative value,
!     for which the exponential function should underflow. The present
!     code tests for this condition and explicitly sets those rate
!     coefficients to 0.
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: icv, is
  REAL(kind=r8) :: tnorm, art_rad
  REAL(kind=r8) :: tnormb
  REAL(kind=r8) :: wrk0(ncv)
  REAL(kind=r8) :: wrk0b(ncv)
!   ..procedures
  INTRINSIC LOG, MIN
  EXTERNAL XERTST
  EXTERNAL B2XVSG
  INTRINSIC MAX
  INTRINSIC ABS
  REAL(kind=r8) :: y1
  REAL(kind=r8) :: y1b
  REAL(kind=r8) :: y2
  REAL(kind=r8) :: y2b
  REAL(r8) :: arg1
  REAL(r8) :: arg1b
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result1b
!   ..initialisation
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
!   ..test nCv, ns
!   ..test ev
!   ..extensive tests on first few calls
  REAL(kind=r8) :: res
  REAL(kind=r8) :: resb
  REAL(kind=r8) :: res0
  REAL(kind=r8) :: resb0
  REAL(kind=r8) :: res1
  REAL(kind=r8) :: resb1
  REAL(kind=r8) :: res2
  REAL(kind=r8) :: resb2
  REAL(kind=r8) :: temp
  REAL(kind=r8) :: temp0
  REAL(kind=r8) :: tempb
  INTEGER*4 :: branch
!   ..compute wrk0()=log(te()/ev)
  wrk0 = LOG(te/ev)
!
! ..main computation
!   ..compute rate coefficients
  DO is=0,ns-1
    DO icv=1,ncv
!      ..compute ionisation rate coefficient
      arg1 = rt%rlsa(icv, 0, is) + rt%rlsa(icv, 1, is)*wrk0(icv)
      CALL PUSHBOOLEAN(b2mod_math_initialised)
      CALL PUSHREAL8(cutlo, r8/8)
      CALL PUSHREAL8(cutll, r8/8)
      res = EXPU(arg1)
!      ..compute recombination rate coefficient
      arg1 = rt%rlra(icv, 0, is) + rt%rlra(icv, 1, is)*wrk0(icv)
      CALL PUSHBOOLEAN(b2mod_math_initialised)
      CALL PUSHREAL8(cutlo, r8/8)
      CALL PUSHREAL8(cutll, r8/8)
      res0 = EXPU(arg1)
!      ..compute heat loss rate coefficient
      arg1 = rt%rlqa(icv, 0, is) + rt%rlqa(icv, 1, is)*wrk0(icv)
      CALL PUSHBOOLEAN(b2mod_math_initialised)
      CALL PUSHREAL8(cutlo, r8/8)
      CALL PUSHREAL8(cutll, r8/8)
      res1 = EXPU(arg1)
      arg1 = rt%rlqr(icv, 0, is) + rt%rlqr(icv, 1, is)*wrk0(icv)
      CALL PUSHBOOLEAN(b2mod_math_initialised)
      CALL PUSHREAL8(cutlo, r8/8)
      CALL PUSHREAL8(cutll, r8/8)
      res2 = EXPU(arg1)
!      ..compute line radiation rate coefficient
      arg1 = rt%rlrd(icv, 0, is) + rt%rlrd(icv, 1, is)*wrk0(icv)
!      ..compute bremsstrahlung radiation rate coefficient
      arg1 = rt%rlbr(icv, 0, is) + rt%rlbr(icv, 1, is)*wrk0(icv)
      IF (zamax(is) .GT. rt%rlza(icv, 0, is) + rt%rlza(icv, 1, is)*wrk0(&
&         icv)) THEN
        y1 = rt%rlza(icv, 0, is) + rt%rlza(icv, 1, is)*wrk0(icv)
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
        y1 = zamax(is)
      END IF
      IF (zamin(is) .LT. y1) THEN
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
      END IF
      IF (zamax(is)**2 .GT. rt%rlz2(icv, 0, is) + rt%rlz2(icv, 1, is)*&
&         wrk0(icv)) THEN
        y2 = rt%rlz2(icv, 0, is) + rt%rlz2(icv, 1, is)*wrk0(icv)
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
        y2 = zamax(is)**2
      END IF
      IF (zamin(is)**2 .LT. y2) THEN
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
      END IF
    END DO
  END DO
!
! ..artificial radiation                DPC 2000.08.25
  art_rad = switch%art_rad
  IF (art_rad .NE. 0.0_R8) THEN
    IF (ncall_b2sqel .EQ. 0) THEN
      IF (art_rad .GE. 0.) THEN
        CALL PUSHCONTROL1B(0)
        art_rad = art_rad
      ELSE
        CALL PUSHCONTROL1B(0)
        art_rad = -art_rad
      END IF
    ELSE
      CALL PUSHCONTROL1B(1)
    END IF
! XPB 2007.11.02 was ns-1
    is = ismain
    DO icv=1,ncv
      IF (0.000001_R8 .LT. te(icv)/(15.0_R8*ev)) THEN
        CALL PUSHREAL8(tnorm, r8/8)
        tnorm = te(icv)/(15.0_R8*ev)
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHREAL8(tnorm, r8/8)
        tnorm = 0.000001_R8
        CALL PUSHCONTROL1B(1)
      END IF
    END DO
    DO icv=ncv,1,-1
      temp = tnorm*tnorm*tnorm
      temp0 = tnorm**1.5_R8 + 1.0/temp
      tempb = -(art_rad*2.0e-31_R8*rtwb%rqa(icv, is)/temp0**2)
      tnormb = (1.5_R8*tnorm**0.5-3*tnorm**2/temp**2)*tempb
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPREAL8(tnorm, r8/8)
        teb(icv) = teb(icv) + tnormb/(15.0_R8*ev)
      ELSE
        CALL POPREAL8(tnorm, r8/8)
      END IF
    END DO
    CALL POPCONTROL1B(branch)
  END IF
  wrk0b = 0.D0
  DO is=ns-1,0,-1
    DO icv=ncv,1,-1
      rtb%rlpi(icv, 0, is) = rtb%rlpi(icv, 0, is) + rtb%rpi(icv, is)
      rtb%rlpi(icv, 1, is) = rtb%rlpi(icv, 1, is) + wrk0(icv)*rtb%rpi(&
&       icv, is)
      wrk0b(icv) = wrk0b(icv) + rt%rlpi(icv, 1, is)*rtb%rpi(icv, is) + &
&       rt%rlpt(icv, 1, is)*rtb%rpt(icv, is)
      rtb%rpi(icv, is) = 0.D0
      rtb%rlpt(icv, 0, is) = rtb%rlpt(icv, 0, is) + rtb%rpt(icv, is)
      rtb%rlpt(icv, 1, is) = rtb%rlpt(icv, 1, is) + wrk0(icv)*rtb%rpt(&
&       icv, is)
      rtb%rpt(icv, is) = 0.D0
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        y2b = rtb%rz2(icv, is)
        rtb%rz2(icv, is) = 0.D0
      ELSE
        rtb%rz2(icv, is) = 0.D0
        y2b = 0.D0
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        rtb%rlz2(icv, 0, is) = rtb%rlz2(icv, 0, is) + y2b
        rtb%rlz2(icv, 1, is) = rtb%rlz2(icv, 1, is) + wrk0(icv)*y2b
        wrk0b(icv) = wrk0b(icv) + rt%rlz2(icv, 1, is)*y2b
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        y1b = rtb%rza(icv, is)
        rtb%rza(icv, is) = 0.D0
      ELSE
        rtb%rza(icv, is) = 0.D0
        y1b = 0.D0
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        rtb%rlza(icv, 0, is) = rtb%rlza(icv, 0, is) + y1b
        rtb%rlza(icv, 1, is) = rtb%rlza(icv, 1, is) + wrk0(icv)*y1b
        wrk0b(icv) = wrk0b(icv) + rt%rlza(icv, 1, is)*y1b
      END IF
      result1b = switch%b2sqel_phm2*ev*rtwb%rqr(icv, is)
      rtwb%rqr(icv, is) = 0.D0
      arg1 = rt%rlqr(icv, 0, is) + rt%rlqr(icv, 1, is)*wrk0(icv)
      resb2 = result1b
      CALL POPREAL8(cutll, r8/8)
      CALL POPREAL8(cutlo, r8/8)
      CALL POPBOOLEAN(b2mod_math_initialised)
      CALL EXPU_B(arg1, arg1b, resb2)
      rtb%rlqr(icv, 0, is) = rtb%rlqr(icv, 0, is) + arg1b
      rtb%rlqr(icv, 1, is) = rtb%rlqr(icv, 1, is) + wrk0(icv)*arg1b
      wrk0b(icv) = wrk0b(icv) + rt%rlqr(icv, 1, is)*arg1b
      result1b = switch%b2sqel_phm2*ev*rtwb%rqa(icv, is)
      rtwb%rqa(icv, is) = 0.D0
      arg1 = rt%rlqa(icv, 0, is) + rt%rlqa(icv, 1, is)*wrk0(icv)
      resb1 = result1b
      CALL POPREAL8(cutll, r8/8)
      CALL POPREAL8(cutlo, r8/8)
      CALL POPBOOLEAN(b2mod_math_initialised)
      CALL EXPU_B(arg1, arg1b, resb1)
      rtb%rlqa(icv, 0, is) = rtb%rlqa(icv, 0, is) + arg1b
      rtb%rlqa(icv, 1, is) = rtb%rlqa(icv, 1, is) + wrk0(icv)*arg1b
      wrk0b(icv) = wrk0b(icv) + rt%rlqa(icv, 1, is)*arg1b
      result1b = switch%b2sqel_phm1*rtwb%rra(icv, is)
      rtwb%rra(icv, is) = 0.D0
      arg1 = rt%rlra(icv, 0, is) + rt%rlra(icv, 1, is)*wrk0(icv)
      resb0 = result1b
      CALL POPREAL8(cutll, r8/8)
      CALL POPREAL8(cutlo, r8/8)
      CALL POPBOOLEAN(b2mod_math_initialised)
      CALL EXPU_B(arg1, arg1b, resb0)
      rtb%rlra(icv, 0, is) = rtb%rlra(icv, 0, is) + arg1b
      rtb%rlra(icv, 1, is) = rtb%rlra(icv, 1, is) + wrk0(icv)*arg1b
      wrk0b(icv) = wrk0b(icv) + rt%rlra(icv, 1, is)*arg1b
      result1b = switch%b2sqel_phm0*rtwb%rsa(icv, is)
      rtwb%rsa(icv, is) = 0.D0
      arg1 = rt%rlsa(icv, 0, is) + rt%rlsa(icv, 1, is)*wrk0(icv)
      resb = result1b
      CALL POPREAL8(cutll, r8/8)
      CALL POPREAL8(cutlo, r8/8)
      CALL POPBOOLEAN(b2mod_math_initialised)
      CALL EXPU_B(arg1, arg1b, resb)
      rtb%rlsa(icv, 0, is) = rtb%rlsa(icv, 0, is) + arg1b
      rtb%rlsa(icv, 1, is) = rtb%rlsa(icv, 1, is) + wrk0(icv)*arg1b
      wrk0b(icv) = wrk0b(icv) + rt%rlsa(icv, 1, is)*arg1b
    END DO
  END DO
  teb = teb + wrk0b/te
END SUBROUTINE B2SQEL_B

