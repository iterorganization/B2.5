  END IF
  coni0b = 0.D0
  coni0b = dvb%coni(:, :, 0)
  floi0b = 0.D0
  floi0b = dvb%floi
  floi0_mdfb = 0.D0
  CALL CALCCOEF_BWD(ncv, nfc, nvx, meth, geo, floi0_mdf, floi0_mdfb, co%&
&             chci, cob%chci, floi0, floi0b, coni0, coni0b)
  fhi0_mdfb = 0.D0
  fhi0_mdfb(:, 1) = fhi0_mdfb(:, 1) + dvb%fhi_mdf(:, 1) + dvb%fhi(:, 1)
  dvb%fhipsch(:, 1) = dvb%fhipsch(:, 1) - dvb%fhi_mdf(:, 1)
  dvb%fhi_mdf(:, 1) = 0.D0
  dvb%fhipsch(:, 0) = dvb%fhipsch(:, 0) - dvb%fhi_mdf(:, 0)
  tifb = 0.D0
  floi0b(:, 1) = floi0b(:, 1) + tif*dvb%fhi(:, 1)
