!
!   ..compute ion fluxes
  dv%fhi(:, 0) = fhi0_mdf(:, 0) + (floi0(:, 0)-floi0_mdf(:, 0))*tif
  dv%fhi(:, 1) = fhi0_mdf(:, 1) + (floi0(:, 1)-floi0_mdf(:, 1))*tif
!
  dv%fhi_mdf(:, 0) = fhi0_mdf(:, 0) - dv%fhipsch(:, 0)
  dv%fhi_mdf(:, 1) = fhi0_mdf(:, 1) - dv%fhipsch(:, 1)
!
!   ..compute floi, coni
!wdk now we always add PSch linearization; do we need to condition
!    this on mdf /= 0?
  CALL CALCCOEF_NODIFF(ncv, nfc, nvx, meth, geo, floi0_mdf, co%chci, &
&                floi0, coni0)
  dv%floi = dv%floi + floi0
  dv%coni(:, :, 0) = dv%coni(:, :, 0) + coni0
!
!   ..compute fhn, flon, conn
  IF (switch%tn_style .LT. 2) THEN
!set zero if no separate equation for tn is solved
    dv%fhn = 0.0_R8
    dv%flon = 0.0_R8
    dv%conn = 0.0_R8
  ELSE
!     ..apply discretization scheme
    CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%tn, flon0, co&
&                  %chcn, dv%fhn, dumm0, dumm1)
!     ..compute flon, conn
    CALL CALCCOEF_NODIFF(ncv, nfc, nvx, meth, geo, flon0, co%chcn, dv%&
&                  flon, dv%conn)
  END IF
!
  IF (switch%solve_keps .GT. 0) THEN
!     ..compute ExB heat fluxes (ion and electron (!))
    flo0_exb = 1.5_R8*dv%fna_exb
    CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%ti, flo0_exb&
&                  , co%chci_exb, dv%fhi_exb, dumm0, dumm1)
    CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%te, flo0_exb&
&                  , co%chce_exb, dv%fhe_exb, dumm0, dumm1)
!
!     ..fluxes for k-equation
!wdk    fflokt and fconkt: fudge factors to approximate enhanced parallel
!wdk    transport of k due to term
!wdk    related to (fluctuations in parallel current) * (fluctuations in potential)
!wdk    factor fconkt now included in b2trno
!wdk    in principle: models same effect as skt_diss (thus: needs b2sikt_fac_sheath_* = 0.0)
    flokt0(:, 0) = (1.0_R8+switch%b2tfhi_fflokt)*dv%fna(:, 0, ismain)
    flokt0(:, 1) = dv%fna(:, 1, ismain)
    cdkt0(:, 0) = co%cdkt(:, 0)
    cdkt0(:, 1) = co%cdkt(:, 1)
!
!     ..apply discretization scheme
    CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%kt, flokt0, &
&                  cdkt0, dv%fkt, dumm0, dumm1)
!
!     ..contribution from ExB heat fluxes
!wdk    at the moment: experimental; it is expected that this contriution
!wdk    is at least partially compensated by the (divergence of the) term
!wdk    related to (fluctuations in diamagnetic current) * (fluctuations in potential)
    dv%fkt = dv%fkt + switch%b2tfhi_fkt_hie*2.0_R8/3.0_R8*(dv%fhi_exb+dv&
&     %fhe_exb)
!
!     ..compute flokt, conkt
    CALL CALCCOEF_NODIFF(ncv, nfc, nvx, meth, geo, flokt0, cdkt0, dv%&
&                  flokt, dv%conkt)
!
    IF (switch%solve_keps .GT. 1) THEN
!
!     ..fluxes for z-equation
      flozt0(:, 0) = (1.0_R8+switch%b2tfhi_fflozt)*dv%fna(:, 0, ismain)
      flozt0(:, 1) = dv%fna(:, 1, ismain)
      cdzt0(:, 0) = co%cdzt(:, 0)
      cdzt0(:, 1) = co%cdzt(:, 1)
!
!     ..apply discretization scheme
      CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%zt, flozt0&
&                    , cdzt0, dv%fzt, dumm0, dumm1)
!
!     ..compute flozt, conzt
      CALL CALCCOEF_NODIFF(ncv, nfc, nvx, meth, geo, flozt0, cdzt0, dv%&
&                    flozt, dv%conzt)
!
    END IF
  END IF
!
!srv 18.06.08 }
!
!
!
  IF (switch%b2tfhi_iout .NE. 0) THEN
!srv 18.06.08 {
    CALL MY_OUT_US(70, nfc, 1, dv%floi(1, 0), 'b2tfhi__floi_th')
    CALL MY_OUT_US(70, nfc, 1, dv%floi(1, 1), 'b2tfhi__floi_r')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 0, 0), &
&                   'b2tfhi__coni_th0')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 0, 1), &
&                   'b2tfhi__coni_th1')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 0, 2), &
&                   'b2tfhi__coni_th2')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 1, 0), &
&                   'b2tfhi__coni_r0')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 1, 1), &
&                   'b2tfhi__coni_r1')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 1, 2), &
&                   'b2tfhi__coni_r2')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi(1, 0), 'b2tfhi__fhi_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi(1, 1), 'b2tfhi__fhi_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 0), &
&                   'b2tfhi__fhi_mdf_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 1), &
&                   'b2tfhi__fhi_mdf_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 0), &
&                   'b2tfhi__fhiPSch_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 1), &
&                   'b2tfhi__fhiPSch_r')
    CALL MY_OUT_US(70, nfc, 1, fhi_32_loc(1, 0), &
&                   'b2tfhi__fhi_32_th')
    CALL MY_OUT_US(70, nfc, 1, fhi_32_loc(1, 1), &
&                   'b2tfhi__fhi_32_r')
    CALL MY_OUT_US(70, nfc, 1, fhi_52_loc(1, 0), &
&                   'b2tfhi__fhi_52_th')
    CALL MY_OUT_US(70, nfc, 1, fhi_52_loc(1, 1), &
&                   'b2tfhi__fhi_52_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvispar(1, 0), &
&                   'b2tfhi__fchvispar_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvispar(1, 1), &
&                   'b2tfhi__fchvispar_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisq(1, 0), &
&                   'b2tfhi__fchvisq_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisq(1, 1), &
&                   'b2tfhi__fchvisq_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisper(1, 0), &
&                   'b2tfhi__fchvisper_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisper(1, 1), &
&                   'b2tfhi__fchvisper_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchinert(1, 0), &
&                   'b2tfhi__fchinert_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchinert(1, 1), &
&                   'b2tfhi__fchinert_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchanml(1, 0), &
&                   'b2tfhi__fchanml_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchanml(1, 1), &
&                   'b2tfhi__fchanml_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fni_he(1, 0), 'b2tfhi__fni_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fni_he(1, 1), 'b2tfhi__fni_r')
    CALL MY_OUT_US(70, ncv, 0, dv%ni(:, 0), 'b2tfhi__ni0')
    CALL MY_OUT_US(70, ncv, 0, dv%ni(:, 1), 'b2tfhi__ni1')
! neutrals
    CALL MY_OUT_US(70, nfc, 1, dv%flon(1, 0), 'b2tfhi__flon_th')
    CALL MY_OUT_US(70, nfc, 1, dv%flon(1, 1), 'b2tfhi__flon_r')
    CALL MY_OUT_US(70, nfc, 1, dv%conn(1, 0), 'b2tfhi__conn_th')
    CALL MY_OUT_US(70, nfc, 1, dv%conn(1, 1), 'b2tfhi__conn_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhn(1, 0), 'b2tfhi__fhn_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhn(1, 1), 'b2tfhi__fhn_r')
  END IF
!
  IF (switch%iout_b2wdat .EQ. 4) THEN
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 0), &
&                   'b2tfhi__fhi_mdf_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 1), &
&                   'b2tfhi__fhi_mdf_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 0), &
&                   'b2tfhi__fhiPSch_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 1), &
&                   'b2tfhi__fhiPSch_r')
    CALL MY_OUT_US(70, nfc, 1, fhi_32_loc(1, 0), &
&                   'b2tfhi__fhi_32_th')
    CALL MY_OUT_US(70, nfc, 1, fhi_32_loc(1, 1), &
&                   'b2tfhi__fhi_32_r')
    CALL MY_OUT_US(70, nfc, 1, fhi_52_loc(1, 0), &
&                   'b2tfhi__fhi_52_th')
    CALL MY_OUT_US(70, nfc, 1, fhi_52_loc(1, 1), &
&                   'b2tfhi__fhi_52_r')
  END IF
!
! ..return
  ncall_b2tfhi = ncall_b2tfhi + 1
  CALL SUBEND()
  RETURN
