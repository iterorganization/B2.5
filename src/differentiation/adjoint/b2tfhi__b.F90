        tempb = switch%fhi_vdia_par*2.5_R8*geo%fcs*geo%fcqalf(:, 0)*&
&         flidiab(:, 0)
        nifb = nifb + dv%vadia(:, 0, is)*tempb
        dvb%vadia(:, 0, is) = dvb%vadia(:, 0, is) + nif*tempb
        CALL INTFACE_BWD(ncv, nfc, mpg%fccv, wght, pl%na(:, is), plb%na(&
&                  :, is), nif, nifb)
      END IF
    END DO
  END IF
  CALL B2TIPSCH_B(ncv, nfc, nvx, ns, switch, geo, geob, mpg, mpgb, dv%&
&           facdrift, co%cddi, cob%cddi, pl%ti, plb%ti, pl%na, plb%na, &
&           dv%floi, dvb%floi, dv%coni, dvb%coni, dv%fhipsch, dvb%&
&           fhipsch)
  dvb%floi = 0.D0
  dvb%coni = 0.D0
  dvb%fhipsch = 0.D0
END SUBROUTINE B2TFHI__B

