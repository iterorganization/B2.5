      IF (solvereg(ireg) .AND. solvireg(ireg) .AND. solvtreg(ireg)) THEN
!srv 22.05.18
        result1 = MED(corte(icv), cortt(icv), corti(icv))
        ctt = cortt(icv) - result1
        IF (-te(icv) .LT. corte(icv) + ctt) THEN
          max7 = corte(icv) + ctt
        ELSE
          max7 = -te(icv)
        END IF
        dte(icv) = rxf*max7
        IF (-ti(icv) .LT. corti(icv) + ctt) THEN
          max8 = corti(icv) + ctt
        ELSE
          max8 = -ti(icv)
        END IF
        dti(icv) = rxf*max8
      ELSE
        IF (-te(icv) .LT. corte(icv)) THEN
          max9 = corte(icv)
        ELSE
          max9 = -te(icv)
        END IF
        dte(icv) = rxf*max9
        IF (-ti(icv) .LT. corti(icv)) THEN
          max10 = corti(icv)
        ELSE
          max10 = -ti(icv)
        END IF
        dti(icv) = rxf*max10
      END IF
    ELSE IF (switch%stylec .EQ. 2) THEN
      IF (-te(icv) .LT. corte(icv)) THEN
        max11 = corte(icv)
      ELSE
        max11 = -te(icv)
      END IF
