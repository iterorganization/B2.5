      ift = mpg%cvft(icv)
      tt0(ift) = tt0(ift) + geo%cvvol(icv)*ni(icv, 1)
      tt1(ift) = tt1(ift) + geo%cvvol(icv)*ni(icv, 1)*dti1(icv)
      tt2(ift) = tt2(ift) + geo%cvvol(icv)*ne(icv)
      tt3(ift) = tt3(ift) + geo%cvvol(icv)*ne(icv)*dte1(icv)
    END IF
  END DO
