!lk 26.10.17
  REAL(kind=r8) :: dte1(ncv), dti1(ncv), tt0(mpg%nft), tt1(mpg%nft), tt2&
& (mpg%nft), tt3(mpg%nft)
!   ..procedures
  EXTERNAL XERTST, B2XVSG_NODIFF, XERRAB
