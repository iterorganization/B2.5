  INTEGER :: icv, is, ireg, ift
  REAL(kind=r8) :: ctt, dte(ncv), dti(ncv), dtn(ncv), dkt(ncv), dzt(ncv)&
& , t0, wrk(ncv), wrk0(ncv), wrkv(nvx)
