  CALL XERTST(0.0_R8 .LE. switch%b2npht_pcm0, 'faulty argument pcm0')
!   ..test sign of na, ni, vol, te, ti, ne
  IF (ncall_b2upht .LT. 3) THEN
    arg1 = ncv*ns
    CALL B2XVSG_NODIFF(arg1, na, 1, 'na', '.gt.')
    arg1 = ncv*2
    CALL B2XVSG_NODIFF(arg1, ni, 1, 'ni', '.gt.')
    CALL B2XVSG_NODIFF(ncv, geo%cvvol, 1, 'vol', '.gt.')
    CALL B2XVSG_NODIFF(ncv, te, 1, 'te', '.gt.')
    CALL B2XVSG_NODIFF(ncv, ti, 1, 'ti', '.gt.')
    CALL B2XVSG_NODIFF(ncv, tn, 1, 'tn', '.gt.')
    CALL B2XVSG_NODIFF(ncv, ne, 1, 'ne', '.gt.')
    CALL B2XVSG_NODIFF(ncv, kt, 1, 'kt', '.ge.')
    CALL B2XVSG_NODIFF(ncv, zt, 1, 'zt', '.ge.')
  END IF
!
! ..apply the correction
!   ..adjust te, ti, po
  tt0 = 0.0e0_R8
  tt1 = 0.0e0_R8
  tt2 = 0.0e0_R8
  tt3 = 0.0e0_R8
  DO icv=1,ncv
    ireg = mpg%cvreg(icv)
!!!   (stylec is a numerical switch, needs experiments)
    IF (switch%stylec .EQ. 0) THEN
      IF (solvereg(ireg) .AND. solvireg(ireg) .AND. solvtreg(ireg)) THEN
        IF (-te(icv) .LT. (corte(icv)+cortt(icv))/2.0_R8) THEN
          max1 = (corte(icv)+cortt(icv))/2.0_R8
        ELSE
          max1 = -te(icv)
        END IF
!srv 22.05.18
        dte(icv) = rxf*max1
        IF (-ti(icv) .LT. (corti(icv)+cortt(icv))/2.0_R8) THEN
          max2 = (corti(icv)+cortt(icv))/2.0_R8
        ELSE
          max2 = -ti(icv)
        END IF
        dti(icv) = rxf*max2
      ELSE
        IF (-te(icv) .LT. corte(icv)) THEN
          max3 = corte(icv)
        ELSE
          max3 = -te(icv)
        END IF
        dte(icv) = rxf*max3
        IF (-ti(icv) .LT. corti(icv)) THEN
          max4 = corti(icv)
        ELSE
          max4 = -ti(icv)
        END IF
