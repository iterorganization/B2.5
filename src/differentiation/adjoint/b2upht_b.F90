  REAL(kind=r8) :: max7
  REAL(kind=r8) :: max8
  REAL(kind=r8) :: max9
  REAL(kind=r8) :: max10
  REAL(kind=r8) :: max11
  REAL(kind=r8) :: max12
  REAL(kind=r8) :: max13
