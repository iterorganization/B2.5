SUBROUTINE CALC_DIST_F_NODIFF(geo, flist, nf, iref, ds)
!
  USE B2MOD_TYPES
  USE B2US_GEO_DIFF
  USE B2MOD_SUBSYS
  IMPLICIT NONE
! Input variables
  TYPE(GEOMETRY), INTENT(IN) :: geo
! Output variables
  INTEGER, INTENT(IN) :: nf, iref, flist(nf)
!
! Intermediate variables
  REAL(kind=r8), INTENT(OUT) :: ds(nf)
!
  REAL(kind=r8) :: dsref
! External functions
  INTEGER :: ifc
!
! Main program
!=============
! Call subini
  EXTERNAL XERTST
!
! Safety for possible reference not within list number
  CALL SUBINI('calc_dist_f')
! Compute the initial length as half the length along the first face
  CALL XERTST(iref .GE. 0 .AND. iref .LE. nf, &
&       'calc_dist_f: iref out of range')
!
! Loop over number of faces
  ds(1) = 0.5_R8*geo%fcht(flist(1))
!
! Compute cumulative distance
  DO ifc=2,nf
    ds(ifc) = ds(ifc-1) + 0.5_R8*(geo%fcht(flist(ifc))+geo%fcht(flist(&
&     ifc-1)))
! If reference face provided, shift length computation
  END DO
! Call subend and return
!
  IF (iref .GT. 1) THEN
! Compute reference point
! Subtract reference length
    dsref = (ds(iref)+ds(iref-1))/2.0_R8
!
    ds = ds - dsref
  END IF
!
  CALL SUBEND()
  RETURN
END SUBROUTINE CALC_DIST_F_NODIFF

