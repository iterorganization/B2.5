      nu1 = 0.0e0_R8
      alpha1 = 0.0e0_R8
!iyv 02.12.13
      kt_neo = 0.0e0_R8
      DO ift=1,mpg%nft
        IF (mpg%cvonclosedsurface(mpg%ftcv(mpg%ftcvp(ift, 1)))) THEN
          DO ic=mpg%ftcvp(ift, 1),mpg%ftcvp(ift, 1)+mpg%ftcvp(ift, 2)-1
            icv = mpg%ftcv(ic)
            IF (switch%lluciani .EQ. 3) THEN
!lk 20.11.07 { iyv 06.12.13
              tauia1(icv) = switch%cvsa_mltpl*tauia(icv, isb)/(1.0e0_R8+&
&               15.12e16_R8/geo%ftconn(ift)*(ti(icv)/ev)**2/ne2(icv))/(&
&               1.0e0_R8+15.12e16_R8*geo%fteps(ift)**1.5e0_R8/geo%ftconn&
&               (ift)*(ti(icv)/ev)**2/ne2(icv))
            ELSE IF (switch%lluciani .EQ. 1) THEN
              tauia1(icv) = tauia(icv, isb)/(1.0_R8+7.5e16_R8/geo%ftconn&
&               (ift)*(ti(icv)/ev)**2/ne2(icv))
            ELSE
              tauia1(icv) = tauia(icv, isb)
            END IF
            nu1(icv) = geo%ftconn(ift)/(tauia(icv, isb)*SQRT(ti(icv)/(mp&
&             *am(isb)))*2.0e0_R8*pi*SQRT(geo%fteps(ift)**3))
!lk 20.11.07 }
            alpha1(icv) = ((-0.17e0_R8+1.05e0_R8*SQRT(nu1(icv))+2.7e0_R8&
&             *nu1(icv)**2*geo%fteps(ift)**3)/(1.0e0_R8+0.7e0_R8*SQRT(&
&             nu1(icv))+nu1(icv)**2*geo%fteps(ift)**3)-1.0e0_R8)*&
&             0.533e0_R8
!iyv 02.12.13
            kt_neo(icv) = (-0.17e0_R8+1.05e0_R8*SQRT(nu1(icv)*s2)+&
&             2.7e0_R8*(nu1(icv)*s2)**2*geo%fteps(ift)**3)/(1.0e0_R8+&
&             0.7e0_R8*SQRT(nu1(icv)*s2)+(nu1(icv)*s2)**2*geo%fteps(ift)&
&             **3)
          END DO
        END IF
      END DO
!to ensure that subsequent steps lead to well-defined value on separatrix:
!copy values of tauia1, nu1, alpha1 across separatrix
!wdk todo: rationalize this when separatrix-structure is introduced?
!
      DO ifc=1,nfc
        IF (mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. (.NOT.mpg%&
&           cvonclosedsurface(mpg%fccv(ifc, 2)))) THEN
          tauia1(mpg%fccv(ifc, 2)) = tauia1(mpg%fccv(ifc, 1))
          alpha1(mpg%fccv(ifc, 2)) = alpha1(mpg%fccv(ifc, 1))
        END IF
        IF (.NOT.mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. mpg%&
&           cvonclosedsurface(mpg%fccv(ifc, 2))) THEN
          tauia1(mpg%fccv(ifc, 1)) = tauia1(mpg%fccv(ifc, 2))
          alpha1(mpg%fccv(ifc, 1)) = alpha1(mpg%fccv(ifc, 2))
        END IF
      END DO
!
!       ..compute qip1
      qip1 = switch%cqip1*ni*ti*SQRT(te+ti)/SQRT(am(isb)*mp)
!
!       ..compute qipgen
!wdk modified computation compared to structured code to 
!wdk facilitate implementation on unstructured grids
      qipgen = qip1*DATAN(qip0/qip1)*SQRT(geo%cvbb(:, 3))
!
!       ..gradient qipgen in cell centers   
      CALL GRADC_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, qipgen, wrkv, wrk0&
&                  )
      wrk0 = wrk0*geo%cvbb(:, 0)/geo%cvbb(:, 3)**3*tauia1*alpha1
!       ..finally calculate the r.h.s. contribution to the parallel balance
!         corresponding to parallel viscosity connected with heat flow
      CALL GRADC_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk0, wrkv, wrk1)
!zero out spurious contributions outside of separatrix
!wdk todo: rationalize this when separatrix-structure is introduced?
!
      DO icv=1,ncv
        IF (.NOT.mpg%cvonclosedsurface(icv)) wrk1(icv) = 0.0_R8
      END DO
!calculate the actual source
!
      smbvh(:, 0) = switch%addvis*geo%cvvol*geo%cvbb(:, 0)*SQRT(geo%cvbb&
&       (:, 3))*geo%cvhz*wrk1
!
!       ..zero out in guard cells
      smbvh(mpg%nci+1:ncv, 0) = 0.0_R8
!
    END IF
!
    IF (switch%addvis1 .NE. 0.0_R8) THEN
!srv 19.01.05 30.01.10
!
!       ..prepare wrk arrays
! wrk2 is used below twice
      wrk2 = SQRT(geo%cvbb(:, 3))
      wrk1 = LOG(geo%cvhz*wrk2)
!
!       ..poloidal differences of wrk1 on cell faces
      wrkv = LOG(geo%vxhz*SQRT(geo%vxbb(:, 3)) + 1.0e-30_R8)
      CALL DIFF_P_NODIFF(ncv, nfc, nvx, 1, geo, mpg, wrk1, wrkv, wrkf)
!
!       ..compute flow and its divergence
      wrkf = wrkf*ft*cvsbhz_cl(:, 0)/SQRT(geo%fcbb(:, 3))
      CALL DIV0_NODIFF(ncv, nfc, mpg, wrkf, wrk0)
!
!       ..add to r.h.s. source
      DO icv=1,mpg%nci
!srv 01.07.05 }
        t0 = wrk0(icv)*wrk2(icv)
        IF (t0 .GT. 0.0e0_R8) THEN
          smbvv(icv, 0) = t0*ub(icv)
        ELSE
          smbvv(icv, 1) = t0
        END IF
      END DO
    END IF
  END IF
!srv 19.01.05 }
!
!srv 17.06.02 }
!
  IF (switch%b2siav_iout .NE. 0) THEN
!srv 17.06.02 {
    WRITE(chns, '(i3.3)') isb
    CALL MY_OUT_US(70, ncv, 0, alpha1, 'b2siav_alpha1')
    CALL MY_OUT_US(70, ncv, 0, kt_neo, 'b2siav_kt_neo')
    CALL MY_OUT_US(70, ncv, 0, tauia1, 'b2siav_tauia1')
    DO k=0,3
      WRITE(chk, '(i1)') k
      arg1(:) = 'b2siav_smovv'//chk//chns
      CALL MY_OUT_US(70, ncv, 0, smbvv(1, k), arg1(:))
      arg1(:) = 'b2siav_smovh'//chk//chns
      CALL MY_OUT_US(70, ncv, 0, smbvh(1, k), arg1(:))
    END DO
    wrks = smbvv(:, 0) + smbvv(:, 1)*ub + smbvv(:, 2)*nb*mb + smbvv(:, 3&
&     )*ub*nb*mb
    arg1(:) = 'b2siav_smovv'//chns
    CALL MY_OUT_US(70, ncv, 0, wrks, arg1(:))
    wrks = smbvh(:, 0) + smbvh(:, 1)*ub + smbvh(:, 2)*nb*mb + smbvh(:, 3&
&     )*ub*nb*mb
    arg1(:) = 'b2siav_smovh'//chns
    CALL MY_OUT_US(70, ncv, 0, wrks, arg1(:))
    arg10(:) = 'b2siav_cvsbhz_th'//chns
    CALL MY_OUT_US(70, nfc, 1, cvsbhz_cl(1, 0), arg10(:))
    arg11(:) = 'b2siav_cvsbhz_r'//chns
    CALL MY_OUT_US(70, nfc, 1, cvsbhz_cl(1, 1), arg11(:))
  END IF
!
!djm Jan2017 Store linearised sources for balance
!WG_TODO      if (balance_netcdf.ne.0) then
!WG_TODO        b2siav_smovh0to3(-1:nx,-1:ny,0:3,isb)=smbvh
!WG_TODO        b2siav_smovv0to3(-1:nx,-1:ny,0:3,isb)=smbvv
!WG_TODO      endif
!
! ..return
  ncall_b2siav = ncall_b2siav + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2SIAV_NODIFF

