  SUBROUTINE B2MOD_MATH_INIT()
    IMPLICIT NONE
    EXTERNAL MACHSFR, MACHSFR4
    REAL(kind=r8) :: MACHSFR
    REAL(kind=r4) :: MACHSFR4
    INTRINSIC LOG
    INTRINSIC EXP
    INTRINSIC MAX
    INTRINSIC REAL
    REAL(kind=r8) :: x1
    REAL(kind=r8) :: y1
    REAL(kind=r4) :: x2
    REAL(kind=r4) :: y2
    REAL(kind=r8) :: result1
    REAL(kind=r4) :: result10
!   ..set cutll, cutlo, cutl4, small_R4_constant
    result1 = MACHSFR()
    cutll = LOG(result1)
    x1 = MACHSFR()
    y1 = EXP(cutll)
    IF (x1 .LT. y1) THEN
      cutlo = y1
    ELSE
      cutlo = x1
    END IF
    result10 = MACHSFR4()
    cutl4 = REAL(LOG(result10), r8)
    x2 = MACHSFR4()
    y2 = REAL(EXP(cutl4), r4)
    IF (x2 .LT. y2) THEN
      small_r4_constant = y2
    ELSE
      small_r4_constant = x2
    END IF
    b2mod_math_initialised = .true.
    RETURN
  END SUBROUTINE B2MOD_MATH_INIT
