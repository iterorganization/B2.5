&             cor_ni(icv, b2espcr(is))*(1.0_R8/hlp-1.0_R8)
          END IF
        END IF
!WG_TODO            endif

      END DO
      DO icv=1,mpg%ncv
        result10 = ROXA(icv, is)
        result2 = ROXA(icv, is)
        x1 = sr%smo(icv, 0, is) + sr%smo(icv, 1, is)*ua(icv, is) + sr%&
&         smo(icv, 2, is)*result10 + sr%smo(icv, 3, is)*result2*ua(icv, &
&         is)
        IF (x1 .GE. 0.) THEN
          abs1 = x1
        ELSE
          abs1 = -x1
        END IF
!WG_TODO            if (resignore(iCv,2).ne.0) then
