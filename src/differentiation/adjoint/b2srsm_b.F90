!     density, parallel momentum, electron heat, atom heat, and charge.
!     The idea is to limit the source strength by (value)/(dt*t._f._r.)
!     - and to do this smoothly!
!
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: is, icv
  INTEGER :: k
!srv 11.09.09 }
  CHARACTER :: chns*3, chk*1
  REAL(kind=r8) :: timescale, invtimescale, timefactor, hlp
  REAL(kind=r8) :: invtimescaleb, hlpb
!djm Jan2017
  REAL(kind=r8) :: snatmp(ncv, 0:1, 0:ns-1), smotmp(ncv, 0:3, 0:ns-1), &
& shetmp(ncv, 0:3), shitmp(ncv, 0:3)
!   ..procedures
  EXTERNAL XERTST, IPGETI, B2XVSG
  INTRINSIC MAXVAL
  EXTERNAL XERRAB
  INTRINSIC ABS
  INTRINSIC MIN
  INTRINSIC MOD
  INTRINSIC SQRT
  REAL(r8) :: x1
  REAL(r8) :: x1b
  REAL(r8) :: x2
  REAL(r8) :: x2b
  REAL(r8) :: x3
  REAL(r8) :: x3b
  REAL(kind=r8), DIMENSION(ncv, 0:ns-1) :: dabs0
  REAL(r8) :: abs0
  REAL(r8) :: abs0b
  REAL(r8) :: abs1
  REAL(r8) :: abs1b
  REAL(r8) :: abs2
  REAL(r8) :: abs2b
  REAL(r8) :: abs3
  REAL(r8) :: abs3b
  REAL(r8) :: abs4
  LOGICAL, DIMENSION(ncv, 0:ns-1) :: mask
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result10
  REAL(kind=r8) :: result10b
  REAL(kind=r8) :: result2
  REAL(kind=r8) :: result2b
  REAL(r8), DIMENSION(ncv) :: arg1
  CHARACTER(len=12) :: arg2
  CHARACTER(len=12) :: arg10
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
!   ..test nCv, ns
!   ..test dtim
  REAL(kind=r8) :: temp
  REAL(kind=r8) :: tempb
  REAL(kind=r8) :: temp0
  REAL(kind=r8) :: temp1
  REAL(kind=r8) :: temp2
  REAL(kind=r8) :: tempb0
  REAL(kind=r8) :: tempb1
  INTEGER*4 :: branch
!
!djm Jan2017 Keep sources at the start to calculate total change for balance routines
!
  IF (switch%b2srsm_enable .NE. 0) THEN
!
!   ..smooth particle and parallel momentum sources
    DO is=0,ns-1
      DO icv=1,mpg%ncv
        IF (sr%sna(icv, 0, is) + sr%sna(icv, 1, is)*na(icv, is) .GE. 0.&
&       ) THEN
          CALL PUSHREAL8(abs0, r8/8)
          abs0 = sr%sna(icv, 0, is) + sr%sna(icv, 1, is)*na(icv, is)
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHREAL8(abs0, r8/8)
          abs0 = -(sr%sna(icv, 0, is)+sr%sna(icv, 1, is)*na(icv, is))
          CALL PUSHCONTROL1B(1)
        END IF
!WG_TODO            if (resignore(iCv,1).ne.0) then
! check particles
        invtimescale = abs0/(na(icv, is)*geo%cvvol(icv))
        CALL PUSHREAL8(hlp, r8/8)
        hlp = invtimescale*time_factor_required*dtim*dtco(is, mpg%cvreg(&
&         icv))*time_factor(icv)
        IF (hlp .GT. 1.0_R8) THEN
          CALL PUSHREAL8(sr%sna(icv, 0, is), r8/8)
          sr%sna(icv, 0, is) = sr%sna(icv, 0, is)/hlp
          CALL PUSHREAL8(sr%sna(icv, 1, is), r8/8)
          sr%sna(icv, 1, is) = sr%sna(icv, 1, is)/hlp
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
      END DO
      DO icv=1,mpg%ncv
        CALL PUSHREAL8(result10, r8/8)
        result10 = ROXA(icv, is)
        CALL PUSHREAL8(result2, r8/8)
        result2 = ROXA(icv, is)
        x1 = sr%smo(icv, 0, is) + sr%smo(icv, 1, is)*ua(icv, is) + sr%&
&         smo(icv, 2, is)*result10 + sr%smo(icv, 3, is)*result2*ua(icv, &
&         is)
        IF (x1 .GE. 0.) THEN
          CALL PUSHREAL8(abs1, r8/8)
          abs1 = x1
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHREAL8(abs1, r8/8)
          abs1 = -x1
          CALL PUSHCONTROL1B(1)
        END IF
!WG_TODO            if (resignore(iCv,2).ne.0) then
! check parallel momentum
        CALL PUSHREAL8(result10, r8/8)
        result10 = ROXA(icv, is)
        invtimescale = abs1/(SQRT(ti(icv)/(am(is)*mp))*result10*geo%&
&         cvvol(icv))
        CALL PUSHREAL8(hlp, r8/8)
        hlp = invtimescale*time_factor_required*dtim*dtmo(is, mpg%cvreg(&
&         icv))*time_factor(icv)
        IF (hlp .GT. 1.0_R8) THEN
          CALL PUSHREAL8(sr%smo(icv, 0, is), r8/8)
          sr%smo(icv, 0, is) = sr%smo(icv, 0, is)/hlp
          CALL PUSHREAL8(sr%smo(icv, 1, is), r8/8)
          sr%smo(icv, 1, is) = sr%smo(icv, 1, is)/hlp
          CALL PUSHREAL8(sr%smo(icv, 2, is), r8/8)
          sr%smo(icv, 2, is) = sr%smo(icv, 2, is)/hlp
          CALL PUSHREAL8(sr%smo(icv, 3, is), r8/8)
          sr%smo(icv, 3, is) = sr%smo(icv, 3, is)/hlp
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
      END DO
    END DO
!   ..smooth electron heat, ion heat and electron particle sources
    DO icv=1,mpg%ncv
      x2 = sr%shi(icv, 0) + sr%shi(icv, 1)*ti(icv) + sr%shi(icv, 2)*ni(&
&       icv, 0) + sr%shi(icv, 3)*ti(icv)*ni(icv, 0)
      IF (x2 .GE. 0.) THEN
        CALL PUSHREAL8(abs2, r8/8)
        abs2 = x2
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHREAL8(abs2, r8/8)
        abs2 = -x2
        CALL PUSHCONTROL1B(1)
      END IF
!WG_TODO          if (resignore(iCv,1).ne.0) then
! check ion energy
      invtimescale = abs2/(ti(icv)*ni(icv, 0)*geo%cvvol(icv))
      CALL PUSHREAL8(hlp, r8/8)
      hlp = invtimescale*time_factor_required*dtim*dtei(mpg%cvreg(icv))*&
&       time_factor(icv)
      IF (hlp .GT. 1.0_R8) THEN
        CALL PUSHREAL8(sr%shi(icv, 0), r8/8)
        sr%shi(icv, 0) = sr%shi(icv, 0)/hlp
        CALL PUSHREAL8(sr%shi(icv, 1), r8/8)
        sr%shi(icv, 1) = sr%shi(icv, 1)/hlp
        CALL PUSHREAL8(sr%shi(icv, 2), r8/8)
        sr%shi(icv, 2) = sr%shi(icv, 2)/hlp
        CALL PUSHREAL8(sr%shi(icv, 3), r8/8)
        sr%shi(icv, 3) = sr%shi(icv, 3)/hlp
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
      END IF
      x3 = sr%she(icv, 0) + sr%she(icv, 1)*te(icv) + sr%she(icv, 2)*ne(&
&       icv) + sr%she(icv, 3)*te(icv)*ne(icv)
      IF (x3 .GE. 0.) THEN
        CALL PUSHREAL8(abs3, r8/8)
        abs3 = x3
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHREAL8(abs3, r8/8)
        abs3 = -x3
        CALL PUSHCONTROL1B(1)
      END IF
! check electron energy
      invtimescale = abs3/(te(icv)*ne(icv)*geo%cvvol(icv))
      hlp = invtimescale*time_factor_required*dtim*dtee(mpg%cvreg(icv))*&
&       time_factor(icv)
      IF (hlp .GT. 1.0_R8) THEN
        CALL PUSHREAL8(sr%she(icv, 0), r8/8)
        sr%she(icv, 0) = sr%she(icv, 0)/hlp
        CALL PUSHREAL8(sr%she(icv, 1), r8/8)
        sr%she(icv, 1) = sr%she(icv, 1)/hlp
        CALL PUSHREAL8(sr%she(icv, 2), r8/8)
        sr%she(icv, 2) = sr%she(icv, 2)/hlp
        CALL PUSHREAL8(sr%she(icv, 3), r8/8)
        sr%she(icv, 3) = sr%she(icv, 3)/hlp
        CALL PUSHCONTROL1B(1)
      ELSE
        CALL PUSHCONTROL1B(0)
      END IF
    END DO
    DO icv=mpg%ncv,1,-1
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        hlpb = 0.D0
      ELSE
        invtimescale = abs3/(te(icv)*ne(icv)*geo%cvvol(icv))
        hlp = invtimescale*time_factor_required*dtim*dtee(mpg%cvreg(icv)&
&         )*time_factor(icv)
        CALL POPREAL8(sr%she(icv, 3), r8/8)
        hlpb = -(sr%she(icv, 3)*srb%she(icv, 3)/hlp**2)
        srb%she(icv, 3) = srb%she(icv, 3)/hlp
        CALL POPREAL8(sr%she(icv, 2), r8/8)
        hlpb = hlpb - sr%she(icv, 2)*srb%she(icv, 2)/hlp**2
        srb%she(icv, 2) = srb%she(icv, 2)/hlp
        CALL POPREAL8(sr%she(icv, 1), r8/8)
        hlpb = hlpb - sr%she(icv, 1)*srb%she(icv, 1)/hlp**2
        srb%she(icv, 1) = srb%she(icv, 1)/hlp
        CALL POPREAL8(sr%she(icv, 0), r8/8)
        hlpb = hlpb - sr%she(icv, 0)*srb%she(icv, 0)/hlp**2
        srb%she(icv, 0) = srb%she(icv, 0)/hlp
      END IF
      invtimescaleb = time_factor(icv)*time_factor_required*dtim*dtee(&
&       mpg%cvreg(icv))*hlpb
      temp2 = geo%cvvol(icv)*te(icv)*ne(icv)
      abs3b = invtimescaleb/temp2
      tempb1 = -(geo%cvvol(icv)*abs3*invtimescaleb/temp2**2)
      teb(icv) = teb(icv) + ne(icv)*tempb1
      neb(icv) = neb(icv) + te(icv)*tempb1
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPREAL8(abs3, r8/8)
        x3b = abs3b
      ELSE
        CALL POPREAL8(abs3, r8/8)
        x3b = -abs3b
      END IF
      tempb0 = sr%she(icv, 3)*x3b
      srb%she(icv, 0) = srb%she(icv, 0) + x3b
      srb%she(icv, 1) = srb%she(icv, 1) + te(icv)*x3b
      teb(icv) = teb(icv) + sr%she(icv, 1)*x3b + ne(icv)*tempb0
      srb%she(icv, 2) = srb%she(icv, 2) + ne(icv)*x3b
      neb(icv) = neb(icv) + sr%she(icv, 2)*x3b + te(icv)*tempb0
      srb%she(icv, 3) = srb%she(icv, 3) + te(icv)*ne(icv)*x3b
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        invtimescale = abs2/(ti(icv)*ni(icv, 0)*geo%cvvol(icv))
        hlp = invtimescale*time_factor_required*dtim*dtei(mpg%cvreg(icv)&
&         )*time_factor(icv)
        CALL POPREAL8(sr%shi(icv, 3), r8/8)
        hlpb = -(sr%shi(icv, 3)*srb%shi(icv, 3)/hlp**2)
        srb%shi(icv, 3) = srb%shi(icv, 3)/hlp
        CALL POPREAL8(sr%shi(icv, 2), r8/8)
        hlpb = hlpb - sr%shi(icv, 2)*srb%shi(icv, 2)/hlp**2
        srb%shi(icv, 2) = srb%shi(icv, 2)/hlp
        CALL POPREAL8(sr%shi(icv, 1), r8/8)
        hlpb = hlpb - sr%shi(icv, 1)*srb%shi(icv, 1)/hlp**2
        srb%shi(icv, 1) = srb%shi(icv, 1)/hlp
        CALL POPREAL8(sr%shi(icv, 0), r8/8)
        hlpb = hlpb - sr%shi(icv, 0)*srb%shi(icv, 0)/hlp**2
        srb%shi(icv, 0) = srb%shi(icv, 0)/hlp
      ELSE
        hlpb = 0.D0
      END IF
      CALL POPREAL8(hlp, r8/8)
      invtimescaleb = time_factor(icv)*time_factor_required*dtim*dtei(&
&       mpg%cvreg(icv))*hlpb
      temp2 = geo%cvvol(icv)*ti(icv)
      temp1 = temp2*ni(icv, 0)
      abs2b = invtimescaleb/temp1
      tempb1 = -(abs2*invtimescaleb/temp1**2)
      tib(icv) = tib(icv) + geo%cvvol(icv)*ni(icv, 0)*tempb1
      nib(icv, 0) = nib(icv, 0) + temp2*tempb1
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPREAL8(abs2, r8/8)
        x2b = abs2b
      ELSE
        CALL POPREAL8(abs2, r8/8)
        x2b = -abs2b
      END IF
      tempb0 = sr%shi(icv, 3)*x2b
      srb%shi(icv, 0) = srb%shi(icv, 0) + x2b
      srb%shi(icv, 1) = srb%shi(icv, 1) + ti(icv)*x2b
      tib(icv) = tib(icv) + sr%shi(icv, 1)*x2b + ni(icv, 0)*tempb0
      srb%shi(icv, 2) = srb%shi(icv, 2) + ni(icv, 0)*x2b
      nib(icv, 0) = nib(icv, 0) + sr%shi(icv, 2)*x2b + ti(icv)*tempb0
      srb%shi(icv, 3) = srb%shi(icv, 3) + ti(icv)*ni(icv, 0)*x2b
    END DO
    DO is=ns-1,0,-1
      DO icv=mpg%ncv,1,-1
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          CALL POPREAL8(sr%smo(icv, 3, is), r8/8)
          hlpb = -(sr%smo(icv, 3, is)*srb%smo(icv, 3, is)/hlp**2)
          srb%smo(icv, 3, is) = srb%smo(icv, 3, is)/hlp
          CALL POPREAL8(sr%smo(icv, 2, is), r8/8)
          hlpb = hlpb - sr%smo(icv, 2, is)*srb%smo(icv, 2, is)/hlp**2
          srb%smo(icv, 2, is) = srb%smo(icv, 2, is)/hlp
          CALL POPREAL8(sr%smo(icv, 1, is), r8/8)
          hlpb = hlpb - sr%smo(icv, 1, is)*srb%smo(icv, 1, is)/hlp**2
          srb%smo(icv, 1, is) = srb%smo(icv, 1, is)/hlp
          CALL POPREAL8(sr%smo(icv, 0, is), r8/8)
          hlpb = hlpb - sr%smo(icv, 0, is)*srb%smo(icv, 0, is)/hlp**2
          srb%smo(icv, 0, is) = srb%smo(icv, 0, is)/hlp
        ELSE
          hlpb = 0.D0
        END IF
        CALL POPREAL8(hlp, r8/8)
        invtimescaleb = time_factor(icv)*time_factor_required*dtim*dtmo(&
&         is, mpg%cvreg(icv))*hlpb
        temp = ti(icv)/(am(is)*mp)
        temp0 = SQRT(temp)
        temp1 = geo%cvvol(icv)*result10
        temp2 = temp1*temp0
        abs1b = invtimescaleb/temp2
        tempb0 = -(abs1*invtimescaleb/temp2**2)
        result10b = geo%cvvol(icv)*temp0*tempb0
        IF (.NOT.temp .EQ. 0.D0) tib(icv) = tib(icv) + temp1*tempb0/(am(&
&           is)*mp*2.0*temp0)
        CALL POPREAL8(result10, r8/8)
        CALL ROXA_B(icv, is, result10b)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          CALL POPREAL8(abs1, r8/8)
          x1b = abs1b
        ELSE
          CALL POPREAL8(abs1, r8/8)
          x1b = -abs1b
        END IF
        tempb = sr%smo(icv, 3, is)*x1b
        srb%smo(icv, 0, is) = srb%smo(icv, 0, is) + x1b
        srb%smo(icv, 1, is) = srb%smo(icv, 1, is) + ua(icv, is)*x1b
        uab(icv, is) = uab(icv, is) + sr%smo(icv, 1, is)*x1b + result2*&
&         tempb
        srb%smo(icv, 2, is) = srb%smo(icv, 2, is) + result10*x1b
        result10b = sr%smo(icv, 2, is)*x1b
        srb%smo(icv, 3, is) = srb%smo(icv, 3, is) + result2*ua(icv, is)*&
&         x1b
        result2b = ua(icv, is)*tempb
        CALL POPREAL8(result2, r8/8)
        CALL ROXA_B(icv, is, result2b)
        CALL POPREAL8(result10, r8/8)
        CALL ROXA_B(icv, is, result10b)
      END DO
      DO icv=mpg%ncv,1,-1
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          invtimescale = abs0/(na(icv, is)*geo%cvvol(icv))
          hlp = invtimescale*time_factor_required*dtim*dtco(is, mpg%&
&           cvreg(icv))*time_factor(icv)
          CALL POPREAL8(sr%sna(icv, 1, is), r8/8)
          hlpb = -(sr%sna(icv, 1, is)*srb%sna(icv, 1, is)/hlp**2)
          srb%sna(icv, 1, is) = srb%sna(icv, 1, is)/hlp
          CALL POPREAL8(sr%sna(icv, 0, is), r8/8)
          hlpb = hlpb - sr%sna(icv, 0, is)*srb%sna(icv, 0, is)/hlp**2
          srb%sna(icv, 0, is) = srb%sna(icv, 0, is)/hlp
        ELSE
          hlpb = 0.D0
        END IF
        CALL POPREAL8(hlp, r8/8)
        invtimescaleb = time_factor(icv)*time_factor_required*dtim*dtco(&
&         is, mpg%cvreg(icv))*hlpb
        temp = geo%cvvol(icv)*na(icv, is)
        abs0b = invtimescaleb/temp
        nab(icv, is) = nab(icv, is) - geo%cvvol(icv)*abs0*invtimescaleb/&
&         temp**2
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          CALL POPREAL8(abs0, r8/8)
          srb%sna(icv, 0, is) = srb%sna(icv, 0, is) + abs0b
          srb%sna(icv, 1, is) = srb%sna(icv, 1, is) + na(icv, is)*abs0b
          nab(icv, is) = nab(icv, is) + sr%sna(icv, 1, is)*abs0b
        ELSE
          CALL POPREAL8(abs0, r8/8)
          srb%sna(icv, 0, is) = srb%sna(icv, 0, is) - abs0b
          srb%sna(icv, 1, is) = srb%sna(icv, 1, is) - na(icv, is)*abs0b
          nab(icv, is) = nab(icv, is) - sr%sna(icv, 1, is)*abs0b
        END IF
      END DO
    END DO
  END IF

CONTAINS
!  Differentiation of roxa in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: na roxa
!   with respect to varying inputs: na
!
  SUBROUTINE ROXA_B(icv, is, roxab)
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: icv, is
    REAL(kind=r8) :: roxa
    REAL(kind=r8) :: roxab
!
    nab(icv, is) = nab(icv, is) + am(is)*mp*roxab
  END SUBROUTINE ROXA_B

!
  FUNCTION ROXA(icv, is)
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: icv, is
    REAL(kind=r8) :: roxa
!
    roxa = am(is)*mp*na(icv, is)
    RETURN
  END FUNCTION ROXA

END SUBROUTINE B2SRSM_B

