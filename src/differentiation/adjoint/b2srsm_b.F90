      arg2 = 'b2srsm_smosm'//chns
      CALL MY_OUT_US(70, ncv, 0, arg1, arg2)
    END DO
  END IF
  IF (switch%b2npco_iout .EQ. 1) THEN
    DO is=0,ns-1
      WRITE(chns, '(i3.3)') is
!srv 07.10.09
      arg1 = sr%sna(:, 0, is) + sr%sna(:, 1, is)*na(:, is)
      arg2 = 'b2srsm_snasm'//chns
      CALL MY_OUT_US(70, ncv, 0, arg1, arg2)
    END DO
  END IF
  IF (switch%b2npht_iout .EQ. 1) THEN
!srv 07.10.09
    arg1 = sr%she(:, 0) + sr%she(:, 1)*te + sr%she(:, 2)*ne + sr%she(:, &
&     3)*te*ne
    CALL MY_OUT_US(70, ncv, 0, arg1, 'b2srsm_shesm')
    arg1 = sr%shi(:, 0) + sr%shi(:, 1)*ti + sr%shi(:, 2)*ni(:, 0) + sr%&
&     shi(:, 3)*ti*ni(:, 0)
    CALL MY_OUT_US(70, ncv, 0, arg1, 'b2srsm_shism')
  END IF
!srv 11.09.09 }
!
  IF (switch%b2srsm_iout .EQ. 1) THEN
    DO is=0,ns-1
      WRITE(chns, '(i3.3)') is
      DO k=0,3
        WRITE(chk, '(i1)') k
        arg10 = 'b2srsm_smosm'//chk//chns
        CALL MY_OUT_US(70, ncv, 0, sr%smo(1, k, is), arg10)
      END DO
      DO k=0,1
        WRITE(chk, '(i1)') k
        arg10 = 'b2srsm_snasm'//chk//chns
        CALL MY_OUT_US(70, ncv, 0, sr%sna(1, k, is), arg10)
      END DO
    END DO
    DO k=0,3
      arg10 = 'b2srsm_shesm'//chk
      CALL MY_OUT_US(70, ncv, 0, sr%she(1, k), arg10)
      arg10 = 'b2srsm_shism'//chk
      CALL MY_OUT_US(70, ncv, 0, sr%shi(1, k), arg10)
    END DO
    DO k=0,1
      arg10 = 'b2srsm_snesm'//chk
      CALL MY_OUT_US(70, ncv, 0, sr%sne(1, k), arg10)
    END DO
  END IF
!
! ..return
  IF (ncall_b2srsm .EQ. 0 .OR. main_call) ncall_b2srsm = ncall_b2srsm + &
&     1
  CALL SUBEND()
  RETURN
