SUBROUTINE GET_ADAS_DATA_NODIFF(reac, ns, nxd, nyd, nx, ny, ne, te, mach&
&)
  USE B2MOD_TYPES
  USE B2MOD_DIAG_DIFF
  USE B2MOD_B2CMPA_DIFF
  USE B2MOD_CONSTANTS
  USE B2MOD_SUBSYS
  IMPLICIT NONE
!..   input parameters (unchanged on exit)
  INTEGER :: ns, nxd, nyd, nx, ny
  REAL(kind=r8) :: ne(0:nxd+1, 0:nyd+1), te(0:nxd+1, 0:nyd+1)
  CHARACTER :: reac*9
!..   output data
  REAL(kind=r8) :: mach(0:nxd+1, 0:nyd+1, 0:ns-1)
!     helpers for reading in data
  INTEGER :: izmax, iz1min, iz1max
  INTEGER :: isdimd, iddimd, itdimd
  PARAMETER (isdimd=92+1, iddimd=100, itdimd=100)
  INTEGER :: nptnl, ndptnl, ndptn, ndptnc, ndcnct, ncnct
  PARAMETER (ndptnl=4, ndptn=128, ndptnc=256, ndcnct=100)
  INTEGER :: nptn(ndptnl), nptnc(ndptnl, ndptn)
  INTEGER :: iptnla(ndptnl), iptna(ndptnl, ndptn)
  INTEGER :: iptnca(ndptnl, ndptn, ndptnc)
  INTEGER :: icnctv(ndcnct)
  INTEGER :: iblmx, ismax
  INTEGER :: isppr(isdimd), ispbr(isdimd), isstgr(isdimd)
  INTEGER :: idmaxd, itmaxd
  LOGICAL :: lres, lstan, lptn
  REAL(kind=r8) :: dnr_ams
  CHARACTER :: dnr_ele*12
  INTEGER :: id
  LOGICAL :: first_stage, last_stage, same_element
!     log10 of densities (in 1/cm3), temperatures (in eV)
  REAL(kind=r8) :: ddensd(iddimd), dtevd(itdimd), logt, logd
!     log10 of rate coefficient (in cm3/s)
  REAL(kind=r8) :: dreac(isdimd, itdimd, iddimd), dtmp(itdimd, iddimd), &
& ans
!..   local variables
  INTEGER :: is, ic, ix, iy
  CHARACTER(len=2) :: elname
  CHARACTER(len=200) :: dsn
!..   procedures
  LOGICAL, EXTERNAL :: OPEN_FILE, STREQL
  EXTERNAL LOWER_CASE, XXDATA_11, XERRAB
  INTRINSIC TRIM
  INTRINSIC INDEX
  INTRINSIC NINT
  INTRINSIC LOG10
!
  CALL SUBINI('get_adas_data')
  is = 0
  dreac = 6.0_R8
  DO WHILE (is .LE. ns - 1)
    elname = is_codes(is)
    CALL LOWER_CASE(elname)
    IF (TRIM(elname) .EQ. 'd' .OR. TRIM(elname) .EQ. 't') elname = 'h'
    dsn = TRIM(reac)//'/'//TRIM(reac)//'_'//TRIM(elname)//'.dat'
    IF (OPEN_FILE(29, dsn, 'data.local/adas/adf11|modules/adas/adf11')) &
&   THEN
!     reading in data
!xpb The number 2 is dependent on the type of data being read
      IF (INDEX(reac, 'acd') .NE. 0) THEN
! recombination
        id = 1
        first_stage = .false.
        last_stage = .true.
      ELSE IF (INDEX(reac, 'scd') .NE. 0) THEN
! ionisation
        id = 2
        first_stage = .true.
        last_stage = .false.
      ELSE IF (INDEX(reac, 'ccd') .NE. 0) THEN
! charge exchange
        id = 3
        first_stage = .false.
        last_stage = .true.
      ELSE IF (INDEX(reac, 'prb') .NE. 0) THEN
! bremsstrahlung + radiative recombination contribution
        id = 4
        first_stage = .false.
        last_stage = .true.
      ELSE IF (INDEX(reac, 'plt') .NE. 0) THEN
! line radiation
        id = 8
        first_stage = .true.
        last_stage = .false.
      ELSE IF (INDEX(reac, 'zcd') .NE. 0) THEN
! effective charge
        id = 10
        first_stage = .true.
        last_stage = .true.
      ELSE IF (INDEX(reac, 'ycd') .NE. 0) THEN
! effective square charge
        id = 11
        first_stage = .true.
        last_stage = .true.
      ELSE IF (INDEX(reac, 'ecd') .NE. 0) THEN
! effective ionisation potential
        id = 12
        first_stage = .true.
        last_stage = .true.
      ELSE
        WRITE(6, *) 'ADAS data type ', TRIM(reac), ' not recognized!'
        CALL SUBEND()
        RETURN
      END IF
      CALL XXDATA_11(29, id, isdimd, iddimd, itdimd, ndptnl, ndptn, &
&              ndptnc, ndcnct, izmax, iz1min, iz1max, nptnl, nptn, nptnc&
&              , iptnla, iptna, iptnca, ncnct, icnctv, iblmx, ismax, &
&              dnr_ele, dnr_ams, isppr, ispbr, isstgr, idmaxd, itmaxd, &
&              ddensd, dtevd, dreac, lres, lstan, lptn)
      IF (izmax .NE. NINT(zn(is))) THEN
        WRITE(*, *) 'ADAS tables: wrong element.'
        WRITE(*, *) 'reading file: ', dsn
        CALL XERRAB('Wrong element in ADAS file')
      END IF
      IF (idmaxd .GT. iddimd .OR. itmaxd .GT. itdimd) THEN
        WRITE(*, *) 'ADAS tables: dimensions too big.'
        WRITE(*, *) 'Change iddimd and itdimd in source code.'
        WRITE(*, *) 'reading file: ', dsn
        CALL XERRAB('Too big dimensions in ADAS tables')
      END IF
!     filling array and computing rate
      DO ic=0,ncnct-1
        IF (((ic .EQ. 0 .AND. first_stage) .OR. (ic .EQ. ncnct - 1 .AND.&
&           last_stage)) .OR. (ic .GT. 0 .AND. ic .LT. ncnct - 1)) THEN
          IF (first_stage) THEN
            dtmp(1:itmaxd, 1:idmaxd) = dreac(ic+1, 1:itmaxd, 1:idmaxd)
          ELSE
            dtmp(1:itmaxd, 1:idmaxd) = dreac(ic, 1:itmaxd, 1:idmaxd)
          END IF
        ELSE IF (id .LT. 10) THEN
          dtmp(1:itmaxd, 1:idmaxd) = 6.0_R8
        ELSE
          dtmp(1:itmaxd, 1:idmaxd) = 0.0_R8
        END IF
        DO ix=0,nx+1
          DO iy=0,ny+1
            logt = LOG10(te(ix, iy)/qe)
            logd = LOG10(ne(ix, iy)/1.0e6_R8)
            CALL INT2D(dtmp(1:itdimd, 1:iddimd), itdimd, iddimd, dtevd, &
&                itmaxd, ddensd, idmaxd, logt, logd, ans)
            IF (id .LT. 10) THEN
              IF ((ic .EQ. 0 .AND. (.NOT.first_stage)) .OR. (ic .EQ. &
&                 ncnct - 1 .AND. (.NOT.last_stage))) THEN
                mach(ix, iy, is+ic) = 0.0_R8
              ELSE
                mach(ix, iy, is+ic) = 10.0_R8**(ans-6.0_R8)
              END IF
            ELSE
              mach(ix, iy, is+ic) = ans
            END IF
          END DO
        END DO
      END DO
      is = is + ncnct
      CLOSE(29) 
    ELSE
      WRITE(*, *) 'No ADAS file found : setting data to 0'
      same_element = .true.
      DO WHILE (same_element)
        mach(0:nx+1, 0:ny+1, is) = 0.0_R8
        ic = is
        is = is + 1
        IF (is .EQ. ns) THEN
          same_element = .false.
        ELSE
          same_element = STREQL(is_codes(is), is_codes(ic))
        END IF
      END DO
    END IF
  END DO
  CALL SUBEND()
  RETURN
END SUBROUTINE GET_ADAS_DATA_NODIFF

