SUBROUTINE OLD_CHANG_HINTON_NODIFF(nx, ny, ns, ismain, jsep, jxa, lambda&
& , safetyfactor, cr, cz, crx, bb, ne, te, ti, na, transport)
  USE B2MOD_TYPES
  USE B2MOD_RATES
  USE B2MOD_INDIRECT
  USE B2MOD_CONSTANTS
  USE B2MOD_B2CMPA_DIFF
  USE B2MOD_SUBSYS
  IMPLICIT NONE
  INTEGER :: nx, ny, ns, ismain, jsep, jxa
  REAL(kind=r8) :: lambda, cr(-1:nx, -1:ny), cz(-1:nx, -1:ny), crx(-1:nx&
& , -1:ny, 0:3), bb(-1:nx, -1:ny, 0:3), ne(-1:nx, -1:ny), te(-1:nx, -1:&
& ny), ti(-1:nx, -1:ny), na(-1:nx, -1:ny, 0:ns-1), safetyfactor(-1:ny), &
& transport(-1:nx, -1:ny, 0:ns-1)
!
  INTEGER :: ix, iy, is, irmin, irmax
  REAL(kind=r8) :: crmin, crmax, czmin, czmax, cr2max, cr2min, cr1max, &
& cr1min, bave, epsilon, lnlam, vthi, omegaci, rhoi, alpha, deltap, f1, &
& f2, taui, nui, nustari, mustari, chi
  EXTERNAL XERRAB
  INTRINSIC MIN
  INTRINSIC MAX
  INTRINSIC LOG
  INTRINSIC SQRT
  REAL(kind=r8) :: y1
!
  CALL SUBINI('old_chang_hinton')
  transport = 0
  DO iy=-1,jsep
    WRITE(*, *) 'iy: ', iy
    ix = jxa
    IF (.NOT.on_closed_surface(ix, iy)) THEN
      WRITE(*, *) 'We have a problem: not in core!'
      CALL XERRAB('Chang-Hinton: Not in core!')
    END IF
    irmin = jxa
    irmax = jxa
    crmin = cr(ix, iy)
    crmax = crmin
    czmin = cz(ix, iy)
    czmax = czmin
    ix = rightix(ix, iy)
    DO WHILE (ix .NE. jxa .AND. ix .GE. -1 .AND. ix .LE. nx)
      IF (cr(ix, iy) .LT. crmin) irmin = ix
      IF (cr(ix, iy) .GT. crmax) irmax = ix
      IF (crmin .GT. cr(ix, iy)) THEN
        crmin = cr(ix, iy)
      ELSE
        crmin = crmin
      END IF
      IF (crmax .LT. cr(ix, iy)) THEN
        crmax = cr(ix, iy)
      ELSE
        crmax = crmax
      END IF
      IF (czmin .GT. cz(ix, iy)) THEN
        czmin = cz(ix, iy)
      ELSE
        czmin = czmin
      END IF
      IF (czmax .LT. cz(ix, iy)) THEN
        czmax = cz(ix, iy)
      ELSE
        czmax = czmax
      END IF
      ix = rightix(ix, iy)
    END DO
    IF (ix .NE. jxa) THEN
      IF (nnreg(0) .GE. 2) THEN
        WRITE(*, *) 'We have a problem: core not continuous!'
        CALL XERRAB('Chang-Hinton: core not continuous!')
      ELSE
        WRITE(*, *) &
&      'Chang-Hinton calculation only available for closed field lines!'
        CALL SUBEND()
        RETURN
      END IF
    END IF
    WRITE(*, *) 'crmin,crmax,czmin,czmax', crmin, crmax, czmin, czmax
    bave = cr(ix, iy)*2.0_R8/(crmax+crmin)*bb(ix, iy, 3)
    WRITE(*, *) 'ne,te,ti,b,q: ', ne(ix, iy), te(ix, iy)/qe, ti(ix, iy)/&
&   qe, bave, safetyfactor(iy)
    epsilon = (crmax-crmin)/(crmax+crmin)
    WRITE(*, *) 'epsilon: ', epsilon
!xpb    Coulomb logarithm formula from Wesson for ion-ion collisions
    IF (lambda .LT. 0.0_R8) THEN
      y1 = 17.3_R8 - LOG(ne(ix, iy)/1.0e20_R8)/2.0_R8 + 1.5_R8*LOG(ti(ix&
&       , iy)/ev/1000.0_R8)
      IF (-lambda .LT. y1) THEN
        lnlam = y1
      ELSE
        lnlam = -lambda
      END IF
    ELSE
      lnlam = lambda
    END IF
    WRITE(*, *) 'lnlam: ', lnlam
    cr2max = 0.5_R8*(crx(irmax, iy, 1)+crx(irmax, iy, 3))
    cr2min = 0.5_R8*(crx(irmin, iy, 1)+crx(irmin, iy, 3))
    cr1max = 0.5_R8*(crx(irmax, iy, 0)+crx(irmax, iy, 2))
    cr1min = 0.5_R8*(crx(irmin, iy, 0)+crx(irmin, iy, 2))
    WRITE(*, *) 'cr2max,cr2min,cr1max,cr1min: ', cr2max, cr2min, cr1max&
&   , cr1min
    deltap = (cr2max+cr2min-(cr1max+cr1min))/(cr2max-cr2min-(cr1max-&
&     cr1min))
    WRITE(*, *) 'deltap: ', deltap
    f1 = (1_R8+1.5_R8*(epsilon**2+epsilon*deltap)+0.375_R8*epsilon**3*&
&     deltap)/(1_R8+0.5_R8*epsilon*deltap)
    WRITE(*, *) 'f1: ', f1
    f2 = SQRT(1_R8-epsilon**2)*(1.0_R8+0.5_R8*epsilon*deltap)/(1_R8+&
&     deltap/epsilon*(SQRT(1_R8-epsilon**2)-1.0_R8))
    WRITE(*, *) 'f2: ', f2
    DO is=0,ns-1
      IF (.NOT.is_neutral(is)) THEN
        WRITE(*, *) 'za,am,na: ', rza(ix, iy, is), am(is), na(ix, iy, is&
&       )
        vthi = SQRT(ti(ix, iy)/mp/am(is))
        WRITE(*, *) 'vthi: ', vthi
        omegaci = rza(ix, iy, is)*qe*bave/mp/am(is)
        WRITE(*, *) 'omegaci: ', omegaci
        rhoi = SQRT(2.0_R8)*vthi/omegaci
        WRITE(*, *) 'rhoi: ', rhoi
!!! think about this !!!
        alpha = na(ix, iy, is)*rz2(ix, iy, is)/na(ix, iy, ismain)/rz2(ix&
&         , iy, ismain)
        WRITE(*, *) 'alpha: ', alpha
        taui = 12.0_R8*pi**1.5_R8*eps0**2*(am(is)*mp)**0.5_R8*ti(ix, iy)&
&         **1.5_R8/(na(ix, iy, is)*rz2(ix, iy, is)**2*qe**4*lnlam)
        WRITE(*, *) 'taui: ', taui
        nui = 1.0_R8/taui
        nustari = nui*(crmax+crmin)/2.0_R8*safetyfactor(iy)/epsilon**&
&         1.5_R8/vthi
        WRITE(*, *) 'nustari: ', nustari
        mustari = nustari*(1.0_R8+1.54_R8*alpha)
        WRITE(*, *) 'mustari: ', mustari
        chi = epsilon**(-1.5_R8)*safetyfactor(iy)**2*rhoi**2/taui*((&
&         0.66_R8*(1.0_R8+1.54_R8*alpha)+(1.88_R8*epsilon**0.5_R8-&
&         1.54_R8*epsilon)*(1_R8+3.75_R8*alpha))/(1.0_R8+1.03_R8*mustari&
&         **0.5_R8+0.31*mustari)*f1+0.59_R8*mustari*epsilon/(1_R8+&
&         0.74_R8*mustari*epsilon**1.5_R8)*(1_R8+1.33_R8*alpha*(1.0_R8+&
&         0.60_R8*alpha)/(1.0_R8+1.79_R8*alpha))*(f1-f2))
        WRITE(*, *) 'chi: ', chi
        transport(ix, iy, is) = chi
        ix = rightix(ix, iy)
        DO WHILE (ix .NE. jxa .AND. ix .GE. -1 .AND. ix .LE. nx)
          transport(ix, iy, is) = chi
          ix = rightix(ix, iy)
        END DO
      END IF
    END DO
  END DO
  CALL SUBEND()
  RETURN
END SUBROUTINE OLD_CHANG_HINTON_NODIFF

