&       result1*SQRT(t0/me)*t0+ne(mpg%fccv(ifc, 1))*result2*SQRT(t1/me)*&
&       t1)/((ne(mpg%fccv(ifc, 1))+ne(mpg%fccv(ifc, 2)))*max1)
    END DO
  END IF
  ncall_b2tlhe = ncall_b2tlhe + 1
  CALL SUBEND()
  RETURN

