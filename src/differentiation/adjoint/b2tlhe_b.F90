  REAL(kind=r8) :: neb(ncv), teb(ncv), chvemxb(nfc)
!     ------------------------------------------------------------------
!     B2TLHE computes the electron heat transport flux limit velocity.
!     ------------------------------------------------------------------
!   ..local variables
  INTEGER :: icv, ifc
  REAL(kind=r8) :: t0, t1
  REAL(kind=r8) :: t0b, t1b
!   ..procedures
  INTRINSIC MAX, SQRT
  EXTERNAL XERTST, B2XVSG_NODIFF
  INTRINSIC ABS
  REAL(kind=r8) :: abs0
  REAL(kind=r8) :: abs1
