      IF (te(mpg%fccv(ifc, 1)) .LT. te(mpg%fccv(ifc, 2))) THEN
        t0 = te(mpg%fccv(ifc, 2))
      ELSE
        t0 = te(mpg%fccv(ifc, 1))
      END IF
      IF (geo%fcpbs(ifc) .GE. 0.) THEN
        abs0 = geo%fcpbs(ifc)
      ELSE
        abs0 = -geo%fcpbs(ifc)
      END IF
!srv 11.01.13
      chvemx(ifc) = cflme*SQRT(t0/me)*abs0
    END DO
  ELSE IF (switch%b2tqce_model .EQ. 2) THEN
! SOLPS4.0 treatment
    DO ifc=1,nfc
      t0 = te(mpg%fccv(ifc, 2))
      t1 = te(mpg%fccv(ifc, 1))
      IF (geo%fcqalf(ifc, 0) .GE. 0.) THEN
        abs1 = geo%fcqalf(ifc, 0)
      ELSE
        abs1 = -geo%fcqalf(ifc, 0)
      END IF
      IF (t0 .LT. t1) THEN
