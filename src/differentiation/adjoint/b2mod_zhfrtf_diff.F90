    END DO
    DO is=0,ns-1
      rho_a_rel(:, is) = rho_a_rel(:, is)/rho
    END DO
    avm_u = avm_u/rho
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_AVM_U

!  Differentiation of b2mod_zhfrtf_tgta in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: *nal *gt_ac *gtalc *avm_u *rho_a_rel
!                *gavm_uc ti na ua
!   with respect to varying inputs: *nal *gt_ac *gtalc *avm_u *rho_a_rel
!                *gavm_uc ti na ua
!   Plus diff mem management of: nal:in gt_ac:in gtalc:in avm_u:in
!                rho_a_rel:in gavm_uc:in mpg.intcellp:in geo.cvbb:in
!                geo.fchc:in geo.fcht:in geo.fcqgam:in geo.fcqalf:in
!                geo.fcqbet:in geo.vxvol:in
!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_TGTA_B(ncv, nfc, nvx, ns, switch, geo, geob, &
&   mpg, mpgb, na, nab, ua, uab, ti, tib)
    USE B2MOD_CONSTANTS
    USE B2US_GEO_DIFF
    USE B2US_MAP_DIFF
    USE B2MOD_SWITCHES_DIFF
    IMPLICIT NONE
!
!.declarations
!   ..input arguments
    INTEGER :: ncv, nfc, nvx, ns
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(GEOMETRY_DIFF) :: geob
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(MAPPING_DIFF) :: mpgb
    TYPE(SWITCHES), INTENT(IN) :: switch
    REAL(kind=r8) :: na(ncv, 0:ns-1), ua(ncv, 0:ns-1), ti(ncv)
    REAL(kind=r8) :: nab(ncv, 0:ns-1), uab(ncv, 0:ns-1), tib(ncv)
!
!   ..local variables
    INTEGER :: is, iz, inucl
    REAL(kind=r8) :: rho(ncv), w_a(ncv, 0:ns-1), t_a(ncv, 0:ns-1), wrkv(&
&   nvx)
    REAL(kind=r8) :: rhob(ncv), w_ab(ncv, 0:ns-1), t_ab(ncv, 0:ns-1), &
&   wrkvb(nvx)
    INTEGER*4 :: branch
    REAL(kind=r8), DIMENSION(ncv) :: temp
    REAL(kind=r8), DIMENSION(ncv) :: tempb
    INTEGER :: ad_to
!
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TGTA calculates temperature gradients and mass-weighted
!     average flow velocities for all ions.
!
!  3. description
!
!     This routine computes:
!     rho_A_rel: relative mass density of the ion "a" to the total ion mass density
!     avm_u: mass-weighted average flow velocity of all ions
!     gtalc: effective temperature gradient of the isonuclear sequence
!           (cell-centred quantity)
!     gt_Ac: temperature gradient of each ion charge state
!           (cell-centred quantity)
!
!-----------------------------------------------------------------------
!.computation
!
!   ..relative mass density and mass-weighted velocity
    rho = 0.0_R8
    avm_u = 0.0_R8
    DO is=0,ns-1
      IF (ALLOCATED(rho_a_rel)) THEN
        CALL PUSHREAL8ARRAY(rho_a_rel(:, is), r8*SIZE(rho_a_rel, 1)/8)
        CALL PUSHCONTROL1B(1)
      ELSE
        CALL PUSHCONTROL1B(0)
      END IF
      rho_a_rel(:, is) = is_i(is)*am(is)*na(:, is)
      rho = rho + rho_a_rel(:, is)
      avm_u = avm_u + rho_a_rel(:, is)*ua(:, is)
    END DO
    DO is=0,ns-1
      IF (ALLOCATED(rho_a_rel)) THEN
        CALL PUSHREAL8ARRAY(rho_a_rel(:, is), r8*SIZE(rho_a_rel, 1)/8)
        CALL PUSHCONTROL1B(1)
      ELSE
        CALL PUSHCONTROL1B(0)
      END IF
      rho_a_rel(:, is) = rho_a_rel(:, is)/rho
    END DO
    IF (ALLOCATED(avm_u)) THEN
      CALL PUSHREAL8ARRAY(avm_u, r8*SIZE(avm_u, 1)/8)
      CALL PUSHCONTROL1B(1)
    ELSE
      CALL PUSHCONTROL1B(0)
    END IF
    avm_u = avm_u/rho
!
!   ..temperature of each ion charge state
    DO is=0,ns-1
      CALL PUSHREAL8ARRAY(w_a(:, is), r8*ncv/8)
      w_a(:, is) = ua(:, is) - avm_u
      t_a(:, is) = ti + switch%zhd_corr*switch%zhd_corrt*(am(is)*mp*w_a(&
&       :, is)**2)/3.0_R8
    END DO
!
!   ..temperature gradient of each ion charge state  in the cell center
    IF (ALLOCATED(gt_ac)) THEN
      CALL PUSHREAL8ARRAY(gt_ac, r8*SIZE(gt_ac, 1)*SIZE(gt_ac, 2)/8)
      CALL PUSHCONTROL1B(1)
    ELSE
      CALL PUSHCONTROL1B(0)
    END IF
    gt_ac = 0.0_R8
    DO is=0,ns-1
      IF (.NOT.is_neutral(is)) THEN
        CALL GRADC_P_FWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, t_a(:, &
&                  is), wrkv, gt_ac(:, is))
        IF (ALLOCATED(gt_ac)) THEN
          CALL PUSHREAL8ARRAY(gt_ac(:, is), r8*SIZE(gt_ac, 1)/8)
          CALL PUSHCONTROL1B(1)
        ELSE
          CALL PUSHCONTROL1B(0)
        END IF
        gt_ac(:, is) = gt_ac(:, is)*geo%cvbb(:, 0)/geo%cvbb(:, 3)
        CALL PUSHCONTROL1B(1)
      ELSE
        CALL PUSHCONTROL1B(0)
      END IF
    END DO
    DO inucl=0,nspecies-1
      iz = znnucl_int(inucl) + 1
      CALL PUSHINTEGER4(iz - 1)
    END DO
!
!   ..average velocity gradient in the cell center
    CALL GRADC_P_FWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, avm_u, wrkv&
&              , gavm_uc)
!
    gavm_ucb = geo%cvbb(:, 0)*gavm_ucb/geo%cvbb(:, 3)
    wrkvb = 0.D0
    CALL GRADC_P_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, avm_u, &
&              avm_ub, wrkv, wrkvb, gavm_uc, gavm_ucb)
    gavm_ucb = 0.D0
    DO inucl=nspecies-1,0,-1
      CALL POPINTEGER4(ad_to)
      DO iz=ad_to,1,-1
        temp = na(:, nucl2s(inucl, iz))/nal(:, inucl)
        tempb = gt_ac(:, nucl2s(inucl, iz))*gtalcb(:, inucl)/nal(:, &
&         inucl)
        gt_acb(:, nucl2s(inucl, iz)) = gt_acb(:, nucl2s(inucl, iz)) + &
&         temp*gtalcb(:, inucl)
        nab(:, nucl2s(inucl, iz)) = nab(:, nucl2s(inucl, iz)) + tempb
        nalb(:, inucl) = nalb(:, inucl) - temp*tempb
      END DO
    END DO
    gtalcb = 0.D0
    t_ab = 0.D0
    DO is=ns-1,0,-1
      CALL POPCONTROL1B(branch)
      IF (branch .NE. 0) THEN
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 1) CALL POPREAL8ARRAY(gt_ac(:, is), r8*SIZE(&
&                                       gt_ac, 1)/8)
        gt_acb(:, is) = geo%cvbb(:, 0)*gt_acb(:, is)/geo%cvbb(:, 3)
        CALL GRADC_P_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, t_a(:, &
&                  is), t_ab(:, is), wrkv, wrkvb, gt_ac(:, is), gt_acb(:&
&                  , is))
        gt_acb(:, is) = 0.D0
      END IF
    END DO
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 1) CALL POPREAL8ARRAY(gt_ac, r8*SIZE(gt_ac, 1)*SIZE(&
&                                   gt_ac, 2)/8)
    gt_acb = 0.D0
    w_ab = 0.D0
    DO is=ns-1,0,-1
      tib = tib + t_ab(:, is)
      w_ab(:, is) = w_ab(:, is) + 2*w_a(:, is)*switch%zhd_corr*switch%&
&       zhd_corrt*am(is)*mp*t_ab(:, is)/3.0_R8
      t_ab(:, is) = 0.D0
      CALL POPREAL8ARRAY(w_a(:, is), r8*ncv/8)
      uab(:, is) = uab(:, is) + w_ab(:, is)
      avm_ub = avm_ub - w_ab(:, is)
      w_ab(:, is) = 0.D0
    END DO
    rhob = 0.D0
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 1) CALL POPREAL8ARRAY(avm_u, r8*SIZE(avm_u, 1)/8)
    rhob = -(avm_u*avm_ub/rho**2)
    avm_ub = avm_ub/rho
    DO is=ns-1,0,-1
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 1) CALL POPREAL8ARRAY(rho_a_rel(:, is), r8*SIZE(&
&                                     rho_a_rel, 1)/8)
      rhob = rhob - rho_a_rel(:, is)*rho_a_relb(:, is)/rho**2
      rho_a_relb(:, is) = rho_a_relb(:, is)/rho
    END DO
    DO is=ns-1,0,-1
      rho_a_relb(:, is) = rho_a_relb(:, is) + ua(:, is)*avm_ub + rhob
      uab(:, is) = uab(:, is) + rho_a_rel(:, is)*avm_ub
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 1) CALL POPREAL8ARRAY(rho_a_rel(:, is), r8*SIZE(&
&                                     rho_a_rel, 1)/8)
      nab(:, is) = nab(:, is) + is_i(is)*am(is)*rho_a_relb(:, is)
      rho_a_relb(:, is) = 0.D0
    END DO
    avm_ub = 0.D0
  END SUBROUTINE B2MOD_ZHFRTF_TGTA_B

!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_TGTA(ncv, nfc, nvx, ns, switch, geo, mpg, na, &
&   ua, ti)
    USE B2MOD_CONSTANTS
    USE B2US_GEO_DIFF
    USE B2US_MAP_DIFF
    USE B2MOD_SWITCHES_DIFF
    IMPLICIT NONE
!
!.declarations
!   ..input arguments
    INTEGER :: ncv, nfc, nvx, ns
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(SWITCHES), INTENT(IN) :: switch
    REAL(kind=r8) :: na(ncv, 0:ns-1), ua(ncv, 0:ns-1), ti(ncv)
!
!   ..local variables
    INTEGER :: is, iz, inucl
    REAL(kind=r8) :: rho(ncv), w_a(ncv, 0:ns-1), t_a(ncv, 0:ns-1), wrkv(&
&   nvx)
!
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TGTA calculates temperature gradients and mass-weighted
!     average flow velocities for all ions.
!
!  3. description
!
!     This routine computes:
!     rho_A_rel: relative mass density of the ion "a" to the total ion mass density
!     avm_u: mass-weighted average flow velocity of all ions
!     gtalc: effective temperature gradient of the isonuclear sequence
!           (cell-centred quantity)
!     gt_Ac: temperature gradient of each ion charge state
!           (cell-centred quantity)
!
!-----------------------------------------------------------------------
!.computation
!
!   ..relative mass density and mass-weighted velocity
    rho = 0.0_R8
    avm_u = 0.0_R8
    DO is=0,ns-1
      rho_a_rel(:, is) = is_i(is)*am(is)*na(:, is)
      rho = rho + rho_a_rel(:, is)
      avm_u = avm_u + rho_a_rel(:, is)*ua(:, is)
    END DO
    DO is=0,ns-1
      rho_a_rel(:, is) = rho_a_rel(:, is)/rho
    END DO
    avm_u = avm_u/rho
!
!   ..temperature of each ion charge state
    DO is=0,ns-1
      w_a(:, is) = ua(:, is) - avm_u
      t_a(:, is) = ti + switch%zhd_corr*switch%zhd_corrt*(am(is)*mp*w_a(&
&       :, is)**2)/3.0_R8
    END DO
!
!   ..temperature gradient of each ion charge state  in the cell center
    gt_ac = 0.0_R8
    DO is=0,ns-1
      IF (.NOT.is_neutral(is)) THEN
        CALL GRADC_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, t_a(:, is), wrkv&
&                     , gt_ac(:, is))
        gt_ac(:, is) = gt_ac(:, is)*geo%cvbb(:, 0)/geo%cvbb(:, 3)
      END IF
    END DO
!
!   ..effective temperature gradient of each isonuclear sequence in the cell center
    gtalc = 0.0_R8
    DO inucl=0,nspecies-1
      DO iz=1,znnucl_int(inucl)
        gtalc(:, inucl) = gtalc(:, inucl) + na(:, nucl2s(inucl, iz))/nal&
&         (:, inucl)*gt_ac(:, nucl2s(inucl, iz))
      END DO
    END DO
!
!   ..average velocity gradient in the cell center
    CALL GRADC_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, avm_u, wrkv, gavm_uc&
&                )
    gavm_uc = gavm_uc*geo%cvbb(:, 0)/geo%cvbb(:, 3)
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_TGTA

!  Differentiation of b2mod_zhfrtf_fr in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: *z2n_xy *av_ualpha *c_r_w smfr_p
!   with respect to varying inputs: *z2n_xy *av_ualpha *c_r_w coef
!                smfr_p
!   Plus diff mem management of: z2n_xy:in av_ualpha:in c_r_w:in
!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_FR_B(icv, is, coef, coefb, smfr_p, smfr_pb)
    IMPLICIT NONE
    INTEGER :: icv, is, inucl
    REAL(kind=r8) :: coef
    REAL(kind=r8) :: coefb
    REAL(kind=r8), DIMENSION(0:1) :: smfr_p
    REAL(kind=r8), DIMENSION(0:1) :: smfr_pb
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_FR calculates the friction force.
!
!  3. description
!
!     This routine computes:
!     -smfr_p(0): smbfrial(iCv,0) part of the friction force
!     -smfr_p(1): smbfrial(iCv,1) part of the friction force
!
!-----------------------------------------------------------------------
!  ..local variabless
    REAL(kind=r8) :: temp
    REAL(kind=r8) :: tempb
    REAL(kind=r8) :: tempb0
    REAL(kind=r8) :: tempb1
!
!   ..the first part of the friction force
    temp = 0
    DO inucl=0,nspecies-1
      temp = temp + z2n_xy(icv, inucl)*rmu(inucl, s2nucl(is)-1)*c_r_w(&
&       icv, inucl, s2nucl(is)-1)
    END DO
!
!   ..the second part of the friction force
    CALL PUSHREAL8(temp, r8/8)
    temp = 0
    DO inucl=0,nspecies-1
      temp = temp + z2n_xy(icv, inucl)*rmu(inucl, s2nucl(is)-1)*c_r_w(&
&       icv, inucl, s2nucl(is)-1)*av_ualpha(icv, inucl)
    END DO
    coefb = temp*smfr_pb(0)
    tempb = coef*smfr_pb(0)
    smfr_pb(0) = 0.D0
    DO inucl=nspecies-1,0,-1
      tempb0 = rmu(inucl, s2nucl(is)-1)*tempb
      c_r_wb0(icv, inucl, s2nucl(is)-1) = c_r_wb0(icv, inucl, s2nucl(is)&
&       -1) + z2n_xy(icv, inucl)*av_ualpha(icv, inucl)*tempb0
      tempb1 = c_r_w(icv, inucl, s2nucl(is)-1)*tempb0
      z2n_xyb(icv, inucl) = z2n_xyb(icv, inucl) + av_ualpha(icv, inucl)*&
&       tempb1
      av_ualphab(icv, inucl) = av_ualphab(icv, inucl) + z2n_xy(icv, &
&       inucl)*tempb1
    END DO
    CALL POPREAL8(temp, r8/8)
    coefb = coefb - temp*smfr_pb(1)
    tempb = -(coef*smfr_pb(1))
    smfr_pb(1) = 0.D0
    DO inucl=nspecies-1,0,-1
      tempb0 = rmu(inucl, s2nucl(is)-1)*tempb
      z2n_xyb(icv, inucl) = z2n_xyb(icv, inucl) + c_r_w(icv, inucl, &
&       s2nucl(is)-1)*tempb0
      c_r_wb0(icv, inucl, s2nucl(is)-1) = c_r_wb0(icv, inucl, s2nucl(is)&
&       -1) + z2n_xy(icv, inucl)*tempb0
    END DO
  END SUBROUTINE B2MOD_ZHFRTF_FR_B

!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_FR(icv, is, coef, smfr_p)
    IMPLICIT NONE
    INTEGER :: icv, is, inucl
    REAL(kind=r8) :: coef
    REAL(kind=r8), DIMENSION(0:1) :: smfr_p
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_FR calculates the friction force.
!
!  3. description
!
!     This routine computes:
!     -smfr_p(0): smbfrial(iCv,0) part of the friction force
!     -smfr_p(1): smbfrial(iCv,1) part of the friction force
!
!-----------------------------------------------------------------------
!  ..local variabless
    REAL(kind=r8) :: temp
!
!   ..the first part of the friction force
    temp = 0
    DO inucl=0,nspecies-1
      temp = temp + z2n_xy(icv, inucl)*rmu(inucl, s2nucl(is)-1)*c_r_w(&
&       icv, inucl, s2nucl(is)-1)
    END DO
    smfr_p(1) = -(coef*temp)
!
!   ..the second part of the friction force
    temp = 0
    DO inucl=0,nspecies-1
      temp = temp + z2n_xy(icv, inucl)*rmu(inucl, s2nucl(is)-1)*c_r_w(&
&       icv, inucl, s2nucl(is)-1)*av_ualpha(icv, inucl)
    END DO
    smfr_p(0) = coef*temp
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_FR

!  Differentiation of b2mod_zhfrtf_tf in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: *nal *ia *gt_ac *gtalc *c_r_ta
!                *c_r_tb smtf nais
!   with respect to varying inputs: *nal *ia *gt_ac *gtalc *c_r_ta
!                *c_r_tb nais
!   Plus diff mem management of: nal:in ia:in gt_ac:in gtalc:in
!                c_r_ta:in c_r_tb:in
!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_TF_B(icv, is, zhcscorr, nais, naisb, smtf, &
&   smtfb, smtf_nofl)
    IMPLICIT NONE
    INTEGER :: icv, is, zhcscorr
    REAL(kind=r8) :: smtf, smtf_nofl, nais
    REAL(kind=r8) :: smtfb, naisb
    INTRINSIC SUM
    REAL(kind=r8), DIMENSION(nspecies) :: temp
    REAL(kind=r8), DIMENSION(nspecies) :: temp0
    REAL(kind=r8), DIMENSION(nspecies) :: tempb
    INTEGER :: ii1
    REAL(kind=r8) :: temp1
    REAL(kind=r8) :: temp2
    REAL(kind=r8) :: temp3
    REAL(kind=r8) :: tempb0
    REAL(kind=r8) :: temp4
    REAL(kind=r8) :: temp5
    REAL(kind=r8) :: tempb1
    REAL(kind=r8) :: tempb2
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TF calculates the thermal force.
!
!  3. description
!
!     This routine computes:
!     -smtf: the thermal force
!     -smtf_nofl: the thermal force without flux limiting
!
!-----------------------------------------------------------------------
!
!   ..the thermal force before charge state correction
!
!   ..the thermal force before charge state correction without flux limiting
!
!   ..apply the charge state correction
    IF (zhcscorr .EQ. 1) THEN
      temp1 = gtalc(icv, s2nucl(is)-1)
      temp2 = nal(icv, s2nucl(is)-1)
      temp3 = nais*gt_ac(icv, is)/temp2
      temp4 = nal(icv, s2nucl(is)-1)
      temp5 = c_r_tb(icv, s2nucl(is)-1)
      tempb0 = -((temp3-ia(icv, is)*temp1)*smtfb)
      tempb1 = -(temp5*temp4*smtfb)
      tempb2 = tempb1/temp2
      iab(icv, is) = iab(icv, is) - temp1*tempb1
      gtalcb(icv, s2nucl(is)-1) = gtalcb(icv, s2nucl(is)-1) - ia(icv, is&
&       )*tempb1
      naisb = naisb + gt_ac(icv, is)*tempb2
      gt_acb(icv, is) = gt_acb(icv, is) + nais*tempb2
      nalb(icv, s2nucl(is)-1) = nalb(icv, s2nucl(is)-1) + temp5*tempb0 -&
&       temp3*tempb2
      c_r_tbb(icv, s2nucl(is)-1) = c_r_tbb(icv, s2nucl(is)-1) + temp4*&
&       tempb0
    END IF
    temp = nal(icv, 0:nspecies-1)*gtalc(icv, 0:nspecies-1)
    temp0 = c_r_ta(icv, s2nucl(is)-1, 0:nspecies-1)
    iab(icv, is) = iab(icv, is) - SUM(temp0*temp)*smtfb
    ii1 = nspecies + 1
    DO ii1=1,nspecies
      c_r_tab(icv, s2nucl(is)-1, ii1-1) = c_r_tab(icv, s2nucl(is)-1, ii1&
&       -1) + temp(ii1)*tempb(ii1)
    END DO
    DO ii1=1,nspecies
      nalb(icv, ii1-1) = nalb(icv, ii1-1) + gtalc(icv, ii1-1)*temp0(ii1)&
&       *tempb(ii1)
    END DO
    DO ii1=1,nspecies
      gtalcb(icv, ii1-1) = gtalcb(icv, ii1-1) + nal(icv, ii1-1)*temp0(&
&       ii1)*tempb(ii1)
    END DO
  END SUBROUTINE B2MOD_ZHFRTF_TF_B

!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_TF(icv, is, zhcscorr, nais, smtf, smtf_nofl)
    IMPLICIT NONE
    INTEGER :: icv, is, zhcscorr
    REAL(kind=r8) :: smtf, smtf_nofl, nais
    INTRINSIC SUM
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TF calculates the thermal force.
!
!  3. description
!
!     This routine computes:
!     -smtf: the thermal force
!     -smtf_nofl: the thermal force without flux limiting
!
!-----------------------------------------------------------------------
!
!   ..the thermal force before charge state correction
    smtf = -(ia(icv, is)*SUM(nal(icv, 0:nspecies-1)*c_r_ta(icv, s2nucl(&
&     is)-1, 0:nspecies-1)*gtalc(icv, 0:nspecies-1)))
!
!   ..the thermal force before charge state correction without flux limiting
    smtf_nofl = -(ia(icv, is)*SUM(nal(icv, 0:nspecies-1)*c_r_ta_nofl(icv&
&     , s2nucl(is)-1, 0:nspecies-1)*gtalc(icv, 0:nspecies-1)))
!
!   ..apply the charge state correction
    IF (zhcscorr .EQ. 1) THEN
      smtf = smtf - c_r_tb(icv, s2nucl(is)-1)*nal(icv, s2nucl(is)-1)*(&
&       nais/nal(icv, s2nucl(is)-1)*gt_ac(icv, is)-ia(icv, is)*gtalc(icv&
&       , s2nucl(is)-1))
!
      smtf_nofl = smtf_nofl - c_r_tb_nofl(icv, s2nucl(is)-1)*nal(icv, &
&       s2nucl(is)-1)*(nais/nal(icv, s2nucl(is)-1)*gt_ac(icv, is)-ia(icv&
&       , is)*gtalc(icv, s2nucl(is)-1))
    END IF
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_TF
