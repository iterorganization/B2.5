        t3 = 1.0_R8/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
        t2b = t2b + t3*SUM(aab(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg&
&         %cvnvp(icv1, 2)-1), MASK=mask2(1:mpg%cvnvp(icv1, 2)))
        CALL POPBOOLEANARRAY(mask2(1:mpg%cvnvp(icv1, 2)), mpg%cvnvp(icv1&
&                      , 2))
      END DO
      DO incv=mpg%vxcvp(ivx1, 2),1,-1
        t3 = 1.0_R8/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
        t1b = t1b - t3*SUM(aab(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg&
&         %cvnvp(icv1, 2)-1), MASK=mask1(1:mpg%cvnvp(icv1, 2)))
        CALL POPBOOLEANARRAY(mask1(1:mpg%cvnvp(icv1, 2)), mpg%cvnvp(icv1&
&                      , 2))
      END DO
    END IF
    flcb0b(ifc, 1) = flcb0b(ifc, 1) + 0.5_R8*t2b - 0.5_R8*t1b
    cvsb0b(ifc, 1) = cvsb0b(ifc, 1) - t2b - t1b
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      t1b = SUM(aab(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2&
&       , 2)-1), MASK=mask0(1:mpg%cvnvp(icv2, 2)))
      t2b = -aab(mpg%cvnvp(icv2, 1))
    ELSE
      t1b = 0.D0
      t2b = 0.D0
    END IF
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      t2b = t2b + SUM(aab(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%&
&       cvnvp(icv1, 2)-1), MASK=mask(1:mpg%cvnvp(icv1, 2)))
      t1b = t1b - aab(mpg%cvnvp(icv1, 1))
    END IF
    flcb0b(ifc, 0) = flcb0b(ifc, 0) + 0.5_R8*t2b - 0.5_R8*t1b
    cvsb0b(ifc, 0) = cvsb0b(ifc, 0) - t2b - t1b
  END DO
!$BWD-OF II-LOOP 
  DO icv=1,ncv
    ireg = mpg%cvreg(icv)
    IF (solvereg(ireg)) THEN
      csb = SQRT(pz(icv)/rzb(icv))
      csb1 = SQRT(ub(icv)**2 + csb**2)
!srv 09.01.01
      IF (switch%b2mndt_style .LT. 2) THEN
        IF (resmb(icv) .GE. 0.) THEN
          abs0 = resmb(icv)
          CALL PUSHCONTROL1B(0)
        ELSE
          abs0 = -resmb(icv)
          CALL PUSHCONTROL1B(1)
        END IF
        aad0b(icv) = aad0b(icv) + aab(mpg%cvnvp(icv, 1))
        tempb = aab(mpg%cvnvp(icv, 1))/(rxg*csb1)
        aab(mpg%cvnvp(icv, 1)) = 0.D0
        abs0b = tempb
        csb1b = -(abs0*tempb/csb1)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          resmbb(icv) = resmbb(icv) + abs0b
        ELSE
          resmbb(icv) = resmbb(icv) - abs0b
        END IF
      ELSE
        aad0b(icv) = aad0b(icv) + aab(mpg%cvnvp(icv, 1))
        aab(mpg%cvnvp(icv, 1)) = 0.D0
        csb1b = 0.D0
      END IF
      aad0b(icv) = aad0b(icv) + aab(mpg%cvnvp(icv, 1))
      aab(mpg%cvnvp(icv, 1)) = 0.D0
      tempb = switch%cfc0*geo%cvvol(icv)*geo%cvhz(icv)*geo%cvbb(icv, 0)*&
&       aad0b(icv)/(geo%cvhx(icv)*geo%cvbb(icv, 3))
      smbb(icv, 3) = smbb(icv, 3) - rob(icv)*aad0b(icv)
      robb(icv) = robb(icv) + csb*tempb - smb(icv, 3)*aad0b(icv)
      smbb(icv, 1) = smbb(icv, 1) - aad0b(icv)
      aad0b(icv) = 0.D0
      csbb = rob(icv)*tempb
      IF (ub(icv)**2 + csb**2 .EQ. 0.D0) THEN
        tempb = 0.D0
      ELSE
        tempb = csb1b/(2.0*SQRT(ub(icv)**2+csb**2))
      END IF
      ubb(icv) = ubb(icv) + 2*ub(icv)*tempb
      csbb = csbb + 2*csb*tempb
      temp = pz(icv)/rzb(icv)
      IF (temp .EQ. 0.D0) THEN
        tempb = 0.D0
      ELSE
        tempb = csbb/(rzb(icv)*2.0*SQRT(temp))
      END IF
      pzb(icv) = pzb(icv) + tempb
      rzbb(icv) = rzbb(icv) - temp*tempb
    ELSE
      aad0b(icv) = 0.D0
      aab(mpg%cvnvp(icv, 1)) = 0.D0
    END IF
  END DO
  CALL CALCCOEF_BWD(ncv, nfc, nvx, switch%b2npmo_discr_meth, geo, flcb, &
&             flcbb, cvsb, cvsbb, flcb0, flcb0b, cvsb0, cvsb0b)
END SUBROUTINE B2USMO_B

