      ELSE
        aa(mpg%cvnvp(icv, 1)) = aad0(icv)
      END IF
    ELSE
      aa(mpg%cvnvp(icv, 1)) = 1.0_R8
      aad0(icv) = 0.0_R8
    END IF
  END DO
