!=======================================================================
!*** A filter to be used with the combfile subroutine to fix the
!*** user.trc file when the b2usrtrc subroutine changes.
!*** Input
!***  data  the data row, already amended by combfile
!***  chdr  the data header, already amended by combfile
!***  nhdr  the number of positions in the data and header rows
!***  mrks  the markers as found in the file
