!            print frd,csrntr(j),(lprntr(i,j),i=1,nnsp)
!          end do !}
!          print frh,'lspntr:',cspntr
!          do j=1,nntr !{
!            print frd,csrntr(j),(lspntr(i,j),i=1,nnsp)
!          end do !}
!        end if !}
!!###}
!}
    IF (nntr .GT. 0) THEN
!{
!
!*** Correct the neutral data
!
!{
      DO j=1,nntr
        m = larea(j)
        k = lnarea(j)
        IF (m .GT. 0 .AND. k .GT. 0) data(k) = data(m)
!{
        DO i=1,nnsp
          m = lprntr(i, j)
          k = lnnntr(i, j)
          l = lntntr(i, j)
          ii = lspntr(i, j)
!}
          IF (k .GT. 0 .AND. l .GT. 0 .AND. m .GT. 0) THEN
!{
            data(m) = ev*data(k)*data(l)
          END IF
          IF (ii .GT. 0) data(ii) = 1.283*data(ii)
!}

        END DO
!}

      END DO
    END IF
    RETURN
!=======================================================================
  END IF
