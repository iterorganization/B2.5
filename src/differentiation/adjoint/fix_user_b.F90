!*** lv     version number from the file (the first marker)
!*** nntr   number of diagnostic surface groups for neutral data
!*** number of neutral species found
!*** larea  positions of the surface area of the groups to be fixed
!*** lntntr positions of temperature of species i at surface group j
!*** lnnntr positions of density of species i at surface group j
!*** lprntr positions of pressure of species i at surface group j
!*** lspntr positions of pumping speed for species i at surface group j
!*** cspntr identifiers of the neutral species
!*** csrntr identifiers of the surface groups
!
  INTEGER :: lv, nntr, nnsp
  INTEGER, ALLOCATABLE :: larea(:), lnarea(:), lntntr(:, :), lnnntr(:, :&
& ), lprntr(:, :), lspntr(:, :)
