                  IF (STREQL(cc, cspntr(j))) THEN
!{
                    ex = .false.
                    GOTO 120
                  END IF
                END DO
!}
 120            IF (ex) THEN
!{
                  k = k + 1
                  j = k
                  cspntr(k) = TRIM(cc)
                END IF
!}
                IF (chdr(i)(1:1) .EQ. 'n') THEN
!{
                  lnnntr(j, ii) = i
                ELSE IF (chdr(i)(1:1) .EQ. 'T') THEN
!}{
                  lntntr(j, ii) = i
                ELSE IF (chdr(i)(1:1) .EQ. 'p') THEN
!}{
                  lprntr(j, ii) = i
                ELSE IF (chdr(i)(1:1) .EQ. 'S') THEN
!}{
                  lspntr(j, ii) = i
                END IF
              END IF
            END IF
!}

          END DO
        END IF
      END IF
    END IF
!!###{
!        if(nntr.gt.0) then !{
!          write(frh,'(7h(1x,a8,,i2,3ha9))') nnsp
