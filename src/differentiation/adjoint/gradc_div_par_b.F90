  wrkf = geo%fcpbs
  CALL DIV0_NODIFF(ncv, nfc, mpg, wrkf, wrkc)
  gfunpar = (gfunpar-fun*wrkc)/geo%cvvol
!
!   ..set zero in guard cells (no reliable information from div0 there!)
