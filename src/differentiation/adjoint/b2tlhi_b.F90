        CALL PUSHREAL8ARRAY(t0, r8/8)
        t0 = ti(mpg%fccv(ifc, 1))
        CALL PUSHCONTROL1B(1)
      END IF
      IF (geo%fcpbs(ifc) .GE. 0.) THEN
        CALL PUSHREAL8ARRAY(abs0, r8/8)
        abs0 = geo%fcpbs(ifc)
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHREAL8ARRAY(abs0, r8/8)
        abs0 = -geo%fcpbs(ifc)
        CALL PUSHCONTROL1B(1)
      END IF
