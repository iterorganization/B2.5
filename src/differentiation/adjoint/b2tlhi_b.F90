  INTEGER :: icv, ifc
  REAL(kind=r8) :: t0, t1
  REAL(kind=r8) :: t0b, t1b
!   ..procedures
  INTRINSIC MAX, SQRT
  EXTERNAL XERTST, B2XVFX_NODIFF
  INTRINSIC ABS
  REAL(kind=r8) :: abs0
  REAL(kind=r8) :: abs1
  REAL(kind=r8) :: max1
  REAL(kind=r8) :: max1b
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
  REAL(kind=r8) :: temp
  REAL(kind=r8) :: tempb
  REAL(kind=r8) :: temp0
  REAL(kind=r8) :: temp1
  REAL(kind=r8) :: tempb0
  REAL(kind=r8) :: tempb1
  REAL(kind=r8) :: temp2
  REAL(kind=r8) :: tempb2
  REAL(kind=r8) :: temp3
  REAL(kind=r8) :: temp4
  REAL(kind=r8) :: tempb3
  INTEGER :: branch
!   ..initialisation
!     ------------------------------------------------------------------
!   ..subprogram start-up calls
!   ..test mp, cflmi
!
!   ..compute flux limit velocity
  IF (switch%b2tqca_model .EQ. 1) THEN
! SOLPS5.0 treatments
