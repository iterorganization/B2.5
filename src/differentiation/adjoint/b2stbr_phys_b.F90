        tif = pl%ti(icv)
        tnf = pl%tn(icv)
        CALL PUSHCONTROL1B(0)
      ELSE
        tef = (pl%te(mpg%fccv(ifc, 1))*geo%fchc(ifc, 2)+pl%te(mpg%fccv(&
&         ifc, 2))*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        tif = (pl%ti(mpg%fccv(ifc, 1))*geo%fchc(ifc, 2)+pl%ti(mpg%fccv(&
&         ifc, 2))*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        tnf = (pl%tn(mpg%fccv(ifc, 1))*geo%fchc(ifc, 2)+pl%tn(mpg%fccv(&
&         ifc, 2))*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        CALL PUSHCONTROL1B(1)
      END IF
      IF (potential_at_guard_cell .EQ. 1) THEN
!        write(6,*) 'Potential taken at guard cell (phys)'
        pof = pl%po(icv)
        CALL PUSHCONTROL1B(1)
      ELSE
!        write(6,*) 'Potential taken at real cell (phys)'
        pof = pl%po(icn)
        CALL PUSHCONTROL1B(0)
      END IF
      zaf(0:ns-1) = (rt%rza(mpg%fccv(ifc, 1), 0:ns-1)*geo%fchc(ifc, 2)+&
&       rt%rza(mpg%fccv(ifc, 2), 0:ns-1)*geo%fchc(ifc, 1))/(geo%fchc(ifc&
&       , 1)+geo%fchc(ifc, 2))
      IF (lcore) THEN
        surface_temp = 0.0_R8
      ELSE
        surface_temp = 0.0_R8
      END IF
!
!    ..loop over species
!mb   Loop implicitly expects that the species are arranged per isonuclear sequence and that the neutral atom of that sequence al
!ways comes first. If not, erroneous results will be obtained.
      is0 = 0
      is1 = 0
      nnsum = 0.0_R8
      nnwwnsum = 0.0_R8
      DO is=nsmin,nsmax
        IF (am(is0) - am(is) .GE. 0.) THEN
          abs0 = am(is0) - am(is)
        ELSE
          abs0 = -(am(is0)-am(is))
        END IF
        IF (zn(is0) .NE. zn(is) .OR. amtol .LT. abs0) THEN
          CALL PUSHINTEGER4(is0)
          is0 = is
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
!dpc
        IF (is_neutral(is)) is1 = is
        IF (is0 .NE. is1) THEN
          CALL PUSHINTEGER4(is0)
          is0 = is1
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        CALL PUSHINTEGER4(is2)
        is2 = -1
        k = 0
        found = .false.
        ad_count = 0
        DO WHILE (.NOT.found .AND. k .LT. nscx)
          IF (is0 .EQ. iscx(k)) THEN
            found = .true.
            is2 = k
          END IF
          k = k + 1
          ad_count = ad_count + 1
        END DO
        CALL PUSHINTEGER4(ad_count)
        IF (0.0_R8 .LT. isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))) &
&       THEN
          max5 = isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
          max5 = 0.0_R8
        END IF
!dpc
!       (species (is) recycles into is0)
!       (is0 corresponds to CX species is2)
! dpc: Eirene coupling 1999.06.28
        CALL PUSHREAL8ARRAY(t0, r8/8)
        t0 = max5*neutral_sources_rescale
        IF (0.0_R8 .LT. tif/ev + (pof-phi_app)*zaf(is)) THEN
          einc = tif/ev + (pof-phi_app)*zaf(is)
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
          einc = 0.0_R8
        END IF
!! end KU Leuven advanced fluid neutral models
        IF (switch%recycle_afn .EQ. 0 .OR. zn(is0) .NE. 1) THEN
!! default neutral recycling/reflection model (always for non-hydrogenic species)
!wdk  recycle toroidal componenent of parallel momentum, factor mrecyc
!     (toroidal piece only, because poloidal piece of parallel velocity flips sign after reflection)
          IF (switch%lin_shi0 .EQ. 1) THEN
            x1 = erecyc(is, istra)*(1.5_R8+switch%boris)*einc*ev
            IF (x1 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              CALL PUSHREAL8ARRAY(max6, r8/8)
              max6 = (1.5_R8+switch%boris)*surface_temp*kbolt
              CALL PUSHCONTROL1B(0)
            ELSE
              CALL PUSHREAL8ARRAY(max6, r8/8)
              max6 = x1
              CALL PUSHCONTROL1B(1)
            END IF
            CALL PUSHCONTROL2B(0)
          ELSE
            x2 = erecyc(is, istra)*(1.5_R8+switch%boris)*einc*ev
            IF (x2 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              CALL PUSHREAL8ARRAY(max7, r8/8)
              max7 = (1.5_R8+switch%boris)*surface_temp*kbolt
              CALL PUSHCONTROL1B(0)
            ELSE
              CALL PUSHREAL8ARRAY(max7, r8/8)
              max7 = x2
              CALL PUSHCONTROL1B(1)
            END IF
            CALL PUSHCONTROL2B(1)
          END IF
        ELSE
!
!! use advanced fluid neutral boundary conditions by N. Horsten et. al
! lvn1
!**************************************************************************************
!*****************     New KU Leuven recycling model for neutrals *********************
!**************************************************************************************
!
! main reference:
!   Horsten, Niels, Giovanni Samaey, and M. Baelmans.
!  "Development and assessment of 2D fluid neutral models that include atomic databases and a microscopic reflection model."
!   Nuclear Fusion 57.11 (2017): 116043.
!
!! area of the face
          CALL PUSHREAL8ARRAY(farea, r8/8)
          farea = geo%fcs(ifc)
          CALL PUSHREAL8ARRAY(cosa, r8/8)
          cosa = geo%fcqalf(ifc, 0)
          sina = geo%fcqalf(ifc, 1)
!
!
!! end of preliminary calculations
!
          IF (is .NE. is0) THEN
!! ion species, recycling
!
!
!mb   net momentum sources from neutral particle recombination
!            Calculate recycled neutral particle flux (assuming thin boundary cells)
            IF (.NOT.lcore) THEN
!
              CALL PUSHREAL8ARRAY(int0r, r8/8)
              CALL PUSHREAL8ARRAY(int0l, r8/8)
              CALL PUSHREAL8ARRAY(int3r, r8/8)
              CALL PUSHREAL8ARRAY(int3l, r8/8)
              CALL PUSHREAL8ARRAY(int6r, r8/8)
              CALL PUSHREAL8ARRAY(int6l, r8/8)
              CALL PUSHREAL8ARRAY(int2r, r8/8)
              CALL PUSHREAL8ARRAY(int2l, r8/8)
              CALL PUSHREAL8ARRAY(int5r, r8/8)
              CALL PUSHREAL8ARRAY(int5l, r8/8)
              CALL PUSHREAL8ARRAY(int1r, r8/8)
              CALL PUSHREAL8ARRAY(int1l, r8/8)
              CALL PUSHREAL8ARRAY(int4r, r8/8)
              CALL PUSHREAL8ARRAY(int4l, r8/8)
              CALL CALCRECYCLEDFLUXES(icv, mpg%nci, ifc, isign, is0, is2&
&                               , is, istra, iwall, phi_app, farea, &
&                               b2recyc(is, istra), recycm(is, istra), &
&                               fluid_frac_hyb(ifc), pl, tif, tef, pof, &
&                               pl%na(icv, is), dv%ne(icv), geo, mpg, t0&
&                               , cosa, sina, switch%use_uy_uz_0, fnnrec&
&                               , fmomrec, nnrec, nnwwnrec, fenerec)
              CALL PUSHCONTROL1B(0)
            ELSE
              CALL PUSHCONTROL1B(1)
              nnrec = 0.0_R8
              nnwwnrec = 0.0_R8
            END IF
!
!
            nnsum = nnsum + nnrec
            nnwwnsum = nnwwnsum + nnwwnrec
!mb   net neutral energy source from recycling
!
            IF (switch%tn_style .EQ. 0) THEN
              CALL PUSHCONTROL3B(0)
            ELSE IF (switch%tn_style .EQ. 1) THEN
              CALL PUSHCONTROL3B(1)
            ELSE
              CALL PUSHCONTROL3B(2)
            END IF
          ELSE
!
!! this else belongs to "if(.not.(is.eq.is0))", hence 'is' will be neutral here
!
!          Identify corresponding ion species
            IF (LNEXT(is, is + 1)) THEN
              CALL PUSHINTEGER4(isi)
              isi = is + 1
              CALL PUSHCONTROL1B(0)
            ELSE
              CALL PUSHCONTROL1B(1)
            END IF
!
!          A conversion is necessary because the BC implemented below were developed in a code that uses a total energy equation,
!          while B2.5 uses internal energy equations.
!
!
!
            IF (switch%tn_style .EQ. 0) THEN
              CALL PUSHCONTROL2B(0)
            ELSE IF (switch%tn_style .EQ. 1) THEN
              CALL PUSHCONTROL2B(1)
            ELSE
              CALL PUSHCONTROL2B(2)
            END IF
!
            IF (maxw(istra) .EQ. 0) THEN
              CALL PUSHREAL8ARRAY(int0r, r8/8)
              CALL PUSHREAL8ARRAY(int0l, r8/8)
              CALL PUSHREAL8ARRAY(int3r, r8/8)
              CALL PUSHREAL8ARRAY(int3l, r8/8)
              CALL PUSHREAL8ARRAY(int6r, r8/8)
              CALL PUSHREAL8ARRAY(int6l, r8/8)
              CALL PUSHREAL8ARRAY(int2r, r8/8)
              CALL PUSHREAL8ARRAY(int2l, r8/8)
              CALL PUSHREAL8ARRAY(int5r, r8/8)
              CALL PUSHREAL8ARRAY(int5l, r8/8)
              CALL PUSHREAL8ARRAY(int1r, r8/8)
              CALL PUSHREAL8ARRAY(int1l, r8/8)
              CALL PUSHREAL8ARRAY(int4r, r8/8)
              CALL PUSHREAL8ARRAY(int4l, r8/8)
              CALL PUSHREAL8ARRAY(cutlo, r8/8)
              CALL PUSHREAL8ARRAY(cutll, r8/8)
              CALL PUSHBOOLEAN(b2mod_math_initialised)
              CALL PUSHREAL8ARRAY(small_r4_constant, r4/8)
              CALL CALCINCIDENTFLUXESDIFFUSION(icv, icn, ifc, isign, is0&
&                                        , is2, isi, istra, phi_app, &
&                                        farea, pl, tif, tnf, tef, pof, &
&                                        dv%ne(icv), geo, rt, dnndy, &
&                                        dnndz, cosa, sina, switch%&
&                                        use_uy_uz_0, switch%use_uy_uz_1&
&                                        , switch%diffusion_bc_safeguard&
&                                        , fnni, fmomni, nni, nnwwni, &
&                                        feneni)
              CALL PUSHCONTROL2B(0)
            ELSE IF (maxw(istra) .EQ. 1) THEN
              CALL PUSHREAL8ARRAY(int0r, r8/8)
              CALL PUSHREAL8ARRAY(int0l, r8/8)
              CALL PUSHREAL8ARRAY(int3r, r8/8)
              CALL PUSHREAL8ARRAY(int3l, r8/8)
              CALL PUSHREAL8ARRAY(int6r, r8/8)
              CALL PUSHREAL8ARRAY(int6l, r8/8)
              CALL PUSHREAL8ARRAY(int2r, r8/8)
              CALL PUSHREAL8ARRAY(int2l, r8/8)
              CALL PUSHREAL8ARRAY(int5r, r8/8)
              CALL PUSHREAL8ARRAY(int5l, r8/8)
              CALL PUSHREAL8ARRAY(int1r, r8/8)
              CALL PUSHREAL8ARRAY(int1l, r8/8)
              CALL PUSHREAL8ARRAY(int4r, r8/8)
              CALL PUSHREAL8ARRAY(int4l, r8/8)
              CALL CALCINCIDENTFLUXESMAXWELLIAN(icv, ifc, is0, isign, &
&                                         istra, farea, pl, tnf, geo, &
&                                         cosa, sina, fnni, fmomni, nni&
&                                         , nnwwni, feneni)
              CALL PUSHCONTROL2B(1)
            ELSE IF (maxw(istra) .EQ. 2) THEN
              CALL PUSHREAL8ARRAY(int0r, r8/8)
              CALL PUSHREAL8ARRAY(int0l, r8/8)
              CALL PUSHREAL8ARRAY(int3r, r8/8)
              CALL PUSHREAL8ARRAY(int3l, r8/8)
              CALL PUSHREAL8ARRAY(int6r, r8/8)
              CALL PUSHREAL8ARRAY(int6l, r8/8)
              CALL PUSHREAL8ARRAY(int2r, r8/8)
              CALL PUSHREAL8ARRAY(int2l, r8/8)
              CALL PUSHREAL8ARRAY(int5r, r8/8)
              CALL PUSHREAL8ARRAY(int5l, r8/8)
              CALL PUSHREAL8ARRAY(int1r, r8/8)
              CALL PUSHREAL8ARRAY(int1l, r8/8)
              CALL PUSHREAL8ARRAY(int4r, r8/8)
              CALL PUSHREAL8ARRAY(int4l, r8/8)
              CALL PUSHREAL8ARRAY(cutlo, r8/8)
              CALL PUSHREAL8ARRAY(cutll, r8/8)
              CALL PUSHBOOLEAN(b2mod_math_initialised)
              CALL PUSHREAL8ARRAY(small_r4_constant, r4/8)
              CALL CALCINCIDENTFLUXESKN(switch%kn_b1, switch%kn_b2, icv&
&                                 , icn, ifc, isign, is0, is2, isi, &
&                                 istra, phi_app, farea, pl, tif, tnf, &
&                                 tef, pof, dv%ne(icv), geo, rt, dnndy, &
&                                 dnndz, cosa, sina, switch%use_uy_uz_0&
&                                 , switch%use_uy_uz_1, switch%&
&                                 l_macro_afn, fnni, fmomni, nni, nnwwni&
&                                 , feneni)
              CALL PUSHCONTROL2B(2)
            ELSE
              CALL PUSHREAL8ARRAY(int0r, r8/8)
              CALL PUSHREAL8ARRAY(int0l, r8/8)
              CALL PUSHREAL8ARRAY(int3r, r8/8)
              CALL PUSHREAL8ARRAY(int3l, r8/8)
              CALL PUSHREAL8ARRAY(int6r, r8/8)
              CALL PUSHREAL8ARRAY(int6l, r8/8)
              CALL PUSHREAL8ARRAY(int2r, r8/8)
              CALL PUSHREAL8ARRAY(int2l, r8/8)
              CALL PUSHREAL8ARRAY(int5r, r8/8)
              CALL PUSHREAL8ARRAY(int5l, r8/8)
              CALL PUSHREAL8ARRAY(int1r, r8/8)
              CALL PUSHREAL8ARRAY(int1l, r8/8)
              CALL PUSHREAL8ARRAY(int4r, r8/8)
              CALL PUSHREAL8ARRAY(int4l, r8/8)
              CALL PUSHREAL8ARRAY(cutlo, r8/8)
              CALL PUSHREAL8ARRAY(cutll, r8/8)
              CALL PUSHBOOLEAN(b2mod_math_initialised)
              CALL PUSHREAL8ARRAY(small_r4_constant, r4/8)
              CALL CALCINCIDENTFLUXESMINKN(icv, icn, ifc, isign, is0, &
&                                    is2, isi, istra, phi_app, farea, pl&
&                                    , tif, tnf, tef, pof, dv%ne(icv), &
&                                    geo, rt, dnndy, dnndz, cosa, sina, &
&                                    switch%use_uy_uz_0, switch%&
&                                    use_uy_uz_1, fnni, fmomni, nni, &
&                                    nnwwni, feneni)
!
              CALL PUSHCONTROL2B(3)
            END IF
!
!
!! save the incident fluid neutral flux
!
!mb   net momentum sources from neutral particle reflection
            IF (.NOT.lcore) THEN
              IF (maxw(istra) .EQ. 0) THEN
                CALL PUSHREAL8ARRAY(int0r, r8/8)
                CALL PUSHREAL8ARRAY(int0l, r8/8)
                CALL PUSHREAL8ARRAY(int3r, r8/8)
                CALL PUSHREAL8ARRAY(int3l, r8/8)
                CALL PUSHREAL8ARRAY(int6r, r8/8)
                CALL PUSHREAL8ARRAY(int6l, r8/8)
                CALL PUSHREAL8ARRAY(int2r, r8/8)
                CALL PUSHREAL8ARRAY(int2l, r8/8)
                CALL PUSHREAL8ARRAY(int5r, r8/8)
                CALL PUSHREAL8ARRAY(int5l, r8/8)
                CALL PUSHREAL8ARRAY(int1r, r8/8)
                CALL PUSHREAL8ARRAY(int1l, r8/8)
                CALL PUSHREAL8ARRAY(int4r, r8/8)
                CALL PUSHREAL8ARRAY(int4l, r8/8)
                CALL PUSHREAL8ARRAY(cutlo, r8/8)
                CALL PUSHREAL8ARRAY(cutll, r8/8)
                CALL PUSHBOOLEAN(b2mod_math_initialised)
                CALL PUSHREAL8ARRAY(small_r4_constant, r4/8)
                CALL CALCREFLECTEDFLUXESDIFFUSION(icv, icn, mpg%nci, ifc&
&                                           , isign, is0, is2, isi, &
&                                           istra, iwall, phi_app, farea&
&                                           , b2recyc(is, istra), recycm&
&                                           (is, istra), fluid_frac_hyb(&
&                                           ifc), pl, tif, tnf, tef, pof&
&                                           , dv%ne(icv), geo, mpg, rt, &
&                                           dnndy, dnndz, cosa, sina, &
&                                           switch%use_uy_uz_0, switch%&
&                                           use_uy_uz_1, switch%&
&                                           diffusion_bc_safeguard, &
&                                           fnnrefl, fmomrefl, nnrefl, &
&                                           nnwwnrefl, fenerefl, fene_el&
&                                          )
                CALL PUSHCONTROL3B(0)
              ELSE IF (maxw(istra) .EQ. 1) THEN
                CALL PUSHREAL8ARRAY(int0r, r8/8)
                CALL PUSHREAL8ARRAY(int0l, r8/8)
                CALL PUSHREAL8ARRAY(int3r, r8/8)
                CALL PUSHREAL8ARRAY(int3l, r8/8)
                CALL PUSHREAL8ARRAY(int6r, r8/8)
                CALL PUSHREAL8ARRAY(int6l, r8/8)
                CALL PUSHREAL8ARRAY(int2r, r8/8)
                CALL PUSHREAL8ARRAY(int2l, r8/8)
                CALL PUSHREAL8ARRAY(int5r, r8/8)
                CALL PUSHREAL8ARRAY(int5l, r8/8)
                CALL PUSHREAL8ARRAY(int1r, r8/8)
                CALL PUSHREAL8ARRAY(int1l, r8/8)
                CALL PUSHREAL8ARRAY(int4r, r8/8)
                CALL PUSHREAL8ARRAY(int4l, r8/8)
                CALL CALCREFLECTEDFLUXESMAXWELLIAN(icv, mpg%nci, ifc, &
&                                            isign, is0, istra, iwall, &
&                                            farea, b2recyc(is, istra), &
&                                            recycm(is, istra), &
&                                            fluid_frac_hyb(ifc), pl, &
&                                            tnf, geo, mpg, cosa, sina, &
&                                            fnnrefl, fmomrefl, nnrefl, &
&                                            nnwwnrefl, fenerefl, &
&                                            fene_el)
                CALL PUSHCONTROL3B(1)
              ELSE IF (maxw(istra) .EQ. 2) THEN
                CALL PUSHREAL8ARRAY(int0r, r8/8)
                CALL PUSHREAL8ARRAY(int0l, r8/8)
                CALL PUSHREAL8ARRAY(int3r, r8/8)
                CALL PUSHREAL8ARRAY(int3l, r8/8)
                CALL PUSHREAL8ARRAY(int6r, r8/8)
                CALL PUSHREAL8ARRAY(int6l, r8/8)
                CALL PUSHREAL8ARRAY(int2r, r8/8)
                CALL PUSHREAL8ARRAY(int2l, r8/8)
                CALL PUSHREAL8ARRAY(int5r, r8/8)
                CALL PUSHREAL8ARRAY(int5l, r8/8)
                IF (ALLOCATED(fna_mol)) THEN
                  CALL PUSHREAL8ARRAY(fna_mol, r8*SIZE(fna_mol, 1)*SIZE(&
&                               fna_mol, 2)/8)
                  CALL PUSHCONTROL1B(1)
                ELSE
                  CALL PUSHCONTROL1B(0)
                END IF
                CALL PUSHREAL8ARRAY(int1r, r8/8)
                CALL PUSHREAL8ARRAY(int1l, r8/8)
                CALL PUSHREAL8ARRAY(int4r, r8/8)
                CALL PUSHREAL8ARRAY(int4l, r8/8)
                CALL PUSHREAL8ARRAY(cutlo, r8/8)
                CALL PUSHREAL8ARRAY(cutll, r8/8)
                CALL PUSHBOOLEAN(b2mod_math_initialised)
                CALL PUSHREAL8ARRAY(small_r4_constant, r4/8)
                CALL CALCREFLECTEDFLUXESKN(switch%kn_b1, switch%kn_b2, &
&                                    icv, icn, mpg%nci, ifc, isign, is0&
&                                    , is2, isi, istra, iwall, phi_app, &
&                                    farea, b2recyc(is, istra), recycm(&
&                                    is, istra), fluid_frac_hyb(ifc), pl&
&                                    , tif, tnf, tef, pof, dv%ne(icv), &
&                                    geo, mpg, rt, dnndy, dnndz, cosa, &
&                                    sina, switch%use_uy_uz_0, switch%&
&                                    use_uy_uz_1, switch%l_macro_afn, &
&                                    fnnrefl, fmomrefl, nnrefl, &
&                                    nnwwnrefl, fenerefl, fene_el)
                CALL PUSHCONTROL3B(2)
              ELSE
                CALL PUSHREAL8ARRAY(int0r, r8/8)
                CALL PUSHREAL8ARRAY(int0l, r8/8)
                CALL PUSHREAL8ARRAY(int3r, r8/8)
                CALL PUSHREAL8ARRAY(int3l, r8/8)
                CALL PUSHREAL8ARRAY(int6r, r8/8)
                CALL PUSHREAL8ARRAY(int6l, r8/8)
                CALL PUSHREAL8ARRAY(int2r, r8/8)
                CALL PUSHREAL8ARRAY(int2l, r8/8)
                CALL PUSHREAL8ARRAY(int5r, r8/8)
                CALL PUSHREAL8ARRAY(int5l, r8/8)
                CALL PUSHREAL8ARRAY(int1r, r8/8)
                CALL PUSHREAL8ARRAY(int1l, r8/8)
                CALL PUSHREAL8ARRAY(int4r, r8/8)
                CALL PUSHREAL8ARRAY(int4l, r8/8)
                CALL PUSHREAL8ARRAY(cutlo, r8/8)
                CALL PUSHREAL8ARRAY(cutll, r8/8)
                CALL PUSHBOOLEAN(b2mod_math_initialised)
                CALL PUSHREAL8ARRAY(small_r4_constant, r4/8)
                CALL CALCREFLECTEDFLUXESMINKN(icv, icn, mpg%nci, ifc, &
&                                       isign, is0, is2, isi, istra, &
&                                       iwall, phi_app, farea, b2recyc(&
&                                       is, istra), recycm(is, istra), &
&                                       fluid_frac_hyb(ifc), pl, tif, &
&                                       tnf, tef, pof, dv%ne(icv), geo, &
&                                       mpg, rt, dnndy, dnndz, cosa, &
&                                       sina, switch%use_uy_uz_0, switch&
&                                       %use_uy_uz_1, fnnrefl, fmomrefl&
&                                       , nnrefl, nnwwnrefl, fenerefl, &
&                                       fene_el)
                CALL PUSHCONTROL3B(3)
              END IF
            ELSE
              nnrefl = 0.0_R8
              nnwwnrefl = 0.0_R8
              CALL PUSHCONTROL3B(4)
            END IF
!
!
            nnsum = nnsum + nni + nnrefl
            nnwwnsum = nnwwnsum + nnwwni + nnwwnrefl
!
            IF (switch%tn_style .EQ. 0) THEN
              CALL PUSHCONTROL2B(0)
            ELSE IF (switch%tn_style .EQ. 1) THEN
              CALL PUSHCONTROL2B(1)
            ELSE
              CALL PUSHCONTROL2B(2)
            END IF
!
! remove the Franck-Condon energy, that has been provided to the neutrals, from the electrons at the boundary
! temporary switch to evaluate the effect.
            IF (switch%remove_fc_el .EQ. 1) THEN
              CALL PUSHCONTROL3B(3)
            ELSE
              CALL PUSHCONTROL3B(4)
            END IF
          END IF
!
          IF (zamax(is) .EQ. zn(is)) THEN
!! see eq. 62-63 of the main reference.
            IF (nnsum .EQ. 0.0_R8) THEN
              nnsum = 1.0_R8
              CALL PUSHCONTROL1B(1)
            ELSE
              CALL PUSHCONTROL1B(0)
            END IF
