&                                   use_uy_uz_0, switch%use_uy_uz_1, &
&                                   switch%l_macro_afn, fnni, fnnib, &
&                                   fmomni, fmomnib, nni, nnib, nnwwni, &
&                                   nnwwnib, feneni, fenenib)
            ELSE
              CALL POPREAL8ARRAY(small_r4_constant, r4/8)
              CALL POPBOOLEAN(b2mod_math_initialised)
              CALL POPREAL8ARRAY(cutll, r8/8)
              CALL POPREAL8ARRAY(cutlo, r8/8)
              CALL POPREAL8ARRAY(int4l, r8/8)
              CALL POPREAL8ARRAY(int4r, r8/8)
              CALL POPREAL8ARRAY(int1l, r8/8)
              CALL POPREAL8ARRAY(int1r, r8/8)
              CALL POPREAL8ARRAY(int5l, r8/8)
              CALL POPREAL8ARRAY(int5r, r8/8)
              CALL POPREAL8ARRAY(int2l, r8/8)
              CALL POPREAL8ARRAY(int2r, r8/8)
              CALL POPREAL8ARRAY(int6l, r8/8)
              CALL POPREAL8ARRAY(int6r, r8/8)
              CALL POPREAL8ARRAY(int3l, r8/8)
              CALL POPREAL8ARRAY(int3r, r8/8)
              CALL POPREAL8ARRAY(int0l, r8/8)
              CALL POPREAL8ARRAY(int0r, r8/8)
              CALL CALCINCIDENTFLUXESMINKN_B(icv, icn, ifc, isign, is0, &
&                                      is2, isi, istra, phi_app, farea, &
&                                      pl, plb, tif, tifb, tnf, tnfb, &
&                                      tef, tefb, pof, dv%ne(icv), dvb%&
&                                      ne(icv), geo, geob, rt, rtb, &
&                                      dnndy, dnndyb, dnndz, dnndzb, &
&                                      cosa, sina, switch%use_uy_uz_0, &
&                                      switch%use_uy_uz_1, fnni, fnnib, &
&                                      fmomni, fmomnib, nni, nnib, &
&                                      nnwwni, nnwwnib, feneni, fenenib)
            END IF
            CALL POPCONTROL2B(branch)
            IF (branch .EQ. 0) THEN
              q_convb = srwb%shi0(icv, 0)
            ELSE IF (branch .EQ. 1) THEN
              q_convb = 0.D0
            ELSE
              q_convb = srwb%shn0(icv, 0)
            END IF
            tempb1 = isign*neutral_sources_rescale*q_convb
            tempb2 = geo%fcpbs(ifc)*tempb1
            dvb%fhm(ifc, 0, is0) = dvb%fhm(ifc, 0, is0) + tempb1
            dvb%fhm(ifc, 1, is0) = dvb%fhm(ifc, 1, is0) + tempb1
            tempb = pl%na(icv, is0)*tempb2
            plb%na(icv, is0) = plb%na(icv, is0) + tnf*pl%ua(icv, is0)*&
&             tempb2
            tnfb = tnfb + pl%ua(icv, is0)*tempb
            plb%ua(icv, is0) = plb%ua(icv, is0) + tnf*tempb
            CALL POPCONTROL1B(branch)
            IF (branch .EQ. 0) CALL POPINTEGER4(isi)
            GOTO 100
          END IF
          nnwwnrecb = nnwwnsumb
          nnrecb = nnsumb
          smonb = srwb%smo0(icv, 0, is0)
          fmomrecb = neutral_sources_rescale*geo%cvhz(icv)*smonb
          snanb = srwb%sna0(icv, 0, is0)
          fnnrecb = neutral_sources_rescale*snanb
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            CALL POPREAL8ARRAY(int4l, r8/8)
            CALL POPREAL8ARRAY(int4r, r8/8)
            CALL POPREAL8ARRAY(int1l, r8/8)
            CALL POPREAL8ARRAY(int1r, r8/8)
            CALL POPREAL8ARRAY(int5l, r8/8)
            CALL POPREAL8ARRAY(int5r, r8/8)
            CALL POPREAL8ARRAY(int2l, r8/8)
            CALL POPREAL8ARRAY(int2r, r8/8)
            CALL POPREAL8ARRAY(int6l, r8/8)
            CALL POPREAL8ARRAY(int6r, r8/8)
            CALL POPREAL8ARRAY(int3l, r8/8)
            CALL POPREAL8ARRAY(int3r, r8/8)
            CALL POPREAL8ARRAY(int0l, r8/8)
            CALL POPREAL8ARRAY(int0r, r8/8)
            CALL CALCRECYCLEDFLUXES_B(icv, mpg%nci, ifc, isign, is0, is2&
&                               , is, istra, iwall, phi_app, phi_appb, &
&                               farea, b2recyc(is, istra), b2recycb(is, &
&                               istra), recycm(is, istra), &
&                               fluid_frac_hyb(ifc), fluid_frac_hybb(ifc&
&                               ), pl, plb, tif, tifb, tef, pof, pofb, &
&                               pl%na(icv, is), dv%ne(icv), geo, mpg, t0&
&                               , t0b, cosa, sina, switch%use_uy_uz_0, &
&                               fnnrec, fnnrecb, fmomrec, fmomrecb, &
&                               nnrec, nnrecb, nnwwnrec, nnwwnrecb, &
&                               fenerec, fenerecb)
          END IF
 100      CALL POPREAL8ARRAY(cosa, r8/8)
          CALL POPREAL8ARRAY(farea, r8/8)
          eincb = 0.D0
        END IF
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          tifb = tifb + eincb/ev
          pofb = pofb + zaf(is)*eincb
          phi_appb = phi_appb - zaf(is)*eincb
          zafb(is) = zafb(is) + (pof-phi_app)*eincb
        ELSE
          eincb = 0.D0
        END IF
        CALL POPREAL8ARRAY(t0, r8/8)
        max5b = neutral_sources_rescale*t0b
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          dvb%fna(ifc, 0, is) = dvb%fna(ifc, 0, is) + isign*max5b
          dvb%fna(ifc, 1, is) = dvb%fna(ifc, 1, is) + isign*max5b
        END IF
        CALL POPINTEGER4(ad_count)
        i0 = ad_count + 1
        CALL POPINTEGER4(is2)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) CALL POPINTEGER4(is0)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) CALL POPINTEGER4(is0)
      END DO
      tempb0 = zafb(0:ns-1)/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
      rtb%rza(mpg%fccv(ifc, 1), 0:ns-1) = rtb%rza(mpg%fccv(ifc, 1), 0:ns&
&       -1) + geo%fchc(ifc, 2)*tempb0
      rtb%rza(mpg%fccv(ifc, 2), 0:ns-1) = rtb%rza(mpg%fccv(ifc, 2), 0:ns&
&       -1) + geo%fchc(ifc, 1)*tempb0
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        plb%po(icn) = plb%po(icn) + pofb
      ELSE
        plb%po(icv) = plb%po(icv) + pofb
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        plb%tn(icv) = plb%tn(icv) + tnfb
        plb%ti(icv) = plb%ti(icv) + tifb
        plb%te(icv) = plb%te(icv) + tefb
      ELSE
        tempb = tnfb/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        plb%tn(mpg%fccv(ifc, 1)) = plb%tn(mpg%fccv(ifc, 1)) + geo%fchc(&
&         ifc, 2)*tempb
        plb%tn(mpg%fccv(ifc, 2)) = plb%tn(mpg%fccv(ifc, 2)) + geo%fchc(&
&         ifc, 1)*tempb
        tempb = tifb/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        plb%ti(mpg%fccv(ifc, 1)) = plb%ti(mpg%fccv(ifc, 1)) + geo%fchc(&
&         ifc, 2)*tempb
        plb%ti(mpg%fccv(ifc, 2)) = plb%ti(mpg%fccv(ifc, 2)) + geo%fchc(&
&         ifc, 1)*tempb
        tempb = tefb/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        plb%te(mpg%fccv(ifc, 1)) = plb%te(mpg%fccv(ifc, 1)) + geo%fchc(&
&         ifc, 2)*tempb
        plb%te(mpg%fccv(ifc, 2)) = plb%te(mpg%fccv(ifc, 2)) + geo%fchc(&
&         ifc, 1)*tempb
      END IF
    END IF
  END SUBROUTINE RECYCLE_B

!
!-----------------------------------------------------------------------
!
!
  SUBROUTINE RECYCLE(istra, nsmin, nsmax, icv, icn, ifc, isign, phi_app&
&   , lcore, dnndy, dnndz)
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: istra, nsmin, nsmax, icv, icn, ifc
    REAL(kind=r8) :: phi_app, pof, isign, dnndy, dnndz
    LOGICAL :: lcore, found
!
    INTEGER :: is0, is1, is2, is, isi, k, iwall
    REAL(kind=r8) :: tef, tif, tnf, taf, zaf(0:ns-1), rpf(0:ns-1), ptf, &
&   t0
! new variables for advanced fluid neutral reflection/recycling model
    REAL(kind=r8) :: fnni, fmomni, nni, nnwwni, feneni, fnnrefl, fnnrec&
&   , fmomrefl, fmomrec, fenerec, fenerefl, fene_el, nnrefl, nnwwnrefl, &
&   nnrec, nnwwnrec, nnsum, nnwwnsum, snan, smon, farea, q_conv, sina, &
&   cosa
    INTRINSIC MAX
    INTRINSIC ABS
    INTRINSIC SQRT
    EXTERNAL XERRAB
    REAL(r8) :: y1
    REAL(r8) :: y2
    REAL(kind=r8) :: x1
    REAL(kind=r8) :: x2
    REAL(kind=r8) :: x3
    REAL(r8) :: max1
    REAL(r8) :: max2
    REAL(r8) :: max3
    REAL(r8) :: max4
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: abs1
    REAL(r8) :: max5
    REAL(kind=r8) :: max6
    REAL(kind=r8) :: max7
    REAL(kind=r8) :: max8
    REAL(kind=r8) :: abs2
    REAL(kind=r8) :: abs3
    REAL(kind=r8) :: abs4
    REAL(r8) :: max9
!
!
! if nsmin = b2species_start = -1, this means that the spatially hybrid method for neutrals is used
! and this strata should be ignored here
    IF (nsmin .LT. 0) THEN
      RETURN
    ELSE
!
      IF (temperature_at_guard_cell .EQ. 1) THEN
        tef = pl%te(icv)
        tif = pl%ti(icv)
        tnf = pl%tn(icv)
      ELSE
        tef = (pl%te(mpg%fccv(ifc, 1))*geo%fchc(ifc, 2)+pl%te(mpg%fccv(&
&         ifc, 2))*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        tif = (pl%ti(mpg%fccv(ifc, 1))*geo%fchc(ifc, 2)+pl%ti(mpg%fccv(&
&         ifc, 2))*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        tnf = (pl%tn(mpg%fccv(ifc, 1))*geo%fchc(ifc, 2)+pl%tn(mpg%fccv(&
&         ifc, 2))*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
      END IF
      IF (potential_at_guard_cell .EQ. 1) THEN
!        write(6,*) 'Potential taken at guard cell (phys)'
        pof = pl%po(icv)
      ELSE
!        write(6,*) 'Potential taken at real cell (phys)'
        pof = pl%po(icn)
      END IF
      zaf(0:ns-1) = (rt%rza(mpg%fccv(ifc, 1), 0:ns-1)*geo%fchc(ifc, 2)+&
&       rt%rza(mpg%fccv(ifc, 2), 0:ns-1)*geo%fchc(ifc, 1))/(geo%fchc(ifc&
&       , 1)+geo%fchc(ifc, 2))
      rpf(0:ns-1) = (rt%rpt(mpg%fccv(ifc, 1), 0:ns-1)*geo%fchc(ifc, 2)+&
&       rt%rpt(mpg%fccv(ifc, 2), 0:ns-1)*geo%fchc(ifc, 1))/(geo%fchc(ifc&
&       , 1)+geo%fchc(ifc, 2))
      y1 = isign*(dv%fhi(ifc, 0)+dv%fhi(ifc, 1)+st_ext%fhi(ifc, 0)+&
&       st_ext%fhi(ifc, 1))
      IF (0.0_R8 .LT. y1) THEN
        max1 = y1
      ELSE
        max1 = 0.0_R8
      END IF
      qin = max1/geo%fcs(ifc)
      DO is=0,ns-1
        IF (0.0_R8 .LT. isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))) &
&       THEN
          max2 = isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))
        ELSE
          max2 = 0.0_R8
        END IF
        qin = qin + max2*((dv%kinrgy(icv, is)+tif)*(1.0_R8-switch%boris)&
&         +rpf(is)*ev)/geo%fcs(ifc)
      END DO
      DO is=0,st_ext%ns-1
        ptf = (st_ext%pt(mpg%fccv(ifc, 1), is)*geo%fchc(ifc, 2)+st_ext%&
&         pt(mpg%fccv(ifc, 2), is)*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+&
&         geo%fchc(ifc, 2))
        taf = (st_ext%ta(mpg%fccv(ifc, 1), is)*geo%fchc(ifc, 2)+st_ext%&
&         ta(mpg%fccv(ifc, 2), is)*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+&
&         geo%fchc(ifc, 2))
        IF (0.0_R8 .LT. isign*(st_ext%fa(ifc, 0, is)+st_ext%fa(ifc, 1, &
&           is))) THEN
          max3 = isign*(st_ext%fa(ifc, 0, is)+st_ext%fa(ifc, 1, is))
        ELSE
          max3 = 0.0_R8
        END IF
        qin = qin + max3*((0.5_R8*st_ext%am(is)*mp*st_ext%ua(icv, is)**2&
&         +taf)*(1.0_R8-switch%boris)+ptf*ev)/geo%fcs(ifc)
      END DO
      y2 = isign*(dv%fhe(ifc, 0)+dv%fhe(ifc, 1)+(dv%fne(ifc, 0)+dv%fne(&
&       ifc, 1))*tef*(1.0_R8-switch%boris))
      IF (0.0_R8 .LT. y2) THEN
        max4 = y2
      ELSE
        max4 = 0.0_R8
      END IF
      qel = max4/geo%fcs(ifc)
      IF (lcore) THEN
        surface_temp = 0.0_R8
      ELSE
        surface_temp = 0.0_R8
      END IF
!
!    ..loop over species
!mb   Loop implicitly expects that the species are arranged per isonuclear sequence and that the neutral atom of that sequence al
!ways comes first. If not, erroneous results will be obtained.
      is0 = 0
      is1 = 0
      nnsum = 0.0_R8
      nnwwnsum = 0.0_R8
      DO is=nsmin,nsmax
        IF (am(is0) - am(is) .GE. 0.) THEN
          abs0 = am(is0) - am(is)
        ELSE
          abs0 = -(am(is0)-am(is))
        END IF
        IF (zn(is0) .NE. zn(is) .OR. amtol .LT. abs0) is0 = is
!dpc
        IF (is_neutral(is)) is1 = is
        IF (am(is1) - am(is) .GE. 0.) THEN
          abs1 = am(is1) - am(is)
        ELSE
          abs1 = -(am(is1)-am(is))
        END IF
        IF (zn(is1) .NE. zn(is) .OR. amtol .LT. abs1) WRITE(*, *) &
&                                        'b2stbr problem: is, is0, is1 '&
&                                                     , is, is0, is1
        IF (is0 .NE. is1) THEN
          IF (ncall_b2stbr_phys .EQ. 0) WRITE(*, *) &
&                                       'b2stbr: setting is0 to is1 ', &
&                                       is0, is1
          is0 = is1
        END IF
        is2 = -1
        k = 0
        found = .false.
        DO WHILE (.NOT.found .AND. k .LT. nscx)
          IF (is0 .EQ. iscx(k)) THEN
            found = .true.
            is2 = k
          END IF
          k = k + 1
        END DO
        IF (0.0_R8 .LT. isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))) &
&       THEN
          max5 = isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))
        ELSE
          max5 = 0.0_R8
        END IF
!dpc
!       (species (is) recycles into is0)
!       (is0 corresponds to CX species is2)
! dpc: Eirene coupling 1999.06.28
        t0 = max5*neutral_sources_rescale
        IF (0.0_R8 .LT. tif/ev + (pof-phi_app)*zaf(is)) THEN
          einc = tif/ev + (pof-phi_app)*zaf(is)
        ELSE
          einc = 0.0_R8
        END IF
! xpb: Incident particle flux (part/m^2/s)
        pflx = t0/geo%fcs(ifc)
        IF (is_neutral(is)) THEN
          vbar = SQRT(8.0_R8*pl%ti(icv)/(pi*am(is)*mp))
! dpc: Eirene coupling 1999.06.28
          pflx = pflx + pl%na(icv, is)*vbar*alpha*&
&           neutral_sources_rescale
        END IF
!! end KU Leuven advanced fluid neutral models
        IF (switch%recycle_afn .EQ. 0 .OR. zn(is0) .NE. 1) THEN
!! default neutral recycling/reflection model (always for non-hydrogenic species)
          srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + b2recyc(is, &
&           istra)*t0
!wdk  recycle toroidal componenent of parallel momentum, factor mrecyc
!     (toroidal piece only, because poloidal piece of parallel velocity flips sign after reflection)
          srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + mrecyc(is, &
&           istra)*am(is)*mp*pl%ua(icv, is)*geo%cvbb(icv, 2)**2/geo%cvbb&
&           (icv, 3)**2*b2recyc(is, istra)*t0*geo%cvhz(icv)
          IF (switch%lin_shi0 .EQ. 1) THEN
            x1 = erecyc(is, istra)*(1.5_R8+switch%boris)*einc*ev
            IF (x1 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              max6 = (1.5_R8+switch%boris)*surface_temp*kbolt
            ELSE
              max6 = x1
            END IF
            srw%shi0(icv, 1) = srw%shi0(icv, 1) + &
&             recycled_neutrals_contr*max6*b2recyc(is, istra)*t0/pl%ti(&
&             icv)
          ELSE
            x2 = erecyc(is, istra)*(1.5_R8+switch%boris)*einc*ev
            IF (x2 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              max7 = (1.5_R8+switch%boris)*surface_temp*kbolt
            ELSE
              max7 = x2
            END IF
            srw%shi0(icv, 0) = srw%shi0(icv, 0) + &
&             recycled_neutrals_contr*max7*b2recyc(is, istra)*t0
          END IF
! xpb: recycled neutrals come back with at least thermal energy
          IF (is2 .NE. -1) THEN
            x3 = erecyc(is, istra)*(1.5_R8+switch%boris)*einc*ev
            IF (x3 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              max8 = (1.5_R8+switch%boris)*surface_temp*kbolt
            ELSE
              max8 = x3
            END IF
            shi0_ff(icv, is2) = shi0_ff(icv, is2) + &
&             recycled_neutrals_contr*max8*b2recyc(is, istra)*t0
          END IF
        ELSE
!
!! use advanced fluid neutral boundary conditions by N. Horsten et. al
! lvn1
!**************************************************************************************
!*****************     New KU Leuven recycling model for neutrals *********************
!**************************************************************************************
!
! main reference:
!   Horsten, Niels, Giovanni Samaey, and M. Baelmans.
!  "Development and assessment of 2D fluid neutral models that include atomic databases and a microscopic reflection model."
!   Nuclear Fusion 57.11 (2017): 116043.
!
!! area of the face
          farea = geo%fcs(ifc)
          cosa = geo%fcqalf(ifc, 0)
          sina = geo%fcqalf(ifc, 1)
!
!
!! end of preliminary calculations
          IF (erecyc(is, istra) .GT. 0.0_R8 .OR. mrecyc(is, istra) .GT. &
&             0.0_R8) WRITE(*, *) &
&                     'recycle_afn incompatible with user-defined '//&
&                     'momentum and energy recycling coefficients > 0'
!
          IF (is .NE. is0) THEN
!! ion species, recycling
!
!
!mb   net momentum sources from neutral particle recombination
!            Calculate recycled neutral particle flux (assuming thin boundary cells)
            IF (.NOT.lcore) THEN
!
              CALL CALCRECYCLEDFLUXES(icv, mpg%nci, ifc, isign, is0, is2&
&                               , is, istra, iwall, phi_app, farea, &
&                               b2recyc(is, istra), recycm(is, istra), &
&                               fluid_frac_hyb(ifc), pl, tif, tef, pof, &
&                               pl%na(icv, is), dv%ne(icv), geo, mpg, t0&
&                               , cosa, sina, switch%use_uy_uz_0, fnnrec&
&                               , fmomrec, nnrec, nnwwnrec, fenerec)
            ELSE
              fnnrec = 0.0_R8
              fmomrec = 0.0_R8
              nnrec = 0.0_R8
              nnwwnrec = 0.0_R8
              fenerec = 0.0_R8
            END IF
!
            snan = fnnrec*neutral_sources_rescale
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + snan
            smon = fmomrec*geo%cvhz(icv)*neutral_sources_rescale
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + smon
!
            nnsum = nnsum + nnrec
            nnwwnsum = nnwwnsum + nnwwnrec
!mb   net neutral energy source from recycling
!
            IF (switch%tn_style .EQ. 0) THEN
              srw%shi0(icv, 0) = srw%shi0(icv, 0) + fenerec*&
&               neutral_sources_rescale
            ELSE IF (switch%tn_style .NE. 1) THEN
!! neutral energy boundary conditions are not used
              srw%shn0(icv, 0) = srw%shn0(icv, 0) + fenerec*&
&               neutral_sources_rescale
            END IF
!
            IF (switch%afn_out .NE. 0) THEN
              fmomrec_out(icv) = fmomrec
              fenerec_out(icv) = fenerec
            END IF
          ELSE
!
!! this else belongs to "if(.not.(is.eq.is0))", hence 'is' will be neutral here
!
!          Identify corresponding ion species
            IF (LNEXT(is, is + 1)) THEN
              isi = is + 1
            ELSE
              CALL XERRAB('Problem with species labeling')
            END IF
!
!          A conversion is necessary because the BC implemented below were developed in a code that uses a total energy equation,
!          while B2.5 uses internal energy equations.
!
!
            q_conv = isign*(tnf*pl%ua(icv, is0)*geo%fcpbs(ifc)*pl%na(icv&
&             , is0)+(dv%fhm(ifc, 0, is0)+dv%fhm(ifc, 1, is0)))*&
&             neutral_sources_rescale
!
            IF (switch%tn_style .EQ. 0) THEN
              srw%shi0(icv, 0) = srw%shi0(icv, 0) + q_conv
            ELSE IF (switch%tn_style .NE. 1) THEN
!! do nothing, no neutral contributions in the boundary conditions of the pure ion energy equation
              srw%shn0(icv, 0) = srw%shn0(icv, 0) + q_conv
            END IF
!
            IF (maxw(istra) .EQ. 0) THEN
              CALL CALCINCIDENTFLUXESDIFFUSION(icv, icn, ifc, isign, is0&
&                                        , is2, isi, istra, phi_app, &
&                                        farea, pl, tif, tnf, tef, pof, &
&                                        dv%ne(icv), geo, rt, dnndy, &
&                                        dnndz, cosa, sina, switch%&
&                                        use_uy_uz_0, switch%use_uy_uz_1&
&                                        , switch%diffusion_bc_safeguard&
&                                        , fnni, fmomni, nni, nnwwni, &
&                                        feneni)
            ELSE IF (maxw(istra) .EQ. 1) THEN
              CALL CALCINCIDENTFLUXESMAXWELLIAN(icv, ifc, is0, isign, &
&                                         istra, farea, pl, tnf, geo, &
&                                         cosa, sina, fnni, fmomni, nni&
&                                         , nnwwni, feneni)
            ELSE IF (maxw(istra) .EQ. 2) THEN
              CALL CALCINCIDENTFLUXESKN(switch%kn_b1, switch%kn_b2, icv&
&                                 , icn, ifc, isign, is0, is2, isi, &
&                                 istra, phi_app, farea, pl, tif, tnf, &
&                                 tef, pof, dv%ne(icv), geo, rt, dnndy, &
&                                 dnndz, cosa, sina, switch%use_uy_uz_0&
&                                 , switch%use_uy_uz_1, switch%&
&                                 l_macro_afn, fnni, fmomni, nni, nnwwni&
&                                 , feneni)
            ELSE
              CALL CALCINCIDENTFLUXESMINKN(icv, icn, ifc, isign, is0, &
&                                    is2, isi, istra, phi_app, farea, pl&
&                                    , tif, tnf, tef, pof, dv%ne(icv), &
&                                    geo, rt, dnndy, dnndz, cosa, sina, &
&                                    switch%use_uy_uz_0, switch%&
&                                    use_uy_uz_1, fnni, fmomni, nni, &
&                                    nnwwni, feneni)
!
            END IF
!
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) - fnni*&
&             neutral_sources_rescale
!
!! save the incident fluid neutral flux
            fnn_inc(ifc, is0) = fnni*isign
!
!mb   net momentum sources from neutral particle reflection
            IF (.NOT.lcore) THEN
              IF (maxw(istra) .EQ. 0) THEN
                CALL CALCREFLECTEDFLUXESDIFFUSION(icv, icn, mpg%nci, ifc&
&                                           , isign, is0, is2, isi, &
&                                           istra, iwall, phi_app, farea&
&                                           , b2recyc(is, istra), recycm&
&                                           (is, istra), fluid_frac_hyb(&
&                                           ifc), pl, tif, tnf, tef, pof&
&                                           , dv%ne(icv), geo, mpg, rt, &
&                                           dnndy, dnndz, cosa, sina, &
&                                           switch%use_uy_uz_0, switch%&
&                                           use_uy_uz_1, switch%&
&                                           diffusion_bc_safeguard, &
&                                           fnnrefl, fmomrefl, nnrefl, &
&                                           nnwwnrefl, fenerefl, fene_el&
&                                          )
              ELSE IF (maxw(istra) .EQ. 1) THEN
                CALL CALCREFLECTEDFLUXESMAXWELLIAN(icv, mpg%nci, ifc, &
&                                            isign, is0, istra, iwall, &
&                                            farea, b2recyc(is, istra), &
&                                            recycm(is, istra), &
&                                            fluid_frac_hyb(ifc), pl, &
&                                            tnf, geo, mpg, cosa, sina, &
&                                            fnnrefl, fmomrefl, nnrefl, &
&                                            nnwwnrefl, fenerefl, &
&                                            fene_el)
              ELSE IF (maxw(istra) .EQ. 2) THEN
                CALL CALCREFLECTEDFLUXESKN(switch%kn_b1, switch%kn_b2, &
&                                    icv, icn, mpg%nci, ifc, isign, is0&
&                                    , is2, isi, istra, iwall, phi_app, &
&                                    farea, b2recyc(is, istra), recycm(&
&                                    is, istra), fluid_frac_hyb(ifc), pl&
&                                    , tif, tnf, tef, pof, dv%ne(icv), &
&                                    geo, mpg, rt, dnndy, dnndz, cosa, &
&                                    sina, switch%use_uy_uz_0, switch%&
&                                    use_uy_uz_1, switch%l_macro_afn, &
&                                    fnnrefl, fmomrefl, nnrefl, &
&                                    nnwwnrefl, fenerefl, fene_el)
              ELSE
                CALL CALCREFLECTEDFLUXESMINKN(icv, icn, mpg%nci, ifc, &
&                                       isign, is0, is2, isi, istra, &
&                                       iwall, phi_app, farea, b2recyc(&
&                                       is, istra), recycm(is, istra), &
&                                       fluid_frac_hyb(ifc), pl, tif, &
&                                       tnf, tef, pof, dv%ne(icv), geo, &
&                                       mpg, rt, dnndy, dnndz, cosa, &
&                                       sina, switch%use_uy_uz_0, switch&
&                                       %use_uy_uz_1, fnnrefl, fmomrefl&
&                                       , nnrefl, nnwwnrefl, fenerefl, &
&                                       fene_el)
              END IF
            ELSE
              fnnrefl = 0.0_R8
              fmomrefl = 0.0_R8
              nnrefl = 0.0_R8
              nnwwnrefl = 0.0_R8
              fenerefl = 0.0_R8
              fene_el = 0.0_R8
            END IF
!
!nh  adding the reflected particle source (with the molecular part subtracted)
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + fnnrefl*&
&             neutral_sources_rescale
            snan = fnnrefl
!
!
            IF (switch%afn_out .NE. 0) THEN
              fmomni_out(icv) = fmomni
              fmomrefl_out(icv) = fmomrefl
              feneni_out(icv) = feneni
              fenerefl_out(icv) = fenerefl
              fnni_out(icv) = fnni
            END IF
!
            smon = (fmomni+fmomrefl)*geo%cvhz(icv)*&
&             neutral_sources_rescale
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + smon
!
            nnsum = nnsum + nni + nnrefl
            nnwwnsum = nnwwnsum + nnwwni + nnwwnrefl
!
            IF (switch%tn_style .EQ. 0) THEN
              srw%shi0(icv, 0) = srw%shi0(icv, 0) + (feneni+fenerefl)*&
&               neutral_sources_rescale
            ELSE IF (switch%tn_style .NE. 1) THEN
!! neutral energy boundary conditions are not used
              srw%shn0(icv, 0) = srw%shn0(icv, 0) + (feneni+fenerefl)*&
&               neutral_sources_rescale
            END IF
!
! remove the Franck-Condon energy, that has been provided to the neutrals, from the electrons at the boundary
! temporary switch to evaluate the effect.
            IF (switch%remove_fc_el .EQ. 1) srw%she0(icv, 0) = srw%she0(&
&               icv, 0) + fene_el*neutral_sources_rescale
          END IF
!
          IF (zamax(is) .EQ. zn(is)) THEN
!! see eq. 62-63 of the main reference.
            IF (nnsum .EQ. 0.0_R8) nnsum = 1.0_R8
            IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
              abs2 = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
            ELSE
              abs2 = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
            END IF
            smon = -(isign*cosa*am(is0)*mp/3.0_R8*nnwwnsum**2/nnsum*&
&             farea*abs2*geo%cvhz(icv))
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + smon*&
&             neutral_sources_rescale
            nnsum = 0.0_R8
            nnwwnsum = 0.0_R8
          END IF
        END IF
!
!    ..recycling into next ionized species
        IF (is .LT. ns - 1) THEN
          IF (LNEXT(is, is + 1)) srw%sna0(icv, 0, is+1) = srw%sna0(icv, &
&             0, is+1) + rcion(is, istra)*t0/neutral_sources_rescale
        END IF
      END DO
! IYS 18.09.2018
! do is = 0, ns-1
!    ..recycling at core into highest ionized stage
      IF (lcore) THEN
!    ..loop over species
        is0 = ns - 1
        is1 = ns - 1
        DO is=nsmax,nsmin,-1
          IF (am(is0) - am(is) .GE. 0.) THEN
            abs3 = am(is0) - am(is)
          ELSE
            abs3 = -(am(is0)-am(is))
          END IF
          IF (zn(is0) .NE. zn(is) .OR. amtol .LT. abs3) is0 = is
!xpb
          IF (zamax(is) .EQ. zn(is)) is1 = is
          IF (am(is1) - am(is) .GE. 0.) THEN
            abs4 = am(is1) - am(is)
          ELSE
            abs4 = -(am(is1)-am(is))
          END IF
          IF (zn(is1) .NE. zn(is) .OR. amtol .LT. abs4) WRITE(*, &
&                                                       '(a,3i3)') &
&                                        'b2stbr problem: is, is0, is1 '&
&                                                       , is, is0, is1
          IF (is0 .NE. is1) THEN
            IF (ncall_b2stbr_phys .EQ. 0) WRITE(*, '(a,2i3)') &
&                                         'b2stbr: setting is0 to is1 '&
&                                         , is0, is1
            is0 = is1
          END IF
          IF (0.0_R8 .LT. isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))&
&         ) THEN
            max9 = isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))
          ELSE
            max9 = 0.0_R8
          END IF
!xpb
!          (species (is) recycles into is0)
! xpb: 2002.02.01
          t0 = max9*core_sources_rescale
          IF (is .NE. is0) THEN
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + b2recyc(is, &
&             istra)*t0
!
!wdk  recycling of true parallel momentum
!     (at core, cell faces will be aligned with flux surfaces)
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + mrecyc(is, &
&             istra)*am(is)*mp*pl%ua(icv, is)*b2recyc(is, istra)*t0*geo%&
&             cvhz(icv)
!srv
            srw%shi0(icv, 0) = srw%shi0(icv, 0) + erecyc(is, istra)*(&
&             1.5_R8+switch%boris)*(zaf(is)*tef+tif)*b2recyc(is, istra)*&
&             t0*recycled_neutrals_contr
          END IF
        END DO
      END IF
! do is = nsmax, nsmin, -1
!
      RETURN
    END IF
  END SUBROUTINE RECYCLE

END SUBROUTINE B2STBR_PHYS_B

