SUBROUTINE B2STBC_NODIFF(ncv, nfc, nvx, ns, ismain, ismain0, switch, geo&
& , mpg, pl, dv, co, rt, rtw, st_ext, srw, psnc, psnl, wrong_flow, &
& main_call)
  USE B2MOD_TYPES
!      use b2mod_geo_corner
  USE B2MOD_CONSTANTS
!      use b2mod_sources
!     1 , only : b2stbc_sna,b2stbc_smo,b2stbc_she,b2stbc_shi,b2stbc_sch,
!     2          b2stbc_sne
!      use b2mod_balance !djm Jan 2017
!     1 , only : b2stbc_sna0to1,b2stbc_smo0to3,b2stbc_she0to3,
!     2          b2stbc_shi0to3,balance_netcdf
  USE B2US_FEEDBACK_DIFF
  USE B2MOD_B2CMPA_DIFF
  USE B2MOD_TIME
  USE B2MOD_SWITCHES_DIFF
  USE B2US_GEO_DIFF
  USE B2US_MAP_DIFF
  USE B2US_PLASMA_DIFF
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_BOUNDARY_NAMELIST_DIFF, ONLY : conpar, enepar, enipar, nbcd,&
& lfeedback, enkpar, potpar, mompar
  USE B2MOD_AD_DIFF, ONLY : my_out_folder, ncall_b2stbc, &
& ncall_b2stbc_phys, nsdmax
  USE B2MOD_MATH_DIFF, ONLY : cutlo, cutll, b2mod_math_initialised, &
& small_r4_constant
  USE B2MOD_SUBSYS
  IMPLICIT NONE
!
!-----------------------------------------------------------------------
!.end b2stbc
!
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx, ns, ismain, ismain0
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(B2PLASMA), INTENT(INOUT) :: pl
  TYPE(B2DERIVATIVES), INTENT(INOUT) :: dv
  TYPE(B2RATES), INTENT(IN) :: rt
  TYPE(B2RATESWORK), INTENT(IN) :: rtw
  TYPE(B2COEFF), INTENT(INOUT) :: co
  TYPE(B2STATEEXT), INTENT(IN) :: st_ext
  TYPE(B2PLASMASNAPSHOT), INTENT(INOUT) :: psnc, psnl
!xpb
  LOGICAL :: main_call
!   ..output arguments (unspecified on entry)
  TYPE(B2SOURCEWORK), INTENT(INOUT) :: srw
!xpb
  LOGICAL :: wrong_flow
!   ..common blocks
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2STBC provides linearised expressions for the sources of
!     density, parallel momentum, electron heat, atom heat, and charge,
!     due to the boundary conditions.
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: is
  REAL(kind=r8) :: praverage(2), testqe, testqi
  REAL(kind=r8) :: wrk(nfc)
!   ..procedures
  INTRINSIC NINT
  EXTERNAL XERTST, SFILL_NODIFF, B2SAXPY_NODIFF, IPGETR, IPGETI
  EXTERNAL B2XVSG_NODIFF, B2XVFF_NODIFF, B2XVPS_NODIFF, B2XXGS, &
&     B2STBC_PHYS_NODIFF
  INTRINSIC MAXVAL
  INTRINSIC MINVAL
  REAL(r8) :: result1
  INTEGER :: arg1
  REAL(r8) :: result2
!   ..initialisation
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2stbc')
!   ..set internal parameters on first call
  IF (ncall_b2stbc .EQ. 0) THEN
    result1 = MAXVAL(dv%facdrift)
    CALL XERTST(.NOT.(switch%integral_current .NE. 0.0_R8 .AND. ((&
&         result1 .GT. 0.0_R8 .AND. switch%b2stbc_fchy_dia .GT. 0.0_R8) &
&         .OR. switch%b2stbc_neoclassical .GT. 0.0_R8)), &
&         'inconsistent boundary settings for current equation')
! dpc-2003-10-23
    lfeedback = switch%b2stbc_feedback .NE. 0
  END IF
!   ..test nCv, nFc, ns
  CALL XERTST(0 .LE. ncv .AND. 0 .LE. nfc, 'faulty argument nCv, nFc')
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
  CALL XERTST(0 .LE. ismain .AND. ismain .LT. ns, &
&       'invalid main plasma species index ismain')
  CALL XERTST(.NOT.is_neutral(ismain), &
&       'invalid main neutral species ismain; must not be neutral')
  CALL XERTST(0 .LE. ismain0 .AND. ismain0 .LT. ns, &
&       'invalid main neutral species index ismain0')
  CALL XERTST(is_neutral(ismain0) .OR. ismain .EQ. ismain0, &
&       'invalid main neutral species ismain0; must be neutral')
!   ..extensive tests on first few calls
  IF (ncall_b2stbc .LT. 3) THEN
!    ..test sign of vol
    CALL B2XVSG_NODIFF(ncv, geo%cvvol, 1, 'vol', '.gt.')
    arg1 = nfc*2
    CALL B2XVSG_NODIFF(arg1, geo%fcvol, 1, 'vol', '.gt.')
!    ..test state
    CALL B2XVPS_NODIFF(ncv, nfc, ns, pl, dv)
!    ..test sign of chce, chci
    wrk(:) = co%chce(:, 0)*geo%fcqalf(:, 0)
    CALL B2XVSG_NODIFF(nfc, wrk, 1, 'chce0', '.ge.')
    wrk(:) = co%chce(:, 1)*geo%fcqalf(:, 1)
    CALL B2XVSG_NODIFF(nfc, wrk, 1, 'chce1', '.ge.')
    wrk(:) = co%chci(:, 0)*geo%fcqalf(:, 0)
    CALL B2XVSG_NODIFF(nfc, wrk, 1, 'chci0', '.ge.')
    wrk(:) = co%chci(:, 1)*geo%fcqalf(:, 1)
    CALL B2XVSG_NODIFF(nfc, wrk, 1, 'chci1', '.ge.')
  END IF
!   ..test facdrift           !xpb
  result1 = MINVAL(dv%facdrift)
  result2 = MAXVAL(dv%facdrift)
  CALL XERTST(0.0_R8 .LE. result1 .AND. 1.0_R8 .GE. result2, &
&       'faulty argument facdrift')
!      if (ncall_b2stbc.eq.1 .and. main_call) then                               !xpb !srv 18.05.09
!        if (maxval(facdrift).eq.0.0_R8) then                             !xpb
!          write (*,*) 'b2stbc_neoclassical =', b2stbc_neoclassical       !xpb
!        else                                                             !xpb
!          write (*,*) 'b2stbc_fchy_dia =', b2stbc_fchy_dia               !xpb
!        endif                                                            !xpb
!      endif
!   ..check kinetic secondary electron emission model
  IF (ncall_b2stbc .EQ. 0 .AND. switch%secmodel .EQ. 1) THEN
    CALL XERTST(NINT(zn(ismain)) .EQ. 1, &
&         'Kinetic secondary emission model only for hydrogen plasmas!')
    WRITE(*, *) 'Kinetic secondary emission model turned on'
    WRITE(*, *) 'Graphite target plates are assumed'
    IF (NINT(zamax(ismain)) .NE. 1) WRITE(*, *) &
&        'WARNING! Kinetic secondary emission expects HYDROGEN plasmas!'
  END IF
!   ..initialise sources to 0
  arg1 = ncv*2*ns
  CALL SFILL_NODIFF(arg1, 0.0_R8, srw%sna0, 1)
  arg1 = ncv*4*ns
  CALL SFILL_NODIFF(arg1, 0.0_R8, srw%smo0, 1)
  arg1 = ncv*2
  CALL SFILL_NODIFF(arg1, 0.0_R8, srw%sne0, 1)
  arg1 = ncv*4
  CALL SFILL_NODIFF(arg1, 0.0_R8, srw%she0, 1)
  arg1 = ncv*4
  CALL SFILL_NODIFF(arg1, 0.0_R8, srw%shi0, 1)
  arg1 = ncv*4
  CALL SFILL_NODIFF(arg1, 0.0_R8, srw%shn0, 1)
  arg1 = ncv*4
  CALL SFILL_NODIFF(arg1, 0.0_R8, srw%sch0, 1)
  arg1 = ncv*4
  CALL SFILL_NODIFF(arg1, 0.0_R8, srw%skt0, 1)
  arg1 = ncv*4
  CALL SFILL_NODIFF(arg1, 0.0_R8, srw%szt0, 1)
!
! ..compute standard form volume sources
!   (This code placed here for want of a better location)
!   ..low-level particle source
  DO is=0,ns-1
    CALL B2SAXPY_NODIFF(ncv, switch%sna0ep, geo%cvvol, 1, srw%sna0(1, 0&
&                 , is), 1)
  END DO
  CALL B2SAXPY_NODIFF(ncv, switch%she0ep, geo%cvvol, 1, srw%she0(1, 0), &
&               1)
  CALL B2SAXPY_NODIFF(ncv, switch%shi0ep, geo%cvvol, 1, srw%shi0(1, 0), &
&               1)
!
  IF (switch%b2stbc_boundary_namelist .GE. 1) THEN
    IF (lfeedback .OR. switch%b2stbc_feedback .NE. 0) CALL &
&     COMPUTE_FEEDBACK(ncv, nfc, nvx, ns, ismain, switch, geo, mpg, pl, &
&                dv, rt, srw, psnc, psnl, main_call, switch%&
&                b2stbc_diagno)
!
    IF (ncall_b2stbc .EQ. 0) WRITE(*, *) ' b2stbc_phys called'
    CALL B2STBC_PHYS_NODIFF(ncv, nfc, nvx, ns, ismain, ismain0, switch, &
&                     geo, mpg, pl, dv, co, rt, rtw, st_ext, srw, &
&                     wrong_flow, main_call, praverage)
    IF (wrong_flow) WRITE(*, *) &
&                   'b2stbc: wrong_flow returned from b2stbc_phys'
  END IF
!boundary_namelist.ge.1
!
!WG_RM      if(lfeedback.or.switch%b2stbc_feedback.ne.0)
!WG_RM     &  call b2stbc_fb_post(nCv, nFc, nVx, ns, ismain,
!WG_RM     &    switch, geo, mpg, pl, dv, srw, main_call)
!
! ..compute conditions on the cuts
!     (no flow)
!WG_TODO*   ..compute suitable large coefficient
!WG_TODO      t0 = 1.0e20_R8*b2ssum(nCv,geo%cvVol,1)
!WG_TODO*     (the dimension of t0 is m**3/s)
!WG_TODO*   ..loop over species
!WG_TODO      do is = 0, ns-1
!WG_TODO*    ..set parallel momentum source
!WG_TODO       call b2xxgs (nx, ny, 0.0_R8, smo0(-1,-1,0,is), 1)
!WG_TODO       call b2xxgs (nx, ny, 0.0_R8, smo0(-1,-1,1,is), 1)
!WG_TODO       call b2xxgs (nx, ny, 0.0_R8, smo0(-1,-1,2,is), 1)
!WG_TODO       call b2xxgs (nx, ny, -t0, smo0(-1,-1,3,is), 1)
!WG_TODO      enddo
!
! added 2001.11.26 dpc/xpb
! to fix problems with the core energy boundary condition arising
! from diamagnetic drifts
  testqe = 0.0_R8
  testqi = 0.0_R8
! ..return
  IF (ncall_b2stbc .EQ. 0 .OR. main_call) ncall_b2stbc = ncall_b2stbc + &
&     1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2STBC_NODIFF

!
!-----------------------------------------------------------------------
!.specification
FUNCTION BCINT_NODIFF(fun, nx, ny, il, ib) RESULT (bcint)
  USE B2MOD_TYPES
  USE B2MOD_GEO_DIFF
  USE B2MOD_INDIRECT
  USE B2MOD_BOUNDARY_NAMELIST_DIFF
  IMPLICIT NONE
!
!-----------------------------------------------------------------------
!.end bcint
!
!   ..output result (undefined upon entry)
  REAL(kind=r8) :: bcint
!   ..input arguments (unchanged on exit)
  INTEGER :: nx, ny, il, ib
  REAL(kind=r8) :: fun(-1:nx, -1:ny)
!
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     BCINT provides interpolated face-centred values for cell-centred
!     quantities (array fun), for use in boundary conditions.
!     The interpolation is done according to distance away from the
!     cell face. The boundary index is ib, and the cell index along
!     the boundary is il.
!
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..procedures
  EXTERNAL XERTST, XERRAB
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
  REAL(kind=r8) :: result3
  REAL(kind=r8) :: result4
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!  ..test input parameters
  CALL XERTST(0 .LE. nx .AND. 0 .LE. ny, 'faulty parameter nx, ny')
  CALL XERTST(0 .LT. ib .AND. nbc .GE. ib, 'faulty parameter ib')
  CALL XERTST(1 .LE. il .AND. bc_list_size(ib) .GE. il, &
&       'faulty parameter il')
!
! ..main computation
  IF (bcchar(ib) .EQ. 'S') THEN
    result1 = HY1(topix(bc_list_x(il, ib), bc_list_y(il, ib)), topiy(&
&     bc_list_x(il, ib), bc_list_y(il, ib)))
    result2 = HY1(bc_list_x(il, ib), bc_list_y(il, ib))
    result3 = HY1(bc_list_x(il, ib), bc_list_y(il, ib))
    result4 = HY1(topix(bc_list_x(il, ib), bc_list_y(il, ib)), topiy(&
&     bc_list_x(il, ib), bc_list_y(il, ib)))
    bcint = (fun(bc_list_x(il, ib), bc_list_y(il, ib))*result1+fun(topix&
&     (bc_list_x(il, ib), bc_list_y(il, ib)), topiy(bc_list_x(il, ib), &
&     bc_list_y(il, ib)))*result2)/(result3+result4)
  ELSE IF (bcchar(ib) .EQ. 'N') THEN
    result1 = HY1(bottomix(bc_list_x(il, ib), bc_list_y(il, ib)), &
&     bottomiy(bc_list_x(il, ib), bc_list_y(il, ib)))
    result2 = HY1(bc_list_x(il, ib), bc_list_y(il, ib))
    result3 = HY1(bc_list_x(il, ib), bc_list_y(il, ib))
    result4 = HY1(bottomix(bc_list_x(il, ib), bc_list_y(il, ib)), &
&     bottomiy(bc_list_x(il, ib), bc_list_y(il, ib)))
    bcint = (fun(bc_list_x(il, ib), bc_list_y(il, ib))*result1+fun(&
&     bottomix(bc_list_x(il, ib), bc_list_y(il, ib)), bottomiy(bc_list_x&
&     (il, ib), bc_list_y(il, ib)))*result2)/(result3+result4)
  ELSE IF (bcchar(ib) .EQ. 'E') THEN
    bcint = (fun(bc_list_x(il, ib), bc_list_y(il, ib))*hx(leftix(&
&     bc_list_x(il, ib), bc_list_y(il, ib)), leftiy(bc_list_x(il, ib), &
&     bc_list_y(il, ib)))+fun(leftix(bc_list_x(il, ib), bc_list_y(il, ib&
&     )), leftiy(bc_list_x(il, ib), bc_list_y(il, ib)))*hx(bc_list_x(il&
&     , ib), bc_list_y(il, ib)))/(hx(bc_list_x(il, ib), bc_list_y(il, ib&
&     ))+hx(leftix(bc_list_x(il, ib), bc_list_y(il, ib)), leftiy(&
&     bc_list_x(il, ib), bc_list_y(il, ib))))
  ELSE IF (bcchar(ib) .EQ. 'W') THEN
    bcint = (fun(bc_list_x(il, ib), bc_list_y(il, ib))*hx(rightix(&
&     bc_list_x(il, ib), bc_list_y(il, ib)), rightiy(bc_list_x(il, ib), &
&     bc_list_y(il, ib)))+fun(rightix(bc_list_x(il, ib), bc_list_y(il, &
&     ib)), rightiy(bc_list_x(il, ib), bc_list_y(il, ib)))*hx(bc_list_x(&
&     il, ib), bc_list_y(il, ib)))/(hx(bc_list_x(il, ib), bc_list_y(il, &
&     ib))+hx(rightix(bc_list_x(il, ib), bc_list_y(il, ib)), rightiy(&
&     bc_list_x(il, ib), bc_list_y(il, ib))))
  ELSE
    CALL XERRAB('bcint : Unknown orientation!')
  END IF
!
! ..return
  RETURN
END FUNCTION BCINT_NODIFF

