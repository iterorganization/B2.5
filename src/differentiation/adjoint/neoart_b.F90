    DO j=0,ncs(i)-1
      coeff(i, j, 3) = coeffc(i, j, 3)*(ev*1000.0_R8*1.0e19_R8)*den(i, j&
&       )*zzs(i, j)*rbt*bav/(dpsidr*b2av)
      coeff(i, j, 4) = 1.0e3_R8*coeffc(i, j, 4)*rbt/(b2av*dpsidr)
    END DO
  END DO
!
  RETURN
