& epsn, xi(0:nsd, 0:nzd-1), uai(0:nsd, 0:nzd, 3), sigma(4), dum, la(3, 3&
& , 0:nsd), lab(3, 3, 0:nsd, 0:nsd), mm(3, 3), nn(3, 3), ta, tb, ma, mb&
