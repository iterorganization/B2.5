      DO j=0,ncs(i)-1
        DO k=1,2
          coeff(i, j, k) = coeff(i, j, k) - coeffc(i, j, k)*(rbt/dpsidr)&
&           **2/(ev/1000.0_R8*zzs(i, j)*b2av)
          coeffc(i, j, k) = 0.0_R8
        END DO
      END DO
    END DO
  END IF
