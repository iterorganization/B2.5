!! and cells on same horizontal line but not next to each other
!! do bookkeeping for cut
            IF (.NOT.(periodic_bc .EQ. 1 .AND. ixnb .EQ. nx - 1 .AND. ix&
&               .EQ. 0)) THEN
!
              IF ((iy .NE. iynb .OR. ix .NE. ixnb + 1) .AND. (.NOT.&
&                 ISGHOSTCELL(cflag(ix, iy, cellflag_type))) .AND. (&
&                 .NOT.ISGHOSTCELL(cflag(ixnb, iynb, cellflag_type)))) &
&             THEN
!! Found a left neighbour inside the domain on same line but not directly
!! left of the cell -> there is a cut at the left face of the cell
!! Figure out right and left end of cut region
!
                IF (ixnb .LT. ix) THEN
!! jump to the left
                  ixr = ix
                  ixl = ixnb + 1
                ELSE
!! jump to the right
                  ixr = ixnb + 1
                  ixl = ix
                END IF
!
                cutfound = .false.
                DO i=1,ic
                  IF (ixr .EQ. rightcut1(i) .OR. ixl .EQ. leftcut1(i)) &
&                 THEN
                    cutfound = .true.
                    GOTO 140
                  END IF
                END DO
 140            IF (.NOT.cutfound) THEN
                  ic = ic + 1
                  WRITE(*, *) 'ic', ic
                  i = ic
                END IF
                IF (iy .GT. bottomcut1(i)) THEN
                  bottomcut1(i) = bottomcut1(i)
                ELSE
                  bottomcut1(i) = iy
                END IF
                IF (iy + 1 .LT. topcut1(i)) THEN
                  topcut1(i) = topcut1(i)
                ELSE
                  topcut1(i) = iy + 1
                END IF
                leftcut1(i) = ixl
                rightcut1(i) = ixr
              END IF
            END IF
          END IF
        END IF
      END DO
      IF (nncut .LT. ic) THEN
        nncut = ic
      ELSE
        nncut = nncut
      END IF
      IF (nncut .GT. def_ncut) THEN
        WRITE(*, *) ' Increase DEF_NCUT in b2mod_dimensions!'
        WRITE(*, *) ' nncut = ', nncut, ' DEF_NCUT = ', def_ncut
        CALL XERTST(nncut .LE. def_ncut, 'faulty parameter DEF_NCUT')
!
      END IF
    END DO
!
    WRITE(*, *) 'istyle', istyle, 'nncut', nncut
    IF (nncut .EQ. 0) WRITE(*, *) 'No cuts found'
    IF (nncut .GE. 1) WRITE(*, '(1x,a,4i5)') &
&               'Calculated leftcut1, rightcut1, topcut1, bottomcut1 = '&
&                     , leftcut1(1), rightcut1(1), topcut1(1), &
&                     bottomcut1(1)
    IF (nncut .GE. 2) WRITE(*, '(1x,a,4i5)') &
&               'Calculated leftcut2, rightcut2, topcut2, bottomcut2 = '&
&                     , leftcut1(2), rightcut1(2), topcut1(2), &
&                     bottomcut1(2)
!
    RETURN
  END SUBROUTINE INIT_CONNECTIVITY

!
  FUNCTION B2GAVG1(nx1, ny1, fun)
!xpb  This performs an average over the whole domain of valid cells on the secondary grid
    USE B2MOD_TYPES
    IMPLICIT NONE
    REAL(kind=r8) :: b2gavg1
    INTEGER :: nx1, ny1
    REAL(kind=r8) :: fun(-1:nx1, -1:ny1), denominator
    INTEGER :: ix, iy, celltype
    LOGICAL :: unused
!
    b2gavg1 = 0.0_R8
    denominator = 0.0_R8
    DO ix=-1,nx1
      DO iy=-1,ny1
        celltype = cflags1(ix, iy, cellflag_type)
        unused = (celltype .EQ. grid_undefined .OR. celltype .EQ. &
&         grid_external) .OR. celltype .EQ. grid_dead
        IF (.NOT.unused) THEN
          b2gavg1 = b2gavg1 + fun(ix, iy)
          denominator = denominator + 1.0_R8
        END IF
      END DO
    END DO
    IF (denominator .GT. 0.0_R8) b2gavg1 = b2gavg1/denominator
!
    RETURN
  END FUNCTION B2GAVG1

! Interpolate a cell quantity cv to a particular vertex
!
  FUNCTION INTERPOLATETOVERTICES1(nx1, ny1, vx_index, cv) RESULT (vx)
    USE B2MOD_B2CMFS
    USE B2MOD_INDIRECT
    USE B2MOD_CONSTANTS
    USE B2MOD_CELLHELPER
    IMPLICIT NONE
!
    INTEGER, INTENT(IN) :: nx1, ny1, vx_index
    REAL(kind=r8), DIMENSION(-1:nx1, -1:ny1), INTENT(IN) :: cv
! internal
    REAL(kind=r8), DIMENSION(-1:nx1, -1:ny1) :: vx
!
    INTEGER :: ix, iy, is, ixx, iyy
! For every vertex, find connected cells and average values
    REAL(kind=r8) :: av, wtot, minval, maxval, w, d, centroid(0:1)
    INTRINSIC SQRT
    INTRINSIC MIN
    INTRINSIC MAX
    INTRINSIC ABS
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: abs1
    REAL(kind=r8) :: result1
!
    DO ix=-1,nx1
      DO iy=-1,ny1
!
        IF (ISUNUSEDCELL(cflags1(ix, iy, cellflag_type))) THEN
          vx(ix, iy) = cv(ix, iy)
        ELSE
!
          wtot = 0.0_R8
          av = 0.0_R8
          minval = HUGE(1.0_R8)
          result1 = HUGE(1.0_R8)
          maxval = -result1
          DO is=1,8
            IF (.NOT.(gmap1%mapcvixvx(gmap1%mapvxi(ix, iy, vx_index), is&
&               ) .EQ. b2_grid_undefined .OR. gmap1%mapcviyvx(gmap1%&
&               mapvxi(ix, iy, vx_index), is) .EQ. b2_grid_undefined)) &
&           THEN
              ixx = gmap1%mapcvixvx(gmap1%mapvxi(ix, iy, vx_index), is)
              iyy = gmap1%mapcviyvx(gmap1%mapvxi(ix, iy, vx_index), is)
              centroid = QUADCENTROID(crx1(ixx, iyy, 0), cry1(ixx, iyy, &
&               0), crx1(ixx, iyy, 1), cry1(ixx, iyy, 1), crx1(ixx, iyy&
&               , 2), cry1(ixx, iyy, 2), crx1(ixx, iyy, 3), cry1(ixx, &
&               iyy, 3))
              d = SQRT((centroid(0)-crx1(ix, iy, vx_index))**2 + (&
&               centroid(1)-cry1(ix, iy, vx_index))**2)
              w = 1.0_R8/d
              av = av + cv(ixx, iyy)*w
              wtot = wtot + w
              IF (minval .GT. cv(ixx, iyy)) THEN
                minval = cv(ixx, iyy)
              ELSE
                minval = minval
              END IF
              IF (maxval .LT. cv(ixx, iyy)) THEN
                maxval = cv(ixx, iyy)
              ELSE
                maxval = maxval
              END IF
            END IF
          END DO
!
!
          vx(ix, iy) = av/wtot
          d = vx(ix, iy) - minval
          IF (minval .NE. 0.0_R8) THEN
            IF (minval .GE. 0.) THEN
              abs0 = minval
            ELSE
              abs0 = -minval
            END IF
            CALL XERTST(d/abs0 .GE. -1.0e-15_R8, &
&                 'Interpolated value smaller than minimum')
          ELSE
            CALL XERTST(d .GE. minval, &
&                 'Interpolated value smaller than minimum')
          END IF
          d = vx(ix, iy) - maxval
          IF (maxval .NE. 0.0_R8) THEN
            IF (maxval .GE. 0.) THEN
              abs1 = maxval
            ELSE
              abs1 = -maxval
            END IF
            CALL XERTST(d/abs1 .LE. 1.0e-15_R8, &
&                 'Interpolated value bigger than maximum')
          ELSE
            CALL XERTST(d .LE. maxval, &
&                 'Interpolated value bigger than maximum')
          END IF
        END IF
      END DO
    END DO
    RETURN
  END FUNCTION INTERPOLATETOVERTICES1

