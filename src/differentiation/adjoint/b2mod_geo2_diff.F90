!
  FUNCTION DIST1(ix, iy, i, j)
    USE B2MOD_MATH_DIFF
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix, iy, i, j
    REAL(kind=r8) :: dist1
    REAL(kind=r8) :: arg1
    REAL(kind=r8) :: arg2
    arg1 = crx1(ix, iy, j) - crx1(ix, iy, i)
    arg2 = cry1(ix, iy, j) - cry1(ix, iy, i)
    dist1 = NORM(arg1, arg2)
    RETURN
  END FUNCTION DIST1

!
  FUNCTION DISTC1(ix1, iy1, ip1, ix2, iy2, ip2)
    USE B2MOD_MATH_DIFF
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix1, iy1, ip1, ix2, iy2, ip2
    REAL(kind=r8) :: distc1
    REAL(kind=r8) :: arg1
    REAL(kind=r8) :: arg2
    arg1 = crx1(ix1, iy1, ip1) - crx1(ix2, iy2, ip2)
    arg2 = cry1(ix1, iy1, ip1) - cry1(ix2, iy2, ip2)
    distc1 = NORM(arg1, arg2)
    RETURN
  END FUNCTION DISTC1

!! Matches right face of cell (ix1,iy1) to left face of cell (ix2,iy2)
!
  LOGICAL FUNCTION MATCHLEFT1(ix1, iy1, ix2, iy2)
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix1, iy1, ix2, iy2
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    result1 = DISTC1(ix1, iy1, 1, ix2, iy2, 0)
    result2 = DISTC1(ix1, iy1, 3, ix2, iy2, 2)
    matchleft1 = result1 + result2 .LT. geom_match_dist
    RETURN
  END FUNCTION MATCHLEFT1

!! Matches top face of cell (ix1,iy1) to bottom face of cell (ix2,iy2)
!
  LOGICAL FUNCTION MATCHBOTTOM1(ix1, iy1, ix2, iy2)
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix1, iy1, ix2, iy2
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    result1 = DISTC1(ix1, iy1, 2, ix2, iy2, 0)
    result2 = DISTC1(ix1, iy1, 3, ix2, iy2, 1)
    matchbottom1 = result1 + result2 .LT. geom_match_dist
    RETURN
  END FUNCTION MATCHBOTTOM1

