        DO icv=ad_to5,1,-1
          CALL POPREAL8(j(icf), r8/8)
          temp0 = b2dataoncf(icv) - cfdata(icf, 2, icv)
          b2voloncfb(icf, icv) = b2voloncfb(icf, icv) + temp0**2*jb(icf)
          b2dataoncfb(icv) = b2dataoncfb(icv) + 2*temp0*b2voloncf(icf, &
&           icv)*jb(icf)
        END DO
        n1 = mpg%cfregp(icf, 2)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2psi(icf, 1:n1), r8*n1/8)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2rr(icf, 1:n1), r8*n1/8)
        CALL POPREAL8ARRAY(psi_omp(1:nomp), r8*nomp/8)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2data(1:n1), r8*n1/8)
        CALL POPREAL8ARRAY(cfdata(icf, 1, 1:n2), r8*n2/8)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2dataoncf(1:n2), r8*n2/8)
        CALL MAP_AND_INTERPOLATE_CF_B(n1, n2, nomp, b2psi(icf, 1:n1), &
&                               b2rr(icf, 1:n1), psi_omp(1:nomp), b2data&
&                               (1:n1), b2datab(1:n1), cfdata(icf, 1, 1:&
&                               n2), b2dataoncf(1:n2), b2dataoncfb(1:n2)&
&                               , maptoomp(icf), ompind(icf, 1:2), &
&                               curr_shift, curr_shiftb)
      ELSE
        curr_shiftb = 0.D0
      END IF
      CALL POPCONTROL4B(branch)
      IF (branch .LT. 6) THEN
        IF (branch .LT. 3) THEN
          IF (branch .EQ. 0) THEN
            ic1 = mpg%cfregp(icf, 1)
            ic2 = ic1 + mpg%cfregp(icf, 2) - 1
            stb%dv%ne(mpg%cfreg(ic1:ic2)) = stb%dv%ne(mpg%cfreg(ic1:ic2)&
&             ) + b2datab(1:n1)/1.0e19_R8
            b2datab(1:n1) = 0.D0
          ELSE IF (branch .EQ. 1) THEN
            ic1 = mpg%cfregp(icf, 1)
            ic2 = ic1 + mpg%cfregp(icf, 2) - 1
            stb%pl%te(mpg%cfreg(ic1:ic2)) = stb%pl%te(mpg%cfreg(ic1:ic2)&
&             ) + b2datab(1:n1)/ev
            b2datab(1:n1) = 0.D0
          ELSE
            ic1 = mpg%cfregp(icf, 1)
            ic2 = ic1 + mpg%cfregp(icf, 2) - 1
            stb%pl%ti(mpg%cfreg(ic1:ic2)) = stb%pl%ti(mpg%cfreg(ic1:ic2)&
&             ) + b2datab(1:n1)/ev
            b2datab(1:n1) = 0.D0
          END IF
        ELSE IF (branch .EQ. 3) THEN
          CALL POPREAL8(j(icf), r8/8)
          jb(icf) = jb(icf)/ev
          CALL POPINTEGER4(ad_from)
          CALL POPINTEGER4(ad_to0)
          DO icv=ad_to0,ad_from,-1
            CALL POPCONTROL1B(branch)
            IF (branch .EQ. 0) THEN
              CALL POPREAL8(j(icf), r8/8)
              stb%pl%te(mpg%cfreg(icv)) = stb%pl%te(mpg%cfreg(icv)) + jb&
&               (icf)
              jb(icf) = 0.D0
            ELSE
              CALL POPREAL8(j(icf), r8/8)
            END IF
          END DO
        ELSE IF (branch .EQ. 4) THEN
          CALL POPREAL8(j(icf), r8/8)
          jb(icf) = cfweight(icf)*jb(icf)
          CALL POPINTEGER4(ad_from0)
          CALL POPINTEGER4(ad_to1)
          DO ifc=ad_to1,ad_from0,-1
            CALL POPCONTROL1B(branch)
            IF (branch .EQ. 0) THEN
              CALL POPREAL8(j(icf), r8/8)
              qqb = jb(icf)
              jb(icf) = 0.D0
            ELSE
              CALL POPREAL8(j(icf), r8/8)
              qqb = 0.D0
            END IF
            tempb = mpg%cffcor(ifc)*qqb/geo%fcs(mpg%cfreg(ifc))
            stb%dv%fht(mpg%cfreg(ifc), 0) = stb%dv%fht(mpg%cfreg(ifc), 0&
&             ) + tempb
            stb%dv%fht(mpg%cfreg(ifc), 1) = stb%dv%fht(mpg%cfreg(ifc), 1&
&             ) + tempb
          END DO
        ELSE
          ic1 = mpg%cfregp(icf, 1)
          ic2 = ic1 + mpg%cfregp(icf, 2) - 1
          gradrb = 0.D0
          gradrb(mpg%cfreg(ic1:ic2)) = gradrb(mpg%cfreg(ic1:ic2)) + &
&           b2datab(1:n1)/1.0e19_R8
          b2datab(1:n1) = 0.D0
          CALL GRADC_R_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, st%dv%&
&                  ne, stb%dv%ne, funv, funvb, gradr, gradrb)
        END IF
      ELSE IF (branch .LT. 9) THEN
        IF (branch .EQ. 6) THEN
          ic1 = mpg%cfregp(icf, 1)
          ic2 = ic1 + mpg%cfregp(icf, 2) - 1
          gradrb = 0.D0
          gradrb(mpg%cfreg(ic1:ic2)) = gradrb(mpg%cfreg(ic1:ic2)) + &
&           b2datab(1:n1)/ev
          b2datab(1:n1) = 0.D0
          CALL GRADC_R_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, st%pl%&
&                  te, stb%pl%te, funv, funvb, gradr, gradrb)
        ELSE IF (branch .EQ. 7) THEN
          ic1 = mpg%cfregp(icf, 1)
          ic2 = ic1 + mpg%cfregp(icf, 2) - 1
          gradrb = 0.D0
          gradrb(mpg%cfreg(ic1:ic2)) = gradrb(mpg%cfreg(ic1:ic2)) + &
&           b2datab(1:n1)/ev
          b2datab(1:n1) = 0.D0
          CALL GRADC_R_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, st%pl%&
&                  ti, stb%pl%ti, funv, funvb, gradr, gradrb)
        ELSE
          csb = 0.D0
          CALL POPINTEGER4(ad_to2)
          DO icv=ad_to2,1,-1
            stb%dv%ne(mpg%cfreg(ic1+icv-1)) = stb%dv%ne(mpg%cfreg(ic1+&
&             icv-1)) + cs(mpg%cfreg(ic1+icv-1))*ev*b2datab(icv)/&
&             1.0e3_R8
            csb(mpg%cfreg(ic1+icv-1)) = csb(mpg%cfreg(ic1+icv-1)) + st%&
&             dv%ne(mpg%cfreg(ic1+icv-1))*ev*b2datab(icv)/1.0e3_R8
            b2datab(icv) = 0.D0
          END DO
          rzb = 0.D0
          pzb = 0.D0
          CALL POPREAL8ARRAY(cs, r8*ncv/8)
          WHERE (pz/rz .EQ. 0.D0) 
            tempb0 = 0.D0
          ELSEWHERE
            tempb0 = csb/(rz*2.0*SQRT(pz/rz))
          END WHERE
          pzb = tempb0
          rzb = -(pz*tempb0/rz)
          CALL POPREAL8ARRAY(pz, r8*ncv/8)
          CALL B2XPPZ_B(ncv, ns, st%dv%ne, stb%dv%ne, st%pl%na, stb%pl%&
&                 na, st%pl%te, stb%pl%te, st%pl%ti, stb%pl%ti, pz, pzb&
&                 , st_ext)
          CALL POPREAL8ARRAY(rz, r8*ncv/8)
          CALL B2XPRZ_B(ncv, ns, mp, am, st%pl%na, stb%pl%na, rz, rzb, &
&                 st_ext)
        END IF
      ELSE IF (branch .EQ. 9) THEN
        CALL POPREAL8(j(icf), r8/8)
        jb(icf) = cfweight(icf)*0.5_R8*jb(icf)
        CALL POPINTEGER4(ad_from1)
        CALL POPINTEGER4(ad_to3)
        DO ifc=ad_to3,ad_from1,-1
          CALL POPREAL8(j(icf), r8/8)
          tempb = 2*(st%dv%fht(mpg%cfreg(ifc), 0)+st%dv%fht(mpg%cfreg(&
&           ifc), 1))*mpg%cffcor(ifc)**2*jb(icf)/geo%fcs(mpg%cfreg(ifc))
          stb%dv%fht(mpg%cfreg(ifc), 0) = stb%dv%fht(mpg%cfreg(ifc), 0) &
&           + tempb
          stb%dv%fht(mpg%cfreg(ifc), 1) = stb%dv%fht(mpg%cfreg(ifc), 1) &
&           + tempb
        END DO
        CALL POPREAL8(j(icf), r8/8)
        jb(icf) = 0.D0
      ELSE IF (branch .EQ. 10) THEN
        CALL POPINTEGER4(ad_to4)
        DO ifc=ad_to4,1,-1
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            b2datab(ifc) = b2datab(ifc)/geo%fcs(mpg%cfreg(ifc+ic1-1))
          ELSE
            b2datab(ifc) = b2datab(ifc)/geo%fcpbs(mpg%cfreg(ifc+ic1-1))
          END IF
          tempb = mpg%cffcor(ifc+ic1-1)*b2datab(ifc)/1.0e6
          b2datab(ifc) = 0.D0
          stb%dv%fht(mpg%cfreg(ifc+ic1-1), 0) = stb%dv%fht(mpg%cfreg(ifc&
&           +ic1-1), 0) + tempb
          stb%dv%fht(mpg%cfreg(ifc+ic1-1), 1) = stb%dv%fht(mpg%cfreg(ifc&
&           +ic1-1), 1) + tempb
        END DO
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) shiftb(cf_to_shift(icf)) = shiftb(cf_to_shift(&
&         icf)) + curr_shiftb/1.0e3_R8
      CALL POPREAL8(curr_shift, r8/8)
      CALL POPINTEGER4(n2)
      CALL POPINTEGER4(n1)
      CALL POPINTEGER4(ic1)
    END DO
  ELSE
    sigmab = 0.D0
    meanb = 0.D0
    shiftb = 0.D0
    corr_lengthb = 0.D0
    IF (ALLOCATED(par_opt_physb)) par_opt_physb = 0.D0
    cfnormb = 0.D0
    voldb = 0.D0
    IF (ALLOCATED(b2voloncfb)) b2voloncfb = 0.D0
    stb%pl%na = 0.D0
    stb%pl%te = 0.D0
    stb%pl%ti = 0.D0
    stb%dv%fht = 0.D0
    stb%dv%ne = 0.D0
  END IF
  DO icf=ncf,1,-1
    CALL POPCONTROL1B(branch)
    IF (branch .NE. 0) THEN
      CALL POPINTEGER4(ad_to)
      DO icv=ad_to,1,-1
        b2voloncfb(icf, icv) = b2voloncfb(icf, icv) + cfdata(icf, 2, icv&
&         )*cfnormb(icf)
      END DO
      b2voloncfb(icf, 1:n2) = b2voloncfb(icf, 1:n2) + voldb(icf)
      voldb(icf) = 0.D0
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2rr(icf, 1:n1), r8*n1/8)
      CALL POPREAL8ARRAY(cfdata(icf, 1, 1:n2), r8*n2/8)
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2data(1:n1), r8*n1/8)
      IF (ALLOCATED(b2datab)) b2datab = 0.D0
      CALL INTERP1D_B(n1, n2, b2rr(icf, 1:n1), cfdata(icf, 1, 1:n2), &
&               b2data(1:n1), b2datab(1:n1), b2voloncf(icf, 1:n2), &
&               b2voloncfb(icf, 1:n2), curr_shift, curr_shiftb)
      CALL POPCONTROL1B(branch)
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) shiftb(cf_to_shift(icf)) = shiftb(cf_to_shift(&
&         icf)) + curr_shiftb/1.0e3_R8
      CALL POPREAL8(curr_shift, r8/8)
      CALL POPINTEGER4(n2)
      CALL POPINTEGER4(n1)
    END IF
  END DO
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
    DO 100 icf=ncf,1,-1
      CALL POPCONTROL2B(branch)
      IF (branch .EQ. 0) THEN
        n1 = mpg%cfregp(icf, 2)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          DO icv=2,nomp-1,1
            CALL POPCONTROL1B(branch)
          END DO
          DO icv=nomp-1,2,-1
            CALL POPCONTROL1B(branch)
          END DO
        ELSE
          DO icv=2,nomp-1,1
            CALL POPCONTROL1B(branch)
          END DO
          DO icv=nomp-1,2,-1
            CALL POPCONTROL1B(branch)
          END DO
        END IF
      ELSE IF (branch .NE. 1) THEN
        GOTO 100
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        ic1 = mpg%cfregp(icf, 1)
        ic2 = ic1 + mpg%cfregp(icf, 2) - 1
      ELSE
        ic1 = mpg%cfregp(icf, 1)
        ic2 = ic1 + mpg%cfregp(icf, 2) - 1
      END IF
      CALL POPINTEGER4(n1)
 100 CONTINUE
  END IF
END SUBROUTINE B2USR_COST_FUNCTION_B
