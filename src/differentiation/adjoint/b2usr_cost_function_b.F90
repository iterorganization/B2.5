        CALL PUSHINTEGER4(isigma)
        isigma = isigma + 1
        CALL PUSHINTEGER4(imean)
        imean = imean + 1
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
      END IF
