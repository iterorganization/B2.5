&                       n2), b2data(1:n1), b2datab(1:n1), b2dataoncf(1:&
&                       n2), b2dataoncfb(1:n2))
              csb = 0.D0
              CALL POPINTEGER4(ad_to8)
              DO icv=ad_to8,1,-1
                stb%dv%ne(mpg%cfreg(ic1+icv-1)) = stb%dv%ne(mpg%cfreg(&
&                 ic1+icv-1)) + cs(mpg%cfreg(ic1+icv-1))*ev*b2datab(icv)&
&                 /1.0e3_R8
                csb(mpg%cfreg(ic1+icv-1)) = csb(mpg%cfreg(ic1+icv-1)) + &
&                 st%dv%ne(mpg%cfreg(ic1+icv-1))*ev*b2datab(icv)/&
&                 1.0e3_R8
                b2datab(icv) = 0.D0
              END DO
              CALL POPINTEGER4(icv)
              rzb = 0.D0
              pzb = 0.D0
              CALL POPREAL8ARRAY(cs, r8*ncv/8)
              WHERE (pz/rz .EQ. 0.D0) 
                tempb0 = 0.D0
              ELSEWHERE
                tempb0 = csb/(rz*2.0*SQRT(pz/rz))
              END WHERE
              pzb = tempb0
              rzb = -(pz*tempb0/rz)
              CALL POPREAL8ARRAY(pz, r8*ncv/8)
              CALL B2XPPZ_B(ncv, ns, st%dv%ne, stb%dv%ne, st%pl%na, stb%&
&                     pl%na, st%pl%te, stb%pl%te, st%pl%ti, stb%pl%ti, &
&                     pz, pzb, st_ext)
              CALL POPREAL8ARRAY(rz, r8*ncv/8)
              CALL B2XPRZ_B(ncv, ns, mp, am, st%pl%na, stb%pl%na, rz, &
&                     rzb, st_ext)
            ELSE
              jb(icf) = cfweight(icf)*vold(icf)*jb(icf)/(cfnorm(icf)**2*&
&               2.0_R8)
              n2 = ncfdata(icf)
              CALL POPINTEGER4(ad_to7)
              DO icv=ad_to7,1,-1
                b2dataoncfb(icv) = b2dataoncfb(icv) + 2*(b2dataoncf(icv)&
&                 -cfdata(icf, 2, icv))*b2voloncf(icf, icv)*jb(icf)
              END DO
              CALL POPINTEGER4(icv)
              n1 = mpg%cfregp(icf, 2)
              CALL POPCONTROL1B(branch)
              IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2dataoncf(1:n2), r8&
&                                             *n2/8)
              CALL INTERP1D_B(n1, n2, b2rr(icf, 1:n1), cfdata(icf, 1, 1:&
&                       n2), b2data(1:n1), b2datab(1:n1), b2dataoncf(1:&
&                       n2), b2dataoncfb(1:n2))
              ic1 = mpg%cfregp(icf, 1)
              ic2 = ic1 + mpg%cfregp(icf, 2) - 1
              gradrb = 0.D0
              gradrb(mpg%cfreg(ic1:ic2)) = gradrb(mpg%cfreg(ic1:ic2)) + &
&               b2datab(1:n1)/ev
              b2datab(1:n1) = 0.D0
              CALL GRADC_R_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, st%&
&                      pl%ti, stb%pl%ti, funv, funvb, gradr, gradrb)
            END IF
          END IF
          GOTO 100
        ELSE IF (branch .EQ. 3) THEN
          jb(icf) = cfweight(icf)*vold(icf)*jb(icf)/(cfnorm(icf)**2*&
&           2.0_R8)
          n2 = ncfdata(icf)
          CALL POPINTEGER4(ad_to6)
          DO icv=ad_to6,1,-1
            b2dataoncfb(icv) = b2dataoncfb(icv) + 2*(b2dataoncf(icv)-&
&             cfdata(icf, 2, icv))*b2voloncf(icf, icv)*jb(icf)
          END DO
          CALL POPINTEGER4(icv)
          n1 = mpg%cfregp(icf, 2)
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2dataoncf(1:n2), r8*n2/&
&                                         8)
          CALL INTERP1D_B(n1, n2, b2rr(icf, 1:n1), cfdata(icf, 1, 1:n2)&
&                   , b2data(1:n1), b2datab(1:n1), b2dataoncf(1:n2), &
&                   b2dataoncfb(1:n2))
          ic1 = mpg%cfregp(icf, 1)
          ic2 = ic1 + mpg%cfregp(icf, 2) - 1
          gradrb = 0.D0
          gradrb(mpg%cfreg(ic1:ic2)) = gradrb(mpg%cfreg(ic1:ic2)) + &
&           b2datab(1:n1)/ev
          b2datab(1:n1) = 0.D0
          CALL GRADC_R_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, st%pl%&
&                  te, stb%pl%te, funv, funvb, gradr, gradrb)
          GOTO 100
        ELSE IF (branch .EQ. 4) THEN
          jb(icf) = cfweight(icf)*vold(icf)*jb(icf)/(cfnorm(icf)**2*&
&           2.0_R8)
          n2 = ncfdata(icf)
          CALL POPINTEGER4(ad_to5)
          DO icv=ad_to5,1,-1
            b2dataoncfb(icv) = b2dataoncfb(icv) + 2*(b2dataoncf(icv)-&
&             cfdata(icf, 2, icv))*b2voloncf(icf, icv)*jb(icf)
          END DO
          CALL POPINTEGER4(icv)
          n1 = mpg%cfregp(icf, 2)
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2dataoncf(1:n2), r8*n2/&
&                                         8)
          CALL INTERP1D_B(n1, n2, b2rr(icf, 1:n1), cfdata(icf, 1, 1:n2)&
&                   , b2data(1:n1), b2datab(1:n1), b2dataoncf(1:n2), &
&                   b2dataoncfb(1:n2))
          ic1 = mpg%cfregp(icf, 1)
          ic2 = ic1 + mpg%cfregp(icf, 2) - 1
          gradrb = 0.D0
          gradrb(mpg%cfreg(ic1:ic2)) = gradrb(mpg%cfreg(ic1:ic2)) + &
&           b2datab(1:n1)/1.0e19_R8
          b2datab(1:n1) = 0.D0
          CALL GRADC_R_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, st%dv%&
&                  ne, stb%dv%ne, funv, funvb, gradr, gradrb)
          GOTO 100
        ELSE
          jb(icf) = 0.D0
          lll_cumb = 0.D0
          priorb = 0.D0
        END IF
      ELSE IF (branch .LT. 9) THEN
        IF (branch .EQ. 6) THEN
          priorb = -(cfweight(icf)*jb(icf))
          lll_cumb = -(cfweight(icf)*jb(icf))
          jb(icf) = 0.D0
        ELSE
          IF (branch .EQ. 7) THEN
            CALL POPINTEGER4(ad_from0)
            CALL POPINTEGER4(ad_to3)
            DO ifc=ad_to3,ad_from0,-1
              max1b = cfweight(icf)*jb(icf)
              jb(icf) = 0.D0
              CALL POPCONTROL1B(branch)
              IF (branch .EQ. 0) THEN
                qqb = max1b
              ELSE
                jb(icf) = jb(icf) + max1b
                qqb = 0.D0
              END IF
              qqb = mpg%cffcor(ifc)*qqb/geo%fcs(mpg%cfreg(ifc))
              DO is=ns-1,0,-1
                stb%dv%fna(mpg%cfreg(ifc), 0, is) = stb%dv%fna(mpg%cfreg&
&                 (ifc), 0, is) + (ev*st%rt%rpt(icv, is)+(1-boris)*(st%&
&                 dv%kinrgy(icv, is)+st%pl%ti(icv)))*qqb
                tempb = st%dv%fna(mpg%cfreg(ifc), 0, is)*qqb
                stb%rt%rpt(icv, is) = stb%rt%rpt(icv, is) + ev*tempb
                stb%dv%kinrgy(icv, is) = stb%dv%kinrgy(icv, is) + (1-&
&                 boris)*tempb
                stb%pl%ti(icv) = stb%pl%ti(icv) + (1-boris)*tempb
              END DO
              stb%dv%fhe(mpg%cfreg(ifc), 0) = stb%dv%fhe(mpg%cfreg(ifc)&
&               , 0) + qqb
              stb%dv%fhi(mpg%cfreg(ifc), 0) = stb%dv%fhi(mpg%cfreg(ifc)&
&               , 0) + qqb
              stb%dv%fne(mpg%cfreg(ifc), 0) = stb%dv%fne(mpg%cfreg(ifc)&
&               , 0) + st%pl%te(icv)*(1-boris)*qqb
              stb%pl%te(icv) = stb%pl%te(icv) + st%dv%fne(mpg%cfreg(ifc)&
&               , 0)*(1-boris)*qqb
              CALL POPCONTROL1B(branch)
              CALL POPCONTROL1B(branch)
              IF (branch .EQ. 0) THEN
                CALL POPINTEGER4(icv)
              ELSE
                CALL POPINTEGER4(icv)
              END IF
            END DO
          ELSE
            jb(icf) = jb(icf)/ev
            CALL POPINTEGER4(ad_from)
            CALL POPINTEGER4(ad_to2)
            DO icv=ad_to2,ad_from,-1
              CALL POPCONTROL1B(branch)
              IF (branch .EQ. 0) THEN
                stb%pl%te(mpg%cfreg(icv)) = stb%pl%te(mpg%cfreg(icv)) + &
&                 jb(icf)
                jb(icf) = 0.D0
              END IF
            END DO
            CALL POPINTEGER4(icv)
          END IF
          GOTO 100
        END IF
      ELSE
        IF (branch .LT. 11) THEN
          IF (branch .EQ. 9) THEN
            jb(icf) = cfweight(icf)*vold(icf)*jb(icf)/(cfnorm(icf)**2*&
&             2.0_R8)
            n2 = ncfdata(icf)
            CALL POPINTEGER4(ad_to1)
            DO icv=ad_to1,1,-1
              b2dataoncfb(icv) = b2dataoncfb(icv) + 2*(b2dataoncf(icv)-&
&               cfdata(icf, 2, icv))*b2voloncf(icf, icv)*jb(icf)
            END DO
            CALL POPINTEGER4(icv)
            n1 = mpg%cfregp(icf, 2)
            CALL POPCONTROL1B(branch)
            IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2dataoncf(1:n2), r8*&
&                                           n2/8)
            CALL INTERP1D_B(n1, n2, b2rr(icf, 1:n1), cfdata(icf, 1, 1:n2&
&                     ), b2data(1:n1), b2datab(1:n1), b2dataoncf(1:n2), &
&                     b2dataoncfb(1:n2))
            ic1 = mpg%cfregp(icf, 1)
            ic2 = ic1 + mpg%cfregp(icf, 2) - 1
            stb%pl%ti(mpg%cfreg(ic1:ic2)) = stb%pl%ti(mpg%cfreg(ic1:ic2)&
&             ) + b2datab(1:n1)/ev
            b2datab(1:n1) = 0.D0
          ELSE
            jb(icf) = cfweight(icf)*vold(icf)*jb(icf)/(cfnorm(icf)**2*&
&             2.0_R8)
            n2 = ncfdata(icf)
            CALL POPINTEGER4(ad_to0)
            DO icv=ad_to0,1,-1
              b2dataoncfb(icv) = b2dataoncfb(icv) + 2*(b2dataoncf(icv)-&
&               cfdata(icf, 2, icv))*b2voloncf(icf, icv)*jb(icf)
            END DO
            CALL POPINTEGER4(icv)
            n1 = mpg%cfregp(icf, 2)
            CALL POPCONTROL1B(branch)
            IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2dataoncf(1:n2), r8*&
&                                           n2/8)
            CALL INTERP1D_B(n1, n2, b2rr(icf, 1:n1), cfdata(icf, 1, 1:n2&
&                     ), b2data(1:n1), b2datab(1:n1), b2dataoncf(1:n2), &
&                     b2dataoncfb(1:n2))
            ic1 = mpg%cfregp(icf, 1)
            ic2 = ic1 + mpg%cfregp(icf, 2) - 1
            stb%pl%te(mpg%cfreg(ic1:ic2)) = stb%pl%te(mpg%cfreg(ic1:ic2)&
&             ) + b2datab(1:n1)/ev
            b2datab(1:n1) = 0.D0
          END IF
        ELSE IF (branch .EQ. 11) THEN
          jb(icf) = cfweight(icf)*vold(icf)*jb(icf)/(cfnorm(icf)**2*&
&           2.0_R8)
          n2 = ncfdata(icf)
          CALL POPINTEGER4(ad_to)
          DO icv=ad_to,1,-1
            b2dataoncfb(icv) = b2dataoncfb(icv) + 2*(b2dataoncf(icv)-&
&             cfdata(icf, 2, icv))*b2voloncf(icf, icv)*jb(icf)
          END DO
          CALL POPINTEGER4(icv)
          n1 = mpg%cfregp(icf, 2)
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2dataoncf(1:n2), r8*n2/&
&                                         8)
          CALL INTERP1D_B(n1, n2, b2rr(icf, 1:n1), cfdata(icf, 1, 1:n2)&
&                   , b2data(1:n1), b2datab(1:n1), b2dataoncf(1:n2), &
&                   b2dataoncfb(1:n2))
          ic1 = mpg%cfregp(icf, 1)
          ic2 = ic1 + mpg%cfregp(icf, 2) - 1
          stb%dv%ne(mpg%cfreg(ic1:ic2)) = stb%dv%ne(mpg%cfreg(ic1:ic2)) &
&           + b2datab(1:n1)/1.0e19_R8
          b2datab(1:n1) = 0.D0
        END IF
        GOTO 100
      END IF
      DO iicf=cfend(1),cfstart(1),-1
        CALL POPINTEGER4(imean)
        CALL POPINTEGER4(isigma)
        lllb = lll_cumb
        n2 = ncfdata(iicf)
        CALL CALC_LOGLIKELIHOOD_B(n2, b2dataoncf(1:n2), b2dataoncfb(1:n2&
&                           ), cfdata(iicf, 2, 1:n2), cfdata(iicf, 3, 1:&
&                           n2), lll, lllb, isigma, imean)
        n1 = mpg%cfregp(iicf, 2)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 1) CALL POPREAL8ARRAY(b2dataoncf(1:n2), r8*n2/8)
        CALL INTERP1D_B(n1, n2, b2rr(iicf, 1:n1), cfdata(iicf, 1, 1:n2)&
&                 , b2data(1:n1), b2datab(1:n1), b2dataoncf(1:n2), &
&                 b2dataoncfb(1:n2))
        CALL POPCONTROL3B(branch)
        IF (branch .LT. 4) THEN
          IF (branch .LT. 2) THEN
            IF (branch .EQ. 0) THEN
              ic1 = mpg%cfregp(iicf, 1)
              ic2 = ic1 + n1 - 1
              stb%dv%ne(mpg%cfreg(ic1:ic2)) = stb%dv%ne(mpg%cfreg(ic1:&
&               ic2)) + b2datab(1:n1)/1.0e19_R8
              b2datab(1:n1) = 0.D0
            ELSE
              ic1 = mpg%cfregp(iicf, 1)
              ic2 = ic1 + n1 - 1
              stb%pl%te(mpg%cfreg(ic1:ic2)) = stb%pl%te(mpg%cfreg(ic1:&
&               ic2)) + b2datab(1:n1)/ev
              b2datab(1:n1) = 0.D0
            END IF
          ELSE IF (branch .EQ. 2) THEN
            ic1 = mpg%cfregp(iicf, 1)
            ic2 = ic1 + n1 - 1
            stb%pl%ti(mpg%cfreg(ic1:ic2)) = stb%pl%ti(mpg%cfreg(ic1:ic2)&
&             ) + b2datab(1:n1)/ev
            b2datab(1:n1) = 0.D0
          ELSE
            ic1 = mpg%cfregp(iicf, 1)
            ic2 = ic1 + n1 - 1
            gradrb = 0.D0
            gradrb(mpg%cfreg(ic1:ic2)) = gradrb(mpg%cfreg(ic1:ic2)) + &
&             b2datab(1:n1)/1.0e19_R8
            b2datab(1:n1) = 0.D0
            CALL GRADC_R_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, st%dv&
&                    %ne, stb%dv%ne, funv, funvb, gradr, gradrb)
          END IF
        ELSE IF (branch .LT. 6) THEN
          IF (branch .EQ. 4) THEN
            ic1 = mpg%cfregp(iicf, 1)
            ic2 = ic1 + n1 - 1
            gradrb = 0.D0
            gradrb(mpg%cfreg(ic1:ic2)) = gradrb(mpg%cfreg(ic1:ic2)) + &
&             b2datab(1:n1)/ev
            b2datab(1:n1) = 0.D0
            CALL GRADC_R_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, st%pl&
&                    %te, stb%pl%te, funv, funvb, gradr, gradrb)
          ELSE
            ic1 = mpg%cfregp(iicf, 1)
            ic2 = ic1 + n1 - 1
            gradrb = 0.D0
            gradrb(mpg%cfreg(ic1:ic2)) = gradrb(mpg%cfreg(ic1:ic2)) + &
&             b2datab(1:n1)/ev
            b2datab(1:n1) = 0.D0
            CALL GRADC_R_B(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, st%pl&
&                    %ti, stb%pl%ti, funv, funvb, gradr, gradrb)
          END IF
        ELSE IF (branch .EQ. 6) THEN
          csb = 0.D0
          CALL POPINTEGER4(ad_to4)
          DO icv=ad_to4,1,-1
            stb%dv%ne(mpg%cfreg(ic1+icv-1)) = stb%dv%ne(mpg%cfreg(ic1+&
&             icv-1)) + cs(mpg%cfreg(ic1+icv-1))*ev*b2datab(icv)/&
&             1.0e3_R8
            csb(mpg%cfreg(ic1+icv-1)) = csb(mpg%cfreg(ic1+icv-1)) + st%&
&             dv%ne(mpg%cfreg(ic1+icv-1))*ev*b2datab(icv)/1.0e3_R8
            b2datab(icv) = 0.D0
          END DO
          CALL POPINTEGER4(icv)
          rzb = 0.D0
          pzb = 0.D0
          CALL POPREAL8ARRAY(cs, r8*ncv/8)
          WHERE (pz/rz .EQ. 0.D0) 
            tempb0 = 0.D0
          ELSEWHERE
            tempb0 = csb/(rz*2.0*SQRT(pz/rz))
          END WHERE
          pzb = tempb0
          rzb = -(pz*tempb0/rz)
          CALL POPREAL8ARRAY(pz, r8*ncv/8)
          CALL B2XPPZ_B(ncv, ns, st%dv%ne, stb%dv%ne, st%pl%na, stb%pl%&
&                 na, st%pl%te, stb%pl%te, st%pl%ti, stb%pl%ti, pz, pzb&
&                 , st_ext)
          CALL POPREAL8ARRAY(rz, r8*ncv/8)
          CALL B2XPRZ_B(ncv, ns, mp, am, st%pl%na, stb%pl%na, rz, rzb, &
&                 st_ext)
        END IF
        CALL POPINTEGER4(ic1)
      END DO
      CALL CALC_LOG_PRIOR_B(prior, priorb, inrange)
 100  CALL POPINTEGER4(n1)
      CALL POPINTEGER4(ic1)
    END DO
  ELSE
    sigmab = 0.D0
    IF (ALLOCATED(par_opt_physb)) par_opt_physb = 0.D0
    meanb = 0.D0
    stb%pl%na = 0.D0
    stb%pl%te = 0.D0
    stb%pl%ti = 0.D0
    stb%dv%fna = 0.D0
    stb%dv%fne = 0.D0
    stb%dv%fhe = 0.D0
    stb%dv%fhi = 0.D0
    stb%dv%kinrgy = 0.D0
    stb%dv%ne = 0.D0
    stb%rt%rpt = 0.D0
  END IF
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
    DO icf=ncf,1,-1
      CALL POPCONTROL2B(branch)
      IF (branch .NE. 0) THEN
        IF (branch .NE. 1) n1 = mpg%cfregp(icf, 2)
        n2 = ncfdata(icf)
        ic1 = mpg%cfregp(icf, 1)
        ic2 = ic1 + mpg%cfregp(icf, 2) - 1
        CALL POPINTEGER4(n1)
      END IF
    END DO
  END IF
END SUBROUTINE B2USR_COST_FUNCTION_B
