      lll_cumb = 0.D0
    END IF
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      DO icf=cfend(1),cfstart(1),-1
        tmpb = jb(1)
        jb(1) = tmpb
        jb(icf) = jb(icf) + tmpb
      END DO
    END IF
    cfnormb = 0.D0
    voldb = 0.D0
    IF (ALLOCATED(b2datab)) b2datab = 0.D0
    IF (ALLOCATED(b2dataoncfb)) b2dataoncfb = 0.D0
    IF (ALLOCATED(b2voloncfb)) b2voloncfb = 0.D0
    stb%pl%na = 0.D0
    stb%pl%te = 0.D0
    stb%pl%ti = 0.D0
    stb%dv%fht = 0.D0
    stb%dv%ne = 0.D0
    funvb = 0.D0
    DO icf=ncf,1,-1
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPINTEGER4(imean)
        CALL POPINTEGER4(isigma)
        lllb = lll_cumb
        n2 = ncfdata(icf)
        CALL CALC_LOGLIKELIHOOD_B(n2, b2dataoncf(1:n2), b2dataoncfb(1:n2&
&                           ), cfdata(icf, 1, 1:n2), cfdata(icf, 2, 1:n2&
&                           ), cfdata(icf, 3, 1:n2), lll, lllb, isigma, &
&                           imean, icf)
