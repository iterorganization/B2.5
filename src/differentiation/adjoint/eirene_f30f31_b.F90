        IF (switch%eir_ua_min .GE. 0.) THEN
          y1 = switch%eir_ua_min
        ELSE
          y1 = -switch%eir_ua_min
        END IF
        IF (switch%eir_ua_max .GT. y1) THEN
          IF (y1 .GT. switch%eir_m_max*cs(ic)) THEN
            vmax = switch%eir_m_max*cs(ic)
          ELSE
            vmax = y1
          END IF
        ELSE IF (switch%eir_ua_max .GT. switch%eir_m_max*cs(ic)) THEN
          vmax = switch%eir_m_max*cs(ic)
        ELSE
          vmax = switch%eir_ua_max
        END IF
        IF (vtot .GT. vmax) THEN
          uub(ic, nflai) = uub(ic, nflai)*vmax/vtot
          vvb(ic, nflai) = vvb(ic, nflai)*vmax/vtot
          wwb(ic, nflai) = wwb(ic, nflai)*vmax/vtot
          upb(ic, nflai) = upb(ic, nflai)*vmax/vtot
          uudiab(ic, nflai) = uudiab(ic, nflai)*vmax/vtot
          vvdiab(ic, nflai) = vvdiab(ic, nflai)*vmax/vtot
        END IF
!
        vxb(ic, nflai) = uub(ic, nflai)*pux(ic) + vvb(ic, nflai)*pvx(ic)
        vyb(ic, nflai) = uub(ic, nflai)*puy(ic) + vvb(ic, nflai)*pvy(ic)
        vzb(ic, nflai) = wwb(ic, nflai)*SIGN(1.0_R8, gm%cveb(ic, 2))
      END DO
! cell faces
      DO ifc=1,nfc
        fnib(ifc, nflai) = dv%fna(ifc, 0, is) + dv%fna(ifc, 1, is)
        IF (is_neutral(is)) THEN
          fnn_inc_b(ifc, nflai) = dv%fnn_inc(ifc, is)
          fnn_mol_b(ifc, nflai) = fna_mol(ifc, is)
        END IF
      END DO
!
!!        endif

    END IF
  END DO
!
!pb  05.03.18
  nflai = COUNT(fluids_list .GT. 0, 1)
!
  IF (switch%eirene_extrap .NE. 0) THEN
! do nothing
    IF (switch%eirene_extrap .EQ. 1) THEN
! we derive the guard cell value from its neighbouring interior cell
      DO ic=mpg%nci+1,mpg%ncv
        ifc = mpg%cvfc(mpg%cvfcp(ic, 1))
        IF (mpg%fccv(ifc, 1) .GT. mpg%fccv(ifc, 2)) THEN
          ic2 = mpg%fccv(ifc, 2)
        ELSE
          ic2 = mpg%fccv(ifc, 1)
        END IF
        teb(ic) = teb(ic2)
        tib(ic) = tib(ic2)
        tnb(ic) = tnb(ic2)
        pob(ic) = pob(ic2)
        dnib(ic, :) = dnib(ic2, :)
      END DO
    ELSE
      WRITE(*, *) switch%eirene_extrap, &
&     ' not available as an eirene_extrap option'
    END IF
  END IF
!
  IF (switch%eirene_print_minmax .NE. 0) THEN
    te_min_val = MINVAL(teb(:))
    te_max_val = MAXVAL(teb(:))
    ti_min_val = MINVAL(tib(:))
    ti_max_val = MAXVAL(tib(:))
    na_min_val = MINVAL(dnib(:, 1:nflai))
    na_max_val = MAXVAL(dnib(:, 1:nflai))
    ua_min_val = MINVAL(uub(:, 1:nflai))
    ua_max_val = MAXVAL(uub(:, 1:nflai))
    WRITE(*, '(a,1p,8(1x,g12.3))') 'minmax:teb,tib,nab,uub ', te_min_val&
&   , te_max_val, ti_min_val, ti_max_val, na_min_val, na_max_val, &
&   ua_min_val, ua_max_val
  END IF
!
  IF (switch%copy_background_iout .NE. 0) THEN
    nflai = 0
    DO is=0,ns-1
      IF (.NOT.is_neutral(is)) THEN
        nflai = nflai + 1
        WRITE(chns, '(i3.3)') is
        WRITE(ccall, '(a1,i1)') '_', icall
        arg17 = 'copy_uub'//chns//ccall
        CALL MY_OUT_US(70, ncv, 0, uub(1, nflai), arg17)
        arg17 = 'copy_vvb'//chns//ccall
        CALL MY_OUT_US(70, ncv, 0, vvb(1, nflai), arg17)
        arg17 = 'copy_wwb'//chns//ccall
        CALL MY_OUT_US(70, ncv, 0, wwb(1, nflai), arg17)
        arg17 = 'copy_upb'//chns//ccall
        CALL MY_OUT_US(70, ncv, 0, upb(1, nflai), arg17)
      END IF
    END DO
    CALL MY_OUT_US(70, ncv, 0, teb, 'copy_teb')
    CALL MY_OUT_US(70, ncv, 0, tib, 'copy_tib')
    CALL MY_OUT_US(70, ncv, 0, tnb, 'copy_tnb')
    CALL MY_OUT_US(70, ncv, 0, pob, 'copy_pob')
    icall = icall + 1
  END IF
!
!  .. define target data
!
  IF (.NOT.ALLOCATED(trgt)) THEN
    ALLOCATE(trgt(mpg%nrc))
  END IF
  DO istra=1,mpg%nrc
    ian = mpg%rcfcp(istra, 1)
    ien = ian + mpg%rcfcp(istra, 2) - 1
    ntrgdat = ien - ian + 1
    IF (ien - ian + 1 .LE. 0) THEN
      trgt(istra)%ntrgdat = 0
    ELSE
!
      CALL ALLOC_TARGET_DATA(istra, ntrgdat, nflai)
!
      DO i=1,ntrgdat
        ifc = mpg%rcfc(ian+i-1)
        trgt(istra)%faces(i) = ifc
        trgt(istra)%fcori(i) = mpg%rcfcor(ian+i-1)
        iv1 = mpg%fcvx(ifc, 1)
        iv2 = mpg%fcvx(ifc, 2)
! index of guard cell
        ic = mpg%rccv(ian+i-1, 1)
        trgt(istra)%flength(i) = SQRT((gm%vxx(iv1)-gm%vxx(iv2))**2+(gm%&
&         vxy(iv1)-gm%vxy(iv2))**2)*100._R8
        trgt(istra)%tet(i) = teb(ic)
        trgt(istra)%tit(i) = tib(ic)
        trgt(istra)%tnt(i) = tnb(ic)
        trgt(istra)%fe(i) = rcfe(istra)
        trgt(istra)%fsh(i) = delta_sheathb(ifc)
        DO ifl=1,nflai
          trgt(istra)%dnit(i, ifl) = dnib(ic, ifl)
          IF (zab(ifl) .NE. 0 .AND. mol(istra) .EQ. 0) THEN
!! ion species
            trgt(istra)%flux(i, ifl) = fnib(ifc, ifl)*mpg%rcfcor(ian+i-1&
&             )*dv%kin_frac_hyb(ifc)
          ELSE IF (zab(ifl) .EQ. 0 .AND. mol(istra) .EQ. 0) THEN
!! neutral atom species
            trgt(istra)%flux(i, ifl) = fnn_inc_b(ifc, ifl)*mpg%rcfcor(&
&             ian+i-1)*dv%kin_frac_hyb(ifc)
            fnib(ifc, ifl) = fnn_inc_b(ifc, ifl)
          ELSE IF (zab(ifl) .EQ. 0 .AND. mol(istra) .EQ. 1) THEN
!! neutral molecule species
            trgt(istra)%flux(i, ifl) = 2.0_R8*fnn_mol_b(ifc, ifl)*dv%&
&             fluid_frac_hyb(ifc)
            fnib(ifc, ifl) = 0.0_R8
          ELSE IF (fnn_mol_b(ifc, ifl) .GT. 0) THEN
            CALL XERRAB('no molecular contribution expected here')
          END IF
          IF (trgt(istra)%flux(i, ifl) .LT. 0.0_R8) THEN
            WRITE(*, *) 'negative flux !! istra, i, ifl = ', istra, i, &
&           ifl
            trgt(istra)%flux(i, ifl) = 0.0_R8
            fnib(ifc, ifl) = 0.0_R8
          END IF
          trgt(istra)%fi(i, ifl) = rcfi(istra)
!
          ebx = gm%fceb(ifc, 0)
          eby = gm%fceb(ifc, 1)
          ebn = SQRT(ebx*ebx + eby*eby)
          IF (ebn .GT. 0) THEN
            ebx = ebx/ebn
            eby = eby/ebn
          END IF
          puxs = ebx
          puys = eby
          pvxs = -(eby*gm%signmf)
          pvys = ebx*gm%signmf
!
          uu = uub(ic, ifl)
          vv = vvb(ic, ifl)
          ww = wwb(ic, ifl)
          trgt(istra)%vxt(i, ifl) = puxs*uu + pvxs*vv
          trgt(istra)%vyt(i, ifl) = puys*uu + pvys*vv
          trgt(istra)%vzt(i, ifl) = ww*SIGN(1.0_R8, gm%fceb(ifc, 2))
          trgt(istra)%fel(i, ifl) = 0.0_R8
          trgt(istra)%vpart(i, ifl) = 0.0_R8
          trgt(istra)%mach(i, ifl) = 1.0_R8
          trgt(istra)%usr(i, ifl) = 0.0_R8
          trgt(istra)%zit(i, ifl) = zab(ifl)
        END DO
      END DO
    END IF
  END DO
!
  ncall = ncall + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE COPY_BACKGROUND_NODIFF

