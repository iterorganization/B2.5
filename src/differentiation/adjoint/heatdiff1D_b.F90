!
!_______________________________________________________________________
! Dirichlet condition(u) at xn. !Neumann condition(du/dx) at x0.
! No. of equations to be solved = ndepth_nml - 1
!_______________________________________________________________________
SUBROUTINE LINES2C(ibnd, ns, t, n, u, du)
  USE B2MOD_TYPES
  USE B2MOD_WALL
  IMPLICIT NONE
!
  INTEGER :: ns, n, i, j, ibnd
  REAL(kind=r8) :: ux, uxx
  REAL(kind=r8) :: x(ndepth), u(ndepth_nml), uu(ndepth_nml+1), du(&
& ndepth_nml), t
  REAL(kind=r8) :: a0, b0, c0, an, bn, cn
  REAL(kind=r8) :: dx(ndepth-1), x0, xn, ts, td
  COMMON /zzline/ dx, x0, xn, x
!
!      print*,t,' ',n,' InpVars ',u
! Maximum no. of equations that can be solved is np.
!
! Call to coefbc to get the b.cs
  ts = u(1)
  td = u(ndepth_nml)
  CALL COEFBC_NODIFF(ibnd, ndepth_nml, ns, t, ts, td, a0, b0, c0, an, bn&
&              , cn)
!
! Neumann condition at x = x0.
! u0 = u(x0-dx,t)
  uu(1) = u(2) + 2.0_R8*dx(1)*(a0*u(1)-c0)/b0
!
! Storing intermediate values.
  DO i=1,ndepth_nml-1
    uu(i+1) = u(i)
  END DO
!
! Dirichlet condition(u) at xn.
  uu(ndepth_nml+1) = cn/an
!
! Calculate du/dt at all grid points.
  DO j=1,ndepth_nml-1
    i = j + 1
    ux = (uu(i+1)-uu(i-1))/(dx(i)+dx(i-1))
    uxx = ((uu(i+1)-uu(i))/dx(i)-(uu(i)-uu(i-1))/dx(i-1))/((dx(i)+dx(i-1&
&     ))/2.0_R8)
    CALL FCN_NODIFF(ibnd, j, x(j), t, uu(i), ux, uxx, du(j))
  END DO
!
  RETURN
END SUBROUTINE LINES2C

!
!..................................................................
!                     SUBROUTINE FCN
!
!  The diffusion equation in our case looks like ->
!       du = (1/rho/Cp(T)) * (K(T)*UXX + gradK(T)*UX + S)
!  where gradK = dK/dx and S is a heat flux source which is a delta
!  function at the plasma facing surface, which is implemented as a
!  boundary condition. Therefore here S = 0.
!  The second term on RHS is grad(K*UX) = dK/dx * UX = dK/dU*(UX)^2
!
SUBROUTINE FCN_NODIFF(ibnd, i, x, t, u, ux, uxx, du)
  USE B2MOD_TYPES
  USE B2MOD_WALL
  IMPLICIT NONE
  INTEGER :: ibnd, i
  REAL(kind=r8) :: x, t, u, du, ux, uxx
  REAL(kind=r8) :: KAPPA_NODIFF, DKAPPABYDT_NODIFF, CP_NODIFF, &
& MASS_DENSITY, ts
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
  REAL(kind=r8) :: result3
  REAL(kind=r8) :: result4
!
  ts = u
  result1 = MASS_DENSITY(ibnd, i)
  result2 = CP_NODIFF(ibnd, i, ts)
  result3 = KAPPA_NODIFF(ibnd, i, ts)
  result4 = DKAPPABYDT_NODIFF(ibnd, i, ts)
  du = 1.0_R8/result1/result2*(result3*uxx+result4*ux*ux)
!
  RETURN
END SUBROUTINE FCN_NODIFF

!______________________________________________________________________
!
!                 subroutine coefbc
!
! Subroutine to specify the boundary condition.
! general boundary condition.
! a0*u + b0*du/dx = c0  at the plasma facing surface.
! an*u + bn*du/dx = cn  at the cooling tube surface.
!
! t, Ts and Td are inputs to the subroutine and a0, b0, c0, an, bn, and cn
! are the outputs from the subroutine.
!
SUBROUTINE COEFBC_NODIFF(ibnd, nd, ns, t, ts, td, a0, b0, c0, an, bn, cn&
&)
  USE B2MOD_TYPES
  IMPLICIT NONE
  INTEGER :: ibnd, nd, ns
  REAL(kind=r8) :: a0, b0, c0, an, bn, cn, t, ts, td
  REAL(kind=r8) :: qtot, tcbc
  LOGICAL :: active
  COMMON /phyparams/ qtot, tcbc, active
  EXTERNAL KAPPA_NODIFF, QEVAP_NODIFF, QRAD_NODIFF, COND_COEF
  REAL(kind=r8) :: KAPPA_NODIFF, QEVAP_NODIFF, QRAD_NODIFF, COND_COEF
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
  REAL(kind=r8) :: result3
  LOGICAL :: arg1
!
!  Coefficients at x=x0
  a0 = 0.0_R8
  b0 = 1.0_R8
  result1 = KAPPA_NODIFF(ibnd, 1, ts)
  result2 = QEVAP_NODIFF(ibnd, ns, ts)
  result3 = QRAD_NODIFF(ibnd, ts, .true.)
  c0 = 1.0_R8/result1*(-qtot+result2+result3)
!
!  Coefficients at x=xn
  an = 0.0_R8
  bn = 1.0_R8
  result1 = KAPPA_NODIFF(ibnd, nd, td)
  arg1 = .NOT.active
  result2 = COND_COEF(arg1, td)
  cn = 1.0_R8/result1*(-(result2*(td-tcbc)))
!! FIXME: Could use additionally the code below once we have a way of applying
!!        the correct emissivity
!xpb        if (.not.active)
!xpb     >    cn=cn-qrad(ibnd,Td,.false.)
!xpb     .        -qevap(ibnd,ns,Td)
!$$$ Old treatment where conductivity to coolant was assumed to be perfect
!$$$      if (active) then
!$$$        an = 1.0_R8
!$$$        bn = 0.0_R8
!$$$        cn = Tcbc
!$$$      else
!$$$        an = 0.0_R8
!$$$        bn = 1.0_R8
!$$$        cn = 1.0_R8/Kappa(ibnd,nd,Td)* !xpb we assume the contact heat conductivity of 100 W.m-2.K-1
!$$$     .   (-100.0_R8*(Td-Tcbc))
!$$$      endif
!
  RETURN
END SUBROUTINE COEFBC_NODIFF

!
!.................................................................
!                      function Kappa
!  function Kappa(T) gives the coefficient of thermal conductivity
!  at temperature T.
!
FUNCTION KAPPA_NODIFF(ibnd, i, t) RESULT (kappa)
  USE B2MOD_TYPES
  USE B2MOD_WALL
  USE B2MOD_LAYER
  IMPLICIT NONE
  INTEGER :: ibnd, i
  REAL(kind=r8) :: kappa, t
  REAL(kind=r8) :: a(5), tcels, teff
  REAL(kind=r8) :: bulk_value, coating_value
  EXTERNAL LAYER_KAPPA_NODIFF, XERRAB
  REAL(kind=r8) :: LAYER_KAPPA_NODIFF
  REAL(kind=r8) :: result1
!
! Units of Kappa are in "W/m/K"
  a(1) = matbulk_kappa_a1(ibnd)
  a(2) = matbulk_kappa_a2(ibnd)
  a(3) = matbulk_kappa_a3(ibnd)
  a(4) = matbulk_kappa_a4(ibnd)
  a(5) = matbulk_kappa_a5(ibnd)
  tcels = t - 273.15_R8
  IF (bulk_temp_unit(ibnd) .EQ. 'K') THEN
    teff = t
  ELSE IF (bulk_temp_unit(ibnd) .EQ. 'C') THEN
    teff = tcels
  ELSE
    CALL XERRAB('Unrecognized temperature units (bulk Kappa) !')
  END IF
  bulk_value = a(1) + (a(2)+(a(3)+(a(4)+a(5)*teff)*teff)*teff)*teff
  IF (coating_thickness(ibnd) .GT. 0.0_R8) THEN
    a(1) = coating_kappa_a1(ibnd)
    a(2) = coating_kappa_a2(ibnd)
    a(3) = coating_kappa_a3(ibnd)
    a(4) = coating_kappa_a4(ibnd)
    a(5) = coating_kappa_a5(ibnd)
    IF (coating_temp_unit(ibnd) .EQ. 'K') THEN
      teff = t
    ELSE IF (coating_temp_unit(ibnd) .EQ. 'C') THEN
      teff = tcels
    ELSE
      CALL XERRAB('Unrecognized temperature units (coat Kappa) !')
    END IF
    coating_value = a(1) + (a(2)+(a(3)+(a(4)+a(5)*teff)*teff)*teff)*teff
  END IF
  IF (target_depth(ibnd, i) .GE. coating_thickness(ibnd) .AND. &
&     target_depth(ibnd, i) .GE. layer_depth(ibnd)) THEN
    kappa = bulk_value
  ELSE IF (target_depth(ibnd, i) .GE. layer_depth(ibnd)) THEN
    kappa = coating_value
  ELSE
    result1 = LAYER_KAPPA_NODIFF(ibnd, t)
    kappa = result1*(layer_depth(ibnd)-target_depth(ibnd, i))/(&
&     target_depth(ibnd, i+1)-target_depth(ibnd, i))
    IF (layer_depth(ibnd) .GE. coating_thickness(ibnd)) THEN
      kappa = kappa + bulk_value*(target_depth(ibnd, i+1)-layer_depth(&
&       ibnd))/(target_depth(ibnd, i+1)-target_depth(ibnd, i))
    ELSE
      kappa = kappa + coating_value*(target_depth(ibnd, i+1)-layer_depth&
&       (ibnd))/(target_depth(ibnd, i+1)-target_depth(ibnd, i))
    END IF
  END IF
!
  RETURN
END FUNCTION KAPPA_NODIFF

