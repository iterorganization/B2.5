!  Stainless -|          |         |
!     Steel   | -14321.9 | 9.44    |
!
FUNCTION QEVAP_NODIFF(ibnd, ns, ts) RESULT (qevap)
  USE B2MOD_TYPES
  USE B2MOD_WALL
  USE B2MOD_B2CMPA_DIFF
  USE B2MOD_SPUTTER_DIFF
  USE B2MOD_SUBSYS
  IMPLICIT NONE
  INTEGER :: ibnd, ns, is, itrack
  LOGICAL, SAVE :: lfirst
  REAL(kind=r8) :: ts, tv, flxthev, qevap, vapor_temp, qthev
  REAL(kind=r8), SAVE :: therm_evap
  EXTERNAL IPGETR
  INTRINSIC MIN
  DATA lfirst /.true./
  DATA therm_evap /0.0_R8/
!
!  Ts       ->  temperature of the plate in Kelvin.
!  flxthev  ->  Flux of target atoms evaporated ( / m^2 /s )
!  qevap -> Energy lost (Joules / m^2 / s)
!
  CALL SUBINI('qevap')
  IF (lfirst) THEN
!$OMP CRITICAL (qevap_heatdiff1D)
    CALL IPGETR('b2stbr_therm_evap', therm_evap)
    CALL XERTST(0.0_R8 .LE. therm_evap .AND. 1.0_R8 .GE. therm_evap, &
&         'faulty parameter therm_evap')
!$OMP END CRITICAL (qevap_heatdiff1D)
    lfirst = .false.
  END IF
!
  DO is=0,ns-1
    itrack = track_index(is)
    IF (is_neutral(is)) THEN
!  Checking inputs
      IF (layer_depth(ibnd) .EQ. 0.0_R8 .OR. itrack .EQ. 0) THEN
        vapor_temp = matsurf_vapor_temp(ibnd)
        IF (vapor_temp .GT. ts) THEN
          tv = ts
        ELSE
          tv = vapor_temp
        END IF
      ELSE IF (itrack .GT. 0 .AND. itrack .LE. ntrack) THEN
        vapor_temp = species_vapor_temp(itrack)
        IF (vapor_temp .GT. ts) THEN
          tv = ts
        ELSE
          tv = vapor_temp
        END IF
      ELSE
        tv = ts
      END IF
      IF (ts .GT. vapor_temp) WRITE(*, '(a,1p,2e12.5)') &
&                        'Input error; Correct so that Ts <= Vapor_temp'&
&                             , ts, vapor_temp
      CALL THERMEV(ibnd, is, tv, flxthev, qthev)
      qevap = qthev*therm_evap
    END IF
  END DO
!
  CALL SUBEND()
  RETURN
END FUNCTION QEVAP_NODIFF

