! Program to solve the heat diffusion equation
!
!  Last modified by Manoj Warrier on (08-02-2004)
!
! Using the numerical method written by M. Kundu to
! SOLVE PARABOLIC EQUATIONS USING THE METHOD OF LINES
!
!.....................................................................
!
! This is the subroutine which solves for a heat diffusion equation
! of the form:
!              doe T         doe^2 T     doe D   doe T
!             ------- =  D  --------- + ------- ------- + sources/sinks
!              doe t         doe^2 x     doe x   doe x
! T is the temperature, t the time variable, x the space variable and
! D the heat diffusion coefficient.
!
! i.e., ....
!       DU = (1/rho/Cp) * (K*UXX + gradK*UX + S)
!       DU is the rate of change of temperature
!       UXX is "doe^2 T / doe^2 x"   and UX is "doe T / doe x"
! with Neumann bondary condition at the plasma facing surface side
! i.e.  UX is specified.
! and a Dirichlet type boundary condition at the coolant side.
! i.e. T is specified.
!
! Inputs:
!  Q = incident total heat flux (Watts/m^2/sec).
!  Tcbc = Temperature at the cooling end (K).
!  dt = Time step of simulation (sec)
!
! Outputs:
!  Tp = Target surface temperature (K).
SUBROUTINE HEATDIFF1D_NODIFF(ibnd, ns0, q, dt, tp, output)
  USE B2MOD_TYPES
  USE B2MOD_WALL
  IMPLICIT NONE
  EXTERNAL LINES2C
!
  INTEGER :: i
  INTEGER :: ibnd, ns0, iwall, output, ns
!
  LOGICAL :: active
  REAL(kind=r8) :: q, dt, qtot, tcbc
  REAL(kind=r8) :: tp(ndepth_nml)
  REAL(kind=r8) :: u(7*ndepth_nml), u1(7*ndepth_nml)
  REAL(kind=r8) :: x(ndepth)
  REAL(kind=r8) :: x0, xn, t0, tn, dx(ndepth-1)
!
  INTEGER :: ier, iflg, iopt, neq, nmax, nstep
  REAL(kind=r8) :: hstep, rerr
  REAL(kind=r8) :: du(7*ndepth_nml), yf(ndepth_nml), ut(ndepth_nml)
  REAL(kind=r8) :: wk(3*ndepth_nml+ndepth_nml*2*ndepth_nml)
  INTEGER :: iwk(ndepth_nml)
  EXTERNAL MSTEP
  INTEGER :: ncall
  COMMON /zzline/ dx, x0, xn, x
  COMMON /phyparams/ qtot, tcbc, active
  COMMON /location/ iwall, ns
!
  INTEGER, SAVE :: npde, nw, niw
  INTEGER, ALLOCATABLE, SAVE :: iw(:)
  REAL(kind=r8), ALLOCATABLE, SAVE :: u_nag(:, :), w(:)
  EXTERNAL XERTST
  INTRINSIC MIN
  DATA ncall /0/
!
  CALL SUBINI('heatdiff1D')
  IF (ncall .EQ. 0) THEN
    ncall = ncall + 1
    npde = 1
    nw = (10+6*npde)*npde*ndepth_nml + (21+3*npde)*npde + 7*ndepth_nml +&
&     56
    niw = npde*ndepth_nml + 25
    ALLOCATE(u_nag(npde, ndepth_nml))
    ALLOCATE(iw(niw))
    ALLOCATE(w(nw))
  END IF
!
  IF (output .GE. 10) THEN
    WRITE(*, *) 'Wall element number =', ibnd
    WRITE(*, *) 'Total energy flux =', q, 'Joules/m^2/s'
    WRITE(*, *) 'Temp at the cold end =', backplate_temp(ibnd), 'K'
    WRITE(*, *) 'Temp at the warm end =', tp(1), 'K'
    WRITE(*, *) 'Thickness of the plate =', plate_thickness(ibnd), 'm'
    IF (inertial_cooling(ibnd)) THEN
      WRITE(*, *) 'Plate element is inertially cooled'
    ELSE
      WRITE(*, *) 'Plate element is actively cooled'
    END IF
    WRITE(*, *) 'Time step of simulation =', dt, 'sec'
  END IF
!
  qtot = q
  tcbc = backplate_temp(ibnd)
  active = .NOT.inertial_cooling(ibnd)
  t0 = 0.0_R8
  tn = dt
  iwall = ibnd
  ns = ns0
!
!  Compute positions and intervals
  x(1:ndepth_nml) = target_depth(ibnd, 1:ndepth_nml)
  x0 = target_depth(ibnd, 1)
  xn = target_depth(ibnd, ndepth_nml)
  DO i=1,ndepth_nml-1
    dx(i) = x(i+1) - x(i)
  END DO
!
! Checking if it is the first call and if it is, call fic(x,u), else
! use previous Tp profile as initial guess.
  IF (dt .LE. 0.0_R8) THEN
!       Calling subroutine fic(ibnd,x,u) which supplies the
!       initial condition u(x) i.e.. the temperature "u" as a
!       function of the position "x" at ndepth_nml grid points.
    CALL FIC(ibnd, x, u)
    DO i=1,ndepth_nml
      tp(i) = u(i)
    END DO
    CALL SUBEND()
    RETURN
  ELSE
    DO i=1,ndepth_nml-1
      u1(i) = tp(i)
    END DO
!
! No. of equations to be solved.
! since we specify T at one end and grad(T) at the other
    neq = ndepth_nml - 1
    iflg = 0
    iopt = 1
    hstep = dt
    nmax = 100000000
    rerr = 1.0e-8_R8
    CALL MSTEP(ibnd, ns, neq, u1, ut, du, hstep, t0, tn, yf, rerr&
&               , nstep, nmax, ier, iflg, iopt, wk, iwk)
    CALL XERTST(ier .EQ. 0, 'ier.ne.0 in heatdiff1D')
    IF (output .GE. 10) WRITE(6, *) 'No. of successful steps = ', nstep
    tp(ndepth_nml) = tcbc
    DO i=1,ndepth_nml-1
      IF (yf(i) .GT. matsurf_vapor_temp(ibnd)) THEN
        tp(i) = matsurf_vapor_temp(ibnd)
      ELSE
        tp(i) = yf(i)
      END IF
    END DO
!
    CALL SUBEND()
    RETURN
  END IF
