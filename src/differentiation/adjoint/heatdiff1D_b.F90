    teff = tcels
  ELSE
    CALL XERRAB('Unrecognized temperature units (bulk dKappadT) !')
  END IF
  bulk_value = a(1) + (2.0_R8*a(2)+(3.0_R8*a(3)+4.0_R8*a(4)*teff)*teff)*&
&   teff
  IF (coating_thickness(ibnd) .GT. 0.0_R8) THEN
    a(1) = coating_kappa_perp_a2(ibnd)
    a(2) = coating_kappa_perp_a3(ibnd)
    a(3) = coating_kappa_perp_a4(ibnd)
    a(4) = coating_kappa_perp_a5(ibnd)
    IF (coating_temp_unit(ibnd) .EQ. 'K') THEN
      teff = t
    ELSE IF (coating_temp_unit(ibnd) .EQ. 'C') THEN
      teff = tcels
    ELSE
      CALL XERRAB('Unrecognized temperature units (coat dKappadT) !')
    END IF
    coating_value = a(1) + (2.0_R8*a(2)+(3.0_R8*a(3)+4.0_R8*a(4)*teff)*&
&     teff)*teff
  END IF
  IF (target_depth(ibnd, i) .GE. coating_thickness(ibnd) .AND. &
&     target_depth(ibnd, i) .GE. layer_depth(ibnd)) THEN
    dkappa_perpbydt = bulk_value
  ELSE IF (target_depth(ibnd, i) .GE. layer_depth(ibnd)) THEN
    dkappa_perpbydt = coating_value
  ELSE
!xpb  The layer is expected to be amorphous and therefore not to support
!xpb  the distinction between perp and parallel directions
    result1 = DLAYER_KAPPABYDT_NODIFF(ibnd, t)
    dkappa_perpbydt = result1*(layer_depth(ibnd)-target_depth(ibnd, i))/&
&     (target_depth(ibnd, i+1)-target_depth(ibnd, i))
    IF (layer_depth(ibnd) .GE. coating_thickness(ibnd)) THEN
      dkappa_perpbydt = dkappa_perpbydt + bulk_value*(target_depth(ibnd&
&       , i+1)-layer_depth(ibnd))/(target_depth(ibnd, i+1)-target_depth(&
&       ibnd, i))
    ELSE
      dkappa_perpbydt = dkappa_perpbydt + coating_value*(target_depth(&
&       ibnd, i+1)-layer_depth(ibnd))/(target_depth(ibnd, i+1)-&
&       target_depth(ibnd, i))
    END IF
  END IF
!
  RETURN
END FUNCTION DKAPPA_PERPBYDT_NODIFF

!
!....................................................................
!                      function Cp
!  function Cp(T) gives the specific heat capacity at const pressure
!  at temperature T.
!
FUNCTION CP_NODIFF(ibnd, i, t) RESULT (cp)
  USE B2MOD_TYPES
  USE B2MOD_WALL
  USE B2MOD_LAYER
  IMPLICIT NONE
  INTEGER :: ibnd, i
  REAL(kind=r8) :: cp, t
  INTEGER :: mf, cf
  REAL(kind=r8) :: b(4), tcels, teff
  REAL(kind=r8) :: cp_bulk, cp_coating
  EXTERNAL LAYER_CP_NODIFF, XERRAB
  REAL(kind=r8) :: LAYER_CP_NODIFF
  REAL(kind=r8) :: result1
!
  tcels = t - 273.15_R8
  b(1) = matbulk_cp_b1(ibnd)
  b(2) = matbulk_cp_b2(ibnd)
  b(3) = matbulk_cp_b3(ibnd)
  b(4) = matbulk_cp_b4(ibnd)
  mf = matbulk_format(ibnd)
  IF (bulk_temp_unit(ibnd) .EQ. 'K') THEN
    teff = t
  ELSE IF (bulk_temp_unit(ibnd) .EQ. 'C') THEN
    teff = tcels
  ELSE
    CALL XERRAB('Unrecognized temperature units (bulk Cp) !')
  END IF
  IF (mf .EQ. 1) THEN
    cp_bulk = b(1) + (b(2)+(b(3)+b(4)*teff)*teff)*teff
  ELSE
    cp_bulk = b(1) + b(2)*teff - b(3)/teff**2
  END IF
!
  IF (coating_thickness(ibnd) .GT. 0.0_R8) THEN
    b(1) = coating_cp_b1(ibnd)
    b(2) = coating_cp_b2(ibnd)
    b(3) = coating_cp_b3(ibnd)
    b(4) = coating_cp_b4(ibnd)
    cf = coating_format(ibnd)
    IF (bulk_temp_unit(ibnd) .EQ. 'K') THEN
      teff = t
    ELSE IF (bulk_temp_unit(ibnd) .EQ. 'C') THEN
      teff = tcels
    ELSE
      CALL XERRAB('Unrecognized temperature units (coat Cp) !')
    END IF
    IF (cf .EQ. 1) THEN
      cp_coating = b(1) + (b(2)+(b(3)+b(4)*teff)*teff)*teff
    ELSE
      cp_coating = b(1) + b(2)*teff - b(3)/teff**2
    END IF
  END IF
  IF (target_depth(ibnd, i) .GE. coating_thickness(ibnd) .AND. &
&     target_depth(ibnd, i) .GE. layer_depth(ibnd)) THEN
    cp = cp_bulk
  ELSE IF (target_depth(ibnd, i) .GE. layer_depth(ibnd)) THEN
    cp = cp_coating
  ELSE
    result1 = LAYER_CP_NODIFF(ibnd, t)
    cp = result1*(layer_depth(ibnd)-target_depth(ibnd, i))/(target_depth&
&     (ibnd, i+1)-target_depth(ibnd, i))
    IF (layer_depth(ibnd) .GE. coating_thickness(ibnd)) THEN
      cp = cp + cp_bulk*(target_depth(ibnd, i+1)-layer_depth(ibnd))/(&
&       target_depth(ibnd, i+1)-target_depth(ibnd, i))
    ELSE
      cp = cp + cp_coating*(target_depth(ibnd, i+1)-layer_depth(ibnd))/(&
&       target_depth(ibnd, i+1)-target_depth(ibnd, i))
    END IF
  END IF
!
  RETURN
END FUNCTION CP_NODIFF

!
! Getting the heat loss due to evaporation and radiation from surface.
!....................................................................
FUNCTION QRAD_NODIFF(ibnd, ts, top_layer) RESULT (qrad)
  USE B2MOD_TYPES
  USE B2MOD_CONSTANTS
  USE B2MOD_WALL
  IMPLICIT NONE
  INTEGER :: ibnd
!     -------------------------------------------------------------------------
!     constants used in emissivity.f
!     -------------------------------------------------------------------------
!     03/21/97 bhs
!     09/12/07 xpb : source CODATA 2006 (http://www.nist.gov/)
!     19/03/18 xpb : source CODATA 2014 (http://www.nist.gov/)
!     03/06/19 xpb : source CODATA 2018 (http://www.nist.gov/)
!     -------------------------------------------------------------------------
!
# ifndef CONSTANTS_PROVIDED
  REAL(kind=r8) :: const_h
  PARAMETER (const_h=6.62607015e-34_R8)
# endif
  REAL(kind=r8) :: ts, tw, sbconst, qrad
  LOGICAL :: top_layer
  PARAMETER (sbconst=pi**5*(4.0_R8/15.0_R8)*(kbolt/const_h)**3*kbolt/c**&
&   2 )
  EXTERNAL LAYER_EMISSIVITY_NODIFF
  REAL(kind=r8) :: LAYER_EMISSIVITY_NODIFF
  REAL(kind=r8) :: result1
!
! Temperature of surrounding environment
  tw = backplate_temp(ibnd)
  IF (layer_depth(ibnd) .EQ. 0.0_R8 .OR. (.NOT.top_layer)) THEN
    qrad = matsurf_blackbody_emiss(ibnd)*sbconst*(ts**4-tw**4)
  ELSE
    result1 = LAYER_EMISSIVITY_NODIFF(ibnd)
    qrad = result1*sbconst*(ts**4-tw**4)
  END IF
!
  RETURN
END FUNCTION QRAD_NODIFF

!
!....................................................................
!   Ref: 1) C. Garcia Rosales, Erosion processes in plasma-wall
!           interactions, J. Nucl. Mater. 211 (1994) Pg.202-214.
!        2) R.A. Langley, Evaporation, Data Compendium for Plasma
!           Surface Interactions, Special Issue, NUCLEAR FUSION,
!           IAEA, Vienna, (1984) 55-61.
!  Table of A and B for some first wall materials in Fusion research:
!  (Ref (2) above).
!  (note that A and B are provided such that the vappres is in Pascals).
!  element    |  A       |  B      |
!  Aluminum   | -16165   | 10.91   |
!  Beryllium  | -16720   | 11.61   |
!  Graphite   | -40181   | 14.80   |
!  Copper     | -17079   | 11.26   |
!  Molybdenum | -33069   | 12.02   |
!  Titanium   | -23340   | 9.79    |
!  Tungsten   | -44485   | 12.74   |
!  Titanium - |          |         |
!     Carbide | -39117.9 | 15.23   |
!  Stainless -|          |         |
!     Steel   | -14321.9 | 9.44    |
!
FUNCTION QEVAP_NODIFF(ibnd, ns, ts) RESULT (qevap)
  USE B2MOD_TYPES
  USE B2MOD_WALL
  USE B2MOD_B2CMPA_DIFF
  USE B2MOD_SPUTTER_DIFF
  USE B2MOD_SUBSYS
  IMPLICIT NONE
  INTEGER :: ibnd, ns, is, itrack
  LOGICAL, SAVE :: lfirst
  REAL(kind=r8) :: ts, tv, flxthev, qevap, vapor_temp, qthev
  REAL(kind=r8), SAVE :: therm_evap
  EXTERNAL IPGETR
  INTRINSIC MIN
  DATA lfirst /.true./
  DATA therm_evap /0.0_R8/
!
!  Ts       ->  temperature of the plate in Kelvin.
!  flxthev  ->  Flux of target atoms evaporated ( / m^2 /s )
!  qevap -> Energy lost (Joules / m^2 / s)
!
  CALL SUBINI('qevap')
  IF (lfirst) THEN
!$OMP CRITICAL (qevap_heatdiff1D)
    CALL IPGETR('b2stbr_therm_evap', therm_evap)
    CALL XERTST(0.0_R8 .LE. therm_evap .AND. 1.0_R8 .GE. therm_evap, &
&         'faulty parameter therm_evap')
!$OMP END CRITICAL (qevap_heatdiff1D)
    lfirst = .false.
  END IF
!
  DO is=0,ns-1
    itrack = track_index(is)
    IF (is_neutral(is)) THEN
!  Checking inputs
      IF (layer_depth(ibnd) .EQ. 0.0_R8 .OR. itrack .EQ. 0) THEN
        vapor_temp = matsurf_vapor_temp(ibnd)
        IF (vapor_temp .GT. ts) THEN
          tv = ts
        ELSE
          tv = vapor_temp
        END IF
      ELSE IF (itrack .GT. 0 .AND. itrack .LE. ntrack) THEN
        vapor_temp = species_vapor_temp(itrack)
        IF (vapor_temp .GT. ts) THEN
          tv = ts
        ELSE
          tv = vapor_temp
        END IF
      ELSE
        tv = ts
      END IF
      IF (ts .GT. vapor_temp) WRITE(*, '(a,1p,2e12.5)') &
&                        'Input error; Correct so that Ts <= Vapor_temp'&
&                             , ts, vapor_temp
      CALL THERMEV(ibnd, is, tv, flxthev, qthev)
      qevap = qthev*therm_evap
    END IF
  END DO
!
  CALL SUBEND()
  RETURN
END FUNCTION QEVAP_NODIFF

