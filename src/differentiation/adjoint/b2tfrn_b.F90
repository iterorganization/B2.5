      CALL POPREAL8ARRAY(floi_eir, r8*nfc*2/8)
      CALL POPREAL8ARRAY(floe_eir(:, 1), r8*nfc/8)
      CALL POPREAL8ARRAY(floe_eir(:, 0), r8*nfc/8)
      CALL POPREAL8ARRAY(fle_eir(:, 1), r8*nfc/8)
      CALL POPREAL8ARRAY(fle_eir(:, 0), r8*nfc/8)
    END IF
  END DO
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
    CALL POPREAL8ARRAY(wrk0, r8*nfc/8)
    CALL POPREAL8ARRAY(wrkvx, r8*nvx/8)
  END IF
  wrk00b = 0.D0
  scurb = 0.D0
  wrk0b = 0.D0
  wrk1b = 0.D0
  wrk2b = 0.D0
  nbfb = 0.D0
  flo_eirb = 0.D0
  dpbb = 0.D0
  wrkvxb = 0.D0
  DO is=ns-1,0,-1
    CALL POPREAL8ARRAY(dv%fna_eir(:, 0, is), r8*SIZE(dv%fna_eir, 1)/8)
    wrk1b = wrk1b - (dpb(:, 0)-wrk2)*dvb%fna_eir(:, 0, is)
    dpbb(:, 0) = dpbb(:, 0) - wrk1*dvb%fna_eir(:, 0, is)
    wrk2b = wrk2b + wrk1*dvb%fna_eir(:, 0, is)
    dpbcb = 0.D0
    DO ifc=nfc,1,-1
      CALL POPREAL8(wrk2(ifc), r8/8)
      tempb4 = geo%fcqgam(ifc, 0)*0.5_R8*(geo%fchc(ifc, 1)+geo%fchc(ifc&
&       , 2))*wrk2b(ifc)
      wrk2b(ifc) = 0.D0
      dpbcb(mpg%fccv(ifc, 1)) = dpbcb(mpg%fccv(ifc, 1)) + tempb4
      dpbcb(mpg%fccv(ifc, 2)) = dpbcb(mpg%fccv(ifc, 2)) + tempb4
      CALL POPREAL8(wrk1(ifc), r8/8)
      tempb3 = switch%b2npco_pcm1*0.5_R8*geo%fcpbs(ifc)*wrk1b(ifc)/(geo%&
&       fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2)))
      wrk1b(ifc) = 0.D0
      dvb%pcca(mpg%fccv(ifc, 1), 1, is) = dvb%pcca(mpg%fccv(ifc, 1), 1, &
&       is) + pl%na(mpg%fccv(ifc, 1), is)*tempb3
      plb%na(mpg%fccv(ifc, 1), is) = plb%na(mpg%fccv(ifc, 1), is) + dv%&
&       pcca(mpg%fccv(ifc, 1), 1, is)*tempb3
      dvb%pcca(mpg%fccv(ifc, 2), 1, is) = dvb%pcca(mpg%fccv(ifc, 2), 1, &
&       is) + pl%na(mpg%fccv(ifc, 2), is)*tempb3
      plb%na(mpg%fccv(ifc, 2), is) = plb%na(mpg%fccv(ifc, 2), is) + dv%&
&       pcca(mpg%fccv(ifc, 2), 1, is)*tempb3
    END DO
    CALL POPREAL8ARRAY(dv%fna_eir(:, :, is), r8*SIZE(dv%fna_eir, 1)*SIZE&
&                (dv%fna_eir, 2)/8)
    cob%cdpa(:, :, is) = cob%cdpa(:, :, is) - dpb*dvb%fna_eir(:, :, is)
    dpbb = dpbb - co%cdpa(:, :, is)*dvb%fna_eir(:, :, is)
    CALL POPREAL8ARRAY(dv%fna_eir(:, 1, is), r8*SIZE(dv%fna_eir, 1)/8)
    tempb2 = (1.0_R8-switch%fnb_drift_hyb)*dvb%fna_eir(:, 1, is)
    tempb = geo%fcs*geo%fcqalf(:, 1)*nbf*tempb2
    scurb(:, 1) = scurb(:, 1) + nbf*tempb2
    nbfb = nbfb + (geo%fcs*geo%fcqalf(:, 1)*(switch%xvecrb*dv%vaecrb(:, &
&     1, is)+switch%xwdia*dv%wadia(:, 1, is))+scur(:, 1))*tempb2
    dvb%vaecrb(:, 1, is) = dvb%vaecrb(:, 1, is) + switch%xvecrb*tempb
    dvb%wadia(:, 1, is) = dvb%wadia(:, 1, is) + switch%xwdia*tempb
    CALL POPREAL8ARRAY(dv%fna_eir(:, 0, is), r8*SIZE(dv%fna_eir, 1)/8)
    tempb2 = (1.0_R8-switch%fnb_drift_hyb)*dvb%fna_eir(:, 0, is)
    tempb = geo%fcs*geo%fcqalf(:, 0)*nbf*tempb2
    scurb(:, 0) = scurb(:, 0) + nbf*tempb2
    nbfb = nbfb + (geo%fcs*geo%fcqalf(:, 0)*(switch%xvecrb*dv%vaecrb(:, &
&     0, is)+switch%xwdia*dv%wadia(:, 0, is))+scur(:, 0))*tempb2
    dvb%vaecrb(:, 0, is) = dvb%vaecrb(:, 0, is) + switch%xvecrb*tempb
    dvb%wadia(:, 0, is) = dvb%wadia(:, 0, is) + switch%xwdia*tempb
    wrk11b = 0.D0
    CALL CALCFLOW_BWD(ncv, nfc, nvx, meth, geo, geob, mpg, mpgb, pl%na(:&
&               , is), plb%na(:, is), flo_eir, flo_eirb, co%cdna(:, :, &
&               is), cob%cdna(:, :, is), dv%fna_eir(:, :, is), dvb%&
&               fna_eir(:, :, is), wrk00, wrk00b, wrk11, wrk11b)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      CALL POPREAL8ARRAY(flo_eir, r8*nfc*2/8)
      scurb = scurb + switch%fnb_drift_hyb*flo_eirb
      CALL POPREAL8ARRAY(scur(:, 1), r8*nfc/8)
      tempb1 = switch%b2tfnb_ycur*scurb(:, 1)/(qe*nbf)
      tempb0 = scurb(:, 1)/(qe*nbf)
      scurb(:, 1) = 0.D0
      dvb%fchviskt(:, 1) = dvb%fchviskt(:, 1) + tempb0
      nbfb = nbfb - dv%fchviskt(:, 1)*tempb0/nbf - (dv%fchinert(:, 1)+dv&
&       %fchvispar(:, 1)+switch%fnb_vis_per*dv%fchvisper(:, 1)+dv%&
&       fchanml(:, 1)+switch%fnb_vis_q*dv%fchvisq(:, 1))*tempb1/nbf
      dvb%fchinert(:, 1) = dvb%fchinert(:, 1) + tempb1
      dvb%fchvispar(:, 1) = dvb%fchvispar(:, 1) + tempb1
      dvb%fchvisper(:, 1) = dvb%fchvisper(:, 1) + switch%fnb_vis_per*&
&       tempb1
      dvb%fchanml(:, 1) = dvb%fchanml(:, 1) + tempb1
      dvb%fchvisq(:, 1) = dvb%fchvisq(:, 1) + switch%fnb_vis_q*tempb1
      CALL POPREAL8ARRAY(scur(:, 0), r8*nfc/8)
      tempb0 = switch%b2tfnb_xcur*scurb(:, 0)/(qe*nbf)
      tempb1 = scurb(:, 0)/(qe*nbf)
      scurb(:, 0) = 0.D0
      dvb%fchviskt(:, 0) = dvb%fchviskt(:, 0) + tempb1
      nbfb = nbfb - dv%fchviskt(:, 0)*tempb1/nbf - (dv%fchinert(:, 0)+dv&
&       %fchvispar(:, 0)+switch%fnb_vis_per*dv%fchvisper(:, 0)+dv%&
&       fchanml(:, 0)+switch%fnb_vis_q*dv%fchvisq(:, 0))*tempb0/nbf
      dvb%fchinert(:, 0) = dvb%fchinert(:, 0) + tempb0
      dvb%fchvispar(:, 0) = dvb%fchvispar(:, 0) + tempb0
      dvb%fchvisper(:, 0) = dvb%fchvisper(:, 0) + switch%fnb_vis_per*&
&       tempb0
      dvb%fchanml(:, 0) = dvb%fchanml(:, 0) + tempb0
      dvb%fchvisq(:, 0) = dvb%fchvisq(:, 0) + switch%fnb_vis_q*tempb0
    END IF
    CALL POPREAL8ARRAY(flo_eir(:, 1), r8*nfc/8)
    cob%cvla(:, 1, is) = cob%cvla(:, 1, is) + flo_eirb(:, 1)
    tempb = switch%fnb_drift_hyb*geo%fcs*geo%fcqalf(:, 1)*flo_eirb(:, 1)
    flo_eirb(:, 1) = 0.D0
    dvb%vaecrb(:, 1, is) = dvb%vaecrb(:, 1, is) + switch%xvecrb*tempb
    dvb%wadia(:, 1, is) = dvb%wadia(:, 1, is) + switch%xwdia*tempb
    CALL POPREAL8ARRAY(flo_eir(:, 0), r8*nfc/8)
    wrk0b = wrk0b + geo%fcpbs*flo_eirb(:, 0)
    cob%cvla(:, 0, is) = cob%cvla(:, 0, is) + flo_eirb(:, 0)
    tempb = switch%fnb_drift_hyb*geo%fcs*geo%fcqalf(:, 0)*flo_eirb(:, 0)
    flo_eirb(:, 0) = 0.D0
    dvb%vaecrb(:, 0, is) = dvb%vaecrb(:, 0, is) + switch%xvecrb*tempb
    dvb%wadia(:, 0, is) = dvb%wadia(:, 0, is) + switch%xwdia*tempb
    CALL INTFACE_BWD(ncv, nfc, mpg%fccv, geo%fcvol, pl%ua(:, is), plb%ua&
&              (:, is), wrk0, wrk0b)
    CALL GRADC_P_BWD(ncv, nfc, nvx, 1, geo, geob, mpg, mpgb, dv%pa(:, is&
&              ), dvb%pa(:, is), wrkvx, wrkvxb, dpbc, dpbcb)
    CALL DIFF_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, dv%pa(:, is), &
&           dvb%pa(:, is), wrkvx, wrkvxb, dpb, dpbb)
    CALL INTFACE_BWD(ncv, nfc, mpg%fccv, weight, pl%na(:, is), plb%na(:&
&              , is), nbf, nbfb)
    CALL POPREAL8ARRAY(weight, r8*nfc*2/8)
    CALL B2XPPB_BWD(ncv, rt%rza(:, is), rtb%rza(:, is), pl%na(:, is), &
&             plb%na(:, is), pl%te, plb%te, pl%ti, plb%ti, pl%tn, plb%tn&
&             , is, dv%pa(:, is), dvb%pa(:, is))
    dvb%pa(:, is) = 0.D0
  END DO
END SUBROUTINE B2TFRN_B

