!wdk   still to be checked: vis_per and vis_q also in b2tfnb?
    IF (NINT(zn(is)) .EQ. 1 .AND. NINT(zamax(is)) .EQ. 1) THEN
      scur(:, 0) = (dv%fchinert(:, 0)+dv%fchvispar(:, 0)+switch%&
&       fnb_vis_per*dv%fchvisper(:, 0)+dv%fchanml(:, 0)+switch%fnb_vis_q&
&       *dv%fchvisq(:, 0))*switch%b2tfnb_xcur/(qe*nbf) + dv%fchviskt(:, &
&       0)/(qe*nbf)
      scur(:, 1) = (dv%fchinert(:, 1)+dv%fchvispar(:, 1)+switch%&
&       fnb_vis_per*dv%fchvisper(:, 1)+dv%fchanml(:, 1)+switch%fnb_vis_q&
&       *dv%fchvisq(:, 1))*switch%b2tfnb_ycur/(qe*nbf) + dv%fchviskt(:, &
&       1)/(qe*nbf)
      flo_eir = flo_eir + switch%fnb_drift_hyb*scur
    END IF
!
!   ..apply discretization scheme for density-part of fna_eir
    CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%na(:, is), &
&                  flo_eir, co%cdna(:, :, is), dv%fna_eir(:, :, is), &
&                  wrk00, wrk11)
!
!   ..add non-hybrid drift contribution
    dv%fna_eir(:, 0, is) = dv%fna_eir(:, 0, is) + (1.0_R8-switch%&
&     fnb_drift_hyb)*((switch%xvecrb*dv%vaecrb(:, 0, is)+switch%xwdia*dv&
&     %wadia(:, 0, is))*geo%fcs*geo%fcqalf(:, 0)+scur(:, 0))*nbf
    dv%fna_eir(:, 1, is) = dv%fna_eir(:, 1, is) + (1.0_R8-switch%&
&     fnb_drift_hyb)*((switch%xvecrb*dv%vaecrb(:, 1, is)+switch%xwdia*dv&
&     %wadia(:, 1, is))*geo%fcs*geo%fcqalf(:, 1)+scur(:, 1))*nbf
!
!   ..add contribution from pressure diffusion
    dv%fna_eir(:, :, is) = dv%fna_eir(:, :, is) - co%cdpa(:, :, is)*dpb
!
!   ..add Rhie-Chow term
    DO ifc=1,nfc
      wrk1(ifc) = switch%b2npco_pcm1*0.5_R8*geo%fcpbs(ifc)*(dv%pcca(mpg%&
&       fccv(ifc, 1), 1, is)*pl%na(mpg%fccv(ifc, 1), is)+dv%pcca(mpg%&
&       fccv(ifc, 2), 1, is)*pl%na(mpg%fccv(ifc, 2), is))/(geo%fcqgam(&
&       ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2)))
      wrk2(ifc) = 0.5_R8*geo%fcqgam(ifc, 0)*(dpbc(mpg%fccv(ifc, 1))+dpbc&
&       (mpg%fccv(ifc, 2)))*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
    END DO
    dv%fna_eir(:, 0, is) = dv%fna_eir(:, 0, is) - wrk1*(dpb(:, 0)-wrk2)
  END DO
!
!is
!
! Drift terms
  result1 = MAXVAL(dv%facdrift)
  result2 = MAXVAL(dv%fac_exb)
  IF (result1 .NE. 0.0_R8 .OR. result2 .NE. 0.0_R8) THEN
!
!     ..computation of diamagnetic drift
!wdk    Note: use implementation consistent with vedia (b2tfed) and
!wdk          wadia (b2tfnb)
!wdk          (original code: cell center approach)
    wrkcv = dv%ne*pl%te
    CALL GRAD_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrkcv, wrkvx, dnete)
    wrk0 = geo%fcbb(:, 2)/geo%fcbb(:, 3)**2
!wdk    to be checked: now we use inverse volume weighting in wkr0
    CALL INTVERTEX_NODIFF(ncv, nvx, mpg, geo%vxvol, dv%ne, wrkvx)
    CALL INTFACE(nvx, nfc, mpg%fcvx, weight, wrkvx, wrk1)
    dv%wedia(:, 0) = wrk0*dnete(:, 1)/(wrk1*qe)
    dv%wedia(:, 1) = -(wrk0*dnete(:, 0)/(wrk1*qe))
  ELSE
    dv%wedia = 0.0_R8
  END IF
!
!   ..compute wrk0
  CALL INTFACE(ncv, nfc, mpg%fccv, geo%fcvol, dv%ue, wrk0)
!
  IF (switch%b2sigp_style .EQ. 2) THEN
    CALL B2XZEF_NODIFF(ncv, ns, rt%rz2, pl%na, dv%ne, zeff, st_ext)
    DO icv=1,ncv
      result10 = FCE2N(zeff(icv))
      c071(icv) = zeff(icv)*result10
    END DO
  ELSE
!srv 13.01.17
    c071 = 0.71e0_R8
  END IF
!
!wdk  interpolate to cell faces!
  CALL INTFACE(ncv, nfc, mpg%fccv, geo%fcvol, c071, c071f)
!
! ..compute fluxes
!wdk  * why the species loop + check for zamax = 1 here?
!wdk    should we not compute fne_eir as sum(fna_eir,is) - fch?
!wdk    (in that case, fch including full diamagnetic current?)
!wdk  * adapted check: added zn(is).eq.1 to avoid double counting
  DO is=0,ns-1
    IF (NINT(zn(is)) .EQ. 1 .AND. NINT(zamax(is)) .EQ. 1) THEN
!
!   ..compute convective coefficients (poloidal and radial)
      fle_eir(:, 0) = geo%fcpbs*wrk0 + (switch%xvecrb*dv%veecrb(:, 0)+&
&       switch%xwdia*dv%wedia(:, 0))*geo%fcs*geo%fcqalf(:, 0)
      fle_eir(:, 1) = (switch%xvecrb*dv%veecrb(:, 1)+switch%xwdia*dv%&
&       wedia(:, 1))*geo%fcs*geo%fcqalf(:, 1)
!
!   ..apply discretization scheme
      CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, dv%ne, fle_eir&
&                    , co%cdna(:, :, is), dv%fne_eir, wrk00, wrk11)
!
!   ..compute convective coefficients electron heat flux
