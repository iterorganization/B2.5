        sum = 0.0_R8
        DO i=1,matsurf_nconstituents(ibnd)
          sum = sum + matsurf_nrelconstituents(ibnd, i)
        END DO
        DO i=1,matsurf_nconstituents(ibnd)
          matsurf_relconstituents(ibnd, i) = matsurf_nrelconstituents(&
&           ibnd, i)/sum
        END DO
