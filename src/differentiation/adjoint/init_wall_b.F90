! sput_mixed_alpha (default 1)
  sput_frac(0) = sput_mixed_alpha
! the remaining constituents each have an unnormalised weight equal
! to the number of monolayers deposited raised to the power
! sput_mixed_beta (default 1)
  DO itrack=1,ntargsp
    sput_frac(0) = sput_frac(0) + monolayer_deposition(ibnd, itrack)**&
&     sput_mixed_beta
  END DO
! the remaining constituents are then normalised by the sum of weights
  DO itrack=1,ntargsp
    sput_frac(itrack) = monolayer_deposition(ibnd, itrack)**&
&     sput_mixed_beta/sput_frac(0)
  END DO
