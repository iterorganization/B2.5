SUBROUTINE COMPOSE_LAYER_NODIFF(ibnd, sput_frac)
  USE B2MOD_TYPES
  USE B2MOD_WALL_DIFF
  USE B2MOD_LAYER_DIFF
  USE B2MOD_B2CMPA_DIFF
  USE B2MOD_SPUTTER_DIFF
  IMPLICIT NONE
  INTEGER :: ibnd
  REAL(kind=r8) :: sput_frac(0:ntargsp)
!
  INTEGER :: j, itrack, nz
  LOGICAL :: found
  EXTERNAL MASS_DENSITY_NODIFF
  REAL(kind=r8) :: MASS_DENSITY_NODIFF
  REAL(kind=r8) :: result1
!
  particle_inventory(ibnd, :) = 0.0_R8
!xpb  Compute layer composition
!xpb  This will need refinement including alloy formation
  nz = matsurf_nconstituents(ibnd)
!xpb  Contribution from the bulk
  layer_composition(ibnd, 1:nz) = matsurf_relconstituents(ibnd, 1:nz)*&
&   sput_frac(0)
  IF (layer_nconstituents(ibnd) .EQ. nz) THEN
    RETURN
  ELSE
!xpb  Inventory of bulk or coating particles
    IF (coating_thickness(ibnd) .GT. 0.0_R8 .AND. target_depth(ibnd, 1) &
&       .LT. coating_thickness(ibnd)) THEN
!xpb  Coating case
      IF (itrack_0(ibnd) .NE. 0) THEN
        result1 = MASS_DENSITY_NODIFF(ibnd, 2)
        particle_inventory(ibnd, itrack_0(ibnd)) = (coating_thickness(&
&         ibnd)-layer_depth(ibnd))*plate_area(ibnd)*result1/(atomic_mass&
&         (matsurf_nzconstituents(ibnd, 1))*mp)
      END IF
    ELSE IF (itrack_0(ibnd) .NE. 0) THEN
!xpb  Deep erosion or no coating case
      result1 = MASS_DENSITY_NODIFF(ibnd, 2)
      particle_inventory(ibnd, itrack_0(ibnd)) = (plate_thickness(ibnd)-&
&       layer_depth(ibnd))*plate_area(ibnd)*result1/(atomic_mass(&
&       matsurf_nzconstituents(ibnd, 1))*mp)
    END IF
!xpb  Additional contribution from deposited material
!xpb  Must check if Z of deposited material already exists in bulk
    DO itrack=1,ntargsp
      found = .false.
      j = 1
      DO WHILE (.NOT.found .AND. j .LE. nz)
        found = zn(target_intcode(itrack)) .EQ. matsurf_nzconstituents(&
&         ibnd, j)
        IF (.NOT.found) j = j + 1
      END DO
      IF (found) THEN
        layer_composition(ibnd, j) = layer_composition(ibnd, j) + &
&         sput_frac(itrack)
      ELSE
        layer_composition(ibnd, nz+itrack) = layer_composition(ibnd, nz+&
&         itrack) + sput_frac(itrack)
      END IF
      IF (sput_frac(itrack) .GT. 0.0_R8) particle_inventory(ibnd, itrack&
&       ) = particle_inventory(ibnd, itrack) + deposition(ibnd, itrack) &
&         - erosion(ibnd, itrack)
    END DO
!
    RETURN
  END IF
END SUBROUTINE COMPOSE_LAYER_NODIFF

!
FUNCTION LAYER_KAPPA_NODIFF(ibnd, t) RESULT (layer_kappa)
  USE B2MOD_TYPES
  USE B2MOD_LAYER_DIFF
  USE B2MOD_WALL_DIFF
  USE B2MOD_SPUTTER_DIFF
  IMPLICIT NONE
  INTEGER :: ibnd
  REAL(kind=r8) :: t, layer_kappa
  INTEGER :: nz, itrack
  REAL(kind=r8) :: tcels, teff, a(5)
  REAL(kind=r8) :: bulk_value, coating_value, layer_value, frac
  EXTERNAL XERRAB
!
  tcels = t - 273.15_R8
!
!xpb First count the bulk contribution
  frac = layer_nrelconstituents(ibnd, 1)/matsurf_relconstituents(ibnd, 1&
&   )
  nz = matsurf_nconstituents(ibnd)
!xpb The bulk or coating surface parameters
  IF (coating_thickness(ibnd) .GT. 0.0_R8 .AND. target_depth(ibnd, 1) &
&     .LT. coating_thickness(ibnd)) THEN
    a(1) = coating_kappa_a1(ibnd)
    a(2) = coating_kappa_a2(ibnd)
    a(3) = coating_kappa_a3(ibnd)
    a(4) = coating_kappa_a4(ibnd)
    a(5) = coating_kappa_a5(ibnd)
    IF (coating_temp_unit(ibnd) .EQ. 'K') THEN
      teff = t
    ELSE IF (coating_temp_unit(ibnd) .EQ. 'C') THEN
      teff = tcels
    ELSE
      CALL XERRAB('Unrecognized temperature units (coat Kappa) !')
    END IF
    coating_value = a(1) + (a(2)+(a(3)+(a(4)+a(5)*teff)*teff)*teff)*teff
    layer_kappa = coating_value*frac
  ELSE
    a(1) = matbulk_kappa_a1(ibnd)
    a(2) = matbulk_kappa_a2(ibnd)
    a(3) = matbulk_kappa_a3(ibnd)
    a(4) = matbulk_kappa_a4(ibnd)
    a(5) = matbulk_kappa_a5(ibnd)
    IF (bulk_temp_unit(ibnd) .EQ. 'K') THEN
      teff = t
    ELSE IF (bulk_temp_unit(ibnd) .EQ. 'C') THEN
      teff = tcels
    ELSE
      CALL XERRAB('Unrecognized temperature units (bulk Kappa) !')
    END IF
    bulk_value = a(1) + (a(2)+(a(3)+(a(4)+a(5)*teff)*teff)*teff)*teff
    layer_kappa = bulk_value*frac
  END IF
  IF (layer_nconstituents(ibnd) .EQ. nz) THEN
    RETURN
  ELSE
!xpb Loop over deposited species contributions
    DO itrack=1,ntargsp
      frac = layer_nrelconstituents(ibnd, nz+itrack)
      a(1) = species_kappa_a1(itrack)
      a(2) = species_kappa_a2(itrack)
