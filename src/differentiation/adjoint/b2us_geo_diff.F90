      DEALLOCATE(g%vxbzb)
!
!  flux surface quantites
      DEALLOCATE(g%cvconn)
      DEALLOCATE(g%ftconn)
      DEALLOCATE(g%fteps)
      DEALLOCATE(g%ftbbav2)
      DEALLOCATE(g%fspsi)
      RETURN
    END IF
  END SUBROUTINE DEALLOC_GEOMETRY

