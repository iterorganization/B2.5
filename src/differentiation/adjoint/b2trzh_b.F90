&             , cf_limh, cf_limhb, cf_limb, cf_limbb, cf_limbh, &
&             cf_limbhb)
      DO j=2*nspecies-1,0,-1
        CALL POPREAL8ARRAY(ainv_mdf(:, j), r8*nspecies/8)
        amtib = amtib + ainv(nspecies:2*nspecies-1, j)*ainv_mdfb(:, j)
        ainvb(nspecies:2*nspecies-1, j) = ainvb(nspecies:2*nspecies-1, j&
&         ) + amti*ainv_mdfb(:, j)
        ainv_mdfb(:, j) = 0.D0
      END DO
      CALL POPREAL8ARRAY(cinv, r8*2**2*nspecies**2/8)
      CALL B2TRCV_B(nspecies, amti, amtib, mdm, n3, n4, n13, n14, n15, &
&             n16, nz2n, nz2nb, cinv, cinvb)
      CALL POPREAL8ARRAY(ainv, r8*2**2*nspecies**2/8)
      CALL B2TRCA_B(nspecies, amti, amtib, n5, n6, n9, n10, n11, n12, &
&             nz2n, nz2nb, ainv, ainvb)
      c_hwab = 0.D0
      c_rwab = 0.D0
    ELSE
      IF (branch .EQ. 2) THEN
        CALL POPREAL8ARRAY(c_rwa_mdf, r8*nspecies**2/8)
        c_rwab = c_rwab + c_rwa_mdfb
        CALL POPREAL8ARRAY(c_hwa_mdf, r8*nspecies**2/8)
        c_hwab = c_hwab + c_hwa_mdfb
        c_rwa_mdfb = 0.D0
        c_hwa_mdfb = 0.D0
      END IF
      DO j=nspecies-1,0,-1
        c_pi_ua_anb(j) = c_pi_ua_anb(j) + 2.0_R8*c_pi_uab(j, j)
        c_pi_uab(j, j) = 0.D0
      END DO
      CALL POPREAL8ARRAY(c_pi_ua, r8*nspecies**2/8)
      CALL B2TRVIS_AN_B(nspecies, mum, mdm, nz2n, nz2nb, c_pi_ua_an, &
&                 c_pi_ua_anb, c_pi_ha_an)
      DO i=nspecies-1,0,-1
        tempb = c_r_tab(icv, i, i)
        temp2 = nz2n(i)/nz2n(0:nspecies-1)
        c_r_ta_anb(:, i) = c_r_ta_anb(:, i) - temp2*c_r_tab(icv, i, 0:&
&         nspecies-1)
        tempb1 = -(c_r_ta_an(:, i)*c_r_tab(icv, i, 0:nspecies-1)/nz2n)
        c_r_tab(icv, i, 0:nspecies-1) = 0.D0
        nz2nb(i) = nz2nb(i) + SUM(tempb1)
        nz2nb = nz2nb - temp2*tempb1
        temp2 = nz2n(0:nspecies-1)/nz2n(i)
        c_r_ta_anb(i, :) = c_r_ta_anb(i, :) + temp2*tempb
        tempb1 = c_r_ta_an(i, :)*tempb/nz2n(i)
        nz2nb = nz2nb + tempb1
        nz2nb(i) = nz2nb(i) - SUM(temp2*tempb1)
      END DO
      wrkcb0 = 0.D0
      CALL POPREAL8ARRAY(hci0_al(icv, :), r8*nspecies/8)
      wrkcb0 = hci0_alb(icv, :)
      hci0_alb(icv, :) = 0.D0
      arg1 = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
      CALL B2TRHT_AN_B(nspecies, icv, arg1, zetap(icv), zetapb(icv), &
&                zeffne, zeffneb, pl%ti(icv), plb%ti(icv), c_hta_an_fl, &
&                c_hta_an_flb, nz2n, nz2nb, rmd2, wrkc, wrkcb0)
      DO i=nspecies-1,0,-1
        CALL POPREAL8(c_hta_an_fl(i), r8/8)
        cf_limhb(i) = cf_limhb(i) + c_hta_an(i)*c_hta_an_flb(i)
        c_hta_anb(i) = c_hta_anb(i) + cf_limh(i)*c_hta_an_flb(i)
        c_hta_an_flb(i) = 0.D0
        CALL POPREAL8ARRAY(c_r_ta_an(i, :), r8*nspecies/8)
        tempb1 = tf_ton_mat(i, 0:nspecies-1)*c_r_ta_anb(i, :)
        c_r_ta_anb(i, :) = 0.D0
        cf_limb0(i) = cf_limb0(i) + SUM(c_r_ta_an_nofl(i, 0:nspecies-1)*&
&         tempb1)
        c_r_ta_an_noflb(i, :) = c_r_ta_an_noflb(i, :) + cf_lim(i)*tempb1
      END DO
      amti = amnucl*mp/pl%ti(icv)
      CALL POPREAL8ARRAY(cf_lim, r8*nspecies/8)
      CALL POPREAL8ARRAY(cf_limh, r8*nspecies/8)
      CALL POPREAL8ARRAY(cf_limb, r8*nspecies/8)
      CALL POPREAL8ARRAY(cf_limbh, r8*nspecies/8)
      CALL B2TRFL_AN_B(icv, switch, mpg, c_hta_an, c_hta_anb, amnucl, &
&                amti, amtib, zetap(icv), zetapb(icv), gamma, cf_lim, &
&                cf_limb0, cf_limh, cf_limhb, cf_limb, cf_limbb, &
&                cf_limbh, cf_limbhb)
      arg1 = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
      CALL B2TRHT_AN_B(nspecies, icv, arg1, zetap(icv), zetapb(icv), &
&                zeffne, zeffneb, pl%ti(icv), plb%ti(icv), c_hta_an, &
&                c_hta_anb, nz2n, nz2nb, rmd2, wrkc, wrkcb0)
      CALL POPREAL8ARRAY(c_hwa, r8*nspecies**2/8)
      CALL POPREAL8ARRAY(c_rwa, r8*nspecies**2/8)
      CALL POPREAL8ARRAY(c_hta_an, r8*nspecies/8)
      CALL POPREAL8ARRAY(c_r_ta_an_nofl, r8*nspecies**2/8)
      CALL B2TRHRTF_AN_B(nspecies, switch, mum, mdm, nz2n, nz2nb, c_hwa(&
&                  0:nspecies-1, 0:nspecies-1), c_hwab(0:nspecies-1, 0:&
&                  nspecies-1), c_rwa(0:nspecies-1, 0:nspecies-1), &
&                  c_rwab(0:nspecies-1, 0:nspecies-1), c_hta_an, &
&                  c_hta_anb, c_rta_an, c_r_ta_an_nofl, c_r_ta_an_noflb)
      wrkccb = 0.D0
      c_pi_uab = 0.D0
    END IF
    CALL POPREAL8ARRAY(c_pi_ub, r8*nspecies/8)
    CALL B2TRCW_B(nspecies, mum, mdm, nz2n, nz2nb, c_pi_ub(0:nspecies-1)&
&           , c_pi_ubb(0:nspecies-1), c_pi_hb(0:nspecies-1))
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 1) CALL POPREAL8ARRAY(c_r_tb_nofl(icv, 0:nspecies-1)&
&                                   , r8*nspecies/8)
    wrkcb0 = wrkcb0 + c_r_tb_noflb(icv, 0:nspecies-1)
    c_r_tb_noflb(icv, 0:nspecies-1) = 0.D0
    CALL POPREAL8ARRAY(wrkc, r8*nspecies/8)
    CALL POPREAL8ARRAY(wrkcb, r8*nspecies**2/8)
    CALL POPREAL8ARRAY(c_htb, r8*nspecies/8)
    CALL B2TRCZ_B(nspecies, mum, mdm, nz2n, nz2nb, wrkc, wrkcb0, wrkcb, &
&           wrkcbb, c_hwb, c_hwbb, c_rwb, c_rwbb, c_htb, c_htbb, c_rtb)
    zeffneb = zeffneb - SUM(z2n_xy(icv, 0:nspecies-1)*nz2nb)/zeffne**2
    z2n_xyb(icv, 0:nspecies-1) = z2n_xyb(icv, 0:nspecies-1) + nz2nb/&
&     zeffne + zeffneb
    CALL POPREAL8(zeffne, r8/8)
    plb%ti(icv) = plb%ti(icv) - SUM(amnucl*amtib)*mp/pl%ti(icv)**2
  END DO
  temp0 = pl%ti/(ev*dv%lnlam)
  temp1 = SQRT(pl%ti/ev)
  tempb0 = temp1*czetap*zetapb/(ev*dv%lnlam)
  WHERE (pl%ti/ev .EQ. 0.D0) 
    plb%ti = plb%ti + tempb0
  ELSEWHERE
    plb%ti = plb%ti + temp0*czetap*zetapb/(ev*2.0*temp1) + tempb0
  END WHERE
  dvb%lnlam = dvb%lnlam - ev*temp0*tempb0
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 1) CALL POPREAL8ARRAY(gt_ac, r8*SIZE(gt_ac, 1)*SIZE(&
&                                 gt_ac, 2)/8)
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 1) CALL POPREAL8ARRAY(gtalc, r8*SIZE(gtalc, 1)*SIZE(&
&                                 gtalc, 2)/8)
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 1) CALL POPREAL8ARRAY(avm_u, r8*SIZE(avm_u, 1)/8)
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 1) CALL POPREAL8ARRAY(rho_a_rel, r8*SIZE(rho_a_rel, 1)&
&                                 *SIZE(rho_a_rel, 2)/8)
  CALL B2MOD_ZHFRTF_TGTA_B(ncv, nfc, nvx, ns, switch, geo, geob, mpg, &
&                    mpgb, pl%na, plb%na, pl%ua, plb%ua, pl%ti, plb%ti)
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 1) CALL POPREAL8ARRAY(z2n_xy, r8*SIZE(z2n_xy, 1)*SIZE(&
&                                 z2n_xy, 2)/8)
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 1) CALL POPREAL8ARRAY(nal, r8*SIZE(nal, 1)*SIZE(nal, 2&
&                                 )/8)
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 1) CALL POPREAL8ARRAY(ia, r8*SIZE(ia, 1)*SIZE(ia, 2)/8&
&                                )
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 1) CALL POPREAL8ARRAY(av_ualpha, r8*SIZE(av_ualpha, 1)&
&                                 *SIZE(av_ualpha, 2)/8)
  CALL B2MOD_ZHFRTF_PREP_B(ncv, ns, pl%na, plb%na, pl%ua, plb%ua)
  CALL POPCONTROL2B(branch)
  IF (branch .EQ. 0) THEN
    DO is=1,nspecies*(nspecies+1)/2-nspecies,1
      CALL POPCONTROL1B(branch)
    END DO
  ELSE IF (branch .NE. 1) THEN
    GOTO 110
  END IF
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 1) CALL POPREAL8ARRAY(rmu, r8*SIZE(rmu, 1)*SIZE(rmu, 2&
&                                 )/8)
  DO is=ns-1,0,-1
    CALL POPINTEGER4(znnucl_int(inucl-1))
    CALL POPINTEGER4(s2nucl(is))
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 1) CALL POPINTEGER4ARRAY(nucl2s(inucl-1, NINT(zamin(&
&                                      is)):NINT(zamax(is))), NINT(zamax&
&                                      (is)) - NINT(zamin(is)) + 1)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      CALL POPREAL8(amnucl(inucl-1), r8/8)
      CALL POPINTEGER4(inucl)
    END IF
  END DO
  CALL POPREAL8(amnucl(0), r8/8)
 110 CONTINUE
END SUBROUTINE B2TRZH_B

