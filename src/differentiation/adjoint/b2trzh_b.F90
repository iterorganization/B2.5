& *nspecies-1), c_rwa(0:nspecies-1, 0:nspecies-1), c_hwa_mdf(0:nspecies-&
& 1, 0:nspecies-1), c_rwa_mdf(0:nspecies-1, 0:nspecies-1), &
& c_r_ta_an_nofl(0:nspecies-1, 0:nspecies-1), c_r_ta_an(0:nspecies-1, 0:&
& nspecies-1), c_rta_an(0:nspecies-1), c_hta_an(0:nspecies-1), &
& c_hta_an_fl(0:nspecies-1), c_pi_ua(0:nspecies-1, 0:nspecies-1), &
& c_pi_ub(0:nspecies-1), c_pi_ua_an(0:nspecies-1), c_pi_ha(0:nspecies-1&
& , 0:nspecies-1), c_pi_hb(0:nspecies-1), c_pi_ha_an(0:nspecies-1), &
& tau_zh(0:nspecies-1, 0:nspecies-1), eta_cl(ncv, 0:ns-1), eta_cl_fl(ncv&
& , 0:ns-1), eta_cl_a_only(ncv, 0:ns-1), eta_cl_a_only_fl(ncv, 0:ns-1), &
& eta_uadp(ncv, 0:nspecies-1, 0:nspecies-1), eta_ubdp(ncv, 0:nspecies-1)&
& , eta_uadp_fl(ncv, 0:nspecies-1, 0:nspecies-1), eta_ubdp_fl(ncv, 0:&
& nspecies-1), eta_hadp(ncv, 0:nspecies-1, 0:nspecies-1), eta_hbdp(ncv, &
& 0:nspecies-1), t0, t1, czetap, zetap(ncv), i_al(0:nspecies-1), cf_limh&
& (0:nspecies-1), cf_limbh(0:nspecies-1), cf_lim(0:nspecies-1), cf_limb(&
& 0:nspecies-1), cf_limv(0:ns-1), cf_limv_al(0:nspecies-1), wrkc(0:&
& nspecies-1), wrkcc(0:nspecies-1, 0:nspecies-1), wrkca(0:nspecies-1, 0:&
& nspecies-1), wrkcb(0:nspecies-1, 0:nspecies-1), wrkg2(0:nspecies-1, 0:&
& nspecies-1), wrkg8(0:nspecies-1, 0:nspecies-1), hci0_nofl(ncv, 0:1), &
& hci0(ncv, 0:1), hci0_al_nofl(ncv, 0:nspecies-1), hci0_al(ncv, 0:&
& nspecies-1), hcib_al_nofl(0:nspecies-1), hcib_al(0:nspecies-1), hci0_a&
& (ncv, 0:ns-1), c_hw(0:nspecies-1, 0:nspecies-1), c_rw(0:nspecies-1, 0:&
& nspecies-1), c_hwa(0:nspecies-1, 0:nspecies-1), c_hwb(0:nspecies-1), &
& c_rwb(0:nspecies-1), c_htb(0:nspecies-1), c_rtb(0:nspecies-1), &
& kappa_hta(0:nspecies-1, 0:nspecies-1), kappa_htb(0:nspecies-1), &
& kappa_rta(0:nspecies-1, 0:nspecies-1), kappa_rtb(0:nspecies-1)
  REAL(kind=r8) :: cvsahz_drho(nfc, 0:1, 0:ns-1), cvsa0(nfc, 0:1, 0:ns-1&
& ), cvsa_drho(nfc, 0:1, 0:ns-1)
  CHARACTER(len=80) :: cvalip
  INTRINSIC MAX
  CHARACTER :: REPEAT
  INTRINSIC NINT
  INTRINSIC SQRT
  INTRINSIC SUM
  INTEGER :: max1
  INTEGER :: max2
  REAL(kind=r8) :: arg1
!
!-----------------------------------------------------------------------
!.computation
!
!   ..subprogram start-up calls
  CALL SUBINI('b2trzh')
!   ..set internal parameters on first call
  IF (ncall_b2trzh .EQ. 0) THEN
    IF (1 .LT. nspecies*(nspecies+1)/2 - nspecies) THEN
      max1 = nspecies*(nspecies+1)/2 - nspecies
    ELSE
      max1 = 1
    END IF
!   ..allocate
    cvalip = REPEAT('0', max1)
!som 26.06.21
    CALL IPGETC('b2tral_zh_tf_toff', cvalip)
    IF (1 .LT. nspecies*(nspecies+1)/2 - nspecies) THEN
      max2 = nspecies*(nspecies+1)/2 - nspecies
    ELSE
      max2 = 1
    END IF
    DO is=1,max2
      zh_tf_toff(is) = cvalip(is:is)
    END DO
!
!   ..calculate the atom number and the atomic mass of each isonuclear sequence
    inucl = 1
    znnucl(0) = zn(0)
    amnucl(0) = am(0)
    zn_temp = zn(0)
    am_temp = am(0)
    DO is=0,ns-1
      IF (NINT(zn(is)) .NE. NINT(zn_temp) .OR. NINT(am(is)) .NE. NINT(&
