!    ..calculate the classical ion conductivity correction with flux limiting
    wrkc = cf_limbh*c_htb
    arg1 = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    CALL B2TRHT_AN_NODIFF(nspecies, icv, arg1, zetap(icv), zeffne, pl%ti&
&                   (icv), wrkc, nz2n, rmd2, hcib_al(0:nspecies-1))
!
!   ..apply charge state correction for the heat flux
    hci0_nofl(icv, 0) = SUM(hci0_al_nofl(icv, 0:nspecies-1) + switch%&
&     zhcscorr*hcib_al_nofl(0:nspecies-1)*(i_al(0:nspecies-1)-1.0_R8))
    hci0(icv, 0) = SUM(hci0_al(icv, 0:nspecies-1) + switch%zhcscorr*&
&     hcib_al(0:nspecies-1)*(i_al(0:nspecies-1)-1.0_R8))
!
!   ..ion heat conductivity for each charge state
    hci0_a(icv, 0:ns-1) = 0.0_R8
    DO i=0,nspecies-1
      DO iz=1,znnucl_int(i)
        hci0_a(icv, nucl2s(i, iz)) = pl%na(icv, nucl2s(i, iz))/nal(icv, &
&         i)*(hci0_al(icv, i)+switch%zhcscorr*hcib_al(i)*(z2n_xy(icv, i)&
&         /(nal(icv, i)*iz**2)-1.0_R8))
      END DO
    END DO
!
    c_hw(0:nspecies-1, 0:nspecies-1) = c_hwa(0:nspecies-1, 0:nspecies-1)
    DO inucl=0,nspecies-1
      c_hw(inucl, inucl) = c_hw(inucl, inucl) + switch%zhcscorr*c_hwb(&
&       inucl) - SUM(c_hwa(0:nspecies-1, inucl))
    END DO
!   ..save heat flux velocity-dependent part transport coefficient
    c_hw_save(icv, 0:nspecies-1, 0:nspecies-1) = c_hw(0:nspecies-1, 0:&
&     nspecies-1)
!
    IF (switch%zh_dist_fun .EQ. 1) THEN
!   ..apply charge state correction for the R-moment
      c_rw(0:nspecies-1, 0:nspecies-1) = c_rwa(0:nspecies-1, 0:nspecies-&
&       1)
      DO inucl=0,nspecies-1
        c_rw(inucl, inucl) = c_rw(inucl, inucl) + switch%zhcscorr*c_rwb(&
&         inucl) - SUM(c_rwa(0:nspecies-1, inucl))
      END DO
!
!    ..moments of the distribution function
      DO inucl=0,nspecies-1
        DO iz=1,znnucl_int(inucl)
          w_out(icv, nucl2s(inucl, iz)) = pl%ua(icv, nucl2s(inucl, iz)) &
&           - avm_u(icv)
!
          htdp_out(icv, nucl2s(inucl, iz)) = SUM(kappa_hta(inucl, 0:&
&           nspecies-1)*gtalc(icv, 0:nspecies-1)) + switch%zhcscorr*&
&           kappa_htb(inucl)*(pl%na(icv, nucl2s(inucl, iz))/nal(icv, &
&           inucl)/ia(icv, nucl2s(inucl, iz))*gt_ac(icv, nucl2s(inucl, &
&           iz))-gtalc(icv, inucl))
!
          hwdp_out(icv, nucl2s(inucl, iz)) = SUM(c_hw(0:nspecies-1, &
&           inucl)*(pl%ua(icv, nucl2s(inucl, iz))-av_ualpha(icv, 0:&
&           nspecies-1)))
!
          rtdpgamma_out(icv, nucl2s(inucl, iz)) = SUM(kappa_rta(inucl, 0&
&           :nspecies-1)*gtalc(icv, 0:nspecies-1)) + switch%zhcscorr*&
&           kappa_rtb(inucl)*(pl%na(icv, nucl2s(inucl, iz))/nal(icv, &
&           inucl)/ia(icv, nucl2s(inucl, iz))*gt_ac(icv, nucl2s(inucl, &
&           iz))-gtalc(icv, inucl))
!
          rwdpgamma_out(icv, nucl2s(inucl, iz)) = SUM(c_rw(0:nspecies-1&
&           , inucl)*(pl%ua(icv, nucl2s(inucl, iz))-av_ualpha(icv, 0:&
&           nspecies-1)))
!
          piu_out(icv, nucl2s(inucl, iz)) = -(4.0e0_R8/3.0e0_R8*eta_cl(&
&           icv, nucl2s(inucl, iz))/(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))&
&           **2*gavm_uc(icv))
!
        END DO
      END DO
    END IF
!

  END DO
!
  mode = switch%b2tral_mode
!
!    ..ion conductivity on the cell boundary
  CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&              fcs, hci0(:, 0), chci0(:, 0))
  CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&              fcs, hci0_nofl(:, 0), chci0_nofl(:, 0))
  DO i=0,nspecies-1
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&                fcs, hci0_al(:, i), chci0_al(:, 0, i))
  END DO
  DO is=0,ns-1
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&                fcs, hci0_a(:, is), chci0_a(:, 0, is))
  END DO
!
!    ..ion velocity-dependent viscosity on the cell boundary
!srv 30.01.23
  cvsa_drho = 0.0_R8
!srv 30.01.23
  cvsahz_drho = 0.0_R8
  cvsa0 = 0.0_R8
  cvsahz0 = 0.0_R8
!
  fcshz = geo%fcs*geo%fchz
  DO ifc=1,nfc
    fcvolhz(ifc, 1) = geo%fcvol(ifc, 1)*geo%cvhz(mpg%fccv(ifc, 1))
    fcvolhz(ifc, 2) = geo%fcvol(ifc, 2)*geo%cvhz(mpg%fccv(ifc, 2))
  END DO
  IF (switch%zh_vis_type .EQ. 0 .OR. switch%zh_vis_type .EQ. 1) THEN
    DO is=0,ns-1
      wrk = eta_cl_fl(:, is)*rho_a_rel(:, is)
      DO ifc=1,nfc
        vsaf0_luc(ifc, 0, is) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(ifc, 1))&
&         +geo%fcvol(ifc, 2)*wrk(mpg%fccv(ifc, 2)))/(geo%fcvol(ifc, 1)+&
&         geo%fcvol(ifc, 2))
      END DO
      vsaf0_luc(:, 1, is) = 0.0_R8
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, wrk, cvsahz0(:, 0, is))
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, &
&                  geo%fcs, wrk, cvsa0(:, 0, is))
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, eta_cl_fl(:, is), cvsahz_drho(:, 0, is))
      wrk = eta_cl(:, is)*rho_a_rel(:, is)
      DO ifc=1,nfc
        vsaf_drho_luc(ifc, 0, is) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(ifc&
&         , 1))+geo%fcvol(ifc, 2)*wrk(mpg%fccv(ifc, 2)))/(geo%fcvol(ifc&
&         , 1)+geo%fcvol(ifc, 2))
      END DO
      vsaf_drho_luc(:, 1, is) = 0.0_R8
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, wrk, cvsahz0_nofl(:, 0, is))
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, &
&                  geo%fcs, eta_cl_fl(:, is), cvsa_drho(:, 0, is))
    END DO
  ELSE IF (switch%zh_vis_type .EQ. 2) THEN
    DO is=0,ns-1
      wrk = eta_cl_a_only_fl(:, is)
      DO ifc=1,nfc
        vsaf0_luc(ifc, 0, is) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(ifc, 1))&
&         +geo%fcvol(ifc, 2)*wrk(mpg%fccv(ifc, 2)))/(geo%fcvol(ifc, 1)+&
&         geo%fcvol(ifc, 2))
      END DO
      vsaf0_luc(:, 1, is) = 0.0_R8
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, eta_cl_a_only_fl(:, is), cvsahz0(:, 0, is))
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, &
&                  geo%fcs, eta_cl_a_only_fl(:, is), cvsa0(:, 0, is))
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, eta_cl_a_only(:, is), cvsahz0_nofl(:, 0, is))
    END DO
  END IF
!
!    ..additional velocity-dependent viscosity on the cell boundary
  DO i=0,nspecies-1
    DO j=0,nspecies-1
      wrk = eta_uadp_fl(:, i, j)
      DO ifc=1,nfc
        vsaf_uadp_luc(ifc, 0, i, j) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(&
&         ifc, 1))+geo%fcvol(ifc, 2)*wrk(mpg%fccv(ifc, 2)))/(geo%fcvol(&
&         ifc, 1)+geo%fcvol(ifc, 2))
      END DO
      vsaf_uadp_luc(:, 1, i, j) = 0.0_R8
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, eta_uadp_fl(:, i, j), cvsahz_uadp_fl(:, 0, i, &
&                  j))
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, &
&                  geo%fcs, eta_uadp_fl(:, i, j), cvsa_uadp_fl(:, 0, i, &
&                  j))
    END DO
!
    wrk = eta_ubdp_fl(:, i)
    DO ifc=1,nfc
      vsaf_ubdp_luc(ifc, 0, i) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(ifc, 1)&
&       )+geo%fcvol(ifc, 2)*wrk(mpg%fccv(ifc, 2)))/(geo%fcvol(ifc, 1)+&
&       geo%fcvol(ifc, 2))
    END DO
    vsaf_ubdp_luc(:, 1, i) = 0.0_R8
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, fcshz&
&                , eta_ubdp_fl(:, i), cvsahz_ubdp_fl(:, 0, i))
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&                fcs, eta_ubdp_fl(:, i), cvsa_ubdp_fl(:, 0, i))
  END DO
!
!    ..ion heat flux dependent viscosity on the cell boundary
  DO i=0,nspecies-1
    DO j=0,nspecies-1
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, eta_hadp(:, i, j), cvsahz_hadp(:, 0, i, j))
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, &
&                  geo%fcs, eta_hadp(:, i, j), cvsa_hadp(:, 0, i, j))
    END DO
!
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, fcshz&
&                , eta_hbdp(:, i), cvsahz_hbdp(:, 0, i))
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&                fcs, eta_hbdp(:, i), cvsa_hbdp(:, 0, i))
  END DO
!
!    ..apply Luciani correction for the ion conductivity
  chci0_luc = 0.0_R8
  chci0_luc(:, 0) = chci0_luc(:, 0)*co%f_luc_ki
  hci0(:, 0) = hci0(:, 0)*co%f_luc_ki_c
  DO i=0,nspecies-1
    chci0_al_luc(:, 0, i) = chci0_al(:, 0, i)*co%f_luc_ki
    hci0_al_luc(:, i) = hci0_al(:, i)*co%f_luc_ki_c
  END DO
  DO is=0,ns-1
    chci0_a_luc(:, 0, is) = chci0_a(:, 0, is)*co%f_luc_ki
    hci0_a_luc(:, is) = hci0_a(:, is)*co%f_luc_ki_c
  END DO
!
!    ..apply Luciani correction for the ion velocity-dependent viscosity
!srv 30.01.23 {
  cvsahz0_luc = cvsahz0
  cvsahz_drho_luc = cvsahz_drho
  cvsa0_luc = cvsa0
!srv 30.01.23 }
  cvsa_drho_luc = cvsa_drho
  DO is=0,ns-1
    IF (.NOT.is_neutral(is)) THEN
      vsaf0_luc(:, 0, is) = vsaf0_luc(:, 0, is)*co%f_luc_et
      vsaf_drho_luc(:, 0, is) = vsaf_drho_luc(:, 0, is)*co%f_luc_et
      cvsahz0_luc(:, 0, is) = cvsahz0(:, 0, is)*co%f_luc_et
      cvsahz_drho_luc(:, 0, is) = cvsahz_drho(:, 0, is)*co%f_luc_et
      cvsa0_luc(:, 0, is) = cvsa0(:, 0, is)*co%f_luc_et
      cvsa_drho_luc(:, 0, is) = cvsa_drho(:, 0, is)*co%f_luc_et
    END IF
  END DO
!
!    ..apply Luciani correction for the addtional part of the
!      velocity-dependent viscosity
!wdk todo: add flux limit to vsaf_* arrays
  DO i=0,nspecies-1
    DO j=0,nspecies-1
      vsaf_uadp_luc(:, 0, i, j) = vsaf_uadp_luc(:, 0, i, j)*co%f_luc_et
      cvsahz_uadp_luc(:, 0, i, j) = cvsahz_uadp_fl(:, 0, i, j)*co%&
&       f_luc_et
      cvsa_uadp_luc(:, 0, i, j) = cvsa_uadp_fl(:, 0, i, j)*co%f_luc_et
    END DO
    vsaf_ubdp_luc(:, 0, i) = vsaf_ubdp_luc(:, 0, i)*co%f_luc_et
    cvsahz_ubdp_luc(:, 0, i) = cvsahz_ubdp_fl(:, 0, i)*co%f_luc_et
    cvsa_ubdp_luc(:, 0, i) = cvsa_ubdp_fl(:, 0, i)*co%f_luc_et
  END DO
!
!    ..apply Luciani and neoclassical corrections for the ion heat flux
!      dependent viscosity
!wdk todo:  add flux limit to vsaf_* arrays
  DO i=0,nspecies-1
    DO j=0,nspecies-1
      cvsahz_hadp_luc(:, 0, i, j) = cvsahz_hadp(:, 0, i, j)*co%f_luc_et*&
&       co%alpha1f
      cvsa_hadp_luc(:, 0, i, j) = cvsa_hadp(:, 0, i, j)*co%f_luc_et*co%&
&       alpha1f
    END DO
    cvsahz_hbdp_luc(:, 0, i) = cvsahz_hbdp(:, 0, i)*co%f_luc_et*co%&
&     alpha1f
    cvsa_hbdp_luc(:, 0, i) = cvsa_hbdp(:, 0, i)*co%f_luc_et*co%alpha1f
  END DO
!
  ncall_b2trzh = ncall_b2trzh + 1
  CALL SUBEND()
  RETURN
!
END SUBROUTINE B2TRZH_NODIFF

