              CALL CFRURE(96, arg1, t_dummy, 'T')
              arg1 = nshpottau + 1
              CALL CFRURE(96, arg1, shpottau_dummy, 'shpottau')
!
              DEALLOCATE(m_dummy)
              DEALLOCATE(t_dummy)
              DEALLOCATE(shpottau_dummy)
            END IF
!
!         Read preintegrated recycling coefficient contributions
            CALL CFRURE(96, ndata, rc1(:, :, :, is, it), 'r1')
