!
!nh 22.02.2020
!     Calculate the part of the reflected flux emitted as molecules. At the moment,
!     the molecules are treated kinetically.
    IF (fnni .GE. 0.0_R8) THEN
      fna_mol_refl = recycm*pt*fnni_nodrifts*area*0.5_R8
      IF (0.0_R8 .LT. (recyc0*fnni*area-2.0_R8*fna_mol_refl)*&
&         fluid_frac_hyb) THEN
        CALL PUSHCONTROL2B(2)
      ELSE
        CALL PUSHCONTROL2B(1)
      END IF
    ELSE
      CALL PUSHCONTROL2B(0)
    END IF
