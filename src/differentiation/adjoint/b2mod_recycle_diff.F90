    USE B2MOD_B2CMPA_DIFF
    USE B2MOD_RATES
    USE B2US_GEO_DIFF
    USE B2US_PLASMA_DIFF
    USE B2US_MAP_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(MAPPING), INTENT(IN) :: mpg
    INTEGER, INTENT(IN) :: icv, icn, nci, ifc, isn, iscx0, isi, iwall, &
