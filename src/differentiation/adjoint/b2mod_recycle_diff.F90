    REAL(kind=r8) :: temp
    REAL(kind=r8) :: tempb
    REAL(r8) :: temp0
    REAL(r8) :: tempb0
    REAL(r8) :: tempb1
    REAL(kind=r8) :: tempb2
    REAL(kind=r8) :: tempb3
!
!     Initialize/calculate some variables
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!nh   neutral velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and only neu
!tral parallel
!     velocity component
    unf = pl%ua(icv, isn)
    ux = unf*pit2
    uy = -(sina*isign*unf*pit)
    uz = -(cosa*isign*unf*pit)
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tf, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Incident particle flux density
!
!     Incident momentum flux densities
!
!     Pressure of incident neutrals
!
!     Density of incident neutrals
    CALL PUSHREAL8(nni, r8/8)
    nni = nnf*i0l
!
!
!     Incident total energy flux density of neutrals
!
!     Convert particle flux densities in fluxes
!
!     Calculate incident poloidal momentum flux of neutrals
!
!     Convert energy flux densities in net energy flux contributions
!
    fenenib = -(area*fenenib)
    fmomnipb = pit*fmomnib
    fmomnitb = pit2*fmomnib
    fmoxb = -(area*fmomnitb)
    tempb3 = isign*area*fmomnipb
    pnb = cosa*isign*area*fmomnipb
    fmozb = cosa*tempb3
    fmoyb = sina*tempb3
    fnnib = area*fnnib
    temp = tf + mn*(ux*ux+uy*uy)/2.0_R8
    tempb2 = -(nnf*fenenib)
    tempb3 = mn*i1l*tempb2/2.0_R8
    i3lb = mn*tempb2/2.0_R8
    nnib = nnib + ux*nnwwnib
    temp0 = 2.0_R8*tf/mn + ux*ux + uy*uy
    tempb0 = nnf*mn*pnb/3.0_R8
    i0lb = nnf*nnib + temp0*tempb0
    tempb1 = i0l*tempb0
    tfb = tfb + i1l*tempb2 + 2.0_R8*tempb1/mn
    i2lb = tempb0 - nnf*mn*fmozb
    tempb = -(mn*fmoyb)
    nnfb = i0l*nnib - (temp*i1l+mn*(i3l/2.0_R8))*fenenib + (temp0*i0l+&
&     i2l)*mn*pnb/3.0_R8 + uy*i1l*tempb - i2l*mn*fmozb
    i1lb = temp*tempb2 + nnf*uy*tempb
    uyb = 2*uy*tempb3 + 2*uy*tempb1 + nnf*i1l*tempb
    tempb = -(mn*fmoxb)
    uxb = 2*ux*tempb3 + nni*nnwwnib + 2*ux*tempb1 + nnf*i1l*tempb
    CALL POPREAL8(nni, r8/8)
    nnfb = nnfb + ux*i1l*tempb - i1l*fnnib
    i1lb = i1lb + nnf*ux*tempb - nnf*fnnib
    uzb = 0.D0
    i4lb = 0.D0
    CALL CALCMOMENTSMAXWELLIAN_B(uz, uzb, tf, tfb, mn, i0l, i0lb, i0r, &
&                          i1l, i1lb, i1r, i2l, i2lb, i2r, i3l, i3lb, &
&                          i3r, i4l, i4lb, i4r, i5l, i5r, i6l, i6r)
    unfb = pit2*uxb - pit*cosa*isign*uzb - pit*sina*isign*uyb
    plb%ua(icv, isn) = plb%ua(icv, isn) + unfb
    plb%na(icv, isn) = plb%na(icv, isn) + nnfb
  END SUBROUTINE CALCINCIDENTFLUXESMAXWELLIAN_B

!
  SUBROUTINE CALCINCIDENTFLUXESMAXWELLIAN(icv, ifc, isn, isign, istra, &
&   area, pl, tf, geo, cosa, sina, fnni, fmomni, nni, nnwwni, feneni)
!nh   08.05.2018
!     This routine calculates the incident neutral particle fluxes based on
!     a truncated Maxwellian for the incident neutrals. These particle fluxes
!     are necessary for the hybrid delta f model. It is assumed that only the
!     neutral parallel drift velocity is non-negligible (unf). This has to be
!     adapted.
    USE B2MOD_B2CMPA_DIFF
    USE B2US_PLASMA_DIFF
    USE B2US_GEO_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
!     Input variables
!     isn is the neutral species index
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(GEOMETRY), INTENT(IN) :: geo
    INTEGER, INTENT(IN) :: icv, ifc, isn, istra
    REAL(kind=r8), INTENT(IN) :: area, tf, isign, cosa, sina
!     Output variables
    REAL(kind=r8), INTENT(OUT) :: fnni, fmomni, nni, nnwwni, feneni
!     Internal variables
    REAL(kind=r8) :: pit, pit2, mn, nnf, ux, uy, uz, i0l, i0r, i1l, i1r&
&   , i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r, fmox, fmoy, fmoz&
&   , pn, fmomnip, fmomnit, unf
    INTRINSIC ABS
!
    CALL SUBINI('CalcIncidentFluxesMaxwellian')
!     Initialize/calculate some variables
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!nh   neutral velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and only neu
!tral parallel
!     velocity component
    unf = pl%ua(icv, isn)
    ux = unf*pit2
    uy = -(sina*isign*unf*pit)
    uz = -(cosa*isign*unf*pit)
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tf, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Incident particle flux density
    fnni = -(nnf*i1l)
!
!     Incident momentum flux densities
    fmox = -(mn*nnf*ux*i1l)
    fmoy = -(mn*nnf*uy*i1l)
    fmoz = -(mn*nnf*i2l)
!
!     Pressure of incident neutrals
    pn = mn/3.0_R8*(nnf*((2.0_R8*tf/mn+ux**2+uy**2)*i0l+i2l))
!
!     Density of incident neutrals
    nni = nnf*i0l
!
    nnwwni = nni*ux
!
!     Incident total energy flux density of neutrals
    feneni = -(nnf*((tf+mn/2.0_R8*(ux**2+uy**2))*i1l+mn/2.0_R8*i3l))
!
!     Convert particle flux densities in fluxes
    fnni = fnni*area
!
!     Calculate incident poloidal momentum flux of neutrals
    fmomnip = isign*(fmoz*cosa+fmoy*sina)*area + isign*pn*area*cosa
    fmomnit = -(fmox*area)
    fmomni = pit*fmomnip + pit2*fmomnit
!
!     Convert energy flux densities in net energy flux contributions
    feneni = -(feneni*area)
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCINCIDENTFLUXESMAXWELLIAN

!  Differentiation of calcrecycledfluxes in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: int4l int1l int2l int3l int0l
!                nnrec fnnrec phi_app tif fmomrec t0 recyc0 fluid_frac_hyb
!                pof *(pl.ua) fenerec nnwwnrec
!   with respect to varying inputs: int4l int1l int2l int3l int0l
!                phi_app tif t0 recyc0 fluid_frac_hyb pof *(pl.ua)
!   Plus diff mem management of: pl.ua:in
!
  SUBROUTINE CALCRECYCLEDFLUXES_B(icv, nci, ifc, isign, isn, iscx0, isi&
&   , istra, iwall, phi_app, phi_appb, area, recyc0, recyc0b, recycm, &
&   fluid_frac_hyb, fluid_frac_hybb, pl, plb, tif, tifb, tef, pof, pofb&
&   , nif, nef, geo, mpg, t0, t0b, cosa, sina, use_uy_uz_0, fnnrec, &
&   fnnrecb, fmomrec, fmomrecb, nnrec, nnrecb, nnwwnrec, nnwwnrecb, &
&   fenerec, fenerecb)
!mb   17.07.17
!     This routine calculates the recycled neutral particle, momentum and energy fluxes based
!     on the diffusion approximation (see papers of Niels Horsten).
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFF
    USE B2MOD_NEUTRALS_NAMELIST_DIFF, ONLY : e_fc, accel_ion
    USE B2US_GEO_DIFF
    USE B2US_PLASMA_DIFF
    USE B2US_MAP_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
!     nif = plasma density on face, isn neutral species index
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFF) :: plb
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
!!      type (B2Rates), intent (in) :: rt
    INTEGER, INTENT(IN) :: icv, nci, ifc, isn, iscx0, isi, iwall, istra&
&   , use_uy_uz_0
    REAL(kind=r8), INTENT(IN) :: phi_app, area, recyc0, recycm, pof, nif&
&   , nef, t0, isign, tif, tef, cosa, sina, fluid_frac_hyb
    REAL(kind=r8) :: phi_appb, recyc0b, pofb, t0b, tifb, fluid_frac_hybb
!! fluid_frac_hyb: in fluid neutral cases or standard spatially hybrid cases this multiplier is always equal to 1.
!! only in the generalized form of the spatially hybrid approach, where the fluid and kinetic treatment are mixed
!! on a single target stratum, this value can deviate from 1.
!     Output variables:
!     fmomni: momentum flux of incident neutrals.
    REAL(kind=r8) :: fnnrec, fmomrec, nnrec, nnwwnrec, fenerec
    REAL(kind=r8) :: fnnrecb, fmomrecb, nnrecb, nnwwnrecb, fenerecb
    EXTERNAL INTP_3DTABLE, INTP_3DTABLE00
    EXTERNAL INTP_3DTABLE_B, INTP_3DTABLE00_B
!     Internal variables
    REAL(kind=r8) :: cs, mj, shpottauj, pf, pcorf, pt, vt, pit, pit2, mn&
&   , ux, uz, i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r&
&   , i6l, i6r, rc1i, rmx1i, rmz1i, rp11i, rp21i, rp31i, re1i, fmox, &
&   fmoy, fmoz, fmomrecp, fmomrect, pnc, fenec, fdni, INTP_3DTABLE, &
&   INTP_3DTABLE00, tifev, upf, fna_mol_rec
    REAL(kind=r8) :: csb, mjb, shpottaujb, pfb, pcorfb, ptb, uxb, uzb, &
&   i0lb, i1lb, i2lb, i3lb, i4lb, rc1ib, rmx1ib, rmz1ib, rp11ib, rp21ib&
&   , rp31ib, re1ib, fmoxb, fmozb, fmomrecpb, fmomrectb, pncb, fenecb, &
&   fdnib, tifevb, upfb, fna_mol_recb
    REAL(kind=r8) :: tab(0:nm, 0:nt, 0:nshpottau), ff(0:1, 0:1, 0:1)
    INTEGER :: is, it, ilt, ilm, ilsh, ip1, ip2, ip3
    INTRINSIC ABS
    INTRINSIC SQRT
    INTRINSIC MAX
    INTRINSIC MIN
    INTRINSIC SIGN
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: abs0b
    REAL(kind=r8) :: min1
    REAL(kind=r8) :: min1b
    REAL(kind=r8) :: min2
    REAL(kind=r8) :: min2b
    REAL(kind=r8) :: tempb
    REAL(kind=r8) :: temp
    REAL(kind=r8) :: temp0
    REAL(kind=r8) :: tempb0
    REAL(kind=r8) :: temp1
    REAL(kind=r8) :: tempb1
    REAL(kind=r8) :: tempb2
    INTEGER :: branch
!
!     Initialize/calculate some variables
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!mb   plasma velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and drifts
    upf = pl%ua(icv, isi)
    ux = upf*pit2
    uz = -(cosa*isign*upf*pit)
!       ion particle flux density
    fdni = t0/area
!
!     Assume poloidal and radial drifts are zero for momentum and energy
!     equations to reduce the dimensionality of the pre-evaluates integrals
!     of TRIM tables. Only toroidal drift remains!
    IF (use_uy_uz_0 .EQ. 0) THEN
      uz = 0.0_R8
      CALL PUSHCONTROL1B(1)
    ELSE
      CALL PUSHCONTROL1B(0)
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!
!     Interpolate tables to find recycling values
!       Isothermal speed of sound and Mach number
    cs = SQRT(2.0_R8*tif/mn)
    IF (ux .GE. 0.) THEN
      abs0 = ux
      CALL PUSHCONTROL1B(0)
    ELSE
      abs0 = -ux
      CALL PUSHCONTROL1B(1)
    END IF
    mj = abs0/cs
    tifev = tif/ev
    is = recycle_index(isn)
    it = target_index(istra)
!       Shpottau value: sheath_potential devided by tau (Ti/Te)
    shpottauj = accel_ion(istra)*qe*(pof-phi_app)/tif
    tab = rc1(:, :, :, is, it)
    rc1i = INTP_3DTABLE(tab, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh, ip1, ip2, ip3)
    IF (rc1i .LT. 0.0_R8) THEN
      rc1i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rc1i = rc1i
    END IF
!
    ff = rmx1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rmx1i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rmx1i .LT. 0.0_R8) THEN
      rmx1i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rmx1i = rmx1i
    END IF
!
    ff = rmz1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rmz1i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rmz1i .LT. 0.0_R8) THEN
      rmz1i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rmz1i = rmz1i
    END IF
!
    ff = rp11(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rp11i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rp11i .LT. 0.0_R8) THEN
      rp11i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rp11i = rp11i
    END IF
!
    ff = rp21(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rp21i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rp21i .LT. 0.0_R8) THEN
      rp21i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rp21i = rp21i
    END IF
!
    ff = rp31(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rp31i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rp31i .LT. 0.0_R8) THEN
      rp31i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rp31i = rp31i
    END IF
!
    ff = re1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    re1i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (re1i .LT. 0.0_R8) THEN
      re1i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      re1i = re1i
    END IF
    IF (rc1i*cs/(-i1l) .GT. recyc0) THEN
      min1 = recyc0
      CALL PUSHCONTROL1B(0)
    ELSE
      min1 = rc1i*cs/(-i1l)
      CALL PUSHCONTROL1B(1)
    END IF
!
!     Calculate fast and thermal recycled parts
    pf = min1 + 1.0e-30_R8
    IF (pf .GT. recyc0) THEN
      min2 = recyc0
      CALL PUSHCONTROL1B(0)
    ELSE
      min2 = pf
      CALL PUSHCONTROL1B(1)
    END IF
    pcorf = min2/pf
    pt = recyc0 - pf + 1.0e-30_R8
!
    vt = SQRT(2.0_R8*e_fc*ev/mn)
!     The reflection coefficients below already account for the probability of
!     fast particle reflection. Like in Eirene, we will only pump fast particles
!     once all thermal particles are pumped.
!
!nh 22.02.2020
!     Calculate the flux emitted as molecules. At the moment, molecules will be
!     treated kinetically.
    fna_mol_rec = recycm*pt*fdni*area*0.5_R8
    IF (0.0_R8 .LT. (recyc0*t0-2.0_R8*fna_mol_rec)*fluid_frac_hyb) THEN
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
    END IF
    fenecb = area*fenerecb
    fmomrecpb = pit*fmomrecb
    fmomrectb = pit2*fmomrecb
    fmoxb = area*fmomrectb
    pncb = cosa*isign*area*fmomrecpb
    fmozb = -(cosa*isign*area*fmomrecpb)
    tempb1 = mn*0.5_R8*(1-recycm)*vt**2*fenecb
    ptb = fdni*fluid_frac_hyb*tempb1
    fdnib = pt*fluid_frac_hyb*tempb1
    fluid_frac_hybb = fluid_frac_hybb + pt*fdni*tempb1
    temp0 = cs*cs*cs
    temp = pcorf*re1i/i1l
    tempb1 = -(mn*0.5_R8*fenecb)
    tempb2 = temp0*fdni*fluid_frac_hyb*tempb1/i1l
    tempb = temp*tempb1
    csb = 3*cs**2*fdni*fluid_frac_hyb*tempb
    fdnib = fdnib + fluid_frac_hyb*temp0*tempb
    fluid_frac_hybb = fluid_frac_hybb + fdni*temp0*tempb
    re1ib = pcorf*tempb2
    i1lb = -(temp*tempb2)
    temp = cs*fdni
    tempb1 = -(SIGN(1.0_R8, ux)*nnwwnrecb)
    tempb0 = fluid_frac_hyb*tempb1/i1l
    pcorfb = re1i*tempb2 + rp31i*temp*tempb0
    tempb = pcorf*rp31i*temp*tempb1/i1l
    rp31ib = pcorf*temp*tempb0
    tempb2 = pcorf*rp31i*tempb0
    tempb1 = (1-recycm)*2.0_R8*nnrecb
    fluid_frac_hybb = fluid_frac_hybb + tempb + pt*fdni*tempb1/vt
    tempb0 = fluid_frac_hyb*tempb1/vt
    fdnib = fdnib + cs*tempb2 + pt*tempb0
    temp1 = pcorf*rp21i/i1l
    tempb1 = -(fdni*fluid_frac_hyb*nnrecb/i1l)
    i1lb = i1lb - fluid_frac_hyb*tempb/i1l - temp1*tempb1
    pcorfb = pcorfb + rp21i*tempb1
    rp21ib = pcorf*tempb1
    tempb1 = mn*2.0_R8*(1-recycm)*vt*pncb
    ptb = ptb + fdni*tempb0 + fdni*fluid_frac_hyb*tempb1/3.0_R8
    tempb0 = pt*tempb1/3.0_R8
    fdnib = fdnib + fluid_frac_hyb*tempb0 - fluid_frac_hyb*temp1*nnrecb
    fluid_frac_hybb = fluid_frac_hybb + fdni*tempb0 - fdni*temp1*nnrecb
    temp1 = cs*cs/(3.0_R8*i1l)
    temp = fdni*fluid_frac_hyb
    tempb0 = -(temp1*mn*pncb)
    tempb1 = -(pcorf*rp11i*temp*mn*pncb/(3.0_R8*i1l))
    csb = csb + fdni*tempb2 + 2*cs*tempb1
    i1lb = i1lb - 3.0_R8*temp1*tempb1
    pcorfb = pcorfb + rp11i*temp*tempb0
    rp11ib = pcorf*temp*tempb0
    tempb2 = pcorf*rp11i*tempb0
    fdnib = fdnib + fluid_frac_hyb*tempb2
    fluid_frac_hybb = fluid_frac_hybb + fdni*tempb2
    tempb1 = mn*2.0_R8*(1-recycm)*vt*fmozb
    ptb = ptb + fdni*fluid_frac_hyb*tempb1/3.0_R8
    tempb0 = pt*tempb1/3.0_R8
    temp1 = fdni*fluid_frac_hyb
    temp0 = cs*cs/i1l
    tempb2 = -(temp0*mn*fmozb)
    rmz1ib = pcorf*temp1*tempb2
    tempb1 = pcorf*rmz1i*tempb2
    fdnib = fdnib + fluid_frac_hyb*tempb0 + fluid_frac_hyb*tempb1
    fluid_frac_hybb = fluid_frac_hybb + fdni*tempb0 + fdni*tempb1
    tempb0 = -(pcorf*rmz1i*temp1*mn*fmozb/i1l)
    csb = csb + 2*cs*tempb0
    i1lb = i1lb - temp0*tempb0
    temp = fdni*fluid_frac_hyb
    temp0 = cs*cs/i1l
    tempb = -(mn*SIGN(1.0_R8, ux)*fmoxb)
    tempb0 = pcorf*rmx1i*temp*tempb/i1l
    tempb1 = temp0*tempb
    pcorfb = pcorfb + rmz1i*temp1*tempb2 + rmx1i*temp*tempb1
    rmx1ib = pcorf*temp*tempb1
    tempb2 = pcorf*rmx1i*tempb1
    fdnib = fdnib + fluid_frac_hyb*tempb2
    fluid_frac_hybb = fluid_frac_hybb + fdni*tempb2
    csb = csb + 2*cs*tempb0
    i1lb = i1lb - temp0*tempb0
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      tempb = fluid_frac_hyb*fnnrecb
      fluid_frac_hybb = fluid_frac_hybb + (recyc0*t0-2.0_R8*fna_mol_rec)&
&       *fnnrecb
      recyc0b = recyc0b + t0*tempb
      t0b = t0b + recyc0*tempb
      fna_mol_recb = -(2.0_R8*tempb)
    ELSE
      fna_mol_recb = 0.D0
    END IF
    tempb = recycm*0.5_R8*area*fna_mol_recb
    ptb = ptb + fdni*tempb
    fdnib = fdnib + pt*tempb
    recyc0b = recyc0b + ptb
    pfb = -ptb - min2*pcorfb/pf**2
    min2b = pcorfb/pf
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      recyc0b = recyc0b + min2b
    ELSE
      pfb = pfb + min2b
    END IF
    min1b = pfb
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      recyc0b = recyc0b + min1b
      rc1ib = 0.D0
    ELSE
      tempb = -(min1b/i1l)
      rc1ib = cs*tempb
      csb = csb + rc1i*tempb
      i1lb = i1lb - rc1i*cs*tempb/i1l
    END IF
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) re1ib = 0.D0
    mjb = 0.D0
    tifevb = 0.D0
    shpottaujb = 0.D0
    CALL INTP_3DTABLE00_B(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                   nshpottau, mj, mjb, tifev, tifevb, shpottauj, &
&                   shpottaujb, ilm, ilt, ilsh, re1ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rp31ib = 0.D0
    ff = rp31(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_B(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                   nshpottau, mj, mjb, tifev, tifevb, shpottauj, &
&                   shpottaujb, ilm, ilt, ilsh, rp31ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rp21ib = 0.D0
    ff = rp21(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_B(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                   nshpottau, mj, mjb, tifev, tifevb, shpottauj, &
&                   shpottaujb, ilm, ilt, ilsh, rp21ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rp11ib = 0.D0
    ff = rp11(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_B(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                   nshpottau, mj, mjb, tifev, tifevb, shpottauj, &
&                   shpottaujb, ilm, ilt, ilsh, rp11ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rmz1ib = 0.D0
    ff = rmz1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_B(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                   nshpottau, mj, mjb, tifev, tifevb, shpottauj, &
&                   shpottaujb, ilm, ilt, ilsh, rmz1ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rmx1ib = 0.D0
    ff = rmx1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_B(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                   nshpottau, mj, mjb, tifev, tifevb, shpottauj, &
&                   shpottaujb, ilm, ilt, ilsh, rmx1ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rc1ib = 0.D0
    CALL INTP_3DTABLE_B(tab, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                 nshpottau, mj, mjb, tifev, tifevb, shpottauj, &
&                 shpottaujb, ilm, ilt, ilsh, ip1, ip2, ip3, rc1ib)
    tempb = accel_ion(istra)*qe*shpottaujb/tif
    pofb = pofb + tempb
    phi_appb = phi_appb - tempb
    tifb = tifb + tifevb/ev - (pof-phi_app)*tempb/tif
    abs0b = mjb/cs
    csb = csb - abs0*mjb/cs**2
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      uxb = abs0b
    ELSE
      uxb = -abs0b
    END IF
    IF (.NOT.2.0_R8*(tif/mn) .EQ. 0.D0) tifb = tifb + 2.0_R8*csb/(mn*2.0&
&       *SQRT(2.0_R8*(tif/mn)))
    uzb = 0.D0
    i0lb = 0.D0
    i2lb = 0.D0
    i3lb = 0.D0
    i4lb = 0.D0
    CALL CALCMOMENTSMAXWELLIAN_B(uz, uzb, tif, tifb, mn, i0l, i0lb, i0r&
&                          , i1l, i1lb, i1r, i2l, i2lb, i2r, i3l, i3lb, &
&                          i3r, i4l, i4lb, i4r, i5l, i5r, i6l, i6r)
    CALL POPCONTROL1B(branch)
    IF (branch .NE. 0) uzb = 0.D0
    t0b = t0b + fdnib/area
    upfb = pit2*uxb - pit*cosa*isign*uzb
    plb%ua(icv, isi) = plb%ua(icv, isi) + upfb
  END SUBROUTINE CALCRECYCLEDFLUXES_B

!
  SUBROUTINE CALCRECYCLEDFLUXES(icv, nci, ifc, isign, isn, iscx0, isi, &
&   istra, iwall, phi_app, area, recyc0, recycm, fluid_frac_hyb, pl, tif&
&   , tef, pof, nif, nef, geo, mpg, t0, cosa, sina, use_uy_uz_0, fnnrec&
&   , fmomrec, nnrec, nnwwnrec, fenerec)
!mb   17.07.17
!     This routine calculates the recycled neutral particle, momentum and energy fluxes based
!     on the diffusion approximation (see papers of Niels Horsten).
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFF
    USE B2MOD_NEUTRALS_NAMELIST_DIFF, ONLY : e_fc, accel_ion
    USE B2US_GEO_DIFF
    USE B2US_PLASMA_DIFF
    USE B2US_MAP_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
!     nif = plasma density on face, isn neutral species index
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
!!      type (B2Rates), intent (in) :: rt
    INTEGER, INTENT(IN) :: icv, nci, ifc, isn, iscx0, isi, iwall, istra&
&   , use_uy_uz_0
    REAL(kind=r8), INTENT(IN) :: phi_app, area, recyc0, recycm, pof, nif&
&   , nef, t0, isign, tif, tef, cosa, sina, fluid_frac_hyb
!! fluid_frac_hyb: in fluid neutral cases or standard spatially hybrid cases this multiplier is always equal to 1.
!! only in the generalized form of the spatially hybrid approach, where the fluid and kinetic treatment are mixed
!! on a single target stratum, this value can deviate from 1.
!     Output variables:
!     fmomni: momentum flux of incident neutrals.
    REAL(kind=r8), INTENT(OUT) :: fnnrec, fmomrec, nnrec, nnwwnrec, &
&   fenerec
    EXTERNAL INTP_3DTABLE, INTP_3DTABLE00
!     Internal variables
    REAL(kind=r8) :: cs, mj, shpottauj, pf, pcorf, pt, vt, pit, pit2, mn&
&   , ux, uz, i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r&
&   , i6l, i6r, rc1i, rmx1i, rmz1i, rp11i, rp21i, rp31i, re1i, fmox, &
&   fmoy, fmoz, fmomrecp, fmomrect, pnc, fenec, fdni, INTP_3DTABLE, &
&   INTP_3DTABLE00, tifev, upf, fna_mol_rec
    REAL(kind=r8) :: tab(0:nm, 0:nt, 0:nshpottau), ff(0:1, 0:1, 0:1)
    INTEGER :: is, it, ilt, ilm, ilsh, ip1, ip2, ip3
    INTRINSIC ABS
    INTRINSIC SQRT
    INTRINSIC MAX
    INTRINSIC MIN
    INTRINSIC SIGN
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: min1
    REAL(kind=r8) :: min2
!
    CALL SUBINI('CalcRecycledFluxes')
!     Initialize/calculate some variables
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!mb   plasma velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and drifts
    upf = pl%ua(icv, isi)
    ux = upf*pit2
    uz = -(cosa*isign*upf*pit)
!       ion particle flux density
    fdni = t0/area
!
!     Assume poloidal and radial drifts are zero for momentum and energy
!     equations to reduce the dimensionality of the pre-evaluates integrals
!     of TRIM tables. Only toroidal drift remains!
    IF (use_uy_uz_0 .EQ. 0) uz = 0.0_R8
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!
!     Interpolate tables to find recycling values
!       Isothermal speed of sound and Mach number
    cs = SQRT(2.0_R8*tif/mn)
    IF (ux .GE. 0.) THEN
      abs0 = ux
    ELSE
      abs0 = -ux
    END IF
    mj = abs0/cs
    tifev = tif/ev
    is = recycle_index(isn)
    it = target_index(istra)
!       Shpottau value: sheath_potential devided by tau (Ti/Te)
    shpottauj = accel_ion(istra)*qe*(pof-phi_app)/tif
    tab = rc1(:, :, :, is, it)
    rc1i = INTP_3DTABLE(tab, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh, ip1, ip2, ip3)
    IF (rc1i .LT. 0.0_R8) THEN
      rc1i = 0.0_R8
    ELSE
      rc1i = rc1i
    END IF
!
    ff = rmx1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rmx1i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rmx1i .LT. 0.0_R8) THEN
      rmx1i = 0.0_R8
    ELSE
      rmx1i = rmx1i
    END IF
!
    ff = rmz1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rmz1i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rmz1i .LT. 0.0_R8) THEN
      rmz1i = 0.0_R8
    ELSE
      rmz1i = rmz1i
    END IF
!
    ff = rp11(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rp11i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rp11i .LT. 0.0_R8) THEN
      rp11i = 0.0_R8
    ELSE
      rp11i = rp11i
    END IF
!
    ff = rp21(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rp21i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rp21i .LT. 0.0_R8) THEN
      rp21i = 0.0_R8
    ELSE
      rp21i = rp21i
    END IF
!
    ff = rp31(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rp31i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rp31i .LT. 0.0_R8) THEN
      rp31i = 0.0_R8
    ELSE
      rp31i = rp31i
    END IF
!
    ff = re1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    re1i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (re1i .LT. 0.0_R8) THEN
      re1i = 0.0_R8
    ELSE
      re1i = re1i
    END IF
    IF (rc1i*cs/(-i1l) .GT. recyc0) THEN
      min1 = recyc0
    ELSE
      min1 = rc1i*cs/(-i1l)
    END IF
!
!     Calculate fast and thermal recycled parts
    pf = min1 + 1.0e-30_R8
    IF (pf .GT. recyc0) THEN
      min2 = recyc0
    ELSE
      min2 = pf
    END IF
    pcorf = min2/pf
    pt = recyc0 - pf + 1.0e-30_R8
!
    vt = SQRT(2.0_R8*e_fc*ev/mn)
!     The reflection coefficients below already account for the probability of
!     fast particle reflection. Like in Eirene, we will only pump fast particles
!     once all thermal particles are pumped.
!
!nh 22.02.2020
!     Calculate the flux emitted as molecules. At the moment, molecules will be
!     treated kinetically.
    fna_mol_rec = recycm*pt*fdni*area*0.5_R8
    fna_mol(ifc, isn) = fna_mol(ifc, isn) + fna_mol_rec
    IF (0.0_R8 .LT. (recyc0*t0-2.0_R8*fna_mol_rec)*fluid_frac_hyb) THEN
      fnnrec = (recyc0*t0-2.0_R8*fna_mol_rec)*fluid_frac_hyb
    ELSE
      fnnrec = 0.0_R8
    END IF
!
!
!     Recycled momentum flux densities
    fmox = pcorf*mn*rmx1i*SIGN(1.0_R8, ux)*cs**2*fdni/(-i1l)*&
&     fluid_frac_hyb
!
    fmoy = 0.0_R8
!
    fmoz = pcorf*mn*rmz1i*cs**2*fdni/(-i1l)*fluid_frac_hyb
!
    fmoz = fmoz + 2.0_R8/3.0_R8*mn*(1-recycm)*pt*fdni*vt*fluid_frac_hyb
!       Pressure of recycled neutrals
    pnc = pcorf*mn/3.0_R8*rp11i*cs**2*fdni/(-i1l)*fluid_frac_hyb
!
    pnc = pnc + 2.0_R8/3.0_R8*mn*(1-recycm)*pt*fdni*vt*fluid_frac_hyb
!       Density of recycled neutrals
    nnrec = pcorf*rp21i*fdni/(-i1l)*fluid_frac_hyb
    nnrec = nnrec + (1-recycm)*pt*fdni*2.0_R8/vt*fluid_frac_hyb
!       product of density and toroidal velocity
    nnwwnrec = pcorf*rp31i*cs*SIGN(1.0_R8, ux)*fdni/(-i1l)*&
&     fluid_frac_hyb
!
!     recycled total energy flux density of neutrals
    fenec = 0.5_R8*pcorf*mn*re1i*cs**3*fdni/(-i1l)*fluid_frac_hyb
    fenec = fenec + 0.5_R8*mn*(1-recycm)*pt*fdni*vt**2*fluid_frac_hyb
!
!
!     Calculate recycled poloidal momentum flux of neutrals
    fmomrecp = -(isign*(fmoz*cosa+fmoy*sina)*area) + isign*pnc*area*cosa
    fmomrect = fmox*area
    fmomrec = pit*fmomrecp + pit2*fmomrect
!     Convert energy flux densities in energy fluxes
    fenerec = fenec*area
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCRECYCLEDFLUXES

