!
!! <- '1': = safeguard for diffusion BCs
      CALL CALCREFLECTEDFLUXESDIFFUSION(icv, icn, nci, ifc, isign, isn, &
&                                 iscx0, isi, istra, iwall, phi_app, &
&                                 area, recyc0, recycm, fluid_frac_hyb, &
