!     reflected momentum flux densities
!
!
!       Pressure of reflected neutrals
!
!       Density of reflected neutrals
!       product of density and toroidal velocity
!     reflected total energy flux density of neutrals
!! the Franck-Condon energy must at least be provided by the electrons.
!
!     Calculate reflected poloidal momentum flux of neutrals
!     Convert energy flux densities in energy fluxes
!
! if incident flux is negative (this can happen if the diffusion approximation is used where it is not valid), use one-sided maxw
!ellian anyway
    IF (fnni .LT. 0.0_R8 .AND. safeguard .EQ. 1) THEN
      CALL CALCREFLECTEDFLUXESMAXWELLIAN_B(icv, nci, ifc, isign, isn, &
&                                    istra, iwall, area, recyc0, recyc0b&
&                                    , recycm, fluid_frac_hyb, &
&                                    fluid_frac_hybb, pl, plb, tnf, tnfb&
&                                    , geo, mpg, cosa, sina, fnnrefl, &
&                                    fnnreflb, fmomrefl, fmomreflb, &
&                                    nnrefl, nnreflb, nnwwnrefl, &
&                                    nnwwnreflb, fenerefl, fenereflb, &
&                                    fene_el, fene_elb)
      fmomreflb = 0.D0
      nnreflb = 0.D0
      fene_elb = 0.D0
      fenereflb = 0.D0
      nnwwnreflb = 0.D0
      fnnreflb = 0.D0
    END IF
    fene_elb = area*fene_elb
    feneb = area*fenereflb
    fmomreflpb = pit*fmomreflb
    fmomrefltb = pit2*fmomreflb
    fmoxb = area*fmomrefltb
    pnb = cosa*isign*area*fmomreflpb
    tempb4 = -(isign*area*fmomreflpb)
    fmozb = cosa*tempb4
    fmoyb = sina*tempb4
    tempb4 = -((1-recycm)*e_fc*ev*fene_elb)
    ptb = fnni_nodrifts*fluid_frac_hyb*tempb4
    fnni_nodriftsb = pt*fluid_frac_hyb*tempb4
    fluid_frac_hybb = fluid_frac_hybb + pt*fnni_nodrifts*tempb4
    tempb4 = (1-recycm)*e_fc*ev*feneb
    ptb = ptb + fnni_nodrifts*fluid_frac_hyb*tempb4
    fnni_nodriftsb = fnni_nodriftsb + pt*fluid_frac_hyb*tempb4
    fluid_frac_hybb = fluid_frac_hybb + pt*fnni_nodrifts*tempb4
    temp4 = fluid_frac_hyb/vtot
    temp2 = cs*cs*cs
    temp1 = cs**4
    temp0 = dnndz*re2i/vtot
    tempb4 = mn*0.5_R8*feneb
    tempb5 = pcorf*vcx*temp4*tempb4
    tempb = (nnf*re1i*temp2+temp0*temp1)*tempb4
    pcorfb = vcx*temp4*tempb
    vcxb = pcorf*temp4*tempb
    tempb3 = pcorf*vcx*tempb/vtot
    fluid_frac_hybb = fluid_frac_hybb + tempb3
    vtotb = -(temp4*tempb3)
    nnfb = re1i*temp2*tempb5
    re1ib = nnf*temp2*tempb5
    csb = (3*cs**2*nnf*re1i+4*cs**3*temp0)*tempb5
    tempb0 = temp1*tempb5/vtot
    re2ib = dnndz*tempb0
    temp4 = fluid_frac_hyb/vtot
    temp2 = cs*cs/vtot
    tempb4 = SIGN(1.0_R8, ux)*nnwwnreflb
    tempb5 = pcorf*vcx*temp4*tempb4
    tempb1 = (nnf*rp31i*cs+dnndz*rp32i*temp2)*tempb4
    tempb3 = pcorf*vcx*tempb1/vtot
    rp31ib = nnf*cs*tempb5
    rp32ib = dnndz*temp2*tempb5
    tempb2 = dnndz*rp32i*tempb5/vtot
    vtotb = vtotb - temp0*tempb0 - temp4*tempb3 - temp2*tempb2
    csb = csb + nnf*rp31i*tempb5 + 2*cs*tempb2
    tempb4 = (1-recycm)*2.0_R8*nnreflb
    fluid_frac_hybb = fluid_frac_hybb + tempb3 + pt*fnni_nodrifts*tempb4&
&     /vt
    tempb3 = fluid_frac_hyb*tempb4/vt
    fnni_nodriftsb = fnni_nodriftsb + pt*tempb3
    temp5 = fluid_frac_hyb/vtot
    temp3 = cs/vtot
    tempb2 = (nnf*rp21i+dnndz*rp22i*temp3)*nnreflb
    pcorfb = pcorfb + vcx*temp4*tempb1 + vcx*temp5*tempb2
    vcxb = vcxb + pcorf*temp4*tempb1 + pcorf*temp5*tempb2
    tempb4 = pcorf*vcx*tempb2/vtot
    fluid_frac_hybb = fluid_frac_hybb + tempb4
    vtotb = vtotb - temp5*tempb4
    tempb4 = (1-recycm)*vt*mn*2.0_R8*pnb
    ptb = ptb + fnni_nodrifts*tempb3 + fnni_nodrifts*fluid_frac_hyb*&
&     tempb4/3.0_R8
    tempb3 = pcorf*vcx*temp5*nnreflb
    dnndzb = dnndzb + re2i*tempb0 + rp32i*temp2*tempb5 + rp22i*temp3*&
&     tempb3
    nnfb = nnfb + rp31i*cs*tempb5 + rp21i*tempb3
    rp21ib = nnf*tempb3
    rp22ib = dnndz*temp3*tempb3
    tempb5 = dnndz*rp22i*tempb3/vtot
    tempb3 = pt*tempb4/3.0_R8
    fnni_nodriftsb = fnni_nodriftsb + fluid_frac_hyb*tempb3
    temp5 = fluid_frac_hyb/(3.0_R8*vtot)
    temp2 = cs*cs*cs
    temp1 = dnndz*rp12i/vtot
    tempb0 = (nnf*rp11i*cs**2+temp1*temp2)*mn*pnb
    pcorfb = pcorfb + vcx*temp5*tempb0
    vcxb = vcxb + pcorf*temp5*tempb0
    tempb4 = pcorf*vcx*tempb0/(3.0_R8*vtot)
    fluid_frac_hybb = fluid_frac_hybb + fnni_nodrifts*tempb3 + tempb4
    tempb3 = pcorf*vcx*temp5*mn*pnb
    csb = csb + tempb5 + (2*cs*nnf*rp11i+3*cs**2*temp1)*tempb3
    tempb1 = temp2*tempb3/vtot
    vtotb = vtotb - temp3*tempb5 - 3.0_R8*temp5*tempb4 - temp1*tempb1
    tempb5 = cs**2*tempb3
    dnndzb = dnndzb + rp12i*tempb1
    rp12ib = dnndz*tempb1
    nnfb = nnfb + rp11i*tempb5
    rp11ib = nnf*tempb5
    tempb4 = (1-recycm)*vt*mn*2.0_R8*fmozb
