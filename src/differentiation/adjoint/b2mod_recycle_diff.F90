    USE B2US_GEO_DIFF
    USE B2US_MAP_DIFF
    USE B2US_PLASMA_DIFF
    USE B2MOD_SWITCHES_DIFF
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ncv, nfc, nvx
    TYPE(SWITCHES), INTENT(IN) :: switch
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(GEOMETRY) :: geob
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(MAPPING_DIFF) :: mpgb
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFF) :: plb
    TYPE(B2DERIVATIVES), INTENT(IN) :: dv
    TYPE(B2DERIVATIVES) :: dvb
    TYPE(B2COEFF), INTENT(INOUT) :: co
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFF) :: rtb
    REAL(kind=r8), INTENT(INOUT) :: dnn(nfc, 0:1)
    REAL(kind=r8), INTENT(INOUT) :: dnnb(nfc, 0:1)
    REAL(kind=r8), INTENT(INOUT) :: fluid_frac_hyb(nfc), kin_frac_hyb(&
&   nfc)
    REAL(kind=r8), INTENT(INOUT) :: fluid_frac_hybb(nfc), kin_frac_hybb(&
&   nfc)
!     local variables
    INTEGER :: istra, i, irc1, isn, ifc
    INTEGER, SAVE :: ncall=0
    INTEGER, SAVE :: style_visc=0
    REAL(kind=r8) :: nnvx(nvx), nevx(nvx), tivx(nvx), min_kn(nstrat), &
&   mfp, mfp_out(ncv), kn, kn_ti, kn_ne, kn_out(ncv), kn_ti_out(ncv), &
&   kn_ne_out(ncv), l_ne_out(ncv), l_ti_out(ncv), dti(nfc, 0:1), dne(nfc&
&   , 0:1), l_ti, l_ne
    REAL(kind=r8) :: nnvxb(nvx), mfpb, knb
    INTRINSIC SQRT
!
    INTEGER :: ad_to
    INTEGER :: branch
!! AFN_TODO generalize species index for multi-speciescases?
!! For now KUL AFN models have only been developed for single species cases.
    isn = 0
    CALL INTVERTEX_FWD(ncv, nvx, mpg, geo%vxvol, pl%na(:, isn), nnvx)
!
!     pre-computation of the minimal mean-free path along a boundary stratum
    min_kn(:) = 1.0_R8
    DO istra=1,nstrai
!   ..loop over boundary cells
      DO i=1,mpg%rccvp(istra, 2)
! the guard cell
        irc1 = mpg%rccv(mpg%rccvp(istra, 1)+i-1, 1)
! the guard face
        CALL PUSHREAL8(cutlo, r8/8)
        CALL PUSHREAL8(cutll, r8/8)
        CALL PUSHBOOLEAN(b2mod_math_initialised)
        CALL PUSHREAL4(small_r4_constant, r4/8)
        CALL CALCMINKN(irc1, isn, istra, pl, dv, rt, switch%l_macro_afn&
&                , min_kn, kn, mfp)
!
!c compute Knudsen number
!! gradient length of Ti
!! gradient length of ne
!          L = min(L_ti,L_ne) !! shortest gradient length
        kn = mfp/switch%l_macro_afn
!
!c compute fluid/kinetic fraction
        IF (switch%use_auto_spatial_hyb .EQ. 1 .AND. hyb_type(istra) &
&           .EQ. 'A') THEN
!! 'A' = automatic
          IF (kn .LE. switch%auto_spatial_hyb_kn_1) THEN
            CALL PUSHCONTROL2B(3)
          ELSE IF (kn .GE. switch%auto_spatial_hyb_kn_2) THEN
            CALL PUSHCONTROL2B(2)
          ELSE
            CALL PUSHCONTROL2B(1)
          END IF
        ELSE
          CALL PUSHCONTROL2B(0)
        END IF
      END DO
      CALL PUSHINTEGER4(i - 1)
    END DO
    kin_frac_hybb = 0.D0
    DO istra=nstrai,1,-1
      CALL POPINTEGER4(ad_to)
      DO i=ad_to,1,-1
        CALL POPCONTROL2B(branch)
        IF (branch .LT. 2) THEN
          IF (branch .EQ. 0) THEN
            irc1 = mpg%rccv(mpg%rccvp(istra, 1)+i-1, 1)
            ifc = mpg%cvfc(mpg%cvfcp(irc1, 1))
            kin_frac_hybb(ifc) = 0.D0
            fluid_frac_hybb(ifc) = 0.D0
            knb = 0.D0
          ELSE
            irc1 = mpg%rccv(mpg%rccvp(istra, 1)+i-1, 1)
            ifc = mpg%cvfc(mpg%cvfcp(irc1, 1))
            kin_frac_hybb(ifc) = kin_frac_hybb(ifc) - fluid_frac_hybb(&
&             ifc)
            fluid_frac_hybb(ifc) = 0.D0
            knb = kin_frac_hybb(ifc)/(switch%auto_spatial_hyb_kn_2-&
&             switch%auto_spatial_hyb_kn_1)
            kin_frac_hybb(ifc) = 0.D0
          END IF
        ELSE IF (branch .EQ. 2) THEN
          irc1 = mpg%rccv(mpg%rccvp(istra, 1)+i-1, 1)
          ifc = mpg%cvfc(mpg%cvfcp(irc1, 1))
          kin_frac_hybb(ifc) = 0.D0
          fluid_frac_hybb(ifc) = 0.D0
          knb = 0.D0
        ELSE
          irc1 = mpg%rccv(mpg%rccvp(istra, 1)+i-1, 1)
          ifc = mpg%cvfc(mpg%cvfcp(irc1, 1))
          kin_frac_hybb(ifc) = 0.D0
          fluid_frac_hybb(ifc) = 0.D0
          knb = 0.D0
        END IF
        mfpb = knb/switch%l_macro_afn
        CALL POPREAL4(small_r4_constant, r4/8)
        CALL POPBOOLEAN(b2mod_math_initialised)
        CALL POPREAL8(cutll, r8/8)
        CALL POPREAL8(cutlo, r8/8)
        CALL CALCMINKN_B(irc1, isn, istra, pl, plb, dv, dvb, rt, rtb, &
&                  switch%l_macro_afn, min_kn, kn, mfp, mfpb)
      END DO
    END DO
    CALL GRAD_NT_B(ncv, nfc, nvx, 1, geo, geob, mpg, mpgb, pl%na(:, isn)&
&            , plb%na(:, isn), nnvx, nnvxb, dnn, dnnb)
    CALL INTVERTEX_BWD(ncv, nvx, mpg, geo%vxvol, pl%na(:, isn), plb%na(:&
&                , isn), nnvx, nnvxb)
  END SUBROUTINE PRECOMPUTE_KUL_QUANT_B

!
  SUBROUTINE PRECOMPUTE_KUL_QUANT(ncv, nfc, nvx, switch, geo, mpg, pl, &
&   dv, co, rt, dnn, fluid_frac_hyb, kin_frac_hyb)
    USE B2MOD_TYPES
    USE B2MOD_NEUTRALS_NAMELIST_DIFF
    USE B2US_GEO_DIFF
    USE B2US_MAP_DIFF
    USE B2US_PLASMA_DIFF
    USE B2MOD_SWITCHES_DIFF
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ncv, nfc, nvx
    TYPE(SWITCHES), INTENT(IN) :: switch
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2DERIVATIVES), INTENT(IN) :: dv
    TYPE(B2COEFF), INTENT(INOUT) :: co
    TYPE(B2RATES), INTENT(IN) :: rt
    REAL(kind=r8), INTENT(INOUT) :: dnn(nfc, 0:1)
    REAL(kind=r8), INTENT(INOUT) :: fluid_frac_hyb(nfc), kin_frac_hyb(&
&   nfc)
!     local variables
    INTEGER :: istra, i, irc1, isn, ifc
    INTEGER, SAVE :: ncall=0
    INTEGER, SAVE :: style_visc=0
    REAL(kind=r8) :: nnvx(nvx), nevx(nvx), tivx(nvx), min_kn(nstrat), &
&   mfp, mfp_out(ncv), kn, kn_ti, kn_ne, kn_out(ncv), kn_ti_out(ncv), &
&   kn_ne_out(ncv), l_ne_out(ncv), l_ti_out(ncv), dti(nfc, 0:1), dne(nfc&
&   , 0:1), l_ti, l_ne
    INTRINSIC SQRT
!
    IF (ncall .EQ. 0) CALL IPGETI('b2sihs_style_visc', style_visc)
!! AFN_TODO generalize species index for multi-speciescases?
!! For now KUL AFN models have only been developed for single species cases.
    isn = 0
    CALL INTVERTEX_NODIFF(ncv, nvx, mpg, geo%vxvol, pl%na(:, isn), nnvx)
    CALL INTVERTEX_NODIFF(ncv, nvx, mpg, geo%vxvol, dv%ne, nevx)
    CALL INTVERTEX_NODIFF(ncv, nvx, mpg, geo%vxvol, pl%ti, tivx)
    CALL GRAD_NT_NODIFF(ncv, nfc, nvx, 1, geo, mpg, pl%na(:, isn), nnvx&
&                 , dnn)
    CALL GRAD_NODIFF(ncv, nfc, nvx, 1, geo, mpg, pl%ti, tivx, dti)
    CALL GRAD_NODIFF(ncv, nfc, nvx, 1, geo, mpg, dv%ne, nevx, dne)
!
    mfp_out = 0.0_R8
!     pre-computation of the minimal mean-free path along a boundary stratum
    min_kn(:) = 1.0_R8
    DO istra=1,nstrai
!   ..loop over boundary cells
      DO i=1,mpg%rccvp(istra, 2)
! the guard cell
        irc1 = mpg%rccv(mpg%rccvp(istra, 1)+i-1, 1)
! the guard face
        ifc = mpg%cvfc(mpg%cvfcp(irc1, 1))
        CALL CALCMINKN(irc1, isn, istra, pl, dv, rt, switch%l_macro_afn&
&                , min_kn, kn, mfp)
        mfp_out(irc1) = mfp
!
!c compute Knudsen number
!! gradient length of Ti
        l_ti = pl%ti(irc1)/SQRT(dti(ifc, 0)**2+dti(ifc, 1)**2)
!! gradient length of ne
        l_ne = dv%ne(irc1)/SQRT(dne(ifc, 0)**2+dne(ifc, 1)**2)
!          L = min(L_ti,L_ne) !! shortest gradient length
        kn = mfp/switch%l_macro_afn
        kn_ti = mfp/l_ti
        kn_ne = mfp/l_ne
        kn_out(irc1) = kn
        kn_ti_out(irc1) = kn_ti
        kn_ne_out(irc1) = kn_ne
        l_ti_out(irc1) = l_ti
        l_ne_out(irc1) = l_ne
!
!c compute fluid/kinetic fraction
        IF (switch%use_auto_spatial_hyb .EQ. 1 .AND. hyb_type(istra) &
&           .EQ. 'A') THEN
!! 'A' = automatic
          IF (kn .LE. switch%auto_spatial_hyb_kn_1) THEN
!! fluid treatment
            fluid_frac_hyb(ifc) = 1.0_R8
            kin_frac_hyb(ifc) = 0.0_R8
          ELSE IF (kn .GE. switch%auto_spatial_hyb_kn_2) THEN
!! kinetic treatment
            fluid_frac_hyb(ifc) = 0.0_R8
            kin_frac_hyb(ifc) = 1.0_R8
          ELSE
!! avoid discontinuities if a target face switches from fluid to kinetic. Linear interpolation in betweeen auto_Kn_1 and auto_Kn_
!2
            kin_frac_hyb(ifc) = (kn-switch%auto_spatial_hyb_kn_1)/(&
&             switch%auto_spatial_hyb_kn_2-switch%auto_spatial_hyb_kn_1)
            fluid_frac_hyb(ifc) = 1.0_R8 - kin_frac_hyb(ifc)
          END IF
        ELSE
!! keep scaling factors both equal to 1.
          fluid_frac_hyb(ifc) = 1.0_R8
          kin_frac_hyb(ifc) = 1.0_R8
        END IF
      END DO
!
!c  choose the type of neutral reflection boundary condition, based on the minimum Kn along that boundary (only used if maxw(istr
!a) = 3).
      IF (min_kn(istra) .LT. switch%kn_b1) THEN
        maxw_eff(istra) = 0
      ELSE IF (min_kn(istra) .GT. switch%kn_b2) THEN
        maxw_eff(istra) = 1
      END IF
    END DO
!         do nothing to prevent oscillations if minimum Kn is just around Kn_b1
    IF (switch%afn_out .NE. 0) THEN
      CALL MY_OUT_US(70, ncv, 0, mfp_out, 'AFN_mfp')
      CALL MY_OUT_US(70, ncv, 0, kn_out, 'AFN_Kn')
      CALL MY_OUT_US(70, ncv, 0, kn_ti_out, 'AFN_Kn_ti')
      CALL MY_OUT_US(70, ncv, 0, kn_ne_out, 'AFN_Kn_ne')
      CALL MY_OUT_US(70, ncv, 0, l_ti_out, 'L_ti')
      CALL MY_OUT_US(70, ncv, 0, l_ne_out, 'L_ne')
    END IF
!
    ncall = ncall + 1
    RETURN
  END SUBROUTINE PRECOMPUTE_KUL_QUANT

