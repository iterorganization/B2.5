!  Differentiation of calcincidentfluxesdiffusion in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: int4l int1l int2l int3l int0l
!                fnni tef feneni nni nnwwni tif dnndy dnndz nef
!                *(rt.rlcx) *(rt.rlsa) fmomni *(pl.na) *(pl.ua)
!                tnf
!   with respect to varying inputs: int4l int1l int2l int3l int0l
!                tef tif dnndy dnndz nef *(rt.rlcx) *(rt.rlsa)
!                *(pl.na) *(pl.ua) tnf
!   Plus diff mem management of: geo.cvbb:in rt.rlcx:in rt.rlsa:in
!                pl.na:in pl.ua:in
!
!
  SUBROUTINE CALCINCIDENTFLUXESDIFFUSION_B(icv, icn, ifc, isign, isn, &
&   iscx0, isi, istra, phi_app, area, pl, plb, tif, tifb, tnf, tnfb, tef&
&   , tefb, pof, nef, nefb, geo, geob, rt, rtb, dnndy, dnndyb, dnndz, &
&   dnndzb, cosa, sina, use_uy_uz_0, use_uy_uz_1, safeguard, fnni, fnnib&
&   , fmomni, fmomnib, nni, nnib, nnwwni, nnwwnib, feneni, fenenib)
!     This routine calculates the incident neutral particle,momentum and energy fluxes based
!     on the diffusion approximation (see papers of Niels Horsten)
! main reference:
!   Horsten, Niels, Giovanni Samaey, and M. Baelmans.
!  "Development and assessment of 2D fluid neutral models that include atomic databases and a microscopic reflection model."
!   Nuclear Fusion 57.11 (2017): 116043.
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFF
    USE B2US_GEO_DIFF
    USE B2US_PLASMA_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFF) :: plb
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(GEOMETRY) :: geob
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFF) :: rtb
    INTEGER, INTENT(IN) :: icv, icn, ifc, isn, iscx0, isi, istra, &
&   use_uy_uz_0, use_uy_uz_1, safeguard
    REAL(kind=r8), INTENT(IN) :: phi_app, area, tnf, tif, tef, pof, nef&
&   , isign, dnndy, dnndz, cosa, sina
    REAL(kind=r8) :: tnfb, tifb, tefb, nefb, dnndyb, dnndzb
!     Output variables:
!     fnni: incident neutral flux
!     fmomni: momentum flux of incident neutrals.
    REAL(kind=r8) :: fnni, fmomni, nni, nnwwni, feneni
    REAL(kind=r8) :: fnnib, fmomnib, nnib, nnwwnib, fenenib
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, pit, pit2, mn, nnf, ux, uy, uz, &
&   i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r&
&   , fmox, fmoy, fmoz, fmomnip, fmomnit, pn, t_av, upf
    REAL(kind=r8) :: vcxb, vionb, vtotb, nnfb, uxb, uyb, uzb, i0lb, i1lb&
&   , i2lb, i3lb, i4lb, fmoxb, fmoyb, fmozb, fmomnipb, fmomnitb, pnb, &
&   t_avb, upfb
    INTEGER :: ic
    INTRINSIC ABS
    INTRINSIC LOG
    REAL(r8) :: arg1
    REAL(r8) :: arg1b
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result1b
    REAL(kind=r8) :: temp
    REAL(kind=r8) :: temp0
    REAL(kind=r8) :: tempb
    REAL(kind=r8) :: tempb0
    REAL(kind=r8) :: temp1
    REAL(kind=r8) :: tempb1
    REAL(kind=r8) :: temp2
    REAL(kind=r8) :: tempb2
    REAL(kind=r8) :: temp3
    REAL(kind=r8) :: tempb3
    REAL(r8) :: temp4
    REAL(r8) :: tempb4
    REAL(r8) :: tempb5
    REAL(r8) :: temp5
    REAL(r8) :: temp6
    REAL(r8) :: tempb6
    REAL(r8) :: tempb7
    REAL(r8) :: temp7
    REAL(r8) :: temp8
    REAL(r8) :: tempb8
    REAL(r8) :: tempb9
    REAL(kind=r8) :: temp9
    REAL(kind=r8) :: tempb10
    REAL(kind=r8) :: tempb11
    REAL(kind=r8) :: temp10
    REAL(kind=r8) :: temp11
    REAL(kind=r8) :: tempb12
    REAL(kind=r8) :: tempb13
    REAL(kind=r8) :: tempb14
    INTEGER :: branch
!
!     Initialize/calculate some variables
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!mb   plasma velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and drifts
    upf = pl%ua(icn, isi)
    ux = upf*pit2
    uy = -(sina*isign*upf*pit)
    uz = -(cosa*isign*upf*pit)
!
!     Calculate rate coefficients at boundary
    vcx = 0.0_R8
    DO ic=0,pl%ns-1
      t_av = (tif+tnf)/2.0_R8
      CALL PUSHREAL8(arg1, r8/8)
      arg1 = rt%rlcx(icv, 0, ic, iscx0) + rt%rlcx(icv, 1, ic, iscx0)*LOG&
&       (t_av/(am(isn)*ev))
      CALL PUSHREAL8(cutlo, r8/8)
      CALL PUSHREAL8(cutll, r8/8)
      CALL PUSHBOOLEAN(b2mod_math_initialised)
      CALL PUSHREAL4(small_r4_constant, r4/8)
      CALL PUSHREAL8(result1, r8/8)
      result1 = EXPU(arg1)
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    CALL PUSHREAL8(arg1, r8/8)
    arg1 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*LOG(tef/ev)
    CALL PUSHREAL8(cutlo, r8/8)
    CALL PUSHREAL8(cutll, r8/8)
    CALL PUSHBOOLEAN(b2mod_math_initialised)
    CALL PUSHREAL4(small_r4_constant, r4/8)
    CALL PUSHREAL8(result1, r8/8)
    result1 = EXPU(arg1)
    vion = result1*nef
    vtot = vion + vcx
!
    IF (use_uy_uz_0 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Incident particle flux density
!
    fnni = vcx/vtot**2*(dnndy*uy*i1l+dnndz*i2l) - nnf*vcx/vtot*i1l
!
!     Assume poloidal and radial drifts are zero for momentum and energy
!     equations to reduce the dimensionality of the pre-evaluates integrals
!     of TRIM tables. Only toroidal drift remains!
    IF (use_uy_uz_1 .EQ. 0) THEN
      CALL PUSHREAL8(uy, r8/8)
      uy = 0.0_R8
      CALL PUSHREAL8(uz, r8/8)
      uz = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL PUSHREAL8(i2l, r8/8)
    CALL PUSHREAL8(i1l, r8/8)
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Incident momentum flux densities
!
!
!
!       Pressure of incident neutrals (total including contribution from the drift velocity)
!
!       Density of incident neutrals
    nni = vcx/vtot*(nnf*i0l-1.0_R8/vtot*(dnndy*uy*i0l+dnndz*i1l))
!
!
!     Incident total energy flux density of neutrals
!
!     Convert particle flux densities in fluxes
    fnni = fnni*area
!
!     Calculate incident poloidal momentum flux of neutrals
!     calculate parallel momentum boundary flux
!     Convert energy flux densities in net energy flux contributions
!
! if incident flux is negative (this can happen if the diffusion approximation is used where it is not valid), use one-sided maxw
!ellian anyway
    IF (fnni .LT. 0.0_R8 .AND. safeguard .EQ. 1) THEN
      CALL CALCINCIDENTFLUXESMAXWELLIAN_B(icv, ifc, isn, isign, istra, &
&                                   area, pl, plb, tnf, tnfb, geo, cosa&
&                                   , sina, fnni, fnnib, fmomni, fmomnib&
&                                   , nni, nnib, nnwwni, nnwwnib, feneni&
&                                   , fenenib)
      fnnib = 0.D0
      fenenib = 0.D0
      nnib = 0.D0
      nnwwnib = 0.D0
      fmomnib = 0.D0
    END IF
    fenenib = -(area*fenenib)
    fmomnipb = pit*fmomnib
    fmomnitb = pit2*fmomnib
    fmoxb = -(area*fmomnitb)
    tempb14 = isign*area*fmomnipb
    pnb = cosa*isign*area*fmomnipb
    fmozb = cosa*tempb14
    fmoyb = sina*tempb14
    fnnib = area*fnnib
    temp1 = uy*i1l
    temp8 = 2.0_R8*tif + mn*(ux*ux+uy*uy)/2.0_R8
    temp7 = temp8*temp1 + mn*uy*i3l/2.0_R8
    temp = tif + mn*(ux*ux+uy*uy)/2.0_R8
    temp9 = temp*i2l + mn*i4l/2.0_R8
    temp2 = (dnndy*temp7+dnndz*temp9)/vtot
    temp10 = tif + mn*(ux*ux+uy*uy)/2.0_R8
    temp11 = temp10*i1l + mn*i3l/2.0_R8
    tempb3 = vcx*fenenib/vtot
    tempb14 = (temp2-nnf*temp11)*fenenib/vtot
    tempb2 = tempb3/vtot
    vtotb = -(vcx*tempb14/vtot) - temp2*tempb2
    nnfb = -(temp11*tempb3)
    tempb12 = -(nnf*tempb3)
    tempb13 = mn*i1l*tempb12/2.0_R8
    dnndyb = dnndyb + temp7*tempb2
    tempb9 = dnndy*tempb2
    dnndzb = dnndzb + temp9*tempb2
    tempb10 = dnndz*tempb2
    tifb = tifb + i1l*tempb12 + i2l*tempb10 + 2.0_R8*temp1*tempb9
    tempb11 = mn*i2l*tempb10/2.0_R8
    i4lb = mn*tempb10/2.0_R8
    tempb = mn*temp1*tempb9/2.0_R8
    uxb = 2*ux*tempb13 + 2*ux*tempb11 + 2*ux*tempb
    nnib = nnib + ux*nnwwnib
    temp2 = (dnndy*uy*i0l+dnndz*i1l)/vtot
    tempb3 = vcx*nnib/vtot
    tempb1 = (nnf*i0l-temp2)*nnib/vtot
    nnfb = nnfb + i0l*tempb3
    tempb2 = -(tempb3/vtot)
    i1lb = temp10*tempb12 + uy*temp8*tempb9 + dnndz*tempb2
    uyb = 2*uy*tempb13 + 2*uy*tempb11 + (i1l*temp8+i3l*mn/2.0_R8)*tempb9&
&     + 2*uy*tempb + dnndy*i0l*tempb2
    vtotb = vtotb - vcx*tempb1/vtot - temp2*tempb2
    i0lb = nnf*tempb3 + dnndy*uy*tempb2
    dnndyb = dnndyb + uy*i0l*tempb2
    dnndzb = dnndzb + i1l*tempb2
    temp3 = vcx/(3.0_R8*vtot)
    temp4 = 2.0_R8*tif/mn + ux*ux + uy*uy
    temp5 = 4.0_R8*tif*uy/mn + ux*ux*uy + uy*uy*uy
    temp6 = temp5*i0l + uy*i2l
    temp7 = 2.0_R8*tif/mn + ux*ux + uy*uy
    temp1 = (dnndy*temp6+dnndz*(temp7*i1l+i3l))/vtot
    tempb2 = temp3*mn*pnb
    tempb3 = (nnf*(temp4*i0l+i2l)-temp1)*mn*pnb/(3.0_R8*vtot)
    vcxb = tempb14 + tempb1 + tempb3
    tempb4 = nnf*tempb2
    tempb1 = -(tempb2/vtot)
    vtotb = vtotb - 3.0_R8*temp3*tempb3 - temp1*tempb1
    tempb6 = dnndy*tempb1
    tempb8 = dnndz*tempb1
    tempb7 = i0l*tempb6
    i0lb = i0lb + temp5*tempb6 + temp4*tempb4
    tempb = 4.0_R8*tempb7/mn
    tempb5 = i0l*tempb4
    temp2 = (dnndy*uy*i2l+dnndz*i3l)/vtot
    tempb3 = vcx*mn*fmozb/vtot
    nnfb = nnfb + (temp4*i0l+i2l)*tempb2 - i2l*tempb3
    tempb2 = tempb3/vtot
    i3lb = mn*tempb12/2.0_R8 + uy*mn*tempb9/2.0_R8 + tempb8 + dnndz*&
&     tempb2
    i2lb = temp*tempb10 + uy*tempb6 + tempb4 + dnndy*uy*tempb2 - nnf*&
&     tempb3
    dnndyb = dnndyb + temp6*tempb1 + uy*i2l*tempb2
    dnndzb = dnndzb + (temp7*i1l+i3l)*tempb1 + i3l*tempb2
    tempb9 = i1l*tempb8
    uyb = uyb + 2*uy*tempb9 + i2l*tempb6 + (ux**2+3*uy**2)*tempb7 + tif*&
&     tempb + 2*uy*tempb5 + dnndy*i2l*tempb2
    tempb1 = (temp2-nnf*i2l)*mn*fmozb/vtot
    vcxb = vcxb + tempb1
    vtotb = vtotb - vcx*tempb1/vtot - temp2*tempb2
    temp0 = dnndy*i1l
    temp = tif/mn + uy*uy
    temp1 = (temp*temp0+dnndz*uy*i2l)/vtot
    tempb2 = vcx*mn*fmoyb/vtot
    tempb3 = (temp1-nnf*uy*i1l)*mn*fmoyb/vtot
    tempb1 = tempb2/vtot
    i1lb = i1lb + temp7*tempb8 + dnndy*temp*tempb1 - nnf*uy*tempb2
    tifb = tifb + 2.0_R8*tempb9/mn + uy*tempb + 2.0_R8*tempb5/mn + temp0&
&     *tempb1/mn
    vtotb = vtotb - vcx*tempb3/vtot - temp1*tempb1
    uyb = uyb + (2*uy*temp0+dnndz*i2l)*tempb1 - nnf*i1l*tempb2
    dnndyb = dnndyb + i1l*temp*tempb1
    dnndzb = dnndzb + uy*i2l*tempb1
    i2lb = i2lb + dnndz*uy*tempb1
    temp = uy*i1l
    temp0 = (dnndy*ux*temp+dnndz*ux*i2l)/vtot
    tempb1 = vcx*mn*fmoxb/vtot
    nnfb = nnfb - uy*i1l*tempb2 - ux*i1l*tempb1
    tempb2 = (temp0-nnf*ux*i1l)*mn*fmoxb/vtot
    vcxb = vcxb + tempb3 + tempb2
    tempb = tempb1/vtot
    uxb = uxb + nni*nnwwnib + 2*ux*tempb9 + 2*ux*uy*tempb7 + 2*ux*tempb5&
&     + (dnndy*temp+dnndz*i2l)*tempb - nnf*i1l*tempb1
    vtotb = vtotb - vcx*tempb2/vtot - temp0*tempb
    dnndyb = dnndyb + ux*temp*tempb
    tempb0 = dnndy*ux*tempb
    i1lb = i1lb + uy*tempb0 - nnf*ux*tempb1
    dnndzb = dnndzb + ux*i2l*tempb
    i2lb = i2lb + dnndz*ux*tempb
    uyb = uyb + i1l*tempb0
    CALL POPREAL8(i1l, r8/8)
    CALL POPREAL8(i2l, r8/8)
    uzb = 0.D0
    CALL CALCMOMENTSMAXWELLIAN_B(uz, uzb, tif, tifb, mn, i0l, i0lb, i0r&
&                          , i1l, i1lb, i1r, i2l, i2lb, i2r, i3l, i3lb, &
&                          i3r, i4l, i4lb, i4r, i5l, i5r, i6l, i6r)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      CALL POPREAL8(uz, r8/8)
      CALL POPREAL8(uy, r8/8)
      uyb = 0.D0
      uzb = 0.D0
    END IF
    temp = vcx/(vtot*vtot)
    temp1 = i1l/vtot
    tempb = temp*fnnib
    tempb0 = (dnndy*uy*i1l+dnndz*i2l)*fnnib/vtot**2
    nnfb = nnfb - vcx*temp1*fnnib
    vcxb = vcxb + tempb0 - nnf*temp1*fnnib
    tempb1 = -(nnf*vcx*fnnib/vtot)
    i1lb = tempb1 + dnndy*uy*tempb
    vtotb = vtotb - temp1*tempb1 - 2*vtot*temp*tempb0
    dnndyb = dnndyb + uy*i1l*tempb
    uyb = uyb + dnndy*i1l*tempb
    dnndzb = dnndzb + i2l*tempb
    i2lb = dnndz*tempb
    i0lb = 0.D0
    i3lb = 0.D0
    i4lb = 0.D0
    CALL CALCMOMENTSMAXWELLIAN_B(uz, uzb, tif, tifb, mn, i0l, i0lb, i0r&
&                          , i1l, i1lb, i1r, i2l, i2lb, i2r, i3l, i3lb, &
&                          i3r, i4l, i4lb, i4r, i5l, i5r, i6l, i6r)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      uyb = 0.D0
      uzb = 0.D0
    END IF
    vionb = vtotb
    vcxb = vcxb + vtotb
    result1b = nef*vionb
    nefb = nefb + result1*vionb
    CALL POPREAL8(result1, r8/8)
    CALL POPREAL4(small_r4_constant, r4/8)
    CALL POPBOOLEAN(b2mod_math_initialised)
    CALL POPREAL8(cutll, r8/8)
    CALL POPREAL8(cutlo, r8/8)
    CALL EXPU_B(arg1, arg1b, result1b)
    CALL POPREAL8(arg1, r8/8)
    rtb%rlsa(icv, 0, isn) = rtb%rlsa(icv, 0, isn) + arg1b
    rtb%rlsa(icv, 1, isn) = rtb%rlsa(icv, 1, isn) + LOG(tef/ev)*arg1b
    tefb = tefb + rt%rlsa(icv, 1, isn)*arg1b/tef
    DO ic=pl%ns-1,0,-1
      plb%na(icv, ic) = plb%na(icv, ic) + result1*vcxb
      result1b = pl%na(icv, ic)*vcxb
      CALL POPREAL8(result1, r8/8)
      CALL POPREAL4(small_r4_constant, r4/8)
      CALL POPBOOLEAN(b2mod_math_initialised)
      CALL POPREAL8(cutll, r8/8)
      CALL POPREAL8(cutlo, r8/8)
      CALL EXPU_B(arg1, arg1b, result1b)
      t_av = (tif+tnf)/2.0_R8
      CALL POPREAL8(arg1, r8/8)
      temp = t_av/(am(isn)*ev)
      rtb%rlcx(icv, 0, ic, iscx0) = rtb%rlcx(icv, 0, ic, iscx0) + arg1b
      rtb%rlcx(icv, 1, ic, iscx0) = rtb%rlcx(icv, 1, ic, iscx0) + LOG(&
&       temp)*arg1b
      t_avb = rt%rlcx(icv, 1, ic, iscx0)*arg1b/(am(isn)*ev*temp)
      tifb = tifb + t_avb/2.0_R8
      tnfb = tnfb + t_avb/2.0_R8
    END DO
    upfb = pit2*uxb - pit*cosa*isign*uzb - pit*sina*isign*uyb
    plb%ua(icn, isi) = plb%ua(icn, isi) + upfb
    plb%na(icv, isn) = plb%na(icv, isn) + nnfb
  END SUBROUTINE CALCINCIDENTFLUXESDIFFUSION_B

!
!
  SUBROUTINE CALCINCIDENTFLUXESDIFFUSION(icv, icn, ifc, isign, isn, &
&   iscx0, isi, istra, phi_app, area, pl, tif, tnf, tef, pof, nef, geo, &
&   rt, dnndy, dnndz, cosa, sina, use_uy_uz_0, use_uy_uz_1, safeguard, &
&   fnni, fmomni, nni, nnwwni, feneni)
!     This routine calculates the incident neutral particle,momentum and energy fluxes based
!     on the diffusion approximation (see papers of Niels Horsten)
! main reference:
!   Horsten, Niels, Giovanni Samaey, and M. Baelmans.
!  "Development and assessment of 2D fluid neutral models that include atomic databases and a microscopic reflection model."
!   Nuclear Fusion 57.11 (2017): 116043.
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFF
    USE B2US_GEO_DIFF
    USE B2US_PLASMA_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(B2RATES), INTENT(IN) :: rt
    INTEGER, INTENT(IN) :: icv, icn, ifc, isn, iscx0, isi, istra, &
&   use_uy_uz_0, use_uy_uz_1, safeguard
    REAL(kind=r8), INTENT(IN) :: phi_app, area, tnf, tif, tef, pof, nef&
&   , isign, dnndy, dnndz, cosa, sina
!     Output variables:
!     fnni: incident neutral flux
!     fmomni: momentum flux of incident neutrals.
    REAL(kind=r8), INTENT(OUT) :: fnni, fmomni, nni, nnwwni, feneni
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, pit, pit2, mn, nnf, ux, uy, uz, &
&   i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r&
&   , fmox, fmoy, fmoz, fmomnip, fmomnit, pn, t_av, upf
    INTEGER :: ic
    INTRINSIC ABS
    INTRINSIC LOG
    REAL(r8) :: arg1
    REAL(kind=r8) :: result1
!
    CALL SUBINI('CalcIncidentFluxesDiffusion')
!     Initialize/calculate some variables
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!mb   plasma velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and drifts
    upf = pl%ua(icn, isi)
    ux = upf*pit2
    uy = -(sina*isign*upf*pit)
    uz = -(cosa*isign*upf*pit)
!
!     Calculate rate coefficients at boundary
    vcx = 0.0_R8
    DO ic=0,pl%ns-1
      t_av = (tif+tnf)/2.0_R8
      arg1 = rt%rlcx(icv, 0, ic, iscx0) + rt%rlcx(icv, 1, ic, iscx0)*LOG&
&       (t_av/(am(isn)*ev))
      result1 = EXPU(arg1)
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*LOG(tef/ev)
    result1 = EXPU(arg1)
    vion = result1*nef
    vtot = vion + vcx
!
    IF (use_uy_uz_0 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Incident particle flux density
!
    fnni = vcx/vtot**2*(dnndy*uy*i1l+dnndz*i2l) - nnf*vcx/vtot*i1l
!
!     Assume poloidal and radial drifts are zero for momentum and energy
!     equations to reduce the dimensionality of the pre-evaluates integrals
!     of TRIM tables. Only toroidal drift remains!
    IF (use_uy_uz_1 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Incident momentum flux densities
    fmox = mn*vcx/vtot*(-(nnf*ux*i1l)+1.0_R8/vtot*(dnndy*ux*uy*i1l+dnndz&
&     *ux*i2l))
!
    fmoy = mn*vcx/vtot*(-(nnf*uy*i1l)+1.0_R8/vtot*(dnndy*(tif/mn+uy**2)*&
&     i1l+dnndz*uy*i2l))
!
    fmoz = mn*vcx/vtot*(-(nnf*i2l)+1.0_R8/vtot*(dnndy*uy*i2l+dnndz*i3l))
!
!       Pressure of incident neutrals (total including contribution from the drift velocity)
    pn = mn/3.0_R8*vcx/vtot*(nnf*((2.0_R8*tif/mn+ux**2+uy**2)*i0l+i2l)-&
&     1.0_R8/vtot*(dnndy*((4.0_R8*tif/mn*uy+ux**2*uy+uy**3)*i0l+uy*i2l)+&
&     dnndz*((2.0_R8*tif/mn+ux**2+uy**2)*i1l+i3l)))
!
!       Density of incident neutrals
    nni = vcx/vtot*(nnf*i0l-1.0_R8/vtot*(dnndy*uy*i0l+dnndz*i1l))
!
    nnwwni = nni*ux
!
!     Incident total energy flux density of neutrals
    feneni = vcx/vtot*(-(nnf*((tif+mn/2.0_R8*(ux**2+uy**2))*i1l+mn/&
&     2.0_R8*i3l))+1.0_R8/vtot*(dnndy*((2.0_R8*tif+mn/2.0_R8*(ux**2+uy**&
&     2))*uy*i1l+mn/2.0_R8*uy*i3l)+dnndz*((tif+mn/2.0_R8*(ux**2+uy**2))*&
&     i2l+mn/2.0_R8*i4l)))
!
!     Convert particle flux densities in fluxes
    fnni = fnni*area
!
!     Calculate incident poloidal momentum flux of neutrals
    fmomnip = isign*(fmoz*cosa+fmoy*sina)*area + isign*pn*area*cosa
    fmomnit = -(fmox*area)
!     calculate parallel momentum boundary flux
    fmomni = pit*fmomnip + pit2*fmomnit
!     Convert energy flux densities in net energy flux contributions
    feneni = -(feneni*area)
!
! if incident flux is negative (this can happen if the diffusion approximation is used where it is not valid), use one-sided maxw
!ellian anyway
    IF (fnni .LT. 0.0_R8 .AND. safeguard .EQ. 1) CALL &
&     CALCINCIDENTFLUXESMAXWELLIAN(icv, ifc, isn, isign, istra, area, pl&
&                            , tnf, geo, cosa, sina, fnni, fmomni, nni, &
&                            nnwwni, feneni)
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCINCIDENTFLUXESDIFFUSION

!  Differentiation of calcincidentfluxeskn in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: int4l int1l int2l int3l int0l
!                fnni tef feneni nni nnwwni tif dnndy dnndz nef
!                *(rt.rlcx) *(rt.rlsa) fmomni *(pl.na) *(pl.ua)
!                tnf
!   with respect to varying inputs: int4l int1l int2l int3l int0l
!                tef tif dnndy dnndz nef *(rt.rlcx) *(rt.rlsa)
!                *(pl.na) *(pl.ua) tnf
!   Plus diff mem management of: geo.cvbb:in rt.rlcx:in rt.rlsa:in
!                pl.na:in pl.ua:in
!
  SUBROUTINE CALCINCIDENTFLUXESKN_B(kn_b1, kn_b2, icv, icn, ifc, isign, &
&   isn, iscx0, isi, istra, phi_app, area, pl, plb, tif, tifb, tnf, tnfb&
&   , tef, tefb, pof, nef, nefb, geo, geob, rt, rtb, dnndy, dnndyb, &
&   dnndz, dnndzb, cosa, sina, use_uy_uz_0, use_uy_uz_1, l, fnni, fnnib&
&   , fmomni, fmomnib, nni, nnib, nnwwni, nnwwnib, feneni, fenenib)
!     This routine is an interface to CalcIncidentFluxesMaxwellian and CalcIncidentFluxesDiffusion
!     It will decide which one to use based on the Knudsen number (Kn) of the neutral atoms.
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFF
    USE B2US_GEO_DIFF
    USE B2US_PLASMA_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFF) :: plb
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(GEOMETRY) :: geob
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFF) :: rtb
    INTEGER, INTENT(IN) :: icv, icn, ifc, isn, iscx0, isi, istra, &
&   use_uy_uz_0, use_uy_uz_1
    REAL(kind=r8), INTENT(IN) :: phi_app, area, tnf, tif, tef, pof, nef&
&   , isign, dnndy, dnndz, cosa, sina, kn_b1, kn_b2, l
    REAL(kind=r8) :: tnfb, tifb, tefb, nefb, dnndyb, dnndzb
    REAL(kind=r8) :: t_av
    REAL(kind=r8) :: t_avb
!     Output variables
    REAL(kind=r8) :: fnni, fmomni, nni, nnwwni, feneni
    REAL(kind=r8) :: fnnib, fmomnib, nnib, nnwwnib, fenenib
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, mn, mfp, c_th, kn
    REAL(kind=r8) :: vcxb, vionb, vtotb, mfpb, c_thb, knb
    INTEGER :: ic
    REAL(kind=r8) :: fnnim, nnwwnim, nnim, fenenim, fmomnim
    REAL(kind=r8) :: fnnimb, nnwwnimb, nnimb, fenenimb, fmomnimb
    REAL(kind=r8) :: fnnid, nnwwnid, nnid, fenenid, fmomnid
    REAL(kind=r8) :: fnnidb, nnwwnidb, nnidb, fenenidb, fmomnidb
    INTRINSIC LOG
    INTRINSIC SQRT
    REAL(r8) :: arg1
    REAL(r8) :: arg1b
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result1b
    REAL(kind=r8) :: temp
!
!
!     Calculate rate coefficients at boundary
    mn = am(isn)*mp
    vcx = 0.0_R8
    DO ic=0,pl%ns-1
      t_av = (tif+tnf)/2.0_R8
      CALL PUSHREAL8(arg1, r8/8)
      arg1 = rt%rlcx(icv, 0, ic, iscx0) + rt%rlcx(icv, 1, ic, iscx0)*LOG&
&       (t_av/(am(isn)*ev))
      CALL PUSHREAL8(cutlo, r8/8)
      CALL PUSHREAL8(cutll, r8/8)
      CALL PUSHBOOLEAN(b2mod_math_initialised)
      CALL PUSHREAL4(small_r4_constant, r4/8)
      CALL PUSHREAL8(result1, r8/8)
      result1 = EXPU(arg1)
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    CALL PUSHREAL8(arg1, r8/8)
    arg1 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*LOG(tef/ev)
    CALL PUSHREAL8(cutlo, r8/8)
    CALL PUSHREAL8(cutll, r8/8)
    CALL PUSHBOOLEAN(b2mod_math_initialised)
    CALL PUSHREAL4(small_r4_constant, r4/8)
    CALL PUSHREAL8(result1, r8/8)
    result1 = EXPU(arg1)
    vion = result1*nef
    vtot = vion + vcx
    c_th = SQRT(tnf/mn)
    mfp = c_th/vtot
!! L = macroscopic length scale
    kn = mfp/l
!
!     if Kn is low, we use the diffusion approximation
!     if Kn is larger, we use the drifting maxwellian approximation
!     if the Kn is in between, we use a linear interpolation
    IF (kn .LT. kn_b1) THEN
      CALL CALCINCIDENTFLUXESDIFFUSION_B(icv, icn, ifc, isign, isn, &
&                                  iscx0, isi, istra, phi_app, area, pl&
&                                  , plb, tif, tifb, tnf, tnfb, tef, &
&                                  tefb, pof, nef, nefb, geo, geob, rt, &
&                                  rtb, dnndy, dnndyb, dnndz, dnndzb, &
&                                  cosa, sina, use_uy_uz_0, use_uy_uz_1&
&                                  , 1, fnni, fnnib, fmomni, fmomnib, &
&                                  nni, nnib, nnwwni, nnwwnib, feneni, &
&                                  fenenib)
      knb = 0.D0
    ELSE IF (kn .GT. kn_b2) THEN
      CALL CALCINCIDENTFLUXESMAXWELLIAN_B(icv, ifc, isn, isign, istra, &
