        END DO
        itm = itm + 1
        target_index(istra) = itm
        target_code(itm) = surf_mat(istra)
 6      WRITE(*, '(1x,i3,1x,i3,1x,a)') istra, target_index(istra), &
&       target_code(target_index(istra))
      END DO
