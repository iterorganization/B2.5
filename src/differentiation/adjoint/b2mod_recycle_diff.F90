  SUBROUTINE CALCREFLECTEDFLUXESMAXWELLIAN_B(icv, nci, ifc, isign, isn, &
&   istra, iwall, area, recyc0, recyc0b, recycm, fluid_frac_hyb, &
&   fluid_frac_hybb, pl, plb, tf, tfb, geo, mpg, cosa, sina, fnnrefl, &
&   fnnreflb, fmomrefl, fmomreflb, nnrefl, nnreflb, nnwwnrefl, &
&   nnwwnreflb, fenerefl, fenereflb, fene_el, fene_elb)
!nh   11.06.18
!     This routine calculates the reflected neutral particle, momentum and energy fluxes with incident neutral flux based on a tr
!uncated Maxwellian
!     for the incident neutrals.
    USE B2MOD_B2CMPA_DIFF
    USE B2MOD_NEUTRALS_NAMELIST_DIFF, ONLY : e_fc
    USE B2US_PLASMA_DIFF
    USE B2US_GEO_DIFF
    USE B2US_MAP_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFF) :: plb
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
    INTEGER, INTENT(IN) :: icv, nci, ifc, isn, iwall, istra
    REAL(kind=r8), INTENT(IN) :: area, recyc0, recycm, isign, tf, sina, &
&   cosa, fluid_frac_hyb
    REAL(kind=r8) :: recyc0b, tfb, fluid_frac_hybb
!     Output variables
    REAL(kind=r8) :: fnnrefl, fmomrefl, nnrefl, nnwwnrefl, fenerefl, &
&   fene_el
    REAL(kind=r8) :: fnnreflb, fmomreflb, nnreflb, nnwwnreflb, fenereflb&
&   , fene_elb
!   ..statement functions
    EXTERNAL INTP_2DTABLE, INTP_2DTABLE0
    EXTERNAL INTP_2DTABLE_B, INTP_2DTABLE0_B
!     Internal variables
    REAL(kind=r8) :: cs, mj, pf, pcorf, pt, vt, pit, pit2, mn, nnf, ux, &
&   uz, i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r, i6l&
&   , i6r, rc1i, rmx1i, rmz1i, rp11i, rp21i, rp31i, re1i, rc2i, rmx2i, &
&   rmy2i, rmz2i, rp12i, rp22i, rp32i, re2i, fnni, fmox, fmoy, fmoz, &
&   fmomreflp, fmomreflt, pn, fene, INTP_2DTABLE, INTP_2DTABLE0, tfev, &
&   unf, fna_mol_refl
    REAL(kind=r8) :: csb, mjb, pfb, pcorfb, ptb, nnfb, uxb, uzb, i0lb, &
&   i1lb, i2lb, i3lb, i4lb, rc1ib, rmx1ib, rmz1ib, rp11ib, rp21ib, &
&   rp31ib, re1ib, fnnib, fmoxb, fmozb, fmomreflpb, fmomrefltb, pnb, &
&   feneb, tfevb, unfb, fna_mol_reflb
    REAL(kind=r8) :: tab(0:nm, 0:nt)
    INTEGER :: is, it, ispt, ilt, ilm, ip1, ip2
    INTRINSIC ABS
    INTRINSIC SQRT
    INTRINSIC MAX
    INTRINSIC MIN
    INTRINSIC SIGN
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: abs0b
    REAL(kind=r8) :: min1
    REAL(kind=r8) :: min1b
    REAL(kind=r8) :: min2
    REAL(kind=r8) :: min2b
    REAL(kind=r8) :: x1b
    REAL(kind=r8) :: temp
    REAL(kind=r8) :: tempb
    REAL(kind=r8) :: tempb0
    REAL(kind=r8) :: temp0
    REAL(kind=r8) :: tempb1
    REAL(kind=r8) :: tempb2
    INTEGER :: branch
    REAL(kind=r8) :: x1
!
!     Initialize/calculate some variables
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!     Neutral velocity components: toroidal (x),tangential (y), and normal (z)
    unf = pl%ua(icv, isn)
    ux = unf*pit2
    uz = -(cosa*isign*unf*pit)
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tf, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Calculate incident particle flux density
    fnni = -(i1l*nnf)
!
!     Interpolate tables to find recycling values
!     Isothermal speed of sound and Mach number
    cs = SQRT(2.0_R8*tf/mn)
    IF (ux .GE. 0.) THEN
      abs0 = ux
      CALL PUSHCONTROL1B(0)
    ELSE
      abs0 = -ux
      CALL PUSHCONTROL1B(1)
    END IF
    mj = abs0/cs
    tfev = tf/ev
    is = recycle_index(isn)
    it = target_index(istra)
! Neutrals are not accelerated to sheath
    ispt = 0
!       Interpolate
    tab = rc1(:, :, ispt, is, it)
    rc1i = INTP_2DTABLE(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt, &
&     ip1, ip2)
    IF (rc1i .LT. 0.0_R8) THEN
      rc1i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rc1i = rc1i
    END IF
    tab = rmx1(:, :, ispt, is, it)
    rmx1i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rmx1i .LT. 0.0_R8) THEN
      rmx1i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rmx1i = rmx1i
    END IF
    tab = rmz1(:, :, ispt, is, it)
    rmz1i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rmz1i .LT. 0.0_R8) THEN
      rmz1i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rmz1i = rmz1i
    END IF
    tab = rp11(:, :, ispt, is, it)
    rp11i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp11i .LT. 0.0_R8) THEN
      rp11i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rp11i = rp11i
    END IF
    tab = rp21(:, :, ispt, is, it)
    rp21i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp21i .LT. 0.0_R8) THEN
      rp21i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rp21i = rp21i
    END IF
    tab = rp31(:, :, ispt, is, it)
    rp31i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp31i .LT. 0.0_R8) THEN
      rp31i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      rp31i = rp31i
    END IF
    tab = re1(:, :, ispt, is, it)
    re1i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (re1i .LT. 0.0_R8) THEN
      re1i = 0.0_R8
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
      re1i = re1i
    END IF
    x1 = nnf*rc1i*cs/fnni
    IF (x1 .GT. recyc0) THEN
      min1 = recyc0
      CALL PUSHCONTROL1B(0)
    ELSE
      min1 = x1
      CALL PUSHCONTROL1B(1)
    END IF
!
!     Calculate fast and thermal reflected parts
    pf = min1 + 1.0e-30_R8
    IF (pf .GT. recyc0) THEN
      min2 = recyc0
      CALL PUSHCONTROL1B(0)
    ELSE
      min2 = pf
      CALL PUSHCONTROL1B(1)
    END IF
    pcorf = min2/pf
    pt = recyc0 - pf + 1.0e-30_R8
    vt = SQRT(2.0_R8*e_fc*ev/mn)
!     The reflection coefficients below already account for the probability of
!     fast particle reflection. Like in Eirene, we will only pump fast particles
!     once all thermal particles are pumped.
!
!nh 22.02.2020
!     Calculate the part of the reflected flux emitted as molecules. At the moment,
!     the molecules are treated kinetically.
    fna_mol_refl = recycm*pt*fnni*area*0.5_R8
    IF (0.0_R8 .LT. (recyc0*fnni*area-2.0_R8*fna_mol_refl)*&
&       fluid_frac_hyb) THEN
      CALL PUSHCONTROL1B(0)
    ELSE
      CALL PUSHCONTROL1B(1)
    END IF
    fene_elb = area*fene_elb
    feneb = area*fenereflb
    fmomreflpb = pit*fmomreflb
    fmomrefltb = pit2*fmomreflb
    fmoxb = area*fmomrefltb
    pnb = cosa*isign*area*fmomreflpb
    fmozb = -(cosa*isign*area*fmomreflpb)
    tempb2 = -((1-recycm)*e_fc*ev*fene_elb)
    ptb = fnni*fluid_frac_hyb*tempb2
    fnnib = pt*fluid_frac_hyb*tempb2
    fluid_frac_hybb = fluid_frac_hybb + pt*fnni*tempb2
    tempb2 = (1-recycm)*e_fc*ev*feneb
    ptb = ptb + fnni*fluid_frac_hyb*tempb2
    fnnib = fnnib + pt*fluid_frac_hyb*tempb2
    fluid_frac_hybb = fluid_frac_hybb + pt*fnni*tempb2
    temp0 = re1i*fluid_frac_hyb
    tempb2 = mn*0.5_R8*feneb
    csb = 3*cs**2*pcorf*nnf*temp0*tempb2
    tempb0 = cs**3*tempb2
    tempb1 = pcorf*nnf*tempb0
    re1ib = fluid_frac_hyb*tempb1
    fluid_frac_hybb = fluid_frac_hybb + re1i*tempb1
    tempb2 = SIGN(1.0_R8, ux)*nnwwnreflb
    tempb1 = rp31i*cs*tempb2
    pcorfb = nnf*temp0*tempb0 + nnf*fluid_frac_hyb*tempb1
    nnfb = pcorf*temp0*tempb0 + pcorf*fluid_frac_hyb*tempb1
    tempb0 = pcorf*nnf*fluid_frac_hyb*tempb2
    rp31ib = cs*tempb0
    tempb2 = (1-recycm)*2.0_R8*nnreflb
    fluid_frac_hybb = fluid_frac_hybb + pcorf*nnf*tempb1 + pt*fnni*&
&     tempb2/vt
    tempb1 = fluid_frac_hyb*tempb2/vt
    fnnib = fnnib + pt*tempb1
    tempb2 = rp21i*fluid_frac_hyb*nnreflb
    pcorfb = pcorfb + nnf*tempb2
    nnfb = nnfb + pcorf*tempb2
    tempb2 = (1-recycm)*vt*mn*2.0_R8*pnb
    ptb = ptb + fnni*tempb1 + fnni*fluid_frac_hyb*tempb2/3.0_R8
    tempb1 = pcorf*nnf*nnreflb
    rp21ib = fluid_frac_hyb*tempb1
    fluid_frac_hybb = fluid_frac_hybb + rp21i*tempb1
    tempb1 = pt*tempb2/3.0_R8
    fnnib = fnnib + fluid_frac_hyb*tempb1
    tempb2 = nnf*rp11i*cs**2*mn*pnb
    fluid_frac_hybb = fluid_frac_hybb + fnni*tempb1 + pcorf*tempb2/&
&     3.0_R8
    tempb1 = pcorf*fluid_frac_hyb*mn*pnb/3.0_R8
    csb = csb + rp31i*tempb0 + 2*cs*nnf*rp11i*tempb1
    tempb0 = cs**2*tempb1
    nnfb = nnfb + rp11i*tempb0
    rp11ib = nnf*tempb0
    pcorfb = pcorfb + fluid_frac_hyb*tempb2/3.0_R8
    tempb2 = (1-recycm)*vt*mn*2.0_R8*fmozb
    ptb = ptb + fnni*fluid_frac_hyb*tempb2/3.0_R8
    tempb1 = pt*tempb2/3.0_R8
    fnnib = fnnib + fluid_frac_hyb*tempb1
    tempb2 = rmz1i*fluid_frac_hyb*mn*fmozb
    tempb0 = pcorf*nnf*cs**2*mn*fmozb
    fluid_frac_hybb = fluid_frac_hybb + fnni*tempb1 + rmz1i*tempb0
    rmz1ib = fluid_frac_hyb*tempb0
    tempb1 = cs**2*tempb2
    pcorfb = pcorfb + nnf*tempb1
    nnfb = nnfb + pcorf*tempb1
    tempb = mn*SIGN(1.0_R8, ux)*fmoxb
    tempb0 = rmx1i*fluid_frac_hyb*tempb
    csb = csb + 2*cs*pcorf*nnf*tempb2 + 2*cs*pcorf*nnf*tempb0
    tempb2 = pcorf*nnf*cs**2*tempb
    rmx1ib = fluid_frac_hyb*tempb2
    fluid_frac_hybb = fluid_frac_hybb + rmx1i*tempb2
    tempb1 = cs**2*tempb0
    pcorfb = pcorfb + nnf*tempb1
    nnfb = nnfb + pcorf*tempb1
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      tempb = fluid_frac_hyb*fnnreflb
      fluid_frac_hybb = fluid_frac_hybb + (area*(recyc0*fnni)-2.0_R8*&
&       fna_mol_refl)*fnnreflb
      recyc0b = recyc0b + fnni*area*tempb
      fnnib = fnnib + recyc0*area*tempb
      fna_mol_reflb = -(2.0_R8*tempb)
    ELSE
      fna_mol_reflb = 0.D0
    END IF
    tempb = recycm*0.5_R8*area*fna_mol_reflb
    ptb = ptb + fnni*tempb
    fnnib = fnnib + pt*tempb
    recyc0b = recyc0b + ptb
    pfb = -ptb - min2*pcorfb/pf**2
    min2b = pcorfb/pf
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      recyc0b = recyc0b + min2b
    ELSE
      pfb = pfb + min2b
    END IF
    min1b = pfb
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      recyc0b = recyc0b + min1b
      x1b = 0.D0
    ELSE
      x1b = min1b
    END IF
    temp = cs/fnni
    nnfb = nnfb + rc1i*temp*x1b
    rc1ib = nnf*temp*x1b
    tempb = nnf*rc1i*x1b/fnni
    csb = csb + tempb
    fnnib = fnnib - temp*tempb
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) re1ib = 0.D0
    mjb = 0.D0
    tfevb = 0.D0
    CALL INTP_2DTABLE0_B(tab, m, t, dm, dt, nm, nt, mj, mjb, tfev, tfevb&
&                  , ilm, ilt, re1ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rp31ib = 0.D0
    tab = rp31(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_B(tab, m, t, dm, dt, nm, nt, mj, mjb, tfev, tfevb&
&                  , ilm, ilt, rp31ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rp21ib = 0.D0
    tab = rp21(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_B(tab, m, t, dm, dt, nm, nt, mj, mjb, tfev, tfevb&
&                  , ilm, ilt, rp21ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rp11ib = 0.D0
    tab = rp11(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_B(tab, m, t, dm, dt, nm, nt, mj, mjb, tfev, tfevb&
&                  , ilm, ilt, rp11ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rmz1ib = 0.D0
    tab = rmz1(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_B(tab, m, t, dm, dt, nm, nt, mj, mjb, tfev, tfevb&
&                  , ilm, ilt, rmz1ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rmx1ib = 0.D0
    tab = rmx1(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_B(tab, m, t, dm, dt, nm, nt, mj, mjb, tfev, tfevb&
&                  , ilm, ilt, rmx1ib)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) rc1ib = 0.D0
    tab = rc1(:, :, ispt, is, it)
    CALL INTP_2DTABLE_B(tab, m, t, dm, dt, nm, nt, mj, mjb, tfev, tfevb&
&                 , ilm, ilt, ip1, ip2, rc1ib)
    tfb = tfb + tfevb/ev
    abs0b = mjb/cs
    csb = csb - abs0*mjb/cs**2
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      uxb = abs0b
    ELSE
      uxb = -abs0b
    END IF
    IF (.NOT.2.0_R8*(tf/mn) .EQ. 0.D0) tfb = tfb + 2.0_R8*csb/(mn*2.0*&
&       SQRT(2.0_R8*(tf/mn)))
    i1lb = -(nnf*fnnib)
    nnfb = nnfb - i1l*fnnib
    uzb = 0.D0
    i0lb = 0.D0
    i2lb = 0.D0
    i3lb = 0.D0
    i4lb = 0.D0
    CALL CALCMOMENTSMAXWELLIAN_B(uz, uzb, tf, tfb, mn, i0l, i0lb, i0r, &
&                          i1l, i1lb, i1r, i2l, i2lb, i2r, i3l, i3lb, &
&                          i3r, i4l, i4lb, i4r, i5l, i5r, i6l, i6r)
    unfb = pit2*uxb - pit*cosa*isign*uzb
    plb%ua(icv, isn) = plb%ua(icv, isn) + unfb
    plb%na(icv, isn) = plb%na(icv, isn) + nnfb
  END SUBROUTINE CALCREFLECTEDFLUXESMAXWELLIAN_B

!
!
  SUBROUTINE CALCREFLECTEDFLUXESMAXWELLIAN(icv, nci, ifc, isign, isn, &
&   istra, iwall, area, recyc0, recycm, fluid_frac_hyb, pl, tf, geo, mpg&
&   , cosa, sina, fnnrefl, fmomrefl, nnrefl, nnwwnrefl, fenerefl, &
&   fene_el)
!nh   11.06.18
!     This routine calculates the reflected neutral particle, momentum and energy fluxes with incident neutral flux based on a tr
!uncated Maxwellian
!     for the incident neutrals.
    USE B2MOD_B2CMPA_DIFF
    USE B2MOD_NEUTRALS_NAMELIST_DIFF, ONLY : e_fc
    USE B2US_PLASMA_DIFF
    USE B2US_GEO_DIFF
    USE B2US_MAP_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
    INTEGER, INTENT(IN) :: icv, nci, ifc, isn, iwall, istra
    REAL(kind=r8), INTENT(IN) :: area, recyc0, recycm, isign, tf, sina, &
&   cosa, fluid_frac_hyb
!     Output variables
    REAL(kind=r8), INTENT(OUT) :: fnnrefl, fmomrefl, nnrefl, nnwwnrefl, &
&   fenerefl, fene_el
!   ..statement functions
    EXTERNAL INTP_2DTABLE, INTP_2DTABLE0
!     Internal variables
    REAL(kind=r8) :: cs, mj, pf, pcorf, pt, vt, pit, pit2, mn, nnf, ux, &
&   uz, i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r, i6l&
&   , i6r, rc1i, rmx1i, rmz1i, rp11i, rp21i, rp31i, re1i, rc2i, rmx2i, &
&   rmy2i, rmz2i, rp12i, rp22i, rp32i, re2i, fnni, fmox, fmoy, fmoz, &
&   fmomreflp, fmomreflt, pn, fene, INTP_2DTABLE, INTP_2DTABLE0, tfev, &
&   unf, fna_mol_refl
    REAL(kind=r8) :: tab(0:nm, 0:nt)
    INTEGER :: is, it, ispt, ilt, ilm, ip1, ip2
    INTRINSIC ABS
    INTRINSIC SQRT
    INTRINSIC MAX
    INTRINSIC MIN
    INTRINSIC SIGN
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: min1
    REAL(kind=r8) :: min2
    REAL(kind=r8) :: x1
!
    CALL SUBINI('CalcReflectedFluxesMaxwellian')
!     Initialize/calculate some variables
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!     Neutral velocity components: toroidal (x),tangential (y), and normal (z)
    unf = pl%ua(icv, isn)
    ux = unf*pit2
    uz = -(cosa*isign*unf*pit)
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tf, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Calculate incident particle flux density
    fnni = -(i1l*nnf)
!
!     Interpolate tables to find recycling values
!     Isothermal speed of sound and Mach number
    cs = SQRT(2.0_R8*tf/mn)
    IF (ux .GE. 0.) THEN
      abs0 = ux
    ELSE
      abs0 = -ux
    END IF
    mj = abs0/cs
    tfev = tf/ev
    is = recycle_index(isn)
    it = target_index(istra)
! Neutrals are not accelerated to sheath
    ispt = 0
!       Interpolate
    tab = rc1(:, :, ispt, is, it)
    rc1i = INTP_2DTABLE(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt, &
&     ip1, ip2)
    IF (rc1i .LT. 0.0_R8) THEN
      rc1i = 0.0_R8
    ELSE
      rc1i = rc1i
    END IF
    tab = rc2(:, :, ispt, is, it)
    rc2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rc2i .LT. 0.0_R8) THEN
      rc2i = 0.0_R8
    ELSE
      rc2i = rc2i
    END IF
    tab = rmx1(:, :, ispt, is, it)
    rmx1i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rmx1i .LT. 0.0_R8) THEN
      rmx1i = 0.0_R8
    ELSE
      rmx1i = rmx1i
    END IF
    tab = rmx2(:, :, ispt, is, it)
    rmx2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rmx2i .LT. 0.0_R8) THEN
      rmx2i = 0.0_R8
    ELSE
      rmx2i = rmx2i
    END IF
    tab = rmy2(:, :, ispt, is, it)
    rmy2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rmy2i .LT. 0.0_R8) THEN
      rmy2i = 0.0_R8
    ELSE
      rmy2i = rmy2i
    END IF
    tab = rmz1(:, :, ispt, is, it)
    rmz1i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rmz1i .LT. 0.0_R8) THEN
      rmz1i = 0.0_R8
    ELSE
      rmz1i = rmz1i
    END IF
    tab = rmz2(:, :, ispt, is, it)
    rmz2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rmz2i .LT. 0.0_R8) THEN
      rmz2i = 0.0_R8
    ELSE
      rmz2i = rmz2i
    END IF
    tab = rp11(:, :, ispt, is, it)
    rp11i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp11i .LT. 0.0_R8) THEN
      rp11i = 0.0_R8
    ELSE
      rp11i = rp11i
    END IF
    tab = rp21(:, :, ispt, is, it)
    rp21i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp21i .LT. 0.0_R8) THEN
      rp21i = 0.0_R8
    ELSE
      rp21i = rp21i
    END IF
    tab = rp31(:, :, ispt, is, it)
    rp31i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp31i .LT. 0.0_R8) THEN
      rp31i = 0.0_R8
    ELSE
      rp31i = rp31i
    END IF
    tab = rp12(:, :, ispt, is, it)
    rp12i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp12i .LT. 0.0_R8) THEN
      rp12i = 0.0_R8
    ELSE
      rp12i = rp12i
    END IF
    tab = rp22(:, :, ispt, is, it)
    rp22i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp22i .LT. 0.0_R8) THEN
      rp22i = 0.0_R8
    ELSE
      rp22i = rp22i
    END IF
    tab = rp32(:, :, ispt, is, it)
    rp32i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp32i .LT. 0.0_R8) THEN
      rp32i = 0.0_R8
    ELSE
      rp32i = rp32i
    END IF
    tab = re1(:, :, ispt, is, it)
    re1i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (re1i .LT. 0.0_R8) THEN
      re1i = 0.0_R8
    ELSE
      re1i = re1i
    END IF
    tab = re2(:, :, ispt, is, it)
    re2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (re2i .LT. 0.0_R8) THEN
      re2i = 0.0_R8
    ELSE
      re2i = re2i
    END IF
    x1 = nnf*rc1i*cs/fnni
    IF (x1 .GT. recyc0) THEN
      min1 = recyc0
    ELSE
      min1 = x1
    END IF
!
!     Calculate fast and thermal reflected parts
    pf = min1 + 1.0e-30_R8
    IF (pf .GT. recyc0) THEN
      min2 = recyc0
    ELSE
      min2 = pf
    END IF
    pcorf = min2/pf
    pt = recyc0 - pf + 1.0e-30_R8
    vt = SQRT(2.0_R8*e_fc*ev/mn)
!     The reflection coefficients below already account for the probability of
!     fast particle reflection. Like in Eirene, we will only pump fast particles
!     once all thermal particles are pumped.
!
!nh 22.02.2020
!     Calculate the part of the reflected flux emitted as molecules. At the moment,
!     the molecules are treated kinetically.
    fna_mol_refl = recycm*pt*fnni*area*0.5_R8
    fna_mol(ifc, isn) = fna_mol(ifc, isn) + fna_mol_refl
    IF (0.0_R8 .LT. (recyc0*fnni*area-2.0_R8*fna_mol_refl)*&
&       fluid_frac_hyb) THEN
      fnnrefl = (recyc0*fnni*area-2.0_R8*fna_mol_refl)*fluid_frac_hyb
    ELSE
      fnnrefl = 0.0_R8
    END IF
!
!     reflected momentum flux densities
    fmox = pcorf*mn*nnf*rmx1i*cs**2*SIGN(1.0_R8, ux)*fluid_frac_hyb
    fmoy = 0._R8
    fmoz = pcorf*mn*nnf*rmz1i*cs**2*fluid_frac_hyb
    fmoz = fmoz + (1-recycm)*pt*2.0_R8/3.0_R8*mn*fnni*vt*fluid_frac_hyb
!     Pressure of reflected neutrals
    pn = pcorf*mn/3.0_R8*nnf*rp11i*cs**2*fluid_frac_hyb
    pn = pn + (1-recycm)*pt*2.0_R8/3.0_R8*mn*fnni*vt*fluid_frac_hyb
!     Density of reflected neutrals
    nnrefl = pcorf*nnf*rp21i*fluid_frac_hyb
    nnrefl = nnrefl + (1-recycm)*pt*fnni*2.0_R8/vt*fluid_frac_hyb
!     Product of density and toroidal velocity
    nnwwnrefl = pcorf*nnf*rp31i*cs*SIGN(1.0_R8, ux)*fluid_frac_hyb
!     reflected total energy flux density of neutrals
    fene = 0.5_R8*pcorf*mn*nnf*re1i*cs**3*fluid_frac_hyb
    fene = fene + (1-recycm)*pt*fnni*e_fc*ev*fluid_frac_hyb
!! the Franck-Condon energy is provided by the electrons
    fene_el = -((1-recycm)*pt*fnni*e_fc*ev*fluid_frac_hyb)
!
!     Calculate reflected poloidal momentum flux of neutrals
    fmomreflp = -(isign*(fmoz*cosa+fmoy*sina)*area) + isign*pn*area*cosa
    fmomreflt = fmox*area
    fmomrefl = pit*fmomreflp + pit2*fmomreflt
!     Convert energy flux densities in energy fluxes
    fenerefl = fene*area
    fene_el = fene_el*area
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCREFLECTEDFLUXESMAXWELLIAN

!  Differentiation of calcmomentsmaxwellian in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: int4l int1l int2l int3l int0l
!                i0l t u i4l i1l i2l i3l
!   with respect to varying inputs: int4l int1l int2l int3l int0l
!                t u
!
  SUBROUTINE CALCMOMENTSMAXWELLIAN_B(u, ub, t, tb, m, i0l, i0lb, i0r, &
&   i1l, i1lb, i1r, i2l, i2lb, i2r, i3l, i3lb, i3r, i4l, i4lb, i4r, i5l&
&   , i5r, i6l, i6r)
! mb This subroutine calculates the moments of a 1D Maxwellian distribution (the
! zeroth until the sixth moment)
! u = drifting velocity
! T = temperature
! m = mass of a particle
    USE B2MOD_SUBSYS
    IMPLICIT NONE
!input variables
!output variables
    REAL(kind=r8), INTENT(IN) :: u, t, m
    REAL(kind=r8) :: ub, tb
    REAL(kind=r8) :: i0l, i0r, i1l, i1r, i2l, i2r
    REAL(kind=r8) :: i0lb, i1lb, i2lb
!internal variables
    REAL(kind=r8) :: i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r
    REAL(kind=r8) :: i3lb, i4lb
    REAL(kind=r8) :: vth, arg
    REAL(kind=r8) :: vthb, argb
!      real (kind=R8):: S15AEC
    INTRINSIC ERF
    EXTERNAL ERF_B
    INTRINSIC SQRT
    INTRINSIC EXP
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result1b
    REAL(kind=r8) :: temp
    REAL(kind=r8) :: temp0
    REAL(kind=r8) :: tempb
    REAL(r8) :: tempb0
    REAL(r8) :: tempb1
    REAL(kind=r8) :: temp1
!      external S15AEC !mb NAG erf function
!
    IF (uold .EQ. u .AND. told .EQ. t .AND. mold .EQ. m) THEN
      int4lb = int4lb + i4lb
      int3lb = int3lb + i3lb
      int2lb = int2lb + i2lb
      int1lb = int1lb + i1lb
      int0lb = int0lb + i0lb
    ELSE
      vth = SQRT(2.0_R8*t/m)
      arg = -(u/vth)
!     int0l = int_{-inf}^{0} M(v) dv
!
!     int0r = int_{0}^{inf} M(v) dv
!
!     int1l = int_{-inf}^{0} v M(v) dv
      result1 = ERF(arg)
!
!     int1r = int_{0}^{inf} v M(v) dv
!
!     int2l = int_{-inf}^{0} v^{2} M(v) dv
      CALL PUSHREAL8(result1, r8/8)
      result1 = ERF(arg)
!
!     int2r = int_{0}^{inf} v^{2} M(v) dv
!
! Implementation as optional arguments gave segmentation errors...
!     int3l = int_{-inf}^{0} v^{3} M(v) dv
      CALL PUSHREAL8(result1, r8/8)
      result1 = ERF(arg)
!
!     int3r = int_{0}^{inf} v^{3} M(v) dv
!
!     int4l = int_{-inf}^{0} v^{4} M(v) dv
      CALL PUSHREAL8(result1, r8/8)
      result1 = ERF(arg)
!
!     int4r = int_{0}^{inf} v^{4} M(v) dv
!
!     int5l = int_{-inf}^{0} v^{5} M(v) dv
!
!     int5r = int_{0}^{inf} v^{5} M(v) dv
!
!     int6l = int_{-inf}^{0} v^{6} M(v) dv
!
!     int6r = int_{0}^{inf} v^{6} M(v) dv
      int4lb = int4lb + i4lb
      int3lb = int3lb + i3lb
      int2lb = int2lb + i2lb
      int1lb = int1lb + i1lb
      int0lb = int0lb + i0lb
      temp0 = SQRT(pi)
      temp = vth*vth*vth
      temp1 = u*u*u
      tempb0 = (result1+1.0_R8)*int4lb
      result1b = (1.5_R8*(t**2/m**2)+3.0_R8*(u**2*(t/m))+u**4/2.0_R8)*&
&       int4lb
      tempb1 = -(EXP(arg**2)*int4lb/temp0)
      argb = -(2*arg*EXP(arg**2)*(1.25_R8*(temp*u)+0.5_R8*(vth*temp1))*&
&       int4lb/temp0)
      CALL POPREAL8(result1, r8/8)
      ub = ub + (temp*1.25_R8+3*u**2*vth*0.5_R8)*tempb1 + (2*u*t*3.0_R8/&
&       m+4*u**3/2.0_R8)*tempb0 + 3*u**2*0.5_R8*(result1+1.0_R8)*int3lb
      CALL ERF_B(arg, argb, result1b)
      temp = SQRT(pi)
      tempb = 1.5_R8*(result1+1.0_R8)*int3lb/m
      result1b = (1.5_R8*(t*u/m)+0.5_R8*u**3)*int3lb
      argb = argb - 2*arg*EXP(arg**2)*(0.5_R8*vth**3+0.5_R8*(vth*u**2))*&
&       int3lb/temp
      CALL POPREAL8(result1, r8/8)
      tb = tb + (2*t*1.5_R8/m**2+u**2*3.0_R8/m)*tempb0 + u*tempb + &
&       0.5_R8*(result1+1.0_R8)*int2lb/m
      tempb0 = -(EXP(arg**2)*int3lb/temp)
      ub = ub + 2*u*vth*0.5_R8*tempb0 + t*tempb + 2*u*0.5_R8*(result1+&
&       1.0_R8)*int2lb
      CALL ERF_B(arg, argb, result1b)
      temp = SQRT(pi)
      result1b = (0.5_R8*(t/m)+0.5_R8*u**2)*int2lb
      argb = argb - 2*arg*EXP(arg**2)*vth*u*0.5_R8*int2lb/temp
      tempb = -(EXP(arg**2)*0.5_R8*int2lb/temp)
      CALL POPREAL8(result1, r8/8)
      CALL ERF_B(arg, argb, result1b)
      temp = SQRT(pi)
      result1b = u*0.5_R8*int1lb
      argb = argb - 2*arg*EXP(arg**2)*vth*0.5_R8*int1lb/temp
      CALL ERF_B(arg, argb, result1b)
      result1b = 0.5_R8*int0lb
      CALL ERF_B(arg, argb, result1b)
      vthb = (3*vth**2*u*1.25_R8+temp1*0.5_R8)*tempb1 + (3*vth**2*0.5_R8&
&       +u**2*0.5_R8)*tempb0 + u*tempb + u*argb/vth**2 - EXP(arg**2)*&
&       0.5_R8*int1lb/temp
      ub = ub + vth*tempb + (result1+1.0_R8)*0.5_R8*int1lb - argb/vth
      IF (.NOT.2.0_R8*(t/m) .EQ. 0.D0) tb = tb + 2.0_R8*vthb/(m*2.0*SQRT&
&         (2.0_R8*(t/m)))
      int4lb = 0.D0
      int1lb = 0.D0
      int2lb = 0.D0
      int3lb = 0.D0
      int0lb = 0.D0
    END IF
  END SUBROUTINE CALCMOMENTSMAXWELLIAN_B

!
  SUBROUTINE CALCMOMENTSMAXWELLIAN(u, t, m, i0l, i0r, i1l, i1r, i2l, i2r&
&   , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
! mb This subroutine calculates the moments of a 1D Maxwellian distribution (the
! zeroth until the sixth moment)
! u = drifting velocity
! T = temperature
! m = mass of a particle
    USE B2MOD_SUBSYS
    IMPLICIT NONE
!input variables
!output variables
    REAL(kind=r8), INTENT(IN) :: u, t, m
    REAL(kind=r8), INTENT(OUT) :: i0l, i0r, i1l, i1r, i2l, i2r
!internal variables
    REAL(kind=r8), INTENT(OUT) :: i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r
    REAL(kind=r8) :: vth, arg
!      real (kind=R8):: S15AEC
    INTRINSIC ERF
    INTRINSIC SQRT
    INTRINSIC EXP
    REAL(kind=r8) :: result1
!      external S15AEC !mb NAG erf function
    CALL SUBINI('CalcMomentsMaxwellian')
!
    IF (uold .EQ. u .AND. told .EQ. t .AND. mold .EQ. m) THEN
!Do not do the same thing twice
      i0l = int0l
      i0r = int0r
      i1l = int1l
      i1r = int1r
      i2l = int2l
      i2r = int2r
      i3l = int3l
      i3r = int3r
      i4l = int4l
      i4r = int4r
      i5l = int5l
      i5r = int5r
      i6l = int6l
      i6r = int6r
      CALL SUBEND()
      RETURN
    ELSE
      vth = SQRT(2.0_R8*t/m)
      arg = -(u/vth)
!     int0l = int_{-inf}^{0} M(v) dv
      result1 = ERF(arg)
      int0l = 0.5_R8*(1.0_R8+result1)
!
!     int0r = int_{0}^{inf} M(v) dv
      result1 = ERF(arg)
      int0r = 0.5_R8*(1.0_R8-result1)
!
!     int1l = int_{-inf}^{0} v M(v) dv
      result1 = ERF(arg)
      int1l = 0.5_R8*u*(1.0_R8+result1) - 0.5_R8/SQRT(pi)*vth*EXP(arg**2&
&       )
!
!     int1r = int_{0}^{inf} v M(v) dv
      result1 = ERF(arg)
      int1r = 0.5_R8*u*(1.0_R8-result1) + 0.5_R8/SQRT(pi)*vth*EXP(arg**2&
&       )
!
!     int2l = int_{-inf}^{0} v^{2} M(v) dv
      result1 = ERF(arg)
      int2l = (0.5_R8*t/m+0.5_R8*u**2)*(1.0_R8+result1) - 0.5_R8/SQRT(pi&
&       )*vth*u*EXP(arg**2)
!
!     int2r = int_{0}^{inf} v^{2} M(v) dv
      result1 = ERF(arg)
      int2r = (0.5_R8*t/m+0.5_R8*u**2)*(1.0_R8-result1) + 0.5_R8/SQRT(pi&
&       )*vth*u*EXP(arg**2)
!
! Implementation as optional arguments gave segmentation errors...
!     int3l = int_{-inf}^{0} v^{3} M(v) dv
      result1 = ERF(arg)
      int3l = (1.5_R8*t/m*u+0.5_R8*u**3)*(1.0_R8+result1) - 1.0_R8/SQRT(&
&       pi)*(0.5_R8*vth**3+0.5_R8*vth*u**2)*EXP(arg**2)
!
!     int3r = int_{0}^{inf} v^{3} M(v) dv
      result1 = ERF(arg)
      int3r = (1.5_R8*t/m*u+0.5_R8*u**3)*(1-result1) + 1.0_R8/SQRT(pi)*(&
&       0.5_R8*vth**3+0.5_R8*vth*u**2)*EXP(arg**2)
!
!     int4l = int_{-inf}^{0} v^{4} M(v) dv
      result1 = ERF(arg)
      int4l = (1.5_R8*(t/m)**2+3.0_R8*t/m*u**2+u**4/2.0_R8)*(1.0_R8+&
&       result1) - 1.0_R8/SQRT(pi)*(1.25_R8*vth**3*u+0.5_R8*vth*u**3)*&
&       EXP(arg**2)
!
!     int4r = int_{0}^{inf} v^{4} M(v) dv
      result1 = ERF(arg)
      int4r = (1.5_R8*(t/m)**2+3.0_R8*t/m*u**2+u**4/2.0_R8)*(1.0_R8-&
&       result1) + 1.0_R8/SQRT(pi)*(1.25_R8*vth**3*u+0.5_R8*vth*u**3)*&
&       EXP(arg**2)
!
!     int5l = int_{-inf}^{0} v^{5} M(v) dv
      result1 = ERF(arg)
      int5l = (7.5_R8*(t/m)**2*u+5.0_R8*t/m*u**3+u**5/2.0_R8)*(1.0_R8+&
&       result1) - 1.0_R8/SQRT(pi)*(vth**5+2.25_R8*vth**3*u**2+0.5_R8*&
&       vth*u**4)*EXP(arg**2)
!
!     int5r = int_{0}^{inf} v^{5} M(v) dv
      result1 = ERF(arg)
      int5r = (7.5_R8*(t/m)**2*u+5.0_R8*t/m*u**3+u**5/2.0_R8)*(1.0_R8-&
&       result1) + 1.0_R8/SQRT(pi)*(vth**5+2.25_R8*vth**3*u**2+0.5_R8*&
&       vth*u**4)*EXP(arg**2)
!
!     int6l = int_{-inf}^{0} v^{6} M(v) dv
      result1 = ERF(arg)
      int6l = (7.5_R8*(t/m)**3+22.5_R8*(t/m)**2*u**2+7.5_R8*t/m*u**4+&
&       0.5_R8*u**6)*(1.0_R8+result1) - 1.0_R8/SQRT(pi)*(4.125_R8*vth**5&
&       *u+3.5_R8*vth**3*u**3+0.5_R8*vth*u**5)*EXP(arg**2)
!
!     int6r = int_{0}^{inf} v^{6} M(v) dv
      result1 = ERF(arg)
      int6r = (7.5_R8*(t/m)**3+22.5_R8*(t/m)**2*u**2+7.5_R8*t/m*u**4+&
&       0.5_R8*u**6)*(1.0_R8-result1) + 1.0_R8/SQRT(pi)*(4.125_R8*vth**5&
&       *u+3.5_R8*vth**3*u**3+0.5_R8*vth*u**5)*EXP(arg**2)
      i0l = int0l
      i0r = int0r
      i1l = int1l
      i1r = int1r
      i2l = int2l
      i2r = int2r
      i3l = int3l
      i3r = int3r
      i4l = int4l
      i4r = int4r
      i5l = int5l
      i5r = int5r
      i6l = int6l
      i6r = int6r
      CALL SUBEND()
      RETURN
    END IF
  END SUBROUTINE CALCMOMENTSMAXWELLIAN

!  Differentiation of calclincomb in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: f kn
!   with respect to varying inputs: f1 f2 kn
!
  SUBROUTINE CALCLINCOMB_B(kn, knb, kn1, kn2, f1, f1b, f2, f2b, f, fb)
    IMPLICIT NONE
!input variables
!output variables
    REAL(kind=r8), INTENT(IN) :: kn, kn1, kn2, f1, f2
    REAL(kind=r8) :: knb, f1b, f2b
    REAL(kind=r8) :: f
    REAL(kind=r8) :: fb
    EXTERNAL XERRAB
!                      F2
!              F       |
!       F1     |       |
!       |      |       |
!      Kn1----Kn------Kn2
    REAL(kind=r8) :: tempb
    tempb = fb/(kn2-kn1)
    knb = knb + (f2-f1)*tempb
    f2b = (kn-kn1)*tempb
    f1b = (kn2-kn)*tempb
  END SUBROUTINE CALCLINCOMB_B

!
  SUBROUTINE CALCLINCOMB(kn, kn1, kn2, f1, f2, f)
    IMPLICIT NONE
!input variables
!output variables
    REAL(kind=r8), INTENT(IN) :: kn, kn1, kn2, f1, f2
    REAL(kind=r8), INTENT(OUT) :: f
    EXTERNAL XERRAB
!                      F2
!              F       |
!       F1     |       |
!       |      |       |
!      Kn1----Kn------Kn2
    IF (kn .LT. kn1 .OR. kn .GT. kn2) CALL XERRAB(&
&                                     'Kn should be between Kn1 and Kn2'&
&                                          )
!
    f = ((kn-kn1)*f2+(kn2-kn)*f1)/(kn2-kn1)
!
    RETURN
  END SUBROUTINE CALCLINCOMB

!  Differentiation of calcminkn in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: *(dv.ne) mfp *(rt.rlcx) *(rt.rlsa)
!                *(pl.na) *(pl.te) *(pl.ti) *(pl.tn)
!   with respect to varying inputs: *(dv.ne) *(rt.rlcx) *(rt.rlsa)
!                *(pl.na) *(pl.te) *(pl.ti) *(pl.tn)
!   Plus diff mem management of: dv.ne:in rt.rlcx:in rt.rlsa:in
!                pl.na:in pl.te:in pl.ti:in pl.tn:in
!
  SUBROUTINE CALCMINKN_B(icv, isn, istra, pl, plb, dv, dvb, rt, rtb, l, &
&   min_kn, kn, mfp, mfpb)
!     This routine calculates the minimum mean free path for a certain stratum
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFF
    USE B2US_PLASMA_DIFF
    USE B2MOD_NEUTRALS_NAMELIST_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFF) :: plb
    TYPE(B2DERIVATIVES), INTENT(IN) :: dv
    TYPE(B2DERIVATIVES) :: dvb
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFF) :: rtb
    INTEGER, INTENT(IN) :: icv, istra, isn
    REAL(kind=r8), INTENT(IN) :: l
!     InOut variables
    REAL(kind=r8), INTENT(INOUT) :: min_kn(nstrat)
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, mn, mfp, c_th, t_av
    REAL(kind=r8) :: vcxb, vionb, vtotb, mfpb, c_thb, t_avb
    REAL(kind=r8) :: tn, ti, ne, te, kn
    REAL(kind=r8) :: tnb, tib, neb, teb
    INTEGER :: ic
    INTRINSIC LOG
    INTRINSIC SQRT
    INTRINSIC MIN
    REAL(r8) :: arg1
    REAL(r8) :: arg1b
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result1b
    REAL(kind=r8) :: temp
!
!
!     Calculate rate coefficients at boundary
    tn = pl%tn(icv)
    ti = pl%ti(icv)
    ne = dv%ne(icv)
    te = pl%te(icv)
    mn = am(isn)*mp
    vcx = 0.0_R8
    DO ic=0,pl%ns-1
      t_av = (ti+tn)/2.0_R8
      CALL PUSHREAL8(arg1, r8/8)
      arg1 = rt%rlcx(icv, 0, ic, isn) + rt%rlcx(icv, 1, ic, isn)*LOG(&
&       t_av/(am(isn)*ev))
      CALL PUSHREAL8(cutlo, r8/8)
      CALL PUSHREAL8(cutll, r8/8)
      CALL PUSHBOOLEAN(b2mod_math_initialised)
      CALL PUSHREAL4(small_r4_constant, r4/8)
      CALL PUSHREAL8(result1, r8/8)
      result1 = EXPU(arg1)
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    CALL PUSHREAL8(arg1, r8/8)
    arg1 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*LOG(te/ev)
    CALL PUSHREAL8(cutlo, r8/8)
    CALL PUSHREAL8(cutll, r8/8)
    CALL PUSHBOOLEAN(b2mod_math_initialised)
    CALL PUSHREAL4(small_r4_constant, r4/8)
    CALL PUSHREAL8(result1, r8/8)
    result1 = EXPU(arg1)
    vion = result1*ne
    vtot = vion + vcx
    c_th = SQRT(tn/mn)
    c_thb = mfpb/vtot
    vtotb = -(c_th*mfpb/vtot**2)
    IF (tn/mn .EQ. 0.D0) THEN
      tnb = 0.D0
    ELSE
      tnb = c_thb/(mn*2.0*SQRT(tn/mn))
    END IF
    vionb = vtotb
    vcxb = vtotb
    result1b = ne*vionb
    neb = result1*vionb
    CALL POPREAL8(result1, r8/8)
    CALL POPREAL4(small_r4_constant, r4/8)
    CALL POPBOOLEAN(b2mod_math_initialised)
    CALL POPREAL8(cutll, r8/8)
    CALL POPREAL8(cutlo, r8/8)
    CALL EXPU_B(arg1, arg1b, result1b)
    CALL POPREAL8(arg1, r8/8)
    rtb%rlsa(icv, 0, isn) = rtb%rlsa(icv, 0, isn) + arg1b
    rtb%rlsa(icv, 1, isn) = rtb%rlsa(icv, 1, isn) + LOG(te/ev)*arg1b
    teb = rt%rlsa(icv, 1, isn)*arg1b/te
    tib = 0.D0
    DO ic=pl%ns-1,0,-1
      plb%na(icv, ic) = plb%na(icv, ic) + result1*vcxb
      result1b = pl%na(icv, ic)*vcxb
      CALL POPREAL8(result1, r8/8)
      CALL POPREAL4(small_r4_constant, r4/8)
      CALL POPBOOLEAN(b2mod_math_initialised)
      CALL POPREAL8(cutll, r8/8)
      CALL POPREAL8(cutlo, r8/8)
      CALL EXPU_B(arg1, arg1b, result1b)
      t_av = (ti+tn)/2.0_R8
      CALL POPREAL8(arg1, r8/8)
      temp = t_av/(am(isn)*ev)
      rtb%rlcx(icv, 0, ic, isn) = rtb%rlcx(icv, 0, ic, isn) + arg1b
      rtb%rlcx(icv, 1, ic, isn) = rtb%rlcx(icv, 1, ic, isn) + LOG(temp)*&
&       arg1b
      t_avb = rt%rlcx(icv, 1, ic, isn)*arg1b/(am(isn)*ev*temp)
      tib = tib + t_avb/2.0_R8
      tnb = tnb + t_avb/2.0_R8
    END DO
    plb%te(icv) = plb%te(icv) + teb
    dvb%ne(icv) = dvb%ne(icv) + neb
    plb%ti(icv) = plb%ti(icv) + tib
    plb%tn(icv) = plb%tn(icv) + tnb
  END SUBROUTINE CALCMINKN_B

!
  SUBROUTINE CALCMINKN(icv, isn, istra, pl, dv, rt, l, min_kn, kn, mfp)
!     This routine calculates the minimum mean free path for a certain stratum
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFF
    USE B2US_PLASMA_DIFF
    USE B2MOD_NEUTRALS_NAMELIST_DIFF
    USE B2MOD_SUBSYS
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2DERIVATIVES), INTENT(IN) :: dv
    TYPE(B2RATES), INTENT(IN) :: rt
    INTEGER, INTENT(IN) :: icv, istra, isn
    REAL(kind=r8), INTENT(IN) :: l
!     InOut variables
    REAL(kind=r8), INTENT(INOUT) :: min_kn(nstrat)
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, mn, mfp, c_th, t_av
    REAL(kind=r8) :: tn, ti, ne, te, kn
    INTEGER :: ic
    INTRINSIC LOG
    INTRINSIC SQRT
    INTRINSIC MIN
    REAL(r8) :: arg1
    REAL(kind=r8) :: result1
!
    CALL SUBINI('CalcMinKn')
!
!     Calculate rate coefficients at boundary
    tn = pl%tn(icv)
    ti = pl%ti(icv)
    ne = dv%ne(icv)
    te = pl%te(icv)
    mn = am(isn)*mp
    vcx = 0.0_R8
    DO ic=0,pl%ns-1
      t_av = (ti+tn)/2.0_R8
      arg1 = rt%rlcx(icv, 0, ic, isn) + rt%rlcx(icv, 1, ic, isn)*LOG(&
&       t_av/(am(isn)*ev))
      result1 = EXPU(arg1)
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*LOG(te/ev)
    result1 = EXPU(arg1)
    vion = result1*ne
    vtot = vion + vcx
    c_th = SQRT(tn/mn)
    mfp = c_th/vtot
    kn = mfp/l
    IF (kn .GT. min_kn(istra)) THEN
      min_kn(istra) = min_kn(istra)
    ELSE
      min_kn(istra) = kn
    END IF
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCMINKN

