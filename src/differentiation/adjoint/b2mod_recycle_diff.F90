      CALL PUSHREAL8(int0r, r8/8)
      CALL PUSHREAL8(int0l, r8/8)
      CALL PUSHREAL8(int3r, r8/8)
      CALL PUSHREAL8(int3l, r8/8)
      CALL PUSHREAL8(int6r, r8/8)
      CALL PUSHREAL8(int6l, r8/8)
