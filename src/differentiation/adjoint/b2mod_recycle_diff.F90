    tempb3 = pt*tempb4/3.0_R8
    fnni_nodriftsb = fnni_nodriftsb + fluid_frac_hyb*tempb3
    temp5 = fluid_frac_hyb/vtot
    temp2 = cs*cs*cs
    temp1 = dnndz*rmz2i/vtot
    tempb0 = (nnf*rmz1i*cs**2+temp1*temp2)*mn*fmozb
    pcorfb = pcorfb + vcx*temp5*tempb0
    vcxb = vcxb + pcorf*temp5*tempb0
    tempb4 = pcorf*vcx*tempb0/vtot
    fluid_frac_hybb = fluid_frac_hybb + fnni_nodrifts*tempb3 + tempb4
    tempb3 = pcorf*vcx*temp5*mn*fmozb
    tempb5 = cs**2*tempb3
    csb = csb + (2*cs*nnf*rmz1i+3*cs**2*temp1)*tempb3
    tempb1 = temp2*tempb3/vtot
    vtotb = vtotb - temp5*tempb4 - temp1*tempb1
    rmz2ib = dnndz*tempb1
    nnfb = nnfb + rmz1i*tempb5
    rmz1ib = nnf*tempb5
    temp5 = fluid_frac_hyb/(vtot*vtot)
    temp4 = cs*cs*cs
    temp2 = dnndy*rmy2i
    tempb5 = -(temp4*temp5*mn*fmoyb)
    tempb2 = pcorf*vcx*tempb5
    dnndyb = dnndyb + rmy2i*tempb2
    rmy2ib = dnndy*tempb2
    temp1 = fluid_frac_hyb/vtot
    temp3 = cs*cs*cs
    tempb2 = mn*SIGN(1.0_R8, ux)*fmoxb
    tempb0 = pcorf*vcx*temp1*tempb2
    tempb = cs**2*tempb0
    tempb3 = temp3*tempb0/vtot
    dnndzb = dnndzb + rmz2i*tempb1 + rmx2i*tempb3
    tempb1 = -(pcorf*vcx*temp2*mn*fmoyb)
    tempb4 = temp4*tempb1/vtot**2
    fluid_frac_hybb = fluid_frac_hybb + tempb4
    vtotb = vtotb - 2*vtot*temp5*tempb4
    temp4 = dnndz*rmx2i/vtot
    csb = csb + 3*cs**2*temp5*tempb1 + (2*cs*nnf*rmx1i+3*cs**2*temp4)*&
&     tempb0
    tempb4 = (nnf*rmx1i*cs**2+temp4*temp3)*tempb2
    pcorfb = pcorfb + vcx*temp2*tempb5 + vcx*temp1*tempb4
    vcxb = vcxb + pcorf*temp2*tempb5 + pcorf*temp1*tempb4
    tempb1 = pcorf*vcx*tempb4/vtot
    fluid_frac_hybb = fluid_frac_hybb + tempb1
    vtotb = vtotb - temp1*tempb1 - temp4*tempb3
