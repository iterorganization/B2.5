    rmx1ib = nnf*tempb
    CALL POPCONTROL2B(branch)
    IF (branch .EQ. 0) THEN
      fnnib = 0.D0
    ELSE
      IF (branch .EQ. 1) THEN
        fnnib = 0.D0
        fna_mol_reflb = 0.D0
      ELSE
