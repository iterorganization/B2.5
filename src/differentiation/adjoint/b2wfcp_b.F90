!    ..write region specs
      WRITE(nprt, '(a)') '*cbregs    nr    breakpoints'
      WRITE(nprt, '(1x,a,t12,i2,(t17,7f9.3))') '''south''', cbnrso, (&
&     cbrbrk(ireg), ireg=cbirso,cbirso+cbnrso-1)
