  USE B2MOD_ZHFRTF_DIFF, ONLY : avm_u, nal, b2mod_zhfrtf_avm_u, &
& b2mod_zhfrtf_prep
  USE B2MOD_B2MREL, ONLY : s2nucl, nucl2s, znnucl_int
  USE B2MOD_SWITCHES_DIFF
  USE B2US_GEO_DIFF
  USE B2US_MAP_DIFF
  USE B2MOD_SUBSYS
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_ZHFRTF_DIFF, ONLY : z2n_xy, ia, av_ualpha, z_to_m1_ast, &
& rho_a_rel
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
!lkw 20.06.2022
  INTEGER, INTENT(IN) :: ncv, nfc, nvx, ns, ismain
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  REAL(kind=r8) :: na(ncv, 0:ns-1), ua(ncv, 0:ns-1), ti(ncv), rza(ncv, 0&
& :ns-1), vsaf_cl(nfc, 0:1, 0:ns-1), vsaf_drho(nfc, 0:1, 0:ns-1), &
& vsaf_uadp_albe(nfc, 0:1, 0:nspecies-1, 0:nspecies-1), vsaf_ubdp_al(nfc&
& , 0:1, 0:nspecies-1), fac_vis(nfc)
!   ..output arguments (unspecified on entry)
  REAL(kind=r8) :: fchvispar_a(nfc, 0:1, 0:ns-1), fchvispar(nfc, 0:1)
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2TVSPA computes the poloidal and radial components of the
!     parallel viscosity current.
!
!  8. further comments
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..global named constants
!
!   ..local variables
  INTEGER :: is, ifc, inucl, iz
  REAL(kind=r8) :: dvpar(nfc), wrk(ncv), wrk0(nfc), wrk1(nfc), wrk2(nfc)&
& , wrk3(nfc), wrk4(nfc), wrkvx(nvx), gonedbsq(nfc, 0:1), wght(nfc, 2)
!   ..procedures
  EXTERNAL XERTST
  EXTERNAL B2XVSG
  INTRINSIC MAXVAL
  INTRINSIC SQRT
  INTRINSIC NINT
  INTRINSIC MINVAL
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
!   ..initialization
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2tvspa')
!   ..set internal parameters on first call
!   ..test nCv, nFc
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
!
  IF (switch%zhdanov_closure .NE. 0 .AND. switch%zhdanov_test .NE. 1 &
&     .AND. switch%zhdanov_visu .NE. 0) THEN
!   ..re-calculate mass-averaged velocity
!som 16.08.21
    CALL B2MOD_ZHFRTF_AVM_U(ncv, ns, na, ua)
!   ..re-calculate isonuclear density
!som 30.04.24
    CALL B2MOD_ZHFRTF_PREP(ncv, ns, na, ua)
  END IF
!
  wght = 1.0_R8
!srv 24.06.99
  dvpar = 0.0_R8
!srv
  result1 = MAXVAL(fac_vis)
  IF (switch%no_current .EQ. 0 .AND. switch%fhe_vis_par .NE. 0.0_R8 &
&     .AND. result1 .NE. 0.0_R8) THEN
!srv
!
!     ..compute gradients of OnedBsq on cell faces
! Note: directly compute magnetic prefactor on faces
!       (original code: averaging from cell centers)
    CALL GRAD_NODIFF(ncv, nfc, nvx, 1, geo, mpg, geo%cvonedbsq, geo%&
&              vxonedbsq, gonedbsq)
!
!     ..compute pre-factor
    wrk0 = geo%fcbb(:, 2)*SQRT(geo%fcbb(:, 3))/(3.0_R8*geo%fcbb(:, 0))
!
!     ..compute parallel viscosity current
    fchvispar = 0.0_R8
!srv 24.06.99
    DO is=0,ns-1
!
      IF ((switch%zhdanov_closure .EQ. 0 .OR. switch%zhdanov_test .EQ. 1&
&         ) .OR. switch%zhdanov_visu .EQ. 0) THEN
!srv 24.06.99
!
!       ..compute dvpar
        IF (is .EQ. ismain .AND. (.NOT.is_neutral(is))) THEN
          wrk = ua(:, is)*SQRT(geo%cvbb(:, 3))
          CALL GRAD_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk, wrkvx, &
&                      wrk1)
          dvpar = wrk0*vsaf_cl(:, 0, is)*wrk1
        ELSE
          dvpar = 0.0_R8
        END IF
      ELSE IF (.NOT.is_neutral(is)) THEN
!
!srv 24.06.99
!
!       ..compute dvpar
        IF (switch%zh_vis_type .EQ. 0) THEN
!som 13.01.22
!
          wrk = ua(:, is)*SQRT(geo%cvbb(:, 3))
          CALL GRAD_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk, wrkvx, &
&                      wrk1)
          dvpar = wrk0*vsaf_drho(:, 0, is)*wrk1
!
        ELSE IF (switch%zh_vis_type .EQ. 1) THEN
!som 13.01.22
!
          wrk = avm_u*SQRT(geo%cvbb(:, 3))
          CALL GRAD_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk, wrkvx, &
&                      wrk1)
          dvpar = wrk0*vsaf_drho(:, 0, is)*wrk1
!
        ELSE IF (switch%zh_vis_type .EQ. 2) THEN
!som 13.01.22
!
          wrk = ua(:, is)*SQRT(geo%cvbb(:, 3))
          CALL GRAD_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk, wrkvx, &
&                      wrk1)
          dvpar = wrk0*vsaf_cl(:, 0, is)*wrk1
!
          wrk = na(:, is)/nal(:, s2nucl(is)-1)
          CALL INTFACE(ncv, nfc, mpg%fccv, wght, wrk, wrk1)
!
!som 30.04.24
          DO inucl=0,nspecies-1
            IF (inucl .NE. s2nucl(is) - 1) THEN
!
              wrk2 = 0.0_R8
              DO iz=1,znnucl_int(inucl)
!
                wrk = ua(:, nucl2s(inucl, iz))*SQRT(geo%cvbb(:, 3))
                CALL GRAD_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk, &
&                            wrkvx, wrk3)
!
                wrk = na(:, nucl2s(inucl, iz))*ti
                CALL INTFACE(ncv, nfc, mpg%fccv, wght, wrk, wrk4)
!
                wrk2 = wrk2 + wrk3*wrk4
              END DO
!
              dvpar = dvpar + wrk0*wrk1*wrk2*vsaf_uadp_albe(:, 0, s2nucl&
&               (is)-1, inucl)
!
            END IF
          END DO
!
          wrk2 = 0.0_R8
!wdk not clear if Zh-closure applies to bundling;
!wdk if yes: need to update if-condition here
          DO iz=1,znnucl_int(s2nucl(is)-1)
            result1 = MAXVAL(rza(:, is))
            result2 = MINVAL(rza(:, is))
            IF (iz .NE. NINT(result1) .OR. iz .NE. NINT(result2)) THEN
!
              wrk = ua(:, nucl2s(s2nucl(is)-1, iz))*SQRT(geo%cvbb(:, 3))
              CALL GRAD_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk, wrkvx&
&                          , wrk3)
!
              wrk = na(:, nucl2s(s2nucl(is)-1, iz))*ti
              CALL INTFACE(ncv, nfc, mpg%fccv, wght, wrk, wrk4)
!
              wrk2 = wrk2 + wrk3*wrk4
            END IF
          END DO
!
          dvpar = dvpar + wrk0*wrk1*wrk2*(vsaf_uadp_albe(:, 0, s2nucl(is&
&           )-1, s2nucl(is)-1)-switch%zhcscorr*vsaf_ubdp_al(:, 0, s2nucl&
&           (is)-1))
!
        END IF
      ELSE
        dvpar = 0.0_R8
      END IF
!
!
!      ..components of the viscosity parallel current
      fchvispar_a(:, 0, is) = -(switch%fhe_vis_par*fac_vis*dvpar*&
&       gonedbsq(:, 1)*geo%fcs*geo%fcqalf(:, 0))
      fchvispar_a(:, 1, is) = switch%fhe_vis_par*fac_vis*dvpar*gonedbsq(&
&       :, 0)*geo%fcs*geo%fcqalf(:, 1)
!
!      ..accumulate individual contributions into fchvispar
      fchvispar = fchvispar + fchvispar_a(:, :, is)
!
!srv 24.06.99
    END DO
!
!     ..set zero outside of core
!lkw 20.06.2022{
    DO ifc=1,nfc
      IF (.NOT.mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. (.NOT.mpg%&
&         cvonclosedsurface(mpg%fccv(ifc, 2)))) THEN
        fchvispar(ifc, 0:1) = 0.0_R8
        fchvispar_a(ifc, 0:1, 0:ns-1) = 0.0_R8
      END IF
!lkw 20.06.2022}

    END DO
  ELSE
!
    fchvispar = 0.0_R8
    fchvispar_a = 0.0_R8
  END IF
!
! ..return
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2tvspa
!
END SUBROUTINE B2TVSPA_NODIFF
