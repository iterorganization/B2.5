SUBROUTINE B2TFCC_B(ncv, nfc, nvx, ns, switch, geo, geob, mpg, mpgb, pl&
& , plb, dv, dvb, co, cob, rt, rtb)
  USE B2MOD_TYPES
  USE B2MOD_CONSTANTS
  USE B2MOD_B2CMPA_DIFF
  USE B2MOD_SWITCHES_DIFF
  USE B2US_GEO_DIFF
  USE B2US_MAP_DIFF
  USE B2US_PLASMA_DIFF
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_AD_DIFF, ONLY : ncall_b2tfcc
  USE B2MOD_SUBSYS
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx, ns
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(GEOMETRY_DIFF) :: geob
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(MAPPING_DIFF) :: mpgb
  TYPE(B2PLASMA), INTENT(IN) :: pl
  TYPE(B2PLASMA_DIFF) :: plb
  TYPE(B2COEFF), INTENT(IN) :: co
  TYPE(B2COEFF) :: cob
  TYPE(B2RATES), INTENT(IN) :: rt
  TYPE(B2RATES_DIFF) :: rtb
!   ..input/output arguments
  TYPE(B2DERIVATIVES), INTENT(INOUT) :: dv
  TYPE(B2DERIVATIVES), INTENT(INOUT) :: dvb
!   ..common blocks
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: is, ifc
  REAL(kind=r8) :: nw(ncv), nwf(nfc), dnw(nfc, 0:1), tef(nfc), tif(nfc)&
& , tnf(nfc), wght(nfc, 2), wrkf(nfc), wrkvx(nvx), flo(nfc, 0:1), con(&
& nfc, 0:1), flo0(nfc, 0:1), con0(nfc, 0:1)
  REAL(kind=r8) :: nwb(ncv), nwfb(nfc), dnwb(nfc, 0:1), tefb(nfc), tifb(&
& nfc), tnfb(nfc), wrkfb(nfc), wrkvxb(nvx), flob(nfc, 0:1), conb(nfc, 0:&
& 1), flo0b(nfc, 0:1), con0b(nfc, 0:1)
!   ..procedures
  EXTERNAL XERTST
  EXTERNAL B2XVSG, DIFF_NODIFF, INTFACE
  EXTERNAL DIFF_FWD, DIFF_BWD, INTFACE_FWD, INTFACE_BWD
  INTRINSIC NINT
  REAL(r8), DIMENSION(nfc) :: tempb
  REAL(kind=r8), DIMENSION(nfc) :: tempb0
  REAL(r8) :: temp
  REAL(r8) :: tempb1
  REAL(r8) :: tempb2
  REAL(r8) :: temp0
  REAL(r8) :: tempb3
  REAL(r8) :: tempb4
  REAL(r8) :: temp1
  REAL(r8) :: tempb5
  INTEGER*4 :: branch
!   ..initialisation
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
!   ..test nCv, nFc, ns
!   ..extensive tests on first few calls
  IF (ncall_b2tfcc .LT. 3) THEN
!    ..test sign of cdpa
    DO is=0,ns-1
      wrkf(:) = co%cdpa(:, 1, is)*geo%fcqalf(:, 1)
    END DO
    CALL PUSHCONTROL1B(0)
  ELSE
    CALL PUSHCONTROL1B(1)
  END IF
!
!   ..interpolate te and ti to cell faces
  wght = 1.0_R8
  CALL INTFACE_FWD(ncv, nfc, mpg%fccv, wght, pl%te, tef)
  CALL INTFACE_FWD(ncv, nfc, mpg%fccv, wght, pl%ti, tif)
  CALL INTFACE_FWD(ncv, nfc, mpg%fccv, wght, pl%tn, tnf)
!
! ..correct coefficients
!
  DO is=0,ns-1
!
    CALL PUSHREAL8ARRAY(nw, r8*ncv/8)
    nw = pl%na(:, is)*(rt%rza(:, is)+1.0_R8)
!
!    ..pre-compute rza interpolated to faces, inverse volume weighted
    CALL INTFACE_FWD(ncv, nfc, mpg%fccv, geo%fcvol, rt%rza(:, is), wrkf)
!
!    ..pre-compute radial differences nw
    CALL DIFF_FWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, nw, wrkvx, dnw&
&          )
!
!    ..interpolate nw to cell faces
    CALL INTFACE_FWD(ncv, nfc, mpg%fccv, wght, nw, nwf)
!
!   ..correct floe, cone
!
!    ..compute poloidal/radial coefficients for pressure diffusion term
    CALL PUSHREAL8ARRAY(flo(:, 0), r8*nfc/8)
    flo(:, 0) = -(switch%xfac*2.5_R8*co%cdpa(:, 0, is)*dnw(:, 0)*wrkf*&
&     tef)
    CALL PUSHREAL8ARRAY(flo(:, 1), r8*nfc/8)
    flo(:, 1) = -(switch%xfac*2.5_R8*co%cdpa(:, 1, is)*dnw(:, 1)*wrkf*&
&     tef)
    CALL PUSHREAL8ARRAY(con(:, 0), r8*nfc/8)
    con(:, 0) = switch%xfac*2.5_R8*co%cdpa(:, 0, is)*nwf*wrkf*tef
    CALL PUSHREAL8ARRAY(con(:, 1), r8*nfc/8)
    con(:, 1) = switch%xfac*2.5_R8*co%cdpa(:, 1, is)*nwf*wrkf*tef
!
!    ..compute coefficients for stencil
    CALL CALCCOEF_FWD(ncv, nfc, nvx, 0, geo, flo, con, flo0, con0)
!
!    ..add to previous contributions
!
!   ..correct floi, coni, flon, conn
!
!    ..compute poloidal/radial coefficients for pressure diffusion term
    IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
      CALL PUSHREAL8ARRAY(flo(:, 0), r8*nfc/8)
      flo(:, 0) = -(switch%xfac*2.5_R8*co%cdpa(:, 0, is)*dnw(:, 0)*tif)
      CALL PUSHREAL8ARRAY(flo(:, 1), r8*nfc/8)
      flo(:, 1) = -(switch%xfac*2.5_R8*co%cdpa(:, 1, is)*dnw(:, 1)*tif)
      CALL PUSHREAL8ARRAY(con(:, 0), r8*nfc/8)
      con(:, 0) = switch%xfac*2.5_R8*co%cdpa(:, 0, is)*nwf*tif
      CALL PUSHREAL8ARRAY(con(:, 1), r8*nfc/8)
      con(:, 1) = switch%xfac*2.5_R8*co%cdpa(:, 1, is)*nwf*tif
      CALL PUSHCONTROL1B(0)
    ELSE
!! in case of default energy equation Tn = Ti
      CALL PUSHREAL8ARRAY(flo(:, 0), r8*nfc/8)
      flo(:, 0) = -(switch%xfac*2.5_R8*co%cdpa(:, 0, is)*dnw(:, 0)*tnf)
      CALL PUSHREAL8ARRAY(flo(:, 1), r8*nfc/8)
      flo(:, 1) = -(switch%xfac*2.5_R8*co%cdpa(:, 1, is)*dnw(:, 1)*tnf)
      CALL PUSHREAL8ARRAY(con(:, 0), r8*nfc/8)
      con(:, 0) = switch%xfac*2.5_R8*co%cdpa(:, 0, is)*nwf*tnf
      CALL PUSHREAL8ARRAY(con(:, 1), r8*nfc/8)
      con(:, 1) = switch%xfac*2.5_R8*co%cdpa(:, 1, is)*nwf*tnf
      CALL PUSHCONTROL1B(1)
    END IF
!
!    ..compute coefficients for stencil
    CALL CALCCOEF_FWD(ncv, nfc, nvx, 0, geo, flo, con, flo0, con0)
!
!    ..add to previous contributions
! the logic statements could be simplified but for now this is not done to improve readability
    IF (switch%tn_style .EQ. 0) THEN
      CALL PUSHCONTROL3B(0)
    ELSE IF (switch%tn_style .EQ. 1) THEN
!! pure ion energy equation, only add ion contributions
      IF (.NOT.is_neutral(is)) THEN
        CALL PUSHCONTROL3B(1)
      ELSE
        CALL PUSHCONTROL3B(2)
      END IF
    ELSE IF (switch%tn_style .EQ. 2) THEN
!! separate ion and neutral energy equation, split contributions
      IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
        CALL PUSHCONTROL3B(3)
      ELSE
        CALL PUSHCONTROL3B(4)
      END IF
    ELSE
      CALL PUSHCONTROL3B(5)
    END IF
  END DO
!
!  ..add contributions from Rhie-Chow term to floe, cone
!
!   ..pre-compute radial differences
  CALL DIFF_FWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, dv%ne, wrkvx, &
&         dnw)
!
!   ..interpolate nwe and nwi to cell faces
  CALL INTFACE_FWD(ncv, nfc, mpg%fccv, wght, dv%ne, nwf)
!
  DO ifc=1,nfc
    CALL PUSHREAL8(con(ifc, 0), r8/8)
    con(ifc, 0) = switch%b2npht_pcm1*1.25_R8*geo%fcpbs(ifc)*(dv%pccm(mpg&
&     %fccv(ifc, 1), 1)*dv%ne(mpg%fccv(ifc, 1))*pl%te(mpg%fccv(ifc, 1))+&
&     dv%pccm(mpg%fccv(ifc, 2), 1)*dv%ne(mpg%fccv(ifc, 2))*pl%te(mpg%&
&     fccv(ifc, 2)))/(geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc&
&     , 2)))
  END DO
!
!   ..compute poloidal/radial coefficients for pressure diffusion term
  CALL PUSHREAL8ARRAY(flo(:, 0), r8*nfc/8)
  flo(:, 0) = -(con(:, 0)*dnw(:, 0))
  CALL PUSHREAL8ARRAY(con(:, 0), r8*nfc/8)
  con(:, 0) = con(:, 0)*nwf
!
  CALL PUSHREAL8ARRAY(flo(:, 1), r8*nfc/8)
  flo(:, 1) = 0.0_R8
  CALL PUSHREAL8ARRAY(con(:, 1), r8*nfc/8)
  con(:, 1) = 0.0_R8
!
!   ..compute coefficients for stencil
  CALL CALCCOEF_FWD(ncv, nfc, nvx, 0, geo, flo, con, flo0, con0)
!
!   ..add to previous contributions
!
!   ..add contributions from Rhie-Chow term to floi, coni, flon, conn
!
  DO is=0,ns-1
!
!    ..pre-compute radial differences
    CALL DIFF_FWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, pl%na(1, is), &
&           wrkvx, dnw)
!
!    ..interpolate nwe to cell faces
    CALL INTFACE_FWD(ncv, nfc, mpg%fccv, wght, nw, nwf)
!
    IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
      DO ifc=1,nfc
        CALL PUSHREAL8(con(ifc, 0), r8/8)
        con(ifc, 0) = switch%b2npht_pcm1*1.25_R8*geo%fcpbs(ifc)*(dv%pccm&
&         (mpg%fccv(ifc, 1), 1)*pl%na(mpg%fccv(ifc, 1), is)*pl%ti(mpg%&
&         fccv(ifc, 1))+dv%pccm(mpg%fccv(ifc, 2), 1)*pl%na(mpg%fccv(ifc&
&         , 2), is)*pl%ti(mpg%fccv(ifc, 2)))/(geo%fcqgam(ifc, 0)*(geo%&
&         fchc(ifc, 1)+geo%fchc(ifc, 2)))
      END DO
      CALL PUSHCONTROL1B(0)
    ELSE
      DO ifc=1,nfc
        CALL PUSHREAL8(con(ifc, 0), r8/8)
        con(ifc, 0) = switch%b2npht_pcm1*1.25_R8*geo%fcpbs(ifc)*(dv%pccm&
&         (mpg%fccv(ifc, 1), 1)*pl%na(mpg%fccv(ifc, 1), is)*pl%tn(mpg%&
&         fccv(ifc, 1))+dv%pccm(mpg%fccv(ifc, 2), 1)*pl%na(mpg%fccv(ifc&
&         , 2), is)*pl%tn(mpg%fccv(ifc, 2)))/(geo%fcqgam(ifc, 0)*(geo%&
&         fchc(ifc, 1)+geo%fchc(ifc, 2)))
      END DO
      CALL PUSHCONTROL1B(1)
    END IF
!
!    ..compute poloidal/radial coefficients for pressure diffusion term
    CALL PUSHREAL8ARRAY(flo(:, 0), r8*nfc/8)
    flo(:, 0) = -(con(:, 0)*dnw(:, 0))
    CALL PUSHREAL8ARRAY(con(:, 0), r8*nfc/8)
    con(:, 0) = con(:, 0)*nwf
!
    CALL PUSHREAL8ARRAY(flo(:, 1), r8*nfc/8)
    flo(:, 1) = 0.0_R8
    CALL PUSHREAL8ARRAY(con(:, 1), r8*nfc/8)
    con(:, 1) = 0.0_R8
!
!    ..compute coefficients for stencil
    CALL CALCCOEF_FWD(ncv, nfc, nvx, 0, geo, flo, con, flo0, con0)
!
!    ..add to previous contributions
! the logic statements could be simplified but for now this is not done to improve readability
    IF (switch%tn_style .EQ. 0) THEN
      CALL PUSHCONTROL3B(0)
    ELSE IF (switch%tn_style .EQ. 1) THEN
!! pure ion energy equation, only add ion contributions
      IF (.NOT.is_neutral(is)) THEN
        CALL PUSHCONTROL3B(1)
      ELSE
        CALL PUSHCONTROL3B(2)
      END IF
    ELSE IF (switch%tn_style .EQ. 2) THEN
!! separate ion and neutral energy equation, split contributions
      IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
        CALL PUSHCONTROL3B(3)
      ELSE
        CALL PUSHCONTROL3B(4)
      END IF
    ELSE
      CALL PUSHCONTROL3B(5)
    END IF
  END DO
  nwfb = 0.D0
  dnwb = 0.D0
  nwb = 0.D0
  con0b = 0.D0
  flo0b = 0.D0
  wrkvxb = 0.D0
  flob = 0.D0
  conb = 0.D0
  DO is=ns-1,0,-1
    CALL POPCONTROL3B(branch)
    IF (branch .LT. 3) THEN
      IF (branch .EQ. 0) THEN
        con0b = con0b + dvb%coni(:, :, 0)
        flo0b = flo0b + dvb%floi
      ELSE IF (branch .EQ. 1) THEN
        con0b = con0b + dvb%coni(:, :, 0)
        flo0b = flo0b + dvb%floi
      END IF
    ELSE IF (branch .EQ. 3) THEN
      con0b = con0b + dvb%coni(:, :, 0)
      flo0b = flo0b + dvb%floi
    ELSE IF (branch .EQ. 4) THEN
      con0b = con0b + dvb%conn
      flo0b = flo0b + dvb%flon
    END IF
    CALL CALCCOEF_BWD(ncv, nfc, nvx, 0, geo, flo, flob, con, conb, flo0&
&               , flo0b, con0, con0b)
    CALL POPREAL8ARRAY(con(:, 1), r8*nfc/8)
    conb(:, 1) = 0.D0
    CALL POPREAL8ARRAY(flo(:, 1), r8*nfc/8)
    flob(:, 1) = 0.D0
    CALL POPREAL8ARRAY(con(:, 0), r8*nfc/8)
    nwfb = nwfb + con(:, 0)*conb(:, 0)
    conb(:, 0) = nwf*conb(:, 0) - dnw(:, 0)*flob(:, 0)
    CALL POPREAL8ARRAY(flo(:, 0), r8*nfc/8)
    dnwb(:, 0) = dnwb(:, 0) - con(:, 0)*flob(:, 0)
    flob(:, 0) = 0.D0
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      DO ifc=nfc,1,-1
        CALL POPREAL8(con(ifc, 0), r8/8)
        temp = dv%pccm(mpg%fccv(ifc, 1), 1)
        temp1 = dv%pccm(mpg%fccv(ifc, 2), 1)
        tempb3 = switch%b2npht_pcm1*1.25_R8*geo%fcpbs(ifc)*conb(ifc, 0)/&
&         (geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2)))
        conb(ifc, 0) = 0.D0
        tempb2 = pl%na(mpg%fccv(ifc, 1), is)*tempb3
        plb%na(mpg%fccv(ifc, 1), is) = plb%na(mpg%fccv(ifc, 1), is) + &
&         temp*pl%ti(mpg%fccv(ifc, 1))*tempb3
        tempb4 = pl%na(mpg%fccv(ifc, 2), is)*tempb3
        plb%na(mpg%fccv(ifc, 2), is) = plb%na(mpg%fccv(ifc, 2), is) + &
&         temp1*pl%ti(mpg%fccv(ifc, 2))*tempb3
        dvb%pccm(mpg%fccv(ifc, 2), 1) = dvb%pccm(mpg%fccv(ifc, 2), 1) + &
&         pl%ti(mpg%fccv(ifc, 2))*tempb4
        plb%ti(mpg%fccv(ifc, 2)) = plb%ti(mpg%fccv(ifc, 2)) + temp1*&
&         tempb4
        dvb%pccm(mpg%fccv(ifc, 1), 1) = dvb%pccm(mpg%fccv(ifc, 1), 1) + &
&         pl%ti(mpg%fccv(ifc, 1))*tempb2
        plb%ti(mpg%fccv(ifc, 1)) = plb%ti(mpg%fccv(ifc, 1)) + temp*&
&         tempb2
      END DO
    ELSE
      DO ifc=nfc,1,-1
        CALL POPREAL8(con(ifc, 0), r8/8)
        temp0 = dv%pccm(mpg%fccv(ifc, 1), 1)
        temp = dv%pccm(mpg%fccv(ifc, 2), 1)
        tempb5 = switch%b2npht_pcm1*1.25_R8*geo%fcpbs(ifc)*conb(ifc, 0)/&
&         (geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2)))
        conb(ifc, 0) = 0.D0
        tempb4 = pl%na(mpg%fccv(ifc, 1), is)*tempb5
        plb%na(mpg%fccv(ifc, 1), is) = plb%na(mpg%fccv(ifc, 1), is) + &
&         temp0*pl%tn(mpg%fccv(ifc, 1))*tempb5
        tempb2 = pl%na(mpg%fccv(ifc, 2), is)*tempb5
        plb%na(mpg%fccv(ifc, 2), is) = plb%na(mpg%fccv(ifc, 2), is) + &
&         temp*pl%tn(mpg%fccv(ifc, 2))*tempb5
        dvb%pccm(mpg%fccv(ifc, 2), 1) = dvb%pccm(mpg%fccv(ifc, 2), 1) + &
&         pl%tn(mpg%fccv(ifc, 2))*tempb2
        plb%tn(mpg%fccv(ifc, 2)) = plb%tn(mpg%fccv(ifc, 2)) + temp*&
&         tempb2
        dvb%pccm(mpg%fccv(ifc, 1), 1) = dvb%pccm(mpg%fccv(ifc, 1), 1) + &
&         pl%tn(mpg%fccv(ifc, 1))*tempb4
        plb%tn(mpg%fccv(ifc, 1)) = plb%tn(mpg%fccv(ifc, 1)) + temp0*&
&         tempb4
      END DO
    END IF
    CALL INTFACE_BWD(ncv, nfc, mpg%fccv, wght, nw, nwb, nwf, nwfb)
    CALL DIFF_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, pl%na(1, is), &
&           plb%na(1, is), wrkvx, wrkvxb, dnw, dnwb)
  END DO
  con0b = con0b + dvb%cone(:, :, 0)
  flo0b = flo0b + dvb%floe
  CALL CALCCOEF_BWD(ncv, nfc, nvx, 0, geo, flo, flob, con, conb, flo0, &
&             flo0b, con0, con0b)
  CALL POPREAL8ARRAY(con(:, 1), r8*nfc/8)
  conb(:, 1) = 0.D0
  CALL POPREAL8ARRAY(flo(:, 1), r8*nfc/8)
  flob(:, 1) = 0.D0
  CALL POPREAL8ARRAY(con(:, 0), r8*nfc/8)
  nwfb = nwfb + con(:, 0)*conb(:, 0)
  conb(:, 0) = nwf*conb(:, 0) - dnw(:, 0)*flob(:, 0)
  CALL POPREAL8ARRAY(flo(:, 0), r8*nfc/8)
  dnwb(:, 0) = dnwb(:, 0) - con(:, 0)*flob(:, 0)
  flob(:, 0) = 0.D0
  DO ifc=nfc,1,-1
    CALL POPREAL8(con(ifc, 0), r8/8)
    tempb1 = switch%b2npht_pcm1*1.25_R8*geo%fcpbs(ifc)*conb(ifc, 0)/(geo&
&     %fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2)))
    conb(ifc, 0) = 0.D0
    dvb%pccm(mpg%fccv(ifc, 1), 1) = dvb%pccm(mpg%fccv(ifc, 1), 1) + dv%&
&     ne(mpg%fccv(ifc, 1))*pl%te(mpg%fccv(ifc, 1))*tempb1
    tempb2 = dv%pccm(mpg%fccv(ifc, 1), 1)*tempb1
    dvb%pccm(mpg%fccv(ifc, 2), 1) = dvb%pccm(mpg%fccv(ifc, 2), 1) + dv%&
&     ne(mpg%fccv(ifc, 2))*pl%te(mpg%fccv(ifc, 2))*tempb1
    tempb3 = dv%pccm(mpg%fccv(ifc, 2), 1)*tempb1
    dvb%ne(mpg%fccv(ifc, 2)) = dvb%ne(mpg%fccv(ifc, 2)) + pl%te(mpg%fccv&
&     (ifc, 2))*tempb3
    plb%te(mpg%fccv(ifc, 2)) = plb%te(mpg%fccv(ifc, 2)) + dv%ne(mpg%fccv&
&     (ifc, 2))*tempb3
    dvb%ne(mpg%fccv(ifc, 1)) = dvb%ne(mpg%fccv(ifc, 1)) + pl%te(mpg%fccv&
&     (ifc, 1))*tempb2
    plb%te(mpg%fccv(ifc, 1)) = plb%te(mpg%fccv(ifc, 1)) + dv%ne(mpg%fccv&
&     (ifc, 1))*tempb2
  END DO
  CALL INTFACE_BWD(ncv, nfc, mpg%fccv, wght, dv%ne, dvb%ne, nwf, nwfb)
  CALL DIFF_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, dv%ne, dvb%ne, &
&         wrkvx, wrkvxb, dnw, dnwb)
  wrkfb = 0.D0
  tefb = 0.D0
  tifb = 0.D0
  tnfb = 0.D0
  DO is=ns-1,0,-1
    CALL POPCONTROL3B(branch)
    IF (branch .LT. 3) THEN
      IF (branch .EQ. 0) THEN
        con0b = con0b + dvb%coni(:, :, 0)
        flo0b = flo0b + dvb%floi
      ELSE IF (branch .EQ. 1) THEN
        con0b = con0b + dvb%coni(:, :, 0)
        flo0b = flo0b + dvb%floi
      END IF
    ELSE IF (branch .EQ. 3) THEN
      con0b = con0b + dvb%coni(:, :, 0)
      flo0b = flo0b + dvb%floi
    ELSE IF (branch .EQ. 4) THEN
      con0b = con0b + dvb%conn
      flo0b = flo0b + dvb%flon
    END IF
    CALL CALCCOEF_BWD(ncv, nfc, nvx, 0, geo, flo, flob, con, conb, flo0&
&               , flo0b, con0, con0b)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      CALL POPREAL8ARRAY(con(:, 1), r8*nfc/8)
      cob%cdpa(:, 1, is) = cob%cdpa(:, 1, is) + nwf*tif*switch%xfac*&
&       2.5_R8*conb(:, 1)
      tempb0 = co%cdpa(:, 1, is)*switch%xfac*2.5_R8*conb(:, 1)
      conb(:, 1) = 0.D0
      nwfb = nwfb + tif*tempb0
      tifb = tifb + nwf*tempb0
      CALL POPREAL8ARRAY(con(:, 0), r8*nfc/8)
      cob%cdpa(:, 0, is) = cob%cdpa(:, 0, is) + nwf*tif*switch%xfac*&
&       2.5_R8*conb(:, 0)
      tempb0 = co%cdpa(:, 0, is)*switch%xfac*2.5_R8*conb(:, 0)
      conb(:, 0) = 0.D0
      nwfb = nwfb + tif*tempb0
      tifb = tifb + nwf*tempb0
      CALL POPREAL8ARRAY(flo(:, 1), r8*nfc/8)
      cob%cdpa(:, 1, is) = cob%cdpa(:, 1, is) - dnw(:, 1)*tif*switch%&
&       xfac*2.5_R8*flob(:, 1)
      tempb0 = -(co%cdpa(:, 1, is)*switch%xfac*2.5_R8*flob(:, 1))
      flob(:, 1) = 0.D0
      dnwb(:, 1) = dnwb(:, 1) + tif*tempb0
      tifb = tifb + dnw(:, 1)*tempb0
      CALL POPREAL8ARRAY(flo(:, 0), r8*nfc/8)
      cob%cdpa(:, 0, is) = cob%cdpa(:, 0, is) - dnw(:, 0)*tif*switch%&
&       xfac*2.5_R8*flob(:, 0)
      tempb0 = -(co%cdpa(:, 0, is)*switch%xfac*2.5_R8*flob(:, 0))
      flob(:, 0) = 0.D0
      dnwb(:, 0) = dnwb(:, 0) + tif*tempb0
      tifb = tifb + dnw(:, 0)*tempb0
    ELSE
      CALL POPREAL8ARRAY(con(:, 1), r8*nfc/8)
      cob%cdpa(:, 1, is) = cob%cdpa(:, 1, is) + nwf*tnf*switch%xfac*&
&       2.5_R8*conb(:, 1)
      tempb0 = co%cdpa(:, 1, is)*switch%xfac*2.5_R8*conb(:, 1)
      conb(:, 1) = 0.D0
      nwfb = nwfb + tnf*tempb0
      tnfb = tnfb + nwf*tempb0
      CALL POPREAL8ARRAY(con(:, 0), r8*nfc/8)
      cob%cdpa(:, 0, is) = cob%cdpa(:, 0, is) + nwf*tnf*switch%xfac*&
&       2.5_R8*conb(:, 0)
      tempb0 = co%cdpa(:, 0, is)*switch%xfac*2.5_R8*conb(:, 0)
      conb(:, 0) = 0.D0
      nwfb = nwfb + tnf*tempb0
      tnfb = tnfb + nwf*tempb0
      CALL POPREAL8ARRAY(flo(:, 1), r8*nfc/8)
      cob%cdpa(:, 1, is) = cob%cdpa(:, 1, is) - dnw(:, 1)*tnf*switch%&
&       xfac*2.5_R8*flob(:, 1)
      tempb0 = -(co%cdpa(:, 1, is)*switch%xfac*2.5_R8*flob(:, 1))
      flob(:, 1) = 0.D0
      dnwb(:, 1) = dnwb(:, 1) + tnf*tempb0
      tnfb = tnfb + dnw(:, 1)*tempb0
      CALL POPREAL8ARRAY(flo(:, 0), r8*nfc/8)
      cob%cdpa(:, 0, is) = cob%cdpa(:, 0, is) - dnw(:, 0)*tnf*switch%&
&       xfac*2.5_R8*flob(:, 0)
      tempb0 = -(co%cdpa(:, 0, is)*switch%xfac*2.5_R8*flob(:, 0))
      flob(:, 0) = 0.D0
      dnwb(:, 0) = dnwb(:, 0) + tnf*tempb0
      tnfb = tnfb + dnw(:, 0)*tempb0
    END IF
    con0b = con0b + dvb%cone(:, :, 0)
    flo0b = flo0b + dvb%floe
    CALL CALCCOEF_BWD(ncv, nfc, nvx, 0, geo, flo, flob, con, conb, flo0&
&               , flo0b, con0, con0b)
    CALL POPREAL8ARRAY(con(:, 1), r8*nfc/8)
    tempb = switch%xfac*2.5_R8*conb(:, 1)
    conb(:, 1) = 0.D0
    cob%cdpa(:, 1, is) = cob%cdpa(:, 1, is) + tef*nwf*wrkf*tempb
    tefb = tefb + co%cdpa(:, 1, is)*nwf*wrkf*tempb
    tempb0 = co%cdpa(:, 1, is)*tef*tempb
    nwfb = nwfb + wrkf*tempb0
    wrkfb = wrkfb + nwf*tempb0
    CALL POPREAL8ARRAY(con(:, 0), r8*nfc/8)
    tempb = switch%xfac*2.5_R8*conb(:, 0)
    conb(:, 0) = 0.D0
    cob%cdpa(:, 0, is) = cob%cdpa(:, 0, is) + tef*nwf*wrkf*tempb
    tefb = tefb + co%cdpa(:, 0, is)*nwf*wrkf*tempb
    tempb0 = co%cdpa(:, 0, is)*tef*tempb
    nwfb = nwfb + wrkf*tempb0
    wrkfb = wrkfb + nwf*tempb0
    CALL POPREAL8ARRAY(flo(:, 1), r8*nfc/8)
    tempb = -(switch%xfac*2.5_R8*flob(:, 1))
    flob(:, 1) = 0.D0
    cob%cdpa(:, 1, is) = cob%cdpa(:, 1, is) + dnw(:, 1)*wrkf*tef*tempb
    dnwb(:, 1) = dnwb(:, 1) + co%cdpa(:, 1, is)*wrkf*tef*tempb
    tempb0 = co%cdpa(:, 1, is)*dnw(:, 1)*tempb
    wrkfb = wrkfb + tef*tempb0
    tefb = tefb + wrkf*tempb0
    CALL POPREAL8ARRAY(flo(:, 0), r8*nfc/8)
    tempb = -(switch%xfac*2.5_R8*flob(:, 0))
    flob(:, 0) = 0.D0
    cob%cdpa(:, 0, is) = cob%cdpa(:, 0, is) + dnw(:, 0)*wrkf*tef*tempb
    dnwb(:, 0) = dnwb(:, 0) + co%cdpa(:, 0, is)*wrkf*tef*tempb
    tempb0 = co%cdpa(:, 0, is)*dnw(:, 0)*tempb
    wrkfb = wrkfb + tef*tempb0
    tefb = tefb + wrkf*tempb0
    CALL INTFACE_BWD(ncv, nfc, mpg%fccv, wght, nw, nwb, nwf, nwfb)
    CALL DIFF_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, nw, nwb, wrkvx&
&           , wrkvxb, dnw, dnwb)
    CALL INTFACE_BWD(ncv, nfc, mpg%fccv, geo%fcvol, rt%rza(:, is), rtb%&
&              rza(:, is), wrkf, wrkfb)
    CALL POPREAL8ARRAY(nw, r8*ncv/8)
    plb%na(:, is) = plb%na(:, is) + (rt%rza(:, is)+1.0_R8)*nwb
    rtb%rza(:, is) = rtb%rza(:, is) + pl%na(:, is)*nwb
    nwb = 0.D0
  END DO
  CALL INTFACE_BWD(ncv, nfc, mpg%fccv, wght, pl%tn, plb%tn, tnf, tnfb)
  CALL INTFACE_BWD(ncv, nfc, mpg%fccv, wght, pl%ti, plb%ti, tif, tifb)
  CALL INTFACE_BWD(ncv, nfc, mpg%fccv, wght, pl%te, plb%te, tef, tefb)
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
    DO is=ns-1,0,-1
      cob%cdpa(:, 1, is) = cob%cdpa(:, 1, is) + geo%fcqalf(:, 1)*wrkfb
      wrkfb = 0.D0
    END DO
  END IF
END SUBROUTINE B2TFCC_B

