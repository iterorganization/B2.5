  ELSE IF (STREQL(chf, 'unformatted')) THEN
!    ..unformatted write
    WRITE(nput) idcod, idtyp, n, id0
    IF (0 .LT. n) WRITE(nput) (refun(i), i=0,n-1)
  ELSE
    CALL XERRAB('cfwure--invalid file form-property: '//chf)
  END IF
  RETURN
