!  Differentiation of init_mapping as a context to call adjoint code (with options context noISIZE r8):
!   Plus diff mem management of: m.divfcor:in-out
!
!*********************************************************************
!
  SUBROUTINE INIT_MAPPING_B(m, mb)
    IMPLICIT NONE
    TYPE(MAPPING), INTENT(INOUT) :: m
    TYPE(MAPPING_DIFF), INTENT(INOUT) :: mb
    INTEGER :: i, j, k, l, indd
    INTEGER :: icv, icv1, icv2, ifc, ifcc, ifs, iva, ivb, inv, inv1, &
&   inv2, ivx, ivx1, ivx2, invmx, inew
    INTEGER :: totalcount, fscount, iactive, icount, fccount
    INTEGER, ALLOCATABLE :: indcv(:), indfsvx(:), indxpt(:), indfc(:), &
&   cvnvloc(:), freg(:), old_face_list(:), new_face_list(:)
    INTEGER :: fcsb(MAXVAL(m%fclbl), m%nci), fcsbcounter(MAXVAL(m%fclbl)&
&   ), cvsb(MAXVAL(m%fclbl), m%nci)
    LOGICAL :: duplicate, left_match_found, right_match_found, is_found
    EXTERNAL IPGETI, XERRAB
    INTRINSIC ALLOCATED
    INTRINSIC MIN
    integer :: result1
    REAL(kind=r8) :: result2
    INTRINSIC MAXVAL
!wdk  This initialization routine precomputes a number of arrays
!wdk  in the mapping that are not stored in the b2fgmtry file, but
!wdk  can be derived directly from data in that file
!
!   ..regions
    m%nnreg(0) = MAXVAL(m%cvreg)
    m%nnreg(1) = MAXVAL(m%fcreg)
!
!   ..set up connectivity information for the matrix stencil
    ALLOCATE(mb%cvnvp(m%ncv, 2))
    mb%cvnvp = 0
    ALLOCATE(m%cvnvp(m%ncv, 2))
    ALLOCATE(cvnvloc(20*m%ncv))
! initial overestimate of size cvNv array
    ALLOCATE(indcv(100))
! (over)estimate of max number number of neighbors an individual cell could have
!
    inv = 1
    DO icv=1,m%ncv
! init: each cell appears in its own stencil
      m%cvnvp(icv, 1) = inv
      indcv = icv
! list all cells surrounding cell iCv, by listing the
! cells connecting to all vertices of cell iCv
      invmx = 1
      DO ivx=1,m%cvvxp(icv, 2)
! the number in the vertex list
        ivx1 = m%cvvx(m%cvvxp(icv, 1)+ivx-1)
! all cells connecting to this vertex
        indcv(invmx+1:invmx+m%vxcvp(ivx1, 2)) = m%vxcv(m%vxcvp(ivx1, 1):&
&         m%vxcvp(ivx1, 1)+m%vxcvp(ivx1, 2)-1)
        invmx = invmx + m%vxcvp(ivx1, 2)
! remove duplicates
      END DO
      cvnvloc(m%cvnvp(icv, 1)) = indcv(1)
      i = 1
      DO 100 inv1=2,invmx
        DO inv2=1,i
          IF (cvnvloc(m%cvnvp(icv, 1)+inv2-1) .EQ. indcv(inv1)) GOTO 100
        END DO
        i = i + 1
        cvnvloc(m%cvnvp(icv, 1)+i-1) = indcv(inv1)
! TODO: sort by cell number, keeping first cell fixed (main diagonal)
 100  CONTINUE
      m%cvnvp(icv, 2) = i
      inv = inv + i
! allocate and fill m%cvNv
    END DO
!
    m%ncmxnv = inv - 1
    ALLOCATE(mb%cvnv(m%ncmxnv))
    mb%cvnv = 0
    ALLOCATE(m%cvnv(m%ncmxnv))
    m%cvnv = cvnvloc(1:m%ncmxnv)
!
!csc this is only needed for adjoint AD in b2usco, b2usmo, etc.
! clean up
    m%mxstencil = MAXVAL(m%cvnvp(1:m%ncv, 2))
!
    DEALLOCATE(cvnvloc)
    DEALLOCATE(indcv)
!
    ALLOCATE(indfsvx(m%nvx))
    m%fcfs = 0
    indfsvx = 0
    totalcount = 0
    DO ifs=1,m%nfs
      fscount = 0
      DO i=m%fsfcp(ifs, 1),m%fsfcp(ifs, 1)+m%fsfcp(ifs, 2)-1
        ifc = m%fsfc(i)
        m%fcfs(ifc) = ifs
        ivx1 = m%fcvx(ifc, 1)
        ivx2 = m%fcvx(ifc, 2)
        IF (m%vxfs(ivx1) .EQ. 0) THEN
          m%vxfs(ivx1) = ifs
          m%nfsvxmx = m%nfsvxmx + 1
          fscount = fscount + 1
          totalcount = totalcount + 1
          indfsvx(totalcount) = ivx1
        END IF
        IF (m%vxfs(ivx2) .EQ. 0) THEN
          m%vxfs(ivx2) = ifs
          m%nfsvxmx = m%nfsvxmx + 1
          fscount = fscount + 1
          totalcount = totalcount + 1
          indfsvx(totalcount) = ivx2
        END IF
      END DO
      IF (fscount .GT. 0) THEN
        m%fsvxp(ifs, 1) = totalcount - fscount + 1
        m%fsvxp(ifs, 2) = fscount
      END IF
    END DO
    m%nfsvxmx = totalcount
    ALLOCATE(mb%fsvx(m%nfsvxmx))
    mb%fsvx = 0
    ALLOCATE(m%fsvx(m%nfsvxmx))
    m%fsvx(1:m%nfsvxmx) = indfsvx(1:m%nfsvxmx)
    DEALLOCATE(indfsvx)
!
    ALLOCATE(indxpt(m%nvx))
    indxpt = 0
    m%nxpt = 0
    iactive = 0
    DO ivx=1,m%nvx
      IF (m%vxfcp(ivx, 2) .GE. 4) THEN
        fccount = 0
        DO i=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
          ifc = m%vxfc(i)
          IF (m%fcfs(ifc) .NE. 0) fccount = fccount + 1
        END DO
        IF (fccount .EQ. 4) THEN
          m%nxpt = m%nxpt + 1
          indxpt(m%nxpt) = ivx
          IF (iactive .EQ. 0) THEN
            DO i=m%vxcvp(ivx, 1),m%vxcvp(ivx, 1)+m%vxcvp(ivx, 2)-1
              IF (iactive .EQ. 0) THEN
                icv = m%vxcv(i)
                IF (m%cvreg(icv) .EQ. 1) iactive = m%nxpt
              END IF
            END DO
          END IF
        END IF
      END IF
    END DO
!
    m%nstr = 0
    m%ntgc = 0
    IF (m%nxpt .GT. 0) THEN
      ALLOCATE(mb%xpt(m%nxpt))
      mb%xpt = 0
      ALLOCATE(m%xpt(m%nxpt))
      m%xpt(1:m%nxpt) = indxpt(1:m%nxpt)
      IF (iactive .NE. 0) THEN
        DO i=1,m%vxfcp(m%xpt(iactive), 2)
          ifs = m%fcfs(m%vxfc(m%vxfcp(m%xpt(iactive), 1)+i-1))
          IF (ifs .NE. 0) THEN
            IF (m%ifssep .EQ. 0) m%ifssep = ifs
            IF (m%ifssep2 .EQ. 0 .AND. ifs .NE. m%ifssep) m%ifssep2 = &
&               ifs
          END IF
        END DO
      END IF
      indxpt = 0
      icount = 0
      ALLOCATE(mb%strvxp(m%nxpt, 2))
      mb%strvxp = 0
      ALLOCATE(m%strvxp(m%nxpt, 2))
      m%strvxp = 0
      m%strvxp(1, 1) = 1
      DO i=1,m%nxpt
        IF (i .GT. 1) m%strvxp(i, 1) = m%strvxp(i-1, 1) + m%strvxp(i-1, &
&           2)
        ifs = m%ifssep
        DO j=m%fsfcp(ifs, 1),m%fsfcp(ifs, 1)+m%fsfcp(ifs, 2)-1
          ifc = m%fsfc(j)
          DO k=1,2
            ivx = m%fcvx(ifc, k)
            fccount = 0
            DO l=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
              IF (m%fcfs(m%vxfc(l)) .EQ. ifs) fccount = fccount + 1
            END DO
            IF (fccount .EQ. 1) THEN
              icount = icount + 1
              m%strvxp(i, 2) = m%strvxp(i, 2) + 1
              indxpt(icount) = ivx
            END IF
          END DO
        END DO
        IF (m%ifssep2 .GT. 0) THEN
          ifs = m%ifssep2
          DO j=m%fsfcp(ifs, 1),m%fsfcp(ifs, 1)+m%fsfcp(ifs, 2)-1
            ifc = m%fsfc(j)
            DO k=1,2
              ivx = m%fcvx(ifc, k)
              fccount = 0
              DO l=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
                IF (m%fcfs(m%vxfc(l)) .EQ. ifs) fccount = fccount + 1
              END DO
              IF (fccount .EQ. 1) THEN
                icount = icount + 1
                m%strvxp(i, 2) = m%strvxp(i, 2) + 1
                indxpt(icount) = ivx
              END IF
            END DO
          END DO
        END IF
      END DO
      m%nstr = icount
      ALLOCATE(mb%strvx(m%nstr))
      mb%strvx = 0
      ALLOCATE(m%strvx(m%nstr))
      m%strvx(1:m%nstr) = indxpt(1:m%nstr)
      ALLOCATE(mb%strdiv(m%nstr))
      mb%strdiv = 0
      ALLOCATE(m%strdiv(m%nstr))
      ALLOCATE(freg(m%nstr))
      m%strdiv = 0
      freg = 0
      DO i=1,m%nstr
        ivx = m%strvx(i)
        DO j=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
          IF (m%strdiv(i) .EQ. 0) THEN
            ifc = m%vxfc(j)
            IF (m%fcreg(ifc) .EQ. 1) THEN
              m%strdiv(i) = 1
              freg(i) = 1
            ELSE IF (m%fcreg(ifc) .EQ. 4) THEN
              m%strdiv(i) = 2
              freg(i) = 4
            ELSE IF (m%fcreg(ifc) .EQ. 5) THEN
              IF (m%nxpt .EQ. 1) THEN
                m%strdiv(i) = 1
              ELSE
                m%strdiv(i) = 3
              END IF
              freg(i) = 5
            ELSE IF (m%fcreg(ifc) .EQ. 8) THEN
              IF (m%nxpt .EQ. 1) THEN
                m%strdiv(i) = 2
              ELSE
                m%strdiv(i) = 4
              END IF
              freg(i) = 8
            END IF
          END IF
        END DO
      END DO
      ALLOCATE(mb%tgvx(m%ntgc))
      mb%tgvx = 0
      ALLOCATE(m%tgvx(m%ntgc))
!csc needed for adjoint AD
    ELSE IF (m%nnreg(0) .EQ. 2) THEN
      ALLOCATE(freg(2))
      ifc = 1
      icount = 0
      DO WHILE (m%ifssep .EQ. 0 .AND. ifc .LE. m%nfc)
        icv1 = m%fccv(ifc, 1)
        icv2 = m%fccv(ifc, 2)
        IF (m%cvreg(icv1) .NE. m%cvreg(icv2) .AND. m%cvreg(icv1) .NE. 0 &
&           .AND. m%cvreg(icv2) .NE. 0) m%ifssep = m%fcfs(ifc)
        IF (m%ifssep .EQ. 0) ifc = ifc + 1
      END DO
      DO i=m%fsfcp(m%ifssep, 1),m%fsfcp(m%ifssep, 1)+m%fsfcp(m%ifssep, 2&
&         )-1
        ifc = m%fsfc(i)
        DO k=1,2
          ivx = m%fcvx(ifc, k)
          DO j=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
            ifcc = m%vxfc(j)
            IF (m%fclbl(ifcc) .NE. 0) THEN
              duplicate = .false.
              DO l=1,icount
                IF (indxpt(l) .EQ. ivx) duplicate = .true.
              END DO
              IF (.NOT.duplicate) THEN
                icount = icount + 1
                indxpt(icount) = ivx
              END IF
            END IF
          END DO
        END DO
      END DO
      m%ntgc = icount
      ALLOCATE(mb%tgvx(m%ntgc))
      mb%tgvx = 0
      ALLOCATE(m%tgvx(m%ntgc))
      m%tgvx(1:m%ntgc) = indxpt(1:m%ntgc)
      ALLOCATE(mb%strdiv(2))
      mb%strdiv = 0
      ALLOCATE(m%strdiv(2))
      m%strdiv(1) = 1
      m%strdiv(2) = 2
      freg(1) = 1
      freg(2) = 2
    ELSE IF (m%nnreg(0) .EQ. 1) THEN
      CALL IPGETI('b2mwti_jsep', m%ifssep)
      ALLOCATE(freg(2))
      freg(1) = 1
      freg(2) = 2
      IF (m%ifssep .GT. 0) THEN
        indxpt = 0
        icount = 0
        ALLOCATE(mb%strvxp(1, 2))
        mb%strvxp = 0
        ALLOCATE(m%strvxp(1, 2))
        m%strvxp(1, 1) = 1
        m%strvxp(1, 2) = 2
        DO j=m%fsfcp(m%ifssep, 1),m%fsfcp(m%ifssep, 1)+m%fsfcp(m%ifssep&
&           , 2)-1
          ifc = m%fsfc(j)
          DO k=1,2
            ivx = m%fcvx(ifc, k)
            fccount = 0
            DO l=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
              IF (m%fcfs(m%vxfc(l)) .EQ. m%ifssep) fccount = fccount + 1
            END DO
            IF (fccount .EQ. 1) THEN
              icount = icount + 1
              indxpt(icount) = ivx
            END IF
          END DO
        END DO
        m%nstr = icount
        IF (m%nstr .GT. 0) THEN
          ALLOCATE(mb%strvx(m%nstr))
          mb%strvx = 0
          ALLOCATE(m%strvx(m%nstr))
          m%strvx(1:m%nstr) = indxpt(1:m%nstr)
          ALLOCATE(mb%strdiv(m%nstr))
          mb%strdiv = 0
          ALLOCATE(m%strdiv(m%nstr))
          m%strdiv = 0
          DO i=1,m%nstr
            ivx = m%strvx(i)
            DO j=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
              IF (m%strdiv(i) .EQ. 0) THEN
                ifc = m%vxfc(j)
                IF (m%fcreg(ifc) .EQ. 1) THEN
                  m%strdiv(i) = 1
                ELSE IF (m%fcreg(ifc) .EQ. 2) THEN
                  m%strdiv(i) = 2
                END IF
              END IF
            END DO
          END DO
        END IF
      END IF
    END IF
    DEALLOCATE(indxpt)
!
    IF (ALLOCATED(m%strdiv)) THEN
      indd = MAXVAL(m%strdiv)
      IF (indd .GT. 0) THEN
        ALLOCATE(mb%ifdiv(MAXVAL(m%strdiv)))
        mb%ifdiv = 0
        ALLOCATE(m%ifdiv(MAXVAL(m%strdiv)))
        ALLOCATE(mb%ivdiv(MAXVAL(m%strdiv)))
        mb%ivdiv = 0
        ALLOCATE(m%ivdiv(MAXVAL(m%strdiv)))
        ALLOCATE(mb%divfcp(MAXVAL(m%strdiv), 2))
        mb%divfcp = 0
        ALLOCATE(m%divfcp(MAXVAL(m%strdiv), 2))
        ALLOCATE(indfc(m%nfc))
        icount = 0
        m%ifdiv = 0
        m%ivdiv = 0
        m%divfcp(1, 1) = 1
        m%divfcp(:, 2) = 0
! first assemble all the faces
        DO i=1,indd
          IF (i .GT. 1) m%divfcp(i, 1) = m%divfcp(i-1, 1) + m%divfcp(i-1&
&             , 2)
          DO ifc=1,m%nfc
            IF (m%fcreg(ifc) .EQ. freg(i)) THEN
              icount = icount + 1
              m%divfcp(i, 2) = m%divfcp(i, 2) + 1
              indfc(icount) = ifc
            END IF
          END DO
        END DO
        ALLOCATE(mb%divfc(icount))
        mb%divfc = 0
        ALLOCATE(m%divfc(icount))
        ALLOCATE(mb%divfcor(icount))
        mb%divfcor = 0.D0
        ALLOCATE(m%divfcor(icount))
        m%divfcor = 0.0_R8
        m%divfc(1:icount) = indfc(1:icount)
! now re-order them
        DO i=1,indd
          ALLOCATE(old_face_list(m%divfcp(i, 2)))
          ALLOCATE(new_face_list(m%divfcp(i, 2)))
          old_face_list(1:m%divfcp(i, 2)) = m%divfc(m%divfcp(i, 1):m%&
&           divfcp(i, 1)+m%divfcp(i, 2)-1)
          new_face_list(:) = 0
          new_face_list(1) = m%divfc(m%divfcp(i, 1))
          old_face_list(1) = 0
          iva = m%fcvx(m%divfc(m%divfcp(i, 1)), 1)
          ivb = m%fcvx(m%divfc(m%divfcp(i, 1)), 2)
          inew = 1
          DO WHILE (inew .LT. m%divfcp(i, 2))
            left_match_found = .false.
            DO j=1,m%divfcp(i, 2)
              IF (.NOT.left_match_found) THEN
                IF (old_face_list(j) .NE. 0) THEN
                  IF (iva .EQ. m%fcvx(old_face_list(j), 1)) THEN
                    left_match_found = .true.
                    iva = m%fcvx(old_face_list(j), 2)
                  ELSE IF (iva .EQ. m%fcvx(old_face_list(j), 2)) THEN
                    left_match_found = .true.
                    iva = m%fcvx(old_face_list(j), 1)
                  END IF
                  IF (left_match_found) THEN
                    indfc(1:inew) = new_face_list(1:inew)
                    new_face_list(1) = old_face_list(j)
                    new_face_list(2:inew+1) = indfc(1:inew)
                    old_face_list(j) = 0
                    inew = inew + 1
                  END IF
                END IF
              END IF
            END DO
            right_match_found = .false.
            DO j=1,m%divfcp(i, 2)
              IF (.NOT.right_match_found) THEN
                IF (old_face_list(j) .NE. 0) THEN
                  IF (ivb .EQ. m%fcvx(old_face_list(j), 1)) THEN
                    right_match_found = .true.
                    ivb = m%fcvx(old_face_list(j), 2)
                  ELSE IF (ivb .EQ. m%fcvx(old_face_list(j), 2)) THEN
                    right_match_found = .true.
                    ivb = m%fcvx(old_face_list(j), 1)
                  END IF
                  IF (right_match_found) THEN
                    new_face_list(inew+1) = old_face_list(j)
                    old_face_list(j) = 0
                    inew = inew + 1
                  END IF
                END IF
              END IF
            END DO
            IF (.NOT.(left_match_found .OR. right_match_found) .AND. &
&               inew .LT. m%divfcp(i, 2)) THEN
              DO j=1,inew
                ifc = new_face_list(j)
                iva = m%fcvx(ifc, 1)
                ivb = m%fcvx(ifc, 2)
                WRITE(6, '(a,4i6)') &
&               'Chain of faces and vertices in new list ', j, ifc, iva&
&               , ivb
              END DO
              DO j=1,m%divfcp(i, 2)
                IF (old_face_list(j) .NE. 0) THEN
                  ifc = m%divfc(m%divfcp(i, 1)+j-1)
                  iva = m%fcvx(ifc, 1)
                  ivb = m%fcvx(ifc, 2)
                  WRITE(6, '(a,4i6)') &
&                 'Left over faces and vertices in old list', j, ifc, &
&                 iva, ivb
                END IF
              END DO
              CALL XERRAB('Broken face chain along target')
            END IF
          END DO
          m%divfc(m%divfcp(i, 1):m%divfcp(i, 1)+m%divfcp(i, 2)-1) = &
&           new_face_list(1:m%divfcp(i, 2))
          DEALLOCATE(old_face_list)
          DEALLOCATE(new_face_list)
        END DO
        DEALLOCATE(indfc)
      END IF
    END IF
    IF (ALLOCATED(freg)) THEN
      DEALLOCATE(freg)
    ELSE
      WRITE(*, *) 'Targetless topology detected :'
      WRITE(*, *) 'Strike point data not defined'
    END IF
!
    ALLOCATE(mb%cvalongboundary(m%ncv))
    mb%cvalongboundary = .false.
    ALLOCATE(m%cvalongboundary(m%ncv))
    m%cvalongboundary = .false.
    fcsbcounter = 0
    DO icv=m%nci+1,m%ncv
      ifc = m%cvfc(m%cvfcp(icv, 1))
      IF (m%fclbl(ifc) .GT. 0) THEN
        fcsbcounter(m%fclbl(ifc)) = fcsbcounter(m%fclbl(ifc)) + 1
        fcsb(m%fclbl(ifc), fcsbcounter(m%fclbl(ifc))) = ifc
        icv1 = m%fccv(ifc, 1)
        icv2 = m%fccv(ifc, 2)
        IF (icv1 .GT. icv2) THEN
          cvsb(m%fclbl(ifc), fcsbcounter(m%fclbl(ifc))) = icv2
        ELSE
          cvsb(m%fclbl(ifc), fcsbcounter(m%fclbl(ifc))) = icv1
        END IF
      END IF
    END DO
    result1 = MAXVAL(m%fclbl)
    DO k=1,result1
      DO j=1,fcsbcounter(k)
        icv2 = cvsb(k, j)
        ifc = fcsb(k, j)
        icv1 = MAXVAL(m%fccv(ifc, 1:2))
        IF (j .LT. fcsbcounter(k)) THEN
          IF (m%cvft(icv2) .EQ. m%cvft(cvsb(k, j+1))) m%cvalongboundary(&
&           icv2) = .true.
        END IF
        IF (j .GT. 1) THEN
          IF (m%cvft(icv2) .EQ. m%cvft(cvsb(k, j-1))) m%cvalongboundary(&
&           icv2) = .true.
        END IF
      END DO
    END DO
!
    ALLOCATE(mb%fcs_wall(m%ncg))
    mb%fcs_wall = 0
    ALLOCATE(m%fcs_wall(m%ncg))
!
    k = 1
    DO l=m%nci+1,m%ncv
      m%fcs_wall(k) = m%cvfc(m%cvfcp(l, 1))
      k = k + 1
    END DO
!
    m%nfci = 0
    DO k=1,m%ncg
      is_found = .false.
      DO l=k+1,m%ncg
        IF (((m%fcvx(m%fcs_wall(k), 1) .EQ. m%fcvx(m%fcs_wall(l), 1) &
&           .OR. m%fcvx(m%fcs_wall(k), 1) .EQ. m%fcvx(m%fcs_wall(l), 2))&
&           .OR. m%fcvx(m%fcs_wall(k), 2) .EQ. m%fcvx(m%fcs_wall(l), 1))&
&           .OR. m%fcvx(m%fcs_wall(k), 2) .EQ. m%fcvx(m%fcs_wall(l), 2)&
&       ) GOTO 110
      END DO
      GOTO 120
 110  is_found = .true.
      inew = m%fcs_wall(k+1)
      m%fcs_wall(k+1) = m%fcs_wall(l)
      m%fcs_wall(l) = inew
      result1 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k+1), 1:2)))
      result2 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k), 1:2)))
      IF (result1 .NE. result2) m%nfci = m%nfci + 1
 120  IF (.NOT.is_found) THEN
        result1 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k-1), 1:2)))
        result2 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k), 1:2)))
        IF (k .LT. m%ncg .OR. (k .EQ. m%ncg .AND. result1 .NE. result2)&
&       ) m%nfci = m%nfci + 1
      END IF
    END DO
    ALLOCATE(mb%fcs_wall_ind(m%nfci, 2))
    mb%fcs_wall_ind = 0
    ALLOCATE(m%fcs_wall_ind(m%nfci, 2))
    j = 1
    l = 1
    DO k=1,m%ncg-1
      result1 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k+1), 1:2)))
      result2 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k), 1:2)))
      IF (result1 .NE. result2) THEN
        m%fcs_wall_ind(l, 1) = j
        m%fcs_wall_ind(l, 2) = k - j + 1
        j = k + 1
        l = l + 1
      END IF
! Doing the last cell by hand if it is on its own flux tube

    END DO
    result1 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(m%ncg-1), 1:2)))
    result2 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(m%ncg), 1:2)))
    IF (result1 .NE. result2) THEN
      m%fcs_wall_ind(l, 1) = m%ncg
      m%fcs_wall_ind(l, 2) = 1
    END IF
!
    WRITE(*, *) 'list of boundary faces in one flux tube:'
    DO j=1,m%nfci
      IF (m%fcs_wall_ind(j, 1) .NE. m%fcs_wall_ind(j, 2)) THEN
        WRITE(*, '(3i4)') j, m%fcs_wall_ind(j, 1), m%fcs_wall_ind(j, 2)
        WRITE(*, *) m%fcs_wall(m%fcs_wall_ind(j, 1):m%fcs_wall_ind(j, 1)&
&       +m%fcs_wall_ind(j, 2)-1)
      END IF
    END DO
!
    WRITE(*, *) 'nXpt, nStr, nTgc ', m%nxpt, m%nstr, m%ntgc
    WRITE(*, *) 'nFsVxmx          ', m%nfsvxmx
!
    RETURN
  END SUBROUTINE INIT_MAPPING_B

!
!*********************************************************************
!
  SUBROUTINE INIT_MAPPING(m)
    IMPLICIT NONE
    TYPE(MAPPING), INTENT(INOUT) :: m
    INTEGER :: i, j, k, l, indd
    INTEGER :: icv, icv1, icv2, ifc, ifcc, ifs, iva, ivb, inv, inv1, &
&   inv2, ivx, ivx1, ivx2, invmx, inew
    INTEGER :: totalcount, fscount, iactive, icount, fccount
    INTEGER, ALLOCATABLE :: indcv(:), indfsvx(:), indxpt(:), indfc(:), &
&   cvnvloc(:), freg(:), old_face_list(:), new_face_list(:)
    INTEGER :: fcsb(MAXVAL(m%fclbl), m%nci), fcsbcounter(MAXVAL(m%fclbl)&
&   ), cvsb(MAXVAL(m%fclbl), m%nci)
    LOGICAL :: duplicate, left_match_found, right_match_found, is_found
    EXTERNAL IPGETI, XERRAB
    INTRINSIC ALLOCATED
    INTRINSIC MIN
    integer :: result1
    REAL(kind=r8) :: result2
    INTRINSIC MAXVAL
!wdk  This initialization routine precomputes a number of arrays
!wdk  in the mapping that are not stored in the b2fgmtry file, but
!wdk  can be derived directly from data in that file
!
!   ..regions
    m%nnreg(0) = MAXVAL(m%cvreg)
    m%nnreg(1) = MAXVAL(m%fcreg)
!
!   ..set up connectivity information for the matrix stencil
    ALLOCATE(m%cvnvp(m%ncv, 2))
    ALLOCATE(cvnvloc(20*m%ncv))
! initial overestimate of size cvNv array
    ALLOCATE(indcv(100))
! (over)estimate of max number number of neighbors an individual cell could have
!
    inv = 1
    DO icv=1,m%ncv
! init: each cell appears in its own stencil
      m%cvnvp(icv, 1) = inv
      indcv = icv
! list all cells surrounding cell iCv, by listing the
! cells connecting to all vertices of cell iCv
      invmx = 1
      DO ivx=1,m%cvvxp(icv, 2)
! the number in the vertex list
        ivx1 = m%cvvx(m%cvvxp(icv, 1)+ivx-1)
! all cells connecting to this vertex
        indcv(invmx+1:invmx+m%vxcvp(ivx1, 2)) = m%vxcv(m%vxcvp(ivx1, 1):&
&         m%vxcvp(ivx1, 1)+m%vxcvp(ivx1, 2)-1)
        invmx = invmx + m%vxcvp(ivx1, 2)
! remove duplicates
      END DO
      cvnvloc(m%cvnvp(icv, 1)) = indcv(1)
      i = 1
      DO 100 inv1=2,invmx
        DO inv2=1,i
          IF (cvnvloc(m%cvnvp(icv, 1)+inv2-1) .EQ. indcv(inv1)) GOTO 100
        END DO
        i = i + 1
        cvnvloc(m%cvnvp(icv, 1)+i-1) = indcv(inv1)
! TODO: sort by cell number, keeping first cell fixed (main diagonal)
 100  CONTINUE
      m%cvnvp(icv, 2) = i
      inv = inv + i
! allocate and fill m%cvNv
    END DO
!
    m%ncmxnv = inv - 1
    ALLOCATE(m%cvnv(m%ncmxnv))
    m%cvnv = cvnvloc(1:m%ncmxnv)
!
!csc this is only needed for adjoint AD in b2usco, b2usmo, etc.
! clean up
    m%mxstencil = MAXVAL(m%cvnvp(1:m%ncv, 2))
!
    DEALLOCATE(cvnvloc)
    DEALLOCATE(indcv)
!
    ALLOCATE(indfsvx(m%nvx))
    m%fcfs = 0
    indfsvx = 0
    totalcount = 0
    DO ifs=1,m%nfs
      fscount = 0
      DO i=m%fsfcp(ifs, 1),m%fsfcp(ifs, 1)+m%fsfcp(ifs, 2)-1
        ifc = m%fsfc(i)
        m%fcfs(ifc) = ifs
        ivx1 = m%fcvx(ifc, 1)
        ivx2 = m%fcvx(ifc, 2)
        IF (m%vxfs(ivx1) .EQ. 0) THEN
          m%vxfs(ivx1) = ifs
          m%nfsvxmx = m%nfsvxmx + 1
          fscount = fscount + 1
          totalcount = totalcount + 1
          indfsvx(totalcount) = ivx1
        END IF
        IF (m%vxfs(ivx2) .EQ. 0) THEN
          m%vxfs(ivx2) = ifs
          m%nfsvxmx = m%nfsvxmx + 1
          fscount = fscount + 1
          totalcount = totalcount + 1
          indfsvx(totalcount) = ivx2
        END IF
      END DO
      IF (fscount .GT. 0) THEN
        m%fsvxp(ifs, 1) = totalcount - fscount + 1
        m%fsvxp(ifs, 2) = fscount
      END IF
    END DO
    m%nfsvxmx = totalcount
    ALLOCATE(m%fsvx(m%nfsvxmx))
    m%fsvx(1:m%nfsvxmx) = indfsvx(1:m%nfsvxmx)
    DEALLOCATE(indfsvx)
!
    ALLOCATE(indxpt(m%nvx))
    indxpt = 0
    m%nxpt = 0
    iactive = 0
    DO ivx=1,m%nvx
      IF (m%vxfcp(ivx, 2) .GE. 4) THEN
        fccount = 0
        DO i=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
          ifc = m%vxfc(i)
          IF (m%fcfs(ifc) .NE. 0) fccount = fccount + 1
        END DO
        IF (fccount .EQ. 4) THEN
          m%nxpt = m%nxpt + 1
          indxpt(m%nxpt) = ivx
          IF (iactive .EQ. 0) THEN
            DO i=m%vxcvp(ivx, 1),m%vxcvp(ivx, 1)+m%vxcvp(ivx, 2)-1
              IF (iactive .EQ. 0) THEN
                icv = m%vxcv(i)
                IF (m%cvreg(icv) .EQ. 1) iactive = m%nxpt
              END IF
            END DO
          END IF
        END IF
      END IF
    END DO
!
    m%nstr = 0
    m%ntgc = 0
    IF (m%nxpt .GT. 0) THEN
      ALLOCATE(m%xpt(m%nxpt))
      m%xpt(1:m%nxpt) = indxpt(1:m%nxpt)
      IF (iactive .NE. 0) THEN
        DO i=1,m%vxfcp(m%xpt(iactive), 2)
          ifs = m%fcfs(m%vxfc(m%vxfcp(m%xpt(iactive), 1)+i-1))
          IF (ifs .NE. 0) THEN
            IF (m%ifssep .EQ. 0) m%ifssep = ifs
            IF (m%ifssep2 .EQ. 0 .AND. ifs .NE. m%ifssep) m%ifssep2 = &
&               ifs
          END IF
        END DO
      END IF
      indxpt = 0
      icount = 0
      ALLOCATE(m%strvxp(m%nxpt, 2))
      m%strvxp = 0
      m%strvxp(1, 1) = 1
      DO i=1,m%nxpt
        IF (i .GT. 1) m%strvxp(i, 1) = m%strvxp(i-1, 1) + m%strvxp(i-1, &
&           2)
        ifs = m%ifssep
        DO j=m%fsfcp(ifs, 1),m%fsfcp(ifs, 1)+m%fsfcp(ifs, 2)-1
          ifc = m%fsfc(j)
          DO k=1,2
            ivx = m%fcvx(ifc, k)
            fccount = 0
            DO l=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
              IF (m%fcfs(m%vxfc(l)) .EQ. ifs) fccount = fccount + 1
            END DO
            IF (fccount .EQ. 1) THEN
              icount = icount + 1
              m%strvxp(i, 2) = m%strvxp(i, 2) + 1
              indxpt(icount) = ivx
            END IF
          END DO
        END DO
        IF (m%ifssep2 .GT. 0) THEN
          ifs = m%ifssep2
          DO j=m%fsfcp(ifs, 1),m%fsfcp(ifs, 1)+m%fsfcp(ifs, 2)-1
            ifc = m%fsfc(j)
            DO k=1,2
              ivx = m%fcvx(ifc, k)
              fccount = 0
              DO l=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
                IF (m%fcfs(m%vxfc(l)) .EQ. ifs) fccount = fccount + 1
              END DO
              IF (fccount .EQ. 1) THEN
                icount = icount + 1
                m%strvxp(i, 2) = m%strvxp(i, 2) + 1
                indxpt(icount) = ivx
              END IF
            END DO
          END DO
        END IF
      END DO
      m%nstr = icount
      ALLOCATE(m%strvx(m%nstr))
      m%strvx(1:m%nstr) = indxpt(1:m%nstr)
      ALLOCATE(m%strdiv(m%nstr))
      ALLOCATE(freg(m%nstr))
      m%strdiv = 0
      freg = 0
      DO i=1,m%nstr
        ivx = m%strvx(i)
        DO j=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
          IF (m%strdiv(i) .EQ. 0) THEN
            ifc = m%vxfc(j)
            IF (m%fcreg(ifc) .EQ. 1) THEN
              m%strdiv(i) = 1
              freg(i) = 1
            ELSE IF (m%fcreg(ifc) .EQ. 4) THEN
              m%strdiv(i) = 2
              freg(i) = 4
            ELSE IF (m%fcreg(ifc) .EQ. 5) THEN
              IF (m%nxpt .EQ. 1) THEN
                m%strdiv(i) = 1
              ELSE
                m%strdiv(i) = 3
              END IF
              freg(i) = 5
            ELSE IF (m%fcreg(ifc) .EQ. 8) THEN
              IF (m%nxpt .EQ. 1) THEN
                m%strdiv(i) = 2
              ELSE
                m%strdiv(i) = 4
              END IF
              freg(i) = 8
            END IF
          END IF
        END DO
      END DO
      ALLOCATE(m%tgvx(m%ntgc))
!csc needed for adjoint AD
    ELSE IF (m%nnreg(0) .EQ. 2) THEN
      ALLOCATE(freg(2))
      ifc = 1
      icount = 0
      DO WHILE (m%ifssep .EQ. 0 .AND. ifc .LE. m%nfc)
        icv1 = m%fccv(ifc, 1)
        icv2 = m%fccv(ifc, 2)
        IF (m%cvreg(icv1) .NE. m%cvreg(icv2) .AND. m%cvreg(icv1) .NE. 0 &
&           .AND. m%cvreg(icv2) .NE. 0) m%ifssep = m%fcfs(ifc)
        IF (m%ifssep .EQ. 0) ifc = ifc + 1
      END DO
      DO i=m%fsfcp(m%ifssep, 1),m%fsfcp(m%ifssep, 1)+m%fsfcp(m%ifssep, 2&
&         )-1
        ifc = m%fsfc(i)
        DO k=1,2
          ivx = m%fcvx(ifc, k)
          DO j=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
            ifcc = m%vxfc(j)
            IF (m%fclbl(ifcc) .NE. 0) THEN
              duplicate = .false.
              DO l=1,icount
                IF (indxpt(l) .EQ. ivx) duplicate = .true.
              END DO
              IF (.NOT.duplicate) THEN
                icount = icount + 1
                indxpt(icount) = ivx
              END IF
            END IF
          END DO
        END DO
      END DO
      m%ntgc = icount
      ALLOCATE(m%tgvx(m%ntgc))
      m%tgvx(1:m%ntgc) = indxpt(1:m%ntgc)
      ALLOCATE(m%strdiv(2))
      m%strdiv(1) = 1
      m%strdiv(2) = 2
      freg(1) = 1
      freg(2) = 2
    ELSE IF (m%nnreg(0) .EQ. 1) THEN
      CALL IPGETI('b2mwti_jsep', m%ifssep)
      ALLOCATE(freg(2))
      freg(1) = 1
      freg(2) = 2
      IF (m%ifssep .GT. 0) THEN
        indxpt = 0
        icount = 0
        ALLOCATE(m%strvxp(1, 2))
        m%strvxp(1, 1) = 1
        m%strvxp(1, 2) = 2
        DO j=m%fsfcp(m%ifssep, 1),m%fsfcp(m%ifssep, 1)+m%fsfcp(m%ifssep&
&           , 2)-1
          ifc = m%fsfc(j)
          DO k=1,2
            ivx = m%fcvx(ifc, k)
            fccount = 0
            DO l=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
              IF (m%fcfs(m%vxfc(l)) .EQ. m%ifssep) fccount = fccount + 1
            END DO
            IF (fccount .EQ. 1) THEN
              icount = icount + 1
              indxpt(icount) = ivx
            END IF
          END DO
        END DO
        m%nstr = icount
        IF (m%nstr .GT. 0) THEN
          ALLOCATE(m%strvx(m%nstr))
          m%strvx(1:m%nstr) = indxpt(1:m%nstr)
          ALLOCATE(m%strdiv(m%nstr))
          m%strdiv = 0
          DO i=1,m%nstr
            ivx = m%strvx(i)
            DO j=m%vxfcp(ivx, 1),m%vxfcp(ivx, 1)+m%vxfcp(ivx, 2)-1
              IF (m%strdiv(i) .EQ. 0) THEN
                ifc = m%vxfc(j)
                IF (m%fcreg(ifc) .EQ. 1) THEN
                  m%strdiv(i) = 1
                ELSE IF (m%fcreg(ifc) .EQ. 2) THEN
                  m%strdiv(i) = 2
                END IF
              END IF
            END DO
          END DO
        END IF
      END IF
    END IF
    DEALLOCATE(indxpt)
!
    IF (ALLOCATED(m%strdiv)) THEN
      indd = MAXVAL(m%strdiv)
      IF (indd .GT. 0) THEN
        ALLOCATE(m%ifdiv(MAXVAL(m%strdiv)))
        ALLOCATE(m%ivdiv(MAXVAL(m%strdiv)))
        ALLOCATE(m%divfcp(MAXVAL(m%strdiv), 2))
        ALLOCATE(indfc(m%nfc))
        icount = 0
        m%ifdiv = 0
        m%ivdiv = 0
        m%divfcp(1, 1) = 1
        m%divfcp(:, 2) = 0
! first assemble all the faces
        DO i=1,indd
          IF (i .GT. 1) m%divfcp(i, 1) = m%divfcp(i-1, 1) + m%divfcp(i-1&
&             , 2)
          DO ifc=1,m%nfc
            IF (m%fcreg(ifc) .EQ. freg(i)) THEN
              icount = icount + 1
              m%divfcp(i, 2) = m%divfcp(i, 2) + 1
              indfc(icount) = ifc
            END IF
          END DO
        END DO
        ALLOCATE(m%divfc(icount))
        ALLOCATE(m%divfcor(icount))
        m%divfcor = 0.0_R8
        m%divfc(1:icount) = indfc(1:icount)
! now re-order them
        DO i=1,indd
          ALLOCATE(old_face_list(m%divfcp(i, 2)))
          ALLOCATE(new_face_list(m%divfcp(i, 2)))
          old_face_list(1:m%divfcp(i, 2)) = m%divfc(m%divfcp(i, 1):m%&
&           divfcp(i, 1)+m%divfcp(i, 2)-1)
          new_face_list(:) = 0
          new_face_list(1) = m%divfc(m%divfcp(i, 1))
          old_face_list(1) = 0
          iva = m%fcvx(m%divfc(m%divfcp(i, 1)), 1)
          ivb = m%fcvx(m%divfc(m%divfcp(i, 1)), 2)
          inew = 1
          DO WHILE (inew .LT. m%divfcp(i, 2))
            left_match_found = .false.
            DO j=1,m%divfcp(i, 2)
              IF (.NOT.left_match_found) THEN
                IF (old_face_list(j) .NE. 0) THEN
                  IF (iva .EQ. m%fcvx(old_face_list(j), 1)) THEN
                    left_match_found = .true.
                    iva = m%fcvx(old_face_list(j), 2)
                  ELSE IF (iva .EQ. m%fcvx(old_face_list(j), 2)) THEN
                    left_match_found = .true.
                    iva = m%fcvx(old_face_list(j), 1)
                  END IF
                  IF (left_match_found) THEN
                    indfc(1:inew) = new_face_list(1:inew)
                    new_face_list(1) = old_face_list(j)
                    new_face_list(2:inew+1) = indfc(1:inew)
                    old_face_list(j) = 0
                    inew = inew + 1
                  END IF
                END IF
              END IF
            END DO
            right_match_found = .false.
            DO j=1,m%divfcp(i, 2)
              IF (.NOT.right_match_found) THEN
                IF (old_face_list(j) .NE. 0) THEN
                  IF (ivb .EQ. m%fcvx(old_face_list(j), 1)) THEN
                    right_match_found = .true.
                    ivb = m%fcvx(old_face_list(j), 2)
                  ELSE IF (ivb .EQ. m%fcvx(old_face_list(j), 2)) THEN
                    right_match_found = .true.
                    ivb = m%fcvx(old_face_list(j), 1)
                  END IF
                  IF (right_match_found) THEN
                    new_face_list(inew+1) = old_face_list(j)
                    old_face_list(j) = 0
                    inew = inew + 1
                  END IF
                END IF
              END IF
            END DO
            IF (.NOT.(left_match_found .OR. right_match_found) .AND. &
&               inew .LT. m%divfcp(i, 2)) THEN
              DO j=1,inew
                ifc = new_face_list(j)
                iva = m%fcvx(ifc, 1)
                ivb = m%fcvx(ifc, 2)
                WRITE(6, '(a,4i6)') &
&               'Chain of faces and vertices in new list ', j, ifc, iva&
&               , ivb
              END DO
              DO j=1,m%divfcp(i, 2)
                IF (old_face_list(j) .NE. 0) THEN
                  ifc = m%divfc(m%divfcp(i, 1)+j-1)
                  iva = m%fcvx(ifc, 1)
                  ivb = m%fcvx(ifc, 2)
                  WRITE(6, '(a,4i6)') &
&                 'Left over faces and vertices in old list', j, ifc, &
&                 iva, ivb
                END IF
              END DO
              CALL XERRAB('Broken face chain along target')
            END IF
          END DO
          m%divfc(m%divfcp(i, 1):m%divfcp(i, 1)+m%divfcp(i, 2)-1) = &
&           new_face_list(1:m%divfcp(i, 2))
          DEALLOCATE(old_face_list)
          DEALLOCATE(new_face_list)
        END DO
        DEALLOCATE(indfc)
      END IF
    END IF
    IF (ALLOCATED(freg)) THEN
      DEALLOCATE(freg)
    ELSE
      WRITE(*, *) 'Targetless topology detected :'
      WRITE(*, *) 'Strike point data not defined'
    END IF
!
    ALLOCATE(m%cvalongboundary(m%ncv))
    m%cvalongboundary = .false.
    fcsbcounter = 0
    DO icv=m%nci+1,m%ncv
      ifc = m%cvfc(m%cvfcp(icv, 1))
      IF (m%fclbl(ifc) .GT. 0) THEN
        fcsbcounter(m%fclbl(ifc)) = fcsbcounter(m%fclbl(ifc)) + 1
        fcsb(m%fclbl(ifc), fcsbcounter(m%fclbl(ifc))) = ifc
        icv1 = m%fccv(ifc, 1)
        icv2 = m%fccv(ifc, 2)
        IF (icv1 .GT. icv2) THEN
          cvsb(m%fclbl(ifc), fcsbcounter(m%fclbl(ifc))) = icv2
        ELSE
          cvsb(m%fclbl(ifc), fcsbcounter(m%fclbl(ifc))) = icv1
        END IF
      END IF
    END DO
    result1 = MAXVAL(m%fclbl)
    DO k=1,result1
      DO j=1,fcsbcounter(k)
        icv2 = cvsb(k, j)
        ifc = fcsb(k, j)
        icv1 = MAXVAL(m%fccv(ifc, 1:2))
        IF (j .LT. fcsbcounter(k)) THEN
          IF (m%cvft(icv2) .EQ. m%cvft(cvsb(k, j+1))) m%cvalongboundary(&
&           icv2) = .true.
        END IF
        IF (j .GT. 1) THEN
          IF (m%cvft(icv2) .EQ. m%cvft(cvsb(k, j-1))) m%cvalongboundary(&
&           icv2) = .true.
        END IF
      END DO
    END DO
!
    ALLOCATE(m%fcs_wall(m%ncg))
!
    k = 1
    DO l=m%nci+1,m%ncv
      m%fcs_wall(k) = m%cvfc(m%cvfcp(l, 1))
      k = k + 1
    END DO
!
    m%nfci = 0
    DO k=1,m%ncg
      is_found = .false.
      DO l=k+1,m%ncg
        IF (((m%fcvx(m%fcs_wall(k), 1) .EQ. m%fcvx(m%fcs_wall(l), 1) &
&           .OR. m%fcvx(m%fcs_wall(k), 1) .EQ. m%fcvx(m%fcs_wall(l), 2))&
&           .OR. m%fcvx(m%fcs_wall(k), 2) .EQ. m%fcvx(m%fcs_wall(l), 1))&
&           .OR. m%fcvx(m%fcs_wall(k), 2) .EQ. m%fcvx(m%fcs_wall(l), 2)&
&       ) THEN
          is_found = .true.
          inew = m%fcs_wall(k+1)
          m%fcs_wall(k+1) = m%fcs_wall(l)
          m%fcs_wall(l) = inew
          result1 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k+1), 1:2)))
          result2 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k), 1:2)))
          IF (result1 .NE. result2) m%nfci = m%nfci + 1
          GOTO 110
        END IF
      END DO
 110  IF (.NOT.is_found) THEN
        result1 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k-1), 1:2)))
        result2 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k), 1:2)))
        IF (k .LT. m%ncg .OR. (k .EQ. m%ncg .AND. result1 .NE. result2)&
&       ) m%nfci = m%nfci + 1
      END IF
    END DO
    ALLOCATE(m%fcs_wall_ind(m%nfci, 2))
    j = 1
    l = 1
    DO k=1,m%ncg-1
      result1 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k+1), 1:2)))
      result2 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(k), 1:2)))
      IF (result1 .NE. result2) THEN
        m%fcs_wall_ind(l, 1) = j
        m%fcs_wall_ind(l, 2) = k - j + 1
        j = k + 1
        l = l + 1
      END IF
! Doing the last cell by hand if it is on its own flux tube

    END DO
    result1 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(m%ncg-1), 1:2)))
    result2 = MAXVAL(m%cvft(m%fccv(m%fcs_wall(m%ncg), 1:2)))
    IF (result1 .NE. result2) THEN
      m%fcs_wall_ind(l, 1) = m%ncg
      m%fcs_wall_ind(l, 2) = 1
    END IF
!
    WRITE(*, *) 'list of boundary faces in one flux tube:'
    DO j=1,m%nfci
      IF (m%fcs_wall_ind(j, 1) .NE. m%fcs_wall_ind(j, 2)) THEN
        WRITE(*, '(3i4)') j, m%fcs_wall_ind(j, 1), m%fcs_wall_ind(j, 2)
        WRITE(*, *) m%fcs_wall(m%fcs_wall_ind(j, 1):m%fcs_wall_ind(j, 1)&
&       +m%fcs_wall_ind(j, 2)-1)
      END IF
    END DO
!
    WRITE(*, *) 'nXpt, nStr, nTgc ', m%nxpt, m%nstr, m%ntgc
    WRITE(*, *) 'nFsVxmx          ', m%nfsvxmx
!
    RETURN
  END SUBROUTINE INIT_MAPPING

