              DO iy=ad_to4,1,-1
                CALL POPCONTROL1B(branch)
              END DO
            END IF
            CALL POPINTEGER4(ad_to2)
            DO is=ad_to2,1,-1
              CALL POPINTEGER4(ad_to1)
              DO iy=ad_to1,1,-1
                DO ift=mpg%nft,1,-1
                  CALL POPCONTROL1B(branch)
                  IF (branch .NE. 0) THEN
                    CALL POPINTEGER4(ad_from)
                    CALL POPINTEGER4(ad_to0)
                    DO ic=ad_to0,ad_from,-1
                      CALL POPCONTROL4B(branch)
                      IF (branch .LT. 6) THEN
                        IF (branch .LT. 3) THEN
                          IF (branch .EQ. 0) THEN
                            GOTO 110
                          ELSE IF (branch .NE. 1) THEN
                            poloidal_scale_factor = 1.0_R8
                            temp0 = qe/pl%te(icv)
                            temp1 = SQRT(temp0)
                            tempb = poloidal_scale_factor*alf0b(icv)
                            alf0b(icv) = 0.D0
                            profb(iy) = profb(iy) + dv%ne(icv)*temp1*&
&                             tempb
                            dvb%ne(icv) = dvb%ne(icv) + prof(iy)*temp1*&
&                             tempb
                            IF (.NOT.temp0 .EQ. 0.D0) plb%te(icv) = plb%&
&                               te(icv) - temp0*prof(iy)*dv%ne(icv)*&
&                               tempb/(pl%te(icv)*2.0*temp1)
                          END IF
                        ELSE IF (branch .NE. 3) THEN
                          IF (branch .EQ. 4) THEN
                            poloidal_scale_factor = 1.0_R8
                            tempb = qe*poloidal_scale_factor*sig0b(icv)
                            sig0b(icv) = 0.D0
                            profb(iy) = profb(iy) + dv%ne(icv)*tempb
                            dvb%ne(icv) = dvb%ne(icv) + prof(iy)*tempb
                          ELSE
                            poloidal_scale_factor = 1.0_R8
                            tempb = poloidal_scale_factor*mp*am(nsp(is))&
&                             *vsa0b(icv, nsp(is))
                            vsa0b(icv, nsp(is)) = 0.D0
                            profb(iy) = profb(iy) + pl%na(icv, nsp(is))*&
&                             tempb
                            plb%na(icv, nsp(is)) = plb%na(icv, nsp(is)) &
&                             + prof(iy)*tempb
                          END IF
                        END IF
                      ELSE IF (branch .LT. 9) THEN
                        IF (branch .EQ. 6) THEN
                          poloidal_scale_factor = 1.0_R8
                          profb(iy) = profb(iy) + poloidal_scale_factor*&
&                           vla0b(icv, 1, nsp(is))
                          vla0b(icv, 1, nsp(is)) = 0.D0
                        ELSE IF (branch .EQ. 7) THEN
                          poloidal_scale_factor = 1.0_R8
                          profb(iy) = profb(iy) + poloidal_scale_factor*&
&                           vla0b(icv, 0, nsp(is))
                          vla0b(icv, 0, nsp(is)) = 0.D0
                        ELSE
                          poloidal_scale_factor = 1.0_R8
                          profb(iy) = profb(iy) + dv%ne(icv)*&
&                           poloidal_scale_factor*hce0b(icv)
                          dvb%ne(icv) = dvb%ne(icv) + prof(iy)*&
&                           poloidal_scale_factor*hce0b(icv)
                          hce0b(icv) = 0.D0
                        END IF
                      ELSE IF (branch .EQ. 9) THEN
                        poloidal_scale_factor = 1.0_R8
                        profb(iy) = profb(iy) + pl%na(icv, nsp(is))*&
&                         poloidal_scale_factor*hcibb(icv, nsp(is))
                        plb%na(icv, nsp(is)) = plb%na(icv, nsp(is)) + &
&                         prof(iy)*poloidal_scale_factor*hcibb(icv, nsp(&
&                         is))
                        hcibb(icv, nsp(is)) = 0.D0
                      ELSE IF (branch .EQ. 10) THEN
                        poloidal_scale_factor = 1.0_R8
                        temp = rt%rza(icv, nsp(is))*pl%te(icv) + pl%ti(&
&                         icv)
                        tempb = poloidal_scale_factor*dpa0b(icv, nsp(is)&
&                         )/temp
                        dpa0b(icv, nsp(is)) = 0.D0
                        profb(iy) = profb(iy) + tempb
                        tempb0 = -(prof(iy)*tempb/temp)
                        rtb%rza(icv, nsp(is)) = rtb%rza(icv, nsp(is)) + &
&                         pl%te(icv)*tempb0
                        plb%te(icv) = plb%te(icv) + rt%rza(icv, nsp(is))&
&                         *tempb0
                        plb%ti(icv) = plb%ti(icv) + tempb0
                      ELSE
                        poloidal_scale_factor = 1.0_R8
                        profb(iy) = profb(iy) + poloidal_scale_factor*&
&                         dna0b(icv, nsp(is))
                        dna0b(icv, nsp(is)) = 0.D0
                      END IF
                      CALL POPCONTROL1B(branch)
                      IF (branch .EQ. 0) THEN
                        CALL POPREAL8ARRAY(prof(1:ndim), r8*ndim/8)
                        pelmb(1:ndim) = pelmb(1:ndim) + profb(1:ndim)
                        profb(1:ndim) = 0.D0
                      ELSE
                        CALL POPREAL8ARRAY(prof(1:ndim), r8*ndim/8)
                        pfb(1:ndim) = pfb(1:ndim) + profb(1:ndim)
                        profb(1:ndim) = 0.D0
                      END IF
 110                  CALL POPINTEGER4(icv)
                    END DO
                  END IF
                END DO
              END DO
            END DO
            CALL POPCONTROL1B(branch)
            CALL POPCONTROL1B(branch)
            IF (branch .EQ. 0) CALL E01BFF_B(ndat, r, rb, felm, felmb, &
&                                      delm, delmb, ndim, pr, pelm, &
&                                      pelmb, ifail)
            CALL E01BFF_B(ndat, r, rb, f, fb, d, db, ndim, pr, pf, pfb, &
&                   ifail)
            CALL POPCONTROL1B(branch)
            IF (branch .EQ. 0) THEN
              CALL POPREAL8ARRAY(delm, r8*102/8)
              CALL E01BEF_B(ndat, r, rb, felm, felmb, delm, delmb, ifail&
&                    )
            END IF
            CALL POPREAL8ARRAY(d, r8*102/8)
            CALL E01BEF_B(ndat, r, rb, f, fb, d, db, ifail)
            CALL POPREAL8ARRAY(pr, r8*102/8)
            CALL POPINTEGER4(ad_to)
            DO i=ad_to,1,-1
              CALL POPCONTROL1B(branch)
              IF (branch .NE. 0) THEN
                CALL POPREAL8ARRAY(felm(i), r8/8)
                tdatab(3, i, kind_coeff, spec) = tdatab(3, i, kind_coeff&
&                 , spec) + felmb(i)
                felmb(i) = 0.D0
              END IF
              CALL POPREAL8ARRAY(f(i), r8/8)
              tdatab(2, i, kind_coeff, spec) = tdatab(2, i, kind_coeff, &
&               spec) + fb(i)
              fb(i) = 0.D0
              CALL POPREAL8ARRAY(r(i), r8/8)
              tdatab(1, i, kind_coeff, spec) = tdatab(1, i, kind_coeff, &
&               spec) + rb(i)
              rb(i) = 0.D0
            END DO
            DO is=ns,1,-1
              CALL POPCONTROL1B(branch)
              IF (branch .NE. 0) THEN
                CALL POPINTEGER4(nspec)
                CALL POPINTEGER4(nsp(nspec+1))
              END IF
            END DO
            CALL POPINTEGER4(nsp(1))
          END IF
          CALL POPINTEGER4(ndat)
        END DO
      END DO
    END DO
    CALL POPCONTROL2B(branch)
    IF (branch .EQ. 0) THEN
      CALL POPINTEGER4(ad_count)
      DO i0=1,ad_count
        CALL POPCONTROL2B(branch)
        tdatab = 0.D0
      END DO
      CALL POPCONTROL1B(branch)
    ELSE
      IF (branch .EQ. 1) tdatab = 0.D0
      CALL POPCONTROL2B(branch)
    END IF
  END SUBROUTINE TRANSPORT_INPUT_B

