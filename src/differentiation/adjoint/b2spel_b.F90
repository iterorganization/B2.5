      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlz2(icv, 1, is))
        rtb%rlz2(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlz2(icv, 1, is), r8/8)
      ELSE
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlz2(icv, 1, is))
        rtb%rlz2(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlz2(icv, 1, is), r8/8)
      END IF
      tempb = ttb/(rtlt(ite+1)-rtlt(ite))
      t1b = t1b + tempb
      t0b = t0b - tempb
      CALL POPREAL8(t1, r8/8)
      fxneb = fxneb + (rtlz2(ite+1, ine+1, is)-rtlz2(ite+1, ine, is))*&
&       t1b + (rtlz2(ite, ine+1, is)-rtlz2(ite, ine, is))*t0b
      t0 = (1.0_R8-fxne)*rtlza(ite, ine, is) + fxne*rtlza(ite, ine+1, is&
&       )
      CALL POPREAL8(rt%rlza(icv, 0, is), r8/8)
      fxteb = fxteb + (t1-t0)*rtb%rlza(icv, 0, is)
      t0b = (1.0_R8-fxte)*rtb%rlza(icv, 0, is)
      t1b = fxte*rtb%rlza(icv, 0, is)
      rtb%rlza(icv, 1, is) = rtb%rlza(icv, 1, is) - rlte*rtb%rlza(icv, 0&
&       , is)
      rlteb = rlteb - rt%rlza(icv, 1, is)*rtb%rlza(icv, 0, is)
      rtb%rlza(icv, 0, is) = 0.D0
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlza(icv, 1, is))
        rtb%rlza(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlza(icv, 1, is), r8/8)
      ELSE
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlza(icv, 1, is))
        rtb%rlza(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlza(icv, 1, is), r8/8)
      END IF
      tempb = ttb/(rtlt(ite+1)-rtlt(ite))
      t1b = t1b + tempb
      t0b = t0b - tempb
      CALL POPREAL8(t1, r8/8)
      fxneb = fxneb + (rtlza(ite+1, ine+1, is)-rtlza(ite+1, ine, is))*&
&       t1b + (rtlza(ite, ine+1, is)-rtlza(ite, ine, is))*t0b
      t0 = (1.0_R8-fxne)*rtlqr(ite, ine, is) + fxne*rtlqr(ite, ine+1, is&
&       )
      CALL POPREAL8(rt%rlqr(icv, 0, is), r8/8)
      fxteb = fxteb + (t1-t0)*rtb%rlqr(icv, 0, is)
      t0b = (1.0_R8-fxte)*rtb%rlqr(icv, 0, is)
      t1b = fxte*rtb%rlqr(icv, 0, is)
      rtb%rlqr(icv, 1, is) = rtb%rlqr(icv, 1, is) - rlte*rtb%rlqr(icv, 0&
&       , is)
      rlteb = rlteb - rt%rlqr(icv, 1, is)*rtb%rlqr(icv, 0, is)
      rtb%rlqr(icv, 0, is) = 0.D0
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlqr(icv, 1, is))
        rtb%rlqr(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlqr(icv, 1, is), r8/8)
      ELSE
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlqr(icv, 1, is))
        rtb%rlqr(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlqr(icv, 1, is), r8/8)
      END IF
      tempb = ttb/(rtlt(ite+1)-rtlt(ite))
      t1b = t1b + tempb
      t0b = t0b - tempb
      CALL POPREAL8(t1, r8/8)
      fxneb = fxneb + (rtlqr(ite+1, ine+1, is)-rtlqr(ite+1, ine, is))*&
&       t1b + (rtlqr(ite, ine+1, is)-rtlqr(ite, ine, is))*t0b
      t0 = (1.0_R8-fxne)*rtlqa(ite, ine, is) + fxne*rtlqa(ite, ine+1, is&
&       )
      CALL POPREAL8(rt%rlqa(icv, 0, is), r8/8)
      fxteb = fxteb + (t1-t0)*rtb%rlqa(icv, 0, is)
      t0b = (1.0_R8-fxte)*rtb%rlqa(icv, 0, is)
      t1b = fxte*rtb%rlqa(icv, 0, is)
      rtb%rlqa(icv, 1, is) = rtb%rlqa(icv, 1, is) - rlte*rtb%rlqa(icv, 0&
&       , is)
      rlteb = rlteb - rt%rlqa(icv, 1, is)*rtb%rlqa(icv, 0, is)
      rtb%rlqa(icv, 0, is) = 0.D0
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlqa(icv, 1, is))
        rtb%rlqa(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlqa(icv, 1, is), r8/8)
      ELSE
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlqa(icv, 1, is))
        rtb%rlqa(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlqa(icv, 1, is), r8/8)
      END IF
      tempb = ttb/(rtlt(ite+1)-rtlt(ite))
      t1b = t1b + tempb
      t0b = t0b - tempb
      CALL POPREAL8(t1, r8/8)
      fxneb = fxneb + (rtlqa(ite+1, ine+1, is)-rtlqa(ite+1, ine, is))*&
&       t1b + (rtlqa(ite, ine+1, is)-rtlqa(ite, ine, is))*t0b
      rtlqab(ite+1, ine, is) = rtlqab(ite+1, ine, is) + (1.0_R8-fxne)*&
&       t1b
      rtlqab(ite+1, ine+1, is) = rtlqab(ite+1, ine+1, is) + fxne*t1b
      rtlqab(ite, ine, is) = rtlqab(ite, ine, is) + (1.0_R8-fxne)*t0b
      rtlqab(ite, ine+1, is) = rtlqab(ite, ine+1, is) + fxne*t0b
      t0 = (1.0_R8-fxne)*rtlra(ite, ine, is) + fxne*rtlra(ite, ine+1, is&
&       )
      CALL POPREAL8(rt%rlra(icv, 0, is), r8/8)
      fxteb = fxteb + (t1-t0)*rtb%rlra(icv, 0, is)
      t0b = (1.0_R8-fxte)*rtb%rlra(icv, 0, is)
      t1b = fxte*rtb%rlra(icv, 0, is)
      rtb%rlra(icv, 1, is) = rtb%rlra(icv, 1, is) - rlte*rtb%rlra(icv, 0&
&       , is)
      rlteb = rlteb - rt%rlra(icv, 1, is)*rtb%rlra(icv, 0, is)
      rtb%rlra(icv, 0, is) = 0.D0
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlra(icv, 1, is))
        rtb%rlra(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlra(icv, 1, is), r8/8)
      ELSE
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlra(icv, 1, is))
        rtb%rlra(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlra(icv, 1, is), r8/8)
      END IF
      tempb = ttb/(rtlt(ite+1)-rtlt(ite))
      t1b = t1b + tempb
      t0b = t0b - tempb
      CALL POPREAL8(t1, r8/8)
      fxneb = fxneb + (rtlra(ite+1, ine+1, is)-rtlra(ite+1, ine, is))*&
&       t1b + (rtlra(ite, ine+1, is)-rtlra(ite, ine, is))*t0b
      rtlrab(ite+1, ine, is) = rtlrab(ite+1, ine, is) + (1.0_R8-fxne)*&
&       t1b
      rtlrab(ite+1, ine+1, is) = rtlrab(ite+1, ine+1, is) + fxne*t1b
      rtlrab(ite, ine, is) = rtlrab(ite, ine, is) + (1.0_R8-fxne)*t0b
      rtlrab(ite, ine+1, is) = rtlrab(ite, ine+1, is) + fxne*t0b
      t0 = (1.0_R8-fxne)*rtlsa(ite, ine, is) + fxne*rtlsa(ite, ine+1, is&
&       )
      CALL POPREAL8(rt%rlsa(icv, 0, is), r8/8)
      fxteb = fxteb + (t1-t0)*rtb%rlsa(icv, 0, is)
      t0b = (1.0_R8-fxte)*rtb%rlsa(icv, 0, is)
      t1b = fxte*rtb%rlsa(icv, 0, is)
      rtb%rlsa(icv, 1, is) = rtb%rlsa(icv, 1, is) - rlte*rtb%rlsa(icv, 0&
&       , is)
      rlteb = rlteb - rt%rlsa(icv, 1, is)*rtb%rlsa(icv, 0, is)
      rtb%rlsa(icv, 0, is) = 0.D0
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlsa(icv, 1, is))
        rtb%rlsa(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlsa(icv, 1, is), r8/8)
      ELSE
        CALL TRIMG_BWD(-20.0_R8, 20.0_R8, tt, ttb, rtb%rlsa(icv, 1, is))
        rtb%rlsa(icv, 1, is) = 0.D0
        CALL POPREAL8(rt%rlsa(icv, 1, is), r8/8)
      END IF
      tempb = ttb/(rtlt(ite+1)-rtlt(ite))
      t1b = t1b + tempb
      t0b = t0b - tempb
      CALL POPREAL8(t1, r8/8)
      fxneb = fxneb + (rtlsa(ite+1, ine+1, is)-rtlsa(ite+1, ine, is))*&
&       t1b + (rtlsa(ite, ine+1, is)-rtlsa(ite, ine, is))*t0b
      rtlsab(ite+1, ine, is) = rtlsab(ite+1, ine, is) + (1.0_R8-fxne)*&
&       t1b
      rtlsab(ite+1, ine+1, is) = rtlsab(ite+1, ine+1, is) + fxne*t1b
      rtlsab(ite, ine, is) = rtlsab(ite, ine, is) + (1.0_R8-fxne)*t0b
      rtlsab(ite, ine+1, is) = rtlsab(ite, ine+1, is) + fxne*t0b
    END DO
    rlneb = fxneb/(rtln(ine+1)-rtln(ine))
    CALL POPINTEGER4(ad_count2)
    DO i2=1,ad_count2
      IF (i2 .EQ. 1) CALL POPCONTROL1B(branch)
    END DO
    CALL POPINTEGER4(ad_count1)
    DO i1=1,ad_count1
      IF (i1 .EQ. 1) CALL POPCONTROL1B(branch)
    END DO
    rlteb = rlteb + fxteb/(rtlt(ite+1)-rtlt(ite))
    CALL POPINTEGER4(ad_count0)
    DO i0=1,ad_count0
      IF (i0 .EQ. 1) CALL POPCONTROL1B(branch)
    END DO
    CALL POPINTEGER4(ad_count)
    DO i=1,ad_count
      IF (i .EQ. 1) CALL POPCONTROL1B(branch)
    END DO
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      y2b = rlneb
    ELSE
      y2b = 0.D0
    END IF
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      y4b = y2b
    ELSE
      y4b = 0.D0
    END IF
    neb(icv) = neb(icv) + y4b/ne(icv)
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      y1b = rlteb
    ELSE
      y1b = 0.D0
    END IF
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      y3b = y1b
    ELSE
      y3b = 0.D0
    END IF
    teb(icv) = teb(icv) + y3b/te(icv)
  END DO
  CALL POPREAL8ARRAY(rt%rlqr, r8*SIZE(rt%rlqr, 1)*SIZE(rt%rlqr, 2)*SIZE(&
&              rt%rlqr, 3)/8)
  CALL POPREAL8ARRAY(rt%rlpi, r8*SIZE(rt%rlpi, 1)*SIZE(rt%rlpi, 2)*SIZE(&
&              rt%rlpi, 3)/8)
  CALL POPREAL8ARRAY(rt%rlpt, r8*SIZE(rt%rlpt, 1)*SIZE(rt%rlpt, 2)*SIZE(&
&              rt%rlpt, 3)/8)
  CALL POPREAL8ARRAY(rt%rlz2, r8*SIZE(rt%rlz2, 1)*SIZE(rt%rlz2, 2)*SIZE(&
&              rt%rlz2, 3)/8)
  CALL POPREAL8ARRAY(rt%rlza, r8*SIZE(rt%rlza, 1)*SIZE(rt%rlza, 2)*SIZE(&
&              rt%rlza, 3)/8)
  CALL POPREAL8ARRAY(rt%rlsa, r8*SIZE(rt%rlsa, 1)*SIZE(rt%rlsa, 2)*SIZE(&
&              rt%rlsa, 3)/8)
  CALL POPREAL8ARRAY(rt%rlra, r8*SIZE(rt%rlra, 1)*SIZE(rt%rlra, 2)*SIZE(&
&              rt%rlra, 3)/8)
  CALL POPREAL8ARRAY(rt%rlqa, r8*SIZE(rt%rlqa, 1)*SIZE(rt%rlqa, 2)*SIZE(&
&              rt%rlqa, 3)/8)
END SUBROUTINE B2SPEL_B
