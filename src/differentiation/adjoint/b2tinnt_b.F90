        CALL POPREAL8ARRAY(pit, r8*ncv/8)
        mask = geo%cvbb(:, 0) .GE. 0.
        CALL POPREAL8ARRAY(vntrc_fluid(:, 1), r8*ncv/8)
        fnacb(:, 1, is0) = fnacb(:, 1, is0) + vntrc_fluidb(:, 1)/&
&         na_fluid(:, is0)
        na_fluidb(:, is0) = na_fluidb(:, is0) - fnac(:, 1, is0)*&
&         vntrc_fluidb(:, 1)/na_fluid(:, is0)**2 - fnac(:, 0, is0)*&
&         vntrc_fluidb(:, 0)/na_fluid(:, is0)**2
        vntrc_fluidb(:, 1) = 0.D0
        CALL POPREAL8ARRAY(vntrc_fluid(:, 0), r8*ncv/8)
        fnacb(:, 0, is0) = fnacb(:, 0, is0) + vntrc_fluidb(:, 0)/&
&         na_fluid(:, is0)
        vntrc_fluidb(:, 0) = 0.D0
        CALL INTCELL_BWD(nfc, ncv, mpg, mpg%intcellr, fnan(:, 1, is0), &
&                  fnanb(:, 1, is0), fnac(:, 1, is0), fnacb(:, 1, is0))
        fnacb(:, 1, is0) = 0.D0
        CALL INTCELL_BWD(nfc, ncv, mpg, mpg%intcellp, fnan(:, 0, is0), &
&                  fnanb(:, 0, is0), fnac(:, 0, is0), fnacb(:, 0, is0))
        fnacb(:, 0, is0) = 0.D0
        CALL POPREAL8ARRAY(fnan(:, 1, is0), r8*nfc/8)
        fnab(:, 1, is0) = fnab(:, 1, is0) + fnanb(:, 1, is0)/(geo%fcs*&
&         geo%fcqalf(:, 1)+eps)
        fnanb(:, 1, is0) = 0.D0
        CALL POPREAL8ARRAY(fnan(:, 0, is0), r8*nfc/8)
        fnab(:, 0, is0) = fnab(:, 0, is0) + fnanb(:, 0, is0)/(geo%fcs*&
&         geo%fcqalf(:, 0)+eps)
        fnanb(:, 0, is0) = 0.D0
      END IF
    END DO
    CALL POPCONTROL1B(branch)
    IF (branch .EQ. 0) THEN
      wrkvxb = 0.D0
      CALL DIFF_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, pl%po, plb%&
&             po, wrkvx, wrkvxb, dpo, dpob)
      DO is=ns-1,0,-1
        wrk = pl%na(:, is)*pl%ti
        wrkb = 0.D0
        CALL DIFF_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, wrk, wrkb&
&               , wrkvx, wrkvxb, wrk0(:, :, is), wrk0b(:, :, is))
        plb%na(:, is) = plb%na(:, is) + pl%ti*wrkb
        plb%ti = plb%ti + pl%na(:, is)*wrkb
      END DO
    ELSE
      wrkvxb = 0.D0
      CALL DIFF_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, pl%ti, plb%&
&             ti, wrkvx, wrkvxb, wrk1, wrk1b)
      DO is=ns-1,0,-1
        wrkb = 0.D0
        CALL DIFF_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, wrk, wrkb&
&               , wrkvx, wrkvxb, wrk0(:, :, is), wrk0b(:, :, is))
        CALL POPREAL8ARRAY(wrk, r8*ncv/8)
        na_totb(:, is) = na_totb(:, is) + wrkb/na_tot(:, is)
      END DO
      CALL DIFF_BWD(ncv, nfc, nvx, 0, geo, geob, mpg, mpgb, pl%po, plb%&
&             po, wrkvx, wrkvxb, dpo, dpob)
      CALL INTFACE_BWD(ncv, nfc, mpg%fccv, geo%fcvol, pl%ti, plb%ti, tif&
&                , tifb)
    END IF
    DO is=ns-1,0,-1
      CALL INTFACE_BWD(ncv, nfc, mpg%fccv, geo%fcvol, na_tot(:, is), &
&                na_totb(:, is), naf(:, is), nafb(:, is))
      CALL POPREAL8ARRAY(na_tot(:, is), r8*ncv/8)
      na_fluidb(:, is) = na_fluidb(:, is) + na_totb(:, is)
      na_totb(:, is) = 0.D0
      plb%na(:, is) = plb%na(:, is) + na_fluidb(:, is)
      na_fluidb(:, is) = 0.D0
    END DO
    CALL POPCONTROL1B(branch)
    IF (branch .NE. 0) THEN
      DO is=ns-1,0,-1
        DO is0=ns-1,0,-1
          csiginb(:, 1, is, is0) = csiginb(:, 1, is, is0) + geo%fcqalf(:&
&           , 1)*wrk2b
          wrk2b = 0.D0
        END DO
      END DO
    END IF
  END IF
END SUBROUTINE B2TINNT_B

