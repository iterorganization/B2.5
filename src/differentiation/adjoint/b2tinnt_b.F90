        CALL POPREAL8ARRAY(vntrc_fluid(:, 0), r8*ncv/8)
        plb%ua(:, is0) = plb%ua(:, is0) - pit*vntrc_fluidb(:, 0)/pit2
        vntrc_fluidb(:, 0) = vntrc_fluidb(:, 0)/pit2
