    CALL MY_OUT_US(70, ncv, 0, sig0, 'b2tqna_in_sig0')
    CALL MY_OUT_US(70, ncv, 0, alf0, 'b2tqna_in_alf0')
  END IF
!
! User transport options
  IF (switch%user_transport .NE. 0) THEN
!! the default transport model calculated above is used. Nothing to do.
    CALL XERRAB('transport model undefined or not yet converted')
  END IF
! sc  implementation of turbulence closure models 29.11.2018-23/9/2020
  IF (switch%transport_keps .NE. 0) CALL SET_TRANSPORT_KEPS_NODIFF(ncv, &
&                                                            nfc, nvx, &
&                                                            ns, ismain&
&                                                            , switch, &
&                                                            geo, mpg, &
&                                                            pl, dv, rt&
&                                                            , dna0, &
&                                                            vsa0, hce0&
&                                                            , hci0, &
&                                                            hcib, sig0&
&                                                            , alf0, &
&                                                            dkt0, dzt0&
&                                                            , dna_exb, &
&                                                            hce_exb, &
&                                                            hci_exb)
!
  IF (switch%transport_afn .NE. 0) CALL SET_TRANSPORT_AFN_NODIFF(ncv, &
&                                                          nfc, nvx, ns&
&                                                          , nscx, iscx&
&                                                          , switch, geo&
&                                                          , mpg, pl, dv&
&                                                          , rt, st_ext&
&                                                          , dna0, dpa0&
&                                                          , vla0, vma0&
&                                                          , vsa0, hci0&
&                                                          , hcn0, hcib)
!
!dpc
  IF (switch%b2tqna_ballooning .NE. 0.0_R8) THEN
    DO icv=1,ncv
      IF (switch%b2tqna_bb_ref/geo%cvbb(icv, 3) .GE. 0.) THEN
        abs3 = switch%b2tqna_bb_ref/geo%cvbb(icv, 3)
      ELSE
        abs3 = -(switch%b2tqna_bb_ref/geo%cvbb(icv, 3))
      END IF
      poloidal_scale_factor = abs3**switch%b2tqna_ballooning*switch%&
&       b2tqna_ballooning_rescale
      DO is=0,ns-1
        IF (.NOT.is_neutral(is)) THEN
          dna0(icv, is) = dna0(icv, is)*poloidal_scale_factor
          dpa0(icv, is) = dpa0(icv, is)*poloidal_scale_factor
          vla0(icv, :, is) = vla0(icv, :, is)*poloidal_scale_factor
          vsa0(icv, is) = vsa0(icv, is)*poloidal_scale_factor
          hci0(icv) = hci0(icv) + hcib(icv, is)*(poloidal_scale_factor-&
&           1.0_R8)
          hcib(icv, is) = hcib(icv, is)*poloidal_scale_factor
        END IF
      END DO
      hce0(icv) = hce0(icv)*poloidal_scale_factor
      IF (switch%b2tqna_ballooning_sig .NE. 0) THEN
        sig0(icv) = sig0(icv)*poloidal_scale_factor
        alf0(icv) = alf0(icv)*poloidal_scale_factor
      END IF
    END DO
  END IF
  IF (switch%b2tqna_limit_coeff .NE. 0) THEN
    DO is=0,ns-1
      IF (.NOT.is_neutral(is)) THEN
        mask(:) = dna0(:, is) .LT. switch%dna_min
        WHERE (mask(:)) 
          dna0(:, is) = switch%dna_min
        ELSEWHERE
          dna0(:, is) = dna0(:, is)
        END WHERE
        y2 = switch%vsa_min*am(is)*mp*pl%na(:, is)
        mask0(:) = vsa0(:, is) .LT. y2
        WHERE (mask0(:)) 
          vsa0(:, is) = y2
        ELSEWHERE
          vsa0(:, is) = vsa0(:, is)
        END WHERE
        mask1(:) = hcib(:, is) .LT. switch%hci_min*pl%na(:, is)
        WHERE (mask1(:)) 
          max6 = switch%hci_min*pl%na(:, is)
        ELSEWHERE
          max6 = hcib(:, is)
        END WHERE
        hci0 = hci0 - hcib(:, is) + max6
        mask2(:) = hcib(:, is) .LT. switch%hci_min*pl%na(:, is)
        WHERE (mask2(:)) 
          hcib(:, is) = switch%hci_min*pl%na(:, is)
        ELSEWHERE
          hcib(:, is) = hcib(:, is)
        END WHERE
      END IF
    END DO
    mask3 = hce0 .LT. switch%hce_min*dv%ne
    WHERE (mask3) 
      hce0 = switch%hce_min*dv%ne
    ELSEWHERE
      hce0 = hce0
    END WHERE
  END IF
!srv 10.11.09 }
  IF (switch%iout_b2wdat .EQ. 4) THEN
!srv 10.11.09 {
    DO is=0,ns-1
      WRITE(chns, '(i3.3)') is
      arg14(:) = 'b2tqna_dna0'//chns
      CALL MY_OUT_US(70, ncv, 0, dna0(1, is), arg14(:))
      arg14(:) = 'b2tqna_dpa0'//chns
      CALL MY_OUT_US(70, ncv, 0, dpa0(1, is), arg14(:))
      wrk0 = vsa0(:, is)/(mp*am(is)*pl%na(:, is))
      arg14(:) = 'b2tqna_vsa0'//chns
      CALL MY_OUT_US(70, ncv, 0, wrk0, arg14(:))
!          call my_out_us(70,nCv,0,vla0(1,0,is),'b2tqna_vla0x'//chns)
      arg15(:) = 'b2tqna_vla0y'//chns
      CALL MY_OUT_US(70, ncv, 0, vla0(1, 1, is), arg15(:))
      wrk0 = hcib(:, is)/pl%na(:, is)
      arg14(:) = 'b2tqna_hcib'//chns
      CALL MY_OUT_US(70, ncv, 0, wrk0, arg14(:))
    END DO
!        call my_out_us(70,nCv,0,hci0,'b2tqna_in_hci0')
    wrk0 = hce0/dv%ne
    CALL MY_OUT_US(70, ncv, 0, wrk0, 'b2tqna_hce0')
    CALL MY_OUT_US(70, ncv, 0, sig0, 'b2tqna_sig0')
    CALL MY_OUT_US(70, ncv, 0, alf0, 'b2tqna_alf0')
  END IF
!dpc
! ..return
  ncall_b2tqna = ncall_b2tqna + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2TQNA_NODIFF

!  Differentiation of set_transport_afn in reverse (adjoint) mode (with options context noISIZE r8):
!   gradient     of useful results: hci0 vsa0 *(dv.ne) *(rt.rlcx)
!                *(rt.rlsa) hcib hcn0 *(pl.na) *(pl.te) *(pl.ti)
!                *(pl.tn) dpa0
!   with respect to varying inputs: vsa0 *(dv.ne) *(rt.rlcx) *(rt.rlsa)
!                hcib *(pl.na) *(pl.te) *(pl.ti) *(pl.tn) dpa0
!   Plus diff mem management of: dv.ne:in rt.rlcx:in rt.rlsa:in
!                pl.na:in pl.te:in pl.ti:in pl.tn:in
!
!
!**************************************************************************************
!*****************     New KU Leuven transport model for neutrals *********************
!**************************************************************************************
SUBROUTINE SET_TRANSPORT_AFN_B(ncv, nfc, nvx, ns, nscx, iscx, switch, &
& geo, mpg, pl, plb, dv, dvb, rt, rtb, st_ext, dna0, dpa0, dpa0b, vla0, &
& vma0, vsa0, vsa0b, hci0, hci0b, hcn0, hcn0b, hcib, hcibb)
  USE B2MOD_TYPES
  USE B2MOD_MATH_DIFF
  USE B2MOD_INDIRECT
  USE B2MOD_CONSTANTS
  USE B2MOD_B2CMPA_DIFF
  USE B2MOD_B2CMPT_DIFF
  USE B2MOD_RATES
  USE B2MOD_TRANSPORT_NAMELIST_DIFF
  USE B2MOD_SWITCHES_DIFF
  USE B2US_GEO_DIFF
  USE B2US_MAP_DIFF
  USE B2US_PLASMA_DIFF
  USE B2MOD_SUBSYS
  IMPLICIT NONE
!
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx, ns, nscx, iscx(0:nscxmax-1)
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(B2PLASMA), INTENT(IN) :: pl
  TYPE(B2PLASMA_DIFF) :: plb
  TYPE(B2DERIVATIVES), INTENT(INOUT) :: dv
  TYPE(B2DERIVATIVES), INTENT(INOUT) :: dvb
  TYPE(B2RATES), INTENT(IN) :: rt
  TYPE(B2RATES_DIFF) :: rtb
  TYPE(B2STATEEXT), INTENT(IN) :: st_ext
  REAL(kind=r8) :: t_av, df0
  REAL(kind=r8) :: t_avb, df0b
!   ..input/output arguments
  REAL(kind=r8), INTENT(INOUT) :: hci0(ncv), hcn0(ncv), hcib(ncv, 0:ns-1&
& )
  REAL(kind=r8), INTENT(INOUT) :: hci0b(ncv), hcn0b(ncv), hcibb(ncv, 0:&
& ns-1)
!   ..output arguments (unspecified on entry)
!srv 15.12.05
  REAL(kind=r8) :: dna0(ncv, 0:ns-1), dpa0(ncv, 0:ns-1), vla0(ncv, 0:1, &
& 0:ns-1), vsa0(ncv, 0:ns-1), vma0(ncv, 0:1, 0:ns-1)
  REAL(kind=r8) :: dpa0b(ncv, 0:ns-1), vsa0b(ncv, 0:ns-1)
!   ..workspace arguments (unspecified on entry and on exit)
  REAL(kind=r8) :: wrk0(ncv), vcx, dion
  REAL(kind=r8) :: wrk0b(ncv), vcxb, dionb
  INTEGER :: is, k, ic, icv
  INTRINSIC SQRT
  INTRINSIC LOG
  INTRINSIC MIN
  INTRINSIC MAX
  INTRINSIC ANY
  REAL(kind=r8) :: y1
  REAL(kind=r8) :: y1b
  REAL(r8) :: arg1
  REAL(r8) :: arg1b
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result1b
!
!   ..subprogram start-up calls
  REAL(kind=r8) :: temp
  REAL(r8) :: temp0
  REAL(kind=r8) :: temp1
  REAL(kind=r8) :: tempb
  REAL(r8) :: tempb0
  INTEGER :: branch
  DO is=0,ns-1
    IF (is_neutral(is) .AND. zn(is) .EQ. 1.0_R8) THEN
! only for hydrogenic species
!
      CALL PUSHINTEGER4(k)
      k = 0
      DO WHILE (iscx(k) .NE. is .AND. k .LT. nscx)
        k = k + 1
      END DO
!
      CALL PUSHREAL8ARRAY(wrk0, r8*ncv/8)
      wrk0 = SQRT(pl%tn/mp)
      DO icv=1,ncv
        CALL PUSHREAL8(vcx, r8/8)
        vcx = 0.0_R8
        DO ic=0,ns-1
          t_av = 0.5_R8*(pl%ti(icv)+pl%tn(icv))
          CALL PUSHREAL8(arg1, r8/8)
          arg1 = rt%rlcx(icv, 0, ic, k) + rt%rlcx(icv, 1, ic, k)*LOG(&
&           t_av/(am(is)*ev))
          CALL PUSHREAL8(cutlo, r8/8)
          CALL PUSHREAL8(cutll, r8/8)
          CALL PUSHBOOLEAN(b2mod_math_initialised)
          CALL PUSHREAL4(small_r4_constant, r4/8)
          CALL PUSHREAL8(result1, r8/8)
          result1 = EXPU(arg1)
          vcx = vcx + pl%na(icv, ic)*result1
        END DO
        CALL PUSHREAL8(arg1, r8/8)
        arg1 = rt%rlsa(icv, 0, is) + rt%rlsa(icv, 1, is)*LOG(pl%te(icv)/&
&         ev)
        CALL PUSHREAL8(cutlo, r8/8)
        CALL PUSHREAL8(cutll, r8/8)
        CALL PUSHBOOLEAN(b2mod_math_initialised)
        CALL PUSHREAL4(small_r4_constant, r4/8)
        CALL PUSHREAL8(dion, r8/8)
        dion = EXPU(arg1)
!         ..For now, no neutral-neutral collisions inlcuded
! limit df0
        CALL PUSHREAL8(df0, r8/8)
        df0 = (wrk0(icv)/SQRT(am(is)))**2/(vcx+dion*dv%ne(icv))
        IF (switch%b2tqna_max_df0 .GT. df0) THEN
          y1 = df0
          CALL PUSHCONTROL1B(0)
        ELSE
          y1 = switch%b2tqna_max_df0
          CALL PUSHCONTROL1B(1)
        END IF
        IF (switch%b2tqna_min_df0 .LT. y1) THEN
          df0 = y1
          CALL PUSHCONTROL1B(0)
        ELSE
          df0 = switch%b2tqna_min_df0
          CALL PUSHCONTROL1B(1)
        END IF
      END DO
      CALL PUSHCONTROL1B(1)
    ELSE
      CALL PUSHCONTROL1B(0)
    END IF
  END DO
  DO is=0,ns-1
!     ..compute hci0
    IF (switch%tn_style .EQ. 0) THEN
      CALL PUSHCONTROL3B(0)
    ELSE IF (switch%tn_style .EQ. 1) THEN
      IF (.NOT.is_neutral(is)) THEN
        CALL PUSHCONTROL3B(1)
      ELSE
        CALL PUSHCONTROL3B(2)
      END IF
    ELSE IF ((.NOT.is_neutral(is)) .OR. zn(is) .NE. 1) THEN
      CALL PUSHCONTROL3B(3)
    ELSE
      CALL PUSHCONTROL3B(4)
    END IF
!     ..compute hcn0
    IF (is_neutral(is) .AND. zn(is) .EQ. 1) THEN
      CALL PUSHCONTROL1B(1)
    ELSE
      CALL PUSHCONTROL1B(0)
    END IF
  END DO
  DO is=ns-1,0,-1
    CALL POPCONTROL1B(branch)
    IF (branch .NE. 0) hcibb(:, is) = hcibb(:, is) + hcn0b
    CALL POPCONTROL3B(branch)
    IF (branch .LT. 2) THEN
      IF (branch .EQ. 0) THEN
        hcibb(:, is) = hcibb(:, is) + hci0b
      ELSE
        hcibb(:, is) = hcibb(:, is) + hci0b
      END IF
    ELSE IF (branch .NE. 2) THEN
      IF (branch .EQ. 3) hcibb(:, is) = hcibb(:, is) + hci0b
    END IF
  END DO
  DO is=ns-1,0,-1
    CALL POPCONTROL1B(branch)
    IF (branch .NE. 0) THEN
      wrk0b = 0.D0
      DO icv=ncv,1,-1
        tempb = dpa0b(icv, is)/pl%tn(icv)
        tempb0 = mp*am(is)*vsa0b(icv, is)
        plb%na(icv, is) = plb%na(icv, is) + df0*2.5_R8*hcibb(icv, is) + &
&         df0*tempb0
        df0b = pl%na(icv, is)*2.5_R8*hcibb(icv, is) + pl%na(icv, is)*&
&         tempb0 + tempb
        hcibb(icv, is) = 0.D0
        vsa0b(icv, is) = 0.D0
        dpa0b(icv, is) = 0.D0
        plb%tn(icv) = plb%tn(icv) - df0*tempb/pl%tn(icv)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          y1b = df0b
        ELSE
          y1b = 0.D0
        END IF
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          df0b = y1b
        ELSE
          df0b = 0.D0
        END IF
        CALL POPREAL8(df0, r8/8)
        temp = SQRT(am(is))*SQRT(am(is))
        temp1 = temp*(vcx+dion*dv%ne(icv))
        wrk0b(icv) = wrk0b(icv) + 2*wrk0(icv)*df0b/temp1
        tempb = -(temp*wrk0(icv)**2*df0b/temp1**2)
        vcxb = tempb
        dionb = dv%ne(icv)*tempb
        dvb%ne(icv) = dvb%ne(icv) + dion*tempb
        CALL POPREAL8(dion, r8/8)
        CALL POPREAL4(small_r4_constant, r4/8)
        CALL POPBOOLEAN(b2mod_math_initialised)
        CALL POPREAL8(cutll, r8/8)
        CALL POPREAL8(cutlo, r8/8)
        CALL EXPU_B(arg1, arg1b, dionb)
        CALL POPREAL8(arg1, r8/8)
        temp0 = pl%te(icv)/ev
        rtb%rlsa(icv, 0, is) = rtb%rlsa(icv, 0, is) + arg1b
        rtb%rlsa(icv, 1, is) = rtb%rlsa(icv, 1, is) + LOG(temp0)*arg1b
        plb%te(icv) = plb%te(icv) + rt%rlsa(icv, 1, is)*arg1b/(ev*temp0)
        DO ic=ns-1,0,-1
          plb%na(icv, ic) = plb%na(icv, ic) + result1*vcxb
          result1b = pl%na(icv, ic)*vcxb
          CALL POPREAL8(result1, r8/8)
          CALL POPREAL4(small_r4_constant, r4/8)
          CALL POPBOOLEAN(b2mod_math_initialised)
          CALL POPREAL8(cutll, r8/8)
          CALL POPREAL8(cutlo, r8/8)
          CALL EXPU_B(arg1, arg1b, result1b)
          t_av = 0.5_R8*(pl%ti(icv)+pl%tn(icv))
          CALL POPREAL8(arg1, r8/8)
          temp = t_av/(am(is)*ev)
          rtb%rlcx(icv, 0, ic, k) = rtb%rlcx(icv, 0, ic, k) + arg1b
          rtb%rlcx(icv, 1, ic, k) = rtb%rlcx(icv, 1, ic, k) + LOG(temp)*&
&           arg1b
          t_avb = rt%rlcx(icv, 1, ic, k)*arg1b/(am(is)*ev*temp)
          plb%ti(icv) = plb%ti(icv) + 0.5_R8*t_avb
          plb%tn(icv) = plb%tn(icv) + 0.5_R8*t_avb
        END DO
        CALL POPREAL8(vcx, r8/8)
      END DO
      CALL POPREAL8ARRAY(wrk0, r8*ncv/8)
      WHERE (.NOT.pl%tn/mp .EQ. 0.D0) plb%tn = plb%tn + wrk0b/(mp*2.0*&
&         SQRT(pl%tn/mp))
      CALL POPINTEGER4(k)
    END IF
  END DO
END SUBROUTINE SET_TRANSPORT_AFN_B

