              plb%na(icv, ic) = plb%na(icv, ic) + result1*vcxb
              result1b = pl%na(icv, ic)*vcxb
              CALL POPREAL4(small_r4_constant, r4/8)
              CALL POPBOOLEAN(b2mod_math_initialised)
              CALL POPREAL8(cutll, r8/8)
              CALL POPREAL8(cutlo, r8/8)
              CALL EXPU_B(arg10, arg10b, result1b)
              temp3 = tav/(am(is)*ev)
              rtb%rlcx(icv, 0, ic, k) = rtb%rlcx(icv, 0, ic, k) + arg10b
              rtb%rlcx(icv, 1, ic, k) = rtb%rlcx(icv, 1, ic, k) + LOG(&
&               temp3)*arg10b
              tavb = tavb + rt%rlcx(icv, 1, ic, k)*arg10b/(am(is)*ev*&
&               temp3)
              CALL POPREAL8(arg10, r8/8)
            END DO
            plb%ti(icv) = plb%ti(icv) + tavb/2.0_R8
            plb%tn(icv) = plb%tn(icv) + tavb/2.0_R8
          END IF
          CALL POPREAL8(vcx, r8/8)
          df0b = 0.D0
        END IF
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPREAL8(df0, r8/8)
        temp = st_ext%ni(icv, 0) + dv%ni(icv, 0)
        dvb%ni(icv, 0) = dvb%ni(icv, 0) - cfdf0(1, is)*1.0e20_R8*df0b/&
&         temp**2
      ELSE
        CALL POPREAL8(df0, r8/8)
        temp = st_ext%ni(icv, 0) + dv%ni(icv, 0)
        temp0 = SQRT(am(is))
        temp1 = temp0*(cfdf0(3, is)*(st_ext%ni(icv, 0)+dv%ni(icv, 0))+&
&         cfdf0(4, is)*dv%ne(icv))
        tempb = cfdf0(2, is)*df0b/temp1
        wrk0b(icv) = wrk0b(icv) + tempb
        tempb0 = -(temp0*wrk0(icv)*tempb/temp1)
        dvb%ni(icv, 0) = dvb%ni(icv, 0) + cfdf0(3, is)*tempb0 - cfdf0(1&
&         , is)*1.0e20_R8*df0b/temp**2
        dvb%ne(icv) = dvb%ne(icv) + cfdf0(4, is)*tempb0
      END IF
    END DO
    CALL POPINTEGER4(k)
  END DO
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb13, hci_exb, hci_exbb, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb12, hce_exb, hce_exbb, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb11, dna_exb, dna_exbb, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb10, dzt0, dzt0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb9, dkt0, dkt0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb8, alf0, alf0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb7, sig0, sig0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb6, hce0, hce0b, 1)
  arg1 = ncv*ns
  CALL SFILL_BWD(arg1, 0.0_R8, dummydiffb5, hcib, hcibb, 1)
  arg1 = ncv*ns
  CALL SFILL_BWD(arg1, 0.0_R8, dummydiffb4, vsa0, vsa0b, 1)
  arg1 = ncv*ns*2
  CALL SFILL_BWD(arg1, 0.0_R8, dummydiffb3, vla0, vla0b, 1)
  arg1 = ncv*ns
  CALL SFILL_BWD(arg1, 0.0_R8, dummydiffb2, dpa0, dpa0b, 1)
  arg1 = ncv*ns
  CALL SFILL_BWD(arg1, 0.0_R8, dummydiffb1, dna0, dna0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb0, hcn0, hcn0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb, hci0, hci0b, 1)
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
    CALL TRANSFORM_TRANSPORT_B(flag_alf, parm_alf, parm_alfb, cfalf, &
&                        cfalfb)
    CALL TRANSFORM_TRANSPORT_B(flag_sig, parm_sig, parm_sigb, cfsig, &
&                        cfsigb)
    CALL TRANSFORM_TRANSPORT_B(flag_hce, parm_hce, parm_hceb, cfhce, &
&                        cfhceb)
    DO is=ns-1,0,-1
      CALL POPCONTROL1B(branch)
      IF (branch .NE. 0) THEN
        CALL TRANSFORM_TRANSPORT_B(flag_hci, parm_hci(is), parm_hcib(is)&
&                            , cfhci(0, is), cfhcib(0, is))
        CALL TRANSFORM_TRANSPORT_B(flag_vsa, parm_vsa(is), parm_vsab(is)&
&                            , cfvsa(0, is), cfvsab(0, is))
        CALL TRANSFORM_TRANSPORT_B(flag_dna, parm_dna(is), parm_dnab(is)&
&                            , cfdna(0, is), cfdnab(0, is))
      END IF
    END DO
  END IF
  WHERE (.NOT.pl%ti/mp .EQ. 0.D0) plb%ti = plb%ti + wrk0b/(mp*2.0*SQRT(&
&     pl%ti/mp))
  CALL POPCONTROL2B(branch)
  IF (branch .LT. 2) THEN
    IF (branch .EQ. 0) THEN
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPINTEGER4(ad_count)
        DO i=1,ad_count
          CALL POPCONTROL2B(branch)
          CALL POPREAL8ARRAY(cflim, r8)
        END DO
        CALL POPCONTROL1B(branch)
      END IF
      GOTO 100
    ELSE
      CALL POPREAL8ARRAY(cflim, r8)
    END IF
  ELSE IF (branch .NE. 2) THEN
    GOTO 100
  END IF
  CALL POPCONTROL2B(branch)
 100 CONTINUE
END SUBROUTINE B2TQNA_B

