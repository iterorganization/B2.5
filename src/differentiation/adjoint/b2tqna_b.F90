      temp = rt%rza(icv, is)*pl%te(icv) + pl%ti(icv)
      temp2 = cfdpa(1, is)/max2
      temp1 = ev*geo%cvbb(icv, 3)
      tempb0 = dpa0b(icv, is)/temp
      dpa0b(icv, is) = 0.D0
      cfdpab(0, is) = cfdpab(0, is) + tempb0
      tempb = 1.0e20_R8*tempb0/max2
      cfdpab(2, is) = cfdpab(2, is) + df0*tempb0
      df0b = df0b + cfdpa(2, is)*tempb0
      tempb2 = 6.25e-2_R8*tempb0/temp1
      tempb3 = -((cfdpa(0, is)+1.0e20_R8*temp2+cfdpa(2, is)*df0+&
&       6.25e-2_R8*(cfdpa(3, is)*pl%te(icv)/temp1))*tempb0/temp)
      rtb%rza(icv, is) = rtb%rza(icv, is) + pl%te(icv)*tempb3
      plb%te(icv) = plb%te(icv) + rt%rza(icv, is)*tempb3 + cfdpa(3, is)*&
&       tempb2
      plb%ti(icv) = plb%ti(icv) + tempb3
      cfdpab(3, is) = cfdpab(3, is) + pl%te(icv)*tempb2
      cfdpab(1, is) = cfdpab(1, is) + tempb
      max2b = -(temp2*tempb)
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPREAL8(max2, r8/8)
        dvb%ne(icv) = dvb%ne(icv) + max2b
      ELSE
        CALL POPREAL8(max2, r8/8)
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .NE. 0) CALL POPREAL8(dna0(icv, is), r8/8)
      CALL POPREAL8(dna0(icv, is), r8/8)
      cfdnab(0, is) = cfdnab(0, is) + dna0b(icv, is)
      tempb0 = 1.0e20_R8*dna0b(icv, is)/max1
      cfdnab(2, is) = cfdnab(2, is) + df0*dna0b(icv, is)
      df0b = df0b + cfdna(2, is)*dna0b(icv, is)
      tempb = 6.25e-2_R8*dna0b(icv, is)/(ev*geo%cvbb(icv, 3))
      dna0b(icv, is) = 0.D0
      cfdnab(3, is) = cfdnab(3, is) + pl%te(icv)*tempb
      plb%te(icv) = plb%te(icv) + cfdna(3, is)*tempb
      cfdnab(1, is) = cfdnab(1, is) + tempb0
      max1b = -(cfdna(1, is)*tempb0/max1)
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPREAL8(max1, r8/8)
        dvb%ne(icv) = dvb%ne(icv) + max1b
      ELSE
        CALL POPREAL8(max1, r8/8)
      END IF
      CALL POPCONTROL2B(branch)
      IF (branch .NE. 0) THEN
        IF (branch .EQ. 1) THEN
          y1b = 0.D0
        ELSE
          y1b = df0b
        END IF
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          df0b = y1b
        ELSE
          df0b = 0.D0
        END IF
      END IF
      CALL POPCONTROL2B(branch)
      IF (branch .NE. 0) THEN
        IF (branch .NE. 1) THEN
          CALL ADDUP_B(din, dinb, dnn, dnnb, df0b)
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            CALL POPREAL8(result1, r8/8)
          ELSE
            CALL POPREAL8(result1, r8/8)
          END IF
          temp = 2.0e-19_R8*pl%na(icv, is)
          plb%na(icv, is) = plb%na(icv, is) - 2.0e-19_R8*switch%&
&           neutral_sources_rescale*dnnb/temp**2
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            dionb = 0.D0
            vcxb = 0.D0
          ELSE
            temp3 = SQRT(am(is))*SQRT(am(is))
            temp2 = temp3*(vcx+dion*dv%ne(icv))
            wrk0b(icv) = wrk0b(icv) + 2*wrk0(icv)*dinb/temp2
            tempb1 = -(temp3*wrk0(icv)**2*dinb/temp2**2)
            vcxb = tempb1
            dionb = dv%ne(icv)*tempb1
            dvb%ne(icv) = dvb%ne(icv) + dion*tempb1
          END IF
          CALL POPREAL8(dion, r8/8)
          CALL POPREAL8(cutll, r8/8)
          CALL POPREAL8(cutlo, r8/8)
          CALL POPBOOLEAN(b2mod_math_initialised)
          CALL EXPU_B(arg10, arg10b, dionb)
          CALL POPREAL8(arg10, r8/8)
          temp = pl%te(icv)/ev
          rtb%rlsa(icv, 0, is) = rtb%rlsa(icv, 0, is) + arg10b
          rtb%rlsa(icv, 1, is) = rtb%rlsa(icv, 1, is) + LOG(temp)*arg10b
          plb%te(icv) = plb%te(icv) + rt%rlsa(icv, 1, is)*arg10b/(ev*&
&           temp)
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            tav = (pl%ti(icv)+pl%tn(icv))/2.0_R8
            tavb = 0.D0
            DO ic=ns-1,0,-1
              plb%na(icv, ic) = plb%na(icv, ic) + result1*vcxb
              result1b = pl%na(icv, ic)*vcxb
              CALL POPREAL8(cutll, r8/8)
              CALL POPREAL8(cutlo, r8/8)
              CALL POPBOOLEAN(b2mod_math_initialised)
              CALL EXPU_B(arg10, arg10b, result1b)
              temp3 = tav/(am(is)*ev)
              rtb%rlcx(icv, 0, ic, k) = rtb%rlcx(icv, 0, ic, k) + arg10b
              rtb%rlcx(icv, 1, ic, k) = rtb%rlcx(icv, 1, ic, k) + LOG(&
&               temp3)*arg10b
              tavb = tavb + rt%rlcx(icv, 1, ic, k)*arg10b/(am(is)*ev*&
&               temp3)
              CALL POPREAL8(arg10, r8/8)
            END DO
            plb%ti(icv) = plb%ti(icv) + tavb/2.0_R8
            plb%tn(icv) = plb%tn(icv) + tavb/2.0_R8
          END IF
          CALL POPREAL8(vcx, r8/8)
          df0b = 0.D0
        END IF
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPREAL8(df0, r8/8)
        temp = st_ext%ni(icv, 0) + dv%ni(icv, 0)
        dvb%ni(icv, 0) = dvb%ni(icv, 0) - cfdf0(1, is)*1.0e20_R8*df0b/&
&         temp**2
      ELSE
        CALL POPREAL8(df0, r8/8)
        temp = st_ext%ni(icv, 0) + dv%ni(icv, 0)
        temp0 = SQRT(am(is))
        temp1 = temp0*(cfdf0(3, is)*(st_ext%ni(icv, 0)+dv%ni(icv, 0))+&
&         cfdf0(4, is)*dv%ne(icv))
        tempb = cfdf0(2, is)*df0b/temp1
        wrk0b(icv) = wrk0b(icv) + tempb
        tempb0 = -(temp0*wrk0(icv)*tempb/temp1)
        dvb%ni(icv, 0) = dvb%ni(icv, 0) + cfdf0(3, is)*tempb0 - cfdf0(1&
&         , is)*1.0e20_R8*df0b/temp**2
        dvb%ne(icv) = dvb%ne(icv) + cfdf0(4, is)*tempb0
      END IF
    END DO
    CALL POPINTEGER4(k)
  END DO
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb13, hci_exb, hci_exbb, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb12, hce_exb, hce_exbb, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb11, dna_exb, dna_exbb, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb10, dzt0, dzt0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb9, dkt0, dkt0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb8, alf0, alf0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb7, sig0, sig0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb6, hce0, hce0b, 1)
  arg1 = ncv*ns
  CALL SFILL_BWD(arg1, 0.0_R8, dummydiffb5, hcib, hcibb, 1)
  arg1 = ncv*ns
  CALL SFILL_BWD(arg1, 0.0_R8, dummydiffb4, vsa0, vsa0b, 1)
  arg1 = ncv*ns*2
  CALL SFILL_BWD(arg1, 0.0_R8, dummydiffb3, vla0, vla0b, 1)
  arg1 = ncv*ns
  CALL SFILL_BWD(arg1, 0.0_R8, dummydiffb2, dpa0, dpa0b, 1)
  arg1 = ncv*ns
  CALL SFILL_BWD(arg1, 0.0_R8, dummydiffb1, dna0, dna0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb0, hcn0, hcn0b, 1)
  CALL SFILL_BWD(ncv, 0.0_R8, dummydiffb, hci0, hci0b, 1)
  CALL POPCONTROL1B(branch)
  IF (branch .EQ. 0) THEN
    CALL POPREAL8ARRAY(cfalf, r8*8/8)
    CALL TRANSFORM_TRANSPORT_B(flag_alf, parm_alf, parm_alfb, cfalf, &
&                        cfalfb)
    CALL POPREAL8ARRAY(cfsig, r8*8/8)
    CALL TRANSFORM_TRANSPORT_B(flag_sig, parm_sig, parm_sigb, cfsig, &
&                        cfsigb)
    CALL POPREAL8ARRAY(cfhce, r8*8/8)
    CALL TRANSFORM_TRANSPORT_B(flag_hce, parm_hce, parm_hceb, cfhce, &
&                        cfhceb)
    DO is=ns-1,0,-1
      CALL POPCONTROL1B(branch)
      IF (branch .NE. 0) THEN
        CALL POPREAL8ARRAY(cfhci(:, is), r8)
        CALL TRANSFORM_TRANSPORT_B(flag_hci, parm_hci(is), parm_hcib(is)&
&                            , cfhci(:, is), cfhcib(:, is))
        CALL POPREAL8ARRAY(cfvsa(:, is), r8)
        CALL TRANSFORM_TRANSPORT_B(flag_vsa, parm_vsa(is), parm_vsab(is)&
&                            , cfvsa(:, is), cfvsab(:, is))
        CALL POPREAL8ARRAY(cfvla(:, is), r8)
        CALL TRANSFORM_TRANSPORT_B(flag_vla, parm_vla(is), parm_vlab(is)&
&                            , cfvla(:, is), cfvlab(:, is))
        CALL POPREAL8ARRAY(cfdpa(:, is), r8)
        CALL TRANSFORM_TRANSPORT_B(flag_dpa, parm_dpa(is), parm_dpab(is)&
&                            , cfdpa(:, is), cfdpab(:, is))
        CALL POPREAL8ARRAY(cfdna(:, is), r8)
        CALL TRANSFORM_TRANSPORT_B(flag_dna, parm_dna(is), parm_dnab(is)&
&                            , cfdna(:, is), cfdnab(:, is))
      END IF
    END DO
  END IF
  WHERE (.NOT.pl%ti/mp .EQ. 0.D0) plb%ti = plb%ti + wrk0b/(mp*2.0*SQRT(&
&     pl%ti/mp))
  CALL POPCONTROL2B(branch)
  IF (branch .LT. 2) THEN
    IF (branch .EQ. 0) THEN
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPINTEGER4(ad_count)
        DO i=1,ad_count
          CALL POPCONTROL2B(branch)
          CALL POPREAL8ARRAY(cflim, r8*8/8)
        END DO
        CALL POPCONTROL1B(branch)
      END IF
      GOTO 100
    ELSE
      CALL POPREAL8ARRAY(cflim, r8*8/8)
    END IF
  ELSE IF (branch .NE. 2) THEN
    GOTO 100
  END IF
  CALL POPCONTROL2B(branch)
 100 CONTINUE
END SUBROUTINE B2TQNA_B

