  REAL(kind=r8) :: dna0(ncv, 0:ns-1), dpa0(ncv, 0:ns-1), vla0(ncv, 0:1, &
& 0:ns-1), vsa0(ncv, 0:ns-1), hci0(ncv), hcn0(ncv), hcib(ncv, 0:ns-1), &
& vma0(ncv, 0:1, 0:ns-1)
  REAL(kind=r8) :: hce0(ncv), sig0(ncv), alf0(ncv), hvi0(ncv, 0:1), hve0&
& (ncv, 0:1), dkt0(ncv), dzt0(ncv), dna_exb(ncv), hce_exb(ncv), hci_exb(&
& ncv)
!   ..workspace arguments (unspecified on entry and on exit)
  REAL(kind=r8) :: wrk0(ncv)
!   ..common blocks
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2TQNA computes the anomalous contributions to the transport
!     coefficients.
!
!
!  3. description (see also routine b2cdca)
!
!     This routine computes the anomalous contributions to the following
!     transport coefficients.
!     - dna: particle transport coefficients for grad(na).
!     - dpa: particle transport coefficients for grad(pa).
!     - vla: anomalous velocity.
!     - vma: anomalous velocity in parallel momentum balance equation. !srv 15.12.05
!     - vsa: viscosity.
!     - hci: atom thermal conductivity.
!     - hce: electron thermal conductivity.
!     - sig: electrical conductivity.
!     - alf: thermo-electric coefficient.
!     - hvi0: all atom thermal strange velocity.
!     - hve0: electron thermal strange velocity.
!     These coefficients occur in the equations as indicated below.
!     Particle transport.
!       fnax(is) = -dna0(is)*(d/dx)na(is)-dpa0(is)*(d/dx)pa(is)+
!         vla0*na(is)
!       fnay(is) = -dna0(is)*(d/dy)na(is)-dpa0(is)*(d/dy)pa(is)+
!         vla0*na(is)
!     Parallel momentum transport.
!       momx(is) = na(is)*up(is)*vma0 - vsa0(is)*(d/dx)up(is)     !srv 15.12.05
!       momy(is) = na(is)*up(is)*vma0 - vsa0(is)*(d/dy)up(is)     !srv 15.12.05
!     Atom heat flux.
!       fhix = -hci0*(d/dx)ti+hvi0*na*ti
!       fhiy = -hci0*(d/dy)ti+hvi0*na*ti
!     Electron heat flux.
!       fhex = alf0*te*ehx-hce0*(d/dx)te+hve0*ne*te
!       fhey = alf0*te*ehy-hce0*(d/dy)te+hve0*ne*te
!     Electric current.
!       fchx = sig0*ehx-alf0*(d/dx)te
!       fchy = sig0*ehy-alf0*(d/dy)te
!     Here, ehx and ehy are the components of the modified electric
!     field: ehx = -(d/dx)po+(1/(qe*ne))*(d/dx)(ne*te),
!     ehy = -(d/dy)po+(1/(ne*te))*(d/dy)(ne*te). See Balescu /1/.
!     The particle and parallel momentum transport is a property of
!     each atomic species, the other transport laws involve summation
!     over all species.
!
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: icv, is, ic, k
!srv 10.11.09
  CHARACTER :: chns*3
  REAL(kind=r8) :: df0, dfvsb, dfhcb, dfhce, dfsig, dfalf, dcx, dion, &
& vcx, lcx, lion, din, dnn, poloidal_scale_factor
  REAL(kind=r8) :: min_df0_tmp, max_df0_tmp, tav
  INTEGER :: elm_count
  LOGICAL :: catch_up
!   ..procedures
  INTRINSIC SQRT
  EXTERNAL XERTST, SFILL_NODIFF, IPGETI, IPGETR
  INTRINSIC INT
  INTRINSIC MOD
  INTRINSIC SUM
  INTRINSIC REAL
  INTRINSIC TRIM
  EXTERNAL B2XVSG
  INTRINSIC NINT
  INTRINSIC LOG
  INTRINSIC MIN
  INTRINSIC MAX
  EXTERNAL XERRAB
  REAL(kind=r8) :: y1
  REAL(kind=r8), DIMENSION(ncv) :: y2
  REAL(r8) :: max1
  REAL(r8) :: max2
  REAL(r8) :: max3
  REAL(r8) :: max4
  REAL(r8) :: max5
  REAL(kind=r8), DIMENSION(ncv) :: max6
  INTEGER :: arg1
  REAL(r8) :: arg10
  REAL(kind=r8) :: result1
  CHARACTER(len=14) :: arg11
  CHARACTER(len=17) :: arg12
  CHARACTER(len=16) :: arg13
  LOGICAL, DIMENSION(ncv) :: mask
  LOGICAL, DIMENSION(ncv) :: mask0
  LOGICAL, DIMENSION(ncv) :: mask1
  LOGICAL, DIMENSION(ncv) :: mask2
  LOGICAL, DIMENSION(ncv) :: mask3
  CHARACTER(len=11) :: arg14
  CHARACTER(len=12) :: arg15
!   ..initialisation
!-----------------------------------------------------------------------
!.computation
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2tqna')
!   ..set internal parameters on first call
  IF (ncall_b2tqna .EQ. 0) THEN
!WG_TODO        call get_jsep(nx,ny,jxi,jxa,jsep)
!WG_TODO        switch%b2tqna_ixref=jxa
!WG_TODO        call xertst (-1.le.switch%b2tqna_ixref.and.switch%b2tqna_ixref.le.nx,
!WG_TODO     1   'faulty argument b2tqna_ixref')
    IF (switch%b2tqna_transport_namelist .EQ. 1) THEN
      CALL WRITE_B2MOD_TRANSPORT_NAMELIST()
      IF (transport_time_mod .GT. 0.0_R8) THEN
        elm_count = INT(tim/transport_time_mod)
      ELSE
        elm_count = 0
      END IF
      IF (transport_time_mod .GT. 0.0_R8) THEN
        catch_up = transport_time_switch .GT. 0.0_R8 .AND. MOD(tim, &
&         transport_time_mod) .GE. transport_time_switch
      ELSE IF (transport_time_switch .GT. 0.0_R8) THEN
        catch_up = tim .GE. transport_time_switch
      ELSE
        catch_up = .false.
      END IF
      DO WHILE (catch_up)
        WRITE(*, *) 'b2tqna: catching up ...'
        CALL READ_B2MOD_TRANSPORT_NAMELIST()
        CALL WRITE_B2MOD_TRANSPORT_NAMELIST()
        IF (transport_time_mod .GT. 0.0_R8) THEN
          catch_up = transport_time_switch .GT. 0.0_R8 .AND. MOD(tim, &
&           transport_time_mod) .GE. transport_time_switch
        ELSE IF (transport_time_switch .GT. 0.0_R8) THEN
          catch_up = tim .GE. transport_time_switch
        ELSE
          catch_up = .false.
        END IF
      END DO
    END IF
    IF (switch%b2tqna_ballooning .NE. 0.0_R8 .AND. switch%b2tqna_bb_ref &
&       .EQ. 0.0_R8) THEN
      switch%b2tqna_bb_ref = SUM(geo%cvbb(:, 3))/REAL(ncv, r8)
      WRITE(*, *) 'b2tqna: bb_ref set to ', switch%b2tqna_bb_ref
    END IF
  ELSE IF (switch%b2tqna_transport_namelist .EQ. 1) THEN
    IF (transport_time_mod .GT. 0.0_R8) THEN
      WRITE(*, *) 'ELM: ', tim, MOD(tim, transport_time_mod), &
&     transport_time_switch, INT(tim/transport_time_mod), elm_count
      catch_up = (MOD(tim, transport_time_mod) .GE. &
&       transport_time_switch .AND. transport_time_switch .GT. 0.0_R8) &
&       .OR. INT(tim/transport_time_mod) .GT. elm_count
    ELSE IF (transport_time_switch .GT. 0.0_R8) THEN
      catch_up = tim .GE. transport_time_switch
      IF (catch_up) WRITE(*, '(a,a)') &
&                   'Switching to next transport namelist file ', TRIM(&
&                   transport_filename)
    ELSE
      catch_up = .false.
    END IF
    IF (catch_up) THEN
      IF (transport_time_mod .GT. 0.0_R8) elm_count = INT(tim/&
&         transport_time_mod)
      CALL READ_B2MOD_TRANSPORT_NAMELIST()
      CALL WRITE_B2MOD_TRANSPORT_NAMELIST()
    END IF
  END IF
!   ..test nCv, nFc, ns
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!   ..extensive tests on first few calls
  IF (ncall_b2tqna .LT. 3) THEN
!    ..test bb
    CALL B2XVSG(nfc, geo%fcbb(1, 3), 1, 'bb3', '.gt.')
!    ..test state
    arg1 = ncv*ns
    CALL B2XVSG(arg1, pl%na, 1, 'na', '.gt.')
    CALL B2XVSG(ncv, pl%te, 1, 'te', '.gt.')
    CALL B2XVSG(ncv, pl%ti, 1, 'ti', '.gt.')
    arg1 = ncv*2
    CALL B2XVSG(arg1, dv%ni, 1, 'ni', '.gt.')
    CALL B2XVSG(ncv, dv%ne, 1, 'ne', '.gt.')
    CALL B2XVSG(ncv, dv%ne2, 1, 'ne2', '.gt.')
  END IF
!
!   ..compute auxiliary wrk0
  wrk0 = SQRT(pl%ti/mp)
  IF (switch%b2tqna_transport_namelist .EQ. 1) THEN
    DO is=0,ns-1
