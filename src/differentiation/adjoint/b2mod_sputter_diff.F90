!
!  REF-1: "Erosion processes in plasma-wall interactions"
!         C. Garcia Rosales
!         Jnl. Nucl. Mat. 211 (1994) 202-214
!
  SUBROUTINE PHYSPUT(eo, zb, z1, z2, am1, am2, itargsp, yldphy)
    IMPLICIT NONE
!
    REAL(kind=r8) :: eo, zb, z1, z2, am1, am2, eth, etf, stoppwr, &
&   yieldpar, ethbyeo, yldphy, pwr1by6, pwr5by6, pwr1by3, pwr2by3, &
&   z123xz223, z123z223sum, am2byam1, ypar1, ypar2, ypar3, eobyetf, &
&   stoppwr1, stoppwr2
    REAL(kind=r8) :: ro, a, f, fs, q, eps, anu, ang_opt, eta, psi, sigma&
&   , t, angcntrb, gam
    INTEGER :: iionflg, itargsp
    INTRINSIC MAX
    INTRINSIC LOG
    INTRINSIC SQRT
    INTRINSIC MIN
    INTRINSIC COS
    INTRINSIC EXP
    REAL(r8) :: y1
    REAL(r8) :: max1
!
!  Inputs to the subroutine
!  eo   -> energy of the incident particle.
!  z1   -> nuclear charge of incident atom.
!  z2   -> nuclear charge of target atom.
!  am1  -> atomic mass of incident atom (amu).
!  am2  -> atomic mass of target atom (amu).
!
!  es, theta and tgdns are passed as a module global variable
!  es   -> surface binding energy (heat of sublimation) of target (eV).
!  theta -> angle (in degrees) with normal to target of incident particle.
!  tgdns -> target density (gms/cc)
!
!  Output from the subroutine
!  yldphy -> The physical sputtering yield.
!
    IF (eo .LE. 0.0_R8) THEN
      yldphy = 0.0_R8
      RETURN
    ELSE
!
!  Initialising
!
      pwr1by6 = 1.0_R8/6.0_R8
      pwr5by6 = 5.0_R8/6.0_R8
      pwr1by3 = 1.0_R8/3.0_R8
      pwr2by3 = 2.0_R8/3.0_R8
!
      z123xz223 = z1**pwr2by3*z2**pwr2by3
      z123z223sum = z1**pwr2by3 + z2**pwr2by3
      am2byam1 = am2/am1
!
      ypar1 = 1.633_R8*z123xz223*z123z223sum**pwr1by3/es(itargsp)**&
&       pwr2by3
      ypar2 = am1**pwr5by6*am2**pwr1by6/(am1+am2)
      ypar3 = (0.15_R8+0.05_R8*am2byam1)/(1.0_R8+0.05_R8*am2byam1**&
&       1.6_R8)
      yieldpar = ypar1*ypar2*ypar3
!
      eth = (7.0_R8/am2byam1**0.54_R8+0.15_R8*am2byam1**1.12_R8)*es(&
&       itargsp)
      ethbyeo = eth/eo
      IF (1.0_R8 .LT. zb) THEN
        max1 = zb
      ELSE
        max1 = 1.0_R8
      END IF
!
!  The Thomas Fermi energy etf used below is valid only for singly
!  charged ions. If it is multiply charged just multiply etf by the
!  charge of the ion to get the etf for multiply charged ions.
!
      etf = 30.74_R8*(am1+am2)/am2*z1*z2*z123z223sum**0.5_R8*max1
!       print*,z1,' ',am1,' ',z2,' ',am2,' ',etf
!
      eobyetf = eo/etf
      stoppwr1 = 0.5_R8*LOG(1.0_R8+1.2288_R8*eobyetf)
      stoppwr2 = eobyetf + 0.1728_R8*eobyetf**0.5_R8 + 0.008_R8*eobyetf&
&       **0.1504_R8
      stoppwr = stoppwr1/stoppwr2
!
!  The angular dependence of the sputtering yield is included for light ion
!  incidence. The yield is valid for heavy ion scattering too, but for that
!  you have to dig out the "f" and "angl_opt" from
!  Ref-2: Institute of Plasma Physics, Nagoya University report - IPPJ-AM-26,
!         Japan.
!  ** Note that I have used etf for E_L (eqn 10 of above reference). e**2
!     is not defined in the report .. at least I did not find it. I initially
!     thought e was the charge of incident ion and used 1, i.e. singly ionised
!     ion, but that gave wrong results. Using etf solved the problem.
!
!  Checking whether it is light ion sputtering or heavy ion sputtering.
!  iionflg -> flag for light/heavy ion.
!  iionflg = 0 => light ion sputtering.
!  iionflg = 1 => heavy ion sputtering.
      IF (am1 .LE. 4.0) THEN
        iionflg = 0
      ELSE
        iionflg = 1
      END IF
!
      ro = (am2/tgdns(itargsp)/avogr)**(1.0_R8/3.0_R8)*1.0e8_R8
      a = 0.4685_R8*SQRT(1.0_R8/z123z223sum)
      IF (iionflg .EQ. 0) THEN
        f = (0.94_R8-1.33e-3_R8*am2byam1)*SQRT(es(itargsp))
        gam = 4.0_R8*am1*am2/(am1+am2)**2
        q = SQRT(es(itargsp)/gam/eo)
        eps = eo/etf
        anu = a/ro*SQRT(1.0_R8/2.0_R8/eps/q)
        ang_opt = 90.0_R8 - 180.0_R8*anu/pi
      ELSE
!         For a really good quantitative study you have to input the
!         value of fs from Ref-2 below
!         I am circumventing the need of making a big database
!         by using fig-3 from Ref-2.
        IF (am2byam1 .LE. 2.0_R8) fs = 1.75_R8
        IF (2.0_R8 .LE. am2byam1 .AND. am2byam1 .LT. 4.0_R8) fs = 1.5_R8
        IF (4.0_R8 .LE. am2byam1 .AND. am2byam1 .LT. 6.0_R8) fs = &
&           1.25_R8
        IF (6.0_R8 .LE. am2byam1 .AND. am2byam1 .LT. 8.0_R8) fs = 1.0_R8
        IF (am2byam1 .GE. 8.0_R8) fs = 0.8_R8
        eta = 1.0_R8 - SQRT(eth/eo)
        IF (eta .GT. 0.0_R8) THEN
          y1 = fs*(1.0_R8+2.5_R8*(1.0_R8-eta)/eta)
          IF (10.0_R8 .GT. y1) THEN
            f = y1
          ELSE
            f = 10.0_R8
          END IF
        ELSE
          f = fs
        END IF
        psi = (a/ro)**(3.0_R8/2.0_R8)*SQRT(z1*z2/SQRT(z123z223sum)/eo)
        ang_opt = 90.0_R8 - 286.0_R8*psi**0.45_R8
      END IF
      sigma = COS(ang_opt*pi/180.0_R8)
      t = 1.0_R8/COS(theta(itargsp)*pi/180.0_R8)
      angcntrb = EXP(f*(sigma*(1.0_R8-t)+LOG(t)))
!  Calculation of the physical sputtering yield
      yldphy = yieldpar*stoppwr*(1.0_R8-ethbyeo**pwr2by3)*(1.0_R8-&
&       ethbyeo)**2*angcntrb
      IF (yldphy .LT. 0.0_R8) THEN
!       write(*,*) 'Physical sputtering yield < 0 , Ei = ',eo
        yldphy = 0.0_R8
      END IF
!
!     print*,yldphy,' ',angcntrb,' ',etf,' ',stoppwr,' ',eo
      RETURN
    END IF
  END SUBROUTINE PHYSPUT

!
!
!******************************************************************
!
!     SUBROUTINE  chmsput
!
!**********************************************************************
!
!  Inputs to the subroutine :
!  --------------------------
!  eo -> energy of incident projectile (eV)
!  z1   -> nuclear charge of incident atom.
!  z2   -> nuclear charge of target atom.
!  am1  -> atomic mass of incident atom (amu).
!  am2  -> atomic mass of target atom (amu).
!  tp   -> temperature of the target plate (K).
!  gamma-> incident ion flux (part/m**2/sec).
!
!  Es is passed as part of the module global variables
!  es   -> surface binding energy (heat of sublimation) of target (eV).
!
!  Outputs from the subroutine :
!  -----------------------------
!  yldchm -> Chemical sputtering yield.
!
!
!  Subroutine to evaluate sputtering yields.
!
  SUBROUTINE CHMSPUT(ibnd, eo, zb, z1, z2, am1, am2, tp, gamma, yldchm)
    IMPLICIT NONE
!
    INTEGER :: ibnd
    REAL(kind=r8) :: eo, zb, z1, z2, am1, am2, tp, gamma, etf, stoppwr, &
&   yieldpar, yldchm
    REAL(kind=r8) :: pwr2by3, z123z223sum, tpev, eobyetf, stoppwr1, &
&   stoppwr2, flxm2, cmy, etherm, erel, edam, edes, csp3num, csp3denom, &
&   csp3, ytherm, d, chd, ydam, ydes, ysurf, yldthmprt
    INTEGER, SAVE :: formula
    INTRINSIC MAX
    INTRINSIC LOG
    INTRINSIC EXP
    INTRINSIC NINT
    REAL(r8) :: max1
    REAL(kind=r8) :: result1
    DATA formula /0/
!
    IF (eo .LE. 0.0_R8 .OR. gamma .LE. 0.0_R8) THEN
      yldchm = 0.0_R8
      RETURN
    ELSE
!
!  Initialising
      pwr2by3 = 2.0_R8/3.0_R8
!
      z123z223sum = z1**pwr2by3 + z2**pwr2by3
      IF (1.0_R8 .LT. zb) THEN
        max1 = zb
      ELSE
        max1 = 1.0_R8
      END IF
!
!  The Thomas Fermi energy etf used below is valid only for singly
!  charged ions. If it is multiply charged just multiply etf by the
!  charge of the ion to get the etf for multiply charged ions.
      etf = 30.74_R8*(am1+am2)/am2*z1*z2*z123z223sum**0.5_R8*max1
      tpev = tp/(ev/kbolt)
!
      eobyetf = eo/etf
      stoppwr1 = 0.5_R8*LOG(1.0_R8+1.2288_R8*eobyetf)
      stoppwr2 = eobyetf + 0.1728_R8*eobyetf**0.5_R8 + 0.008_R8*eobyetf&
&       **0.1504_R8
      stoppwr = stoppwr1/stoppwr2
!
      flxm2 = gamma
! Haasz-Davis or Roth formula
!xpb Modified to follow new formula from Roth et al., PSI16
!      flxlmt = 1.0e30_R8*exp(-1.4/tpev)
!      if (flxm2.gt.flxlmt) then  ! Assuming high flux
!        Cmy = 1.0_R8/(1.0_R8+3.0e-23_R8*flxm2)
!      else
!        Cmy = 1.0_R8/(1.0_R8+3.0e7_R8*exp(-1.4/tpev))
!      endif
!
!xpb - 16.06.05 Corrected unphysical flux dependence behaviour pointed
!xpb - out by Detlev Reiter.
      IF (formula .EQ. 1) THEN
!  Haasz-Davis formula, 1998, with flx. dep from Roth, Nucl.Fus 2004
        chd = 1.0_R8/(1.0_R8+(1.67e-22_R8*flxm2)**0.54_R8)
        result1 = YHAASZ97M(eo, tp)
        yldchm = chd*result1
      ELSE
!
        cmy = 1.0_R8/((1.0_R8+(flxm2/6.0e21_R8)**0.54_R8)*(1.0_R8+&
&         3.0e7_R8*EXP(-(1.6_R8/tpev))))
        etherm = 1.7_R8
! erel =1.8 (for pure Graphite);
!      =1.5 (for Si,Ti,W doped graphite)
!      =1.2 (for B doped graphite)
        erel = matsurf_chemsput_erel(ibnd)
        edam = 15.0_R8
        edes = 2.0_R8
        csp3num = cmy*(2.0e-32_R8*flxm2+EXP(-(etherm/tpev)))
        csp3denom = 2.0e-32_R8*flxm2 + (1.0_R8+2.0e29_R8/flxm2*EXP(-(&
&         erel/tpev)))*EXP(-(etherm/tpev))
        csp3 = csp3num/csp3denom
        ytherm = csp3*0.033_R8*EXP(-(etherm/tpev))/(2.0e-32_R8*flxm2+EXP&
&         (-(etherm/tpev)))
        IF (NINT(am1) .EQ. 1) THEN
          yieldpar = 0.035_R8
        ELSE IF (NINT(am1) .EQ. 2) THEN
          yieldpar = 0.1_R8
        ELSE IF (NINT(am1) .EQ. 3) THEN
          yieldpar = 0.12_R8
        END IF
        d = 250.0_R8/am1
        ydam = yieldpar*stoppwr*(1.0_R8-(edam/eo)**pwr2by3)*(1.0_R8-edam&
&         /eo)**2.0_R8
        ydes = yieldpar*stoppwr*(1.0_R8-(edes/eo)**pwr2by3)*(1.0_R8-edes&
&         /eo)**2.0_R8
        ysurf = csp3*ydes/(1.0_R8+EXP((eo-65.0_R8)/40.0_R8))
        yldthmprt = ytherm*(1.0_R8+d*ydam)
        yldchm = yldthmprt + ysurf
      END IF
      IF (yldchm .LE. 0.0_R8) THEN
!       write(*,*) 'Chemical sputtering yield < 0, Ei = ',eo
        yldchm = 0.0_R8
      END IF
!
      RETURN
    END IF
  END SUBROUTINE CHMSPUT

!
!  Subroutine to evaluate sputtering yields of
!  Graphite due to Radiation Enhanced Sublimation.
!
!  NOTE : These formula are valid only for normal incidence
!         and have to be modified for angular incidence.
!
  SUBROUTINE RESSPUT(eo, zb, z1, z2, am1, am2, tp, gamma_in, issput, &
&   yldres)
    IMPLICIT NONE
    REAL(kind=r8) :: eo, zb, z1, z2, am1, am2, tp, gamma_in, yldres
!
    REAL(kind=r8) :: pwr2by3, z123z223sum, tpev, am2byam1, stoppwr, &
&   stoppwr1, stoppwr2, yldparres, eobyetf, ethbyeo, eth, etf, gamma
    INTEGER :: issput, itargsp
    EXTERNAL XERRAB
    INTRINSIC NINT
    INTRINSIC MAX
    INTRINSIC EXP
    INTRINSIC LOG
    REAL(r8) :: max1
!  Inputs to the code
!
!  eo   -> incident ion energy (in eV).
!  z1   -> nuclear charge of incident atom.
!  z2   -> nuclear charge of target atom.
!  am1  -> atomic mass of incident atom (amu).
!  am2  -> atomic mass of target atom (amu).
!  tp   -> temperature of the target plate (eV).
!  gamma -> incident ion flux (part/m**2/sec).
!
!  es is passed as a module global variable
!  es   -> surface binding energy (heat of sublimation) of target (eV).
!
    IF (issput .EQ. target_intcode(1)) THEN
      itargsp = 1
    ELSE IF (ntargsp .GE. 2 .AND. issput .EQ. target_intcode(2)) THEN
      itargsp = 2
    ELSE IF (ntargsp .GE. 3 .AND. issput .EQ. target_intcode(3)) THEN
      itargsp = 3
    ELSE
      CALL XERRAB('ressput: wrong issput')
    END IF
!
    gamma = gamma_in
    IF ((eo .LE. 0.0_R8 .OR. gamma .LE. 0.0_R8) .OR. NINT(z2) .NE. 6) &
&   THEN
      yldres = 0.0_R8
      RETURN
    ELSE
!
      pwr2by3 = 2.0_R8/3.0_R8
      gamma = gamma/1.0e4_R8
!
      z123z223sum = z1**pwr2by3 + z2**pwr2by3
      am2byam1 = am2/am1
!
      eth = (7.0_R8/am2byam1**0.54_R8+0.15_R8*am2byam1**1.12_R8)*es(&
&       itargsp)
      IF (1.0_R8 .LT. zb) THEN
        max1 = zb
      ELSE
        max1 = 1.0_R8
      END IF
!
!  The Thomas Fermi energy etf used below is valid only for singly
!  charged ions. If it is multiply charged just multiply etf by the
!  charge of the ion to get the etf for multiply charged ions.
!
      etf = 30.74_R8*(am1+am2)/am2*z1*z2*z123z223sum**0.5_R8*max1
!
      tpev = tp/(ev/kbolt)
!
      yldparres = 54.0_R8*am1**1.18_R8*EXP((-0.78_R8)/tpev)*(gamma/&
&       1.0e16_R8)**(-0.1_R8)
!
      ethbyeo = eth/eo
      eobyetf = eo/etf
      stoppwr1 = 0.5_R8*LOG(1.0_R8+1.2288_R8*eobyetf)
      stoppwr2 = eobyetf + 0.1728_R8*eobyetf**0.5_R8 + 0.008_R8*eobyetf&
&       **0.1504_R8
      stoppwr = stoppwr1/stoppwr2
!
!  Calculation of the physical sputtering yield
!
!  Calculation of the RES sputtering yield
!  assuming that the flux factor is unity.
!
      yldres = yldparres*stoppwr*(1.0_R8-ethbyeo**pwr2by3)*(1.0_R8-&
&       ethbyeo)**2
!
      gamma = gamma*1.0e4_R8
      IF (yldres .LE. 0.0_R8) THEN
!       write(*,*) 'Radiation Enhanced Sublimation < 0, Ei = ', eo
        yldres = 0.0_R8
      END IF
!
      RETURN
    END IF
  END SUBROUTINE RESSPUT

!
!
!   Subroutine to evaluate thermal evaporation.
!
!   Ref: 1) C. Garcia Rosales, Erosion processes in plasma-wall
!           interactions, J. Nucl. Mater. 211 (1994) Pg.202-214.
!        2) R.A. Langley, Evaporation, Data Compendium for Plasma
!           Surface Interactions, Special Issue, NUCLEAR FUSION,
!           IAEA, Vienna, (1984) 55-61.
!
!   Written by Manoj Warrier on 17-05-96.
!
!********************************************************************************
!
!  This routine takes the temperature of the plate ( tp ), and the
!  atomic mass ( am2 ) of the target as inputs and outputs the flux of
!  atoms evaporated from the plate ( flxthev ) per meter**2 per sec.
!
!  Table of A and B for some first wall materials in Fusion research:
!  (Ref (2) above).
!  (note that A and B are provided such that the vappres is in Pascals.
!  element    |  A       |  B      |
!  Aluminum   | -16165   | 10.91   |
!  Beryllium  | -16720   | 11.61   |
!  Graphite   | -40181   | 14.80   |
!  Copper     | -17079   | 11.26   |
!  Molybdenum | -33069   | 12.02   |
!  Titanium   | -23340   | 9.79    |
!  Tungsten   | -44485   | 12.74   |
!  Titanium - |          |         |
!     Carbide | -39117.9 | 15.23   |
!  Stainless -|          |         |
!     Steel   | -14321.9 | 9.44    |
!
  SUBROUTINE THERMEV(ibnd, is, tp, flxthev, qthev)
    USE B2MOD_B2CMPA_DIFF
    IMPLICIT NONE
    INTEGER :: ibnd, is, i, itrack, nz
    REAL(kind=r8) :: tp, a, b, am2, vappres, alpha
    REAL(kind=r8) :: flxthev, qthev
    LOGICAL :: bulk
    INTRINSIC NINT
    INTRINSIC SQRT
!
! Inputs:
!  tp       ->  temperature of the plate in Kelvin.
!  am2      ->  atomic mass of the target ( in amu )
!  A        ->  Parameter in typical Vapor pressure curve.
!  B        ->  Parameter in typical Vapor pressure curve.
!  alpha    ->  Sticking Coefficient ( = 0.6-0.9 for metals and
!               0.05 for C_3 molecules and 0.4 for C )
!
! Output:
!  flxthev  ->  Flux of target atoms evaporated ( /m**2/sec )
!  qthev    ->  Total heat of sublimation (Joules / m^2 / s)
!
!  vappres  ->  Vapour Pressure of the target in Pascals.
!
    itrack = track_index(is)
    flxthev = 0.0_R8
    qthev = 0.0_R8
    nz = matsurf_nconstituents(ibnd)
!
!xpb Contribution from bulk if species is part of bulk
    bulk = .false.
    DO i=1,nz
      IF (NINT(zn(is)) .EQ. matsurf_nzconstituents(ibnd, i)) THEN
        a = matsurf_vapprs_a(ibnd)
        b = matsurf_vapprs_b(ibnd)
        am2 = atomic_mass(matsurf_nzconstituents(ibnd, i))
        alpha = matsurf_stick_coef(ibnd)
        bulk = .true.
      END IF
    END DO
!
    IF (bulk) THEN
      vappres = 10.0_R8**(a/tp+b)
      flxthev = alpha*vappres/SQRT(2.0_R8*pi*am2*mp*tp*kbolt)*(&
&       layer_nrelconstituents(ibnd, 1)/matsurf_relconstituents(ibnd, 1)&
&       )
      IF (flxthev .LE. 0.0_R8) THEN
!       write(*,*) 'Thermal evaporation < 0, Surface temp = ',tp
        flxthev = 0.0_R8
      END IF
      qthev = flxthev*am(is)*mp*matsurf_heat_sublim(ibnd)
    END IF
!
! this is not a tracked species
    IF (.NOT.(itrack .GT. 0 .AND. itrack .LE. ntrack)) THEN
      RETURN
    ELSE IF (nz .EQ. layer_nconstituents(ibnd)) THEN
! we are not doing mixed materials
      RETURN
    ELSE
!
!xpb Contribution from re-deposited species
!xpb if using the mixed matarials model
!xpb Also need to consider alloy contributions
      a = species_vapprs_a(itrack)
      b = species_vapprs_b(itrack)
      alpha = species_stick_coef(itrack)
      am2 = atomic_mass(layer_nzconstituents(ibnd, nz+itrack))
      vappres = 10.0_R8**(a/tp+b)
      flxthev = flxthev + alpha*vappres/SQRT(2.0_R8*pi*am2*mp*tp*kbolt)*&
&       layer_nrelconstituents(ibnd, nz+itrack)
      qthev = qthev + flxthev*am(is)*mp*matsurf_heat_sublim(ibnd)
!
      RETURN
    END IF
  END SUBROUTINE THERMEV

!
!
!  Subroutine to find the surface temperature of a plasma facing
!  surface assuming a 1-D steady state, heat diffusion with a
!  thermal conductivity that varies with temperature as
!   K(T) = 1/(KA * T + KB)
!
  SUBROUTINE HEATDIFF0D(ibnd, qin, qel, tp)
    USE B2MOD_MATH_DIFF
    IMPLICIT NONE
    INTEGER :: ibnd
    REAL(kind=r8) :: qin, qel, qtot, tl, l, k, kb, ka, tp, tc, td
    LOGICAL, SAVE :: coating
    EXTERNAL LAYER_KAPPA_NODIFF
    REAL(kind=r8) :: LAYER_KAPPA_NODIFF
    INTRINSIC MIN
    INTRINSIC MAX
    REAL(kind=r8) :: y1
    REAL(kind=r8) :: y2
    REAL(kind=r8) :: y3
    REAL(kind=r8) :: y4
    REAL(kind=r8) :: y5
    REAL(kind=r8) :: y6
    REAL(kind=r8) :: y7
    REAL(kind=r8) :: y8
    REAL(kind=r8) :: y9
    REAL(kind=r8) :: y10
    REAL(kind=r8) :: y11
    REAL(kind=r8) :: min1
    REAL(kind=r8) :: min2
    REAL(kind=r8) :: min3
    REAL(kind=r8) :: min4
    REAL(kind=r8) :: arg1
    REAL(kind=r8) :: result1
!
!  Inputs:
!  qin -> incident ion heat flux (Joules/m^2/sec)
!  qel -> incident electron heat flux (Joules/m^2/sec)
!  TL  -> Temperature at x=L (x=0 is plasma facing surface,
!                             x=L is the cold end).
!  L   -> Thickness of the target plate (m).
!  KB  -> parameter in the expression for conductivity (see above)
!  KA  -> parameter in the expression for conductivity (see above)
!
!  Outputs:
!  Tp -> Surface temperature (at x=0) (in Kelvins).
!
!  Used module variables:
!  layer_thickness : deposited - eroded
!  layer_depth : sum of deposition, i.e. minimum depth over which material mixing occurs
!
!  Intermediate variables:
!  Tc -> Temperature at interface between coating and bulk (in Kelvins).
!  Td -> Temperature at interface between mixing layer and original wall material.
!
!  Remarks:
!  Consider adding the option of layer_thermal_contact
!
    tl = backplate_temp(ibnd)
    l = plate_thickness(ibnd)
    IF (l .EQ. 0.0_R8) THEN
      tp = tl
      RETURN
    ELSE
      qtot = qel + qin
      coating = coating_thickness(ibnd) .GT. 0.0_R8 .AND. &
&       coating_thickness(ibnd) + layer_thickness(ibnd) .GT. 0.0_R8
      IF (.NOT.coating) THEN
        ka = matbulk_kappa_ka(ibnd)
        kb = matbulk_kappa_kb(ibnd)
        IF (ka .NE. 0.0_R8) THEN
          IF (l .GT. l + layer_thickness(ibnd)) THEN
            min1 = l + layer_thickness(ibnd)
          ELSE
            min1 = l
          END IF
          arg1 = ka*qtot*min1
          result1 = EXPU2(arg1)
          y7 = (tl+kb/ka)*result1 - kb/ka
          IF (matsurf_vapor_temp(ibnd) .GT. y7) THEN
            y1 = y7
          ELSE
            y1 = matsurf_vapor_temp(ibnd)
          END IF
          IF (tl .LT. y1) THEN
            td = y1
          ELSE
            td = tl
          END IF
        ELSE
          IF (l .GT. l + layer_thickness(ibnd)) THEN
            min2 = l + layer_thickness(ibnd)
          ELSE
            min2 = l
          END IF
          y8 = tl + min2*qtot*kb
          IF (matsurf_vapor_temp(ibnd) .GT. y8) THEN
            y2 = y8
          ELSE
            y2 = matsurf_vapor_temp(ibnd)
          END IF
          IF (tl .LT. y2) THEN
            td = y2
          ELSE
            td = tl
          END IF
        END IF
      ELSE IF (coating) THEN
        ka = matbulk_kappa_ka(ibnd)
        kb = matbulk_kappa_kb(ibnd)
        IF (ka .NE. 0.0_R8) THEN
          arg1 = ka*qtot*(l-coating_thickness(ibnd))
          result1 = EXPU2(arg1)
          y9 = (tl+kb/ka)*result1 - kb/ka
          IF (matsurf_vapor_temp(ibnd) .GT. y9) THEN
            y3 = y9
          ELSE
            y3 = matsurf_vapor_temp(ibnd)
          END IF
          IF (tl .LT. y3) THEN
            tc = y3
          ELSE
            tc = tl
          END IF
        ELSE
          IF (matsurf_vapor_temp(ibnd) .GT. tl + (l-coating_thickness(&
&             ibnd))*qtot*kb) THEN
            y4 = tl + (l-coating_thickness(ibnd))*qtot*kb
          ELSE
            y4 = matsurf_vapor_temp(ibnd)
          END IF
          IF (tl .LT. y4) THEN
            tc = y4
          ELSE
            tc = tl
          END IF
        END IF
        ka = coating_kappa_ka(ibnd)
        kb = coating_kappa_kb(ibnd)
        IF (ka .NE. 0.0_R8) THEN
          IF (coating_thickness(ibnd) .GT. coating_thickness(ibnd) + &
&             layer_thickness(ibnd)) THEN
            min3 = coating_thickness(ibnd) + layer_thickness(ibnd)
          ELSE
            min3 = coating_thickness(ibnd)
          END IF
          arg1 = ka*qtot*min3
          result1 = EXPU2(arg1)
          y10 = (tc+kb/ka)*result1 - kb/ka
          IF (matsurf_vapor_temp(ibnd) .GT. y10) THEN
            y5 = y10
          ELSE
            y5 = matsurf_vapor_temp(ibnd)
          END IF
          IF (tc .LT. y5) THEN
            td = y5
          ELSE
            td = tc
          END IF
        ELSE
          IF (coating_thickness(ibnd) .GT. coating_thickness(ibnd) + &
&             layer_thickness(ibnd)) THEN
            min4 = coating_thickness(ibnd) + layer_thickness(ibnd)
          ELSE
            min4 = coating_thickness(ibnd)
          END IF
          y11 = tc + min4*qtot*kb
          IF (matsurf_vapor_temp(ibnd) .GT. y11) THEN
            y6 = y11
          ELSE
            y6 = matsurf_vapor_temp(ibnd)
          END IF
          IF (tc .LT. y6) THEN
            td = y6
          ELSE
            td = tc
          END IF
        END IF
      END IF
      IF (layer_depth(ibnd) .GT. 0.0_R8) THEN
        k = LAYER_KAPPA_NODIFF(ibnd, td)
        IF (matsurf_vapor_temp(ibnd) .GT. td + qtot*layer_depth(ibnd)/k&
&       ) THEN
          tp = td + qtot*layer_depth(ibnd)/k
        ELSE
          tp = matsurf_vapor_temp(ibnd)
        END IF
      ELSE
        tp = td
      END IF
!
      RETURN
    END IF
  END SUBROUTINE HEATDIFF0D

!
!  A FORTRAN FUNCTION to evaluate empirical formulae for number and
!  energy BACKSCATTERING COEFFICIENTS of light ions incident on
!  Elemental and Compound targets.
!
!***********************************************************************
!
!  program copied from
!
!  NAYOGA UNIVERSITY REPORT #  IPPJ-AM-41 ( Data On The Backscattering
!  Coefficients Of Light Ions From Solids ) - R.Ito, T.Tabata, N.Itoh,
!  K.Morita, T.Kato, H.Tawara.
!
!  Modifications :
!
!       i)  Converted into double precision by Manoj.
!
!       ii) Include heavy particle backscattering as described in the
!           paper : Present Status of Data Compilation on Ion
!           Backscattering. - Tatsuo Tabata and Rinsuke Ito
!           Osaka Prefectural Radiation Research Institute,
!           Shinke-cho, Sakai, Osaka 593 - IPPJ-AM-64, Nagoya
!           University Report.
!
!***********************************************************************
!
!  call RNION for the number backscattering coefficient and
!  REION for the energy backscattering coefficient.
!
!  valid up to any mass number for incident ions, provided m1/m2 < 4.5,
!  where m1 is the mass of the incident ion and m2 is the mass of the
!  target atom.
!
  FUNCTION RNION(energy, nz1, m1, ibnd, theta)
    IMPLICIT NONE
    REAL(kind=r8) :: energy, rnion, theta
    INTEGER :: ibnd, nz1, m1
!
    INTEGER :: i, n
    INTEGER, SAVE :: icount
    REAL(kind=r8) :: th, z1, z2, a1, a2, r, r0, as1, as2, sl, a2eff, sum&
&   , delta, z1p, z2p, z2eff, saeff, sbeff, wi, fmu, aa, zz, eps, sa, sb&
&   , w, fi0, tau, beta2, epsb, ch, redengy, psinum, psiden1, psiden2, &
&   psi, sklfact, r0heavy, feps
    INTRINSIC SQRT
    INTRINSIC LOG
    REAL(kind=r8) :: result1
    DATA icount /0/
    n = 1
!
!  energy backscattering coefficient of light ions
    IF (nz1 .EQ. 1) THEN
      IF (m1 .LT. 1 .OR. m1 .GT. 3) THEN
        GOTO 2
      ELSE
        a1 = a1t(m1)
      END IF
    ELSE IF (nz1 .EQ. 2) THEN
      IF (m1 .LT. 3 .OR. m1 .GT. 4) THEN
        GOTO 2
      ELSE
        a1 = a1t(m1+1)
      END IF
    ELSE IF (nz1 .GT. 2) THEN
      a1 = a1t(nz1+3)
!     write(*,*)'atomic # of projectile > 2'
!  finding effective a2 :
!xpb   in mixed materials scenarios, the layer_ arrays may vary over time
      a2eff = 0.0_R8
      sum = 0.0_R8
      DO i=1,layer_nconstituents(ibnd)
        a2 = a2t(layer_nzconstituents(ibnd, i))
        a2eff = a2eff + layer_nrelconstituents(ibnd, i)*a2
        sum = sum + layer_nrelconstituents(ibnd, i)
      END DO
      a2 = a2eff/sum
      delta = a1/a2
!     write(*,*)'delta = ',delta
      IF (delta .GT. 4.5_R8 .AND. icount .LE. 100) THEN
        icount = icount + 1
        WRITE(*, '(a,f7.2,a)') 'delta projectile/target > 4.5', delta, &
&       '. Continuing...'
      END IF
    ELSE
      GOTO 2
    END IF
    z1 = nz1
    z1p = z1**(2.0_R8/3.0_R8)
    z2eff = 0.0_R8
    a2eff = 0.0_R8
    saeff = 0.0_R8
    sbeff = 0.0_R8
    sum = 0.0_R8
!
    DO i=1,layer_nconstituents(ibnd)
      z2 = layer_nzconstituents(ibnd, i)
      a2 = a2t(layer_nzconstituents(ibnd, i))
      wi = layer_nrelconstituents(ibnd, i)
      z2eff = z2eff + wi*z2
      a2eff = a2eff + wi*a2
      sum = sum + wi
      fmu = a1/a2
      aa = 1.0_R8 + fmu
      z2p = z2**(2.0_R8/3.0_R8)
      zz = SQRT(1.0_R8 + z1p/z2p)
      eps = FNEPS(energy, z1, a1, z2, a2)
      sa = 0.0793*SQRT(eps)/fmu
      w = z1*zz*aa/z2p
      wi = wi*z1/w/a2
      saeff = saeff + wi*sa
      sl = dd(layer_nzconstituents(ibnd, i))*z1p*(aa/zz)**1.5_R8*sa/SQRT&
&       (a1)
      IF (z2 .LE. 12.9_R8) THEN
        fi0 = 12.0_R8 + 7.0_R8/z2
      ELSE
        fi0 = 9.76_R8 + 58.5_R8/z2**1.19_R8
      END IF
      tau = eps*z2**2.0_R8*w/a1/3.03055e7_R8
      beta2 = tau*(tau+2.0_R8)/(tau+1.0_R8)**2
      epsb = 1.022e6_R8*beta2/z2/fi0
      IF (z1 .LE. 2.9_R8) THEN
        ch = 100.0_R8*z1/z2
      ELSE
        ch = 5.0_R8
      END IF
      sb = 61.474_R8/fmu*w*(LOG(epsb/(1.0_R8-beta2)+1.0_R8+ch/epsb)-&
&       beta2)/fi0/epsb
      result1 = RANGEN(eps)
      sb = 2.0_R8*eps/result1 + sl*sb/(0.25_R8*sl+sb)
      sbeff = sbeff + wi*sb
    END DO
    z2 = z2eff/sum
    a2 = a2eff/sum
    sa = saeff
    sb = sbeff
    eps = FNEPS(energy, z1, a1, z2, a2)
    IF (delta .GT. 0.5_R8) THEN
      redengy = 1.0_R8/(1+(eps/0.104_R8)**0.577_R8+(eps/0.73_R8)**1.5_R8&
&       )
      fmu = a1/a2
      psinum = 1.0_R8 + 24.1_R8*fmu**(-3.995_R8)
      psiden1 = fmu/((1+fmu)*(eps/1.84_R8+1.0_R8))
      psiden2 = (1.0_R8+fmu)**3*eps/(fmu**3*(1.0_R8-3.0_R8/(2.0_R8*fmu)+&
&       0.9_R8/fmu**2+1.0_R8/(2.0_R8*fmu**3))*(eps+13.3_R8))
      psi = psinum/(psiden1+psiden2)
!
!  sbeff/saeff corresponds to the rho_a/rho-t in the reference paper (of course
!  sbeff is not equal to rho_a nor is saeff equal to rho_t).
!
      sklfact = z1**(2.0_R8/3.0_R8)*SQRT(a1)*sb/sa*fmu**2/(1.0_R8+fmu)**&
&       2/psi
      r0heavy = redengy/sklfact
      r = 1.0_R8/(1.0_R8+(eps/0.133_R8)**0.285_R8) + 0.530_R8/(1.0_R8+(&
&       85.0_R8/eps)**1.46_R8)
      th = theta*pi/180.0_R8
!       write(*,*) 'did heavy projectile calculation for delta > 0.5 '
      IF (n .EQ. 1) GOTO 80
    END IF
!
!     write(*,*) 'did light projectile calculation delta < 0.5 '
!
    feps = (2.0_R8*eps-3.0_R8)/(eps+1.0_R8)
    r0 = 0.705_R8*sa/sb/(1.0_R8+a1/a2)**feps
    r0 = r0/(1.0_R8+(eps/0.047_R8)**0.597_R8+(eps/0.619_R8)**1.5_R8)
    th = theta*pi/180.0_R8
 80 r = 1.0_R8/(1.0_R8+(eps/0.133_R8)**0.285) + 0.530/(1.0_R8+(85.0_R8/&
&     eps)**1.46_R8)
    IF (delta .GT. 0.5_R8) THEN
!       write(*,*) 'did heavy projectile calculation for rnion'
      IF (th .NE. 0.0_R8) THEN
!         write(*,*) 'did theta <> 0 calculation for rnion'
        r0 = r0heavy
        as1 = 7.38_R8*eps**0.359_R8
        as2 = 0.836_R8/eps**0.087_R8
        rnion = RNE(r0, th, as1, as2)
        RETURN
      ELSE
!         write(*,*) 'did theta = 0 calculation for rnion'
        rnion = r0heavy
!         write(*,*) r0heavy, rnion, r, reion
        RETURN
      END IF
    ELSE
!       write(*,*) 'did light projectile calculation for rnion'
      r0 = r0/r
      IF (th .EQ. 0.0_R8) THEN
        rnion = r0
        RETURN
      ELSE
        as1 = 7.38_R8*eps**0.359_R8
        as2 = 0.836_R8/eps**0.087_R8
        rnion = RNE(r0, th, as1, as2)
        RETURN
      END IF
    END IF
 2  WRITE(6, 600) 
    rnion = 0.0_R8
    RETURN
!
 600 FORMAT('  undefined projectile is called  ')
  END FUNCTION RNION

