!
  FUNCTION RANGEN(epsiln)
    IMPLICIT NONE
    REAL(kind=r8) :: epsiln, rangen
    REAL(kind=r8) :: x, x1, x2
    REAL(kind=r8), SAVE :: aa, bb, cc
    INTRINSIC LOG
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    DATA aa, bb, cc /0.56258_R8, 1.1776_R8, 0.62680_R8/
    x = LOG(bb*epsiln)
    x1 = (cc-1.0_R8)*x
    x2 = -(2.0_R8*x)
    result1 = EXPINT(x1)
    result2 = EXPINT(x2)
    rangen = (result1-result2)/aa/bb
    RETURN
  END FUNCTION RANGEN

!
  FUNCTION EXPINT(x)
    IMPLICIT NONE
!  incomplete gamma function : gamma ( 0 , x )
!  gamma( 0 , x ) = - ei ( - x )        for x > 0
!  gamma( 0 , x ) = - eibar( - x )      for x < 0
    REAL(kind=r8) :: expint, x
    REAL(kind=r8) :: f, p, q, t, z
    REAL(kind=r8), SAVE :: a(4), b(4), conver
    INTRINSIC DBLE
    INTRINSIC ABS
    INTRINSIC LOG
    INTRINSIC EXP
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: abs1
    DATA a /0.2677737343_R8, 8.6347608925_R8, 18.059016973_R8, &
&        8.5733287401_R8/
    DATA b /3.9584969228_R8, 21.0996530827_R8, 25.6329561486_R8, &
&        9.5733223454_R8/
    DATA conver /1.0e-7_R8/
