!
  FUNCTION REFLECT(is, te, ibnd)
    USE B2MOD_B2CMPA_DIFF
    IMPLICIT NONE
    INTEGER :: is, ibnd
    REAL(kind=r8) :: te, reflect
    REAL(kind=r8) :: reffrac, reflrn_hilim, reflrn_lolim, reflrn_te, f
    INTEGER :: iss, ib, it, ih, lng
    INTEGER :: isp, itargsp
    INTRINSIC ALLOCATED
    INTRINSIC NINT
    REAL(kind=r8) :: arg1
    INTEGER :: arg2
    INTEGER :: arg3
!     write(*,*) 'reflect called',is,te/ev
    IF (.NOT.ALLOCATED(sputter_index)) THEN
      reflect = 0.0_R8
      RETURN
    ELSE
      iss = sputter_index(is)
      reflect = 0.0_R8
      DO isp=1,layer_nconstituents(ibnd)
        itargsp = target_index(layer_nzconstituents(ibnd, isp))
        lng = reflect_length(iss, itargsp)
        IF (lng .EQ. -1 .OR. te .LE. 0.0_R8) THEN
! no data
          reffrac = 0.0_R8
        ELSE IF (lng .EQ. 0) THEN
! analytic formula
          arg1 = te/ev
          arg2 = NINT(zn(is))
          arg3 = NINT(am(is))
          reffrac = RNION(arg1, arg2, arg3, ibnd, theta(itargsp))
        ELSE IF (te .LE. reflect_energy(1, iss, itargsp)) THEN
! TRIM low Te extrapolation
!         write(*,*) 'te <= lower limit'
          arg1 = reflect_energy(1, iss, itargsp)/ev
          arg2 = NINT(zn(is))
          arg3 = NINT(am(is))
          reflrn_lolim = RNION(arg1, arg2, arg3, ibnd, theta(itargsp))
          arg1 = te/ev
          arg2 = NINT(zn(is))
          arg3 = NINT(am(is))
          reflrn_te = RNION(arg1, arg2, arg3, ibnd, theta(itargsp))
          reffrac = reflect_coefficient(1, iss, itargsp)/reflrn_lolim*&
&           reflrn_te
        ELSE IF (te .GE. reflect_energy(lng, iss, itargsp)) THEN
! TRIM high Te extrapolation
!         write(*,*) 'te >= upper limit'
          arg1 = reflect_energy(lng, iss, itargsp)/ev
          arg2 = NINT(zn(is))
          arg3 = NINT(am(is))
          reflrn_hilim = RNION(arg1, arg2, arg3, ibnd, theta(itargsp))
          arg1 = te/ev
          arg2 = NINT(zn(is))
          arg3 = NINT(am(is))
          reflrn_te = RNION(arg1, arg2, arg3, ibnd, theta(itargsp))
          reffrac = reflect_coefficient(lng, iss, itargsp)/reflrn_hilim*&
&           reflrn_te
        ELSE
! TRIM data interpolation
          ib = 1
          it = lng
          DO WHILE (it - ib .GT. 1)
            ih = (ib+it)/2
            IF (te .LT. reflect_energy(ih, iss, itargsp)) THEN
              it = ih
            ELSE IF (te .GT. reflect_energy(ih, iss, itargsp)) THEN
              ib = ih
            ELSE
              reffrac = reflect_coefficient(ih, iss, itargsp)
              RETURN
            END IF
          END DO
!         write(*,*)
!    1     reflect_energy(ib,iss,itargsp)/ev,te/ev,
!    2     reflect_energy(it,iss,itargsp)/ev
          f = (te-reflect_energy(ib, iss, itargsp))/(reflect_energy(it, &
&           iss, itargsp)-reflect_energy(ib, iss, itargsp))
          IF (jagged_rates .EQ. 0) THEN
            reffrac = f*(reflect_coefficient(it, iss, itargsp)-&
&             reflect_coefficient(ib, iss, itargsp)) + &
&             reflect_coefficient(ib, iss, itargsp)
          ELSE
            reffrac = f*(reflect_coefficient(it, iss, itargsp)-&
&             reflect_coefficient(ib, iss, itargsp)) + &
&             reflect_coefficient(it, iss, itargsp)
          END IF
        END IF
        reflect = reflect + reffrac*layer_nrelconstituents(ibnd, isp)
      END DO
      RETURN
    END IF
  END FUNCTION REFLECT

