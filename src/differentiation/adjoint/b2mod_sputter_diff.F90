!
  FUNCTION F(z1, z2)
    IMPLICIT NONE
    REAL(kind=r8) :: f, z1, z2
    INTRINSIC SQRT
    f = SQRT(z1**(2.0_R8/3.0_R8) + z2**(2.0_R8/3.0_R8))
    RETURN
  END FUNCTION F

!
  FUNCTION FNEPS(e, z1, a1, z2, a2)
    IMPLICIT NONE
    REAL(kind=r8) :: fneps, e, z1, a1, z2, a2
    REAL(kind=r8) :: result1
    result1 = F(z1, z2)
    fneps = 0.032534_R8*e/(z1*z2*(1.0_R8+a1/a2)*result1)
    RETURN
  END FUNCTION FNEPS

!
  FUNCTION RNE(r0, th, as1, as2)
    IMPLICIT NONE
    REAL(kind=r8) :: rne, r0, th, as1, as2
    INTRINSIC TAN
    rne = r0 + (1.0_R8-r0)/(1.0_R8+as1/TAN(th)**(2.0_R8*as2))
    RETURN
  END FUNCTION RNE

