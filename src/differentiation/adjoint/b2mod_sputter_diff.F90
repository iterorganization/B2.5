!
  FUNCTION SPUTTER(is, zb, issput, te)
    USE B2MOD_B2CMPA_DIFF
    IMPLICIT NONE
    INTEGER :: is, issput
    REAL(kind=r8) :: sputter, zb, te, yldphy_lowlim, yldphy_hilim, &
&   yldphy_te, f
    INTEGER :: iss, ib, it, ih, lng, itargsp
    EXTERNAL XERRAB
    REAL(kind=r8) :: arg1
!      write(*,*) 'sputter called',is,te
    iss = sputter_index(is)
    IF (issput .EQ. target_intcode(1)) THEN
      itargsp = 1
    ELSE IF (ntargsp .GE. 2 .AND. issput .EQ. target_intcode(2)) THEN
      itargsp = 2
    ELSE IF (ntargsp .GE. 3 .AND. issput .EQ. target_intcode(3)) THEN
      itargsp = 3
    ELSE
      WRITE(0, *) 'ntargsp,issput,target_intcode', ntargsp, issput, &
&     target_intcode(ntargsp)
      CALL XERRAB('sputter: wrong issput')
    END IF
    lng = sputter_length(iss, itargsp)
    IF (lng .EQ. 0) THEN
      arg1 = te/ev
      CALL PHYSPUT(arg1, zb, zn(is), zn(issput), am(is), am(issput), &
&            itargsp, sputter)
      RETURN
    ELSE IF (lng .EQ. -1 .OR. te .LE. 0.0_R8) THEN
      sputter = 0.0_R8
      RETURN
    ELSE IF (te .LE. sputter_energy(1, iss, itargsp)) THEN
!
!       write(*,*) 'te <= lower limit'
      arg1 = sputter_energy(1, iss, itargsp)/ev
      CALL PHYSPUT(arg1, zb, zn(is), zn(issput), am(is), am(issput), &
&            itargsp, yldphy_lowlim)
      arg1 = te/ev
      CALL PHYSPUT(arg1, zb, zn(is), zn(issput), am(is), am(issput), &
&            itargsp, yldphy_te)
      IF (yldphy_lowlim .GT. 0.0_R8 .AND. yldphy_te .GT. 0.0_R8) THEN
        sputter = sputter_coefficient(1, iss, itargsp)/yldphy_lowlim*&
&         yldphy_te
      ELSE
        sputter = 0.0_R8
      END IF
      RETURN
    ELSE IF (te .GE. sputter_energy(lng, iss, itargsp)) THEN
!       write(*,*) 'te >= upper limit'
      arg1 = sputter_energy(lng, iss, itargsp)/ev
      CALL PHYSPUT(arg1, zb, zn(is), zn(issput), am(is), am(issput), &
&            itargsp, yldphy_hilim)
      arg1 = te/ev
      CALL PHYSPUT(arg1, zb, zn(is), zn(issput), am(is), am(issput), &
&            itargsp, yldphy_te)
      sputter = sputter_coefficient(lng, iss, itargsp)/yldphy_hilim*&
&       yldphy_te
      RETURN
    ELSE
      ib = 1
      it = lng
      DO WHILE (it - ib .GT. 1)
! new
!   10  ih=(ib+it)/2
        ih = (ib+it)/2
        IF (te .LT. sputter_energy(ih, iss, itargsp)) THEN
          it = ih
        ELSE IF (te .GT. sputter_energy(ih, iss, itargsp)) THEN
          ib = ih
        ELSE
          sputter = sputter_coefficient(ih, iss, itargsp)
          RETURN
        END IF
!       write(*,*) it-ib
!       if(it-ib.gt.1) goto 10
! new

      END DO
!       write(*,*) sputter_energy(ib,iss,itargsp),te,
!                  sputter_energy(it,iss,itargsp)
      f = (te-sputter_energy(ib, iss, itargsp))/(sputter_energy(it, iss&
&       , itargsp)-sputter_energy(ib, iss, itargsp))
      IF (jagged_rates .EQ. 0) THEN
        sputter = f*(sputter_coefficient(it, iss, itargsp)-&
&         sputter_coefficient(ib, iss, itargsp)) + sputter_coefficient(&
&         ib, iss, itargsp)
      ELSE
        sputter = f*(sputter_coefficient(it, iss, itargsp)-&
&         sputter_coefficient(ib, iss, itargsp)) + sputter_coefficient(&
&         it, iss, itargsp)
      END IF
      RETURN
    END IF
  END FUNCTION SPUTTER

