SUBROUTINE B2TRHRTF_AN_NODIFF_NODIFF(nspecies, switch, mum, mdm, nz2n, &
& c_hwa_an, c_rwa_an, c_hta_an, c_rta_an, c_r_ta_an)
  USE B2MOD_TYPES
  USE B2MOD_SUBSYS
  USE B2MOD_ZZH_DIFFV_DIFFV
  USE B2MOD_SWITCHES_DIFFV_DIFFV
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: nspecies
  TYPE(SWITCHES), INTENT(IN) :: switch
  REAL(kind=r8) :: nz2n(0:nspecies-1), mum(0:nspecies-1, 0:nspecies-1), &
& mdm(0:nspecies-1, 0:nspecies-1)
!   ..output arguments
  REAL(kind=r8) :: c_r_ta_an(0:nspecies-1, 0:nspecies-1), c_hwa_an(0:&
& nspecies-1, 0:nspecies-1), c_rwa_an(0:nspecies-1, 0:nspecies-1), &
& c_hta_an(0:nspecies-1), c_rta_an(0:nspecies-1)
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2TRHRTF_AN computes the heat flux, the R-moment and the thermal
!     force transport coefficient analytically.
!
!  3. description
!     - c_R_TA_an: the thermal force transport coefficient (not the same as c_R_TA)
!     - c_hwA_an: the heat flux velocity-dependent part of the transport coefficient
!     - c_rwA_an: the r-moment velocity-dependent part of the transport coefficient
!     - c_hTA_an: the heat flux temperature-dependent of the part transport coefficient
!
!-----------------------------------------------------------------------
!.declarations
!   ..local variables
  INTEGER :: i, j
  REAL(kind=r8) :: sqrt2
  REAL(kind=r8) :: z5s(0:nspecies-1), z9s(0:nspecies-1), z11s(0:nspecies&
& -1), rz2n(0:nspecies-1), delta_al(0:nspecies-1)
  INTRINSIC SQRT
  REAL(kind=r8), DIMENSION(nspecies) :: result1
!
!-----------------------------------------------------------------------
!.computation
!
!   ..subprogram start-up calls
  CALL SUBINI('b2trhrtf_an')
!   ..main
  sqrt2 = SQRT(2.0_R8)
!    ..Z-variables
  DO i=0,nspecies-1
    rz2n(0:nspecies-1) = nz2n(0:nspecies-1)/nz2n(i)
    z5s(i) = B2MOD_ZZH_Z5S(nspecies, i, mum(i, 0:nspecies-1), mdm(i, 0:&
&     nspecies-1), rz2n)
    z9s(i) = B2MOD_ZZH_Z9S(nspecies, i, mum(i, 0:nspecies-1), mdm(i, 0:&
&     nspecies-1), rz2n)
    z11s(i) = B2MOD_ZZH_Z11S(nspecies, i, mum(i, 0:nspecies-1), mdm(i, 0&
&     :nspecies-1), rz2n)
  END DO
!
!    ..denominator of the analytical transport coefficients
  DO i=0,nspecies-1
    delta_al(i) = 1 + 433.0_R8*sqrt2/288.0_R8*z11s(i) + 65.0_R8*sqrt2/&
&     32.0_R8*z5s(i) - 23.0_R8*sqrt2/16.0_R8*z9s(i) + 5629.0_R8/&
&     1152.0_R8*z5s(i)*z11s(i) - 529.0_R8/128.0_R8*z9s(i)**2
  END DO
!
!    ..the thermal force transport coefficient
  DO i=0,nspecies-1
    result1 = SQRT(mum(i, 0:nspecies-1))
    c_r_ta_an(i, 0:nspecies-1) = 25.0_R8*sqrt2/16.0_R8*mum(i, 0:nspecies&
&     -1)*result1*(1.5_R8+433.0_R8*sqrt2/240.0_R8*z11s(i)-0.5_R8*mum(i, &
&     0:nspecies-1)*(1.0_R8+23.0_R8*sqrt2/8.0_R8*z9s(i)))/delta_al(i)
  END DO
!
!    ..the heat flux temperature-dependent part of the transport coefficient
  DO i=0,nspecies-1
    c_hta_an(i) = 125.0_R8/32.0_R8*(1.0_R8+433.0_R8*sqrt2/360.0_R8*z11s(&
&     i))/delta_al(i)
  END DO
!
!    ..the R-moment temperature-dependent part of the transport coefficient
  DO i=0,nspecies-1
    c_rta_an(i) = 175.0_R8/48.0_R8*(1.0_R8+23.0_R8*sqrt2/8.0_R8*z9s(i))/&
&     delta_al(i)
  END DO
!
!    ..the heat flux velocity-dependent part of the transport coefficient
  DO i=0,nspecies-1
    c_hwa_an(0:nspecies-1, i) = c_r_ta_an(i, 0:nspecies-1)*(nz2n(0:&
&     nspecies-1)/nz2n(i))
  END DO
!
!    ..the R-moment velocity-dependent part of the transport coefficient
  DO i=0,nspecies-1
    result1 = SQRT(mum(i, 0:nspecies-1))
    c_rwa_an(0:nspecies-1, i) = -(35.0_R8*sqrt2/24.0_R8*mum(i, 0:&
&     nspecies-1)*result1*(-1.5_R8-69.0_R8*sqrt2/16.0_R8*z9s(i)+2.5_R8*&
&     mum(i, 0:nspecies-1)*(1.0_R8+13.0_R8*sqrt2/8.0_R8*z5s(i)))/&
&     delta_al(i)*(nz2n(0:nspecies-1)/nz2n(i)))
  END DO
!
!    ..the mdf correction
  IF (switch%zh_analyt_mdf .EQ. 1) THEN
    DO j=0,nspecies-1
      DO i=0,nspecies-1
        IF (mdm(i, j) .GT. 1.0_R8) THEN
          c_hwa_an(j, i) = 0.0_R8
          c_rwa_an(j, i) = 0.0_R8
        END IF
      END DO
    END DO
  END IF
!
  CALL SUBEND()
  RETURN
!
END SUBROUTINE B2TRHRTF_AN_NODIFF_NODIFF

