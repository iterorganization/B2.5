& , c_hwa_an, c_hwa_and, c_rwa_an, c_rwa_and, c_hta_an, c_hta_and, &
& c_rta_an, c_r_ta_an, c_r_ta_and, nbdirs)
  USE B2MOD_TYPES
  USE B2MOD_SUBSYS
  USE B2MOD_ZZH_DIFFV_DIFFV
  USE B2MOD_SWITCHES_DIFFV_DIFFV
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: nspecies
  TYPE(SWITCHES), INTENT(IN) :: switch
  REAL(kind=r8) :: nz2n(0:nspecies-1), mum(0:nspecies-1, 0:nspecies-1), &
& mdm(0:nspecies-1, 0:nspecies-1)
  REAL(kind=r8) :: nz2nd(nbdirsmax, 0:nspecies-1)
!   ..output arguments
  REAL(kind=r8) :: c_r_ta_an(0:nspecies-1, 0:nspecies-1), c_hwa_an(0:&
& nspecies-1, 0:nspecies-1), c_rwa_an(0:nspecies-1, 0:nspecies-1), &
& c_hta_an(0:nspecies-1), c_rta_an(0:nspecies-1)
  REAL(kind=r8) :: c_r_ta_and(nbdirsmax, 0:nspecies-1, 0:nspecies-1), &
& c_hwa_and(nbdirsmax, 0:nspecies-1, 0:nspecies-1), c_rwa_and(nbdirsmax&
& , 0:nspecies-1, 0:nspecies-1), c_hta_and(nbdirsmax, 0:nspecies-1)
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2TRHRTF_AN computes the heat flux, the R-moment and the thermal
!     force transport coefficient analytically.
!
!  3. description
!     - c_R_TA_an: the thermal force transport coefficient (not the same as c_R_TA)
!     - c_hwA_an: the heat flux velocity-dependent part of the transport coefficient
!     - c_rwA_an: the r-moment velocity-dependent part of the transport coefficient
!     - c_hTA_an: the heat flux temperature-dependent of the part transport coefficient
!
!-----------------------------------------------------------------------
!.declarations
!   ..local variables
  INTEGER :: i, j
  REAL(kind=r8) :: sqrt2
  REAL(kind=r8) :: z5s(0:nspecies-1), z9s(0:nspecies-1), z11s(0:nspecies&
& -1), rz2n(0:nspecies-1), delta_al(0:nspecies-1)
  REAL(kind=r8) :: z5sd(nbdirsmax, 0:nspecies-1), z9sd(nbdirsmax, 0:&
& nspecies-1), z11sd(nbdirsmax, 0:nspecies-1), rz2nd(nbdirsmax, 0:&
& nspecies-1), delta_ald(nbdirsmax, 0:nspecies-1)
  INTRINSIC SQRT
  REAL(kind=r8), DIMENSION(nspecies) :: result1
  INTEGER :: nd
  REAL(kind=r8), DIMENSION(nspecies) :: temp
  REAL(kind=r8) :: temp0
  REAL(kind=r8), DIMENSION(nspecies) :: temp1
  REAL(r8) :: temp2
  REAL(kind=r8), DIMENSION(nspecies) :: temp3
  INTEGER :: nbdirs
!
!-----------------------------------------------------------------------
!.computation
!
!   ..subprogram start-up calls
  CALL SUBINI('b2trhrtf_an')
!   ..main
  sqrt2 = SQRT(2.0_R8)
  z5sd = 0.d0
  z9sd = 0.d0
  z11sd = 0.d0
!    ..Z-variables
  DO i=0,nspecies-1
    temp = nz2n(0:nspecies-1)/nz2n(i)
    DO nd=1,nbdirs
      rz2nd(nd, 0:nspecies-1) = (nz2nd(nd, 0:nspecies-1)-temp*nz2nd(nd, &
&       i))/nz2n(i)
    END DO
    rz2n(0:nspecies-1) = temp
    CALL B2MOD_ZZH_Z5S_DV(nspecies, i, mum(i, 0:nspecies-1), mdm(i, 0:&
&                   nspecies-1), rz2n, rz2nd, z5s(i), z5sd(:, i), nbdirs&
&                  )
    CALL B2MOD_ZZH_Z9S_DV(nspecies, i, mum(i, 0:nspecies-1), mdm(i, 0:&
&                   nspecies-1), rz2n, rz2nd, z9s(i), z9sd(:, i), nbdirs&
&                  )
    CALL B2MOD_ZZH_Z11S_DV(nspecies, i, mum(i, 0:nspecies-1), mdm(i, 0:&
&                    nspecies-1), rz2n, rz2nd, z11s(i), z11sd(:, i), &
&                    nbdirs)
  END DO
  delta_ald = 0.d0
!
!    ..denominator of the analytical transport coefficients
  DO i=0,nspecies-1
    temp0 = z5s(i)/1152.0_R8
    DO nd=1,nbdirs
      delta_ald(nd, i) = sqrt2*433.0_R8*z11sd(nd, i)/288.0_R8 + sqrt2*&
&       65.0_R8*z5sd(nd, i)/32.0_R8 - (sqrt2*23.0_R8/16.0_R8+529.0_R8*2*&
&       z9s(i)/128.0_R8)*z9sd(nd, i) + 5629.0_R8*(z11s(i)*z5sd(nd, i)/&
&       1152.0_R8+temp0*z11sd(nd, i))
    END DO
    delta_al(i) = sqrt2*433.0_R8*(z11s(i)/288.0_R8) + sqrt2*65.0_R8*(z5s&
&     (i)/32.0_R8) - sqrt2*23.0_R8*(z9s(i)/16.0_R8) + 5629.0_R8*(temp0*&
&     z11s(i)) - 529.0_R8*(z9s(i)*z9s(i)/128.0_R8) + 1
  END DO
!
!    ..the thermal force transport coefficient
  DO i=0,nspecies-1
    result1 = SQRT(mum(i, 0:nspecies-1))
    temp = 25.0_R8*sqrt2*result1*mum(i, 0:nspecies-1)
    temp1 = (433.0_R8*sqrt2*z11s(i)/240.0_R8-0.5_R8*mum(i, 0:nspecies-1)&
&     *(23.0_R8*sqrt2*z9s(i)/8.0_R8+1.0_R8)+1.5_R8)/(16.0_R8*delta_al(i)&
&     )
    DO nd=1,nbdirs
      c_r_ta_and(nd, i, 0:nspecies-1) = temp*(sqrt2*433.0_R8*z11sd(nd, i&
&       )/240.0_R8-mum(i, 0:nspecies-1)*0.5_R8*sqrt2*23.0_R8*z9sd(nd, i)&
&       /8.0_R8-temp1*16.0_R8*delta_ald(nd, i))/(16.0_R8*delta_al(i))
    END DO
    c_r_ta_an(i, 0:nspecies-1) = temp*temp1
  END DO
!
!    ..the heat flux temperature-dependent part of the transport coefficient
  DO i=0,nspecies-1
    temp0 = (433.0_R8*sqrt2*z11s(i)/360.0_R8+1.0_R8)/(32.0_R8*delta_al(i&
&     ))
    DO nd=1,nbdirs
      c_hta_and(nd, i) = 125.0_R8*(sqrt2*433.0_R8*z11sd(nd, i)/360.0_R8-&
&       temp0*32.0_R8*delta_ald(nd, i))/(32.0_R8*delta_al(i))
    END DO
    c_hta_an(i) = 125.0_R8*temp0
  END DO
!
!    ..the R-moment temperature-dependent part of the transport coefficient
  DO i=0,nspecies-1
    c_rta_an(i) = 175.0_R8/48.0_R8*(1.0_R8+23.0_R8*sqrt2/8.0_R8*z9s(i))/&
&     delta_al(i)
  END DO
!
!    ..the heat flux velocity-dependent part of the transport coefficient
  DO i=0,nspecies-1
    temp1 = nz2n(0:nspecies-1)/nz2n(i)
    DO nd=1,nbdirs
      c_hwa_and(nd, 0:nspecies-1, i) = temp1*c_r_ta_and(nd, i, 0:&
&       nspecies-1) + c_r_ta_an(i, 0:nspecies-1)*(nz2nd(nd, 0:nspecies-1&
&       )-temp1*nz2nd(nd, i))/nz2n(i)
    END DO
    c_hwa_an(0:nspecies-1, i) = c_r_ta_an(i, 0:nspecies-1)*temp1
  END DO
!
!    ..the R-moment velocity-dependent part of the transport coefficient
  DO i=0,nspecies-1
    result1 = SQRT(mum(i, 0:nspecies-1))
    temp1 = 35.0_R8*sqrt2*result1*mum(i, 0:nspecies-1)
    temp2 = 24.0_R8*delta_al(i)*nz2n(i)
    temp = nz2n(0:nspecies-1)/temp2
    temp3 = 2.5_R8*mum(i, 0:nspecies-1)*(13.0_R8*sqrt2*z5s(i)/8.0_R8+&
&     1.0_R8) - 69.0_R8*sqrt2*z9s(i)/16.0_R8 - 1.5_R8
    DO nd=1,nbdirs
      c_rwa_and(nd, 0:nspecies-1, i) = -(temp1*(temp*(mum(i, 0:nspecies-&
&       1)*2.5_R8*sqrt2*13.0_R8*z5sd(nd, i)/8.0_R8-sqrt2*69.0_R8*z9sd(nd&
&       , i)/16.0_R8)+temp3*(nz2nd(nd, 0:nspecies-1)-temp*(nz2n(i)*&
&       24.0_R8*delta_ald(nd, i)+24.0_R8*delta_al(i)*nz2nd(nd, i)))/&
&       temp2))
    END DO
    c_rwa_an(0:nspecies-1, i) = -(temp1*(temp3*temp))
  END DO
!
!    ..the mdf correction
  IF (switch%zh_analyt_mdf .EQ. 1) THEN
    DO j=0,nspecies-1
      DO i=0,nspecies-1
        IF (mdm(i, j) .GT. 1.0_R8) THEN
          DO nd=1,nbdirs
            c_hwa_and(nd, j, i) = 0.d0
            c_rwa_and(nd, j, i) = 0.d0
          END DO
          c_hwa_an(j, i) = 0.0_R8
          c_rwa_an(j, i) = 0.0_R8
        END IF
      END DO
    END DO
  END IF
!
  CALL SUBEND()
  RETURN
!
END SUBROUTINE B2TRHRTF_AN_DV_NODIFF

