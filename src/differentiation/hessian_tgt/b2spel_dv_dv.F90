      rt%rlbr(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlbr(icv, 1&
&       , is)*rlte
!      ..compute effective charge state
      temp0 = rtlza(ite, ine+1, is)
      DO nd=1,nbdirs
        t0d(nd) = (temp0-rtlza(ite, ine, is))*fxned(nd)
      END DO
      t0 = rtlza(ite, ine, is)*(1.0_R8-fxne) + temp0*fxne
      temp0 = rtlza(ite+1, ine, is)
      temp = rtlza(ite+1, ine+1, is)
      DO nd=1,nbdirs
        t1d(nd) = (temp-temp0)*fxned(nd)
      END DO
      t1 = temp0*(1.0_R8-fxne) + temp*fxne
      temp0 = rtlt(ite+1) - rtlt(ite)
      DO nd=1,nbdirs
        ttd(nd) = (t1d(nd)-t0d(nd))/temp0
      END DO
      tt = (t1-t0)/temp0
      IF (ltst0) THEN
        CALL TRIMG_DV(-20.0_R8, 20.0_R8, tt, ttd, rt%rlza(icv, 1, is), &
&               rtd%rlza(:, icv, 1, is), nbdirs)
      ELSE
        CALL TRIMG_DV(-20.0_R8, 20.0_R8, tt, ttd, rt%rlza(icv, 1, is), &
&               rtd%rlza(:, icv, 1, is), nbdirs)
      END IF
!      ..compute effective square charge
      temp0 = rtlz2(ite, ine+1, is)
      DO nd=1,nbdirs
        rtd%rlza(nd, icv, 0, is) = (1.0_R8-fxte)*t0d(nd) - (t0-t1)*fxted&
&         (nd) + fxte*t1d(nd) - rlte*rtd%rlza(nd, icv, 1, is) - rt%rlza(&
&         icv, 1, is)*rlted(nd)
        t0d(nd) = (temp0-rtlz2(ite, ine, is))*fxned(nd)
      END DO
      rt%rlza(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlza(icv, 1&
&       , is)*rlte
      t0 = rtlz2(ite, ine, is)*(1.0_R8-fxne) + temp0*fxne
      temp0 = rtlz2(ite+1, ine, is)
      temp = rtlz2(ite+1, ine+1, is)
      DO nd=1,nbdirs
        t1d(nd) = (temp-temp0)*fxned(nd)
      END DO
      t1 = temp0*(1.0_R8-fxne) + temp*fxne
      temp0 = rtlt(ite+1) - rtlt(ite)
      DO nd=1,nbdirs
        ttd(nd) = (t1d(nd)-t0d(nd))/temp0
      END DO
      tt = (t1-t0)/temp0
      IF (ltst0) THEN
        CALL TRIMG_DV(-20.0_R8, 20.0_R8, tt, ttd, rt%rlz2(icv, 1, is), &
&               rtd%rlz2(:, icv, 1, is), nbdirs)
      ELSE
        CALL TRIMG_DV(-20.0_R8, 20.0_R8, tt, ttd, rt%rlz2(icv, 1, is), &
&               rtd%rlz2(:, icv, 1, is), nbdirs)
      END IF
!      ..compute cumulative ionisation potential
      temp0 = rtlpt(ite, ine+1, is)
      DO nd=1,nbdirs
        rtd%rlz2(nd, icv, 0, is) = (1.0_R8-fxte)*t0d(nd) - (t0-t1)*fxted&
&         (nd) + fxte*t1d(nd) - rlte*rtd%rlz2(nd, icv, 1, is) - rt%rlz2(&
&         icv, 1, is)*rlted(nd)
        t0d(nd) = (temp0-rtlpt(ite, ine, is))*fxned(nd)
      END DO
      rt%rlz2(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlz2(icv, 1&
&       , is)*rlte
      t0 = rtlpt(ite, ine, is)*(1.0_R8-fxne) + temp0*fxne
      temp0 = rtlpt(ite+1, ine, is)
      temp = rtlpt(ite+1, ine+1, is)
      DO nd=1,nbdirs
        t1d(nd) = (temp-temp0)*fxned(nd)
      END DO
      t1 = temp0*(1.0_R8-fxne) + temp*fxne
      temp0 = rtlt(ite+1) - rtlt(ite)
      DO nd=1,nbdirs
        ttd(nd) = (t1d(nd)-t0d(nd))/temp0
      END DO
      tt = (t1-t0)/temp0
      IF (ltst0) THEN
        CALL TRIMG_DV(-20.0_R8, 20.0_R8, tt, ttd, rt%rlpt(icv, 1, is), &
&               rtd%rlpt(:, icv, 1, is), nbdirs)
      ELSE
        CALL TRIMG_DV(-20.0_R8, 20.0_R8, tt, ttd, rt%rlpt(icv, 1, is), &
&               rtd%rlpt(:, icv, 1, is), nbdirs)
      END IF
!      ..compute effective ionisation potential
      temp0 = rtlpi(ite, ine+1, is)
      DO nd=1,nbdirs
        rtd%rlpt(nd, icv, 0, is) = (1.0_R8-fxte)*t0d(nd) - (t0-t1)*fxted&
&         (nd) + fxte*t1d(nd) - rlte*rtd%rlpt(nd, icv, 1, is) - rt%rlpt(&
&         icv, 1, is)*rlted(nd)
        t0d(nd) = (temp0-rtlpi(ite, ine, is))*fxned(nd)
      END DO
      rt%rlpt(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlpt(icv, 1&
&       , is)*rlte
      t0 = rtlpi(ite, ine, is)*(1.0_R8-fxne) + temp0*fxne
      temp0 = rtlpi(ite+1, ine, is)
      temp = rtlpi(ite+1, ine+1, is)
      DO nd=1,nbdirs
        t1d(nd) = (temp-temp0)*fxned(nd)
      END DO
      t1 = temp0*(1.0_R8-fxne) + temp*fxne
      temp0 = rtlt(ite+1) - rtlt(ite)
      DO nd=1,nbdirs
        ttd(nd) = (t1d(nd)-t0d(nd))/temp0
      END DO
      tt = (t1-t0)/temp0
      IF (ltst0) THEN
        CALL TRIMG_DV(-20.0_R8, 20.0_R8, tt, ttd, rt%rlpi(icv, 1, is), &
&               rtd%rlpi(:, icv, 1, is), nbdirs)
      ELSE
        CALL TRIMG_DV(-20.0_R8, 20.0_R8, tt, ttd, rt%rlpi(icv, 1, is), &
&               rtd%rlpi(:, icv, 1, is), nbdirs)
      END IF
      DO nd=1,nbdirs
        rtd%rlpi(nd, icv, 0, is) = (1.0_R8-fxte)*t0d(nd) - (t0-t1)*fxted&
&         (nd) + fxte*t1d(nd) - rlte*rtd%rlpi(nd, icv, 1, is) - rt%rlpi(&
&         icv, 1, is)*rlted(nd)
      END DO
      rt%rlpi(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlpi(icv, 1&
&       , is)*rlte
    END DO
  END DO
!
! ..return
  ncall_b2spel = ncall_b2spel + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2SPEL_DV_NODIFF

