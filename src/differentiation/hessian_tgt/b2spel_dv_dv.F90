SUBROUTINE B2SPEL_NODIFF_NODIFF(ncv, ns, ev, te, ne, rt)
  USE B2MOD_TYPES
  USE B2MOD_B2CMRC_DIFFV_DIFFV
  USE B2US_PLASMA_DIFFV_DIFFV
  USE B2MOD_MATH_DIFFV_DIFFV
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_AD_DIFFV_DIFFV, ONLY : ncall_b2spel
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!
!-----------------------------------------------------------------------
!.scribbles
!
!!!   Note: This routine should be rewritten for vectorisation.
!
!-----------------------------------------------------------------------
!.end b2spel
!
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, ns
  REAL(kind=r8) :: ev, te(ncv), ne(ncv)
!   ..output arguments (unspecified on entry)
  TYPE(B2RATES), INTENT(INOUT) :: rt
!   ..common blocks
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2SPEL computes on all cells of the mesh a log-log representation
!     of the rate coefficients for ionisation, recombination, radiation
!     and electron heat loss.
!
!
!  3. description (see also routine b2cdca)
!
!     This routine uses table interpolation to compute on each cell
!     and for each atomic species a local representation of the rate
!     coefficients for ionisation, recombination, electron heat
!     loss and radiation. The representation is linear in the log of
!     the rate coefficient versus the log of the temperature.
!     The tables store the log of the rate coefficients as a function
!     of log(te/ev) and log(ne). Linear interpolation is used.
!     See the appropriate routines elsewhere in the code for a
!     description of the computation of the table entries.
!
!
!  8. further comments
!
!     Linear interpolation in the table is employed to represent the
!     log of each rate coefficient as a function of log(te/ev) and
!     log(ne). If log(te/ev) falls outside the domain of the table
!     then linear extrapolation in that variable is used; if log(ne)
!     falls outside the domain of the table then a prudent zeroth-order
!     extrapolation is used.
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: icv, is, ite, ine
  LOGICAL :: ltst0
  REAL(kind=r8) :: rlte, rlne, fxte, fxne, t0, t1, tt
!   ..procedures
  INTRINSIC MIN, MAX, LOG
  EXTERNAL XERTST
  EXTERNAL B2XVSG
  REAL(kind=r8) :: y1
  REAL(kind=r8) :: y2
  REAL(kind=r8) :: y3
  REAL(kind=r8) :: y4
  REAL(kind=r8) :: arg1
!   ..initialisation
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2spel')
!   ..test nCv, ns
  CALL XERTST(0 .LT. ncv, 'faulty argument nCv')
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!   ..test ev
  CALL XERTST(0.0_R8 .LT. ev, 'faulty argument ev')
!   ..extensive tests on first few calls
  IF (ncall_b2spel .LT. 3) THEN
    CALL B2XVSG(ncv, te, 1, 'te', '.gt.')
    CALL B2XVSG(ncv, ne, 1, 'ne', '.gt.')
  END IF
!
! ..compute rate coefficients
!   ..initialise indices for interpolation
  ite = 0
  ine = 0
!   ..loop over cells
  DO icv=1,ncv
!     ..set ltst0
    arg1 = te(icv)/ev
    ltst0 = rtlt(0) .LE. LOG(arg1)
    arg1 = te(icv)/ev
    y3 = LOG(arg1)
    IF (rtlt(rtnt) .GT. y3) THEN
      y1 = y3
    ELSE
      y1 = rtlt(rtnt)
    END IF
    IF (rtlt(0) .LT. y1) THEN
      rlte = y1
    ELSE
      rlte = rtlt(0)
    END IF
    y4 = LOG(ne(icv))
    IF (rtln(rtnn) .GT. y4) THEN
      y2 = y4
    ELSE
      y2 = rtln(rtnn)
    END IF
    IF (rtln(0) .LT. y2) THEN
      rlne = y2
    ELSE
      rlne = rtln(0)
    END IF
!     ..find ite
!       (linear search starting from previous ite, to establish
!         0.le.ite.lt.rtnt;
!         ite.eq.0.or.rtlt(ite).le.rlte;
!         ite.eq.rtnt-1.or.rlte.lt.rtlt(ite+1).)
 1  IF (ite .LT. rtnt - 1) THEN
      IF (rtlt(ite+1) .LE. rlte) THEN
        ite = ite + 1
        GOTO 1
      END IF
    END IF
 2  IF (0 .LT. ite) THEN
      IF (rlte .LT. rtlt(ite)) THEN
        ite = ite - 1
        GOTO 2
      END IF
    END IF
!     ..compute coefficient for linear interpolation
    fxte = (rlte-rtlt(ite))/(rtlt(ite+1)-rtlt(ite))
!     ..find ine
!       (linear search starting from previous ine, to establish
!         0.le.ine.lt.rtnn;
!         ine.eq.0.or.rtln(ine).le.rlne;
!         ine.eq.rtnn-1.or.rlne.lt.rtln(ine+1).)
 3  IF (ine .LT. rtnn - 1) THEN
      IF (rtln(ine+1) .LE. rlne) THEN
        ine = ine + 1
        GOTO 3
      END IF
    END IF
 4  IF (0 .LT. ine) THEN
      IF (rlne .LT. rtln(ine)) THEN
        ine = ine - 1
        GOTO 4
      END IF
    END IF
!     ..compute coefficient for linear interpolation
    fxne = (rlne-rtln(ine))/(rtln(ine+1)-rtln(ine))
!     ..compute log-log rate coefficients
    DO is=0,ns-1
!      ..compute ionisation rate coefficient
      t0 = (1.0_R8-fxne)*rtlsa(ite, ine, is) + fxne*rtlsa(ite, ine+1, is&
&       )
      t1 = (1.0_R8-fxne)*rtlsa(ite+1, ine, is) + fxne*rtlsa(ite+1, ine+1&
&       , is)
      tt = (t1-t0)/(rtlt(ite+1)-rtlt(ite))
      IF (ltst0) THEN
        rt%rlsa(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      ELSE
        rt%rlsa(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      END IF
      rt%rlsa(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlsa(icv, 1&
&       , is)*rlte
!      ..compute recombination rate coefficient
      t0 = (1.0_R8-fxne)*rtlra(ite, ine, is) + fxne*rtlra(ite, ine+1, is&
&       )
      t1 = (1.0_R8-fxne)*rtlra(ite+1, ine, is) + fxne*rtlra(ite+1, ine+1&
&       , is)
      tt = (t1-t0)/(rtlt(ite+1)-rtlt(ite))
      IF (ltst0) THEN
        rt%rlra(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      ELSE
        rt%rlra(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      END IF
      rt%rlra(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlra(icv, 1&
&       , is)*rlte
!      ..compute heat loss rate coefficient
      t0 = (1.0_R8-fxne)*rtlqa(ite, ine, is) + fxne*rtlqa(ite, ine+1, is&
&       )
      t1 = (1.0_R8-fxne)*rtlqa(ite+1, ine, is) + fxne*rtlqa(ite+1, ine+1&
&       , is)
      tt = (t1-t0)/(rtlt(ite+1)-rtlt(ite))
      IF (ltst0) THEN
        rt%rlqa(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      ELSE
        rt%rlqa(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      END IF
      rt%rlqa(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlqa(icv, 1&
&       , is)*rlte
      t0 = (1.0_R8-fxne)*rtlqr(ite, ine, is) + fxne*rtlqr(ite, ine+1, is&
&       )
      t1 = (1.0_R8-fxne)*rtlqr(ite+1, ine, is) + fxne*rtlqr(ite+1, ine+1&
&       , is)
      tt = (t1-t0)/(rtlt(ite+1)-rtlt(ite))
      IF (ltst0) THEN
        rt%rlqr(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      ELSE
        rt%rlqr(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      END IF
      rt%rlqr(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlqr(icv, 1&
&       , is)*rlte
!      ..compute line radiation rate coefficient
      t0 = (1.0_R8-fxne)*rtlrd(ite, ine, is) + fxne*rtlrd(ite, ine+1, is&
&       )
      t1 = (1.0_R8-fxne)*rtlrd(ite+1, ine, is) + fxne*rtlrd(ite+1, ine+1&
&       , is)
      tt = (t1-t0)/(rtlt(ite+1)-rtlt(ite))
      IF (ltst0) THEN
        rt%rlrd(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      ELSE
        rt%rlrd(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      END IF
      rt%rlrd(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlrd(icv, 1&
&       , is)*rlte
!      ..compute bremsstrahlung radiation rate coefficient
      t0 = (1.0_R8-fxne)*rtlbr(ite, ine, is) + fxne*rtlbr(ite, ine+1, is&
&       )
      t1 = (1.0_R8-fxne)*rtlbr(ite+1, ine, is) + fxne*rtlbr(ite+1, ine+1&
&       , is)
      tt = (t1-t0)/(rtlt(ite+1)-rtlt(ite))
      IF (ltst0) THEN
        rt%rlbr(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      ELSE
        rt%rlbr(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      END IF
      rt%rlbr(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlbr(icv, 1&
&       , is)*rlte
!      ..compute effective charge state
      t0 = (1.0_R8-fxne)*rtlza(ite, ine, is) + fxne*rtlza(ite, ine+1, is&
&       )
      t1 = (1.0_R8-fxne)*rtlza(ite+1, ine, is) + fxne*rtlza(ite+1, ine+1&
&       , is)
      tt = (t1-t0)/(rtlt(ite+1)-rtlt(ite))
      IF (ltst0) THEN
        rt%rlza(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      ELSE
        rt%rlza(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      END IF
      rt%rlza(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlza(icv, 1&
&       , is)*rlte
!      ..compute effective square charge
      t0 = (1.0_R8-fxne)*rtlz2(ite, ine, is) + fxne*rtlz2(ite, ine+1, is&
&       )
      t1 = (1.0_R8-fxne)*rtlz2(ite+1, ine, is) + fxne*rtlz2(ite+1, ine+1&
&       , is)
      tt = (t1-t0)/(rtlt(ite+1)-rtlt(ite))
      IF (ltst0) THEN
        rt%rlz2(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      ELSE
        rt%rlz2(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      END IF
      rt%rlz2(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlz2(icv, 1&
&       , is)*rlte
!      ..compute cumulative ionisation potential
      t0 = (1.0_R8-fxne)*rtlpt(ite, ine, is) + fxne*rtlpt(ite, ine+1, is&
&       )
      t1 = (1.0_R8-fxne)*rtlpt(ite+1, ine, is) + fxne*rtlpt(ite+1, ine+1&
&       , is)
      tt = (t1-t0)/(rtlt(ite+1)-rtlt(ite))
      IF (ltst0) THEN
        rt%rlpt(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      ELSE
        rt%rlpt(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      END IF
      rt%rlpt(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlpt(icv, 1&
&       , is)*rlte
!      ..compute effective ionisation potential
      t0 = (1.0_R8-fxne)*rtlpi(ite, ine, is) + fxne*rtlpi(ite, ine+1, is&
&       )
      t1 = (1.0_R8-fxne)*rtlpi(ite+1, ine, is) + fxne*rtlpi(ite+1, ine+1&
&       , is)
      tt = (t1-t0)/(rtlt(ite+1)-rtlt(ite))
      IF (ltst0) THEN
        rt%rlpi(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      ELSE
        rt%rlpi(icv, 1, is) = TRIMG(-20.0_R8, 20.0_R8, tt)
      END IF
      rt%rlpi(icv, 0, is) = (1.0_R8-fxte)*t0 + fxte*t1 - rt%rlpi(icv, 1&
&       , is)*rlte
    END DO
  END DO
!
! ..return
  ncall_b2spel = ncall_b2spel + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2SPEL_NODIFF_NODIFF
