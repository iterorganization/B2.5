    CALL B2XVSG(ncv, dv%ne2, 1, 'ne2', '.gt.')
  END IF
!   ..compute nirm (atom density weighted by 1/sqrt(am)
  CALL B2XPNR_NODIFF(ncv, ns, pl%na, nirm)
!
! ..compute flux limit velocities
!   ..compute electron heat flux limit velocity
  cflme = cflim(0)
  CALL B2TLHE_NODIFF_NODIFF(ncv, nfc, me, cflme, switch, geo, mpg, dv%ne&
&                     , pl%te, chvemx)
!   ..compute all atom heat flux limit velocity
  cflmi = cflim(1)
  CALL B2TLHI_NODIFF_NODIFF(ncv, nfc, mp, cflmi, switch, geo, mpg, dv%ni&
&                     , nirm, st_ext%ni, pl%ti, chvimx)
!
! ..return
  ncall_b2trql = ncall_b2trql + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2trql
!
END SUBROUTINE B2TRQL_NODIFF_NODIFF

