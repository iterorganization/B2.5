      ALLOCATE(lwldlis_old(SIZE(lwldlis, 1)))
      ALLOCATE(leirlis_old(SIZE(leirlis, 1)))
      ALLOCATE(lb25lis_old(SIZE(lb25lis, 1)))
      lwldlis_old(:) = lwldlis(:, il)
      leirlis_old(:) = leirlis(:, il)
      lb25lis_old(:) = lb25lis(:, il)
      ALLOCATE(lis_ind(nwldlis_old))
!cc*** Order elements of original plot zone in CCW direction
      DO i=1,nwldlis_old
        IF (lwldlis_old(i) .GE. 0.) THEN
          k1 = lwldlis_old(i)
        ELSE
          k1 = -lwldlis_old(i)
        END IF
        xx1 = pp1(1, k1)
        yy1 = pp1(2, k1)
        distmin = 1.0e5_R8
        flagmin = 1
        DO i1=1,nwldlis(1)
          IF (lwldlis(i1, 1) .GE. 0.) THEN
            k2 = lwldlis(i1, 1)
          ELSE
            k2 = -lwldlis(i1, 1)
          END IF
          xx2 = pp1(1, k2)
          yy2 = pp1(2, k2)
          arg1 = (xx1-xx2)**2 + (yy1-yy2)**2
          distance = SQRT(arg1)
!}
          IF (distance .LT. distmin) THEN
!{
            distmin = distance
            flagmin = i1
          END IF
        END DO
        lis_ind(i) = flagmin
!Sort
      END DO
      DO i1=1,nwldlis_old-1
        cont = MINLOC(lis_ind(i1:nwldlis_old), 1)
        itmp = lis_ind(cont+i1-1)
        lis_ind(cont+i1-1) = lis_ind(i1)
        lis_ind(i1) = itmp
        itmp = lwldlis_old(cont+i1-1)
        lwldlis_old(cont+i1-1) = lwldlis_old(i1)
        lwldlis_old(i1) = itmp
        itmp = leirlis_old(cont+i1-1)
        leirlis_old(cont+i1-1) = leirlis_old(i1)
        leirlis_old(i1) = itmp
        itmp = lb25lis_old(cont+i1-1)
        lb25lis_old(cont+i1-1) = lb25lis_old(i1)
        lb25lis_old(i1) = itmp
      END DO
      DEALLOCATE(lis_ind)
!
!cc*** Check if a plot zone has starting and ending element which is inside a target
      IF (.NOT.isrecycling(il)) THEN
        IF (lwldlis_old(1) .GE. 0.) THEN
          k1 = lwldlis_old(1)
        ELSE
          k1 = -lwldlis_old(1)
        END IF
        IF (lwldlis_old(nwldlis_old) .GE. 0.) THEN
          k2 = lwldlis_old(nwldlis_old)
        ELSE
          k2 = -lwldlis_old(nwldlis_old)
        END IF
        flagmin = 0
        DO i1=1,nwldlis(1)
          IF (lwldlis(i1, 1) .GE. 0.) THEN
            ksel = lwldlis(i1, 1)
          ELSE
            ksel = -lwldlis(i1, 1)
          END IF
          IF (ksel .EQ. k1 .OR. ksel .EQ. k2) flagmin = 1
        END DO
        IF (flagmin .EQ. 0) isrecycling(il) = .true.
      END IF
!}
!
!cc*** Substitute B2 faces only when overlapping original plot zone target elements
      IF (isrecycling(il)) THEN
!{ inside target
        k1 = lwldlis_old(1)
        k2 = lwldlis_old(nwldlis_old)
        distmin = 1.0e5_R8
        flagmin = 1
        distmin1 = 1.0e5_R8
        flagmin1 = 1
        DO i=1,nwldlis(1)
          xx1 = pp1(1, k1)
          yy1 = pp1(2, k1)
          ksel = lwldlis(i, 1)
          xx2 = pp1(1, ksel)
          yy2 = pp1(2, ksel)
          arg1 = (xx1-xx2)**2 + (yy1-yy2)**2
          distance = SQRT(arg1)
!}
          IF (distance .LT. distmin) THEN
!{
            distmin = distance
            flagmin = i
          END IF
        END DO
        DO i=flagmin+1,nwldlis(1)
          ksel = lwldlis(i, 1)
          xx1 = pp2(1, k2)
          yy1 = pp2(2, k2)
          xx2 = pp2(1, ksel)
          yy2 = pp2(2, ksel)
          arg1 = (xx1-xx2)**2 + (yy1-yy2)**2
          distance = SQRT(arg1)
!}
          IF (distance .LT. distmin1) THEN
!{
            distmin1 = distance
            flagmin1 = i
          END IF
!}

        END DO
        cont = 1
        DO i=flagmin,flagmin1
          lwldlis_old(cont) = lwldlis(i, 1)
          leirlis_old(cont) = leirlis(i, 1)
          lb25lis_old(cont) = lb25lis(i, 1)
          cont = cont + 1
        END DO
        nwldlis_old = cont - 1
        nwldlis(il) = cont - 1
!*** Now change also the new plot zone according to the CW or CCW order
        IF (lwlddir(il)) THEN
!CCW order
          DO i=1,nwldlis(il)
            lwldlis(i, il) = lwldlis_old(i)
            leirlis(i, il) = leirlis_old(i)
            lb25lis(i, il) = lb25lis_old(i)
          END DO
        ELSE
!CW order
          DO i=1,nwldlis(il)
            lwldlis(i, il) = lwldlis_old(nwldlis(il)-i+1)
            leirlis(i, il) = leirlis_old(nwldlis(il)-i+1)
            lb25lis(i, il) = lb25lis_old(nwldlis(il)-i+1)
          END DO
        END IF
      END IF
      DEALLOCATE(lwldlis_old)
      DEALLOCATE(leirlis_old)
      DEALLOCATE(lb25lis_old)
!}
    END DO
!
!
    DEALLOCATE(isrecycling)
    DEALLOCATE(traversed)
    DEALLOCATE(counted)
    DEALLOCATE(ib)
    DEALLOCATE(it)
    DEALLOCATE(ie)
!}
!
!*** Calculate the segment areas and related quantities
!
    IF (grid_mode .LT. 2) THEN
!{
      kn = lppp + mpg%divfcp(ntrgts, 1) + mpg%divfcp(ntrgts, 2) - 1
    ELSE
!}{
      kn = lppp
!}
      IF (switch%use_eirene .NE. 0) THEN
!{
!{
        DO j=ncns+1,nstsi
          kn = kn + eirdiag_nds_ind(j+1) - eirdiag_nds_ind(j) - 1
!}
        END DO
      ELSE
!}{
!{
        DO j=1,nstsi
          kn = kn + COUNT(mpg%fclbl .EQ. j)
!}
        END DO
      END IF
    END IF
!{
    DO i=1,kn
      arg1 = (pp1(1, i)-pp2(1, i))**2 + (pp1(2, i)-pp2(2, i))**2
      segm_len(i) = SQRT(arg1)
      IF (isymm .EQ. 0) THEN
        segm_area(i) = width*segm_len(i)
      ELSE IF (isymm .EQ. 1 .OR. isymm .EQ. 2) THEN
        segm_area(i) = pi*(pp1(1, i)+pp2(1, i))*segm_len(i)
      ELSE IF (isymm .EQ. 3 .OR. isymm .EQ. 4) THEN
        segm_area(i) = pi*(pp1(2, i)+pp2(2, i))*segm_len(i)
      END IF
!}

    END DO
    DO il=1,nwldprt
      IF (lwldlis(1, il) .GE. 0) THEN
        iabs1 = lwldlis(1, il)
      ELSE
        iabs1 = -lwldlis(1, il)
      END IF
      d = 0.5_R8*segm_len(iabs1)
      x = d
      wldrfdst(1, il) = x
      DO i=2,nwldlis(il)
        x = x + d
        IF (lwldlis(i, il) .GE. 0) THEN
          iabs2 = lwldlis(i, il)
        ELSE
          iabs2 = -lwldlis(i, il)
        END IF
        d = 0.5_R8*segm_len(iabs2)
        x = x + d
        wldrfdst(i, il) = x
      END DO
    END DO
!
!*** Produce the data files containing the geometry of the shadowing
!*** structure and of the plot zones
!
!*** The whole wall including the refined targets (shadowing)
!
    OPEN(lun_w, file='shadow.dat') 
    WRITE(lun_w, '(2a)') '# Wall layout : ', TRIM(label)
    WRITE(lun_w, '(3a)') '# R', tab, 'Z'
!{
    DO i=1,nwldlis(1)
      IF (lwldlis(i, 1) .GE. 0) THEN
        k = lwldlis(i, 1)
      ELSE
        k = -lwldlis(i, 1)
      END IF
      WRITE(lun_w, '(1p,2(1x,e15.8))') pp1(1, k), pp1(2, k)
!}
    END DO
    WRITE(lun_w, '(1p,2(1x,e15.8))') pp2(1, k), pp2(2, k)
    CLOSE(lun_w) 
!
!*** The plot zones: one file for each zone. Here we also write the
!*** coordinates of the segment centers and the reference distance
!
!{
    DO il=2,nwldprt
      l = LEN_TRIM(pltzlbl(il))
!}
!*** create the filename for the output
      IF (l .EQ. 0) THEN
!{
!*** use the zone index to label the plot zone
        WRITE(fn_hlp, '(a,i2.2,a)') 'wlly_', il - 1, '.dat'
      ELSE
!}{
        fn_hlp = 'wlly_'//pltzlbl(il)(:l)//'.dat'
      END IF
      OPEN(lun_w, file=fn_hlp) 
      WRITE(lun_w, '(4a)') '#Layout of plot zone ', pltzlbl(il)(:l), &
&     ' : ', label
      WRITE(lun_w, '(11a)') '#R', tab, 'Z', tab, 'Rc', tab, 'Zc', tab, &
&     'x', tab, 'DG_elm'
!{
      DO i=1,nwldlis(il)
        IF (lwldlis(i, il) .GE. 0) THEN
          k = lwldlis(i, il)
        ELSE
          k = -lwldlis(i, il)
        END IF
        WRITE(lun_w, '(1p,e12.5,4(a1,e12.5),a1,i5)') pp1(1, k), tab, pp1&
&       (2, k), tab, 0.5_R8*(pp1(1, k)+pp2(1, k)), tab, 0.5_R8*(pp1(2, k&
&       )+pp2(2, k)), tab, wldrfdst(i, il), tab, k
!}
      END DO
      WRITE(lun_w, '(1p,e12.5,4(a1,e12.5))') pp2(1, k), tab, pp2(2, k)
      CLOSE(lun_w) 
!}
    END DO
!*** Fill in the input arrays for b2pwrdld
!
    nwldrad = 0
    IF (grid_mode .LT. 2) THEN
!{
      DO j=1,ntrgts
        IF (itrg(j) .NE. 0) THEN
!{
          DO i=1,mpg%divfcp(itrg(j), 2)
            IF (ccw(itrg(j))) THEN
              lwldrad(nwldrad+mpg%divfcp(itrg(j), 2)+1-i) = lppp + &
&               nwldrad + i
            ELSE
              lwldrad(nwldrad+i) = lppp + nwldrad + i
            END IF
!}

          END DO
          nwldrad = nwldrad + mpg%divfcp(itrg(j), 2)
!}
        END IF
      END DO
    ELSE IF (switch%use_eirene .NE. 0) THEN
!{
      DO j=ncns+1,nstsi
        IF (eirdiag_nds_ind(j) + 1 .NE. eirdiag_nds_ind(j+1)) THEN
!{
          DO i=eirdiag_nds_ind(j)+1,eirdiag_nds_ind(j+1)-1
            IF (ccw(ntrgts+j)) THEN
              lwldrad(nwldrad+eirdiag_nds_ind(j+1)-i) = lppp + nwldrad +&
&               i - eirdiag_nds_ind(j)
            ELSE
              lwldrad(nwldrad+i-eirdiag_nds_ind(j)) = lppp + nwldrad + i&
&               - eirdiag_nds_ind(j)
            END IF
!}

          END DO
          nwldrad = nwldrad + eirdiag_nds_ind(j+1) - (eirdiag_nds_ind(j)&
&           +1)
!}
        END IF
      END DO
    ELSE
!{
      DO j=1,nstsi
        nrfc = COUNT(mpg%fclbl .EQ. j)
!{
        DO i=1,nrfc
          IF (.NOT.ccw(ntrgts+j)) THEN
            lwldrad(nwldrad+nrfc-i+1) = lppp + nwldrad + i
          ELSE
            lwldrad(nwldrad+i) = lppp + nwldrad + i
          END IF
!}

        END DO
        nwldrad = nwldrad + nrfc
!}
      END DO
    END IF
    DEALLOCATE(ccw)
    IF (ALLOCATED(first_face)) THEN
      DEALLOCATE(first_face)
    END IF
!
!======================================================================
    CALL SUBEND()
    RETURN
  END IF
END SUBROUTINE B2PWLTRG_NODIFF

