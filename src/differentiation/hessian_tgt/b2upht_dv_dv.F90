      t0 = switch%b2npht_pcm0*pccm0(icv)*wrk(icv)
      DO is=0,ns-1
        IF (solvmreg(is, ireg)) ua(icv, is) = ua(icv, is) - t0
      END DO
    END DO
  END IF
!
  IF (switch%tn_style .LE. 1) tn = ti
!
! ..return
  ncall_b2upht = ncall_b2upht + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2UPHT_NODIFF_NODIFF

