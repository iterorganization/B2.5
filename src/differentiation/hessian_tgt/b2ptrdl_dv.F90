SUBROUTINE B2PTRDL_NODIFF(nwe, ntg, rdl, tg_only)
!
!  version : 07.11.97 13:22
!
!======================================================================
!*** Calculates the radiation power load onto the targets
!*** rdl(i,1,:) corresponds to the West and rdl(i,2,:) to the East target
!*** Last index 0 is a sum over all species
!*** Power is in W/m**2 (previously, MW/m**2)
!======================================================================
  USE B2US_DATA_DIFFV_DIFFV
  USE B2MOD_TYPES
  USE B2MOD_B2CMPA_DIFFV
  USE B2MOD_B2PLOT
  USE B2MOD_NEUTRALS_NAMELIST_DIFFV_DIFFV
  USE B2MOD_B2PLOT_WALL_LOADING
  USE B2MOD_DIMENSIONS
  USE B2MOD_EIRBRA
  IMPLICIT NONE
  INTEGER, INTENT(IN) :: nwe, ntg
  REAL(kind=r8) :: rdl(nwe, ntg, 0:def_natm)
  LOGICAL, INTENT(IN) :: tg_only
  INTEGER :: i, ii, j, jj, k, m, n, nrfc
  INTRINSIC MAXVAL
  INTRINSIC COUNT
  INTEGER :: result1
!}
!======================================================================
!*** If the load is already calculated, just fill in the output array
!
  IF (.NOT.l2rdldt) THEN
!{
!----------------------------------------------------------------------
!*** Input the DG data and prepare the geometry
!
    CALL B2PWINP()
!<<<
    IF (nwldlis(1) .EQ. 0) WRITE(*, *) 'No shadowing structure found!'
!}
!>>>
    IF (.NOT.l2pwinp) THEN
!{
      WRITE(*, *) '**** b2ptrdl: error detected in the input data ', &
&     '==> no radiation load calculated!!!'
!{
      DO k=0,def_natm
!{
        DO j=1,ntg
!{
          result1 = MAXVAL(mpg%divfcp(:, 2))
          DO i=1,result1
            rdl(i, j, k) = 0.0_R8
!}
          END DO
!}

        END DO
!}

      END DO
      RETURN
    ELSE
!----------------------------------------------------------------------
!*** Calculate the load
!
      CALL B2PWRDLD_NODIFF()
!}
!
      IF (lrdlsp .AND. lrdlcr) THEN
!{
        PRINT*, '#### b2ptrdl: both lrdlsp and lrdlcr are true - ', &
&       'internal error'
        lrdlsp = .false.
        lrdlcr = .false.
      END IF
      l2rdldt = .true.
    END IF
  END IF
!----------------------------------------------------------------------
!*** Arrange the output data
!
  n = nspecies
  k = 0
!}
  IF (tg_only) THEN
!{
!{
    DO j=1,ntrgts
      IF (itrg(j) .NE. 0) THEN
!{
        DO i=1,mpg%divfcp(itrg(j), 2)
          k = k + 1
          rdl(i, itrg(j), 0) = wldrdld(lwldrad(k))
!}
          IF (lrdlsp) THEN
!{
!{
            DO m=1,n
              rdl(i, itrg(j), m) = wldrdlsp(lwldrad(k), m)
!}
            END DO
          END IF
!}
          IF (lrdlcr) THEN
!{
            rdl(i, itrg(j), 1) = wldrdlsp(lwldrad(k), 1)
          END IF
!}

        END DO
!}

      END IF
    END DO
  ELSE IF (switch%use_eirene .NE. 0) THEN
!}{
    jj = 0
!{
    DO j=ncns+1,nsts
      IF (eirdiag_nds_ind(j) + 1 .NE. eirdiag_nds_ind(j+1)) THEN
        jj = jj + 1
        DO i=eirdiag_nds_ind(j)+1,eirdiag_nds_ind(j+1)-1
          k = k + 1
          IF (lwldrad(k) .NE. 0) THEN
            ii = i - eirdiag_nds_ind(j)
            rdl(ii, jj, 0) = wldrdld(lwldrad(k))
!}
            IF (lrdlsp) THEN
!{
!{
              DO m=1,n
                rdl(ii, jj, m) = wldrdlsp(lwldrad(k), m)
!}
              END DO
            END IF
!}
            IF (lrdlcr) THEN
!{
              rdl(ii, jj, 1) = wldrdlsp(lwldrad(k), 1)
            END IF
          END IF
        END DO
!}

      END IF
    END DO
  ELSE IF (grid_mode .EQ. 2) THEN
!}{
!{
    DO j=1,nstsi
      nrfc = COUNT(mpg%fclbl .EQ. j)
!{
      DO i=1,nrfc
        k = k + 1
        IF (lwldrad(k) .NE. 0) THEN
          rdl(i, j, 0) = wldrdld(lwldrad(k))
!}
          IF (lrdlsp) THEN
!{
!{
            DO m=1,n
              rdl(i, j, m) = wldrdlsp(lwldrad(k), m)
!}
            END DO
          END IF
!}
          IF (lrdlcr) THEN
!{
            rdl(i, j, 1) = wldrdlsp(lwldrad(k), 1)
          END IF
!}

        END IF
      END DO
!}

    END DO
  END IF
!======================================================================
  RETURN
END SUBROUTINE B2PTRDL_NODIFF

