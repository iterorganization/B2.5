!   ..output arguments (unspecified on entry)
  REAL(kind=r8) :: flv(nfc, 0:1, 0:ns-1)
!   ..common blocks
!     ------------------------------------------------------------------
!     B2TLV0 computes the neutral viscosity flux limit.
!     ------------------------------------------------------------------
!   ..local variables
  INTEGER :: ifc, is
  CHARACTER :: charns*3
  REAL(kind=r8) :: alpha, gamma, qcl, qfl, tfl, maxfluxlimit(0:1), dubf(&
& nfc, 0:1), ubv(nvx)
!   ..initialization
!   ..procedures
  INTRINSIC MAX, ABS, SQRT
  EXTERNAL XERTST
  REAL(kind=r8) :: abs0
  REAL(kind=r8) :: abs1
  REAL(kind=r8) :: abs2
  REAL(kind=r8) :: abs3
  REAL(kind=r8) :: abs4
  INTEGER :: arg1
  REAL(kind=r8) :: arg10
  REAL(kind=r8) :: result1
  CHARACTER(len=10) :: arg11
  CHARACTER(len=9) :: arg12
!     ------------------------------------------------------------------
!   ..subprogram start-up calls
  CALL SUBINI('b2tlv0')
!   ..test nCv, nFc, ns
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!
  arg1 = nfc*2*ns
  CALL SFILL_NODIFF_NODIFF(arg1, 1.0_R8, flv, 1)
  CALL SFILL_NODIFF_NODIFF(2, 1.0_R8, maxfluxlimit, 1)
!
  alpha = switch%b2tlv0_alpha
  gamma = switch%b2tlv0_gamma
!
!   ..nothing done if alpha.eq.0
  IF (alpha .NE. 0.0_R8) THEN
    IF (switch%b2tlv0_style .EQ. 0) THEN
      DO is=0,ns-1
        IF (is_neutral(is)) THEN
!    ..compute differences of parallel velocity
          CALL DIFF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, ua(1, is)&
&                           , ubv, dubf)
          DO ifc=1,nfc
            IF (cvsa(ifc, 0, is)*dubf(ifc, 0) .GE. 0.) THEN
              qcl = cvsa(ifc, 0, is)*dubf(ifc, 0)
            ELSE
              qcl = -(cvsa(ifc, 0, is)*dubf(ifc, 0))
            END IF
            IF (geo%fcqalf(ifc, 0) .GE. 0.) THEN
              abs0 = geo%fcqalf(ifc, 0)
            ELSE
              abs0 = -geo%fcqalf(ifc, 0)
            END IF
            qfl = alpha*geo%fcs(ifc)*abs0*(na(mpg%fccv(ifc, 1), is)*tn(&
&             mpg%fccv(ifc, 1))+na(mpg%fccv(ifc, 2), is)*tn(mpg%fccv(ifc&
&             , 2)))/2.0_R8
            IF (qcl/(qfl+b2tlv0_cutlo) .GE. 0.) THEN
              abs1 = qcl/(qfl+b2tlv0_cutlo)
            ELSE
              abs1 = -(qcl/(qfl+b2tlv0_cutlo))
            END IF
            tfl = 1.0_R8/(1.0_R8+abs1**gamma)**(1.0_R8/gamma)
            cvsa(ifc, 0, is) = cvsa(ifc, 0, is)*tfl
            cvsahz(ifc, 0, is) = cvsahz(ifc, 0, is)*tfl
            flv(ifc, 0, is) = tfl
            IF (maxfluxlimit(0) .LT. tfl) THEN
              maxfluxlimit(0) = tfl
            ELSE
              maxfluxlimit(0) = maxfluxlimit(0)
            END IF
            IF (cvsa(ifc, 1, is)*dubf(ifc, 1) .GE. 0.) THEN
              qcl = cvsa(ifc, 1, is)*dubf(ifc, 1)
            ELSE
              qcl = -(cvsa(ifc, 1, is)*dubf(ifc, 1))
            END IF
            IF (geo%fcqalf(ifc, 1) .GE. 0.) THEN
              abs2 = geo%fcqalf(ifc, 1)
            ELSE
              abs2 = -geo%fcqalf(ifc, 1)
            END IF
            qfl = alpha*geo%fcs(ifc)*abs2*(na(mpg%fccv(ifc, 1), is)*tn(&
&             mpg%fccv(ifc, 1))+na(mpg%fccv(ifc, 2), is)*tn(mpg%fccv(ifc&
&             , 2)))/2.0_R8
            IF (qcl/(qfl+b2tlv0_cutlo) .GE. 0.) THEN
              abs3 = qcl/(qfl+b2tlv0_cutlo)
            ELSE
              abs3 = -(qcl/(qfl+b2tlv0_cutlo))
            END IF
            tfl = 1.0_R8/(1.0_R8+abs3**gamma)**(1.0_R8/gamma)
            cvsa(ifc, 1, is) = cvsa(ifc, 1, is)*tfl
            cvsahz(ifc, 1, is) = cvsahz(ifc, 1, is)*tfl
            flv(ifc, 1, is) = tfl
            IF (maxfluxlimit(1) .LT. tfl) THEN
              maxfluxlimit(1) = tfl
            ELSE
              maxfluxlimit(1) = maxfluxlimit(1)
            END IF
          END DO
        END IF
      END DO
    ELSE
!wdk    flux limit total gradient-driven flow, not individual poloidal
!wdk    or radial contributions, since neutral transport is isotropic
      DO is=0,ns-1
        IF (is_neutral(is)) THEN
!    ..compute differences of parallel velocity
          CALL DIFF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, ua(1, is)&
&                           , ubv, dubf)
          DO ifc=1,nfc
!    ..limit total gradient
            arg10 = dubf(ifc, 0)**2 + dubf(ifc, 1)**2
            result1 = SQRT(arg10)
            qcl = cvsa0(ifc, is)*result1
            qfl = alpha*geo%fcs(ifc)*(na(mpg%fccv(ifc, 1), is)*tn(mpg%&
&             fccv(ifc, 1))+na(mpg%fccv(ifc, 2), is)*tn(mpg%fccv(ifc, 2)&
&             ))/2.0_R8
            IF (qcl/(qfl+b2tlv0_cutlo) .GE. 0.) THEN
              abs4 = qcl/(qfl+b2tlv0_cutlo)
            ELSE
              abs4 = -(qcl/(qfl+b2tlv0_cutlo))
            END IF
            tfl = 1.0_R8/(1.0_R8+abs4**gamma)**(1.0_R8/gamma)
!    ..poloidal
            cvsa(ifc, 0, is) = cvsa(ifc, 0, is)*tfl
            cvsahz(ifc, 0, is) = cvsahz(ifc, 0, is)*tfl
            flv(ifc, 0, is) = tfl
            IF (maxfluxlimit(0) .LT. tfl) THEN
              maxfluxlimit(0) = tfl
            ELSE
              maxfluxlimit(0) = maxfluxlimit(0)
            END IF
!    ..radial
            cvsa(ifc, 1, is) = cvsa(ifc, 1, is)*tfl
            cvsahz(ifc, 1, is) = cvsahz(ifc, 1, is)*tfl
            flv(ifc, 1, is) = tfl
            IF (maxfluxlimit(1) .LT. tfl) THEN
              maxfluxlimit(1) = tfl
            ELSE
              maxfluxlimit(1) = maxfluxlimit(1)
            END IF
          END DO
        END IF
      END DO
    END IF
  END IF
!
!      write(*,'(1x,a,1p,2e10.2)') 'b2tlv0: maxfluxlimit = ',maxfluxlimit
!
  IF (switch%b2tlv0_iout .NE. 0) THEN
    DO is=0,ns-1
      IF (is_neutral(is)) THEN
        WRITE(charns, '(i3.3)') is
        arg11 = 'fl_visc_th'//charns
        CALL MY_OUT_US(70, nfc, 1, flv(1, 0, is), arg11)
        arg12 = 'fl_visc_r'//charns
        CALL MY_OUT_US(70, nfc, 1, flv(1, 1, is), arg12)
      END IF
    END DO
  END IF
!
!   ..return
  CALL SUBEND()
  RETURN
!     ------------------------------------------------------------------
END SUBROUTINE B2TLV0_NODIFF_NODIFF

