FUNCTION TFLUXAV_NODIFF_NODIFF(quant, mpg, geo, ift) RESULT (tfluxav)
!
!---------------------------------------------------------------------
!.documentation
!
!   1. purpose
!
!      TFLUXAV computes the flux tube average of a quantity
!
!   2. parameters
!
!      quant - array(1:mpg%nCv) quantity to be averaged
!      iFt   - label of the flux tube
!
!   3. description
!
!      This function computes and returns the flux tube average
!      of the quantity quant on the flux tube with the label iFt.
!      It allows for a special treatment if Ift is the first flux
!      tube outside the separatrix, otherwise it assumes the flux
!      tube is closed.
!      The integration is made using the trapeze rule.
!----------------------------------------------------------------------
!.declarations
  USE B2MOD_TYPES
  USE B2US_MAP_DIFFV_DIFFV
  USE B2US_GEO_DIFFV_DIFFV
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(GEOMETRY), INTENT(IN) :: geo
  REAL(kind=r8) :: precision, tfluxav
  PARAMETER (precision=1e-8_R8)
!   ..input arguments (unchanged on exit)
  REAL(kind=r8) :: quant(mpg%ncv)
!   ..local variables
  REAL(kind=r8) :: qm, qp, qsum, wsum
  INTEGER :: i, j, ift, icv, icn, icm, icp, ifc
  LOGICAL :: on_separatrix
!   ..procedures
  EXTERNAL XERTST
  INTRINSIC ABS
  REAL(kind=r8) :: abs0
!
!.flux surface averaging
  CALL XERTST(1 .LE. ift .AND. ift .LE. mpg%nft, 'faulty argument iFt')
  qsum = 0.0_R8
  wsum = 0.0_R8
  DO i=1,mpg%ftcvp(ift, 2)
    icv = mpg%ftcv(mpg%ftcvp(ift, 1)+i-1)
    on_separatrix = .true.
    IF (.NOT.mpg%cvonclosedsurface(icv)) THEN
      on_separatrix = .false.
      DO j=1,mpg%cvfcp(icv, 2)
        IF (.NOT.on_separatrix) THEN
          ifc = mpg%cvfc(mpg%cvfcp(icv, 1)+j-1)
          IF (mpg%fccv(ifc, 1) .EQ. icv) THEN
            icn = mpg%fccv(ifc, 2)
          ELSE
            icn = mpg%fccv(ifc, 1)
          END IF
          on_separatrix = mpg%cvonclosedsurface(icn)
        END IF
      END DO
    END IF
    IF (on_separatrix) THEN
      IF (i .GT. 1) THEN
        icm = mpg%ftcv(mpg%ftcvp(ift, 1)+i-2)
      ELSE
        icm = mpg%ftcv(mpg%ftcvp(ift, 1)+mpg%ftcvp(ift, 2)-1)
      END IF
      IF (i .LT. mpg%ftcvp(ift, 2)) THEN
        icp = mpg%ftcv(mpg%ftcvp(ift, 1)+i)
      ELSE
        icp = mpg%ftcv(mpg%ftcvp(ift, 1))
      END IF
      qm = (quant(icm)*geo%cvhx(icv)/geo%cvbb(icv, 0)+quant(icv)*geo%&
&       cvhx(icm)/geo%cvbb(icm, 0))/(geo%cvhx(icm)/geo%cvbb(icm, 0)+geo%&
&       cvhx(icv)/geo%cvbb(icv, 0))
      qp = (quant(icp)*geo%cvhx(icv)/geo%cvbb(icv, 0)+quant(icv)*geo%&
&       cvhx(icp)/geo%cvbb(icp, 0))/(geo%cvhx(icv)/geo%cvbb(icv, 0)+geo%&
&       cvhx(icp)/geo%cvbb(icp, 0))
      qsum = qsum + 0.5_R8*geo%cvhx(icv)/geo%cvbb(icv, 0)*(0.5_R8*(qm+&
&       quant(icv))+0.5_R8*(quant(icv)+qp))
      wsum = wsum + geo%cvhx(icv)/geo%cvbb(icv, 0)
    END IF
  END DO
  IF (wsum .GE. 0.) THEN
    abs0 = wsum
  ELSE
    abs0 = -wsum
  END IF
  IF (abs0 .LT. precision) THEN
! psauter
    tfluxav = 0.0_R8
  ELSE
    tfluxav = qsum/wsum
  END IF
!
!   ..return
  RETURN
END FUNCTION TFLUXAV_NODIFF_NODIFF

