SUBROUTINE B2PWRDLD_NODIFF()
!
!  version : 18.02.98 18:24
!
!======================================================================
!*** Calculates radiation load onto the specified wall sections
!*** The list of the wall segments is to be specified in lwldrad
!======================================================================
  USE B2MOD_TYPES
  USE B2MOD_DIAG_DIFFV_DIFFV
  USE B2MOD_B2CMPA_DIFFV
  USE B2MOD_B2PLOT
  USE B2MOD_RDLOAD
  USE B2MOD_NEUTRALS_NAMELIST_DIFFV_DIFFV
  USE B2MOD_B2PLOT_WALL_LOADING
  USE B2US_DATA_DIFFV_DIFFV
  USE B2MOD_DIMENSIONS
  IMPLICIT NONE
  INTEGER :: iatm, imol, iion
  INTEGER :: i, j, k, l, m, is, icv
  REAL(kind=r8) :: u
  INTRINSIC IABS
!======================================================================
!*** Fill in the input arrays for rlcomp.
!*** First, translate the information on the wall segments belonging
!*** to the "shadowing structure" and "plot zones" into the format used
!*** in rlcomp. That is, we make a "structure" polygon, which is closed,
!*** and the target list referring to the segments of this polygon.
!
  lrdlsp = .false.
  lrdlcr = .false.
!{
  DO i=1,nsgmx
    luwrk(i) = 0
!}
  END DO
  nplgs = nwldlis(1)
  nbndry = nplgs
  DO i=1,nplgs
    IF (lwldlis(i, 1) .GE. 0) THEN
      l = lwldlis(i, 1)
    ELSE
      l = -lwldlis(i, 1)
    END IF
    luwrk(l) = i
    xp(i) = pp1(1, l)
    yp(i) = pp1(2, l)
    lbnd(i) = i
    radld(i) = 0.0_R8
  END DO
!
!
!
!*** Now specify the radiation sources - in W (remember, the additional
!*** sources are in MW !!!)
!
  DO icv=1,mpg%nci
    u = 0.0_R8
    DO is=0,state%ns-1
      u = u + state%srw%rqrad(icv, is) + state%srw%rqbrm(icv, is)
    END DO
    DO is=1,natmi
      u = u - eneutrad(icv, is, 0)
    END DO
    DO is=1,nmoli
      u = u - emolrad(icv, is, 0)
    END DO
    DO is=1,nioni
      u = u - eionrad(icv, is, 0)
    END DO
    ssrcm(icv) = u
    xysrc(1, icv) = geo%cvx(icv)
    xysrc(2, icv) = geo%cvy(icv)
  END DO
  k = mpg%nci
!
  IF (nadsrc .GT. 0 .AND. add_rad .GT. 0.0_R8) THEN
    u = 1.0e6_R8*add_rad/nadsrc
    DO i=1,nadsrc
      k = k + 1
      ssrcm(k) = u
      xysrc(1, k) = addsrc_xy(1, i)
      xysrc(2, k) = addsrc_xy(2, i)
    END DO
  END IF
!
  nsrc = k
!
!*** Calculate the radiation load
!
  CALL RLCOMP_NODIFF()
!      write(*,*) 'b2pwrdld: after rlcomp'
  WRITE(*, *) '  Total radiation         : ', ttsrc
  IF (add_rad .GT. 0.0_R8) WRITE(*, *) '(of which add. core rad.) : ', &
&                          add_rad*1.0e6
  IF (irdparm .EQ. 1) THEN
    IF (grid_mode .EQ. 2) THEN
      WRITE(*, *) '  Reaching the surfaces   : ', tttrg
    ELSE
      WRITE(*, *) '  Reaching the targets    : ', tttrg
    END IF
  ELSE IF (irdparm .EQ. 2 .AND. nwldprt .GT. 1) THEN
    WRITE(*, *) '  Reaching the plot zones : ', tttrg
  END IF
!----------------------------------------------------------------------
!*** Extract the data and put them in the output module.
!*** After rlcomp, the radiation loads are stored in radld (b2mod_rdload)
!*** in the first ntrg elements, in the order given by the ltrg array.
!*** We need them put in wldrdld (b2mod_b2plot_wall_loading.F) in the
!*** positions just after lppp, in the following order:
!***  Western target, cells ny-1 to lytg;
!*** [ inner top target, cells lytg to ny-1;
!***   outer top target, cells ny-1 to lytg ];
!***  Eastern target, cells lytg to ny-1
!*** Normalisation per unit area is also done here.
!
  DO i=1,ntrg
    l = ltrg(i)
    wldrdld(lwldlis(l, 1)) = radld(i)/segm_area(lwldlis(l, 1))
  END DO
!
!{
  DO i=1,nsgmx
!{
    DO j=1,nspecies
      wldrdlsp(i, j) = 0.0_R8
!}
    END DO
!}

  END DO
!}
!
  IF (ildparm .EQ. 99) THEN
!{
!
!*** Calculate the radiation load split by impurity species from B2
!
!{
    DO j=1,nspecies
      WRITE(*, *) '  Radiation split: ', species_list(j)
!{
      DO i=1,nplgs
        radld(i) = 0.0_R8
!}
      END DO
!{
      DO icv=1,mpg%nci
        ssrcm(icv) = 0.0_R8
!{
        DO iatm=1,natmi
          IF (latmscl(iatm) .EQ. j) ssrcm(icv) = ssrcm(icv) - eneutrad(&
&             icv, iatm, 0)
!}

        END DO
!{
        DO imol=1,nmoli
          iatm = lmolscl(imol)
          IF (iatm .NE. 0) THEN
            IF (latmscl(iatm) .EQ. j) ssrcm(icv) = ssrcm(icv) - emolrad(&
&               icv, imol, 0)
!}

          END IF
        END DO
!{
        DO iion=1,nioni
          iatm = lionscl(iion)
          IF (iatm .NE. 0) THEN
            IF (latmscl(iatm) .EQ. j) ssrcm(icv) = ssrcm(icv) - eionrad(&
&               icv, iion, 0)
!}

          END IF
        END DO
        DO is=eb2spcr(j),eb2spcr(j)+nfluids(j)
          ssrcm(icv) = ssrcm(icv) + state%srw%rqrad(icv, is) + state%srw&
&           %rqbrm(icv, is)
        END DO
        xysrc(1, icv) = geo%cvx(icv)
        xysrc(2, icv) = geo%cvy(icv)
!}
      END DO
      nsrc = mpg%nci
      CALL RLCOMP_NODIFF()
      WRITE(*, *) '  Total radiation         : ', ttsrc
      IF (irdparm .EQ. 1) THEN
        IF (grid_mode .EQ. 2) THEN
          WRITE(*, *) '  Reaching the surfaces   : ', tttrg
        ELSE
          WRITE(*, *) '  Reaching the targets    : ', tttrg
        END IF
      ELSE IF (irdparm .EQ. 2 .AND. nwldprt .GT. 1) THEN
        WRITE(*, *) '  Reaching the plot zones : ', tttrg
      END IF
!{
      DO i=1,ntrg
        l = ltrg(i)
        wldrdlsp(lwldlis(l, 1), j) = radld(i)/segm_area(lwldlis(l, 1))
!}
      END DO
!}

    END DO
    lrdlsp = .true.
  END IF
!}
!
  IF (ildparm .EQ. 98 .AND. nadsrc .GT. 0 .AND. add_rad .GT. 0.0_R8) &
& THEN
!{
!
!*** Calculate the radiation load from the core sources
!
    WRITE(*, *) '  Extra radiation from the core       : ', 1.0e6_R8*&
&   add_rad
!{
    DO i=1,nplgs
      radld(i) = 0.0_R8
!}
    END DO
    k = 0
    u = 1.0e6_R8*add_rad/nadsrc
!{
    DO i=1,nadsrc
      k = k + 1
      ssrcm(k) = u
      xysrc(1, k) = addsrc_xy(1, i)
      xysrc(2, k) = addsrc_xy(2, i)
!}
    END DO
    nsrc = k
    CALL RLCOMP_NODIFF()
    WRITE(*, *) '  Core radiation reaching the targets : ', tttrg
    is = 1
!{
    DO i=1,ntrg
      l = ltrg(i)
      wldrdlsp(lwldlis(l, 1), is) = radld(i)/segm_area(lwldlis(l, 1))
!}
    END DO
    lrdlcr = .true.
  END IF
  RETURN
!======================================================================
END SUBROUTINE B2PWRDLD_NODIFF

