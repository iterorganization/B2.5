    REAL(kind=r8) :: roxa
    REAL(kind=r8), DIMENSION(nbdirsmax) :: roxad
    INTEGER :: nd
    INTEGER :: nbdirs
!
    DO nd=1,nbdirs
      roxad(nd) = am(is)*mp*nad(nd, icv, is)
    END DO
    roxa = am(is)*mp*na(icv, is)
    RETURN
  END SUBROUTINE ROXA_DV

