  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
!   ..test me, cflme
  CALL XERTST(0.0_R8 .LT. me, 'faulty argument me')
  CALL XERTST(0.0_R8 .LE. cflme, 'faulty argument cflme')
! Init local flux limit to constant value
!   ..extensive tests on first few calls
  IF (ncall_b2tlhe .LT. 3) THEN
!    ..test sign of ne, te
    CALL B2XVSG(ncv, ne, 1, 'ne', '.gt.')
    CALL B2XVSG(ncv, te, 1, 'te', '.gt.')
  END IF
!
! Enhanced flux limit for low-density regions
  cflme_loc = cflme
!
  IF (switch%b2tlhe_far_sol .EQ. 1) THEN
! Attempt enhanced flux limit for low-density regions
    nefd = 0.d0
    nefd0 = 0.D0
    nefdd = 0.D0
    CALL INTFACE_DV_DV(ncv, nfc, mpg%fccv, geo%fcvol, ne, ned0, ned, &
&                nedd, nef, nefd0, nefd, nefdd, nbdirs, nbdirs0)
    cflme_locd = 0.d0
    cflme_locd0 = 0.D0
    cflme_locdd = 0.D0
    DO ifc=1,nfc
      IF (nef(ifc) .LE. switch%b2tlhe_ne_min) THEN
! Set at min value
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            cflme_locdd(nd0, nd, ifc) = 0.D0
          END DO
          cflme_locd(nd, ifc) = 0.d0
        END DO
        DO nd0=1,nbdirs0
          cflme_locd0(nd0, ifc) = 0.D0
        END DO
        cflme_loc(ifc) = switch%b2tlhe_cflme_min
      ELSE IF (nef(ifc) .LT. switch%b2tlhe_ne_max) THEN
! Interpolate for smooth transition
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            cflme_locdd(nd0, nd, ifc) = (cflme-switch%b2tlhe_cflme_min)*&
&             nefdd(nd0, nd, ifc)/(switch%b2tlhe_ne_max-switch%&
&             b2tlhe_ne_min)
          END DO
          cflme_locd(nd, ifc) = (cflme-switch%b2tlhe_cflme_min)*nefd(nd&
&           , ifc)/(switch%b2tlhe_ne_max-switch%b2tlhe_ne_min)
        END DO
        DO nd0=1,nbdirs0
          cflme_locd0(nd0, ifc) = (cflme-switch%b2tlhe_cflme_min)*nefd0(&
&           nd0, ifc)/(switch%b2tlhe_ne_max-switch%b2tlhe_ne_min)
        END DO
        cflme_loc(ifc) = (cflme*(nef(ifc)-switch%b2tlhe_ne_min)+switch%&
&         b2tlhe_cflme_min*(switch%b2tlhe_ne_max-nef(ifc)))/(switch%&
&         b2tlhe_ne_max-switch%b2tlhe_ne_min)
      END IF
    END DO
  ELSE
    cflme_locd = 0.d0
    cflme_locd0 = 0.D0
    cflme_locdd = 0.D0
  END IF
!
!   ..compute flux limit velocity
  IF (switch%b2tqce_model .EQ. 1 .OR. switch%b2tqce_model .EQ. 3) THEN
    arg1dd = 0.D0
    t0dd = 0.D0
    result1dd = 0.D0
! SOLPS5.0 treatments
!wdk perhaps the if-test should be based on a 'mode' switch, instead of b2tqce_model,
!    because now both options are interchangeable
    DO ifc=1,nfc
      IF (te(mpg%fccv(ifc, 1)) .LT. te(mpg%fccv(ifc, 2))) THEN
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            t0dd(nd0, nd) = tedd(nd0, nd, mpg%fccv(ifc, 2))
          END DO
          t0d(nd) = ted(nd, mpg%fccv(ifc, 2))
        END DO
        DO nd0=1,nbdirs0
          t0d0(nd0) = ted0(nd0, mpg%fccv(ifc, 2))
        END DO
        t0 = te(mpg%fccv(ifc, 2))
      ELSE
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            t0dd(nd0, nd) = tedd(nd0, nd, mpg%fccv(ifc, 1))
          END DO
          t0d(nd) = ted(nd, mpg%fccv(ifc, 1))
        END DO
        DO nd0=1,nbdirs0
          t0d0(nd0) = ted0(nd0, mpg%fccv(ifc, 1))
        END DO
        t0 = te(mpg%fccv(ifc, 1))
      END IF
      IF (geo%fcpbs(ifc) .GE. 0.) THEN
        abs0 = geo%fcpbs(ifc)
      ELSE
        abs0 = -geo%fcpbs(ifc)
      END IF
      arg1 = t0/me
      temp4 = SQRT(arg1)
      DO nd0=1,nbdirs0
        arg1d0(nd0) = t0d0(nd0)/me
        IF (arg1 .EQ. 0.D0) THEN
          tempd(nd0) = 0.D0
        ELSE
          tempd(nd0) = arg1d0(nd0)/(2.0*temp4)
        END IF
        result1d0(nd0) = tempd(nd0)
      END DO
      temp = temp4
      result1 = temp
      DO nd=1,nbdirs
!srv 11.01.13
        DO nd0=1,nbdirs0
          arg1dd(nd0, nd) = t0dd(nd0, nd)/me
        END DO
        arg1d(nd) = t0d(nd)/me
        IF (arg1 .EQ. 0.d0) THEN
          DO nd0=1,nbdirs0
            result1dd(nd0, nd) = 0.D0
          END DO
          result1d(nd) = 0.d0
        ELSE
          temp4 = arg1d(nd)/(2.0*temp)
          DO nd0=1,nbdirs0
            result1dd(nd0, nd) = (arg1dd(nd0, nd)-temp4*2.0*tempd(nd0))/&
&             (2.0*temp)
          END DO
          result1d(nd) = temp4
        END IF
        DO nd0=1,nbdirs0
          chvemxdd(nd0, nd, ifc) = abs0*(cflme_locd(nd, ifc)*result1d0(&
&           nd0)+result1*cflme_locdd(nd0, nd, ifc)+result1d(nd)*&
&           cflme_locd0(nd0, ifc)+cflme_loc(ifc)*result1dd(nd0, nd))
        END DO
        chvemxd(nd, ifc) = abs0*(result1*cflme_locd(nd, ifc)+cflme_loc(&
&         ifc)*result1d(nd))
      END DO
      DO nd0=1,nbdirs0
        chvemxd0(nd0, ifc) = abs0*(result1*cflme_locd0(nd0, ifc)+&
&         cflme_loc(ifc)*result1d0(nd0))
      END DO
      chvemx(ifc) = cflme_loc(ifc)*result1*abs0
    END DO
  ELSE IF (switch%b2tqce_model .EQ. 2) THEN
    arg1dd = 0.D0
    result2dd = 0.D0
    arg2dd = 0.D0
    max1dd = 0.D0
    t0dd = 0.D0
    result4dd = 0.D0
    t1dd = 0.D0
! SOLPS4.0 treatment
    DO ifc=1,nfc
      DO nd=1,nbdirs
        DO nd0=nd,nbdirs0
          t0dd(nd0, nd) = tedd(nd0, nd, mpg%fccv(ifc, 2))
          t1dd(nd0, nd) = tedd(nd0, nd, mpg%fccv(ifc, 1))
        END DO
        t0d(nd) = ted(nd, mpg%fccv(ifc, 2))
        t1d(nd) = ted(nd, mpg%fccv(ifc, 1))
      END DO
      DO nd0=1,nbdirs0
        t0d0(nd0) = ted0(nd0, mpg%fccv(ifc, 2))
        t1d0(nd0) = ted0(nd0, mpg%fccv(ifc, 1))
      END DO
      t0 = te(mpg%fccv(ifc, 2))
      t1 = te(mpg%fccv(ifc, 1))
      IF (geo%fcqalf(ifc, 0) .GE. 0.) THEN
        abs1 = geo%fcqalf(ifc, 0)
      ELSE
        abs1 = -geo%fcqalf(ifc, 0)
      END IF
      IF (t0 .LT. t1) THEN
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            max1dd(nd0, nd) = t1dd(nd0, nd)
          END DO
          max1d(nd) = t1d(nd)
        END DO
        DO nd0=1,nbdirs0
          max1d0(nd0) = t1d0(nd0)
        END DO
        max1 = t1
      ELSE
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            max1dd(nd0, nd) = t0dd(nd0, nd)
          END DO
          max1d(nd) = t0d(nd)
        END DO
        DO nd0=1,nbdirs0
          max1d0(nd0) = t0d0(nd0)
        END DO
        max1 = t0
      END IF
!wdk 10.03.16 Divide by ne*te at cell faces, so chvemx contains the same information
!           as case b2tqce_model.eq.1
!           Removes need for different treatment later on in the code (b2tfhe), and
!           ensures consistency with b2trcl_conductive_limit .ne. 0
      result1 = PIT(mpg%fccv(ifc, 2))
      arg1 = t0/me
      temp4 = SQRT(arg1)
      DO nd0=1,nbdirs0
        arg1d0(nd0) = t0d0(nd0)/me
        IF (arg1 .EQ. 0.D0) THEN
          tempd(nd0) = 0.D0
        ELSE
          tempd(nd0) = arg1d0(nd0)/(2.0*temp4)
        END IF
      END DO
      temp = temp4
      DO nd=1,nbdirs
        DO nd0=nd,nbdirs0
          arg1dd(nd0, nd) = t0dd(nd0, nd)/me
        END DO
        arg1d(nd) = t0d(nd)/me
        IF (arg1 .EQ. 0.d0) THEN
          DO nd0=1,nbdirs0
            result2dd(nd0, nd) = 0.D0
          END DO
          result2d(nd) = 0.d0
        ELSE
          temp4 = arg1d(nd)/(2.0*temp)
          DO nd0=1,nbdirs0
            result2dd(nd0, nd) = (arg1dd(nd0, nd)-temp4*2.0*tempd(nd0))/&
&             (2.0*temp)
          END DO
          result2d(nd) = temp4
        END IF
        DO nd0=1,nbdirs0
          arg2dd(nd0, nd) = t1dd(nd0, nd)/me
        END DO
        arg2d(nd) = t1d(nd)/me
      END DO
      arg2 = t1/me
      temp4 = SQRT(arg2)
      DO nd0=1,nbdirs0
        result2d0(nd0) = tempd(nd0)
        arg2d0(nd0) = t1d0(nd0)/me
        IF (arg2 .EQ. 0.D0) THEN
          tempd(nd0) = 0.D0
        ELSE
          tempd(nd0) = arg2d0(nd0)/(2.0*temp4)
        END IF
      END DO
      result2 = temp
      result3 = PIT(mpg%fccv(ifc, 1))
      temp = temp4
      DO nd=1,nbdirs
        IF (arg2 .EQ. 0.d0) THEN
          DO nd0=1,nbdirs0
            result4dd(nd0, nd) = 0.D0
          END DO
          result4d(nd) = 0.d0
        ELSE
          temp4 = arg2d(nd)/(2.0*temp)
          DO nd0=1,nbdirs0
            result4dd(nd0, nd) = (arg2dd(nd0, nd)-temp4*2.0*tempd(nd0))/&
&             (2.0*temp)
          END DO
          result4d(nd) = temp4
        END IF
      END DO
      result4 = temp
      temp0 = ne(mpg%fccv(ifc, 1)) + ne(mpg%fccv(ifc, 2))
      temp1 = temp0*max1
      temp4 = cflme_loc(ifc)/temp1
      DO nd0=1,nbdirs0
        result4d0(nd0) = tempd(nd0)
        temp0d(nd0) = ned0(nd0, mpg%fccv(ifc, 1)) + ned0(nd0, mpg%fccv(&
&         ifc, 2))
        temp1d(nd0) = max1*temp0d(nd0) + temp0*max1d0(nd0)
        temp2d(nd0) = (cflme_locd0(nd0, ifc)-temp4*temp1d(nd0))/temp1
        temp3d(nd0) = result1*(result2*t0*ned0(nd0, mpg%fccv(ifc, 2))+ne&
&         (mpg%fccv(ifc, 2))*(t0*result2d0(nd0)+result2*t0d0(nd0))) + &
&         result3*(result4*t1*ned0(nd0, mpg%fccv(ifc, 1))+ne(mpg%fccv(&
&         ifc, 1))*(t1*result4d0(nd0)+result4*t1d0(nd0)))
      END DO
      temp = geo%fcs(ifc)*abs1
      temp2 = temp4
      temp3 = result1*ne(mpg%fccv(ifc, 2))*result2*t0 + result3*ne(mpg%&
&       fccv(ifc, 1))*result4*t1
      DO nd=1,nbdirs
        temp4 = t1*result4d(nd) + result4*t1d(nd)
        temp5 = ned(nd, mpg%fccv(ifc, 1))
        temp6 = t0*result2d(nd) + result2*t0d(nd)
        temp7 = ned(nd, mpg%fccv(ifc, 2))
        temp8 = result1*(result2*t0*temp7+ne(mpg%fccv(ifc, 2))*temp6) + &
&         result3*(result4*t1*temp5+ne(mpg%fccv(ifc, 1))*temp4)
        temp9 = temp3/temp1
        temp10 = ned(nd, mpg%fccv(ifc, 1)) + ned(nd, mpg%fccv(ifc, 2))
        temp11 = max1*temp10 + temp0*max1d(nd)
        temp12 = cflme_locd(nd, ifc) - temp2*temp11
        DO nd0=1,nbdirs0
          chvemxdd(nd0, nd, ifc) = temp*(temp8*temp2d(nd0)+temp2*(&
&           result1*(temp7*(t0*result2d0(nd0)+result2*t0d0(nd0))+result2&
&           *t0*nedd(nd0, nd, mpg%fccv(ifc, 2))+temp6*ned0(nd0, mpg%fccv&
&           (ifc, 2))+ne(mpg%fccv(ifc, 2))*(result2d(nd)*t0d0(nd0)+t0*&
&           result2dd(nd0, nd)+t0d(nd)*result2d0(nd0)+result2*t0dd(nd0, &
&           nd)))+result3*(temp5*(t1*result4d0(nd0)+result4*t1d0(nd0))+&
&           result4*t1*nedd(nd0, nd, mpg%fccv(ifc, 1))+temp4*ned0(nd0, &
&           mpg%fccv(ifc, 1))+ne(mpg%fccv(ifc, 1))*(result4d(nd)*t1d0(&
&           nd0)+t1*result4dd(nd0, nd)+t1d(nd)*result4d0(nd0)+result4*&
&           t1dd(nd0, nd))))+temp9*(cflme_locdd(nd0, nd, ifc)-temp11*&
&           temp2d(nd0)-temp2*(temp10*max1d0(nd0)+max1*(nedd(nd0, nd, &
&           mpg%fccv(ifc, 1))+nedd(nd0, nd, mpg%fccv(ifc, 2)))+max1d(nd)&
&           *temp0d(nd0)+temp0*max1dd(nd0, nd)))+temp12*(temp3d(nd0)-&
&           temp9*temp1d(nd0))/temp1)
        END DO
        chvemxd(nd, ifc) = temp*(temp2*temp8+temp12*temp9)
      END DO
      DO nd0=1,nbdirs0
        chvemxd0(nd0, ifc) = temp*(temp2*temp3d(nd0)+temp3*temp2d(nd0))
      END DO
      chvemx(ifc) = temp*(temp3*temp2)
    END DO
  END IF
  ncall_b2tlhe = ncall_b2tlhe + 1
  CALL SUBEND()
  RETURN
!     ------------------------------------------------------------------

CONTAINS
!
  FUNCTION PIT(icv)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    REAL(kind=r8) :: pit
    INTEGER, INTENT(IN) :: icv
    INTRINSIC ABS
    REAL(kind=r8) :: abs2
    IF (geo%cvbb(icv, 0) .GE. 0.) THEN
      abs2 = geo%cvbb(icv, 0)
    ELSE
      abs2 = -geo%cvbb(icv, 0)
    END IF
!
    pit = abs2/geo%cvbb(icv, 3)
!
    RETURN
  END FUNCTION PIT

END SUBROUTINE B2TLHE_DV_DV

