SUBROUTINE B2TXCC_NODIFF_NODIFF(ncv, nfc, increase_transp_coefs, mode, &
& geo, mpg, fcvol, fcs, kap, con)
  USE B2MOD_TYPES
  USE B2US_GEO_DIFFV_DIFFV
  USE B2US_MAP_DIFFV_DIFFV
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
  INTEGER, INTENT(IN) :: ncv, nfc, mode
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  REAL(kind=r8) :: fcvol(nfc, 2), fcs(nfc), kap(ncv), con(nfc), &
& increase_transp_coefs
!     ------------------------------------------------------------------
!     B2TXCC computes normal conductances between neighbouring cells, 
!     given the conductivities.
!     ------------------------------------------------------------------
  INTEGER :: ifc, cvs(2)
  REAL(kind=r8) :: t0, t1
  INTRINSIC MAX, SQRT
  EXTERNAL XERTST
  REAL(r8) :: y1
  REAL(r8) :: max1
  REAL(r8) :: result1
!     ------------------------------------------------------------------
!   ..subprogram start-up calls
  CALL SUBINI('b2txcc')
!   ..test mode
  CALL XERTST(-1 .LE. mode .AND. mode .LE. 3, &
&       'b2txcc--faulty argument mode')
!
!   ..compute con
  IF (mode .EQ. -1) THEN
!    ..use harmonic averaging
    DO ifc=1,nfc
      t0 = fcvol(ifc, 1)*kap(mpg%fccv(ifc, 1))/geo%fchc(ifc, 1)**2
      t1 = fcvol(ifc, 2)*kap(mpg%fccv(ifc, 2))/geo%fchc(ifc, 2)**2
      IF (t0*t1 .GT. 0.0_R8) THEN
        con(ifc) = t0*t1/(t0+t1)/geo%fcqgam(ifc, 0)**2
      ELSE
        con(ifc) = 0.0_R8
      END IF
    END DO
  ELSE IF (mode .EQ. 0) THEN
!    ..use geometric averaging
    DO ifc=1,nfc
      y1 = fcvol(ifc, 1)*kap(mpg%fccv(ifc, 1))/geo%fchc(ifc, 1)*(fcvol(&
&       ifc, 2)*kap(mpg%fccv(ifc, 2))/geo%fchc(ifc, 2))
      IF (0.0_R8 .LT. y1) THEN
        max1 = y1
      ELSE
        max1 = 0.0_R8
      END IF
      result1 = SQRT(max1)
      con(ifc) = 1.0_R8/geo%fcqgam(ifc, 0)**2*result1/(geo%fchc(ifc, 1)+&
&       geo%fchc(ifc, 2))
      IF (increase_transp_coefs .GT. 1.0_R8) THEN
        cvs = mpg%fccv(ifc, 1:2)
        IF (mpg%cvalongboundary(cvs(1)) .AND. mpg%cvalongboundary(cvs(2)&
&           )) con(ifc) = con(ifc)*increase_transp_coefs
      END IF
    END DO
  ELSE IF (mode .EQ. 1) THEN
!    ..use arithmetic averaging (SOLPS5.0 formulation)
    DO ifc=1,nfc
      con(ifc) = 1.0_R8/geo%fcqgam(ifc, 0)**2*(fcvol(ifc, 1)*kap(mpg%&
&       fccv(ifc, 1))+fcvol(ifc, 2)*kap(mpg%fccv(ifc, 2)))/(geo%fchc(ifc&
&       , 1)+geo%fchc(ifc, 2))**2
      IF (increase_transp_coefs .GT. 1.0_R8) THEN
        cvs = mpg%fccv(ifc, 1:2)
        IF (mpg%cvalongboundary(cvs(1)) .AND. mpg%cvalongboundary(cvs(2)&
&           )) con(ifc) = con(ifc)*increase_transp_coefs
      END IF
    END DO
  ELSE IF (mode .EQ. 2) THEN
!    ..use arithmetic averaging (SOLPS4.0 formulation)
    DO ifc=1,nfc
      con(ifc) = fcs(ifc)/geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)*kap(mpg%&
&       fccv(ifc, 1))+geo%fchc(ifc, 2)*kap(mpg%fccv(ifc, 2)))/(geo%fchc(&
&       ifc, 1)+geo%fchc(ifc, 2))**2
      IF (increase_transp_coefs .GT. 1.0_R8) THEN
        cvs = mpg%fccv(ifc, 1:2)
        IF (mpg%cvalongboundary(cvs(1)) .AND. mpg%cvalongboundary(cvs(2)&
&           )) con(ifc) = con(ifc)*increase_transp_coefs
      END IF
    END DO
  ELSE IF (mode .EQ. 3) THEN
!    ..use arithmetic averaging (drift formulation)
    DO ifc=1,nfc
      con(ifc) = fcs(ifc)/(geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc&
&       (ifc, 2)))*(geo%fcvol(ifc, 1)*kap(mpg%fccv(ifc, 1))+geo%fcvol(&
&       ifc, 2)*kap(mpg%fccv(ifc, 2)))/(geo%fcvol(ifc, 1)+geo%fcvol(ifc&
&       , 2))
      IF (increase_transp_coefs .GT. 1.0_R8) THEN
        cvs = mpg%fccv(ifc, 1:2)
        IF (mpg%cvalongboundary(cvs(1)) .AND. mpg%cvalongboundary(cvs(2)&
&           )) con(ifc) = con(ifc)*increase_transp_coefs
      END IF
    END DO
  END IF
!   ..return
  CALL SUBEND()
  RETURN
!     ------------------------------------------------------------------
END SUBROUTINE B2TXCC_NODIFF_NODIFF

