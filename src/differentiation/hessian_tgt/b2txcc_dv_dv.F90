        IF (mpg%cvalongboundary(cvs(1)) .AND. mpg%cvalongboundary(cvs(2)&
&           )) THEN
          DO nd=1,nbdirs
            cond(nd, ifc) = increase_transp_coefs*cond(nd, ifc)
          END DO
          con(ifc) = con(ifc)*increase_transp_coefs
        END IF
      END IF
    END DO
  ELSE IF (mode .EQ. 1) THEN
!    ..use arithmetic averaging (SOLPS5.0 formulation)
    DO ifc=1,nfc
      temp1 = geo%fcqgam(ifc, 0)*geo%fcqgam(ifc, 0)*((geo%fchc(ifc, 1)+&
&       geo%fchc(ifc, 2))*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2)))
      DO nd=1,nbdirs
        cond(nd, ifc) = (fcvol(ifc, 1)*kapd(nd, mpg%fccv(ifc, 1))+fcvol(&
&         ifc, 2)*kapd(nd, mpg%fccv(ifc, 2)))/temp1
      END DO
      con(ifc) = (fcvol(ifc, 1)*kap(mpg%fccv(ifc, 1))+fcvol(ifc, 2)*kap(&
&       mpg%fccv(ifc, 2)))/temp1
      IF (increase_transp_coefs .GT. 1.0_R8) THEN
        cvs = mpg%fccv(ifc, 1:2)
        IF (mpg%cvalongboundary(cvs(1)) .AND. mpg%cvalongboundary(cvs(2)&
&           )) THEN
          DO nd=1,nbdirs
            cond(nd, ifc) = increase_transp_coefs*cond(nd, ifc)
          END DO
          con(ifc) = con(ifc)*increase_transp_coefs
        END IF
      END IF
    END DO
  ELSE IF (mode .EQ. 2) THEN
!    ..use arithmetic averaging (SOLPS4.0 formulation)
    DO ifc=1,nfc
      temp1 = geo%fcqgam(ifc, 0)*((geo%fchc(ifc, 1)+geo%fchc(ifc, 2))*(&
&       geo%fchc(ifc, 1)+geo%fchc(ifc, 2)))
      DO nd=1,nbdirs
        cond(nd, ifc) = fcs(ifc)*(geo%fchc(ifc, 1)*kapd(nd, mpg%fccv(ifc&
&         , 1))+geo%fchc(ifc, 2)*kapd(nd, mpg%fccv(ifc, 2)))/temp1
      END DO
      con(ifc) = fcs(ifc)*((geo%fchc(ifc, 1)*kap(mpg%fccv(ifc, 1))+geo%&
&       fchc(ifc, 2)*kap(mpg%fccv(ifc, 2)))/temp1)
      IF (increase_transp_coefs .GT. 1.0_R8) THEN
        cvs = mpg%fccv(ifc, 1:2)
        IF (mpg%cvalongboundary(cvs(1)) .AND. mpg%cvalongboundary(cvs(2)&
&           )) THEN
          DO nd=1,nbdirs
            cond(nd, ifc) = increase_transp_coefs*cond(nd, ifc)
          END DO
          con(ifc) = con(ifc)*increase_transp_coefs
        END IF
      END IF
    END DO
  ELSE IF (mode .EQ. 3) THEN
!    ..use arithmetic averaging (drift formulation)
    DO ifc=1,nfc
      temp1 = geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))*(&
&       geo%fcvol(ifc, 1)+geo%fcvol(ifc, 2))
      DO nd=1,nbdirs
        cond(nd, ifc) = fcs(ifc)*(geo%fcvol(ifc, 1)*kapd(nd, mpg%fccv(&
&         ifc, 1))+geo%fcvol(ifc, 2)*kapd(nd, mpg%fccv(ifc, 2)))/temp1
      END DO
      con(ifc) = fcs(ifc)*((geo%fcvol(ifc, 1)*kap(mpg%fccv(ifc, 1))+geo%&
&       fcvol(ifc, 2)*kap(mpg%fccv(ifc, 2)))/temp1)
      IF (increase_transp_coefs .GT. 1.0_R8) THEN
        cvs = mpg%fccv(ifc, 1:2)
        IF (mpg%cvalongboundary(cvs(1)) .AND. mpg%cvalongboundary(cvs(2)&
&           )) THEN
          DO nd=1,nbdirs
            cond(nd, ifc) = increase_transp_coefs*cond(nd, ifc)
          END DO
          con(ifc) = con(ifc)*increase_transp_coefs
        END IF
      END IF
    END DO
  END IF
!   ..return
  CALL SUBEND()
  RETURN
!     ------------------------------------------------------------------
END SUBROUTINE B2TXCC_DV_NODIFF

