SUBROUTINE B2TRFR_NODIFF_NODIFF(nnucl, switch, mum, rmu, z2n, c_hwa, &
& c_rwa, c_r_wb, c_r_wa, c_r_w)
  USE B2MOD_TYPES
  USE B2MOD_SUBSYS
  USE B2MOD_SWITCHES_DIFFV_DIFFV
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: nnucl
  TYPE(SWITCHES), INTENT(IN) :: switch
  REAL(kind=r8) :: z2n(0:nnucl-1), mum(0:nnucl-1, 0:nnucl-1), rmu(0:&
& nnucl-1, 0:nnucl-1)
  REAL(kind=r8) :: c_hwa(0:nnucl-1, 0:nnucl-1), c_rwa(0:nnucl-1, 0:nnucl&
& -1), c_r_wb(0:nnucl-1, 0:nnucl-1)
!   ..output arguments 
  REAL(kind=r8) :: c_r_w(0:nnucl-1, 0:nnucl-1), c_r_wa(0:nnucl-1, 0:&
& nnucl-1)
!   ..common blocks
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2TRFR computes the friction force transport coefficient.
!
!  3. description 
!
!     This routine computes:
!     - c_R_wA: the friction force transport coefficient
!               for the isonuclear sequence
!     - c_R_w: the total friction force transport coefficient 
!
!-----------------------------------------------------------------------
!.declarations
!   ..local variables
  INTEGER :: i, j, k
  REAL(kind=r8) :: temp, rel
  REAL(kind=r8) :: c_r_wa1, c_r_wa2, c_r_wa3
!
!-----------------------------------------------------------------------
!.computation
!
!   ..subprogram start-up calls
  CALL SUBINI('b2trfr')
!   ..main
!   ..the friction force transport coefficient
  rel = 3.0_R8/14.0_R8
  DO i=0,nnucl-1
    DO j=0,nnucl-1
      c_r_wa1 = 0
      c_r_wa2 = 0
      c_r_wa3 = 0
      DO k=0,nnucl-1
        IF (k .NE. i) THEN
          temp = rmu(i, k)/rmu(i, j)*(z2n(k)/z2n(j))
!   ..first part
          c_r_wa1 = c_r_wa1 - mum(i, k)*temp*(0.6_R8*c_hwa(j, i)-rel*mum&
&           (i, k)*c_rwa(j, i))
!   ..second part
          c_r_wa2 = c_r_wa2 + mum(k, i)*temp*(0.6_R8*c_hwa(j, k)-rel*mum&
&           (k, i)*c_rwa(j, k))
        END IF
!   ..third part
        c_r_wa3 = c_r_wa3 - mum(j, i)*(0.6_R8*c_hwa(k, j)-rel*mum(j, i)*&
&         c_rwa(k, j))
      END DO
!   ..finally
      c_r_wa(j, i) = 1 + c_r_wa1 + c_r_wa2 + c_r_wa3
    END DO
  END DO
!
!   ..apply charge state correction 
  c_r_w = c_r_wa
  DO i=0,nnucl-1
    IF (switch%zhcscorr .EQ. 1) THEN
      temp = 0
      DO k=0,nnucl-1
        temp = temp + rmu(i, k)/rmu(i, i)*(z2n(k)/z2n(i))*(c_r_wb(k, i)-&
&         c_r_wa(k, i))
      END DO
      c_r_w(i, i) = c_r_w(i, i) + temp
    ELSE
      c_r_w(i, i) = 1
    END IF
  END DO
  CALL SUBEND()
  RETURN
!
END SUBROUTINE B2TRFR_NODIFF_NODIFF

