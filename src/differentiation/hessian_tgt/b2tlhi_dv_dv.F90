  REAL(kind=r8) :: temp11
  INTEGER :: nbdirs0
!   ..initialisation
!     ------------------------------------------------------------------
!   ..subprogram start-up calls
  CALL SUBINI('b2tlhi')
!   ..test mp, cflmi
  CALL XERTST(0.0_R8 .LT. mp, 'faulty argument mp')
  CALL XERTST(0.0_R8 .LE. cflmi, 'faulty argument cflmi')
! Init local flux limit to constant value
!
! Enhanced flux limit for low-density regions
  cflmi_loc = cflmi
!
  IF (switch%b2tlhi_far_sol .EQ. 1) THEN
! Attempt enhanced flux limit for low-density regions
    DO nd=1,nbdirsmax
      nifd(nd, :) = 0.d0
    END DO
    nifdd(:, :, :) = 0.0_8
    nifd0(:, :) = 0.0_8
    CALL INTFACE_DV_DV(ncv, nfc, mpg%fccv, geo%fcvol, ni(:, 0), nid0(:, &
&                :, 0), nid(:, :, 0), nidd(:, :, :, 0), nif, nifd0, nifd&
&                , nifdd, nbdirs, nbdirs0)
    DO nd=1,nbdirsmax
      cflmi_locd(nd, :) = 0.d0
    END DO
    cflmi_locd0(:, :) = 0.0_8
    cflmi_locdd(:, :, :) = 0.0_8
    DO ifc=1,nfc
      IF (nif(ifc) .LE. switch%b2tlhi_ni_min) THEN
! Set at min value
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            cflmi_locdd(nd0, nd, ifc) = 0.0_8
          END DO
          cflmi_locd(nd, ifc) = 0.d0
        END DO
        DO nd0=1,nbdirs0
          cflmi_locd0(nd0, ifc) = 0.0_8
        END DO
        cflmi_loc(ifc) = switch%b2tlhi_ni_min
      ELSE IF (nif(ifc) .LT. switch%b2tlhi_ni_max) THEN
! Interpolate for smooth transition
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            cflmi_locdd(nd0, nd, ifc) = (cflmi-switch%b2tlhi_cflmi_min)*&
&             nifdd(nd0, nd, ifc)/(switch%b2tlhi_ni_max-switch%&
&             b2tlhi_ni_min)
          END DO
          cflmi_locd(nd, ifc) = (cflmi-switch%b2tlhi_cflmi_min)*nifd(nd&
&           , ifc)/(switch%b2tlhi_ni_max-switch%b2tlhi_ni_min)
        END DO
        DO nd0=1,nbdirs0
          cflmi_locd0(nd0, ifc) = (cflmi-switch%b2tlhi_cflmi_min)*nifd0(&
&           nd0, ifc)/(switch%b2tlhi_ni_max-switch%b2tlhi_ni_min)
        END DO
        cflmi_loc(ifc) = (cflmi*(nif(ifc)-switch%b2tlhi_ni_min)+switch%&
&         b2tlhi_cflmi_min*(switch%b2tlhi_ni_max-nif(ifc)))/(switch%&
&         b2tlhi_ni_max-switch%b2tlhi_ni_min)
      END IF
    END DO
  ELSE
    DO nd=1,nbdirsmax
      cflmi_locd(nd, :) = 0.d0
    END DO
    cflmi_locd0(:, :) = 0.0_8
    cflmi_locdd(:, :, :) = 0.0_8
  END IF
!
!   ..compute flux limit velocity
  IF (switch%b2tqca_model .EQ. 1) THEN
    arg1dd(:, :) = 0.0_8
    t0dd(:, :) = 0.0_8
    result1dd(:, :) = 0.0_8
! SOLPS5.0 treatments
    DO ifc=1,nfc
      IF (ti(mpg%fccv(ifc, 1)) .LT. ti(mpg%fccv(ifc, 2))) THEN
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            t0dd(nd0, nd) = tidd(nd0, nd, mpg%fccv(ifc, 2))
          END DO
          t0d(nd) = tid(nd, mpg%fccv(ifc, 2))
        END DO
        DO nd0=1,nbdirs0
          t0d0(nd0) = tid0(nd0, mpg%fccv(ifc, 2))
        END DO
        t0 = ti(mpg%fccv(ifc, 2))
      ELSE
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            t0dd(nd0, nd) = tidd(nd0, nd, mpg%fccv(ifc, 1))
          END DO
          t0d(nd) = tid(nd, mpg%fccv(ifc, 1))
        END DO
        DO nd0=1,nbdirs0
          t0d0(nd0) = tid0(nd0, mpg%fccv(ifc, 1))
        END DO
        t0 = ti(mpg%fccv(ifc, 1))
      END IF
      IF (geo%fcpbs(ifc) .GE. 0.) THEN
        abs0 = geo%fcpbs(ifc)
      ELSE
        abs0 = -geo%fcpbs(ifc)
      END IF
      arg1 = t0/mp
      temp3 = SQRT(arg1)
      DO nd0=1,nbdirs0
        arg1d0(nd0) = t0d0(nd0)/mp
        IF (arg1 .EQ. 0.0_8) THEN
          tempd(nd0) = 0.0_8
        ELSE
          tempd(nd0) = arg1d0(nd0)/(2.0*temp3)
        END IF
      END DO
      temp = temp3
      DO nd=1,nbdirs
!srv 11.01.13
        DO nd0=1,nbdirs0
          arg1dd(nd0, nd) = t0dd(nd0, nd)/mp
        END DO
        arg1d(nd) = t0d(nd)/mp
        IF (arg1 .EQ. 0.d0) THEN
          DO nd0=1,nbdirs0
            result1dd(nd0, nd) = 0.0_8
          END DO
          result1d(nd) = 0.d0
        ELSE
          temp3 = arg1d(nd)/(2.0*temp)
          DO nd0=1,nbdirs0
            result1dd(nd0, nd) = (arg1dd(nd0, nd)-temp3*2.0*tempd(nd0))/&
&             (2.0*temp)
          END DO
          result1d(nd) = temp3
        END IF
      END DO
      result1 = temp
      temp = ni_ext(mpg%fccv(ifc, 1), 0) + ni_ext(mpg%fccv(ifc, 2), 0) +&
&       ni(mpg%fccv(ifc, 1), 0) + ni(mpg%fccv(ifc, 2), 0)
      temp0 = nirm(mpg%fccv(ifc, 1)) + nirm(mpg%fccv(ifc, 2))
      temp3 = cflmi_loc(ifc)/temp
      DO nd0=1,nbdirs0
        result1d0(nd0) = tempd(nd0)
        tempd(nd0) = nid0(nd0, mpg%fccv(ifc, 1), 0) + nid0(nd0, mpg%fccv&
&         (ifc, 2), 0)
        temp0d(nd0) = nirmd0(nd0, mpg%fccv(ifc, 1)) + nirmd0(nd0, mpg%&
&         fccv(ifc, 2))
        temp1d(nd0) = result1*temp0*(cflmi_locd0(nd0, ifc)-temp3*tempd(&
&         nd0))/temp + temp3*(temp0*result1d0(nd0)+result1*temp0d(nd0))
      END DO
      temp1 = temp3*(result1*temp0)
      DO nd=1,nbdirs
        temp3 = result1*cflmi_locd(nd, ifc) + cflmi_loc(ifc)*result1d(nd&
&         )
        temp4 = nirmd(nd, mpg%fccv(ifc, 1)) + nirmd(nd, mpg%fccv(ifc, 2)&
&         )
        temp5 = nid(nd, mpg%fccv(ifc, 1), 0) + nid(nd, mpg%fccv(ifc, 2)&
&         , 0)
        temp6 = (temp0*temp3+cflmi_loc(ifc)*result1*temp4-temp1*temp5)/&
&         temp
        DO nd0=1,nbdirs0
          chvimxdd(nd0, nd, ifc) = abs0*(temp3*temp0d(nd0)+temp0*(&
&           cflmi_locd(nd, ifc)*result1d0(nd0)+result1*cflmi_locdd(nd0, &
&           nd, ifc)+result1d(nd)*cflmi_locd0(nd0, ifc)+cflmi_loc(ifc)*&
&           result1dd(nd0, nd))+temp4*(result1*cflmi_locd0(nd0, ifc)+&
&           cflmi_loc(ifc)*result1d0(nd0))+cflmi_loc(ifc)*result1*(&
&           nirmdd(nd0, nd, mpg%fccv(ifc, 1))+nirmdd(nd0, nd, mpg%fccv(&
&           ifc, 2)))-temp5*temp1d(nd0)-temp1*(nidd(nd0, nd, mpg%fccv(&
&           ifc, 1), 0)+nidd(nd0, nd, mpg%fccv(ifc, 2), 0))-temp6*tempd(&
&           nd0))/temp
        END DO
        chvimxd(nd, ifc) = abs0*temp6
      END DO
      DO nd0=1,nbdirs0
        chvimxd0(nd0, ifc) = abs0*temp1d(nd0)
      END DO
      chvimx(ifc) = abs0*temp1
    END DO
  ELSE IF (switch%b2tqca_model .EQ. 2) THEN
    arg1dd(:, :) = 0.0_8
    result2dd(:, :) = 0.0_8
    arg2dd(:, :) = 0.0_8
    max1dd(:, :) = 0.0_8
    t0dd(:, :) = 0.0_8
    result4dd(:, :) = 0.0_8
    t1dd(:, :) = 0.0_8
! SOLPS4.0 treatment
    DO ifc=1,nfc
      DO nd=1,nbdirs
        DO nd0=nd,nbdirs0
          t0dd(nd0, nd) = tidd(nd0, nd, mpg%fccv(ifc, 2))
          t1dd(nd0, nd) = tidd(nd0, nd, mpg%fccv(ifc, 1))
        END DO
        t0d(nd) = tid(nd, mpg%fccv(ifc, 2))
        t1d(nd) = tid(nd, mpg%fccv(ifc, 1))
      END DO
      DO nd0=1,nbdirs0
        t0d0(nd0) = tid0(nd0, mpg%fccv(ifc, 2))
        t1d0(nd0) = tid0(nd0, mpg%fccv(ifc, 1))
      END DO
      t0 = ti(mpg%fccv(ifc, 2))
      t1 = ti(mpg%fccv(ifc, 1))
      IF (geo%fcqalf(ifc, 0) .GE. 0.) THEN
        abs1 = geo%fcqalf(ifc, 0)
      ELSE
        abs1 = -geo%fcqalf(ifc, 0)
      END IF
      IF (t0 .LT. t1) THEN
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            max1dd(nd0, nd) = t1dd(nd0, nd)
          END DO
          max1d(nd) = t1d(nd)
        END DO
        DO nd0=1,nbdirs0
          max1d0(nd0) = t1d0(nd0)
        END DO
        max1 = t1
      ELSE
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            max1dd(nd0, nd) = t0dd(nd0, nd)
          END DO
          max1d(nd) = t0d(nd)
        END DO
        DO nd0=1,nbdirs0
          max1d0(nd0) = t0d0(nd0)
        END DO
        max1 = t0
      END IF
!wdk 10.03.16 Divide by ni*ti at cell faces, so chvimx contains the same information
!           as case b2tqca_model.eq.1
!           Removes need for different treatment later on in the code (b2tfhi), and
!           ensures consistency with b2trcl_conductive_limit .ne. 0
      result1 = PIT(mpg%fccv(ifc, 2))
      arg1 = t0/mp
      temp6 = SQRT(arg1)
      DO nd0=1,nbdirs0
        arg1d0(nd0) = t0d0(nd0)/mp
        IF (arg1 .EQ. 0.0_8) THEN
          temp1d(nd0) = 0.0_8
        ELSE
          temp1d(nd0) = arg1d0(nd0)/(2.0*temp6)
        END IF
      END DO
      temp1 = temp6
      DO nd=1,nbdirs
        DO nd0=nd,nbdirs0
          arg1dd(nd0, nd) = t0dd(nd0, nd)/mp
        END DO
        arg1d(nd) = t0d(nd)/mp
        IF (arg1 .EQ. 0.d0) THEN
          DO nd0=1,nbdirs0
            result2dd(nd0, nd) = 0.0_8
          END DO
          result2d(nd) = 0.d0
        ELSE
          temp6 = arg1d(nd)/(2.0*temp1)
          DO nd0=1,nbdirs0
            result2dd(nd0, nd) = (arg1dd(nd0, nd)-temp6*2.0*temp1d(nd0))&
&             /(2.0*temp1)
          END DO
          result2d(nd) = temp6
        END IF
        DO nd0=1,nbdirs0
          arg2dd(nd0, nd) = t1dd(nd0, nd)/mp
        END DO
        arg2d(nd) = t1d(nd)/mp
      END DO
      arg2 = t1/mp
      temp6 = SQRT(arg2)
      DO nd0=1,nbdirs0
        result2d0(nd0) = temp1d(nd0)
        arg2d0(nd0) = t1d0(nd0)/mp
        IF (arg2 .EQ. 0.0_8) THEN
          temp1d(nd0) = 0.0_8
        ELSE
          temp1d(nd0) = arg2d0(nd0)/(2.0*temp6)
        END IF
      END DO
      result2 = temp1
      result3 = PIT(mpg%fccv(ifc, 1))
      temp1 = temp6
      DO nd=1,nbdirs
        IF (arg2 .EQ. 0.d0) THEN
          DO nd0=1,nbdirs0
            result4dd(nd0, nd) = 0.0_8
          END DO
          result4d(nd) = 0.d0
        ELSE
          temp6 = arg2d(nd)/(2.0*temp1)
          DO nd0=1,nbdirs0
            result4dd(nd0, nd) = (arg2dd(nd0, nd)-temp6*2.0*temp1d(nd0))&
&             /(2.0*temp1)
          END DO
          result4d(nd) = temp6
        END IF
      END DO
      result4 = temp1
      temp0 = ni_ext(mpg%fccv(ifc, 1), 0) + ni_ext(mpg%fccv(ifc, 2), 0) &
&       + ni(mpg%fccv(ifc, 1), 0) + ni(mpg%fccv(ifc, 2), 0)
      temp6 = cflmi_loc(ifc)/(temp0*max1)
      DO nd0=1,nbdirs0
        result4d0(nd0) = temp1d(nd0)
        temp0d(nd0) = nid0(nd0, mpg%fccv(ifc, 1), 0) + nid0(nd0, mpg%&
&         fccv(ifc, 2), 0)
        tempd(nd0) = (cflmi_locd0(nd0, ifc)-temp6*(max1*temp0d(nd0)+&
&         temp0*max1d0(nd0)))/(temp0*max1)
        temp2d(nd0) = result1*(result2*t0*nirmd0(nd0, mpg%fccv(ifc, 2))+&
&         nirm(mpg%fccv(ifc, 2))*(t0*result2d0(nd0)+result2*t0d0(nd0))) &
&         + result3*(result4*t1*nirmd0(nd0, mpg%fccv(ifc, 1))+nirm(mpg%&
&         fccv(ifc, 1))*(t1*result4d0(nd0)+result4*t1d0(nd0)))
      END DO
      temp1 = geo%fcs(ifc)*abs1
      temp = temp6
      temp2 = result1*nirm(mpg%fccv(ifc, 2))*result2*t0 + result3*nirm(&
&       mpg%fccv(ifc, 1))*result4*t1
      DO nd=1,nbdirs
        temp6 = t1*result4d(nd) + result4*t1d(nd)
        temp5 = nirmd(nd, mpg%fccv(ifc, 1))
        temp4 = t0*result2d(nd) + result2*t0d(nd)
        temp3 = nirmd(nd, mpg%fccv(ifc, 2))
        temp7 = result1*(result2*t0*temp3+nirm(mpg%fccv(ifc, 2))*temp4) &
&         + result3*(result4*t1*temp5+nirm(mpg%fccv(ifc, 1))*temp6)
        temp8 = temp2/(temp0*max1)
        temp9 = nid(nd, mpg%fccv(ifc, 1), 0) + nid(nd, mpg%fccv(ifc, 2)&
&         , 0)
        temp10 = max1*temp9 + temp0*max1d(nd)
        temp11 = cflmi_locd(nd, ifc) - temp*temp10
        DO nd0=1,nbdirs0
          chvimxdd(nd0, nd, ifc) = temp1*(temp7*tempd(nd0)+temp*(result1&
&           *(temp3*(t0*result2d0(nd0)+result2*t0d0(nd0))+result2*t0*&
&           nirmdd(nd0, nd, mpg%fccv(ifc, 2))+temp4*nirmd0(nd0, mpg%fccv&
&           (ifc, 2))+nirm(mpg%fccv(ifc, 2))*(result2d(nd)*t0d0(nd0)+t0*&
&           result2dd(nd0, nd)+t0d(nd)*result2d0(nd0)+result2*t0dd(nd0, &
&           nd)))+result3*(temp5*(t1*result4d0(nd0)+result4*t1d0(nd0))+&
&           result4*t1*nirmdd(nd0, nd, mpg%fccv(ifc, 1))+temp6*nirmd0(&
&           nd0, mpg%fccv(ifc, 1))+nirm(mpg%fccv(ifc, 1))*(result4d(nd)*&
&           t1d0(nd0)+t1*result4dd(nd0, nd)+t1d(nd)*result4d0(nd0)+&
&           result4*t1dd(nd0, nd))))+temp8*(cflmi_locdd(nd0, nd, ifc)-&
&           temp10*tempd(nd0)-temp*(temp9*max1d0(nd0)+max1*(nidd(nd0, nd&
&           , mpg%fccv(ifc, 1), 0)+nidd(nd0, nd, mpg%fccv(ifc, 2), 0))+&
&           max1d(nd)*temp0d(nd0)+temp0*max1dd(nd0, nd)))+temp11*(temp2d&
&           (nd0)-temp8*(max1*temp0d(nd0)+temp0*max1d0(nd0)))/(temp0*&
&           max1))
        END DO
        chvimxd(nd, ifc) = temp1*(temp*temp7+temp11*temp8)
      END DO
      DO nd0=1,nbdirs0
        chvimxd0(nd0, ifc) = temp1*(temp*temp2d(nd0)+temp2*tempd(nd0))
      END DO
      chvimx(ifc) = temp1*(temp2*temp)
    END DO
  END IF
  CALL SUBEND()
  RETURN

CONTAINS
!     ------------------------------------------------------------------
!
  FUNCTION PIT(icv)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    REAL(kind=r8) :: pit
    INTEGER, INTENT(IN) :: icv
    INTRINSIC ABS
    REAL(kind=r8) :: abs2
    IF (geo%cvbb(icv, 0) .GE. 0.) THEN
      abs2 = geo%cvbb(icv, 0)
    ELSE
      abs2 = -geo%cvbb(icv, 0)
    END IF
!
    pit = abs2/geo%cvbb(icv, 3)
!
    RETURN
  END FUNCTION PIT

END SUBROUTINE B2TLHI_DV_DV

