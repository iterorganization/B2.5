    CALL B2XVSG(nfc, gm%fcbb(:, 0), 1, 'fcBb0', '.ge.')
    CALL B2XVSG(nfc, gm%fcbb(:, 1), 1, 'fcBb1', '.ge.')
    CALL B2XVSG(nfc, gm%fcbb(:, 2), 1, 'fcBb2', '.ge.')
    CALL B2XVSG(nfc, gm%fcbb(:, 3), 1, 'fcBb3', '.ge.')
