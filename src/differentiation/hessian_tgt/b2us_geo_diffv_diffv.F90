  SUBROUTINE CHECK_GEOMETRY(ncv, nfc, nvx, nfs, nft, gm)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ncv, nfc, nvx, nfs, nft
    TYPE(GEOMETRY), INTENT(IN) :: gm
