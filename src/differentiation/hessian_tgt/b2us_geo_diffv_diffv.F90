    CALL B2XVSG(nvx, gm%vxbb(:, 0), 1, 'vxBb0', '.ge.')
    CALL B2XVSG(nvx, gm%vxbb(:, 1), 1, 'vxBb1', '.ge.')
    CALL B2XVSG(nvx, gm%vxbb(:, 2), 1, 'vxBb2', '.ge.')
    CALL B2XVSG(nvx, gm%vxbb(:, 3), 1, 'vxBb3', '.ge.')
