  FUNCTION PITCH(g, icv)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: icv
    TYPE(GEOMETRY), INTENT(IN) :: g
    REAL(kind=r8) :: pitch
    INTRINSIC SQRT
    REAL(kind=r8) :: arg1
    REAL(kind=r8) :: result1
    arg1 = g%cvbb(icv, 0)**2 + g%cvbb(icv, 1)**2
    result1 = SQRT(arg1)
    pitch = result1/g%cvbb(icv, 3)
    RETURN
  END FUNCTION PITCH
