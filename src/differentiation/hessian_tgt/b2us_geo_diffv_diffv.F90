!   ..check sign of magnetic field components
    CALL B2XVSG(ncv, gm%cvbb(:, 0), 1, 'cvBb0', '.ge.')
    CALL B2XVSG(ncv, gm%cvbb(:, 1), 1, 'cvBb1', '.ge.')
    CALL B2XVSG(ncv, gm%cvbb(:, 2), 1, 'cvBb2', '.ge.')
    CALL B2XVSG(ncv, gm%cvbb(:, 3), 1, 'cvBb3', '.ge.')
