  USE B2MOD_AD_DIFFV_DIFFV, ONLY : my_out_folder
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv
  REAL(kind=r8) :: ne2(ncv), lnlam(ncv), te(ncv)
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(SWITCHES), INTENT(IN) :: switch
!   ..output arguments (unspecified on entry)
  REAL(kind=r8), INTENT(OUT) :: collisnumf(mpg%nfc), collisnumc(mpg%ncv)
!   ..common blocks
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  REAL(kind=r8) :: wrk1(mpg%nfc, 2), tauf(mpg%nfc), vte(mpg%nfc), &
& collisnum(mpg%nft), tau(ncv), t0, t1, ctaue, wrkc(mpg%ncv)
  INTEGER :: ift, k, ifc, icv
  INTRINSIC SQRT
  REAL(r8) :: arg1
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
  REAL(kind=r8) :: result10
  REAL(kind=r8), DIMENSION(ncv) :: arg10
!
!-----------------------------------------------------------------------
!.computation
!
  t1 = 4.0_R8*pi*eps0/qe/qe
  arg1 = 2.0_R8*pi
  result1 = SQRT(arg1)
  result2 = SQRT(ev)
  t0 = 3.0_R8/(4.0_R8*result1)*t1*(t1*ev*result2)
  IF (switch%b2tqce_model .EQ. 1 .OR. switch%b2tqce_model .EQ. 3) THEN
    result10 = SQRT(me)
    ctaue = t0*result10
  ELSE IF (switch%b2tqce_model .EQ. 2) THEN
! lower precision as per SOLPS4.0
    ctaue = 3.5e11_R8
  END IF
  DO icv=1,ncv
    t0 = te(icv)/ev
    result10 = SQRT(t0)
    tau(icv) = ctaue/lnlam(icv)*t0*result10/ne2(icv)
  END DO
!
  wrk1 = 1.0_R8
  CALL INTFACE(ncv, mpg%nfc, mpg%fccv, wrk1, tau, tauf)
  arg10 = te/me
  wrkc = SQRT(arg10)
  CALL INTFACE(ncv, mpg%nfc, mpg%fccv, wrk1, wrkc, vte)
  collisnum = 0.0_R8
  DO ift=1,mpg%nft
    DO k=mpg%ftfcp(ift, 1),mpg%ftfcp(ift, 1)+mpg%ftfcp(ift, 2)-1
      ifc = mpg%ftfc(k)
      collisnum(ift) = collisnum(ift) + 1/(tauf(ifc)*vte(ifc))*(geo%fchc&
&       (ifc, 1)+geo%fchc(ifc, 2))*geo%fcbb(ifc, 3)/geo%fcbb(ifc, 0)
    END DO
  END DO
!
  collisnumf = 0.0_R8
  DO ifc=1,mpg%nfc
    ift = mpg%cvft(mpg%fccv(ifc, 1))
    IF (ift .EQ. 0) ift = mpg%cvft(mpg%fccv(ifc, 2))
    collisnumf(ifc) = collisnum(ift)
  END DO
!
  collisnumc = 0.0_R8
  DO icv=1,mpg%ncv
    ift = mpg%cvft(icv)
    IF (ift .NE. 0) collisnumc(icv) = collisnum(ift)
  END DO
!
  IF (switch%iout_b2wdat .EQ. 4) THEN
    CALL MY_OUT_US(70, mpg%ncv, 1, tau, 'b2txnce_tau')
    CALL MY_OUT_US(70, mpg%nft, 1, collisnum, 'b2txnce_numcole')
    CALL MY_OUT_US(70, mpg%nfc, 1, collisnumf, 'b2txnce_numcolef'&
&                  )
    CALL MY_OUT_US(70, mpg%ncv, 1, collisnumc, 'b2txnce_numcolec'&
&                  )
  END IF
!
  RETURN
END SUBROUTINE B2TXNCE_NODIFF_NODIFF

