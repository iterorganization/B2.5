SUBROUTINE B2TFCC_NODIFF_NODIFF(ncv, nfc, nvx, ns, switch, geo, mpg, pl&
& , dv, co, rt)
  USE B2MOD_TYPES
  USE B2MOD_CONSTANTS
  USE B2MOD_B2CMPA_DIFFV_DIFFV
  USE B2MOD_SWITCHES_DIFFV_DIFFV
  USE B2US_GEO_DIFFV_DIFFV
  USE B2US_MAP_DIFFV_DIFFV
