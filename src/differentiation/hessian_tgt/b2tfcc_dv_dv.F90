      condd(nd0, nd, :, 1) = 0.0_8
    END DO
    flod(nd, :, 0) = -(dnw(:, 0)*cond(nd, :, 0)+con(:, 0)*dnwd(nd, :, 0)&
&     )
    cond(nd, :, 0) = nwf*cond(nd, :, 0) + con(:, 0)*nwfd(nd, :)
    flod(nd, :, 1) = 0.d0
    cond(nd, :, 1) = 0.d0
