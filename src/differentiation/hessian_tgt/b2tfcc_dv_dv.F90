            dvdd%conn(nd0, nd, :, :) = dvdd%conn(nd0, nd, :, :) + con0dd&
&             (nd0, nd, :, :)
          END DO
          dvd%flon(nd, :, :) = dvd%flon(nd, :, :) + flo0d(nd, :, :)
