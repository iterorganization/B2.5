        temp9 = tnf*nwfd(nd, :) + nwf*tnfd(nd, :)
        DO nd0=1,nbdirs0
          condd(nd0, nd, :, 0) = switch%xfac*2.5_R8*(cod%cdpa(nd, :, 0, &
