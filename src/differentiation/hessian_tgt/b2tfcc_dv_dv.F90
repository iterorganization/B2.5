  IMPLICIT NONE
!
!-----------------------------------------------------------------------
!.end b2tfcc
!
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx, ns
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(B2PLASMA), INTENT(IN) :: pl
  TYPE(B2COEFF), INTENT(IN) :: co
  TYPE(B2RATES), INTENT(IN) :: rt
!   ..input/output arguments
  TYPE(B2DERIVATIVES), INTENT(INOUT) :: dv
!   ..common blocks
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: is, ifc
  REAL(kind=r8) :: nw(ncv), nwf(nfc), dnw(nfc, 0:1), tef(nfc), tif(nfc)&
& , tnf(nfc), wght(nfc, 2), wrkf(nfc), wrkvx(nvx), flo(nfc, 0:1), con(&
& nfc, 0:1), flo0(nfc, 0:1), con0(nfc, 0:1)
!   ..procedures
  EXTERNAL XERTST
  EXTERNAL B2XVSG, DIFF_NODIFF_NODIFF, INTFACE
  INTRINSIC NINT
!   ..initialisation
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2tfcc')
!   ..test nCv, nFc, ns
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!   ..extensive tests on first few calls
  IF (ncall_b2tfcc .LT. 3) THEN
!    ..test sign of cdpa
    DO is=0,ns-1
      wrkf(:) = co%cdpa(:, 0, is)*geo%fcqalf(:, 0)
      CALL B2XVSG(nfc, wrkf, 1, 'cdpa0', '.ge.')
      wrkf(:) = co%cdpa(:, 1, is)*geo%fcqalf(:, 1)
      CALL B2XVSG(nfc, wrkf, 1, 'cdpa1', '.ge.')
    END DO
  END IF
!
!   ..interpolate te and ti to cell faces
  wght = 1.0_R8
  CALL INTFACE(ncv, nfc, mpg%fccv, wght, pl%te, tef)
  CALL INTFACE(ncv, nfc, mpg%fccv, wght, pl%ti, tif)
  CALL INTFACE(ncv, nfc, mpg%fccv, wght, pl%tn, tnf)
!
! ..correct coefficients
!
  DO is=0,ns-1
!
    nw = pl%na(:, is)*(rt%rza(:, is)+1.0_R8)
!
!    ..pre-compute rza interpolated to faces, inverse volume weighted
    CALL INTFACE(ncv, nfc, mpg%fccv, geo%fcvol, rt%rza(:, is), wrkf)
!
!    ..pre-compute radial differences nw
    CALL DIFF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, nw, wrkvx, dnw)
!
!    ..interpolate nw to cell faces
    CALL INTFACE(ncv, nfc, mpg%fccv, wght, nw, nwf)
!
!   ..correct floe, cone
!
!    ..compute poloidal/radial coefficients for pressure diffusion term
    flo(:, 0) = -(switch%xfac*2.5_R8*co%cdpa(:, 0, is)*dnw(:, 0)*wrkf*&
&     tef)
    flo(:, 1) = -(switch%xfac*2.5_R8*co%cdpa(:, 1, is)*dnw(:, 1)*wrkf*&
&     tef)
    con(:, 0) = switch%xfac*2.5_R8*co%cdpa(:, 0, is)*nwf*wrkf*tef
    con(:, 1) = switch%xfac*2.5_R8*co%cdpa(:, 1, is)*nwf*wrkf*tef
!
!    ..compute coefficients for stencil
    CALL CALCCOEF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, flo, con, flo0, &
&                         con0)
!
!    ..add to previous contributions
    dv%floe = dv%floe + flo0
    dv%cone(:, :, 0) = dv%cone(:, :, 0) + con0
!
!   ..correct floi, coni, flon, conn
!
!    ..compute poloidal/radial coefficients for pressure diffusion term
    IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
      flo(:, 0) = -(switch%xfac*2.5_R8*co%cdpa(:, 0, is)*dnw(:, 0)*tif)
      flo(:, 1) = -(switch%xfac*2.5_R8*co%cdpa(:, 1, is)*dnw(:, 1)*tif)
      con(:, 0) = switch%xfac*2.5_R8*co%cdpa(:, 0, is)*nwf*tif
      con(:, 1) = switch%xfac*2.5_R8*co%cdpa(:, 1, is)*nwf*tif
    ELSE
!! in case of default energy equation Tn = Ti
      flo(:, 0) = -(switch%xfac*2.5_R8*co%cdpa(:, 0, is)*dnw(:, 0)*tnf)
      flo(:, 1) = -(switch%xfac*2.5_R8*co%cdpa(:, 1, is)*dnw(:, 1)*tnf)
      con(:, 0) = switch%xfac*2.5_R8*co%cdpa(:, 0, is)*nwf*tnf
      con(:, 1) = switch%xfac*2.5_R8*co%cdpa(:, 1, is)*nwf*tnf
    END IF
!
!    ..compute coefficients for stencil
    CALL CALCCOEF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, flo, con, flo0, &
&                         con0)
!
!    ..add to previous contributions
! the logic statements could be simplified but for now this is not done to improve readability
    IF (switch%tn_style .EQ. 0) THEN
!! default combined ion-neutral energy equation, include both ions and neutrals in "ion" terms.
      dv%floi = dv%floi + flo0
      dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
    ELSE IF (switch%tn_style .EQ. 1) THEN
!! pure ion energy equation, only add ion contributions
      IF (.NOT.is_neutral(is)) THEN
        dv%floi = dv%floi + flo0
        dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
      END IF
    ELSE IF (switch%tn_style .EQ. 2) THEN
!! separate ion and neutral energy equation, split contributions
      IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
        dv%floi = dv%floi + flo0
        dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
      ELSE
        dv%flon = dv%flon + flo0
        dv%conn = dv%conn + con0
      END IF
    END IF
  END DO
!
!! end loop over species
!
!  ..add contributions from Rhie-Chow term to floe, cone
!
!   ..pre-compute radial differences
  CALL DIFF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, dv%ne, wrkvx, dnw)
!
!   ..interpolate nwe and nwi to cell faces
  CALL INTFACE(ncv, nfc, mpg%fccv, wght, dv%ne, nwf)
!
  DO ifc=1,nfc
    con(ifc, 0) = switch%b2npht_pcm1*1.25_R8*geo%fcpbs(ifc)*(dv%pccm(mpg&
&     %fccv(ifc, 1), 1)*dv%ne(mpg%fccv(ifc, 1))*pl%te(mpg%fccv(ifc, 1))+&
&     dv%pccm(mpg%fccv(ifc, 2), 1)*dv%ne(mpg%fccv(ifc, 2))*pl%te(mpg%&
&     fccv(ifc, 2)))/(geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc&
&     , 2)))
  END DO
!
!   ..compute poloidal/radial coefficients for pressure diffusion term
  flo(:, 0) = -(con(:, 0)*dnw(:, 0))
  con(:, 0) = con(:, 0)*nwf
!
  flo(:, 1) = 0.0_R8
  con(:, 1) = 0.0_R8
!
!   ..compute coefficients for stencil
  CALL CALCCOEF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, flo, con, flo0, &
&                       con0)
!
!   ..add to previous contributions
  dv%floe = dv%floe + flo0
  dv%cone(:, :, 0) = dv%cone(:, :, 0) + con0
!
!   ..add contributions from Rhie-Chow term to floi, coni, flon, conn
!
  DO is=0,ns-1
!
!    ..pre-compute radial differences
    CALL DIFF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, pl%na(1, is), &
&                     wrkvx, dnw)
!
!    ..interpolate nwe to cell faces
    CALL INTFACE(ncv, nfc, mpg%fccv, wght, nw, nwf)
!
    IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
      DO ifc=1,nfc
        con(ifc, 0) = switch%b2npht_pcm1*1.25_R8*geo%fcpbs(ifc)*(dv%pccm&
&         (mpg%fccv(ifc, 1), 1)*pl%na(mpg%fccv(ifc, 1), is)*pl%ti(mpg%&
&         fccv(ifc, 1))+dv%pccm(mpg%fccv(ifc, 2), 1)*pl%na(mpg%fccv(ifc&
&         , 2), is)*pl%ti(mpg%fccv(ifc, 2)))/(geo%fcqgam(ifc, 0)*(geo%&
&         fchc(ifc, 1)+geo%fchc(ifc, 2)))
      END DO
    ELSE
      DO ifc=1,nfc
        con(ifc, 0) = switch%b2npht_pcm1*1.25_R8*geo%fcpbs(ifc)*(dv%pccm&
&         (mpg%fccv(ifc, 1), 1)*pl%na(mpg%fccv(ifc, 1), is)*pl%tn(mpg%&
&         fccv(ifc, 1))+dv%pccm(mpg%fccv(ifc, 2), 1)*pl%na(mpg%fccv(ifc&
&         , 2), is)*pl%tn(mpg%fccv(ifc, 2)))/(geo%fcqgam(ifc, 0)*(geo%&
&         fchc(ifc, 1)+geo%fchc(ifc, 2)))
      END DO
    END IF
!
!    ..compute poloidal/radial coefficients for pressure diffusion term
    flo(:, 0) = -(con(:, 0)*dnw(:, 0))
    con(:, 0) = con(:, 0)*nwf
!
    flo(:, 1) = 0.0_R8
    con(:, 1) = 0.0_R8
!
!    ..compute coefficients for stencil
    CALL CALCCOEF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, flo, con, flo0, &
&                         con0)
!
!    ..add to previous contributions
! the logic statements could be simplified but for now this is not done to improve readability
    IF (switch%tn_style .EQ. 0) THEN
!! default combined ion-neutral energy equation, include both ions and neutrals in "ion" terms.
      dv%floi = dv%floi + flo0
      dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
    ELSE IF (switch%tn_style .EQ. 1) THEN
!! pure ion energy equation, only add ion contributions
      IF (.NOT.is_neutral(is)) THEN
        dv%floi = dv%floi + flo0
        dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
      END IF
    ELSE IF (switch%tn_style .EQ. 2) THEN
!! separate ion and neutral energy equation, split contributions
      IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
        dv%floi = dv%floi + flo0
        dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
      ELSE
        dv%flon = dv%flon + flo0
        dv%conn = dv%conn + con0
      END IF
    END IF
  END DO
!
!! end loop over species
!
  IF (switch%b2npht_iout .NE. 0) THEN
    CALL MY_OUT_US(70, nfc, 1, dv%floe(1, 0), 'b2tfcc_floex')
    CALL MY_OUT_US(70, nfc, 1, dv%floe(1, 1), 'b2tfcc_floey')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 0, 0), 'b2tfcc_conex')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 1, 0), 'b2tfcc_coney')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 0, 1), 'b2tfcc_conex1')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 1, 1), 'b2tfcc_coney1')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 0, 2), 'b2tfcc_conex2')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 1, 2), 'b2tfcc_coney2')
  END IF
!
! ..return
  ncall_b2tfcc = ncall_b2tfcc + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2TFCC_NODIFF_NODIFF

