        dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
      ELSE
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            dvdd%flon(nd0, nd, :, :) = dvdd%flon(nd0, nd, :, :) + flo0dd&
