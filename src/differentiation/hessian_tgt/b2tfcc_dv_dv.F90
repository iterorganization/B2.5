        DO nd0=1,nbdirs0
          condd(nd0, nd, :, 1) = switch%xfac*2.5_R8*(cod%cdpa(nd, :, 1, &
&           is)*(tnf*nwfd0(nd0, :)+nwf*tnfd0(nd0, :))+nwf*tnf*codd%cdpa(&
