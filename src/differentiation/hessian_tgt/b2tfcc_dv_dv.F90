  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!   ..extensive tests on first few calls
  IF (ncall_b2tfcc .LT. 3) THEN
    DO nd=1,nbdirsmax
      wrkfd(nd, :) = 0.d0
    END DO
!    ..test sign of cdpa
    DO is=0,ns-1
      DO nd=1,nbdirs
        wrkfd(nd, :) = 0.d0
        wrkfd(nd, :) = geo%fcqalf(:, 1)*cod%cdpa(nd, :, 1, is)
      END DO
      wrkf(:) = co%cdpa(:, 0, is)*geo%fcqalf(:, 0)
      CALL B2XVSG(nfc, wrkf, 1, 'cdpa0', '.ge.')
      wrkf(:) = co%cdpa(:, 1, is)*geo%fcqalf(:, 1)
      CALL B2XVSG(nfc, wrkf, 1, 'cdpa1', '.ge.')
    END DO
  ELSE
    DO nd=1,nbdirsmax
      wrkfd(nd, :) = 0.d0
    END DO
  END IF
!
!   ..interpolate te and ti to cell faces
  wght = 1.0_R8
  DO nd=1,nbdirsmax
    tefd(nd, :) = 0.d0
  END DO
  CALL INTFACE_DV(ncv, nfc, mpg%fccv, wght, pl%te, pld%te, tef, tefd, &
&           nbdirs)
  DO nd=1,nbdirsmax
    tifd(nd, :) = 0.d0
  END DO
  CALL INTFACE_DV(ncv, nfc, mpg%fccv, wght, pl%ti, pld%ti, tif, tifd, &
&           nbdirs)
  DO nd=1,nbdirsmax
    tnfd(nd, :) = 0.d0
  END DO
  CALL INTFACE_DV(ncv, nfc, mpg%fccv, wght, pl%tn, pld%tn, tnf, tnfd, &
&           nbdirs)
  DO nd=1,nbdirsmax
    nwfd(nd, :) = 0.d0
  END DO
  DO nd=1,nbdirsmax
    dnwd(nd, :, :) = 0.d0
  END DO
  DO nd=1,nbdirsmax
    nwd(nd, :) = 0.d0
  END DO
  DO nd=1,nbdirsmax
    con0d(nd, :, :) = 0.d0
  END DO
  DO nd=1,nbdirsmax
    flo0d(nd, :, :) = 0.d0
  END DO
  DO nd=1,nbdirsmax
    wrkvxd(nd, :) = 0.d0
  END DO
  DO nd=1,nbdirsmax
    flod(nd, :, :) = 0.d0
  END DO
  DO nd=1,nbdirsmax
    cond(nd, :, :) = 0.d0
  END DO
!
! ..correct coefficients
!
  DO is=0,ns-1
!
    DO nd=1,nbdirs
      nwd(nd, :) = (rt%rza(:, is)+1.0_R8)*pld%na(nd, :, is) + pl%na(:, &
&       is)*rtd%rza(nd, :, is)
    END DO
    nw = pl%na(:, is)*(rt%rza(:, is)+1.0_R8)
!
!    ..pre-compute rza interpolated to faces, inverse volume weighted
    CALL INTFACE_DV(ncv, nfc, mpg%fccv, geo%fcvol, rt%rza(:, is), rtd%&
&             rza(:, :, is), wrkf, wrkfd, nbdirs)
!
!    ..pre-compute radial differences nw
    CALL DIFF_DV_NODIFF(ncv, nfc, nvx, 0, geo, geod, mpg, mpgd, nw, nwd&
&                 , wrkvx, wrkvxd, dnw, dnwd, nbdirs)
!
!    ..interpolate nw to cell faces
    CALL INTFACE_DV(ncv, nfc, mpg%fccv, wght, nw, nwd, nwf, nwfd, nbdirs&
&            )
!
!   ..correct floe, cone
!
!    ..compute poloidal/radial coefficients for pressure diffusion term
    temp = co%cdpa(:, 0, is)*dnw(:, 0)
    DO nd=1,nbdirs
      flod(nd, :, 0) = -(switch%xfac*2.5_R8*(wrkf*tef*(dnw(:, 0)*cod%&
&       cdpa(nd, :, 0, is)+co%cdpa(:, 0, is)*dnwd(nd, :, 0))+temp*(tef*&
&       wrkfd(nd, :)+wrkf*tefd(nd, :))))
      cond(nd, :, 0) = switch%xfac*2.5_R8*(nwf*wrkf*(tef*cod%cdpa(nd, :&
&       , 0, is)+co%cdpa(:, 0, is)*tefd(nd, :))+co%cdpa(:, 0, is)*tef*(&
&       wrkf*nwfd(nd, :)+nwf*wrkfd(nd, :)))
      cond(nd, :, 1) = switch%xfac*2.5_R8*(nwf*wrkf*(tef*cod%cdpa(nd, :&
&       , 1, is)+co%cdpa(:, 1, is)*tefd(nd, :))+co%cdpa(:, 1, is)*tef*(&
&       wrkf*nwfd(nd, :)+nwf*wrkfd(nd, :)))
    END DO
    flo(:, 0) = -(switch%xfac*2.5_R8*(temp*(wrkf*tef)))
    temp = co%cdpa(:, 1, is)*dnw(:, 1)
    DO nd=1,nbdirs
      flod(nd, :, 1) = -(switch%xfac*2.5_R8*(wrkf*tef*(dnw(:, 1)*cod%&
&       cdpa(nd, :, 1, is)+co%cdpa(:, 1, is)*dnwd(nd, :, 1))+temp*(tef*&
&       wrkfd(nd, :)+wrkf*tefd(nd, :))))
    END DO
    flo(:, 1) = -(switch%xfac*2.5_R8*(temp*(wrkf*tef)))
    con(:, 0) = switch%xfac*2.5_R8*co%cdpa(:, 0, is)*nwf*wrkf*tef
    con(:, 1) = switch%xfac*2.5_R8*co%cdpa(:, 1, is)*nwf*wrkf*tef
!
!    ..compute coefficients for stencil
    CALL CALCCOEF_DV_NODIFF(ncv, nfc, nvx, 0, geo, flo, flod, con, cond&
&                     , flo0, flo0d, con0, con0d, nbdirs)
    DO nd=1,nbdirs
!
!    ..add to previous contributions
      dvd%floe(nd, :, :) = dvd%floe(nd, :, :) + flo0d(nd, :, :)
      dvd%cone(nd, :, :, 0) = dvd%cone(nd, :, :, 0) + con0d(nd, :, :)
    END DO
    dv%floe = dv%floe + flo0
    dv%cone(:, :, 0) = dv%cone(:, :, 0) + con0
!
!   ..correct floi, coni, flon, conn
!
!    ..compute poloidal/radial coefficients for pressure diffusion term
    IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
      DO nd=1,nbdirs
        flod(nd, :, 0) = -(switch%xfac*2.5_R8*(dnw(:, 0)*tif*cod%cdpa(nd&
&         , :, 0, is)+co%cdpa(:, 0, is)*(tif*dnwd(nd, :, 0)+dnw(:, 0)*&
&         tifd(nd, :))))
        flod(nd, :, 1) = -(switch%xfac*2.5_R8*(dnw(:, 1)*tif*cod%cdpa(nd&
&         , :, 1, is)+co%cdpa(:, 1, is)*(tif*dnwd(nd, :, 1)+dnw(:, 1)*&
&         tifd(nd, :))))
        cond(nd, :, 0) = switch%xfac*2.5_R8*(nwf*tif*cod%cdpa(nd, :, 0, &
&         is)+co%cdpa(:, 0, is)*(tif*nwfd(nd, :)+nwf*tifd(nd, :)))
        cond(nd, :, 1) = switch%xfac*2.5_R8*(nwf*tif*cod%cdpa(nd, :, 1, &
&         is)+co%cdpa(:, 1, is)*(tif*nwfd(nd, :)+nwf*tifd(nd, :)))
      END DO
      flo(:, 0) = -(switch%xfac*2.5_R8*co%cdpa(:, 0, is)*dnw(:, 0)*tif)
      flo(:, 1) = -(switch%xfac*2.5_R8*co%cdpa(:, 1, is)*dnw(:, 1)*tif)
      con(:, 0) = switch%xfac*2.5_R8*co%cdpa(:, 0, is)*nwf*tif
      con(:, 1) = switch%xfac*2.5_R8*co%cdpa(:, 1, is)*nwf*tif
    ELSE
      DO nd=1,nbdirs
!! in case of default energy equation Tn = Ti
        flod(nd, :, 0) = -(switch%xfac*2.5_R8*(dnw(:, 0)*tnf*cod%cdpa(nd&
&         , :, 0, is)+co%cdpa(:, 0, is)*(tnf*dnwd(nd, :, 0)+dnw(:, 0)*&
&         tnfd(nd, :))))
        flod(nd, :, 1) = -(switch%xfac*2.5_R8*(dnw(:, 1)*tnf*cod%cdpa(nd&
&         , :, 1, is)+co%cdpa(:, 1, is)*(tnf*dnwd(nd, :, 1)+dnw(:, 1)*&
&         tnfd(nd, :))))
        cond(nd, :, 0) = switch%xfac*2.5_R8*(nwf*tnf*cod%cdpa(nd, :, 0, &
&         is)+co%cdpa(:, 0, is)*(tnf*nwfd(nd, :)+nwf*tnfd(nd, :)))
        cond(nd, :, 1) = switch%xfac*2.5_R8*(nwf*tnf*cod%cdpa(nd, :, 1, &
&         is)+co%cdpa(:, 1, is)*(tnf*nwfd(nd, :)+nwf*tnfd(nd, :)))
      END DO
      flo(:, 0) = -(switch%xfac*2.5_R8*co%cdpa(:, 0, is)*dnw(:, 0)*tnf)
      flo(:, 1) = -(switch%xfac*2.5_R8*co%cdpa(:, 1, is)*dnw(:, 1)*tnf)
      con(:, 0) = switch%xfac*2.5_R8*co%cdpa(:, 0, is)*nwf*tnf
      con(:, 1) = switch%xfac*2.5_R8*co%cdpa(:, 1, is)*nwf*tnf
    END IF
!
!    ..compute coefficients for stencil
    CALL CALCCOEF_DV_NODIFF(ncv, nfc, nvx, 0, geo, flo, flod, con, cond&
&                     , flo0, flo0d, con0, con0d, nbdirs)
!
!    ..add to previous contributions
! the logic statements could be simplified but for now this is not done to improve readability
    IF (switch%tn_style .EQ. 0) THEN
      DO nd=1,nbdirs
!! default combined ion-neutral energy equation, include both ions and neutrals in "ion" terms.
        dvd%floi(nd, :, :) = dvd%floi(nd, :, :) + flo0d(nd, :, :)
        dvd%coni(nd, :, :, 0) = dvd%coni(nd, :, :, 0) + con0d(nd, :, :)
      END DO
      dv%floi = dv%floi + flo0
      dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
    ELSE IF (switch%tn_style .EQ. 1) THEN
!! pure ion energy equation, only add ion contributions
      IF (.NOT.is_neutral(is)) THEN
        DO nd=1,nbdirs
          dvd%floi(nd, :, :) = dvd%floi(nd, :, :) + flo0d(nd, :, :)
          dvd%coni(nd, :, :, 0) = dvd%coni(nd, :, :, 0) + con0d(nd, :, :&
&           )
        END DO
        dv%floi = dv%floi + flo0
        dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
      END IF
    ELSE IF (switch%tn_style .EQ. 2) THEN
!! separate ion and neutral energy equation, split contributions
      IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
        DO nd=1,nbdirs
          dvd%floi(nd, :, :) = dvd%floi(nd, :, :) + flo0d(nd, :, :)
          dvd%coni(nd, :, :, 0) = dvd%coni(nd, :, :, 0) + con0d(nd, :, :&
&           )
        END DO
        dv%floi = dv%floi + flo0
        dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
      ELSE
        DO nd=1,nbdirs
          dvd%flon(nd, :, :) = dvd%flon(nd, :, :) + flo0d(nd, :, :)
          dvd%conn(nd, :, :) = dvd%conn(nd, :, :) + con0d(nd, :, :)
        END DO
        dv%flon = dv%flon + flo0
        dv%conn = dv%conn + con0
      END IF
    END IF
  END DO
!
!! end loop over species
!
!  ..add contributions from Rhie-Chow term to floe, cone
!
!   ..pre-compute radial differences
  CALL DIFF_DV_NODIFF(ncv, nfc, nvx, 0, geo, geod, mpg, mpgd, dv%ne, dvd&
&               %ne, wrkvx, wrkvxd, dnw, dnwd, nbdirs)
!
!   ..interpolate nwe and nwi to cell faces
  CALL INTFACE_DV(ncv, nfc, mpg%fccv, wght, dv%ne, dvd%ne, nwf, nwfd, &
&           nbdirs)
!
  DO ifc=1,nfc
    temp0 = 1.25_R8*switch%b2npht_pcm1*geo%fcpbs(ifc)
    temp1 = geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
    temp2 = dv%ne(mpg%fccv(ifc, 1))*pl%te(mpg%fccv(ifc, 1))
    temp3 = dv%pccm(mpg%fccv(ifc, 1), 1)
    temp4 = dv%ne(mpg%fccv(ifc, 2))*pl%te(mpg%fccv(ifc, 2))
    temp5 = dv%pccm(mpg%fccv(ifc, 2), 1)
    DO nd=1,nbdirs
      cond(nd, ifc, 0) = temp0*(temp2*dvd%pccm(nd, mpg%fccv(ifc, 1), 1)+&
&       temp3*(pl%te(mpg%fccv(ifc, 1))*dvd%ne(nd, mpg%fccv(ifc, 1))+dv%&
&       ne(mpg%fccv(ifc, 1))*pld%te(nd, mpg%fccv(ifc, 1)))+temp4*dvd%&
&       pccm(nd, mpg%fccv(ifc, 2), 1)+temp5*(pl%te(mpg%fccv(ifc, 2))*dvd&
&       %ne(nd, mpg%fccv(ifc, 2))+dv%ne(mpg%fccv(ifc, 2))*pld%te(nd, mpg&
&       %fccv(ifc, 2))))/temp1
    END DO
    con(ifc, 0) = temp0*((temp3*temp2+temp5*temp4)/temp1)
  END DO
  DO nd=1,nbdirs
!
!   ..compute poloidal/radial coefficients for pressure diffusion term
    flod(nd, :, 0) = -(dnw(:, 0)*cond(nd, :, 0)+con(:, 0)*dnwd(nd, :, 0)&
&     )
    cond(nd, :, 0) = nwf*cond(nd, :, 0) + con(:, 0)*nwfd(nd, :)
!
    flod(nd, :, 1) = 0.d0
    cond(nd, :, 1) = 0.d0
  END DO
  flo(:, 0) = -(con(:, 0)*dnw(:, 0))
  con(:, 0) = con(:, 0)*nwf
  flo(:, 1) = 0.0_R8
  con(:, 1) = 0.0_R8
!
!   ..compute coefficients for stencil
  CALL CALCCOEF_DV_NODIFF(ncv, nfc, nvx, 0, geo, flo, flod, con, cond, &
&                   flo0, flo0d, con0, con0d, nbdirs)
  DO nd=1,nbdirs
!
!   ..add to previous contributions
    dvd%floe(nd, :, :) = dvd%floe(nd, :, :) + flo0d(nd, :, :)
    dvd%cone(nd, :, :, 0) = dvd%cone(nd, :, :, 0) + con0d(nd, :, :)
  END DO
  dv%floe = dv%floe + flo0
  dv%cone(:, :, 0) = dv%cone(:, :, 0) + con0
!
!   ..add contributions from Rhie-Chow term to floi, coni, flon, conn
!
  DO is=0,ns-1
!
!    ..pre-compute radial differences
    CALL DIFF_DV_NODIFF(ncv, nfc, nvx, 0, geo, geod, mpg, mpgd, pl%na(1&
&                 , is), pld%na(:, 1, is), wrkvx, wrkvxd, dnw, dnwd, &
&                 nbdirs)
!
!    ..interpolate nwe to cell faces
    CALL INTFACE_DV(ncv, nfc, mpg%fccv, wght, nw, nwd, nwf, nwfd, nbdirs&
&            )
!
    IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
      DO ifc=1,nfc
        temp1 = 1.25_R8*switch%b2npht_pcm1*geo%fcpbs(ifc)
        temp0 = geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        temp5 = pl%na(mpg%fccv(ifc, 1), is)
        temp4 = dv%pccm(mpg%fccv(ifc, 1), 1)
        temp3 = temp4*pl%ti(mpg%fccv(ifc, 1))
        temp2 = pl%na(mpg%fccv(ifc, 2), is)
        temp6 = dv%pccm(mpg%fccv(ifc, 2), 1)
        temp7 = temp6*pl%ti(mpg%fccv(ifc, 2))
        DO nd=1,nbdirs
          cond(nd, ifc, 0) = temp1*(temp5*(pl%ti(mpg%fccv(ifc, 1))*dvd%&
&           pccm(nd, mpg%fccv(ifc, 1), 1)+temp4*pld%ti(nd, mpg%fccv(ifc&
&           , 1)))+temp3*pld%na(nd, mpg%fccv(ifc, 1), is)+temp2*(pl%ti(&
&           mpg%fccv(ifc, 2))*dvd%pccm(nd, mpg%fccv(ifc, 2), 1)+temp6*&
&           pld%ti(nd, mpg%fccv(ifc, 2)))+temp7*pld%na(nd, mpg%fccv(ifc&
&           , 2), is))/temp0
        END DO
        con(ifc, 0) = temp1*((temp3*temp5+temp7*temp2)/temp0)
      END DO
    ELSE
      DO ifc=1,nfc
        temp1 = 1.25_R8*switch%b2npht_pcm1*geo%fcpbs(ifc)
        temp0 = geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        temp7 = pl%na(mpg%fccv(ifc, 1), is)
        temp6 = dv%pccm(mpg%fccv(ifc, 1), 1)
        temp5 = temp6*pl%tn(mpg%fccv(ifc, 1))
        temp4 = pl%na(mpg%fccv(ifc, 2), is)
        temp3 = dv%pccm(mpg%fccv(ifc, 2), 1)
        temp2 = temp3*pl%tn(mpg%fccv(ifc, 2))
        DO nd=1,nbdirs
          cond(nd, ifc, 0) = temp1*(temp7*(pl%tn(mpg%fccv(ifc, 1))*dvd%&
&           pccm(nd, mpg%fccv(ifc, 1), 1)+temp6*pld%tn(nd, mpg%fccv(ifc&
&           , 1)))+temp5*pld%na(nd, mpg%fccv(ifc, 1), is)+temp4*(pl%tn(&
&           mpg%fccv(ifc, 2))*dvd%pccm(nd, mpg%fccv(ifc, 2), 1)+temp3*&
&           pld%tn(nd, mpg%fccv(ifc, 2)))+temp2*pld%na(nd, mpg%fccv(ifc&
&           , 2), is))/temp0
        END DO
        con(ifc, 0) = temp1*((temp5*temp7+temp2*temp4)/temp0)
      END DO
    END IF
    DO nd=1,nbdirs
!
!    ..compute poloidal/radial coefficients for pressure diffusion term
      flod(nd, :, 0) = -(dnw(:, 0)*cond(nd, :, 0)+con(:, 0)*dnwd(nd, :, &
&       0))
      cond(nd, :, 0) = nwf*cond(nd, :, 0) + con(:, 0)*nwfd(nd, :)
!
      flod(nd, :, 1) = 0.d0
      cond(nd, :, 1) = 0.d0
    END DO
    flo(:, 0) = -(con(:, 0)*dnw(:, 0))
    con(:, 0) = con(:, 0)*nwf
    flo(:, 1) = 0.0_R8
    con(:, 1) = 0.0_R8
!
!    ..compute coefficients for stencil
    CALL CALCCOEF_DV_NODIFF(ncv, nfc, nvx, 0, geo, flo, flod, con, cond&
&                     , flo0, flo0d, con0, con0d, nbdirs)
!
!    ..add to previous contributions
! the logic statements could be simplified but for now this is not done to improve readability
    IF (switch%tn_style .EQ. 0) THEN
      DO nd=1,nbdirs
!! default combined ion-neutral energy equation, include both ions and neutrals in "ion" terms.
        dvd%floi(nd, :, :) = dvd%floi(nd, :, :) + flo0d(nd, :, :)
        dvd%coni(nd, :, :, 0) = dvd%coni(nd, :, :, 0) + con0d(nd, :, :)
      END DO
      dv%floi = dv%floi + flo0
      dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
    ELSE IF (switch%tn_style .EQ. 1) THEN
!! pure ion energy equation, only add ion contributions
      IF (.NOT.is_neutral(is)) THEN
        DO nd=1,nbdirs
          dvd%floi(nd, :, :) = dvd%floi(nd, :, :) + flo0d(nd, :, :)
          dvd%coni(nd, :, :, 0) = dvd%coni(nd, :, :, 0) + con0d(nd, :, :&
&           )
        END DO
        dv%floi = dv%floi + flo0
        dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
      END IF
    ELSE IF (switch%tn_style .EQ. 2) THEN
!! separate ion and neutral energy equation, split contributions
      IF ((.NOT.is_neutral(is)) .OR. NINT(zn(is)) .NE. 1) THEN
        DO nd=1,nbdirs
          dvd%floi(nd, :, :) = dvd%floi(nd, :, :) + flo0d(nd, :, :)
          dvd%coni(nd, :, :, 0) = dvd%coni(nd, :, :, 0) + con0d(nd, :, :&
&           )
        END DO
        dv%floi = dv%floi + flo0
        dv%coni(:, :, 0) = dv%coni(:, :, 0) + con0
      ELSE
        DO nd=1,nbdirs
          dvd%flon(nd, :, :) = dvd%flon(nd, :, :) + flo0d(nd, :, :)
          dvd%conn(nd, :, :) = dvd%conn(nd, :, :) + con0d(nd, :, :)
        END DO
        dv%flon = dv%flon + flo0
        dv%conn = dv%conn + con0
      END IF
    END IF
  END DO
!
!! end loop over species
!
  IF (switch%b2npht_iout .NE. 0) THEN
    CALL MY_OUT_US(70, nfc, 1, dv%floe(1, 0), 'b2tfcc_floex')
    CALL MY_OUT_US(70, nfc, 1, dv%floe(1, 1), 'b2tfcc_floey')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 0, 0), 'b2tfcc_conex')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 1, 0), 'b2tfcc_coney')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 0, 1), 'b2tfcc_conex1')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 1, 1), 'b2tfcc_coney1')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 0, 2), 'b2tfcc_conex2')
    CALL MY_OUT_US(70, nfc, 1, dv%cone(1, 1, 2), 'b2tfcc_coney2')
  END IF
!
! ..return
  ncall_b2tfcc = ncall_b2tfcc + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2TFCC_DV_NODIFF

