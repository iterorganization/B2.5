      temp15 = pld%te(nd, mpg%fccv(ifc, 2))
      temp16 = dvd%ne(nd, mpg%fccv(ifc, 2))
      temp17 = pl%te(mpg%fccv(ifc, 2))*temp16 + dv%ne(mpg%fccv(ifc, 2))*&
