      con(:, 1) = switch%xfac*2.5_R8*co%cdpa(:, 1, is)*nwf*tif
    ELSE
      DO nd=1,nbdirs
!! in case of default energy equation Tn = Ti
        temp9 = tnf*dnwd(nd, :, 0) + dnw(:, 0)*tnfd(nd, :)
        DO nd0=1,nbdirs0
          flodd(nd0, nd, :, 0) = -(switch%xfac*2.5_R8*(cod%cdpa(nd, :, 0&
