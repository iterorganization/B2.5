        END DO
        DO nd0=1,nbdirs0
          z_to_m1_astd0(nd0, :, inucl) = z_to_m1_astd0(nd0, :, inucl) + &
&           tempd(nd0, :)
        END DO
        z_to_m1_ast(:, inucl) = z_to_m1_ast(:, inucl) + temp
      END DO
    END DO
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_PREP_DV_DV

