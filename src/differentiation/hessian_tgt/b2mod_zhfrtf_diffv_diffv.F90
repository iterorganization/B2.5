&                    , t_a(:, is), t_ad0(:, :, is), t_ad(:, :, is), &
&                    t_add(:, :, :, is), wrkv, wrkvd0, wrkvd, wrkvdd, &
&                    gt_ac(:, is), gt_acd0(:, :, is), gt_acd(:, :, is), &
&                    gt_acdd(:, :, :, is), nbdirs, nbdirs0)
        DO nd=1,nbdirs
          DO nd0=nd,nbdirs0
            gt_acdd(nd0, nd, :, is) = geo%cvbb(:, 0)*gt_acdd(nd0, nd, :&
&             , is)/geo%cvbb(:, 3)
          END DO
          gt_acd(nd, :, is) = geo%cvbb(:, 0)*gt_acd(nd, :, is)/geo%cvbb(&
&           :, 3)
        END DO
        DO nd0=1,nbdirs0
          gt_acd0(nd0, :, is) = geo%cvbb(:, 0)*gt_acd0(nd0, :, is)/geo%&
&           cvbb(:, 3)
        END DO
