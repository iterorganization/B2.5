      temp0 = z2n_xy(icv, inucl)*av_ualpha(icv, inucl)
      temp2 = c_r_w(icv, inucl, s2nucl(is)-1)
      DO nd=1,nbdirs
        tempd(nd) = tempd(nd) + temp1*(temp0*c_r_wd(nd, icv, inucl, &
