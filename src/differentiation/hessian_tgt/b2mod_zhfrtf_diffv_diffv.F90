        avm_ud(nd, :) = avm_ud(nd, :) + ua(:, is)*rho_a_reld(nd, :, is) &
&         + rho_a_rel(:, is)*uad(nd, :, is)
      END DO
      DO nd0=1,nbdirs0
        rhod0(nd0, :) = rhod0(nd0, :) + rho_a_reld0(nd0, :, is)
        avm_ud0(nd0, :) = avm_ud0(nd0, :) + ua(:, is)*rho_a_reld0(nd0, :&
&         , is) + rho_a_rel(:, is)*uad0(nd0, :, is)
      END DO
      rho = rho + rho_a_rel(:, is)
      avm_u = avm_u + rho_a_rel(:, is)*ua(:, is)
    END DO
