    REAL(kind=r8) :: smtf, smtf_nofl, nais
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: smtfd0, naisd0
    REAL(kind=r8), DIMENSION(nbdirsmax) :: smtfd, naisd
    REAL(kind=r8), DIMENSION(nbdirsmax0, nbdirsmax) :: smtfdd, naisdd
