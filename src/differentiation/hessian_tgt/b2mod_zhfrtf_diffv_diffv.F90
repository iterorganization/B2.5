&           nd0, :, inucl))/nal(:, inucl)
        END DO
        temp0 = temp2
        DO nd=1,nbdirs
          temp2 = nad(nd, :, nucl2s(inucl, iz)) - temp0*nald(nd, :, &
&           inucl)
          temp3 = gt_ac(:, nucl2s(inucl, iz))/nal(:, inucl)
          temp4 = gt_acd(nd, :, nucl2s(inucl, iz))
          DO nd0=1,nbdirs0
            gtalcdd(nd0, nd, :, inucl) = gtalcdd(nd0, nd, :, inucl) + &
&             temp2*(gt_acd0(nd0, :, nucl2s(inucl, iz))-temp3*nald0(nd0&
&             , :, inucl))/nal(:, inucl) + temp3*(nadd(nd0, nd, :, &
&             nucl2s(inucl, iz))-nald(nd, :, inucl)*temp0d(nd0, :)-temp0&
&             *naldd(nd0, nd, :, inucl)) + temp4*temp0d(nd0, :) + temp0*&
&             gt_acdd(nd0, nd, :, nucl2s(inucl, iz))
          END DO
          gtalcd(nd, :, inucl) = gtalcd(nd, :, inucl) + temp3*temp2 + &
&           temp0*temp4
        END DO
        DO nd0=1,nbdirs0
          gtalcd0(nd0, :, inucl) = gtalcd0(nd0, :, inucl) + gt_ac(:, &
&           nucl2s(inucl, iz))*temp0d(nd0, :) + temp0*gt_acd0(nd0, :, &
&           nucl2s(inucl, iz))
        END DO
        gtalc(:, inucl) = gtalc(:, inucl) + temp0*gt_ac(:, nucl2s(inucl&
&         , iz))
      END DO
    END DO
!
!   ..average velocity gradient in the cell center
    CALL GRADC_P_DV_DV(ncv, nfc, nvx, 0, geo, geod0, geod, mpg, mpgd, &
&                avm_u, avm_ud0, avm_ud, avm_udd, wrkv, wrkvd0, wrkvd, &
&                wrkvdd, gavm_uc, gavm_ucd0, gavm_ucd, gavm_ucdd, nbdirs&
&                , nbdirs0)
    DO nd=1,nbdirs
      DO nd0=nd,nbdirs0
        gavm_ucdd(nd0, nd, :) = geo%cvbb(:, 0)*gavm_ucdd(nd0, nd, :)/geo&
&         %cvbb(:, 3)
      END DO
      gavm_ucd(nd, :) = geo%cvbb(:, 0)*gavm_ucd(nd, :)/geo%cvbb(:, 3)
    END DO
    DO nd0=1,nbdirs0
      gavm_ucd0(nd0, :) = geo%cvbb(:, 0)*gavm_ucd0(nd0, :)/geo%cvbb(:, 3&
&       )
    END DO
    gavm_uc = gavm_uc*geo%cvbb(:, 0)/geo%cvbb(:, 3)
!
    RETURN
