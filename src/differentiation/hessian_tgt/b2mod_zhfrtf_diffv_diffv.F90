    REAL(kind=r8), DIMENSION(nbdirsmax0, nspecies) :: tempd
    REAL(kind=r8), DIMENSION(nspecies) :: temp0
    REAL(kind=r8), DIMENSION(nbdirsmax0, nspecies) :: temp0d
    REAL(kind=r8) :: temp1
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: temp1d
    REAL(kind=r8) :: temp2
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: temp2d
    REAL(kind=r8) :: temp3
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: temp3d
    REAL(kind=r8) :: temp4
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: temp4d
    REAL(kind=r8) :: temp5
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: temp5d
    REAL(kind=r8) :: temp6
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: temp6d
    REAL(kind=r8) :: temp7
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: temp7d
    INTEGER :: nbdirs
    INTEGER :: nd0
    REAL(kind=r8), DIMENSION(nspecies) :: temp8
    REAL(kind=r8), DIMENSION(nspecies) :: temp9
    REAL(kind=r8) :: temp10
    REAL(kind=r8) :: temp11
    REAL(kind=r8) :: temp12
    REAL(kind=r8) :: temp13
    REAL(kind=r8) :: temp14
    REAL(kind=r8) :: temp15
    REAL(kind=r8) :: temp16
    INTEGER :: nbdirs0
    temp = nal(icv, 0:nspecies-1)*gtalc(icv, 0:nspecies-1)
    temp0 = c_r_ta(icv, s2nucl(is)-1, 0:nspecies-1)
    DO nd0=1,nbdirs0
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TF calculates the thermal force.
!
!  3. description
!
!     This routine computes:
!     -smtf: the thermal force
!     -smtf_nofl: the thermal force without flux limiting
!
!-----------------------------------------------------------------------
!
!   ..the thermal force before charge state correction
      tempd(nd0, :) = gtalc(icv, 0:nspecies-1)*nald0(nd0, icv, 0:&
&       nspecies-1) + nal(icv, 0:nspecies-1)*gtalcd0(nd0, icv, 0:&
&       nspecies-1)
      temp0d(nd0, :) = c_r_tad0(nd0, icv, s2nucl(is)-1, 0:nspecies-1)
      arg1d0(nd0, :) = temp*temp0d(nd0, :) + temp0*tempd(nd0, :)
      temp1d(nd0) = SUM(arg1d0(nd0, :))
    END DO
    arg1 = temp0*temp
    temp1 = SUM(arg1)
    arg1dd = 0.D0
    DO nd=1,nbdirs
      temp8 = c_r_tad(nd, icv, s2nucl(is)-1, 0:nspecies-1)
      temp9 = gtalc(icv, 0:nspecies-1)*nald(nd, icv, 0:nspecies-1) + nal&
&       (icv, 0:nspecies-1)*gtalcd(nd, icv, 0:nspecies-1)
      arg1d(nd, :) = temp*temp8 + temp0*temp9
      temp10 = SUM(arg1d(nd, :))
      DO nd0=1,nbdirs0
        arg1dd(nd0, nd, :) = temp8*tempd(nd0, :) + temp*c_r_tadd(nd0, nd&
&         , icv, s2nucl(is)-1, 0:nspecies-1) + temp9*temp0d(nd0, :) + &
&         temp0*(nald(nd, icv, 0:nspecies-1)*gtalcd0(nd0, icv, 0:&
&         nspecies-1)+gtalc(icv, 0:nspecies-1)*naldd(nd0, nd, icv, 0:&
&         nspecies-1)+gtalcd(nd, icv, 0:nspecies-1)*nald0(nd0, icv, 0:&
&         nspecies-1)+nal(icv, 0:nspecies-1)*gtalcdd(nd0, nd, icv, 0:&
&         nspecies-1))
        smtfdd(nd0, nd) = -(iad(nd, icv, is)*temp1d(nd0)) - temp1*iadd(&
&         nd0, nd, icv, is) - temp10*iad0(nd0, icv, is) - ia(icv, is)*&
&         SUM(arg1dd(nd0, nd, :))
      END DO
      smtfd(nd) = -(temp1*iad(nd, icv, is)) - ia(icv, is)*temp10
    END DO
    DO nd0=1,nbdirs0
      smtfd0(nd0) = -(temp1*iad0(nd0, icv, is)+ia(icv, is)*temp1d(nd0))
    END DO
    smtf = -(ia(icv, is)*temp1)
!
!   ..the thermal force before charge state correction without flux limiting
    arg1 = nal(icv, 0:nspecies-1)*c_r_ta_nofl(icv, s2nucl(is)-1, 0:&
&     nspecies-1)*gtalc(icv, 0:nspecies-1)
    smtf_nofl = -(ia(icv, is)*SUM(arg1))
!
!   ..apply the charge state correction
    IF (zhcscorr .EQ. 1) THEN
      temp1 = gtalc(icv, s2nucl(is)-1)
      temp2 = nal(icv, s2nucl(is)-1)
      temp10 = nais/temp2
      temp5 = nal(icv, s2nucl(is)-1)
      temp6 = c_r_tb(icv, s2nucl(is)-1)
      DO nd0=1,nbdirs0
        temp1d(nd0) = gtalcd0(nd0, icv, s2nucl(is)-1)
        temp2d(nd0) = nald0(nd0, icv, s2nucl(is)-1)
        temp3d(nd0) = temp10*gt_acd0(nd0, icv, is) + gt_ac(icv, is)*(&
&         naisd0(nd0)-temp10*temp2d(nd0))/temp2
        temp4d(nd0) = temp3d(nd0) - temp1*iad0(nd0, icv, is) - ia(icv, &
&         is)*temp1d(nd0)
        temp5d(nd0) = nald0(nd0, icv, s2nucl(is)-1)
        temp6d(nd0) = c_r_tbd0(nd0, icv, s2nucl(is)-1)
        temp7d(nd0) = temp5*temp6d(nd0) + temp6*temp5d(nd0)
      END DO
      temp3 = gt_ac(icv, is)*temp10
      temp4 = temp3 - ia(icv, is)*temp1
      temp7 = temp6*temp5
      DO nd=1,nbdirs
        temp10 = nald(nd, icv, s2nucl(is)-1)
        temp11 = c_r_tbd(nd, icv, s2nucl(is)-1)
        temp12 = temp5*temp11 + temp6*temp10
        temp13 = gtalcd(nd, icv, s2nucl(is)-1)
        temp14 = nald(nd, icv, s2nucl(is)-1)
        temp15 = (gt_ac(icv, is)*naisd(nd)+nais*gt_acd(nd, icv, is)-&
&         temp3*temp14)/temp2
        temp16 = temp15 - temp1*iad(nd, icv, is) - ia(icv, is)*temp13
        DO nd0=1,nbdirs0
          smtfdd(nd0, nd) = smtfdd(nd0, nd) - temp12*temp4d(nd0) - temp4&
&           *(temp11*temp5d(nd0)+temp5*c_r_tbdd(nd0, nd, icv, s2nucl(is)&
&           -1)+temp10*temp6d(nd0)+temp6*naldd(nd0, nd, icv, s2nucl(is)-&
&           1)) - temp16*temp7d(nd0) - temp7*((naisd(nd)*gt_acd0(nd0, &
&           icv, is)+gt_ac(icv, is)*naisdd(nd0, nd)+gt_acd(nd, icv, is)*&
&           naisd0(nd0)+nais*gt_acdd(nd0, nd, icv, is)-temp14*temp3d(nd0&
&           )-temp3*naldd(nd0, nd, icv, s2nucl(is)-1)-temp15*temp2d(nd0)&
&           )/temp2-iad(nd, icv, is)*temp1d(nd0)-temp1*iadd(nd0, nd, icv&
&           , is)-temp13*iad0(nd0, icv, is)-ia(icv, is)*gtalcdd(nd0, nd&
&           , icv, s2nucl(is)-1))
        END DO
        smtfd(nd) = smtfd(nd) - temp4*temp12 - temp7*temp16
      END DO
      DO nd0=1,nbdirs0
        smtfd0(nd0) = smtfd0(nd0) - temp4*temp7d(nd0) - temp7*temp4d(nd0&
&         )
      END DO
      smtf = smtf - temp7*temp4
!
      smtf_nofl = smtf_nofl - c_r_tb_nofl(icv, s2nucl(is)-1)*nal(icv, &
&       s2nucl(is)-1)*(nais/nal(icv, s2nucl(is)-1)*gt_ac(icv, is)-ia(icv&
&       , is)*gtalc(icv, s2nucl(is)-1))
    END IF
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_TF_DV_DV

!  Differentiation of b2mod_zhfrtf_tf in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: smtf
!   with respect to varying inputs: *nal *ia *gt_ac *gtalc *c_r_ta
!                *c_r_tb nais
!   Plus diff mem management of: nal:in ia:in gt_ac:in gtalc:in
!                c_r_ta:in c_r_tb:in
!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_TF_DV(icv, is, zhcscorr, nais, naisd, smtf, &
&   smtfd, smtf_nofl, nbdirs)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!  Hint: nbdirsmax should be the maximum number of differentiation directions
    INTEGER :: icv, is, zhcscorr
    REAL(kind=r8) :: smtf, smtf_nofl, nais
    REAL(kind=r8), DIMENSION(nbdirsmax) :: smtfd, naisd
    INTRINSIC SUM
    REAL(kind=r8), DIMENSION(nspecies) :: arg1
    REAL(kind=r8), DIMENSION(nbdirsmax, nspecies) :: arg1d
    INTEGER :: nd
    REAL(kind=r8), DIMENSION(nspecies) :: temp
    REAL(kind=r8), DIMENSION(nspecies) :: temp0
    REAL(kind=r8) :: temp1
    REAL(kind=r8) :: temp2
    REAL(kind=r8) :: temp3
    REAL(kind=r8) :: temp4
    REAL(kind=r8) :: temp5
    REAL(kind=r8) :: temp6
    REAL(kind=r8) :: temp7
    INTEGER :: nbdirs
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TF calculates the thermal force.
!
!  3. description
!
!     This routine computes:
!     -smtf: the thermal force
!     -smtf_nofl: the thermal force without flux limiting
!
!-----------------------------------------------------------------------
!
!   ..the thermal force before charge state correction
    temp = nal(icv, 0:nspecies-1)*gtalc(icv, 0:nspecies-1)
    temp0 = c_r_ta(icv, s2nucl(is)-1, 0:nspecies-1)
    arg1 = temp0*temp
    temp1 = SUM(arg1)
    DO nd=1,nbdirs
      arg1d(nd, :) = temp*c_r_tad(nd, icv, s2nucl(is)-1, 0:nspecies-1) +&
&       temp0*(gtalc(icv, 0:nspecies-1)*nald(nd, icv, 0:nspecies-1)+nal(&
&       icv, 0:nspecies-1)*gtalcd(nd, icv, 0:nspecies-1))
      smtfd(nd) = -(temp1*iad(nd, icv, is)+ia(icv, is)*SUM(arg1d(nd, :))&
&       )
    END DO
    smtf = -(ia(icv, is)*temp1)
!
!   ..the thermal force before charge state correction without flux limiting
    arg1 = nal(icv, 0:nspecies-1)*c_r_ta_nofl(icv, s2nucl(is)-1, 0:&
&     nspecies-1)*gtalc(icv, 0:nspecies-1)
    smtf_nofl = -(ia(icv, is)*SUM(arg1))
!
!   ..apply the charge state correction
    IF (zhcscorr .EQ. 1) THEN
      temp1 = gtalc(icv, s2nucl(is)-1)
      temp2 = nal(icv, s2nucl(is)-1)
      temp3 = nais*gt_ac(icv, is)/temp2
      temp4 = temp3 - ia(icv, is)*temp1
      temp5 = nal(icv, s2nucl(is)-1)
      temp6 = c_r_tb(icv, s2nucl(is)-1)
      temp7 = temp6*temp5
      DO nd=1,nbdirs
        smtfd(nd) = smtfd(nd) - temp4*(temp5*c_r_tbd(nd, icv, s2nucl(is)&
&         -1)+temp6*nald(nd, icv, s2nucl(is)-1)) - temp7*((gt_ac(icv, is&
&         )*naisd(nd)+nais*gt_acd(nd, icv, is)-temp3*nald(nd, icv, &
&         s2nucl(is)-1))/temp2-temp1*iad(nd, icv, is)-ia(icv, is)*gtalcd&
&         (nd, icv, s2nucl(is)-1))
      END DO
      smtf = smtf - temp7*temp4
!
      smtf_nofl = smtf_nofl - c_r_tb_nofl(icv, s2nucl(is)-1)*nal(icv, &
&       s2nucl(is)-1)*(nais/nal(icv, s2nucl(is)-1)*gt_ac(icv, is)-ia(icv&
&       , is)*gtalc(icv, s2nucl(is)-1))
    END IF
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_TF_DV

!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_TF(icv, is, zhcscorr, nais, smtf, smtf_nofl)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER :: icv, is, zhcscorr
    REAL(kind=r8) :: smtf, smtf_nofl, nais
    INTRINSIC SUM
    REAL(kind=r8), DIMENSION(nspecies) :: arg1
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TF calculates the thermal force.
!
!  3. description
!
!     This routine computes:
!     -smtf: the thermal force
!     -smtf_nofl: the thermal force without flux limiting
!
!-----------------------------------------------------------------------
!
!   ..the thermal force before charge state correction
    arg1 = nal(icv, 0:nspecies-1)*c_r_ta(icv, s2nucl(is)-1, 0:nspecies-1&
&     )*gtalc(icv, 0:nspecies-1)
    smtf = -(ia(icv, is)*SUM(arg1))
!
!   ..the thermal force before charge state correction without flux limiting
    arg1 = nal(icv, 0:nspecies-1)*c_r_ta_nofl(icv, s2nucl(is)-1, 0:&
&     nspecies-1)*gtalc(icv, 0:nspecies-1)
    smtf_nofl = -(ia(icv, is)*SUM(arg1))
!
!   ..apply the charge state correction
    IF (zhcscorr .EQ. 1) THEN
      smtf = smtf - c_r_tb(icv, s2nucl(is)-1)*nal(icv, s2nucl(is)-1)*(&
&       nais/nal(icv, s2nucl(is)-1)*gt_ac(icv, is)-ia(icv, is)*gtalc(icv&
&       , s2nucl(is)-1))
!
      smtf_nofl = smtf_nofl - c_r_tb_nofl(icv, s2nucl(is)-1)*nal(icv, &
&       s2nucl(is)-1)*(nais/nal(icv, s2nucl(is)-1)*gt_ac(icv, is)-ia(icv&
&       , is)*gtalc(icv, s2nucl(is)-1))
    END IF
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_TF
