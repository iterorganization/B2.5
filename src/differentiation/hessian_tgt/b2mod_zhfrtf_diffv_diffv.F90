      temp1 = rho_a_rel(:, is)/rho
      DO nd0=1,nbdirs0
        temp0d(nd0, :) = (rho_a_reld0(nd0, :, is)-temp1*rhod0(nd0, :))/&
&         rho
      END DO
      temp0 = temp1
      DO nd=1,nbdirs
