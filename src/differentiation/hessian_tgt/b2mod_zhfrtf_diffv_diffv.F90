&   nbdirsmax0, nbdirsmax, ncv, 0:ns-1), tidd(nbdirsmax0, nbdirsmax, ncv&
&   )
!
!   ..local variables
    INTEGER :: is, iz, inucl
    REAL(kind=r8) :: rho(ncv), w_a(ncv, 0:ns-1), t_a(ncv, 0:ns-1), wrkv(&
&   nvx)
    REAL(kind=r8) :: rhod0(nbdirsmax0, ncv), w_ad0(nbdirsmax0, ncv, 0:ns&
&   -1), t_ad0(nbdirsmax0, ncv, 0:ns-1), wrkvd0(nbdirsmax0, nvx)
    REAL(kind=r8) :: rhod(nbdirsmax, ncv), w_ad(nbdirsmax, ncv, 0:ns-1)&
&   , t_ad(nbdirsmax, ncv, 0:ns-1), wrkvd(nbdirsmax, nvx)
    REAL(kind=r8) :: rhodd(nbdirsmax0, nbdirsmax, ncv), w_add(nbdirsmax0&
&   , nbdirsmax, ncv, 0:ns-1), t_add(nbdirsmax0, nbdirsmax, ncv, 0:ns-1)&
&   , wrkvdd(nbdirsmax0, nbdirsmax, nvx)
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(kind=r8), DIMENSION(ncv) :: temp0
    REAL(kind=r8), DIMENSION(nbdirsmax0, ncv) :: temp0d
    INTEGER :: nbdirs
    INTEGER :: nd0
    REAL(kind=r8), DIMENSION(ncv) :: temp1
    REAL(kind=r8), DIMENSION(ncv) :: temp2
    REAL(kind=r8), DIMENSION(SIZE(gt_ac, 1)) :: temp3
    REAL(kind=r8), DIMENSION(SIZE(gt_acd, 2)) :: temp4
    INTEGER :: nbdirs0
