!  Differentiation of b2mod_zhfrtf_avm_u in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: *avm_u *rho_a_rel
!   with respect to varying inputs: *avm_u *rho_a_rel na ua
!   Plus diff mem management of: avm_u:in rho_a_rel:in
!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_AVM_U_DV_DV(ncv, ns, na, nad0, nad, nadd, ua, &
&   uad0, uad, uadd, nbdirs, nbdirs0)
    USE B2MOD_CONSTANTS
!  Hint: nbdirsmax should be the maximum number of differentiation directions
    USE B2MOD_DIFFSIZES
!  Hint: nbdirsmax0 should be the maximum number of differentiation directions
    IMPLICIT NONE
    INTEGER :: ncv, ns
!
!.declarations
!   ..input arguments
    REAL(kind=r8) :: na(ncv, 0:ns-1), ua(ncv, 0:ns-1)
    REAL(kind=r8) :: nad0(nbdirsmax0, ncv, 0:ns-1), uad0(nbdirsmax0, ncv&
&   , 0:ns-1)
    REAL(kind=r8) :: nad(nbdirsmax, ncv, 0:ns-1), uad(nbdirsmax, ncv, 0:&
&   ns-1)
    REAL(kind=r8) :: nadd(nbdirsmax0, nbdirsmax, ncv, 0:ns-1), uadd(&
&   nbdirsmax0, nbdirsmax, ncv, 0:ns-1)
!
!   ..local variables
    INTEGER :: is
    REAL(kind=r8) :: rho(ncv)
    REAL(kind=r8) :: rhod0(nbdirsmax0, ncv)
    REAL(kind=r8) :: rhod(nbdirsmax, ncv)
    REAL(kind=r8) :: rhodd(nbdirsmax0, nbdirsmax, ncv)
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(kind=r8), DIMENSION(ncv) :: temp0
    REAL(kind=r8), DIMENSION(nbdirsmax0, ncv) :: temp0d
    INTEGER :: nbdirs
    INTEGER :: nd0
    REAL(kind=r8), DIMENSION(ncv) :: temp1
    REAL(kind=r8), DIMENSION(ncv) :: temp2
    INTEGER :: nbdirs0
!
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_AVM_U calculates the mass-weighted average flow
!     velocity of all ions.
!
!  3. description
!
!     This routine computes:
!     rho_A_rel: relative mass density of the ion "a" to
!                the total ion mass density
!     avm_u: mass-weighted average flow velocity of all ions
!
!-----------------------------------------------------------------------
!.computation
!
    rho = 0.0_R8
    DO nd=1,nbdirs
      DO nd0=nd,nbdirs0
        avm_udd(nd0, nd, :) = 0.D0
      END DO
      avm_ud(nd, :) = 0.d0
    END DO
    DO nd0=1,nbdirs0
      avm_ud0(nd0, :) = 0.D0
    END DO
    avm_u = 0.0_R8
    rhod = 0.d0
    rhodd = 0.D0
    rhod0 = 0.D0
    DO is=0,ns-1
      temp = is_i(is)*am(is)
      DO nd0=1,nbdirs0
        rho_a_reld0(nd0, :, is) = temp*nad0(nd0, :, is)
      END DO
      rho_a_rel(:, is) = temp*na(:, is)
      DO nd=1,nbdirs
        rho_a_reld(nd, :, is) = temp*nad(nd, :, is)
        DO nd0=1,nbdirs0
          rho_a_reldd(nd0, nd, :, is) = temp*nadd(nd0, nd, :, is)
          rhodd(nd0, nd, :) = rhodd(nd0, nd, :) + rho_a_reldd(nd0, nd, :&
&           , is)
          avm_udd(nd0, nd, :) = avm_udd(nd0, nd, :) + rho_a_reld(nd, :, &
&           is)*uad0(nd0, :, is) + ua(:, is)*rho_a_reldd(nd0, nd, :, is)&
&           + uad(nd, :, is)*rho_a_reld0(nd0, :, is) + rho_a_rel(:, is)*&
&           uadd(nd0, nd, :, is)
        END DO
        rhod(nd, :) = rhod(nd, :) + rho_a_reld(nd, :, is)
        avm_ud(nd, :) = avm_ud(nd, :) + ua(:, is)*rho_a_reld(nd, :, is) &
&         + rho_a_rel(:, is)*uad(nd, :, is)
      END DO
      DO nd0=1,nbdirs0
        rhod0(nd0, :) = rhod0(nd0, :) + rho_a_reld0(nd0, :, is)
        avm_ud0(nd0, :) = avm_ud0(nd0, :) + ua(:, is)*rho_a_reld0(nd0, :&
&         , is) + rho_a_rel(:, is)*uad0(nd0, :, is)
      END DO
      rho = rho + rho_a_rel(:, is)
      avm_u = avm_u + rho_a_rel(:, is)*ua(:, is)
    END DO
    DO is=0,ns-1
      temp1 = rho_a_rel(:, is)/rho
      DO nd0=1,nbdirs0
        temp0d(nd0, :) = (rho_a_reld0(nd0, :, is)-temp1*rhod0(nd0, :))/&
&         rho
      END DO
      temp0 = temp1
      DO nd=1,nbdirs
        temp1 = (rho_a_reld(nd, :, is)-temp0*rhod(nd, :))/rho
        DO nd0=1,nbdirs0
          rho_a_reldd(nd0, nd, :, is) = (rho_a_reldd(nd0, nd, :, is)-&
&           rhod(nd, :)*temp0d(nd0, :)-temp0*rhodd(nd0, nd, :)-temp1*&
&           rhod0(nd0, :))/rho
        END DO
        rho_a_reld(nd, :, is) = temp1
      END DO
      DO nd0=1,nbdirs0
        rho_a_reld0(nd0, :, is) = temp0d(nd0, :)
      END DO
      rho_a_rel(:, is) = temp0
    END DO
    DO nd=1,nbdirs
      temp1 = avm_u/rho
      temp2 = (avm_ud(nd, :)-temp1*rhod(nd, :))/rho
      DO nd0=1,nbdirs0
        avm_udd(nd0, nd, :) = (avm_udd(nd0, nd, :)-rhod(nd, :)*(avm_ud0(&
&         nd0, :)-temp1*rhod0(nd0, :))/rho-temp1*rhodd(nd0, nd, :)-temp2&
&         *rhod0(nd0, :))/rho
      END DO
      avm_ud(nd, :) = temp2
    END DO
    DO nd0=1,nbdirs0
      avm_ud0(nd0, :) = (avm_ud0(nd0, :)-avm_u*rhod0(nd0, :)/rho)/rho
    END DO
    avm_u = avm_u/rho
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_AVM_U_DV_DV

