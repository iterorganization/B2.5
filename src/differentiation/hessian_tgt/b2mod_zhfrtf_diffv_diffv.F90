    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER :: ncv, ns
!
!.declarations
!   ..input arguments
    REAL(kind=r8) :: na(ncv, 0:ns-1), ua(ncv, 0:ns-1)
    REAL(kind=r8) :: nad(nbdirsmax, ncv, 0:ns-1), uad(nbdirsmax, ncv, 0:&
