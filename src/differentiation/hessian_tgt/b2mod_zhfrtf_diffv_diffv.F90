      END DO
      w_a(:, is) = ua(:, is) - avm_u
      temp = switch%zhd_corr*switch%zhd_corrt*am(is)*mp
      DO nd=1,nbdirs
        w_ad(nd, :, is) = uad(nd, :, is) - avm_ud(nd, :)
        DO nd0=1,nbdirs0
          w_add(nd0, nd, :, is) = uadd(nd0, nd, :, is) - avm_udd(nd0, nd&
&           , :)
          t_add(nd0, nd, :, is) = tidd(nd0, nd, :) + temp*2*(w_ad(nd, :&
&           , is)*w_ad0(nd0, :, is)/3.0_R8+w_a(:, is)*w_add(nd0, nd, :, &
&           is)/3.0_R8)
        END DO
        t_ad(nd, :, is) = tid(nd, :) + temp*2*w_a(:, is)*w_ad(nd, :, is)&
&         /3.0_R8
      END DO
      DO nd0=1,nbdirs0
        t_ad0(nd0, :, is) = tid0(nd0, :) + temp*2*w_a(:, is)*w_ad0(nd0, &
&         :, is)/3.0_R8
      END DO
      t_a(:, is) = ti + temp*(w_a(:, is)*w_a(:, is)/3.0_R8)
    END DO
