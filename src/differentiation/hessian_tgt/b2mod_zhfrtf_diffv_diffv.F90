    REAL(kind=r8) :: rho(ncv)
    REAL(kind=r8) :: rhod(nbdirsmax, ncv)
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(kind=r8), DIMENSION(ncv) :: temp0
    INTEGER :: nbdirs
!
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_AVM_U calculates the mass-weighted average flow
!     velocity of all ions.
!
!  3. description
!
!     This routine computes:
!     rho_A_rel: relative mass density of the ion "a" to
!                the total ion mass density
!     avm_u: mass-weighted average flow velocity of all ions
!
!-----------------------------------------------------------------------
!.computation
!
    rho = 0.0_R8
    DO nd=1,nbdirs
      avm_ud(nd, :) = 0.d0
    END DO
    avm_u = 0.0_R8
    rhod = 0.d0
    DO is=0,ns-1
      temp = is_i(is)*am(is)
      rho_a_rel(:, is) = temp*na(:, is)
      DO nd=1,nbdirs
        rho_a_reld(nd, :, is) = temp*nad(nd, :, is)
        rhod(nd, :) = rhod(nd, :) + rho_a_reld(nd, :, is)
        avm_ud(nd, :) = avm_ud(nd, :) + ua(:, is)*rho_a_reld(nd, :, is) &
&         + rho_a_rel(:, is)*uad(nd, :, is)
      END DO
      rho = rho + rho_a_rel(:, is)
      avm_u = avm_u + rho_a_rel(:, is)*ua(:, is)
    END DO
    DO is=0,ns-1
      temp0 = rho_a_rel(:, is)/rho
      DO nd=1,nbdirs
        rho_a_reld(nd, :, is) = (rho_a_reld(nd, :, is)-temp0*rhod(nd, :)&
&         )/rho
      END DO
      rho_a_rel(:, is) = temp0
    END DO
    DO nd=1,nbdirs
      avm_ud(nd, :) = (avm_ud(nd, :)-avm_u*rhod(nd, :)/rho)/rho
    END DO
    avm_u = avm_u/rho
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_AVM_U_DV

