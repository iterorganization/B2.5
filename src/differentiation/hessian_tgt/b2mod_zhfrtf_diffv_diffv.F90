!   ..effective temperature gradient of each isonuclear sequence in the cell center
    DO nd=1,nbdirs
      DO nd0=nd,nbdirs0
        gtalcdd(nd0, nd, :, :) = 0.D0
      END DO
      gtalcd(nd, :, :) = 0.d0
    END DO
    DO nd0=1,nbdirs0
      gtalcd0(nd0, :, :) = 0.D0
    END DO
