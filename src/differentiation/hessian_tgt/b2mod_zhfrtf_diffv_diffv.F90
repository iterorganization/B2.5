&         nd0, :)-temp1*rhod0(nd0, :))/rho-temp1*rhodd(nd0, nd, :)-temp2&
&         *rhod0(nd0, :))/rho
      END DO
      avm_ud(nd, :) = temp2
    END DO
    DO nd0=1,nbdirs0
      avm_ud0(nd0, :) = (avm_ud0(nd0, :)-avm_u*rhod0(nd0, :)/rho)/rho
    END DO
    avm_u = avm_u/rho
    w_ad = 0.d0
    t_ad = 0.d0
    w_ad0 = 0.D0
    t_ad0 = 0.D0
    w_add = 0.D0
    t_add = 0.D0
!
!   ..temperature of each ion charge state
