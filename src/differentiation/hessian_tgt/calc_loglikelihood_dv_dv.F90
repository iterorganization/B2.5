      ldetcd(nd) = 0.d0
    END DO
  END SELECT
!
!     Calculate log-likelihood
  arg11 = 2.0_R8*pi
  DO nd=1,nbdirs
    llld(nd) = -(0.5_R8*(ldetcd(nd)+SUM(invc_zd(nd, :))))
  END DO
  lll = -(0.5_R8*(nn*LOG(arg11)+ldetc+SUM(invc_z)))
!
  CALL SUBEND()
  RETURN
END SUBROUTINE CALC_LOGLIKELIHOOD_DV_NODIFF

