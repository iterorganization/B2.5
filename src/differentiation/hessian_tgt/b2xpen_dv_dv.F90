        temp = tn(mpg%fccv(ifc, 1)) + tn(mpg%fccv(ifc, 2))
        DO nd=1,nbdirs
          fntd(nd, ifc, 0:1) = fntd(nd, ifc, 0:1) + 0.5_R8*(temp*fna_53d&
&           (nd, ifc, 0:1, is)+fna_53(ifc, 0:1, is)*(tnd(nd, mpg%fccv(&
&           ifc, 1))+tnd(nd, mpg%fccv(ifc, 2))))
        END DO
        fnt(ifc, 0:1) = fnt(ifc, 0:1) + 0.5_R8*(fna_53(ifc, 0:1, is)*&
&         temp)
      END IF
    END DO
  END DO
! calculate total fluxes (fht)
  DO nd=1,nbdirs
    fhtd(nd, :, :) = fhed(nd, :, :) + fhid(nd, :, :) + fhnd(nd, :, :) + &
&     fhjd(nd, :, :) + (1.0_R8-boris)*fntd(nd, :, :)
  END DO
  fht = fhe + fhi + fhn + fhj + fnt*(1.0_R8-boris)
  DO is=0,ns-1
    DO nd=1,nbdirs
      fhtd(nd, :, :) = fhtd(nd, :, :) + fhpd(nd, :, :, is) + (1.0_R8-&
&       boris)*fhmd(nd, :, :, is)
    END DO
    fht = fht + fhp(:, :, is) + fhm(:, :, is)*(1.0_R8-boris)
  END DO
!$$$  call subend ()
  RETURN
END SUBROUTINE B2XPEN_DV_NODIFF

