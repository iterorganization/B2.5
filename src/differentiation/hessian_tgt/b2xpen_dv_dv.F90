SUBROUTINE B2XPEN_NODIFF_NODIFF(ncv, nfc, ns, mpg, fna, fna_53, fne_53, &
& fch, fhe, fhi, fhn, rpt, te, ti, tn, po, boris, fhm, fhp, fhj, fht)
  USE B2MOD_TYPES
  USE B2MOD_CONSTANTS
  USE B2US_MAP_DIFFV_DIFFV
  USE B2MOD_B2CMPA_DIFFV_DIFFV
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!     ------------------------------------------------------------------
  INTEGER :: ncv, nfc, ns
! inputs
  TYPE(MAPPING), INTENT(IN) :: mpg
  REAL(kind=r8) :: fna(nfc, 0:1, 0:ns-1), fna_53(nfc, 0:1, 0:ns-1), &
& fne_53(nfc, 0:1), fch(nfc, 0:1), fhe(nfc, 0:1), fhi(nfc, 0:1), fhn(nfc&
& , 0:1), rpt(ncv, 0:ns-1), te(ncv), ti(ncv), tn(ncv), po(ncv), boris
! outputs
  REAL(kind=r8) :: fhm(nfc, 0:1, 0:ns-1), fhp(nfc, 0:1, 0:ns-1), fhj(nfc&
& , 0:1), fht(nfc, 0:1)
!     ------------------------------------------------------------------
!     B2XPEN computes contributions to the total energy fluxes,
!     as well as the total energy fluxes
!     The contributions from external species are not included and must
!     be added explicitly in the calling routine when needed.
!     ------------------------------------------------------------------
  INTEGER :: ifc, is
  REAL(kind=r8) :: fnt(nfc, 0:1)
!     ------------------------------------------------------------------
!$$$  call subini ('b2xpen')
!
!     calculate additional fluxes (fhj, fhp)
!wdk  note that fhm is now computed in b2urmo
  DO ifc=1,nfc
    fhj(ifc, 0:1) = -(0.5_R8*fch(ifc, 0:1)*(po(mpg%fccv(ifc, 1))+po(mpg%&
&     fccv(ifc, 2))))
    fnt(ifc, 0:1) = 0.5_R8*fne_53(ifc, 0:1)*(te(mpg%fccv(ifc, 1))+te(mpg&
&     %fccv(ifc, 2)))
  END DO
  DO is=0,ns-1
    DO ifc=1,nfc
      fhp(ifc, 0:1, is) = 0.5_R8*fna(ifc, 0:1, is)*(rpt(mpg%fccv(ifc, 1)&
&       , is)+rpt(mpg%fccv(ifc, 2), is))*ev
      IF (.NOT.is_neutral(is)) THEN
        fnt(ifc, 0:1) = fnt(ifc, 0:1) + 0.5_R8*fna_53(ifc, 0:1, is)*(ti(&
&         mpg%fccv(ifc, 1))+ti(mpg%fccv(ifc, 2)))
      ELSE
        fnt(ifc, 0:1) = fnt(ifc, 0:1) + 0.5_R8*fna_53(ifc, 0:1, is)*(tn(&
&         mpg%fccv(ifc, 1))+tn(mpg%fccv(ifc, 2)))
      END IF
    END DO
  END DO
! calculate total fluxes (fht)
  fht = fhe + fhi + fhn + fhj + fnt*(1.0_R8-boris)
  DO is=0,ns-1
    fht = fht + fhp(:, :, is) + fhm(:, :, is)*(1.0_R8-boris)
  END DO
!$$$  call subend ()
  RETURN
END SUBROUTINE B2XPEN_NODIFF_NODIFF

