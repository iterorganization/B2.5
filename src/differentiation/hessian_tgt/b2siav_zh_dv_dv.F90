&                 , 2)/(geo%cvbb(icv, 0)*geo%cvbb(icv, 3))*(nal(icv, &
&                 inucl)*ti(icv)/qe*z_to_m1_ast(icv, inucl))*gti(icv, 1)
              END DO
            END IF
!to ensure that subsequent steps lead to well-defined value on separatrix:
!copy value of qip0 across separatrix
!wdk todo: rationalize this when separatrix-structure is introduced?

          END DO
!
          DO ifc=1,nfc
            IF (mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. (.NOT.mpg%&
&               cvonclosedsurface(mpg%fccv(ifc, 2)))) qip0(mpg%fccv(ifc&
&             , 2)) = qip0(mpg%fccv(ifc, 1))
            IF (.NOT.mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. mpg%&
&               cvonclosedsurface(mpg%fccv(ifc, 2))) qip0(mpg%fccv(ifc, &
&             1)) = qip0(mpg%fccv(ifc, 2))
          END DO
        END IF
!
!
!         ..compute qip1
        arg1 = te + ti
        result10 = SQRT(arg1)
        arg2 = amnucl(inucl)*mp
        result2 = SQRT(arg2)
        qip1 = switch%cqip1*nal(:, inucl)*ti*result10/result2
!
!         ..compute qipgen
        arg1 = qip0/qip1
        arg20 = geo%cvbb(:, 3)
        result11 = SQRT(arg20)
        qipgen_al(:, inucl) = qip1*DATAN(arg1)*result11
!
!wdk modified computation compared to structured code to
!wdk facilitate implementation on unstructured grids
      END DO
!
!       ..main contribution
!
!       ..gradient qipgen_al in cell centers
      DO inucl=0,nspecies-1
        CALL GRAD_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, qipgen_al(&
&                           :, inucl), wrkv, wrkns(:, inucl))
      END DO
!
      wrkf0 = 0.0_R8
      DO inucl=0,nspecies-1
        wrkf0 = wrkf0 + wrkns(:, inucl)*vsaf_hadp_albe(:, 0, s2nucl(isb)&
&         -1, inucl)
      END DO
!
      wrk0 = na(:, isb)/nal(:, s2nucl(isb)-1)
      CALL INTFACE(ncv, nfc, mpg%fccv, wght, wrk0, wrkf1)
!
      wrkf0 = wrkf0*wrkf1/(geo%fcbb(:, 0)*geo%fcbb(:, 3))
!
!       ..finally calculate the r.h.s. contribution
      CALL INTCELL_NODIFF_NODIFF(nfc, ncv, mpg, mpg%intcellp, wrkf0, &
&                          wrk0)
!zero out spurious contributions outside of separatrix
!wdk todo: rationalize this when separatrix-structure is introduced?
      CALL GRADC_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk0, wrkv&
&                          , wrk1)
!
      DO icv=1,ncv
        IF (.NOT.mpg%cvonclosedsurface(icv)) wrk1(icv) = 0.0_R8
!calculate the actual source

      END DO
!
      arg10 = geo%cvbb(:, 3)
      result11 = SQRT(arg10)
      smbvh(:, 0) = switch%addvis*ft_h*geo%cvvol*geo%cvbb(:, 0)*result11&
&       *geo%cvhz*wrk1
!
!       ..zero out in guard cells
      smbvh(mpg%nci+1:ncv, 0) = 0.0_R8
!
!       ..charge state correction
      IF (switch%zhcscorr .GT. 0 .AND. switch%zhcscorr_vq .GT. 0) THEN
!
!         ..gradient qipgen in cell centers
        CALL GRAD_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, qipgen, &
&                           wrkv, wrkf0)
!
!         ..calculate the difference from parallel heat flux of ion
        wrk0 = na(:, isb)/nal(:, s2nucl(isb)-1)
        CALL INTFACE(ncv, nfc, mpg%fccv, wght, wrk0, wrkf1)
        CALL INTFACE(ncv, nfc, mpg%fccv, wght, ia(:, isb), wrkf2)
!
        wrkf0 = wrkf1/(geo%fcbb(:, 0)*geo%fcbb(:, 3))*vsaf_hbdp_al(:, 0&
&         , s2nucl(isb)-1)*(wrkf0/wrkf2-wrkns(:, s2nucl(isb)-1))
!
!         ..calculate the r.h.s. contribution to the parallel balance
!           corresponding to parallel viscosity connected with heat flow
        CALL INTCELL_NODIFF_NODIFF(nfc, ncv, mpg, mpg%intcellp, wrkf0, &
&                            wrk0)
!zero out spurious contributions outside of separatrix
!wdk todo: rationalize this when separatrix-structure is introduced?
        CALL GRADC_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk0, &
&                            wrkv, wrk1)
!
        DO icv=1,ncv
          IF (.NOT.mpg%cvonclosedsurface(icv)) wrk1(icv) = 0.0_R8
!calculate the actual source

        END DO
!
        arg10 = geo%cvbb(:, 3)
        result11 = SQRT(arg10)
        smbvh(:, 0) = smbvh(:, 0) + switch%addvis*ft_h*geo%cvvol*geo%&
&         cvbb(:, 0)*result11*geo%cvhz*wrk1
!
!         ..zero out in guard cells
        smbvh(mpg%nci+1:ncv, 0) = 0.0_R8
!
      END IF
    END IF
!
!
    IF (switch%addvis1 .NE. 0.0_R8) THEN
!srv 19.01.05 30.01.10
!
!       ..prepare wrk arrays
! wrk2 is used below twice
      wrk2 = SQRT(geo%cvbb(:, 3))
      arg1 = geo%cvhz*wrk2
      wrk1 = LOG(arg1)
!
!       ..poloidal differences of wrk1 on cell faces
      arg11 = geo%vxbb(:, 3)
      result12 = SQRT(arg11)
      arg21 = geo%vxhz*result12 + 1.0e-30_R8
      wrkv = LOG(arg21)
      CALL DIFF_P_NODIFF_NODIFF(ncv, nfc, nvx, 1, geo, mpg, wrk1, wrkv, &
&                         wrkf)
!
!       ..compute flow and its divergence
      IF (switch%zh_vis_type .EQ. 2) THEN
        arg12 = geo%fcbb(:, 3)
        result13 = SQRT(arg12)
        wrkf = wrkf*cvsbhz_cl(:, 0)/result13
        CALL DIV0_NODIFF_NODIFF(ncv, nfc, mpg, wrkf, wrk0)
      ELSE
        arg12 = geo%fcbb(:, 3)
        result13 = SQRT(arg12)
        wrkf = wrkf*cvsbhz_drho(:, 0)/result13
        CALL DIV0_NODIFF_NODIFF(ncv, nfc, mpg, wrkf, wrk0)
      END IF
!
!       ..add to r.h.s. source
      IF (switch%zh_vis_type .EQ. 0 .OR. switch%zh_vis_type .EQ. 2) THEN
        wrk1 = ft*wrk0*wrk2
      ELSE IF (switch%zh_vis_type .EQ. 1) THEN
        wrk1 = ft*wrk0*wrk2*rho_rel
      ELSE
        CALL XERRAB('wrong value of zh_vis_type')
      END IF
      DO icv=1,mpg%nci
        t0 = wrk1(icv)
        IF (t0 .GT. 0.0e0_R8) THEN
          smbvv(icv, 0) = t0*ua(icv, isb)
        ELSE
          smbvv(icv, 1) = t0
        END IF
!srv 19.01.05 }

      END DO
!
!
!       ..viscosity source with contribution from other ion velocities  !som 13.08.21
!
!       ..calculate part of the mass-averaged velocity which depends on the other
!         ion velocities
      IF (switch%zh_vis_type .EQ. 1) THEN
!
        u_res_b = (avm_u-rho_rel*ua(:, isb))*wrk2
!
!         ..gradient u_res in cell centers
        CALL GRAD_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, u_res_b, &
&                           wrkv, wrkf0)
!
!         ..calculate the difference from parallel heat flux of ion
        wrkf0 = wrkf0*vsbf_drho(:, 0)/(geo%fcbb(:, 0)*geo%fcbb(:, 3))
!
!         ..calculate the r.h.s. contribution to the parallel balance
!           corresponding to parallel viscosity connected with heat flow
        CALL INTCELL_NODIFF_NODIFF(nfc, ncv, mpg, mpg%intcellp, wrkf0, &
&                            wrk0)
!calculate the actual source
        CALL GRADC_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk0, &
&                            wrkv, wrk1)
!
        smbvi(:, 0) = ft*wrk1*wrk2*geo%cvvol*geo%cvbb(:, 0)*geo%cvhz
!
!         ..zero out in guard cells
        smbvi(mpg%nci+1:ncv, 0) = 0.0_R8
!
      ELSE IF (switch%zh_vis_type .EQ. 2) THEN
!
        wrkf = 0.0_R8
        DO inucl=0,nspecies-1
!
          IF (inucl .NE. s2nucl(isb) - 1) THEN
!
            wrkf0 = 0.0_R8
            DO iz=1,znnucl_int(inucl)
!
              u_res_b = ua(:, nucl2s(inucl, iz))*wrk2
              CALL GRAD_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, &
&                                 u_res_b, wrkv, wrkf1)
!
              wrk1 = na(:, nucl2s(inucl, iz))*ti
              CALL INTFACE(ncv, nfc, mpg%fccv, wght, wrk1, wrkf2)
!
              wrkf0 = wrkf0 + wrkf1*wrkf2
!
            END DO
!
            wrkf = wrkf + wrkf0*vsaf_uadp_albe(:, 0, s2nucl(isb)-1, &
&             inucl)
!
          END IF
        END DO
!
        wrkf0 = 0.0_R8
!wdk not clear if Zh-closure applies to bundling;
!wdk if yes: need to update if-condition here
        DO iz=1,znnucl_int(s2nucl(isb)-1)
          result1 = MAXVAL(rzb)
          result2 = MINVAL(rzb)
          IF (iz .NE. NINT(result1) .OR. iz .NE. NINT(result2)) THEN
!
            u_res_b = ua(:, nucl2s(s2nucl(isb)-1, iz))*wrk2
            CALL GRAD_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, &
&                               u_res_b, wrkv, wrkf1)
!
            wrk1 = na(:, nucl2s(s2nucl(isb)-1, iz))*ti
            CALL INTFACE(ncv, nfc, mpg%fccv, wght, wrk1, wrkf2)
!
            wrkf0 = wrkf0 + wrkf1*wrkf2
!
          END IF
        END DO
!
!wdk common multipliers
        wrkf = wrkf + wrkf0*(vsaf_uadp_albe(:, 0, s2nucl(isb)-1, s2nucl(&
&         isb)-1)-switch%zhcscorr*vsaf_ubdp_al(:, 0, s2nucl(isb)-1))
!
        wrk1 = na(:, isb)/nal(:, s2nucl(isb)-1)
        CALL INTFACE(ncv, nfc, mpg%fccv, wght, wrk1, wrkf1)
!
        wrkf = wrkf*wrkf1/(geo%fcbb(:, 0)*geo%fcbb(:, 3))
!
!         ..calculate the r.h.s. contribution to the parallel balance
!           corresponding to parallel viscosity connected with heat flow
        CALL INTCELL_NODIFF_NODIFF(nfc, ncv, mpg, mpg%intcellp, wrkf, &
&                            wrk0)
!calculate the actual source
        CALL GRADC_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrk0, &
&                            wrkv, wrk1)
!
        smbvi(:, 0) = ft*wrk1*wrk2*geo%cvvol*geo%cvbb(:, 0)*geo%cvhz
!
!         ..zero out in guard cells
        smbvi(mpg%nci+1:ncv, 0) = 0.0_R8
!
      END IF
    END IF
  END IF
!
!
!srv 17.06.02 }
!
  IF (switch%b2siav_iout .NE. 0) THEN
!srv 17.06.02 {
    WRITE(chns, '(i3.3)') isb
    DO k=0,3
      WRITE(chk, '(i1)') k
      arg13 = 'b2siav_smovv'//chk//chns
      CALL MY_OUT_US(70, ncv, 0, smbvv(1, k), arg13)
      arg13 = 'b2siav_smovh'//chk//chns
      CALL MY_OUT_US(70, ncv, 0, smbvh(1, k), arg13)
    END DO
    wrks = smbvv(:, 0) + smbvv(:, 1)*ua(:, isb) + smbvv(:, 2)*na(:, isb)&
&     *mb + smbvv(:, 3)*ua(:, isb)*na(:, isb)*mb
    arg13 = 'b2siav_smovv'//chns
    CALL MY_OUT_US(70, ncv, 0, wrks, arg13)
    wrks = smbvh(:, 0) + smbvh(:, 1)*ua(:, isb) + smbvh(:, 2)*na(:, isb)&
&     *mb + smbvh(:, 3)*ua(:, isb)*na(:, isb)*mb
    arg13 = 'b2siav_smovh'//chns
    CALL MY_OUT_US(70, ncv, 0, wrks, arg13)
    arg14 = 'b2siav_cvsbhz_drho_th'//chns
    CALL MY_OUT_US(70, nfc, 1, cvsbhz_drho(1, 0), arg14)
    arg15 = 'b2siav_cvsbhz_drho_r'//chns
    CALL MY_OUT_US(70, nfc, 1, cvsbhz_drho(1, 1), arg15)
  END IF
!
!djm Jan2017 Store linearised sources for balance
  IF (balance_netcdf .NE. 0) THEN
    b2siav_smovh0to3(1:ncv, 0:3, isb) = smbvh
    b2siav_smovv0to3(1:ncv, 0:3, isb) = smbvv
  END IF
!
! ..return
  ncall_b2siav = ncall_b2siav + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2siav_zh
!
END SUBROUTINE B2SIAV_ZH_NODIFF_NODIFF

