&             .OR. bccon(is, ib) .EQ. 2) .OR. bccon(is, ib) .EQ. 3) .OR.&
&             bccon(is, ib) .EQ. 4) .OR. bccon(is, ib) .EQ. 9) .OR. &
&             bccon(is, ib) .EQ. 10) .OR. bccon(is, ib) .EQ. 14) .OR. &
