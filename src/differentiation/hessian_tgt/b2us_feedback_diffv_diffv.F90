&       .EQ. 8)) .OR. (fb_actuator(ifb) .EQ. 6 .AND. bcpot(fb_ib(ifb)) &
&       .EQ. 8)) .OR. (fb_actuator(ifb) .EQ. 7 .AND. (bccon(fb_species(&
&       ifb), fb_ib(ifb)) .EQ. 1 .OR. bccon(fb_species(ifb), fb_ib(ifb))&
&       .EQ. 28)))
    END DO
