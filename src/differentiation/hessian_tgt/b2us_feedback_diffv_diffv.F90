          fbregp(ifb, 1) = ifbreg
          fbregp(ifb, 2) = fb_regp(ifb, 2)
          fbreg(ifbreg:ifbreg+fbregp(ifb, 2)-1) = fb_reg(fb_regp(ifb, 1)&
&           :fb_regp(ifb, 1)+fb_regp(ifb, 2)-1)
          ifbreg = ifbreg + fb_regp(ifb, 2)
          done = .true.
        END IF
