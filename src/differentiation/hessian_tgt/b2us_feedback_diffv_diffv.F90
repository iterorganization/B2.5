!                saved_fb_actuatord fb_target fb_prev fb_current
!                fb_prevd fb_currentd fb_constd fb_const charge_frac
!                fb_targetd saved_fb_actuator fb_rescale charge_fracd
!                fb_rescaled *(psnc.na) *(psnc.ne) *(psnc.ni) *(psnc.kinrgy)
!                *(dv.fna) *(dv.fna_mdf) *(dv.fna_32) *(dv.fna_he)
!                *(dv.fnapsch) *(dv.fna_fcor) *(dv.fna_eir) *(dv.kinrgy)
!                *(dv.ne) *(dv.ni) *(dv.nn) *(psnl.na) *(psnl.ne)
!                *(psnl.ni) *(psnl.kinrgy) *(dvd.fna) *(dvd.fna_mdf)
!                *(dvd.fna_32) *(dvd.fna_he) *(dvd.fnapsch) *(dvd.fna_fcor)
!                *(dvd.fna_eir) *(dvd.kinrgy) *(dvd.ne) *(psnld.na)
!                *(psnld.ne) *(psnld.ni) *(psnld.kinrgy) *(pld.na)
!                *(pl.na) *(psncd.na) *(psncd.ne) *(psncd.ni) *(psncd.kinrgy)
!   with respect to varying inputs: potpard enepar conpard enipard
!                enepard conpar potpar enipar userfluxparm userfluxparmd
!                saved_fb_actuatord fb_target fb_prev fb_current
!                fb_prevd fb_currentd fb_constd fb_const charge_frac
!                fb_targetd saved_fb_actuator fb_rescale charge_fracd
!                fb_rescaled *(psnc.na) *(psnc.ne) *(psnc.ni) *(psnc.kinrgy)
!                *(dv.fch) *(dv.fna) *(dv.fna_mdf) *(dv.fna_32)
!                *(dv.fna_he) *(dv.fnapsch) *(dv.fna_fcor) *(dv.fna_eir)
!                *(dv.fhe) *(dv.fhi) *(dv.kinrgy) *(dv.ne) *(dv.ni)
!                *(dv.nn) *(psnl.na) *(psnl.ne) *(psnl.ni) *(psnl.kinrgy)
!                *(rtd.rza) *(rt.rza) *(dvd.fch) *(dvd.fna) *(dvd.fna_mdf)
!                *(dvd.fna_32) *(dvd.fna_he) *(dvd.fnapsch) *(dvd.fna_fcor)
!                *(dvd.fna_eir) *(dvd.fhe) *(dvd.fhi) *(dvd.kinrgy)
!                *(dvd.ne) *(dvd.ni) *(psnld.na) *(psnld.ne) *(psnld.ni)
!                *(psnld.kinrgy) *(pld.na) *(pl.na) *(psncd.na)
!                *(psncd.ne) *(psncd.ni) *(psncd.kinrgy)
!   Plus diff mem management of: psnc.na:in psnc.ne:in psnc.ni:in
!                psnc.fna:in psnc.kinrgy:in dv.fch:in dv.fna:in
!                dv.fna_mdf:in dv.fna_52:in dv.fna_32:in dv.fna_nodrift:in
!                dv.fna_he:in dv.fnapsch:in dv.fna_fcor:in dv.fna_eir:in
!                dv.fhe:in dv.fhi:in dv.kinrgy:in dv.ne:in dv.ni:in
!                dv.nn:in psnl.na:in psnl.ne:in psnl.ni:in psnl.fna:in
!                psnl.kinrgy:in rtd.rza:in rt.rza:in dvd.fch:in
!                dvd.fna:in dvd.fna_mdf:in dvd.fna_32:in dvd.fna_he:in
!                dvd.fnapsch:in dvd.fna_fcor:in dvd.fna_eir:in
!                dvd.fhe:in dvd.fhi:in dvd.kinrgy:in dvd.ne:in
!                dvd.ni:in psnld.na:in psnld.ne:in psnld.ni:in
!                psnld.kinrgy:in pld.na:in pl.na:in psncd.na:in
!                psncd.ne:in psncd.ni:in psncd.kinrgy:in
!  Differentiation of compute_feedback in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: enepar conpar potpar enipar
!                userfluxparm fb_target fb_prev fb_current fb_const
!                charge_frac saved_fb_actuator fb_rescale *(psnc.na)
!                *(psnc.ne) *(psnc.ni) *(psnc.kinrgy) *(dv.fna)
!                *(dv.fna_mdf) *(dv.fna_32) *(dv.fna_he) *(dv.fnapsch)
!                *(dv.fna_fcor) *(dv.fna_eir) *(dv.kinrgy) *(dv.ne)
!                *(psnl.na) *(psnl.ne) *(psnl.ni) *(psnl.kinrgy)
!                *(pl.na)
!   with respect to varying inputs: enepar conpar potpar enipar
!                userfluxparm fb_target fb_prev fb_current fb_const
!                charge_frac saved_fb_actuator fb_rescale *(psnc.na)
!                *(psnc.ne) *(psnc.ni) *(psnc.kinrgy) *(dv.fch)
!                *(dv.fna) *(dv.fna_mdf) *(dv.fna_32) *(dv.fna_he)
!                *(dv.fnapsch) *(dv.fna_fcor) *(dv.fna_eir) *(dv.fhe)
!                *(dv.fhi) *(dv.kinrgy) *(dv.ne) *(dv.ni) *(psnl.na)
!                *(psnl.ne) *(psnl.ni) *(psnl.kinrgy) *(rt.rza)
!                *(pl.na)
!   Plus diff mem management of: psnc.na:in psnc.ne:in psnc.ni:in
!                psnc.fna:in psnc.kinrgy:in dv.fch:in dv.fna:in
!                dv.fna_mdf:in dv.fna_52:in dv.fna_32:in dv.fna_nodrift:in
!                dv.fna_he:in dv.fnapsch:in dv.fna_fcor:in dv.fna_eir:in
!                dv.fhe:in dv.fhi:in dv.kinrgy:in dv.ne:in dv.ni:in-out
!                dv.nn:in-out psnl.na:in psnl.ne:in psnl.ni:in
!                psnl.fna:in psnl.kinrgy:in rt.rza:in pl.na:in
!
  SUBROUTINE COMPUTE_FEEDBACK_DV_DV(ncv, nfc, ns, ismain, switch, geo, &
&   mpg, pl, pld0, pld, pldd, dv, dvd0, dvd, dvdd, rt, rtd0, rtd, rtdd, &
&   psnc, psncd0, psncd, psncdd, psnl, psnld0, psnld, psnldd, main_call&
&   , nbdirs, nbdirs0)
    USE B2MOD_DIFFSIZES
!  Hint: nCv should be the size of dimension 1 of array temp
!  Hint: nbdirsmax0 should be the maximum number of differentiation directions
    IMPLICIT NONE
!
!  Hint: nCv should be the size of dimension 1 of array temp
!  Hint: nbdirsmax should be the maximum number of differentiation directions
!   ..input arguments (unchanged on exit)
    INTEGER :: ncv, nfc, ns, ismain
    TYPE(SWITCHES), INTENT(IN) :: switch
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(B2PLASMA), INTENT(INOUT) :: pl
    TYPE(B2PLASMA_DIFFV0), INTENT(INOUT) :: pld0
    TYPE(B2PLASMA_DIFFV), INTENT(INOUT) :: pld
    TYPE(B2PLASMA_DIFFV_DIFFV), INTENT(INOUT) :: pldd
    TYPE(B2DERIVATIVES), INTENT(INOUT) :: dv
    TYPE(B2DERIVATIVES_DIFFV0), INTENT(INOUT) :: dvd0
    TYPE(B2DERIVATIVES_DIFFV), INTENT(INOUT) :: dvd
    TYPE(B2DERIVATIVES_DIFFV_DIFFV), INTENT(INOUT) :: dvdd
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFFV0), INTENT(IN) :: rtd0
    TYPE(B2RATES_DIFFV), INTENT(IN) :: rtd
    TYPE(B2RATES_DIFFV_DIFFV), INTENT(IN) :: rtdd
    TYPE(B2PLASMASNAPSHOT), INTENT(INOUT) :: psnc, psnl
    TYPE(B2PLASMASNAPSHOT_DIFFV0), INTENT(INOUT) :: psncd0, psnld0
    TYPE(B2PLASMASNAPSHOT_DIFFV), INTENT(INOUT) :: psncd, psnld
    TYPE(B2PLASMASNAPSHOT_DIFFV_DIFFV), INTENT(INOUT) :: psncdd, psnldd
    LOGICAL :: main_call
!   ..local variables
    INTEGER :: ifb, ifc, ifc1, ifc2, icv, ic1, ic2, is, is_start, is_end&
&   , iss
    INTEGER, SAVE :: ncall=0
    REAL(kind=r8) :: sum_e, total_flux, vol_feedback, sum_s
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: sum_ed0, total_fluxd0, &
&   sum_sd0
    REAL(kind=r8), DIMENSION(nbdirsmax) :: sum_ed, total_fluxd, sum_sd
    REAL(kind=r8), DIMENSION(nbdirsmax0, nbdirsmax) :: sum_edd, &
&   total_fluxdd, sum_sdd
    INTRINSIC MAXVAL
    EXTERNAL XERRAB
    INTRINSIC LOG
    INTRINSIC TANH
    INTRINSIC EXP
    INTRINSIC MAX
    INTRINSIC MIN
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    REAL(kind=r8) :: arg1
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: arg1d0
    REAL(kind=r8), DIMENSION(nbdirsmax) :: arg1d
    REAL(kind=r8), DIMENSION(nbdirsmax0, nbdirsmax) :: arg1dd
    REAL(kind=r8) :: arg2
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: arg2d0
    REAL(kind=r8), DIMENSION(nbdirsmax) :: arg2d
    REAL(kind=r8), DIMENSION(nbdirsmax0, nbdirsmax) :: arg2dd
    REAL(kind=r8) :: pwy1
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: pwy1d0
    REAL(kind=r8), DIMENSION(nbdirsmax) :: pwy1d
    REAL(kind=r8), DIMENSION(nbdirsmax0, nbdirsmax) :: pwy1dd
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(kind=r8), DIMENSION(nbdirsmax0) :: tempd
    REAL(r8) :: temp0
    REAL(r8), DIMENSION(nbdirsmax0) :: temp0d
    REAL(kind=r8), DIMENSION(ncv) :: temp1
    REAL(kind=r8), DIMENSION(nbdirsmax0, ncv) :: temp1d
    INTEGER :: nbdirs
    INTEGER :: nd0
    REAL(kind=r8), DIMENSION(def_nsd) :: temp2
    REAL(kind=r8), DIMENSION(def_nsd) :: temp3
    REAL(kind=r8) :: temp4
    REAL(kind=r8) :: temp5
    REAL(r8) :: temp6
    REAL(r8), DIMENSION(nCv) :: temp7
    INTEGER :: nbdirs0
!
    CALL SUBINI('compute_feedback')
    IF (ncall .EQ. 0) THEN
!   ..test nCv, nFc, ns
      CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, &
&           'faulty argument nCv, nFc')
      CALL XERTST(1 .LE. ns, 'faulty argument ns')
      CALL XERTST(0 .LE. ismain .AND. ismain .LT. ns, &
&           'invalid main plasma species index ismain')
      CALL XERTST(.NOT.is_neutral(ismain), &
&           'invalid main plasma species ismain; must not be neutral')
    END IF
!
    result1 = MAXVAL(fb_target(1:nfb))
    result2 = MAXVAL(fb_rescale_option(1:nfb))
    IF (result1 .GT. 0.0_R8 .OR. result2 .GT. 0) THEN
      sum_sdd(:, :) = 0.0_8
      arg1dd(:, :) = 0.0_8
      arg2dd(:, :) = 0.0_8
      total_fluxdd(:, :) = 0.0_8
      sum_edd(:, :) = 0.0_8
      pwy1dd(:, :) = 0.0_8
! we have a request for feedback control
! the idea here is to break the actions into 3 phases
! 1/ calculate something based on the present state of the plasma depending on
!    "fb_type"
! 2/ compare this with the reference value and calculate a change
! 3/ apply this in some way based on "fb_actuator"
!
      DO ifb=1,nfb
        IF (fb_type(ifb) .GT. 0 .AND. fb_rescale_option(ifb) .GT. 0 &
&           .AND. fb_actuator(ifb) .GT. 0) THEN
!
          IF (main_call) THEN
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_prevdd(nd0, nd, ifb) = saved_fb_actuatordd(nd0, nd, &
&                 ifb)
              END DO
              fb_prevd(nd, ifb) = saved_fb_actuatord(nd, ifb)
            END DO
            DO nd0=1,nbdirs0
              fb_prevd0(nd0, ifb) = saved_fb_actuatord0(nd0, ifb)
            END DO
            fb_prev(ifb) = saved_fb_actuator(ifb)
          END IF
!
!
! phase 1:
          SELECT CASE  (fb_type(ifb)) 
          CASE (0) 

          CASE (1) 
!
! no action
!
! local density
!
            IF (ncall .EQ. 0) WRITE(*, *) &
&                             'compute_feedback_type: using ', &
&                             'local_density for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_currentdd(nd0, nd, ifb) = 0.0_8
              END DO
              fb_currentd(nd, ifb) = 0.d0
            END DO
            DO nd0=1,nbdirs0
              fb_currentd0(nd0, ifb) = 0.0_8
            END DO
            fb_current(ifb) = 0.0_R8
            vol_feedback = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                ic1 = fbregp(ifb, 1)
                ic2 = ic1 + fbregp(ifb, 2) - 1
                DO icv=ic1,ic2
                  DO nd=1,nbdirs
                    DO nd0=nd,nbdirs0
                      fb_currentdd(nd0, nd, ifb) = fb_currentdd(nd0, nd&
&                       , ifb) + geo%cvvol(fbreg(icv))*pldd%na(nd0, nd, &
&                       fbreg(icv), is)
                    END DO
                    fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + geo%&
&                     cvvol(fbreg(icv))*pld%na(nd, fbreg(icv), is)
                  END DO
                  DO nd0=1,nbdirs0
                    fb_currentd0(nd0, ifb) = fb_currentd0(nd0, ifb) + &
&                     geo%cvvol(fbreg(icv))*pld0%na(nd0, fbreg(icv), is)
                  END DO
                  fb_current(ifb) = fb_current(ifb) + pl%na(fbreg(icv), &
&                   is)*geo%cvvol(fbreg(icv))
                  IF (is .EQ. is_start) vol_feedback = vol_feedback + &
&                     geo%cvvol(fbreg(icv))
                END DO
                is_end = is
              END IF
            END DO
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_currentdd(nd0, nd, ifb) = fb_currentdd(nd0, nd, ifb)/&
&                 vol_feedback
              END DO
              fb_currentd(nd, ifb) = fb_currentd(nd, ifb)/vol_feedback
            END DO
            DO nd0=1,nbdirs0
              fb_currentd0(nd0, ifb) = fb_currentd0(nd0, ifb)/&
&               vol_feedback
            END DO
            fb_current(ifb) = fb_current(ifb)/vol_feedback
          CASE (2) 
!
! local electron density
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'local_electron_density for sequence ', &
&                             b2espcr(fb_species(ifb))
!
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_currentdd(nd0, nd, ifb) = 0.0_8
              END DO
              fb_currentd(nd, ifb) = 0.d0
            END DO
            DO nd0=1,nbdirs0
              fb_currentd0(nd0, ifb) = 0.0_8
            END DO
            fb_current(ifb) = 0.0_R8
            vol_feedback = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
            ic1 = fbregp(ifb, 1)
            ic2 = ic1 + fbregp(ifb, 2) - 1
            DO icv=ic1,ic2
              DO nd=1,nbdirs
                DO nd0=nd,nbdirs0
                  fb_currentdd(nd0, nd, ifb) = fb_currentdd(nd0, nd, ifb&
&                   ) + geo%cvvol(fbreg(icv))*dvdd%ne(nd0, nd, fbreg(icv&
&                   ))
                END DO
                fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + geo%cvvol(&
&                 fbreg(icv))*dvd%ne(nd, fbreg(icv))
              END DO
              DO nd0=1,nbdirs0
                fb_currentd0(nd0, ifb) = fb_currentd0(nd0, ifb) + geo%&
&                 cvvol(fbreg(icv))*dvd0%ne(nd0, fbreg(icv))
              END DO
              fb_current(ifb) = fb_current(ifb) + dv%ne(fbreg(icv))*geo%&
&               cvvol(fbreg(icv))
              vol_feedback = vol_feedback + geo%cvvol(fbreg(icv))
            END DO
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_currentdd(nd0, nd, ifb) = fb_currentdd(nd0, nd, ifb)/&
&                 vol_feedback
              END DO
              fb_currentd(nd, ifb) = fb_currentd(nd, ifb)/vol_feedback
            END DO
            DO nd0=1,nbdirs0
              fb_currentd0(nd0, ifb) = fb_currentd0(nd0, ifb)/&
&               vol_feedback
            END DO
            fb_current(ifb) = fb_current(ifb)/vol_feedback
          CASE (3) 
!
! nesepm electron density
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'nesepm_electron_density for sequence ', &
&                             b2espcr(fb_species(ifb))
!
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_currentdd(nd0, nd, ifb) = 0.5_R8*(dvdd%ne(nd0, nd, &
&                 omp(icsepomp-1))+dvdd%ne(nd0, nd, omp(icsepomp)))
              END DO
              fb_currentd(nd, ifb) = 0.5_R8*(dvd%ne(nd, omp(icsepomp-1))&
&               +dvd%ne(nd, omp(icsepomp)))
            END DO
            DO nd0=1,nbdirs0
              fb_currentd0(nd0, ifb) = 0.5_R8*(dvd0%ne(nd0, omp(icsepomp&
&               -1))+dvd0%ne(nd0, omp(icsepomp)))
            END DO
            fb_current(ifb) = 0.5_R8*(dv%ne(omp(icsepomp-1))+dv%ne(omp(&
&             icsepomp)))
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (4) 
!
! total particle content for that sequence
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'total particle content for sequence ', &
&                             b2espcr(fb_species(ifb))
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_currentdd(nd0, nd, ifb) = 0.0_8
              END DO
              fb_currentd(nd, ifb) = 0.d0
            END DO
            DO nd0=1,nbdirs0
              fb_currentd0(nd0, ifb) = 0.0_8
            END DO
            fb_current(ifb) = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                ic1 = fbregp(ifb, 1)
                ic2 = ic1 + fbregp(ifb, 2) - 1
                DO icv=ic1,ic2
                  IF (mpg%cvreg(fbreg(icv)) .NE. 0 .AND. fbreg(icv) .LE.&
&                     mpg%nci) THEN
                    DO nd=1,nbdirs
                      DO nd0=nd,nbdirs0
                        fb_currentdd(nd0, nd, ifb) = fb_currentdd(nd0, &
&                         nd, ifb) + geo%cvvol(fbreg(icv))*pldd%na(nd0, &
&                         nd, fbreg(icv), is)
                      END DO
                      fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + geo%&
&                       cvvol(fbreg(icv))*pld%na(nd, fbreg(icv), is)
                    END DO
                    DO nd0=1,nbdirs0
                      fb_currentd0(nd0, ifb) = fb_currentd0(nd0, ifb) + &
&                       geo%cvvol(fbreg(icv))*pld0%na(nd0, fbreg(icv), &
&                       is)
                    END DO
                    fb_current(ifb) = fb_current(ifb) + pl%na(fbreg(icv)&
&                     , is)*geo%cvvol(fbreg(icv))
                  END IF
                END DO
                is_end = is
              END IF
            END DO
          CASE (5) 
!
! total ion content for that sequence (not including neutrals)
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'total ion content for sequence ', b2espcr&
&                             (fb_species(ifb))
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_currentdd(nd0, nd, ifb) = 0.0_8
              END DO
              fb_currentd(nd, ifb) = 0.d0
            END DO
            DO nd0=1,nbdirs0
              fb_currentd0(nd0, ifb) = 0.0_8
            END DO
            fb_current(ifb) = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                IF (.NOT.is_neutral(is)) THEN
                  ic1 = fbregp(ifb, 1)
                  ic2 = ic1 + fbregp(ifb, 2) - 1
                  DO icv=ic1,ic2
                    IF (mpg%cvreg(fbreg(icv)) .NE. 0 .AND. fbreg(icv) &
&                       .LE. mpg%nci) THEN
                      DO nd=1,nbdirs
                        DO nd0=nd,nbdirs0
                          fb_currentdd(nd0, nd, ifb) = fb_currentdd(nd0&
&                           , nd, ifb) + geo%cvvol(fbreg(icv))*pldd%na(&
&                           nd0, nd, fbreg(icv), is)
                        END DO
                        fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + &
&                         geo%cvvol(fbreg(icv))*pld%na(nd, fbreg(icv), &
&                         is)
                      END DO
                      DO nd0=1,nbdirs0
                        fb_currentd0(nd0, ifb) = fb_currentd0(nd0, ifb) &
&                         + geo%cvvol(fbreg(icv))*pld0%na(nd0, fbreg(icv&
&                         ), is)
                      END DO
                      fb_current(ifb) = fb_current(ifb) + pl%na(fbreg(&
&                       icv), is)*geo%cvvol(fbreg(icv))
                    END IF
                  END DO
                  is_end = is
                END IF
              END IF
            END DO
          CASE (6) 
!
! neutral particle flux through the core boundary (??)
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'core boundary flux for sequence ', &
&                             b2espcr(fb_species(ifb))
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_currentdd(nd0, nd, ifb) = fb_constdd(nd0, nd, ifb)
              END DO
              fb_currentd(nd, ifb) = fb_constd(nd, ifb)
            END DO
            DO nd0=1,nbdirs0
