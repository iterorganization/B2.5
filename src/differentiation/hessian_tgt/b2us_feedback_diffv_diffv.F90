&               fb_reg_par(ifb, 2) .EQ. fb_reg_par(ifbb, 2)) THEN
              fbregp(ifb, :) = fbregp(ifbb, :)
              done = .true.
            END IF
          END DO
          IF (done) THEN
            GOTO 100
          ELSE
