                    saved_fb_actuatordd(nd0, nd, ifb) = &
&                     saved_fb_actuatord(nd, ifb)*fb_rescaled0(nd0, ifb)&
&                     + fb_rescale(ifb)*saved_fb_actuatordd(nd0, nd, ifb&
&                     ) + fb_rescaled(nd, ifb)*saved_fb_actuatord0(nd0, &
&                     ifb) + saved_fb_actuator(ifb)*fb_rescaledd(nd0, nd&
&                     , ifb)
                  END DO
                  saved_fb_actuatord(nd, ifb) = fb_rescale(ifb)*&
&                   saved_fb_actuatord(nd, ifb) + saved_fb_actuator(ifb)&
&                   *fb_rescaled(nd, ifb)
                END DO
                DO nd0=1,nbdirs0
                  saved_fb_actuatord0(nd0, ifb) = fb_rescale(ifb)*&
&                   saved_fb_actuatord0(nd0, ifb) + saved_fb_actuator(&
&                   ifb)*fb_rescaled0(nd0, ifb)
                END DO
                saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*&
&                 fb_rescale(ifb)
              ELSE
                DO nd=1,nbdirs
                  DO nd0=nd,nbdirs0
                    saved_fb_actuatordd(nd0, nd, ifb) = fb_rescaledd(nd0&
&                     , nd, ifb)
                  END DO
                  saved_fb_actuatord(nd, ifb) = fb_rescaled(nd, ifb)
                END DO
                DO nd0=1,nbdirs0
                  saved_fb_actuatord0(nd0, ifb) = fb_rescaled0(nd0, ifb)
                END DO
                saved_fb_actuator(ifb) = fb_rescale(ifb)
              END IF
!
              DO nd=1,nbdirs
                DO nd0=nd,nbdirs0
                  conpardd(nd0, nd, fb_species(ifb), fb_ib(ifb), 1) = &
&                   saved_fb_actuatordd(nd0, nd, ifb)
                END DO
                conpard(nd, fb_species(ifb), fb_ib(ifb), 1) = &
&                 saved_fb_actuatord(nd, ifb)
              END DO
              DO nd0=1,nbdirs0
                conpard0(nd0, fb_species(ifb), fb_ib(ifb), 1) = &
&                 saved_fb_actuatord0(nd0, ifb)
              END DO
              conpar(fb_species(ifb), fb_ib(ifb), 1) = saved_fb_actuator&
&               (ifb)
            CASE DEFAULT
!
              WRITE(*, *) 'fb_actuator not coded ', fb_type(ifb)
            END SELECT
!
            IF (fb_actuator(ifb) .EQ. 1 .OR. fb_actuator(ifb) .EQ. 3) &
&             WRITE(*, '(a,1p,i3,g15.6)') 'feedback_actuator ', b2espcr(&
&             fb_species(ifb)), saved_fb_actuator(ifb)
          CASE (1) 
!
! no action
!
! rescale slowed by na_feedback_alpha
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_option: using ', &
&                             'slowed_rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            temp4 = fb_target(ifb)/fb_current(ifb)
            DO nd0=1,nbdirs0
              tempd(nd0) = (fb_targetd0(nd0, ifb)-temp4*fb_currentd0(nd0&
&               , ifb))/fb_current(ifb)
            END DO
            temp = temp4
            DO nd=1,nbdirs
              temp4 = (fb_alpha(ifb)+1.0_R8)*fb_current(ifb)
              temp5 = (fb_targetd(nd, ifb)-temp*fb_currentd(nd, ifb))/&
&               temp4
              DO nd0=1,nbdirs0
                fb_rescaledd(nd0, nd, ifb) = fb_alpha(ifb)*(fb_targetdd(&
&                 nd0, nd, ifb)-fb_currentd(nd, ifb)*tempd(nd0)-temp*&
&                 fb_currentdd(nd0, nd, ifb)-temp5*(fb_alpha(ifb)+1.0_R8&
&                 )*fb_currentd0(nd0, ifb))/temp4
              END DO
              fb_rescaled(nd, ifb) = fb_alpha(ifb)*temp5
            END DO
            DO nd0=1,nbdirs0
              fb_rescaled0(nd0, ifb) = fb_alpha(ifb)*tempd(nd0)/(&
&               fb_alpha(ifb)+1.0_R8)
            END DO
            fb_rescale(ifb) = (fb_alpha(ifb)*temp+1.0_R8)/(fb_alpha(ifb)&
&             +1.0_R8)
          CASE (2) 
!
! pure rescale
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_option: using ', &
&                             'rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            temp5 = fb_target(ifb)/fb_current(ifb)
            DO nd0=1,nbdirs0
              tempd(nd0) = (fb_targetd0(nd0, ifb)-temp5*fb_currentd0(nd0&
&               , ifb))/fb_current(ifb)
            END DO
            temp = temp5
            DO nd=1,nbdirs
              temp5 = (fb_targetd(nd, ifb)-temp*fb_currentd(nd, ifb))/&
&               fb_current(ifb)
              DO nd0=1,nbdirs0
                fb_rescaledd(nd0, nd, ifb) = (fb_targetdd(nd0, nd, ifb)-&
&                 fb_currentd(nd, ifb)*tempd(nd0)-temp*fb_currentdd(nd0&
&                 , nd, ifb)-temp5*fb_currentd0(nd0, ifb))/fb_current(&
&                 ifb)
              END DO
              fb_rescaled(nd, ifb) = temp5
            END DO
            DO nd0=1,nbdirs0
              fb_rescaled0(nd0, ifb) = tempd(nd0)
            END DO
            fb_rescale(ifb) = temp
          CASE (3) 
!
! rescale slowed by tanh_log
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_option: using ', &
&                             'tanh_log_rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
! 2**(tanh(log(x)/b)*log(a)/log(2))
            temp5 = fb_target(ifb)/fb_current(ifb)
            DO nd0=1,nbdirs0
              tempd(nd0) = (fb_targetd0(nd0, ifb)-temp5*fb_currentd0(nd0&
&               , ifb))/fb_current(ifb)
              arg1d0(nd0) = tempd(nd0)
            END DO
            temp = temp5
            arg1 = temp
            DO nd=1,nbdirs
              temp5 = (fb_targetd(nd, ifb)-temp*fb_currentd(nd, ifb))/&
&               fb_current(ifb)
              DO nd0=1,nbdirs0
                arg1dd(nd0, nd) = (fb_targetdd(nd0, nd, ifb)-fb_currentd&
&                 (nd, ifb)*tempd(nd0)-temp*fb_currentdd(nd0, nd, ifb)-&
&                 temp5*fb_currentd0(nd0, ifb))/fb_current(ifb)
              END DO
              arg1d(nd) = temp5
              temp5 = arg1d(nd)/(fb_beta(ifb)*arg1)
              DO nd0=1,nbdirs0
                arg2dd(nd0, nd) = (arg1dd(nd0, nd)-temp5*fb_beta(ifb)*&
&                 arg1d0(nd0))/(fb_beta(ifb)*arg1)
              END DO
              arg2d(nd) = temp5
            END DO
            DO nd0=1,nbdirs0
              arg2d0(nd0) = arg1d0(nd0)/(fb_beta(ifb)*arg1)
            END DO
            arg2 = LOG(arg1)/fb_beta(ifb)
            temp = LOG(fb_alpha(ifb))
            temp0 = LOG(2.0_R8)
            DO nd=1,nbdirs
              temp5 = arg2d(nd)/temp0
              temp4 = -(TANH(arg2)*TANH(arg2)) + 1.0
              DO nd0=1,nbdirs0
                pwy1dd(nd0, nd) = temp*(temp4*arg2dd(nd0, nd)/temp0-&
&                 temp5*2*TANH(arg2)*(1.0-TANH(arg2)**2)*arg2d0(nd0))
              END DO
              pwy1d(nd) = temp*(temp4*temp5)
            END DO
            pwy1 = temp*(TANH(arg2)/temp0)
            temp6 = 2.0_R8**pwy1
            DO nd0=1,nbdirs0
              pwy1d0(nd0) = temp*(1.0-TANH(arg2)**2)*arg2d0(nd0)/temp0
              temp0d(nd0) = temp6*LOG(2.0_R8)*pwy1d0(nd0)
            END DO
            temp0 = temp6
            DO nd=1,nbdirs
              temp6 = LOG(2.0_R8)
              DO nd0=1,nbdirs0
                fb_rescaledd(nd0, nd, ifb) = temp6*(pwy1d(nd)*temp0d(nd0&
&                 )+temp0*pwy1dd(nd0, nd))
              END DO
              fb_rescaled(nd, ifb) = temp6*(temp0*pwy1d(nd))
            END DO
            DO nd0=1,nbdirs0
              fb_rescaled0(nd0, ifb) = temp0d(nd0)
            END DO
            fb_rescale(ifb) = temp0
          CASE (4) 
!
! rescale done according to SOLPS4 formula:
! N = A + B*(time-T)
!
            IF (ncall .EQ. 0) THEN
              WRITE(*, '(a,a,i3)') 'compute_feedback_option: using ', &
&             'SOLPS4 linear increase for sequence ', b2espcr(fb_species&
&             (ifb))
              IF (fb_const(ifb) .LT. 0.0_R8) THEN
                DO nd=1,nbdirs
                  DO nd0=nd,nbdirs0
                    fb_constdd(nd0, nd, ifb) = 0.0_8
                  END DO
                  fb_constd(nd, ifb) = 0.d0
                END DO
                DO nd0=1,nbdirs0
                  fb_constd0(nd0, ifb) = 0.0_8
                END DO
                fb_const(ifb) = 0.0_R8
                is_start = -1
                is_end = -1
                DO is=0,ns-1
                  IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                    IF (is_start .EQ. -1) is_start = is
                    ic1 = fbregp(ifb, 1)
                    ic2 = ic1 + fbregp(ifb, 2) - 1
                    DO icv=ic1,ic2
                      IF (mpg%cvreg(fbreg(icv)) .NE. 0 .AND. fbreg(icv) &
&                         .LE. mpg%nci) THEN
                        DO nd=1,nbdirs
                          DO nd0=nd,nbdirs0
                            fb_constdd(nd0, nd, ifb) = fb_constdd(nd0, &
&                             nd, ifb) + geo%cvvol(fbreg(icv))*pldd%na(&
&                             nd0, nd, fbreg(icv), is)
                          END DO
                          fb_constd(nd, ifb) = fb_constd(nd, ifb) + geo%&
&                           cvvol(fbreg(icv))*pld%na(nd, fbreg(icv), is)
                        END DO
                        DO nd0=1,nbdirs0
                          fb_constd0(nd0, ifb) = fb_constd0(nd0, ifb) + &
&                           geo%cvvol(fbreg(icv))*pld0%na(nd0, fbreg(icv&
&                           ), is)
                        END DO
                        fb_const(ifb) = fb_const(ifb) + pl%na(fbreg(icv)&
&                         , is)*geo%cvvol(fbreg(icv))
                      END IF
                    END DO
                    is_end = is
                  END IF
                END DO
              END IF
              DO nd=1,nbdirs
                DO nd0=nd,nbdirs0
                  fb_prevdd(nd0, nd, ifb) = fb_currentdd(nd0, nd, ifb)
                END DO
                fb_prevd(nd, ifb) = fb_currentd(nd, ifb)
              END DO
              DO nd0=1,nbdirs0
                fb_prevd0(nd0, ifb) = fb_currentd0(nd0, ifb)
              END DO
              fb_prev(ifb) = fb_current(ifb)
            END IF
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
!
                fb_targetdd(nd0, nd, ifb) = fb_constdd(nd0, nd, ifb)
                fb_rescaledd(nd0, nd, :) = fb_alpha(ifb)*((fb_targetdd(&
&                 nd0, nd, ifb)-fb_currentdd(nd0, nd, ifb))/ddtim+(&
&                 fb_prevdd(nd0, nd, ifb)-fb_currentdd(nd0, nd, ifb))/&
&                 dt_prev)
                saved_fb_actuatordd(nd0, nd, ifb) = fb_currentdd(nd0, nd&
&                 , ifb)
              END DO
              fb_targetd(nd, ifb) = fb_constd(nd, ifb)
              fb_rescaled(nd, :) = fb_alpha(ifb)*((fb_targetd(nd, ifb)-&
&               fb_currentd(nd, ifb))/ddtim+(fb_prevd(nd, ifb)-&
&               fb_currentd(nd, ifb))/dt_prev)
              saved_fb_actuatord(nd, ifb) = fb_currentd(nd, ifb)
            END DO
            DO nd0=1,nbdirs0
              fb_targetd0(nd0, ifb) = fb_constd0(nd0, ifb)
              fb_rescaled0(nd0, :) = fb_alpha(ifb)*((fb_targetd0(nd0, &
&               ifb)-fb_currentd0(nd0, ifb))/ddtim+(fb_prevd0(nd0, ifb)-&
&               fb_currentd0(nd0, ifb))/dt_prev)
              saved_fb_actuatord0(nd0, ifb) = fb_currentd0(nd0, ifb)
            END DO
            fb_target(ifb) = fb_const(ifb) + fb_beta(ifb)*(tim-fb_time(&
&             ifb))
            fb_rescale = fb_alpha(ifb)*((fb_target(ifb)-fb_current(ifb))&
&             /ddtim+(fb_prev(ifb)-fb_current(ifb))/dt_prev)
            saved_fb_actuator(ifb) = fb_current(ifb)
          CASE (5) 
!
! rescale done according to SOLPS4 formula:
! Nt = A*exp((time-T)*B)
!
            IF (ncall .EQ. 0) THEN
              WRITE(*, *) 'compute_feedback_option: using ', &
&             'SOLPS4 exponential decay for sequence ', b2espcr(&
&             fb_species(ifb))
              IF (fb_const(ifb) .LT. 0.0_R8) THEN
                DO nd=1,nbdirs
                  DO nd0=nd,nbdirs0
                    fb_constdd(nd0, nd, ifb) = 0.0_8
                  END DO
                  fb_constd(nd, ifb) = 0.d0
                END DO
                DO nd0=1,nbdirs0
                  fb_constd0(nd0, ifb) = 0.0_8
                END DO
                fb_const(ifb) = 0.0_R8
                is_start = -1
                is_end = -1
                DO is=0,ns-1
                  IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                    IF (is_start .EQ. -1) is_start = is
                    DO icv=1,mpg%nci
                      IF (mpg%cvreg(icv) .NE. 0) THEN
                        DO nd=1,nbdirs
                          DO nd0=nd,nbdirs0
                            fb_constdd(nd0, nd, ifb) = fb_constdd(nd0, &
&                             nd, ifb) + geo%cvvol(icv)*pldd%na(nd0, nd&
&                             , icv, is)
                          END DO
                          fb_constd(nd, ifb) = fb_constd(nd, ifb) + geo%&
&                           cvvol(icv)*pld%na(nd, icv, is)
                        END DO
                        DO nd0=1,nbdirs0
                          fb_constd0(nd0, ifb) = fb_constd0(nd0, ifb) + &
&                           geo%cvvol(icv)*pld0%na(nd0, icv, is)
                        END DO
                        fb_const(ifb) = fb_const(ifb) + pl%na(icv, is)*&
&                         geo%cvvol(icv)
                      END IF
                    END DO
                    is_end = is
                  END IF
                END DO
              END IF
              DO nd=1,nbdirs
                DO nd0=nd,nbdirs0
                  fb_prevdd(nd0, nd, ifb) = fb_currentdd(nd0, nd, ifb)
                END DO
                fb_prevd(nd, ifb) = fb_currentd(nd, ifb)
              END DO
              DO nd0=1,nbdirs0
                fb_prevd0(nd0, ifb) = fb_currentd0(nd0, ifb)
              END DO
              fb_prev(ifb) = fb_current(ifb)
            END IF
!
            arg1 = fb_beta(ifb)*(tim-fb_time(ifb))
            temp = EXP(arg1)
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_targetdd(nd0, nd, ifb) = temp*fb_constdd(nd0, nd, ifb&
&                 )
                fb_rescaledd(nd0, nd, ifb) = fb_alpha(ifb)*((fb_targetdd&
&                 (nd0, nd, ifb)-fb_currentdd(nd0, nd, ifb))/ddtim+(&
&                 fb_prevdd(nd0, nd, ifb)-fb_currentdd(nd0, nd, ifb))/&
&                 dt_prev)
                saved_fb_actuatordd(nd0, nd, ifb) = fb_currentdd(nd0, nd&
&                 , ifb)
              END DO
              fb_targetd(nd, ifb) = temp*fb_constd(nd, ifb)
              fb_rescaled(nd, ifb) = fb_alpha(ifb)*((fb_targetd(nd, ifb)&
&               -fb_currentd(nd, ifb))/ddtim+(fb_prevd(nd, ifb)-&
&               fb_currentd(nd, ifb))/dt_prev)
              saved_fb_actuatord(nd, ifb) = fb_currentd(nd, ifb)
            END DO
            DO nd0=1,nbdirs0
              fb_targetd0(nd0, ifb) = temp*fb_constd0(nd0, ifb)
              fb_rescaled0(nd0, ifb) = fb_alpha(ifb)*((fb_targetd0(nd0, &
&               ifb)-fb_currentd0(nd0, ifb))/ddtim+(fb_prevd0(nd0, ifb)-&
&               fb_currentd0(nd0, ifb))/dt_prev)
              saved_fb_actuatord0(nd0, ifb) = fb_currentd0(nd0, ifb)
            END DO
            fb_target(ifb) = temp*fb_const(ifb)
            fb_rescale(ifb) = fb_alpha(ifb)*((fb_target(ifb)-fb_current(&
&             ifb))/ddtim+(fb_prev(ifb)-fb_current(ifb))/dt_prev)
            saved_fb_actuator(ifb) = fb_current(ifb)
          CASE (6) 
!
! rescale slowed by fb_alpha (SOLPS4 style)
!
            DO nd=1,nbdirs
              DO nd0=nd,nbdirs0
                fb_rescaledd(nd0, nd, ifb) = fb_alpha(ifb)*fb_currentdd(&
&                 nd0, nd, ifb)
              END DO
              fb_rescaled(nd, ifb) = fb_alpha(ifb)*fb_currentd(nd, ifb)
            END DO
            DO nd0=1,nbdirs0
              fb_rescaled0(nd0, ifb) = fb_alpha(ifb)*fb_currentd0(nd0, &
&               ifb)
            END DO
            fb_rescale(ifb) = fb_alpha(ifb)*fb_current(ifb)
          CASE DEFAULT
!
            WRITE(*, *) 'fb_rescale_option not coded ', &
&           fb_rescale_option(ifb)
          END SELECT
          WRITE(*, '(a,1x,1p,i3,6g15.6)') 'feedback_rescale ', b2espcr&
&           (fb_species(ifb)), fb_current(ifb), fb_target(ifb), &
&           fb_rescale(ifb), fb_prev(ifb), ddtim, dt_prev

        END IF
!
!
        feedback_namelist_used = (feedback_namelist_used .OR. &
&         fb_actuator(ifb) .EQ. 1) .OR. fb_actuator(ifb) .EQ. 3
      END DO
!
!ifb
!
      dt_prev = ddtim
    END IF
!
!
    ncall = ncall + 1
    CALL SUBEND()
    RETURN
  END SUBROUTINE COMPUTE_FEEDBACK_DV_DV

!  Differentiation of compute_feedback in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: enepar conpar potpar enipar
!                userfluxparm fb_target fb_prev fb_current fb_const
!                charge_frac saved_fb_actuator fb_rescale *(psnc.na)
!                *(psnc.ne) *(psnc.ni) *(psnc.kinrgy) *(dv.fna)
!                *(dv.fna_mdf) *(dv.fna_32) *(dv.fna_he) *(dv.fnapsch)
!                *(dv.fna_fcor) *(dv.fna_eir) *(dv.kinrgy) *(dv.ne)
!                *(psnl.na) *(psnl.ne) *(psnl.ni) *(psnl.kinrgy)
!                *(pl.na)
!   with respect to varying inputs: enepar conpar potpar enipar
!                userfluxparm fb_target fb_prev fb_current fb_const
!                charge_frac saved_fb_actuator fb_rescale *(psnc.na)
!                *(psnc.ne) *(psnc.ni) *(psnc.kinrgy) *(dv.fch)
!                *(dv.fna) *(dv.fna_mdf) *(dv.fna_32) *(dv.fna_he)
!                *(dv.fnapsch) *(dv.fna_fcor) *(dv.fna_eir) *(dv.fhe)
!                *(dv.fhi) *(dv.kinrgy) *(dv.ne) *(dv.ni) *(psnl.na)
!                *(psnl.ne) *(psnl.ni) *(psnl.kinrgy) *(rt.rza)
!                *(pl.na)
!   Plus diff mem management of: psnc.na:in psnc.ne:in psnc.ni:in
!                psnc.fna:in psnc.kinrgy:in dv.fch:in dv.fna:in
!                dv.fna_mdf:in dv.fna_52:in dv.fna_32:in dv.fna_nodrift:in
!                dv.fna_he:in dv.fnapsch:in dv.fna_fcor:in dv.fna_eir:in
!                dv.fhe:in dv.fhi:in dv.kinrgy:in dv.ne:in dv.ni:in-out
!                dv.nn:in-out psnl.na:in psnl.ne:in psnl.ni:in
!                psnl.fna:in psnl.kinrgy:in rt.rza:in pl.na:in
!
  SUBROUTINE COMPUTE_FEEDBACK_DV(ncv, nfc, ns, ismain, switch, geo, mpg&
&   , pl, pld, dv, dvd, rt, rtd, psnc, psncd, psnl, psnld, main_call, &
&   nbdirs)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!
!  Hint: nCv should be the size of dimension 1 of array temp
!  Hint: nbdirsmax should be the maximum number of differentiation directions
!   ..input arguments (unchanged on exit)
    INTEGER :: ncv, nfc, ns, ismain
    TYPE(SWITCHES), INTENT(IN) :: switch
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(B2PLASMA), INTENT(INOUT) :: pl
    TYPE(B2PLASMA_DIFFV), INTENT(INOUT) :: pld
    TYPE(B2DERIVATIVES), INTENT(INOUT) :: dv
    TYPE(B2DERIVATIVES_DIFFV), INTENT(INOUT) :: dvd
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFFV), INTENT(IN) :: rtd
    TYPE(B2PLASMASNAPSHOT), INTENT(INOUT) :: psnc, psnl
    TYPE(B2PLASMASNAPSHOT_DIFFV), INTENT(INOUT) :: psncd, psnld
    LOGICAL :: main_call
!   ..local variables
    INTEGER :: ifb, ifc, ifc1, ifc2, icv, ic1, ic2, is, is_start, is_end&
&   , iss
    INTEGER, SAVE :: ncall=0
    REAL(kind=r8) :: sum_e, total_flux, vol_feedback, sum_s
    REAL(kind=r8), DIMENSION(nbdirsmax) :: sum_ed, total_fluxd, sum_sd
    INTRINSIC MAXVAL
    EXTERNAL XERRAB
    INTRINSIC LOG
    INTRINSIC TANH
    INTRINSIC EXP
    INTRINSIC MAX
    INTRINSIC MIN
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    REAL(kind=r8) :: arg1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: arg1d
    REAL(kind=r8) :: arg2
    REAL(kind=r8), DIMENSION(nbdirsmax) :: arg2d
    REAL(kind=r8) :: pwy1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: pwy1d
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(r8) :: temp0
    REAL(kind=r8), DIMENSION(ncv) :: temp1
    INTEGER :: nbdirs
!
    CALL SUBINI('compute_feedback')
    IF (ncall .EQ. 0) THEN
!   ..test nCv, nFc, ns
      CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, &
&           'faulty argument nCv, nFc')
      CALL XERTST(1 .LE. ns, 'faulty argument ns')
      CALL XERTST(0 .LE. ismain .AND. ismain .LT. ns, &
&           'invalid main plasma species index ismain')
      CALL XERTST(.NOT.is_neutral(ismain), &
&           'invalid main plasma species ismain; must not be neutral')
    END IF
!
    result1 = MAXVAL(fb_target(1:nfb))
    result2 = MAXVAL(fb_rescale_option(1:nfb))
    IF (result1 .GT. 0.0_R8 .OR. result2 .GT. 0) THEN
! we have a request for feedback control
! the idea here is to break the actions into 3 phases
! 1/ calculate something based on the present state of the plasma depending on
!    "fb_type"
! 2/ compare this with the reference value and calculate a change
! 3/ apply this in some way based on "fb_actuator"
!
      DO ifb=1,nfb
        IF (fb_type(ifb) .GT. 0 .AND. fb_rescale_option(ifb) .GT. 0 &
&           .AND. fb_actuator(ifb) .GT. 0) THEN
!
          IF (main_call) THEN
            DO nd=1,nbdirs
              fb_prevd(nd, ifb) = saved_fb_actuatord(nd, ifb)
            END DO
            fb_prev(ifb) = saved_fb_actuator(ifb)
          END IF
!
!
! phase 1:
          SELECT CASE  (fb_type(ifb)) 
          CASE (0) 

          CASE (1) 
!
! no action
!
! local density
!
            IF (ncall .EQ. 0) WRITE(*, *) &
&                             'compute_feedback_type: using ', &
&                             'local_density for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            vol_feedback = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                ic1 = fbregp(ifb, 1)
                ic2 = ic1 + fbregp(ifb, 2) - 1
                DO icv=ic1,ic2
                  DO nd=1,nbdirs
                    fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + geo%&
&                     cvvol(fbreg(icv))*pld%na(nd, fbreg(icv), is)
                  END DO
                  fb_current(ifb) = fb_current(ifb) + pl%na(fbreg(icv), &
&                   is)*geo%cvvol(fbreg(icv))
                  IF (is .EQ. is_start) vol_feedback = vol_feedback + &
&                     geo%cvvol(fbreg(icv))
                END DO
                is_end = is
              END IF
            END DO
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = fb_currentd(nd, ifb)/vol_feedback
            END DO
            fb_current(ifb) = fb_current(ifb)/vol_feedback
          CASE (2) 
!
! local electron density
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'local_electron_density for sequence ', &
&                             b2espcr(fb_species(ifb))
!
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            vol_feedback = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
            ic1 = fbregp(ifb, 1)
            ic2 = ic1 + fbregp(ifb, 2) - 1
            DO icv=ic1,ic2
              DO nd=1,nbdirs
                fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + geo%cvvol(&
&                 fbreg(icv))*dvd%ne(nd, fbreg(icv))
              END DO
              fb_current(ifb) = fb_current(ifb) + dv%ne(fbreg(icv))*geo%&
&               cvvol(fbreg(icv))
              vol_feedback = vol_feedback + geo%cvvol(fbreg(icv))
            END DO
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = fb_currentd(nd, ifb)/vol_feedback
            END DO
            fb_current(ifb) = fb_current(ifb)/vol_feedback
          CASE (3) 
!
! nesepm electron density
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'nesepm_electron_density for sequence ', &
&                             b2espcr(fb_species(ifb))
!
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.5_R8*(dvd%ne(nd, omp(icsepomp-1))&
&               +dvd%ne(nd, omp(icsepomp)))
            END DO
            fb_current(ifb) = 0.5_R8*(dv%ne(omp(icsepomp-1))+dv%ne(omp(&
&             icsepomp)))
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (4) 
!
! total particle content for that sequence
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'total particle content for sequence ', &
&                             b2espcr(fb_species(ifb))
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                ic1 = fbregp(ifb, 1)
                ic2 = ic1 + fbregp(ifb, 2) - 1
                DO icv=ic1,ic2
                  IF (mpg%cvreg(fbreg(icv)) .NE. 0 .AND. fbreg(icv) .LE.&
&                     mpg%nci) THEN
                    DO nd=1,nbdirs
                      fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + geo%&
&                       cvvol(fbreg(icv))*pld%na(nd, fbreg(icv), is)
                    END DO
                    fb_current(ifb) = fb_current(ifb) + pl%na(fbreg(icv)&
&                     , is)*geo%cvvol(fbreg(icv))
                  END IF
                END DO
                is_end = is
              END IF
            END DO
          CASE (5) 
!
! total ion content for that sequence (not including neutrals)
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'total ion content for sequence ', b2espcr&
&                             (fb_species(ifb))
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                IF (.NOT.is_neutral(is)) THEN
                  ic1 = fbregp(ifb, 1)
                  ic2 = ic1 + fbregp(ifb, 2) - 1
                  DO icv=ic1,ic2
                    IF (mpg%cvreg(fbreg(icv)) .NE. 0 .AND. fbreg(icv) &
&                       .LE. mpg%nci) THEN
                      DO nd=1,nbdirs
                        fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + &
&                         geo%cvvol(fbreg(icv))*pld%na(nd, fbreg(icv), &
&                         is)
                      END DO
                      fb_current(ifb) = fb_current(ifb) + pl%na(fbreg(&
&                       icv), is)*geo%cvvol(fbreg(icv))
                    END IF
                  END DO
                  is_end = is
                END IF
              END IF
            END DO
          CASE (6) 
!
! neutral particle flux through the core boundary (??)
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'core boundary flux for sequence ', &
&                             b2espcr(fb_species(ifb))
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = fb_constd(nd, ifb)
            END DO
            fb_current(ifb) = fb_const(ifb)
            WRITE(*, *) 'WARNING: fb_type = 6 needs to be'//&
&           ' adapted for hybrid methods!'
!
            CALL XERRAB('fb_type=6 not yet adapted for'//&
&                 ' pure fluid cases')
            is_start = eb2spcr(b2espcr(fb_species(ifb)))
            is_end = eb2spcr(b2espcr(fb_species(ifb))) + nfluids(b2espcr&
&             (fb_species(ifb)))
            charge_frac = 0.0_R8
            sum_s = 0.0_R8
            DO nd=1,nbdirsmax
              charge_fracd(nd, :) = 0.d0
            END DO
            DO nd=1,nbdirsmax
              sum_sd(nd) = 0.d0
            END DO
            DO is=is_start,is_end
              iss = is - is_start + 1
              IF (.NOT.(is_neutral(is) .AND. switch%use_eirene .NE. 0)) &
&             THEN
                ic1 = fbregp(ifb, 1)
                ic2 = ic1 + fbregp(ifb, 2) - 1
                DO icv=ic1,ic2
                  IF (mpg%cvonclosedsurface(icv)) THEN
                    DO nd=1,nbdirs
                      sum_sd(nd) = sum_sd(nd) + geo%cvvol(icv)*pld%na(nd&
&                       , icv, is)
                      charge_fracd(nd, iss) = charge_fracd(nd, iss) + &
&                       geo%cvvol(icv)*pld%na(nd, icv, is)
                    END DO
                    sum_s = sum_s + pl%na(icv, is)*geo%cvvol(icv)
                    charge_frac(iss) = charge_frac(iss) + pl%na(icv, is)&
&                     *geo%cvvol(icv)
                  END IF
                END DO
              END IF
            END DO
            DO nd=1,nbdirs
              charge_fracd(nd, :) = (charge_fracd(nd, :)-charge_frac*&
&               sum_sd(nd)/sum_s)/sum_s
            END DO
            charge_frac = charge_frac/sum_s
          CASE (7) 
!
! relative average concentration of this species at the separatrix
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                       'average separatrix concentration for sequence '&
&                             , b2espcr(fb_species(ifb))
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            sum_e = 0.0_R8
            is_start = -1
            is_end = -1
            DO nd=1,nbdirsmax
              sum_ed(nd) = 0.d0
            END DO
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                IF (.NOT.(is_neutral(is) .AND. switch%use_eirene .NE. 0)&
&               ) THEN
                  ifc1 = mpg%fsfcp(mpg%ifssep, 1)
                  ifc2 = ifc1 + mpg%fsfcp(mpg%ifssep, 2) - 1
! csc Here the idea is to follow the separatrix along its flux surface, then use the two CVs
!     belonging to each face of the flux surface
                  DO ifc=ifc1,ifc2
                    ic1 = mpg%fccv(mpg%fsfc(ifc), 1)
                    ic2 = mpg%fccv(mpg%fsfc(ifc), 2)
                    IF (mpg%cvonclosedsurface(ic1) .OR. mpg%&
&                       cvonclosedsurface(ic2)) THEN
                      DO nd=1,nbdirs
                        fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + &
&                         geo%fcs(mpg%fsfc(ifc))*(pld%na(nd, ic1, is)+&
&                         pld%na(nd, ic2, is))
                      END DO
                      fb_current(ifb) = fb_current(ifb) + (pl%na(ic1, is&
&                       )+pl%na(ic2, is))*geo%fcs(mpg%fsfc(ifc))
                      IF (is .EQ. is_start + 1) THEN
                        DO nd=1,nbdirs
                          sum_ed(nd) = sum_ed(nd) + geo%fcs(mpg%fsfc(ifc&
&                           ))*(dvd%ne(nd, ic1)+dvd%ne(nd, ic2))
                        END DO
                        sum_e = sum_e + (dv%ne(ic1)+dv%ne(ic2))*geo%fcs(&
&                         mpg%fsfc(ifc))
                      END IF
                    END IF
                  END DO
                  IF (mpg%ifssep2 .GT. 0) THEN
                    ifc1 = mpg%fsfcp(mpg%ifssep2, 1)
                    ifc2 = ifc1 + mpg%fsfcp(mpg%ifssep2, 2) - 1
                    DO ifc=ifc1,ifc2
                      ic1 = mpg%fccv(mpg%fsfc(ifc), 1)
                      ic2 = mpg%fccv(mpg%fsfc(ifc), 2)
                      IF (mpg%cvonclosedsurface(ic1) .OR. mpg%&
&                         cvonclosedsurface(ic2)) THEN
                        DO nd=1,nbdirs
                          fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + &
&                           geo%fcs(mpg%fsfc(ifc))*(pld%na(nd, ic1, is)+&
&                           pld%na(nd, ic2, is))
                        END DO
                        fb_current(ifb) = fb_current(ifb) + (pl%na(ic1, &
&                         is)+pl%na(ic2, is))*geo%fcs(mpg%fsfc(ifc))
                        IF (is .EQ. is_start + 1) THEN
                          DO nd=1,nbdirs
                            sum_ed(nd) = sum_ed(nd) + geo%fcs(mpg%fsfc(&
&                             ifc))*(dvd%ne(nd, ic1)+dvd%ne(nd, ic2))
                          END DO
                          sum_e = sum_e + (dv%ne(ic1)+dv%ne(ic2))*geo%&
&                           fcs(mpg%fsfc(ifc))
                        END IF
                      END IF
                    END DO
                  END IF
                  is_end = is
                END IF
              END IF
            END DO
            temp = fb_current(ifb)/sum_e
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = (fb_currentd(nd, ifb)-temp*sum_ed(&
&               nd))/sum_e
            END DO
            fb_current(ifb) = temp
          CASE (8) 
!
! relative average concentration of this species on desired domain
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using average ', &
&                             'concentration for sequence ', b2espcr(&
&                             fb_species(ifb))
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            sum_e = 0.0_R8
            is_start = -1
            is_end = -1
            DO nd=1,nbdirsmax
              sum_ed(nd) = 0.d0
            END DO
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                IF (.NOT.(is_neutral(is) .AND. switch%use_eirene .NE. 0)&
&               ) THEN
                  ic1 = fbregp(ifb, 1)
                  ic2 = ic1 + fbregp(ifb, 2) - 1
                  DO icv=ic1,ic2
                    DO nd=1,nbdirs
                      fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + geo%&
&                       cvvol(fbreg(icv))*pld%na(nd, fbreg(icv), is)
                    END DO
                    fb_current(ifb) = fb_current(ifb) + pl%na(fbreg(icv)&
&                     , is)*geo%cvvol(fbreg(icv))
                    IF (is .EQ. is_start + 1) THEN
                      DO nd=1,nbdirs
                        sum_ed(nd) = sum_ed(nd) + geo%cvvol(fbreg(icv))*&
&                         dvd%ne(nd, fbreg(icv))
                      END DO
                      sum_e = sum_e + dv%ne(fbreg(icv))*geo%cvvol(fbreg(&
&                       icv))
                    END IF
                  END DO
                  is_end = is
                END IF
              END IF
            END DO
            temp = fb_current(ifb)/sum_e
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = (fb_currentd(nd, ifb)-temp*sum_ed(&
&               nd))/sum_e
            END DO
            fb_current(ifb) = temp
          CASE (9) 
!
! inner midplane electron density
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                       'inner_midplance_electron_density for sequence '&
&                             , b2espcr(fb_species(ifb))
!
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.5_R8*(dvd%ne(nd, imp(icsepimp-1))&
&               +dvd%ne(nd, imp(icsepimp)))
            END DO
            fb_current(ifb) = 0.5_R8*(dv%ne(imp(icsepimp-1))+dv%ne(imp(&
&             icsepimp)))
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (10) 
!
! (core) boundary radial particle flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'radial particle flux for sequence ', &
&                             b2espcr(fb_species(ifb))
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            ifc1 = fbregp(ifb, 1)
            ifc2 = ifc1 + fbregp(ifb, 2) - 1
            DO ifc=ifc1,ifc2
              DO nd=1,nbdirs
                fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + dvd%fna(nd&
&                 , fbreg(ifc), 1, fb_species(ifb))
              END DO
              fb_current(ifb) = fb_current(ifb) + dv%fna(fbreg(ifc), 1, &
&               fb_species(ifb))
            END DO
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (11) 
!
! (core) boundary radial electron heat flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_type: using ', &
&                             'electron heat flux'
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            ifc1 = fbregp(ifb, 1)
            ifc2 = ifc1 + fbregp(ifb, 2) - 1
            DO ifc=ifc1,ifc2
              DO nd=1,nbdirs
                fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + dvd%fhe(nd&
&                 , fbreg(ifc), 1)
              END DO
              fb_current(ifb) = fb_current(ifb) + dv%fhe(fbreg(ifc), 1)
            END DO
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (12) 
!
! (core) boundary radial ion heat flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_type: using ', &
&                             'ion heat flux '
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            ifc1 = fbregp(ifb, 1)
            ifc2 = ifc1 + fbregp(ifb, 2) - 1
            DO ifc=ifc1,ifc2
              DO nd=1,nbdirs
                fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + dvd%fhi(nd&
&                 , fbreg(ifc), 1)
              END DO
              fb_current(ifb) = fb_current(ifb) + dv%fhi(fbreg(ifc), 1)
            END DO
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (13) 
!
! (core) boundary radial current
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_type: using ', &
&                             'current '
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            ifc1 = fbregp(ifb, 1)
            ifc2 = ifc1 + fbregp(ifb, 2) - 1
            DO ifc=ifc1,ifc2
              DO nd=1,nbdirs
                fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + dvd%fch(nd&
&                 , fbreg(ifc), 1)
              END DO
              fb_current(ifb) = fb_current(ifb) + dv%fch(fbreg(ifc), 1)
            END DO
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (14) 
!
! total particle content
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'total particle content for sequence ', &
&                             b2espcr(fb_species(ifb))
!
            is_start = -1
            is_end = -1
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = 0.0_R8
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                DO icv=1,ncv
                  DO nd=1,nbdirs
                    fb_currentd(nd, ifb) = fb_currentd(nd, ifb) + geo%&
&                     cvvol(icv)*pld%na(nd, icv, is)
                  END DO
                  fb_current(ifb) = fb_current(ifb) + pl%na(icv, is)*geo&
&                   %cvvol(icv)
                END DO
                is_end = is
              END IF
            END DO
          CASE (15) 
! in principle nothing to be done here...
!
! volumetric recombination
!
            IF (ncall .EQ. 0) THEN
              WRITE(*, '(a,a)') 'compute_feedback_type: using ', &
&             'volumetric recombination '
              IF (cum_volrec .LT. 1.0e4_R8) THEN
                cum_volrec = 1.0e4_R8
                WRITE(*, '(a,a)') &
&               'Warning! compute_feedback_type: setting ', &
&               'cum_volrec=1e4 for first timestep'
              END IF
            END IF
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = 0.d0
            END DO
            fb_current(ifb) = cum_volrec
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (16) 
!
! nepedm
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_type: using ', &
&                             'pedestal density '
            DO nd=1,nbdirs
              fb_currentd(nd, ifb) = dvd%ne(nd, omp(icped))
            END DO
            fb_current(ifb) = dv%ne(omp(icped))
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE DEFAULT
!
            WRITE(*, *) 'fb_type not coded ', fb_type(ifb)
            CALL XERRAB('fb_type not coded')
          END SELECT
!
!
! phase 2:
!
          SELECT CASE  (fb_rescale_option(ifb)) 
          CASE (0) 

          CASE (1) 
!
! no action
!
! rescale slowed by na_feedback_alpha
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_option: using ', &
&                             'slowed_rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            temp = fb_target(ifb)/fb_current(ifb)
            DO nd=1,nbdirs
              fb_rescaled(nd, ifb) = fb_alpha(ifb)*(fb_targetd(nd, ifb)-&
&               temp*fb_currentd(nd, ifb))/((fb_alpha(ifb)+1.0_R8)*&
&               fb_current(ifb))
            END DO
            fb_rescale(ifb) = (fb_alpha(ifb)*temp+1.0_R8)/(fb_alpha(ifb)&
&             +1.0_R8)
          CASE (2) 
!
! pure rescale
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_option: using ', &
&                             'rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            temp = fb_target(ifb)/fb_current(ifb)
            DO nd=1,nbdirs
              fb_rescaled(nd, ifb) = (fb_targetd(nd, ifb)-temp*&
&               fb_currentd(nd, ifb))/fb_current(ifb)
            END DO
            fb_rescale(ifb) = temp
          CASE (3) 
!
! rescale slowed by tanh_log
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_option: using ', &
&                             'tanh_log_rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
! 2**(tanh(log(x)/b)*log(a)/log(2))
            temp = fb_target(ifb)/fb_current(ifb)
            arg1 = temp
            DO nd=1,nbdirs
              arg1d(nd) = (fb_targetd(nd, ifb)-temp*fb_currentd(nd, ifb)&
&               )/fb_current(ifb)
              arg2d(nd) = arg1d(nd)/(fb_beta(ifb)*arg1)
            END DO
            arg2 = LOG(arg1)/fb_beta(ifb)
            temp = LOG(fb_alpha(ifb))
            temp0 = LOG(2.0_R8)
            DO nd=1,nbdirs
              pwy1d(nd) = temp*(1.0-TANH(arg2)**2)*arg2d(nd)/temp0
            END DO
            pwy1 = temp*(TANH(arg2)/temp0)
            temp0 = 2.0_R8**pwy1
            DO nd=1,nbdirs
              fb_rescaled(nd, ifb) = temp0*LOG(2.0_R8)*pwy1d(nd)
            END DO
            fb_rescale(ifb) = temp0
          CASE (4) 
!
! rescale done according to SOLPS4 formula:
! N = A + B*(time-T)
!
            IF (ncall .EQ. 0) THEN
              WRITE(*, '(a,a,i3)') 'compute_feedback_option: using ', &
&             'SOLPS4 linear increase for sequence ', b2espcr(fb_species&
&             (ifb))
              IF (fb_const(ifb) .LT. 0.0_R8) THEN
                DO nd=1,nbdirs
                  fb_constd(nd, ifb) = 0.d0
                END DO
                fb_const(ifb) = 0.0_R8
                is_start = -1
                is_end = -1
                DO is=0,ns-1
                  IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                    IF (is_start .EQ. -1) is_start = is
                    ic1 = fbregp(ifb, 1)
                    ic2 = ic1 + fbregp(ifb, 2) - 1
                    DO icv=ic1,ic2
                      IF (mpg%cvreg(fbreg(icv)) .NE. 0 .AND. fbreg(icv) &
&                         .LE. mpg%nci) THEN
                        DO nd=1,nbdirs
                          fb_constd(nd, ifb) = fb_constd(nd, ifb) + geo%&
&                           cvvol(fbreg(icv))*pld%na(nd, fbreg(icv), is)
                        END DO
                        fb_const(ifb) = fb_const(ifb) + pl%na(fbreg(icv)&
&                         , is)*geo%cvvol(fbreg(icv))
                      END IF
                    END DO
                    is_end = is
                  END IF
                END DO
              END IF
              DO nd=1,nbdirs
                fb_prevd(nd, ifb) = fb_currentd(nd, ifb)
              END DO
              fb_prev(ifb) = fb_current(ifb)
            END IF
            DO nd=1,nbdirs
!
              fb_targetd(nd, ifb) = fb_constd(nd, ifb)
              fb_rescaled(nd, :) = fb_alpha(ifb)*((fb_targetd(nd, ifb)-&
&               fb_currentd(nd, ifb))/ddtim+(fb_prevd(nd, ifb)-&
&               fb_currentd(nd, ifb))/dt_prev)
              saved_fb_actuatord(nd, ifb) = fb_currentd(nd, ifb)
            END DO
            fb_target(ifb) = fb_const(ifb) + fb_beta(ifb)*(tim-fb_time(&
&             ifb))
            fb_rescale = fb_alpha(ifb)*((fb_target(ifb)-fb_current(ifb))&
&             /ddtim+(fb_prev(ifb)-fb_current(ifb))/dt_prev)
            saved_fb_actuator(ifb) = fb_current(ifb)
          CASE (5) 
!
! rescale done according to SOLPS4 formula:
! Nt = A*exp((time-T)*B)
!
            IF (ncall .EQ. 0) THEN
              WRITE(*, *) 'compute_feedback_option: using ', &
&             'SOLPS4 exponential decay for sequence ', b2espcr(&
&             fb_species(ifb))
              IF (fb_const(ifb) .LT. 0.0_R8) THEN
                DO nd=1,nbdirs
                  fb_constd(nd, ifb) = 0.d0
                END DO
                fb_const(ifb) = 0.0_R8
                is_start = -1
                is_end = -1
                DO is=0,ns-1
                  IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                    IF (is_start .EQ. -1) is_start = is
                    DO icv=1,mpg%nci
                      IF (mpg%cvreg(icv) .NE. 0) THEN
                        DO nd=1,nbdirs
                          fb_constd(nd, ifb) = fb_constd(nd, ifb) + geo%&
&                           cvvol(icv)*pld%na(nd, icv, is)
                        END DO
                        fb_const(ifb) = fb_const(ifb) + pl%na(icv, is)*&
&                         geo%cvvol(icv)
                      END IF
                    END DO
                    is_end = is
                  END IF
                END DO
              END IF
              DO nd=1,nbdirs
                fb_prevd(nd, ifb) = fb_currentd(nd, ifb)
              END DO
              fb_prev(ifb) = fb_current(ifb)
            END IF
!
            arg1 = fb_beta(ifb)*(tim-fb_time(ifb))
            temp = EXP(arg1)
            DO nd=1,nbdirs
              fb_targetd(nd, ifb) = temp*fb_constd(nd, ifb)
              fb_rescaled(nd, ifb) = fb_alpha(ifb)*((fb_targetd(nd, ifb)&
&               -fb_currentd(nd, ifb))/ddtim+(fb_prevd(nd, ifb)-&
&               fb_currentd(nd, ifb))/dt_prev)
              saved_fb_actuatord(nd, ifb) = fb_currentd(nd, ifb)
            END DO
            fb_target(ifb) = temp*fb_const(ifb)
            fb_rescale(ifb) = fb_alpha(ifb)*((fb_target(ifb)-fb_current(&
&             ifb))/ddtim+(fb_prev(ifb)-fb_current(ifb))/dt_prev)
            saved_fb_actuator(ifb) = fb_current(ifb)
          CASE (6) 
!
! rescale slowed by fb_alpha (SOLPS4 style)
!
            DO nd=1,nbdirs
              fb_rescaled(nd, ifb) = fb_alpha(ifb)*fb_currentd(nd, ifb)
            END DO
            fb_rescale(ifb) = fb_alpha(ifb)*fb_current(ifb)
          CASE DEFAULT
!
            WRITE(*, *) 'fb_rescale_option not coded ', &
&           fb_rescale_option(ifb)
          END SELECT
!
          WRITE(*, '(a,1x,1p,i3,6g15.6)') 'feedback_rescale ', b2espcr(&
&         fb_species(ifb)), fb_current(ifb), fb_target(ifb), fb_rescale(&
&         ifb), fb_prev(ifb), ddtim, dt_prev
!
!
! phase 3:
!
          SELECT CASE  (fb_actuator(ifb)) 
          CASE (0) 

          CASE (1) 
!
! no action
!
! neutral particle flux => gas puff
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_actuator: using ', &
&                             'gas_puff for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            IF (ncall .EQ. 0 .AND. saved_fb_actuator(ifb) .EQ. 0.0_R8) &
&           THEN
              IF (conpar(is_start, fb_ib(ifb), 1) .GT. 0.0_R8) THEN
                DO nd=1,nbdirs
                  saved_fb_actuatord(nd, ifb) = conpard(nd, is_start, &
&                   fb_ib(ifb), 1)
                END DO
                saved_fb_actuator(ifb) = conpar(is_start, fb_ib(ifb), 1)
                WRITE(message, '(a,i3,a,i2,a)') &
&               'Initial gas puff strength set by CONPAR(', is_start, &
&               ',', fb_ib(ifb), ',1) !'
              ELSE IF (userfluxparm(fb_istra(ifb), 1) .GT. 0.0_R8) THEN
                DO nd=1,nbdirs
                  saved_fb_actuatord(nd, ifb) = userfluxparmd(nd, &
&                   fb_istra(ifb), 1)
                END DO
                saved_fb_actuator(ifb) = userfluxparm(fb_istra(ifb), 1)
                WRITE(message, '(a,i3,a,i2,a)') &
&               'Initial gas puff strength set by USERFLUXPARM(', &
&               fb_istra(ifb), ',1)  !'
              ELSE
                DO nd=1,nbdirs
                  saved_fb_actuatord(nd, ifb) = 0.d0
                END DO
                saved_fb_actuator(ifb) = 1.0e18_R8
                WRITE(*, *) 'Warning!  ', 'The gas puff was ', &
&               'started with an initial rate of 1e18!'
              END IF
            END IF
!
            IF (fb_rescale_option(ifb) .LT. 4) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescale(ifb)*&
&                 saved_fb_actuatord(nd, ifb) + saved_fb_actuator(ifb)*&
&                 fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
            IF (fb_puff_min(ifb) .GE. 0.0_R8) THEN
              IF (fb_puff_min(ifb) .LT. saved_fb_actuator(ifb)) THEN
                saved_fb_actuator(ifb) = saved_fb_actuator(ifb)
              ELSE
                DO nd=1,nbdirs
                  saved_fb_actuatord(nd, ifb) = 0.d0
                END DO
                saved_fb_actuator(ifb) = fb_puff_min(ifb)
              END IF
            END IF
            IF (fb_puff_max(ifb) .GT. 0.0_R8) THEN
              IF (fb_puff_max(ifb) .GT. saved_fb_actuator(ifb)) THEN
                saved_fb_actuator(ifb) = saved_fb_actuator(ifb)
              ELSE
                DO nd=1,nbdirs
                  saved_fb_actuatord(nd, ifb) = 0.d0
                END DO
                saved_fb_actuator(ifb) = fb_puff_max(ifb)
              END IF
            END IF
!
            IF (fb_overshoot(ifb) .GT. 1.0_R8 .AND. fb_current(ifb) .GT.&
&               fb_target(ifb)*fb_overshoot(ifb)) THEN
! csc Here the idea is to change conpar if fluid neutrals are used, otherwise userfluxparm
!     This way it is easier to generalize the schemes when hybrid neutrals are used (to be done!)
              IF (switch%use_eirene .EQ. 0) THEN
                DO nd=1,nbdirs
                  conpard(nd, is_start, fb_ib(ifb), 1) = 0.d0
                END DO
                conpar(is_start, fb_ib(ifb), 1) = 0.0_R8
              ELSE
                DO nd=1,nbdirs
                  userfluxparmd(nd, fb_istra(ifb), 1) = 0.d0
                END DO
                userfluxparm(fb_istra(ifb), 1) = 0.0_R8
              END IF
            ELSE IF (switch%use_eirene .EQ. 0) THEN
              DO nd=1,nbdirs
                conpard(nd, is_start, fb_ib(ifb), 1) = &
&                 saved_fb_actuatord(nd, ifb)
              END DO
              conpar(is_start, fb_ib(ifb), 1) = saved_fb_actuator(ifb)
            ELSE
              DO nd=1,nbdirs
                userfluxparmd(nd, fb_istra(ifb), 1) = saved_fb_actuatord&
&                 (nd, ifb)
              END DO
              userfluxparm(fb_istra(ifb), 1) = saved_fb_actuator(ifb)
            END IF
          CASE (2) 
!
! rescale na
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_actuator: using ', &
&                             'density rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            DO is=is_start,is_end
              DO nd=1,nbdirs
                pld%na(nd, :, is) = fb_rescale(ifb)*pld%na(nd, :, is) + &
&                 pl%na(:, is)*fb_rescaled(nd, ifb)
                dvd%kinrgy(nd, :, is) = fb_rescale(ifb)*dvd%kinrgy(nd, :&
&                 , is) + dv%kinrgy(:, is)*fb_rescaled(nd, ifb)
                dvd%fna(nd, :, 0:1, is) = fb_rescale(ifb)*dvd%fna(nd, :&
&                 , 0:1, is) + dv%fna(:, 0:1, is)*fb_rescaled(nd, ifb)
                dvd%fna_mdf(nd, :, 0:1, is) = fb_rescale(ifb)*dvd%&
&                 fna_mdf(nd, :, 0:1, is) + dv%fna_mdf(:, 0:1, is)*&
&                 fb_rescaled(nd, ifb)
                dvd%fna_fcor(nd, :, 0:1, is) = fb_rescale(ifb)*dvd%&
&                 fna_fcor(nd, :, 0:1, is) + dv%fna_fcor(:, 0:1, is)*&
&                 fb_rescaled(nd, ifb)
                dvd%fna_he(nd, :, 0:1, is) = fb_rescale(ifb)*dvd%fna_he(&
&                 nd, :, 0:1, is) + dv%fna_he(:, 0:1, is)*fb_rescaled(nd&
&                 , ifb)
                dvd%fnapsch(nd, :, 0:1, is) = fb_rescale(ifb)*dvd%&
&                 fnapsch(nd, :, 0:1, is) + dv%fnapsch(:, 0:1, is)*&
&                 fb_rescaled(nd, ifb)
                dvd%fna_eir(nd, :, 0:1, is) = fb_rescale(ifb)*dvd%&
&                 fna_eir(nd, :, 0:1, is) + dv%fna_eir(:, 0:1, is)*&
&                 fb_rescaled(nd, ifb)
                dvd%fna_32(nd, :, 0:1, is) = fb_rescale(ifb)*dvd%fna_32(&
&                 nd, :, 0:1, is) + dv%fna_32(:, 0:1, is)*fb_rescaled(nd&
&                 , ifb)
              END DO
              pl%na(:, is) = pl%na(:, is)*fb_rescale(ifb)
              dv%kinrgy(:, is) = dv%kinrgy(:, is)*fb_rescale(ifb)
              temp1 = (fb_rescale(ifb)-1.0_R8)*pl%na(:, is)
              DO nd=1,nbdirs
                dvd%ne(nd, :) = dvd%ne(nd, :) + rt%rza(:, is)*(pl%na(:, &
&                 is)*fb_rescaled(nd, ifb)+(fb_rescale(ifb)-1.0_R8)*pld%&
&                 na(nd, :, is)) + temp1*rtd%rza(nd, :, is)
              END DO
              dv%ne = dv%ne + temp1*rt%rza(:, is)
              dv%fna(:, 0:1, is) = dv%fna(:, 0:1, is)*fb_rescale(ifb)
              dv%fna_mdf(:, 0:1, is) = dv%fna_mdf(:, 0:1, is)*fb_rescale&
&               (ifb)
              dv%fna_fcor(:, 0:1, is) = dv%fna_fcor(:, 0:1, is)*&
&               fb_rescale(ifb)
              dv%fna_nodrift(:, 0:1, is) = dv%fna_nodrift(:, 0:1, is)*&
&               fb_rescale(ifb)
              dv%fna_he(:, 0:1, is) = dv%fna_he(:, 0:1, is)*fb_rescale(&
&               ifb)
              dv%fnapsch(:, 0:1, is) = dv%fnapsch(:, 0:1, is)*fb_rescale&
&               (ifb)
              dv%fna_eir(:, 0:1, is) = dv%fna_eir(:, 0:1, is)*fb_rescale&
&               (ifb)
              dv%fna_32(:, 0:1, is) = dv%fna_32(:, 0:1, is)*fb_rescale(&
&               ifb)
              dv%fna_52(:, 0:1, is) = dv%fna_52(:, 0:1, is)*fb_rescale(&
&               ifb)
            END DO
            DO nd=1,nbdirs
!   ..initialise previous state and fluxes
              psnld%na(nd, :, :) = pld%na(nd, :, :)
              psncd%na(nd, :, :) = pld%na(nd, :, :)
              psnld%ne(nd, :) = dvd%ne(nd, :)
              psncd%ne(nd, :) = dvd%ne(nd, :)
              psnld%kinrgy(nd, :, :) = dvd%kinrgy(nd, :, :)
              psncd%kinrgy(nd, :, :) = dvd%kinrgy(nd, :, :)
            END DO
            psnl%na = pl%na
            psnc%na = pl%na
            psnl%ne = dv%ne
            psnc%ne = dv%ne
            psnl%fna = dv%fna
            psnc%fna = dv%fna
            psnl%kinrgy = dv%kinrgy
            psnc%kinrgy = dv%kinrgy
!   ..compute initial ni
            CALL B2XPNI_NODIFF_NODIFF(ncv, ns, pl%na, dv%ni)
            CALL B2XPNN_NODIFF_NODIFF(ncv, ns, pl%na, dv%nn)
            DO nd=1,nbdirs
!   ..compute ni0
              psnld%ni(nd, :, :) = dvd%ni(nd, :, :)
              psncd%ni(nd, :, :) = dvd%ni(nd, :, :)
            END DO
            psnl%ni = dv%ni
            psnc%ni = dv%ni
          CASE (3) 
!
! charged species particle flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_actuator: using ', &
&                             'boundary flux condition for sequence ', &
&                             b2espcr(fb_species(ifb))
!
            is_start = eb2spcr(b2espcr(fb_species(ifb))) + 1
            is_end = eb2spcr(b2espcr(fb_species(ifb))) + nfluids(b2espcr&
&             (fb_species(ifb)))
!
            IF (feedback_namelist_used .AND. ncall .EQ. 0 .AND. &
&               saved_fb_actuator(ifb) .EQ. 0.0_R8) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = conpard(nd, is_end, fb_ib(&
&                 ifb), 1)
              END DO
              saved_fb_actuator(ifb) = conpar(is_end, fb_ib(ifb), 1)
            END IF
!
            IF (fb_type(ifb) .EQ. 6) THEN
!csc, this is for backward compatibility, can probably be removed
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = (1.0_R8-fb_alpha(ifb))*&
&                 saved_fb_actuatord(nd, ifb) + fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = (1.0_R8-fb_alpha(ifb))*&
&               saved_fb_actuator(ifb) + fb_rescale(ifb)
            ELSE IF (fb_rescale_option(ifb) .LT. 4) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescale(ifb)*&
&                 saved_fb_actuatord(nd, ifb) + saved_fb_actuator(ifb)*&
&                 fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
!
            IF (fb_puff_min(ifb) .GE. 0.0_R8) THEN
              IF (fb_puff_min(ifb) .LT. saved_fb_actuator(ifb)) THEN
                saved_fb_actuator(ifb) = saved_fb_actuator(ifb)
              ELSE
                DO nd=1,nbdirs
                  saved_fb_actuatord(nd, ifb) = 0.d0
                END DO
                saved_fb_actuator(ifb) = fb_puff_min(ifb)
              END IF
            END IF
            IF (fb_puff_max(ifb) .GT. 0.0_R8) THEN
              IF (fb_puff_max(ifb) .GT. saved_fb_actuator(ifb)) THEN
                saved_fb_actuator(ifb) = saved_fb_actuator(ifb)
              ELSE
                DO nd=1,nbdirs
                  saved_fb_actuatord(nd, ifb) = 0.d0
                END DO
                saved_fb_actuator(ifb) = fb_puff_max(ifb)
              END IF
            END IF
!
            IF (fb_overshoot(ifb) .GT. 1.0_R8 .AND. fb_current(ifb) .GT.&
&               fb_target(ifb)*fb_overshoot(ifb)) THEN
              total_flux = 0.0_R8
              DO nd=1,nbdirsmax
                total_fluxd(nd) = 0.d0
              END DO
            ELSE
              DO nd=1,nbdirs
                total_fluxd(nd) = saved_fb_actuatord(nd, ifb)
              END DO
              total_flux = saved_fb_actuator(ifb)
            END IF
!
            IF (fb_type(ifb) .EQ. 6) THEN
              DO is=is_start,is_end
                iss = is - is_start + 1
                DO nd=1,nbdirs
                  conpard(nd, is, fb_ib(ifb), 1) = charge_frac(iss)*&
&                   total_fluxd(nd) + total_flux*charge_fracd(nd, iss)
                END DO
                conpar(is, fb_ib(ifb), 1) = total_flux*charge_frac(iss)
              END DO
            ELSE
              DO nd=1,nbdirs
                conpard(nd, fb_species(ifb), fb_ib(ifb), 1) = &
&                 total_fluxd(nd)
              END DO
              conpar(fb_species(ifb), fb_ib(ifb), 1) = total_flux
            END IF
          CASE (4) 
!
! electron heat flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_actuator: using ', &
&                             'electron heat flux boundary condition'
!
            IF (feedback_namelist_used .AND. ncall .EQ. 0 .AND. &
&               saved_fb_actuator(ifb) .EQ. 0.0_R8) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = enepard(nd, fb_ib(ifb), 1)
              END DO
              saved_fb_actuator(ifb) = enepar(fb_ib(ifb), 1)
            END IF
!
            IF (fb_rescale_option(ifb) .LT. 4) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescale(ifb)*&
&                 saved_fb_actuatord(nd, ifb) + saved_fb_actuator(ifb)*&
&                 fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
!
            DO nd=1,nbdirs
              enepard(nd, fb_ib(ifb), 1) = saved_fb_actuatord(nd, ifb)
            END DO
            enepar(fb_ib(ifb), 1) = saved_fb_actuator(ifb)
          CASE (5) 
!
! ion heat flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_actuator: using ', &
&                             'ion heat flux boundary condition'
!
            IF (feedback_namelist_used .AND. ncall .EQ. 0 .AND. &
&               saved_fb_actuator(ifb) .EQ. 0.0_R8) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = enipard(nd, fb_ib(ifb), 1)
              END DO
              saved_fb_actuator(ifb) = enipar(fb_ib(ifb), 1)
            END IF
!
            IF (fb_rescale_option(ifb) .LT. 4) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescale(ifb)*&
&                 saved_fb_actuatord(nd, ifb) + saved_fb_actuator(ifb)*&
&                 fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
!
            DO nd=1,nbdirs
              enipard(nd, fb_ib(ifb), 1) = saved_fb_actuatord(nd, ifb)
            END DO
            enipar(fb_ib(ifb), 1) = saved_fb_actuator(ifb)
          CASE (6) 
!
! current
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_actuator: using ', &
&                             'electric current boundary condition'
!
            IF (feedback_namelist_used .AND. ncall .EQ. 0 .AND. &
&               saved_fb_actuator(ifb) .EQ. 0.0_R8) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = potpard(nd, fb_ib(ifb), 1)
              END DO
              saved_fb_actuator(ifb) = potpar(fb_ib(ifb), 1)
            END IF
!
            IF (fb_rescale_option(ifb) .LT. 4) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescale(ifb)*&
&                 saved_fb_actuatord(nd, ifb) + saved_fb_actuator(ifb)*&
&                 fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
!
            DO nd=1,nbdirs
              potpard(nd, fb_ib(ifb), 1) = saved_fb_actuatord(nd, ifb)
            END DO
            potpar(fb_ib(ifb), 1) = saved_fb_actuator(ifb)
          CASE (7) 
!
! density
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_actuator: using ', &
&                             'density condition for sequence ', b2espcr&
&                             (fb_species(ifb))
!
            IF (feedback_namelist_used .AND. ncall .EQ. 0 .AND. &
&               saved_fb_actuator(ifb) .EQ. 0.0_R8) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = conpard(nd, fb_species(ifb&
&                 ), fb_ib(ifb), 1)
              END DO
              saved_fb_actuator(ifb) = conpar(fb_species(ifb), fb_ib(ifb&
&               ), 1)
            END IF
!
            IF (fb_rescale_option(ifb) .LT. 4) THEN
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescale(ifb)*&
&                 saved_fb_actuatord(nd, ifb) + saved_fb_actuator(ifb)*&
&                 fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              DO nd=1,nbdirs
                saved_fb_actuatord(nd, ifb) = fb_rescaled(nd, ifb)
              END DO
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
!
            DO nd=1,nbdirs
              conpard(nd, fb_species(ifb), fb_ib(ifb), 1) = &
&               saved_fb_actuatord(nd, ifb)
            END DO
            conpar(fb_species(ifb), fb_ib(ifb), 1) = saved_fb_actuator(&
&             ifb)
          CASE DEFAULT
!
            WRITE(*, *) 'fb_actuator not coded ', fb_type(ifb)
          END SELECT
!
          IF (fb_actuator(ifb) .EQ. 1 .OR. fb_actuator(ifb) .EQ. 3) &
&           WRITE(*, '(a,1p,i3,g15.6)') 'feedback_actuator ', b2espcr(&
&           fb_species(ifb)), saved_fb_actuator(ifb)
        END IF
!
!
        feedback_namelist_used = (feedback_namelist_used .OR. &
&         fb_actuator(ifb) .EQ. 1) .OR. fb_actuator(ifb) .EQ. 3
      END DO
!
!ifb
!
      dt_prev = ddtim
    END IF
!
!
    ncall = ncall + 1
    CALL SUBEND()
    RETURN
  END SUBROUTINE COMPUTE_FEEDBACK_DV

!
  SUBROUTINE COMPUTE_FEEDBACK(ncv, nfc, ns, ismain, switch, geo, mpg, pl&
&   , dv, rt, psnc, psnl, main_call)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!
!   ..input arguments (unchanged on exit)
    INTEGER :: ncv, nfc, ns, ismain
    TYPE(SWITCHES), INTENT(IN) :: switch
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(B2PLASMA), INTENT(INOUT) :: pl
    TYPE(B2DERIVATIVES), INTENT(INOUT) :: dv
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2PLASMASNAPSHOT), INTENT(INOUT) :: psnc, psnl
    LOGICAL :: main_call
!   ..local variables
    INTEGER :: ifb, ifc, ifc1, ifc2, icv, ic1, ic2, is, is_start, is_end&
&   , iss
    INTEGER, SAVE :: ncall=0
    REAL(kind=r8) :: sum_e, total_flux, vol_feedback, sum_s
    INTRINSIC MAXVAL
    EXTERNAL XERRAB
    INTRINSIC LOG
    INTRINSIC TANH
    INTRINSIC EXP
    INTRINSIC MAX
    INTRINSIC MIN
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    REAL(kind=r8) :: arg1
    REAL(kind=r8) :: arg2
    REAL(kind=r8) :: pwy1
!
    CALL SUBINI('compute_feedback')
    IF (ncall .EQ. 0) THEN
!   ..test nCv, nFc, ns
      CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, &
&           'faulty argument nCv, nFc')
      CALL XERTST(1 .LE. ns, 'faulty argument ns')
      CALL XERTST(0 .LE. ismain .AND. ismain .LT. ns, &
&           'invalid main plasma species index ismain')
      CALL XERTST(.NOT.is_neutral(ismain), &
&           'invalid main plasma species ismain; must not be neutral')
    END IF
!
    result1 = MAXVAL(fb_target(1:nfb))
    result2 = MAXVAL(fb_rescale_option(1:nfb))
    IF (result1 .GT. 0.0_R8 .OR. result2 .GT. 0) THEN
! we have a request for feedback control
! the idea here is to break the actions into 3 phases
! 1/ calculate something based on the present state of the plasma depending on
!    "fb_type"
! 2/ compare this with the reference value and calculate a change
! 3/ apply this in some way based on "fb_actuator"
!
      DO ifb=1,nfb
        IF (fb_type(ifb) .GT. 0 .AND. fb_rescale_option(ifb) .GT. 0 &
&           .AND. fb_actuator(ifb) .GT. 0) THEN
!
          IF (main_call) fb_prev(ifb) = saved_fb_actuator(ifb)
!
!
! phase 1:
          SELECT CASE  (fb_type(ifb)) 
          CASE (0) 

          CASE (1) 
!
! no action
!
! local density
!
            IF (ncall .EQ. 0) WRITE(*, *) &
&                             'compute_feedback_type: using ', &
&                             'local_density for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            fb_current(ifb) = 0.0_R8
            vol_feedback = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                ic1 = fbregp(ifb, 1)
                ic2 = ic1 + fbregp(ifb, 2) - 1
                DO icv=ic1,ic2
                  fb_current(ifb) = fb_current(ifb) + pl%na(fbreg(icv), &
&                   is)*geo%cvvol(fbreg(icv))
                  IF (is .EQ. is_start) vol_feedback = vol_feedback + &
&                     geo%cvvol(fbreg(icv))
                END DO
                is_end = is
              END IF
            END DO
            fb_current(ifb) = fb_current(ifb)/vol_feedback
          CASE (2) 
!
! local electron density
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'local_electron_density for sequence ', &
&                             b2espcr(fb_species(ifb))
!
            fb_current(ifb) = 0.0_R8
            vol_feedback = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
            ic1 = fbregp(ifb, 1)
            ic2 = ic1 + fbregp(ifb, 2) - 1
            DO icv=ic1,ic2
              fb_current(ifb) = fb_current(ifb) + dv%ne(fbreg(icv))*geo%&
&               cvvol(fbreg(icv))
              vol_feedback = vol_feedback + geo%cvvol(fbreg(icv))
            END DO
            fb_current(ifb) = fb_current(ifb)/vol_feedback
          CASE (3) 
!
! nesepm electron density
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'nesepm_electron_density for sequence ', &
&                             b2espcr(fb_species(ifb))
!
            fb_current(ifb) = 0.5_R8*(dv%ne(omp(icsepomp-1))+dv%ne(omp(&
&             icsepomp)))
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (4) 
!
! total particle content for that sequence
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'total particle content for sequence ', &
&                             b2espcr(fb_species(ifb))
            fb_current(ifb) = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                ic1 = fbregp(ifb, 1)
                ic2 = ic1 + fbregp(ifb, 2) - 1
                DO icv=ic1,ic2
                  IF (mpg%cvreg(fbreg(icv)) .NE. 0 .AND. fbreg(icv) .LE.&
&                     mpg%nci) fb_current(ifb) = fb_current(ifb) + pl%na&
&                     (fbreg(icv), is)*geo%cvvol(fbreg(icv))
                END DO
                is_end = is
              END IF
            END DO
          CASE (5) 
!
! total ion content for that sequence (not including neutrals)
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'total ion content for sequence ', b2espcr&
&                             (fb_species(ifb))
            fb_current(ifb) = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                IF (.NOT.is_neutral(is)) THEN
                  ic1 = fbregp(ifb, 1)
                  ic2 = ic1 + fbregp(ifb, 2) - 1
                  DO icv=ic1,ic2
                    IF (mpg%cvreg(fbreg(icv)) .NE. 0 .AND. fbreg(icv) &
&                       .LE. mpg%nci) fb_current(ifb) = fb_current(ifb) &
&                       + pl%na(fbreg(icv), is)*geo%cvvol(fbreg(icv))
                  END DO
                  is_end = is
                END IF
              END IF
            END DO
          CASE (6) 
!
! neutral particle flux through the core boundary (??)
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'core boundary flux for sequence ', &
&                             b2espcr(fb_species(ifb))
            fb_current(ifb) = fb_const(ifb)
            WRITE(*, *) 'WARNING: fb_type = 6 needs to be'//&
&           ' adapted for hybrid methods!'
!
            CALL XERRAB('fb_type=6 not yet adapted for'//&
&                 ' pure fluid cases')
            is_start = eb2spcr(b2espcr(fb_species(ifb)))
            is_end = eb2spcr(b2espcr(fb_species(ifb))) + nfluids(b2espcr&
&             (fb_species(ifb)))
            charge_frac = 0.0_R8
            sum_s = 0.0_R8
            DO is=is_start,is_end
              iss = is - is_start + 1
              IF (.NOT.(is_neutral(is) .AND. switch%use_eirene .NE. 0)) &
&             THEN
                ic1 = fbregp(ifb, 1)
                ic2 = ic1 + fbregp(ifb, 2) - 1
                DO icv=ic1,ic2
                  IF (mpg%cvonclosedsurface(icv)) THEN
                    sum_s = sum_s + pl%na(icv, is)*geo%cvvol(icv)
                    charge_frac(iss) = charge_frac(iss) + pl%na(icv, is)&
&                     *geo%cvvol(icv)
                  END IF
                END DO
              END IF
            END DO
            charge_frac = charge_frac/sum_s
          CASE (7) 
!
! relative average concentration of this species at the separatrix
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                       'average separatrix concentration for sequence '&
&                             , b2espcr(fb_species(ifb))
            fb_current(ifb) = 0.0_R8
            sum_e = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                IF (.NOT.(is_neutral(is) .AND. switch%use_eirene .NE. 0)&
&               ) THEN
                  ifc1 = mpg%fsfcp(mpg%ifssep, 1)
                  ifc2 = ifc1 + mpg%fsfcp(mpg%ifssep, 2) - 1
! csc Here the idea is to follow the separatrix along its flux surface, then use the two CVs
!     belonging to each face of the flux surface
                  DO ifc=ifc1,ifc2
                    ic1 = mpg%fccv(mpg%fsfc(ifc), 1)
                    ic2 = mpg%fccv(mpg%fsfc(ifc), 2)
                    IF (mpg%cvonclosedsurface(ic1) .OR. mpg%&
&                       cvonclosedsurface(ic2)) THEN
                      fb_current(ifb) = fb_current(ifb) + (pl%na(ic1, is&
&                       )+pl%na(ic2, is))*geo%fcs(mpg%fsfc(ifc))
                      IF (is .EQ. is_start + 1) sum_e = sum_e + (dv%ne(&
&                         ic1)+dv%ne(ic2))*geo%fcs(mpg%fsfc(ifc))
                    END IF
                  END DO
                  IF (mpg%ifssep2 .GT. 0) THEN
                    ifc1 = mpg%fsfcp(mpg%ifssep2, 1)
                    ifc2 = ifc1 + mpg%fsfcp(mpg%ifssep2, 2) - 1
                    DO ifc=ifc1,ifc2
                      ic1 = mpg%fccv(mpg%fsfc(ifc), 1)
                      ic2 = mpg%fccv(mpg%fsfc(ifc), 2)
                      IF (mpg%cvonclosedsurface(ic1) .OR. mpg%&
&                         cvonclosedsurface(ic2)) THEN
                        fb_current(ifb) = fb_current(ifb) + (pl%na(ic1, &
&                         is)+pl%na(ic2, is))*geo%fcs(mpg%fsfc(ifc))
                        IF (is .EQ. is_start + 1) sum_e = sum_e + (dv%ne&
&                           (ic1)+dv%ne(ic2))*geo%fcs(mpg%fsfc(ifc))
                      END IF
                    END DO
                  END IF
                  is_end = is
                END IF
              END IF
            END DO
            fb_current(ifb) = fb_current(ifb)/sum_e
          CASE (8) 
!
! relative average concentration of this species on desired domain
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using average ', &
&                             'concentration for sequence ', b2espcr(&
&                             fb_species(ifb))
            fb_current(ifb) = 0.0_R8
            sum_e = 0.0_R8
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                IF (.NOT.(is_neutral(is) .AND. switch%use_eirene .NE. 0)&
&               ) THEN
                  ic1 = fbregp(ifb, 1)
                  ic2 = ic1 + fbregp(ifb, 2) - 1
                  DO icv=ic1,ic2
                    fb_current(ifb) = fb_current(ifb) + pl%na(fbreg(icv)&
&                     , is)*geo%cvvol(fbreg(icv))
                    IF (is .EQ. is_start + 1) sum_e = sum_e + dv%ne(&
&                       fbreg(icv))*geo%cvvol(fbreg(icv))
                  END DO
                  is_end = is
                END IF
              END IF
            END DO
            fb_current(ifb) = fb_current(ifb)/sum_e
          CASE (9) 
!
! inner midplane electron density
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                       'inner_midplance_electron_density for sequence '&
&                             , b2espcr(fb_species(ifb))
!
            fb_current(ifb) = 0.5_R8*(dv%ne(imp(icsepimp-1))+dv%ne(imp(&
&             icsepimp)))
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (10) 
!
! (core) boundary radial particle flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'radial particle flux for sequence ', &
&                             b2espcr(fb_species(ifb))
            fb_current(ifb) = 0.0_R8
            ifc1 = fbregp(ifb, 1)
            ifc2 = ifc1 + fbregp(ifb, 2) - 1
            DO ifc=ifc1,ifc2
              fb_current(ifb) = fb_current(ifb) + dv%fna(fbreg(ifc), 1, &
&               fb_species(ifb))
            END DO
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (11) 
!
! (core) boundary radial electron heat flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_type: using ', &
&                             'electron heat flux'
            fb_current(ifb) = 0.0_R8
            ifc1 = fbregp(ifb, 1)
            ifc2 = ifc1 + fbregp(ifb, 2) - 1
            DO ifc=ifc1,ifc2
              fb_current(ifb) = fb_current(ifb) + dv%fhe(fbreg(ifc), 1)
            END DO
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (12) 
!
! (core) boundary radial ion heat flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_type: using ', &
&                             'ion heat flux '
            fb_current(ifb) = 0.0_R8
            ifc1 = fbregp(ifb, 1)
            ifc2 = ifc1 + fbregp(ifb, 2) - 1
            DO ifc=ifc1,ifc2
              fb_current(ifb) = fb_current(ifb) + dv%fhi(fbreg(ifc), 1)
            END DO
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (13) 
!
! (core) boundary radial current
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_type: using ', &
&                             'current '
            fb_current(ifb) = 0.0_R8
            ifc1 = fbregp(ifb, 1)
            ifc2 = ifc1 + fbregp(ifb, 2) - 1
            DO ifc=ifc1,ifc2
              fb_current(ifb) = fb_current(ifb) + dv%fch(fbreg(ifc), 1)
            END DO
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (14) 
!
! total particle content
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_type: using ', &
&                             'total particle content for sequence ', &
&                             b2espcr(fb_species(ifb))
!
            is_start = -1
            is_end = -1
            fb_current(ifb) = 0.0_R8
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                DO icv=1,ncv
                  fb_current(ifb) = fb_current(ifb) + pl%na(icv, is)*geo&
&                   %cvvol(icv)
                END DO
                is_end = is
              END IF
            END DO
          CASE (15) 
! in principle nothing to be done here...
!
! volumetric recombination
!
            IF (ncall .EQ. 0) THEN
              WRITE(*, '(a,a)') 'compute_feedback_type: using ', &
&             'volumetric recombination '
              IF (cum_volrec .LT. 1.0e4_R8) THEN
                cum_volrec = 1.0e4_R8
                WRITE(*, '(a,a)') &
&               'Warning! compute_feedback_type: setting ', &
&               'cum_volrec=1e4 for first timestep'
              END IF
            END IF
            fb_current(ifb) = cum_volrec
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE (16) 
!
! nepedm
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_type: using ', &
&                             'pedestal density '
            fb_current(ifb) = dv%ne(omp(icped))
            is_start = -1
            is_end = -1
            DO is=0,ns-1
              IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                IF (is_start .EQ. -1) is_start = is
                is_end = is
              END IF
            END DO
          CASE DEFAULT
!
            WRITE(*, *) 'fb_type not coded ', fb_type(ifb)
            CALL XERRAB('fb_type not coded')
          END SELECT
!
!
! phase 2:
!
          SELECT CASE  (fb_rescale_option(ifb)) 
          CASE (0) 

          CASE (1) 
!
! no action
!
! rescale slowed by na_feedback_alpha
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_option: using ', &
&                             'slowed_rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            fb_rescale(ifb) = (1.0_R8+fb_alpha(ifb)*fb_target(ifb)/&
&             fb_current(ifb))/(1.0_R8+fb_alpha(ifb))
          CASE (2) 
!
! pure rescale
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_option: using ', &
&                             'rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            fb_rescale(ifb) = fb_target(ifb)/fb_current(ifb)
          CASE (3) 
!
! rescale slowed by tanh_log
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_option: using ', &
&                             'tanh_log_rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
! 2**(tanh(log(x)/b)*log(a)/log(2))
            arg1 = fb_target(ifb)/fb_current(ifb)
            arg2 = LOG(arg1)/fb_beta(ifb)
            pwy1 = TANH(arg2)*LOG(fb_alpha(ifb))/LOG(2.0_R8)
            fb_rescale(ifb) = 2.0_R8**pwy1
          CASE (4) 
!
! rescale done according to SOLPS4 formula:
! N = A + B*(time-T)
!
            IF (ncall .EQ. 0) THEN
              WRITE(*, '(a,a,i3)') 'compute_feedback_option: using ', &
&             'SOLPS4 linear increase for sequence ', b2espcr(fb_species&
&             (ifb))
              IF (fb_const(ifb) .LT. 0.0_R8) THEN
                fb_const(ifb) = 0.0_R8
                is_start = -1
                is_end = -1
                DO is=0,ns-1
                  IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                    IF (is_start .EQ. -1) is_start = is
                    ic1 = fbregp(ifb, 1)
                    ic2 = ic1 + fbregp(ifb, 2) - 1
                    DO icv=ic1,ic2
                      IF (mpg%cvreg(fbreg(icv)) .NE. 0 .AND. fbreg(icv) &
&                         .LE. mpg%nci) fb_const(ifb) = fb_const(ifb) + &
&                         pl%na(fbreg(icv), is)*geo%cvvol(fbreg(icv))
                    END DO
                    is_end = is
                  END IF
                END DO
              END IF
              fb_prev(ifb) = fb_current(ifb)
            END IF
!
            fb_target(ifb) = fb_const(ifb) + fb_beta(ifb)*(tim-fb_time(&
&             ifb))
            fb_rescale = fb_alpha(ifb)*((fb_target(ifb)-fb_current(ifb))&
&             /ddtim+(fb_prev(ifb)-fb_current(ifb))/dt_prev)
            saved_fb_actuator(ifb) = fb_current(ifb)
          CASE (5) 
!
! rescale done according to SOLPS4 formula:
! Nt = A*exp((time-T)*B)
!
            IF (ncall .EQ. 0) THEN
              WRITE(*, *) 'compute_feedback_option: using ', &
&             'SOLPS4 exponential decay for sequence ', b2espcr(&
&             fb_species(ifb))
              IF (fb_const(ifb) .LT. 0.0_R8) THEN
                fb_const(ifb) = 0.0_R8
                is_start = -1
                is_end = -1
                DO is=0,ns-1
                  IF (b2espcr(is) .EQ. b2espcr(fb_species(ifb))) THEN
                    IF (is_start .EQ. -1) is_start = is
                    DO icv=1,mpg%nci
                      IF (mpg%cvreg(icv) .NE. 0) fb_const(ifb) = &
&                         fb_const(ifb) + pl%na(icv, is)*geo%cvvol(icv)
                    END DO
                    is_end = is
                  END IF
                END DO
              END IF
              fb_prev(ifb) = fb_current(ifb)
            END IF
!
            arg1 = fb_beta(ifb)*(tim-fb_time(ifb))
            fb_target(ifb) = fb_const(ifb)*EXP(arg1)
            fb_rescale(ifb) = fb_alpha(ifb)*((fb_target(ifb)-fb_current(&
&             ifb))/ddtim+(fb_prev(ifb)-fb_current(ifb))/dt_prev)
            saved_fb_actuator(ifb) = fb_current(ifb)
          CASE (6) 
!
! rescale slowed by fb_alpha (SOLPS4 style)
!
            fb_rescale(ifb) = fb_alpha(ifb)*fb_current(ifb)
          CASE DEFAULT
!
            WRITE(*, *) 'fb_rescale_option not coded ', &
&           fb_rescale_option(ifb)
          END SELECT
!
          WRITE(*, '(a,1x,1p,i3,6g15.6)') 'feedback_rescale ', b2espcr(&
&         fb_species(ifb)), fb_current(ifb), fb_target(ifb), fb_rescale(&
&         ifb), fb_prev(ifb), ddtim, dt_prev
!
!
! phase 3:
!
          SELECT CASE  (fb_actuator(ifb)) 
          CASE (0) 

          CASE (1) 
!
! no action
!
! neutral particle flux => gas puff
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_actuator: using ', &
&                             'gas_puff for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            IF (ncall .EQ. 0 .AND. saved_fb_actuator(ifb) .EQ. 0.0_R8) &
&           THEN
              IF (conpar(is_start, fb_ib(ifb), 1) .GT. 0.0_R8) THEN
                saved_fb_actuator(ifb) = conpar(is_start, fb_ib(ifb), 1)
                WRITE(message, '(a,i3,a,i2,a)') &
&               'Initial gas puff strength set by CONPAR(', is_start, &
&               ',', fb_ib(ifb), ',1) !'
              ELSE IF (userfluxparm(fb_istra(ifb), 1) .GT. 0.0_R8) THEN
                saved_fb_actuator(ifb) = userfluxparm(fb_istra(ifb), 1)
                WRITE(message, '(a,i3,a,i2,a)') &
&               'Initial gas puff strength set by USERFLUXPARM(', &
&               fb_istra(ifb), ',1)  !'
              ELSE
                saved_fb_actuator(ifb) = 1.0e18_R8
                WRITE(*, *) 'Warning!  ', 'The gas puff was ', &
&               'started with an initial rate of 1e18!'
              END IF
            END IF
!
            IF (fb_rescale_option(ifb) .LT. 4) THEN
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
            IF (fb_puff_min(ifb) .GE. 0.0_R8) THEN
              IF (fb_puff_min(ifb) .LT. saved_fb_actuator(ifb)) THEN
                saved_fb_actuator(ifb) = saved_fb_actuator(ifb)
              ELSE
                saved_fb_actuator(ifb) = fb_puff_min(ifb)
              END IF
            END IF
            IF (fb_puff_max(ifb) .GT. 0.0_R8) THEN
              IF (fb_puff_max(ifb) .GT. saved_fb_actuator(ifb)) THEN
                saved_fb_actuator(ifb) = saved_fb_actuator(ifb)
              ELSE
                saved_fb_actuator(ifb) = fb_puff_max(ifb)
              END IF
            END IF
!
            IF (fb_overshoot(ifb) .GT. 1.0_R8 .AND. fb_current(ifb) .GT.&
&               fb_target(ifb)*fb_overshoot(ifb)) THEN
! csc Here the idea is to change conpar if fluid neutrals are used, otherwise userfluxparm
!     This way it is easier to generalize the schemes when hybrid neutrals are used (to be done!)
              IF (switch%use_eirene .EQ. 0) THEN
                conpar(is_start, fb_ib(ifb), 1) = 0.0_R8
              ELSE
                userfluxparm(fb_istra(ifb), 1) = 0.0_R8
              END IF
            ELSE IF (switch%use_eirene .EQ. 0) THEN
              conpar(is_start, fb_ib(ifb), 1) = saved_fb_actuator(ifb)
            ELSE
              userfluxparm(fb_istra(ifb), 1) = saved_fb_actuator(ifb)
            END IF
          CASE (2) 
!
! rescale na
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_actuator: using ', &
&                             'density rescale for sequence ', b2espcr(&
&                             fb_species(ifb))
!
            DO is=is_start,is_end
              pl%na(:, is) = pl%na(:, is)*fb_rescale(ifb)
              dv%kinrgy(:, is) = dv%kinrgy(:, is)*fb_rescale(ifb)
              dv%ne = dv%ne + (fb_rescale(ifb)-1.0_R8)*pl%na(:, is)*rt%&
&               rza(:, is)
              dv%fna(:, 0:1, is) = dv%fna(:, 0:1, is)*fb_rescale(ifb)
              dv%fna_mdf(:, 0:1, is) = dv%fna_mdf(:, 0:1, is)*fb_rescale&
&               (ifb)
              dv%fna_fcor(:, 0:1, is) = dv%fna_fcor(:, 0:1, is)*&
&               fb_rescale(ifb)
              dv%fna_nodrift(:, 0:1, is) = dv%fna_nodrift(:, 0:1, is)*&
&               fb_rescale(ifb)
              dv%fna_he(:, 0:1, is) = dv%fna_he(:, 0:1, is)*fb_rescale(&
&               ifb)
              dv%fnapsch(:, 0:1, is) = dv%fnapsch(:, 0:1, is)*fb_rescale&
&               (ifb)
              dv%fna_eir(:, 0:1, is) = dv%fna_eir(:, 0:1, is)*fb_rescale&
&               (ifb)
              dv%fna_32(:, 0:1, is) = dv%fna_32(:, 0:1, is)*fb_rescale(&
&               ifb)
              dv%fna_52(:, 0:1, is) = dv%fna_52(:, 0:1, is)*fb_rescale(&
&               ifb)
            END DO
!   ..initialise previous state and fluxes
            psnl%na = pl%na
            psnc%na = pl%na
            psnl%ne = dv%ne
            psnc%ne = dv%ne
            psnl%fna = dv%fna
            psnc%fna = dv%fna
            psnl%kinrgy = dv%kinrgy
            psnc%kinrgy = dv%kinrgy
!   ..compute initial ni
            CALL B2XPNI_NODIFF_NODIFF(ncv, ns, pl%na, dv%ni)
            CALL B2XPNN_NODIFF_NODIFF(ncv, ns, pl%na, dv%nn)
!   ..compute ni0
            psnl%ni = dv%ni
            psnc%ni = dv%ni
          CASE (3) 
!
! charged species particle flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_actuator: using ', &
&                             'boundary flux condition for sequence ', &
&                             b2espcr(fb_species(ifb))
!
            is_start = eb2spcr(b2espcr(fb_species(ifb))) + 1
            is_end = eb2spcr(b2espcr(fb_species(ifb))) + nfluids(b2espcr&
&             (fb_species(ifb)))
!
            IF (feedback_namelist_used .AND. ncall .EQ. 0 .AND. &
&               saved_fb_actuator(ifb) .EQ. 0.0_R8) saved_fb_actuator(&
&             ifb) = conpar(is_end, fb_ib(ifb), 1)
!
            IF (fb_type(ifb) .EQ. 6) THEN
!csc, this is for backward compatibility, can probably be removed
              saved_fb_actuator(ifb) = (1.0_R8-fb_alpha(ifb))*&
&               saved_fb_actuator(ifb) + fb_rescale(ifb)
            ELSE IF (fb_rescale_option(ifb) .LT. 4) THEN
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
!
            IF (fb_puff_min(ifb) .GE. 0.0_R8) THEN
              IF (fb_puff_min(ifb) .LT. saved_fb_actuator(ifb)) THEN
                saved_fb_actuator(ifb) = saved_fb_actuator(ifb)
              ELSE
                saved_fb_actuator(ifb) = fb_puff_min(ifb)
              END IF
            END IF
            IF (fb_puff_max(ifb) .GT. 0.0_R8) THEN
              IF (fb_puff_max(ifb) .GT. saved_fb_actuator(ifb)) THEN
                saved_fb_actuator(ifb) = saved_fb_actuator(ifb)
              ELSE
                saved_fb_actuator(ifb) = fb_puff_max(ifb)
              END IF
            END IF
!
            IF (fb_overshoot(ifb) .GT. 1.0_R8 .AND. fb_current(ifb) .GT.&
&               fb_target(ifb)*fb_overshoot(ifb)) THEN
              total_flux = 0.0_R8
            ELSE
              total_flux = saved_fb_actuator(ifb)
            END IF
!
            IF (fb_type(ifb) .EQ. 6) THEN
              DO is=is_start,is_end
                iss = is - is_start + 1
                conpar(is, fb_ib(ifb), 1) = total_flux*charge_frac(iss)
              END DO
            ELSE
              conpar(fb_species(ifb), fb_ib(ifb), 1) = total_flux
            END IF
          CASE (4) 
!
! electron heat flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_actuator: using ', &
&                             'electron heat flux boundary condition'
!
            IF (feedback_namelist_used .AND. ncall .EQ. 0 .AND. &
&               saved_fb_actuator(ifb) .EQ. 0.0_R8) saved_fb_actuator(&
&             ifb) = enepar(fb_ib(ifb), 1)
!
            IF (fb_rescale_option(ifb) .LT. 4) THEN
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
!
            enepar(fb_ib(ifb), 1) = saved_fb_actuator(ifb)
          CASE (5) 
!
! ion heat flux
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_actuator: using ', &
&                             'ion heat flux boundary condition'
!
            IF (feedback_namelist_used .AND. ncall .EQ. 0 .AND. &
&               saved_fb_actuator(ifb) .EQ. 0.0_R8) saved_fb_actuator(&
&             ifb) = enipar(fb_ib(ifb), 1)
!
            IF (fb_rescale_option(ifb) .LT. 4) THEN
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
!
            enipar(fb_ib(ifb), 1) = saved_fb_actuator(ifb)
          CASE (6) 
!
! current
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a)') &
&                             'compute_feedback_actuator: using ', &
&                             'electric current boundary condition'
!
            IF (feedback_namelist_used .AND. ncall .EQ. 0 .AND. &
&               saved_fb_actuator(ifb) .EQ. 0.0_R8) saved_fb_actuator(&
&             ifb) = potpar(fb_ib(ifb), 1)
!
            IF (fb_rescale_option(ifb) .LT. 4) THEN
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
!
            potpar(fb_ib(ifb), 1) = saved_fb_actuator(ifb)
          CASE (7) 
!
! density
!
            IF (ncall .EQ. 0) WRITE(*, '(a,a,i3)') &
&                             'compute_feedback_actuator: using ', &
&                             'density condition for sequence ', b2espcr&
&                             (fb_species(ifb))
!
            IF (feedback_namelist_used .AND. ncall .EQ. 0 .AND. &
&               saved_fb_actuator(ifb) .EQ. 0.0_R8) saved_fb_actuator(&
&             ifb) = conpar(fb_species(ifb), fb_ib(ifb), 1)
!
            IF (fb_rescale_option(ifb) .LT. 4) THEN
              saved_fb_actuator(ifb) = saved_fb_actuator(ifb)*fb_rescale&
&               (ifb)
            ELSE
              saved_fb_actuator(ifb) = fb_rescale(ifb)
            END IF
!
            conpar(fb_species(ifb), fb_ib(ifb), 1) = saved_fb_actuator(&
&             ifb)
          CASE DEFAULT
!
            WRITE(*, *) 'fb_actuator not coded ', fb_type(ifb)
          END SELECT
!
          IF (fb_actuator(ifb) .EQ. 1 .OR. fb_actuator(ifb) .EQ. 3) &
&           WRITE(*, '(a,1p,i3,g15.6)') 'feedback_actuator ', b2espcr(&
&           fb_species(ifb)), saved_fb_actuator(ifb)
        END IF
!
!
        feedback_namelist_used = (feedback_namelist_used .OR. &
&         fb_actuator(ifb) .EQ. 1) .OR. fb_actuator(ifb) .EQ. 3
      END DO
!
!ifb
!
      dt_prev = ddtim
    END IF
!
!
    ncall = ncall + 1
    CALL SUBEND()
    RETURN
  END SUBROUTINE COMPUTE_FEEDBACK

!
!
  SUBROUTINE DEALLOC_FEEDBACK()
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    IF (feedback_allocated) THEN
      DEALLOCATE(fbregp)
      DEALLOCATE(fbreg)
      feedback_allocated = .false.
    END IF
  END SUBROUTINE DEALLOC_FEEDBACK

!
  SUBROUTINE DO_FACES(nfc, mpg, ifb, ifbreg)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(MAPPING), INTENT(IN) :: mpg
    INTEGER, INTENT(IN) :: ifb, nfc
    INTEGER, INTENT(INOUT) :: ifbreg
    INTEGER :: ifc
!
    DO ifc=1,nfc
      IF (mpg%fcreg(ifc) .EQ. fb_reg_par(ifb, 1) .OR. (fb_reg_par(ifb, 2&
&         ) .GT. 0 .AND. mpg%fcreg(ifc) .EQ. fb_reg_par(ifb, 2))) THEN
        fbreg(ifbreg) = ifc
        ifbreg = ifbreg + 1
      END IF
    END DO
!
    RETURN
  END SUBROUTINE DO_FACES

!
  SUBROUTINE DO_VOLUMES(ncv, mpg, ifb, ifbreg)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(MAPPING), INTENT(IN) :: mpg
    INTEGER, INTENT(IN) :: ifb, ncv
    INTEGER, INTENT(INOUT) :: ifbreg
    INTEGER :: icv
!
    DO icv=1,ncv
      IF (mpg%cvreg(icv) .EQ. fb_reg_par(ifb, 1) .OR. (fb_reg_par(ifb, 2&
&         ) .GT. 0 .AND. mpg%cvreg(icv) .EQ. fb_reg_par(ifb, 2))) THEN
        fbreg(ifbreg) = icv
        ifbreg = ifbreg + 1
      END IF
    END DO
!
    RETURN
  END SUBROUTINE DO_VOLUMES

!
!**********************************************************************
!
  SUBROUTINE CONV_FBPARMS(m)
    USE B2MOD_FEEDBACK_DIFFV_DIFFV
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(MAPPING), INTENT(INOUT) :: m
    INTEGER :: is, ix, iy, icv, iss, ifbreg
    LOGICAL :: done
!
!number of feedback imposed
    nfb = nspecies
    CALL XERTST(nfb .LE. def_natm, &
&         'b2us_feedback: increase size of DEF_NATM')
    fb_type(1:nfb) = na_feedback_choice(0:nspecies-1)
    fb_rescale_option(1:nfb) = na_feedback_option(0:nspecies-1)
    fb_actuator(1:nfb) = na_feedback_actuator(0:nspecies-1)
    fb_target(1:nfb) = na_feedback_target(0:nspecies-1)
    fb_alpha(1:nfb) = na_feedback_alpha(0:nspecies-1)
    fb_beta(1:nfb) = na_feedback_beta(0:nspecies-1)
    fb_const(1:nfb) = na_feedback_const(0:nspecies-1)
    fb_time(1:nfb) = na_feedback_time(0:nspecies-1)
    fb_ib(1:nfb) = na_feedback_ib(0:nspecies-1)
    fb_puff_min(1:nfb) = na_feedback_puff_min(0:nspecies-1)
    fb_puff_max(1:nfb) = na_feedback_puff_max(0:nspecies-1)
    fb_overshoot(1:nfb) = na_feedback_overshoot(0:nspecies-1)
    ifbreg = 1
    DO is=0,nspecies-1
!new format specifies feedbacks on fluid species
      fb_species(is+1) = eb2spcr(is+1)
      IF (((na_feedback_ix1(is) .GT. -2 .OR. na_feedback_ix2(is) .GT. -2&
&         ) .OR. na_feedback_iy1(is) .GT. -2) .OR. na_feedback_iy2(is) &
&         .GT. -2) THEN
! check if same region has already been done
        done = .false.
        DO iss=0,is-1
          IF (na_feedback_ix1(is) .EQ. na_feedback_ix1(iss) .AND. &
&             na_feedback_ix2(is) .EQ. na_feedback_ix2(iss) .AND. &
&             na_feedback_iy1(is) .EQ. na_feedback_iy1(iss) .AND. &
&             na_feedback_iy2(is) .EQ. na_feedback_iy2(iss)) THEN
            fb_regp(is+1, :) = fb_regp(iss+1, :)
            done = .true.
          END IF
        END DO
        IF (.NOT.done) THEN
          fb_regp(is+1, 1) = ifbreg
          DO ix=na_feedback_ix1(is),na_feedback_ix2(is)
            DO iy=na_feedback_iy1(is),na_feedback_iy2(is)
              CALL XERTST(ifbreg .LE. nmxfbreg, &
&                   'b2us_feedback: increase size of nMxFbReg')
              icv = m%imapcv(ix, iy)
              IF (icv .NE. 0) THEN
                fb_reg(ifbreg) = icv
                ifbreg = ifbreg + 1
              END IF
            END DO
          END DO
          fb_regp(is+1, 2) = ifbreg - fb_regp(is+1, 1)
        END IF
      END IF
    END DO
    nfbreg = ifbreg - 1
!
    RETURN
  END SUBROUTINE CONV_FBPARMS

