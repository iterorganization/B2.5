  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!   ..test ev, amh0
  CALL XERTST(0 .LT. ev, 'faulty argument ev')
  CALL XERTST(0 .LT. amh0 .AND. amh0 .LE. 3, &
&       'faulty argument range amh0')
!   ..extensive tests on first few calls
  IF (ncall_b2spcx .LT. 3) THEN
    CALL B2XVSG(ncv, th0, 1, 'th0', '.gt.')
    CALL B2XVSG(ncv, ne, 1, 'ne', '.gt.')
  END IF
!
! ..compute rate coefficients
!   ..initialise indices for interpolation
  it0 = 0
  in0 = 0
!   ..loop over cells
  DO icv=1,ncv
    arg1 = th0(icv)/(amh0*ev)
    DO nd=1,nbdirs
      arg1d(nd) = th0d(nd, icv)/(amh0*ev)
      y3d(nd) = arg1d(nd)/arg1
    END DO
    y3 = LOG(arg1)
    IF (rtlt(rtnt) .GT. y3) THEN
      DO nd=1,nbdirs
        y1d(nd) = y3d(nd)
      END DO
      y1 = y3
    ELSE
      y1 = rtlt(rtnt)
      DO nd=1,nbdirsmax
        y1d(nd) = 0.d0
      END DO
    END IF
    IF (rtlt(0) .LT. y1) THEN
      DO nd=1,nbdirs
        rlt0d(nd) = y1d(nd)
      END DO
      rlt0 = y1
    ELSE
      rlt0 = rtlt(0)
      DO nd=1,nbdirsmax
        rlt0d(nd) = 0.d0
      END DO
    END IF
    DO nd=1,nbdirs
      y4d(nd) = ned(nd, icv)/ne(icv)
    END DO
    y4 = LOG(ne(icv))
    IF (rtln(rtnn) .GT. y4) THEN
      DO nd=1,nbdirs
        y2d(nd) = y4d(nd)
      END DO
      y2 = y4
    ELSE
      y2 = rtln(rtnn)
      DO nd=1,nbdirsmax
        y2d(nd) = 0.d0
      END DO
    END IF
    IF (rtln(0) .LT. y2) THEN
      DO nd=1,nbdirs
        rln0d(nd) = y2d(nd)
      END DO
      rln0 = y2
    ELSE
      rln0 = rtln(0)
      DO nd=1,nbdirsmax
        rln0d(nd) = 0.d0
      END DO
    END IF
!     ..find it0
!       (linear search starting from previous it0, to establish
!         0.le.it0.lt.rtnt;
!         it0.eq.0.or.rtlt(it0).le.rlt0;
!         it0.eq.rtnt-1.or.rlt0.lt.rtlt(it0+1).)
 1  IF (it0 .LT. rtnt - 1) THEN
      IF (rtlt(it0+1) .LE. rlt0) THEN
        it0 = it0 + 1
        GOTO 1
      END IF
    END IF
 2  IF (0 .LT. it0) THEN
      IF (rlt0 .LT. rtlt(it0)) THEN
        it0 = it0 - 1
        GOTO 2
      END IF
    END IF
!     ..compute coefficient for linear interpolation
    temp = rtlt(it0+1) - rtlt(it0)
    DO nd=1,nbdirs
      fxt0d(nd) = rlt0d(nd)/temp
    END DO
    fxt0 = (rlt0-rtlt(it0))/temp
!     ..find in0
!       (linear search starting from previous in0, to establish
!         0.le.in0.lt.rtnn;
!         in0.eq.0.or.rtln(in0).le.rln0;
!         in0.eq.rtnn-1.or.rln0.lt.rtln(in0+1).)
 3  IF (in0 .LT. rtnn - 1) THEN
      IF (rtln(in0+1) .LE. rln0) THEN
        in0 = in0 + 1
        GOTO 3
      END IF
    END IF
 4  IF (0 .LT. in0) THEN
      IF (rln0 .LT. rtln(in0)) THEN
        in0 = in0 - 1
        GOTO 4
      END IF
    END IF
!     ..compute coefficient for linear interpolation
    temp = rtln(in0+1) - rtln(in0)
    DO nd=1,nbdirs
      fxn0d(nd) = rln0d(nd)/temp
    END DO
    fxn0 = (rln0-rtln(in0))/temp
!     ..compute log-log rate coefficients
    DO is=0,ns-1
!      ..compute charge exchange rate coefficient
      temp = rtlcx(it0, in0+1, is)
      DO nd=1,nbdirs
        t0d(nd) = (1-fxn0)*rtlcxd(nd, it0, in0, is) - (rtlcx(it0, in0, &
&         is)-temp)*fxn0d(nd) + fxn0*rtlcxd(nd, it0, in0+1, is)
      END DO
      t0 = (1-fxn0)*rtlcx(it0, in0, is) + fxn0*temp
      temp = rtlcx(it0+1, in0, is)
      temp0 = rtlcx(it0+1, in0+1, is)
      DO nd=1,nbdirs
        t1d(nd) = (1-fxn0)*rtlcxd(nd, it0+1, in0, is) - (temp-temp0)*&
&         fxn0d(nd) + fxn0*rtlcxd(nd, it0+1, in0+1, is)
      END DO
      t1 = (1-fxn0)*temp + fxn0*temp0
      temp0 = rtlt(it0+1) - rtlt(it0)
      DO nd=1,nbdirs
        ttd(nd) = (t1d(nd)-t0d(nd))/temp0
      END DO
      tt = (t1-t0)/temp0
      CALL TRIMG_DV(-20.0_R8, 20.0_R8, tt, ttd, rlcx0(icv, 1, is), &
&             rlcx0d(:, icv, 1, is), nbdirs)
      DO nd=1,nbdirs
        rlcx0d(nd, icv, 0, is) = (1-fxt0)*t0d(nd) - (t0-t1)*fxt0d(nd) + &
&         fxt0*t1d(nd) - rlt0*rlcx0d(nd, icv, 1, is) - rlcx0(icv, 1, is)&
&         *rlt0d(nd)
      END DO
      rlcx0(icv, 0, is) = (1-fxt0)*t0 + fxt0*t1 - rlcx0(icv, 1, is)*rlt0
    END DO
  END DO
!
! ..return
  ncall_b2spcx = ncall_b2spcx + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2SPCX_DV_NODIFF

