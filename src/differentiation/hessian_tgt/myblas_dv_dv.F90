FUNCTION B2S2MIN_NODIFF_NODIFF(n1, n2, sx, inc1x, inc2x) RESULT (b2s2min&
&)
  USE B2MOD_TYPES
  USE B2MOD_MATH_DIFFV_DIFFV
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
  REAL(kind=r8) :: b2s2min
  INTEGER :: n1, n2, inc1x, inc2x
  REAL(kind=r8) :: sx(*)
!     ------------------------------------------------------------------
!     COMPUTE MINIMUM VALUE IN THE D.P. REAL MATRIX DESCRIBED BY
!     (N1, N2, SX, INC1X, INC2X). BLAS**2 STORAGE CONVENTIONS.
!     N1 AND N2 MUST BE POSITIVE.
!     ------------------------------------------------------------------
  INTEGER :: i, i2
  EXTERNAL smin, XERRAB
  REAL(kind=r8) :: t, smin
  INTRINSIC MIN
  REAL(kind=r8) :: y1
  INTEGER :: arg1
!     ------------------------------------------------------------------
  IF (n1 .LT. 1 .OR. n2 .LT. 1) THEN
    CALL XERRAB('b2s2min: invalid n1/n2 values')
!        - - - - - - -
!     ---------------------------------------------------BJB-83/04/06---
  ELSE IF (n1*inc1x .EQ. inc2x) THEN
    arg1 = n1*n2
    b2s2min = smin(arg1, sx, inc1x)
    RETURN
!        - - - - - - -
  ELSE
    i2 = IBASE1(n2, inc2x)
    t = smin(n1, sx(i2), inc1x)
    i2 = i2 + inc2x
    DO i=2,n2
      y1 = smin(n1, sx(i2), inc1x)
      IF (t .GT. y1) THEN
        t = y1
      ELSE
        t = t
      END IF
      i2 = i2 + inc2x
    END DO
    b2s2min = t
    RETURN
!        - - - - - - -
  END IF
END FUNCTION B2S2MIN_NODIFF_NODIFF

