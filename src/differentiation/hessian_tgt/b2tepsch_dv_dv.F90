SUBROUTINE B2TEPSCH_NODIFF_NODIFF(ncv, nfc, nvx, switch, geo, mpg, &
& facdrift, cdde, te, ne, floe, cone, fhepsch)
  USE B2MOD_TYPES
  USE B2MOD_B2CMPA_DIFFV
  USE B2MOD_SWITCHES_DIFFV_DIFFV
  USE B2US_GEO_DIFFV_DIFFV
  USE B2US_MAP_DIFFV_DIFFV
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  REAL(kind=r8) :: facdrift(nfc), cdde(nfc, 0:1), te(ncv), ne(ncv)
!   ..output arguments (unspecified on entry)
  REAL(kind=r8) :: floe(nfc, 0:1), cone(nfc, 0:1, 0:2), fhepsch(nfc, 0:1&
& )
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2ETPSCH computes the poloidal and radial components of the
!     Pfirsch-Schlueter electron heat flux, and the coefficients that
!     specify its linearization.
!
!  8. further comments
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: ifc
  INTEGER, SAVE :: ncall=0
  REAL(kind=r8) :: facdriftm, nete(ncv), nete2(ncv), netev(nvx), nete2v(&
& nvx), dnete2(nfc, 0:1), floe0(nfc, 0:1), cone0(nfc, 0:1), cdde_eff(nfc&
& , 0:1), dumm0(nfc, 0:1)
  INTRINSIC MAXVAL
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2tepsch')
!   ..test nCv, nFc
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
!
!   ..set dummy arrays
  dumm0 = 0.0_R8
!
!srv 13.10.06
  fhepsch = 0.0_R8
!srv 02.01.07
  cone = 0.0_R8
  floe = 0.0_R8
  facdriftm = MAXVAL(facdrift)
  IF (facdriftm .NE. 0.0_R8) THEN
!
!   ..precompute face and vertex values of nete2
    nete = ne*te
    nete2 = ne*te*te
    CALL INTVERTEX_NODIFF_NODIFF(ncv, nvx, mpg, geo%vxvol, nete, netev)
    CALL INTVERTEX_NODIFF_NODIFF(ncv, nvx, mpg, geo%vxvol, nete2, nete2v&
&                         )
    CALL DIFF_NODIFF_NODIFF(ncv, nfc, nvx, 1, geo, mpg, nete2, nete2v, &
&                     dnete2)
!
!   ..compute P.Sch heat flux
    fhepsch(:, 0) = -(2.5_R8*switch%fhepsch*facdrift*cdde(:, 0)*geo%&
&     fcqalf(:, 0)*dnete2(:, 1))
    fhepsch(:, 1) = 2.5_R8*switch%fhepsch*facdrift*cdde(:, 1)*geo%fcqalf&
&     (:, 1)*dnete2(:, 0)
!     ..linearization coefficients for P.Sch flows
    cdde_eff(:, 0) = 5.0_R8*switch%fhepsch*facdrift*cdde(:, 1)*geo%&
&     fcqalf(:, 1)
    cdde_eff(:, 1) = -(5.0_R8*switch%fhepsch*facdrift*cdde(:, 0)*geo%&
&     fcqalf(:, 0))
    CALL CALCCOEF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, dumm0, cdde_eff, &
&                         floe0, cone0)
    DO ifc=1,nfc
      floe(ifc, 0) = 0.5_R8*floe0(ifc, 0)*(nete(mpg%fccv(ifc, 1))+nete(&
&       mpg%fccv(ifc, 2))) - cone0(ifc, 0)*(nete(mpg%fccv(ifc, 2))-nete(&
&       mpg%fccv(ifc, 1)))
      floe(ifc, 1) = 0.5_R8*floe0(ifc, 1)*(netev(mpg%fcvx(ifc, 1))+netev&
&       (mpg%fcvx(ifc, 2))) - cone0(ifc, 1)*(netev(mpg%fcvx(ifc, 2))-&
&       netev(mpg%fcvx(ifc, 1)))
!wdk      For now, directly add these contributions into cone(,,0),
!wdk      so they are directly added into the stencil.
!wdk      Might need to move back to cone(,,1) and dedicated treatment
!wdk      in b2ux9p if we decide to move away from inverse volume weighting
!wdk      to compute vertex values of drifts!
      cone(ifc, 0, 0) = 0.5_R8*cone0(ifc, 0)*(nete(mpg%fccv(ifc, 1))+&
&       nete(mpg%fccv(ifc, 2))) - 0.25_R8*floe0(ifc, 0)*(nete(mpg%fccv(&
&       ifc, 2))-nete(mpg%fccv(ifc, 1)))
      cone(ifc, 1, 0) = 0.5_R8*cone0(ifc, 1)*(netev(mpg%fcvx(ifc, 1))+&
&       netev(mpg%fcvx(ifc, 2))) - 0.25_R8*floe0(ifc, 1)*(netev(mpg%fcvx&
&       (ifc, 2))-netev(mpg%fcvx(ifc, 1)))
    END DO
  END IF
! ..return
  ncall = ncall + 1
  CALL SUBEND()
  RETURN
!-----------------------------------------------------------------------
!.end b2tepsch
END SUBROUTINE B2TEPSCH_NODIFF_NODIFF

