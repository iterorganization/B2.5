      DO nd=1,nbdirs
        floed(nd, ifc, 0) = floe0(ifc, 0)*0.5_R8*(neted(nd, mpg%fccv(ifc&
&         , 1))+neted(nd, mpg%fccv(ifc, 2))) - cone0(ifc, 0)*(neted(nd, &
&         mpg%fccv(ifc, 2))-neted(nd, mpg%fccv(ifc, 1)))
        floed(nd, ifc, 1) = floe0(ifc, 1)*0.5_R8*(netevd(nd, mpg%fcvx(&
&         ifc, 1))+netevd(nd, mpg%fcvx(ifc, 2))) - cone0(ifc, 1)*(netevd&
&         (nd, mpg%fcvx(ifc, 2))-netevd(nd, mpg%fcvx(ifc, 1)))
!wdk      For now, directly add these contributions into cone(,,0),
!wdk      so they are directly added into the stencil.
!wdk      Might need to move back to cone(,,1) and dedicated treatment
!wdk      in b2ux9p if we decide to move away from inverse volume weighting
!wdk      to compute vertex values of drifts!
        coned(nd, ifc, 0, 0) = cone0(ifc, 0)*0.5_R8*(neted(nd, mpg%fccv(&
&         ifc, 1))+neted(nd, mpg%fccv(ifc, 2))) - floe0(ifc, 0)*0.25_R8*&
&         (neted(nd, mpg%fccv(ifc, 2))-neted(nd, mpg%fccv(ifc, 1)))
        coned(nd, ifc, 1, 0) = cone0(ifc, 1)*0.5_R8*(netevd(nd, mpg%fcvx&
&         (ifc, 1))+netevd(nd, mpg%fcvx(ifc, 2))) - floe0(ifc, 1)*&
&         0.25_R8*(netevd(nd, mpg%fcvx(ifc, 2))-netevd(nd, mpg%fcvx(ifc&
&         , 1)))
      END DO
      floe(ifc, 0) = 0.5_R8*floe0(ifc, 0)*(nete(mpg%fccv(ifc, 1))+nete(&
&       mpg%fccv(ifc, 2))) - cone0(ifc, 0)*(nete(mpg%fccv(ifc, 2))-nete(&
&       mpg%fccv(ifc, 1)))
      floe(ifc, 1) = 0.5_R8*floe0(ifc, 1)*(netev(mpg%fcvx(ifc, 1))+netev&
&       (mpg%fcvx(ifc, 2))) - cone0(ifc, 1)*(netev(mpg%fcvx(ifc, 2))-&
&       netev(mpg%fcvx(ifc, 1)))
      cone(ifc, 0, 0) = 0.5_R8*cone0(ifc, 0)*(nete(mpg%fccv(ifc, 1))+&
&       nete(mpg%fccv(ifc, 2))) - 0.25_R8*floe0(ifc, 0)*(nete(mpg%fccv(&
&       ifc, 2))-nete(mpg%fccv(ifc, 1)))
      cone(ifc, 1, 0) = 0.5_R8*cone0(ifc, 1)*(netev(mpg%fcvx(ifc, 1))+&
&       netev(mpg%fcvx(ifc, 2))) - 0.25_R8*floe0(ifc, 1)*(netev(mpg%fcvx&
&       (ifc, 2))-netev(mpg%fcvx(ifc, 1)))
    END DO
  ELSE
    floed = 0.d0
    fhepschd = 0.d0
    coned = 0.d0
  END IF
! ..return
  ncall = ncall + 1
  CALL SUBEND()
  RETURN
!-----------------------------------------------------------------------
!.end b2tepsch
END SUBROUTINE B2TEPSCH_DV_NODIFF

