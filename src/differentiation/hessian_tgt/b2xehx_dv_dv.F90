SUBROUTINE B2XEHX_NODIFF_NODIFF(ncv, nfc, nvx, switch, geo, mpg, te, po&
& , ne, ehx)
  USE B2MOD_TYPES
  USE B2MOD_CONSTANTS
  USE B2US_GEO_DIFFV_DIFFV
  USE B2US_MAP_DIFFV_DIFFV
  USE B2MOD_SWITCHES_DIFFV_DIFFV
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_AD_DIFFV_DIFFV, ONLY : ncall_b2xehx
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  REAL(kind=r8) :: te(ncv), po(ncv), ne(ncv)
!   ..output arguments (unspecified on entry)
  REAL(kind=r8) :: ehx(nfc)
!   ..common blocks
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2XEHX computes the modified poloidal electrical field on all cell
!     faces.
!
!  8. further comments
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..global named constants
!
!   ..local variables
  REAL(kind=r8) :: dpop(nfc), wrk0(nfc), wrk1(nfc), wrk2(nfc), wrkv(nvx)&
& , nete(ncv), ones(nfc, 2)
!   ..procedures
  EXTERNAL XERTST
  EXTERNAL B2XVSG
  INTRINSIC LOG
  REAL(kind=r8), DIMENSION(ncv) :: arg1
!   ..initialization
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2xehx')
!   ..test nCv, nFc
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
!   ..extensive tests on first few calls
  IF (ncall_b2xehx .LT. 3) THEN
!    ..test sign of ne, te, csig
    CALL B2XVSG(ncv, ne, 1, 'ne', '.gt.')
    CALL B2XVSG(ncv, te, 1, 'te', '.gt.')
  END IF
!srv 29.01.20 }
!
!   ..compute ehx
  IF (switch%pot_eq .NE. 1 .AND. switch%no_current .NE. 0) THEN
!srv 29.01.20 {
    ehx = 0.0_R8
  ELSE IF (switch%mode_ehx .EQ. 0) THEN
    ones = 1.0_R8
    nete = ne*te
!       ..compute poloidal differences of po and pe
    CALL DIFF_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, po, wrkv, dpop&
&                      )
    CALL DIFF_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, nete, wrkv, &
&                       wrk0)
!       ..interpolate ne to faces
    CALL INTFACE(ncv, nfc, mpg%fccv, ones, ne, wrk1)
!       ..compute ehx      
    ehx = -dpop + wrk0/(qe*wrk1)
  ELSE IF (switch%mode_ehx .EQ. 1) THEN
    nete = ne*te
!       ..compute poloidal differences of po and pe
    CALL DIFF_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, po, wrkv, dpop&
&                      )
    CALL DIFF_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, nete, wrkv, &
&                       wrk0)
!       ..interpolate ne to faces
    CALL INTFACE(ncv, nfc, mpg%fccv, geo%fchc, ne, wrk1)
!       ..compute ehx
    ehx = -dpop + wrk0/(qe*wrk1)
  ELSE
    ones = 1.0_R8
!       ..compute poloidal differences of po, te and log(ne) on faces
    CALL DIFF_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, po, wrkv, dpop&
&                      )
    CALL DIFF_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, te, wrkv, wrk0&
&                      )
    arg1 = LOG(ne)
    CALL DIFF_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, arg1, wrkv, &
&                       wrk1)
!       ..interpolate te to faces
    CALL INTFACE(ncv, nfc, mpg%fccv, ones, te, wrk2)
!       ..compute ehx      
    ehx = -dpop + (wrk0+wrk1*wrk2)/qe
  END IF
!
! ..return
  ncall_b2xehx = ncall_b2xehx + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2xehx
!
END SUBROUTINE B2XEHX_NODIFF_NODIFF

