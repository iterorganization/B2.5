                pwr2d(nd) = pwy2*pwr11*pwx2d(nd)
              END IF
              t2d(nd) = -(pwr2d(nd)/pwr2**2)
!    ..poloidal
              cdpad(nd, ifc, 0, is) = t2*cdpad(nd, ifc, 0, is) + cdpa(&
&               ifc, 0, is)*t2d(nd)
              cdpahzd(nd, ifc, 0, is) = t2*cdpahzd(nd, ifc, 0, is) + &
&               cdpahz(ifc, 0, is)*t2d(nd)
            END DO
            cdpa(ifc, 0, is) = cdpa(ifc, 0, is)*t2
            cdpahz(ifc, 0, is) = cdpahz(ifc, 0, is)*t2
            flc(ifc, 0, is) = t2
            IF (maxfluxlimit(0) .LT. t2) THEN
              maxfluxlimit(0) = t2
            ELSE
              maxfluxlimit(0) = maxfluxlimit(0)
            END IF
            DO nd=1,nbdirs
!    ..radial
              cdpad(nd, ifc, 1, is) = t2*cdpad(nd, ifc, 1, is) + cdpa(&
&               ifc, 1, is)*t2d(nd)
              cdpahzd(nd, ifc, 1, is) = t2*cdpahzd(nd, ifc, 1, is) + &
&               cdpahz(ifc, 1, is)*t2d(nd)
            END DO
            cdpa(ifc, 1, is) = cdpa(ifc, 1, is)*t2
            cdpahz(ifc, 1, is) = cdpahz(ifc, 1, is)*t2
            flc(ifc, 1, is) = t2
            IF (maxfluxlimit(1) .LT. t2) THEN
              maxfluxlimit(1) = t2
            ELSE
              maxfluxlimit(1) = maxfluxlimit(1)
            END IF
          END DO
        END IF
      END DO
    END IF
  END IF
!
!      write(*,'(1x,a,1p,2e10.2)') 'b2tlc0: maxfluxlimit = ',maxfluxlimit
!
  IF (switch%b2tlc0_iout .NE. 0) THEN
    DO is=0,ns-1
      IF (is_neutral(is)) THEN
        WRITE(charns, '(i3.3)') is
        arg12(:) = 'fl_fn_th'//charns
        CALL MY_OUT_US(70, nfc, 1, flc(1, 0, is), arg12(:))
        arg13(:) = 'fl_fn_r'//charns
        CALL MY_OUT_US(70, nfc, 1, flc(1, 1, is), arg13(:))
      END IF
    END DO
  END IF
!
!   ..return
  CALL SUBEND()
  RETURN
END SUBROUTINE B2TLC0_DV_NODIFF

