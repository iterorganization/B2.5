      END IF
    END DO
!
!   ..fully assign to main ion species if needed
    IF (switch%anml_all_ions .EQ. 0) THEN
      DO is=0,ns-1
        IF (is .EQ. ismain) THEN
          fchanml_a(:, :, is) = fchanml
        ELSE
          fchanml_a(:, :, is) = 0.0_R8
        END IF
      END DO
    END IF
  END IF
!
!
! ..return
  CALL SUBEND()
  RETURN
