SUBROUTINE B2TNPSCH_DV_NODIFF(ncv, nfc, nvx, switch, geo, geod, mpg, &
& mpgd, facdrift, cddb, cddbd, ti, tid, nb, nbd, kmprti, kmprtid, flob, &
& flobd, conb, conbd, fnbpsch, fnbpschd, nbdirs)
  USE B2MOD_TYPES
  USE B2MOD_B2CMPA_DIFFV
  USE B2MOD_SWITCHES_DIFFV_DIFFV
  USE B2US_GEO_DIFFV_DIFFV
  USE B2US_MAP_DIFFV_DIFFV
  USE B2MOD_SUBSYS
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(GEOMETRY_DIFFV), INTENT(IN) :: geod
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(MAPPING_DIFFV), INTENT(IN) :: mpgd
  REAL(kind=r8) :: facdrift(nfc), cddb(nfc, 0:1), ti(ncv), nb(ncv), &
& kmprti(ncv)
  REAL(kind=r8) :: cddbd(nbdirsmax, nfc, 0:1), tid(nbdirsmax, ncv), nbd(&
& nbdirsmax, ncv), kmprtid(nbdirsmax, ncv)
!   ..output arguments (unspecified on entry)
  REAL(kind=r8) :: flob(nfc, 0:1), conb(nfc, 0:1, 0:2), fnbpsch(nfc, 0:1&
& )
  REAL(kind=r8) :: flobd(nbdirsmax, nfc, 0:1), conbd(nbdirsmax, nfc, 0:1&
& , 0:2), fnbpschd(nbdirsmax, nfc, 0:1)
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2TNPSCH computes the poloidal and radial components of the
!     Pfirsch-Schlueter particle flux of one atomic species, and the
!     coefficients that specify its linearization.
!
!  8. further comments
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: ifc
  INTEGER, SAVE :: ncall=0
  REAL(kind=r8) :: facdriftm, nbti(ncv), nbtiv(nvx), kmprtiv(nvx), dnbti&
& (nfc, 0:1), flob0(nfc, 0:1), conb0(nfc, 0:1), cddb_eff(nfc, 0:1), &
& dumm0(nfc, 0:1)
  REAL(kind=r8) :: nbtid(nbdirsmax, ncv), nbtivd(nbdirsmax, nvx), &
& kmprtivd(nbdirsmax, nvx), dnbtid(nbdirsmax, nfc, 0:1), flob0d(&
& nbdirsmax, nfc, 0:1), conb0d(nbdirsmax, nfc, 0:1), cddb_effd(nbdirsmax&
& , nfc, 0:1), dumm0d(nbdirsmax, nfc, 0:1)
  INTRINSIC MAXVAL
  INTEGER :: nd
  REAL(kind=r8), DIMENSION(nfc) :: temp
  REAL(kind=r8) :: temp0
  INTEGER :: nbdirs
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2tnpsch')
!   ..test nCv, nFc
  CALL XERTST(0 .LE. ncv .AND. 0 .LE. nfc, 'faulty argument nCv, nFc')
!
!   ..set dummy arrays
  dumm0 = 0.0_R8
!
!srv 13.10.06
  fnbpsch = 0.0e0_R8
!srv 02.01.07
  DO nd=1,nbdirs
    conbd(nd, :, :, :) = 0.d0
  END DO
  conb = 0.0e0_R8
  flob = 0.0e0_R8
  facdriftm = MAXVAL(facdrift)
  IF (facdriftm .NE. 0.0e0_R8) THEN
!
!     ..linearization coefficients for P.Sch flows
    cddb_effd = 0.d0
    DO nd=1,nbdirs
!
!     ..precompute face and vertex values of nbti and kmprti
      nbtid(nd, :) = ti*nbd(nd, :) + nb*tid(nd, :)
      cddb_effd(nd, :, 0) = -(switch%fnbpsch*facdrift*geo%fcqalf(:, 1)*&
&       cddbd(nd, :, 1))
      cddb_effd(nd, :, 1) = switch%fnbpsch*facdrift*geo%fcqalf(:, 0)*&
&       cddbd(nd, :, 0)
    END DO
    nbti = nb*ti
    nbtivd = 0.d0
    CALL INTVERTEX_DV_NODIFF(ncv, nvx, mpg, geo%vxvol, nbti, nbtid, &
&                      nbtiv, nbtivd, nbdirs)
    kmprtivd = 0.d0
    CALL INTVERTEX_DV_NODIFF(ncv, nvx, mpg, geo%vxvol, kmprti, kmprtid, &
&                      kmprtiv, kmprtivd, nbdirs)
    dnbtid = 0.d0
    CALL DIFF_DV_NODIFF(ncv, nfc, nvx, 1, geo, geod, mpg, mpgd, nbti, &
&                 nbtid, nbtiv, nbtivd, dnbti, dnbtid, nbdirs)
!
!     ..compute P.Sch heat flux
    fnbpschd = 0.d0
    temp = switch%fnbpsch*facdrift*geo%fcqalf(:, 0)
    DO nd=1,nbdirs
      fnbpschd(nd, :, 0) = temp*(dnbti(:, 1)*cddbd(nd, :, 0)+cddb(:, 0)*&
&       dnbtid(nd, :, 1))
    END DO
    fnbpsch(:, 0) = temp*(cddb(:, 0)*dnbti(:, 1))
    temp = switch%fnbpsch*facdrift*geo%fcqalf(:, 1)
    DO nd=1,nbdirs
      fnbpschd(nd, :, 1) = -(temp*(dnbti(:, 0)*cddbd(nd, :, 1)+cddb(:, 1&
&       )*dnbtid(nd, :, 0)))
    END DO
    fnbpsch(:, 1) = -(temp*(cddb(:, 1)*dnbti(:, 0)))
    cddb_eff(:, 0) = -(switch%fnbpsch*facdrift*cddb(:, 1)*geo%fcqalf(:, &
&     1))
    cddb_eff(:, 1) = switch%fnbpsch*facdrift*cddb(:, 0)*geo%fcqalf(:, 0)
    conb0d = 0.d0
    flob0d = 0.d0
    dumm0d = 0.d0
    CALL CALCCOEF_DV_NODIFF(ncv, nfc, nvx, 0, geo, dumm0, dumm0d, &
&                     cddb_eff, cddb_effd, flob0, flob0d, conb0, conb0d&
&                     , nbdirs)
    flobd = 0.d0
    conbd = 0.d0
    DO ifc=1,nfc
      temp0 = kmprti(mpg%fccv(ifc, 2)) - kmprti(mpg%fccv(ifc, 1))
      DO nd=1,nbdirs
        flobd(nd, ifc, 0) = flob0(ifc, 0)*0.5_R8*(kmprtid(nd, mpg%fccv(&
&         ifc, 1))+kmprtid(nd, mpg%fccv(ifc, 2))) - temp0*conb0d(nd, ifc&
&         , 0) - conb0(ifc, 0)*(kmprtid(nd, mpg%fccv(ifc, 2))-kmprtid(nd&
&         , mpg%fccv(ifc, 1)))
      END DO
      flob(ifc, 0) = flob0(ifc, 0)*0.5_R8*(kmprti(mpg%fccv(ifc, 1))+&
&       kmprti(mpg%fccv(ifc, 2))) - conb0(ifc, 0)*temp0
      temp0 = kmprtiv(mpg%fcvx(ifc, 2)) - kmprtiv(mpg%fcvx(ifc, 1))
      DO nd=1,nbdirs
        flobd(nd, ifc, 1) = flob0(ifc, 1)*0.5_R8*(kmprtivd(nd, mpg%fcvx(&
&         ifc, 1))+kmprtivd(nd, mpg%fcvx(ifc, 2))) - temp0*conb0d(nd, &
&         ifc, 1) - conb0(ifc, 1)*(kmprtivd(nd, mpg%fcvx(ifc, 2))-&
&         kmprtivd(nd, mpg%fcvx(ifc, 1)))
      END DO
      flob(ifc, 1) = flob0(ifc, 1)*0.5_R8*(kmprtiv(mpg%fcvx(ifc, 1))+&
&       kmprtiv(mpg%fcvx(ifc, 2))) - conb0(ifc, 1)*temp0
!wdk      For now, directly add these contributions into conb(,,0),
!wdk      so they are directly added into the stencil.
!wdk      Might need to move back to conb(,,3) and dedicated treatment
!wdk      in b2ux9p if we decide to move away from inverse volume weighting
!wdk      to compute vertex values of drifts!
      temp0 = kmprti(mpg%fccv(ifc, 1)) + kmprti(mpg%fccv(ifc, 2))
      DO nd=1,nbdirs
        conbd(nd, ifc, 0, 0) = 0.5_R8*(temp0*conb0d(nd, ifc, 0)+conb0(&
&         ifc, 0)*(kmprtid(nd, mpg%fccv(ifc, 1))+kmprtid(nd, mpg%fccv(&
&         ifc, 2)))) - flob0(ifc, 0)*0.25_R8*(kmprtid(nd, mpg%fccv(ifc, &
&         2))-kmprtid(nd, mpg%fccv(ifc, 1)))
      END DO
      conb(ifc, 0, 0) = 0.5_R8*(conb0(ifc, 0)*temp0) - flob0(ifc, 0)*&
&       0.25_R8*(kmprti(mpg%fccv(ifc, 2))-kmprti(mpg%fccv(ifc, 1)))
      temp0 = kmprtiv(mpg%fcvx(ifc, 1)) + kmprtiv(mpg%fcvx(ifc, 2))
      DO nd=1,nbdirs
        conbd(nd, ifc, 1, 0) = 0.5_R8*(temp0*conb0d(nd, ifc, 1)+conb0(&
&         ifc, 1)*(kmprtivd(nd, mpg%fcvx(ifc, 1))+kmprtivd(nd, mpg%fcvx(&
&         ifc, 2)))) - flob0(ifc, 1)*0.25_R8*(kmprtivd(nd, mpg%fcvx(ifc&
&         , 2))-kmprtivd(nd, mpg%fcvx(ifc, 1)))
      END DO
      conb(ifc, 1, 0) = 0.5_R8*(conb0(ifc, 1)*temp0) - flob0(ifc, 1)*&
&       0.25_R8*(kmprtiv(mpg%fcvx(ifc, 2))-kmprtiv(mpg%fcvx(ifc, 1)))
    END DO
  ELSE
    flobd = 0.d0
    conbd = 0.d0
    fnbpschd = 0.d0
  END IF
!
! ..return
  ncall = ncall + 1
  CALL SUBEND()
  RETURN
!-----------------------------------------------------------------------
!.end b2tnpsch
END SUBROUTINE B2TNPSCH_DV_NODIFF

