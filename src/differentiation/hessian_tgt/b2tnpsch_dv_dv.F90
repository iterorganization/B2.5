SUBROUTINE B2TNPSCH_NODIFF_NODIFF(ncv, nfc, nvx, switch, geo, mpg, &
& facdrift, cddb, ti, nb, kmprti, flob, conb, fnbpsch)
  USE B2MOD_TYPES
  USE B2MOD_B2CMPA_DIFFV
  USE B2MOD_SWITCHES_DIFFV_DIFFV
  USE B2US_GEO_DIFFV_DIFFV
  USE B2US_MAP_DIFFV_DIFFV
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  REAL(kind=r8) :: facdrift(nfc), cddb(nfc, 0:1), ti(ncv), nb(ncv), &
& kmprti(ncv)
!   ..output arguments (unspecified on entry)
  REAL(kind=r8) :: flob(nfc, 0:1), conb(nfc, 0:1, 0:2), fnbpsch(nfc, 0:1&
& )
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2TNPSCH computes the poloidal and radial components of the
!     Pfirsch-Schlueter particle flux of one atomic species, and the
!     coefficients that specify its linearization.
!
!  8. further comments
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: ifc
  INTEGER, SAVE :: ncall=0
  REAL(kind=r8) :: facdriftm, nbti(ncv), nbtiv(nvx), kmprtiv(nvx), dnbti&
& (nfc, 0:1), flob0(nfc, 0:1), conb0(nfc, 0:1), cddb_eff(nfc, 0:1), &
& dumm0(nfc, 0:1)
  INTRINSIC MAXVAL
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2tnpsch')
!   ..test nCv, nFc
  CALL XERTST(0 .LE. ncv .AND. 0 .LE. nfc, 'faulty argument nCv, nFc')
!
!   ..set dummy arrays
  dumm0 = 0.0_R8
!
!srv 13.10.06
  fnbpsch = 0.0e0_R8
!srv 02.01.07
  conb = 0.0e0_R8
  flob = 0.0e0_R8
  facdriftm = MAXVAL(facdrift)
  IF (facdriftm .NE. 0.0e0_R8) THEN
!
!     ..precompute face and vertex values of nbti and kmprti
    nbti = nb*ti
    CALL INTVERTEX_NODIFF_NODIFF(ncv, nvx, mpg, geo%vxvol, nbti, nbtiv)
    CALL INTVERTEX_NODIFF_NODIFF(ncv, nvx, mpg, geo%vxvol, kmprti, &
&                          kmprtiv)
    CALL DIFF_NODIFF_NODIFF(ncv, nfc, nvx, 1, geo, mpg, nbti, nbtiv, &
&                     dnbti)
!
!     ..compute P.Sch heat flux
    fnbpsch(:, 0) = switch%fnbpsch*facdrift*cddb(:, 0)*geo%fcqalf(:, 0)*&
&     dnbti(:, 1)
    fnbpsch(:, 1) = -(switch%fnbpsch*facdrift*cddb(:, 1)*geo%fcqalf(:, 1&
&     )*dnbti(:, 0))
!
!     ..linearization coefficients for P.Sch flows
    cddb_eff(:, 0) = -(switch%fnbpsch*facdrift*cddb(:, 1)*geo%fcqalf(:, &
&     1))
    cddb_eff(:, 1) = switch%fnbpsch*facdrift*cddb(:, 0)*geo%fcqalf(:, 0)
    CALL CALCCOEF_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, dumm0, cddb_eff, &
&                         flob0, conb0)
    DO ifc=1,nfc
      flob(ifc, 0) = 0.5_R8*flob0(ifc, 0)*(kmprti(mpg%fccv(ifc, 1))+&
&       kmprti(mpg%fccv(ifc, 2))) - conb0(ifc, 0)*(kmprti(mpg%fccv(ifc, &
&       2))-kmprti(mpg%fccv(ifc, 1)))
      flob(ifc, 1) = 0.5_R8*flob0(ifc, 1)*(kmprtiv(mpg%fcvx(ifc, 1))+&
&       kmprtiv(mpg%fcvx(ifc, 2))) - conb0(ifc, 1)*(kmprtiv(mpg%fcvx(ifc&
&       , 2))-kmprtiv(mpg%fcvx(ifc, 1)))
!wdk      For now, directly add these contributions into conb(,,0),
!wdk      so they are directly added into the stencil.
!wdk      Might need to move back to conb(,,3) and dedicated treatment
!wdk      in b2ux9p if we decide to move away from inverse volume weighting
!wdk      to compute vertex values of drifts!
      conb(ifc, 0, 0) = 0.5_R8*conb0(ifc, 0)*(kmprti(mpg%fccv(ifc, 1))+&
&       kmprti(mpg%fccv(ifc, 2))) - 0.25_R8*flob0(ifc, 0)*(kmprti(mpg%&
&       fccv(ifc, 2))-kmprti(mpg%fccv(ifc, 1)))
      conb(ifc, 1, 0) = 0.5_R8*conb0(ifc, 1)*(kmprtiv(mpg%fcvx(ifc, 1))+&
&       kmprtiv(mpg%fcvx(ifc, 2))) - 0.25_R8*flob0(ifc, 1)*(kmprtiv(mpg%&
&       fcvx(ifc, 2))-kmprtiv(mpg%fcvx(ifc, 1)))
    END DO
  END IF
!
! ..return
  ncall = ncall + 1
  CALL SUBEND()
  RETURN
!-----------------------------------------------------------------------
!.end b2tnpsch
END SUBROUTINE B2TNPSCH_NODIFF_NODIFF

