  EXTERNAL XERTST, XERRAB
  INTRINSIC NINT
  INTRINSIC INT
  INTRINSIC ABS
  REAL(r8) :: y1
  REAL(r8) :: min1
  REAL(kind=r8) :: abs0
  REAL(kind=r8) :: abs1
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
  REAL(kind=r8) :: arg1
  REAL(kind=r8) :: pwx1
  REAL(kind=r8) :: pwr1
  REAL(kind=r8) :: pwx2
  REAL(kind=r8) :: pwr2
  REAL(kind=r8) :: pwx3
  REAL(kind=r8) :: pwr3
  REAL(r8) :: arg10
