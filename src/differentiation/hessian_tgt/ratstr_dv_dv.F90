!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('ratstr')
!   ..set cutll, cutlo
  IF (.NOT.b2mod_math_initialised) CALL B2MOD_MATH_INIT()
!   ..test nz, nt, nn
  CALL XERTST(0 .LT. nz .AND. nz .LE. nzm, 'ratstr--faulty argument nz')
  CALL XERTST(0 .LE. nt .AND. 0 .LE. nn, &
&       'ratstr--faulty argument nt or nn')
!   ..test rtt, rtn
  DO i=0,nt
    CALL XERTST(0.0_R8 .LT. rtt(i), 'ratstr--faulty argument array rtt')
  END DO
  DO i=0,nn
    CALL XERTST(0.0_R8 .LT. rtn(i), 'ratstr--faulty argument array rtn')
  END DO
!   ..position the input file
  REWIND(nrid) 
 81 READ(nrid, '(//g12.2,i12)', end=82) mass, nzi
  IF (nzi .EQ. nz) THEN
    GOTO 83
  ELSE
    DO k=0,nzi-1
      READ(nrid, '(///1x)', end=82) 
    END DO
    GOTO 81
  END IF
