  PARAMETER (cm3=1.0e-6_R8, zlradf=2.6e-30_R8/ev, bremsf=1.69e-38_R8/ev&
& , zexp1=1.0_R8/17.0_R8, zexp2=2.0_R8/17.0_R8, zexp3=12.0_R8/17.0_R8)
!   ..further input data
  REAL(kind=r8) :: d1(0:nzm-1), f1(0:nzm-1), g1(0:nzm-1), d2(0:nzm-1), &
& f2(0:nzm-1), g2(0:nzm-1), a1(0:nzm-1), b1(0:nzm-1), c1(0:nzm-1), a2(0:&
& nzm-1), b2(0:nzm-1), c2(0:nzm-1), x1(0:nzm-1), x2(0:nzm-1), z1(0:nzm-1&
& ), z2(0:nzm-1), qz(0:nzm-1), ze(0:nzm-1)
!   ..procedures
