  FUNCTION HYDFUD(temp, dens)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    REAL(kind=r8) :: hydfud, temp, dens
    INTRINSIC EXP
    REAL(kind=r8) :: pwr10
    REAL(r8) :: arg11
    pwr10 = temp**(-0.60_R8)
    arg11 = 4.4_R8*(dens/1.0e20_R8)**0.15_R8*pwr10
    hydfud = EXP(arg11)
    RETURN
  END FUNCTION HYDFUD

!
  FUNCTION SEI1(zz)
    USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    REAL(kind=r8) :: sei1, zz
    INTRINSIC LOG
    REAL(r8) :: arg11
    arg11 = 1.0_R8 + 1.0_R8/zz
    sei1 = LOG(arg11) - 0.4_R8/(1.0_R8+zz)**2
    RETURN
  END FUNCTION SEI1

