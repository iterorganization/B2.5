SUBROUTINE B2SIKT_NODIFF_NODIFF(ncv, nfc, nvx, ns, ismain, switch, geo, &
& mpg, pl, dv, rza, co, st_ext, she0, shi0, skt0, skt_prod, skt_diss)
  USE B2MOD_TYPES
  USE B2MOD_CONSTANTS
  USE B2MOD_B2CMFS
  USE B2MOD_B2CMPA_DIFFV
!      use b2mod_keps
  USE B2MOD_USER_NAMELIST_DIFFV_DIFFV, ONLY : nomp, omp, icsepomp
  USE B2US_GEO_DIFFV_DIFFV
  USE B2US_MAP_DIFFV_DIFFV
  USE B2US_PLASMA_DIFFV_DIFFV
  USE B2MOD_SWITCHES_DIFFV_DIFFV
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_AD_DIFFV_DIFFV, ONLY : ncall_b2sikt
  USE B2MOD_SUBSYS
!  Hint: nCv should be the size of dimension 1 of array arg1
!  Hint: nCv should be the size of dimension 1 of array result1
!  Hint: nCv should be the size of dimension 1 of array cvbb
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER, INTENT(IN) :: ncv, nfc, nvx, ns, ismain
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(B2PLASMA), INTENT(IN) :: pl
  TYPE(B2DERIVATIVES), INTENT(IN) :: dv
  TYPE(B2COEFF), INTENT(IN) :: co
  TYPE(B2STATEEXT), INTENT(IN) :: st_ext
  REAL(kind=r8) :: rza(ncv, 0:ns-1)
!   ..output arguments (unspecified on entry)
  REAL(kind=r8) :: she0(ncv, 0:3), shi0(ncv, 0:3), skt0(ncv, 0:3), &
& skt_prod(ncv), skt_diss(ncv)
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2SIKT computes the sources in the energy equations due to the
!     k-eps model.
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: ic
!
  REAL(kind=r8) :: wrks(ncv), wrkv(nvx), rz(ncv), pz(ncv), cs(ncv), t0, &
& t1, t2, t3, t4, wrk0(ncv), wrk1(ncv), wrk2(ncv), wrk3(ncv), wrkf(nfc)&
& , wrkg0(ncv, 0:1), wrkg1(ncv, 0:1), wrkg2(ncv, 0:1), dnac(ncv, 0:1), &
& dlbc(ncv, 0:1), dtec(ncv, 0:1), dtic(ncv, 0:1), dktc(ncv), fac_diss, &
& fac_sheath, csfs(ncv), connfs(ncv), rhol(ncv)
!   ..procedures
  INTRINSIC MIN, MAX, SQRT
  EXTERNAL XERTST, IPGETI, IPGETR, SFILL_NODIFF_NODIFF
  EXTERNAL B2XVSG
  INTRINSIC ABS
  INTRINSIC MINVAL
  INTRINSIC LOG
  EXTERNAL XERRAB
  INTRINSIC MOD
  REAL(kind=r8), DIMENSION(ncv) :: dabs0
  REAL(kind=r8) :: dabs1
  REAL(kind=r8) :: min1
  REAL(kind=r8) :: min2
  REAL(kind=r8) :: min3
  INTEGER :: arg1
  REAL(kind=r8), DIMENSION(ncv) :: arg10
  REAL(r8), DIMENSION(ncv) :: arg11
  REAL(kind=r8), DIMENSION(ncv) :: result1
  REAL(r8) :: arg12
  REAL(kind=r8) :: result10
  REAL(kind=r8) :: result11
  REAL(kind=r8), DIMENSION(ncv) :: arg13
  REAL(r8), DIMENSION(ncv) :: result12
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2sikt')
!   ..set internal parameters on first call
  IF (ncall_b2sikt .EQ. 0) THEN
    IF (switch%b2sikt_sheath_local .NE. 1) THEN
      CALL XERTST(nomp .GT. 0, &
&           'No CVs in omp list, check rzomp in b2.user.parameters')
      CALL XERTST(icsepomp .GT. 0, &
&           'Invalid icsepomp value, check rzomp in b2.user.parameters')
    END IF
  END IF
!   ..test nCv, ns
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv')
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!   ..extensive tests on first few calls
  IF (ncall_b2sikt .LT. 3) THEN
!    ..test sign of vol
    CALL B2XVSG(ncv, geo%cvvol, 1, 'vol', '.gt.')
!    ..test sign of na, te, ti, ne, ni
    arg1 = ncv*ns
    CALL B2XVSG(arg1, pl%na, 1, 'na', '.gt.')
    CALL B2XVSG(ncv, pl%te, 1, 'te', '.gt.')
    CALL B2XVSG(ncv, pl%ti, 1, 'ti', '.gt.')
    CALL B2XVSG(ncv, pl%kt, 1, 'kt', '.ge.')
    CALL B2XVSG(ncv, dv%ne, 1, 'ne', '.gt.')
    arg1 = ncv*2
    CALL B2XVSG(arg1, dv%ni, 1, 'ni', '.gt.')
  END IF
!
! ..compute heat source terms
!   ..initialize sources to zero
  arg1 = ncv*4
  CALL SFILL_NODIFF_NODIFF(arg1, 0.0_R8, she0, 1)
  arg1 = ncv*4
  CALL SFILL_NODIFF_NODIFF(arg1, 0.0_R8, shi0, 1)
  arg1 = ncv*4
  CALL SFILL_NODIFF_NODIFF(arg1, 0.0_R8, skt0, 1)
  CALL SFILL_NODIFF_NODIFF(ncv, 0.0_R8, skt_prod, 1)
  CALL SFILL_NODIFF_NODIFF(ncv, 0.0_R8, skt_diss, 1)
  arg1 = ncv*2
  CALL SFILL_NODIFF_NODIFF(arg1, 0.0_R8, dnac, 1)
  arg1 = ncv*2
  CALL SFILL_NODIFF_NODIFF(arg1, 0.0_R8, dlbc, 1)
  arg1 = ncv*2
  CALL SFILL_NODIFF_NODIFF(arg1, 0.0_R8, dtec, 1)
  arg1 = ncv*2
  CALL SFILL_NODIFF_NODIFF(arg1, 0.0_R8, dtic, 1)
  CALL SFILL_NODIFF_NODIFF(ncv, 0.0_R8, dktc, 1)
  IF (switch%b2sikt_model .EQ. 1) THEN
!wdk  KU Leuven k model
!wdk  source : driven by ExB energy flux
!wdk  sink   :
!wdk   - ad hoc term, ~1/(connection length), as a proxy for enhanced
!wdk     parallel transport due to parallel current fluctuations
    CALL B2XPRZ_NODIFF_NODIFF(ncv, ns, mp, am, pl%na, rz, st_ext)
    CALL B2XPPZ_NODIFF_NODIFF(ncv, ns, dv%ne, pl%na, pl%te, pl%ti, pz, &
&                       st_ext)
    arg10 = pz/rz
    cs = SQRT(arg10)
    IF (switch%b2sikt_sheath_local .EQ. 1) THEN
      csfs = cs
      connfs = geo%cvconn
      WHERE (rza(:, ismain)*qe*geo%cvbb(:, 3) .GE. 0.) 
        dabs0 = rza(:, ismain)*qe*geo%cvbb(:, 3)
      ELSEWHERE
        dabs0 = -(rza(:, ismain)*qe*geo%cvbb(:, 3))
      END WHERE
      arg11 = 2.0_R8*pl%ti*(am(ismain)*mp)
      result1 = SQRT(arg11)
      rhol = result1/dabs0
    ELSE
      csfs = cs(omp(icsepomp))
      connfs = geo%cvconn(omp(icsepomp))
      IF (rza(omp(icsepomp), ismain)*qe*geo%cvbb(omp(icsepomp), 3) .GE. &
&         0.) THEN
        dabs1 = rza(omp(icsepomp), ismain)*qe*geo%cvbb(omp(icsepomp), 3)
      ELSE
        dabs1 = -(rza(omp(icsepomp), ismain)*qe*geo%cvbb(omp(icsepomp), &
&         3))
      END IF
      arg12 = 2.0_R8*pl%ti(omp(icsepomp))*(am(ismain)*mp)
      result10 = SQRT(arg12)
      rhol = result10/dabs1
    END IF
    result11 = MINVAL(connfs(1:mpg%nci))
    CALL XERTST(result11 .GT. 0.0_R8, ' connfs in b2sikt <=0!!')
!   ..compute wrk arrays
    IF (switch%b2sikt_style .EQ. 0) THEN
!      ..logarithm of magnetic field in cell center
      arg13 = geo%cvbb(:, 3)
      wrks(:) = 2.0_R8*LOG(arg13)
!      ..gradients na and log(bb) in poloidal and radial direction
      CALL GRADC_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, pl%na(:, &
&                        ismain), wrkv, dnac)
      CALL GRADC_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrks, wrkv, &
&                        dlbc)
      CALL GRADC_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, pl%te, wrkv, &
&                        dtec)
      CALL GRADC_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, pl%ti, wrkv, &
&                        dtic)
!      ..computations for sink due to Reynolds stress
      IF (switch%b2sikt_fac_vis_rs .NE. 0.0_R8) THEN
        CALL INTCELL_NODIFF_NODIFF(nfc, ncv, mpg, mpg%intcellp, dv%&
&                            vaecrb(:, 0, ismain), wrk0)
        CALL INTCELL_NODIFF_NODIFF(nfc, ncv, mpg, mpg%intcellr, dv%&
&                            vaecrb(:, 1, ismain), wrk1)
        wrk2 = wrk0*dlbc(:, 0) + wrk1*dlbc(:, 1)
        wrks = -(2.0_R8/3.0_R8*geo%cvvol*pl%na(:, ismain)*wrk2*(switch%&
&         b2sikt_fac_vis_rs*co%dna_exb*am(ismain)*mp*wrk2+pl%kt))
        CALL GRADC_DIV_NODIFF_NODIFF(ncv, nfc, nvx, 1, geo, mpg, wrk0, &
&                              dv%vaecrb(:, 0, ismain), wrkg0)
        CALL GRADC_DIV_NODIFF_NODIFF(ncv, nfc, nvx, 1, geo, mpg, wrk1, &
&                              dv%vaecrb(:, 1, ismain), wrkg1)
        wrkf = dv%vaecrb(:, 0, ismain)*geo%fcbb(:, 3)/geo%fcbb(:, 2)
        wrk2 = wrk0*geo%cvbb(:, 3)/geo%cvbb(:, 2)
        CALL GRADC_DIV_NODIFF_NODIFF(ncv, nfc, nvx, 1, geo, mpg, wrk2, &
&                              wrkf, wrkg2)
        wrks = wrks + switch%b2sikt_fac_vis_rs*2.0_R8*geo%cvvol*co%&
&         dna_exb*am(ismain)*mp*pl%na(:, ismain)*(wrkg0(:, 0)**2+wrkg1(:&
&         , 1)**2+0.5_R8*(wrkg0(:, 1)**2+wrkg1(:, 0)**2+wrkg2(:, 0)**2+&
&         wrkg2(:, 1)**2)+wrkg1(:, 0)*wrkg0(:, 1))
      ELSE
        wrks = 0.0_R8
      END IF
!      ..individual source contributions
      wrk0 = geo%cvvol*(pl%ti*co%dna_exb*(dnac(:, 0)*dlbc(:, 0)+dnac(:, &
&       1)*dlbc(:, 1))+switch%b2sikt_fac_aniso*2.0_R8/3.0_R8*co%hci_exb*&
&       (dtic(:, 0)*dlbc(:, 0)+dtic(:, 1)*dlbc(:, 1)))
      wrk1 = geo%cvvol*(pl%te*co%dna_exb*(dnac(:, 0)*dlbc(:, 0)+dnac(:, &
&       1)*dlbc(:, 1))+switch%b2sikt_fac_aniso*2.0_R8/3.0_R8*co%hce_exb*&
&       (dtec(:, 0)*dlbc(:, 0)+dtec(:, 1)*dlbc(:, 1)))
      wrk2 = dv%ni(:, 1)*geo%cvvol*csfs/connfs
      IF (switch%b2sikt_fac_diss_core_mode .EQ. 0) THEN
        wrk3 = co%sigx_kt*geo%cvvol*pl%kt*(geo%cvbb(:, 3)/(geo%cvbb(:, 0&
&         )*geo%cvconn))**2
      ELSE IF (switch%b2sikt_fac_diss_core_mode .EQ. 1) THEN
        wrk3 = co%sigx_kt*geo%cvvol*pl%kt*(geo%cvbb(:, 3)/(geo%cvbb(:, 0&
&         )*switch%b2sikt_lpar_ref))**2
      ELSE
        result12 = SQRT(pl%kt)
        CALL GRADC_P_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, result12&
&                            , wrkv, dktc)
        wrk3 = co%sigx_kt*geo%cvvol*dktc**2
      END IF
    ELSE
      CALL XERRAB('wrong value of b2sikt_style')
    END IF
!
!     ..sources
    DO ic=1,mpg%nci
      IF (MOD(mpg%cvreg(ic), 4) .EQ. 1) THEN
        fac_sheath = switch%b2sikt_fac_sheath_core
        fac_diss = switch%b2sikt_fac_diss_core
      ELSE
        fac_sheath = switch%b2sikt_fac_sheath
        fac_diss = switch%b2sikt_fac_diss
      END IF
      t0 = wrk0(ic)
      t1 = wrk1(ic)
      t2 = fac_sheath*wrk2(ic)
      t3 = fac_diss*wrk3(ic)
      t4 = wrks(ic)
      IF (switch%b2sikt_min_source .EQ. 1 .AND. t0 + t1 - pl%kt(ic)*t2 -&
&         t3 .LT. 0.0_R8) THEN
!experimental
        t0 = 0.0_R8
        t1 = 0.0_R8
        t2 = 0.0_R8
        t3 = 0.0_R8
      END IF
      IF (switch%b2sikt_kt_source_stab .EQ. 0) THEN
        she0(ic, 0) = -t1 + t2*pl%kt(ic) + t3
        shi0(ic, 0) = -t0
        skt0(ic, 0) = t0 + t1 - t3 - t4
        skt0(ic, 1) = -t2
      ELSE
        IF (-t1 + t2*pl%kt(ic) + t3 .LT. 0.0_R8) THEN
          she0(ic, 0) = 0.0_R8
        ELSE
          she0(ic, 0) = -t1 + t2*pl%kt(ic) + t3
        END IF
        IF (-t1 + t2*pl%kt(ic) + t3 .GT. 0.0_R8) THEN
          min1 = 0.0_R8
        ELSE
          min1 = -t1 + t2*pl%kt(ic) + t3
        END IF
        she0(ic, 1) = min1/pl%te(ic)
        IF (-t0 .LT. 0.0_R8) THEN
          shi0(ic, 0) = 0.0_R8
        ELSE
          shi0(ic, 0) = -t0
        END IF
        IF (-t0 .GT. 0.0_R8) THEN
          min2 = 0.0_R8
        ELSE
          min2 = -t0
        END IF
        shi0(ic, 1) = min2/pl%ti(ic)
        IF (t0 + t1 .LT. 0.0_R8) THEN
          skt0(ic, 0) = 0.0_R8
        ELSE
          skt0(ic, 0) = t0 + t1
        END IF
        IF (t0 + t1 .GT. 0.0_R8) THEN
          min3 = 0.0_R8
        ELSE
          min3 = t0 + t1
        END IF
        skt0(ic, 1) = (min3-t3-t4)/(1.0e-8_R8*ev+pl%kt(ic)) - t2
      END IF
      skt_prod(ic) = t0 + t1
      skt_diss(ic) = t2*pl%kt(ic) + t3 + t4
    END DO
  ELSE
    CALL XERRAB('wrong value of b2sikt_model')
  END IF
!
! ..output
  IF (switch%b2sikt_iout .EQ. 1) THEN
    wrks = she0(:, 0) + she0(:, 1)*pl%te + she0(:, 2)*dv%ne + she0(:, 3)&
&     *pl%te*dv%ne
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_she')
    wrks = shi0(:, 0) + shi0(:, 1)*pl%ti + shi0(:, 2)*dv%ni(:, 0) + shi0&
&     (:, 3)*pl%ti*dv%ni(:, 0)
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_shi')
    wrks = skt0(:, 0) + skt0(:, 1)*pl%kt + skt0(:, 2)*dv%ni(:, 1) + skt0&
&     (:, 3)*pl%kt*dv%ni(:, 1)
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_skt')
    CALL MY_OUT_US(70, ncv, 0, skt_prod, 'b2sikt_skt_prod')
    CALL MY_OUT_US(70, ncv, 0, skt_diss, 'b2sikt_skt_diss')
    wrks = geo%cvvol(:)*((pl%ti(:)+pl%te(:))*co%dna_exb(:)*(dnac(:, 0)*&
&     dlbc(:, 0)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_na_x')
    wrks = geo%cvvol(:)*((pl%ti(:)+pl%te(:))*co%dna_exb(:)*(dnac(:, 1)*&
&     dlbc(:, 1)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_na_y')
    wrks = geo%cvvol(:)*(2.0_R8/3.0_R8*co%hce_exb(:)*switch%&
&     b2sikt_fac_aniso*(dtec(:, 0)*dlbc(:, 0)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_te_x')
    wrks = geo%cvvol(:)*(2.0_R8/3.0_R8*co%hce_exb(:)*switch%&
&     b2sikt_fac_aniso*(dtec(:, 1)*dlbc(:, 1)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_te_y')
    wrks = geo%cvvol(:)*(2.0_R8/3.0_R8*co%hci_exb(:)*switch%&
&     b2sikt_fac_aniso*(dtic(:, 0)*dlbc(:, 0)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_ti_x')
    wrks = geo%cvvol(:)*(2.0_R8/3.0_R8*co%hci_exb(:)*switch%&
&     b2sikt_fac_aniso*(dtic(:, 1)*dlbc(:, 1)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_ti_y')
    CALL MY_OUT_US(70, ncv, 0, connfs, 'b2sikt_connfs')
    CALL MY_OUT_US(70, ncv, 0, csfs, 'b2sikt_csfs')
    CALL MY_OUT_US(70, ncv, 0, dnac(:, 0), 'b2sikt_dnac0')
    CALL MY_OUT_US(70, ncv, 0, dnac(:, 1), 'b2sikt_dnac1')
    CALL MY_OUT_US(70, ncv, 0, dtec(:, 0), 'b2sikt_dtec0')
    CALL MY_OUT_US(70, ncv, 0, dtec(:, 1), 'b2sikt_dtec1')
    CALL MY_OUT_US(70, ncv, 0, dtic(:, 0), 'b2sikt_dtic0')
    CALL MY_OUT_US(70, ncv, 0, dtic(:, 1), 'b2sikt_dtic1')
    CALL MY_OUT_US(70, ncv, 0, dlbc(:, 0), 'b2sikt_dlbc0')
    CALL MY_OUT_US(70, ncv, 0, dlbc(:, 1), 'b2sikt_dlbc1')
!!some temporary/experimental output computations for source due to Reynolds stress
    CALL INTCELL_NODIFF_NODIFF(nfc, ncv, mpg, mpg%intcellp, dv%vaecrb(:&
&                        , 0, ismain), wrk0)
    CALL INTCELL_NODIFF_NODIFF(nfc, ncv, mpg, mpg%intcellr, dv%vaecrb(:&
&                        , 1, ismain), wrk1)
    wrk2 = wrk0*dlbc(:, 0) + wrk1*dlbc(:, 1)
    wrks = -(2.0_R8/3.0_R8*geo%cvvol*pl%na(:, ismain)*wrk2*(switch%&
&     b2sikt_fac_vis_rs*co%dna_exb*am(ismain)*mp*wrk2+pl%kt))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_RS_symmetric')
    CALL GRADC_DIV_NODIFF_NODIFF(ncv, nfc, nvx, 1, geo, mpg, wrk0, dv%&
&                          vaecrb(:, 0, ismain), wrkg0)
    CALL GRADC_DIV_NODIFF_NODIFF(ncv, nfc, nvx, 1, geo, mpg, wrk1, dv%&
&                          vaecrb(:, 1, ismain), wrkg1)
    wrkf = dv%vaecrb(:, 0, ismain)*geo%fcbb(:, 3)/geo%fcbb(:, 2)
    wrk2 = wrk0*geo%cvbb(:, 3)/geo%cvbb(:, 2)
    CALL GRADC_DIV_NODIFF_NODIFF(ncv, nfc, nvx, 1, geo, mpg, wrk2, wrkf&
&                          , wrkg2)
    wrks = switch%b2sikt_fac_vis_rs*2.0_R8*geo%cvvol*co%dna_exb*am(&
&     ismain)*mp*pl%na(:, ismain)*(wrkg0(:, 0)**2+wrkg1(:, 1)**2+0.5_R8*&
&     (wrkg0(:, 1)**2+wrkg1(:, 0)**2+wrkg2(:, 0)**2+wrkg2(:, 1)**2)+&
&     wrkg1(:, 0)*wrkg0(:, 1))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_RS_deviatoric')
  END IF
!
! ..return
  ncall_b2sikt = ncall_b2sikt + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2sikt
!
END SUBROUTINE B2SIKT_NODIFF_NODIFF

