  radld = 0.0_R8
  DO i=1,nsrc
    ssrc = ssrcm(i)
    ttsrc = ttsrc + ssrc
    xsrc = xysrc(1, i)
    ysrc = xysrc(2, i)
!<<<
!        print *
!        print *,'Source number, ssrc, xsrc, ysrc'
