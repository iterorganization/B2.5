!*** Computation of the radiation loads.
!*** Returns the total radiation power coming to each of the specified
!*** wall segments with possible shadowing taken into account.
!*** The radiation loads are calculated using a cylindrical approximation.
!***
!*** Input:
!***  xysrc   R and Z coordinates of the point sources
!***  ssrcm   their intensities
!***  xp,yp   coordinates of the boundary polygon P
