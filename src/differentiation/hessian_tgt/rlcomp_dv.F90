!<<<
  IF (uu_dbg) THEN
    PRINT*, 'rlcomp: nsrc, ntrg = ', nsrc, ntrg
    OPEN(lun_s, file='sources.dat') 
    WRITE(lun_s, '(2a)') '# Radiation sources : ', TRIM(label)
    WRITE(lun_s, '(5a)') '# R', tab, 'Z', tab, 'S'
  END IF
