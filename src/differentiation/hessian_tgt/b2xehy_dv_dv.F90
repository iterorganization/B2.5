  USE B2MOD_TYPES
  USE B2MOD_CONSTANTS
  USE B2MOD_SWITCHES_DIFFV_DIFFV
  USE B2US_GEO_DIFFV_DIFFV
  USE B2US_MAP_DIFFV_DIFFV
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_AD_DIFFV_DIFFV, ONLY : ncall_b2xehy
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  REAL(kind=r8) :: te(ncv), po(ncv), ne(ncv)
!   ..output arguments (unspecified on entry)
  REAL(kind=r8) :: ehy(nfc)
!   ..common blocks
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2XEHY computes the modified radial electrical field on all cell
!     faces.
!
!  8. further comments
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..global named constants
!
!   ..local variables
  REAL(kind=r8) :: dpor(nfc), wrk0(nfc), wrk1(nfc), wrk2(nfc), work(ncv)&
& , wrkv(nvx), ones(nfc, 2)
!   ..procedures
  EXTERNAL XERTST
  EXTERNAL B2XVSG
  INTRINSIC LOG
!   ..initialization
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2xehy')
!   ..test nCv, nFc
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
!   ..extensive tests on first few calls
  IF (ncall_b2xehy .LT. 3) THEN
!    ..test sign of ne, te, csig
    CALL B2XVSG(ncv, ne, 1, 'ne', '.gt.')
    CALL B2XVSG(ncv, te, 1, 'te', '.gt.')
  END IF
!srv 29.01.20 }
!
!   ..compute ehy
  IF (switch%pot_eq .NE. 1 .AND. switch%no_current .NE. 0) THEN
!srv 29.01.20 {
    ehy = 0.0_R8
  ELSE IF (switch%mode_ehy .EQ. 0) THEN
    ones = 1.0_R8
!       ..compute radial differences of po and pe
    CALL DIFF_R_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, po, wrkv, dpor&
&                      )
    work = ne*te
    CALL DIFF_R_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, work, wrkv, &
&                       wrk0)
!       ..interpolate ne to faces
    CALL INTFACE(ncv, nfc, mpg%fccv, ones, ne, wrk1)
!       ..compute ehy
    ehy = -dpor + wrk0/(qe*wrk1)
  ELSE IF (switch%mode_ehy .EQ. 1) THEN
    CALL DIFF_R_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, po, wrkv, dpor&
&                      )
    work = ne*te
    CALL DIFF_R_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, work, wrkv, &
&                       wrk0)
!       ..interpolate ne to faces
!wdk small loss of backwards compatibility:
!wdk now inverse distance weighting based on full distance cell center to face center ("hy")
!wdk instead of radial distance ("hy1_")
    CALL INTFACE(ncv, nfc, mpg%fccv, geo%fchc, ne, wrk1)
!       ..compute ehy
    ehy = -dpor + wrk0/(qe*wrk1)
  ELSE
    ones = 1.0_R8
!       ..compute radial differences of po, te and log(ne) on faces
    CALL DIFF_R_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, po, wrkv, dpor&
&                      )
    CALL DIFF_R_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, te, wrkv, wrk0&
&                      )
    work = LOG(ne)
    CALL DIFF_R_NODIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, work, wrkv, &
&                       wrk1)
!       ..interpolate te to faces
    CALL INTFACE(ncv, nfc, mpg%fccv, ones, te, wrk2)
!       ..compute ehy      
    ehy = -dpor + (wrk0+wrk1*wrk2)/qe
  END IF
!
! ..return
  ncall_b2xehy = ncall_b2xehy + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2xehy
!
END SUBROUTINE B2XEHY_NODIFF_NODIFF

