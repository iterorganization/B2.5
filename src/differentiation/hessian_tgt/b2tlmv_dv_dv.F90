!sw 18sep2014 TESTING only (SOLPS4 discr), i.e. switch off mom.flux
!             limit
!      else if(style.eq.2) then
!       fl = 1.0_R8
!srv 30.05.08
    DO ifc=1,nfc
      IF (.NOT.(.NOT.is_neutral(isb) .AND. mpg%cvonclosedsurface(mpg%&
&         fccv(ifc, 1)) .AND. mpg%cvonclosedsurface(mpg%fccv(ifc, 2)))) &
&     THEN
!srv 12.04.08
        qcl = -(cvsbx(ifc)*dub(ifc))
        IF (geo%fcpbs(ifc) .GE. 0.) THEN
          abs2 = geo%fcpbs(ifc)
        ELSE
          abs2 = -geo%fcpbs(ifc)
        END IF
!srv 11.01.13
        qfl = cflmv_loc(ifc)*abs2*(nb(mpg%fccv(ifc, 1))*tb(mpg%fccv(ifc&
&         , 1))+nb(mpg%fccv(ifc, 2))*tb(mpg%fccv(ifc, 2)))/2.0_R8
        IF (qfl .GT. 0.0_R8) THEN
          IF (qcl/qfl .GE. 0.) THEN
            abs3 = qcl/qfl
          ELSE
            abs3 = -(qcl/qfl)
          END IF
          tfl = 1.0_R8 + abs3
        ELSE
          tfl = 1.0_R8
        END IF
        cvsbx(ifc) = cvsbx(ifc)/tfl
        cvsbxhz(ifc) = cvsbxhz(ifc)/tfl
        vsbfx(ifc) = vsbfx(ifc)/tfl
        fl(ifc) = tfl
      ELSE
        fl(ifc) = 1.0_R8
      END IF
    END DO
  END IF
!srv 12.04.04 }
!
  IF (switch%b2tlmv_iout .NE. 0) THEN
!srv 12.04.04 {
    WRITE(charns, '(i3.3)') isb
    arg1(:) = 'cflmv-1'//charns
    CALL MY_OUT_US(70, nfc, 1, fl, arg1(:))
  END IF
!
  ncall_b2tlmv = ncall_b2tlmv + 1
  CALL SUBEND()
  RETURN
