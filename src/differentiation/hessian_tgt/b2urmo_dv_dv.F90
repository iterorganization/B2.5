!       fmbx = flubx*ub-cvsbx*(d/dx)ub
!       fmby = fluby*up-cvsby*(d/dy)ub
!     The right hand side has a linearised representation with
!     coefficients given by smb. This linearisation may include a
!     contribution from an implicit discretisation in time.
!
!     The input arguments smb, flub and cvsb contain appropriate
!     geometric coefficients so that in the present routine the
!     expression for the integrated residual on any cell does not
!     involve any geometric factors. However, the correct computation
!     of (radial) differences of ub across the faces requires knowledge
!     of geometric factors.
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: ifc
!      integer, save :: no_Ptncr_x_co = 0, no_Ptncr_y_co = 0             !srv 06.07.06
!srv 02.06.11
  CHARACTER :: chns*3
  REAL(kind=r8) :: flf(nfc, 0:1), flv(nfc, 0:1), wrk(ncv), wrkf(nfc, 0:1&
& ), wrkv(nvx)
  REAL(kind=r8) :: flfd(nbdirsmax, nfc, 0:1), flvd(nbdirsmax, nfc, 0:1)&
& , wrkd(nbdirsmax, ncv), wrkfd(nbdirsmax, nfc, 0:1), wrkvd(nbdirsmax, &
& nvx)
  REAL(kind=r8), PARAMETER :: eps=1.0e-60_R8
  REAL(kind=r8) :: ubv(nvx), dub(nfc, 0:1)
!   ..procedures
  EXTERNAL XERTST
  EXTERNAL B2XVSG, CALCFLOW_NODIFF_NODIFF, CALCCOEF_NODIFF_NODIFF
  EXTERNAL CALCFLOW_DV_NODIFF
  CHARACTER(len=23) :: arg1
  CHARACTER(len=22) :: arg10
  CHARACTER(len=19) :: arg11
  CHARACTER(len=18) :: arg12
  INTEGER :: nd
  INTEGER :: nbdirs
!   ..initialisation
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2urmo')
!   ..test nCv, nFc
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
!   ..extensive tests on first few calls
  IF (ncall_b2urmo .LT. 3) THEN
!    ..test sign of rob, smb1, smb3
    CALL B2XVSG(ncv, rob, 1, 'rob', '.gt.')
    CALL B2XVSG(ncv, smb(1, 1), 1, 'smb1', '.le.')
    CALL B2XVSG(ncv, smb(1, 3), 1, 'smb3', '.le.')
!    ..test sign of cvsb
    wrkfd = 0.d0
    DO nd=1,nbdirs
      wrkfd(nd, :, 0) = geo%fcqalf(:, 0)*cvsbd(nd, :, 0)
      wrkfd(nd, :, 1) = geo%fcqalf(:, 1)*cvsbd(nd, :, 1)
    END DO
    wrkf(:, 0) = cvsb(:, 0)*geo%fcqalf(:, 0)
    wrkf(:, 1) = cvsb(:, 1)*geo%fcqalf(:, 1)
    CALL B2XVSG(nfc, wrkf(:, 0), 1, 'cvsb0', '.ge.')
    CALL B2XVSG(nfc, wrkf(:, 1), 1, 'cvsb1', '.ge.')
  ELSE
    wrkfd = 0.d0
  END IF
!
! ..compute flux
!wdk note: compared to original code, flv is now based on "effective" 
!wdk       viscosity, and will be zero if cvsb is zero
  flvd = 0.d0
  flfd = 0.d0
  CALL CALCFLOW_DV_NODIFF(ncv, nfc, nvx, switch%b2npmo_discr_meth, geo, &
&                   geod, mpg, mpgd, ub, ubd, flub, flubd, cvsb, cvsbd, &
&                   fmb, fmbd, flf, flfd, flv, flvd, nbdirs)
!wdk store effective viscosity, for use in viscous heating
  wrkvd = 0.d0
  CALL DIFF_DV_NODIFF(ncv, nfc, nvx, 0, geo, geod, mpg, mpgd, ub, ubd, &
&               wrkv, wrkvd, wrkf, wrkfd, nbdirs)
  DO nd=1,nbdirs
    cvsb_effd(nd, :, :) = -((flvd(nd, :, :)-flv*wrkfd(nd, :, :)/(eps+&
&     wrkf))/(eps+wrkf))
  END DO
  cvsb_eff = -(flv/(wrkf+eps))
! Note: flf and flv used as dummy arrays in this call !!
! To be checked:
! - consistency in case of drifts (in particular: fna_cor?)
! - should contribution from flubv be in here?
!
!
  IF (switch%b2npmo_iout .EQ. 1 .OR. switch%iout_b2wdat .EQ. 4) THEN
    WRITE(chns, '(i3.3)') isb
    arg1 = 'b2urmo_etaPat_gradua_th'//chns
    CALL MY_OUT_US(70, nfc, 1, flv(1, 0), arg1)
    arg10 = 'b2urmo_etaPat_gradua_r'//chns
    CALL MY_OUT_US(70, nfc, 1, flv(1, 1), arg10)
    arg11 = 'b2urmo_etaPat_ua_th'//chns
    CALL MY_OUT_US(70, nfc, 1, flf(1, 0), arg11)
    arg12 = 'b2urmo_etaPat_ua_r'//chns
    CALL MY_OUT_US(70, nfc, 1, flf(1, 1), arg12)
  END IF
  DO nd=1,nbdirs
!
! ..compute kinetic energy flux
    wrkd(nd, :) = 0.5_R8*2*ub*ubd(nd, :)
    wrkfd(nd, :, :) = flubd(nd, :, :) + flubvd(nd, :, :)
!
! ..compute residual
!   ..contribution from right hand side
    resmbd(nd, :) = smbd(nd, :, 0) + ub*smbd(nd, :, 1) + smb(:, 1)*ubd(&
&     nd, :) + rob*smbd(nd, :, 2) + smb(:, 2)*robd(nd, :) + rob*ub*smbd(&
&     nd, :, 3) + smb(:, 3)*(ub*robd(nd, :)+rob*ubd(nd, :))
  END DO
  wrk = 0.5_R8*ub**2
  wrkf = flub + flubv
  CALL CALCFLOW_DV_NODIFF(ncv, nfc, nvx, switch%b2npmo_discr_meth, geo, &
&                   geod, mpg, mpgd, wrk, wrkd, wrkf, wrkfd, cvsb, cvsbd&
&                   , fkb, fkbd, flf, flfd, flv, flvd, nbdirs)
  DO nd=1,nbdirs
    fkbd(nd, :, 0) = fkbd(nd, :, 0)/geo%fchz
    fkbd(nd, :, 1) = fkbd(nd, :, 1)/geo%fchz
  END DO
  fkb(:, 0) = fkb(:, 0)/geo%fchz
  fkb(:, 1) = fkb(:, 1)/geo%fchz
  resmb = smb(:, 0) + smb(:, 1)*ub + smb(:, 2)*rob + smb(:, 3)*rob*ub
!
!   ..contribution from fmb
  DO ifc=1,nfc
    DO nd=1,nbdirs
      resmbd(nd, mpg%fccv(ifc, 1)) = resmbd(nd, mpg%fccv(ifc, 1)) - fmbd&
&       (nd, ifc, 0) - fmbd(nd, ifc, 1)
      resmbd(nd, mpg%fccv(ifc, 2)) = resmbd(nd, mpg%fccv(ifc, 2)) + fmbd&
&       (nd, ifc, 0) + fmbd(nd, ifc, 1)
    END DO
    resmb(mpg%fccv(ifc, 1)) = resmb(mpg%fccv(ifc, 1)) - fmb(ifc, 0) - &
&     fmb(ifc, 1)
    resmb(mpg%fccv(ifc, 2)) = resmb(mpg%fccv(ifc, 2)) + fmb(ifc, 0) + &
&     fmb(ifc, 1)
  END DO
!
! ..return
  ncall_b2urmo = ncall_b2urmo + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2urmo
!
END SUBROUTINE B2URMO_DV_NODIFF

