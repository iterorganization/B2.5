  IF (switch%b2shdt_iout .EQ. 1) THEN
    DO k=0,3
      WRITE(chk, '(i1)') k
      arg10(:) = 'b2shdt_shedt'//chk
      CALL MY_OUT_US(70, ncv, 0, shedt(:, k), arg10(:))
      arg10(:) = 'b2shdt_shidt'//chk
      CALL MY_OUT_US(70, ncv, 0, shidt(:, k), arg10(:))
      arg10(:) = 'b2shdt_shndt'//chk
      CALL MY_OUT_US(70, ncv, 0, shndt(:, k), arg10(:))
      arg10(:) = 'b2shdt_sktdt'//chk
      CALL MY_OUT_US(70, ncv, 0, sktdt(:, k), arg10(:))
      arg10(:) = 'b2shdt_sztdt'//chk
      CALL MY_OUT_US(70, ncv, 0, sztdt(:, k), arg10(:))
    END DO
  END IF
!
! ..return
  ncall_b2shdt = ncall_b2shdt + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2SHDT_NODIFF_NODIFF

