SUBROUTINE B2TXCY_NODIFF_NODIFF(ncv, nfc, mode, geo, mpg, fcvol, fcs, &
& kapy, cony)
  USE B2MOD_TYPES
  USE B2US_GEO_DIFFV_DIFFV
  USE B2US_MAP_DIFFV_DIFFV
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!     ------------------------------------------------------------------
  INTEGER, INTENT(IN) :: ncv, nfc, mode
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  REAL(kind=r8) :: fcvol(nfc, 2), fcs(nfc), kapy(ncv), cony(nfc)
!     ------------------------------------------------------------------
!     B2TXCY computes radial conductances between neighbouring cells,
!     given the conductivities.
!     ------------------------------------------------------------------
  INTEGER :: ifc
  REAL(kind=r8) :: t0, t1
  INTRINSIC MAX, SQRT
  EXTERNAL XERTST
  REAL(r8) :: y1
  REAL(r8) :: max1
  REAL(r8) :: result1
!     ------------------------------------------------------------------
!   ..subprogram start-up calls
  CALL SUBINI('b2txcy')
!   ..test mode
  CALL XERTST(-1 .LE. mode .AND. mode .LE. 3, &
&       'b2txcy--faulty argument mode')
!
!wdk factor 0.5 (vs. 2.0 in original code)!
!wdk note that fcHc is typically hx/2 or hy/2!
!
!   ..compute cony
  IF (mode .EQ. -1) THEN
!    ..use harmonic averaging
    DO ifc=1,nfc
      t0 = fcvol(ifc, 1)*kapy(mpg%fccv(ifc, 1))/geo%fchc(ifc, 1)**2
      t1 = fcvol(ifc, 2)*kapy(mpg%fccv(ifc, 2))/geo%fchc(ifc, 2)**2
      IF (t0*t1 .GT. 0.0_R8) THEN
        cony(ifc) = t0*t1/(t0+t1)*geo%fcqalf(ifc, 1)/geo%fcqgam(ifc, 0)&
&         **2
      ELSE
        cony(ifc) = 0.0_R8
      END IF
    END DO
  ELSE IF (mode .EQ. 0) THEN
!    ..use geometric averaging
    DO ifc=1,nfc
      y1 = fcvol(ifc, 1)*kapy(mpg%fccv(ifc, 1))/geo%fchc(ifc, 1)*(fcvol(&
&       ifc, 2)*kapy(mpg%fccv(ifc, 2))/geo%fchc(ifc, 2))
      IF (0.0_R8 .LT. y1) THEN
        max1 = y1
      ELSE
        max1 = 0.0_R8
      END IF
      result1 = SQRT(max1)
      cony(ifc) = geo%fcqalf(ifc, 1)/geo%fcqgam(ifc, 0)**2*result1/(geo%&
&       fchc(ifc, 1)+geo%fchc(ifc, 2))
    END DO
  ELSE IF (mode .EQ. 1) THEN
!    ..use arithmetic averaging (SOLPS5.0 formulation)
    DO ifc=1,nfc
      cony(ifc) = geo%fcqalf(ifc, 1)/geo%fcqgam(ifc, 0)**2*(fcvol(ifc, 1&
&       )*kapy(mpg%fccv(ifc, 1))+fcvol(ifc, 2)*kapy(mpg%fccv(ifc, 2)))/(&
&       geo%fchc(ifc, 1)+geo%fchc(ifc, 2))**2
    END DO
  ELSE IF (mode .EQ. 2) THEN
!    ..use arithmetic averaging (SOLPS4.0 formulation)
    DO ifc=1,nfc
      cony(ifc) = fcs(ifc)*geo%fcqalf(ifc, 1)/geo%fcqgam(ifc, 0)*(geo%&
&       fchc(ifc, 1)*kapy(mpg%fccv(ifc, 1))+geo%fchc(ifc, 2)*kapy(mpg%&
&       fccv(ifc, 2)))/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))**2
    END DO
  ELSE IF (mode .EQ. 3) THEN
!    ..use arithmetic averaging (drift formulation)
    DO ifc=1,nfc
      cony(ifc) = fcs(ifc)*geo%fcqalf(ifc, 1)/(geo%fcqgam(ifc, 0)*(geo%&
&       fchc(ifc, 1)+geo%fchc(ifc, 2)))*(geo%fcvol(ifc, 1)*kapy(mpg%fccv&
&       (ifc, 1))+geo%fcvol(ifc, 2)*kapy(mpg%fccv(ifc, 2)))/(geo%fcvol(&
&       ifc, 1)+geo%fcvol(ifc, 2))
    END DO
  END IF
!   ..return
  CALL SUBEND()
  RETURN
END SUBROUTINE B2TXCY_NODIFF_NODIFF

