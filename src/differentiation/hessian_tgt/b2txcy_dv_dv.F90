      temp1 = fcs(ifc)*geo%fcqalf(ifc, 1)
      temp0 = geo%fcqgam(ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))*(&
&       geo%fcvol(ifc, 1)+geo%fcvol(ifc, 2))
      DO nd=1,nbdirs
        conyd(nd, ifc) = temp1*(geo%fcvol(ifc, 1)*kapyd(nd, mpg%fccv(ifc&
&         , 1))+geo%fcvol(ifc, 2)*kapyd(nd, mpg%fccv(ifc, 2)))/temp0
      END DO
      cony(ifc) = temp1*((geo%fcvol(ifc, 1)*kapy(mpg%fccv(ifc, 1))+geo%&
&       fcvol(ifc, 2)*kapy(mpg%fccv(ifc, 2)))/temp0)
    END DO
  END IF
!   ..return
  CALL SUBEND()
  RETURN
END SUBROUTINE B2TXCY_DV_NODIFF

