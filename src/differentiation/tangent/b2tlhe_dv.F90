&         b2tlhe_cflme_min*(switch%b2tlhe_ne_max-nef(ifc)))/(switch%&
&         b2tlhe_ne_max-switch%b2tlhe_ne_min)
      END IF
    END DO
  END IF
!
!   ..compute flux limit velocity
  IF (switch%b2tqce_model .EQ. 1 .OR. switch%b2tqce_model .EQ. 3) THEN
! SOLPS5.0 treatments
!wdk perhaps the if-test should be based on a 'mode' switch, instead of b2tqce_model,
!    because now both options are interchangeable
