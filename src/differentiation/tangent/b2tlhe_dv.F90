      result1 = SQRT(arg1)
      chvemx(ifc) = cflme_loc(ifc)*result1*abs0
    END DO
  ELSE IF (switch%b2tqce_model .EQ. 2) THEN
! SOLPS4.0 treatment
    DO ifc=1,nfc
      t0 = te(mpg%fccv(ifc, 2))
      t1 = te(mpg%fccv(ifc, 1))
      IF (geo%fcqalf(ifc, 0) .GE. 0.) THEN
        abs1 = geo%fcqalf(ifc, 0)
      ELSE
        abs1 = -geo%fcqalf(ifc, 0)
      END IF
      IF (t0 .LT. t1) THEN
        max1 = t1
      ELSE
        max1 = t0
      END IF
!wdk 10.03.16 Divide by ne*te at cell faces, so chvemx contains the same information
!           as case b2tqce_model.eq.1
!           Removes need for different treatment later on in the code (b2tfhe), and
!           ensures consistency with b2trcl_conductive_limit .ne. 0
      result1 = PIT(mpg%fccv(ifc, 2))
      arg1 = t0/me
      result2 = SQRT(arg1)
      result3 = PIT(mpg%fccv(ifc, 1))
      arg2 = t1/me
      result4 = SQRT(arg2)
      chvemx(ifc) = cflme_loc(ifc)*geo%fcs(ifc)*abs1*(ne(mpg%fccv(ifc, 2&
&       ))*result1*result2*t0+ne(mpg%fccv(ifc, 1))*result3*result4*t1)/(&
&       (ne(mpg%fccv(ifc, 1))+ne(mpg%fccv(ifc, 2)))*max1)
    END DO
  END IF
  ncall_b2tlhe = ncall_b2tlhe + 1
  CALL SUBEND()
  RETURN

