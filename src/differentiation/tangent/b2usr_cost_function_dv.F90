          END DO
          DO icv=nomp-1,2,-1
            result1 = MAXVAL(b2psi(icf, 1:n1))
            IF (psi_omp(icv) .GT. result1) ic2 = icv - 1
          END DO
        ELSE
! decreasing psi
          DO icv=2,nomp-1
            result1 = MAXVAL(b2psi(icf, 1:n1))
            IF (psi_omp(icv) .GT. result1) ic1 = icv + 1
          END DO
          DO icv=nomp-1,2,-1
            result1 = MINVAL(b2psi(icf, 1:n1))
            IF (psi_omp(icv) .LT. result1) ic2 = icv - 1
          END DO
