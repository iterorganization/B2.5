    isigma = 1
    imean = 1
    IF (ALLOCATED(b2datad)) THEN
      DO nd=1,nbdirsmax
        b2datad(nd, :) = 0.D0
      END DO
    END IF
    IF (ALLOCATED(b2dataoncfd)) THEN
      DO nd=1,nbdirsmax
        b2dataoncfd(nd, :) = 0.D0
      END DO
    END IF
