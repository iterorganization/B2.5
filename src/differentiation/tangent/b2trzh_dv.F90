          rwdpgamma_out(icv, nucl2s(inucl, iz)) = SUM(arg12)
!
          piu_out(icv, nucl2s(inucl, iz)) = -(4.0e0_R8/3.0e0_R8*eta_cl(&
&           icv, nucl2s(inucl, iz))/(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))&
&           **2*gavm_uc(icv))
!
        END DO
      END DO
    END IF
!

  END DO
!
  mode = switch%b2tral_mode
!
!    ..ion conductivity on the cell boundary
  CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
