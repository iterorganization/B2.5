& 0:nspecies-1), cf_limv(0:ns-1), cf_limv_al(0:nspecies-1), wrkc(0:&
& nspecies-1), wrkcc(0:nspecies-1, 0:nspecies-1), wrkca(0:nspecies-1, 0:&
& nspecies-1), wrkcb(0:nspecies-1, 0:nspecies-1), wrkg2(0:nspecies-1, 0:&
& nspecies-1), wrkg8(0:nspecies-1, 0:nspecies-1), hci0_nofl(ncv, 0:1), &
& hci0(ncv, 0:1), hci0_al_nofl(ncv, 0:nspecies-1), hci0_al(ncv, 0:&
& nspecies-1), hcib_al_nofl(0:nspecies-1), hcib_al(0:nspecies-1), hci0_a&
& (ncv, 0:ns-1), c_hw(0:nspecies-1, 0:nspecies-1), c_rw(0:nspecies-1, 0:&
& nspecies-1), c_hwa(0:nspecies-1, 0:nspecies-1), c_hwb(0:nspecies-1), &
& c_rwb(0:nspecies-1), c_htb(0:nspecies-1), c_rtb(0:nspecies-1), &
& kappa_hta(0:nspecies-1, 0:nspecies-1), kappa_htb(0:nspecies-1), &
& kappa_rta(0:nspecies-1, 0:nspecies-1), kappa_rtb(0:nspecies-1)
  REAL(kind=r8) :: cvsahz_drho(nfc, 0:1, 0:ns-1), cvsa0(nfc, 0:1, 0:ns-1&
& ), cvsa_drho(nfc, 0:1, 0:ns-1)
  CHARACTER(len=80) :: cvalip
  INTRINSIC MAX
  CHARACTER :: REPEAT
  INTRINSIC NINT
  INTRINSIC SQRT
  INTRINSIC SUM
  INTEGER :: max1
  INTEGER :: max2
  REAL(r8) :: arg1
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
  REAL(kind=r8) :: result10
  REAL(r8), DIMENSION(nCv) :: arg10
  REAL(r8), DIMENSION(nCv) :: result11
  REAL(kind=r8) :: arg11
  REAL(kind=r8), DIMENSION(nspecies) :: arg12
!
!-----------------------------------------------------------------------
!.computation
!
!   ..subprogram start-up calls
  CALL SUBINI('b2trzh')
!   ..set internal parameters on first call
  IF (ncall_b2trzh .EQ. 0) THEN
    IF (1 .LT. nspecies*(nspecies+1)/2 - nspecies) THEN
      max1 = nspecies*(nspecies+1)/2 - nspecies
    ELSE
      max1 = 1
    END IF
!   ..allocate
    cvalip = REPEAT('0', max1)
!som 26.06.21
    CALL IPGETC('b2tral_zh_tf_toff', cvalip)
    IF (1 .LT. nspecies*(nspecies+1)/2 - nspecies) THEN
      max2 = nspecies*(nspecies+1)/2 - nspecies
    ELSE
      max2 = 1
    END IF
    DO is=1,max2
      zh_tf_toff(is) = cvalip(is:is)
    END DO
!
!   ..calculate the atom number and the atomic mass of each isonuclear sequence
    inucl = 1
    znnucl(0) = zn(0)
    amnucl(0) = am(0)
    zn_temp = zn(0)
    am_temp = am(0)
    DO is=0,ns-1
      IF (NINT(zn(is)) .NE. NINT(zn_temp) .OR. NINT(am(is)) .NE. NINT(&
&         am_temp)) THEN
        inucl = inucl + 1
        znnucl(inucl-1) = zn(is)
        amnucl(inucl-1) = am(is)
        zn_temp = zn(is)
        am_temp = am(is)
      END IF
!   ..correspondence matrix
      nucl2s(inucl-1, NINT(zamin(is)):NINT(zamax(is))) = is
      s2nucl(is) = inucl
      znnucl_int(inucl-1) = NINT(znnucl(inucl-1))
    END DO
!   ..calculate the N-coefficients and all mass-related variables
!som 02.08.2021
    CALL B2TRCN(nspecies, amnucl, n2, n3, n4, n5, n6, n8, n9, n10, n11, &
&         n12, n13, n14, n15, n16, mu, rmu, rmd2, mum, mdm)
    IF (switch%amfact_on .NE. 0) THEN
!   ..correct mass if is needed
      amnucl_mdf(0:nspecies-1) = amnucl(0:nspecies-1)*amfact(0:nspecies-&
&       1)
!som 28.01.2022
      CALL B2TRCN(nspecies, amnucl_mdf, n2_mdf, n3_mdf, n4_mdf, n5_mdf, &
&           n6_mdf, n8_mdf, n9_mdf, n10_mdf, n11_mdf, n12_mdf, n13_mdf, &
&           n14_mdf, n15_mdf, n16_mdf, mu_mdf, rmu_mdf, rmd2_mdf, &
&           mum_mdf, mdm_mdf)
    END IF
!
!   ..calculate the matrix of thermal force switches
    IF (nspecies .GT. 1) THEN
      i = nspecies - 1
      j = i - 1
      DO is=nspecies*(nspecies+1)/2-nspecies,1,-1
        IF (zh_tf_toff(is) .EQ. '1') THEN
          tf_ton_mat(i, j) = 0.0_R8
          tf_ton_mat(j, i) = 0.0_R8
        ELSE
          tf_ton_mat(i, j) = 1.0_R8
          tf_ton_mat(j, i) = 1.0_R8
        END IF
        j = j - 1
        IF (j .EQ. -1) THEN
          i = i - 1
          j = i - 1
        END IF
      END DO
    END IF
  END IF
!
  gamma = switch%b2tlc0_gamma
!
!   ..main part
!
!srv 05.07.24 {
  eta_cl_a_only = 0.0_R8
!srv 05.07.24 }
  eta_cl_a_only_fl = 0.0_R8
!
!   ..calculate the average Z**2*n and density of each isonuclear sequence
  CALL B2MOD_ZHFRTF_PREP(ncv, ns, pl%na, pl%ua)
!
!   ..calculate temperature gradients
  CALL B2MOD_ZHFRTF_TGTA(ncv, nfc, nvx, ns, switch, geo, mpg, pl%na, pl%&
&                  ua, pl%ti)
!
!   ..compute zetap
  t1 = 4.0_R8*pi*eps0/qe/qe
  arg1 = 2.0_R8*pi
  result1 = SQRT(arg1)
  result2 = SQRT(ev)
  t0 = 3.0_R8/(4.0_R8*result1)*t1*(t1*ev*result2)
  result10 = SQRT(mp)
  czetap = t0*result10
  arg10 = pl%ti/ev
  result11 = SQRT(arg10)
  zetap = czetap*(pl%ti/ev)*result11/dv%lnlam
!
!   ..compute viscous flux limit profile
  CALL CFL_TANH_PROFILE(ncv, geo, mpg, cflim(2), cflmv_core, cflmv_sol, &
&                 cflmv_tanh_a, cflmv_tanh_b, cfl)
!
!   ..calculate transport coefficietns
  DO icv=1,ncv
    amti = amnucl*mp/pl%ti(icv)
    zeffne = SUM(z2n_xy(icv, 0:nspecies-1))
!    ..normalized average Z**2*n
    nz2n = z2n_xy(icv, 0:nspecies-1)/zeffne
!    ..calculate the correction for each charge state (heat flux/R-moment/friction)
    CALL B2TRCZ_NODIFF(nspecies, mum, mdm, nz2n, wrkc, wrkcb, c_hwb, &
&                c_rwb, c_htb, c_rtb)
    c_r_tb_nofl(icv, 0:nspecies-1) = wrkc
    c_r_wb(icv, 0:nspecies-1, 0:nspecies-1) = wrkcb
!    ..calculate the correction for each charge state (viscous stress tensor)
    CALL B2TRCW_NODIFF(nspecies, mum, mdm, nz2n, c_pi_ub(0:nspecies-1), &
&                c_pi_hb(0:nspecies-1))
!
    IF (switch%zh_imp_analyt .EQ. 0) THEN
!    ..calculate the inverted matrix for the heat flux/R-moment
      CALL B2TRCA(nspecies, amti, n5, n6, n9, n10, n11, n12, nz2n, ainv)
!    ..calculate the inverted matrix for the viscous stress tensor
      CALL B2TRCV(nspecies, amti, mdm, n3, n4, n13, n14, n15, n16, nz2n&
&           , cinv)
!
!    ..re-normalize part of the inverted matrix
      DO j=0,2*nspecies-1
        ainv_mdf(0:nspecies-1, j) = amti(0:nspecies-1)*ainv(nspecies:2*&
&         nspecies-1, j)
      END DO
!
!    ..flux-limiting coefficient
      CALL B2TRFL_NODIFF(icv, switch, mpg, ainv, zeffne, amti, zetap(icv&
&                  ), gamma, cf_lim, cf_limh, cf_limb, cf_limbh)
      DO i=0,nspecies-1
        ainv_fl(i, 0:nspecies-1) = cf_lim(i)*ainv(i, 0:nspecies-1)
        ainv_mdf_fl(i, 0:nspecies-1) = cf_lim(i)*ainv_mdf(i, 0:nspecies-&
&         1)
        ainv_fl_h(i, 0:nspecies-1) = cf_limh(i)*ainv(i, 0:nspecies-1)
      END DO
!    ..G-objects for the thermal and friction forces
      DO j=0,nspecies-1
        g2(:, j) = nz2n(:)*nz2n(j)*n2(:, j)
        g8(:, j) = nz2n(:)*nz2n(j)*n8(:, j)
      END DO
!
!    ..calculate the classical ion conductivity without flux limiting
      arg11 = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
      CALL B2TRHT_NODIFF(nspecies, icv, arg11, zetap(icv), zeffne, pl%ti&
&                  (icv), ainv, wrkc)
      hci0_al_nofl(icv, 0:nspecies-1) = wrkc
!    ..calculate the classical ion conductivity with flux limiting
      arg11 = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
      CALL B2TRHT_NODIFF(nspecies, icv, arg11, zetap(icv), zeffne, pl%ti&
&                  (icv), ainv_fl_h, wrkc)
      hci0_al(icv, 0:nspecies-1) = wrkc
!    ..calculate the velocity parts of heat flux and R-transport coefficient
      CALL B2TRHW(nspecies, g2, g8, mum, ainv, ainv_mdf, c_hwa(0:&
&           nspecies-1, 0:nspecies-1), c_rwa(0:nspecies-1, 0:nspecies-1)&
&          )
!    ..calculate the thermal force transport coefficient without flux limiting
      CALL B2TRTF(nspecies, g2, g8, mum, mdm, ainv, ainv_mdf, wrkcc)
      c_r_ta_nofl(icv, 0:nspecies-1, 0:nspecies-1) = wrkcc
!    ..calculate the thermal force transport coefficient with flux limiting
      wrkg2 = g2*tf_ton_mat
      wrkg8 = g8*tf_ton_mat
      CALL B2TRTF(nspecies, wrkg2, wrkg8, mum, mdm, ainv_fl, ainv_mdf_fl&
&           , wrkcc)
      c_r_ta(icv, 0:nspecies-1, 0:nspecies-1) = wrkcc
!
      DO j=0,nspecies-1
!    ..calculate the transport coefficient of the velocity-dependent part
!      of the viscosity stress
        c_pi_ua(0:nspecies-1, j) = -(nz2n(0:nspecies-1)*nz2n(j)*cinv(0:&
&         nspecies-1, j)/rmu(0:nspecies-1, j))
!    ..calculate the transport coefficient of the heat flux dependent part
!      of the viscosity stress
        c_pi_ha(0:nspecies-1, j) = -(nz2n(0:nspecies-1)*nz2n(j)*(cinv(0:&
&         nspecies-1, j)+3.5_R8*cinv(0:nspecies-1, j+nspecies))/rmu(0:&
&         nspecies-1, j))
      END DO
!    ..correct the thermal force
      IF (switch%amfact_on .NE. 0) THEN
        amti_mdf = amnucl_mdf*mp/pl%ti(icv)
!
!    ..calculate the inverted matrix for the heat flux/R-moment
        CALL B2TRCA(nspecies, amti_mdf, n5_mdf, n6_mdf, n9_mdf, n10_mdf&
&             , n11_mdf, n12_mdf, nz2n, ainv)
!
!    ..re-normalize part of the inverted matrix
        DO j=0,2*nspecies-1
          ainv_mdf(0:nspecies-1, j) = amti_mdf(0:nspecies-1)*ainv(&
&           nspecies:2*nspecies-1, j)
        END DO
!
!    ..flux-limiting coefficient
        DO i=0,nspecies-1
          ainv_fl(i, 0:nspecies-1) = cf_lim(i)*ainv(i, 0:nspecies-1)
          ainv_mdf_fl(i, 0:nspecies-1) = cf_lim(i)*ainv_mdf(i, 0:&
&           nspecies-1)
        END DO
!    ..G-objects for the thermal and friction forces
        DO j=0,nspecies-1
          g2(:, j) = nz2n(:)*nz2n(j)*n2_mdf(:, j)
          g8(:, j) = nz2n(:)*nz2n(j)*n8_mdf(:, j)
        END DO
!
!    ..calculate the velocity parts of heat flux and R-transport coefficient
        CALL B2TRHW(nspecies, g2, g8, mum_mdf, ainv, ainv_mdf, c_hwa_mdf&
&             (0:nspecies-1, 0:nspecies-1), c_rwa_mdf(0:nspecies-1, 0:&
&             nspecies-1))
!    ..calculate the thermal force transport coefficient without flux limiting
        CALL B2TRTF(nspecies, g2, g8, mum_mdf, mdm_mdf, ainv, ainv_mdf, &
&             wrkcc)
        c_r_ta_nofl(icv, 0:nspecies-1, 0:nspecies-1) = wrkcc
!    ..calculate the thermal force transport coefficient with flux limiting
        wrkg2 = g2*tf_ton_mat
        wrkg8 = g8*tf_ton_mat
        CALL B2TRTF(nspecies, wrkg2, wrkg8, mum_mdf, mdm_mdf, ainv_fl, &
&             ainv_mdf_fl, wrkcc)
        c_r_ta(icv, 0:nspecies-1, 0:nspecies-1) = wrkcc
      END IF
!
!    ..for the distribution function
      IF (switch%zh_dist_fun .EQ. 1) THEN
        DO i=0,nspecies-1
          kappa_hta(i, 0:nspecies-1) = 2.5_R8*(zetap(icv)/(zeffne**2*mp)&
&           )*ainv(i, 0:nspecies-1)*nal(icv, 0:nspecies-1)
          kappa_htb(i) = -(zetap(icv)/(zeffne**2*mp)*(c_htb(i)/rmd2(i)*&
&           nal(icv, i)/nz2n(i)**2))
!
          kappa_rta(i, 0:nspecies-1) = 2.5_R8*(zetap(icv)/(zeffne**2*mp)&
&           )*ainv_mdf(i, 0:nspecies-1)*nal(icv, 0:nspecies-1)
          kappa_rtb(i) = -(zetap(icv)/(zeffne**2*mp)*(c_rtb(i)/rmd2(i)*&
&           nal(icv, i)/nz2n(i)**2))
        END DO
      END IF
    ELSE
!
!    ..the analytical thermal force transport coefficient
      CALL B2TRHRTF_AN_NODIFF(nspecies, switch, mum, mdm, nz2n, c_hwa(0:&
&                       nspecies-1, 0:nspecies-1), c_rwa(0:nspecies-1, 0&
&                       :nspecies-1), c_hta_an, c_rta_an, c_r_ta_an_nofl&
&                      )
!    ..calculate the classical ion conductivity without flux limiting
      arg11 = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
      CALL B2TRHT_AN_NODIFF(nspecies, icv, arg11, zetap(icv), zeffne, pl&
&                     %ti(icv), c_hta_an, nz2n, rmd2, wrkc)
      hci0_al_nofl(icv, 0:nspecies-1) = wrkc
!    ..the thermal force transport coefficient without flux limiting
      DO i=0,nspecies-1
        arg12 = c_r_ta_an_nofl(i, 0:nspecies-1)*(nz2n(0:nspecies-1)/nz2n&
&         (i))
        temp = SUM(arg12)
        c_r_ta_nofl(icv, i, 0:nspecies-1) = -(c_r_ta_an_nofl(0:nspecies-&
&         1, i)*(nz2n(i)/nz2n(0:nspecies-1)))
        c_r_ta_nofl(icv, i, i) = c_r_ta_nofl(icv, i, i) + temp
      END DO
!
!    ..flux-limiting coefficient (analytical)
      CALL B2TRFL_AN_NODIFF(icv, switch, mpg, c_hta_an, amnucl, amti, &
&                     zetap(icv), gamma, cf_lim, cf_limh, cf_limb, &
&                     cf_limbh)
      DO i=0,nspecies-1
        c_r_ta_an(i, 0:nspecies-1) = cf_lim(i)*c_r_ta_an_nofl(i, 0:&
&         nspecies-1)*tf_ton_mat(i, 0:nspecies-1)
        c_hta_an_fl(i) = cf_limh(i)*c_hta_an(i)
      END DO
!    ..calculate the classical ion conductivity with flux limiting
      arg11 = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
      CALL B2TRHT_AN_NODIFF(nspecies, icv, arg11, zetap(icv), zeffne, pl&
&                     %ti(icv), c_hta_an_fl, nz2n, rmd2, wrkc)
      hci0_al(icv, 0:nspecies-1) = wrkc
!    ..the thermal force transport coefficient with flux limiting
      DO i=0,nspecies-1
        arg12 = c_r_ta_an(i, 0:nspecies-1)*(nz2n(0:nspecies-1)/nz2n(i))
        temp = SUM(arg12)
        c_r_ta(icv, i, 0:nspecies-1) = -(c_r_ta_an(0:nspecies-1, i)*(&
&         nz2n(i)/nz2n(0:nspecies-1)))
        c_r_ta(icv, i, i) = c_r_ta(icv, i, i) + temp
      END DO
