      CALL B2TRHT_AN_DV(nspecies, icv, arg11, zetap(icv), zetapd(:, icv)&
&                 , zeffne, zeffned, pl%ti(icv), pld%ti(:, icv), &
&                 c_hta_an_fl, c_hta_an_fld, nz2n, nz2nd, rmd2, wrkc, &
&                 wrkcd, nbdirs)
      DO nd=1,nbdirs
        hci0_ald(nd, icv, 0:nspecies-1) = wrkcd(nd, :)
      END DO
      hci0_al(icv, 0:nspecies-1) = wrkc
!    ..the thermal force transport coefficient with flux limiting
      DO i=0,nspecies-1
        temp3 = nz2n(0:nspecies-1)/nz2n(i)
        DO nd=1,nbdirs
          arg12d(nd, :) = temp3*c_r_ta_and(nd, i, 0:nspecies-1) + &
&           c_r_ta_an(i, 0:nspecies-1)*(nz2nd(nd, 0:nspecies-1)-temp3*&
&           nz2nd(nd, i))/nz2n(i)
          tempd(nd) = SUM(arg12d(nd, :))
        END DO
        arg12 = c_r_ta_an(i, 0:nspecies-1)*temp3
        temp = SUM(arg12)
        temp3 = nz2n(i)/nz2n(0:nspecies-1)
        DO nd=1,nbdirs
          c_r_tad(nd, icv, i, 0:nspecies-1) = -(temp3*c_r_ta_and(nd, 0:&
&           nspecies-1, i)+c_r_ta_an(0:nspecies-1, i)*(nz2nd(nd, i)-&
&           temp3*nz2nd(nd, 0:nspecies-1))/nz2n(0:nspecies-1))
          c_r_tad(nd, icv, i, i) = c_r_tad(nd, icv, i, i) + tempd(nd)
        END DO
        c_r_ta(icv, i, 0:nspecies-1) = -(c_r_ta_an(0:nspecies-1, i)*&
&         temp3)
        c_r_ta(icv, i, i) = c_r_ta(icv, i, i) + temp
      END DO
!
!    ..the analytical viscous stress transport coefficient
      CALL B2TRVIS_AN_DV(nspecies, mum, mdm, nz2n, nz2nd, c_pi_ua_an, &
&                  c_pi_ua_and, c_pi_ha_an, nbdirs)
!
      c_pi_ua = 0.0_R8
      c_pi_ha = 0.0_R8
      c_pi_uad = 0.D0
      DO j=0,nspecies-1
!    ..calculate the transport coefficient of the velocity-dependent part
!      of the viscosity stress
        DO nd=1,nbdirs
          c_pi_uad(nd, j, j) = 2.0_R8*c_pi_ua_and(nd, j)
        END DO
        c_pi_ua(j, j) = 2.0_R8*c_pi_ua_an(j)
!    ..calculate the transport coefficient of the heat flux dependent part
!      of the viscosity stress
        c_pi_ha(j, j) = 2.0_R8*c_pi_ha_an(j)
      END DO
!
!    ..for the distribution function
      IF (switch%zh_dist_fun .EQ. 1) THEN
        kappa_hta = 0.0_R8
        kappa_rta = 0.0_R8
        DO i=0,nspecies-1
          kappa_hta(i, i) = -(zetap(icv)/(zeffne**2*mp)*(c_hta_an(i)/&
&           rmd2(i)*nal(icv, i)/nz2n(i)**2))
          kappa_htb(i) = -(zetap(icv)/(zeffne**2*mp)*(c_htb(i)/rmd2(i)*&
&           nal(icv, i)/nz2n(i)**2))
!
          kappa_rta(i, i) = -(zetap(icv)/(zeffne**2*mp)*(c_rta_an(i)/&
&           rmd2(i)*nal(icv, i)/nz2n(i)**2))
          kappa_rtb(i) = -(zetap(icv)/(zeffne**2*mp)*(c_rtb(i)/rmd2(i)*&
&           nal(icv, i)/nz2n(i)**2))
        END DO
      END IF
!
      IF (switch%amfact_on .NE. 0) THEN
        DO nd=1,nbdirs
          c_hwa_mdfd(nd, 0:nspecies-1, 0:nspecies-1) = c_hwad(nd, 0:&
&           nspecies-1, 0:nspecies-1)
          c_rwa_mdfd(nd, 0:nspecies-1, 0:nspecies-1) = c_rwad(nd, 0:&
&           nspecies-1, 0:nspecies-1)
        END DO
        c_hwa_mdf(0:nspecies-1, 0:nspecies-1) = c_hwa(0:nspecies-1, 0:&
&         nspecies-1)
        c_rwa_mdf(0:nspecies-1, 0:nspecies-1) = c_rwa(0:nspecies-1, 0:&
&         nspecies-1)
      END IF
    END IF
    DO nd=1,nbdirs
!
!
!    ..calculate the thermal force correction for each charge state
      c_r_tbd(nd, icv, 0:nspecies-1) = c_r_tb_nofl(icv, 0:nspecies-1)*&
&       cf_limbd(nd, 0:nspecies-1) + cf_limb(0:nspecies-1)*c_r_tb_nofld(&
&       nd, icv, 0:nspecies-1)
!
!    ..calculate the friction force transport coefficient
      wrkcd(nd, :) = z2n_xyd(nd, icv, 0:nspecies-1)
    END DO
    c_r_tb(icv, 0:nspecies-1) = cf_limb(0:nspecies-1)*c_r_tb_nofl(icv, 0&
&     :nspecies-1)
    wrkc = z2n_xy(icv, 0:nspecies-1)
    wrkccd = 0.D0
    CALL B2TRFR_DV(nspecies, switch, mum, rmu, wrkc, wrkcd, c_hwa(0:&
&            nspecies-1, 0:nspecies-1), c_hwad(:, 0:nspecies-1, 0:&
&            nspecies-1), c_rwa(0:nspecies-1, 0:nspecies-1), c_rwad(:, 0&
&            :nspecies-1, 0:nspecies-1), wrkcb, wrkcbd, wrkca, wrkcad, &
&            wrkcc, wrkccd, nbdirs)
    c_r_wa(icv, 0:nspecies-1, 0:nspecies-1) = wrkca
    c_r_wb(icv, 0:nspecies-1, 0:nspecies-1) = wrkcb
    DO nd=1,nbdirs
      c_r_wd(nd, icv, 0:nspecies-1, 0:nspecies-1) = wrkccd(nd, :, :)
    END DO
    c_r_w(icv, 0:nspecies-1, 0:nspecies-1) = wrkcc
!
    IF (switch%amfact_on .NE. 0) THEN
      DO nd=1,nbdirs
        wrkcd(nd, :) = z2n_xyd(nd, icv, 0:nspecies-1)
      END DO
      wrkc = z2n_xy(icv, 0:nspecies-1)
      wrkccd = 0.D0
      CALL B2TRFR_DV(nspecies, switch, mum_mdf, rmu_mdf, wrkc, wrkcd, &
&              c_hwa_mdf(0:nspecies-1, 0:nspecies-1), c_hwa_mdfd(:, 0:&
&              nspecies-1, 0:nspecies-1), c_rwa_mdf(0:nspecies-1, 0:&
&              nspecies-1), c_rwa_mdfd(:, 0:nspecies-1, 0:nspecies-1), &
&              wrkcb, wrkcbd, wrkca, wrkcad, wrkcc, wrkccd, nbdirs)
      c_r_wa(icv, 0:nspecies-1, 0:nspecies-1) = wrkca
      c_r_wb(icv, 0:nspecies-1, 0:nspecies-1) = wrkcb
      DO nd=1,nbdirs
        c_r_wd(nd, icv, 0:nspecies-1, 0:nspecies-1) = wrkccd(nd, :, :)
      END DO
      c_r_w(icv, 0:nspecies-1, 0:nspecies-1) = wrkcc
    END IF
!
!    ..calculate the classical eta coefficient including charge state correction
    DO nd=1,nbdirs
      eta_cld(nd, icv, 0:ns-1) = 0.D0
    END DO
    eta_cl(icv, 0:ns-1) = 0.0_R8
    DO i=0,nspecies-1
      temp3 = z2n_xy(icv, i)*z2n_xy(icv, 0:nspecies-1)
      temp5 = zetap(icv)*nal(icv, i)/temp3
      DO nd=1,nbdirs
        tau_zhd(nd, i, 0:nspecies-1) = rmu(i, 0:nspecies-1)*(nal(icv, i)&
&         *zetapd(nd, icv)+zetap(icv)*nald(nd, icv, i)-temp5*(z2n_xy(icv&
&         , 0:nspecies-1)*z2n_xyd(nd, icv, i)+z2n_xy(icv, i)*z2n_xyd(nd&
&         , icv, 0:nspecies-1)))/temp3
      END DO
      tau_zh(i, 0:nspecies-1) = rmu(i, 0:nspecies-1)*temp5
!
!    ..the velocity-dependent part
      temp5 = nal(icv, 0:nspecies-1)/nal(icv, i)
      temp3 = c_pi_ua(i, 0:nspecies-1)*tau_zh(i, 0:nspecies-1)
      DO nd=1,nbdirs
        arg12d(nd, :) = temp5*(tau_zh(i, 0:nspecies-1)*c_pi_uad(nd, i, 0&
&         :nspecies-1)+c_pi_ua(i, 0:nspecies-1)*tau_zhd(nd, i, 0:&
&         nspecies-1)) + temp3*(nald(nd, icv, 0:nspecies-1)-temp5*nald(&
&         nd, icv, i))/nal(icv, i)
        tempd(nd) = SUM(arg12d(nd, :))
      END DO
      arg12 = temp3*temp5
      temp = SUM(arg12)
!
      DO iz=1,znnucl_int(i)
        temp6 = iz*iz*nal(icv, i)
        temp7 = z2n_xy(icv, i)/temp6
        temp8 = c_pi_ub(i)*tau_zh(i, i)
        temp9 = temp + 2.0_R8*switch%zhcscorr*temp8*(temp7-1.0_R8)
        temp10 = pl%na(icv, nucl2s(i, iz))
        temp11 = temp10*pl%ti(icv)
        DO nd=1,nbdirs
          eta_cld(nd, icv, nucl2s(i, iz)) = temp9*(pl%ti(icv)*pld%na(nd&
&           , icv, nucl2s(i, iz))+temp10*pld%ti(nd, icv)) + temp11*(&
&           tempd(nd)+switch%zhcscorr*2.0_R8*((temp7-1.0_R8)*(tau_zh(i, &
&           i)*c_pi_ubd(nd, i)+c_pi_ub(i)*tau_zhd(nd, i, i))+temp8*(&
&           z2n_xyd(nd, icv, i)-temp7*iz**2*nald(nd, icv, i))/temp6))
        END DO
        eta_cl(icv, nucl2s(i, iz)) = temp11*temp9
      END DO
!
      DO j=0,nspecies-1
        DO nd=1,nbdirs
          eta_uadpd(nd, icv, i, j) = tau_zh(i, j)*c_pi_uad(nd, i, j) + &
&           c_pi_ua(i, j)*tau_zhd(nd, i, j)
        END DO
        eta_uadp(icv, i, j) = c_pi_ua(i, j)*tau_zh(i, j)
      END DO
!
      DO nd=1,nbdirs
        eta_ubdpd(nd, icv, i) = 2.0_R8*(tau_zh(i, i)*c_pi_ubd(nd, i)+&
&         c_pi_ub(i)*tau_zhd(nd, i, i))
      END DO
      eta_ubdp(icv, i) = 2.0_R8*c_pi_ub(i)*tau_zh(i, i)
!
!    ..the heat flux dependent part
      DO j=0,nspecies-1
        eta_hadp(icv, i, j) = c_pi_ha(i, j)*tau_zh(i, j)
      END DO
!
      eta_hbdp(icv, i) = 2.0_R8*c_pi_hb(i)*tau_zh(i, i)
!
!    ..the effective velocity-dependent part for the flux limiting
      DO nd=1,nbdirs
        eta_cl_al_eff_xyd(nd, i) = 0.D0
      END DO
      eta_cl_al_eff_xy(i) = 0.0_R8
      DO j=0,nspecies-1
        temp11 = pl%ti(icv)*pl%na(icv, j)
        DO nd=1,nbdirs
          eta_cl_al_eff_xyd(nd, i) = eta_cl_al_eff_xyd(nd, i) + temp11*&
&           eta_uadpd(nd, icv, i, j) + eta_uadp(icv, i, j)*(pl%na(icv, j&
&           )*pld%ti(nd, icv)+pl%ti(icv)*pld%na(nd, icv, j))
        END DO
        eta_cl_al_eff_xy(i) = eta_cl_al_eff_xy(i) + eta_uadp(icv, i, j)*&
&         temp11
!
      END DO
    END DO
    DO nd=1,nbdirs
!
!    ..calculate flux limiting for the viscous stress
      natid(nd, 0:ns-1) = pl%ti(icv)*pld%na(nd, icv, 0:ns-1) + pl%na(icv&
&       , 0:ns-1)*pld%ti(nd, icv)
      eta_cl_xyd(nd, 0:ns-1) = eta_cld(nd, icv, 0:ns-1)
!
      naltid(nd, 0:nspecies-1) = pl%ti(icv)*nald(nd, icv, 0:nspecies-1) &
&       + nal(icv, 0:nspecies-1)*pld%ti(nd, icv)
    END DO
    nati(0:ns-1) = pl%na(icv, 0:ns-1)*pl%ti(icv)
    eta_cl_xy(0:ns-1) = eta_cl(icv, 0:ns-1)
    CALL B2TRFL_VIS_DV(icv, mpg, ns, eta_cl_xy, eta_cl_xyd, nati, natid&
&                , gamma, cfl(icv), cf_limv, cf_limvd, nbdirs)
    nalti(0:nspecies-1) = nal(icv, 0:nspecies-1)*pl%ti(icv)
    CALL B2TRFL_VIS_DV(icv, mpg, nspecies, eta_cl_al_eff_xy, &
&                eta_cl_al_eff_xyd, nalti, naltid, gamma, cfl(icv), &
&                cf_limv_al, cf_limv_ald, nbdirs)
!
!    ..the velocity-dependent part
    DO is=0,ns-1
!    ..apply bx^2
      temp9 = geo%cvbb(icv, 0)*geo%cvbb(icv, 0)
      temp8 = geo%cvbb(icv, 3)*geo%cvbb(icv, 3)
      eta_cl(icv, is) = temp9*(eta_cl(icv, is)/temp8)
      DO nd=1,nbdirs
        eta_cld(nd, icv, is) = temp9*eta_cld(nd, icv, is)/temp8
!    ..apply flux limiting
        eta_cl_fld(nd, icv, is) = cf_limv(is)*eta_cld(nd, icv, is) + &
&         eta_cl(icv, is)*cf_limvd(nd, is)
      END DO
      eta_cl_fl(icv, is) = eta_cl(icv, is)*cf_limv(is)
    END DO
!
!    ..the velocity-dependent part improved form
    DO i=0,nspecies-1
!    ..apply bx^2
      DO j=0,nspecies-1
        temp9 = geo%cvbb(icv, 0)*geo%cvbb(icv, 0)
        temp8 = geo%cvbb(icv, 3)*geo%cvbb(icv, 3)
        DO nd=1,nbdirs
          eta_uadpd(nd, icv, i, j) = temp9*eta_uadpd(nd, icv, i, j)/&
&           temp8
        END DO
        eta_uadp(icv, i, j) = temp9*(eta_uadp(icv, i, j)/temp8)
      END DO
!
      temp9 = geo%cvbb(icv, 0)*geo%cvbb(icv, 0)
      temp8 = geo%cvbb(icv, 3)*geo%cvbb(icv, 3)
      DO nd=1,nbdirs
        eta_ubdpd(nd, icv, i) = temp9*eta_ubdpd(nd, icv, i)/temp8
      END DO
      eta_ubdp(icv, i) = temp9*(eta_ubdp(icv, i)/temp8)
!
      DO iz=1,znnucl_int(i)
        temp9 = 1.0/ia(icv, nucl2s(i, iz))
        temp8 = eta_uadp(icv, i, i) + switch%zhcscorr*eta_ubdp(icv, i)*(&
&         temp9-1.0_R8)
        temp11 = pl%ti(icv)/nal(icv, i)
        temp10 = pl%na(icv, nucl2s(i, iz))
        temp12 = temp10*temp10*temp11
        DO nd=1,nbdirs
          eta_cl_a_onlyd(nd, icv, nucl2s(i, iz)) = temp8*(temp11*2*&
&           temp10*pld%na(nd, icv, nucl2s(i, iz))+temp10**2*(pld%ti(nd, &
&           icv)-temp11*nald(nd, icv, i))/nal(icv, i)) + temp12*(&
&           eta_uadpd(nd, icv, i, i)+switch%zhcscorr*((temp9-1.0_R8)*&
&           eta_ubdpd(nd, icv, i)-eta_ubdp(icv, i)*temp9*iad(nd, icv, &
&           nucl2s(i, iz))/ia(icv, nucl2s(i, iz))))
        END DO
        eta_cl_a_only(icv, nucl2s(i, iz)) = temp12*temp8
!
!    ..apply flux limiting
        temp9 = eta_cl_a_only(icv, nucl2s(i, iz))
        DO nd=1,nbdirs
          eta_cl_a_only_fld(nd, icv, nucl2s(i, iz)) = cf_limv_al(i)*&
&           eta_cl_a_onlyd(nd, icv, nucl2s(i, iz)) + temp9*cf_limv_ald(&
&           nd, i)
        END DO
        eta_cl_a_only_fl(icv, nucl2s(i, iz)) = temp9*cf_limv_al(i)
!
        DO j=0,nspecies-1
          DO nd=1,nbdirs
            eta_uadp_fld(nd, icv, i, j) = cf_limv_al(i)*eta_uadpd(nd, &
&             icv, i, j) + eta_uadp(icv, i, j)*cf_limv_ald(nd, i)
          END DO
          eta_uadp_fl(icv, i, j) = eta_uadp(icv, i, j)*cf_limv_al(i)
        END DO
!
        DO nd=1,nbdirs
          eta_ubdp_fld(nd, icv, i) = cf_limv_al(i)*eta_ubdpd(nd, icv, i)&
&           + eta_ubdp(icv, i)*cf_limv_ald(nd, i)
        END DO
        eta_ubdp_fl(icv, i) = eta_ubdp(icv, i)*cf_limv_al(i)
      END DO
    END DO
!
!    ..the heat flux dependent part
    DO i=0,nspecies-1
!    ..apply bx^2
      DO j=0,nspecies-1
        eta_hadp(icv, i, j) = eta_hadp(icv, i, j)*(geo%cvbb(icv, 0)/geo%&
&         cvbb(icv, 3))**2
      END DO
!
      eta_hbdp(icv, i) = eta_hbdp(icv, i)*(geo%cvbb(icv, 0)/geo%cvbb(icv&
&       , 3))**2
    END DO
!
!   ..tilde I for each isonuclear sequence
    DO inucl=0,nspecies-1
      i_al(inucl) = 0.0_R8
      DO iz=1,znnucl_int(inucl)
        i_al(inucl) = i_al(inucl) + (pl%na(icv, nucl2s(inucl, iz))/nal(&
&         icv, inucl))**2/ia(icv, nucl2s(inucl, iz))
      END DO
    END DO
!
!    ..calculate the classical ion conductivity correction
    arg11 = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    CALL B2TRHT_AN_NODIFF(nspecies, icv, arg11, zetap(icv), zeffne, pl%&
&                   ti(icv), c_htb, nz2n, rmd2, hcib_al_nofl(0:nspecies-&
&                   1))
    DO nd=1,nbdirs
!    ..calculate the classical ion conductivity correction with flux limiting
      wrkcd(nd, :) = c_htb*cf_limbhd(nd, :) + cf_limbh*c_htbd(nd, :)
!
!   ..ion heat conductivity for each charge state
      hci0_ad(nd, icv, 0:ns-1) = 0.D0
    END DO
    wrkc = cf_limbh*c_htb
    arg11 = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    CALL B2TRHT_AN_DV(nspecies, icv, arg11, zetap(icv), zetapd(:, icv), &
&               zeffne, zeffned, pl%ti(icv), pld%ti(:, icv), wrkc, wrkcd&
&               , nz2n, nz2nd, rmd2, hcib_al(0:nspecies-1), hcib_ald(:, &
&               0:nspecies-1), nbdirs)
!
!   ..apply charge state correction for the heat flux
    arg12 = hci0_al_nofl(icv, 0:nspecies-1) + switch%zhcscorr*&
&     hcib_al_nofl(0:nspecies-1)*(i_al(0:nspecies-1)-1.0_R8)
    hci0_nofl(icv, 0) = SUM(arg12)
    arg12 = hci0_al(icv, 0:nspecies-1) + switch%zhcscorr*hcib_al(0:&
&     nspecies-1)*(i_al(0:nspecies-1)-1.0_R8)
    hci0(icv, 0) = SUM(arg12)
    hci0_a(icv, 0:ns-1) = 0.0_R8
    DO i=0,nspecies-1
      DO iz=1,znnucl_int(i)
        temp9 = iz*iz*nal(icv, i)
        temp8 = z2n_xy(icv, i)/temp9
        temp7 = hci0_al(icv, i) + switch%zhcscorr*hcib_al(i)*(temp8-&
&         1.0_R8)
        temp12 = pl%na(icv, nucl2s(i, iz))/nal(icv, i)
        DO nd=1,nbdirs
          hci0_ad(nd, icv, nucl2s(i, iz)) = temp7*(pld%na(nd, icv, &
&           nucl2s(i, iz))-temp12*nald(nd, icv, i))/nal(icv, i) + temp12&
&           *(hci0_ald(nd, icv, i)+switch%zhcscorr*((temp8-1.0_R8)*&
&           hcib_ald(nd, i)+hcib_al(i)*(z2n_xyd(nd, icv, i)-temp8*iz**2*&
&           nald(nd, icv, i))/temp9))
        END DO
        hci0_a(icv, nucl2s(i, iz)) = temp12*temp7
      END DO
    END DO
!
    DO nd=1,nbdirs
      c_hwd(nd, 0:nspecies-1, 0:nspecies-1) = c_hwad(nd, 0:nspecies-1, 0&
&       :nspecies-1)
    END DO
    c_hw(0:nspecies-1, 0:nspecies-1) = c_hwa(0:nspecies-1, 0:nspecies-1)
    DO inucl=0,nspecies-1
      DO nd=1,nbdirs
        c_hwd(nd, inucl, inucl) = c_hwd(nd, inucl, inucl) + switch%&
&         zhcscorr*c_hwbd(nd, inucl) - SUM(c_hwad(nd, 0:nspecies-1, &
&         inucl))
      END DO
      c_hw(inucl, inucl) = c_hw(inucl, inucl) + switch%zhcscorr*c_hwb(&
&       inucl) - SUM(c_hwa(0:nspecies-1, inucl))
    END DO
!   ..save heat flux velocity-dependent part transport coefficient
    DO nd=1,nbdirs
      c_hw_saved(nd, icv, 0:nspecies-1, 0:nspecies-1) = c_hwd(nd, 0:&
&       nspecies-1, 0:nspecies-1)
    END DO
    c_hw_save(icv, 0:nspecies-1, 0:nspecies-1) = c_hw(0:nspecies-1, 0:&
&     nspecies-1)
!
    IF (switch%zh_dist_fun .EQ. 1) THEN
!   ..apply charge state correction for the R-moment
      c_rw(0:nspecies-1, 0:nspecies-1) = c_rwa(0:nspecies-1, 0:nspecies-&
&       1)
      DO inucl=0,nspecies-1
        c_rw(inucl, inucl) = c_rw(inucl, inucl) + switch%zhcscorr*c_rwb(&
&         inucl) - SUM(c_rwa(0:nspecies-1, inucl))
      END DO
!
!    ..moments of the distribution function
      DO inucl=0,nspecies-1
        DO iz=1,znnucl_int(inucl)
          w_out(icv, nucl2s(inucl, iz)) = pl%ua(icv, nucl2s(inucl, iz)) &
&           - avm_u(icv)
!
          arg12 = kappa_hta(inucl, 0:nspecies-1)*gtalc(icv, 0:nspecies-1&
&           )
          htdp_out(icv, nucl2s(inucl, iz)) = SUM(arg12) + switch%&
&           zhcscorr*kappa_htb(inucl)*(pl%na(icv, nucl2s(inucl, iz))/nal&
&           (icv, inucl)/ia(icv, nucl2s(inucl, iz))*gt_ac(icv, nucl2s(&
&           inucl, iz))-gtalc(icv, inucl))
!
          arg12 = c_hw(0:nspecies-1, inucl)*(pl%ua(icv, nucl2s(inucl, iz&
&           ))-av_ualpha(icv, 0:nspecies-1))
          hwdp_out(icv, nucl2s(inucl, iz)) = SUM(arg12)
!
          arg12 = kappa_rta(inucl, 0:nspecies-1)*gtalc(icv, 0:nspecies-1&
&           )
          rtdpgamma_out(icv, nucl2s(inucl, iz)) = SUM(arg12) + switch%&
&           zhcscorr*kappa_rtb(inucl)*(pl%na(icv, nucl2s(inucl, iz))/nal&
&           (icv, inucl)/ia(icv, nucl2s(inucl, iz))*gt_ac(icv, nucl2s(&
&           inucl, iz))-gtalc(icv, inucl))
!
          arg12 = c_rw(0:nspecies-1, inucl)*(pl%ua(icv, nucl2s(inucl, iz&
&           ))-av_ualpha(icv, 0:nspecies-1))
          rwdpgamma_out(icv, nucl2s(inucl, iz)) = SUM(arg12)
!
          piu_out(icv, nucl2s(inucl, iz)) = -(4.0e0_R8/3.0e0_R8*eta_cl(&
&           icv, nucl2s(inucl, iz))/(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))&
&           **2*gavm_uc(icv))
!
        END DO
      END DO
    END IF
!

  END DO
!
  mode = switch%b2tral_mode
!
!    ..ion conductivity on the cell boundary
  CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&              fcs, hci0(:, 0), chci0(:, 0))
  CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&              fcs, hci0_nofl(:, 0), chci0_nofl(:, 0))
  DO i=0,nspecies-1
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&                fcs, hci0_al(:, i), chci0_al(:, 0, i))
  END DO
  DO is=0,ns-1
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&                fcs, hci0_a(:, is), chci0_a(:, 0, is))
  END DO
!
!    ..ion velocity-dependent viscosity on the cell boundary
!srv 30.01.23
  cvsa_drho = 0.0_R8
!srv 30.01.23
  cvsahz_drho = 0.0_R8
  cvsa0 = 0.0_R8
  cvsahz0 = 0.0_R8
!
  fcshz = geo%fcs*geo%fchz
  DO ifc=1,nfc
    fcvolhz(ifc, 1) = geo%fcvol(ifc, 1)*geo%cvhz(mpg%fccv(ifc, 1))
    fcvolhz(ifc, 2) = geo%fcvol(ifc, 2)*geo%cvhz(mpg%fccv(ifc, 2))
  END DO
  IF (switch%zh_vis_type .EQ. 0 .OR. switch%zh_vis_type .EQ. 1) THEN
    cvsahz0d = 0.D0
    vsaf0_lucd = 0.D0
    cvsahz_drhod = 0.D0
    cvsa_drhod = 0.D0
    cvsa0d = 0.D0
    DO is=0,ns-1
      DO nd=1,nbdirs
        wrkd(nd, :) = rho_a_rel(:, is)*eta_cl_fld(nd, :, is) + eta_cl_fl&
&         (:, is)*rho_a_reld(nd, :, is)
      END DO
      wrk = eta_cl_fl(:, is)*rho_a_rel(:, is)
      DO ifc=1,nfc
        temp9 = geo%fcvol(ifc, 1) + geo%fcvol(ifc, 2)
        DO nd=1,nbdirs
          vsaf0_lucd(nd, ifc, 0, is) = (geo%fcvol(ifc, 1)*wrkd(nd, mpg%&
&           fccv(ifc, 1))+geo%fcvol(ifc, 2)*wrkd(nd, mpg%fccv(ifc, 2)))/&
&           temp9
        END DO
        vsaf0_luc(ifc, 0, is) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(ifc, 1))&
&         +geo%fcvol(ifc, 2)*wrk(mpg%fccv(ifc, 2)))/temp9
      END DO
      CALL B2TXCX_DV(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, fcshz, &
&              wrk, wrkd, cvsahz0(:, 0, is), cvsahz0d(:, :, 0, is), &
&              nbdirs)
      CALL B2TXCX_DV(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&              fcs, wrk, wrkd, cvsa0(:, 0, is), cvsa0d(:, :, 0, is), &
&              nbdirs)
      DO nd=1,nbdirs
        vsaf0_lucd(nd, :, 1, is) = 0.D0
        wrkd(nd, :) = rho_a_rel(:, is)*eta_cld(nd, :, is) + eta_cl(:, is&
&         )*rho_a_reld(nd, :, is)
      END DO
      vsaf0_luc(:, 1, is) = 0.0_R8
      CALL B2TXCX_DV(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, fcshz, &
&              eta_cl_fl(:, is), eta_cl_fld(:, :, is), cvsahz_drho(:, 0&
&              , is), cvsahz_drhod(:, :, 0, is), nbdirs)
      wrk = eta_cl(:, is)*rho_a_rel(:, is)
      DO ifc=1,nfc
        temp9 = geo%fcvol(ifc, 1) + geo%fcvol(ifc, 2)
        DO nd=1,nbdirs
          vsaf_drho_lucd(nd, ifc, 0, is) = (geo%fcvol(ifc, 1)*wrkd(nd, &
&           mpg%fccv(ifc, 1))+geo%fcvol(ifc, 2)*wrkd(nd, mpg%fccv(ifc, 2&
&           )))/temp9
        END DO
        vsaf_drho_luc(ifc, 0, is) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(ifc&
&         , 1))+geo%fcvol(ifc, 2)*wrk(mpg%fccv(ifc, 2)))/temp9
      END DO
      DO nd=1,nbdirs
        vsaf_drho_lucd(nd, :, 1, is) = 0.D0
      END DO
      vsaf_drho_luc(:, 1, is) = 0.0_R8
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, wrk, cvsahz0_nofl(:, 0, is))
      CALL B2TXCX_DV(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&              fcs, eta_cl_fl(:, is), eta_cl_fld(:, :, is), cvsa_drho(:&
&              , 0, is), cvsa_drhod(:, :, 0, is), nbdirs)
    END DO
  ELSE IF (switch%zh_vis_type .EQ. 2) THEN
    cvsahz0d = 0.D0
    vsaf0_lucd = 0.D0
    cvsa0d = 0.D0
    DO is=0,ns-1
      DO nd=1,nbdirs
        wrkd(nd, :) = eta_cl_a_only_fld(nd, :, is)
      END DO
      wrk = eta_cl_a_only_fl(:, is)
      DO ifc=1,nfc
        temp9 = geo%fcvol(ifc, 1) + geo%fcvol(ifc, 2)
        DO nd=1,nbdirs
          vsaf0_lucd(nd, ifc, 0, is) = (geo%fcvol(ifc, 1)*wrkd(nd, mpg%&
&           fccv(ifc, 1))+geo%fcvol(ifc, 2)*wrkd(nd, mpg%fccv(ifc, 2)))/&
&           temp9
        END DO
        vsaf0_luc(ifc, 0, is) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(ifc, 1))&
&         +geo%fcvol(ifc, 2)*wrk(mpg%fccv(ifc, 2)))/temp9
      END DO
      DO nd=1,nbdirs
        vsaf0_lucd(nd, :, 1, is) = 0.D0
      END DO
      vsaf0_luc(:, 1, is) = 0.0_R8
      CALL B2TXCX_DV(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, fcshz, &
&              eta_cl_a_only_fl(:, is), eta_cl_a_only_fld(:, :, is), &
&              cvsahz0(:, 0, is), cvsahz0d(:, :, 0, is), nbdirs)
      CALL B2TXCX_DV(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&              fcs, eta_cl_a_only_fl(:, is), eta_cl_a_only_fld(:, :, is)&
&              , cvsa0(:, 0, is), cvsa0d(:, :, 0, is), nbdirs)
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, eta_cl_a_only(:, is), cvsahz0_nofl(:, 0, is))
    END DO
    cvsahz_drhod = 0.D0
    cvsa_drhod = 0.D0
  ELSE
    cvsahz0d = 0.D0
    vsaf0_lucd = 0.D0
    cvsahz_drhod = 0.D0
    cvsa_drhod = 0.D0
    cvsa0d = 0.D0
  END IF
!
!    ..additional velocity-dependent viscosity on the cell boundary
  DO i=0,nspecies-1
    DO j=0,nspecies-1
      DO nd=1,nbdirs
        wrkd(nd, :) = eta_uadp_fld(nd, :, i, j)
      END DO
      wrk = eta_uadp_fl(:, i, j)
      DO ifc=1,nfc
        temp9 = geo%fcvol(ifc, 1) + geo%fcvol(ifc, 2)
        DO nd=1,nbdirs
          vsaf_uadp_lucd(nd, ifc, 0, i, j) = (geo%fcvol(ifc, 1)*wrkd(nd&
&           , mpg%fccv(ifc, 1))+geo%fcvol(ifc, 2)*wrkd(nd, mpg%fccv(ifc&
&           , 2)))/temp9
        END DO
        vsaf_uadp_luc(ifc, 0, i, j) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(&
&         ifc, 1))+geo%fcvol(ifc, 2)*wrk(mpg%fccv(ifc, 2)))/temp9
      END DO
      DO nd=1,nbdirs
        vsaf_uadp_lucd(nd, :, 1, i, j) = 0.D0
      END DO
      vsaf_uadp_luc(:, 1, i, j) = 0.0_R8
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, eta_uadp_fl(:, i, j), cvsahz_uadp_fl(:, 0, i, &
&                  j))
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, &
&                  geo%fcs, eta_uadp_fl(:, i, j), cvsa_uadp_fl(:, 0, i, &
&                  j))
    END DO
!
    DO nd=1,nbdirs
      wrkd(nd, :) = eta_ubdp_fld(nd, :, i)
    END DO
    wrk = eta_ubdp_fl(:, i)
    DO ifc=1,nfc
      temp9 = geo%fcvol(ifc, 1) + geo%fcvol(ifc, 2)
      DO nd=1,nbdirs
        vsaf_ubdp_lucd(nd, ifc, 0, i) = (geo%fcvol(ifc, 1)*wrkd(nd, mpg%&
&         fccv(ifc, 1))+geo%fcvol(ifc, 2)*wrkd(nd, mpg%fccv(ifc, 2)))/&
&         temp9
      END DO
      vsaf_ubdp_luc(ifc, 0, i) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(ifc, 1)&
&       )+geo%fcvol(ifc, 2)*wrk(mpg%fccv(ifc, 2)))/temp9
    END DO
    DO nd=1,nbdirs
      vsaf_ubdp_lucd(nd, :, 1, i) = 0.D0
    END DO
    vsaf_ubdp_luc(:, 1, i) = 0.0_R8
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, fcshz&
&                , eta_ubdp_fl(:, i), cvsahz_ubdp_fl(:, 0, i))
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&                fcs, eta_ubdp_fl(:, i), cvsa_ubdp_fl(:, 0, i))
  END DO
!
!    ..ion heat flux dependent viscosity on the cell boundary
  DO i=0,nspecies-1
    DO j=0,nspecies-1
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, &
&                  fcshz, eta_hadp(:, i, j), cvsahz_hadp(:, 0, i, j))
      CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, &
&                  geo%fcs, eta_hadp(:, i, j), cvsa_hadp(:, 0, i, j))
    END DO
!
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, fcvolhz, fcshz&
&                , eta_hbdp(:, i), cvsahz_hbdp(:, 0, i))
    CALL B2TXCX_NODIFF(ncv, nfc, 0.0_R8, mode, geo, mpg, geo%fcvol, geo%&
&                fcs, eta_hbdp(:, i), cvsa_hbdp(:, 0, i))
  END DO
!
!    ..apply Luciani correction for the ion conductivity
  chci0_luc = 0.0_R8
  chci0_lucd = 0.D0
  DO nd=1,nbdirs
    chci0_lucd(nd, :, 0) = chci0_luc(:, 0)*cod%f_luc_ki(nd, :)
  END DO
  chci0_luc(:, 0) = chci0_luc(:, 0)*co%f_luc_ki
  hci0(:, 0) = hci0(:, 0)*co%f_luc_ki_c
  DO i=0,nspecies-1
    chci0_al_luc(:, 0, i) = chci0_al(:, 0, i)*co%f_luc_ki
    DO nd=1,nbdirs
      hci0_al_lucd(nd, :, i) = co%f_luc_ki_c*hci0_ald(nd, :, i) + &
&       hci0_al(:, i)*cod%f_luc_ki_c(nd, :)
    END DO
    hci0_al_luc(:, i) = hci0_al(:, i)*co%f_luc_ki_c
  END DO
  DO is=0,ns-1
    chci0_a_luc(:, 0, is) = chci0_a(:, 0, is)*co%f_luc_ki
    DO nd=1,nbdirs
      hci0_a_lucd(nd, :, is) = co%f_luc_ki_c*hci0_ad(nd, :, is) + hci0_a&
&       (:, is)*cod%f_luc_ki_c(nd, :)
    END DO
    hci0_a_luc(:, is) = hci0_a(:, is)*co%f_luc_ki_c
  END DO
  DO nd=1,nbdirs
!
!    ..apply Luciani correction for the ion velocity-dependent viscosity
!srv 30.01.23 {
    cvsahz0_lucd(nd, :, :, :) = cvsahz0d(nd, :, :, :)
    cvsahz_drho_lucd(nd, :, :, :) = cvsahz_drhod(nd, :, :, :)
    cvsa0_lucd(nd, :, :, :) = cvsa0d(nd, :, :, :)
!srv 30.01.23 }
    cvsa_drho_lucd(nd, :, :, :) = cvsa_drhod(nd, :, :, :)
  END DO
  cvsahz0_luc = cvsahz0
  cvsahz_drho_luc = cvsahz_drho
  cvsa0_luc = cvsa0
  cvsa_drho_luc = cvsa_drho
  DO is=0,ns-1
    IF (.NOT.is_neutral(is)) THEN
      DO nd=1,nbdirs
        vsaf0_lucd(nd, :, 0, is) = co%f_luc_et*vsaf0_lucd(nd, :, 0, is) &
&         + vsaf0_luc(:, 0, is)*cod%f_luc_et(nd, :)
        vsaf_drho_lucd(nd, :, 0, is) = co%f_luc_et*vsaf_drho_lucd(nd, :&
&         , 0, is) + vsaf_drho_luc(:, 0, is)*cod%f_luc_et(nd, :)
        cvsahz0_lucd(nd, :, 0, is) = co%f_luc_et*cvsahz0d(nd, :, 0, is) &
&         + cvsahz0(:, 0, is)*cod%f_luc_et(nd, :)
        cvsahz_drho_lucd(nd, :, 0, is) = co%f_luc_et*cvsahz_drhod(nd, :&
&         , 0, is) + cvsahz_drho(:, 0, is)*cod%f_luc_et(nd, :)
        cvsa0_lucd(nd, :, 0, is) = co%f_luc_et*cvsa0d(nd, :, 0, is) + &
&         cvsa0(:, 0, is)*cod%f_luc_et(nd, :)
        cvsa_drho_lucd(nd, :, 0, is) = co%f_luc_et*cvsa_drhod(nd, :, 0, &
&         is) + cvsa_drho(:, 0, is)*cod%f_luc_et(nd, :)
      END DO
      vsaf0_luc(:, 0, is) = vsaf0_luc(:, 0, is)*co%f_luc_et
      vsaf_drho_luc(:, 0, is) = vsaf_drho_luc(:, 0, is)*co%f_luc_et
      cvsahz0_luc(:, 0, is) = cvsahz0(:, 0, is)*co%f_luc_et
      cvsahz_drho_luc(:, 0, is) = cvsahz_drho(:, 0, is)*co%f_luc_et
      cvsa0_luc(:, 0, is) = cvsa0(:, 0, is)*co%f_luc_et
      cvsa_drho_luc(:, 0, is) = cvsa_drho(:, 0, is)*co%f_luc_et
    END IF
  END DO
!
!    ..apply Luciani correction for the addtional part of the
!      velocity-dependent viscosity
!wdk todo: add flux limit to vsaf_* arrays
  DO i=0,nspecies-1
    DO j=0,nspecies-1
      DO nd=1,nbdirs
        vsaf_uadp_lucd(nd, :, 0, i, j) = co%f_luc_et*vsaf_uadp_lucd(nd, &
&         :, 0, i, j) + vsaf_uadp_luc(:, 0, i, j)*cod%f_luc_et(nd, :)
      END DO
      vsaf_uadp_luc(:, 0, i, j) = vsaf_uadp_luc(:, 0, i, j)*co%f_luc_et
      cvsahz_uadp_luc(:, 0, i, j) = cvsahz_uadp_fl(:, 0, i, j)*co%&
&       f_luc_et
      cvsa_uadp_luc(:, 0, i, j) = cvsa_uadp_fl(:, 0, i, j)*co%f_luc_et
    END DO
    DO nd=1,nbdirs
      vsaf_ubdp_lucd(nd, :, 0, i) = co%f_luc_et*vsaf_ubdp_lucd(nd, :, 0&
&       , i) + vsaf_ubdp_luc(:, 0, i)*cod%f_luc_et(nd, :)
    END DO
    vsaf_ubdp_luc(:, 0, i) = vsaf_ubdp_luc(:, 0, i)*co%f_luc_et
    cvsahz_ubdp_luc(:, 0, i) = cvsahz_ubdp_fl(:, 0, i)*co%f_luc_et
    cvsa_ubdp_luc(:, 0, i) = cvsa_ubdp_fl(:, 0, i)*co%f_luc_et
  END DO
!
!    ..apply Luciani and neoclassical corrections for the ion heat flux
!      dependent viscosity
!wdk todo:  add flux limit to vsaf_* arrays
  DO i=0,nspecies-1
    DO j=0,nspecies-1
      cvsahz_hadp_luc(:, 0, i, j) = cvsahz_hadp(:, 0, i, j)*co%f_luc_et*&
&       co%alpha1f
      cvsa_hadp_luc(:, 0, i, j) = cvsa_hadp(:, 0, i, j)*co%f_luc_et*co%&
&       alpha1f
    END DO
    cvsahz_hbdp_luc(:, 0, i) = cvsahz_hbdp(:, 0, i)*co%f_luc_et*co%&
&     alpha1f
    cvsa_hbdp_luc(:, 0, i) = cvsa_hbdp(:, 0, i)*co%f_luc_et*co%alpha1f
  END DO
!
  ncall_b2trzh = ncall_b2trzh + 1
  CALL SUBEND()
  RETURN
!
END SUBROUTINE B2TRZH_DV

