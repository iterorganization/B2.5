      c_pi_ua = 0.0_R8
      c_pi_ha = 0.0_R8
      DO j=0,nspecies-1
!    ..calculate the transport coefficient of the velocity-dependent part
!      of the viscosity stress
        c_pi_ua(j, j) = 2.0_R8*c_pi_ua_an(j)
!    ..calculate the transport coefficient of the heat flux dependent part
