  END DO
  IF (switch%zh_vis_type .EQ. 0 .OR. switch%zh_vis_type .EQ. 1) THEN
    DO is=0,ns-1
      wrk = eta_cl_fl(:, is)*rho_a_rel(:, is)
      DO ifc=1,nfc
        vsaf0_luc(ifc, 0, is) = (geo%fcvol(ifc, 1)*wrk(mpg%fccv(ifc, 1))&
