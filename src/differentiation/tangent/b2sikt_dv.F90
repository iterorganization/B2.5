        temp6 = SQRT(pl%kt)
        DO nd=1,nbdirs
          WHERE (pl%kt .EQ. 0.D0) 
            result12d(nd, :) = 0.D0
          ELSEWHERE
            result12d(nd, :) = pld%kt(nd, :)/(2.0*temp6)
          END WHERE
        END DO
        result12 = temp6
        dktcd = 0.D0
        CALL GRADC_P_DV(ncv, nfc, nvx, 0, geo, geod, mpg, mpgd, result12&
&                 , result12d, wrkv, wrkvd, dktc, dktcd, nbdirs)
        DO nd=1,nbdirs
          wrk3d(nd, :) = geo%cvvol*(dktc**2*cod%sigx_kt(nd, :)+co%&
&           sigx_kt*2*dktc*dktcd(nd, :))
        END DO
        wrk3 = co%sigx_kt*geo%cvvol*dktc**2
        shi0d = 0.D0
        skt0d = 0.D0
        she0d = 0.D0
      END IF
    ELSE
      CALL XERRAB('wrong value of b2sikt_style')
      shi0d = 0.D0
      skt0d = 0.D0
      she0d = 0.D0
      wrksd = 0.D0
      wrk0d = 0.D0
      wrk1d = 0.D0
      wrk2d = 0.D0
      wrk3d = 0.D0
    END IF
!
!     ..sources
    DO ic=1,mpg%nci
      IF (MOD(mpg%cvreg(ic), 4) .EQ. 1) THEN
        DO nd=1,nbdirs
          fac_sheathd(nd) = switchd%b2sikt_fac_sheath_core(nd)
          fac_dissd(nd) = switchd%b2sikt_fac_diss_core(nd)
        END DO
        fac_sheath = switch%b2sikt_fac_sheath_core
        fac_diss = switch%b2sikt_fac_diss_core
      ELSE
        DO nd=1,nbdirs
          fac_sheathd(nd) = switchd%b2sikt_fac_sheath(nd)
          fac_dissd(nd) = switchd%b2sikt_fac_diss(nd)
        END DO
        fac_sheath = switch%b2sikt_fac_sheath
        fac_diss = switch%b2sikt_fac_diss
      END IF
      DO nd=1,nbdirs
        t0d(nd) = wrk0d(nd, ic)
        t1d(nd) = wrk1d(nd, ic)
        t2d(nd) = wrk2(ic)*fac_sheathd(nd) + fac_sheath*wrk2d(nd, ic)
        t3d(nd) = wrk3(ic)*fac_dissd(nd) + fac_diss*wrk3d(nd, ic)
        t4d(nd) = wrksd(nd, ic)
      END DO
      t0 = wrk0(ic)
      t1 = wrk1(ic)
      t2 = fac_sheath*wrk2(ic)
      t3 = fac_diss*wrk3(ic)
      t4 = wrks(ic)
      IF (switch%b2sikt_min_source .EQ. 1 .AND. t0 + t1 - pl%kt(ic)*t2 -&
&         t3 .LT. 0.0_R8) THEN
!experimental
        t0 = 0.0_R8
        t1 = 0.0_R8
        t2 = 0.0_R8
        t3 = 0.0_R8
        t0d = 0.D0
        t1d = 0.D0
        t2d = 0.D0
        t3d = 0.D0
      END IF
      IF (switch%b2sikt_kt_source_stab .EQ. 0) THEN
        DO nd=1,nbdirs
          she0d(nd, ic, 0) = pl%kt(ic)*t2d(nd) + t2*pld%kt(nd, ic) - t1d&
&           (nd) + t3d(nd)
          shi0d(nd, ic, 0) = -t0d(nd)
          skt0d(nd, ic, 0) = t0d(nd) + t1d(nd) - t3d(nd) - t4d(nd)
          skt0d(nd, ic, 1) = -t2d(nd)
        END DO
        she0(ic, 0) = -t1 + t2*pl%kt(ic) + t3
        shi0(ic, 0) = -t0
        skt0(ic, 0) = t0 + t1 - t3 - t4
        skt0(ic, 1) = -t2
      ELSE
        IF (-t1 + t2*pl%kt(ic) + t3 .LT. 0.0_R8) THEN
          DO nd=1,nbdirs
            she0d(nd, ic, 0) = 0.D0
          END DO
          she0(ic, 0) = 0.0_R8
        ELSE
          DO nd=1,nbdirs
            she0d(nd, ic, 0) = pl%kt(ic)*t2d(nd) + t2*pld%kt(nd, ic) - &
&             t1d(nd) + t3d(nd)
          END DO
          she0(ic, 0) = -t1 + t2*pl%kt(ic) + t3
        END IF
        IF (-t1 + t2*pl%kt(ic) + t3 .GT. 0.0_R8) THEN
          min1 = 0.0_R8
          min1d = 0.D0
        ELSE
          DO nd=1,nbdirs
            min1d(nd) = pl%kt(ic)*t2d(nd) + t2*pld%kt(nd, ic) - t1d(nd) &
&             + t3d(nd)
          END DO
          min1 = -t1 + t2*pl%kt(ic) + t3
        END IF
        temp7 = min1/pl%te(ic)
        DO nd=1,nbdirs
          she0d(nd, ic, 1) = (min1d(nd)-temp7*pld%te(nd, ic))/pl%te(ic)
        END DO
        she0(ic, 1) = temp7
        IF (-t0 .LT. 0.0_R8) THEN
          DO nd=1,nbdirs
            shi0d(nd, ic, 0) = 0.D0
          END DO
          shi0(ic, 0) = 0.0_R8
        ELSE
          DO nd=1,nbdirs
            shi0d(nd, ic, 0) = -t0d(nd)
          END DO
          shi0(ic, 0) = -t0
        END IF
        IF (-t0 .GT. 0.0_R8) THEN
          min2 = 0.0_R8
          min2d = 0.D0
        ELSE
          DO nd=1,nbdirs
            min2d(nd) = -t0d(nd)
          END DO
          min2 = -t0
        END IF
        temp7 = min2/pl%ti(ic)
        DO nd=1,nbdirs
          shi0d(nd, ic, 1) = (min2d(nd)-temp7*pld%ti(nd, ic))/pl%ti(ic)
        END DO
        shi0(ic, 1) = temp7
        IF (t0 + t1 .LT. 0.0_R8) THEN
          DO nd=1,nbdirs
            skt0d(nd, ic, 0) = 0.D0
          END DO
          skt0(ic, 0) = 0.0_R8
        ELSE
          DO nd=1,nbdirs
            skt0d(nd, ic, 0) = t0d(nd) + t1d(nd)
          END DO
          skt0(ic, 0) = t0 + t1
        END IF
        IF (t0 + t1 .GT. 0.0_R8) THEN
          min3 = 0.0_R8
          min3d = 0.D0
        ELSE
          DO nd=1,nbdirs
            min3d(nd) = t0d(nd) + t1d(nd)
          END DO
          min3 = t0 + t1
        END IF
        temp7 = 1.0e-8_R8*ev + pl%kt(ic)
        temp8 = (min3-t3-t4)/temp7
        DO nd=1,nbdirs
          skt0d(nd, ic, 1) = (min3d(nd)-t3d(nd)-t4d(nd)-temp8*pld%kt(nd&
&           , ic))/temp7 - t2d(nd)
        END DO
        skt0(ic, 1) = temp8 - t2
      END IF
      skt_prod(ic) = t0 + t1
      skt_diss(ic) = t2*pl%kt(ic) + t3 + t4
    END DO
  ELSE
    CALL XERRAB('wrong value of b2sikt_model')
    shi0d = 0.D0
    skt0d = 0.D0
    she0d = 0.D0
  END IF
!
! ..output
  IF (switch%b2sikt_iout .EQ. 1) THEN
    wrks = she0(:, 0) + she0(:, 1)*pl%te + she0(:, 2)*dv%ne + she0(:, 3)&
&     *pl%te*dv%ne
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_she')
    wrks = shi0(:, 0) + shi0(:, 1)*pl%ti + shi0(:, 2)*dv%ni(:, 0) + shi0&
&     (:, 3)*pl%ti*dv%ni(:, 0)
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_shi')
    wrks = skt0(:, 0) + skt0(:, 1)*pl%kt + skt0(:, 2)*dv%ni(:, 1) + skt0&
&     (:, 3)*pl%kt*dv%ni(:, 1)
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_skt')
    CALL MY_OUT_US(70, ncv, 0, skt_prod, 'b2sikt_skt_prod')
    CALL MY_OUT_US(70, ncv, 0, skt_diss, 'b2sikt_skt_diss')
    wrks = geo%cvvol(:)*((pl%ti(:)+pl%te(:))*co%dna_exb(:)*(dnac(:, 0)*&
&     dlbc(:, 0)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_na_x')
    wrks = geo%cvvol(:)*((pl%ti(:)+pl%te(:))*co%dna_exb(:)*(dnac(:, 1)*&
&     dlbc(:, 1)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_na_y')
    wrks = geo%cvvol(:)*(2.0_R8/3.0_R8*co%hce_exb(:)*switch%&
&     b2sikt_fac_aniso*(dtec(:, 0)*dlbc(:, 0)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_te_x')
    wrks = geo%cvvol(:)*(2.0_R8/3.0_R8*co%hce_exb(:)*switch%&
&     b2sikt_fac_aniso*(dtec(:, 1)*dlbc(:, 1)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_te_y')
    wrks = geo%cvvol(:)*(2.0_R8/3.0_R8*co%hci_exb(:)*switch%&
&     b2sikt_fac_aniso*(dtic(:, 0)*dlbc(:, 0)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_ti_x')
    wrks = geo%cvvol(:)*(2.0_R8/3.0_R8*co%hci_exb(:)*switch%&
&     b2sikt_fac_aniso*(dtic(:, 1)*dlbc(:, 1)))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_prod_ti_y')
    CALL MY_OUT_US(70, ncv, 0, connfs, 'b2sikt_connfs')
    CALL MY_OUT_US(70, ncv, 0, csfs, 'b2sikt_csfs')
    CALL MY_OUT_US(70, ncv, 0, dnac(:, 0), 'b2sikt_dnac0')
    CALL MY_OUT_US(70, ncv, 0, dnac(:, 1), 'b2sikt_dnac1')
    CALL MY_OUT_US(70, ncv, 0, dtec(:, 0), 'b2sikt_dtec0')
    CALL MY_OUT_US(70, ncv, 0, dtec(:, 1), 'b2sikt_dtec1')
    CALL MY_OUT_US(70, ncv, 0, dtic(:, 0), 'b2sikt_dtic0')
    CALL MY_OUT_US(70, ncv, 0, dtic(:, 1), 'b2sikt_dtic1')
    CALL MY_OUT_US(70, ncv, 0, dlbc(:, 0), 'b2sikt_dlbc0')
    CALL MY_OUT_US(70, ncv, 0, dlbc(:, 1), 'b2sikt_dlbc1')
!!some temporary/experimental output computations for source due to Reynolds stress
    wrk0d = 0.D0
    CALL INTCELL_DV(nfc, ncv, mpg, mpg%intcellp, dv%vaecrb(:, 0, ismain)&
&             , dvd%vaecrb(:, :, 0, ismain), wrk0, wrk0d, nbdirs)
    wrk1d = 0.D0
    CALL INTCELL_DV(nfc, ncv, mpg, mpg%intcellr, dv%vaecrb(:, 1, ismain)&
&             , dvd%vaecrb(:, :, 1, ismain), wrk1, wrk1d, nbdirs)
    wrk2 = wrk0*dlbc(:, 0) + wrk1*dlbc(:, 1)
    wrks = -(2.0_R8/3.0_R8*geo%cvvol*pl%na(:, ismain)*wrk2*(switch%&
&     b2sikt_fac_vis_rs*co%dna_exb*am(ismain)*mp*wrk2+pl%kt))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_RS_symmetric')
    CALL GRADC_DIV_DV(ncv, nfc, nvx, 1, geo, geod, mpg, mpgd, wrk0, &
&               wrk0d, dv%vaecrb(:, 0, ismain), dvd%vaecrb(:, :, 0, &
&               ismain), wrkg0, wrkg0d, nbdirs)
    CALL GRADC_DIV_DV(ncv, nfc, nvx, 1, geo, geod, mpg, mpgd, wrk1, &
&               wrk1d, dv%vaecrb(:, 1, ismain), dvd%vaecrb(:, :, 1, &
&               ismain), wrkg1, wrkg1d, nbdirs)
    wrkf = dv%vaecrb(:, 0, ismain)*geo%fcbb(:, 3)/geo%fcbb(:, 2)
    wrk2 = wrk0*geo%cvbb(:, 3)/geo%cvbb(:, 2)
    CALL GRADC_DIV_NODIFF(ncv, nfc, nvx, 1, geo, mpg, wrk2, wrkf, wrkg2)
    wrks = switch%b2sikt_fac_vis_rs*2.0_R8*geo%cvvol*co%dna_exb*am(&
&     ismain)*mp*pl%na(:, ismain)*(wrkg0(:, 0)**2+wrkg1(:, 1)**2+0.5_R8*&
&     (wrkg0(:, 1)**2+wrkg1(:, 0)**2+wrkg2(:, 0)**2+wrkg2(:, 1)**2)+&
&     wrkg1(:, 0)*wrkg0(:, 1))
    CALL MY_OUT_US(70, ncv, 0, wrks, 'b2sikt_RS_deviatoric')
  END IF
!
! ..return
  ncall_b2sikt = ncall_b2sikt + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2sikt
!
END SUBROUTINE B2SIKT_DV

