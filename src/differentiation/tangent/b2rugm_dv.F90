            offset = (iface-cellflag_leftface+1)*(-10)
            cflags(ix, iy, iface) = offset - region(ix, iy, 0)
            cflags(bottomix(ix, iy), bottomiy(ix, iy), cellflag_topface)&
&            = cflags(ix, iy, iface)
          END IF
        END IF
      END DO
    END DO
  END IF
  cell_width = hx
  cell_height(:, :) = hy(:, :)*qz(:, :, 1)
  IF (b2fgmtry_version .GE. '01.001.033') THEN
    CALL CFRURE_OPT(nget, n2, cell_width, 'cell_width')
    CALL CFRURE_OPT(nget, n2, cell_height, 'cell_height')
  END IF
!  ..interpolate magnetic field to cell faces
  CALL B2AGBF(nx, ny, isymm, periodic_bc, nncut, crx, cry, fpsi, ffbz, &
&       vol, gs, qz, qc, qs, bb, wbbl, wbbr, wbbv, region, leftix, &
&       leftiy, bottomix, bottomiy, rightix, rightiy, topix, topiy, &
&       cflags)
!  ..init some arrays for classical grids
!   ..read  additional geometric parameters for extended grids
  IF (b2fgmtry_version .GE. '03.002.000') THEN
    arg1 = n2*4
    CALL CFRURE(nget, arg1, hc, 'hc')
    arg1 = n2*2
    CALL CFRURE(nget, arg1, ht, 'ht')
    arg1 = n2*2
    CALL CFRURE(nget, arg1, qac, 'qac')
    arg1 = n2*2
    CALL CFRURE(nget, arg1, qas, 'qas')
    arg1 = n2*3
    CALL CFRURE(nget, arg1, ebc, 'ebc')
  ELSE
    hc(:, :, 0) = 0.5*hx(:, :)
    hc(:, :, 1) = 0.5*hy(:, :)
    hc(:, :, 2) = 0.5*hx(:, :)
    hc(:, :, 3) = 0.5*hy(:, :)
    arg14(:, :) = wbbl(:, :, 0)
    qac(:, :, 0) = qc(:, :, 0)*SIGN(1._R8, arg14(:, :))
    arg14(:, :) = wbbl(:, :, 0)
    qas(:, :, 0) = -(qs(:, :, 0)*SIGN(1._R8, arg14(:, :)))
    qac(:, :, 1) = 0.0_R8
! Initialize ebc and ht for 'classical' grids
    arg15(:, :) = wbbv(:, :, 0)
    qas(:, :, 1) = 1.0_R8*SIGN(1._R8, arg15(:, :))
    DO iy=-1,ny
! Unit vectors of line between W/E face centers
      DO ix=-1,nx
!
        t0 = crx(ix, iy, 1) + crx(ix, iy, 3) - (crx(ix, iy, 0)+crx(ix, &
&         iy, 2))
        t1 = cry(ix, iy, 1) + cry(ix, iy, 3) - (cry(ix, iy, 0)+cry(ix, &
&         iy, 2))
! Unit vector of magnetic field in Cartesian X,Y,Z coordinates
        t2 = NORM(t0, t1)
!
        ebc(ix, iy, 0) = bb(ix, iy, 0)/bb(ix, iy, 3)*t0/t2
        ebc(ix, iy, 1) = bb(ix, iy, 0)/bb(ix, iy, 3)*t1/t2
! Length along faces, ht
        ebc(ix, iy, 2) = bb(ix, iy, 2)/bb(ix, iy, 3)
!
        IF (leftix(ix, iy) .NE. -2) THEN
          IF (ht_style .EQ. 0) THEN
! Implementation intended for backwards compatibility
! testing
            ht(ix, iy, 0) = 0.5_R8*(hy(leftix(ix, iy), leftiy(ix, iy))+&
&             hy(ix, iy))
          ELSE
! Precise computation
            ht(ix, iy, 0) = DIST(ix, iy, 0, 2)
          END IF
        ELSE
          ht(ix, iy, 0) = 0.0_R8
        END IF
        IF (bottomiy(ix, iy) .NE. -2) THEN
          IF (ht_style .EQ. 0) THEN
! Implementation intended for backwards compatibility
! testing
            ht(ix, iy, 1) = 0.5_R8*(hx(bottomix(ix, iy), bottomiy(ix, iy&
&             ))+hx(ix, iy))
          ELSE
! Precise computation
            ht(ix, iy, 1) = DIST(ix, iy, 0, 1)
          END IF
        ELSE
          ht(ix, iy, 1) = 0.0_R8
        END IF
      END DO
    END DO
  END IF
  IF (b2fgmtry_version .LT. '01.001.063') THEN
!srv 15.02.11
! Moved here; b2xgbs now requires cflags and qac
    CALL B2XGBS_NODIFF(nx, ny, wbbl, wbbv, gs, qac, pbs, leftix, leftiy&
&                , bottomix, bottomiy, cflags)
  END IF
!   ..test sign of bb
  CALL B2XVSG_NODIFF(n2, bb(-1, -1, 3), 1, 'bb3', '.ge.')
  CALL B2XVSG_NODIFF(n2, wbbl(-1, -1, 3), 1, 'wbbl3', '.ge.')
  CALL B2XVSG_NODIFF(n2, wbbr(-1, -1, 3), 1, 'wbbr3', '.ge.')
  CALL B2XVSG_NODIFF(n2, wbbv(-1, -1, 3), 1, 'wbbv3', '.ge.')
  arg1 = (nx+3)*(ny+3)
  CALL B2XVSG_NODIFF(arg1, wbbc(-1, -1, 3), 1, 'wbbc3', '.ge.')
!   ..test geometry
  result11 = MAXVAL(region(-1:nx, -1:ny, 1))
  result2 = MINVAL(region(-1:nx, -1:ny, 2))
  CALL XERTST(result11 .GT. 0 .OR. result2 .LT. 0, &
&       'X-regions not set, re-run b2ag!')
  result11 = MAXVAL(region(-1:nx, -1:ny, 2))
  result2 = MINVAL(region(-1:nx, -1:ny, 1))
  CALL XERTST(result11 .GT. 0 .OR. result2 .LT. 0, &
&       'Y-regions not set, re-run b2ag!')
  CALL B2XVGM_NODIFF(nx, ny, vol, hx, hy, qz, qc, qs, gs, pbs, &
&              cell_width, cell_height)
!   ..create B2GridMap data structure
  IF (nget .NE. 70) THEN
! skip case when reading b2fgmtry.old from b2yt
    CALL B2CREATEMAP(nx, ny, crx, cry, cflags, leftix, leftiy, rightix, &
&              rightiy, topix, topiy, bottomix, bottomiy, .true., gmap, &
&              .false.)
    mapinitialized = .true.
  END IF
!
! ..return
  CALL SUBEND()
  RETURN
END SUBROUTINE B2RUGM_NODIFF

