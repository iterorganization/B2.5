    END DO
    t1 = -(0.5_R8*flob(ifc, 0)) - conb(ifc, 0, 0) - conb(ifc, 0, 2)
    t2 = 0.5_R8*flob(ifc, 0) - conb(ifc, 0, 0) - conb(ifc, 0, 1)
    IF (solvereg(mpg%cvreg(icv1))) THEN
      CALL RSCAL_DV(pb(icv1), pbd(:, icv1), result1, result1d, nbdirs)
      DO nd=1,nbdirs
        aad(nd, mpg%cvnvp(icv1, 1)) = aad(nd, mpg%cvnvp(icv1, 1)) - &
&         result1*t1d(nd) - t1*result1d(nd)
      END DO
      aa(mpg%cvnvp(icv1, 1)) = aa(mpg%cvnvp(icv1, 1)) - t1*result1
      CALL RSCAL_DV(pb(icv2), pbd(:, icv2), result1, result1d, nbdirs)
