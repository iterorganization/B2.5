    INTRINSIC ABS
    INTRINSIC SQRT
    INTRINSIC NINT
    INTRINSIC EXP
    REAL(r8) :: y9
    REAL(r8) :: y10
    REAL(kind=r8) :: x1
    REAL(kind=r8) :: x2
    REAL(r8) :: max1
    REAL(r8) :: max2
    REAL(r8) :: max3
    REAL(r8) :: max4
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: abs1
    REAL(r8) :: max5
    REAL(kind=r8) :: max6
    REAL(kind=r8) :: max7
    REAL(kind=r8) :: abs2
    REAL(kind=r8) :: abs3
    REAL(r8) :: max8
    REAL(kind=r8) :: arg12
    REAL(kind=r8) :: arg2
    REAL(r8) :: arg13
    REAL(kind=r8) :: pwx1
    REAL(kind=r8) :: pwr1
    INTEGER :: arg14
    REAL(kind=r8) :: result10
!
    iwall = xymap(ix, iy)
    tef = (te(ix, iy)*hhn+te(ixn, iyn)*hh)/(hhn+hh)
    tif = (ti(ix, iy)*hhn+ti(ixn, iyn)*hh)/(hhn+hh)
    IF (potential_at_guard_cell .EQ. 1) THEN
!        write(6,*) 'Potential taken at guard cell (bas)'
      pof = po(ix, iy)
    ELSE
!        write(6,*) 'Potential taken at real cell (bas)'
      pof = po(ixn, iyn)
    END IF
    zaf(0:ns-1) = (rza(ix, iy, 0:ns-1)*hhn+rza(ixn, iyn, 0:ns-1)*hh)/(&
&     hhn+hh)
    rpf(0:ns-1) = (rpt(ix, iy, 0:ns-1)*hhn+rpt(ixn, iyn, 0:ns-1)*hh)/(&
&     hhn+hh)
    y9 = isign*(fhi(ixf, iyf, idir, idir)+fhi_ext(ixf, iyf, idir, idir))
    IF (0.0_R8 .LT. y9) THEN
      max1 = y9
    ELSE
      max1 = 0.0_R8
    END IF
    qin = max1/area
    DO is=0,ns-1
      IF (0.0_R8 .LT. isign*fna(ixf, iyf, idir, idir, is)) THEN
        max2 = isign*fna(ixf, iyf, idir, idir, is)
      ELSE
        max2 = 0.0_R8
      END IF
      qin = qin + max2*((kinrgy(ix, iy, is)+tif)*(1.0_R8-boris)+rpf(is)*&
&       ev)/area
    END DO
    DO is=0,ns_ext-1
      ptf = (pt_ext(ix, iy, is)*hhn+pt_ext(ixn, iyn, is)*hh)/(hhn+hh)
      taf = (ta_ext(ix, iy, is)*hhn+ta_ext(ixn, iyn, is)*hh)/(hhn+hh)
      IF (0.0_R8 .LT. isign*fa_ext(ixf, iyf, idir, idir, is)) THEN
        max3 = isign*fa_ext(ixf, iyf, idir, idir, is)
      ELSE
        max3 = 0.0_R8
      END IF
      qin = qin + max3*((0.5_R8*am_ext(is)*mp*ua_ext(ix, iy, is)**2+taf)&
&       *(1.0_R8-boris)+ptf*ev)/area
    END DO
    y10 = isign*(fhe(ixf, iyf, idir, idir)+fne(ixf, iyf, idir, idir)*tef&
&     *(1.0_R8-boris))
    IF (0.0_R8 .LT. y10) THEN
      max4 = y10
    ELSE
      max4 = 0.0_R8
    END IF
    qel = max4/area
    IF (lcore) THEN
      surface_temp = 0.0_R8
    ELSE
      IF (iwall .EQ. 0) THEN
        WRITE(*, *) 'xymap returned zero for ix,iy = ', ix, iy
        CALL XERRAB('Invalid iwall setting!')
      END IF
      IF (plate_model .EQ. 0) THEN
        CALL HEATDIFF0D(iwall, qin, qel, surface_temp)
        target_temp(iwall, 1) = surface_temp
      ELSE IF (plate_model .EQ. 1) THEN
        IF (main_call) THEN
          wrk(1:ndepth_nml) = target_temp(iwall, 1:ndepth_nml)
          arg12 = qin + qel
          arg2 = dtim*plate_time_factor(iwall)
          CALL HEATDIFF1D_NODIFF(iwall, ns, arg12, arg2, wrk, output)
          target_temp(iwall, 1:ndepth_nml) = wrk(1:ndepth_nml)
        END IF
        surface_temp = target_temp(iwall, 1)
      ELSE IF (plate_model .EQ. 2) THEN
        surface_temp = target_temp(iwall, 1)
      END IF
    END IF
!
!    ..loop over species
    is0 = 0
    is1 = 0
    DO is=0,ns-1
      IF (am(is0) - am(is) .GE. 0.) THEN
        abs0 = am(is0) - am(is)
      ELSE
        abs0 = -(am(is0)-am(is))
      END IF
      IF (zn(is0) .NE. zn(is) .OR. amtol .LT. abs0) is0 = is
!dpc
      IF (is_neutral(is)) is1 = is
      IF (am(is1) - am(is) .GE. 0.) THEN
        abs1 = am(is1) - am(is)
      ELSE
        abs1 = -(am(is1)-am(is))
      END IF
      IF (zn(is1) .NE. zn(is) .OR. amtol .LT. abs1) WRITE(*, *) &
&                                        'b2stbr problem: is, is0, is1 '&
&                                                   , is, is0, is1
      IF (is0 .NE. is1) THEN
        IF (ncall .EQ. 0) WRITE(*, *) 'b2stbr: setting is0 to is1 ', is0&
&                         , is1
        is0 = is1
      END IF
      is2 = -1
      k = 0
      found = .false.
      DO WHILE (.NOT.found .AND. k .LT. nscx)
        IF (is0 .EQ. iscx(k)) THEN
          found = .true.
          is2 = k
        END IF
        k = k + 1
      END DO
      IF (0.0_R8 .LT. isign*fna(ixf, iyf, idir, idir, is)) THEN
        max5 = isign*fna(ixf, iyf, idir, idir, is)
      ELSE
        max5 = 0.0_R8
      END IF
!dpc
!       (species (is) recycles into is0)
!       (is0 corresponds to CX species is2)
! dpc: Eirene coupling 1999.06.28
      t0 = max5*neutral_sources_rescale
      IF (0.0_R8 .LT. tif/ev + (pof-phi_app)*zaf(is)) THEN
        einc = tif/ev + (pof-phi_app)*zaf(is)
      ELSE
        einc = 0.0_R8
      END IF
! xpb: Incident particle flux (part/m^2/s)
      pflx = t0/area
      IF (is_neutral(is)) THEN
        arg13 = 8.0_R8*ti(ix, iy)/(pi*am(is)*mp)
        vbar = SQRT(arg13)
! dpc: Eirene coupling 1999.06.28
        pflx = pflx + na(ix, iy, is)*vbar*alpha*neutral_sources_rescale
      END IF
      sna0(ix, iy, 0, is0) = sna0(ix, iy, 0, is0) + cbrec(0, is, ireg)*&
&       t0
      itrack = track_index(is0)
      IF (itrack .GT. 0 .AND. itrack .LE. ntrack .AND. iwall .GT. 0 &
&         .AND. main_call) deposition(iwall, itrack) = deposition(iwall&
&         , itrack) + dtim*plate_time_factor(iwall)*(1.0_R8-cbrec(0, is&
&         , ireg))*t0
      x1 = cbrec(1, is, ireg)*(1.5_R8+boris)*einc*ev
      IF (x1 .LT. (1.5_R8+boris)*surface_temp*kbolt) THEN
        max6 = (1.5_R8+boris)*surface_temp*kbolt
      ELSE
        max6 = x1
      END IF
      shi0(ix, iy, 0) = shi0(ix, iy, 0) + recycled_neutrals_contr*max6*&
&       cbrec(0, is, ireg)*t0
! xpb: recycled neutrals come back with at least thermal energy
      IF (is2 .NE. -1) THEN
        x2 = cbrec(1, is, ireg)*(1.5_R8+boris)*einc*ev
        IF (x2 .LT. (1.5_R8+boris)*surface_temp*kbolt) THEN
          max7 = (1.5_R8+boris)*surface_temp*kbolt
        ELSE
          max7 = x2
        END IF
        shi0_ff(ix, iy, is2) = shi0_ff(ix, iy, is2) + &
&         recycled_neutrals_contr*max7*cbrec(0, is, ireg)*t0
      END IF
      IF (.NOT.lcore) THEN
! xpb: no sputtering in core!
! chemical sputtering
        IF (sput_frc .GT. 0.0_R8 .AND. NINT(zn(is)) .EQ. sput_src .AND. &
&           sput_dst .GE. 0 .AND. sput_dst .LT. ns) THEN
          IF (sput_chem_model .EQ. 1) THEN
            IF (einc .GT. 0.0_R8) THEN
! dpc: cut off chem sputter at 1eV
              pwx1 = sput_chem_cutoff_alpha/einc
              pwr1 = pwx1**sput_chem_cutoff_beta
              t1 = t0*(sput_frc+cbrec(4, is, ireg))*EXP(-pwr1)
! dpc: cut off chem sputter at 1eV
              pwx1 = sput_chem_cutoff_alpha/einc
              pwr1 = pwx1**sput_chem_cutoff_beta
              chemical_sputtering(iwall, is) = (sput_frc+cbrec(4, is, &
&               ireg))*EXP(-pwr1)
            ELSE
              t1 = 0.0_R8
              chemical_sputtering(iwall, is) = 0.0_R8
            END IF
          ELSE
            arg12 = pflx/neutral_sources_rescale
            CALL CHMSPUT(iwall, einc, zaf(is), zn(is), zn(sput_dst), am(&
&                  is), am(sput_dst), surface_temp, arg12, yldchm)
            t1 = pflx*neutral_sources_rescale*yldchm*(sput_frc+cbrec(4, &
&             is, ireg))*area
            chemical_sputtering(iwall, is) = yldchm
          END IF
          t2 = t1*(1.5_R8+boris)*surface_temp*kbolt
          IF (ncall .NE. 0 .OR. sputter_namelist_used) t1 = (&
&             sputter_yield(ix, iy, is, 1)+sput_chem_alpha*t1)/(1.0_R8+&
&             sput_chem_alpha)
          sputter_yield(ix, iy, is, 1) = t1
          tchem(is) = tchem(is) + t1
          sna0(ix, iy, 0, sput_dst) = sna0(ix, iy, 0, sput_dst) + t1
          itrack = track_index(sput_dst)
          IF (itrack .GT. 0 .AND. itrack .LE. ntrack .AND. iwall .GT. 0 &
&             .AND. main_call) erosion(iwall, itrack) = erosion(iwall, &
&             itrack) + dtim*plate_time_factor(iwall)*t1
          IF (sputter_energy_on .NE. 0) THEN
            IF (ncall .NE. 0 .OR. sputter_namelist_used) t2 = (&
&               sputter_yield2(ix, iy, is, 1)+sput_chem_alpha*t2)/(&
&               1.0_R8+sput_chem_alpha)
            sputter_yield2(ix, iy, is, 1) = t2
            tchee(is) = tchee(is) + t2
            shi0(ix, iy, 0) = shi0(ix, iy, 0) + t2
            IF (is2 .NE. -1) shi0_ff(ix, iy, is2) = shi0_ff(ix, iy, is2)&
&               + t2
          ELSE
            sputter_yield2(ix, iy, is, 1) = 0.0_R8
          END IF
        ELSE
          sputter_yield(ix, iy, is, 1) = 0.0_R8
          sputter_yield2(ix, iy, is, 1) = 0.0_R8
          chemical_sputtering(iwall, is) = 0.0_R8
        END IF
! physical sputtering
        IF (sput_phys .GT. 0.0_R8 .AND. sput_dst .GE. 0 .AND. sput_dst &
&           .LT. ns) THEN
          IF (redep_max .LT. f_redep(ixn, iyn, sput_dst)) THEN
            redep_max = f_redep(ixn, iyn, sput_dst)
          ELSE
            redep_max = redep_max
          END IF
          arg14 = sput_dst + 1
          arg2 = (pof-phi_app)*zaf(sput_dst+1)*ev
          result10 = SPUTTER(arg14, zaf(sput_dst+1), sput_dst, arg2)
          redep_self_sputter = result10*(sput_phys+cbrec(3, sput_dst+1, &
&           ireg))
          IF (f_redep(ixn, iyn, sput_dst) .GT. 0.0_R8 .AND. &
&             redep_self_sputter .GT. 0.0_R8 .AND. ((output .GT. 2 .OR. &
&             (main_call .AND. output .GE. 2)) .OR. redep_self_sputter &
&             .GE. 1.0_R8)) WRITE(*, '(a,1x,a2,2i4,1p,2g12.5)') &
&                           'Self-sputtering coefficient ', is_codes(&
&                           sput_dst), ix, iy, pof - phi_app, &
&                           redep_self_sputter
          IF (yself_max .LT. redep_self_sputter) THEN
            yself_max = redep_self_sputter
          ELSE
            yself_max = yself_max
          END IF
          arg12 = einc*ev
          result10 = SPUTTER(is, zaf(is), sput_dst, arg12)
          t1 = t0*result10*(sput_phys+cbrec(3, is, ireg))*(1.0_R8-&
&           f_redep(ixn, iyn, sput_dst))/(1.0_R8-f_redep(ixn, iyn, &
&           sput_dst)*redep_self_sputter)
! the two lines above are the reduction of sputtering due to prompt redeposition
! and then the multiplication due to self-sputtering of the redeposited particles
          arg12 = einc*ev
          result10 = SPUTTER(is, zaf(is), sput_dst, arg12)
          physical_sputtering(iwall, is) = result10*(sput_phys+cbrec(3, &
&           is, ireg))*(1.0_R8-f_redep(ixn, iyn, sput_dst))/(1.0_R8-&
&           f_redep(ixn, iyn, sput_dst)*redep_self_sputter)
          IF (ncall .NE. 0 .OR. sputter_namelist_used) t1 = (&
&             sputter_yield(ix, iy, is, 2)+sput_phys_alpha*t1)/(1.0_R8+&
&             sput_phys_alpha)
          sputter_yield(ix, iy, is, 2) = t1
          tphys(is) = tphys(is) + t1
          sna0(ix, iy, 0, sput_dst) = sna0(ix, iy, 0, sput_dst) + t1
          itrack = track_index(sput_dst)
          IF (itrack .GT. 0 .AND. itrack .LE. ntrack .AND. iwall .GT. 0 &
&             .AND. main_call) THEN
!xpb  We count here as eroded all the sputtered material, including the promptly redeposited fraction
!xpb  We add this promptly redeposited fraction in the deposited material
            erosion(iwall, itrack) = erosion(iwall, itrack) + dtim*&
&             plate_time_factor(iwall)*t1/(1.0_R8-f_redep(ixn, iyn, &
&             sput_dst))*(1.0_R8-f_redep(ixn, iyn, sput_dst)*&
&             redep_self_sputter)
            deposition(iwall, itrack) = deposition(iwall, itrack) + dtim&
&             *plate_time_factor(iwall)*t1*(1.0_R8-1.0_R8/(1.0_R8-&
&             f_redep(ixn, iyn, sput_dst))*(1.0_R8-f_redep(ixn, iyn, &
&             sput_dst)*redep_self_sputter))
          END IF
          IF (sputter_energy_on .NE. 0) THEN
            arg14 = sput_dst + 1
            arg2 = (pof-phi_app)*zaf(sput_dst+1)*ev
            result10 = SPUTTER2(arg14, sput_dst, arg2)
            redep_self_energy = result10*(sput_phys+cbrec(3, sput_dst+1&
&             , ireg))
            arg12 = einc*ev
            result10 = SPUTTER2(is, sput_dst, arg12)
            t2 = t0*result10*(sput_phys+cbrec(3, is, ireg))*einc*ev*(&
&             1.0_R8-f_redep(ixn, iyn, sput_dst))
!xpb  We now must remove the energy lost in ionizing the promptly redeposited material
!xpb  but from the electrons!
            arg12 = einc*ev
            result10 = SPUTTER(is, zaf(is), sput_dst, arg12)
            t3 = -(t0*ev*result10*(sput_phys+cbrec(3, is, ireg))*rpi(ixn&
&             , iyn, sput_dst+1)*f_redep(ixn, iyn, sput_dst)/(1.0_R8-&
&             f_redep(ixn, iyn, sput_dst)*redep_self_sputter))
!xpb  We must now add the energy brought back in by the self-sputtered material
            arg12 = einc*ev
            result10 = SPUTTER(is, zaf(is), sput_dst, arg12)
            t2 = t2 + t0*result10*(sput_phys+cbrec(3, is, ireg))*f_redep&
&             (ixn, iyn, sput_dst)*redep_self_energy*(pof-phi_app)*ev/(&
&             1.0_R8-f_redep(ixn, iyn, sput_dst)*redep_self_sputter)
            arg12 = einc*ev
            result10 = SPUTTER2(is, sput_dst, arg12)
            physical_sputtering_energy(iwall, is) = result10*(sput_phys+&
&             cbrec(3, is, ireg))
            IF (ncall .NE. 0 .OR. sputter_namelist_used) t2 = (&
&               sputter_yield2(ix, iy, is, 2)+sput_phys_alpha*t2)/(1+&
&               sput_phys_alpha)
            sputter_yield2(ix, iy, is, 2) = t2
            tphye(is) = tphye(is) + t2 + t3
            shi0(ix, iy, 0) = shi0(ix, iy, 0) + t2
            she0(ix, iy, 0) = she0(ix, iy, 0) + t3
            IF (is2 .NE. -1) shi0_ff(ix, iy, is2) = shi0_ff(ix, iy, is2)&
&               + t2
          ELSE
            physical_sputtering_energy(iwall, is) = 0.0_R8
            sputter_yield2(ix, iy, is, 2) = 0.0_R8
          END IF
        ELSE
          physical_sputtering(iwall, is) = 0.0_R8
          physical_sputtering_energy(iwall, is) = 0.0_R8
          sputter_yield(ix, iy, is, 2) = 0.0_R8
          sputter_yield2(ix, iy, is, 2) = 0.0_R8
        END IF
! thermal evaporation of species is if bulk or tracked
        itrack = track_index(is)
        IF (therm_evap .GT. 0.0_R8 .AND. iwall .GT. 0 .AND. (is .EQ. &
&           sput_dst .OR. (is_neutral(is) .AND. (itrack .GT. 0 .AND. &
&           itrack .LE. ntrack)))) THEN
          CALL THERMEV(iwall, is, surface_temp, gammaevap, t2)
          thermal_evaporation(iwall, is) = gammaevap
          t1 = gammaevap*area*therm_evap
          t2 = t1*(1.5_R8+boris)*surface_temp*kbolt
          sna0(ix, iy, 0, is) = sna0(ix, iy, 0, is) + t1
          IF (itrack .GT. 0 .AND. itrack .LE. ntrack .AND. iwall .GT. 0 &
&             .AND. main_call) erosion(iwall, itrack) = erosion(iwall, &
&             itrack) + dtim*plate_time_factor(iwall)*t1
          shi0(ix, iy, 0) = shi0(ix, iy, 0) + t2
          IF (is2 .NE. -1) shi0_ff(ix, iy, is2) = shi0_ff(ix, iy, is2) +&
&             t2
          thevp(is) = thevp(is) + t1
          thvpe(is) = thvpe(is) + t2
        ELSE
          thermal_evaporation(iwall, is) = 0.0_R8
        END IF
! radiation enhanced sublimation  ! the plate is assumed to be carbon !!!
        IF (sput_res .GT. 0.0_R8 .AND. sput_dst .GE. 0 .AND. sput_dst &
&           .LT. ns) THEN
          arg12 = pflx/neutral_sources_rescale
          CALL RESSPUT(einc, zaf(is), zn(is), zn(sput_dst), am(is), am(&
&                sput_dst), surface_temp, arg12, sput_dst, yldres)
          res_sputtering(iwall, is) = yldres
          t1 = yldres*neutral_sources_rescale*area*sput_res
          t2 = t1*(1.5_R8+boris)*surface_temp*kbolt
          sna0(ix, iy, 0, sput_dst) = sna0(ix, iy, 0, sput_dst) + t1
          itrack = track_index(sput_dst)
          IF (itrack .GT. 0 .AND. itrack .LE. ntrack .AND. iwall .GT. 0 &
&             .AND. main_call) erosion(iwall, itrack) = erosion(iwall, &
&             itrack) + dtim*plate_time_factor(iwall)*t1
          shi0(ix, iy, 0) = shi0(ix, iy, 0) + t2
          IF (is2 .NE. -1) shi0_ff(ix, iy, is2) = shi0_ff(ix, iy, is2) +&
&             t2
          tresn(is) = tresn(is) + t1
          trese(is) = trese(is) + t2
        ELSE
          res_sputtering(iwall, is) = 0.0_R8
        END IF
! back scattering
        IF (cbrec(0, is, ireg) .EQ. 0.0_R8 .AND. cbrec(1, is, ireg) .EQ.&
&           0.0_R8 .AND. (.NOT.is_neutral(is)) .AND. reflection_on .NE. &
&           0) THEN
! use reflection model
          arg12 = einc*ev
          result10 = REFLECT(is, arg12, iwall)
          t1 = t0*result10
          arg12 = einc*ev
          result10 = REFLECT2(is, arg12, iwall)
          t2 = t0*result10*einc*ev
          itrack = track_index(is0)
          IF (itrack .GT. 0 .AND. itrack .LE. ntrack .AND. iwall .GT. 0 &
&             .AND. main_call) deposition(iwall, itrack) = deposition(&
&             iwall, itrack) - dtim*plate_time_factor(iwall)*t1
          arg12 = einc*ev
          backscattering(iwall, is) = REFLECT(is, arg12, iwall)
          arg12 = einc*ev
          backscattering_energy(iwall, is) = REFLECT2(is, arg12, iwall)
          IF (reflection_on .EQ. 1) THEN
            sna0(ix, iy, 0, is) = sna0(ix, iy, 0, is) + t1
          ELSE
! reflection to the neutral
            sna0(ix, iy, 0, is0) = sna0(ix, iy, 0, is0) + t1
          END IF
          shi0(ix, iy, 0) = shi0(ix, iy, 0) + t2
          IF (is2 .NE. -1) shi0_ff(ix, iy, is2) = shi0_ff(ix, iy, is2) +&
&             t2
          trfln(is) = trfln(is) + t1
          trfle(is) = trfle(is) + t2
        ELSE
          backscattering(iwall, is) = 0.0_R8
          backscattering_energy(iwall, is) = 0.0_R8
        END IF
      END IF
!    ..recycling into next ionized species
      IF (is .LT. ns - 1) THEN
        IF (LNEXT(is, is + 1)) sna0(ix, iy, 0, is+1) = sna0(ix, iy, 0, &
&           is+1) + cbrec(6, is, ireg)*t0/neutral_sources_rescale
      END IF
    END DO
!    ..recycling at core into highest ionized stage
    IF (lcore) THEN
!    ..loop over species
      is0 = ns - 1
      is1 = ns - 1
      DO is=ns-1,0,-1
        IF (am(is0) - am(is) .GE. 0.) THEN
          abs2 = am(is0) - am(is)
        ELSE
          abs2 = -(am(is0)-am(is))
        END IF
        IF (zn(is0) .NE. zn(is) .OR. amtol .LT. abs2) is0 = is
!xpb
        IF (zamax(is) .EQ. zn(is)) is1 = is
        IF (am(is1) - am(is) .GE. 0.) THEN
          abs3 = am(is1) - am(is)
        ELSE
          abs3 = -(am(is1)-am(is))
        END IF
        IF (zn(is1) .NE. zn(is) .OR. amtol .LT. abs3) WRITE(*, *) &
&                                        'b2stbr problem: is, is0, is1 '&
&                                                     , is, is0, is1
        IF (is0 .NE. is1) THEN
          IF (ncall .EQ. 0) WRITE(*, *) 'b2stbr: setting is0 to is1 ', &
&                           is0, is1
          is0 = is1
        END IF
        IF (0.0_R8 .LT. isign*fna(ixf, iyf, idir, idir, is)) THEN
          max8 = isign*fna(ixf, iyf, idir, idir, is)
        ELSE
          max8 = 0.0_R8
        END IF
!xpb
!          (species (is) recycles into is0)
! xpb: 2002.02.01
        t0 = max8*core_sources_rescale
        IF (is .NE. is0) THEN
          sna0(ix, iy, 0, is0) = sna0(ix, iy, 0, is0) + cbrec(0, is, &
&           ireg)*t0
          shi0(ix, iy, 0) = shi0(ix, iy, 0) + recycled_neutrals_contr*&
&           cbrec(1, is, ireg)*(1.5_R8+boris)*(zaf(is)*tef+tif)*t0
        END IF
      END DO
    END IF
  END SUBROUTINE RECYCLE

