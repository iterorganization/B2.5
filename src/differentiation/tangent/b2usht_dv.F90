SUBROUTINE B2USHT_NODIFF(ncv, nfc, nvx, ns, switch, geo, mpg, itcnt, ne&
& , ni, nn, te, ti, tn, kt, zt, floe, cone, floi, coni, flon, conn, &
& flokt, conkt, flozt, conzt, she, shi, shn, skt, szt, ceqp, resht, &
& reshe, reshi, reshn, reskt, reszt, cortt, corte, corti, cortn, corkt, &
& corzt, flo0, con0, aa)
  USE B2MOD_TYPES
  USE B2MOD_NUMERICS_NAMELIST_DIFFV
  USE B2MOD_SWITCHES_DIFFV
  USE B2MOD_CONSTANTS
  USE B2US_GEO_DIFFV
  USE B2US_MAP_DIFFV
  USE B2MOD_MA28_FOR_US, ONLY : restart_ma28_for_us
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_AD_DIFFV, ONLY : ncall_b2usht
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx, ns, itcnt
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  REAL(kind=r8) :: ne(ncv), ni(ncv, 0:1), nn(ncv), te(ncv), ti(ncv), tn(&
& ncv), kt(ncv), zt(ncv), floe(nfc, 0:1), floi(nfc, 0:1), flon(nfc, 0:1)&
& , flokt(nfc, 0:1), flozt(nfc, 0:1), cone(nfc, 0:1, 0:2), coni(nfc, 0:1&
& , 0:2), conn(nfc, 0:1), conkt(nfc, 0:1), conzt(nfc, 0:1)
  REAL(kind=r8) :: she(ncv, 0:3), shi(ncv, 0:3), shn(ncv, 0:3), skt(ncv&
& , 0:3), szt(ncv, 0:3), ceqp(ncv), resht(ncv), reshe(ncv), reshi(ncv), &
& reshn(ncv), reskt(ncv), reszt(ncv)
!   ..output arguments (unspecified on entry)
  REAL(kind=r8) :: cortt(ncv), corte(ncv), corti(ncv), cortn(ncv), corkt&
& (ncv), corzt(ncv)
!   ..workspace arguments (unspecified on entry and on exit)
  REAL(kind=r8) :: flo0(nfc, 0:1), con0(nfc, 0:1, 0:2), aa(mpg%ncmxnv)
  REAL(kind=r8) :: dumm(1)
!
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2USHT computes a correction to the fields te and ti.
!
!-----------------------------------------------------------------------
!                 2_____3_____4                     5___________6
!                 |     |     |                     |     |     |
!                 |     |     |                     |     |     |
!                -1_____0_____1                     |_____|_____|
!                 |     |     |                     |     |     |
!                 |     |     |                     |     |     |
!                -4____-3____-2                    -6_____|____-5
!
!                (ix-1,iy+1)______(ix,iy+1)____(ix+1,iy+1)
!                     |              |              |
!                     |              |              |
!                     |              |              |
!                (ix-1,iy  )______(ix,iy)______(ix+1,iy  )
!                     |              |              |
!                     |              |              |
!                     |              |              |
!                (ix-1,iy-1)______(ix,iy-1)____(ix+1,iy-1)
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: icv, ifc, icv1, icv2, ivx1, ivx2, incv
!      integer, save :: no_Ptncr_x_he = 0, no_Ptncr_y_he = 0            !srv 06.07.06
!      integer, save :: no_Ptncr_x_hi = 0, no_Ptncr_y_hi = 0            !srv 06.07.06
!      integer, save :: iadd_9 = 1                                      !srv 02.01.07
  INTEGER :: ireg
  LOGICAL :: lnonzero
  REAL(kind=r8) :: t0, t1, t2, t3, t11, t22
  LOGICAL :: new_matrix
!   ..procedures
  INTRINSIC ABS, SQRT, DIM
  EXTERNAL XERTST, SFILL_NODIFF
!srv 02.01.07
  EXTERNAL B2XVSG, B2UXUS
  INTRINSIC ANY
  INTRINSIC MIN
  INTRINSIC REAL
  REAL(kind=r8) :: abs0
  REAL(kind=r8) :: abs1
  REAL(kind=r8) :: abs2
  REAL(kind=r8) :: abs3
  REAL(kind=r8) :: abs4
  REAL(kind=r8) :: abs5
  REAL(r8) :: arg1
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
!   ..initialization
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!csc needed for correct initialization in adjoint AD
  icv1 = 1
  icv2 = 1
!   ..subprogram start-up calls
  CALL SUBINI('b2usht')
!   ..test nCv, nFc, nVx, ns
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc .AND. 0 .LT. nvx, &
&       'faulty argument nCv, nFc, nVx')
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!   ..test itcnt, rxg
  CALL XERTST(0 .LE. itcnt, 'faulty argument itcnt')
  CALL XERTST(0.0_R8 .LT. switch%b2npht_rxg, 'faulty argument rxg')
!   ..extensive tests on first few calls
  IF (ncall_b2usht .LT. 3) THEN
!    ..test sign of ne, ni, te, ti
    CALL B2XVSG(ncv, ne, 1, 'ne', '.gt.')
    CALL B2XVSG(ncv, ni, 1, 'ni', '.gt.')
    CALL B2XVSG(ncv, te, 1, 'te', '.gt.')
    CALL B2XVSG(ncv, ti, 1, 'ti', '.gt.')
    CALL B2XVSG(ncv, kt, 1, 'kt', '.ge.')
    CALL B2XVSG(ncv, zt, 1, 'zt', '.ge.')
!    ..test sign of she, shi, ceqp
    IF (switch%b2mndt_style .NE. 2) CALL B2XVSG(ncv, she(1, 0), 1, &
&                                         'she0', '.ge.')
    CALL B2XVSG(ncv, she(1, 1), 1, 'she1', '.le.')
    CALL B2XVSG(ncv, she(1, 2), 1, 'she2', '.ge.')
    CALL B2XVSG(ncv, she(1, 3), 1, 'she3', '.le.')
    IF (switch%b2mndt_style .NE. 2) CALL B2XVSG(ncv, shi(1, 0), 1, &
&                                         'shi0', '.ge.')
    CALL B2XVSG(ncv, shi(1, 1), 1, 'shi1', '.le.')
    CALL B2XVSG(ncv, shi(1, 2), 1, 'shi2', '.ge.')
    CALL B2XVSG(ncv, shi(1, 3), 1, 'shi3', '.le.')
    IF (switch%b2mndt_style .NE. 2) CALL B2XVSG(ncv, shn(1, 0), 1, &
&                                         'shn0', '.ge.')
    CALL B2XVSG(ncv, shn(1, 1), 1, 'shn1', '.le.')
    CALL B2XVSG(ncv, shn(1, 2), 1, 'shn2', '.ge.')
    CALL B2XVSG(ncv, shn(1, 3), 1, 'shn3', '.le.')
    CALL B2XVSG(ncv, ceqp, 1, 'ceqp', '.ge.')
  END IF
!
  IF (switch%tn_style .LE. 1) THEN
!! in case we solve a combined ion-neutral energy equation (default) or pure ion energy equation we make sure
!! the neutral fluxes and sources are zero.
    conn = 0.0_R8
    flon = 0.0_R8
    shn = 0.0_R8
  END IF
!srv 22.05.18
!
  IF (ANY(solveee(0:mpg%nnreg(0))) .AND. ANY(solveei(0:mpg%nnreg(0))) &
&     .AND. ANY(solveen(0:mpg%nnreg(0))) .AND. ANY(solveet(0:mpg%nnreg(0&
&     )))) THEN
!srv 22.05.18
    new_matrix = .false.
    DO ireg=0,mpg%nnreg(0)
      new_matrix = (new_matrix .OR. solveet(ireg)) .NEQV. last_solve_9(&
&       ireg)
    END DO
    IF (new_matrix .OR. .true.) CALL RESTART_MA28_FOR_US()
! ..total heat correction equation
!   ..compute flo0, con0
    flo0 = floe + floi + flon
    con0(:, :, 0) = cone(:, :, 0) + coni(:, :, 0) + conn
    IF (switch%b2usht_iout .NE. 0) THEN
      CALL MY_OUT_US(70, nfc, 1, flo0(1, 0), 'b2usht_flo0x')
      CALL MY_OUT_US(70, nfc, 1, flo0(1, 1), 'b2usht_flo0y')
      CALL MY_OUT_US(70, nfc, 1, con0(1, 0, 0), &
&                     'b2usht_con0xtest')
      CALL MY_OUT_US(70, nfc, 1, con0(1, 1, 0), &
&                     'b2usht_con0ytest')
      CALL MY_OUT_US(70, nfc, 1, con0(1, 0, 1), 'b2usht_con0x10')
      CALL MY_OUT_US(70, nfc, 1, con0(1, 1, 1), 'b2usht_con0y10')
      CALL MY_OUT_US(70, nfc, 1, con0(1, 0, 2), 'b2usht_con0x20')
      CALL MY_OUT_US(70, nfc, 1, con0(1, 1, 2), 'b2usht_con0y20')
    END IF
!
!   ..initialize matrix to zero
    aa = 0.0_R8
!$AD II-LOOP
!   ..compute the nine-point matrix
    DO icv=1,ncv
      ireg = mpg%cvreg(icv)
      t0 = 1.0e60_R8
      IF (solveee(ireg)) THEN
        aa(mpg%cvnvp(icv, 1)) = aa(mpg%cvnvp(icv, 1)) - she(icv, 1) - &
&         she(icv, 3)*ne(icv)
        IF (t0 .GT. te(icv)) THEN
          t0 = te(icv)
        ELSE
          t0 = t0
        END IF
      END IF
      IF (solveei(ireg)) THEN
        aa(mpg%cvnvp(icv, 1)) = aa(mpg%cvnvp(icv, 1)) - shi(icv, 1) - &
&         shi(icv, 3)*ni(icv, 0)
        IF (t0 .GT. ti(icv)) THEN
          t0 = ti(icv)
        ELSE
          t0 = t0
        END IF
      END IF
      IF (solveen(ireg) .AND. switch%tn_style .EQ. 2) THEN
        aa(mpg%cvnvp(icv, 1)) = aa(mpg%cvnvp(icv, 1)) - shn(icv, 1) - &
&         shn(icv, 3)*nn(icv)
        IF (t0 .GT. tn(icv)) THEN
          t0 = tn(icv)
        ELSE
          t0 = t0
        END IF
      END IF
      IF ((solveee(ireg) .OR. solveei(ireg)) .OR. (solveen(ireg) .AND. &
&         switch%tn_style .EQ. 2)) THEN
        IF (switch%b2mndt_style .LT. 2) THEN
          IF (resht(icv) .GE. 0.) THEN
            abs0 = resht(icv)
          ELSE
            abs0 = -resht(icv)
          END IF
          aa(mpg%cvnvp(icv, 1)) = aa(mpg%cvnvp(icv, 1)) + abs0/(switch%&
&           b2npht_rxg*t0)
        END IF
      END IF
    END DO
!
    IF (switch%b2usht_iout .NE. 0) THEN
      CALL MY_OUT_US(70, ncv, 0, she(1, 1), 'b2usht_she1')
      CALL MY_OUT_US(70, ncv, 0, she(1, 3), 'b2usht_she3')
      CALL MY_OUT_US(70, ncv, 0, shi(1, 1), 'b2usht_shi1')
      CALL MY_OUT_US(70, ncv, 0, shi(1, 3), 'b2usht_shi3')
      CALL MY_OUT_US(70, ncv, 0, shn(1, 1), 'b2usht_shn1')
      CALL MY_OUT_US(70, ncv, 0, shn(1, 3), 'b2usht_shn3')
      CALL MY_OUT_US(70, ncv, 0, ne, 'b2usht_ne')
      CALL MY_OUT_US(70, ncv, 0, ni, 'b2usht_ni')
      CALL MY_OUT_US(70, ncv, 0, te, 'b2usht_te')
      CALL MY_OUT_US(70, ncv, 0, ti, 'b2usht_ti')
