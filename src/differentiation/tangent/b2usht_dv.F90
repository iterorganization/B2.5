      t1 = -(0.5_R8*flo0(ifc, 0)) - con0(ifc, 0, 0)
      t2 = 0.5_R8*flo0(ifc, 0) - con0(ifc, 0, 0)
      IF (solvezt(mpg%cvreg(icv1))) THEN
        DO nd=1,nbdirs
          aad(nd, mpg%cvnvp(icv1, 1)) = aad(nd, mpg%cvnvp(icv1, 1)) - &
&           t1d(nd)
        END DO
        aa(mpg%cvnvp(icv1, 1)) = aa(mpg%cvnvp(icv1, 1)) - t1
        DO nd=1,nbdirs
          WHERE (mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%&
&             cvnvp(icv1, 2)-1) .EQ. icv2) aad(nd, mpg%cvnvp(icv1, 1):&
&           mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aad(nd, mpg%cvnvp&
&             (icv1, 1):mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t2d(&
&             nd)
        END DO
        WHERE (mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%cvnvp(&
&           icv1, 2)-1) .EQ. icv2) aa(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1&
&         , 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1):mpg%cvnvp(&
&           icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t2
      END IF
      IF (solvezt(mpg%cvreg(icv2))) THEN
        DO nd=1,nbdirs
          aad(nd, mpg%cvnvp(icv2, 1)) = aad(nd, mpg%cvnvp(icv2, 1)) - &
&           t2d(nd)
        END DO
        aa(mpg%cvnvp(icv2, 1)) = aa(mpg%cvnvp(icv2, 1)) - t2
        DO nd=1,nbdirs
          WHERE (mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%&
&             cvnvp(icv2, 2)-1) .EQ. icv1) aad(nd, mpg%cvnvp(icv2, 1):&
&           mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aad(nd, mpg%cvnvp&
&             (icv2, 1):mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) + t1d(&
&             nd)
        END DO
        WHERE (mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%cvnvp(&
&           icv2, 2)-1) .EQ. icv1) aa(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2&
&         , 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1):mpg%cvnvp(&
&           icv2, 1)+mpg%cvnvp(icv2, 2)-1) + t1
      END IF
!     ..contributions from flo0(:,1), con0(:,1,:) ('9-point' contributions)
! number of v1
      ivx1 = mpg%fcvx(ifc, 1)
! number of v2
      ivx2 = mpg%fcvx(ifc, 2)
      DO nd=1,nbdirs
        t1d(nd) = -(0.5_R8*flo0d(nd, ifc, 1)) - con0d(nd, ifc, 1, 0)
        t2d(nd) = 0.5_R8*flo0d(nd, ifc, 1) - con0d(nd, ifc, 1, 0)
      END DO
      t1 = -(0.5_R8*flo0(ifc, 1)) - con0(ifc, 1, 0)
      t2 = 0.5_R8*flo0(ifc, 1) - con0(ifc, 1, 0)
      t11 = 0.0_R8
      DO incv=1,mpg%vxcvp(ivx1, 2)
        t11 = t11 + 1.0_R8/geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)
      END DO
      t22 = 0.0_R8
      DO incv=1,mpg%vxcvp(ivx2, 2)
        t22 = t22 + 1.0_R8/geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)
! contribution to equation of iCv1
      END DO
! contribution to equation of iCv2
      IF (solvezt(mpg%cvreg(icv1))) THEN
! contribution from iVx1
        DO incv=1,mpg%vxcvp(ivx1, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
          t3 = 1.0_R8/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
          DO nd=1,nbdirs
            WHERE (mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%&
&               cvnvp(icv1, 2)-1) .EQ. icv) aad(nd, mpg%cvnvp(icv1, 1):&
&             mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aad(nd, mpg%&
&               cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) &
&               - t3*t1d(nd)
          END DO
          WHERE (mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%&
&             cvnvp(icv1, 2)-1) .EQ. icv) aa(mpg%cvnvp(icv1, 1):mpg%&
&           cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1)&
&             :mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) - t3*t1
! contribution from iVx2
        END DO
        DO incv=1,mpg%vxcvp(ivx2, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
          t3 = 1.0_R8/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
          DO nd=1,nbdirs
            WHERE (mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%&
&               cvnvp(icv1, 2)-1) .EQ. icv) aad(nd, mpg%cvnvp(icv1, 1):&
&             mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aad(nd, mpg%&
&               cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) &
&               + t3*t2d(nd)
          END DO
          WHERE (mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%&
&             cvnvp(icv1, 2)-1) .EQ. icv) aa(mpg%cvnvp(icv1, 1):mpg%&
&           cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1)&
&             :mpg%cvnvp(icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t3*t2
        END DO
      END IF
      IF (solvezt(mpg%cvreg(icv2))) THEN
! contribution from iVx1
        DO incv=1,mpg%vxcvp(ivx1, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
          t3 = 1.0_R8/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
          DO nd=1,nbdirs
            WHERE (mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%&
&               cvnvp(icv2, 2)-1) .EQ. icv) aad(nd, mpg%cvnvp(icv2, 1):&
&             mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aad(nd, mpg%&
&               cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) &
&               + t3*t1d(nd)
          END DO
          WHERE (mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%&
&             cvnvp(icv2, 2)-1) .EQ. icv) aa(mpg%cvnvp(icv2, 1):mpg%&
&           cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1)&
&             :mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) + t3*t1
! contribution from iVx2
        END DO
        DO incv=1,mpg%vxcvp(ivx2, 2)
          icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
          t3 = 1.0_R8/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
          DO nd=1,nbdirs
            WHERE (mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%&
&               cvnvp(icv2, 2)-1) .EQ. icv) aad(nd, mpg%cvnvp(icv2, 1):&
&             mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aad(nd, mpg%&
&               cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) &
&               - t3*t2d(nd)
          END DO
          WHERE (mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%&
&             cvnvp(icv2, 2)-1) .EQ. icv) aa(mpg%cvnvp(icv2, 1):mpg%&
&           cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1)&
&             :mpg%cvnvp(icv2, 1)+mpg%cvnvp(icv2, 2)-1) - t3*t2
        END DO
      END IF
    END DO
!   ..check for empty matrix lines and replace with dummy equation
    DO icv=1,ncv
      lnonzero = .false.
      DO incv=1,mpg%cvnvp(icv, 2)
        lnonzero = lnonzero .OR. aa(mpg%cvnvp(icv, 1)+incv-1) .NE. &
&         0.0_R8
      END DO
      IF (.NOT.lnonzero) THEN
        DO nd=1,nbdirs
          aad(nd, mpg%cvnvp(icv, 1)) = 0.D0
          resztd(nd, icv) = 0.D0
        END DO
        aa(mpg%cvnvp(icv, 1)) = 1.0_R8
        reszt(icv) = 0.0_R8
      END IF
    END DO
!
!   ..solve the correction equation
    corztd = 0.D0
    CALL B2UXUS_DV(ncv, mpg, aa, aad, itcnt, reszt, resztd, corzt, &
&            corztd, 'b2usht_zt', switch%b2uxus_style, nbdirs)
  ELSE
    corztd = 0.D0
  END IF
!
  IF (switch%b2usht_iout .NE. 0) THEN
    CALL MY_OUT_US(70, mpg%ncmxnv, 0, aa, 'b2usht_zt_aa')
    CALL MY_OUT_US(70, ncv, 0, reszt, 'b2usht_reszt')
    CALL MY_OUT_US(70, ncv, 0, corzt, 'b2usht_corzt')
  END IF
!
!
!
!   ..return
  ncall_b2usht = ncall_b2usht + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2usht
!
END SUBROUTINE B2USHT_DV

