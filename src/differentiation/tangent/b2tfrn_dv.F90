SUBROUTINE B2TFRN_NODIFF(ncv, nfc, nvx, ns, switch, geo, mpg, pl, dv, co&
& , rt, st_ext)
  USE B2MOD_TYPES
!      use b2mod_boundary_namelist
  USE B2MOD_CONSTANTS
  USE B2MOD_B2CMPA_DIFFV
  USE B2MOD_B2CMFS
!srv 13.01.17
  USE B2MOD_TRANSPORT_FUN_DIFFV
  USE B2MOD_SWITCHES_DIFFV
  USE B2US_GEO_DIFFV
  USE B2US_MAP_DIFFV
  USE B2US_PLASMA_DIFFV
! csc The following are not necessary for computation but are needed
!     for adjoint AD to avoid side-effect variables
  USE B2MOD_AD_DIFFV, ONLY : ncall_b2tfrn
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!
!-----------------------------------------------------------------------
!.end b2tfrn
!
!   ..input arguments (unchanged on exit)
  INTEGER :: ncv, nfc, nvx, ns
  TYPE(SWITCHES), INTENT(IN) :: switch
  TYPE(GEOMETRY), INTENT(IN) :: geo
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(B2PLASMA), INTENT(IN) :: pl
  TYPE(B2RATES), INTENT(IN) :: rt
  TYPE(B2COEFF), INTENT(IN) :: co
  TYPE(B2STATEEXT), INTENT(IN) :: st_ext
!   ..input/output arguments
  TYPE(B2DERIVATIVES), INTENT(INOUT) :: dv
!   ..common blocks
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2TFRN computes the particle and energy fluxes between
!     neighbouring cells which are to be provided to Eirene.
!
!  3. description (see also routine b2cdca)
!
!     This routine computes the particle fluxes
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  INTEGER :: icv, ifc, is, isb, meth
!srv 16.09.02
  CHARACTER :: chns*3
!srv 14.07.10
  REAL(kind=r8) :: flo_eir(nfc, 0:1), scur(nfc, 0:1), fle_eir(nfc, 0:1)&
& , floe_eir(nfc, 0:1), floi_eir(nfc, 0:1)
  REAL(kind=r8) :: nbf(nfc), pbf(nfc), dpb(nfc, 0:1), dpbc(ncv), dnete(&
& nfc, 0:1), weight(nfc, 2)
!srv 13.01.17
  REAL(kind=r8) :: wrk0(nfc), wrk1(nfc), wrk2(nfc), wrkvx(nvx), wrk00(&
& nfc, 2), wrk11(nfc, 2), wrkcv(ncv), zeff(ncv), c071(ncv), c071f(nfc)
! The following switches are only used in 'WG-TODO' blocks, i.e. not yet converted to wide grid functionality
!      integer, save :: no_Ptncr_x_he = 0, no_Ptncr_y_he = 0             !srv 06.07.06
!      integer, save :: no_Ptncr_x_hi = 0, no_Ptncr_y_hi = 0             !srv 06.07.06
!      integer, save :: no_Ptncr_x_co = 0, no_Ptncr_y_co = 0             !srv 06.07.06
!   ..procedures
  EXTERNAL XERTST, IPGETI, IPGETR
  EXTERNAL B2XVSG, INTFACE
  INTRINSIC NINT
  INTRINSIC MAXVAL
  REAL(r8) :: result1
  REAL(r8) :: result2
  REAL(kind=r8) :: result10
  CHARACTER(len=17) :: arg1
  CHARACTER(len=16) :: arg10
  CHARACTER(len=15) :: arg11
  CHARACTER(len=14) :: arg12
  CHARACTER(len=13) :: arg13
  CHARACTER(len=9) :: arg14
!   ..initialisation
  scur(:, :) = 0.0_R8
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2tfrn')
!   ..test nCv, nFc
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
!   ..set internal parameters on first call
!      if (ncall_b2tfrn.eq.0) then
! The following switches are only used in 'WG-TODO' blocks, i.e. not yet converted to wide grid functionality
!        call ipgeti ('b2tfnb_no_Ptncr_x', no_Ptncr_x_co)                 !srv 06.07.06
!        call ipgeti ('b2tfnb_no_Ptncr_y', no_Ptncr_y_co)                 !srv 06.07.06
!        call ipgeti ('b2tfhi_no_Ptncr_x', no_Ptncr_x_hi)                 !srv 06.07.06
!        call ipgeti ('b2tfhi_no_Ptncr_y', no_Ptncr_y_hi)                 !srv 06.07.06
!        call ipgeti ('b2tfhe_no_Ptncr_x', no_Ptncr_x_he)                 !srv 06.07.06
!        call ipgeti ('b2tfhe_no_Ptncr_y', no_Ptncr_y_he)                 !srv 06.07.06
!      endif
!   ..extensive tests on first few calls
  IF (ncall_b2tfrn .LT. 3) THEN
!    ..test sign of ne
    CALL B2XVSG(ncv, dv%ne, 1, 'ne', '.gt.')
  END IF
!
  meth = switch%b2tfnb_discr_meth
!
  DO is=0,ns-1
!
!    ..compute partial pressure
    CALL B2XPPB_NODIFF(ncv, rt%rza(:, is), pl%na(:, is), pl%te, pl%ti, &
&                pl%tn, is, dv%pa(:, is))
!
!   ..interpolate na and pa to cell faces
    weight = 1.0_R8
    CALL INTFACE(ncv, nfc, mpg%fccv, weight, pl%na(:, is), nbf)
    CALL INTFACE(ncv, nfc, mpg%fccv, weight, dv%pa(:, is), pbf)
!
!   ..compute differences of pb
    CALL DIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, dv%pa(:, is), wrkvx, &
&              dpb)
    CALL GRADC_P_NODIFF(ncv, nfc, nvx, 1, geo, mpg, dv%pa(:, is), wrkvx&
&                 , dpbc)
!
!   ..compute wrk0
    CALL INTFACE(ncv, nfc, mpg%fccv, geo%fcvol, pl%ua(:, is), wrk0)
!
!   ..compute convective coefficients (poloidal and radial)
    flo_eir(:, 0) = geo%fcpbs*wrk0 + co%cvla(:, 0, is) + switch%&
&     fnb_drift_hyb*(switch%xvecrb*dv%vaecrb(:, 0, is)+switch%xwdia*dv%&
&     wadia(:, 0, is))*geo%fcs*geo%fcqalf(:, 0)
    flo_eir(:, 1) = co%cvla(:, 1, is) + switch%fnb_drift_hyb*(switch%&
&     xvecrb*dv%vaecrb(:, 1, is)+switch%xwdia*dv%wadia(:, 1, is))*geo%&
&     fcs*geo%fcqalf(:, 1)
!
!   ..add contribution from currents
!wdk   condition made consistent with b2tfnb => check zn(is) instead of rza(ix,iy,is)
!wdk   still to be checked: vis_per and vis_q also in b2tfnb?
    IF (NINT(zn(is)) .EQ. 1 .AND. NINT(zamax(is)) .EQ. 1) THEN
      scur(:, 0) = (dv%fchinert(:, 0)+dv%fchvispar(:, 0)+switch%&
&       fnb_vis_per*dv%fchvisper(:, 0)+dv%fchanml(:, 0)+switch%fnb_vis_q&
&       *dv%fchvisq(:, 0))*switch%b2tfnb_xcur/(qe*nbf) + dv%fchviskt(:, &
&       0)/(qe*nbf)
      scur(:, 1) = (dv%fchinert(:, 1)+dv%fchvispar(:, 1)+switch%&
&       fnb_vis_per*dv%fchvisper(:, 1)+dv%fchanml(:, 1)+switch%fnb_vis_q&
&       *dv%fchvisq(:, 1))*switch%b2tfnb_ycur/(qe*nbf) + dv%fchviskt(:, &
&       1)/(qe*nbf)
      flo_eir = flo_eir + switch%fnb_drift_hyb*scur
    END IF
!
!   ..apply discretization scheme for density-part of fna_eir
    CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%na(:, is), &
&                  flo_eir, co%cdna(:, :, is), dv%fna_eir(:, :, is), &
&                  wrk00, wrk11)
!
!   ..add non-hybrid drift contribution
    dv%fna_eir(:, 0, is) = dv%fna_eir(:, 0, is) + (1.0_R8-switch%&
&     fnb_drift_hyb)*((switch%xvecrb*dv%vaecrb(:, 0, is)+switch%xwdia*dv&
&     %wadia(:, 0, is))*geo%fcs*geo%fcqalf(:, 0)+scur(:, 0))*nbf
    dv%fna_eir(:, 1, is) = dv%fna_eir(:, 1, is) + (1.0_R8-switch%&
&     fnb_drift_hyb)*((switch%xvecrb*dv%vaecrb(:, 1, is)+switch%xwdia*dv&
&     %wadia(:, 1, is))*geo%fcs*geo%fcqalf(:, 1)+scur(:, 1))*nbf
!
!   ..add contribution from pressure diffusion
    dv%fna_eir(:, :, is) = dv%fna_eir(:, :, is) - co%cdpa(:, :, is)*dpb
!
!   ..add Rhie-Chow term
    DO ifc=1,nfc
      wrk1(ifc) = switch%b2npco_pcm1*0.5_R8*geo%fcpbs(ifc)*(dv%pcca(mpg%&
&       fccv(ifc, 1), 1, is)*pl%na(mpg%fccv(ifc, 1), is)+dv%pcca(mpg%&
&       fccv(ifc, 2), 1, is)*pl%na(mpg%fccv(ifc, 2), is))/(geo%fcqgam(&
&       ifc, 0)*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2)))
      wrk2(ifc) = 0.5_R8*geo%fcqgam(ifc, 0)*(dpbc(mpg%fccv(ifc, 1))+dpbc&
&       (mpg%fccv(ifc, 2)))*(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
    END DO
    dv%fna_eir(:, 0, is) = dv%fna_eir(:, 0, is) - wrk1*(dpb(:, 0)-wrk2)
  END DO
!
!is
!
! Drift terms
  result1 = MAXVAL(dv%facdrift)
  result2 = MAXVAL(dv%fac_exb)
  IF (result1 .NE. 0.0_R8 .OR. result2 .NE. 0.0_R8) THEN
!
!     ..computation of diamagnetic drift
!wdk    Note: use implementation consistent with vedia (b2tfed) and
!wdk          wadia (b2tfnb)
!wdk          (original code: cell center approach)
    wrkcv = dv%ne*pl%te
    CALL GRAD_NODIFF(ncv, nfc, nvx, 0, geo, mpg, wrkcv, wrkvx, dnete)
    wrk0 = geo%fcbb(:, 2)/geo%fcbb(:, 3)**2
!wdk    to be checked: now we use inverse volume weighting in wkr0
    CALL INTVERTEX_NODIFF(ncv, nvx, mpg, geo%vxvol, dv%ne, wrkvx)
    CALL INTFACE(nvx, nfc, mpg%fcvx, weight, wrkvx, wrk1)
    dv%wedia(:, 0) = wrk0*dnete(:, 1)/(wrk1*qe)
    dv%wedia(:, 1) = -(wrk0*dnete(:, 0)/(wrk1*qe))
  ELSE
    dv%wedia = 0.0_R8
  END IF
!
!   ..compute wrk0
  CALL INTFACE(ncv, nfc, mpg%fccv, geo%fcvol, dv%ue, wrk0)
!
  IF (switch%b2sigp_style .EQ. 2) THEN
    CALL B2XZEF_NODIFF(ncv, ns, rt%rz2, pl%na, dv%ne, zeff, st_ext)
    DO icv=1,ncv
      result10 = FCE2N(zeff(icv))
      c071(icv) = zeff(icv)*result10
    END DO
  ELSE
!srv 13.01.17
    c071 = 0.71_R8
  END IF
!
!wdk  interpolate to cell faces!
  CALL INTFACE(ncv, nfc, mpg%fccv, geo%fcvol, c071, c071f)
!
! ..compute fluxes
!wdk  * why the species loop + check for zamax = 1 here?
!wdk    should we not compute fne_eir as sum(fna_eir,is) - fch?
!wdk    (in that case, fch including full diamagnetic current?)
!wdk  * adapted check: added zn(is).eq.1 to avoid double counting
  DO is=0,ns-1
    IF (NINT(zn(is)) .EQ. 1 .AND. NINT(zamax(is)) .EQ. 1) THEN
!
!   ..compute convective coefficients (poloidal and radial)
      fle_eir(:, 0) = geo%fcpbs*wrk0 + (switch%xvecrb*dv%veecrb(:, 0)+&
&       switch%xwdia*dv%wedia(:, 0))*geo%fcs*geo%fcqalf(:, 0)
      fle_eir(:, 1) = (switch%xvecrb*dv%veecrb(:, 1)+switch%xwdia*dv%&
&       wedia(:, 1))*geo%fcs*geo%fcqalf(:, 1)
!
!   ..apply discretization scheme
      CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, dv%ne, fle_eir&
&                    , co%cdna(:, :, is), dv%fne_eir, wrk00, wrk11)
!
!   ..compute convective coefficients electron heat flux
      floe_eir(:, 0) = 2.5_R8*dv%fne_eir(:, 0) - c071f*dv%fch_p(:, 0)/qe&
&       *switch%fch_pte
      floe_eir(:, 1) = 2.5_R8*dv%fne_eir(:, 1)
!
!   ..apply discretization scheme
      CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%te, &
&                    floe_eir, co%chce, dv%fhe_eir, wrk00, wrk11)
!
!   ..compute convective coefficients ion heat flux
      floi_eir = 0.0_R8
      DO isb=0,ns-1
        IF (.NOT.is_neutral(isb)) floi_eir = floi_eir + dv%fna_eir(:, :&
&           , isb)
      END DO
      floi_eir(:, 0) = 2.5_R8*floi_eir(:, 0)
      floi_eir(:, 1) = 2.5_R8*floi_eir(:, 1)
!
!   ..apply discretization scheme
      CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%ti, &
&                    floi_eir, co%chci, dv%fhi_eir, wrk00, wrk11)
!
    END IF
  END DO
!srv 18.06.08 }
!
  IF (switch%b2tfrn_iout .NE. 0) THEN
!srv 18.06.08 {
    DO is=0,ns-1
      WRITE(chns, '(i3.3)') is
      arg1(:) = 'b2tfrn_fna_eir_th'//chns
      CALL MY_OUT_US(70, nfc, 1, dv%fna_eir(1, 0, is), arg1(:))
      arg10(:) = 'b2tfrn_fna_eir_r'//chns
      CALL MY_OUT_US(70, nfc, 1, dv%fna_eir(1, 1, is), arg10(:))
      arg11(:) = 'b2tfrn_vecrb_th'//chns
      CALL MY_OUT_US(70, nfc, 1, dv%vaecrb(1, 0, is), arg11(:))
      arg12(:) = 'b2tfrn_vecrb_r'//chns
      CALL MY_OUT_US(70, nfc, 1, dv%vaecrb(1, 1, is), arg12(:))
      arg11(:) = 'b2tfrn_wadia_th'//chns
      CALL MY_OUT_US(70, nfc, 1, dv%wadia(1, 0, is), arg11(:))
      arg12(:) = 'b2tfrn_wadia_r'//chns
      CALL MY_OUT_US(70, nfc, 1, dv%wadia(1, 1, is), arg12(:))
      arg12(:) = 'b2tfrn_cdna_th'//chns
      CALL MY_OUT_US(70, nfc, 1, co%cdna(1, 0, is), arg12(:))
      arg13(:) = 'b2tfrn_cdna_r'//chns
      CALL MY_OUT_US(70, nfc, 1, co%cdna(1, 1, is), arg13(:))
      arg14(:) = 'b2tfrn_ua'//chns
      CALL MY_OUT_US(70, ncv, 0, pl%ua(1, is), arg14(:))
      arg14(:) = 'b2tfrn_na'//chns
      CALL MY_OUT_US(70, ncv, 0, pl%na(1, is), arg14(:))
    END DO
    CALL MY_OUT_US(70, nfc, 1, dv%fne_eir(1, 0), &
&                   'b2tfrn_fne_eir_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fne_eir(1, 1), &
&                   'b2tfrn_fne_eir_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhe_eir(1, 0), &
&                   'b2tfrn_fhe_eir_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhe_eir(1, 1), &
&                   'b2tfrn_fhe_eir_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_eir(1, 0), &
&                   'b2tfrn_fhi_eir_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_eir(1, 1), &
&                   'b2tfrn_fhi_eir_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fch_p(1, 0), 'b2tfrn_fch_p_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fch_p(1, 1), 'b2tfrn_fch_p_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvispar(1, 0), &
&                   'b2tfrn_fchvispar_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvispar(1, 1), &
&                   'b2tfrn_fchvispar_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchinert(1, 0), &
&                   'b2tfrn_fchinert_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchinert(1, 1), &
&                   'b2tfrn_fchinert_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchanml(1, 0), &
&                   'b2tfrn_fchanml_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchanml(1, 1), &
&                   'b2tfrn_fchanml_r')
    CALL MY_OUT_US(70, nfc, 1, dv%wedia(1, 0), 'b2tfrn_wedia_th')
    CALL MY_OUT_US(70, nfc, 1, dv%wedia(1, 1), 'b2tfrn_wedia_r')
    CALL MY_OUT_US(70, nfc, 1, dv%ue, 'b2tfrn_ve')
    CALL MY_OUT_US(70, ncv, 0, dv%ne, 'b2tfrn_ne')
    CALL MY_OUT_US(70, ncv, 0, pl%te, 'b2tfrn_te')
    CALL MY_OUT_US(70, ncv, 0, pl%ti, 'b2tfrn_ti')
    CALL MY_OUT_US(70, ncv, 0, pl%po, 'b2tfrn_po')
  END IF
!
! ..return
  ncall_b2tfrn = ncall_b2tfrn + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2TFRN_NODIFF

