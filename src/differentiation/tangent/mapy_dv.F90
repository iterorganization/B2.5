SUBROUTINE MAPY_NODIFF(nx, ny, ns, field, work)
  USE B2MOD_TYPES
  USE B2MOD_INDIRECT_DIFFV
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
  INTEGER :: nx, ny, ns
  REAL(kind=r8) :: field(-1:nx, -1:ny, 0:ns-1), work(-1:nx, -1:ny, 0:ns-&
& 1)
  INTEGER :: ix, iy, is
!
  DO is=0,ns-1
    DO iy=-1,ny
      DO ix=-1,nx
        IF (bottomiy(ix, iy) .EQ. -2) THEN
          work(ix, iy, is) = field(ix, iy+1, is)
        ELSE IF (topiy(ix, iy) .EQ. ny + 1) THEN
          work(ix, iy, is) = field(ix, iy, is)
        ELSE
          work(ix, iy, is) = 0.5_R8*(field(topix(ix, iy), topiy(ix, iy)&
&           , is)+field(ix, iy, is))
        END IF
      END DO
    END DO
  END DO
  DO is=0,ns-1
    DO iy=-1,ny
      DO ix=-1,nx
        field(ix, iy, is) = work(ix, iy, is)
      END DO
    END DO
  END DO
  RETURN
END SUBROUTINE MAPY_NODIFF

