FUNCTION INSIDE_CELL_NODIFF(mpg, geo, r, z, icv) RESULT (inside_cell)
  USE B2MOD_TYPES
  USE B2US_MAP_DIFFV
  USE B2US_GEO_DIFFV
  USE B2MOD_CONSTANTS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(GEOMETRY), INTENT(IN) :: geo
