  REAL(kind=r8) :: r1, r2, z1, z2, angle
  LOGICAL, ALLOCATABLE :: done(:)
  INTRINSIC SQRT
  INTRINSIC ASIN
  INTRINSIC NINT
  REAL(kind=r8) :: arg1
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: arg2
  REAL(kind=r8) :: result2
  REAL(kind=r8) :: arg3
  REAL(kind=r8) :: result3
!
!xpb  We use the subtended angle method to handle arbitrary cell shapes
!xpb  The cell sides must be travelled in order
!
  IF (icv .LE. 0 .OR. icv .GT. mpg%nci) THEN
    inside_cell = .false.
    RETURN
  ELSE
    ALLOCATE(done(mpg%cvfcp(icv, 2)))
    done = .false.
    ifc = mpg%cvfc(mpg%cvfcp(icv, 1))
    ivx1 = mpg%fcvx(ifc, 1)
    ivx2 = mpg%fcvx(ifc, 2)
    r1 = geo%vxx(ivx1)
    z1 = geo%vxy(ivx1)
    r2 = geo%vxx(ivx2)
    z2 = geo%vxy(ivx2)
    arg1 = (r1-r)**2 + (z1-z)**2
    result1 = SQRT(arg1)
    arg2 = (r2-r)**2 + (z2-z)**2
    result2 = SQRT(arg2)
    arg3 = ((r1-r)*(z2-z)-(z1-z)*(r2-r))/(result1*result2)
    angle = ASIN(arg3)
    done(1) = .true.
    DO WHILE (ivx1 .NE. ivx2)
      DO i=2,mpg%cvfcp(icv, 2)
        IF (.NOT.done(i)) THEN
          ifc = mpg%cvfc(mpg%cvfcp(icv, 1)+i-1)
          ivx = mpg%fcvx(ifc, 1)
          IF (ivx .EQ. ivx1) THEN
            r1 = geo%vxx(mpg%fcvx(ifc, 2))
            z1 = geo%vxy(mpg%fcvx(ifc, 2))
            r2 = geo%vxx(mpg%fcvx(ifc, 1))
            z2 = geo%vxy(mpg%fcvx(ifc, 1))
            ivx1 = mpg%fcvx(ifc, 2)
            done(i) = .true.
          ELSE IF (ivx .EQ. ivx2) THEN
            r2 = geo%vxx(mpg%fcvx(ifc, 2))
            z2 = geo%vxy(mpg%fcvx(ifc, 2))
            r1 = geo%vxx(mpg%fcvx(ifc, 1))
            z1 = geo%vxy(mpg%fcvx(ifc, 1))
            ivx2 = mpg%fcvx(ifc, 2)
            done(i) = .true.
          END IF
          IF (.NOT.done(i)) THEN
            ivx = mpg%fcvx(ifc, 2)
            IF (ivx .EQ. ivx1) THEN
              r1 = geo%vxx(mpg%fcvx(ifc, 1))
              z1 = geo%vxy(mpg%fcvx(ifc, 1))
              r2 = geo%vxx(mpg%fcvx(ifc, 2))
              z2 = geo%vxy(mpg%fcvx(ifc, 2))
              ivx1 = mpg%fcvx(ifc, 1)
              done(i) = .true.
            ELSE IF (ivx .EQ. ivx2) THEN
              r2 = geo%vxx(mpg%fcvx(ifc, 1))
              z2 = geo%vxy(mpg%fcvx(ifc, 1))
              r1 = geo%vxx(mpg%fcvx(ifc, 2))
              z1 = geo%vxy(mpg%fcvx(ifc, 2))
              ivx2 = mpg%fcvx(ifc, 1)
              done(i) = .true.
            END IF
          END IF
          IF (done(i)) THEN
            arg1 = (r1-r)**2 + (z1-z)**2
            result1 = SQRT(arg1)
            arg2 = (r2-r)**2 + (z2-z)**2
            result2 = SQRT(arg2)
            arg3 = ((r1-r)*(z2-z)-(z1-z)*(r2-r))/(result1*result2)
            result3 = ASIN(arg3)
            angle = angle + result3
          END IF
        END IF
      END DO
    END DO
    arg1 = angle/(2.0_R8*pi)
    inside_cell = NINT(arg1) .NE. 0
    DEALLOCATE(done)
!
    RETURN
  END IF
END FUNCTION INSIDE_CELL_NODIFF

