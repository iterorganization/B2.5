    dte(icv) = dte1(icv)
    dti(icv) = dti1(icv)
!xpb !nh
    IF (solvireg(ireg)) THEN
      IF (switch%b2upht_ti_max*ev .GT. ti(icv) + dti(icv)) THEN
        DO nd=1,nbdirs
          y1d(nd) = tid(nd, icv) + dtid(nd, icv)
        END DO
        y1 = ti(icv) + dti(icv)
      ELSE
        DO nd=1,nbdirs
          y1d(nd) = 0.D0
        END DO
        y1 = switch%b2upht_ti_max*ev
        y1d = 0.D0
      END IF
      IF (switch%b2upht_ti_min*ev .LT. y1) THEN
        DO nd=1,nbdirs
          tid(nd, icv) = y1d(nd)
        END DO
        ti(icv) = y1
      ELSE
        DO nd=1,nbdirs
          tid(nd, icv) = 0.D0
        END DO
        ti(icv) = switch%b2upht_ti_min*ev
      END IF
    END IF
    IF (solvereg(ireg)) THEN
      IF (switch%b2upht_rte_min*ti(icv) .LT. te(icv)) THEN
        te(icv) = te(icv)
      ELSE
        DO nd=1,nbdirs
          ted(nd, icv) = switch%b2upht_rte_min*tid(nd, icv)
        END DO
        te(icv) = switch%b2upht_rte_min*ti(icv)
      END IF
      IF (switch%b2upht_te_max*ev .GT. te(icv) + dte(icv)) THEN
        DO nd=1,nbdirs
          y2d(nd) = ted(nd, icv) + dted(nd, icv)
        END DO
        y2 = te(icv) + dte(icv)
      ELSE
        DO nd=1,nbdirs
          y2d(nd) = 0.D0
        END DO
        y2 = switch%b2upht_te_max*ev
        y2d = 0.D0
      END IF
      IF (switch%b2upht_te_min*ev .LT. y2) THEN
        DO nd=1,nbdirs
          ted(nd, icv) = y2d(nd)
        END DO
        te(icv) = y2
      ELSE
        DO nd=1,nbdirs
          ted(nd, icv) = 0.D0
        END DO
        te(icv) = switch%b2upht_te_min*ev
      END IF
    END IF
    IF (solvpreg(ireg) .AND. po_solve .AND. switch%pot_eq .EQ. 1 .AND. &
&       icv .LE. mpg%nci .AND. add_te_corr_to_po(ireg)) THEN
      DO nd=1,nbdirs
        pod(nd, icv) = pod(nd, icv) + dted(nd, icv)/qe
      END DO
      po(icv) = po(icv) + dte(icv)/qe
    ELSE IF (switch%pot_eq .EQ. 2) THEN
      DO nd=1,nbdirs
        pod(nd, icv) = 3.1_R8*ted(nd, icv)/qe
      END DO
      po(icv) = 3.1_R8*te(icv)/qe
    END IF
    IF (solvnreg(ireg)) THEN
      IF (switch%b2upht_tn_max*ev .GT. tn(icv) + dtn(icv)) THEN
        DO nd=1,nbdirs
          y3d(nd) = tnd(nd, icv) + dtnd(nd, icv)
        END DO
        y3 = tn(icv) + dtn(icv)
      ELSE
        DO nd=1,nbdirs
          y3d(nd) = 0.D0
        END DO
        y3 = switch%b2upht_tn_max*ev
        y3d = 0.D0
      END IF
      IF (switch%b2upht_tn_min*ev .LT. y3) THEN
        DO nd=1,nbdirs
          tnd(nd, icv) = y3d(nd)
        END DO
        tn(icv) = y3
      ELSE
        DO nd=1,nbdirs
          tnd(nd, icv) = 0.D0
        END DO
        tn(icv) = switch%b2upht_tn_min*ev
      END IF
    END IF
    IF (switch%solve_keps .GT. 0 .AND. solvkreg(ireg)) THEN
      IF (switch%b2upht_kt_max*ev .GT. kt(icv) + dkt(icv)) THEN
        DO nd=1,nbdirs
          y4d(nd) = ktd(nd, icv) + dktd(nd, icv)
        END DO
        y4 = kt(icv) + dkt(icv)
      ELSE
        DO nd=1,nbdirs
          y4d(nd) = 0.D0
        END DO
        y4 = switch%b2upht_kt_max*ev
        y4d = 0.D0
      END IF
      IF (switch%b2upht_kt_min*ev .LT. y4) THEN
        DO nd=1,nbdirs
          ktd(nd, icv) = y4d(nd)
        END DO
        kt(icv) = y4
      ELSE
        DO nd=1,nbdirs
          ktd(nd, icv) = 0.D0
        END DO
        kt(icv) = switch%b2upht_kt_min*ev
      END IF
    END IF
    IF (switch%solve_keps .GT. 1 .AND. solvzreg(ireg)) THEN
      IF (switch%b2upht_zt_max*ev .GT. zt(icv) + dzt(icv)) THEN
        DO nd=1,nbdirs
          y5d(nd) = ztd(nd, icv) + dztd(nd, icv)
        END DO
        y5 = zt(icv) + dzt(icv)
      ELSE
        DO nd=1,nbdirs
          y5d(nd) = 0.D0
        END DO
        y5 = switch%b2upht_zt_max*ev
        y5d = 0.D0
      END IF
      IF (switch%b2upht_zt_min*ev .LT. y5) THEN
        DO nd=1,nbdirs
          ztd(nd, icv) = y5d(nd)
        END DO
        zt(icv) = y5
      ELSE
        DO nd=1,nbdirs
          ztd(nd, icv) = 0.D0
        END DO
        zt(icv) = switch%b2upht_zt_min*ev
      END IF
    END IF
  END DO
!   ..adjust ua
  DO nd=1,nbdirs
    wrk0d(nd, :) = dte*ned(nd, :) + ne*dted(nd, :) + dti*nid(nd, :, 0) +&
&     ni(:, 0)*dtid(nd, :)
  END DO
  wrk0 = ne*dte + ni(:, 0)*dti
  wrkd = 0.D0
  wrkvd = 0.D0
  CALL GRADC_P_DV(ncv, nfc, nvx, 0, geo, geod, mpg, mpgd, wrk0, wrk0d, &
&           wrkv, wrkvd, wrk, wrkd, nbdirs)
  IF (ua_solve) THEN
    DO icv=1,mpg%nci
      ireg = mpg%cvreg(icv)
      DO nd=1,nbdirs
        t0d(nd) = switch%b2npht_pcm0*(wrk(icv)*pccm0d(nd, icv)+pccm0(icv&
&         )*wrkd(nd, icv))
      END DO
      t0 = switch%b2npht_pcm0*pccm0(icv)*wrk(icv)
      DO is=0,ns-1
        IF (solvmreg(is, ireg)) THEN
          DO nd=1,nbdirs
            uad(nd, icv, is) = uad(nd, icv, is) - t0d(nd)
          END DO
          ua(icv, is) = ua(icv, is) - t0
        END IF
      END DO
    END DO
  END IF
!
  IF (switch%tn_style .LE. 1) THEN
    DO nd=1,nbdirs
      tnd(nd, :) = tid(nd, :)
    END DO
    tn = ti
  END IF
!
! ..return
  ncall_b2upht = ncall_b2upht + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2upht
!
END SUBROUTINE B2UPHT_DV

