SUBROUTINE MAPX_NODIFF(nx, ny, ns, field, work)
  USE B2MOD_TYPES
  USE B2MOD_INDIRECT_DIFFV
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
  INTEGER :: nx, ny, ns, ix, iy, is
  REAL(kind=r8) :: field(-1:nx, -1:ny, 0:ns-1), work(-1:nx, -1:ny, 0:ns-&
& 1)
!
  DO is=0,ns-1
    DO iy=-1,ny
      DO ix=-1,nx
        IF (leftix(ix, iy) .EQ. -2) THEN
          work(ix, iy, is) = field(rightix(ix, iy), rightiy(ix, iy), is)
        ELSE IF (rightix(ix, iy) .EQ. nx + 1) THEN
          work(ix, iy, is) = field(ix, iy, is)
        ELSE
          work(ix, iy, is) = 0.5_R8*(field(rightix(ix, iy), rightiy(ix, &
&           iy), is)+field(ix, iy, is))
        END IF
      END DO
    END DO
  END DO
  DO is=0,ns-1
    DO iy=-1,ny
      DO ix=-1,nx
        field(ix, iy, is) = work(ix, iy, is)
      END DO
    END DO
  END DO
  RETURN
END SUBROUTINE MAPX_NODIFF

