&       %fccv(ifc, 2)))
!    ..radial
      flowf(ifc, 1) = UPWIND(flo(ifc, 1), fun(mpg%fccv(ifc, 1)), fun(mpg&
&       %fccv(ifc, 2)))
    END DO
!
!    ..add conductive piece
    flow = flowf - con*dfun
!
  ELSE IF (meth .EQ. 2) THEN
