  INTRINSIC ABS
  EXTERNAL XERRAB
  REAL(kind=r8) :: abs0
  REAL(kind=r8) :: abs1
  REAL(kind=r8) :: arg1
  REAL(kind=r8) :: result1
!-----------------------------------------------------------------------
!.computation      
!
  IF (meth .EQ. 0) THEN
! CENTRAL scheme
!
!    ..differences of fun at cell faces
    CALL DIFF_NODIFF(ncv, nfc, nvx, 0, geo, mpg, fun, funv, dfun)
!
!    ..convective piece
    DO ifc=1,nfc
!    ..poloidal
      flowf(ifc, 0) = 0.5_R8*flo(ifc, 0)*(fun(mpg%fccv(ifc, 1))+fun(mpg%&
