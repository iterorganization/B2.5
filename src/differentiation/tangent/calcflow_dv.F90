!   ..local variables
  INTEGER :: ifc
  REAL(kind=r8) :: dfun(nfc, 0:1), funv(nvx), t0
  REAL(kind=r8), PARAMETER :: eps=1.0e-60_R8
  EXTERNAL UPWIND, HYBR
