                  IF (.NOT.mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND.&
&                     mpg%cvonclosedsurface(mpg%fccv(ifc, 2))) qip0(mpg%&
&                   fccv(ifc, 1)) = qip0(mpg%fccv(ifc, 2))
                END DO
              END DO
            END IF
!
!
            IF (switch%b2siav_qstyle .EQ. 1) THEN
!
              DO ift=1,mpg%nft
                IF (mpg%cvonclosedsurface(mpg%ftcv(mpg%ftcvp(ift, 1)))) &
&               THEN
                  DO ic=mpg%ftcvp(ift, 1),mpg%ftcvp(ift, 1)+mpg%ftcvp(&
&                     ift, 2)-1
                    icv = mpg%ftcv(ic)
                    qip0(icv) = -(hci_al_ast(icv, inucl)*gti(icv, 0)*geo&
&                     %cvbb(icv, 3)/geo%cvbb(icv, 0)) - 2.5e0_R8*geo%&
&                     cvbb(icv, 2)/(geo%cvbb(icv, 0)*geo%cvbb(icv, 3))*(&
&                     nal(icv, inucl)*ti(icv)/qe*z_to_m1_ast(icv, inucl)&
&                     )*gti(icv, 1)
                  END DO
                END IF
!to ensure that subsequent steps lead to well-defined value on separatrix:
!copy value of qip0 across separatrix
!wdk todo: rationalize this when separatrix-structure is introduced?

              END DO
!
              DO ifc=1,nfc
                IF (mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. (.NOT.&
&                   mpg%cvonclosedsurface(mpg%fccv(ifc, 2)))) qip0(mpg%&
&                 fccv(ifc, 2)) = qip0(mpg%fccv(ifc, 1))
                IF (.NOT.mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. &
&                   mpg%cvonclosedsurface(mpg%fccv(ifc, 2))) qip0(mpg%&
&                 fccv(ifc, 1)) = qip0(mpg%fccv(ifc, 2))
              END DO
            END IF
!
!
!         ..compute qip1
            arg1 = te + ti
            result1 = SQRT(arg1)
            arg2 = amnucl(inucl)*mp
            result2 = SQRT(arg2)
            qip1 = switch%cqip1*nal(:, inucl)*ti*result1/result2
!
!         ..compute qipgen and its poloidal gradient on faces
            arg1 = qip0/qip1
            arg20 = geo%cvbb(:, 3)
            result10 = SQRT(arg20)
            qipgen = qip1*DATAN(arg1)*result10
!set gqipgen_al to zero on faces not in touch with core
!wdk todo: rationalize this when separatrix-structure is introduced?
            CALL GRAD_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, qipgen, wrkv&
&                        , gqipgen_al(:, inucl))
!
            DO ifc=1,nfc
              IF (.NOT.mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. (&
&                 .NOT.mpg%cvonclosedsurface(mpg%fccv(ifc, 2)))) &
&               gqipgen_al(ifc, inucl) = 0.0_R8
            END DO
!

          END DO
!
!       ..interpolate pre-factors to faces
          wrkc = na(:, is)/nal(:, s2nucl(is)-1)
          CALL INTFACE(ncv, nfc, mpg%fccv, wght, wrkc, naf)
          CALL INTFACE(ncv, nfc, mpg%fccv, wght, ia(:, is), iaf)
!
          wrkf = 0.0_R8
          DO inucl=0,nspecies-1
            wrkf = wrkf + vsaf_hadp_albe(:, 0, s2nucl(is)-1, inucl)*&
&             gqipgen_al(:, inucl)
          END DO
          IF (switch%zhcscorr .GT. 0 .AND. switch%zhcscorr_vq .GT. 0) &
&           wrkf = wrkf + vsaf_hbdp_al(:, 0, s2nucl(is)-1)*(gqipgen/iaf-&
&             gqipgen_al(:, s2nucl(is)-1))
          arg11 = geo%fcbb(:, 3)
          result12 = SQRT(arg11)
          wrkf = wrkf*naf*geo%fcbb(:, 2)*result12/geo%fcbb(:, 0)/3.0_R8
!
!       ..calculate the components of the parallel viscosity current
!         connected with the heat flux 
          fchvisq_a(:, 0, is) = -(0.4_R8*wrkf*gonedbsq(:, 1)*geo%fcs*geo&
&           %fcqalf(:, 0))
          fchvisq_a(:, 1, is) = 0.4_R8*wrkf*gonedbsq(:, 0)*geo%fcs*geo%&
&           fcqalf(:, 1)
!
        END IF
!
!      ..accumulate individual contributions into fchvisq
        fchvisq = fchvisq + fchvisq_a(:, :, is)
!
      END DO
    END IF
  END IF
! ..return
  ncall = ncall + 1
  CALL SUBEND()
  RETURN
!
!-----------------------------------------------------------------------
!.end b2tvsq
!
END SUBROUTINE B2TVSQ_NODIFF

