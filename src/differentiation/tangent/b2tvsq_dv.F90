!   ..procedures
  EXTERNAL XERTST
  EXTERNAL B2XVSG
  INTRINSIC MAXVAL
  INTRINSIC NINT
  INTRINSIC SQRT
  INTRINSIC DATAN
  REAL(kind=r8), DIMENSION(ncv) :: arg1
  REAL(kind=r8), DIMENSION(ncv) :: result1
  REAL(kind=r8) :: arg2
  REAL(kind=r8) :: result2
  REAL(kind=r8), DIMENSION(nCv) :: arg20
  REAL(kind=r8), DIMENSION(nCv) :: result10
  REAL(kind=r8) :: arg10
  REAL(kind=r8) :: result11
  REAL(kind=r8), DIMENSION(nFc) :: arg11
  REAL(kind=r8), DIMENSION(nFc) :: result12
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2tvsq')
!   ..test nCv, nFc
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
!srv 11.07.99
  fchvisq = 0.0_R8
  fac_vism = MAXVAL(fac_vis)
!srv 16.10.17
  IF (fac_vism .NE. 0.0_R8 .AND. switch%fhe_vis_q .NE. 0.0_R8 .AND. &
&     switch%no_current .EQ. 0) THEN
!
!     ..compute gradients of ti in cell centers
    CALL GRADC_NODIFF(ncv, nfc, nvx, 0, geo, mpg, ti, wrkv, gti)
!
!     ..compute gradients of OnedBsq on cell faces
    CALL GRAD_NODIFF(ncv, nfc, nvx, 1, geo, mpg, geo%cvonedbsq, geo%&
&              vxonedbsq, gonedbsq)
!
!srv 12.05.11
    DO is=0,ns-1
      IF (NINT(zn(is)) .EQ. 1 .AND. NINT(zamax(is)) .EQ. 1) THEN
!srv 12.05.11
!
        CALL B2TTIA_NODIFF(ncv, ns, ti, rz2, ne2, lnlam, tauia)
!srv 01.07.09
!
!       ..calculate the parallel heat flux of ion at the center of cell !lk 23.05.06 05.07.07 {
!
        qip0 = 0.0_R8
!
        IF (switch%b2siav_qstyle .EQ. 0) THEN
!
          DO ift=1,mpg%nft
            IF (mpg%cvonclosedsurface(mpg%ftcv(mpg%ftcvp(ift, 1)))) THEN
              DO ic=mpg%ftcvp(ift, 1),mpg%ftcvp(ift, 1)+mpg%ftcvp(ift, 2&
&                 )-1
                icv = mpg%ftcv(ic)
                qip0(icv) = -(2.5_R8*geo%cvbb(icv, 3)*geo%cvbb(icv, 2)/(&
&                 geo%cvbb(icv, 0)*geo%ftbbav2(ift))*(ni(icv)*ti(icv)/qe&
&                 )*gti(icv, 1))
              END DO
            END IF
          END DO
!to ensure that subsequent steps lead to well-defined value on separatrix:
!copy value of qip0 across separatrix
!wdk todo: rationalize this when separatrix-structure is introduced?
!
          DO ifc=1,nfc
            IF (mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. (.NOT.mpg%&
&               cvonclosedsurface(mpg%fccv(ifc, 2)))) qip0(mpg%fccv(ifc&
&             , 2)) = qip0(mpg%fccv(ifc, 1))
            IF (.NOT.mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. mpg%&
&               cvonclosedsurface(mpg%fccv(ifc, 2))) qip0(mpg%fccv(ifc, &
&             1)) = qip0(mpg%fccv(ifc, 2))
          END DO
!smoothing: interpolate to faces and back to cell centers
!
          wght = 1.0_R8
          DO k=1,nsmooth_tvsq
            CALL INTFACE(ncv, nfc, mpg%fccv, wght, qip0, wrkf)
            CALL INTCELL_NODIFF(nfc, ncv, mpg, mpg%intcellp, wrkf, qip0)
!copy value of qip0 across separatrix
!wdk todo: rationalize this when separatrix-structure is introduced?
            DO ifc=1,nfc
              IF (mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. (.NOT.&
&                 mpg%cvonclosedsurface(mpg%fccv(ifc, 2)))) qip0(mpg%&
&               fccv(ifc, 2)) = qip0(mpg%fccv(ifc, 1))
              IF (.NOT.mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. mpg&
&                 %cvonclosedsurface(mpg%fccv(ifc, 2))) qip0(mpg%fccv(&
&               ifc, 1)) = qip0(mpg%fccv(ifc, 2))
            END DO
          END DO
        END IF
!
!
        IF (switch%b2siav_qstyle .EQ. 1) THEN
!
          DO ift=1,mpg%nft
            IF (mpg%cvonclosedsurface(mpg%ftcv(mpg%ftcvp(ift, 1)))) THEN
              DO ic=mpg%ftcvp(ift, 1),mpg%ftcvp(ift, 1)+mpg%ftcvp(ift, 2&
&                 )-1
                icv = mpg%ftcv(ic)
                qip0(icv) = -(hcix_c(icv)*gti(icv, 0)*geo%cvbb(icv, 3)/&
&                 geo%cvbb(icv, 0)) - 2.5_R8*geo%cvbb(icv, 2)/(geo%cvbb(&
&                 icv, 0)*geo%cvbb(icv, 3))*(ni(icv)*ti(icv)/qe)*gti(icv&
&                 , 1)
              END DO
            END IF
          END DO
!to ensure that subsequent steps lead to well-defined value on separatrix:
!copy value of qip0 across separatrix
!wdk todo: rationalize this when separatrix-structure is introduced?
!
          DO ifc=1,nfc
            IF (mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. (.NOT.mpg%&
&               cvonclosedsurface(mpg%fccv(ifc, 2)))) qip0(mpg%fccv(ifc&
&             , 2)) = qip0(mpg%fccv(ifc, 1))
            IF (.NOT.mpg%cvonclosedsurface(mpg%fccv(ifc, 1)) .AND. mpg%&
