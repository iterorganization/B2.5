!     For (i,j,k) in (0:rtnt,0:rtnn,0:rtns-1), rtlz2(i,j,k) specifies
!     the effective squared charge at
!     electron temperature rtt(i), electron density rtn(j), for the
!     superstage k. rtlz2 has dimension e**2.
!      rtlpt - (0:rtnt,0:rtnn,0:rtns-1) real*8 array.
!     For (i,j,k) in (0:rtnt,0:rtnn,0:rtns-1), rtlpt(i,j,k) specifies
!     the cumulative ionisation potential at
!     electron temperature rtt(i), electron density rtn(j),
!     to reach superstage k. rtlpt has dimension eV.
!      rtlpi - (0:rtnt,0:rtnn,0:rtns-1) real*8 array.
!     For (i,j,k) in (0:rtnt,0:rtnn,0:rtns-1), rtlpi(i,j,k) specifies
!     the effective ionisation potential at
!     electron temperature rtt(i), electron density rtn(j),
!     to reach superstage k from superstage k-1. rtlpi has dimension eV.
!
!
!  8. further comments
!
!     See routine b2cdca for explanation of 'un*formatted file'.
!
!-----------------------------------------------------------------------
!.declarations
!
!   ..local variables
  LOGICAL :: lt0
  INTEGER :: k, idum(0:9)
  REAL(kind=r8) :: lnzero, t0
  REAL(kind=r8), ALLOCATABLE :: rtza(:), rtza2(:)
  PARAMETER (lnzero=-1.0e30_R8)
  EXTERNAL XERTST, smax, CFRURE, CFRURE_DEP, CFRUIN, CFRUIN_OPT, &
&     CFRUIN_DEP
!   ..procedures
  REAL(kind=r8) :: smax
  INTRINSIC MAXVAL
  INTRINSIC MINVAL
  INTEGER :: arg1
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2rurc')
!   ..test nget
  CALL XERTST(1 .LE. nget, 'faulty argument nget')
!   ..set cutll
  IF (.NOT.b2mod_math_initialised) CALL B2MOD_MATH_INIT()
!
! ..read rate coefficients
  IF (version .GE. '03.000.007') THEN
    idum(0) = -1
    CALL CFRUIN_DEP(nget, 1, idum, 'fix_recomb')
  END IF
  IF (idum(0) .GE. 0) THEN
    WRITE(*, *) 'You are using a deprecated b2frates file format!'
    WRITE(*, *) 'Re-run b2ar to correct'
    WRITE(0, *) 'You are using a deprecated b2frates file format!'
    WRITE(0, *) 'Re-run b2ar to correct'
    fix_recomb = idum(0)
  END IF
!   ..read dimensions
  CALL CFRUIN(nget, 3, idum, 'rtnt,rtnn,rtns')
  CALL ALLOC_B2MOD_B2CMRC(idum(0), idum(1), idum(2))
!   ..read species data
  IF (version .LT. '01.001.024') THEN
    ALLOCATE(rtza(0:idum(2)-1))
    ALLOCATE(rtza2(0:idum(2)-1))
    CALL CFRURE(nget, rtns, rtza, 'rtza')
    CALL CFRURE(nget, rtns, rtzn, 'rtzn')
    CALL CFRURE(nget, rtns, rtza2, 'rtza2')
    rtzmin(0:rtns-1) = rtza(0:rtns-1)
    rtzmax(0:rtns-1) = rtza(0:rtns-1)
  ELSE
    CALL CFRURE(nget, rtns, rtzmin, 'rtzmin')
    CALL CFRURE(nget, rtns, rtzmax, 'rtzmax')
    CALL CFRURE(nget, rtns, rtzn, 'rtzn')
  END IF
!   ..read abscissae
  arg1 = rtnt + 1
  CALL CFRURE(nget, arg1, rtt, 'rtt')
  arg1 = rtnn + 1
  CALL CFRURE(nget, arg1, rtn, 'rtn')
  arg1 = rtnt + 1
  CALL CFRURE(nget, arg1, rtlt, 'rtlt')
  arg1 = rtnn + 1
  CALL CFRURE(nget, arg1, rtln, 'rtln')
!   ..read rate coefficients
  arg1 = (rtnt+1)*(rtnn+1)*rtns
  CALL CFRURE(nget, arg1, rtlsa, 'rtlsa')
  arg1 = (rtnt+1)*(rtnn+1)*rtns
  CALL CFRURE(nget, arg1, rtlra, 'rtlra')
  arg1 = (rtnt+1)*(rtnn+1)*rtns
  CALL CFRURE(nget, arg1, rtlqa, 'rtlqa')
  IF (version .GE. '03.002.000') THEN
    rtlqr = 0.0_R8
    arg1 = (rtnt+1)*(rtnn+1)*rtns
    CALL CFRURE_DEP(nget, arg1, rtlqr, 'rtlqa_ra')
    result1 = MAXVAL(rtlqr)
    result2 = MINVAL(rtlqr)
    IF (result1 .EQ. 0.0_R8 .AND. result2 .EQ. 0.0_R8) THEN
      arg1 = (rtnt+1)*(rtnn+1)*rtns
      CALL CFRURE(nget, arg1, rtlqr, 'rtlqr')
    END IF
  ELSE
    rtlqr = lnzero
  END IF
  arg1 = (rtnt+1)*(rtnn+1)*rtns
  CALL CFRURE(nget, arg1, rtlcx, 'rtlcx')
  IF (version .GE. '01.001.016') THEN
    arg1 = (rtnt+1)*(rtnn+1)*rtns
    CALL CFRURE(nget, arg1, rtlrd, 'rtlrd')
  ELSE
    rtlrd = lnzero
  END IF
  IF (version .GE. '01.001.019') THEN
    arg1 = (rtnt+1)*(rtnn+1)*rtns
    CALL CFRURE(nget, arg1, rtlbr, 'rtlbr')
  ELSE
    rtlbr = lnzero
  END IF
  IF (version .GE. '01.001.024') THEN
    arg1 = (rtnt+1)*(rtnn+1)*rtns
    CALL CFRURE(nget, arg1, rtlza, 'rtlza')
    arg1 = (rtnt+1)*(rtnn+1)*rtns
    CALL CFRURE(nget, arg1, rtlz2, 'rtlz2')
    arg1 = (rtnt+1)*(rtnn+1)*rtns
    CALL CFRURE(nget, arg1, rtlpt, 'rtlpt')
    arg1 = (rtnt+1)*(rtnn+1)*rtns
    CALL CFRURE(nget, arg1, rtlpi, 'rtlpi')
  ELSE
    CALL INITIALISE_POTENTIALS(rtns)
    DO k=0,rtns-1
      rtlza(0:rtnt, 0:rtnn, k) = (rtzmin(k)+rtzmax(k))/2.0_R8
      rtlz2(0:rtnt, 0:rtnn, k) = ((rtzmin(k)+rtzmax(k))/2.0_R8)**2
      rtlpt(0:rtnt, 0:rtnn, k) = (potmin(k)+potmax(k))/2.0_R8
      rtlpi(0:rtnt, 0:rtnn, k) = (potimin(k)+potimax(k))/2.0_R8
    END DO
  END IF
! corresponding to the default pre-3.0.7 version
  fix_recomb = 0
  zmax_recomb = 1
  IF (version .GE. '03.000.007') THEN
    CALL CFRUIN_OPT(nget, 2, idum, 'fix_recomb,zmax_recomb')
    fix_recomb = idum(0)
    zmax_recomb = idum(1)
  END IF
!
! ..test rate coefficients
!   ..test ionisation rate coefficients
  DO k=0,rtns-1
    IF (k .EQ. rtns - 1) THEN
      lt0 = .true.
    ELSE
      lt0 = .NOT.LRTNEXT(k, k + 1)
    END IF
!      (the meaning of lt0 is 'species k cannot ionise to k+1')
    IF (lt0) THEN
!       (ionisation rate must be 0)
      arg1 = (rtnt+1)*(rtnn+1)
      t0 = smax(arg1, rtlsa(0, 0, k), 1)
      CALL XERTST(t0 .LT. cutll, 'faulty entry in rtlsa table')
    END IF
  END DO
!   ..test recombination rate coefficients
  DO k=0,rtns-1
    IF (k .EQ. 0) THEN
      lt0 = .true.
    ELSE
      lt0 = .NOT.LRTNEXT(k - 1, k)
    END IF
!      (the meaning of lt0 is 'species k cannot recombine to k-1')
    IF (lt0) THEN
!       (recombination rate must be 0)
      arg1 = (rtnt+1)*(rtnn+1)
      t0 = smax(arg1, rtlra(0, 0, k), 1)
      CALL XERTST(t0 .LT. cutll, 'faulty entry in rtlra table')
    END IF
  END DO
!   ..test charge exchange rate coefficients
  DO k=0,rtns-1
    IF (k .EQ. 0) THEN
      lt0 = .true.
    ELSE
      lt0 = .NOT.LRTNEXT(k - 1, k)
    END IF
!      (the meaning of lt0 is 'species k cannot recombine to k-1')
    IF (lt0) THEN
!       (charge exchange rate must be 0)
      arg1 = (rtnt+1)*(rtnn+1)
      t0 = smax(arg1, rtlcx(0, 0, k), 1)
      CALL XERTST(t0 .LT. cutll, 'faulty entry in rtlcx table')
    END IF
  END DO
!
  CALL SUBEND()
  RETURN
END SUBROUTINE B2RURC_NODIFF

