!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2stcx')
!   ..test nCv, nFc, ns
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
  CALL XERTST(0 .LE. ismain .AND. ismain .LT. ns, &
&       'faulty argument ismain')
!   ..test is0
  CALL XERTST(0 .LE. is0 .AND. is0 .LT. ns - 1, 'faulty argument is0')
  result1 = LNEXT(is0, is0 + 1)
  CALL XERTST(result1, &
&       'faulty argument is0: not related to species is0+1')
!   ..extensive tests on first few calls
  IF (ncall_b2stcx .LT. 3) THEN
!    ..test sign of vol, na, ti, ni
    CALL B2XVSG(ncv, geo%cvvol, 1, 'vol', '.gt.')
    arg1 = ncv*ns
    CALL B2XVSG(arg1, na, 1, 'na', '.gt.')
    CALL B2XVSG(ncv, ti, 1, 'ti', '.gt.')
    CALL B2XVSG(ncv, tn, 1, 'tn', '.gt.')
    arg1 = ncv*2
    CALL B2XVSG(arg1, ni, 1, 'ni', '.gt.')
    WHERE (ua .GE. 0.) 
      dabs0 = ua
    ELSEWHERE
      dabs0 = -ua
    END WHERE
!    ..test velocities
    result10 = MAXVAL(dabs0)
    CALL XERTST(result10 .LT. c, 'Supra-luminal velocities !')
!    ..test rate coefficients
    DO is=1,ns-1
      IF (.NOT.LNEXT(is - 1, is)) THEN
        result11 = damax(ncv, rcx(1, is), 1)
        CALL XERTST(result11 .EQ. 0.0_R8, &
&             'faulty argument rcx: nonzero for unrelated species')
      END IF
    END DO
  END IF
!
!tmp.dpc
!       write(*,*) 'DPC: b2stcx: is0 ',is0
!       do is = 1, ns-1
!        write(*,*) 'is,lnext,damax(rcx) ',
!     1   is,lnext(is-1,is),damax(n2,rcx(-1,-1,is),1)
!       enddo
!tmp.dpc
!
! ..compute source terms
!   ..initialise sources to 0
  arg1 = ncv*2*ns
  DO nd=1,nbdirsmax
    dummyzerodiffd(nd) = 0.D0
  END DO
  CALL SFILL_DV(arg1, 0.0_R8, dummyzerodiffd, sna0, sna0d, 1, nbdirs)
  arg1 = ncv*4*ns
  DO nd=1,nbdirsmax
    dummyzerodiffd0(nd) = 0.D0
  END DO
  CALL SFILL_DV(arg1, 0.0_R8, dummyzerodiffd0, smq0, smq0d, 1, nbdirs)
  arg1 = ncv*4
  DO nd=1,nbdirsmax
    dummyzerodiffd1(nd) = 0.D0
  END DO
  CALL SFILL_DV(arg1, 0.0_R8, dummyzerodiffd1, shi0, shi0d, 1, nbdirs)
  arg1 = ncv*4
  DO nd=1,nbdirsmax
    dummyzerodiffd2(nd) = 0.D0
  END DO
  CALL SFILL_DV(arg1, 0.0_R8, dummyzerodiffd2, shn0, shn0d, 1, nbdirs)
!   ..compute sources due to charge exchange
  DO is=1,ns-1
    IF (LNEXT(is - 1, is)) THEN
!     ..particle source
      IF (is - 1 .NE. is0) THEN
!        (This is a special case: charge exchange between H0 and H1,
!        for which there is no particle source.)
        DO icv=1,mpg%nci
          IF (mpg%cvreg(icv) .NE. 0) THEN
            t0 = rcx(icv, is)*geo%cvvol(icv)
!        ..compute particle source
            temp = t0*na(icv, is0)
            DO nd=1,nbdirs
!        ..compute rate for is->is-1 and is0->is0+1
              t0d(nd) = geo%cvvol(icv)*rcxd(nd, icv, is)
              sna0d(nd, icv, 0, is-1) = sna0d(nd, icv, 0, is-1) + na(icv&
&               , is)*(na(icv, is0)*t0d(nd)+t0*nad(nd, icv, is0)) + temp&
&               *nad(nd, icv, is)
              sna0d(nd, icv, 1, is) = sna0d(nd, icv, 1, is) - na(icv, &
&               is0)*t0d(nd) - t0*nad(nd, icv, is0)
              sna0d(nd, icv, 1, is0) = sna0d(nd, icv, 1, is0) - na(icv, &
&               is)*t0d(nd) - t0*nad(nd, icv, is)
            END DO
            sna0(icv, 0, is-1) = sna0(icv, 0, is-1) + temp*na(icv, is)
            temp = t0*na(icv, is0)
            DO nd=1,nbdirs
              sna0d(nd, icv, 0, is0+1) = sna0d(nd, icv, 0, is0+1) + na(&
&               icv, is)*(na(icv, is0)*t0d(nd)+t0*nad(nd, icv, is0)) + &
&               temp*nad(nd, icv, is)
            END DO
            sna0(icv, 0, is0+1) = sna0(icv, 0, is0+1) + temp*na(icv, is)
            sna0(icv, 1, is) = sna0(icv, 1, is) - t0*na(icv, is0)
            sna0(icv, 1, is0) = sna0(icv, 1, is0) - t0*na(icv, is)
            rcxna(icv, is) = rcxna(icv, is) + t0*na(icv, is0)*na(icv, is&
&             )
            rcxna(icv, is0) = rcxna(icv, is0) - t0*na(icv, is0)*na(icv, &
&             is)
            rcxnareg(mpg%cvreg(icv), is) = rcxnareg(mpg%cvreg(icv), is) &
&             + t0*na(icv, is0)*na(icv, is)
            rcxnareg(mpg%cvreg(icv), is0) = rcxnareg(mpg%cvreg(icv), is0&
&             ) - t0*na(icv, is0)*na(icv, is)
          END IF
        END DO
      END IF
!     ..heat source
      DO icv=1,mpg%nci
        IF (mpg%cvreg(icv) .NE. 0) THEN
          DO nd=1,nbdirs
!       ..compute rate for is->is-1 and is0->is0+1
            t0d(nd) = geo%cvvol(icv)*rcxd(nd, icv, is)
!       ..compute atom heat source
            du0d(nd) = uad(nd, icv, is) - uad(nd, icv, is-1)
            du1d(nd) = uad(nd, icv, is0) - uad(nd, icv, is0+1)
          END DO
          t0 = rcx(icv, is)*geo%cvvol(icv)
          du0 = ua(icv, is) - ua(icv, is-1)
          du1 = ua(icv, is0) - ua(icv, is0+1)
          IF (switch%tn_style .EQ. 0) THEN
            temp = (am(is)+am(is-1))*(du0*du0)/2.0_R8 + (am(is0)+am(is0+&
&             1))*(du1*du1)/2.0_R8
            temp0 = t0*na(icv, is0)/2.0_R8
            temp1 = temp0*na(icv, is)
            DO nd=1,nbdirs
              t1d(nd) = mp*(1.0_R8-switch%boris)*(temp*(na(icv, is)*(na(&
&               icv, is0)*t0d(nd)/2.0_R8+t0*nad(nd, icv, is0)/2.0_R8)+&
&               temp0*nad(nd, icv, is))+temp1*((am(is)+am(is-1))*2*du0*&
&               du0d(nd)/2.0_R8+(am(is0)+am(is0+1))*2*du1*du1d(nd)/&
&               2.0_R8))
              shi0d(nd, icv, 0) = shi0d(nd, icv, 0) + t1d(nd)
            END DO
            t1 = mp*(1.0_R8-switch%boris)*(temp1*temp)
            shi0(icv, 0) = shi0(icv, 0) + t1
          ELSE IF (switch%tn_style .EQ. 1) THEN
            IF (is_neutral(is-1)) THEN
! CX with singly-charged ion
              temp1 = mp*(am(is0)+am(is0+1))
              DO nd=1,nbdirs
                tkind(nd) = temp1*2*du1*du1d(nd)/2.0_R8**2
              END DO
              tkin = temp1*(du1*du1/(2.0_R8*2.0_R8))
              temp1 = na(icv, is0)*na(icv, is)
              DO nd=1,nbdirs
                t1d(nd) = t0*tkin*(na(icv, is)*nad(nd, icv, is0)+na(icv&
&                 , is0)*nad(nd, icv, is)) + temp1*(tkin*t0d(nd)+t0*&
&                 tkind(nd))
              END DO
              t1 = temp1*(t0*tkin)
            ELSE
              tkin = mp/2.0_R8*((am(is)+am(is-1))/2.0_R8*du0**2+(am(is0)&
&               +am(is0+1))/2.0_R8*du1**2)
              temp1 = tkin + 1.5_R8*tn(icv)
              temp0 = na(icv, is)*temp1
              temp = t0*na(icv, is0)
              DO nd=1,nbdirs
! CX with multiply-charged ion
                tkind(nd) = mp*((am(is)+am(is-1))*2*du0*du0d(nd)/2.0_R8+&
&                 (am(is0)+am(is0+1))*2*du1*du1d(nd)/2.0_R8)/2.0_R8
                t1d(nd) = temp0*(na(icv, is0)*t0d(nd)+t0*nad(nd, icv, &
&                 is0)) + temp*(temp1*nad(nd, icv, is)+na(icv, is)*(&
&                 tkind(nd)+1.5_R8*tnd(nd, icv)))
              END DO
              t1 = temp*temp0
            END IF
            DO nd=1,nbdirs
              shi0d(nd, icv, 0) = shi0d(nd, icv, 0) + t1d(nd)
            END DO
            shi0(icv, 0) = shi0(icv, 0) + t1
          ELSE
!tn_style=2
            IF (NINT(zn(is)) .EQ. 1 .AND. NINT(zn(is0)) .EQ. 1) THEN
! CX between hydrogenic species
              temp1 = mp*(am(is0)+am(is0+1))
              DO nd=1,nbdirs
                tkind(nd) = temp1*2*du1*du1d(nd)/2.0_R8**2
              END DO
              tkin = temp1*(du1*du1/(2.0_R8*2.0_R8))
              temp2 = 1.5_R8*(tn(icv)-ti(icv)) + tkin
              temp1 = t0*na(icv, is0)
              temp0 = temp1*na(icv, is)
              DO nd=1,nbdirs
                t1d(nd) = temp2*(na(icv, is)*(na(icv, is0)*t0d(nd)+t0*&
&                 nad(nd, icv, is0))+temp1*nad(nd, icv, is)) + temp0*(&
&                 1.5_R8*(tnd(nd, icv)-tid(nd, icv))+tkind(nd))
              END DO
              t1 = temp0*temp2
              temp1 = mp*(am(is)+am(is-1))
              DO nd=1,nbdirs
                tkind(nd) = temp1*2*du0*du0d(nd)/2.0_R8**2
              END DO
              tkin = temp1*(du0*du0/(2.0_R8*2.0_R8))
              temp2 = 1.5_R8*(ti(icv)-tn(icv)) + tkin
              temp1 = t0*na(icv, is0)
              temp0 = temp1*na(icv, is)
              DO nd=1,nbdirs
                t1nd(nd) = temp2*(na(icv, is)*(na(icv, is0)*t0d(nd)+t0*&
&                 nad(nd, icv, is0))+temp1*nad(nd, icv, is)) + temp0*(&
&                 1.5_R8*(tid(nd, icv)-tnd(nd, icv))+tkind(nd))
              END DO
              t1n = temp0*temp2
            ELSE IF (NINT(zn(is0)) .EQ. 1) THEN
              tkin = mp/2.0_R8*((am(is)+am(is-1))/2.0_R8*du0**2+(am(is0)&
&               +am(is0+1))/2.0_R8*du1**2)
              temp1 = tkin + 1.5_R8*tn(icv)
              temp0 = na(icv, is)*temp1
              temp = t0*na(icv, is0)
              DO nd=1,nbdirs
! only is0/is0+1 hydrogenic
                tkind(nd) = mp*((am(is)+am(is-1))*2*du0*du0d(nd)/2.0_R8+&
&                 (am(is0)+am(is0+1))*2*du1*du1d(nd)/2.0_R8)/2.0_R8
                t1d(nd) = temp0*(na(icv, is0)*t0d(nd)+t0*nad(nd, icv, &
&                 is0)) + temp*(temp1*nad(nd, icv, is)+na(icv, is)*(&
&                 tkind(nd)+1.5_R8*tnd(nd, icv)))
              END DO
              t1 = temp*temp0
              tkin = mp/2.0_R8*((am(is)+am(is-1))/2.0_R8)*du0**2
              temp1 = na(icv, is0)*na(icv, is)
              DO nd=1,nbdirs
                t1nd(nd) = -(1.5_R8*(t0*tn(icv)*(na(icv, is)*nad(nd, icv&
&                 , is0)+na(icv, is0)*nad(nd, icv, is))+temp1*(tn(icv)*&
&                 t0d(nd)+t0*tnd(nd, icv))))
              END DO
              t1n = -(1.5_R8*(temp1*(t0*tn(icv))))
            ELSE IF (NINT(zn(is)) .EQ. 1) THEN
! only is/is-1 hydrogenic
              temp1 = mp*(am(is0)+am(is0+1))
              DO nd=1,nbdirs
                tkind(nd) = temp1*2*du1*du1d(nd)/2.0_R8**2
              END DO
              tkin = temp1*(du1*du1/(2.0_R8*2.0_R8))
              temp1 = tkin - 1.5_R8*ti(icv)
              temp0 = na(icv, is)*temp1
              temp = t0*na(icv, is0)
              DO nd=1,nbdirs
                t1d(nd) = temp0*(na(icv, is0)*t0d(nd)+t0*nad(nd, icv, &
&                 is0)) + temp*(temp1*nad(nd, icv, is)+na(icv, is)*(&
&                 tkind(nd)-1.5_R8*tid(nd, icv)))
              END DO
              t1 = temp*temp0
              temp1 = mp*(am(is)+am(is-1))
              DO nd=1,nbdirs
                tkind(nd) = temp1*2*du0*du0d(nd)/2.0_R8**2
              END DO
              tkin = temp1*(du0*du0/(2.0_R8*2.0_R8))
              temp2 = 1.5_R8*ti(icv) + tkin
              temp1 = na(icv, is)*temp2
              temp0 = t0*na(icv, is0)
              DO nd=1,nbdirs
                t1nd(nd) = temp1*(na(icv, is0)*t0d(nd)+t0*nad(nd, icv, &
&                 is0)) + temp0*(temp2*nad(nd, icv, is)+na(icv, is)*(&
&                 1.5_R8*tid(nd, icv)+tkind(nd)))
              END DO
              t1n = temp0*temp1
            ELSE
! no hydrogenic species involved: standard treatment
              temp1 = (am(is)+am(is-1))*(du0*du0)/2.0_R8 + (am(is0)+am(&
&               is0+1))*(du1*du1)/2.0_R8
              temp0 = t0*na(icv, is0)/2.0_R8
              temp = temp0*na(icv, is)
              DO nd=1,nbdirs
                t1d(nd) = mp*(1.0_R8-switch%boris)*(temp1*(na(icv, is)*(&
&                 na(icv, is0)*t0d(nd)/2.0_R8+t0*nad(nd, icv, is0)/&
&                 2.0_R8)+temp0*nad(nd, icv, is))+temp*((am(is)+am(is-1)&
&                 )*2*du0*du0d(nd)/2.0_R8+(am(is0)+am(is0+1))*2*du1*du1d&
&                 (nd)/2.0_R8))
              END DO
              t1 = mp*(1.0_R8-switch%boris)*(temp*temp1)
              t1n = 0.0_R8
              DO nd=1,nbdirsmax
                t1nd(nd) = 0.D0
              END DO
            END IF
            DO nd=1,nbdirs
              shi0d(nd, icv, 0) = shi0d(nd, icv, 0) + t1d(nd)
              shn0d(nd, icv, 0) = shn0d(nd, icv, 0) + t1nd(nd)
            END DO
            shi0(icv, 0) = shi0(icv, 0) + t1
            shn0(icv, 0) = shn0(icv, 0) + t1n
          END IF
          rcxhi(icv, is) = rcxhi(icv, is) + t1
          rcxhireg(mpg%cvreg(icv), is) = rcxhireg(mpg%cvreg(icv), is) + &
&           t1
        END IF
      END DO
!     ..parallel momentum source
      rg0 = switch%b2stcx_rg0
!!   (rg0 for numerical stabilisation; needs experiments.) (default = 1.0)
      DO icv=1,mpg%nci
        IF (mpg%cvreg(icv) .NE. 0) THEN
!       ..compute rate for is->is-1 and is0->is0+1
!srv 09.01.01
          temp1 = geo%cvvol(icv)*geo%cvhz(icv)
          temp0 = rcx(icv, is)*na(icv, is0)
          DO nd=1,nbdirs
            t0d(nd) = temp1*(na(icv, is)*(na(icv, is0)*rcxd(nd, icv, is)&
&             +rcx(icv, is)*nad(nd, icv, is0))+temp0*nad(nd, icv, is))
            t1d(nd) = (am(is)+am(is-1))*mp*t0d(nd)/2.0_R8
            t2d(nd) = (am(is0)+am(is0+1))*mp*t0d(nd)/2.0_R8
          END DO
          t0 = temp1*(temp0*na(icv, is))
          t1 = (am(is)+am(is-1))/2.0_R8*mp*t0
          t2 = (am(is0)+am(is0+1))/2.0_R8*mp*t0
          IF (switch%b2stcx_styl0 .EQ. 0) THEN
            temp1 = ua(icv, is) + rg0*ua(icv, is-1)
            DO nd=1,nbdirs
!       ..compute parallel momentum source
              smq0d(nd, icv, 0, is) = smq0d(nd, icv, 0, is) + rg0*(ua(&
&               icv, is)*t1d(nd)+t1*uad(nd, icv, is))
              smq0d(nd, icv, 1, is) = smq0d(nd, icv, 1, is) - (rg0+&
&               1.0_R8)*t1d(nd)
              smq0d(nd, icv, 0, is-1) = smq0d(nd, icv, 0, is-1) + temp1*&
&               t1d(nd) + t1*(uad(nd, icv, is)+rg0*uad(nd, icv, is-1))
              smq0d(nd, icv, 1, is-1) = smq0d(nd, icv, 1, is-1) - rg0*&
&               t1d(nd)
              smq0d(nd, icv, 1, is0+1) = smq0d(nd, icv, 1, is0+1) - rg0*&
&               t2d(nd)
              smq0d(nd, icv, 0, is0) = smq0d(nd, icv, 0, is0) + rg0*(ua(&
&               icv, is0)*t2d(nd)+t2*uad(nd, icv, is0))
              smq0d(nd, icv, 1, is0) = smq0d(nd, icv, 1, is0) - (rg0+&
&               1.0_R8)*t2d(nd)
            END DO
            smq0(icv, 0, is) = smq0(icv, 0, is) + rg0*t1*ua(icv, is)
            smq0(icv, 1, is) = smq0(icv, 1, is) - (1.0_R8+rg0)*t1
            smq0(icv, 0, is-1) = smq0(icv, 0, is-1) + t1*temp1
            smq0(icv, 1, is-1) = smq0(icv, 1, is-1) - rg0*t1
            temp1 = ua(icv, is0) + rg0*ua(icv, is0+1)
            DO nd=1,nbdirs
              smq0d(nd, icv, 0, is0+1) = smq0d(nd, icv, 0, is0+1) + &
&               temp1*t2d(nd) + t2*(uad(nd, icv, is0)+rg0*uad(nd, icv, &
&               is0+1))
            END DO
            smq0(icv, 0, is0+1) = smq0(icv, 0, is0+1) + t2*temp1
            smq0(icv, 1, is0+1) = smq0(icv, 1, is0+1) - rg0*t2
