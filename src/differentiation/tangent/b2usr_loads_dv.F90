!VK CORRECT CALCULATION OF THE HEAT LOADS IN B2
!
SUBROUTINE B2USR_LOADS_NODIFF(nx, ny, ns, nxtl, nxtr, boris, wtarg_max)
  USE B2MOD_TYPES
  USE B2MOD_GEO_DIFFV
  USE B2MOD_RATES
  USE B2MOD_PLASMA_DIFFV
  USE B2MOD_INDIRECT
  USE B2MOD_EXTERNAL_DIFFV
  USE B2MOD_CONSTANTS
  USE B2MOD_BOUNDARY_NAMELIST_DIFFV
  USE B2MOD_NEUTRALS_NAMELIST_DIFFV
  USE B2MOD_DIMENSIONS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!
  INTEGER :: nx, ny, ns, nxtl, nxtr
  REAL(kind=r8) :: boris
!
  INTEGER, PARAMETER :: ntrgm=def_ncut
!
  REAL(kind=r8), INTENT(OUT) :: wtarg_max(ntrgm)
!
!AREAS OF SURFACE ELEMENTS
  REAL(kind=r8), SAVE :: strg(0:def_nyd-1, ntrgm)
!POLOIDAL INDEX FOR EACH TARGET
  INTEGER, SAVE :: ltrgpos(ntrgm)
!NUMBER OF TARGETS
  INTEGER, SAVE :: ntrgs, shift(ntrgm)
  REAL(kind=r8) :: wtarg(0:ny-1, ntrgm), wnutrg(0:ny-1, ntrgm), wnutpr(0&
& :ny-1, ntrgm)
  INTEGER :: k, j, l, iy, is
  REAL(kind=r8) :: hlp
  CHARACTER(len=32) :: frm
!
  LOGICAL :: ifirst
  INTRINSIC ABS
  REAL(kind=r8) :: x1
  REAL(kind=r8) :: x2
  REAL(kind=r8) :: x3
  REAL(kind=r8) :: abs0
  REAL(kind=r8) :: abs1
  DATA ifirst /.true./
!
  IF (ifirst) THEN
! PREPARING GEOMETRY DATA
    IF (nncut .LE. 1) THEN
      ntrgs = 2
    ELSE
      ntrgs = 4
    END IF
! A PIECE OF CODE FROM B2PLOT
    ltrgpos(1) = -1
    k = 2
!}
    IF (ntrgs .EQ. 4) THEN
      ltrgpos(2) = nxtl
      ltrgpos(3) = nxtr + 1
      k = 4
    END IF
    ltrgpos(k) = nx
! AREAS OF THE SURFACE ELEMENTS
    l = 0
!{
    DO j=1,nncut*2
      k = ltrgpos(j)
      wtarg_max(j) = 0.0_R8
      DO iy=0,ny-1
        strg(iy, j) = gs(k+l, iy, 0)
      END DO
      CALL B2NEUT_IND(2, k + l, 1, ny, shift(j))
!index range in the Eirene arrays
      IF (shift(j) .LT. 0) THEN
        WRITE(*, *) 'Error in b2usr_loads: the EIRENE data on '//&
&       'neutral fluxes is incorrect. ', &
&       'No neutral fluxes will be calculated.'
      ELSE
        CALL B2NEUT_SURFCHECK(strg(0, j), j, ny, shift(j))
      END IF
      l = 1 - l
    END DO
!}
    ifirst = .false.
  END IF
!
  WRITE(*, *) 'Calculating incident power flux in b2usr_loads'
!
  l = 0
!{
  DO j=1,ntrgs
    k = ltrgpos(j)
    CALL B2NEUT_ENG(wnutrg(0, j), wnutpr(0, j), ny, shift(j))
    wtarg_max(j) = 0.0_R8
    DO iy=0,ny-1
      x1 = fhe(k+l, iy, 0, 0) + fhi(k+l, iy, 0, 0) + fhi_ext(k+l, iy, 0&
&       , 0)
      IF (x1 .GE. 0.) THEN
        hlp = x1
      ELSE
        hlp = -x1
      END IF
      DO is=0,ns-1
        x2 = fna(k+l, iy, 0, 0, is)*(rpt(k, iy, is)*ev+(1.0_R8-boris)*&
&         kinrgy(k, iy, is))
        IF (x2 .GE. 0.) THEN
          abs0 = x2
        ELSE
          abs0 = -x2
        END IF
        hlp = hlp + abs0
      END DO
      DO is=0,ns_ext-1
        x3 = fa_ext(k+l, iy, 0, 0, is)*(pt_ext(k, iy, is)*ev+(1.0_R8-&
&         boris)*(0.5_R8*am_ext(is)*mp)*ua_ext(k, iy, is)**2)
        IF (x3 .GE. 0.) THEN
          abs1 = x3
        ELSE
          abs1 = -x3
        END IF
        hlp = hlp + abs1
      END DO
      hlp = hlp/strg(iy, j)
      wtarg(iy, j) = hlp + wnutrg(iy, j) - wnutpr(iy, j)
      IF (wtarg(iy, j) .GT. wtarg_max(j)) wtarg_max(j) = wtarg(iy, j)
    END DO
!sw 02jan2017 corrected flip of l variable (i.e.put it AFTER iy loop)
    l = 1 - l
  END DO
!}
!
  IF (debug_flags(81) .GT. 0) THEN
    WRITE(frm, 700) ntrgs
! total power
    OPEN(3, file='targb2.datv') 
    DO iy=0,ny-1
      WRITE(3, frm) (wtarg(iy, j), j=1,ntrgs)
    END DO
    CLOSE(3) 
! neutrals
    OPEN(3, file='targb2n.datv') 
    DO iy=0,ny-1
      WRITE(3, frm) (wnutrg(iy, j), j=1,ntrgs)
    END DO
    CLOSE(3) 
! plasma
    DO j=1,ntrgs
      DO iy=0,ny-1
        wtarg(iy, j) = wtarg(iy, j) - wnutrg(iy, j)
      END DO
    END DO
    OPEN(3, file='targb2pl.datv') 
    DO iy=0,ny-1
      WRITE(3, frm) (wtarg(iy, j), j=1,ntrgs)
    END DO
    CLOSE(3) 
  END IF
  RETURN
 700 FORMAT('(',i3,'(2x,e15.7))')
END SUBROUTINE B2USR_LOADS_NODIFF

