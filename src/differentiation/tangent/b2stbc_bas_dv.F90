          END IF
!xpb
!xpb
!sv
          smo0(nx, iy, 2, is) = smo0(nx, iy, 2, is) + cbsmo(1, is, ireg)&
&           *csbm*pbs(nx, iy, 0) + cbsmo(2, is, ireg)*cstm*pbs(nx, iy, 0&
&           ) + cbsmo(5, is, ireg)*max26/na(nx, iy, is)
          IF (0.0_R8 .LT. fna(nx, iy, 0, 0, is)) THEN
            max27 = fna(nx, iy, 0, 0, is)
          ELSE
            max27 = 0.0_R8
          END IF
          smo0(nx, iy, 3, is) = smo0(nx, iy, 3, is) + cbsmo(3, is, ireg)&
&           *s0 + cbsmo(4, is, ireg)*s1 + cbsmo(6, is, ireg)*max27/na(nx&
&           , iy, is)
!     ..compute atom heat source
          t0 = na(nx, iy, is)/ni(nx, iy, 0)
          IF (0.0_R8 .LT. -fna(nx, iy, 0, 0, is)) THEN
            max28 = -fna(nx, iy, 0, 0, is)
          ELSE
            max28 = 0.0_R8
          END IF
          shi0(nx, iy, 0) = shi0(nx, iy, 0) + cbshi(0, is, ireg)*t0*s0 +&
&           cbshi(6, is, ireg)*max28
          shi0(nx, iy, 1) = shi0(nx, iy, 1) + cbshi(1, is, ireg)*t0*s0
          IF (0.0_R8 .LT. fna(nx, iy, 0, 0, is)) THEN
            max29 = fna(nx, iy, 0, 0, is)
          ELSE
            max29 = 0.0_R8
          END IF
          shi0(nx, iy, 3) = shi0(nx, iy, 3) + cbshi(2, is, ireg)*t0*csbm&
&           *s0 + cbshi(3, is, ireg)*t0*csbm*s1 + cbshi(4, is, ireg)*t0*&
&           cstm*s0 + cbshi(5, is, ireg)*t0*cstm*s1 + cbshi(7, is, ireg)&
&           *max29/ni(nx, iy, 0)
        END DO
        shi_test = shi0(nx, iy, 0) + shi0(nx, iy, 1)*ti(nx, iy) + shi0(&
&         nx, iy, 2)*ni(nx, iy, 0) + shi0(nx, iy, 3)*ni(nx, iy, 0)*ti(nx&
&         , iy)
        IF (shi_test .GT. 0.0_R8 .AND. diagno .GE. 2) WRITE(*, *) &
&                                                     'shi_test>0: E ', &
&                                                     iy, shi0(nx, iy, :&
&                                                     ), ti(nx, iy)/qe, &
&                                                     ni(nx, iy, 0), s0&
&                                                     , s1, csbm, cstm, &
&                                                     ireg, cbshi(:, :, &
&                                                     ireg)
        IF (0.0_R8 .LT. -fne(nx, iy, 0, 0)) THEN
          max30 = -fne(nx, iy, 0, 0)
        ELSE
          max30 = 0.0_R8
        END IF
!    ..compute electron heat source
        she0(nx, iy, 0) = she0(nx, iy, 0) + cbshe(0, ireg)*s0 + cbshe(6&
&         , ireg)*max30
        she0(nx, iy, 1) = she0(nx, iy, 1) + cbshe(1, ireg)*s0
        IF (0.0_R8 .LT. fne(nx, iy, 0, 0)) THEN
          max31 = fne(nx, iy, 0, 0)
        ELSE
          max31 = 0.0_R8
        END IF
        she0(nx, iy, 3) = she0(nx, iy, 3) + cbshe(2, ireg)*vte*s0 + &
&         cbshe(3, ireg)*vte*s1 + cbshe(4, ireg)*cstm*s0 + cbshe(5, ireg&
&         )*cstm*s1 + cbshe(7, ireg)*max31/ne(nx, iy)
        she_test = she0(nx, iy, 0) + she0(nx, iy, 1)*te(nx, iy) + she0(&
&         nx, iy, 2)*ne(nx, iy) + she0(nx, iy, 3)*ne(nx, iy)*te(nx, iy)
        IF (she_test .GT. 0.0_R8 .AND. diagno .GE. 2) WRITE(*, *) &
&                                                     'she_test>0: E ', &
&                                                     iy, she0(nx, iy, :&
&                                                     ), te(nx, iy)/qe, &
&                                                     ne(nx, iy), s0, s1&
&                                                     , vte, cstm, ireg&
&                                                     , cbshe(:, ireg)
!    ..compute charge source
        sch0(nx, iy, 2) = sch0(nx, iy, 2) + cbsch(0, ireg)*qe*vte*s0 + &
&         cbsch(2, ireg)*qe*vte*s1 + cbsch(4, ireg)*qe*cstm*s1
        sch0(nx, iy, 3) = sch0(nx, iy, 3) + cbsch(1, ireg)*qe**2*vte*s0/&
&         te(nx, iy) + cbsch(3, ireg)*qe**2*vte*s1/te(nx, iy) + cbsch(5&
&         , ireg)*qe**2*cstm*s1/te(nx, iy)
        IF (secmodel .EQ. 1) THEN
          IF (ne(nx, iy)/(rza(nx, iy, ismain)*na(nx, iy, ismain)) .GT. &
&             1.1_R8) THEN
            WRITE(*, *) 'Large ne contribution from impurities!'
            WRITE(*, *) 'Kinetic s.e.c. model may not be valid!'
          END IF
          CALL ESEEC0_NODIFF(tef, tif, nef, pof, wbbl(nx, iy, 0), wbbl(&
&                      nx, iy, 3), 0, nx, iy, nx, ny, ns, ismain, seec, &
&                      diagno)
!cfs
        ELSE
          seec = 1.0_R8 - cbsch(7, ireg)
        END IF
        IF (cbsch(6, ireg)*fchi .LT. (1.0_R8-seec)*fche) THEN
          max32 = (1.0_R8-seec)*fche
        ELSE
          max32 = cbsch(6, ireg)*fchi
        END IF
!       if (po(nx,iy).ge.0.0_R8) then                                    !sv
        t0 = max32*qe/tef
!       else                                                             !sv
!         t0 = 0.0_R8                                                    !sv
!       endif                                                            !sv
        sch0(nx, iy, 0) = sch0(nx, iy, 0) + (1.0_R8-seec)*fche - cbsch(6&
&         , ireg)*fchi + t0*po(nx, iy)
        sch0(nx, iy, 1) = sch0(nx, iy, 1) - t0
      END IF
    END IF
  END DO
!
! ..return
  IF (ncall .EQ. 0 .OR. main_call) ncall = ncall + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2STBC_BAS_NODIFF

