!-----------------------------------------------------------------------
!.end b2stel
!
!
  FUNCTION ROXA(icv, is)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    REAL(kind=r8) :: roxa
    INTEGER, INTENT(IN) :: icv, is
!
    roxa = am(is)*mp*pl%na(icv, is)
!
    RETURN
  END FUNCTION ROXA

