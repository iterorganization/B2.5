FUNCTION FLUXAV_NODIFF(quant, mpg, geo, ift) RESULT (fluxav)
!
!---------------------------------------------------------------------
!.documentation
!
!   1. purpose
!
!      FLUXAV computes the flux tube average of a quantity
!
!   2. parameters
!
!      quant - array(1:mpg%nCv) quantity to be averaged
!      iFt   - label of the flux tube
!
!   3. description
!
!      This function computes and returns the flux tube average
!      of the quantity quant on the flux tube with index iFt.
!      It assumes that the flux tube is closed, i.e., inside the core
!      or the the pedestal region.
!      The integration is made using the trapeze rule.
!----------------------------------------------------------------------
!.declarations
  USE B2MOD_TYPES
  USE B2US_MAP_DIFFV
  USE B2US_GEO_DIFFV
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
  TYPE(MAPPING), INTENT(IN) :: mpg
  TYPE(GEOMETRY), INTENT(IN) :: geo
!
  REAL(kind=r8) :: precision, fluxav
  PARAMETER (precision=1e-8_R8)
!   ..input arguments (unchanged on exit)
  INTEGER :: ift
  REAL(kind=r8) :: quant(mpg%ncv)
!   ..local variables
  INTEGER :: i, icv, icm, icp
  REAL(kind=r8) :: qm, qp, qsum, wsum
!   ..procedures
  EXTERNAL XERTST
  INTRINSIC ABS
  REAL(kind=r8) :: abs0
!
!.flux tube averaging
  CALL XERTST(1 .LE. ift .AND. ift .LE. mpg%nft, 'faulty argument iFt')
  qsum = 0.0_R8
  wsum = 0.0_R8
  DO i=1,mpg%ftcvp(ift, 2)
    icv = mpg%ftcv(mpg%ftcvp(ift, 1)+i-1)
    IF (mpg%cvonclosedsurface(icv)) THEN
      IF (i .GT. 1) THEN
        icm = mpg%ftcv(mpg%ftcvp(ift, 1)+i-2)
      ELSE
        icm = mpg%ftcv(mpg%ftcvp(ift, 1)+mpg%ftcvp(ift, 2)-1)
      END IF
      IF (i .LT. mpg%ftcvp(ift, 2)) THEN
        icp = mpg%ftcv(mpg%ftcvp(ift, 1)+i)
      ELSE
        icp = mpg%ftcv(mpg%ftcvp(ift, 1))
      END IF
      qm = (quant(icm)*geo%cvhx(icv)/geo%cvbb(icv, 0)+quant(icv)*geo%&
&       cvhx(icm)/geo%cvbb(icm, 0))/(geo%cvhx(icm)/geo%cvbb(icm, 0)+geo%&
&       cvhx(icv)/geo%cvbb(icv, 0))
      qp = (quant(icp)*geo%cvhx(icv)/geo%cvbb(icv, 0)+quant(icv)*geo%&
&       cvhx(icp)/geo%cvbb(icp, 0))/(geo%cvhx(icv)/geo%cvbb(icv, 0)+geo%&
&       cvhx(icp)/geo%cvbb(icp, 0))
      qsum = qsum + 0.5_R8*geo%cvhx(icv)/geo%cvbb(icv, 0)*(0.5_R8*(qm+&
&       quant(icv))+0.5_R8*(quant(icv)+qp))
      wsum = wsum + geo%cvhx(icv)/geo%cvbb(icv, 0)
    END IF
  END DO
  IF (wsum .GE. 0.) THEN
    abs0 = wsum
  ELSE
    abs0 = -wsum
  END IF
  IF (abs0 .LT. precision) THEN
! psauter
    fluxav = 0.0_R8
  ELSE
    fluxav = qsum/wsum
  END IF
!
!   ..return
  RETURN
END FUNCTION FLUXAV_NODIFF

