  SUBROUTINE PREP_GEOMETRY(nx, ny, ncv, nci, nfc, nvx, nfs, nft, nvmxcv&
&   , gm, mpg)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!
    INTEGER, INTENT(IN) :: nx, ny, ncv, nci, nfc, nvx, nfs, nft, nvmxcv
    INTEGER :: ix, iy, ic, ifc, ivx, ift
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(GEOMETRY), INTENT(OUT) :: gm
    INTRINSIC ABS
    INTRINSIC SUM
    INTRINSIC SIGN
    REAL(kind=r8), DIMENSION(4) :: arg1
    REAL(kind=r8) :: arg10
!
    CALL ALLOC_GEOMETRY(gm, ncv, nfc, nvx, nfs, nft, nvmxcv)
!
! cell volumes
    DO iy=-1,ny
      DO ix=-1,nx
        IF (mpg%imapcv(ix, iy) .NE. 0) THEN
! wdk sign/orientation of field now included in angles/unit vector cvEb
          ic = mpg%imapcv(ix, iy)
          WHERE (bb(ix, iy, 0:3) .GE. 0.0) 
            gm%cvbb(ic, 0:3) = bb(ix, iy, 0:3)
          ELSEWHERE
            gm%cvbb(ic, 0:3) = -bb(ix, iy, 0:3)
          END WHERE
          gm%cveb(ic, 0:2) = ebc(ix, iy, 0:2)
! cell center
          arg1(:) = crx(ix, iy, 0:3)
          gm%cvx(ic) = 0.25_R8*SUM(arg1(:))
          arg1(:) = cry(ix, iy, 0:3)
          gm%cvy(ic) = 0.25_R8*SUM(arg1(:))
          gm%cvsz(ic) = gs(ix, iy, 2)
          gm%cvhz(ic) = hz(ix, iy)
          gm%cvhx(ic) = hx(ix, iy)
! cos
          gm%cvqgam(ic, 0) = qz(ix, iy, 1)
! sin
          gm%cvqgam(ic, 1) = -qz(ix, iy, 0)
          gm%cvvol(ic) = vol(ix, iy)
          gm%cvonedbsq(ic) = onedbsq(ix, iy)
!wdk to check: need abs here?
          gm%cvbzb(ic) = bzb(ix, iy)
        END IF
      END DO
    END DO
!
! x faces
    DO iy=0,ny
      DO ix=0,nx
        IF (mpg%imapfcx(ix, iy) .NE. 0) THEN
! wdk sign of field now included in angles
          ifc = mpg%imapfcx(ix, iy)
          WHERE (wbbl(ix, iy, 0:3) .GE. 0.0) 
            gm%fcbb(ifc, 0:3) = wbbl(ix, iy, 0:3)
          ELSEWHERE
            gm%fcbb(ifc, 0:3) = -wbbl(ix, iy, 0:3)
          END WHERE
          gm%fcs(ifc) = gs(ix, iy, 0)
          gm%fchc(ifc, 1) = hc(leftix(ix, iy), leftiy(ix, iy), 2)
          gm%fchc(ifc, 2) = hc(ix, iy, 0)
          gm%fcht(ifc) = ht(ix, iy, 0)
          gm%fcvol(ifc, 1) = 0.5_R8*vol(leftix(ix, iy), leftiy(ix, iy))
          gm%fcvol(ifc, 2) = 0.5_R8*vol(ix, iy)
          gm%fcqgam(ifc, 0) = qc(ix, iy, 0)
          gm%fcqgam(ifc, 1) = -qs(ix, iy, 0)
          gm%fcqalf(ifc, 0) = qac(ix, iy, 0)
          gm%fcqalf(ifc, 1) = qas(ix, iy, 0)
          gm%fcqbet(ifc, 0) = gm%fcqgam(ifc, 0)*gm%fcqalf(ifc, 0) + gm%&
&           fcqgam(ifc, 1)*gm%fcqalf(ifc, 1)
! wdk account for negative toroidal field through sign of angles
          gm%fcqbet(ifc, 1) = gm%fcqgam(ifc, 1)*gm%fcqalf(ifc, 0) - gm%&
&           fcqgam(ifc, 0)*gm%fcqalf(ifc, 1)
          arg10 = wbbl(ix, iy, 2)
          gm%fcqalf(ifc, 1) = gm%fcqalf(ifc, 1)*SIGN(1.0_R8, arg10)
          arg10 = wbbl(ix, iy, 2)
          gm%fcqbet(ifc, 0) = gm%fcqbet(ifc, 0)*SIGN(1.0_R8, arg10)
          gm%fcpbs(ifc) = pbs(ix, iy, 0)
          gm%fcpbshz(ifc) = pbshz(ix, iy, 0)
        END IF
      END DO
    END DO
!
! y faces
    DO ix=0,nx
      DO iy=0,ny
        IF (mpg%imapfcy(ix, iy) .NE. 0) THEN
! wdk sign of field now included in angles
          ifc = mpg%imapfcy(ix, iy)
          WHERE (wbbv(ix, iy, 0:3) .GE. 0.0) 
            gm%fcbb(ifc, 0:3) = wbbv(ix, iy, 0:3)
          ELSEWHERE
            gm%fcbb(ifc, 0:3) = -wbbv(ix, iy, 0:3)
          END WHERE
          gm%fcs(ifc) = gs(ix, iy, 1)
          gm%fchc(ifc, 1) = hc(bottomix(ix, iy), bottomiy(ix, iy), 3)
          gm%fchc(ifc, 2) = hc(ix, iy, 1)
          gm%fcht(ifc) = ht(ix, iy, 1)
          gm%fcvol(ifc, 1) = 0.5_R8*vol(bottomix(ix, iy), bottomiy(ix, &
&           iy))
          gm%fcvol(ifc, 2) = 0.5_R8*vol(ix, iy)
          gm%fcqgam(ifc, 0) = qc(ix, iy, 1)
          gm%fcqgam(ifc, 1) = qs(ix, iy, 1)
          gm%fcqalf(ifc, 0) = qac(ix, iy, 1)
          gm%fcqalf(ifc, 1) = qas(ix, iy, 1)
          gm%fcqbet(ifc, 0) = gm%fcqgam(ifc, 0)*gm%fcqalf(ifc, 0) + gm%&
&           fcqgam(ifc, 1)*gm%fcqalf(ifc, 1)
! wdk account for negative toroidal field through sign of angles
          gm%fcqbet(ifc, 1) = gm%fcqgam(ifc, 1)*gm%fcqalf(ifc, 0) - gm%&
&           fcqgam(ifc, 0)*gm%fcqalf(ifc, 1)
          arg10 = wbbv(ix, iy, 2)
          gm%fcqalf(ifc, 1) = gm%fcqalf(ifc, 1)*SIGN(1.0_R8, arg10)
          arg10 = wbbv(ix, iy, 2)
          gm%fcqbet(ifc, 0) = gm%fcqbet(ifc, 0)*SIGN(1.0_R8, arg10)
          gm%fcpbs(ifc) = pbs(ix, iy, 1)
          gm%fcpbshz(ifc) = pbshz(ix, iy, 1)
        END IF
      END DO
    END DO
!
! vertices
    gm%vxx = 0._R8
    gm%vxy = 0._R8
    DO iy=0,ny
      DO ix=0,nx
        IF (mpg%imapvx(ix, iy) .NE. 0) THEN
          ivx = mpg%imapvx(ix, iy)
          WHERE (wbbc(ix, iy, 0:3) .GE. 0.0) 
            gm%vxbb(ivx, 0:3) = wbbc(ix, iy, 0:3)
          ELSEWHERE
            gm%vxbb(ivx, 0:3) = -wbbc(ix, iy, 0:3)
          END WHERE
          gm%vxx(ivx) = crx(ix, iy, 0)
          gm%vxy(ivx) = cry(ix, iy, 0)
          gm%vxffbz(ivx) = ffbz(ix, iy, 0)
          gm%vxfpsi(ivx) = fpsi(ix, iy, 0, 0)
        END IF
      END DO
    END DO
!
! Psi for each flux surface
    DEALLOCATE(gm%fspsi)
    ALLOCATE(gm%fspsi(mpg%nfs))
    gm%fspsi = 0.0_R8
    DO ift=1,nfs
      ifc = mpg%fsfc(mpg%fsfcp(ift, 1))
      gm%fspsi(ift) = gm%vxfpsi(mpg%fcvx(ifc, 1))
    END DO
! calculate connection length
    CALL CALC_CONN(mpg, gm)
  END SUBROUTINE PREP_GEOMETRY

