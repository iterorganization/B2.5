              END IF
            END IF
          END IF
        END IF
      END DO
    END DO
!
! Final pass: adapt local connectivity arrays of guard cells
    DO iy=-1,ny
      DO ix=-1,nx
        IF (ISGHOSTCELL(cflags(ix, iy, cellflag_type))) THEN
          IF (icorner(ix, iy) .EQ. 0) THEN
            bix = bottomix(ix, iy)
            biy = bottomiy(ix, iy)
            IF (biy .NE. -2) THEN
              IF (ISBOUNDARYCELL(cflags(bix, biy, cellflag_type))) THEN
! N guard cell
                IF (locrightix(ix, iy) .EQ. nx + 1) THEN
                  locrightix(ix, iy) = loctopix(rightix(bix, biy), &
&                   rightiy(bix, biy))
                  locrightiy(ix, iy) = loctopiy(rightix(bix, biy), &
&                   rightiy(bix, biy))
                END IF
              END IF
            END IF
            lix = leftix(ix, iy)
            liy = leftiy(ix, iy)
            IF (lix .NE. -2) THEN
              IF (ISBOUNDARYCELL(cflags(lix, liy, cellflag_type))) THEN
! For E guard cells
                IF (loctopiy(ix, iy) .EQ. ny + 1) THEN
                  loctopix(ix, iy) = locrightix(topix(lix, liy), topiy(&
&                   lix, liy))
                  loctopiy(ix, iy) = locrightiy(topix(lix, liy), topiy(&
&                   lix, liy))
                END IF
              END IF
            END IF
          END IF
        END IF
      END DO
    END DO
!
!
! Sanity check
    cellerror = .false.
    DO iy=-1,ny
      DO ix=-1,nx
        IF (ISREALCELL(cflags(ix, iy, cellflag_type))) THEN
          IF (locrightix(ix, iy) .EQ. nx + 1 .OR. locrightiy(ix, iy) &
&             .EQ. ny + 1) THEN
            WRITE(*, *) 'Real cell ix, iy = ', ix, iy
            WRITE(*, *) 'locrightix, locrightiy = ', locrightix(ix, iy)&
&           , locrightiy(ix, iy)
            cellerror = .true.
!              call xerrab ('Problem in definition locrightix.')
          END IF
          IF (loctopix(ix, iy) .EQ. nx + 1 .OR. loctopiy(ix, iy) .EQ. ny&
&             + 1) THEN
            WRITE(*, *) 'Real cell ix, iy = ', ix, iy
            WRITE(*, *) 'loctopix, loctopiy = ', loctopix(ix, iy), &
&           loctopiy(ix, iy)
            cellerror = .true.
!              call xerrab ('Problem in definition loctopiy.')
          END IF
        ELSE IF (ISGHOSTCELL(cflags(ix, iy, cellflag_type))) THEN
          IF (icorner(ix, iy) .EQ. 0) THEN
            bix = bottomix(ix, iy)
            biy = bottomiy(ix, iy)
            IF (biy .NE. -2) THEN
              IF (ISBOUNDARYCELL(cflags(bix, biy, cellflag_type))) THEN
! N guard cell
                IF (locrightix(ix, iy) .EQ. nx + 1 .OR. locrightiy(ix, &
&                   iy) .EQ. ny + 1) THEN
                  WRITE(*, *) 'Ghost cell ix, iy = ', ix, iy
                  WRITE(*, *) 'locrightix, locrightiy = ', locrightix(ix&
&                 , iy), locrightiy(ix, iy)
                  cellerror = .true.
!                    call xerrab ('Problem in definition locrightix.')
                END IF
              END IF
            END IF
            lix = leftix(ix, iy)
            liy = leftiy(ix, iy)
            IF (lix .NE. -2) THEN
              IF (ISBOUNDARYCELL(cflags(lix, liy, cellflag_type))) THEN
! For E guard cells
                IF (loctopix(ix, iy) .EQ. nx + 1 .OR. loctopiy(ix, iy) &
&                   .EQ. ny + 1) THEN
                  WRITE(*, *) 'Ghost cell ix, iy = ', ix, iy
                  WRITE(*, *) 'loctopix, loctopiy = ', loctopix(ix, iy)&
&                 , loctopiy(ix, iy)
                  cellerror = .true.
!                    call xerrab ('Problem in definition loctopiy.')
                END IF
              END IF
            END IF
          END IF
        END IF
      END DO
    END DO
!
    IF (cellerror) CALL XERRAB(&
&                        'Problem in definition local connectivity.')
!
    m%nvx = (nx+2)*(ny+2) - nghostvxi
    IF (1 .LT. (1+1*nncut)*(ny+2)) THEN
      m%nfs = (1+1*nncut)*(ny+2)
    ELSE
      m%nfs = 1
    END IF
    m%nft = m%nfs
!
! max. no. of entries in cvVx (vertex list of each cell)
    m%ncmxvx = 4*m%nci + 2*m%ncg
!
! max. no. of entries in cvFc (face list of each cell)
    m%ncmxfc = 4*m%nci + m%ncg
!
! max. no. of entries in vxCv (cell list of each vertex)
! corner vertices have only 3;
! each X-point vertex has 8; duplicate X-point vertices for slabs
!      m%nVmxCv = 4 * m%nVx - 4*nncut + 2*nncut*4
    m%nvmxcv = 4*(nx+1)*(ny+1) + 2*nncut*4
!
! max. no. of entries is vxFc (face list of each vertex)
! corner vertices have only 2;
! other vertices on boundaries have only 3;
! each X-point vertex has 8; duplicate X-point vertices for slabs
!      m%nVmxFc = 4 * m%nVx - 4*2*nncut - 2*((nx-1) + (ny-1)) + 2*nncut*4
    m%nvmxfc = 4*(nx+1)*(ny+1) - 2*(nx-1+(ny-1))
!
! max no. of entries in matrix
!      m%nCmxNv = 9*m%nCi - 4*nncut + ! internal cells (corners only 8)
!     .           6*m%nCg - 8*nncut + ! guard cells (at corners only 5)
!     .           2*8*nncut*4         ! correction for cells around X-points
!      m%nCmxNv = 9 * (nx+1) * (ny+1)
! there are 2 guard cells
    m%ncmxnv = 9*(nx+2)*(ny+2)
    WRITE(*, *) 'Mapping defined, with'
    WRITE(*, *) 'nCv, nCi, nCg  = ', m%ncv, m%nci, m%ncg
    WRITE(*, *) 'nFc, nVx       = ', m%nfc, m%nvx
    WRITE(*, *) 'nFs, nFt       = ', m%nfs, m%nft
    WRITE(*, *) 'nCmxFc, nCmxVx = ', m%ncmxfc, m%ncmxvx
    WRITE(*, *) 'nVmxFc, nVmxCv = ', m%nvmxfc, m%nvmxcv
    WRITE(*, *) 'nCmxNv         = ', m%ncmxnv
!
    CALL ALLOC_MAPPING(m)
!
! Compose the mapping matrix from structured to unstructured index
! for Cv's
! 0 means not in use in unstructured
    m%imapcv = 0
    ici = 0
    icg = m%nci
    DO iy=0,ny+1
ixloop:DO ix=0,nx+1
        IF (ISREALCELL(cflags(ix-1, iy-1, cellflag_type))) THEN
          ici = ici + 1
          m%imapcv(ix-1, iy-1) = ici
        ELSE IF (ISGHOSTCELL(cflags(ix-1, iy-1, cellflag_type))) THEN
          IF (icorner(ix-1, iy-1) .EQ. 0) THEN
            icg = icg + 1
            m%imapcv(ix-1, iy-1) = icg
          END IF
        END IF
      END DO ixloop
    END DO
!
    IF (ici .NE. m%nci) CALL XERRAB(&
&                   'Inconsistency between counting and mapping in nCi!'&
&                            )
    IF (icg .NE. m%ncv) CALL XERRAB(&
&                   'Inconsistency between counting and mapping in nCv!'&
&                            )
!
! for Fcx's and Fcy's
! 0 means not in use in unstructured
    m%imapfcx = 0
    ici = 0
    DO iy=0,ny
      DO ix=0,nx
        IF (ighostfcx(ix, iy) .EQ. 0) THEN
          ici = ici + 1
          m%imapfcx(ix, iy) = ici
        END IF
      END DO
    END DO
!
! 0 means not in use in unstructured
    m%imapfcy = 0
    DO ix=0,nx
      DO iy=0,ny
        IF (ighostfcy(ix, iy) .EQ. 0) THEN
          ici = ici + 1
          m%imapfcy(ix, iy) = ici
        END IF
      END DO
    END DO
!
    IF (ici .NE. m%nfc) CALL XERRAB(&
&                   'Inconsistency between counting and mapping in nFc!'&
&                            )
!
    ier = 0
    DO iy=-1,ny
      DO ix=-1,nx
        IF (ANY(cflags(ix, iy, cellflag_leftface:cellflag_topface) .NE. &
&           0)) THEN
! leftface
          IF (cflags(ix, iy, cellflag_leftface) .NE. 0) THEN
            lbl = cflags(ix, iy, cellflag_leftface)
            ifc = m%imapfcx(ix, iy)
            IF (ifc .GT. 0) THEN
              IF (m%fclbl(ifc) .EQ. 0) THEN
                m%fclbl(ifc) = lbl
              ELSE IF (m%fclbl(ifc) .NE. lbl) THEN
                WRITE(*, '(a,5i6)') 'LEFTFACE: ix,iy,iFc,fcLbl,cflags '&
&               , ix, iy, ifc, m%fclbl(ifc), lbl
                ier = ier + 1
              END IF
            END IF
          END IF
! rightface
          IF (cflags(ix, iy, cellflag_rightface) .NE. 0) THEN
            lbl = cflags(ix, iy, cellflag_rightface)
            IF (rightix(ix, iy) .LT. nx + 1) THEN
              ifc = m%imapfcx(rightix(ix, iy), rightiy(ix, iy))
            ELSE
              ifc = 0
            END IF
            IF (ifc .GT. 0) THEN
              IF (m%fclbl(ifc) .EQ. 0) THEN
                m%fclbl(ifc) = lbl
              ELSE IF (m%fclbl(ifc) .NE. lbl) THEN
                WRITE(*, '(a,5i6)') 'RIGHTFACE: ix,iy,iFc,fcLbl,cflags '&
&               , ix, iy, ifc, m%fclbl(ifc), lbl
                ier = ier + 1
              END IF
            END IF
          END IF
! bottomface
          IF (cflags(ix, iy, cellflag_bottomface) .NE. 0) THEN
            lbl = cflags(ix, iy, cellflag_bottomface)
            ifc = m%imapfcy(ix, iy)
            IF (ifc .GT. 0) THEN
              IF (m%fclbl(ifc) .EQ. 0) THEN
                m%fclbl(ifc) = lbl
              ELSE IF (m%fclbl(ifc) .NE. lbl) THEN
                WRITE(*, '(a,5i6)') &
&               'BOTTOMFACE: ix,iy,iFc,fcLbl,cflags ', ix, iy, ifc, m%&
&               fclbl(ifc), lbl
                ier = ier + 1
              END IF
            END IF
          END IF
! topface
          IF (cflags(ix, iy, cellflag_topface) .NE. 0) THEN
            lbl = cflags(ix, iy, cellflag_topface)
            IF (topiy(ix, iy) .LT. ny + 1) THEN
              ifc = m%imapfcy(topix(ix, iy), topiy(ix, iy))
            ELSE
              ifc = 0
            END IF
            IF (ifc .GT. 0) THEN
              IF (m%fclbl(ifc) .EQ. 0) THEN
                m%fclbl(ifc) = lbl
              ELSE IF (m%fclbl(ifc) .NE. lbl) THEN
                WRITE(*, '(a,5i6)') 'TOPFACE: ix,iy,iFc,fcLbl,cflags ', &
&               ix, iy, ifc, m%fclbl(ifc), lbl
                ier = ier + 1
              END IF
            END IF
          END IF
        END IF
      END DO
    END DO
!
    WRITE(*, *) 'face labels set ', COUNT(m%fclbl .NE. 0)
!
    WRITE(*, *) 'face labels '
    DO ifc=1,m%nfc
      IF (m%fclbl(ifc) .NE. 0) WRITE(*, *) ifc, m%fclbl(ifc)
    END DO
!
    IF (ier .GT. 0) CALL XERRAB('calc_mapping -- problem with fcLbl')
!
! for Vx
! 0 means not in use in unstructured
    m%imapvx = 0
    ici = 0
    DO iy=-1,ny
      DO ix=-1,nx
        IF (ighostvx(ix, iy) .EQ. 0) THEN
          ici = ici + 1
          m%imapvx(ix, iy) = ici
        END IF
      END DO
    END DO
!
    IF (ici .NE. m%nvx) CALL XERRAB(&
&                   'Inconsistency between counting and mapping in nVx!'&
&                            )
!
!     assign same vertex number to coinciding vertices
    WRITE(*, *) 'Merging vertices'
    DO iy=-1,ny
      DO ix=-1,nx
        ix2 = ix
        iy2 = iy
        DO WHILE (ighostvx(ix2, iy2) .EQ. 1)
          ix2o = ix2
          iy2o = iy2
          ix2 = ixmapghostvx(ix2o, iy2)
          iy2 = iymapghostvx(ix2o, iy2)
          IF (((ix2 .EQ. -2 .OR. ix2 .EQ. nx + 1) .OR. iy2 .EQ. -2) .OR.&
&             iy2 .EQ. ny + 1) GOTO 100
        END DO
 100    IF (iy2 .LT. -1) THEN
          x1 = -1
        ELSE
          x1 = iy2
        END IF
        IF (x1 .GT. ny) THEN
          min1 = ny
        ELSE
          min1 = x1
        END IF
        IF (ix2 .LT. -1) THEN
          x4 = -1
        ELSE
          x4 = ix2
        END IF
        IF (x4 .GT. nx) THEN
          min3 = nx
        ELSE
          min3 = x4
        END IF
        m%imapvx(ix, iy) = m%imapvx(min3, min1)
      END DO
    END DO
!
! consistency check
    DO iy=-1,ny
      DO ix=-1,nx
        IF (ighostvx(ix, iy) .EQ. 0 .AND. m%imapvx(ix, iy) .EQ. 0) THEN
          WRITE(*, *) 'ix, iy = ', ix, iy
          CALL XERRAB('Problem with vertex mapping')
        END IF
      END DO
    END DO
!
    WRITE(*, *) 'Merged vertices'
!
! store indices of corner coordinates
    m%icornvx = 0
    IF (ncorneri .GT. 0) THEN
!  sort corner points per ix position first
      DO j=ncorneri-1,1,-1
        swapped = .false.
        DO i=1,j
          IF (ixcorner(i) .GT. ixcorner(i+1)) THEN
            itmpx = ixcorner(i)
            itmpy = iycorner(i)
            ixcorner(i) = ixcorner(i+1)
            iycorner(i) = iycorner(i+1)
            ixcorner(i+1) = itmpx
            iycorner(i+1) = itmpy
            swapped = .true.
          END IF
        END DO
        IF (.NOT.swapped) GOTO 110
      END DO
!
!  now sort corner points in iy position
 110  DO j=ncorneri-1,1,-1
        swapped = .false.
        DO i=1,j
          IF (ixcorner(i) .EQ. ixcorner(i+1)) THEN
            IF (iycorner(i) .GT. iycorner(i+1)) THEN
              itmpx = ixcorner(i)
              itmpy = iycorner(i)
              ixcorner(i) = ixcorner(i+1)
              iycorner(i) = iycorner(i+1)
              ixcorner(i+1) = itmpx
              iycorner(i+1) = itmpy
              swapped = .true.
            END IF
          END IF
        END DO
        IF (.NOT.swapped) GOTO 120
      END DO
!
 120  icount = 0
      DO ic=1,ncorneri
        icount = icount + 1
        ix = ixcorner(ic)
        IF (COUNT(m%ictcntrl(1:m%nctcnt) .EQ. ix) .GT. 0) ix = ix + 1
        IF (ix .LT. 0) THEN
          x2 = 0
        ELSE
          x2 = ix
        END IF
        IF (x2 .GT. nx) THEN
          ix = nx
        ELSE
          ix = x2
        END IF
        iy = iycorner(ic)
        IF (iy .LT. 0) THEN
          x3 = 0
        ELSE
          x3 = iy
        END IF
        IF (x3 .GT. ny) THEN
          iy = ny
        ELSE
          iy = x3
        END IF
        m%icornvx(icount) = m%imapvx(ix, iy)
      END DO
    END IF
!
!
! no. of faces and vertices to be considered for this cell; only consider all internal and boundary cells are either quadrilatera
!l or triangle
    m%cvfcp(m%nci+1:m%ncv, 2) = 1
    m%cvvxp(m%nci+1:m%ncv, 2) = 2
!
    DO iy=-1,ny
      DO ix=-1,nx
        IF (m%imapcv(ix, iy) .NE. 0 .AND. m%imapcv(ix, iy) .LE. m%nci) &
&       THEN
          xco = crx(ix, iy, 0:3)
          yco = cry(ix, iy, 0:3)
          result11 = CELLGEOTYPE(xco, yco)
          IF (result11 .EQ. cgeo_quad) THEN
            m%cvfcp(m%imapcv(ix, iy), 2) = 4
            m%cvvxp(m%imapcv(ix, iy), 2) = 4
          ELSE
            m%cvfcp(m%imapcv(ix, iy), 2) = 3
            m%cvvxp(m%imapcv(ix, iy), 2) = 3
          END IF
        END IF
      END DO
    END DO
!
! starting index in the list of cell faces
! starting index for every cell in the cvVx list and its number of vertices
    m%cvfcp(1, 1) = 1
    m%cvvxp(1, 1) = 1
    DO i=2,m%ncv
      m%cvfcp(i, 1) = m%cvfcp(i-1, 1) + m%cvfcp(i-1, 2)
      m%cvvxp(i, 1) = m%cvvxp(i-1, 1) + m%cvvxp(i-1, 2)
    END DO
!
! list of cell faces and vertices for cells
! interpolation coefficients for interpolation to cell centers
    DO iy=0,ny+1
      DO ix=0,nx+1
        IF (m%imapcv(ix-1, iy-1) .NE. 0 .AND. m%imapcv(ix-1, iy-1) .LE. &
&           m%nci) THEN
          ic = m%imapcv(ix-1, iy-1)
          xco = crx(ix-1, iy-1, 0:3)
          yco = cry(ix-1, iy-1, 0:3)
          result11 = CELLGEOTYPE(xco, yco)
          SELECT CASE  (result11) 
          CASE (cgeo_quad) 
            if1 = m%cvfcp(ic, 1)
            if2 = if1 + 1
            if3 = if2 + 1
            if4 = if3 + 1
            m%cvfc(if1) = m%imapfcx(ix-1, iy-1)
            m%cvfc(if2) = m%imapfcx(rightix(ix-1, iy-1), rightiy(ix-1, &
&             iy-1))
            m%cvfc(if3) = m%imapfcy(ix-1, iy-1)
            m%cvfc(if4) = m%imapfcy(topix(ix-1, iy-1), topiy(ix-1, iy-1)&
&             )
            m%intcellp(if1) = 1.0_R8
            m%intcellp(if2) = 1.0_R8
            m%intcellp(if3) = 0.0_R8
            m%intcellp(if4) = 0.0_R8
            m%intcellr(if1) = 0.0_R8
            m%intcellr(if2) = 0.0_R8
            m%intcellr(if3) = 1.0_R8
            m%intcellr(if4) = 1.0_R8
            m%cvvx(if1) = m%imapvx(ix-1, iy-1)
            m%cvvx(if2) = m%imapvx(rightix(ix-1, iy-1), rightiy(ix-1, iy&
&             -1))
            m%cvvx(if3) = m%imapvx(topix(ix-1, iy-1), topiy(ix-1, iy-1))
            m%cvvx(if4) = m%imapvx(locrightix(loctopix(ix-1, iy-1), &
&             loctopiy(ix-1, iy-1)), locrightiy(loctopix(ix-1, iy-1), &
&             loctopiy(ix-1, iy-1)))
          CASE (cgeo_tria_noleft) 
            if1 = m%cvfcp(ic, 1)
            if2 = if1 + 1
            if3 = if2 + 1
            m%cvfc(if1) = m%imapfcx(rightix(ix-1, iy-1), rightiy(ix-1, &
&             iy-1))
            m%cvfc(if2) = m%imapfcy(ix-1, iy-1)
            m%cvfc(if3) = m%imapfcy(topix(ix-1, iy-1), topiy(ix-1, iy-1)&
&             )
            m%intcellp(if1) = 1.0_R8
            IF (fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 1, 0) .GE. 0.&
&           ) THEN
              abs0 = fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 1, 0)
            ELSE
              abs0 = -(fpsi(ix-1, iy-1, 2, 0)-fpsi(ix-1, iy-1, 1, 0))
            END IF
            IF (fpsi(ix-1, iy-1, 3, 0) - fpsi(ix-1, iy-1, 1, 0) .GE. 0.&
&           ) THEN
              abs9 = fpsi(ix-1, iy-1, 3, 0) - fpsi(ix-1, iy-1, 1, 0)
            ELSE
              abs9 = -(fpsi(ix-1, iy-1, 3, 0)-fpsi(ix-1, iy-1, 1, 0))
            END IF
            m%intcellp(if2) = abs0/abs9
            IF (fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 3, 0) .GE. 0.&
&           ) THEN
              abs1 = fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 3, 0)
            ELSE
              abs1 = -(fpsi(ix-1, iy-1, 2, 0)-fpsi(ix-1, iy-1, 3, 0))
            END IF
            IF (fpsi(ix-1, iy-1, 3, 0) - fpsi(ix-1, iy-1, 1, 0) .GE. 0.&
&           ) THEN
              abs10 = fpsi(ix-1, iy-1, 3, 0) - fpsi(ix-1, iy-1, 1, 0)
            ELSE
              abs10 = -(fpsi(ix-1, iy-1, 3, 0)-fpsi(ix-1, iy-1, 1, 0))
            END IF
            m%intcellp(if3) = abs1/abs10
            m%intcellr(if1) = 1.0_R8
            IF (fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 1, 0) .GE. 0.&
&           ) THEN
              abs2 = fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 1, 0)
            ELSE
              abs2 = -(fpsi(ix-1, iy-1, 2, 0)-fpsi(ix-1, iy-1, 1, 0))
            END IF
            IF (fpsi(ix-1, iy-1, 3, 0) - fpsi(ix-1, iy-1, 1, 0) .GE. 0.&
&           ) THEN
              abs11 = fpsi(ix-1, iy-1, 3, 0) - fpsi(ix-1, iy-1, 1, 0)
            ELSE
              abs11 = -(fpsi(ix-1, iy-1, 3, 0)-fpsi(ix-1, iy-1, 1, 0))
            END IF
            m%intcellr(if2) = abs2/abs11
            IF (fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 3, 0) .GE. 0.&
&           ) THEN
              abs3 = fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 3, 0)
            ELSE
              abs3 = -(fpsi(ix-1, iy-1, 2, 0)-fpsi(ix-1, iy-1, 3, 0))
            END IF
            IF (fpsi(ix-1, iy-1, 3, 0) - fpsi(ix-1, iy-1, 1, 0) .GE. 0.&
&           ) THEN
              abs12 = fpsi(ix-1, iy-1, 3, 0) - fpsi(ix-1, iy-1, 1, 0)
            ELSE
              abs12 = -(fpsi(ix-1, iy-1, 3, 0)-fpsi(ix-1, iy-1, 1, 0))
            END IF
            m%intcellr(if3) = abs3/abs12
            m%cvvx(if1) = m%imapvx(ix-1, iy-1)
            m%cvvx(if2) = m%imapvx(rightix(ix-1, iy-1), rightiy(ix-1, iy&
&             -1))
            m%cvvx(if3) = m%imapvx(locrightix(loctopix(ix-1, iy-1), &
