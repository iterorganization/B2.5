!srv 16.11.22 {
  SUBROUTINE CONV_MIDPLANE(nx, ny)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER :: nx, ny
    REAL(kind=r8) :: ro(2), zo(2), rzomp(2, 2)
    REAL(kind=r8) :: ri(2), zi(2), rzimp(2, 2)
! This subroutine calculates arrays rzomp and rzimp with the R-Z coordinates
! of two points laying on the straight line going through all
! radial outer and inner midlane cells
! Writes the coordinates into b2.user.parameters
    INTEGER :: ix, iy, iout, n_omp, n_imp
    INTRINSIC MIN
    INTRINSIC ABS
    EXTERNAL XERRAB
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: abs1
    REAL(kind=r8) :: abs2
    REAL(kind=r8) :: abs3
    REAL(kind=r8) :: abs4
    REAL(kind=r8) :: abs5
    REAL(kind=r8) :: abs6
    REAL(kind=r8) :: abs7
    REAL(kind=r8) :: abs8
    REAL(kind=r8) :: abs9
    REAL(kind=r8) :: abs10
    REAL(kind=r8) :: abs11
!
!
!   ..find the poloidal number of outer midplane cell as the cell with min Bz
!
    IF (nnreg(0) .EQ. 8) THEN
      n_omp = rightcut(2)
      IF (topcut(1) .GT. topcut(2)) THEN
        iy = topcut(2)
      ELSE
        iy = topcut(1)
      END IF
      DO ix=rightcut(2)+1,rightcut(1)-1
        IF (bb(n_omp, iy, 2) .EQ. 0.0_R8) n_omp = ix
        IF (bb(ix, iy, 2) .GE. 0.) THEN
          abs0 = bb(ix, iy, 2)
        ELSE
          abs0 = -bb(ix, iy, 2)
        END IF
        IF (bb(n_omp, iy, 2) .GE. 0.) THEN
          abs6 = bb(n_omp, iy, 2)
        ELSE
          abs6 = -bb(n_omp, iy, 2)
        END IF
        IF (abs0 .LT. abs6) n_omp = ix
      END DO
    ELSE IF (nnreg(0) .GE. 4) THEN
      n_omp = leftcut(1)
      iy = topcut(1)
      DO ix=leftcut(1)+1,rightcut(1)-1
        IF (bb(n_omp, iy, 2) .EQ. 0.0_R8) n_omp = ix
        IF (bb(ix, iy, 2) .GE. 0.) THEN
          abs1 = bb(ix, iy, 2)
        ELSE
          abs1 = -bb(ix, iy, 2)
        END IF
        IF (bb(n_omp, iy, 2) .GE. 0.) THEN
          abs7 = bb(n_omp, iy, 2)
        ELSE
          abs7 = -bb(n_omp, iy, 2)
        END IF
        IF (abs1 .LT. abs7) n_omp = ix
      END DO
    ELSE
      WRITE(*, *) ' conv_midplane: nnreg = ', nnreg(0)
      n_omp = (nx+2)/2
      iy = ny/2
      DO ix=-1,nx
        IF (bb(n_omp, iy, 2) .EQ. 0.0_R8) n_omp = ix
        IF (bb(ix, iy, 2) .GE. 0.) THEN
          abs2 = bb(ix, iy, 2)
        ELSE
          abs2 = -bb(ix, iy, 2)
        END IF
        IF (bb(n_omp, iy, 2) .GE. 0.) THEN
          abs8 = bb(n_omp, iy, 2)
        ELSE
          abs8 = -bb(n_omp, iy, 2)
        END IF
        IF (abs2 .LT. abs8) n_omp = ix
      END DO
    END IF
    WRITE(*, *) ' Found OMP location at ix = ', n_omp
    DO WHILE (ISREALCELL(cflags(n_omp, iy+1, cellflag_type)))
      iy = iy + 1
    END DO
! R-coordinate of center of [n_omp,-1] cell
    ro(1) = 0.25_R8*(crx(n_omp, -1, 0)+crx(n_omp, -1, 1)+crx(n_omp, -1, &
&     2)+crx(n_omp, -1, 3))
! Z-coordinate of center of [n_omp,-1] cell
    zo(1) = 0.25_R8*(cry(n_omp, -1, 0)+cry(n_omp, -1, 1)+cry(n_omp, -1, &
&     2)+cry(n_omp, -1, 3))
! R-coordinate of center of [n_omp,ny] cell
    ro(2) = 0.25_R8*(crx(n_omp, iy, 0)+crx(n_omp, iy, 1)+crx(n_omp, iy, &
&     2)+crx(n_omp, iy, 3))
! Z-coordinate of center of [n_omp,ny] cell
    zo(2) = 0.25_R8*(cry(n_omp, iy, 0)+cry(n_omp, iy, 1)+cry(n_omp, iy, &
&     2)+cry(n_omp, iy, 3))
    IF (ro(2) .GT. ro(1)) THEN
! take a coordinate to the left of ro(1)
      rzomp(1, 1) = ro(1)*0.9_R8
! calculate, corresponding to rzomp(1,1), Z-coordinate of point
! laying on the straight line going through the points ro(1),zo(1) and ro(2),zo(2)
      rzomp(2, 1) = zo(1) + (rzomp(1, 1)-ro(1))*(zo(2)-zo(1))/(ro(2)-ro(&
&       1))
! take a coordinate to the right of ro(2)
      rzomp(1, 2) = ro(2)*1.1_R8
! calculate, corresponding to rzomp(1,2), Z-coordinate of point
! laying on the straight line going through the points ro(1),zo(1) and ro(2),zo(2)
      rzomp(2, 2) = zo(1) + (rzomp(1, 2)-ro(1))*(zo(2)-zo(1))/(ro(2)-ro(&
&       1))
    ELSE
      WRITE(*, *) ' ro(1:2) :', ro(1), ro(2)
      WRITE(*, *) ' zo(1:2) :', zo(1), zo(2)
      CALL XERRAB('conv_midplane: unexpected coordinates for rzomp')
    END IF
!
!   ..find the poloidal number of the inner midplane cell as the cell with max Bz
!
    IF (nnreg(0) .EQ. 8) THEN
      n_imp = leftcut(2)
      IF (topcut(1) .GT. topcut(2)) THEN
        iy = topcut(2)
      ELSE
        iy = topcut(1)
      END IF
      DO ix=leftcut(1)+1,leftcut(2)-1
        IF (bb(n_imp, iy, 2) .EQ. 0.0_R8) n_imp = ix
        IF (bb(ix, iy, 2) .GE. 0.) THEN
          abs3 = bb(ix, iy, 2)
        ELSE
          abs3 = -bb(ix, iy, 2)
        END IF
        IF (bb(n_imp, iy, 2) .GE. 0.) THEN
          abs9 = bb(n_imp, iy, 2)
        ELSE
          abs9 = -bb(n_imp, iy, 2)
        END IF
        IF (abs3 .GT. abs9) n_imp = ix
      END DO
    ELSE IF (nnreg(0) .GE. 4) THEN
      n_imp = leftcut(1)
      iy = topcut(1)
      DO ix=leftcut(1)+1,rightcut(1)-1
        IF (bb(n_imp, iy, 2) .EQ. 0.0_R8) n_imp = ix
        IF (bb(ix, iy, 2) .GE. 0.) THEN
          abs4 = bb(ix, iy, 2)
        ELSE
          abs4 = -bb(ix, iy, 2)
        END IF
        IF (bb(n_imp, iy, 2) .GE. 0.) THEN
          abs10 = bb(n_imp, iy, 2)
        ELSE
          abs10 = -bb(n_imp, iy, 2)
        END IF
        IF (abs4 .GT. abs10) n_imp = ix
      END DO
    ELSE
      WRITE(*, *) ' conv_midplane: nnreg = ', nnreg(0)
      n_imp = (nx+2)/4
      iy = ny/2
      DO ix=-1,nx
        IF (bb(n_imp, iy, 2) .EQ. 0.0_R8) n_imp = ix
        IF (bb(ix, iy, 2) .GE. 0.) THEN
          abs5 = bb(ix, iy, 2)
        ELSE
          abs5 = -bb(ix, iy, 2)
        END IF
        IF (bb(n_imp, iy, 2) .GE. 0.) THEN
          abs11 = bb(n_imp, iy, 2)
        ELSE
          abs11 = -bb(n_imp, iy, 2)
        END IF
        IF (abs5 .GT. abs11) n_imp = ix
      END DO
    END IF
    WRITE(*, *) ' Found IMP location at ix = ', n_imp
    DO WHILE (ISREALCELL(cflags(n_imp, iy+1, cellflag_type)))
      iy = iy + 1
    END DO
!
! R-coordinate of center of [n_imp,ny] cell
    ri(1) = 0.25_R8*(crx(n_imp, iy, 0)+crx(n_imp, iy, 1)+crx(n_imp, iy, &
&     2)+crx(n_imp, iy, 3))
! Z-coordinate of center of [n_imp,ny] cell
    zi(1) = 0.25_R8*(cry(n_imp, iy, 0)+cry(n_imp, iy, 1)+cry(n_imp, iy, &
&     2)+cry(n_imp, iy, 3))
! R-coordinate of center of [n_imp,-1] cell
    ri(2) = 0.25_R8*(crx(n_imp, -1, 0)+crx(n_imp, -1, 1)+crx(n_imp, -1, &
&     2)+crx(n_imp, -1, 3))
! Z-coordinate of center of [n_imp,-1] cell
    zi(2) = 0.25_R8*(cry(n_imp, -1, 0)+cry(n_imp, -1, 1)+cry(n_imp, -1, &
&     2)+cry(n_imp, -1, 3))
!
    IF (ri(2) .GT. ri(1)) THEN
! take a coordinate to the left of ri(1)
      rzimp(1, 1) = ri(1)*0.9_R8
! calculate, corresponding to rzimp(1,1), Z-coordinate of point
! laying on the straight line going through the points ri(1),zi(1) and ri(2),zi(2)
      rzimp(2, 1) = zi(1) + (rzimp(1, 1)-ri(1))*(zi(2)-zi(1))/(ri(2)-ri(&
&       1))
! take a coordinate to the right of ri(2)
      rzimp(1, 2) = ri(2)*1.1_R8
! calculate, corresponding to rzimp(1,2), Z-coordinate of point
! laying on the straight line going through the points ri(1),zi(1) and ri(2),zi(2)
      rzimp(2, 2) = zi(1) + (rzimp(1, 2)-ri(1))*(zi(2)-zi(1))/(ri(2)-ri(&
&       1))
    ELSE
      WRITE(*, *) ' ri(1:2) :', ri(1), ri(2)
      WRITE(*, *) ' zi(1:2) :', zi(1), zi(2)
      CALL XERRAB('conv_midplane: unexpected coordinates for rzimp')
    END IF
!
    OPEN(newunit=iout, file='info.dat') 
    WRITE(iout, '(a,i4)') ' Number of outer midplane cell =', n_omp
    WRITE(iout, '(a,f6.2,a,f6.2)') ' Ro1 = ', ro(1), ' Zo1 = ', zo(1)
    WRITE(iout, '(a,f6.2,a,f6.2)') ' Ro2 = ', ro(2), ' Zo2 = ', zo(2)
    WRITE(iout, '(a,i4)') ' Number of inner midplane cell =', n_imp
    WRITE(iout, '(a,f6.2,a,f6.2)') ' Ri1 = ', ri(1), ' Zi1 = ', zi(1)
    WRITE(iout, '(a,f6.2,a,f6.2)') ' Ri2 = ', ri(2), ' Zi2 = ', zi(2)
    CLOSE(iout) 
    OPEN(newunit=iout, file='b2us.user.parameters') 
    WRITE(iout, '(a)') '&user'
    WRITE(iout, '(a,f8.2,a)') 'rzomp(1,1) = ', rzomp(1, 1), ','
    WRITE(iout, '(a,f8.2,a)') 'rzomp(2,1) = ', rzomp(2, 1), ','
    WRITE(iout, '(a,f8.2,a)') 'rzomp(1,2) = ', rzomp(1, 2), ','
    WRITE(iout, '(a,f8.2,a)') 'rzomp(2,2) = ', rzomp(2, 2), ','
    WRITE(iout, '(a,f8.2,a)') 'rzimp(1,1) = ', rzimp(1, 1), ','
    WRITE(iout, '(a,f8.2,a)') 'rzimp(2,1) = ', rzimp(2, 1), ','
    WRITE(iout, '(a,f8.2,a)') 'rzimp(1,2) = ', rzimp(1, 2), ','
    WRITE(iout, '(a,f8.2,a)') 'rzimp(2,2) = ', rzimp(2, 2), ','
    WRITE(iout, '(a)') '/'
    CLOSE(iout) 
!
    RETURN
  END SUBROUTINE CONV_MIDPLANE

