!
    DEALLOCATE(indfc)
    DEALLOCATE(indcv)
    DEALLOCATE(fh)
    DEALLOCATE(ch)
!
! regions (cell volumes)
    DO iy=-1,ny
      DO ix=-1,nx
        IF (m%imapcv(ix, iy) .NE. 0) THEN
          ic = m%imapcv(ix, iy)
          m%cvreg(ic) = region(ix, iy, 0)
          m%cvonclosedsurface(ic) = on_closed_surface(ix, iy)
        END IF
      END DO
    END DO
!
! regions (faces)
! x faces
    nfcxreg = MAXVAL(region(-1:nx, -1:ny, 1))
    DO iy=0,ny-1
      DO ix=0,nx
        IF (m%imapfcx(ix, iy) .NE. 0) THEN
          ifc = m%imapfcx(ix, iy)
          IF (region(ix, iy, 1) .GT. 0) THEN
            m%fcreg(ifc) = region(ix, iy, 1)
          ELSE IF (region(ix, iy, 1) .LT. 0) THEN
            IF (region(ix, iy, 1) .GE. 0.) THEN
              abs8 = region(ix, iy, 1)
            ELSE
              abs8 = -region(ix, iy, 1)
            END IF
! actually a y-face region!
            m%fcreg(ifc) = abs8 + nfcxreg
          END IF
        END IF
      END DO
    END DO
!
! y faces
    DO ix=0,nx-1
      DO iy=0,ny
        IF (m%imapfcy(ix, iy) .NE. 0) THEN
          ifc = m%imapfcy(ix, iy)
          IF (region(ix, iy, 2) .GT. 0) THEN
            m%fcreg(ifc) = region(ix, iy, 2) + nfcxreg
          ELSE IF (region(ix, iy, 2) .LT. 0) THEN
            IF (region(ix, iy, 2) .GE. 0.) THEN
              m%fcreg(ifc) = region(ix, iy, 2)
            ELSE
              m%fcreg(ifc) = -region(ix, iy, 2)
            END IF
          END IF
        END IF
      END DO
    END DO
!
    mxreg = MAXVAL(m%cvreg(1:m%ncv))
!
! identify the various flux tubes and surfaces
! init to 0
    m%ftcv = 0
! init to 0
    m%ftfc = 0
! init to 0
    m%fsfc = 0
! init to 0
    m%cvft = 0
    m%ftcvp = 0
    m%ftfcp = 0
    m%fsfcp = 0
    m%ftlbl = 0
    IF (m%isclassicalgrid .EQ. 1) THEN
      CALL CONV_REGIONS(nnreg)
! csc these arrays are needed because otherwise the nFt with which the mapping is written
!     is different wrt the nFt with which they have been allocated
      ALLOCATE(ftcvp(m%nft, 2))
      ALLOCATE(ftfcp(m%nft, 2))
      ALLOCATE(fsfcp(m%nfs, 2))
      ALLOCATE(ftreg(m%nft))
      ALLOCATE(ftlbl(m%nft))
      ftcvp = 0
      ftfcp = 0
      fsfcp = 0
      ftreg = 0
      ftlbl = 0
      ift = 0
      ifs = 0
      icv = 0
      ifcc = 0
      ifccy = 0
      ilbl = 0
      IF (nncut .EQ. 0) THEN
!Linear case
        nyreg = 1
        ALLOCATE(nxreg(nyreg))
        ALLOCATE(reg(1, nyreg))
        ALLOCATE(nseg(1, nyreg))
        ALLOCATE(ixstart(1, nyreg, 1))
        ALLOCATE(ixend(1, nyreg, 1))
        ALLOCATE(iystart(nyreg))
        ALLOCATE(iyend(nyreg))
        iystart(1) = -1
        iyend(1) = ny
        nxreg(1) = 1
!
        nseg(1, 1) = 1
        ixstart(1, 1, 1) = -1
        ixend(1, 1, 1) = nx
        reg(1, 1) = 1
      ELSE IF (nncut .EQ. 1) THEN
        nyreg = 2
        ALLOCATE(nxreg(nyreg))
        ALLOCATE(reg(2, nyreg))
        ALLOCATE(nseg(2, nyreg))
        ALLOCATE(ixstart(2, nyreg, 2))
        ALLOCATE(ixend(2, nyreg, 2))
        ALLOCATE(iystart(nyreg))
        ALLOCATE(iyend(nyreg))
        iystart(1) = -1
        iyend(1) = topcut(1) - 1
        iystart(2) = topcut(1)
        iyend(2) = ny
        nxreg(1) = 2
        nxreg(2) = 1
!segments for pfr
        nseg(1, 1) = 2
!segments for core
        nseg(2, 1) = 1
!segments for SOL
        nseg(1, 2) = 1
        ixstart(1, 1, 1) = -1
        ixend(1, 1, 1) = leftcut(1) - 1
        ixstart(1, 1, 2) = rightcut(1)
        ixend(1, 1, 2) = nx
        ixstart(2, 1, 1) = leftcut(1)
        ixend(2, 1, 1) = rightcut(1) - 1
        ixstart(1, 2, 1) = -1
        ixend(1, 2, 1) = nx
        reg(1, 1) = 3
        reg(2, 1) = 1
        reg(1, 2) = 2
      ELSE
        nyreg = 3
        CALL GET_NXT(nx, nxtl, nxtr)
        ALLOCATE(nxreg(nyreg))
        ALLOCATE(reg(3, nyreg))
        ALLOCATE(nseg(3, nyreg))
        ALLOCATE(ixstart(3, nyreg, 2))
        ALLOCATE(ixend(3, nyreg, 2))
        ALLOCATE(iystart(nyreg))
        ALLOCATE(iyend(nyreg))
        nxreg(1) = 3
!both DDN & DDNU
        nxreg(2) = 2
        nxreg(3) = 2
        iystart(1) = -1
        IF (topcut(1) .GT. topcut(2)) THEN
          min2 = topcut(2)
        ELSE
          min2 = topcut(1)
        END IF
        iyend(1) = min2 - 1
        IF (topcut(1) .GT. topcut(2)) THEN
          iystart(2) = topcut(2)
        ELSE
          iystart(2) = topcut(1)
        END IF
        IF (topcut(1) .LT. topcut(2)) THEN
          max1 = topcut(2)
        ELSE
          max1 = topcut(1)
        END IF
        iyend(2) = max1 - 1
        IF (topcut(1) .LT. topcut(2)) THEN
          iystart(3) = topcut(2)
        ELSE
          iystart(3) = topcut(1)
        END IF
        iyend(3) = ny
!segments for pfr1/core/pfr2
        nseg(:, 1) = 2
!segments for sol1/pfr2
        nseg(:, 2) = 2
!segments for sol2/sol3
! pfr1
        nseg(:, 3) = 1
        ixstart(1, 1, 1) = -1
        ixend(1, 1, 1) = leftcut(1) - 1
        ixstart(1, 1, 2) = rightcut(1)
        ixend(1, 1, 2) = nx
! core
        reg(1, 1) = 3
        ixstart(2, 1, 1) = leftcut(1)
        ixend(2, 1, 1) = leftcut(2) - 1
        ixstart(2, 1, 2) = rightcut(2)
        ixend(2, 1, 2) = rightcut(1) - 1
! pfr2
        reg(2, 1) = 1
        ixstart(3, 1, 1) = nxtr - 1
        ixend(3, 1, 1) = rightcut(2) - 1
        ixstart(3, 1, 2) = leftcut(2)
        ixend(3, 1, 2) = nxtl - 1
        reg(3, 1) = 3
! sol1(ddnu)/pfr2(ddnd)
        IF (topcut(1) .GT. topcut(2)) THEN
!DDNU
          ix = 1
          reg(1, 2) = 2
          reg(2, 2) = 3
        ELSE IF (topcut(1) .LT. topcut(2)) THEN
!DDND
          ix = 2
          reg(1, 2) = 3
          reg(2, 2) = 2
        ELSE
          ix = 1
!CDN
          nxreg(2) = 0
        END IF
        ixstart(1, 2, 1) = nxtr - 1
        ixend(1, 2, 1) = rightcut(ix) - 1
        ixstart(1, 2, 2) = leftcut(ix)
! pfr2(ddnu)/sol1(ddnd)
        ixend(1, 2, 2) = nxtl - 1
        ixstart(2, 2, 1) = -1
        ixend(2, 2, 1) = leftcut(ix) - 1
        ixstart(2, 2, 2) = rightcut(ix)
! sol2
        ixend(2, 2, 2) = nx
        ixstart(1, 3, 1) = -1
        ixend(1, 3, 1) = nxtl - 1
! sol3
        reg(1, 3) = 2
        ixstart(2, 3, 1) = nxtr - 1
        ixend(2, 3, 1) = nx
        reg(2, 3) = 2
      END IF
      DO iyreg=1,nyreg
        DO iy=iystart(iyreg),iyend(iyreg)
          ilbl = ilbl + 1
          DO ixreg=1,nxreg(iyreg)
            istrtc = -1
            istrtfc = -1
            istrtfy = -1
            DO is=1,nseg(ixreg, iyreg)
              DO ix=ixstart(ixreg, iyreg, is),ixend(ixreg, iyreg, is)
                IF (m%imapcv(ix, iy) .NE. 0) THEN
                  ic = m%imapcv(ix, iy)
                  ifc = m%imapfcx(ix, iy)
                  ifcy = m%imapfcy(ix, iy)
                  IF (istrtc .EQ. -1) THEN
                    ift = ift + 1
                    icv = icv + 1
                    m%ftcv(icv) = ic
                    ftcvp(ift, 1) = icv
                    m%cvft(ic) = ift
                    ftreg(ift) = reg(ixreg, iyreg)
                    ftlbl(ift) = ilbl
                    istrtc = 1
                  ELSE
                    icv = icv + 1
                    m%ftcv(icv) = ic
                    m%cvft(ic) = ift
                    ftcvp(ift, 2) = icv - ftcvp(ift, 1) + 1
                  END IF
                  IF (ifc .NE. 0) THEN
                    IF (istrtfc .EQ. -1) THEN
                      ifcc = ifcc + 1
                      m%ftfc(ifcc) = ifc
                      ftfcp(ift, 1) = ifcc
                      istrtfc = 1
                    ELSE
                      ifcc = ifcc + 1
                      m%ftfc(ifcc) = ifc
                      ftfcp(ift, 2) = ifcc - ftfcp(ift, 1) + 1
                    END IF
                  END IF
                  IF (ifcy .NE. 0) THEN
                    IF (istrtfy .EQ. -1) THEN
                      ifs = ifs + 1
                      ifccy = ifccy + 1
                      m%fsfc(ifccy) = ifcy
                      fsfcp(ifs, 1) = ifccy
                      istrtfy = 1
                    ELSE
                      ifccy = ifccy + 1
                      m%fsfc(ifccy) = ifcy
                      fsfcp(ifs, 2) = ifccy - fsfcp(ifs, 1) + 1
                    END IF
                  END IF
                END IF
              END DO
            END DO
          END DO
        END DO
      END DO
!ix
!is
!ixreg
!iy
!iyreg
! csc last flux surface needs ny + 1
      DO ixreg=1,nxreg(nyreg)
        istrtfy = -1
        DO is=1,nseg(ixreg, nyreg)
          DO ix=ixstart(ixreg, nyreg, is),ixend(ixreg, nyreg, is)
            IF (m%imapcv(ix, ny) .NE. 0) THEN
              ifcy = m%imapfcy(ix, ny)
              IF (ifcy .NE. 0) THEN
                IF (istrtfy .EQ. -1) THEN
                  ifs = ifs + 1
                  ifccy = ifccy + 1
                  m%fsfc(ifccy) = ifcy
                  fsfcp(ifs, 1) = ifccy
                  istrtfy = 1
                ELSE
                  ifccy = ifccy + 1
                  m%fsfc(ifccy) = ifcy
                  fsfcp(ifs, 2) = ifccy - fsfcp(ifs, 1) + 1
                END IF
              END IF
            END IF
          END DO
        END DO
      END DO
!ix
!is
!ixreg
      m%nft = ift
      m%nfs = ifs
! csc Now that real number of flux surfaces is known re-allocate and assign ftFcP and ftCvP
      DEALLOCATE(m%ftfcp)
      DEALLOCATE(m%ftcvp)
      DEALLOCATE(m%fsfcp)
      DEALLOCATE(m%ftreg)
      DEALLOCATE(m%ftlbl)
      ALLOCATE(m%ftfcp(m%nft, 2))
      ALLOCATE(m%ftcvp(m%nft, 2))
      ALLOCATE(m%fsfcp(m%nfs, 2))
      ALLOCATE(m%ftreg(m%nft))
      ALLOCATE(m%ftlbl(m%nft))
      m%ftfcp = ftfcp(1:m%nft, :)
      m%ftcvp = ftcvp(1:m%nft, :)
      m%fsfcp = fsfcp(1:m%nfs, :)
      m%ftreg = ftreg(1:m%nft)
      m%ftlbl = ftlbl(1:m%nft)
      DEALLOCATE(ftfcp)
      DEALLOCATE(ftcvp)
      DEALLOCATE(fsfcp)
      DEALLOCATE(ftreg)
      DEALLOCATE(ftlbl)
      DEALLOCATE(ixstart)
      DEALLOCATE(ixend)
      DEALLOCATE(iystart)
      DEALLOCATE(iyend)
      DEALLOCATE(reg)
      DEALLOCATE(nxreg)
      DEALLOCATE(nseg)
    ELSE
      WRITE(*, *) 'WARNING flux surfaces not yet available'//&
&     ' for non-structured grids'
    END IF
!
!     Create b2.user.parameters file with definition of outer/inner midplanes
    CALL CONV_MIDPLANE(nx, ny)
!
    DEALLOCATE(icorner)
    DEALLOCATE(ighostcv)
    DEALLOCATE(ixghostcv)
    DEALLOCATE(iyghostcv)
    DEALLOCATE(ixcorner)
    DEALLOCATE(iycorner)
    DEALLOCATE(ighostfcx)
    DEALLOCATE(ixghostfcx)
    DEALLOCATE(iyghostfcx)
    DEALLOCATE(ighostfcy)
    DEALLOCATE(ixghostfcy)
    DEALLOCATE(iyghostfcy)
    DEALLOCATE(ighostvx)
    DEALLOCATE(ixghostvx)
    DEALLOCATE(iyghostvx)
!      deallocate (locleftix)
!      deallocate (locleftiy)
    DEALLOCATE(locrightix)
    DEALLOCATE(locrightiy)
!      deallocate (locbottomix)
!      deallocate (locbottomiy)
    DEALLOCATE(loctopix)
    DEALLOCATE(loctopiy)
!
    RETURN
  END SUBROUTINE CALC_MAPPING

