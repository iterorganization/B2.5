              abs4 = fpsi(ix-1, iy-1, 0, 0) - fpsi(ix-1, iy-1, 3, 0)
            ELSE
              abs4 = -(fpsi(ix-1, iy-1, 0, 0)-fpsi(ix-1, iy-1, 3, 0))
            END IF
            IF (fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 0, 0) .GE. 0.&
&           ) THEN
              abs13 = fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 0, 0)
            ELSE
              abs13 = -(fpsi(ix-1, iy-1, 2, 0)-fpsi(ix-1, iy-1, 0, 0))
            END IF
            m%intcellp(if2) = abs4/abs13
            IF (fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 3, 0) .GE. 0.&
&           ) THEN
              abs5 = fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 3, 0)
            ELSE
              abs5 = -(fpsi(ix-1, iy-1, 2, 0)-fpsi(ix-1, iy-1, 3, 0))
            END IF
            IF (fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 0, 0) .GE. 0.&
&           ) THEN
              abs14 = fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 0, 0)
            ELSE
              abs14 = -(fpsi(ix-1, iy-1, 2, 0)-fpsi(ix-1, iy-1, 0, 0))
            END IF
            m%intcellp(if3) = abs5/abs14
            m%intcellr(if1) = 1.0_R8
            IF (fpsi(ix-1, iy-1, 0, 0) - fpsi(ix-1, iy-1, 3, 0) .GE. 0.&
&           ) THEN
              abs6 = fpsi(ix-1, iy-1, 0, 0) - fpsi(ix-1, iy-1, 3, 0)
            ELSE
              abs6 = -(fpsi(ix-1, iy-1, 0, 0)-fpsi(ix-1, iy-1, 3, 0))
            END IF
            IF (fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 0, 0) .GE. 0.&
&           ) THEN
              abs15 = fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 0, 0)
            ELSE
              abs15 = -(fpsi(ix-1, iy-1, 2, 0)-fpsi(ix-1, iy-1, 0, 0))
            END IF
            m%intcellr(if2) = abs6/abs15
            IF (fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 3, 0) .GE. 0.&
&           ) THEN
              abs7 = fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 3, 0)
            ELSE
              abs7 = -(fpsi(ix-1, iy-1, 2, 0)-fpsi(ix-1, iy-1, 3, 0))
            END IF
            IF (fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 0, 0) .GE. 0.&
&           ) THEN
              abs16 = fpsi(ix-1, iy-1, 2, 0) - fpsi(ix-1, iy-1, 0, 0)
            ELSE
              abs16 = -(fpsi(ix-1, iy-1, 2, 0)-fpsi(ix-1, iy-1, 0, 0))
            END IF
            m%intcellr(if3) = abs7/abs16
            m%cvvx(if1) = m%imapvx(ix-1, iy-1)
!cwdk is this not the vertex that was thrown out??ctb solved?
            m%cvvx(if2) = m%imapvx(locrightix(ix-1, iy-1), locrightiy(ix&
&             -1, iy-1))
            m%cvvx(if3) = m%imapvx(loctopix(ix-1, iy-1), loctopiy(ix-1, &
&             iy-1))
          CASE (cgeo_tria_notop) 
            if1 = m%cvfcp(ic, 1)
            if2 = if1 + 1
            if3 = if2 + 1
            m%cvfc(if1) = m%imapfcx(ix-1, iy-1)
            m%cvfc(if2) = m%imapfcx(rightix(ix-1, iy-1), rightiy(ix-1, &
&             iy-1))
            m%cvfc(if3) = m%imapfcy(ix-1, iy-1)
            m%intcellp(if1) = 1.0_R8
            m%intcellp(if2) = 1.0_R8
            m%intcellp(if3) = 0.0_R8
            m%intcellr(if1) = 1.0_R8
            m%intcellr(if2) = 1.0_R8
            m%intcellr(if3) = 0.0_R8
            m%cvvx(if1) = m%imapvx(ix-1, iy-1)
            m%cvvx(if2) = m%imapvx(locrightix(ix-1, iy-1), locrightiy(ix&
&             -1, iy-1))
!cwdk is this not the vertex that was thrown out?? ctb solved?
            m%cvvx(if3) = m%imapvx(loctopix(ix-1, iy-1), loctopiy(ix-1, &
&             iy-1))
          END SELECT
        ELSE IF (m%imapcv(ix-1, iy-1) .NE. 0 .AND. m%imapcv(ix-1, iy-1) &
&           .GT. m%nci) THEN
! N guard cell
          bound = .false.
!
!tb  the vertex numbering might not be consistent for fcVx, to be checked
          ixb = bottomix(ix-1, iy-1)
          iyb = bottomiy(ix-1, iy-1)
! S guard cell
          IF (iyb .NE. -2) THEN
            IF (ISBOUNDARYCELL(cflags(ixb, iyb, cellflag_type))) THEN
              ic = m%imapcv(ix-1, iy-1)
              if1 = m%cvfcp(ic, 1)
              m%cvfc(if1) = m%imapfcy(ix-1, iy-1)
              m%intcellp(if1) = 1.0_R8
              m%intcellr(if1) = 1.0_R8
              if1 = m%cvvxp(ic, 1)
              m%cvvx(if1) = m%imapvx(ix-1, iy-1)
              m%cvvx(if1+1) = m%imapvx(locrightix(ix-1, iy-1), &
&               locrightiy(ix-1, iy-1))
              bound = .true.
            END IF
          END IF
!
          ixb = topix(ix-1, iy-1)
          iyb = topiy(ix-1, iy-1)
! E guard cell
          IF (iyb .NE. ny + 1) THEN
            IF (ISBOUNDARYCELL(cflags(ixb, iyb, cellflag_type))) THEN
              IF (bound) CALL XERRAB('calc_mapping - double boundary')
              ic = m%imapcv(ix-1, iy-1)
              if1 = m%cvfcp(ic, 1)
              m%cvfc(if1) = m%imapfcy(ixb, iyb)
              m%intcellp(if1) = 1.0_R8
              m%intcellr(if1) = 1.0_R8
              if1 = m%cvvxp(ic, 1)
              m%cvvx(if1) = m%imapvx(ixb, iyb)
              m%cvvx(if1+1) = m%imapvx(locrightix(ixb, iyb), locrightiy(&
&               ixb, iyb))
              bound = .true.
            END IF
          END IF
!
          ixb = leftix(ix-1, iy-1)
          iyb = leftiy(ix-1, iy-1)
! W guard cell
          IF (ixb .NE. -2) THEN
            IF (ISBOUNDARYCELL(cflags(ixb, iyb, cellflag_type))) THEN
              IF (bound) CALL XERRAB('calc_mapping - double boundary')
              ic = m%imapcv(ix-1, iy-1)
              if1 = m%cvfcp(ic, 1)
              m%cvfc(if1) = m%imapfcx(ix-1, iy-1)
              m%intcellp(if1) = 1.0_R8
              m%intcellr(if1) = 1.0_R8
              if1 = m%cvvxp(ic, 1)
              m%cvvx(if1) = m%imapvx(ix-1, iy-1)
              m%cvvx(if1+1) = m%imapvx(loctopix(ix-1, iy-1), loctopiy(ix&
&               -1, iy-1))
              bound = .true.
            END IF
          END IF
!
          ixb = rightix(ix-1, iy-1)
          iyb = rightiy(ix-1, iy-1)
          IF (ixb .NE. nx + 1) THEN
            IF (ISBOUNDARYCELL(cflags(ixb, iyb, cellflag_type))) THEN
              IF (bound) CALL XERRAB('calc_mapping - double boundary')
              ic = m%imapcv(ix-1, iy-1)
              if1 = m%cvfcp(ic, 1)
              m%cvfc(if1) = m%imapfcx(ixb, iyb)
              m%intcellp(if1) = 1.0_R8
              m%intcellr(if1) = 1.0_R8
              if1 = m%cvvxp(ic, 1)
              m%cvvx(if1) = m%imapvx(ixb, iyb)
              m%cvvx(if1+1) = m%imapvx(loctopix(ixb, iyb), loctopiy(ixb&
&               , iyb))
              bound = .true.
            END IF
          END IF
!
          IF (.NOT.bound) CALL XERRAB(&
&                               'Problem with guard cell definition.')
        END IF
      END DO
    END DO
!
!
! listing for every face the two corresponding cells
! and the corresponding vertices
! x faces
    DO iy=1,ny
      DO ix=1,nx+1
        IF (m%imapfcx(ix-1, iy-1) .NE. 0) THEN
          ifc = m%imapfcx(ix-1, iy-1)
          m%fccv(ifc, 1) = m%imapcv(leftix(ix-1, iy-1), leftiy(ix-1, iy-&
&           1))
          m%fccv(ifc, 2) = m%imapcv(ix-1, iy-1)
          m%fcvx(ifc, 1) = m%imapvx(ix-1, iy-1)
          m%fcvx(ifc, 2) = m%imapvx(loctopix(ix-1, iy-1), loctopiy(ix-1&
&           , iy-1))
        END IF
      END DO
    END DO
!
!
! y faces
!
    DO ix=1,nx
      DO iy=1,ny+1
        IF (m%imapfcy(ix-1, iy-1) .NE. 0) THEN
          ifc = m%imapfcy(ix-1, iy-1)
          m%fccv(ifc, 2) = m%imapcv(ix-1, iy-1)
          m%fccv(ifc, 1) = m%imapcv(bottomix(ix-1, iy-1), bottomiy(ix-1&
&           , iy-1))
          m%fcvx(ifc, 2) = m%imapvx(ix-1, iy-1)
          m%fcvx(ifc, 1) = m%imapvx(locrightix(ix-1, iy-1), locrightiy(&
&           ix-1, iy-1))
        END IF
      END DO
    END DO
!
! merging x-point vertex !!! TB not changed... seems not to be necessary
    DO icut=1,nncut
      ix1 = leftcut(icut)
      iy1 = topcut(icut)
      ix2 = rightcut(icut)
      iy2 = topcut(icut)
      ivx1 = m%imapvx(ix1, iy1)
!wdk Check whether X-point vertices actually match
!    (One or both of the vertices might fall outside of domain)
      ivx2 = m%imapvx(ix2, iy2)
!
      IF (POINTS_MATCH(crx(ix1, iy1, 0), cry(ix1, iy1, 0), crx(ix2, iy2&
&         , 0), cry(ix2, iy2, 0))) THEN
        WHERE (m%imapvx .EQ. ivx2) m%imapvx = ivx1
        WHERE (m%cvvx .EQ. ivx2) m%cvvx = ivx1
        WHERE (m%fcvx .EQ. ivx2) m%fcvx = ivx1
!
        WRITE(*, *) 'X-point ', icut, ' merged.'
      ELSE
        WRITE(*, *) 'X-point ', icut, ' not merged!'
      END IF
    END DO
! sanity check
!
    DO ic=1,m%ncv
      DO ifc=m%cvfcp(ic, 1),m%cvfcp(ic, 1)+m%cvfcp(ic, 2)-1
        IF (m%cvfc(ifc) .EQ. 0) THEN
          WRITE(*, *) 'ic, ifc = ', ic, ifc
          CALL XERRAB('Problem with mapping: undefined face')
        END IF
      END DO
      DO ivx=m%cvvxp(ic, 1),m%cvvxp(ic, 1)+m%cvvxp(ic, 2)-1
        IF (m%cvvx(ivx) .EQ. 0) THEN
          WRITE(*, *) 'ic, ivx = ', ic, ivx
          CALL XERRAB('Problem with mapping: undefined vertex')
        END IF
      END DO
    END DO
!
    DO ifc=1,m%nfc
      IF (m%fccv(ifc, 1) .EQ. 0) THEN
        WRITE(*, *) 'ifc = ', ifc
        CALL XERRAB('Problem with mapping: undefined cv1')
      END IF
      IF (m%fccv(ifc, 2) .EQ. 0) THEN
        WRITE(*, *) 'ifc = ', ifc
        CALL XERRAB('Problem with mapping: undefined cv2')
      END IF
      IF (m%fcvx(ifc, 1) .EQ. 0) THEN
        WRITE(*, *) 'ifc = ', ifc
        CALL XERRAB('Problem with mapping: undefined vx1')
      END IF
      IF (m%fcvx(ifc, 2) .EQ. 0) THEN
        WRITE(*, *) 'ifc = ', ifc
        CALL XERRAB('Problem with mapping: undefined vx2')
      END IF
    END DO
!
!
! listing for every vertex the cell and face numbers
! and the starting points in the list
    ALLOCATE(indfc(2*m%nfc))
    indfc(1:m%nfc) = (/(i, i=1,m%nfc)/)
    indfc(m%nfc+1:2*m%nfc) = (/(i, i=1,m%nfc)/)
