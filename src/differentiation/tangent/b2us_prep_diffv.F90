          st%dv%fna(ifc, 0:1, 0:ns-1) = fna(ix, iy, 1, 0:1, 0:ns-1)
          st%dv%fna_mdf(ifc, 0:1, 0:ns-1) = fna_mdf(ix, iy, 1, 0:1, 0:ns&
&           -1)
          st%dv%fna_he(ifc, 0:1, 0:ns-1) = fna_he(ix, iy, 1, 0:1, 0:ns-1&
