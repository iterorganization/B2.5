      m%vxcvp(ivx, 2) = COUNT(ch .EQ. ivx)
      m%vxcvp(ivx+1, 1) = m%vxcvp(ivx, 1) + m%vxcvp(ivx, 2)
      IF (m%vxcvp(ivx, 2) .GT. 0) m%vxcv(m%vxcvp(ivx, 1):m%vxcvp(ivx+1, &
