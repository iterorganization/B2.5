SUBROUTINE PREHD2D_NODIFF(ns, isize, list_x, list_y, dt, isign, ixf, iyf&
& , idir, area, boris)
  USE B2MOD_TYPES
  USE B2MOD_WALL_DIFFV
  USE B2MOD_RATES
  USE B2MOD_PLASMA_DIFFV
  USE B2MOD_CONSTANTS
  USE B2MOD_EXTERNAL_DIFFV
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!
  INTEGER :: ns, isize, isign, idir
  INTEGER :: list_x(isize), list_y(isize)
  REAL(kind=r8) :: dt, boris
  INTEGER :: ixf(isize), iyf(isize)
  REAL(kind=r8) :: area(isize)
!
  INTEGER :: iwall, is
  INTEGER :: bndx(isize)
  REAL(kind=r8) :: qin, qel
  REAL(kind=r8) :: q(isize), tp(isize, ndepth_nml)
!
  EXTERNAL XERTST
  INTRINSIC MAX
  REAL(r8) :: y1
  REAL(r8) :: y2
  REAL(r8) :: max1
  REAL(r8) :: max2
  REAL(r8) :: max3
  REAL(r8) :: max4
!
  CALL SUBINI('prehd2D')
  CALL XERTST(isize .GE. 1, 'faulty input isize')
!
  DO iwall=1,isize
    bndx(iwall) = xymap(list_x(iwall), list_y(iwall))
    y1 = isign*(fhi(ixf(iwall), iyf(iwall), idir, idir)+fhi_ext(ixf(&
&     iwall), iyf(iwall), idir, idir))
    IF (0.0_R8 .LT. y1) THEN
      max1 = y1
    ELSE
      max1 = 0.0_R8
    END IF
    qin = max1/area(iwall)
    DO is=0,ns-1
      IF (0.0_R8 .LT. isign*fna(ixf(iwall), iyf(iwall), idir, idir, is)&
&     ) THEN
        max2 = isign*fna(ixf(iwall), iyf(iwall), idir, idir, is)
      ELSE
        max2 = 0.0_R8
      END IF
      qin = qin + max2*((kinrgy(list_x(iwall), list_y(iwall), is)+ti(&
&       list_x(iwall), list_y(iwall)))*(1.0_R8-boris)+rpt(ixf(iwall), &
&       iyf(iwall), is)*ev)/area(iwall)
    END DO
    DO is=0,ns_ext-1
      IF (0.0_R8 .LT. isign*fa_ext(ixf(iwall), iyf(iwall), idir, idir, &
&         is)) THEN
        max3 = isign*fa_ext(ixf(iwall), iyf(iwall), idir, idir, is)
      ELSE
        max3 = 0.0_R8
      END IF
      qin = qin + max3*((0.5_R8*am_ext(is)*mp*ua_ext(list_x(iwall), &
&       list_y(iwall), is)**2+ta_ext(list_x(iwall), list_y(iwall), is))*&
&       (1.0_R8-boris)+pt_ext(ixf(iwall), iyf(iwall), is)*ev)/area(iwall&
&       )
    END DO
    y2 = isign*(fhe(ixf(iwall), iyf(iwall), idir, idir)+fne(ixf(iwall), &
&     iyf(iwall), idir, idir)*te(list_x(iwall), list_y(iwall))*(1.0_R8-&
&     boris))
    IF (0.0_R8 .LT. y2) THEN
      max4 = y2
    ELSE
      max4 = 0.0_R8
    END IF
    qel = max4/area(iwall)
    q(iwall) = qin + qel
    tp(iwall, 1:ndepth_nml) = target_temp(bndx(iwall), 1:ndepth_nml)
  END DO
!
  CALL HEATDIFF2D_NODIFF(isize, ns, bndx, q, dt, tp)
!
  DO iwall=1,isize
    target_temp(bndx(iwall), 1:ndepth_nml) = tp(iwall, 1:ndepth_nml)
  END DO
!
  CALL SUBEND()
  RETURN
END SUBROUTINE PREHD2D_NODIFF

!
