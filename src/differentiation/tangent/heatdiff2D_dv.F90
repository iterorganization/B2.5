!
! This is the subroutine which solves for a heat diffusion equation
! of the form:
!              doe T         doe^2 T     doe D   doe T
!             ------- =  D  --------- + ------- ------- + sources/sinks
!              doe t         doe^2 x     doe x   doe x
! T is the temperature, t the time variable, x the space variable and
! D the heat diffusion coefficient.
!
! i.e., ....
!       DU = (1/rho/Cp) * (K*UXX + gradK*UX + S)
!       DU is the rate of change of temperature
!       UXX is "doe^2 T / doe^2 x"   and UX is "doe T / doe x"
! with Neumann bondary condition at the plasma facing surface side
! i.e.  UX is specified.
! and a Dirichlet type boundary condition at the coolant side.
! i.e. T is specified.
!
! Inputs:
!  Q = incident total heat flux (Watts/m^2/sec).
!  Tcbc = Temperature at the cooling end (K).
!  dt = Time step of simulation (sec)
!
! Outputs:
!  Tp = Target surface temperature (K).
SUBROUTINE HEATDIFF2D_NODIFF(nw, ns, bndx, q, dt, tp)
  USE B2MOD_TYPES
  USE B2MOD_WALL_DIFFV
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!
  INTEGER :: nw, ns, n2
  INTEGER :: bndx(nw)
  REAL(kind=r8) :: q(nw), dt, tp(nw, ndepth_nml)
!
  REAL(kind=r8) :: qtot, tcbc
  COMMON /phyparams/ qtot, tcbc
!
  INTEGER :: i, iwall, iters, itmax
  PARAMETER (itmax=100)
  REAL(kind=r8) :: u(nw, ndepth_nml), rhs(nw, ndepth_nml)
  REAL(kind=r8), ALLOCATABLE, SAVE :: aaf(:), wrk0(:), wrk1(:), wrk2(:)
  INTEGER, ALLOCATABLE, SAVE :: iwrk(:)
  REAL(kind=r8) :: aa5(nw, ndepth_nml, -2:2), nrmc(0:itmax-1)
  REAL(kind=r8) :: t0, ta, tb, tc, x(ndepth_nml), y(nw, ndepth_nml), rho
  EXTERNAL CP_NODIFF, KAPPA_NODIFF, DKAPPABYDT_NODIFF, KAPPA_PERP_NODIFF&
&     , DKAPPA_PERPBYDT_NODIFF, QEVAP_NODIFF, QRAD_NODIFF, &
&     MASS_DENSITY_NODIFF, COND_COEF
!
  REAL(kind=r8) :: CP_NODIFF, KAPPA_NODIFF, DKAPPABYDT_NODIFF, &
& KAPPA_PERP_NODIFF, DKAPPA_PERPBYDT_NODIFF, QEVAP_NODIFF, QRAD_NODIFF, &
& MASS_DENSITY_NODIFF, COND_COEF
  EXTERNAL SLV5PT, FIC_NODIFF
  INTRINSIC MIN
  INTEGER :: min1
  INTEGER :: min2
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
!
  CALL SUBINI('heatdf2D')
!
! Checking if it is the first call and if it is, call fic(x,u), else
! use previous Tp profile as initial guess.
!
  IF (dt .LE. 0.0_R8) THEN
!       Calling subroutine fic(ibnd,x,u) which supplies the
!       initial condition u(x) i.e.. the temperature "u" as a
!       function of the position "x" at ndepth_nml grid points.
    DO iwall=1,nw
      qtot = q(iwall)
      tcbc = backplate_temp(bndx(iwall))
      CALL FIC_NODIFF(bndx(iwall), x, u(iwall, 1:ndepth_nml))
      DO i=1,ndepth_nml
        tp(iwall, i) = u(iwall, i)
      END DO
    END DO
    CALL SUBEND()
    RETURN
  ELSE
    DO iwall=1,nw
      DO i=1,ndepth_nml
        u(iwall, i) = tp(iwall, i)
      END DO
    END DO
!
!   ..set n2
    n2 = nw*ndepth_nml
    IF (nw .GT. ndepth_nml) THEN
      min1 = ndepth_nml
    ELSE
      min1 = nw
    END IF
!    ..obtain workspace for slv5pt
    ALLOCATE(aaf(1:n2*(3*min1+1)))
    ALLOCATE(wrk0(1:n2))
    ALLOCATE(wrk1(1:1))
    ALLOCATE(wrk2(1:1))
    ALLOCATE(iwrk(1:n2))
!
!   ..initialise the aa5 matrix and rhs vector
    aa5(1:nw, 1:ndepth_nml, -2:2) = 0.0_R8
    rhs(1:nw, 1:ndepth_nml) = 0.0_R8
!
!   ..rho Cp dT/dt - K d2T/dx2 - d/dx (K dT/dx) = S
!
!   ..K d2T/dx2 = [ 2 K / (x(i+1)-x(i-1)) ] * [ T(i+1) / (x(i+1)-x(i)) -
!   ..                T(i) * (1/(x(i-1)-x(i)) + 1/(x(i+1)-x(i))) +
!   ..                T(i-1) / (x(i-1)-x(i)) ]
!
!   ..d/dx (K dT/dx) = dK/dt * (dT/dx)**2
!   ..               = dK/dt * [ T(i+1) * (T(i+1)-T(i-1))/(x(i+1)-x(i-1))**2 -
!   ..                           T(i-1) * (T(i+1)-T(i-1))/(x(i+1)-x(i-1))**2 ]
!
!   ..build depth neighbours (i,j-1) and (i,j+1) terms
    DO iwall=1,nw
      x(1:ndepth_nml) = target_depth(bndx(iwall), 1:ndepth_nml)
!    ..treat incoming heat flux b.c.
      i = 1
      result1 = QEVAP_NODIFF(bndx(iwall), ns, tp(iwall, i))
      result2 = QRAD_NODIFF(bndx(iwall), tp(iwall, i), .true.)
      qtot = q(iwall) - result1 - result2
      result1 = KAPPA_NODIFF(bndx(iwall), i, tp(iwall, i))
      t0 = result1/(x(2)-x(1))
      aa5(iwall, i, -2) = 0.0_R8
      aa5(iwall, i, 0) = aa5(iwall, i, 0) + t0
      aa5(iwall, i, 2) = aa5(iwall, i, 2) - t0
      rhs(iwall, i) = rhs(iwall, i) + qtot
!    ..main terms
      DO i=2,ndepth_nml-1
        result1 = DKAPPABYDT_NODIFF(bndx(iwall), i, tp(iwall, i))
        ta = result1*(tp(iwall, i+1)-tp(iwall, i-1))/(x(i+1)-x(i-1))**2
        result1 = KAPPA_NODIFF(bndx(iwall), i, tp(iwall, i))
        tb = 2.0_R8*result1/(x(i+1)-x(i))/(x(i+1)-x(i-1))
        result1 = KAPPA_NODIFF(bndx(iwall), i, tp(iwall, i))
        tc = 2.0_R8*result1/(x(i)-x(i-1))/(x(i+1)-x(i-1))
        aa5(iwall, i, -2) = aa5(iwall, i, -2) + ta - tc
        aa5(iwall, i, 0) = aa5(iwall, i, 0) + tb + tc
        aa5(iwall, i, 2) = aa5(iwall, i, 2) - ta - tb
      END DO
!    ..treat backplate cooling b.c.
      i = ndepth_nml
      result1 = KAPPA_NODIFF(bndx(iwall), i, tp(iwall, i))
      t0 = result1/(x(i)-x(i-1))
      aa5(iwall, i, -2) = aa5(iwall, i, -2) - t0
      aa5(iwall, i, 0) = aa5(iwall, i, 0) + t0
      aa5(iwall, i, 2) = 0.0_R8
!! FIXME : Could also include radiative and evaporative cooling term here !!
!!         But for this need correct properties of back side material
      result1 = COND_COEF(inertial_cooling(bndx(iwall)), tp(iwall, i))
      rhs(iwall, i) = rhs(iwall, i) - result1*(tp(iwall, i)-&
&       backplate_temp(bndx(iwall)))
    END DO
!xpb        if (inertial_cooling(bndx(iwall))) then
!xpb          rhs(iwall,i) = rhs(iwall,i) -
!xpb     .      qrad(bndx(iwall),Tp(iwall,i),.false.) -
!xpb     .     qevap(bndx(iwall),ns,Tp(iwall,i))
!xpb        end if
!$$$ Old code, which assumed perfect heat contact between material and coolant was:
!$$$        if (inertial_cooling(iwall)) then
!$$$          t0 = Kappa(bndx(iwall),i,Tp(iwall,i)) / (x(i)-x(i-1))
!$$$          aa5(iwall,i,-2) = aa5(iwall,i,-2) - t0
!$$$          aa5(iwall,i, 0) = aa5(iwall,i, 0) + t0
!$$$          aa5(iwall,i, 2) = 0.0_R8
!$$$!! FIXME : Could also include radiative and evaporative cooling term here !!
!$$$          rhs(iwall,i) = rhs(iwall,i) -
!$$$     .     100.0_R8*(Tp(iwall,i)-backplate_temp(bndx(iwall))) ! we assume the contact heat conductivity of 100 W.m-2.K-1
!$$$        else
!$$$          aa5(iwall,i,-2) = 0.0_R8
!$$$          aa5(iwall,i, 0) = 1.0_R8
!$$$          aa5(iwall,i, 2) = 0.0_R8
!$$$          rhs(iwall,i) = backplate_temp(bndx(iwall))
!$$$        endif
!
!   ..build side neighbours (i-1,j) and (i+1,j) terms
    DO i=1,ndepth_nml
      y(1:nw, i) = target_width(bndx(1:nw), i)
    END DO
!    ..treat radiative cooling b.c.
    iwall = 1
    DO i=1,ndepth_nml-1
!! FIXME: Careful about the area over which the radiative cooling occurs, it should be here the side area, not the top area !!
!! Need to make sure the correct side material properties are used !!
      result1 = QRAD_NODIFF(bndx(iwall), tp(iwall, i), .false.)
      result2 = QEVAP_NODIFF(bndx(iwall), ns, tp(iwall, i))
      qtot = -result1 - result2
      result1 = KAPPA_PERP_NODIFF(bndx(iwall), i, tp(iwall, i))
      t0 = result1/(0.5_R8*(y(iwall, i)+y(iwall+1, i)))
      aa5(iwall, i, -1) = 0.0_R8
      aa5(iwall, i, 0) = aa5(iwall, i, 0) + t0
      aa5(iwall, i, 1) = aa5(iwall, i, 1) - t0
      rhs(iwall, i) = rhs(iwall, i) + qtot
    END DO
!    ..main terms
    DO iwall=2,nw-1
      DO i=1,ndepth_nml-1
        IF (y(iwall, i) .GT. 0.0_R8) THEN
          result1 = DKAPPA_PERPBYDT_NODIFF(bndx(iwall), i, tp(iwall, i))
          ta = result1*(tp(iwall+1, i)-tp(iwall-1, i))/(y(iwall, i)+&
&           0.5_R8*(y(iwall-1, i)+y(iwall+1, i)))**2
          result1 = KAPPA_PERP_NODIFF(bndx(iwall), i, tp(iwall, i))
          tb = 2.0_R8*result1/y(iwall, i)/(y(iwall+1, i)+y(iwall, i))
          result1 = KAPPA_PERP_NODIFF(bndx(iwall), i, tp(iwall, i))
          tc = 2.0_R8*result1/y(iwall, i)/(y(iwall, i)+y(iwall-1, i))
        END IF
        aa5(iwall, i, -1) = aa5(iwall, i, -1) + ta - tc
        aa5(iwall, i, 0) = aa5(iwall, i, 0) + tb + tc
        aa5(iwall, i, 1) = aa5(iwall, i, 1) - ta - tb
      END DO
    END DO
!    ..treat radiative cooling b.c.
    iwall = nw
    DO i=1,ndepth_nml-1
!! FIXME: Careful about the area over which the radiative cooling occurs, it should be here the side area, not the top area !!
!! Can also include evaporative cooling term !!
!! Need to make sure the correct side material properties are used !!
      result1 = QRAD_NODIFF(bndx(iwall), tp(iwall, i), .false.)
      result2 = QEVAP_NODIFF(bndx(iwall), ns, tp(iwall, i))
      qtot = -result1 - result2
      result1 = KAPPA_PERP_NODIFF(bndx(iwall), i, tp(iwall, i))
      t0 = result1/(0.5_R8*(y(iwall, i)+y(iwall-1, i)))
      aa5(iwall, i, -1) = aa5(iwall, i, -1) - t0
      aa5(iwall, i, 0) = aa5(iwall, i, 0) + t0
      aa5(iwall, i, 1) = 0.0_R8
      rhs(iwall, i) = rhs(iwall, i) + qtot
    END DO
!
!   ..add the d/dt term contribution
    DO iwall=1,nw
      DO i=1,ndepth_nml-1
        IF (target_vol(bndx(iwall), i) .GT. 0.0_R8) THEN
          result1 = MASS_DENSITY_NODIFF(bndx(iwall), i)
          rho = result1*(x(i+1)-x(i))
!! FIXME : Need to test if proper quantity here is
! or may be :
!    .             target_vol(bndx(iwall),i)
          result1 = CP_NODIFF(bndx(iwall), i, tp(iwall, i))
          t0 = rho*result1/dt
          aa5(iwall, i, 0) = aa5(iwall, i, 0) + t0
          rhs(iwall, i) = rhs(iwall, i) + t0*tp(iwall, i)
        END IF
      END DO
    END DO
!
!   ..correct equations for special case of zero or negative width
    DO iwall=2,nw-1
      DO i=1,ndepth_nml
        IF (target_width(bndx(iwall), i) .LE. 0.0_R8) THEN
          aa5(iwall, i, -2) = 0.0_R8
          aa5(iwall, i, -1) = -0.5_R8
          aa5(iwall, i, 0) = 1.0_R8
          aa5(iwall, i, 1) = -0.5_R8
          aa5(iwall, i, 2) = 0.0_R8
          rhs(iwall, i) = 0.0_R8
        END IF
      END DO
    END DO
!
!   ..solve the temperature equation
!    ..using slv5pt
    iters = 1
    IF (nw .GT. ndepth_nml) THEN
      min2 = ndepth_nml
    ELSE
      min2 = nw
    END IF
    CALL SLV5PT(nw, ndepth_nml, aa5, rhs, iters, u, nrmc, min2, aaf, &
&         wrk0, iwrk)
!
!   ..copy over new solution
    DO iwall=1,nw
      DO i=1,ndepth_nml
        IF (u(iwall, i) .GT. matsurf_vapor_temp(bndx(iwall))) THEN
          tp(iwall, i) = matsurf_vapor_temp(bndx(iwall))
        ELSE
          tp(iwall, i) = u(iwall, i)
        END IF
      END DO
    END DO
!
    DEALLOCATE(aaf)
    DEALLOCATE(wrk0)
    DEALLOCATE(wrk1)
    DEALLOCATE(wrk2)
    DEALLOCATE(iwrk)
    CALL SUBEND()
    RETURN
  END IF
END SUBROUTINE HEATDIFF2D_NODIFF

