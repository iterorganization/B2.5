&                             icv))
                          END DO
                          alf0(icv) = poloidal_scale_factor*prof(iy)*dv%&
&                           ne(icv)*result1
!
                        ELSE
                          WRITE(*, *) 'Option', kind_coeff, &
&                         'not yet implemented'
                        END IF
                      END IF
                    END DO
                  END IF
                END DO
              END DO
            END DO
