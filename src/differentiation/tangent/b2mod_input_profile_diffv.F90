  SUBROUTINE REGION_PROFILE(mpg, geo, kind_data, ndat, r, ndim, pr, ifa&
&   , nfitanf)
!
    USE B2MOD_INDIRECT_DIFFV
    USE B2MOD_USER_NAMELIST_DIFFV, ONLY : nomp, omp, icsepomp
    USE B2US_GEO_DIFFV
    USE B2US_MAP_DIFFV
    USE B2MOD_MWTI, ONLY : output_ds_cv
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(GEOMETRY), INTENT(IN) :: geo
    INTEGER :: i, ifa
    INTEGER :: nfitanf, nfitend, ndim, ndat, kind_data
    REAL(kind=r8) :: r(nrr), pr(nrr), prtmp
    REAL(kind=r8), ALLOCATABLE :: pra(:), prdsa(:)
    INTEGER :: ncall, nya
    CHARACTER(len=256) :: filenamer
    LOGICAL :: exist
    EXTERNAL IPGETR
    SAVE ncall
    EXTERNAL FIND_FILE
    INTEGER :: arg1
    DATA ncall /0/
!
    CALL SUBINI('region_profile')
    IF (ncall .EQ. 0) THEN
      IF (nomp .GT. 0) THEN
        ALLOCATE(pra(nomp))
        ALLOCATE(prdsa(nomp))
        arg1 = icsepomp - 1
        CALL OUTPUT_DS_CV(mpg, geo, nomp, omp, arg1, 'dsp')
      END IF
      CALL IPGETI('b2trno_csig_an_style', csig_an_style)
    END IF
!
    filenamer = 'dsp'
    CALL FIND_FILE(filenamer, exist)
    IF (exist) THEN
      OPEN(unit=91, file=filenamer) 
      i = 0
      nya = 0
 91   i = i + 1
      READ(91, *, end=92, err=97) prtmp
      prdsa(i) = prtmp
      nya = i
      IF (i .LT. nomp) GOTO 91
 92   nfitanf = 1
      ifa = 0
      DO i=1,nya
! input in metres from separatrix
        IF (kind_data .EQ. 1) pra(i) = prdsa(i)
! input in poloidal rho
        IF (kind_data .EQ. 2) THEN
          WRITE(*, *) 'Input profile in poloidal rho not yet coded!'
          GOTO 97
        ELSE
!
          IF (pra(i) .LT. r(1)) nfitanf = nfitanf + 1
          nfitend = i - 1
          IF (pra(i) .GT. r(ndat)) THEN
            GOTO 100
          ELSE
            nfitend = i
          END IF
        END IF
      END DO
!
 100  IF (nfitend .GT. nfitanf) THEN
!
        ndim = nfitend + 1 - nfitanf
!     allocate (pr(1:ndim))
!
        DO i=1,ndim
          pr(i) = pra(nfitanf-1+i)
        END DO
        GOTO 108
      END IF
 97   ifa = 1
!
 108  CLOSE(91) 
      ncall = ncall + 1
      CALL SUBEND()
      RETURN
    ELSE
      ifa = 1
      CALL SUBEND()
      RETURN
    END IF
  END SUBROUTINE REGION_PROFILE

