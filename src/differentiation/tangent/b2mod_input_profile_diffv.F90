!
!-----------------------------------------------------------------------
!
  SUBROUTINE TRANSPORT_INPUT(ncv, ns, geo, mpg, pl, dv, rt, hcib, dna0, &
&   dpa0, vla0, vsa0, hci0, hce0, sig0, alf0, ncall)
!
!**************************************************************************
!*     subroutine for reading in experimental transport coefficients from *
!*     the midplane and calculating transport coefficients                *
!*     for B2.5 (written by Heimo Buerbaumer 1999)                        *
!*     Inputfile: b2.transport.inputfile                                  *
!**************************************************************************
    USE B2MOD_TIME
    USE B2MOD_CONSTANTS
    USE B2MOD_B2CMPA_DIFFV
    USE B2MOD_USER_NAMELIST_DIFFV, ONLY : nomp, omp, icsepomp
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ncv, ns
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2DERIVATIVES), INTENT(IN) :: dv
    TYPE(B2RATES), INTENT(IN) :: rt
!   ..output arguments (unspecified on entry)
    REAL(kind=r8) :: dna0(ncv, 0:ns-1), dpa0(ncv, 0:ns-1), vla0(ncv, 0:1&
&   , 0:ns-1), vsa0(ncv, 0:ns-1), hci0(ncv), hcib(ncv, 0:ns-1)
    REAL(kind=r8) :: hce0(ncv), sig0(ncv), alf0(ncv)
    INTEGER :: iy, iy2, i, ifail, spec, nspec, iir, ift, icv, ndim, is, &
&   nsp(ns+1), iloop, ncall, ndat, kind_coeff, kind_data, ic, lbl
    REAL(kind=r8) :: f(nrr), r(nrr), pr(nrr), pf(nrr), d(nrr), pelm(nrr)&
&   , felm(nrr), delm(nrr), prof(nrr)
    REAL(kind=r8) :: poloidal_scale_factor
    LOGICAL, SAVE :: elm_region, elm_data
    CHARACTER(len=26) :: hlp_frm
    LOGICAL :: catch_up, lpflux, ldiv
    INTRINSIC SQRT
    EXTERNAL CALC_DIST_NODIFF, E01BEF, E01BFF
    EXTERNAL XERRAB
    INTRINSIC INT
    INTRINSIC MOD
    INTRINSIC ANY
    INTRINSIC TRIM
    REAL(kind=r8) :: arg1
    REAL(kind=r8) :: result1
!
    IF (ncall .EQ. 0) THEN
      CALL XERTST(cvregmax .GE. mpg%nnreg(0), &
&           'Increase CVREGMAX in b2mod_numerics_namelist')
      tr_ip_new_files = .true.
!  read the profile data
      IF (transport_ip_time_mod .GT. 0.0_R8) THEN
        CALL XERRAB('ELM transport inputifile not coded yet for WG')
        arg1 = tim/transport_ip_time_mod
        tr_ip_elm_count = INT(arg1)
      ELSE
        tr_ip_elm_count = 0
      END IF
      IF (transport_ip_time_mod .GT. 0.0_R8) THEN
        catch_up = MOD(tim, transport_ip_time_mod) .GE. &
&         transport_ip_time_switch .AND. transport_ip_time_switch .GT. &
&         0.0_R8
      ELSE IF (transport_ip_time_switch .GT. 0.0_R8) THEN
        catch_up = tim .GE. transport_ip_time_switch
      ELSE
        catch_up = .false.
      END IF
      DO WHILE (catch_up)
        WRITE(*, *) 'transport_input: catching up ...'
        CALL READ_TRANSPORT_INPUT(transport_ip_filename)
        IF (transport_ip_time_mod .GT. 0.0_R8) THEN
          catch_up = transport_ip_time_switch .GT. 0.0_R8 .AND. MOD(tim&
&           , transport_ip_time_mod) .GE. transport_ip_time_switch
        ELSE IF (transport_ip_time_switch .GT. 0.0_R8) THEN
          catch_up = tim .GE. transport_ip_time_switch
        ELSE
          catch_up = .false.
        END IF
      END DO
      CALL XERTST(nomp .GT. 0, &
&           'No CVs in omp list, check rzomp in b2.user.parameters')
      CALL XERTST(icsepomp .GT. 0, &
&           'Invalid icsepomp value, check rzomp in b2.user.parameters')
      IF (ANY(poloidal_scaling)) CALL XERRAB(&
&                                'Poloidal scaling not yet coded for WG'&
&                                     )
      CALL XERTST(no_div .OR. mpg%isclassicalgrid .EQ. 1, &
&           'no_div should be true for non-classical vessel mode grids')
    ELSE
      IF (transport_ip_time_mod .GT. 0.0_R8) THEN
        arg1 = tim/transport_ip_time_mod
        WRITE(*, '(a,1p,3(1x,e14.7),2i4)') 'ELM: ', tim, MOD(tim, &
&       transport_ip_time_mod), transport_ip_time_switch, INT(arg1), &
&       tr_ip_elm_count
        arg1 = tim/transport_ip_time_mod
        catch_up = (MOD(tim, transport_ip_time_mod) .GE. &
&         transport_ip_time_switch .AND. transport_ip_time_switch .GT. &
&         0.0_R8) .OR. INT(arg1) .GT. tr_ip_elm_count
      ELSE IF (transport_ip_time_switch .GT. 0.0_R8) THEN
        catch_up = tim .GE. transport_ip_time_switch
        IF (catch_up) WRITE(*, '(a,a)') &
&                     'Switching to next transport input file ', TRIM(&
&                     transport_ip_filename)
      ELSE
        catch_up = .false.
      END IF
      IF (catch_up) THEN
        IF (transport_ip_time_mod .GT. 0.0_R8) THEN
          arg1 = tim/transport_ip_time_mod
          tr_ip_elm_count = INT(arg1)
        END IF
        tr_ip_new_files = .true.
        CALL READ_TRANSPORT_INPUT(transport_ip_filename)
      END IF
    END IF
!
    IF (tr_ip_new_files) THEN
      OPEN(94, file='da1') 
      OPEN(93, file='da2') 
    END IF
!
    elm_data = .false.
!        write (*,*) 'vor loop'
    iloop = 0
    DO kind_coeff=1,nkind_coeff
      DO spec=0,ns-1
        DO kind_data=1,nkind_data
!           write (*,*) 'loop-begin', kind_coeff,spec,kind_data
          ndat = ndata(kind_data, kind_coeff, spec)
          IF (ndat .NE. 0) THEN
            CALL XERTST(ndat .LE. nrr, &
&            'Increase NYD in b2mod_dimensions to match size of profile'&
&                )
            nspec = 1
            nsp(1) = spec
            DO is=1,ns
              IF (addspec(is, kind_coeff, spec) .GE. 0) THEN
                nsp(nspec+1) = addspec(is, kind_coeff, spec)
                nspec = nspec + 1
              END IF
            END DO
            DO i=1,ndat
              r(i) = tdata(1, i, kind_coeff, spec)
              f(i) = tdata(2, i, kind_coeff, spec)
              IF (elm_data) felm(i) = tdata(3, i, kind_coeff, spec)
            END DO
!             if (tr_ip_new_files) write(93,*) r(i),f(i),felm(i)
! csc for now identify pr with r-coordinate of midplane CVs
! csvdk reworked the implementation to be consistent with structured
! csvdk code. Now based on euclidean distance in (R,Z) coordinates.
!
            CALL CALC_DIST_NODIFF(geo, omp, nomp, icsepomp, pr)
!
            CALL E01BEF(ndat, r, f, d, ifail)
            IF (elm_data) CALL E01BEF(ndat, r, felm, delm, ifail)
!           write(*,*) 'zwischen,ifail=',ifail
! FIXME
            ndim = nomp
            CALL E01BFF(ndat, r, f, d, ndim, pr, pf, ifail)
!           if (iloop.eq.0) print *,'PF=',pf(1:ndim)
            IF (elm_data) CALL E01BFF(ndat, r, felm, delm, ndim, pr, &
&                               pelm, ifail)
!
            IF (tr_ip_new_files) THEN
!
              IF (elm_data) THEN
                DO iy=1,ndat
                  WRITE(93, *) r(iy), f(iy), felm(iy)
                END DO
!
                DO iy=1,ndim
                  WRITE(94, *) pr(iy), pf(iy), pelm(iy)
                END DO
              ELSE
                DO iy=1,ndat
                  WRITE(93, *) r(iy), f(iy)
                END DO
!
                DO iy=1,ndim
                  WRITE(94, *) pr(iy), pf(iy)
                END DO
              END IF
            END IF
!
            DO is=1,nspec
              DO 110 iy=1,ndim
                IF (mpg%isclassicalgrid .EQ. 0) THEN
! skip guard cells
                  IF (iy .EQ. 1 .OR. iy .EQ. ndim) GOTO 110
                END IF
                iy2 = iy
                iir = mpg%cvreg(omp(iy))
                IF (iir .LE. 0 .OR. iir .GT. cvregmax) THEN
                  WRITE(*, *) 'transport_input: out of bounds ', &
&                 'iy,region(ix,iy) ', iy, iir
                  iir = cvregmax
                END IF
                lbl = mpg%ftlbl(mpg%cvft(omp(iy)))
                DO 100 ift=1,mpg%nft
                  IF (mpg%isclassicalgrid .EQ. 1) THEN
                    IF (mpg%ftlbl(ift) .NE. lbl) THEN
                      GOTO 100
                    ELSE
                      iy2 = iy
                    END IF
                  ELSE IF (ift .NE. mpg%cvft(omp(iy))) THEN
                    IF (ift .LE. mpg%cvft(omp(ndim-1)) .OR. ift .GE. mpg&
&                       %cvft(omp(2))) THEN
                      GOTO 100
                    ELSE
!nh flux tube outside OMP ==> take last OMP flux tube
                      iy2 = ndim
                    END IF
                  ELSE
                    iy2 = iy
                  END IF
!
                  DO ic=mpg%ftcvp(ift, 1),mpg%ftcvp(ift, 1)+mpg%ftcvp(&
&                     ift, 2)-1
                    icv = mpg%ftcv(ic)
                    iir = mpg%cvreg(icv)
                    lpflux = mpg%ftreg(mpg%cvft(icv)) .EQ. 3
                    ldiv = MOD(iir - 1, 4) + 1 .GE. 3
                    poloidal_scale_factor = 1.0_R8
                    elm_region = .false.
                    IF (.NOT.(((.NOT.region_flags(iir, kind_coeff)) .OR.&
&                       (lpflux .AND. no_pflux)) .OR. (ldiv .AND. no_div&
&                       ))) THEN
!
                      IF (elm_region) THEN
                        prof(1:ndim) = pelm(1:ndim)
                      ELSE
                        prof(1:ndim) = pf(1:ndim)
                      END IF
!
                      IF (kind_coeff .EQ. 1) THEN
                        dna0(icv, nsp(is)) = prof(iy2)*&
&                         poloidal_scale_factor
!
                      ELSE IF (kind_coeff .EQ. 2) THEN
                        dpa0(icv, nsp(is)) = prof(iy2)*&
&                         poloidal_scale_factor/(rt%rza(icv, nsp(is))*pl&
&                         %te(icv)+pl%ti(icv))
!
                      ELSE IF (kind_coeff .EQ. 3) THEN
                        hcib(icv, nsp(is)) = poloidal_scale_factor*prof(&
&                         iy2)*pl%na(icv, nsp(is))
!
                      ELSE IF (kind_coeff .EQ. 4) THEN
                        hce0(icv) = prof(iy2)*dv%ne(icv)*&
&                         poloidal_scale_factor
!
                      ELSE IF (kind_coeff .EQ. 5) THEN
                        vla0(icv, 0, nsp(is)) = prof(iy2)*&
&                         poloidal_scale_factor
!
                      ELSE IF (kind_coeff .EQ. 6) THEN
                        vla0(icv, 1, nsp(is)) = prof(iy2)*&
&                         poloidal_scale_factor
!
                      ELSE IF (kind_coeff .EQ. 7) THEN
                        vsa0(icv, nsp(is)) = poloidal_scale_factor*prof(&
&                         iy2)*mp*am(nsp(is))*pl%na(icv, nsp(is))
!
                      ELSE IF (kind_coeff .EQ. 8) THEN
                        IF (csig_an_style .EQ. 0) THEN
                          sig0(icv) = prof(iy2)*qe*dv%ne(icv)*&
&                           poloidal_scale_factor
                        ELSE IF (csig_an_style .EQ. 1 .AND. nomp .GT. 0&
&                       ) THEN
                          sig0(icv) = prof(iy2)*qe*dv%ne(omp(1))*&
&                           poloidal_scale_factor
                        END IF
                      ELSE IF (kind_coeff .EQ. 9) THEN
!
                        arg1 = qe/pl%te(icv)
                        result1 = SQRT(arg1)
                        alf0(icv) = poloidal_scale_factor*prof(iy2)*dv%&
&                         ne(icv)*result1
!
                      ELSE
                        WRITE(*, *) 'Option', kind_coeff, &
&                       'not yet implemented'
                      END IF
                    END IF
                  END DO
 100            CONTINUE
 110          CONTINUE
!
!ic
!ift=1,mpg%nFt
!iy=1,ndim
              IF (mpg%isclassicalgrid .EQ. 0) THEN
! treatment of guard cells
                DO 120 icv=1,ncv
                  IF (mpg%cvfcp(icv, 2) .EQ. 1) THEN
                    iir = mpg%cvreg(icv)
                    IF (iir .EQ. 1 .OR. iir .EQ. 5) THEN
! core boundary
                      iy2 = 1
                    ELSE IF (iir .EQ. 2 .OR. iir .EQ. 6) THEN
! SOL boundary
                      iy2 = ndim
                    ELSE
                      GOTO 120
                    END IF
                    lpflux = mpg%ftreg(mpg%cvft(icv)) .EQ. 3
                    ldiv = MOD(iir - 1, 4) + 1 .GE. 3
                    poloidal_scale_factor = 1.0_R8
                    elm_region = .false.
                    IF (.NOT.(((.NOT.region_flags(iir, kind_coeff)) .OR.&
&                       (lpflux .AND. no_pflux)) .OR. (ldiv .AND. no_div&
&                       ))) THEN
!
                      IF (elm_region) THEN
                        prof(1:ndim) = pelm(1:ndim)
                      ELSE
                        prof(1:ndim) = pf(1:ndim)
                      END IF
!
                      IF (kind_coeff .EQ. 1) THEN
                        dna0(icv, nsp(is)) = prof(iy2)*&
&                         poloidal_scale_factor
!
                      ELSE IF (kind_coeff .EQ. 2) THEN
                        dpa0(icv, nsp(is)) = prof(iy2)*&
&                         poloidal_scale_factor/(rt%rza(icv, nsp(is))*pl&
&                         %te(icv)+pl%ti(icv))
!
                      ELSE IF (kind_coeff .EQ. 3) THEN
                        hcib(icv, nsp(is)) = poloidal_scale_factor*prof(&
&                         iy2)*pl%na(icv, nsp(is))
!
                      ELSE IF (kind_coeff .EQ. 4) THEN
                        hce0(icv) = prof(iy2)*dv%ne(icv)*&
&                         poloidal_scale_factor
!
                      ELSE IF (kind_coeff .EQ. 5) THEN
                        vla0(icv, 0, nsp(is)) = prof(iy2)*&
&                         poloidal_scale_factor
!
                      ELSE IF (kind_coeff .EQ. 6) THEN
                        vla0(icv, 1, nsp(is)) = prof(iy2)*&
&                         poloidal_scale_factor
!
                      ELSE IF (kind_coeff .EQ. 7) THEN
                        vsa0(icv, nsp(is)) = poloidal_scale_factor*prof(&
&                         iy2)*mp*am(nsp(is))*pl%na(icv, nsp(is))
!
                      ELSE IF (kind_coeff .EQ. 8) THEN
                        IF (csig_an_style .EQ. 0) THEN
                          sig0(icv) = prof(iy2)*qe*dv%ne(icv)*&
&                           poloidal_scale_factor
                        ELSE IF (csig_an_style .EQ. 1 .AND. nomp .GT. 0&
&                       ) THEN
                          sig0(icv) = prof(iy2)*qe*dv%ne(omp(1))*&
&                           poloidal_scale_factor
                        END IF
                      ELSE IF (kind_coeff .EQ. 9) THEN
!
                        arg1 = qe/pl%te(icv)
                        result1 = SQRT(arg1)
                        alf0(icv) = poloidal_scale_factor*prof(iy2)*dv%&
&                         ne(icv)*result1
!
                      ELSE
                        WRITE(*, *) 'Option', kind_coeff, &
&                       'not yet implemented'
                      END IF
                    END IF
                  END IF
 120            CONTINUE
              END IF
            END DO
!
!is=1,nspec
            IF (ncall .EQ. 0) THEN
              IF (elm_data) THEN
                WRITE(hlp_frm, '(a,i3,a)') '(a,1x,a,', nspec, &
&               '(1x,i3),3f14.7)'
                DO iy=1,ndim
                  IF (kind_coeff .EQ. 1) WRITE(*, hlp_frm) &
&                                        'transport=Dna', 'spec=', (nsp(&
&                                        i), i=1,nspec), pr(iy), pf(iy)&
&                                        , pelm(iy)
                  IF (kind_coeff .EQ. 2) WRITE(*, hlp_frm) &
&                                        'transport=Dpa', 'spec=', (nsp(&
&                                        i), i=1,nspec), pr(iy), pf(iy)&
&                                        , pelm(iy)
                  IF (kind_coeff .EQ. 3) WRITE(*, hlp_frm) &
&                                        'transport=hci', 'spec=', (nsp(&
&                                        i), i=1,nspec), pr(iy), pf(iy)&
&                                        , pelm(iy)
                  IF (kind_coeff .EQ. 4) WRITE(*, '(a,3f14.7)') &
&                                        'transport=hce', pr(iy), pf(iy)&
&                                        , pelm(iy)
                  IF (kind_coeff .EQ. 5) WRITE(*, hlp_frm) &
&                                        'transport=vlax', 'spec=', (nsp&
&                                        (i), i=1,nspec), pr(iy), pf(iy)&
&                                        , pelm(iy)
