! WDK/SRV 21.07.2016 factor 4/3 removed from vsa, but added explicitly to cvsahz (not cvsa),
!         according to old model.eq.3 treatment
          result11 = SQRT(2.0_R8)
          temp = switch%b2tqca_phm0*eta*result11
          DO nd=1,nbdirs
! SOLPS5.0 model : Balescu 29-moment result
            hcid(nd, icv, 0) = hcid(nd, icv, 0) + fki*2.5_R8*(tnp*pld%ti&
&             (nd, icv)+pl%ti(icv)*tnpd(nd))/(mp*am(is))
            vsad(nd, icv, 0, is) = temp*(tnp*pld%ti(nd, icv)+pl%ti(icv)*&
&             tnpd(nd))
          END DO
          hci(icv, 0) = hci(icv, 0) + 2.5_R8*(pl%ti(icv)/(mp*am(is)))*&
&           fki*tnp
          vsa(icv, 0, is) = temp*(pl%ti(icv)*tnp)
        ELSE IF (switch%b2tqca_model .EQ. 2) THEN
! old SOLPS4.0 model : Braginskii
          w = 0.0_R8
          t = 0.0_R8
          DO nd=1,nbdirsmax
            td(nd) = 0.D0
          END DO
          DO nd=1,nbdirsmax
            wd(nd) = 0.D0
          END DO
          DO js=0,ns-1
            arg10 = 2.0_R8*mp*am(is)*am(js)/(am(is)+am(js))
            result10 = SQRT(arg10)
            arg11 = (am(is)+am(js))/2.0_R8
            result1 = SQRT(arg11)
            temp1 = rt%rz2(icv, js)/result1
            DO nd=1,nbdirs
              wd(nd) = wd(nd) + result10*(pl%na(icv, js)*rtd%rz2(nd, icv&
&               , js)+rt%rz2(icv, js)*pld%na(nd, icv, js))
              td(nd) = td(nd) + pl%na(icv, js)*rtd%rz2(nd, icv, js)/&
&               result1 + temp1*pld%na(nd, icv, js)
            END DO
            w = w + rt%rz2(icv, js)*pl%na(icv, js)*result10
            t = t + temp1*pl%na(icv, js)
          END DO
          DO js=0,st_ext%ns-1
            arg10 = 2.0_R8*mp*am(is)*st_ext%am(js)/(am(is)+st_ext%am(js)&
&             )
            result10 = SQRT(arg10)
            w = w + st_ext%za2(icv, js)*st_ext%na(icv, js)*result10
            arg11 = (am(is)+st_ext%am(js))/2.0_R8
            result1 = SQRT(arg11)
            t = t + st_ext%za2(icv, js)*st_ext%na(icv, js)/result1
          END DO
!xpb            ctaui = ctaup*sqrt(2.0_R8)/lnlam/rz2(iCv,is)  ! lower precision as per 4.0
!srv 01.07.09
          temp1 = 2.1e13_R8/(dv%lnlam(icv)*rt%rz2(icv, is))
          arg11 = ev*mp
          result10 = SQRT(arg11)
          DO nd=1,nbdirs
            ctauid(nd) = -(temp1*(rt%rz2(icv, is)*dvd%lnlam(nd, icv)+dv%&
&             lnlam(icv)*rtd%rz2(nd, icv, is))/(dv%lnlam(icv)*rt%rz2(icv&
&             , is)))
            fxid(nd) = ci*ctauid(nd)/(ev*result10)
            arg10d(nd) = pld%ti(nd, icv)
          END DO
          ctaui = temp1
          fxi = ci*ctaui/(ev*result10)
          result10 = PITX(icv)
          arg10 = pl%ti(icv)
          temp1 = SQRT(arg10)
          DO nd=1,nbdirs
            IF (arg10 .EQ. 0.D0) THEN
              result20d(nd) = 0.D0
            ELSE
              result20d(nd) = arg10d(nd)/(2.0*temp1)
            END IF
            arg10d(nd) = pld%ti(nd, icv)
          END DO
          result20 = temp1
          temp1 = pl%ti(icv)*pl%ti(icv)*fxi*result20
          temp0 = pl%na(icv, is)/w
          DO nd=1,nbdirs
            hcid(nd, icv, 0) = hcid(nd, icv, 0) + result10**2*(temp1*(&
&             pld%na(nd, icv, is)-temp0*wd(nd))/w+temp0*(fxi*result20*2*&
&             pl%ti(icv)*pld%ti(nd, icv)+pl%ti(icv)**2*(result20*fxid(nd&
&             )+fxi*result20d(nd))))
          END DO
          hci(icv, 0) = hci(icv, 0) + result10*result10*(temp0*temp1)
!wdk      no factor 4/3 here, for consistent treatment with model.eq.1
          result10 = SQRT(ev)
          result2 = PITX(icv)
          arg10 = pl%ti(icv)
          temp1 = SQRT(arg10)
          DO nd=1,nbdirs
            IF (arg10 .EQ. 0.D0) THEN
              result3d(nd) = 0.D0
            ELSE
              result3d(nd) = arg10d(nd)/(2.0*temp1)
            END IF
          END DO
          result3 = temp1
          temp = switch%b2tqca_phm0*eta*(result2*result2)
          temp2 = ev*result10*t
          temp1 = pl%ti(icv)*pl%ti(icv)/temp2
          temp0 = pl%na(icv, is)*ctaui*result3
          DO nd=1,nbdirs
            vsad(nd, icv, 0, is) = temp*(temp1*(ctaui*result3*pld%na(nd&
&             , icv, is)+pl%na(icv, is)*(result3*ctauid(nd)+ctaui*&
&             result3d(nd)))+temp0*(2*pl%ti(icv)*pld%ti(nd, icv)-temp1*&
&             ev*result10*td(nd))/temp2)
          END DO
          vsa(icv, 0, is) = temp*(temp0*temp1)
        ELSE
          CALL XERRAB('b2tqca--invalid value b2tqca_model > 2. ')
        END IF
      END DO
      IF ((switch%b2npht_iout .NE. 0 .OR. switch%b2tqca_iout .NE. 0) &
&         .OR. switch%iout_b2wdat .EQ. 4) THEN
        WRITE(chns, '(i3.3)') is
        arg12(:) = 'b2tqca_taua'//chns
        CALL MY_OUT_US(70, ncv, 0, tau, arg12(:))
      END IF
    END IF
  END DO
!   ..compute vsay, hciy
!!!   (classical perpendicular transport is set to 0)
  DO is=0,ns-1
    DO nd=1,nbdirsmax
      dummyzerodiffd0(nd) = 0.D0
    END DO
    CALL SFILL_DV(ncv, 0.0_R8, dummyzerodiffd0, vsa(1, 1, is), vsad(:, 1&
&           , 1, is), 1, nbdirs)
  END DO
  DO nd=1,nbdirsmax
    dummyzerodiffd1(nd) = 0.D0
  END DO
  CALL SFILL_DV(ncv, 0.0_R8, dummyzerodiffd1, hci(1, 1), hcid(:, 1, 1), &
&         1, nbdirs)
!srv 16.06.08 }
!
  IF (switch%b2tqca_iout .NE. 0 .OR. switch%b2npmo_iout .NE. 0) THEN
!srv 16.06.08 30.01.10 {
    DO is=0,ns-1
      WRITE(chns, '(i3.3)') is
      arg13(:) = 'b2tqca_vsa_th'//chns
      CALL MY_OUT_US(70, ncv, 0, vsa(1, 0, is), arg13(:))
      arg14(:) = 'b2tqca_vsa_r'//chns
      CALL MY_OUT_US(70, ncv, 0, vsa(1, 1, is), arg14(:))
    END DO
  END IF
!        call my_out_us(70,nCv,0,pl%na(1,is),'b2tqca_na'//chns)
!        call my_out_us(70,nCv,0,lnlam,'b2tqca_lnlam')
!        call my_out_us(70,nCv,0,pl%ti,'b2tqca_ti')
!        call my_out_us(70,nCv,0,dv%ne,'b2tqca_ne')
!        call my_out_us(70,nCv,0,dv%ne2,'b2tqca_ne2')
!        call my_out_us(70,nCv,0,geo%cvBb(1,0),'b2tqca_Bx')
!        call my_out_us(70,nCv,0,geo%cvBb(1,3),'b2tqca_BB')
!
  IF (switch%b2tqca_iout .NE. 0 .OR. switch%iout_b2wdat .EQ. 4) THEN
!srv 16.06.08 30.01.10 {
    CALL MY_OUT_US(70, ncv, 0, dv%lnlam, 'b2tqca_lnlam')
  END IF
!srv 16.06.08 30.01.10 }
!
  IF (switch%b2tqca_iout .NE. 0 .OR. switch%b2npht_iout .NE. 0) THEN
    CALL MY_OUT_US(70, ncv, 0, hci(1, 0), 'b2tqca_hci_th')
    CALL MY_OUT_US(70, ncv, 0, hci(1, 1), 'b2tqca_hci_r')
  END IF
!
! ..return
  ncall_b2tqca = ncall_b2tqca + 1
  CALL SUBEND()
  RETURN

CONTAINS
