  FUNCTION PITX(icv)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    REAL(kind=r8) :: pitx
    INTEGER, INTENT(IN) :: icv
    INTRINSIC ABS
    REAL(kind=r8) :: abs0
    IF (geo%cvbb(icv, 0) .GE. 0.) THEN
      abs0 = geo%cvbb(icv, 0)
    ELSE
      abs0 = -geo%cvbb(icv, 0)
    END IF
!
    pitx = abs0/geo%cvbb(icv, 3)
!
    RETURN
  END FUNCTION PITX

