SUBROUTINE CALC_DIST_NODIFF(geo, clist, nc, iref, ds)
!
  USE B2MOD_TYPES
  USE B2US_GEO_DIFFV
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
! Input variables
  TYPE(GEOMETRY), INTENT(IN) :: geo
! Output variables
  INTEGER, INTENT(IN) :: nc, iref, clist(nc)
!
! Intermediate variables
  REAL(kind=r8), INTENT(OUT) :: ds(nc)
!
  REAL(kind=r8) :: dsref
! External functions
  INTEGER :: icv
!
! Main program
!=============
! Call subini
  EXTERNAL XERTST
  INTRINSIC SQRT
  REAL(kind=r8) :: arg1
  REAL(kind=r8) :: result1
!
! csc added safety for possible reference not within list number
  CALL SUBINI('calc_dist')
! Compute the initial length as the distance of the first cell to
! the first boundary face
  CALL XERTST(iref .GE. 0 .AND. iref .LE. nc, &
&       'calc_dist: iref out of range')
!
! Loop over number of cells
  ds(1) = 0
!
! Compute cumulative distance
  DO icv=2,nc
    arg1 = (geo%cvx(clist(icv))-geo%cvx(clist(icv-1)))**2 + (geo%cvy(&
&     clist(icv))-geo%cvy(clist(icv-1)))**2
    result1 = SQRT(arg1)
    ds(icv) = ds(icv-1) + result1
! If reference cell provided, shift distance computation
  END DO
! Call subend and return
!
  IF (iref .GT. 1) THEN
! Compute reference point
! Subtract reference length
    dsref = (ds(iref)+ds(iref-1))/2.0_R8
!
    ds = ds - dsref
  END IF
!
  CALL SUBEND()
  RETURN
END SUBROUTINE CALC_DIST_NODIFF

