        WRITE(*, '(1x,a,i3,a,i3,a,g10.3)') 'Chemical sputtering from ', &
&       sput_src, ' producing ', sput_dst, ' at rate ', switch%&
&       b2stbr_sput_frc
      ELSE IF (sput_chem_model .EQ. 0) THEN
