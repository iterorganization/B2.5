  new_sputter_namelist = neutrals_namelist .EQ. 1 .OR. switch%use_eirene&
&   .NE. 0
!
  CALL READ_NEUTRALS_NAMELIST_DV(ns, mpg, mpgd, switch, &
&                          new_sputter_namelist, nbdirs)
  CALL XERTST(nstrat .LE. nstraid, &
