  IF (neutrals_time_mod .GT. 0.0_R8) THEN
    arg1 = tim/neutrals_time_mod
    neutrals_count = INT(arg1)
  ELSE
    neutrals_count = 0
  END IF
  IF (neutrals_time_mod .GT. 0.0_R8) THEN
