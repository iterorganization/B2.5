!     See the companion routine b2mwqt for a precise description.
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2mwq0')
!   ..test nput
  CALL XERTST(1 .LE. nput, 'faulty argument nput')
!   ..test ns
  CALL XERTST(1 .LE. ns, 'faulty argument ns')
!   ..set nfld
  nfld = 0
  IF (1 .LE. switch%b2mwqt_style) nfld = nfld + 2 + 2*(2*ns+7)
  IF (2 .LE. switch%b2mwqt_style) nfld = nfld + 36*(ns+3)
  IF (3 .LE. switch%b2mwqt_style) nfld = nfld + 40*(ns+3)
  IF (4 .LE. switch%b2mwqt_style) nfld = nfld + 72*(ns+3)
!   ..set ll
!     (silly time; ll is needed as an argument to cfwuin)
  ll(0) = 0
  ll(1) = 1
!   ..write nfld
  idum(0) = nfld
  CALL CFWUIN(nput, 1, idum(0), 'nfld')
!
! ..write titles and labels
  k = 0
  IF (1 .LE. switch%b2mwqt_style) THEN
!    ..iteration counters
    chtit = 'time step counter'
    chlab = 'itim'
    CALL CFWUIN(nput, 1, ll(0), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'iteration counter'
    chlab = 'itcnt'
    CALL CFWUIN(nput, 1, ll(0), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
!     ..norms of residuals
    DO is=0,ns-1
      WRITE(id, '(i3)') is
      chtit = 'norm of residual of continuity equation, species '//id
      chlab = 'aresco('//id//')'
      CALL CFWUIN(nput, 1, ll(1), 'linlog')
      CALL CFWUCH(nput, 60, chtit, 'title')
      CALL CFWUCH(nput, 12, chlab, 'label')
      k = k + 1
    END DO
    DO is=0,ns-1
      WRITE(id, '(i3)') is
      chtit = 'norm of residual of parallel momentum equation, species '&
&       //id
      chlab = 'aresmo('//id//')'
      CALL CFWUIN(nput, 1, ll(1), 'linlog')
      CALL CFWUCH(nput, 60, chtit, 'title')
      CALL CFWUCH(nput, 12, chlab, 'label')
      k = k + 1
    END DO
    chtit = 'norm of residual of total parallel momentum equation'
    chlab = 'aresmt'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of residual of electron heat equation'
    chlab = 'areshe'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of residual of all atom heat equation'
    chlab = 'areshi'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of residual of the fluid neutral heat equation'
    chlab = 'areshn'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of residual of electric potential equation'
    chlab = 'arespo'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of residual of turbulent kinetic energy equation'
    chlab = 'areskt'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of residual of turbulent enstrophy equation'
    chlab = 'areszt'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
!    ..norms of corrections
    DO is=0,ns-1
      WRITE(id, '(i3)') is
      chtit = 'norm of correction of pressure, species '//id
      chlab = 'acorpa('//id//')'
      CALL CFWUIN(nput, 1, ll(1), 'linlog')
      CALL CFWUCH(nput, 60, chtit, 'title')
      CALL CFWUCH(nput, 12, chlab, 'label')
      k = k + 1
    END DO
    DO is=0,ns-1
      WRITE(id, '(i3)') is
      chtit = 'norm of correction of velocity, species '//id
      chlab = 'acorua('//id//')'
      CALL CFWUIN(nput, 1, ll(1), 'linlog')
      CALL CFWUCH(nput, 60, chtit, 'title')
      CALL CFWUCH(nput, 12, chlab, 'label')
      k = k + 1
    END DO
    chtit = 'norm of correction of combined velocity'
    chlab = 'acorut'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of correction of electron temperature'
    chlab = 'acorte'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of correction of all atom temperature'
    chlab = 'acorti'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of correction of the fluid neutral heat equation'
    chlab = 'acortn'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of correction of electric potential'
    chlab = 'acorpo'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of correction of turbulent kinetic energy'
    chlab = 'acorkt'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
    chtit = 'norm of correction of turbulent enstrophy'
    chlab = 'acorzt'
    CALL CFWUIN(nput, 1, ll(1), 'linlog')
    CALL CFWUCH(nput, 60, chtit, 'title')
    CALL CFWUCH(nput, 12, chlab, 'label')
    k = k + 1
  END IF
  IF (2 .LE. switch%b2mwqt_style) THEN
!    ..total content on regions
    DO is=0,ns-1
      WRITE(id, '(i3)') is
      DO iiy=-1,4
        DO iix=-1,4
          WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
          chtit = 'total particle content, species '//id//', region '//&
&           idr
          chlab = 'int-na('//id//')'
          CALL CFWUIN(nput, 1, ll(0), 'linlog')
          CALL CFWUCH(nput, 60, chtit, 'title')
          CALL CFWUCH(nput, 12, chlab, 'label')
          k = k + 1
        END DO
      END DO
    END DO
    DO iiy=-1,4
      DO iix=-1,4
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'total electron heat content, region '//idr
        chlab = 'int-ee'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
    DO iiy=-1,4
      DO iix=-1,4
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'total all atom heat content, region '//idr
        chlab = 'int-ei'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
    DO iiy=-1,4
      DO iix=-1,4
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'total electric potential (qe*ne*po), region '//idr
        chlab = 'int-po'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
  END IF
  IF (3 .LE. switch%b2mwqt_style) THEN
!    ..integrated fluxes
    DO is=0,ns-1
      WRITE(id, '(i3)') is
      DO iiy=0,3
        DO iix=0,4
          WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
          chtit = 'particle flux along x, species '//id//', region '//&
&           idr
          chlab = 'rfnax'
          CALL CFWUIN(nput, 1, ll(0), 'linlog')
          CALL CFWUCH(nput, 60, chtit, 'title')
          CALL CFWUCH(nput, 12, chlab, 'label')
          k = k + 1
        END DO
      END DO
      DO iiy=0,4
        DO iix=0,3
          WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
          chtit = 'particle flux along y, species '//id//', region '//&
&           idr
          chlab = 'rfnay'
          CALL CFWUIN(nput, 1, ll(0), 'linlog')
          CALL CFWUCH(nput, 60, chtit, 'title')
          CALL CFWUCH(nput, 12, chlab, 'label')
          k = k + 1
        END DO
      END DO
    END DO
    DO iiy=0,3
      DO iix=0,4
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'electron heat flux along x, region '//idr
        chlab = 'rfhex'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
    DO iiy=0,4
      DO iix=0,3
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'electron heat flux along y, region '//idr
        chlab = 'rfhey'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
    DO iiy=0,3
      DO iix=0,4
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'all atom heat flux along x, region '//idr
        chlab = 'rfhix'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
    DO iiy=0,4
      DO iix=0,3
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'all atom heat flux along y, region '//idr
        chlab = 'rfhiy'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
    DO iiy=0,3
      DO iix=0,4
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'electric charge flux along x, region '//idr
        chlab = 'rfchx'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
    DO iiy=0,4
      DO iix=0,3
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'electric charge flux along y, region '//idr
        chlab = 'rfchy'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
  END IF
  IF (4 .LE. switch%b2mwqt_style) THEN
!    ..min and max on regions
    DO is=0,ns-1
      WRITE(id, '(i3)') is
      DO iiy=-1,4
        DO iix=-1,4
          WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
          chtit = 'minimum density, species '//id//', region '//idr
          chlab = 'na0('//id//')'
          CALL CFWUIN(nput, 1, ll(0), 'linlog')
          CALL CFWUCH(nput, 60, chtit, 'title')
          CALL CFWUCH(nput, 12, chlab, 'label')
          k = k + 1
          chtit = 'maximum density, species '//id//', region '//idr
          chlab = 'na1('//id//')'
          CALL CFWUIN(nput, 1, ll(0), 'linlog')
          CALL CFWUCH(nput, 60, chtit, 'title')
          CALL CFWUCH(nput, 12, chlab, 'label')
          k = k + 1
        END DO
      END DO
    END DO
    DO iiy=-1,4
      DO iix=-1,4
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'minimum electron temperature, region '//idr
        chlab = 'te0'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
        chtit = 'maximum electron temperature, region '//idr
        chlab = 'te1'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
    DO iiy=-1,4
      DO iix=-1,4
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'minimum all atom temperature, region '//idr
        chlab = 'ti0'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
        chtit = 'maximum all atom temperature, region '//idr
        chlab = 'ti1'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
    DO iiy=-1,4
      DO iix=-1,4
        WRITE(idr, '(a,i2,a,i2,a)') '(', iix, ',', iiy, ')'
        chtit = 'minimum electric potential, region '//idr
        chlab = 'po0'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
        chtit = 'maximum electric potential, region '//idr
        chlab = 'po1'
        CALL CFWUIN(nput, 1, ll(0), 'linlog')
        CALL CFWUCH(nput, 60, chtit, 'title')
        CALL CFWUCH(nput, 12, chlab, 'label')
        k = k + 1
      END DO
    END DO
