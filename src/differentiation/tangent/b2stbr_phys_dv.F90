&         -1))/temp
      END DO
      zaf(0:ns-1) = (geo%fchc(ifc, 2)*rt%rza(mpg%fccv(ifc, 1), 0:ns-1)+&
&       geo%fchc(ifc, 1)*rt%rza(mpg%fccv(ifc, 2), 0:ns-1))/temp
      rpf(0:ns-1) = (rt%rpt(mpg%fccv(ifc, 1), 0:ns-1)*geo%fchc(ifc, 2)+&
&       rt%rpt(mpg%fccv(ifc, 2), 0:ns-1)*geo%fchc(ifc, 1))/(geo%fchc(ifc&
&       , 1)+geo%fchc(ifc, 2))
      y1 = isign*(dv%fhi(ifc, 0)+dv%fhi(ifc, 1)+st_ext%fhi(ifc, 0)+&
&       st_ext%fhi(ifc, 1))
      IF (0.0_R8 .LT. y1) THEN
        max1 = y1
      ELSE
        max1 = 0.0_R8
      END IF
      qin = max1/geo%fcs(ifc)
      DO is=0,ns-1
        IF (0.0_R8 .LT. isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))) &
&       THEN
          max2 = isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))
        ELSE
          max2 = 0.0_R8
        END IF
        qin = qin + max2*((dv%kinrgy(icv, is)+tif)*(1.0_R8-switch%boris)&
&         +rpf(is)*ev)/geo%fcs(ifc)
      END DO
      DO is=0,st_ext%ns-1
        ptf = (st_ext%pt(mpg%fccv(ifc, 1), is)*geo%fchc(ifc, 2)+st_ext%&
&         pt(mpg%fccv(ifc, 2), is)*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+&
&         geo%fchc(ifc, 2))
        taf = (st_ext%ta(mpg%fccv(ifc, 1), is)*geo%fchc(ifc, 2)+st_ext%&
&         ta(mpg%fccv(ifc, 2), is)*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+&
&         geo%fchc(ifc, 2))
        IF (0.0_R8 .LT. isign*(st_ext%fa(ifc, 0, is)+st_ext%fa(ifc, 1, &
&           is))) THEN
          max3 = isign*(st_ext%fa(ifc, 0, is)+st_ext%fa(ifc, 1, is))
        ELSE
          max3 = 0.0_R8
        END IF
        qin = qin + max3*((0.5_R8*st_ext%am(is)*mp*st_ext%ua(icv, is)**2&
&         +taf)*(1.0_R8-switch%boris)+ptf*ev)/geo%fcs(ifc)
      END DO
      y2 = isign*(dv%fhe(ifc, 0)+dv%fhe(ifc, 1)+(dv%fne(ifc, 0)+dv%fne(&
&       ifc, 1))*tef*(1.0_R8-switch%boris))
      IF (0.0_R8 .LT. y2) THEN
        max4 = y2
      ELSE
        max4 = 0.0_R8
      END IF
      qel = max4/geo%fcs(ifc)
      IF (lcore) THEN
        surface_temp = 0.0_R8
      ELSE
        surface_temp = 0.0_R8
      END IF
!
!    ..loop over species
!mb   Loop implicitly expects that the species are arranged per isonuclear sequence and that the neutral atom of that sequence al
!ways comes first. If not, erroneous results will be obtained.
      is0 = 0
      is1 = 0
      nnsum = 0.0_R8
      nnwwnsum = 0.0_R8
      DO nd=1,nbdirsmax
        nnwwnsumd(nd) = 0.D0
      END DO
      DO nd=1,nbdirsmax
        nnsumd(nd) = 0.D0
      END DO
      DO is=nsmin,nsmax
        IF (am(is0) - am(is) .GE. 0.) THEN
          abs0 = am(is0) - am(is)
        ELSE
          abs0 = -(am(is0)-am(is))
        END IF
        IF (NINT(zn(is0)) .NE. NINT(zn(is)) .OR. amtol .LT. abs0) is0 = &
&           is
!dpc
        IF (is_neutral(is)) is1 = is
        IF (am(is1) - am(is) .GE. 0.) THEN
          abs1 = am(is1) - am(is)
        ELSE
          abs1 = -(am(is1)-am(is))
        END IF
        IF (NINT(zn(is1)) .NE. NINT(zn(is)) .OR. amtol .LT. abs1) WRITE(&
&                                                                 *, *) &
&                                        'b2stbr problem: is, is0, is1 '&
&                                                                 , is, &
&                                                                 is0, &
&                                                                 is1
        IF (is0 .NE. is1) THEN
          IF (ncall_b2stbr_phys .EQ. 0) WRITE(*, *) &
&                                       'b2stbr: setting is0 to is1 ', &
&                                       is0, is1
          is0 = is1
        END IF
        is2 = -1
        k = 0
        found = .false.
        DO WHILE (.NOT.found .AND. k .LT. nscx)
          IF (is0 .EQ. iscx(k)) THEN
            found = .true.
            is2 = k
          END IF
          k = k + 1
        END DO
        IF (0.0_R8 .LT. isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))) &
&       THEN
          DO nd=1,nbdirs
            max5d(nd) = isign*(dvd%fna(nd, ifc, 0, is)+dvd%fna(nd, ifc, &
&             1, is))
          END DO
          max5 = isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))
        ELSE
          max5 = 0.0_R8
          DO nd=1,nbdirsmax
            max5d(nd) = 0.D0
          END DO
        END IF
!dpc
!       (species (is) recycles into is0)
!       (is0 corresponds to CX species is2)
! dpc: Eirene coupling 1999.06.28
        DO nd=1,nbdirs
          t0d(nd) = strasclfl(istra)*switch%neutral_sources_rescale*&
&           max5d(nd)
        END DO
        t0 = max5*switch%neutral_sources_rescale*strasclfl(istra)
        IF (0.0_R8 .LT. tif/ev + (pof-phi_app)*zaf(is)) THEN
          DO nd=1,nbdirs
            eincd(nd) = tifd(nd)/ev + zaf(is)*(pofd(nd)-phi_appd(nd)) + &
&             (pof-phi_app)*zafd(nd, is)
          END DO
          einc = tif/ev + (pof-phi_app)*zaf(is)
        ELSE
          einc = 0.0_R8
          DO nd=1,nbdirsmax
            eincd(nd) = 0.D0
          END DO
        END IF
! xpb: Incident particle flux (part/m^2/s)
        pflx = t0/geo%fcs(ifc)
        IF (is_neutral(is)) THEN
          arg11 = 8.0_R8*pl%ti(icv)/(pi*am(is)*mp)
          vbar = SQRT(arg11)
! dpc: Eirene coupling 1999.06.28
          pflx = pflx + pl%na(icv, is)*vbar*switch%b2stbr_alpha*switch%&
&           neutral_sources_rescale*strasclfl(istra)
        END IF
!! end KU Leuven advanced fluid neutral models
        IF (switch%recycle_afn .EQ. 0 .OR. NINT(zn(is0)) .NE. 1) THEN
!wdk  recycle toroidal componenent of parallel momentum, factor mrecyc
!     (toroidal piece only, because poloidal piece of parallel velocity flips sign after reflection)
          temp = mrecyc(is, istra)*geo%cvhz(icv)*am(is)*mp*(geo%cvbb(icv&
&           , 2)*geo%cvbb(icv, 2))
          temp0 = geo%cvbb(icv, 3)*geo%cvbb(icv, 3)
          temp1 = pl%ua(icv, is)/temp0
          temp2 = b2recyc(is, istra)*t0
          DO nd=1,nbdirs
!! default neutral recycling/reflection model (always for non-hydrogenic species)
            srwd%sna0(nd, icv, 0, is0) = srwd%sna0(nd, icv, 0, is0) + t0&
&             *b2recycd(nd, is, istra) + b2recyc(is, istra)*t0d(nd)
            srwd%smo0(nd, icv, 0, is0) = srwd%smo0(nd, icv, 0, is0) + &
&             temp*(temp1*(t0*b2recycd(nd, is, istra)+b2recyc(is, istra)&
&             *t0d(nd))+temp2*pld%ua(nd, icv, is)/temp0)
          END DO
          srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + b2recyc(is, &
&           istra)*t0
          srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + temp*(temp2*&
&           temp1)
          IF (switch%lin_shi0 .EQ. 1) THEN
            temp2 = erecyc(is, istra)*(switch%boris+1.5_R8)
            DO nd=1,nbdirs
              x1d(nd) = temp2*ev*eincd(nd)
            END DO
            x1 = temp2*(ev*einc)
            IF (x1 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              max6 = (1.5_R8+switch%boris)*surface_temp*kbolt
              DO nd=1,nbdirsmax
                max6d(nd) = 0.D0
              END DO
            ELSE
              DO nd=1,nbdirs
                max6d(nd) = x1d(nd)
              END DO
              max6 = x1
            END IF
            temp2 = b2recyc(is, istra)/pl%ti(icv)
            DO nd=1,nbdirs
              srwd%shi0(nd, icv, 1) = srwd%shi0(nd, icv, 1) + switch%&
&               recycled_neutrals_contr*(max6*t0*(b2recycd(nd, is, istra&
&               )-temp2*pld%ti(nd, icv))/pl%ti(icv)+temp2*(t0*max6d(nd)+&
&               max6*t0d(nd)))
            END DO
            srw%shi0(icv, 1) = srw%shi0(icv, 1) + switch%&
&             recycled_neutrals_contr*(temp2*(max6*t0))
          ELSE
            temp2 = erecyc(is, istra)*(switch%boris+1.5_R8)
            DO nd=1,nbdirs
              x2d(nd) = temp2*ev*eincd(nd)
            END DO
            x2 = temp2*(ev*einc)
            IF (x2 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              max7 = (1.5_R8+switch%boris)*surface_temp*kbolt
              DO nd=1,nbdirsmax
                max7d(nd) = 0.D0
              END DO
            ELSE
              DO nd=1,nbdirs
                max7d(nd) = x2d(nd)
              END DO
              max7 = x2
            END IF
            DO nd=1,nbdirs
              srwd%shi0(nd, icv, 0) = srwd%shi0(nd, icv, 0) + switch%&
&               recycled_neutrals_contr*(max7*t0*b2recycd(nd, is, istra)&
&               +b2recyc(is, istra)*(t0*max7d(nd)+max7*t0d(nd)))
            END DO
            srw%shi0(icv, 0) = srw%shi0(icv, 0) + switch%&
&             recycled_neutrals_contr*max7*b2recyc(is, istra)*t0
          END IF
! xpb: recycled neutrals come back with at least thermal energy
          IF (is2 .NE. -1) THEN
            x3 = erecyc(is, istra)*(1.5_R8+switch%boris)*einc*ev
            IF (x3 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              max8 = (1.5_R8+switch%boris)*surface_temp*kbolt
            ELSE
              max8 = x3
            END IF
            shi0_ff(icv, is2) = shi0_ff(icv, is2) + switch%&
&             recycled_neutrals_contr*max8*b2recyc(is, istra)*t0
          END IF
        ELSE
!
!! use advanced fluid neutral boundary conditions by N. Horsten et. al
! lvn1
!**************************************************************************************
!*****************     New KU Leuven recycling model for neutrals *********************
!**************************************************************************************
!
! main reference:
!   Horsten, Niels, Giovanni Samaey, and M. Baelmans.
!  "Development and assessment of 2D fluid neutral models that include atomic databases and a microscopic reflection model."
!   Nuclear Fusion 57.11 (2017): 116043.
!
!! area of the face
          farea = geo%fcs(ifc)
          cosa = geo%fcqalf(ifc, 0)
          sina = geo%fcqalf(ifc, 1)
!
          IF (is .NE. is0) THEN
!! ion species, recycling
!
!
!mb   net momentum sources from neutral particle recombination
!            Calculate recycled neutral particle flux (assuming thin boundary cells)
            IF (.NOT.lcore) THEN
!
              CALL CALCRECYCLEDFLUXES_DV(icv, mpg%nci, ifc, isign, is0, &
&                                  is2, is, istra, iwall, phi_app, &
&                                  phi_appd, farea, b2recyc(is, istra), &
&                                  b2recycd(1, is, istra), recycm(is, &
&                                  istra), dv%fluid_frac_hyb(ifc), dvd%&
&                                  fluid_frac_hyb(1, ifc), pl, pld, tif&
&                                  , tifd, tef, pof, pofd, pl%na(icv, is&
&                                  ), dv%ne(icv), geo, geod, mpg, t0, &
&                                  t0d, cosa, sina, switch%use_uy_uz_0, &
&                                  fnnrec, fnnrecd, fmomrec, fmomrecd, &
&                                  nnrec, nnrecd, nnwwnrec, nnwwnrecd, &
&                                  fenerec, fenerecd, nbdirs)
            ELSE
              fnnrec = 0.0_R8
              fmomrec = 0.0_R8
              nnrec = 0.0_R8
              nnwwnrec = 0.0_R8
              fenerec = 0.0_R8
              DO nd=1,nbdirsmax
                nnrecd(nd) = 0.D0
              END DO
              DO nd=1,nbdirsmax
                fnnrecd(nd) = 0.D0
              END DO
              DO nd=1,nbdirsmax
                fmomrecd(nd) = 0.D0
              END DO
              DO nd=1,nbdirsmax
                fenerecd(nd) = 0.D0
              END DO
              DO nd=1,nbdirsmax
                nnwwnrecd(nd) = 0.D0
              END DO
            END IF
            temp2 = geo%cvhz(icv)*strasclfl(istra)
            DO nd=1,nbdirs
!
              snand(nd) = strasclfl(istra)*switch%&
&               neutral_sources_rescale*fnnrecd(nd)
              srwd%sna0(nd, icv, 0, is0) = srwd%sna0(nd, icv, 0, is0) + &
&               snand(nd)
              smond(nd) = temp2*switch%neutral_sources_rescale*fmomrecd(&
&               nd)
              srwd%smo0(nd, icv, 0, is0) = srwd%smo0(nd, icv, 0, is0) + &
&               smond(nd)
!
              nnsumd(nd) = nnsumd(nd) + nnrecd(nd)
              nnwwnsumd(nd) = nnwwnsumd(nd) + nnwwnrecd(nd)
            END DO
            snan = fnnrec*switch%neutral_sources_rescale*strasclfl(istra&
&             )
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + snan
            smon = temp2*(switch%neutral_sources_rescale*fmomrec)
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + smon
            nnsum = nnsum + nnrec
            nnwwnsum = nnwwnsum + nnwwnrec
!mb   net neutral energy source from recycling
!
            IF (switch%tn_style .EQ. 0) THEN
              DO nd=1,nbdirs
                srwd%shi0(nd, icv, 0) = srwd%shi0(nd, icv, 0) + &
&                 strasclfl(istra)*switch%neutral_sources_rescale*&
&                 fenerecd(nd)
              END DO
              srw%shi0(icv, 0) = srw%shi0(icv, 0) + fenerec*switch%&
&               neutral_sources_rescale*strasclfl(istra)
            ELSE IF (switch%tn_style .NE. 1) THEN
!! neutral energy boundary conditions are not used
              DO nd=1,nbdirs
                srwd%shn0(nd, icv, 0) = srwd%shn0(nd, icv, 0) + &
&                 strasclfl(istra)*switch%neutral_sources_rescale*&
&                 fenerecd(nd)
              END DO
              srw%shn0(icv, 0) = srw%shn0(icv, 0) + fenerec*switch%&
&               neutral_sources_rescale*strasclfl(istra)
            END IF
!
            IF (switch%afn_out .NE. 0) THEN
              fmomrec_out(icv) = fmomrec
              fenerec_out(icv) = fenerec
            END IF
          ELSE
!
!! this else belongs to "if(.not.(is.eq.is0))", hence 'is' will be neutral here
!
!          Identify corresponding ion species
            IF (LNEXT(is, is + 1)) THEN
              isi = is + 1
            ELSE
              CALL XERRAB('Problem with species labeling')
            END IF
!
!          A conversion is necessary because the BC implemented below were developed in a code that uses a total energy equation,
!          while B2.5 uses internal energy equations.
!
!
            temp2 = isign*switch%neutral_sources_rescale*strasclfl(istra&
&             )
            temp0 = tnf*pl%ua(icv, is0)
            DO nd=1,nbdirs
              q_convd(nd) = temp2*(geo%fcpbs(ifc)*(pl%na(icv, is0)*(pl%&
&               ua(icv, is0)*tnfd(nd)+tnf*pld%ua(nd, icv, is0))+temp0*&
&               pld%na(nd, icv, is0))+dvd%fhm(nd, ifc, 0, is0)+dvd%fhm(&
&               nd, ifc, 1, is0))
            END DO
            q_conv = temp2*(geo%fcpbs(ifc)*(temp0*pl%na(icv, is0))+dv%&
&             fhm(ifc, 0, is0)+dv%fhm(ifc, 1, is0))
!
            IF (switch%tn_style .EQ. 0) THEN
              DO nd=1,nbdirs
                srwd%shi0(nd, icv, 0) = srwd%shi0(nd, icv, 0) + q_convd(&
&                 nd)
              END DO
              srw%shi0(icv, 0) = srw%shi0(icv, 0) + q_conv
            ELSE IF (switch%tn_style .NE. 1) THEN
!! do nothing, no neutral contributions in the boundary conditions of the pure ion energy equation
              DO nd=1,nbdirs
                srwd%shn0(nd, icv, 0) = srwd%shn0(nd, icv, 0) + q_convd(&
&                 nd)
              END DO
              srw%shn0(icv, 0) = srw%shn0(icv, 0) + q_conv
            END IF
!
            IF (maxw(istra) .EQ. 0) THEN
              CALL CALCINCIDENTFLUXESDIFFUSION_DV(icv, icn, ifc, isign, &
&                                           is0, is2, isi, istra, &
&                                           phi_app, farea, pl, pld, tif&
&                                           , tifd, tnf, tnfd, tef, tefd&
&                                           , pof, dv%ne(icv), dvd%ne(1&
&                                           , icv), geo, geod, rt, rtd, &
&                                           dnndy, dnndyd, dnndz, dnndzd&
&                                           , cosa, sina, switch%&
&                                           use_uy_uz_0, switch%&
&                                           use_uy_uz_1, switch%&
&                                           diffusion_bc_safeguard, fnni&
&                                           , fnnid, fmomni, fmomnid, &
&                                           nni, nnid, nnwwni, nnwwnid, &
&                                           feneni, fenenid, nbdirs)
            ELSE IF (maxw(istra) .EQ. 1) THEN
              CALL CALCINCIDENTFLUXESMAXWELLIAN_DV(icv, ifc, is0, isign&
&                                            , istra, farea, pl, pld, &
&                                            tnf, tnfd, geo, geod, cosa&
&                                            , sina, fnni, fnnid, fmomni&
&                                            , fmomnid, nni, nnid, &
&                                            nnwwni, nnwwnid, feneni, &
&                                            fenenid, nbdirs)
            ELSE IF (maxw(istra) .EQ. 2) THEN
              CALL CALCINCIDENTFLUXESKN_DV(switch%kn_b1, switch%kn_b2, &
&                                    icv, icn, ifc, isign, is0, is2, isi&
&                                    , istra, phi_app, farea, pl, pld, &
&                                    tif, tifd, tnf, tnfd, tef, tefd, &
&                                    pof, dv%ne(icv), dvd%ne(1, icv), &
&                                    geo, geod, rt, rtd, dnndy, dnndyd, &
&                                    dnndz, dnndzd, cosa, sina, switch%&
&                                    use_uy_uz_0, switch%use_uy_uz_1, &
&                                    switch%l_macro_afn, fnni, fnnid, &
&                                    fmomni, fmomnid, nni, nnid, nnwwni&
&                                    , nnwwnid, feneni, fenenid, nbdirs)
            ELSE
              CALL CALCINCIDENTFLUXESMINKN_DV(icv, icn, ifc, isign, is0&
&                                       , is2, isi, istra, phi_app, &
&                                       farea, pl, pld, tif, tifd, tnf, &
&                                       tnfd, tef, tefd, pof, dv%ne(icv)&
&                                       , dvd%ne(1, icv), geo, geod, rt&
&                                       , rtd, dnndy, dnndyd, dnndz, &
&                                       dnndzd, cosa, sina, switch%&
&                                       use_uy_uz_0, switch%use_uy_uz_1&
&                                       , fnni, fnnid, fmomni, fmomnid, &
&                                       nni, nnid, nnwwni, nnwwnid, &
&                                       feneni, fenenid, nbdirs)
!
            END IF
!
            DO nd=1,nbdirs
              srwd%sna0(nd, icv, 0, is0) = srwd%sna0(nd, icv, 0, is0) - &
&               strasclfl(istra)*switch%neutral_sources_rescale*fnnid(nd&
&               )
            END DO
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) - fnni*switch%&
&             neutral_sources_rescale*strasclfl(istra)
!
!! save the incident fluid neutral flux
            dv%fnn_inc(ifc, is0) = fnni*isign
!
!mb   net momentum sources from neutral particle reflection
            IF (.NOT.lcore) THEN
              IF (maxw(istra) .EQ. 0) THEN
                CALL CALCREFLECTEDFLUXESDIFFUSION_DV(icv, icn, mpg%nci, &
&                                              ifc, isign, is0, is2, isi&
&                                              , istra, iwall, phi_app, &
&                                              farea, b2recyc(is, istra)&
&                                              , b2recycd(1, is, istra)&
&                                              , recycm(is, istra), dv%&
&                                              fluid_frac_hyb(ifc), dvd%&
&                                              fluid_frac_hyb(1, ifc), &
&                                              pl, pld, tif, tifd, tnf, &
&                                              tnfd, tef, tefd, pof, dv%&
&                                              ne(icv), dvd%ne(1, icv), &
&                                              geo, geod, mpg, rt, rtd, &
&                                              dnndy, dnndyd, dnndz, &
&                                              dnndzd, cosa, sina, &
&                                              switch%use_uy_uz_0, &
&                                              switch%use_uy_uz_1, &
&                                              switch%&
&                                              diffusion_bc_safeguard, &
&                                              fnnrefl, fnnrefld, &
&                                              fmomrefl, fmomrefld, &
&                                              nnrefl, nnrefld, &
&                                              nnwwnrefl, nnwwnrefld, &
&                                              fenerefl, fenerefld, &
&                                              fene_el, fene_eld, nbdirs&
&                                             )
              ELSE IF (maxw(istra) .EQ. 1) THEN
                CALL CALCREFLECTEDFLUXESMAXWELLIAN_DV(icv, mpg%nci, ifc&
&                                               , isign, is0, istra, &
&                                               iwall, farea, b2recyc(is&
&                                               , istra), b2recycd(1, is&
&                                               , istra), recycm(is, &
&                                               istra), dv%&
&                                               fluid_frac_hyb(ifc), dvd&
&                                               %fluid_frac_hyb(1, ifc)&
&                                               , pl, pld, tnf, tnfd, &
&                                               geo, geod, mpg, cosa, &
&                                               sina, fnnrefl, fnnrefld&
&                                               , fmomrefl, fmomrefld, &
&                                               nnrefl, nnrefld, &
&                                               nnwwnrefl, nnwwnrefld, &
&                                               fenerefl, fenerefld, &
&                                               fene_el, fene_eld, &
&                                               nbdirs)
              ELSE IF (maxw(istra) .EQ. 2) THEN
                CALL CALCREFLECTEDFLUXESKN_DV(switch%kn_b1, switch%kn_b2&
&                                       , icv, icn, mpg%nci, ifc, isign&
&                                       , is0, is2, isi, istra, iwall, &
&                                       phi_app, farea, b2recyc(is, &
&                                       istra), b2recycd(1, is, istra), &
&                                       recycm(is, istra), dv%&
&                                       fluid_frac_hyb(ifc), dvd%&
&                                       fluid_frac_hyb(1, ifc), pl, pld&
&                                       , tif, tifd, tnf, tnfd, tef, &
&                                       tefd, pof, dv%ne(icv), dvd%ne(1&
&                                       , icv), geo, geod, mpg, rt, rtd&
&                                       , dnndy, dnndyd, dnndz, dnndzd, &
&                                       cosa, sina, switch%use_uy_uz_0, &
&                                       switch%use_uy_uz_1, switch%&
&                                       l_macro_afn, fnnrefl, fnnrefld, &
&                                       fmomrefl, fmomrefld, nnrefl, &
&                                       nnrefld, nnwwnrefl, nnwwnrefld, &
&                                       fenerefl, fenerefld, fene_el, &
&                                       fene_eld, nbdirs)
              ELSE
                CALL CALCREFLECTEDFLUXESMINKN_DV(icv, icn, mpg%nci, ifc&
&                                          , isign, is0, is2, isi, istra&
&                                          , iwall, phi_app, farea, &
&                                          b2recyc(is, istra), b2recycd(&
&                                          1, is, istra), recycm(is, &
&                                          istra), dv%fluid_frac_hyb(ifc&
&                                          ), dvd%fluid_frac_hyb(1, ifc)&
&                                          , pl, pld, tif, tifd, tnf, &
&                                          tnfd, tef, tefd, pof, dv%ne(&
&                                          icv), dvd%ne(1, icv), geo, &
&                                          geod, mpg, rt, rtd, dnndy, &
&                                          dnndyd, dnndz, dnndzd, cosa, &
&                                          sina, switch%use_uy_uz_0, &
&                                          switch%use_uy_uz_1, fnnrefl, &
&                                          fnnrefld, fmomrefl, fmomrefld&
&                                          , nnrefl, nnrefld, nnwwnrefl&
&                                          , nnwwnrefld, fenerefl, &
&                                          fenerefld, fene_el, fene_eld&
&                                          , nbdirs)
              END IF
            ELSE
              fnnrefl = 0.0_R8
              fmomrefl = 0.0_R8
              nnrefl = 0.0_R8
              nnwwnrefl = 0.0_R8
              fenerefl = 0.0_R8
              fene_el = 0.0_R8
              DO nd=1,nbdirsmax
                fmomrefld(nd) = 0.D0
              END DO
              DO nd=1,nbdirsmax
                nnrefld(nd) = 0.D0
              END DO
              DO nd=1,nbdirsmax
                fene_eld(nd) = 0.D0
              END DO
              DO nd=1,nbdirsmax
                nnwwnrefld(nd) = 0.D0
              END DO
              DO nd=1,nbdirsmax
                fenerefld(nd) = 0.D0
              END DO
              DO nd=1,nbdirsmax
                fnnrefld(nd) = 0.D0
              END DO
            END IF
!
!nh  adding the reflected particle source (with the molecular part subtracted)
            DO nd=1,nbdirs
              srwd%sna0(nd, icv, 0, is0) = srwd%sna0(nd, icv, 0, is0) + &
&               strasclfl(istra)*switch%neutral_sources_rescale*fnnrefld&
&               (nd)
            END DO
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + fnnrefl*&
&             switch%neutral_sources_rescale*strasclfl(istra)
            snan = fnnrefl
!
            IF (switch%afn_out .NE. 0) THEN
              fmomni_out(icv) = fmomni
              fmomrefl_out(icv) = fmomrefl
              feneni_out(icv) = feneni
              fenerefl_out(icv) = fenerefl
              fnni_out(icv) = fnni
            END IF
            DO nd=1,nbdirs
!
              smond(nd) = switch%neutral_sources_rescale*strasclfl(istra&
&               )*geo%cvhz(icv)*(fmomnid(nd)+fmomrefld(nd))
              srwd%smo0(nd, icv, 0, is0) = srwd%smo0(nd, icv, 0, is0) + &
&               smond(nd)
!
              nnsumd(nd) = nnsumd(nd) + nnid(nd) + nnrefld(nd)
              nnwwnsumd(nd) = nnwwnsumd(nd) + nnwwnid(nd) + nnwwnrefld(&
&               nd)
            END DO
            smon = (fmomni+fmomrefl)*geo%cvhz(icv)*switch%&
&             neutral_sources_rescale*strasclfl(istra)
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + smon
            nnsum = nnsum + nni + nnrefl
            nnwwnsum = nnwwnsum + nnwwni + nnwwnrefl
!
            IF (switch%tn_style .EQ. 0) THEN
              DO nd=1,nbdirs
                srwd%shi0(nd, icv, 0) = srwd%shi0(nd, icv, 0) + switch%&
&                 neutral_sources_rescale*strasclfl(istra)*(fenenid(nd)+&
&                 fenerefld(nd))
              END DO
              srw%shi0(icv, 0) = srw%shi0(icv, 0) + (feneni+fenerefl)*&
&               switch%neutral_sources_rescale*strasclfl(istra)
            ELSE IF (switch%tn_style .NE. 1) THEN
!! neutral energy boundary conditions are not used
              DO nd=1,nbdirs
                srwd%shn0(nd, icv, 0) = srwd%shn0(nd, icv, 0) + switch%&
&                 neutral_sources_rescale*strasclfl(istra)*(fenenid(nd)+&
&                 fenerefld(nd))
              END DO
              srw%shn0(icv, 0) = srw%shn0(icv, 0) + (feneni+fenerefl)*&
&               switch%neutral_sources_rescale*strasclfl(istra)
            END IF
!
! remove the Franck-Condon energy, that has been provided to the neutrals, from the electrons at the boundary
! temporary switch to evaluate the effect.
            IF (switch%remove_fc_el .EQ. 1) THEN
              DO nd=1,nbdirs
                srwd%she0(nd, icv, 0) = srwd%she0(nd, icv, 0) + &
&                 strasclfl(istra)*switch%neutral_sources_rescale*&
&                 fene_eld(nd)
              END DO
              srw%she0(icv, 0) = srw%she0(icv, 0) + fene_el*switch%&
&               neutral_sources_rescale*strasclfl(istra)
            END IF
          END IF
!
          IF (NINT(zamax(is)) .EQ. NINT(zn(is))) THEN
!! see eq. 62-63 of the main reference.
            IF (nnsum .EQ. 0.0_R8) THEN
              nnsum = 1.0_R8
              DO nd=1,nbdirsmax
                nnsumd(nd) = 0.D0
              END DO
            END IF
            temp2 = isign*cosa*geo%cvbb(icv, 0)*mp*farea*am(is0)*geo%&
&             cvhz(icv)
            temp1 = 3.0_R8*geo%cvbb(icv, 3)
            temp3 = temp1*nnsum
            temp0 = nnwwnsum*nnwwnsum/temp3
            DO nd=1,nbdirs
              smond(nd) = -(temp2*(2*nnwwnsum*nnwwnsumd(nd)-temp0*temp1*&
&               nnsumd(nd))/temp3)
              srwd%smo0(nd, icv, 0, is0) = srwd%smo0(nd, icv, 0, is0) + &
&               strasclfl(istra)*switch%neutral_sources_rescale*smond(nd&
&               )
            END DO
            smon = -(temp2*temp0)
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + smon*switch%&
&             neutral_sources_rescale*strasclfl(istra)
            nnsum = 0.0_R8
            nnwwnsum = 0.0_R8
            DO nd=1,nbdirsmax
              nnwwnsumd(nd) = 0.D0
            END DO
            DO nd=1,nbdirsmax
              nnsumd(nd) = 0.D0
            END DO
          END IF
        END IF
!
!    ..recycling into next ionized species
        IF (is .LT. ns - 1) THEN
          IF (LNEXT(is, is + 1)) THEN
            DO nd=1,nbdirs
              srwd%sna0(nd, icv, 0, is+1) = srwd%sna0(nd, icv, 0, is+1) &
&               + rcion(is, istra)*t0d(nd)/(switch%&
&               neutral_sources_rescale*strasclfl(istra))
            END DO
            srw%sna0(icv, 0, is+1) = srw%sna0(icv, 0, is+1) + rcion(is, &
&             istra)*t0/(switch%neutral_sources_rescale*strasclfl(istra)&
&             )
          END IF
        END IF
      END DO
! IYS 18.09.2018
! do is = 0, ns-1
!    ..recycling at core into highest ionized stage
      IF (lcore) THEN
!    ..loop over species
        is0 = ns - 1
        is1 = ns - 1
        DO is=nsmax,nsmin,-1
          IF (am(is0) - am(is) .GE. 0.) THEN
            abs2 = am(is0) - am(is)
          ELSE
            abs2 = -(am(is0)-am(is))
          END IF
          IF (NINT(zn(is0)) .NE. NINT(zn(is)) .OR. amtol .LT. abs2) is0&
&            = is
!xpb
          IF (NINT(zamax(is)) .EQ. NINT(zn(is))) is1 = is
          IF (am(is1) - am(is) .GE. 0.) THEN
            abs3 = am(is1) - am(is)
          ELSE
            abs3 = -(am(is1)-am(is))
          END IF
          IF (NINT(zn(is1)) .NE. NINT(zn(is)) .OR. amtol .LT. abs3) &
&           WRITE(*, '(a,3i3)') 'b2stbr problem: is, is0, is1 ', is, is0&
&           , is1
          IF (is0 .NE. is1) THEN
            IF (ncall_b2stbr_phys .EQ. 0) WRITE(*, '(a,2i3)') &
&                                         'b2stbr: setting is0 to is1 '&
&                                         , is0, is1
            is0 = is1
          END IF
          IF (0.0_R8 .LT. isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))&
&         ) THEN
            DO nd=1,nbdirs
              max9d(nd) = isign*(dvd%fna(nd, ifc, 0, is)+dvd%fna(nd, ifc&
&               , 1, is))
            END DO
            max9 = isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))
          ELSE
            max9 = 0.0_R8
            DO nd=1,nbdirsmax
              max9d(nd) = 0.D0
            END DO
          END IF
!xpb
!          (species (is) recycles into is0)
! xpb: 2002.02.01
          DO nd=1,nbdirs
            t0d(nd) = switch%b2stbr_core_sources_rescale*max9d(nd)
          END DO
          t0 = max9*switch%b2stbr_core_sources_rescale
          IF (is .NE. is0) THEN
!
!wdk  recycling of true parallel momentum
!     (at core, cell faces will be aligned with flux surfaces)
            temp2 = mrecyc(is, istra)*geo%cvhz(icv)*am(is)*mp
            temp0 = b2recyc(is, istra)*t0
            DO nd=1,nbdirs
              srwd%sna0(nd, icv, 0, is0) = srwd%sna0(nd, icv, 0, is0) + &
&               t0*b2recycd(nd, is, istra) + b2recyc(is, istra)*t0d(nd)
              srwd%smo0(nd, icv, 0, is0) = srwd%smo0(nd, icv, 0, is0) + &
&               temp2*(temp0*pld%ua(nd, icv, is)+pl%ua(icv, is)*(t0*&
&               b2recycd(nd, is, istra)+b2recyc(is, istra)*t0d(nd)))
            END DO
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + b2recyc(is, &
&             istra)*t0
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + temp2*(pl%ua&
&             (icv, is)*temp0)
!srv
            temp2 = erecyc(is, istra)*(switch%boris+1.5_R8)*switch%&
&             recycled_neutrals_contr
            temp0 = b2recyc(is, istra)*t0
            temp = zaf(is)*tef + tif
            DO nd=1,nbdirs
              srwd%shi0(nd, icv, 0) = srwd%shi0(nd, icv, 0) + temp2*(&
&               temp0*(tef*zafd(nd, is)+zaf(is)*tefd(nd)+tifd(nd))+temp*&
&               (t0*b2recycd(nd, is, istra)+b2recyc(is, istra)*t0d(nd)))
            END DO
            srw%shi0(icv, 0) = srw%shi0(icv, 0) + temp2*(temp*temp0)
          END IF
        END DO
      END IF
! do is = nsmax, nsmin, -1
!
      RETURN
    END IF
  END SUBROUTINE RECYCLE_DV

!
!-----------------------------------------------------------------------
!
!
  SUBROUTINE RECYCLE(istra, nsmin, nsmax, icv, icn, ifc, isign, phi_app&
&   , lcore, dnndy, dnndz)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: istra, nsmin, nsmax, icv, icn, ifc
    REAL(kind=r8) :: phi_app, pof, isign, dnndy, dnndz
    LOGICAL :: lcore, found
!
    INTEGER :: is0, is1, is2, is, isi, k, iwall
    REAL(kind=r8) :: tef, tif, tnf, taf, zaf(0:ns-1), rpf(0:ns-1), ptf, &
&   t0
! new variables for advanced fluid neutral reflection/recycling model
    REAL(kind=r8) :: fnni, fmomni, nni, nnwwni, feneni, fnnrefl, fnnrec&
&   , fmomrefl, fmomrec, fenerec, fenerefl, fene_el, nnrefl, nnwwnrefl, &
&   nnrec, nnwwnrec, nnsum, nnwwnsum, snan, smon, farea, q_conv, sina, &
&   cosa
    INTRINSIC MAX
    INTRINSIC NINT
    INTRINSIC ABS
    INTRINSIC SQRT
    EXTERNAL XERRAB
    REAL(r8) :: y1
    REAL(r8) :: y2
    REAL(kind=r8) :: x1
    REAL(kind=r8) :: x2
    REAL(kind=r8) :: x3
    REAL(r8) :: max1
    REAL(r8) :: max2
    REAL(r8) :: max3
    REAL(r8) :: max4
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: abs1
    REAL(r8) :: max5
    REAL(kind=r8) :: max6
    REAL(kind=r8) :: max7
    REAL(kind=r8) :: max8
    REAL(kind=r8) :: abs2
    REAL(kind=r8) :: abs3
    REAL(r8) :: max9
    REAL(r8) :: arg11
!
!
! if nsmin = b2species_start = -1, this means that the spatially hybrid method for neutrals is used
! and this strata should be ignored here
    IF (nsmin .LT. 0) THEN
      RETURN
    ELSE
!
      IF (switch%b2stbr_temperature_at_guard_cell .EQ. 1) THEN
        tef = pl%te(icv)
        tif = pl%ti(icv)
        tnf = pl%tn(icv)
      ELSE
        tef = (pl%te(mpg%fccv(ifc, 1))*geo%fchc(ifc, 2)+pl%te(mpg%fccv(&
&         ifc, 2))*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        tif = (pl%ti(mpg%fccv(ifc, 1))*geo%fchc(ifc, 2)+pl%ti(mpg%fccv(&
&         ifc, 2))*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
        tnf = (pl%tn(mpg%fccv(ifc, 1))*geo%fchc(ifc, 2)+pl%tn(mpg%fccv(&
&         ifc, 2))*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+geo%fchc(ifc, 2))
      END IF
      IF (switch%b2stbr_potential_at_guard_cell .EQ. 1) THEN
!        write(6,*) 'Potential taken at guard cell (phys)'
        pof = pl%po(icv)
      ELSE
!        write(6,*) 'Potential taken at real cell (phys)'
        pof = pl%po(icn)
      END IF
      zaf(0:ns-1) = (rt%rza(mpg%fccv(ifc, 1), 0:ns-1)*geo%fchc(ifc, 2)+&
&       rt%rza(mpg%fccv(ifc, 2), 0:ns-1)*geo%fchc(ifc, 1))/(geo%fchc(ifc&
&       , 1)+geo%fchc(ifc, 2))
      rpf(0:ns-1) = (rt%rpt(mpg%fccv(ifc, 1), 0:ns-1)*geo%fchc(ifc, 2)+&
&       rt%rpt(mpg%fccv(ifc, 2), 0:ns-1)*geo%fchc(ifc, 1))/(geo%fchc(ifc&
&       , 1)+geo%fchc(ifc, 2))
      y1 = isign*(dv%fhi(ifc, 0)+dv%fhi(ifc, 1)+st_ext%fhi(ifc, 0)+&
&       st_ext%fhi(ifc, 1))
      IF (0.0_R8 .LT. y1) THEN
        max1 = y1
      ELSE
        max1 = 0.0_R8
      END IF
      qin = max1/geo%fcs(ifc)
      DO is=0,ns-1
        IF (0.0_R8 .LT. isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))) &
&       THEN
          max2 = isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))
        ELSE
          max2 = 0.0_R8
        END IF
        qin = qin + max2*((dv%kinrgy(icv, is)+tif)*(1.0_R8-switch%boris)&
&         +rpf(is)*ev)/geo%fcs(ifc)
      END DO
      DO is=0,st_ext%ns-1
        ptf = (st_ext%pt(mpg%fccv(ifc, 1), is)*geo%fchc(ifc, 2)+st_ext%&
&         pt(mpg%fccv(ifc, 2), is)*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+&
&         geo%fchc(ifc, 2))
        taf = (st_ext%ta(mpg%fccv(ifc, 1), is)*geo%fchc(ifc, 2)+st_ext%&
&         ta(mpg%fccv(ifc, 2), is)*geo%fchc(ifc, 1))/(geo%fchc(ifc, 1)+&
&         geo%fchc(ifc, 2))
        IF (0.0_R8 .LT. isign*(st_ext%fa(ifc, 0, is)+st_ext%fa(ifc, 1, &
&           is))) THEN
          max3 = isign*(st_ext%fa(ifc, 0, is)+st_ext%fa(ifc, 1, is))
        ELSE
          max3 = 0.0_R8
        END IF
        qin = qin + max3*((0.5_R8*st_ext%am(is)*mp*st_ext%ua(icv, is)**2&
&         +taf)*(1.0_R8-switch%boris)+ptf*ev)/geo%fcs(ifc)
      END DO
      y2 = isign*(dv%fhe(ifc, 0)+dv%fhe(ifc, 1)+(dv%fne(ifc, 0)+dv%fne(&
&       ifc, 1))*tef*(1.0_R8-switch%boris))
      IF (0.0_R8 .LT. y2) THEN
        max4 = y2
      ELSE
        max4 = 0.0_R8
      END IF
      qel = max4/geo%fcs(ifc)
      IF (lcore) THEN
        surface_temp = 0.0_R8
      ELSE
        surface_temp = 0.0_R8
      END IF
!
!    ..loop over species
!mb   Loop implicitly expects that the species are arranged per isonuclear sequence and that the neutral atom of that sequence al
!ways comes first. If not, erroneous results will be obtained.
      is0 = 0
      is1 = 0
      nnsum = 0.0_R8
      nnwwnsum = 0.0_R8
      DO is=nsmin,nsmax
        IF (am(is0) - am(is) .GE. 0.) THEN
          abs0 = am(is0) - am(is)
        ELSE
          abs0 = -(am(is0)-am(is))
        END IF
        IF (NINT(zn(is0)) .NE. NINT(zn(is)) .OR. amtol .LT. abs0) is0 = &
&           is
!dpc
        IF (is_neutral(is)) is1 = is
        IF (am(is1) - am(is) .GE. 0.) THEN
          abs1 = am(is1) - am(is)
        ELSE
          abs1 = -(am(is1)-am(is))
        END IF
        IF (NINT(zn(is1)) .NE. NINT(zn(is)) .OR. amtol .LT. abs1) WRITE(&
&                                                                 *, *) &
&                                        'b2stbr problem: is, is0, is1 '&
&                                                                 , is, &
&                                                                 is0, &
&                                                                 is1
        IF (is0 .NE. is1) THEN
          IF (ncall_b2stbr_phys .EQ. 0) WRITE(*, *) &
&                                       'b2stbr: setting is0 to is1 ', &
&                                       is0, is1
          is0 = is1
        END IF
        is2 = -1
        k = 0
        found = .false.
        DO WHILE (.NOT.found .AND. k .LT. nscx)
          IF (is0 .EQ. iscx(k)) THEN
            found = .true.
            is2 = k
          END IF
          k = k + 1
        END DO
        IF (0.0_R8 .LT. isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))) &
&       THEN
          max5 = isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))
        ELSE
          max5 = 0.0_R8
        END IF
!dpc
!       (species (is) recycles into is0)
!       (is0 corresponds to CX species is2)
! dpc: Eirene coupling 1999.06.28
        t0 = max5*switch%neutral_sources_rescale*strasclfl(istra)
        IF (0.0_R8 .LT. tif/ev + (pof-phi_app)*zaf(is)) THEN
          einc = tif/ev + (pof-phi_app)*zaf(is)
        ELSE
          einc = 0.0_R8
        END IF
! xpb: Incident particle flux (part/m^2/s)
        pflx = t0/geo%fcs(ifc)
        IF (is_neutral(is)) THEN
          arg11 = 8.0_R8*pl%ti(icv)/(pi*am(is)*mp)
          vbar = SQRT(arg11)
! dpc: Eirene coupling 1999.06.28
          pflx = pflx + pl%na(icv, is)*vbar*switch%b2stbr_alpha*switch%&
&           neutral_sources_rescale*strasclfl(istra)
        END IF
!! end KU Leuven advanced fluid neutral models
        IF (switch%recycle_afn .EQ. 0 .OR. NINT(zn(is0)) .NE. 1) THEN
!! default neutral recycling/reflection model (always for non-hydrogenic species)
          srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + b2recyc(is, &
&           istra)*t0
!wdk  recycle toroidal componenent of parallel momentum, factor mrecyc
!     (toroidal piece only, because poloidal piece of parallel velocity flips sign after reflection)
          srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + mrecyc(is, &
&           istra)*am(is)*mp*pl%ua(icv, is)*geo%cvbb(icv, 2)**2/geo%cvbb&
&           (icv, 3)**2*b2recyc(is, istra)*t0*geo%cvhz(icv)
          IF (switch%lin_shi0 .EQ. 1) THEN
            x1 = erecyc(is, istra)*(1.5_R8+switch%boris)*einc*ev
            IF (x1 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              max6 = (1.5_R8+switch%boris)*surface_temp*kbolt
            ELSE
              max6 = x1
            END IF
            srw%shi0(icv, 1) = srw%shi0(icv, 1) + switch%&
&             recycled_neutrals_contr*max6*b2recyc(is, istra)*t0/pl%ti(&
&             icv)
          ELSE
            x2 = erecyc(is, istra)*(1.5_R8+switch%boris)*einc*ev
            IF (x2 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              max7 = (1.5_R8+switch%boris)*surface_temp*kbolt
            ELSE
              max7 = x2
            END IF
            srw%shi0(icv, 0) = srw%shi0(icv, 0) + switch%&
&             recycled_neutrals_contr*max7*b2recyc(is, istra)*t0
          END IF
! xpb: recycled neutrals come back with at least thermal energy
          IF (is2 .NE. -1) THEN
            x3 = erecyc(is, istra)*(1.5_R8+switch%boris)*einc*ev
            IF (x3 .LT. (1.5_R8+switch%boris)*surface_temp*kbolt) THEN
              max8 = (1.5_R8+switch%boris)*surface_temp*kbolt
            ELSE
              max8 = x3
            END IF
            shi0_ff(icv, is2) = shi0_ff(icv, is2) + switch%&
&             recycled_neutrals_contr*max8*b2recyc(is, istra)*t0
          END IF
        ELSE
!
!! use advanced fluid neutral boundary conditions by N. Horsten et. al
! lvn1
!**************************************************************************************
!*****************     New KU Leuven recycling model for neutrals *********************
!**************************************************************************************
!
! main reference:
!   Horsten, Niels, Giovanni Samaey, and M. Baelmans.
!  "Development and assessment of 2D fluid neutral models that include atomic databases and a microscopic reflection model."
!   Nuclear Fusion 57.11 (2017): 116043.
!
!! area of the face
          farea = geo%fcs(ifc)
          cosa = geo%fcqalf(ifc, 0)
          sina = geo%fcqalf(ifc, 1)
!
          IF (is .NE. is0) THEN
!! ion species, recycling
!
!
!mb   net momentum sources from neutral particle recombination
!            Calculate recycled neutral particle flux (assuming thin boundary cells)
            IF (.NOT.lcore) THEN
!
              CALL CALCRECYCLEDFLUXES(icv, mpg%nci, ifc, isign, is0, is2&
&                               , is, istra, iwall, phi_app, farea, &
&                               b2recyc(is, istra), recycm(is, istra), &
&                               dv%fluid_frac_hyb(ifc), pl, tif, tef, &
&                               pof, pl%na(icv, is), dv%ne(icv), geo, &
&                               mpg, t0, cosa, sina, switch%use_uy_uz_0&
&                               , fnnrec, fmomrec, nnrec, nnwwnrec, &
&                               fenerec)
            ELSE
              fnnrec = 0.0_R8
              fmomrec = 0.0_R8
              nnrec = 0.0_R8
              nnwwnrec = 0.0_R8
              fenerec = 0.0_R8
            END IF
!
            snan = fnnrec*switch%neutral_sources_rescale*strasclfl(istra&
&             )
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + snan
            smon = fmomrec*geo%cvhz(icv)*switch%neutral_sources_rescale*&
&             strasclfl(istra)
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + smon
!
            nnsum = nnsum + nnrec
            nnwwnsum = nnwwnsum + nnwwnrec
!mb   net neutral energy source from recycling
!
            IF (switch%tn_style .EQ. 0) THEN
              srw%shi0(icv, 0) = srw%shi0(icv, 0) + fenerec*switch%&
&               neutral_sources_rescale*strasclfl(istra)
            ELSE IF (switch%tn_style .NE. 1) THEN
!! neutral energy boundary conditions are not used
              srw%shn0(icv, 0) = srw%shn0(icv, 0) + fenerec*switch%&
&               neutral_sources_rescale*strasclfl(istra)
            END IF
!
            IF (switch%afn_out .NE. 0) THEN
              fmomrec_out(icv) = fmomrec
              fenerec_out(icv) = fenerec
            END IF
          ELSE
!
!! this else belongs to "if(.not.(is.eq.is0))", hence 'is' will be neutral here
!
!          Identify corresponding ion species
            IF (LNEXT(is, is + 1)) THEN
              isi = is + 1
            ELSE
              CALL XERRAB('Problem with species labeling')
            END IF
!
!          A conversion is necessary because the BC implemented below were developed in a code that uses a total energy equation,
!          while B2.5 uses internal energy equations.
!
!
            q_conv = isign*(tnf*pl%ua(icv, is0)*geo%fcpbs(ifc)*pl%na(icv&
&             , is0)+(dv%fhm(ifc, 0, is0)+dv%fhm(ifc, 1, is0)))*switch%&
&             neutral_sources_rescale*strasclfl(istra)
!
            IF (switch%tn_style .EQ. 0) THEN
              srw%shi0(icv, 0) = srw%shi0(icv, 0) + q_conv
            ELSE IF (switch%tn_style .NE. 1) THEN
!! do nothing, no neutral contributions in the boundary conditions of the pure ion energy equation
              srw%shn0(icv, 0) = srw%shn0(icv, 0) + q_conv
            END IF
!
            IF (maxw(istra) .EQ. 0) THEN
              CALL CALCINCIDENTFLUXESDIFFUSION(icv, icn, ifc, isign, is0&
&                                        , is2, isi, istra, phi_app, &
&                                        farea, pl, tif, tnf, tef, pof, &
&                                        dv%ne(icv), geo, rt, dnndy, &
&                                        dnndz, cosa, sina, switch%&
&                                        use_uy_uz_0, switch%use_uy_uz_1&
&                                        , switch%diffusion_bc_safeguard&
&                                        , fnni, fmomni, nni, nnwwni, &
&                                        feneni)
            ELSE IF (maxw(istra) .EQ. 1) THEN
              CALL CALCINCIDENTFLUXESMAXWELLIAN(icv, ifc, is0, isign, &
&                                         istra, farea, pl, tnf, geo, &
&                                         cosa, sina, fnni, fmomni, nni&
&                                         , nnwwni, feneni)
            ELSE IF (maxw(istra) .EQ. 2) THEN
              CALL CALCINCIDENTFLUXESKN(switch%kn_b1, switch%kn_b2, icv&
&                                 , icn, ifc, isign, is0, is2, isi, &
&                                 istra, phi_app, farea, pl, tif, tnf, &
&                                 tef, pof, dv%ne(icv), geo, rt, dnndy, &
&                                 dnndz, cosa, sina, switch%use_uy_uz_0&
&                                 , switch%use_uy_uz_1, switch%&
&                                 l_macro_afn, fnni, fmomni, nni, nnwwni&
&                                 , feneni)
            ELSE
              CALL CALCINCIDENTFLUXESMINKN(icv, icn, ifc, isign, is0, &
&                                    is2, isi, istra, phi_app, farea, pl&
&                                    , tif, tnf, tef, pof, dv%ne(icv), &
&                                    geo, rt, dnndy, dnndz, cosa, sina, &
&                                    switch%use_uy_uz_0, switch%&
&                                    use_uy_uz_1, fnni, fmomni, nni, &
&                                    nnwwni, feneni)
!
            END IF
!
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) - fnni*switch%&
&             neutral_sources_rescale*strasclfl(istra)
!
!! save the incident fluid neutral flux
            dv%fnn_inc(ifc, is0) = fnni*isign
!
!mb   net momentum sources from neutral particle reflection
            IF (.NOT.lcore) THEN
              IF (maxw(istra) .EQ. 0) THEN
                CALL CALCREFLECTEDFLUXESDIFFUSION(icv, icn, mpg%nci, ifc&
&                                           , isign, is0, is2, isi, &
&                                           istra, iwall, phi_app, farea&
&                                           , b2recyc(is, istra), recycm&
&                                           (is, istra), dv%&
&                                           fluid_frac_hyb(ifc), pl, tif&
&                                           , tnf, tef, pof, dv%ne(icv)&
&                                           , geo, mpg, rt, dnndy, dnndz&
&                                           , cosa, sina, switch%&
&                                           use_uy_uz_0, switch%&
&                                           use_uy_uz_1, switch%&
&                                           diffusion_bc_safeguard, &
&                                           fnnrefl, fmomrefl, nnrefl, &
&                                           nnwwnrefl, fenerefl, fene_el&
&                                          )
              ELSE IF (maxw(istra) .EQ. 1) THEN
                CALL CALCREFLECTEDFLUXESMAXWELLIAN(icv, mpg%nci, ifc, &
&                                            isign, is0, istra, iwall, &
&                                            farea, b2recyc(is, istra), &
&                                            recycm(is, istra), dv%&
&                                            fluid_frac_hyb(ifc), pl, &
&                                            tnf, geo, mpg, cosa, sina, &
&                                            fnnrefl, fmomrefl, nnrefl, &
&                                            nnwwnrefl, fenerefl, &
&                                            fene_el)
              ELSE IF (maxw(istra) .EQ. 2) THEN
                CALL CALCREFLECTEDFLUXESKN(switch%kn_b1, switch%kn_b2, &
&                                    icv, icn, mpg%nci, ifc, isign, is0&
&                                    , is2, isi, istra, iwall, phi_app, &
&                                    farea, b2recyc(is, istra), recycm(&
&                                    is, istra), dv%fluid_frac_hyb(ifc)&
&                                    , pl, tif, tnf, tef, pof, dv%ne(icv&
&                                    ), geo, mpg, rt, dnndy, dnndz, cosa&
&                                    , sina, switch%use_uy_uz_0, switch%&
&                                    use_uy_uz_1, switch%l_macro_afn, &
&                                    fnnrefl, fmomrefl, nnrefl, &
&                                    nnwwnrefl, fenerefl, fene_el)
              ELSE
                CALL CALCREFLECTEDFLUXESMINKN(icv, icn, mpg%nci, ifc, &
&                                       isign, is0, is2, isi, istra, &
&                                       iwall, phi_app, farea, b2recyc(&
&                                       is, istra), recycm(is, istra), &
&                                       dv%fluid_frac_hyb(ifc), pl, tif&
&                                       , tnf, tef, pof, dv%ne(icv), geo&
&                                       , mpg, rt, dnndy, dnndz, cosa, &
&                                       sina, switch%use_uy_uz_0, switch&
&                                       %use_uy_uz_1, fnnrefl, fmomrefl&
&                                       , nnrefl, nnwwnrefl, fenerefl, &
&                                       fene_el)
              END IF
            ELSE
              fnnrefl = 0.0_R8
              fmomrefl = 0.0_R8
              nnrefl = 0.0_R8
              nnwwnrefl = 0.0_R8
              fenerefl = 0.0_R8
              fene_el = 0.0_R8
            END IF
!
!nh  adding the reflected particle source (with the molecular part subtracted)
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + fnnrefl*&
&             switch%neutral_sources_rescale*strasclfl(istra)
            snan = fnnrefl
!
            IF (switch%afn_out .NE. 0) THEN
              fmomni_out(icv) = fmomni
              fmomrefl_out(icv) = fmomrefl
              feneni_out(icv) = feneni
              fenerefl_out(icv) = fenerefl
              fnni_out(icv) = fnni
            END IF
!
            smon = (fmomni+fmomrefl)*geo%cvhz(icv)*switch%&
&             neutral_sources_rescale*strasclfl(istra)
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + smon
!
            nnsum = nnsum + nni + nnrefl
            nnwwnsum = nnwwnsum + nnwwni + nnwwnrefl
!
            IF (switch%tn_style .EQ. 0) THEN
              srw%shi0(icv, 0) = srw%shi0(icv, 0) + (feneni+fenerefl)*&
&               switch%neutral_sources_rescale*strasclfl(istra)
            ELSE IF (switch%tn_style .NE. 1) THEN
!! neutral energy boundary conditions are not used
              srw%shn0(icv, 0) = srw%shn0(icv, 0) + (feneni+fenerefl)*&
&               switch%neutral_sources_rescale*strasclfl(istra)
            END IF
!
! remove the Franck-Condon energy, that has been provided to the neutrals, from the electrons at the boundary
! temporary switch to evaluate the effect.
            IF (switch%remove_fc_el .EQ. 1) srw%she0(icv, 0) = srw%she0(&
&               icv, 0) + fene_el*switch%neutral_sources_rescale*&
&               strasclfl(istra)
          END IF
!
          IF (NINT(zamax(is)) .EQ. NINT(zn(is))) THEN
!! see eq. 62-63 of the main reference.
            IF (nnsum .EQ. 0.0_R8) nnsum = 1.0_R8
            smon = -(isign*cosa*am(is0)*mp/3.0_R8*nnwwnsum**2/nnsum*&
&             farea*geo%cvbb(icv, 0)/geo%cvbb(icv, 3)*geo%cvhz(icv))
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + smon*switch%&
&             neutral_sources_rescale*strasclfl(istra)
            nnsum = 0.0_R8
            nnwwnsum = 0.0_R8
          END IF
        END IF
!
!    ..recycling into next ionized species
        IF (is .LT. ns - 1) THEN
          IF (LNEXT(is, is + 1)) srw%sna0(icv, 0, is+1) = srw%sna0(icv, &
&             0, is+1) + rcion(is, istra)*t0/(switch%&
&             neutral_sources_rescale*strasclfl(istra))
        END IF
      END DO
! IYS 18.09.2018
! do is = 0, ns-1
!    ..recycling at core into highest ionized stage
      IF (lcore) THEN
!    ..loop over species
        is0 = ns - 1
        is1 = ns - 1
        DO is=nsmax,nsmin,-1
          IF (am(is0) - am(is) .GE. 0.) THEN
            abs2 = am(is0) - am(is)
          ELSE
            abs2 = -(am(is0)-am(is))
          END IF
          IF (NINT(zn(is0)) .NE. NINT(zn(is)) .OR. amtol .LT. abs2) is0&
&            = is
!xpb
          IF (NINT(zamax(is)) .EQ. NINT(zn(is))) is1 = is
          IF (am(is1) - am(is) .GE. 0.) THEN
            abs3 = am(is1) - am(is)
          ELSE
            abs3 = -(am(is1)-am(is))
          END IF
          IF (NINT(zn(is1)) .NE. NINT(zn(is)) .OR. amtol .LT. abs3) &
&           WRITE(*, '(a,3i3)') 'b2stbr problem: is, is0, is1 ', is, is0&
&           , is1
          IF (is0 .NE. is1) THEN
            IF (ncall_b2stbr_phys .EQ. 0) WRITE(*, '(a,2i3)') &
&                                         'b2stbr: setting is0 to is1 '&
&                                         , is0, is1
            is0 = is1
          END IF
          IF (0.0_R8 .LT. isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))&
&         ) THEN
            max9 = isign*(dv%fna(ifc, 0, is)+dv%fna(ifc, 1, is))
          ELSE
            max9 = 0.0_R8
          END IF
!xpb
!          (species (is) recycles into is0)
! xpb: 2002.02.01
          t0 = max9*switch%b2stbr_core_sources_rescale
          IF (is .NE. is0) THEN
            srw%sna0(icv, 0, is0) = srw%sna0(icv, 0, is0) + b2recyc(is, &
&             istra)*t0
!
!wdk  recycling of true parallel momentum
!     (at core, cell faces will be aligned with flux surfaces)
            srw%smo0(icv, 0, is0) = srw%smo0(icv, 0, is0) + mrecyc(is, &
&             istra)*am(is)*mp*pl%ua(icv, is)*b2recyc(is, istra)*t0*geo%&
&             cvhz(icv)
!srv
            srw%shi0(icv, 0) = srw%shi0(icv, 0) + erecyc(is, istra)*(&
&             1.5_R8+switch%boris)*(zaf(is)*tef+tif)*b2recyc(is, istra)*&
&             t0*switch%recycled_neutrals_contr
          END IF
        END DO
      END IF
! do is = nsmax, nsmin, -1
!
      RETURN
    END IF
  END SUBROUTINE RECYCLE

END SUBROUTINE B2STBR_PHYS_DV

