!      ratio of Banana orbit to density gradient length
          deltawn(ix, iy, is) = wb(ix, iy, is)/lnn(ix, iy, is)
!      ratio of Banana orbit to temperature gradient length
          deltawt(ix, iy, is) = wb(ix, iy, is)/ltt(ix, iy, is)
!      ion gyro-frequency
          omega(ix, iy, is) = rza(ix, iy, is)*e_cgs*bb(ix, iy, 3)*gauss/&
&           (am(is)*mp*gramm*c_cgs)
!      ratio of the total collision frequency to the gyro-frequency
          deltab(ix, iy, is) = nua(ix, iy, is)/omega(ix, iy, is)
!      measure for the density perturbation in a collision dominated
!      regime
          deltapar(ix, iy, is) = (1.-fc_(iy))**4.*nustar(ix, iy, is)*wb(&
&           ix, iy, is)*rmaj_(iy)*nua(ix, iy, is)/(minlnlt**2.*omega(ix&
&           , iy, is))
        END IF
      END DO
!      minimum gradient length
      IF (lnn(ix, iy, ns) .LT. ltt(ix, iy, ns)) THEN
        minlnlt = lnn(ix, iy, ns)
      ELSE
        minlnlt = ltt(ix, iy, ns)
      END IF
!      Larmor radius
      rhol(ix, iy, ns) = me*gramm*c_cgs*vtha(ix, iy, ns)/(e_cgs*bb(ix, &
&       iy, 3)*gauss*cm)
!      ratio of Larmor radius to minimum gradient length
      deltal(ix, iy, ns) = rhol(ix, iy, ns)/minlnlt
!      width of Banana orbit
      wb(ix, iy, ns) = me*gramm*c_cgs*(1.-fc_(iy))*vtha(ix, iy, ns)/(&
&       e_cgs*bb(ix, iy, 0)*gauss*cm)
!      ratio of Banana orbit to minimum gradient length
      deltaw(ix, iy, ns) = wb(ix, iy, ns)/minlnlt
!      ratio of Banana orbit to density gradient length
      deltawn(ix, iy, ns) = wb(ix, iy, ns)/lnn(ix, iy, ns)
!      ratio of Banana orbit to temperature gradient length
      deltawt(ix, iy, ns) = wb(ix, iy, ns)/ltt(ix, iy, ns)
!      electron gyro-frequency
      omega(ix, iy, ns) = e_cgs*bb(ix, iy, 3)*gauss/(me*gramm*c_cgs)
!      ratio of the total collision frequency to the gyro-frequency
      deltab(ix, iy, ns) = nua(ix, iy, ns)/omega(ix, iy, ns)
!      measure for the density perturbation in a collision dominated
!      regime
      deltapar(ix, iy, ns) = (1.-fc_(iy))**4.*nustar(ix, iy, ns)*wb(ix, &
&       iy, ns)*rmaj_(iy)*nua(ix, iy, ns)/(minlnlt**2.*omega(ix, iy, ns)&
&       )
    END DO
  END DO
!
!
!   ..return
  CALL SUBEND()
  RETURN
END SUBROUTINE NEO_VALIDITY_NODIFF

!
SUBROUTINE CHANG_HINTON_NODIFF(nx, ny, ns, jsep, jxa, lambda, &
& safetyfactor, ne, te, ti, na, transport)
  USE B2MOD_TYPES
  USE B2MOD_GEO_DIFFV
  USE B2MOD_INDIRECT
  USE B2MOD_CONSTANTS
  USE B2MOD_RATES
  USE B2MOD_B2CMPA_DIFFV
  USE B2MOD_B2PLOT_DEBUG
  USE B2MOD_EXTERNAL_DIFFV
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
  INTEGER :: nx, ny, ns, jsep, jxa
  REAL(kind=r8) :: lambda, ne(-1:nx, -1:ny), te(-1:nx, -1:ny), ti(-1:nx&
& , -1:ny), na(-1:nx, -1:ny, 0:ns-1), safetyfactor(-1:ny), transport(-1:&
& nx, -1:ny, 0:ns-1)
!
  INTEGER :: ix, iy, is, irmin, irmax, is_fuel
  REAL(kind=r8) :: crmin, crmax, czmin, czmax, cr2max, cr2min, cr1max, &
& cr1min, bave, epsilon, lnlam, vthi, omegaci, rhoi, alpha, deltap, f1, &
& f2, taui, nui, nustari, mustari, chi, bracket1, bracket2, ammin
  EXTERNAL XERTST
  INTRINSIC MIN
  INTRINSIC MAX
  INTRINSIC LOG
  INTRINSIC SQRT
  REAL(kind=r8) :: y1
  REAL(kind=r8) :: y2
  REAL(kind=r8) :: y3
  REAL(kind=r8) :: y4
  REAL(kind=r8) :: y5
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: arg1
  REAL(kind=r8) :: arg2
  REAL(kind=r8) :: result2
  REAL(r8) :: result10
  REAL(kind=r8) :: pwr1
!
  CALL SUBINI('chang_hinton')
  transport = 0.0_R8
  DO iy=-1,jsep
    IF (b2plot_debug .GE. 8) WRITE(*, *) 'iy: ', iy
    ix = jxa
    CALL XERTST(on_closed_surface(ix, iy), 'Not in core')
    irmin = jxa
    irmax = jxa
    crmin = CR(ix, iy)
    crmax = crmin
    czmin = CZ(ix, iy)
    czmax = czmin
    ix = rightix(ix, iy)
    DO WHILE (ix .NE. jxa .AND. ix .GE. -1 .AND. ix .LE. nx)
      result1 = CR(ix, iy)
      IF (result1 .LT. crmin) irmin = ix
      result1 = CR(ix, iy)
      IF (result1 .GT. crmax) irmax = ix
      y1 = CR(ix, iy)
      IF (crmin .GT. y1) THEN
        crmin = y1
      ELSE
        crmin = crmin
      END IF
      y2 = CR(ix, iy)
      IF (crmax .LT. y2) THEN
        crmax = y2
      ELSE
        crmax = crmax
      END IF
      y3 = CZ(ix, iy)
      IF (czmin .GT. y3) THEN
        czmin = y3
      ELSE
        czmin = czmin
      END IF
      y4 = CZ(ix, iy)
      IF (czmax .LT. y4) THEN
        czmax = y4
      ELSE
        czmax = czmax
      END IF
      ix = rightix(ix, iy)
    END DO
    CALL XERTST(ix .EQ. jxa, 'Core not continuous')
    IF (b2plot_debug .GE. 8) WRITE(*, *) 'crmin,crmax,czmin,czmax', &
&                            crmin, crmax, czmin, czmax
    result1 = CR(ix, iy)
    bave = result1*2.0_R8/(crmax+crmin)*bb(ix, iy, 3)
    IF (b2plot_debug .GE. 8) WRITE(*, *) 'ne,te,ti,b,q: ', ne(ix, iy), &
&                            te(ix, iy)/qe, ti(ix, iy)/qe, bave, &
&                            safetyfactor(iy)
    epsilon = (crmax-crmin)/(crmax+crmin)
    IF (b2plot_debug .GE. 8) WRITE(*, *) 'epsilon: ', epsilon
    IF (lambda .LT. 0.0_R8) THEN
      arg1 = ne(ix, iy)/1e20_R8
      arg2 = ti(ix, iy)/ev/1000.0_R8
      y5 = 17.3_R8 - LOG(arg1)/2.0_R8 + 1.5_R8*LOG(arg2)
      IF (-lambda .LT. y5) THEN
        lnlam = y5
      ELSE
        lnlam = -lambda
      END IF
    ELSE
      lnlam = lambda
    END IF
    IF (b2plot_debug .GE. 8) WRITE(*, *) 'lnlam: ', lnlam
    cr2max = 0.5_R8*(crx(irmax, iy, 1)+crx(irmax, iy, 3))
    cr2min = 0.5_R8*(crx(irmin, iy, 1)+crx(irmin, iy, 3))
    cr1max = 0.5_R8*(crx(irmax, iy, 0)+crx(irmax, iy, 2))
    cr1min = 0.5_R8*(crx(irmin, iy, 0)+crx(irmin, iy, 2))
    IF (b2plot_debug .GE. 8) WRITE(*, *) 'cr2max,cr2min,cr1max,cr1min: '&
&                            , cr2max, cr2min, cr1max, cr1min
    deltap = (cr2max+cr2min-(cr1max+cr1min))/(cr2max-cr2min-(cr1max-&
&     cr1min))
    deltap = -2._R8
    IF (b2plot_debug .GE. 8) WRITE(*, *) 'deltap: ', deltap
    f1 = (1_R8+1.5_R8*(epsilon**2+epsilon*deltap)+0.375_R8*epsilon**3*&
&     deltap)/(1_R8+0.5_R8*epsilon*deltap)
    IF (b2plot_debug .GE. 8) WRITE(*, *) 'f1: ', f1
    arg1 = 1_R8 - epsilon**2
    result1 = SQRT(arg1)
    arg2 = 1_R8 - epsilon**2
    result2 = SQRT(arg2)
    f2 = result1*(1.0_R8+0.5_R8*epsilon*deltap)/(1_R8+deltap/epsilon*(&
&     result2-1.0_R8))
    IF (b2plot_debug .GE. 8) WRITE(*, *) 'f2: ', f2
!   ..calculate alpha
    alpha = 0._R8
    ammin = 54._R8
    is_fuel = 1
    DO is=0,ns-1
      IF (.NOT.is_neutral(is) .AND. am(is) .LT. ammin) THEN
        is_fuel = is
        ammin = am(is)
      END IF
    END DO
    DO is=0,ns-1
      IF (.NOT.is_neutral(is) .AND. is .NE. is_fuel) alpha = alpha + na(&
&         ix, iy, is)*rz2(ix, iy, is)
    END DO
    DO is=0,ns_ext-1
      IF (.NOT.is_ext_neutral(is)) alpha = alpha + na_ext(ix, iy, is)*&
&         za2_ext(ix, iy, is)
    END DO
    alpha = alpha/(na(ix, iy, is_fuel)*rz2(ix, iy, is_fuel))
    IF (b2plot_debug .GE. 8) WRITE(*, *) 'alpha: ', alpha
    DO is=0,ns-1
      IF (.NOT.is_neutral(is)) THEN
        IF (b2plot_debug .GE. 8) WRITE(*, *) 'rza,am,na: ', rza(ix, iy, &
&                                is), am(is), na(ix, iy, is)
        arg1 = ti(ix, iy)/mp/am(is)
        vthi = SQRT(arg1)
        IF (b2plot_debug .GE. 8) WRITE(*, *) 'vthi: ', vthi
        omegaci = rza(ix, iy, is)*qe*bave/mp/am(is)
        IF (b2plot_debug .GE. 8) WRITE(*, *) 'omegaci: ', omegaci
        result10 = SQRT(2.0_R8)
        rhoi = result10*vthi/omegaci
        IF (b2plot_debug .GE. 8) WRITE(*, *) 'rhoi: ', rhoi
        taui = 12.0_R8*pi**1.5_R8*eps0**2*(am(is)*mp)**0.5_R8*ti(ix, iy)&
&         **1.5_R8/(na(ix, iy, is)*rz2(ix, iy, is)**2*qe**4*lnlam)
        IF (b2plot_debug .GE. 8) WRITE(*, *) 'taui: ', taui
        nui = 1.0_R8/taui
        nustari = nui*(crmax+crmin)/2.0_R8*safetyfactor(iy)/epsilon**&
&         1.5_R8/vthi
        IF (b2plot_debug .GE. 8) WRITE(*, *) 'nustari: ', nustari
        mustari = nustari*(1.0_R8+1.54_R8*alpha)
        IF (b2plot_debug .GE. 8) WRITE(*, *) 'mustari: ', mustari
        bracket1 = (0.66_R8*(1.0_R8+1.54_R8*alpha)+(1.88_R8*epsilon**&
&         0.5_R8-1.54_R8*epsilon)*(1.0_R8+3.75_R8*alpha))/(1.0_R8+&
&         1.03_R8*mustari**0.5_R8+0.31*mustari)
        bracket2 = 0.59_R8*mustari*epsilon/(1_R8+0.74_R8*mustari*epsilon&
&         **1.5_R8)*(1_R8+1.33_R8*alpha*(1.0_R8+0.60_R8*alpha)/(1.0_R8+&
&         1.79_R8*alpha))
        IF (b2plot_debug .GE. 8) WRITE(*, *) 'bracket1 =', bracket1
        IF (b2plot_debug .GE. 8) WRITE(*, *) 'bracket2 =', bracket2
        pwr1 = epsilon**(-1.5_R8)
        chi = pwr1*safetyfactor(iy)**2*rhoi**2/taui*((0.66_R8*(1.0_R8+&
&         1.54_R8*alpha)+(1.88_R8*epsilon**0.5_R8-1.54_R8*epsilon)*(1_R8&
&         +3.75_R8*alpha))/(1.0_R8+1.03_R8*mustari**0.5_R8+0.31*mustari)&
&         *f1+0.59_R8*mustari*epsilon/(1_R8+0.74_R8*mustari*epsilon**&
&         1.5_R8)*(1_R8+1.33_R8*alpha*(1.0_R8+0.60_R8*alpha)/(1.0_R8+&
&         1.79_R8*alpha))*(f1-f2))
        IF (b2plot_debug .GE. 8) WRITE(*, *) 'chi: ', chi
        transport(ix, iy, is) = chi
        ix = rightix(ix, iy)
        DO WHILE (ix .NE. jxa .AND. ix .GE. -1 .AND. ix .LE. nx)
          transport(ix, iy, is) = chi
          ix = rightix(ix, iy)
        END DO
      END IF
    END DO
  END DO
  CALL SUBEND()
  RETURN
END SUBROUTINE CHANG_HINTON_NODIFF
