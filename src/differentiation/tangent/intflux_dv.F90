          abs0 = geo%fcqalf(ifc, idir)
        ELSE
          abs0 = -geo%fcqalf(ifc, idir)
        END IF
        wrkf(ifc, idir) = faceflux(ifc, idir)/qf(ifc)/(geo%fcs(ifc)*abs0&
&         )
      ELSE
        wrkf(ifc, idir) = 0.0_R8
      END IF
    END DO
  END DO
!
! ..interpolate the effective velocity to cell centers
  CALL INTCELL_NODIFF(nfc, ncv, mpg, mpg%intcellp, wrkf(:, 0), wrkc(:, 0&
&               ))
  CALL INTCELL_NODIFF(nfc, ncv, mpg, mpg%intcellr, wrkf(:, 1), wrkc(:, 1&
&               ))
!
! ..multiply the interpolated velocity
! ..by the projected cell areas and the cell-centered quantity density
