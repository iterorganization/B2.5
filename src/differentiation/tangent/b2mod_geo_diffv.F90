! We identify the top edge
      DO ix=-1,nx
        iy = ny
        DO WHILE (iy .GT. -1 .AND. cflag(ix, iy, cellflag_type) .NE. &
&                 grid_internal)
          IF (cflag(ix, iy, cellflag_type) .EQ. grid_boundary) THEN
            geotype = CELLGEOTYPE(crx(ix, iy, :), cry(ix, iy, :))
            IF (cflag(ix, iy, cellflag_topface) .NE. grid_undefined &
&               .AND. cflag(ix, iy, cellflag_topface) .NE. &
&               lefttargetindex(1) .AND. cflag(ix, iy, cellflag_topface)&
&               .NE. righttargetindex(1) .AND. geotype .NE. &
&               cgeo_tria_notop) region(topix(ix, iy), topiy(ix, iy), 2)&
&              = 2
            IF (cflag(ix, iy, cellflag_rightface) .NE. grid_undefined &
&               .AND. cflag(ix, iy, cellflag_rightface) .NE. &
&               lefttargetindex(1) .AND. cflag(ix, iy, &
&               cellflag_rightface) .NE. righttargetindex(1) .AND. &
&               geotype .NE. cgeo_tria_noright) region(rightix(ix, iy), &
&             rightiy(ix, iy), 1) = -2
            IF (cflag(ix, iy, cellflag_leftface) .NE. grid_undefined &
&               .AND. cflag(ix, iy, cellflag_leftface) .NE. &
&               lefttargetindex(1) .AND. cflag(ix, iy, cellflag_leftface&
&               ) .NE. righttargetindex(1) .AND. geotype .NE. &
&               cgeo_tria_noleft) region(ix, iy, 1) = -2
          END IF
          iy = iy - 1
        END DO
      END DO
      nnreg(2) = 2
    END IF
!
    DO iy=-1,ny
      DO ix=-1,nx
        IF (region(ix, iy, 0) .EQ. 0 .OR. ISGHOSTCELL(cflag(ix, iy, &
&           cellflag_type))) THEN
          resignore(ix, iy, 1) = 0
          resignore(ix, iy, 2) = 0
        ELSE
          resignore(ix, iy, 1) = 1
          resignore(ix, iy, 2) = 1
        END IF
      END DO
    END DO
!
    RETURN
  END SUBROUTINE INIT_REGION_EXTENDED

!
  FUNCTION ABS_DIFF(p1, p2)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    REAL(kind=r8) :: abs_diff, p1, p2
    INTRINSIC ABS
    IF (p1 - p2 .GE. 0.) THEN
      abs_diff = p1 - p2
    ELSE
      abs_diff = -(p1-p2)
    END IF
    RETURN
  END FUNCTION ABS_DIFF

!
  FUNCTION CR(ix, iy)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix, iy
    REAL(kind=r8) :: cr
    cr = 0.25_R8*(crx(ix, iy, 0)+crx(ix, iy, 1)+crx(ix, iy, 2)+crx(ix, &
&     iy, 3))
    RETURN
  END FUNCTION CR

!
  FUNCTION CZ(ix, iy)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix, iy
    REAL(kind=r8) :: cz
    cz = 0.25_R8*(cry(ix, iy, 0)+cry(ix, iy, 1)+cry(ix, iy, 2)+cry(ix, &
&     iy, 3))
    RETURN
  END FUNCTION CZ

!
  FUNCTION DIST(ix, iy, i, j)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix, iy, i, j
    REAL(kind=r8) :: dist
    REAL(kind=r8) :: arg1
    REAL(kind=r8) :: arg2
    arg1 = crx(ix, iy, j) - crx(ix, iy, i)
    arg2 = cry(ix, iy, j) - cry(ix, iy, i)
    dist = NORM(arg1, arg2)
    RETURN
  END FUNCTION DIST

!
  FUNCTION DISTC(ix1, iy1, ip1, ix2, iy2, ip2)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix1, iy1, ip1, ix2, iy2, ip2
    REAL(kind=r8) :: distc
    REAL(kind=r8) :: arg1
    REAL(kind=r8) :: arg2
    arg1 = crx(ix1, iy1, ip1) - crx(ix2, iy2, ip2)
    arg2 = cry(ix1, iy1, ip1) - cry(ix2, iy2, ip2)
    distc = NORM(arg1, arg2)
    RETURN
  END FUNCTION DISTC

!
  FUNCTION DISX(ix, iy)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix, iy
    REAL(kind=r8) :: disx
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    result1 = DIST(bottomix(ix, iy), bottomiy(ix, iy), 2, 3)
    result2 = DIST(ix, iy, 0, 1)
    disx = (result1+result2)/2.0_R8
    RETURN
  END FUNCTION DISX

!
  FUNCTION DISY(ix, iy)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix, iy
    REAL(kind=r8) :: disy
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    result1 = DIST(leftix(ix, iy), leftiy(ix, iy), 1, 3)
    result2 = DIST(ix, iy, 0, 2)
    disy = (result1+result2)/2.0_R8
    RETURN
  END FUNCTION DISY

!
  FUNCTION HY1(ix, iy)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix, iy
    REAL(kind=r8) :: hy1
    hy1 = hy(ix, iy)*qz(ix, iy, 1)
    RETURN
  END FUNCTION HY1

!
  LOGICAL FUNCTION MATCH(ix1, iy1, ix2, iy2)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix1, iy1, ix2, iy2
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    result1 = DISTC(ix1, iy1, 1, ix2, iy2, 0)
    result2 = DISTC(ix1, iy1, 3, ix2, iy2, 2)
    match = result1 + result2 .LT. geom_match_dist
    RETURN
  END FUNCTION MATCH

!! Matches right face of cell (ix1,iy1) to left face of cell (ix2,iy2)
!
  LOGICAL FUNCTION MATCHLEFT(ix1, iy1, ix2, iy2)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix1, iy1, ix2, iy2
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    result1 = DISTC(ix1, iy1, 1, ix2, iy2, 0)
    result2 = DISTC(ix1, iy1, 3, ix2, iy2, 2)
    matchleft = result1 + result2 .LT. geom_match_dist
    RETURN
  END FUNCTION MATCHLEFT

!! Matches top face of cell (ix1,iy1) to bottom face of cell (ix2,iy2)
!
  LOGICAL FUNCTION MATCHBOTTOM(ix1, iy1, ix2, iy2)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix1, iy1, ix2, iy2
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    result1 = DISTC(ix1, iy1, 2, ix2, iy2, 0)
    result2 = DISTC(ix1, iy1, 3, ix2, iy2, 1)
    matchbottom = result1 + result2 .LT. geom_match_dist
    RETURN
  END FUNCTION MATCHBOTTOM

!
  FUNCTION PIT(ix, iy)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ix, iy
    REAL(kind=r8) :: pit
    INTRINSIC SQRT
    REAL(kind=r8) :: arg1
    REAL(kind=r8) :: result1
    arg1 = bb(ix, iy, 0)**2 + bb(ix, iy, 1)**2
    result1 = SQRT(arg1)
    pit = result1/bb(ix, iy, 3)
    RETURN
  END FUNCTION PIT

