! region 3 === left inboard divertor
! region 4 === left outboard divertor
! region 5 === right core
! region 6 === right SOL
! region 7 === right inboard divertor
! region 8 === right outboard divertor
!
! For extended grid cases, the logic is to give the same number as in the
! rectangular grid case for the same topological boundary, although this
! means mixing x- and y- directed faces for the faces in contact with the
! targets and the vacuum vessel walls. To do so, when a y-directed face
! should be included in a traditionally x-directed surface (or vice versa),
! we assign it a negative number being minus the number of the x-directed
! region it should belong to.
!
    INTEGER, INTENT(OUT) :: region(-1:nx, -1:ny, 0:2), nnreg(0:2), &
&   resignore(-1:nx, -1:ny, 2)
!
    INTEGER :: ix, iy, ix1, ix2
    INTEGER :: inseliy, inselix1, inselix2, iyt, geotype, iface, offset
    INTEGER :: ixbreak
    INTEGER :: lefttargetindex(2), righttargetindex(2)
    LOGICAL :: celltotest
!
    REAL(kind=r8) :: xvertices(0:3), yvertices(0:3)
    EXTERNAL XERRAB
    INTRINSIC MAX
    INTRINSIC MIN
    INTEGER :: max1
    INTEGER :: min1
    INTEGER :: max2
    INTEGER :: min2
    INTEGER :: min3
    INTEGER :: min4
    REAL(kind=r8) :: result1
    REAL(kind=r8) :: result2
    CALL IPGETR('b2agfs_geom_match_dist', geom_match_dist)
    CALL XERTST(geom_match_dist .GT. 0.0_R8, &
&         'faulty argument geom_match_dist')
!
! if stellarator island, limiter or cylindrical slab case, find periodicity domain coordinate
    IF (periodic_bc .EQ. 1) THEN
      IF (nncut .EQ. 1) THEN
! stellarator island is on North side
        inseliy = ny + 1
        inselix1 = -2
        inselix2 = -2
        DO 100 iy=ny,-1,-1
          DO ix=2,0,-1
            IF (MATCH(nx - ix, iy, ix - 1, iy)) THEN
              inselix1 = ix - 1
              inselix2 = nx - ix
              inseliy = iy
              GOTO 100
            END IF
          END DO
 100    CONTINUE
      ELSE IF (nncut .EQ. 0) THEN
! limited region is on South side
        inseliy = -2
        inselix1 = -2
        inselix2 = -2
        DO iy=ny-1,0,-1
          IF (inseliy .LE. -2) THEN
            ix = -1
            ix1 = -2
            DO WHILE (ix1 .EQ. -2 .AND. ix .LT. nx)
              xvertices(:) = crx(ix, iy, :)
              yvertices(:) = cry(ix, iy, :)
              geotype = CELLGEOTYPE(xvertices, yvertices)
              celltotest = cflag(ix, iy, cellflag_type) .EQ. &
&               grid_internal .OR. (cflag(ix, iy, cellflag_type) .EQ. &
&               grid_boundary .AND. cflag(ix, iy, cellflag_topface) .NE.&
&               grid_undefined .AND. geotype .NE. cgeo_tria_notop)
              IF (.NOT.celltotest) THEN
                ix = ix + 1
              ELSE
                ix1 = ix
              END IF
            END DO
            IF (ix1 .NE. -2) THEN
              ix = nx
              ix2 = -2
              DO WHILE (ix2 .EQ. -2 .AND. ix .GT. -2)
                xvertices(:) = crx(ix, iy, :)
                yvertices(:) = cry(ix, iy, :)
                geotype = CELLGEOTYPE(xvertices, yvertices)
                celltotest = cflag(ix, iy, cellflag_type) .EQ. &
&                 grid_internal .OR. (cflag(ix, iy, cellflag_type) .EQ. &
&                 grid_boundary .AND. cflag(ix, iy, cellflag_topface) &
&                 .NE. grid_undefined .AND. geotype .NE. cgeo_tria_notop&
&                 )
                IF (.NOT.celltotest) THEN
                  ix = ix - 1
                ELSE
                  ix2 = ix
                END IF
              END DO
              IF (ix2 .NE. -2) THEN
                IF (ix1 .NE. ix2 .AND. MATCH(ix2, iy, ix1, iy)) THEN
                  inselix1 = ix1
                  inselix2 = ix2
                  result1 = DISTC(ix1, iy, 0, ix1, iy, 2)
                  result2 = DISTC(ix2, iy, 1, ix2, iy, 3)
                  IF (result1 .LT. geom_match_dist .AND. result2 .LT. &
&                     geom_match_dist) THEN
                    inseliy = iy - 1
                  ELSE
                    inseliy = iy
                  END IF
                END IF
              END IF
            END IF
          END IF
        END DO
        IF (inseliy .EQ. ny - 1) THEN
! Test for cylindrical slab case : then the guard cells match as well
          IF (MATCH(inselix2, ny, inselix1, ny)) inseliy = ny
        END IF
      ELSE
        CALL XERRAB('Unexpected geometry!')
      END IF
    END IF
! volume component
    region = 0
! Ghost cells inherit region number from internal neighbour cell
    IF (nx .EQ. 1) THEN
! 1d radial
      DO iy=-1,ny
        DO ix=-1,nx
          IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) region(ix&
&           , iy, 0) = 1
        END DO
      END DO
      nnreg(0) = 1
    ELSE IF (ny .EQ. 1) THEN
! 1d parallel
      DO iy=-1,ny
        DO ix=-1,nx
          IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) region(ix&
&           , iy, 0) = 1
        END DO
      END DO
      nnreg(0) = 1
    ELSE IF (nncut .EQ. 0 .AND. periodic_bc .EQ. 1) THEN
! limiter and cylindrical slab cases
      DO iy=-1,inseliy
        DO ix=inselix1,inselix2
          IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) region(ix&
&           , iy, 0) = 1
        END DO
      END DO
      DO iy=inseliy+1,ny
        DO ix=-1,nx
          IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) region(ix&
&           , iy, 0) = 2
        END DO
      END DO
      IF (inseliy .EQ. ny) THEN
! cylindrical slab
        nnreg(0) = 1
      ELSE
! limiter geometry
        nnreg(0) = 2
      END IF
    ELSE IF (nncut .EQ. 0 .AND. periodic_bc .EQ. 0) THEN
! 2-D slab case
      DO iy=-1,ny
        DO ix=-1,nx
          IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) region(ix&
&           , iy, 0) = 1
        END DO
      END DO
      nnreg(0) = 1
    ELSE IF (topcut(1) .GE. -1) THEN
      IF (nncut .EQ. 1) THEN
        DO ix=-1,leftcut(1)-1
          DO iy=-1,ny
            IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) THEN
              IF (periodic_bc .NE. 1 .OR. iy .LT. topcut(1)) region(ix, &
&               iy, 0) = 3
              IF (periodic_bc .EQ. 1 .AND. iy .GE. topcut(1) .AND. iy &
&                 .LT. inseliy) region(ix, iy, 0) = 2
              IF (periodic_bc .EQ. 1 .AND. iy .GE. inseliy .AND. ix .GE.&
&                 inselix1 .AND. ix .LE. inselix2) region(ix, iy, 0) = 5
            END IF
          END DO
        END DO
        DO ix=leftcut(1),rightcut(1)-1
          DO iy=bottomcut(1),topcut(1)-1
            IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) region(&
&             ix, iy, 0) = 1
          END DO
          IF (periodic_bc .EQ. 0) THEN
            DO iy=topcut(1),ny
              IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) &
&               region(ix, iy, 0) = 2
            END DO
          ELSE IF (periodic_bc .EQ. 1) THEN
            DO iy=topcut(1),inseliy-1
              IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) &
&               region(ix, iy, 0) = 2
            END DO
            DO iy=inseliy,ny
              IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type)) .AND. &
&                 ix .GE. inselix1 .AND. ix .LE. inselix2) region(ix, iy&
&               , 0) = 5
            END DO
          END IF
        END DO
        DO ix=rightcut(1),nx
          DO iy=-1,ny
            IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) THEN
              IF (periodic_bc .EQ. 0 .OR. iy .LT. topcut(1)) region(ix, &
&               iy, 0) = 4
              IF (periodic_bc .EQ. 1 .AND. iy .GE. topcut(1) .AND. iy &
&                 .LT. inseliy) region(ix, iy, 0) = 2
              IF (periodic_bc .EQ. 1 .AND. iy .GE. inseliy .AND. ix .GE.&
&                 inselix1 .AND. ix .LE. inselix2) region(ix, iy, 0) = 5
            END IF
          END DO
        END DO
        IF (periodic_bc .EQ. 1) THEN
          nnreg(0) = 5
        ELSE
          nnreg(0) = 4
        END IF
      ELSE IF (nncut .EQ. 2) THEN
        DO ix=-1,leftcut(1)-1
          DO iy=-1,ny
            IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) region(&
&             ix, iy, 0) = 3
          END DO
        END DO
        DO ix=leftcut(1),leftcut(2)-1
          IF (bottomcut(1) .LT. bottomcut(2)) THEN
            max1 = bottomcut(2)
          ELSE
            max1 = bottomcut(1)
          END IF
          DO iy=max1,ny
            IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) THEN
              IF (topcut(1) .GT. topcut(2)) THEN
                min1 = topcut(2)
              ELSE
                min1 = topcut(1)
              END IF
              IF (iy .LT. min1) THEN
                region(ix, iy, 0) = 1
              ELSE
                region(ix, iy, 0) = 2
              END IF
            END IF
          END DO
        END DO
        DO iy=-1,ny
          ix = leftcut(2)
          DO WHILE (ISINDOMAIN(nx, ny, leftix(ix, iy), leftiy(ix, iy)) &
&                   .AND. (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type&
&                   ))))
            region(ix, iy, 0) = 4
            ix = ix + 1
          END DO
          ix = rightcut(2) - 1
          DO WHILE (ISINDOMAIN(nx, ny, rightix(ix, iy), rightiy(ix, iy))&
&                   .AND. (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type&
&                   ))))
            region(ix, iy, 0) = 7
            ix = ix - 1
          END DO
        END DO
        DO ix=rightcut(2),rightcut(1)-1
          IF (bottomcut(1) .LT. bottomcut(2)) THEN
            max2 = bottomcut(2)
          ELSE
            max2 = bottomcut(1)
          END IF
          DO iy=max2,ny
            IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) THEN
              IF (topcut(1) .GT. topcut(2)) THEN
                min2 = topcut(2)
              ELSE
                min2 = topcut(1)
              END IF
              IF (iy .LT. min2) THEN
                region(ix, iy, 0) = 5
              ELSE
                region(ix, iy, 0) = 6
              END IF
            END IF
          END DO
        END DO
        DO ix=rightcut(1),nx
          DO iy=-1,ny
            IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) region(&
&             ix, iy, 0) = 8
          END DO
        END DO
        nnreg(0) = 8
      END IF
    END IF
!
    DO ix=-1,nx
      DO iy=-1,ny
        IF (cflag(ix, iy, cellflag_type) .EQ. grid_boundary) THEN
          xvertices(:) = crx(ix, iy, :)
          yvertices(:) = cry(ix, iy, :)
          geotype = CELLGEOTYPE(xvertices, yvertices)
!
          IF (cflag(ix, iy, cellflag_leftface) .NE. grid_undefined .AND.&
&             geotype .NE. cgeo_tria_noleft) region(leftix(ix, iy), &
&           leftiy(ix, iy), 0) = region(ix, iy, 0)
          IF (cflag(ix, iy, cellflag_bottomface) .NE. grid_undefined &
&             .AND. geotype .NE. cgeo_tria_nobot) region(bottomix(ix, iy&
&           ), bottomiy(ix, iy), 0) = region(ix, iy, 0)
          IF (cflag(ix, iy, cellflag_rightface) .NE. grid_undefined &
&             .AND. geotype .NE. cgeo_tria_noright) region(rightix(ix, &
&           iy), rightiy(ix, iy), 0) = region(ix, iy, 0)
          IF (cflag(ix, iy, cellflag_topface) .NE. grid_undefined .AND. &
&             geotype .NE. cgeo_tria_notop) region(topix(ix, iy), topiy(&
&           ix, iy), 0) = region(ix, iy, 0)
        END IF
      END DO
    END DO
! Set unused cells to region 0
!
    DO iy=-1,ny
      DO ix=-1,nx
        IF (ISUNUSEDCELL(cflag(ix, iy, cellflag_type))) region(ix, iy, 0&
&         ) = 0
      END DO
    END DO
! Check all valid cells have a nonzero region number
!
    DO ix=-1,nx
      DO iy=-1,ny
        IF (.NOT.ISUNUSEDCELL(cflag(ix, iy, cellflag_type)) .AND. region&
&           (ix, iy, 0) .EQ. 0) WRITE(*, *) 'REGION not set for ', ix, &
&                               iy, 0
      END DO
    END DO
! After we know the volumetric region numbers, we fix the boundary indices for
! non-structure boundaries. CARRE2 sets them to -1 for all boundary faces not
! on a structure. Here they are set to -REGION.
! The corresponding face of the guard cell receives the same number.
!
!
    DO iy=-1,ny
      DO ix=-1,nx
        IF (cflag(ix, iy, cellflag_type) .EQ. grid_boundary) THEN
          DO iface=cellflag_leftface,cellflag_topface
            IF (cflag(ix, iy, iface) .EQ. boundary_nostructure) THEN
              offset = (iface-cellflag_leftface+1)*(-10)
              cflag(ix, iy, iface) = offset - region(ix, iy, 0)
              xvertices(:) = crx(ix, iy, :)
              yvertices(:) = cry(ix, iy, :)
              geotype = CELLGEOTYPE(xvertices, yvertices)
              IF (iface .EQ. cellflag_leftface .AND. geotype .NE. &
&                 cgeo_tria_noleft) THEN
                cflag(leftix(ix, iy), leftiy(ix, iy), cellflag_rightface&
&               ) = cflag(ix, iy, iface)
              ELSE IF (iface .EQ. cellflag_rightface .AND. geotype .NE. &
&                 cgeo_tria_noright) THEN
                cflag(rightix(ix, iy), rightiy(ix, iy), &
&               cellflag_leftface) = cflag(ix, iy, iface)
              ELSE IF (iface .EQ. cellflag_topface .AND. geotype .NE. &
&                 cgeo_tria_notop) THEN
                cflag(topix(ix, iy), topiy(ix, iy), cellflag_bottomface)&
&                = cflag(ix, iy, iface)
              ELSE IF (iface .EQ. cellflag_bottomface .AND. geotype .NE.&
&                 cgeo_tria_nobot) THEN
                cflag(bottomix(ix, iy), bottomiy(ix, iy), &
&               cellflag_topface) = cflag(ix, iy, iface)
              END IF
            END IF
          END DO
        END IF
      END DO
    END DO
! set up region where the residuals should be ignored (none yet)
!
    IF (nncut .EQ. 0 .AND. periodic_bc .EQ. 1 .AND. nnreg(0) .EQ. 2) &
&   THEN
! limiter case
! We identify the structure index of the left target
      iy = inseliy + 1
      ix = inselix1
      lefttargetindex(1) = grid_undefined
      DO WHILE (ix .GE. -1 .AND. lefttargetindex(1) .EQ. grid_undefined)
        IF (cflag(ix, iy, cellflag_type) .EQ. grid_boundary) THEN
          xvertices(:) = crx(ix, iy, :)
          yvertices(:) = cry(ix, iy, :)
          geotype = CELLGEOTYPE(xvertices, yvertices)
          IF (geotype .NE. cgeo_tria_noleft .AND. cflag(ix, iy, &
&             cellflag_leftface) .NE. grid_undefined) THEN
            lefttargetindex(1) = cflag(ix, iy, cellflag_leftface)
          ELSE IF (geotype .NE. cgeo_tria_notop .AND. cflag(ix, iy, &
&             cellflag_topface) .NE. grid_undefined) THEN
            lefttargetindex(1) = cflag(ix, iy, cellflag_topface)
          ELSE IF (geotype .NE. cgeo_tria_nobot .AND. cflag(ix, iy, &
&             cellflag_bottomface) .NE. grid_undefined) THEN
            lefttargetindex(1) = cflag(ix, iy, cellflag_bottomface)
          ELSE
            CALL XERRAB(&
&            'Invalid cflags settings (L), should not have gotten here!'&
&                )
          END IF
        ELSE
          ix = ix - 1
        END IF
      END DO
      CALL XERTST(lefttargetindex(1) .NE. grid_undefined, &
&           'Left target not found!')
! We identify the structure index of the right target
      iy = inseliy + 1
      ix = inselix2
      righttargetindex(1) = grid_undefined
      DO WHILE (ix .LE. nx .AND. righttargetindex(1) .EQ. grid_undefined&
&        )
        IF (cflag(ix, iy, cellflag_type) .EQ. grid_boundary) THEN
          xvertices(:) = crx(ix, iy, :)
          yvertices(:) = cry(ix, iy, :)
          geotype = CELLGEOTYPE(xvertices, yvertices)
          IF (geotype .NE. cgeo_tria_noright .AND. cflag(ix, iy, &
&             cellflag_rightface) .NE. grid_undefined) THEN
            righttargetindex(1) = cflag(ix, iy, cellflag_rightface)
          ELSE IF (geotype .NE. cgeo_tria_notop .AND. cflag(ix, iy, &
&             cellflag_topface) .NE. grid_undefined) THEN
            righttargetindex(1) = cflag(ix, iy, cellflag_topface)
          ELSE IF (geotype .NE. cgeo_tria_nobot .AND. cflag(ix, iy, &
&             cellflag_bottomface) .NE. grid_undefined) THEN
            righttargetindex(1) = cflag(ix, iy, cellflag_bottomface)
          ELSE
            CALL XERRAB(&
&            'Invalid cflags settings (R), should not have gotten here!'&
&                )
          END IF
        ELSE
          ix = ix + 1
        END IF
      END DO
      CALL XERTST(righttargetindex(1) .NE. grid_undefined, &
&           'Right target not found!')
!
! X-flux component
      DO iy=inseliy+1,ny
! We find the cells contacting the left target: x-region 1
        ix = -1
        DO WHILE (ix .LT. nx .AND. cflag(ix, iy, cellflag_type) .NE. &
&                 grid_internal)
          IF (cflag(ix, iy, cellflag_type) .EQ. grid_boundary) THEN
            xvertices(:) = crx(ix, iy, :)
            yvertices(:) = cry(ix, iy, :)
            geotype = CELLGEOTYPE(xvertices, yvertices)
            IF (cflag(ix, iy, cellflag_leftface) .EQ. lefttargetindex(1)&
&               .AND. geotype .NE. cgeo_tria_noleft) region(ix, iy, 1)&
&              = 1
            IF (cflag(ix, iy, cellflag_topface) .EQ. lefttargetindex(1) &
&               .AND. geotype .NE. cgeo_tria_notop) region(topix(ix, iy)&
&             , topiy(ix, iy), 2) = -1
            IF (cflag(ix, iy, cellflag_bottomface) .EQ. lefttargetindex(&
&               1) .AND. geotype .NE. cgeo_tria_nobot) region(ix, iy, 2)&
&              = -1
          END IF
          ix = ix + 1
        END DO
! We find the cells contacting the right target: x-region 2
        ix = nx
        DO WHILE (ix .GT. -1 .AND. cflag(ix, iy, cellflag_type) .NE. &
&                 grid_internal)
          IF (cflag(ix, iy, cellflag_type) .EQ. grid_boundary) THEN
            geotype = CELLGEOTYPE(crx(ix, iy, :), cry(ix, iy, :))
            IF (cflag(ix, iy, cellflag_rightface) .EQ. righttargetindex(&
&               1) .AND. geotype .NE. cgeo_tria_noright) region(rightix(&
&             ix, iy), rightiy(ix, iy), 1) = 2
            IF (cflag(ix, iy, cellflag_topface) .EQ. righttargetindex(1)&
&               .AND. geotype .NE. cgeo_tria_notop) region(topix(ix, iy)&
&             , topiy(ix, iy), 2) = -2
            IF (cflag(ix, iy, cellflag_bottomface) .EQ. righttargetindex&
&               (1) .AND. geotype .NE. cgeo_tria_nobot) region(ix, iy, 2&
&             ) = -2
          END IF
          ix = ix - 1
        END DO
      END DO
! We find the core periodicity line: x-region 3
      DO iy=-1,inseliy
        IF (.NOT.ISUNUSEDCELL(cflag(inselix1, iy, cellflag_type))) &
&         region(inselix1, iy, 1) = 3
      END DO
      nnreg(1) = 3
! Y-flux component
      DO ix=inselix1,inselix2
! We find the core boundary: y-region 1
        iy = -1
        DO WHILE (iy .LE. inseliy .AND. (.NOT.ISREALCELL(cflag(ix, iy, &
&                 cellflag_type))))
          iy = iy + 1
        END DO
        IF (iy .LE. inseliy) region(ix, iy, 2) = 1
! We find the separatrix: y-region 2
        region(topix(ix, inseliy), topiy(ix, inseliy), 2) = 2
      END DO
! We find the contact to the main chamber wall: y-region 3
      DO ix=-1,nx
        iy = ny
        DO WHILE (iy .GT. inseliy .AND. cflag(ix, iy, cellflag_type) &
&                 .NE. grid_internal)
          IF (cflag(ix, iy, cellflag_type) .EQ. grid_boundary) THEN
            geotype = CELLGEOTYPE(crx(ix, iy, :), cry(ix, iy, :))
            IF (cflag(ix, iy, cellflag_topface) .NE. lefttargetindex(1) &
&               .AND. cflag(ix, iy, cellflag_topface) .NE. &
&               righttargetindex(1) .AND. cflag(ix, iy, cellflag_topface&
&               ) .NE. grid_undefined .AND. geotype .NE. cgeo_tria_notop&
&           ) region(topix(ix, iy), topiy(ix, iy), 2) = 3
            IF (cflag(ix, iy, cellflag_rightface) .NE. lefttargetindex(1&
&               ) .AND. cflag(ix, iy, cellflag_rightface) .NE. &
&               righttargetindex(1) .AND. cflag(ix, iy, &
&               cellflag_rightface) .NE. grid_undefined .AND. geotype &
&               .NE. cgeo_tria_noright) region(rightix(ix, iy), rightiy(&
&             ix, iy), 1) = -3
            IF (cflag(ix, iy, cellflag_leftface) .NE. lefttargetindex(1)&
&               .AND. cflag(ix, iy, cellflag_leftface) .NE. &
&               righttargetindex(1) .AND. cflag(ix, iy, &
&               cellflag_leftface) .NE. grid_undefined .AND. geotype &
&               .NE. cgeo_tria_noleft) region(ix, iy, 1) = -3
          END IF
          iy = iy - 1
        END DO
      END DO
      nnreg(2) = 3
    ELSE IF (nncut .EQ. 0 .AND. periodic_bc .EQ. 1 .AND. nnreg(0) .EQ. 1&
&   ) THEN
! cylindrical slab case
      region(inselix1, :, 1) = 1
      nnreg(1) = 1
      DO ix=inselix1,inselix2
        region(topix(ix, -1), topiy(ix, -1), 2) = 1
        region(ix, ny, 2) = 1
      END DO
      nnreg(2) = 2
    ELSE IF (topcut(1) .GE. -1) THEN
! we have at least 1 X-point
      IF (periodic_bc .EQ. 1) THEN
        iyt = inseliy - 1
      ELSE
        iyt = ny
      END IF
! We identify the structure index of the left target
      iy = topcut(1) + 1
      ix = leftcut(1) - 1
      lefttargetindex(1) = grid_undefined
      DO WHILE (ix .GT. -1 .AND. lefttargetindex(1) .EQ. grid_undefined)
        IF (cflag(ix, iy, cellflag_type) .EQ. grid_boundary) THEN
          geotype = CELLGEOTYPE(crx(ix, iy, :), cry(ix, iy, :))
          IF (geotype .NE. cgeo_tria_noleft .AND. cflag(ix, iy, &
&             cellflag_leftface) .NE. grid_undefined) THEN
            lefttargetindex(1) = cflag(ix, iy, cellflag_leftface)
