  SUBROUTINE READ_B2MOD_USER_NAMELIST(ns, geo, m, sw)
    USE B2MOD_B2CMPA_DIFFV
    USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ns
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(INOUT) :: m
    TYPE(SWITCHES), INTENT(IN) :: sw
    INTEGER :: ic, ifc
!
    LOGICAL :: file_ok
    CHARACTER(len=260) :: filename
    INTEGER :: i
    LOGICAL :: done, ifail, found
    INTEGER :: j, k, is, im
    LOGICAL :: old_style
    LOGICAL, SAVE :: hydrogen, helium, neon
    EXTERNAL IPGETI, FIND_FILE, XERTST
    INTRINSIC TRIM
    EXTERNAL XERRAB
    INTRINSIC MAXVAL
    INTRINSIC NINT
    REAL(kind=r8) :: result1
!
    CALL SUBINI('readuser')
!
!  -- USER --
    filename = user_filename
    CALL FIND_FILE(filename, file_ok)
    IF (file_ok) THEN
      OPEN(99, file=filename) 
      WRITE(*, *) 'Reading namelist user from ', TRIM(filename)
      READ(99, user) 
      CLOSE(99) 
      WRITE(*, *) 'Read namelist user from ', TRIM(filename)
      user_namelist_used = .true.
    ELSE
      WRITE(*, *) 'No ', TRIM(filename)
      user_namelist_used = .false.
      filedata(ntrc_usr) = .false.
    END IF
!
    nomp = 0
!Need to add check for monotonicity in list
    omp = 0
! Define OMP list by finding CVs which are intersected by the rzomp segment
    CALL CV_INTERSECTIONS_NODIFF(geo, m, rzomp, omp, nomp, nromp, 1, &
&                          ifail)
    CALL XERTST(.NOT.ifail, 'Increase DEF_NYD in b2mod_dimensions')
! Here we need to guarantee ordering of CVs. With classical grids the conversion
! is done such that CVs are ordered from left to right and bottom to top wrt
! the structured computational domain. With fully unstructured grids this is not guaranteed.
! IDEA: project the cell center onto the segment and order CVs based on distance wrt one of the segment's extremes
! SAME FOR IMP
    DO ic=2,nomp
      IF (geo%cvx(omp(ic)) .LT. geo%cvx(omp(ic-1))) CALL XERRAB(&
&                                                  'OMP LIST NOT SORTED'&
&                                                        )
    END DO
!   ..Definition of separatrix position in OMP list
    icsepomp = 0
    ic = 1
    found = .false.
    DO WHILE (ic .LE. nomp .AND. (.NOT.found))
      DO i=m%cvfcp(omp(ic), 1),m%cvfcp(omp(ic), 1)+m%cvfcp(omp(ic), 2)-1
        ifc = m%cvfc(i)
        IF ((m%fcfs(ifc) .EQ. m%ifssep .AND. m%ifssep .NE. 0) .OR. (m%&
&           fcfs(ifc) .EQ. m%ifssep2 .AND. m%ifssep2 .NE. 0)) THEN
          icsepomp = ic + 1
          ifsepomp = ifc
          found = .true.
        END IF
      END DO
      ic = ic + 1
    END DO
! csc get psi for each OMP cell
    psi_omp = 0.0_R8
    DO ic=1,nomp
      psi_omp(ic) = geo%cvfpsi(omp(ic))
    END DO
!
!xpb convert old-style j_h_at and l_h_mol to deal with isotope mixes
    result1 = MAXVAL(j_h_at)
    old_style = result1 .EQ. 0
    IF (j_h_at(1) .NE. 0 .AND. j_h_at(2) .EQ. 0 .AND. j_h_at(3) .EQ. 0) &
&     old_style = NINT(am(eb2atcr(j_h_at(1)))) .NE. 1
!*** Set default values according to current species list
    hydrogen = .false.
    done = .false.
    is = 0
    DO WHILE (is .LT. ns)
      hydrogen = hydrogen .OR. (is_neutral(is) .AND. NINT(zn(is)) .EQ. 1&
&       )
      IF (is_neutral(is) .AND. NINT(zn(is)) .EQ. 1) THEN
        IF (old_style .AND. NINT(am(is)) .NE. 1 .AND. (.NOT.done)) THEN
          IF (j_h_at(1) .NE. 0) THEN
            i = NINT(am(j_h_at(1)))
            j_h_at(i) = j_h_at(1)
            j_h_at(1) = 0
          ELSE
            i = NINT(am(is))
          END IF
          DO im=1,nnmoli
            l_h_mol(im, i) = l_h_mol(im, 1)
            l_h_mol(im, 1) = 0
          END DO
          done = .true.
        END IF
        IF (j_h_at(NINT(am(is))) .EQ. 0) j_h_at(NINT(am(is))) = b2eatcr(&
&           is)
      END IF
      is = is + 1
    END DO
    helium = .false.
    is = 0
    DO WHILE (.NOT.helium .AND. is .LT. ns)
      helium = is_neutral(is) .AND. NINT(zn(is)) .EQ. 2
      IF (helium .AND. j_he_at .EQ. 0) j_he_at = b2eatcr(is)
      is = is + 1
    END DO
    neon = .false.
    is = 0
    DO WHILE (.NOT.neon .AND. is .LT. ns)
      neon = is_neutral(is) .AND. NINT(zn(is)) .EQ. 10
      IF (neon .AND. j_ne_at .EQ. 0) j_ne_at = b2eatcr(is)
      is = is + 1
    END DO
!
!   ..test user namelist input
    CALL XERTST(0.0_R8 .LE. fusion_power, 'faulty input fusion_power')
    CALL XERTST(0.0_R8 .LE. spmp_nom, 'faulty input Spmp_nom')
    CALL XERTST(0.0_R8 .LE. spmp_he_to_d, 'faulty input Spmp_He_to_D')
    CALL XERTST(0 .LE. npfrgrp, 'faulty input npfrgrp')
    DO i=1,npfrgrp
      CALL XERTST(1 .LE. ipfrgrp(i) .AND. ipfrgrp(i) .LE. jpfrgrp(i), &
&           'faulty inputs ipfrgrp, jpfrgrp')
    END DO
    DO i=2,npfrgrp
      CALL XERTST(jpfrgrp(i-1) .LT. ipfrgrp(i), &
&           'faulty inputs ipfrgrp, jpfrgrp')
    END DO
    CALL XERTST(0 .LE. nntrgrp, 'faulty input nntrgrp')
    DO i=1,nntrgrp
      CALL XERTST(1 .LE. intrgrp(i) .AND. intrgrp(i) .LE. jntrgrp(i), &
&           'faulty inputs intrgrp, jntrgrp')
    END DO
    DO i=2,nntrgrp
      CALL XERTST(jntrgrp(i-1) .LT. intrgrp(i), &
&           'faulty inputs intrgrp, jntrgrp')
    END DO
    result1 = MAXVAL(j_h_at)
    CALL XERTST(.NOT.(hydrogen .AND. result1 .EQ. 0), &
&         'faulty input j_h_at')
    DO i=1,3
      IF (j_h_at(i) .NE. 0) THEN
        IF (hydrogen) CALL XERTST(NINT(zn(eb2atcr(j_h_at(i)))) .EQ. 1, &
&                           'faulty input j_h_at')
      END IF
    END DO
    DO i=1,2
      DO j=i+1,3
        IF (j_h_at(i) .NE. 0 .AND. j_h_at(j) .NE. 0) THEN
          IF (NINT(am(eb2atcr(j_h_at(i)))) .EQ. NINT(am(eb2atcr(j_h_at(j&
&             ))))) THEN
            WRITE(6, *) &
&           'Non-standard setting of hydrogen isotope species:'
            WRITE(6, '(1x,a,i3)') &
&           'More than one hydrogen species defined with mass ', NINT(am&
&           (eb2atcr(j_h_at(i))))
            WRITE(6, '(1x,a,3i3)') 'j_h_at : ', (j_h_at(k), k=1,3)
            WRITE(6, '(1x,a,3i3)') 'masses : ', (NINT(am(eb2atcr(j_h_at(&
&           k)))), k=1,3)
          END IF
        END IF
      END DO
    END DO
    IF (sw%b2stbr_neutrals_namelist .EQ. 1 .OR. sw%use_eirene .NE. 0) &
&   THEN
      DO i=1,3
        IF (j_h_at(i) .NE. 0) THEN
          IF (user_namelist_used) THEN
            result1 = MAXVAL(l_h_mol)
            IF (result1 .GT. 0) THEN
              DO is=1,nnmoli
                CALL XERTST(l_h_mol(is, i) .EQ. mlcmp(j_h_at(i), is), &
&                     'faulty inputs l_h_mol, mlcmp')
              END DO
            ELSE
              DO is=1,nnmoli
                l_h_mol(is, i) = mlcmp(j_h_at(i), is)
              END DO
            END IF
          END IF
        END IF
      END DO
    END IF
    IF (helium) CALL XERTST(NINT(zn(eb2atcr(j_he_at))) .EQ. 2, &
&                     'faulty input j_he_at')
    IF (neon) CALL XERTST(NINT(zn(eb2atcr(j_ne_at))) .EQ. 10, &
&                   'faulty input j_ne_at')
!
!   ..Create list of IMP control volumes
! Same logic as OMP list, find CVs intersected by rzimp segment
    nimp = 0
    imp = 0
    CALL CV_INTERSECTIONS_NODIFF(geo, m, rzimp, imp, nimp, nromp, 1, &
&                          ifail)
    CALL XERTST(.NOT.ifail, 'Increase DEF_NYD in b2mod_dimensions')
    DO ic=2,nimp
      IF (geo%cvx(imp(ic)) .LT. geo%cvx(imp(ic-1))) CALL XERRAB(&
&                                                  'IMP LIST NOT SORTED'&
&                                                        )
    END DO
! Find position of separatrix in IMP list (same as for OMP)
    icsepimp = 0
    ic = 1
    found = .false.
    DO WHILE (ic .LE. nimp .AND. (.NOT.found))
      DO i=m%cvfcp(imp(ic), 1),m%cvfcp(imp(ic), 1)+m%cvfcp(imp(ic), 2)-1
        ifc = m%cvfc(i)
        IF ((m%fcfs(ifc) .EQ. m%ifssep .AND. m%ifssep .NE. 0) .OR. (m%&
&           fcfs(ifc) .EQ. m%ifssep2 .AND. m%ifssep2 .NE. 0)) THEN
          icsepimp = ic + 1
          ifsepimp = ifc
          found = .true.
        END IF
      END DO
      ic = ic + 1
    END DO
    IF (.NOT.found) WRITE(*, '(a/a/a)') &
&         'Warning: did not find separatrix location in IMP definition,'&
&                   , &
&          '         check specification of RZIMP in b2.user.parameters'&
&                   , '         if IMP is needed.'
    IF (m%isclassicalgrid .EQ. 1) CALL XERTST(m%fsfcp(m%ifssep, 2) .GT. &
&                                       0, 'No separatrix faces!')
    WRITE(*, *) 'SEPARATRIX FLUX SURFACE AT iFs=', m%ifssep
    IF (m%ifssep2 .GT. 0) WRITE(*, *) &
&                   'SECOND HALF OF CDN SEPARATRIX FLUX SURFACE AT iFs='&
&                         , m%ifssep2
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE READ_B2MOD_USER_NAMELIST

