  SUBROUTINE CONV_BCPARMS(ns, m)
    USE B2MOD_GEO_DIFFV
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ns
    TYPE(MAPPING), INTENT(INOUT) :: m
    INTEGER :: ib, ibc, is, inbc, noss, iss, indss, i, ifc, icv, idb
    INTEGER :: bcs(nbc), bce(nbc), bcl(nbc)
    INTEGER :: minlbl(nbc), maxlbl(nbc)
    INTEGER, ALLOCATABLE :: ifchit(:)
    CHARACTER(len=1) :: lchar(nbc)
    CHARACTER(len=3) :: ss
    INTRINSIC COUNT
    EXTERNAL XERRAB
    INTRINSIC MIN
    INTRINSIC MAX
    EXTERNAL FIND_FACES
    REAL(kind=r8) :: result1
!
! number of boundary conditions
    m%nbc = nbc
! number of cells with imposed BC
    m%mxnbc = 0
    DO ib=1,nbc
      bcs(ib) = bcstart(ib)
      bce(ib) = bcend(ib)
      IF (bcchar(ib) .EQ. 'A') THEN
! find the number of faces for this boundary condition
!wdk need more complex test here; present scheme double counts;
!wdk also need to correct for degenerate faces?
        bcl(ib) = COUNT(cflags(:, :, 2:5) .GE. bcstart(ib) .AND. cflags(&
&         :, :, 2:5) .LE. bcend(ib))
      ELSE
! exclude corner cells if present
        IF (bcchar(ib) .EQ. 'N') THEN
          IF (m%imapcv(bottomix(bcstart(ib), bcpos(ib)), bottomiy(&
&             bcstart(ib), bcpos(ib))) .GT. m%nci) bcs(ib) = bcs(ib) + 1
          IF (m%imapcv(bottomix(bcend(ib), bcpos(ib)), bottomiy(bcend(ib&
&             ), bcpos(ib))) .GT. m%nci) bce(ib) = bce(ib) - 1
        ELSE IF (bcchar(ib) .EQ. 'S') THEN
          IF (m%imapcv(topix(bcstart(ib), bcpos(ib)), topiy(bcstart(ib)&
&             , bcpos(ib))) .GT. m%nci) bcs(ib) = bcs(ib) + 1
          IF (m%imapcv(topix(bcend(ib), bcpos(ib)), topiy(bcend(ib), &
&             bcpos(ib))) .GT. m%nci) bce(ib) = bce(ib) - 1
        ELSE IF (bcchar(ib) .EQ. 'E') THEN
          IF (m%imapcv(leftix(bcpos(ib), bcstart(ib)), leftiy(bcpos(ib)&
&             , bcstart(ib))) .GT. m%nci) bcs(ib) = bcs(ib) + 1
          IF (m%imapcv(leftix(bcpos(ib), bcend(ib)), leftiy(bcpos(ib), &
&             bcend(ib))) .GT. m%nci) bce(ib) = bce(ib) - 1
        ELSE IF (bcchar(ib) .EQ. 'W') THEN
          IF (m%imapcv(rightix(bcpos(ib), bcstart(ib)), rightiy(bcpos(ib&
&             ), bcstart(ib))) .GT. m%nci) bcs(ib) = bcs(ib) + 1
          IF (m%imapcv(rightix(bcpos(ib), bcend(ib)), rightiy(bcpos(ib)&
&             , bcend(ib))) .GT. m%nci) bce(ib) = bce(ib) - 1
        ELSE
          CALL XERRAB('bcchar not yet available for WG')
        END IF
        bcl(ib) = bce(ib) - bcs(ib) + 1
      END IF
      m%mxnbc = m%mxnbc + bcl(ib)
    END DO
    m%mxnbc = m%ncg
    IF (m%mxnbc .NE. m%ncg) CALL XERRAB('conv_bcparms -- wrong mxnBc')
!
    CALL ALLOC_MAPPING_BC(m)
    OPEN(newunit=idb, file='bound.out') 
!
    minlbl = HUGE(1)
    result1 = HUGE(1)
    maxlbl = -result1
    lchar = ' '
!
! index in bcCv
    inbc = 0
    DO ib=1,nbc
      m%bcfcp(ib, 1) = inbc + 1
      m%bcfcp(ib, 2) = bcl(ib)
      IF (bcchar(ib) .EQ. 'S') THEN
        DO ibc=1,bcl(ib)
! number of the corresponding domain face
          inbc = inbc + 1
          m%bcfc(inbc) = m%cvfc(m%cvfcp(m%imapcv(bcs(ib)+ibc-1, bcpos(ib&
&           )), 1))
          m%bcfcor(inbc) = -1.0_R8
          IF (minlbl(ib) .GT. m%fclbl(m%bcfc(inbc))) THEN
            minlbl(ib) = m%fclbl(m%bcfc(inbc))
          ELSE
            minlbl(ib) = minlbl(ib)
          END IF
          IF (maxlbl(ib) .LT. m%fclbl(m%bcfc(inbc))) THEN
            maxlbl(ib) = m%fclbl(m%bcfc(inbc))
          ELSE
            maxlbl(ib) = maxlbl(ib)
          END IF
          lchar(ib) = 'A'
        END DO
      ELSE IF (bcchar(ib) .EQ. 'N') THEN
        DO ibc=1,bcl(ib)
          inbc = inbc + 1
          m%bcfc(inbc) = m%cvfc(m%cvfcp(m%imapcv(bcs(ib)+ibc-1, bcpos(ib&
&           )), 1))
          m%bcfcor(inbc) = 1.0_R8
          IF (minlbl(ib) .GT. m%fclbl(m%bcfc(inbc))) THEN
            minlbl(ib) = m%fclbl(m%bcfc(inbc))
          ELSE
            minlbl(ib) = minlbl(ib)
          END IF
          IF (maxlbl(ib) .LT. m%fclbl(m%bcfc(inbc))) THEN
            maxlbl(ib) = m%fclbl(m%bcfc(inbc))
          ELSE
            maxlbl(ib) = maxlbl(ib)
          END IF
          lchar(ib) = 'A'
        END DO
      ELSE IF (bcchar(ib) .EQ. 'W') THEN
        DO ibc=1,bcl(ib)
          inbc = inbc + 1
          m%bcfc(inbc) = m%cvfc(m%cvfcp(m%imapcv(bcpos(ib), bcs(ib)+ibc-&
&           1), 1))
          m%bcfcor(inbc) = -1.0_R8
          IF (minlbl(ib) .GT. m%fclbl(m%bcfc(inbc))) THEN
            minlbl(ib) = m%fclbl(m%bcfc(inbc))
          ELSE
            minlbl(ib) = minlbl(ib)
          END IF
          IF (maxlbl(ib) .LT. m%fclbl(m%bcfc(inbc))) THEN
            maxlbl(ib) = m%fclbl(m%bcfc(inbc))
          ELSE
            maxlbl(ib) = maxlbl(ib)
          END IF
          lchar(ib) = 'A'
        END DO
      ELSE IF (bcchar(ib) .EQ. 'E') THEN
        DO ibc=1,bcl(ib)
          inbc = inbc + 1
          m%bcfc(inbc) = m%cvfc(m%cvfcp(m%imapcv(bcpos(ib), bcs(ib)+ibc-&
&           1), 1))
          m%bcfcor(inbc) = 1.0_R8
          IF (minlbl(ib) .GT. m%fclbl(m%bcfc(inbc))) THEN
            minlbl(ib) = m%fclbl(m%bcfc(inbc))
          ELSE
            minlbl(ib) = minlbl(ib)
          END IF
          IF (maxlbl(ib) .LT. m%fclbl(m%bcfc(inbc))) THEN
            maxlbl(ib) = m%fclbl(m%bcfc(inbc))
          ELSE
            maxlbl(ib) = maxlbl(ib)
          END IF
          lchar(ib) = 'A'
        END DO
      ELSE IF (bcchar(ib) .EQ. 'A') THEN
        minlbl(ib) = bcstart(ib)
        maxlbl(ib) = bcend(ib)
        lchar(ib) = 'A'
        bcl(ib) = 0
        noss = bcend(ib) - bcstart(ib) + 1
        DO iss=1,noss
          indss = bcstart(ib) + iss - 1
          WRITE(*, *) ' surface index ', indss
!  find all faces belonging to surface structure INDSS
          CALL FIND_FACES(indss, inbc, bcl(ib), m%mxnbc, m%bcfc, m%&
&                   bcfcor, m, idb)
        END DO
! iss
        WRITE(ss, '(i3)') ib
        CALL XERTST(bcl(ib) .GT. 0, &
&             'No faces found for boundary condition = '//ss)
        m%bcfcp(ib, 2) = bcl(ib)
      ELSE
        CALL XERRAB('conv_bcparms -- wrong BCCHAR')
      END IF
    END DO
    CLOSE(idb) 
!
    acchar(1:nbc) = lchar(1:nbc)
    abcstart(1:nbc) = minlbl(1:nbc)
! now check that all boundary faces are assigned to a BC
    abcend(1:nbc) = maxlbl(1:nbc)
!
    CALL CHECK_BOUNDARY_LABELS_NODIFF(m, nbc, abcstart(1:nbc), abcend(1:&
&                               nbc), .true.)
!
    IF (inbc .NE. m%ncg) THEN
      WRITE(*, *) 'inbc, nCg ', inbc, m%ncg
! try to find missing or doubled faces
      ALLOCATE(ifchit(m%nfc))
      ifchit = 0
! for all guard cells mark face to plasma cell
      DO i=1,m%ncg
        icv = m%nci + i
        ifc = m%cvfc(m%cvfcp(icv, 1))
        ifchit(ifc) = ifchit(ifc) + 1
      END DO
! remove mark from boundary faces
      DO ib=1,m%nbc
        DO i=1,m%bcfcp(ib, 2)
          ifc = m%bcfc(i)
          ifchit(ifc) = ifchit(ifc) - 1
        END DO
      END DO
      IF (COUNT(ifchit .NE. 0) .GT. 0) THEN
        WRITE(*, *) 'suspicious faces '
        DO i=1,m%nfc
          IF (ifchit(i) .NE. 0) THEN
            WRITE(*, *) 'iFc ', i, ifchit(i)
            WRITE(*, *) 'fcCv ', m%fccv(i, 1), m%fccv(i, 2)
          END IF
        END DO
      END IF
!
      CALL XERRAB('conv_bcparms -- problem setting up bc arrays')
    END IF
!
! now convert feedback BC: BCCON 11/12-->8, BCENE/I/POT 10-->8
    DO ib=1,m%nbc
      IF (bcene(ib) .EQ. 10) bcene(ib) = 8
      IF (bceni(ib) .EQ. 10) bceni(ib) = 8
      IF (bcpot(ib) .EQ. 10) bcpot(ib) = 8
      DO is=0,ns-1
        IF (bccon(is, ib) .EQ. 11 .OR. bccon(is, ib) .EQ. 12) bccon(is, &
&         ib) = 8
      END DO
    END DO
!
    RETURN
  END SUBROUTINE CONV_BCPARMS

