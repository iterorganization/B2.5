  FUNCTION GET_PHI_APPLIED(boundary_namelist, mpg, ifc)
    USE B2MOD_TYPES
    USE B2MOD_BOUNDARY_SOURCES_DIFFV
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!
    REAL(kind=r8) :: get_phi_applied
    TYPE(MAPPING), INTENT(IN) :: mpg
    INTEGER, INTENT(IN) :: boundary_namelist, ifc
    INTEGER :: i, ib, icv
    LOGICAL :: found
    INTRINSIC MIN
!
    get_phi_applied = 0.0_R8
    IF (boundary_namelist .EQ. 1) THEN
      ib = 1
      found = .false.
      DO WHILE (ib .LE. nbc .AND. (.NOT.found))
        IF ((((bcchar(ib) .EQ. 'S' .OR. bcchar(ib) .EQ. 'N') .OR. bcchar&
&           (ib) .EQ. 'E') .OR. bcchar(ib) .EQ. 'W') .OR. bcchar(ib) &
&           .EQ. 'A') THEN
          i = 1
          DO WHILE (i .LE. mpg%bcfcp(ib, 2) .AND. (.NOT.found))
            found = mpg%bcfc(mpg%bcfcp(ib, 1)+i-1) .EQ. ifc
            i = i + 1
          END DO
        END IF
        IF (.NOT.found) ib = ib + 1
      END DO
      IF (found) THEN
        SELECT CASE  (bcpot(ib)) 
        CASE (1, 4, 21, 22) 
          get_phi_applied = potpar(ib, 1)
        CASE (3, 11) 
          get_phi_applied = potpar(ib, 2)
        CASE (7) 
          IF (mpg%fccv(ifc, 1) .GT. mpg%fccv(ifc, 2)) THEN
            icv = mpg%fccv(ifc, 2)
          ELSE
            icv = mpg%fccv(ifc, 1)
          END IF
          get_phi_applied = bv_po(icv)
        END SELECT
      END IF
    END IF
    RETURN
  END FUNCTION GET_PHI_APPLIED

!  Differentiation of getphiapplied in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: getphiapplied
!   with respect to varying inputs: potpar
!
  SUBROUTINE GETPHIAPPLIED_DV(boundary_namelist, mpg, icv, getphiapplied&
&   , getphiappliedd, nbdirs)
    USE B2MOD_TYPES
    USE B2US_MAP_DIFFV
    USE B2MOD_BOUNDARY_SOURCES_DIFFV
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!
    REAL(kind=r8) :: getphiapplied
    REAL(kind=r8), DIMENSION(nbdirsmax) :: getphiappliedd
    INTEGER :: boundary_namelist, icv
    TYPE(MAPPING), INTENT(IN) :: mpg
    INTEGER :: i, ib
    LOGICAL :: found
    INTEGER :: nd
    INTEGER :: nbdirs
!
    getphiapplied = 0.0_R8
    IF (boundary_namelist .EQ. 1) THEN
      ib = 1
      found = .false.
      DO WHILE (ib .LE. nbc .AND. (.NOT.found))
        IF (((bcchar(ib) .EQ. 'S' .OR. bcchar(ib) .EQ. 'N') .OR. bcchar(&
&           ib) .EQ. 'E') .OR. bcchar(ib) .EQ. 'W') THEN
          i = 1
          DO WHILE (i .LE. mpg%bccvp(ib, 2) .AND. (.NOT.found))
            found = mpg%bccv(mpg%bccvp(ib, 1)+i-1, 1) .EQ. icv
            i = i + 1
          END DO
        END IF
        IF (.NOT.found) ib = ib + 1
      END DO
      IF (found) THEN
        SELECT CASE  (bcpot(ib)) 
        CASE (1, 4, 21, 22) 
          DO nd=1,nbdirs
            getphiappliedd(nd) = potpard(nd, ib, 1)
          END DO
          getphiapplied = potpar(ib, 1)
        CASE (3, 11) 
          DO nd=1,nbdirs
