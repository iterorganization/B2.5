          END DO
        CASE DEFAULT
          DO nd=1,nbdirsmax
            getphiappliedd(nd) = 0.D0
          END DO
        END SELECT
      ELSE
        DO nd=1,nbdirsmax
          getphiappliedd(nd) = 0.D0
        END DO
      END IF
    ELSE
      DO nd=1,nbdirsmax
        getphiappliedd(nd) = 0.D0
      END DO
    END IF
    RETURN
  END SUBROUTINE GETPHIAPPLIED_DV

