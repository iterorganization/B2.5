  SUBROUTINE READ_B2MOD_BOUNDARY_NAMELIST_ST(nx, ny, ns, dotest)
    USE B2MOD_INDIRECT_DIFFV
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: nx, ny, ns
    LOGICAL, INTENT(IN) :: dotest
    INTEGER :: is, ib, ic, ix, iy, ixx, iyy, iyn
    LOGICAL :: covered, corner, guard_cell, found
    LOGICAL :: is_flux_surface
!
    LOGICAL :: file_ok, todo, assigned
    LOGICAL, ALLOCATABLE :: old_style(:), done(:)
    CHARACTER(len=260) :: filename
    EXTERNAL FIND_FILE, XERRAB, XERTST
    INTRINSIC TRIM
    INTRINSIC MAXVAL
    REAL(kind=r8) :: result1
!
    filename = boundary_filename
    boundary_time_mod = 0.0_R8
    boundary_time_switch = 0.0_R8
    CALL FIND_FILE(filename, file_ok)
    IF (file_ok) THEN
      OPEN(99, file=filename) 
      CALL CFVERR(99, boundary_version)
      WRITE(*, *) 'Reading namelist boundary from ', TRIM(filename)
      READ(99, boundary) 
      WRITE(*, *) 'Read namelist boundary from ', TRIM(filename)
      CLOSE(99) 
    ELSE
      CALL XERRAB('No '//TRIM(filename))
    END IF
!
    acchar = bcchar
    abcstart = bcstart
    abcend = bcend
!
    IF (nbc .GT. 0) THEN
      ALLOCATE(old_style(nbc))
      DO ib=1,nbc
        IF (bc_list_size(ib) .EQ. 0) THEN
          IF (bcchar(ib) .EQ. 'A') THEN
! Automatic creation of boundary condition list from structures
            old_style(ib) = .false.
            CALL BUILD_BOUNDARY_LIST(bcstart(ib), bcend(ib), &
&                              bc_list_size(ib), bc_list_x(:, ib), &
&                              bc_list_y(:, ib), bc_list_char(:, ib))
          ELSE
! Automatic creation of boundary condition list from old style cell block definition
            old_style(ib) = .true.
            bc_list_size(ib) = bcend(ib) - bcstart(ib) + 1
            bc_list_char(1:bc_list_size(ib), ib) = bcchar(ib)
            IF ((bcchar(ib) .EQ. 'N' .OR. bcchar(ib) .EQ. 'S') .OR. &
&               bcchar(ib) .EQ. 'X') THEN
              DO ix=bcstart(ib),bcend(ib)
                bc_list_x(ix-bcstart(ib)+1, ib) = ix
              END DO
              bc_list_y(1:bc_list_size(ib), ib) = bcpos(ib)
            ELSE IF ((bcchar(ib) .EQ. 'E' .OR. bcchar(ib) .EQ. 'W') .OR.&
&               bcchar(ib) .EQ. 'Y') THEN
              bc_list_x(1:bc_list_size(ib), ib) = bcpos(ib)
              DO iy=bcstart(ib),bcend(ib)
                bc_list_y(iy-bcstart(ib)+1, ib) = iy
              END DO
            END IF
          END IF
        ELSE
          old_style(ib) = .false.
        END IF
      END DO
    END IF
    isonfluxboundary = .false.
    DO ib=1,nbc
      is_flux_surface = bcchar(ib) .EQ. 'N' .OR. bcchar(ib) .EQ. 'S'
      IF (bcchar(ib) .EQ. 'A') THEN
        ix = 1
        is_flux_surface = bc_list_char(ix, ib) .EQ. 'N' .OR. &
&         bc_list_char(ix, ib) .EQ. 'S'
        DO WHILE (is_flux_surface .AND. ix .LT. bc_list_size(ib))
          IF (bc_list_char(ix, ib) .EQ. 'N') THEN
            iy = bottomiy(bc_list_x(ix, ib), bc_list_y(ix, ib))
          ELSE IF (bc_list_char(ix, ib) .EQ. 'S') THEN
            iy = topiy(bc_list_x(ix, ib), bc_list_y(ix, ib))
          END IF
          IF (bc_list_char(ix+1, ib) .EQ. 'N') THEN
            iyn = bottomiy(bc_list_x(ix+1, ib), bc_list_y(ix+1, ib))
          ELSE IF (bc_list_char(ix+1, ib) .EQ. 'S') THEN
            iyn = topiy(bc_list_x(ix+1, ib), bc_list_y(ix+1, ib))
          END IF
          is_flux_surface = is_flux_surface .AND. iy .EQ. iyn .AND. &
&           bc_list_char(ix, ib) .EQ. bc_list_char(ix+1, ib)
          ix = ix + 1
        END DO
      END IF
      IF (is_flux_surface) THEN
        DO ix=1,bc_list_size(ib)
          isonfluxboundary(bc_list_x(ix, ib), bc_list_y(ix, ib)) = &
&           .true.
        END DO
      END IF
    END DO
!
!.. Make a list of core boundaries
    result1 = MAXVAL(lcbs)
    todo = result1 .GT. 0
    IF (.NOT.todo) THEN
      ncbs = 0
    ELSE
      ncbs = 1
      DO WHILE (lcbs(ncbs+1) .GT. 0 .AND. ncbs .LT. nbc)
        ncbs = ncbs + 1
      END DO
    END IF
    ALLOCATE(done(nbc))
    done = .false.
    DO ib=1,nbc
      IF (bcchar(ib) .EQ. 'S' .OR. bcchar(ib) .EQ. 'A') THEN
        IF (old_style(ib)) THEN
          IF (on_closed_surface(bcstart(ib), bcpos(ib)) .AND. &
&             on_closed_surface(bcend(ib), bcpos(ib))) THEN
            IF (.NOT.todo) THEN
              ncbs = ncbs + 1
              lcbs(ncbs) = ib
              done(ncbs) = .true.
            ELSE
              ic = 1
              assigned = .false.
              DO WHILE (ic .LE. ncbs)
                IF (lcbs(ic) .EQ. ib) THEN
                  CALL XERTST(.NOT.done(ic), &
&                       'Core boundary already done!')
                  assigned = .true.
                  done(ic) = .true.
                END IF
                ic = ic + 1
              END DO
              IF (.NOT.assigned) THEN
                ncbs = ncbs + 1
                done(ncbs) = .true.
                lcbs(ncbs) = ib
                WRITE(*, *) 'Added boundary ', ib, &
&               ' to list of core boundaries'
              END IF
            END IF
          END IF
        ELSE
          found = .true.
          DO ix=1,bc_list_size(ib)
            found = found .AND. on_closed_surface(bc_list_x(ix, ib), &
&             bc_list_y(ix, ib))
          END DO
          IF (found) THEN
            IF (.NOT.todo) THEN
              ncbs = ncbs + 1
              lcbs(ncbs) = ib
              done(ncbs) = .true.
            ELSE
              ic = 1
              assigned = .false.
              DO WHILE (ic .LE. ncbs)
                IF (lcbs(ic) .EQ. ib) THEN
                  CALL XERTST(.NOT.done(ic), &
&                       'Core boundary already done!')
                  assigned = .true.
                  done(ic) = .true.
                END IF
                ic = ic + 1
              END DO
              IF (.NOT.assigned) THEN
                ncbs = ncbs + 1
                done(ncbs) = .true.
                lcbs(ncbs) = ib
                WRITE(*, *) 'Added boundary ', ib, &
&               ' to list of core boundaries'
              END IF
            END IF
          END IF
        END IF
      END IF
    END DO
    IF (ny .GT. 1) THEN
      IF (nnreg(0) .GT. 1 .OR. periodic_bc .EQ. 1) CALL XERTST(1 .LE. &
&                                                        ncbs, &
&                                               'faulty parameters ncbs'&
&                                                       )
    END IF
    DO ib=1,ncbs
      CALL XERTST(1 .LE. lcbs(ib) .AND. lcbs(ib) .LE. nbc, &
&           'faulty parameter lcbs')
      CALL XERTST(done(ib), 'Did not find all declared core boundaries!'&
&          )
    END DO
    DEALLOCATE(done)
    IF ((.NOT.file_ok) .OR. (.NOT.dotest)) THEN
      IF (.NOT.file_ok) WRITE(*, *) 'No ', TRIM(filename)
      WRITE(*, *) 'Testing skipped!'
      RETURN
    ELSE
!
!   ..test input
      CALL XERTST(0 .LE. nbc, 'faulty input nbc')
      CALL XERTST(0 .LE. nniso, 'faulty input nniso')
      CALL XERTST(0.0_R8 .LE. gammae, 'faulty input gammae')
      CALL XERTST(0.0_R8 .LE. gammai, 'faulty input gammai')
      CALL XERTST(0.0_R8 .LT. teiso, 'faulty input teiso')
      CALL XERTST(0.0_R8 .LT. tiiso, 'faulty input tiiso')
      CALL XERTST(0.0_R8 .LE. boundary_time_mod, &
&           'faulty input boundary_time_mod')
      CALL XERTST(0.0_R8 .LE. boundary_time_switch, &
&           'faulty input boundary_time_switch')
      DO is=0,ns-1
        CALL XERTST(0.0_R8 .LT. niiso(is), 'faulty input niiso')
      END DO
      DO ib=1,nbc
        IF (bcchar(ib) .EQ. 'N') THEN
          IF (old_style(ib)) THEN
            CALL XERTST(1 .LE. bcpos(ib) .AND. bcpos(ib) .LE. ny + 1, &
&                 'faulty input bcpos (North)')
            CALL XERTST(-1 .LE. bcstart(ib) .AND. bcstart(ib) .LE. nx + &
&                 1, 'faulty input bcstart (North)')
            CALL XERTST(-1 .LE. bcend(ib) .AND. bcend(ib) .LE. nx + 1, &
&                 'faulty input bcend (North)')
          ELSE
            DO ic=1,bc_list_size(ib)
              CALL XERTST(1 .LE. bc_list_y(ic, ib) .AND. bc_list_y(ic, &
&                   ib) .LE. ny + 1, 'faulty input bc_list_y (North)')
              CALL XERTST(-1 .LE. bc_list_x(ic, ib) .AND. bc_list_x(ic, &
&                   ib) .LE. nx + 1, 'faulty input bc_list_x (North)')
            END DO
          END IF
        ELSE IF (bcchar(ib) .EQ. 'S') THEN
          IF (old_style(ib)) THEN
            CALL XERTST(-1 .LE. bcpos(ib) .AND. bcpos(ib) .LE. ny - 1, &
&                 'faulty input bcpos (South)')
            CALL XERTST(-1 .LE. bcstart(ib) .AND. bcstart(ib) .LE. nx + &
&                 1, 'faulty input bcstart (South)')
            CALL XERTST(-1 .LE. bcend(ib) .AND. bcend(ib) .LE. nx + 1, &
&                 'faulty input bcend (South)')
          ELSE
            DO ic=1,bc_list_size(ib)
              CALL XERTST(-1 .LE. bc_list_y(ic, ib) .AND. bc_list_y(ic, &
&                   ib) .LE. ny - 1, 'faulty input bc_list_y (South)')
              CALL XERTST(-1 .LE. bc_list_x(ic, ib) .AND. bc_list_x(ic, &
&                   ib) .LE. nx + 1, 'faulty input bc_list_x (South)')
            END DO
          END IF
        ELSE IF (bcchar(ib) .EQ. 'E') THEN
          IF (old_style(ib)) THEN
            CALL XERTST(1 .LE. bcpos(ib) .AND. bcpos(ib) .LE. nx + 1, &
&                 'faulty input bcpos (East)')
            CALL XERTST(-1 .LE. bcstart(ib) .AND. bcstart(ib) .LE. ny + &
&                 1, 'faulty input bcstart (East)')
            CALL XERTST(-1 .LE. bcend(ib) .AND. bcend(ib) .LE. ny + 1, &
&                 'faulty input bcend (East)')
          ELSE
            DO ic=1,bc_list_size(ib)
              CALL XERTST(-1 .LE. bc_list_y(ic, ib) .AND. bc_list_y(ic, &
&                   ib) .LE. ny + 1, 'faulty input bc_list_y (East)')
              CALL XERTST(1 .LE. bc_list_x(ic, ib) .AND. bc_list_x(ic, &
&                   ib) .LE. nx + 1, 'faulty input bc_list_x (East)')
            END DO
          END IF
        ELSE IF (bcchar(ib) .EQ. 'W') THEN
          IF (old_style(ib)) THEN
            CALL XERTST(-1 .LE. bcpos(ib) .AND. bcpos(ib) .LE. nx - 1, &
&                 'faulty input bcpos (West)')
            CALL XERTST(-1 .LE. bcstart(ib) .AND. bcstart(ib) .LE. ny + &
&                 1, 'faulty input bcstart (West)')
            CALL XERTST(-1 .LE. bcend(ib) .AND. bcend(ib) .LE. ny + 1, &
&                 'faulty input bcend (West)')
          ELSE
            DO ic=1,bc_list_size(ib)
              CALL XERTST(-1 .LE. bc_list_y(ic, ib) .AND. bc_list_y(ic, &
&                   ib) .LE. ny + 1, 'faulty input bc_list_y (West)')
              CALL XERTST(-1 .LE. bc_list_x(ic, ib) .AND. bc_list_x(ic, &
&                   ib) .LE. nx - 1, 'faulty input bc_list_x (West)')
            END DO
          END IF
        ELSE IF (bcchar(ib) .EQ. 'X') THEN
          IF (old_style(ib)) THEN
            CALL XERTST(0 .LE. bcpos(ib) .AND. bcpos(ib) .LE. ny, &
&                 'faulty input bcpos (X)')
            CALL XERTST(-1 .LE. bcstart(ib) .AND. bcstart(ib) .LE. nx + &
&                 1, 'faulty input bcstart (X)')
            CALL XERTST(-1 .LE. bcend(ib) .AND. bcend(ib) .LE. nx + 1, &
&                 'faulty input bcend (X)')
          ELSE
            DO ic=1,bc_list_size(ib)
              CALL XERTST(0 .LE. bc_list_y(ic, ib) .AND. bc_list_y(ic, &
&                   ib) .LE. ny, 'faulty input bc_list_y (X)')
              CALL XERTST(-1 .LE. bc_list_x(ic, ib) .AND. bc_list_x(ic, &
&                   ib) .LE. nx + 1, 'faulty input bc_list_x (X)')
            END DO
          END IF
        ELSE IF (bcchar(ib) .EQ. 'Y') THEN
          IF (old_style(ib)) THEN
            CALL XERTST(0 .LE. bcpos(ib) .AND. bcpos(ib) .LE. nx, &
&                 'faulty input bcpos (Y)')
            CALL XERTST(-1 .LE. bcstart(ib) .AND. bcstart(ib) .LE. ny + &
&                 1, 'faulty input bcstart (Y)')
            CALL XERTST(-1 .LE. bcend(ib) .AND. bcend(ib) .LE. ny + 1, &
&                 'faulty input bcend (Y)')
          ELSE
            DO ic=1,bc_list_size(ib)
              CALL XERTST(-1 .LE. bc_list_y(ic, ib) .AND. bc_list_y(ic, &
&                   ib) .LE. ny + 1, 'faulty input bc_list_y (Y)')
              CALL XERTST(0 .LE. bc_list_x(ic, ib) .AND. bc_list_x(ic, &
&                   ib) .LE. nx, 'faulty input bc_list_x (Y)')
            END DO
          END IF
        END IF
        IF (old_style(ib)) CALL XERTST(bcstart(ib) .LE. bcend(ib), &
&                                'faulty inputs bcstart > bcend')
        DO is=0,ns-1
          CALL XERTST(0 .LE. bccon(is, ib), 'faulty input bccon')
          CALL XERTST(0 .LE. bcmom(is, ib), 'faulty input bcmom')
        END DO
        CALL XERTST(0 .LE. bcene(ib), 'faulty input bcene')
        CALL XERTST(0 .LE. bceni(ib), 'faulty input bcene')
        CALL XERTST(0 .LE. bcpot(ib), 'faulty input bcpot')
        CALL XERTST(0 .LE. bcenk(ib), 'faulty input bcenk')
        CALL XERTST(0 .LE. bcenz(ib), 'faulty input bcenz')
      END DO
      DO ib=1,nniso
        CALL XERTST(-1 .LE. nxiso1(ib) .AND. nxiso1(ib) .LE. nx, &
&             'faulty input nxiso1')
        CALL XERTST(-1 .LE. nxiso2(ib) .AND. nxiso2(ib) .LE. nx, &
&             'faulty input nxiso2')
        CALL XERTST(-1 .LE. nyiso1(ib) .AND. nyiso1(ib) .LE. ny, &
&             'faulty input nyiso1')
        CALL XERTST(-1 .LE. nyiso2(ib) .AND. nyiso2(ib) .LE. ny, &
&             'faulty input nyiso2')
        DO ix=nxiso1(ib),nxiso2(ib)
          DO iy=nyiso1(ib),nyiso2(ib)
            region(ix, iy, 0) = 0
            resignore(ix, iy, 1) = 0
            resignore(ix, iy, 2) = 0
          END DO
        END DO
      END DO
      DO ib=1,nniso
        DO ix=nxiso1(ib),nxiso2(ib)
          DO iy=nyiso1(ib),nyiso2(ib)
            DO ixx=-1,nx
              DO iyy=-1,ny
                IF (region(ixx, iyy, 0) .NE. 0) THEN
                  IF (leftix(ixx, iyy) .EQ. ix .AND. leftiy(ixx, iyy) &
&                     .EQ. iy) THEN
                    WRITE(*, *) 'updated left neighbour of ', ixx, iyy
                    leftix(ixx, iyy) = -2
                    leftiy(ixx, iyy) = -2
                  END IF
                  IF (rightix(ixx, iyy) .EQ. ix .AND. rightiy(ixx, iyy) &
&                     .EQ. iy) THEN
                    WRITE(*, *) 'updated right neighbour of ', ixx, iyy
                    rightix(ixx, iyy) = nx + 1
                    rightiy(ixx, iyy) = ny + 1
                  END IF
                  IF (bottomix(ixx, iyy) .EQ. ix .AND. bottomiy(ixx, iyy&
&                     ) .EQ. iy) THEN
                    WRITE(*, *) 'updated bottom neighbour of ', ixx, iyy
                    bottomix(ixx, iyy) = -2
                    bottomiy(ixx, iyy) = -2
                  END IF
                  IF (topix(ixx, iyy) .EQ. ix .AND. topiy(ixx, iyy) .EQ.&
&                     iy) THEN
                    WRITE(*, *) 'updated top neighbour of ', ixx, iyy
                    topix(ixx, iyy) = nx + 1
                    topiy(ixx, iyy) = ny + 1
                  END IF
                END IF
              END DO
            END DO
          END DO
        END DO
      END DO
!..  test that all boundary cells to the real domain are covered at least once
!..  we exclude corners from this test
      DO ix=-1,nx
        DO iy=-1,ny
          guard_cell = region(ix, iy, 0) .NE. 0 .AND. (((leftix(ix, iy) &
&           .EQ. -2 .OR. rightix(ix, iy) .EQ. nx + 1) .OR. bottomiy(ix, &
&           iy) .EQ. -2) .OR. topiy(ix, iy) .EQ. ny + 1)
          corner = (leftix(ix, iy) .EQ. -2 .OR. rightix(ix, iy) .EQ. nx &
&           + 1) .AND. (bottomiy(ix, iy) .EQ. -2 .OR. topiy(ix, iy) .EQ.&
&           ny + 1)
          covered = .false.
          DO ib=1,nbc
            IF (old_style(ib)) THEN
              IF (bcchar(ib) .EQ. 'N' .OR. bcchar(ib) .EQ. 'S') covered&
&                = covered .OR. (iy .EQ. bcpos(ib) .AND. ix .GE. bcstart&
&                 (ib) .AND. ix .LE. bcend(ib))
              IF (bcchar(ib) .EQ. 'E' .OR. bcchar(ib) .EQ. 'W') covered&
&                = covered .OR. (ix .EQ. bcpos(ib) .AND. iy .GE. bcstart&
&                 (ib) .AND. iy .LE. bcend(ib))
            ELSE
              DO ic=1,bc_list_size(ib)
                covered = covered .OR. (iy .EQ. bc_list_y(ic, ib) .AND. &
&                 ix .EQ. bc_list_x(ic, ib))
              END DO
            END IF
          END DO
          IF (guard_cell .AND. (.NOT.covered) .AND. (.NOT.corner)) THEN
            WRITE(*, '(a,i4,a,i4,a)') 'Guard cell (', ix, ',', iy, &
&           ') not included in b2.boundary.parameters!'
            CALL XERRAB('Incomplete boundary definition')
          END IF
        END DO
      END DO
!..  test that boundary cells are guard cells
      DO ib=1,nbc
        IF (bcchar(ib) .EQ. 'N') THEN
          IF (old_style(ib)) THEN
            DO ix=bcstart(ib),bcend(ib)
              CALL XERTST(topiy(ix, bcpos(ib)) .EQ. ny + 1, &
&                   'inconsistent North boundary!')
            END DO
          ELSE
            DO ic=1,bc_list_size(ib)
              CALL XERTST(topiy(bc_list_x(ic, ib), bc_list_y(ic, ib)) &
&                   .EQ. ny + 1, 'inconsistent North boundary!')
            END DO
          END IF
        END IF
        IF (bcchar(ib) .EQ. 'S') THEN
          IF (old_style(ib)) THEN
            DO ix=bcstart(ib),bcend(ib)
              CALL XERTST(bottomiy(ix, bcpos(ib)) .EQ. -2, &
&                   'inconsistent South boundary!')
            END DO
          ELSE
            DO ic=1,bc_list_size(ib)
              CALL XERTST(bottomiy(bc_list_x(ic, ib), bc_list_y(ic, ib))&
&                   .EQ. -2, 'inconsistent South boundary!')
            END DO
          END IF
        END IF
        IF (bcchar(ib) .EQ. 'E') THEN
          IF (old_style(ib)) THEN
            DO iy=bcstart(ib),bcend(ib)
              CALL XERTST(rightix(bcpos(ib), iy) .EQ. nx + 1, &
&                   'inconsistent East boundary!')
            END DO
          ELSE
            DO ic=1,bc_list_size(ib)
              CALL XERTST(rightix(bc_list_x(ic, ib), bc_list_y(ic, ib)) &
&                   .EQ. nx + 1, 'inconsistent East boundary!')
            END DO
          END IF
        END IF
        IF (bcchar(ib) .EQ. 'W') THEN
          IF (old_style(ib)) THEN
            DO iy=bcstart(ib),bcend(ib)
              CALL XERTST(leftix(bcpos(ib), iy) .EQ. -2, &
&                   'inconsistent West boundary!')
            END DO
          ELSE
            DO ic=1,bc_list_size(ib)
              CALL XERTST(leftix(bc_list_x(ic, ib), bc_list_y(ic, ib)) &
&                   .EQ. -2, 'inconsistent West boundary!')
            END DO
          END IF
        END IF
      END DO
!.. Test consistency with region boundaries
      DO ic=1,nncut
        found = .false.
        ib = 0
        DO WHILE (ib .LT. nbc .AND. (.NOT.found))
          ib = ib + 1
          IF (old_style(ib)) THEN
            found = bcend(ib) .EQ. leftcut(ic) - 1 .AND. bcchar(ib) .EQ.&
&             'S'
          ELSE
            ix = 1
            DO WHILE (.NOT.found .AND. ix .LE. bc_list_size(ib))
              found = bc_list_x(ix, ib) .EQ. leftcut(ic) - 1 .AND. &
&               bc_list_y(ix, ib) .LE. topcut(ic)
              ix = ix + 1
            END DO
          END IF
        END DO
        CALL XERTST(found, &
&             'Mismatch (1) with region boundaries (South) !')
        found = .false.
        ib = 0
        DO WHILE (ib .LT. nbc .AND. (.NOT.found))
          ib = ib + 1
          IF (old_style(ib)) THEN
            found = bcstart(ib) .EQ. leftcut(ic) .AND. bcchar(ib) .EQ. &
&             'S'
          ELSE
            ix = 1
            DO WHILE (.NOT.found .AND. ix .LE. bc_list_size(ib))
              found = bc_list_x(ix, ib) .EQ. leftcut(ic) .AND. bc_list_y&
&               (ix, ib) .LE. topcut(ic)
              ix = ix + 1
            END DO
          END IF
        END DO
        CALL XERTST(found, &
&             'Mismatch (2) with region boundaries (South) !')
        found = .false.
        ib = 0
        DO WHILE (ib .LT. nbc .AND. (.NOT.found))
          ib = ib + 1
          IF (old_style(ib)) THEN
            found = bcend(ib) .EQ. rightcut(ic) - 1 .AND. bcchar(ib) &
&             .EQ. 'S'
          ELSE
            ix = 1
            DO WHILE (.NOT.found .AND. ix .LE. bc_list_size(ib))
              found = bc_list_x(ix, ib) .EQ. rightcut(ic) - 1 .AND. &
&               bc_list_y(ix, ib) .LE. topcut(ic)
              ix = ix + 1
            END DO
          END IF
        END DO
        CALL XERTST(found, &
&             'Mismatch (3) with region boundaries (South) !')
        found = .false.
        ib = 0
        DO WHILE (ib .LT. nbc .AND. (.NOT.found))
          ib = ib + 1
          IF (old_style(ib)) THEN
            found = bcstart(ib) .EQ. rightcut(ic) .AND. bcchar(ib) .EQ. &
&             'S'
          ELSE
            ix = 1
            DO WHILE (.NOT.found .AND. ix .LE. bc_list_size(ib))
              found = bc_list_x(ix, ib) .EQ. rightcut(ic) .AND. &
&               bc_list_y(ix, ib) .LE. topcut(ic)
              ix = ix + 1
            END DO
          END IF
        END DO
        CALL XERTST(found, &
&             'Mismatch (4) with region boundaries (South) !')
      END DO
      RETURN
    END IF
  END SUBROUTINE READ_B2MOD_BOUNDARY_NAMELIST_ST

!  Differentiation of read_b2mod_boundary_namelist_us as a context to call tangent code (with options multiDirectional context no
!ISIZE r8):
!   Plus diff mem management of: m.bcfcor:in-out m.divfcor:in
!
  SUBROUTINE READ_B2MOD_BOUNDARY_NAMELIST_US_DV(ns, m, md0, dotest, &
&   nbdirs)
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!
    INTEGER, INTENT(IN) :: ns
    TYPE(MAPPING), INTENT(INOUT) :: m
    TYPE(MAPPING_DIFFV), INTENT(INOUT) :: md0
    LOGICAL, INTENT(IN) :: dotest
    INTEGER :: is, ib, ic, ifc, noss, iss, indss, idb, nbcfc, inbc
    INTEGER :: ian, ien
    CHARACTER(len=3) :: ss
!
    LOGICAL :: file_ok, todo, assigned
    LOGICAL, ALLOCATABLE :: done(:)
    CHARACTER(len=260) :: filename
    EXTERNAL FIND_FILE, XERRAB, CHECK_BOUNDARY_LABELS_NODIFF
    INTRINSIC TRIM
    INTRINSIC COUNT
    EXTERNAL FIND_FACES
    INTRINSIC ANY
    INTRINSIC MAXVAL
    REAL(kind=r8) :: result1
    INTEGER :: nbdirs
!
    filename = boundary_filename
    boundary_time_mod = 0.0_R8
    boundary_time_switch = 0.0_R8
    CALL FIND_FILE(filename, file_ok)
    IF (file_ok) THEN
      OPEN(99, file=filename) 
      CALL CFVERR(99, boundary_version)
      WRITE(*, *) 'Reading namelist boundary from ', TRIM(filename)
      READ(99, boundary) 
      WRITE(*, *) 'Read namelist boundary from ', TRIM(filename)
      CLOSE(99) 
    ELSE
      CALL XERRAB('No '//TRIM(filename))
    END IF
!
    acchar = bcchar
    abcstart = bcstart
    abcend = bcend
! now check that all boundary faces are assigned to a BC
!
    IF (nbc .GT. 0) THEN
!
      m%nbc = nbc
      m%mxnbc = COUNT(m%fclbl .NE. 0)
      CALL ALLOC_MAPPING_BC_DV(m, md0, nbdirs)
      m%bcfcp = 0
!
      OPEN(newunit=idb, file='debug_bound.out') 
      inbc = 0
      DO ib=1,nbc
        IF (bcchar(ib) .EQ. 'A') THEN
          nbcfc = 0
          m%bcfcp(ib, 1) = inbc + 1
          noss = bcend(ib) - bcstart(ib) + 1
          DO iss=1,noss
            indss = bcstart(ib) + iss - 1
            WRITE(*, *) ' surface index ', indss
! find all faces belonging to surface structure INDSS
            CALL FIND_FACES(indss, inbc, nbcfc, m%mxnbc, m%bcfc, m%&
&                     bcfcor, m, idb)
          END DO
! iss
          WRITE(ss, '(i3)') ib
          CALL XERTST(nbcfc .GT. 0, &
&               'No faces found for boundary condition = '//ss)
          m%bcfcp(ib, 2) = nbcfc
        ELSE
          CALL XERRAB('BCCHAR /= A')
        END IF
      END DO
      CLOSE(idb) 
    END IF
!      call bc_to_struct (m)
!
    CALL CHECK_BOUNDARY_LABELS_NODIFF(m, nbc, bcstart(1:nbc), bcend(1:&
&                               nbc), .true.)
!
    IF (ANY(bc_list_size .GT. 0)) CALL XERRAB(&
&                  'read_b2mod_boundary_namelist_us -- bc_list_size > 0'&
&                                      )
!
    CALL BC_TO_STRUCT_DV(m, md0, nbdirs)
!
    IF (nbc .GT. 0) THEN
!.. Make a list of core boundaries
      result1 = MAXVAL(lcbs)
      todo = result1 .GT. 0
      IF (.NOT.todo) THEN
        ncbs = 0
      ELSE
        ncbs = 1
        DO WHILE (lcbs(ncbs+1) .GT. 0 .AND. ncbs .LT. nbc)
          ncbs = ncbs + 1
        END DO
      END IF
      ALLOCATE(done(nbc))
      done = .false.
      DO ib=1,nbc
        IF (bcchar(ib) .EQ. 'S' .OR. bcchar(ib) .EQ. 'A') THEN
          ian = m%bccvp(ib, 1)
          ien = m%bccvp(ib, 1) + m%bccvp(ib, 2) - 1
          IF (m%cvonclosedsurface(m%bccv(ian, 2)) .AND. m%&
&             cvonclosedsurface(m%bccv(ien, 2))) THEN
            IF (.NOT.todo) THEN
              ncbs = ncbs + 1
              lcbs(ncbs) = ib
              done(ncbs) = .true.
            ELSE
              ic = 1
              assigned = .false.
              DO WHILE (ic .LE. ncbs)
                IF (lcbs(ic) .EQ. ib) THEN
                  CALL XERTST(.NOT.done(ic), &
&                       'Core boundary already done!')
                  assigned = .true.
                  done(ic) = .true.
                END IF
                ic = ic + 1
              END DO
              IF (.NOT.assigned) THEN
                ncbs = ncbs + 1
                done(ncbs) = .true.
                lcbs(ncbs) = ib
                WRITE(*, *) 'Added boundary ', ib, &
&               ' to list of core boundaries'
              END IF
            END IF
          END IF
        END IF
      END DO
      result1 = MAXVAL(m%fsvxp(:, 2))
      IF (result1 .GT. 2) THEN
        IF (m%nnreg(0) .GT. 1 .OR. m%periodic_bc .EQ. 1) CALL XERTST(1 &
&                                                              .LE. ncbs&
&                                                              , &
&                                               'faulty parameters ncbs'&
&                                                             )
      END IF
      DO ib=1,ncbs
        CALL XERTST(1 .LE. lcbs(ib) .AND. lcbs(ib) .LE. nbc, &
&             'faulty parameter lcbs')
        CALL XERTST(done(ib), &
&             'Did not find all declared core boundaries!')
      END DO
      DEALLOCATE(done)
    END IF
!   ..build reverse face correspondence array
    DO ib=1,nbc
      DO ic=1,m%bcfcp(ib, 2)
        ifc = m%bcfc(m%bcfcp(ib, 1)+ic-1)
        m%fcbc(ifc, 1) = m%bcfcp(ib, 1) + ic - 1
        m%fcbc(ifc, 2) = ib
      END DO
    END DO
    IF ((.NOT.file_ok) .OR. (.NOT.dotest)) THEN
      IF (.NOT.file_ok) WRITE(*, *) 'No ', TRIM(filename)
      WRITE(*, *) 'Testing skipped!'
      RETURN
    ELSE
!
!   ..test input
      CALL XERTST(0 .LE. nbc, 'faulty input nbc')
      CALL XERTST(0 .LE. nniso, 'faulty input nniso')
      CALL XERTST(0.0_R8 .LE. gammae, 'faulty input gammae')
      CALL XERTST(0.0_R8 .LE. gammai, 'faulty input gammai')
      CALL XERTST(0.0_R8 .LT. teiso, 'faulty input teiso')
      CALL XERTST(0.0_R8 .LT. tiiso, 'faulty input tiiso')
      CALL XERTST(0.0_R8 .LE. boundary_time_mod, &
&           'faulty input boundary_time_mod')
      CALL XERTST(0.0_R8 .LE. boundary_time_switch, &
&           'faulty input boundary_time_switch')
      DO is=0,ns-1
        CALL XERTST(0.0_R8 .LT. niiso(is), 'faulty input niiso')
      END DO
      DO ib=1,nbc
        CALL XERTST(bcstart(ib) .LE. bcend(ib), &
&             'faulty inputs bcstart > bcend')
        DO is=0,ns-1
          CALL XERTST(0 .LE. bccon(is, ib), 'faulty input bccon')
          CALL XERTST(0 .LE. bcmom(is, ib), 'faulty input bcmom')
        END DO
        CALL XERTST(0 .LE. bcene(ib), 'faulty input bcene')
        CALL XERTST(0 .LE. bceni(ib), 'faulty input bcene')
        CALL XERTST(0 .LE. bcpot(ib), 'faulty input bcpot')
        CALL XERTST(0 .LE. bcenk(ib), 'faulty input bcenk')
        CALL XERTST(0 .LE. bcenz(ib), 'faulty input bcenz')
      END DO
      RETURN
    END IF
  END SUBROUTINE READ_B2MOD_BOUNDARY_NAMELIST_US_DV

