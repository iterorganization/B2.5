SUBROUTINE SPECIES_NODIFF(is, string, decorate)
  USE B2MOD_TYPES
  USE B2MOD_B2PLOT_DIFFV
  USE B2MOD_ELEMENTS_DIFFV
  USE B2MOD_B2CMPA_DIFFV
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
  CHARACTER(len=*) :: string
  EXTERNAL LNBLNK
  INTEGER :: is, izn, izmax, izmin, imi, len, LNBLNK
  LOGICAL :: decorate
  INTRINSIC NINT
  INTRINSIC TRIM
!
  CALL SUBINI('species')
  izn = NINT(zn(is))
  izmin = NINT(zamin(is))
  izmax = NINT(zamax(is))
  imi = NINT(am(is))
  CALL XERTST(izn .GE. 0, 'Inconsistent nuclear charge input!')
  CALL XERTST(izmin .LE. izn .AND. izmax .LE. izn, &
&       'Inconsistent charge input!')
  CALL XERTST(imi .GT. 0, 'Inconsistent mass input!')
  IF (izn .EQ. 0) THEN
    string = ' n'
  ELSE IF (izn .EQ. 1) THEN
    IF (imi .EQ. 1) string = ' H'
    IF (imi .EQ. 2) string = ' D'
    IF (imi .EQ. 3) string = ' T'
  ELSE
    string = ' '//TRIM(elements(izn))
  END IF
  len = LNBLNK(string)
  IF (decorate) string(len+1:len+2) = '^{'
  len = LNBLNK(string)
  IF (izmin .GT. 0) string(len+1:len+1) = '+'
  len = LNBLNK(string)
  IF (izmin .LT. 10) THEN
    WRITE(string(len+1:len+1), '(i1)') izmin
  ELSE
    WRITE(string(len+1:len+2), '(i2)') izmin
  END IF
  len = LNBLNK(string)
  IF (izmax .GT. izmin) THEN
    string(len+1:len+1) = '_'
    IF (izmax .GT. 0) string(len+2:len+2) = '+'
    len = LNBLNK(string)
    IF (izmax .LT. 10) THEN
      WRITE(string(len+1:len+1), '(i1)') izmax
    ELSE
      WRITE(string(len+1:len+2), '(i2)') izmax
    END IF
  END IF
  len = LNBLNK(string)
  IF (decorate) string(len+1:len+1) = '}'
  CALL SUBEND()
  RETURN
END SUBROUTINE SPECIES_NODIFF

