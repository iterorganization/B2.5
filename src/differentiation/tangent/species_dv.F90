  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
  CHARACTER(len=*) :: string
  EXTERNAL LNBLNK
  INTEGER :: is, izn, izmax, izmin, imi, len, LNBLNK
  LOGICAL :: decorate
  INTRINSIC NINT
  INTRINSIC TRIM
  INTRINSIC ABS
