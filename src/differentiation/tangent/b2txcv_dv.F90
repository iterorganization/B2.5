SUBROUTINE B2TXCV_NODIFF(nx, ny, mode, vsb, cvsb)
  USE B2MOD_TYPES
  USE B2MOD_GEO_DIFFV
  USE B2MOD_INDIRECT_DIFFV
  USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
  IMPLICIT NONE
!     ------------------------------------------------------------------
  INTEGER :: nx, ny, mode
  REAL(kind=r8) :: vsb(-1:nx, -1:ny, 0:1), cvsb(-1:nx, -1:ny, 0:1)
!     ------------------------------------------------------------------
!     B2TXCV is obsolete.
!     ------------------------------------------------------------------
!     ------------------------------------------------------------------
!     B2TXCV computes viscous conductances between neighbouring
!     staggered cells, given the viscosities.
!     The viscosities vsb(,,0) and vsb(,,1) are defined on cells.
!     Because the parallel velocity is defined on cell faces staggered
!     in the x-direction, the viscous conductance cvsb(,,0) must be
!     defined on cells, and the viscous conductance cvsb(,,1) must be
!     defined on cell corners.
!     ------------------------------------------------------------------
  INTEGER :: ix, iy
  REAL(kind=r8) :: t0, t1
  INTRINSIC MAX, SQRT
  EXTERNAL B2XXGS, HARM
  REAL(kind=r8) :: HARM
  EXTERNAL XERTST
  REAL(r8) :: y1
  REAL(r8) :: max1
  REAL(kind=r8) :: result1
  REAL(kind=r8) :: result2
  REAL(r8) :: result10
  REAL(kind=r8) :: result3
  REAL(kind=r8) :: result4
!     ------------------------------------------------------------------
!   ..subprogram start-up calls
  CALL SUBINI('b2txcv')
!   ..test mode
  CALL XERTST(-1 .LE. mode .AND. mode .LE. 2, &
&       'b2txcv--faulty argument mode')
!   ..compute cvsb(,,0)
  DO iy=-1,ny
    DO ix=-1,nx
      IF (leftix(ix, iy) .EQ. -2) THEN
        cvsb(ix, iy, 0) = 0.0_R8
      ELSE IF (rightix(ix, iy) .EQ. nx + 1) THEN
        cvsb(ix, iy, 0) = 0.0_R8
      ELSE
        cvsb(ix, iy, 0) = vol(ix, iy)*vsb(ix, iy, 0)/hx(ix, iy)**2
      END IF
    END DO
  END DO
!   ..reset null regions
  CALL B2XXGS(nx, ny, 0.0_R8, cvsb(-1, -1, 0), 0)
!   ..compute cvsb(,,1)
!     (temporary assignment to cvsb(,,1))
  IF (mode .EQ. -1) THEN
!    ..use harmonic averaging
    DO iy=-1,ny
      DO ix=-1,nx
        IF (bottomiy(ix, iy) .EQ. -2) THEN
          cvsb(ix, iy, 1) = 0.0_R8
        ELSE
          result1 = HY1(bottomix(ix, iy), bottomiy(ix, iy))
          t0 = vol(bottomix(ix, iy), bottomiy(ix, iy))*vsb(bottomix(ix, &
&           iy), bottomiy(ix, iy), 1)/result1**2
          result1 = HY1(ix, iy)
          t1 = vol(ix, iy)*vsb(ix, iy, 1)/result1**2
          IF (t0*t1 .LE. 0.0_R8) THEN
            cvsb(ix, iy, 1) = 0.0_R8
          ELSE
            cvsb(ix, iy, 1) = HARM(t0, t1)
          END IF
        END IF
      END DO
    END DO
  ELSE IF (mode .EQ. 0) THEN
!    ..use geometric averaging
    DO iy=-1,ny
      DO ix=-1,nx
        IF (bottomiy(ix, iy) .EQ. -2) THEN
          cvsb(ix, iy, 1) = 0.0_R8
        ELSE
          result1 = HY1(bottomix(ix, iy), bottomiy(ix, iy))
          result2 = HY1(ix, iy)
          y1 = vol(bottomix(ix, iy), bottomiy(ix, iy))*vsb(bottomix(ix, &
&           iy), bottomiy(ix, iy), 1)/result1*(vol(ix, iy)*vsb(ix, iy, 1&
&           )/result2)
          IF (0.0_R8 .LT. y1) THEN
            max1 = y1
          ELSE
            max1 = 0.0_R8
          END IF
          result10 = SQRT(max1)
          result2 = HY1(bottomix(ix, iy), bottomiy(ix, iy))
          result3 = HY1(ix, iy)
          cvsb(ix, iy, 1) = 2.0_R8*result10/(result2+result3)
        END IF
      END DO
    END DO
  ELSE IF (mode .EQ. 1) THEN
!    ..use arithmetic averaging (SOLPS5.0 formulation)
    DO iy=-1,ny
      DO ix=-1,nx
        IF (bottomiy(ix, iy) .EQ. -2) THEN
          cvsb(ix, iy, 1) = 0.0_R8
        ELSE
          result1 = HY1(bottomix(ix, iy), bottomiy(ix, iy))
          result2 = HY1(ix, iy)
          cvsb(ix, iy, 1) = 2.0_R8*(vol(bottomix(ix, iy), bottomiy(ix, &
&           iy))*vsb(bottomix(ix, iy), bottomiy(ix, iy), 1)+vol(ix, iy)*&
&           vsb(ix, iy, 1))/(result1+result2)**2
        END IF
      END DO
    END DO
  ELSE IF (mode .EQ. 2) THEN
!    ..use arithmetic averaging (SOLPS4.0 formulation)
    DO iy=-1,ny
      DO ix=-1,nx
        IF (bottomiy(ix, iy) .EQ. -2) THEN
          cvsb(ix, iy, 1) = 0.0_R8
        ELSE
          result1 = HY1(bottomix(ix, iy), bottomiy(ix, iy))
          result2 = HY1(ix, iy)
          result3 = HY1(bottomix(ix, iy), bottomiy(ix, iy))
          result4 = HY1(ix, iy)
          cvsb(ix, iy, 1) = 2.0_R8*(result1*vsb(bottomix(ix, iy), &
&           bottomiy(ix, iy), 1)+result2*vsb(ix, iy, 1))/(result3+&
&           result4)**2
        END IF
      END DO
    END DO
  END IF
!     (transfer cvsb(,,1) to cell corners)
  DO ix=nx,-1,-1
    DO iy=-1,ny
      IF (leftix(ix, iy) .NE. -2) THEN
        cvsb(ix, iy, 1) = (cvsb(ix, iy, 1)+cvsb(leftix(ix, iy), leftiy(&
&         ix, iy), 1))/2
      ELSE
        cvsb(ix, iy, 1) = 0.0_R8
      END IF
    END DO
  END DO
!   ..reset null regions
  CALL B2XXGS(nx, ny, 0.0_R8, cvsb(-1, -1, 1), 3)
!   ..return
  CALL SUBEND()
  RETURN
END SUBROUTINE B2TXCV_NODIFF

