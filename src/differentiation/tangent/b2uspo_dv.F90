&           icv1, 2)-1) .EQ. icv) aa(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, &
&         1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1):mpg%cvnvp(&
&           icv1, 1)+mpg%cvnvp(icv1, 2)-1) - t3*t1
! contribution from iVx2
      END DO
      DO incv=1,mpg%vxcvp(ivx2, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
        t3 = 1.0_R8/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
        WHERE (mpg%cvnv(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, 1)+mpg%cvnvp(&
&           icv1, 2)-1) .EQ. icv) aa(mpg%cvnvp(icv1, 1):mpg%cvnvp(icv1, &
&         1)+mpg%cvnvp(icv1, 2)-1) = aa(mpg%cvnvp(icv1, 1):mpg%cvnvp(&
&           icv1, 1)+mpg%cvnvp(icv1, 2)-1) + t3*t2
      END DO
    END IF
    IF (solvereg(mpg%cvreg(icv2))) THEN
! contribution from iVx1
      DO incv=1,mpg%vxcvp(ivx1, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx1, 1)+incv-1)
        t3 = 1.0_R8/(geo%vxvol(mpg%vxcvp(ivx1, 1)+incv-1)*t11)
        WHERE (mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%cvnvp(&
&           icv2, 2)-1) .EQ. icv) aa(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, &
&         1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1):mpg%cvnvp(&
&           icv2, 1)+mpg%cvnvp(icv2, 2)-1) + t3*t1
! contribution from iVx2
      END DO
      DO incv=1,mpg%vxcvp(ivx2, 2)
        icv = mpg%vxcv(mpg%vxcvp(ivx2, 1)+incv-1)
        t3 = 1.0_R8/(geo%vxvol(mpg%vxcvp(ivx2, 1)+incv-1)*t22)
        WHERE (mpg%cvnv(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, 1)+mpg%cvnvp(&
&           icv2, 2)-1) .EQ. icv) aa(mpg%cvnvp(icv2, 1):mpg%cvnvp(icv2, &
&         1)+mpg%cvnvp(icv2, 2)-1) = aa(mpg%cvnvp(icv2, 1):mpg%cvnvp(&
&           icv2, 1)+mpg%cvnvp(icv2, 2)-1) - t3*t2
      END DO
    END IF
  END DO
!$AD II-LOOP
!   ..correct RHS if not solving
  DO icv=1,ncv
    lnonzero = .false.
    DO incv=1,mpg%cvnvp(icv, 2)
      lnonzero = lnonzero .OR. aa(mpg%cvnvp(icv, 1)+incv-1) .NE. 0.0_R8
    END DO
    IF (.NOT.lnonzero) THEN
      aa(mpg%cvnvp(icv, 1)) = 1.0_R8
      respo(icv) = 0.0_R8
    END IF
    IF (.NOT.solvereg(mpg%cvreg(icv))) respo(icv) = 0.0_R8
  END DO
!
!   ..solve the correction equation
  WRITE(*, '(1x,a,a)') 'Calling b2uxus from '//TRIM(name)
!srv 18.03.10
  CALL B2UXUS(ncv, mpg, aa, itcnt, respo, corpo, name)
!
  IF (switch%b2uspo_iout .NE. 0) THEN
!srv 08.07.08 {
    DO k=0,3
      WRITE(chark, '(i1)') k
      arg1 = 'b2uspo_sch'//chark
      CALL MY_OUT_US(70, ncv, 0, sch(1, k), arg1)
    END DO
    result1 = MAXVAL(mpg%cvnvp(1:ncv, 2))
    DO k=1,result1
      WRITE(charnv, '(i2.2)') k
      DO icv=1,ncv
        IF (k .LE. mpg%cvnvp(icv, 2)) THEN
          wrk(icv) = aa(mpg%cvnvp(icv, 1)+k-1)
        ELSE
          wrk(icv) = 0.0_R8
        END IF
      END DO
      arg1 = 'b2uspo_aam'//charnv
      CALL MY_OUT_US(70, ncv, 0, wrk, arg1)
    END DO
    CALL MY_OUT_US(70, ncv, 0, respo, 'b2uspo_respo')
    CALL MY_OUT_US(70, ncv, 0, corpo, 'b2uspo_corpo')
  END IF
!
! ..return
  ncall_b2uspo = ncall_b2uspo + 1
  CALL SUBEND()
  RETURN
!
