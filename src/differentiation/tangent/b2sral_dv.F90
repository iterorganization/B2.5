&               smo0, st%srw%she0, st%srw%shi0, st%srw%sch0, st%srw%sne0&
&               , ncall_b2sral)
!   ..zero out contributions in dead cells and core boundary
    DO is=0,ns-1
      CALL B2XZDD_NODIFF(ncv, 1, switch, mpg, st%srw%sna0(1, 0, is))
      CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%smo0(1, 0, is))
    END DO
    CALL B2XZDD_NODIFF(ncv, 1, switch, mpg, st%srw%sne0)
    CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%she0)
    CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%shi0)
    CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%sch0)
!   ..add to previous contributions
    arg1 = ncv*2*ns
    CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%sna0, 1, st%sr%sna, 1)
    arg1 = ncv*4*ns
    CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%smo0, 1, st%sr%smo, 1)
    arg1 = ncv*2
    CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%sne0, 1, st%sr%sne, 1)
    arg1 = ncv*4
    CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%she0, 1, st%sr%she, 1)
    arg1 = ncv*4
    CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%shi0, 1, st%sr%shi, 1)
    arg1 = ncv*4
    CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%sch0, 1, st%sr%sch, 1)
  END IF
!
!   ..compute sources due to electron-atom processes
!     (parallel momentum source must be pure friction)
  CALL B2STEL_NODIFF(ncv, nfc, ns, ismain, switch, geo, mpg, st%pl, st%&
&              dv, st%rt, st%rtw, st%srw)
!srv 11.09.09 {
!   ..zero out contributions in dead cells and core boundary
  DO is=0,ns-1
    CALL B2XZDD_NODIFF(ncv, 1, switch, mpg, st%srw%sna0(1, 0, is))
    CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%smq0(1, 0, is))
  END DO
  CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%she0)
  CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%shi0)
  CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%shn0)
!   ..add to previous contributions
  arg1 = ncv*2*ns
  CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%sna0, 1, st%sr%sna, 1)
  arg1 = ncv*4*ns
  CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%smq0, 1, st%sr%smq, 1)
  arg1 = ncv*4
  CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%she0, 1, st%sr%she, 1)
  arg1 = ncv*4
  CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%shi0, 1, st%sr%shi, 1)
  arg1 = ncv*4
  CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%shn0, 1, st%sr%shn, 1)
!     (no contribution to sch or sne)
!   ..compute sources due to charge exchange
  rcxnareg = 0.0_R8
  rcxhireg = 0.0_R8
  rcxmoreg = 0.0_R8
  st%srw%rcxna = 0.0_R8
  st%srw%rcxhi = 0.0_R8
  st%srw%rcxmo = 0.0_R8
! IYS 06.11.2017
!
  IF (switch%iout_b2wdat .EQ. 4) THEN
! IYS 06.11.2017
    IF (.NOT.ALLOCATED(shi0_cx)) THEN
      WRITE(*, *) 'Allocating arrays for CX'
      ALLOCATE(shi0_cx(ncv, 0:3))
      ALLOCATE(sna0_cx(ncv, 0:1, 0:ns-1))
      ALLOCATE(smo0_cx(ncv, 0:3, 0:ns-1))
      arg1 = ncv*2*ns
      CALL SFILL_NODIFF(arg1, 0.0_R8, sna0_cx, 1)
      arg1 = ncv*4*ns
      CALL SFILL_NODIFF(arg1, 0.0_R8, smo0_cx, 1)
      arg1 = ncv*4
      CALL SFILL_NODIFF(arg1, 0.0_R8, shi0_cx, 1)
    END IF
  END IF
!
  DO k=0,nscx-1
!    ..compute charge exchange rate coefficients
    CALL B2SQCX_NODIFF(ncv, ns, ev, switch, am(iscx(k)), st%pl%ti, st%pl&
&                %tn, st%rt%rlcx(1:ncv, 0:1, 0:ns-1, k), st%rtw%rcx)
!    ..compute sources due to charge exchange
!      (parallel momentum source must be pure friction)
    CALL B2STCX_NODIFF(ncv, nfc, ns, iscx(k), ismain, switch, geo, mpg, &
&                st%pl%na, st%pl%ua, st%pl%ti, st%pl%tn, st%dv%ni, st%&
&                rtw%rcx, st%srw%sna0, st%srw%smq0, st%srw%shi0, st%srw%&
&                shn0, st%srw%rcxna, st%srw%rcxmo, st%srw%rcxhi)
!   ..zero out contributions in dead cells and core boundary
    DO is=0,ns-1
      CALL B2XZDD_NODIFF(ncv, 1, switch, mpg, st%srw%sna0(1, 0, is))
      CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%smq0(1, 0, is))
    END DO
    CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%shi0)
    CALL B2XZDD_NODIFF(ncv, 3, switch, mpg, st%srw%shn0)
!    ..add to previous contributions
    arg1 = ncv*2*ns
    CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%sna0, 1, st%sr%sna, 1)
    arg1 = ncv*4*ns
    CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%smq0, 1, st%sr%smq, 1)
    arg1 = ncv*4
    CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%shi0, 1, st%sr%shi, 1)
    arg1 = ncv*4
    CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%shn0, 1, st%sr%shn, 1)
! IYS 06.11.2017
!      (no contribution to she, sne or sch)
    IF (switch%iout_b2wdat .EQ. 4) THEN
! IYS 06.11.2017
      WRITE(*, *) 'Summing cx sources over hydrogenic neutrals'
      arg1 = ncv*2*ns
      CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%sna0, 1, sna0_cx, 1)
      arg1 = ncv*4*ns
      CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%smq0, 1, smo0_cx, 1)
      arg1 = ncv*4
      CALL B2SAXPY_NODIFF(arg1, 1.0_R8, st%srw%shi0, 1, shi0_cx, 1)
    END IF
  END DO
!
  IF (switch%iout_b2wdat .EQ. 4) THEN
! IYS 06.11.2017
    WRITE(*, *) 'Writing CX sources'
    DO is=0,ns-1
      WRITE(chns, '(i3.3)') is
      wrk(:) = sna0_cx(:, 0, is) + sna0_cx(:, 1, is)*st%pl%na(:, is)
      arg11(:) = 'b2stcx_sna_'//chns
      CALL MY_OUT_US(70, ncv, 0, wrk, arg11(:))
      wrk(:) = shi0_cx(:, 0) + shi0_cx(:, 1)*st%pl%ti + shi0_cx(:, 2)*st&
&       %dv%ni(:, 0) + shi0_cx(:, 3)*st%pl%ti*st%dv%ni(:, 0)
      CALL MY_OUT_US(70, ncv, 0, wrk, 'b2stcx_shi')
      wrk(:) = smo0_cx(:, 0, is) + smo0_cx(:, 1, is)*st%pl%ua(:, is) + &
&       smo0_cx(:, 2, is)*st%pl%na(:, is)*mp*am(is) + smo0_cx(:, 3, is)*&
&       st%pl%ua(:, is)*st%pl%na(:, is)*mp*am(is)
      arg12(:) = 'b2stcx_smq'//chns
      CALL MY_OUT_US(70, ncv, 0, wrk, arg12(:))
    END DO
    IF (ALLOCATED(shi0_cx)) THEN
      WRITE(*, *) 'Deallocating arrays for cx'
      DEALLOCATE(shi0_cx)
      DEALLOCATE(sna0_cx)
      DEALLOCATE(smo0_cx)
    END IF
  END IF
!srv 30.06.08 }
!
  IF (switch%b2sral_iout .NE. 0) THEN
!srv 30.06.08 {
    DO is=0,ns-1
      WRITE(chns, '(i3.3)') is
      DO k=0,1
        WRITE(chk, '(i1)') k
        arg12(:) = 'b2sral_sna'//chk//chns
        CALL MY_OUT_US(70, ncv, 0, st%sr%sna(1, k, is), arg12(:))
      END DO
      DO k=0,3
        WRITE(chk, '(i1)') k
        arg12(:) = 'b2sral_smo'//chk//chns
        CALL MY_OUT_US(70, ncv, 0, st%sr%smo(1, k, is), arg12(:))
        arg12(:) = 'b2sral_smq'//chk//chns
        CALL MY_OUT_US(70, ncv, 0, st%sr%smq(1, k, is), arg12(:))
      END DO
    END DO
    DO k=0,3
      WRITE(chk, '(i1)') k
      arg12(:) = 'b2sral_she'//chk
      CALL MY_OUT_US(70, ncv, 0, st%sr%she(1, k), arg12(:))
      arg12(:) = 'b2sral_shi'//chk
      CALL MY_OUT_US(70, ncv, 0, st%sr%shi(1, k), arg12(:))
      arg12(:) = 'b2sral_shn'//chk
      CALL MY_OUT_US(70, ncv, 0, st%sr%shn(1, k), arg12(:))
      arg12(:) = 'b2sral_sch'//chk
      CALL MY_OUT_US(70, ncv, 0, st%sr%sch(1, k), arg12(:))
    END DO
    DO k=0,1
      WRITE(chk, '(i1)') k
      arg12(:) = 'b2sral_sne'//chk
      CALL MY_OUT_US(70, ncv, 0, st%sr%sne(1, k), arg12(:))
    END DO
  END IF
!
!sw 17oct2012, erase sna, smo, smq, (she, shi, sch) for fluid neutrals
  IF (no_fluid_neutral_sources .EQ. 1) THEN
    DO is=0,ns-1
      IF (is_neutral(is)) THEN
        st%sr%sna(:, :, is) = 0.d0
        st%sr%smo(:, :, is) = 0.d0
        st%sr%smq(:, :, is) = 0.d0
      END IF
    END DO
  END IF
!
! ..return
  IF (ncall_b2sral .EQ. 0 .OR. main_call) ncall_b2sral = ncall_b2sral + &
&     1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2SRAL_NODIFF

