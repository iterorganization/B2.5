!   ..local variables
  INTEGER :: ifc, is, meth
  REAL(kind=r8) :: floi0(nfc, 0:1), coni0(nfc, 0:1), floi0_mdf(nfc, 0:1)&
& , wght(nfc, 2), dumm0(nfc, 0:1), dumm1(nfc, 0:1), dumm2(nfc, 0:1), &
& dumm3(nfc, 0:1), dumm4(nfc, 0:1), fhi0_mdf(nfc, 0:1), flon0(nfc, 0:1)&
& , flokt0(nfc, 0:1), flozt0(nfc, 0:1), flo0_exb(nfc, 0:1)
!pax
!srv 25.01.02
  REAL(kind=r8) :: flidia(nfc, 0:1), facdriftm, nif(nfc), nnf(nfc), tif(&
& nfc), tnf(nfc), wrkf(nfc, 0:1), nati(ncv), nati2(ncv), nativ(nvx), &
& nati2v(nvx), dnati2(nfc, 0:1), cddi(nfc, 0:1), cdkt0(nfc, 0:1), cdzt0(&
& nfc, 0:1)
!djm Jan2017
  REAL(kind=r8) :: scur(nfc, 0:1)
! The following switches are only used in 'WG-TODO' blocks, i.e. not yet converted to wide grid functionality
!      integer, save :: no_Ptncr_x_hi = 0, no_Ptncr_y_hi = 0             !srv 06.07.06
!      integer, save :: b2tfhi_hybr2=0, b2_upwind=0, flo53=0
!      integer, save :: b2kin=0, b2vis=0
!      real (kind=R8), save :: drift_hyb = 1.0_R8
!      integer, save :: conductive_limit = 1                             !srv 16.06.08 17.01.12
!      integer, save :: lim_flux = 0                                    !srv 04.02.03
!   ..procedures
  EXTERNAL XERTST
  EXTERNAL B2XVSG, B2XVFF_NODIFF
  INTRINSIC MAXVAL
  REAL(r8), DIMENSION(nCv) :: arg1
!   ..initialization
!
!-----------------------------------------------------------------------
!.computation
!
! ..preliminaries
!   ..subprogram start-up calls
  CALL SUBINI('b2tfhi_')
!srv
! The following switches are only used in 'WG-TODO' blocks, i.e. not yet converted to wide grid functionality
!       call ipgeti ('b2tfhi_no_Ptncr_x', no_Ptncr_x_hi)                 !srv 06.07.06
!       call ipgeti ('b2tfhi_no_Ptncr_y', no_Ptncr_y_hi)                 !srv 06.07.06
!       call ipgeti ('b2tfhi_flo53', flo53)
!       call ipgeti ('b2tfhi_upwind', b2_upwind)
!       call ipgeti ('b2tfhi_hybr2', b2tfhi_hybr2)
!       call ipgeti ('b2tfhi_b2kin', b2kin)
!       call ipgeti ('b2tfhi_b2vis', b2vis)
!       call ipgetr ('b2tfhi_drift_hyb',drift_hyb)
!       call ipgeti ('b2trcl_conductive_limit', conductive_limit)        !srv 16.06.08 17.01.12
!       call ipgeti ('b2tfhi_lim_flux', lim_flux)                        !srv 04.02.03
!   ..test nCv, nFc
  CALL XERTST(0 .LT. ncv .AND. 0 .LT. nfc, 'faulty argument nCv, nFc')
!   ..extensive tests on first few calls
  IF (ncall_b2tfhi .LT. 3) THEN
!    ..test sign of ni, ti, chci
    CALL B2XVSG(ncv, dv%ni, 1, 'ni', '.gt.')
    CALL B2XVSG(ncv, pl%ti, 1, 'ti', '.gt.')
    CALL B2XVSG(ncv, pl%kt, 1, 'kt', '.ge.')
    wrkf(:, 0) = co%chci(:, 0)*geo%fcqalf(:, 0)
    CALL B2XVSG(nfc, wrkf(:, 0), 1, 'chci0', '.ge.')
    wrkf(:, 1) = co%chci(:, 1)*geo%fcqalf(:, 1)
    CALL B2XVSG(nfc, wrkf(:, 1), 1, 'chci1', '.ge.')
    wrkf(:, 0) = co%cdkt(:, 0)*geo%fcqalf(:, 0)
    CALL B2XVSG(nfc, wrkf(:, 0), 1, 'cdkt0', '.ge.')
    wrkf(:, 1) = co%cdkt(:, 1)*geo%fcqalf(:, 1)
    CALL B2XVSG(nfc, wrkf(:, 1), 1, 'cdkt1', '.ge.')
    wrkf(:, 0) = co%cdzt(:, 0)*geo%fcqalf(:, 0)
    CALL B2XVSG(nfc, wrkf(:, 0), 1, 'cdzt0', '.ge.')
    wrkf(:, 1) = co%cdzt(:, 1)*geo%fcqalf(:, 1)
    CALL B2XVSG(nfc, wrkf(:, 1), 1, 'cdzt1', '.ge.')
  END IF
!
  meth = switch%b2tfhi_discr_meth
!
!   ..set dummy arrays
  dumm0 = 0.0_R8
  dumm1 = 0.0_R8
  facdriftm = MAXVAL(dv%facdrift)
!srv 13.10.06
  dv%fhipsch = 0.0_R8
!srv 02.01.07
  dv%coni = 0.0_R8
  dv%floi = 0.0_R8
  IF (facdriftm .NE. 0.0_R8) THEN
    DO is=0,ns-1
      IF (.NOT.is_neutral(is)) THEN
!
!      ..precompute face and vertex values of nati and nati2
        nati = pl%na(:, is)*pl%ti
        nati2 = pl%na(:, is)*pl%ti*pl%ti
        CALL INTVERTEX_NODIFF(ncv, nvx, mpg, geo%vxvol, nati, nativ)
        CALL INTVERTEX_NODIFF(ncv, nvx, mpg, geo%vxvol, nati2, nati2v)
        CALL DIFF_NODIFF(ncv, nfc, nvx, 1, geo, mpg, nati2, nati2v, &
&                  dnati2)
!
!      ..compute P.Sch heat flux
        dv%fhipsch(:, 0) = 2.5_R8*switch%fhipsch*dv%facdrift*co%cddi(:, &
&         0, is)*dnati2(:, 1)
        dv%fhipsch(:, 1) = -(2.5_R8*switch%fhipsch*dv%facdrift*co%cddi(:&
&         , 1, is)*dnati2(:, 0))
!      ..linearization coefficients for P.Sch flows
        cddi(:, 0) = -(5.0_R8*switch%fhipsch*dv%facdrift*co%cddi(:, 1, &
&         is))
        cddi(:, 1) = 5.0_R8*switch%fhipsch*dv%facdrift*co%cddi(:, 0, is)
        CALL CALCCOEF_NODIFF(ncv, nfc, nvx, 0, geo, dumm0, cddi, floi0, &
&                      coni0)
        DO ifc=1,nfc
          dv%floi(ifc, 0) = dv%floi(ifc, 0) + 0.5_R8*floi0(ifc, 0)*(nati&
&           (mpg%fccv(ifc, 1))+nati(mpg%fccv(ifc, 2))) - coni0(ifc, 0)*(&
&           nati(mpg%fccv(ifc, 2))-nati(mpg%fccv(ifc, 1)))
          dv%floi(ifc, 1) = dv%floi(ifc, 1) + 0.5_R8*floi0(ifc, 1)*(&
&           nativ(mpg%fcvx(ifc, 1))+nativ(mpg%fcvx(ifc, 2))) - coni0(ifc&
&           , 1)*(nativ(mpg%fcvx(ifc, 2))-nativ(mpg%fcvx(ifc, 1)))
!wdk      For now, directly add these contributions into coni(,,0),
!wdk      so they are directly added into the stencil.
!wdk      Might need to move back to dv%coni(,,1) and dedicated treatment
!wdk      in b2ux9p if we decide to move away from inverse volume weighting
!wdk      to compute vertex values of drifts!
          dv%coni(ifc, 0, 0) = dv%coni(ifc, 0, 0) + 0.5_R8*coni0(ifc, 0)&
&           *(nati(mpg%fccv(ifc, 1))+nati(mpg%fccv(ifc, 2))) - 0.25_R8*&
&           floi0(ifc, 0)*(nati(mpg%fccv(ifc, 2))-nati(mpg%fccv(ifc, 1))&
&           )
          dv%coni(ifc, 1, 0) = dv%coni(ifc, 1, 0) + 0.5_R8*coni0(ifc, 1)&
&           *(nativ(mpg%fcvx(ifc, 1))+nativ(mpg%fcvx(ifc, 2))) - 0.25_R8&
&           *floi0(ifc, 1)*(nativ(mpg%fcvx(ifc, 2))-nativ(mpg%fcvx(ifc, &
&           1)))
        END DO
      END IF
    END DO
  END IF
!
  wght = 1.0_R8
!srv 25.01.02 {
  flidia = 0.0_R8
!srv 18.01.07
  IF (facdriftm .NE. 0.0_R8) THEN
!srv 18.01.07
    DO is=0,ns-1
      IF (.NOT.is_neutral(is)) THEN
!      ..interpolate na(:,is) to cell faces
        CALL INTFACE(ncv, nfc, mpg%fccv, wght, pl%na(:, is), nif)
!      ..compute flidia
        flidia(:, 0) = flidia(:, 0) + 2.5_R8*switch%fhi_vdia_par*nif*dv%&
&         vadia(:, 0, is)*geo%fcs*geo%fcqalf(:, 0)
        flidia(:, 1) = flidia(:, 1) + 2.5_R8*switch%fhi_vdia_par*nif*dv%&
&         vadia(:, 1, is)*geo%fcs*geo%fcqalf(:, 1)
      END IF
    END DO
  END IF
!srv 25.01.02 }
!
!
!   ..interpolate ni and ti to cell faces
!
  CALL INTFACE(ncv, nfc, mpg%fccv, wght, pl%ti, tif)
  CALL INTFACE(ncv, nfc, mpg%fccv, wght, pl%tn, tnf)
!! in case of default energy equation, tn will be equal to ti
!
  IF (switch%tn_style .EQ. 0) THEN
!! default combined ion-neutral energy equation
    CALL INTFACE(ncv, nfc, mpg%fccv, wght, dv%ni(:, 0), nif)
  ELSE IF (switch%tn_style .EQ. 1) THEN
!! pure ion equation
    CALL INTFACE(ncv, nfc, mpg%fccv, wght, dv%ni(:, 1), nif)
  ELSE
!! separate tn equation for hydrogenic atoms, all other species in ti-equation
    arg1(:) = dv%ni(:, 0) - dv%nn
    CALL INTFACE(ncv, nfc, mpg%fccv, wght, arg1(:), nif)
    CALL INTFACE(ncv, nfc, mpg%fccv, wght, dv%nn, nnf)
  END IF
!
!   ..contributions from currents
  scur(:, 0) = 1.5_R8/qe*(dv%fchinert(:, 0)+dv%fchvispar(:, 0)+dv%&
&   fchanml(:, 0)+dv%fchvisper(:, 0)+dv%fchvisq(:, 0))*switch%&
&   b2tfnb_xcur + 1.5_R8/qe*dv%fchviskt(:, 0)
  scur(:, 1) = 1.5_R8/qe*(dv%fchinert(:, 1)+dv%fchvispar(:, 1)+dv%&
&   fchanml(:, 1)+switch%fnb_vis_per*dv%fchvisper(:, 1)+switch%fnb_vis_q&
&   *dv%fchvisq(:, 1))*switch%b2tfnb_ycur + 1.5_R8/qe*dv%fchviskt(:, 1)
!floi0
!
  IF (switch%b2tfhi_iout .EQ. 2) THEN
    CALL MY_OUT_US(70, nfc, 1, co%chcn(:, 0), 'chcn0')
    CALL MY_OUT_US(70, nfc, 1, co%chci(:, 0), 'chci0')
    CALL MY_OUT_US(70, nfc, 1, co%chcn(:, 1), 'chcn1')
    CALL MY_OUT_US(70, nfc, 1, co%chci(:, 1), 'chci1')
  END IF
!
!
!   ..convective coefficients
  floi0(:, 0) = 1.5_R8*dv%fni_he(:, 0) + co%chvi(:, 0)*nif + flidia(:, 0&
&   ) + scur(:, 0)
!floi0_mdf
  floi0(:, 1) = 1.5_R8*dv%fni_he(:, 1) + co%chvi(:, 1)*nif + flidia(:, 1&
&   ) + scur(:, 1)
!
  floi0_mdf(:, 0) = 1.5_R8*dv%fni_he(:, 0) + co%chvi(:, 0)*nif + scur(:&
&   , 0)
!flon0
  floi0_mdf(:, 1) = 1.5_R8*dv%fni_he(:, 1) + co%chvi(:, 1)*nif + scur(:&
&   , 1)
!
  IF (switch%tn_style .EQ. 2) THEN
    flon0(:, 0) = 1.5_R8*dv%fnn(:, 0) + co%chvi(:, 0)*nnf
    flon0(:, 1) = 1.5_R8*dv%fnn(:, 1) + co%chvi(:, 1)*nnf
  END IF
!
!   ..apply discretization scheme
  CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%ti, floi0_mdf, &
&                co%chci, fhi0_mdf, dumm0, dumm1)
!
!   ..compute ion fluxes
  dv%fhi(:, 0) = fhi0_mdf(:, 0) + (floi0(:, 0)-floi0_mdf(:, 0))*tif
  dv%fhi(:, 1) = fhi0_mdf(:, 1) + (floi0(:, 1)-floi0_mdf(:, 1))*tif
!
  dv%fhi_mdf(:, 0) = fhi0_mdf(:, 0) - dv%fhipsch(:, 0)
  dv%fhi_mdf(:, 1) = fhi0_mdf(:, 1) - dv%fhipsch(:, 1)
!
!   ..compute floi, coni
  CALL CALCCOEF_NODIFF(ncv, nfc, nvx, meth, geo, floi0_mdf, co%chci, &
&                floi0, coni0)
!wdk now we always add PSch linearization; do we need to condition
!    this on mdf /= 0?
  dv%floi = dv%floi + floi0
  dv%coni(:, :, 0) = dv%coni(:, :, 0) + coni0
!
!   ..compute fhn, flon, conn
  IF (switch%tn_style .LT. 2) THEN
!set zero if no separate equation for tn is solved
    dv%fhn = 0.0_R8
    dv%flon = 0.0_R8
    dv%conn = 0.0_R8
  ELSE
!     ..apply discretization scheme
    CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%tn, flon0, co&
&                  %chcn, dv%fhn, dumm0, dumm1)
!     ..compute flon, conn
    CALL CALCCOEF_NODIFF(ncv, nfc, nvx, meth, geo, flon0, co%chcn, dv%&
&                  flon, dv%conn)
  END IF
!
  IF (switch%solve_keps .GT. 0) THEN
!     ..compute ExB heat fluxes (ion and electron (!))
    flo0_exb = 1.5_R8*dv%fna_exb
    CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%ti, flo0_exb&
&                  , co%chci_exb, dv%fhi_exb, dumm0, dumm1)
    CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%te, flo0_exb&
&                  , co%chce_exb, dv%fhe_exb, dumm0, dumm1)
!
!     ..fluxes for k-equation
!wdk    fflokt and fconkt: fudge factors to approximate enhanced parallel
!wdk    transport of k due to term
!wdk    related to (fluctuations in parallel current) * (fluctuations in potential)
!wdk    factor fconkt now included in b2trno
!wdk    in principle: models same effect as skt_diss (thus: needs b2sikt_fac_sheath_* = 0.0)
    flokt0(:, 0) = (1.0_R8+switch%b2tfhi_fflokt)*dv%fna(:, 0, ismain)
    flokt0(:, 1) = dv%fna(:, 1, ismain)
    cdkt0(:, 0) = co%cdkt(:, 0)
    cdkt0(:, 1) = co%cdkt(:, 1)
!
!     ..apply discretization scheme
    CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%kt, flokt0, &
&                  cdkt0, dv%fkt, dumm0, dumm1)
!
!     ..contribution from ExB heat fluxes
!wdk    at the moment: experimental; it is expected that this contriution
!wdk    is at least partially compensated by the (divergence of the) term
!wdk    related to (fluctuations in diamagnetic current) * (fluctuations in potential)
    dv%fkt = dv%fkt + switch%b2tfhi_fkt_hie*2.0_R8/3.0_R8*(dv%fhi_exb+dv&
&     %fhe_exb)
!
!     ..compute flokt, conkt
    CALL CALCCOEF_NODIFF(ncv, nfc, nvx, meth, geo, flokt0, cdkt0, dv%&
&                  flokt, dv%conkt)
!
    IF (switch%solve_keps .GT. 1) THEN
!
!     ..fluxes for z-equation
      flozt0(:, 0) = (1.0_R8+switch%b2tfhi_fflozt)*dv%fna(:, 0, ismain)
      flozt0(:, 1) = dv%fna(:, 1, ismain)
      cdzt0(:, 0) = co%cdzt(:, 0)
      cdzt0(:, 1) = co%cdzt(:, 1)
!
!     ..apply discretization scheme
      CALL CALCFLOW_NODIFF(ncv, nfc, nvx, meth, geo, mpg, pl%zt, flozt0&
&                    , cdzt0, dv%fzt, dumm0, dumm1)
!
!     ..compute flozt, conzt
      CALL CALCCOEF_NODIFF(ncv, nfc, nvx, meth, geo, flozt0, cdzt0, dv%&
&                    flozt, dv%conzt)
!
    END IF
  END IF
!
!srv 18.06.08 }
!
!
!
  IF (switch%b2tfhi_iout .NE. 0) THEN
!srv 18.06.08 {
    CALL MY_OUT_US(70, nfc, 1, dv%floi(1, 0), 'b2tfhi__floi_th')
    CALL MY_OUT_US(70, nfc, 1, dv%floi(1, 1), 'b2tfhi__floi_r')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 0, 0), &
&                   'b2tfhi__coni_th0')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 0, 1), &
&                   'b2tfhi__coni_th1')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 0, 2), &
&                   'b2tfhi__coni_th2')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 1, 0), &
&                   'b2tfhi__coni_r0')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 1, 1), &
&                   'b2tfhi__coni_r1')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 1, 2), &
&                   'b2tfhi__coni_r2')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi(1, 0), 'b2tfhi__fhi_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi(1, 1), 'b2tfhi__fhi_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 0), &
&                   'b2tfhi__fhi_mdf_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 1), &
&                   'b2tfhi__fhi_mdf_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 0), &
&                   'b2tfhi__fhiPSch_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 1), &
&                   'b2tfhi__fhiPSch_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvispar(1, 0), &
&                   'b2tfhi__fchvispar_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvispar(1, 1), &
&                   'b2tfhi__fchvispar_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisq(1, 0), &
&                   'b2tfhi__fchvisq_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisq(1, 1), &
&                   'b2tfhi__fchvisq_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisper(1, 0), &
&                   'b2tfhi__fchvisper_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisper(1, 1), &
&                   'b2tfhi__fchvisper_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchinert(1, 0), &
&                   'b2tfhi__fchinert_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchinert(1, 1), &
&                   'b2tfhi__fchinert_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchanml(1, 0), &
&                   'b2tfhi__fchanml_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchanml(1, 1), &
&                   'b2tfhi__fchanml_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fni_he(1, 0), 'b2tfhi__fni_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fni_he(1, 1), 'b2tfhi__fni_r')
    CALL MY_OUT_US(70, ncv, 0, dv%ni(:, 0), 'b2tfhi__ni0')
    CALL MY_OUT_US(70, ncv, 0, dv%ni(:, 1), 'b2tfhi__ni1')
! neutrals
    CALL MY_OUT_US(70, nfc, 1, dv%flon(1, 0), 'b2tfhi__flon_th')
    CALL MY_OUT_US(70, nfc, 1, dv%flon(1, 1), 'b2tfhi__flon_r')
    CALL MY_OUT_US(70, nfc, 1, dv%conn(1, 0), 'b2tfhi__conn_th')
    CALL MY_OUT_US(70, nfc, 1, dv%conn(1, 1), 'b2tfhi__conn_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhn(1, 0), 'b2tfhi__fhn_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhn(1, 1), 'b2tfhi__fhn_r')
  END IF
!
  IF (switch%iout_b2wdat .EQ. 4) THEN
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 0), &
&                   'b2tfhi__fhi_mdf_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 1), &
&                   'b2tfhi__fhi_mdf_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 0), &
&                   'b2tfhi__fhiPSch_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 1), &
&                   'b2tfhi__fhiPSch_r')
  END IF
!
! ..return
  ncall_b2tfhi = ncall_b2tfhi + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2TFHI__NODIFF

