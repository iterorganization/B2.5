    CALL INTFACE_DV(ncv, nfc, mpg%fccv, wght, dv%nn, dvd%nn, nnf, nnfd, &
&             nbdirs)
  END IF
!
!   ..contributions from currents
  DO nd=1,nbdirsmax
    scurd(nd, :, :) = 0.D0
  END DO
  DO nd=1,nbdirs
    scurd(nd, :, 0) = switch%b2tfnb_xcur*1.5_R8*(dvd%fchinert(nd, :, 0)+&
&     dvd%fchvispar(nd, :, 0)+dvd%fchanml(nd, :, 0)+dvd%fchvisper(nd, :&
&     , 0)+dvd%fchvisq(nd, :, 0))/qe + 1.5_R8*dvd%fchviskt(nd, :, 0)/qe
    scurd(nd, :, 1) = switch%b2tfnb_ycur*1.5_R8*(dvd%fchinert(nd, :, 1)+&
&     dvd%fchvispar(nd, :, 1)+dvd%fchanml(nd, :, 1)+switch%fnb_vis_per*&
&     dvd%fchvisper(nd, :, 1)+switch%fnb_vis_q*dvd%fchvisq(nd, :, 1))/qe&
&     + 1.5_R8*dvd%fchviskt(nd, :, 1)/qe
  END DO
  scur(:, 0) = 1.5_R8/qe*(dv%fchinert(:, 0)+dv%fchvispar(:, 0)+dv%&
&   fchanml(:, 0)+dv%fchvisper(:, 0)+dv%fchvisq(:, 0))*switch%&
&   b2tfnb_xcur + 1.5_R8/qe*dv%fchviskt(:, 0)
  scur(:, 1) = 1.5_R8/qe*(dv%fchinert(:, 1)+dv%fchvispar(:, 1)+dv%&
&   fchanml(:, 1)+switch%fnb_vis_per*dv%fchvisper(:, 1)+switch%fnb_vis_q&
&   *dv%fchvisq(:, 1))*switch%b2tfnb_ycur + 1.5_R8/qe*dv%fchviskt(:, 1)
!floi0
!
  IF (switch%b2tfhi_iout .EQ. 2) THEN
    CALL MY_OUT_US(70, nfc, 1, co%chcn(:, 0), 'chcn0')
    CALL MY_OUT_US(70, nfc, 1, co%chci(:, 0), 'chci0')
    CALL MY_OUT_US(70, nfc, 1, co%chcn(:, 1), 'chcn1')
    CALL MY_OUT_US(70, nfc, 1, co%chci(:, 1), 'chci1')
  END IF
!
!
!   ..convective coefficients
  DO nd=1,nbdirsmax
    floi0d(nd, :, :) = 0.D0
  END DO
!
  DO nd=1,nbdirsmax
    floi0_mdfd(nd, :, :) = 0.D0
  END DO
  DO nd=1,nbdirs
    floi0d(nd, :, 0) = 1.5_R8*dvd%fni_he(nd, :, 0) + co%chvi(:, 0)*nifd(&
&     nd, :) + flidiad(nd, :, 0) + scurd(nd, :, 0)
!floi0_mdf
    floi0d(nd, :, 1) = 1.5_R8*dvd%fni_he(nd, :, 1) + co%chvi(:, 1)*nifd(&
&     nd, :) + flidiad(nd, :, 1) + scurd(nd, :, 1)
    floi0_mdfd(nd, :, 0) = 1.5_R8*dvd%fni_he(nd, :, 0) + co%chvi(:, 0)*&
&     nifd(nd, :) + scurd(nd, :, 0)
!flon0
    floi0_mdfd(nd, :, 1) = 1.5_R8*dvd%fni_he(nd, :, 1) + co%chvi(:, 1)*&
&     nifd(nd, :) + scurd(nd, :, 1)
  END DO
  floi0(:, 0) = 1.5_R8*dv%fni_he(:, 0) + co%chvi(:, 0)*nif + flidia(:, 0&
&   ) + scur(:, 0)
  floi0(:, 1) = 1.5_R8*dv%fni_he(:, 1) + co%chvi(:, 1)*nif + flidia(:, 1&
&   ) + scur(:, 1)
  floi0_mdf(:, 0) = 1.5_R8*dv%fni_he(:, 0) + co%chvi(:, 0)*nif + scur(:&
&   , 0)
  floi0_mdf(:, 1) = 1.5_R8*dv%fni_he(:, 1) + co%chvi(:, 1)*nif + scur(:&
&   , 1)
!
  IF (switch%tn_style .EQ. 2) THEN
    DO nd=1,nbdirsmax
      flon0d(nd, :, :) = 0.D0
    END DO
    DO nd=1,nbdirs
      flon0d(nd, :, 0) = 1.5_R8*dvd%fnn(nd, :, 0) + co%chvi(:, 0)*nnfd(&
&       nd, :)
      flon0d(nd, :, 1) = 1.5_R8*dvd%fnn(nd, :, 1) + co%chvi(:, 1)*nnfd(&
&       nd, :)
    END DO
    flon0(:, 0) = 1.5_R8*dv%fnn(:, 0) + co%chvi(:, 0)*nnf
    flon0(:, 1) = 1.5_R8*dv%fnn(:, 1) + co%chvi(:, 1)*nnf
  ELSE
    DO nd=1,nbdirsmax
      flon0d(nd, :, :) = 0.D0
    END DO
  END IF
!
!   ..apply discretization scheme
  DO nd=1,nbdirsmax
    dumm0d(nd, :, :) = 0.D0
  END DO
  DO nd=1,nbdirsmax
    fhi0_mdfd(nd, :, :) = 0.D0
  END DO
  CALL CALCFLOW_DV(ncv, nfc, nvx, meth, geo, geod, mpg, mpgd, pl%ti, pld&
&            %ti, floi0_mdf, floi0_mdfd, co%chci, cod%chci, fhi0_mdf, &
&            fhi0_mdfd, dumm0, dumm0d, dumm1, dumm1d, nbdirs)
  DO nd=1,nbdirs
!
!   ..compute ion fluxes
    dvd%fhi(nd, :, 0) = fhi0_mdfd(nd, :, 0) + tif*(floi0d(nd, :, 0)-&
&     floi0_mdfd(nd, :, 0)) + (floi0(:, 0)-floi0_mdf(:, 0))*tifd(nd, :)
    dvd%fhi(nd, :, 1) = fhi0_mdfd(nd, :, 1) + tif*(floi0d(nd, :, 1)-&
&     floi0_mdfd(nd, :, 1)) + (floi0(:, 1)-floi0_mdf(:, 1))*tifd(nd, :)
!
    dvd%fhi_mdf(nd, :, 0) = fhi0_mdfd(nd, :, 0) - dvd%fhipsch(nd, :, 0)
    dvd%fhi_mdf(nd, :, 1) = fhi0_mdfd(nd, :, 1) - dvd%fhipsch(nd, :, 1)
  END DO
  dv%fhi(:, 0) = fhi0_mdf(:, 0) + (floi0(:, 0)-floi0_mdf(:, 0))*tif
  dv%fhi(:, 1) = fhi0_mdf(:, 1) + (floi0(:, 1)-floi0_mdf(:, 1))*tif
  dv%fhi_mdf(:, 0) = fhi0_mdf(:, 0) - dv%fhipsch(:, 0)
  dv%fhi_mdf(:, 1) = fhi0_mdf(:, 1) - dv%fhipsch(:, 1)
!
!   ..compute floi, coni
  CALL CALCCOEF_DV(ncv, nfc, nvx, meth, geo, floi0_mdf, floi0_mdfd, co%&
&            chci, cod%chci, floi0, floi0d, coni0, coni0d, nbdirs)
  DO nd=1,nbdirs
!wdk now we always add PSch linearization; do we need to condition
!    this on mdf /= 0?
    dvd%floi(nd, :, :) = dvd%floi(nd, :, :) + floi0d(nd, :, :)
    dvd%coni(nd, :, :, 0) = dvd%coni(nd, :, :, 0) + coni0d(nd, :, :)
  END DO
  dv%floi = dv%floi + floi0
  dv%coni(:, :, 0) = dv%coni(:, :, 0) + coni0
!
!   ..compute fhn, flon, conn
  IF (switch%tn_style .LT. 2) THEN
    DO nd=1,nbdirs
!set zero if no separate equation for tn is solved
      dvd%fhn(nd, :, :) = 0.D0
      dvd%flon(nd, :, :) = 0.D0
      dvd%conn(nd, :, :) = 0.D0
    END DO
    dv%fhn = 0.0_R8
    dv%flon = 0.0_R8
    dv%conn = 0.0_R8
  ELSE
!     ..apply discretization scheme
    CALL CALCFLOW_DV(ncv, nfc, nvx, meth, geo, geod, mpg, mpgd, pl%tn, &
&              pld%tn, flon0, flon0d, co%chcn, cod%chcn, dv%fhn, dvd%fhn&
&              , dumm0, dumm0d, dumm1, dumm1d, nbdirs)
!     ..compute flon, conn
    CALL CALCCOEF_DV(ncv, nfc, nvx, meth, geo, flon0, flon0d, co%chcn, &
&              cod%chcn, dv%flon, dvd%flon, dv%conn, dvd%conn, nbdirs)
  END IF
!
  IF (switch%solve_keps .GT. 0) THEN
!     ..compute ExB heat fluxes (ion and electron (!))
    DO nd=1,nbdirs
      flo0_exbd(nd, :, :) = 1.5_R8*dvd%fna_exb(nd, :, :)
    END DO
    flo0_exb = 1.5_R8*dv%fna_exb
    CALL CALCFLOW_DV(ncv, nfc, nvx, meth, geo, geod, mpg, mpgd, pl%ti, &
&              pld%ti, flo0_exb, flo0_exbd, co%chci_exb, cod%chci_exb, &
&              dv%fhi_exb, dvd%fhi_exb, dumm0, dumm0d, dumm1, dumm1d, &
&              nbdirs)
    CALL CALCFLOW_DV(ncv, nfc, nvx, meth, geo, geod, mpg, mpgd, pl%te, &
&              pld%te, flo0_exb, flo0_exbd, co%chce_exb, cod%chce_exb, &
&              dv%fhe_exb, dvd%fhe_exb, dumm0, dumm0d, dumm1, dumm1d, &
&              nbdirs)
!
!     ..fluxes for k-equation
!wdk    fflokt and fconkt: fudge factors to approximate enhanced parallel
!wdk    transport of k due to term
!wdk    related to (fluctuations in parallel current) * (fluctuations in potential)
!wdk    factor fconkt now included in b2trno
!wdk    in principle: models same effect as skt_diss (thus: needs b2sikt_fac_sheath_* = 0.0)
    DO nd=1,nbdirsmax
      flokt0d(nd, :, :) = 0.D0
    END DO
    DO nd=1,nbdirsmax
      cdkt0d(nd, :, :) = 0.D0
    END DO
    DO nd=1,nbdirs
      flokt0d(nd, :, 0) = dv%fna(:, 0, ismain)*switchd%b2tfhi_fflokt(nd)&
&       + (switch%b2tfhi_fflokt+1.0_R8)*dvd%fna(nd, :, 0, ismain)
      flokt0d(nd, :, 1) = dvd%fna(nd, :, 1, ismain)
      cdkt0d(nd, :, 0) = cod%cdkt(nd, :, 0)
      cdkt0d(nd, :, 1) = cod%cdkt(nd, :, 1)
    END DO
    flokt0(:, 0) = (1.0_R8+switch%b2tfhi_fflokt)*dv%fna(:, 0, ismain)
    flokt0(:, 1) = dv%fna(:, 1, ismain)
    cdkt0(:, 0) = co%cdkt(:, 0)
    cdkt0(:, 1) = co%cdkt(:, 1)
!
!     ..apply discretization scheme
    CALL CALCFLOW_DV(ncv, nfc, nvx, meth, geo, geod, mpg, mpgd, pl%kt, &
&              pld%kt, flokt0, flokt0d, cdkt0, cdkt0d, dv%fkt, dvd%fkt, &
&              dumm0, dumm0d, dumm1, dumm1d, nbdirs)
!
!     ..contribution from ExB heat fluxes
!wdk    at the moment: experimental; it is expected that this contriution
!wdk    is at least partially compensated by the (divergence of the) term
!wdk    related to (fluctuations in diamagnetic current) * (fluctuations in potential)
    DO nd=1,nbdirs
      dvd%fkt(nd, :, :) = dvd%fkt(nd, :, :) + 2.0_R8*((dv%fhi_exb+dv%&
&       fhe_exb)*switchd%b2tfhi_fkt_hie(nd)/3.0_R8+switch%b2tfhi_fkt_hie&
&       *(dvd%fhi_exb(nd, :, :)+dvd%fhe_exb(nd, :, :))/3.0_R8)
    END DO
    dv%fkt = dv%fkt + switch%b2tfhi_fkt_hie*2.0_R8/3.0_R8*(dv%fhi_exb+dv&
&     %fhe_exb)
!
!     ..compute flokt, conkt
    CALL CALCCOEF_DV(ncv, nfc, nvx, meth, geo, flokt0, flokt0d, cdkt0, &
&              cdkt0d, dv%flokt, dvd%flokt, dv%conkt, dvd%conkt, nbdirs)
!
    IF (switch%solve_keps .GT. 1) THEN
!
!     ..fluxes for z-equation
      DO nd=1,nbdirsmax
        flozt0d(nd, :, :) = 0.D0
      END DO
      DO nd=1,nbdirsmax
        cdzt0d(nd, :, :) = 0.D0
      END DO
      DO nd=1,nbdirs
        flozt0d(nd, :, 0) = dv%fna(:, 0, ismain)*switchd%b2tfhi_fflozt(&
&         nd) + (switch%b2tfhi_fflozt+1.0_R8)*dvd%fna(nd, :, 0, ismain)
        flozt0d(nd, :, 1) = dvd%fna(nd, :, 1, ismain)
        cdzt0d(nd, :, 0) = cod%cdzt(nd, :, 0)
        cdzt0d(nd, :, 1) = cod%cdzt(nd, :, 1)
      END DO
      flozt0(:, 0) = (1.0_R8+switch%b2tfhi_fflozt)*dv%fna(:, 0, ismain)
      flozt0(:, 1) = dv%fna(:, 1, ismain)
      cdzt0(:, 0) = co%cdzt(:, 0)
      cdzt0(:, 1) = co%cdzt(:, 1)
!
!     ..apply discretization scheme
      CALL CALCFLOW_DV(ncv, nfc, nvx, meth, geo, geod, mpg, mpgd, pl%zt&
&                , pld%zt, flozt0, flozt0d, cdzt0, cdzt0d, dv%fzt, dvd%&
&                fzt, dumm0, dumm0d, dumm1, dumm1d, nbdirs)
!
!     ..compute flozt, conzt
      CALL CALCCOEF_DV(ncv, nfc, nvx, meth, geo, flozt0, flozt0d, cdzt0&
&                , cdzt0d, dv%flozt, dvd%flozt, dv%conzt, dvd%conzt, &
&                nbdirs)
!
    END IF
  END IF
!
!srv 18.06.08 }
!
!
!
  IF (switch%b2tfhi_iout .NE. 0) THEN
!srv 18.06.08 {
    CALL MY_OUT_US(70, nfc, 1, dv%floi(1, 0), 'b2tfhi__floi_th')
    CALL MY_OUT_US(70, nfc, 1, dv%floi(1, 1), 'b2tfhi__floi_r')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 0, 0), &
&                   'b2tfhi__coni_th0')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 0, 1), &
&                   'b2tfhi__coni_th1')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 0, 2), &
&                   'b2tfhi__coni_th2')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 1, 0), &
&                   'b2tfhi__coni_r0')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 1, 1), &
&                   'b2tfhi__coni_r1')
    CALL MY_OUT_US(70, nfc, 1, dv%coni(1, 1, 2), &
&                   'b2tfhi__coni_r2')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi(1, 0), 'b2tfhi__fhi_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi(1, 1), 'b2tfhi__fhi_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 0), &
&                   'b2tfhi__fhi_mdf_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 1), &
&                   'b2tfhi__fhi_mdf_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 0), &
&                   'b2tfhi__fhiPSch_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 1), &
&                   'b2tfhi__fhiPSch_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvispar(1, 0), &
&                   'b2tfhi__fchvispar_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvispar(1, 1), &
&                   'b2tfhi__fchvispar_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisq(1, 0), &
&                   'b2tfhi__fchvisq_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisq(1, 1), &
&                   'b2tfhi__fchvisq_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisper(1, 0), &
&                   'b2tfhi__fchvisper_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchvisper(1, 1), &
&                   'b2tfhi__fchvisper_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchinert(1, 0), &
&                   'b2tfhi__fchinert_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchinert(1, 1), &
&                   'b2tfhi__fchinert_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fchanml(1, 0), &
&                   'b2tfhi__fchanml_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fchanml(1, 1), &
&                   'b2tfhi__fchanml_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fni_he(1, 0), 'b2tfhi__fni_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fni_he(1, 1), 'b2tfhi__fni_r')
    CALL MY_OUT_US(70, ncv, 0, dv%ni(:, 0), 'b2tfhi__ni0')
    CALL MY_OUT_US(70, ncv, 0, dv%ni(:, 1), 'b2tfhi__ni1')
! neutrals
    CALL MY_OUT_US(70, nfc, 1, dv%flon(1, 0), 'b2tfhi__flon_th')
    CALL MY_OUT_US(70, nfc, 1, dv%flon(1, 1), 'b2tfhi__flon_r')
    CALL MY_OUT_US(70, nfc, 1, dv%conn(1, 0), 'b2tfhi__conn_th')
    CALL MY_OUT_US(70, nfc, 1, dv%conn(1, 1), 'b2tfhi__conn_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhn(1, 0), 'b2tfhi__fhn_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhn(1, 1), 'b2tfhi__fhn_r')
  END IF
!
  IF (switch%iout_b2wdat .EQ. 4) THEN
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 0), &
&                   'b2tfhi__fhi_mdf_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi_mdf(1, 1), &
&                   'b2tfhi__fhi_mdf_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 0), &
&                   'b2tfhi__fhiPSch_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhipsch(1, 1), &
&                   'b2tfhi__fhiPSch_r')
  END IF
!
! ..return
  ncall_b2tfhi = ncall_b2tfhi + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2TFHI__DV

