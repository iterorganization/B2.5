&   , t_ad(nbdirsmax, ncv, 0:ns-1), wrkvd(nbdirsmax, nvx)
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(kind=r8), DIMENSION(ncv) :: temp0
    INTEGER :: nbdirs
!
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TGTA calculates temperature gradients and mass-weighted
!     average flow velocities for all ions.
!
!  3. description
!
!     This routine computes:
!     rho_A_rel: relative mass density of the ion "a" to the total ion mass density
!     avm_u: mass-weighted average flow velocity of all ions
!     gtalc: effective temperature gradient of the isonuclear sequence
!           (cell-centred quantity)
!     gt_Ac: temperature gradient of each ion charge state
!           (cell-centred quantity)
!
!-----------------------------------------------------------------------
!.computation
!
!   ..relative mass density and mass-weighted velocity
    rho = 0.0_R8
    DO nd=1,nbdirs
      avm_ud(nd, :) = 0.D0
    END DO
    avm_u = 0.0_R8
    rhod = 0.D0
    DO is=0,ns-1
      temp = is_i(is)*am(is)
      rho_a_rel(:, is) = temp*na(:, is)
      DO nd=1,nbdirs
        rho_a_reld(nd, :, is) = temp*nad(nd, :, is)
        rhod(nd, :) = rhod(nd, :) + rho_a_reld(nd, :, is)
        avm_ud(nd, :) = avm_ud(nd, :) + ua(:, is)*rho_a_reld(nd, :, is) &
&         + rho_a_rel(:, is)*uad(nd, :, is)
      END DO
      rho = rho + rho_a_rel(:, is)
      avm_u = avm_u + rho_a_rel(:, is)*ua(:, is)
    END DO
    DO is=0,ns-1
      temp0 = rho_a_rel(:, is)/rho
      DO nd=1,nbdirs
        rho_a_reld(nd, :, is) = (rho_a_reld(nd, :, is)-temp0*rhod(nd, :)&
&         )/rho
      END DO
      rho_a_rel(:, is) = temp0
    END DO
    DO nd=1,nbdirs
      avm_ud(nd, :) = (avm_ud(nd, :)-avm_u*rhod(nd, :)/rho)/rho
    END DO
    avm_u = avm_u/rho
    w_ad = 0.D0
    t_ad = 0.D0
!
!   ..temperature of each ion charge state
    DO is=0,ns-1
      w_a(:, is) = ua(:, is) - avm_u
      temp = switch%zhd_corr*switch%zhd_corrt*am(is)*mp
      DO nd=1,nbdirs
        w_ad(nd, :, is) = uad(nd, :, is) - avm_ud(nd, :)
        t_ad(nd, :, is) = tid(nd, :) + temp*2*w_a(:, is)*w_ad(nd, :, is)&
&         /3.0_R8
      END DO
      t_a(:, is) = ti + temp*(w_a(:, is)*w_a(:, is)/3.0_R8)
    END DO
!
!   ..temperature gradient of each ion charge state  in the cell center
    DO nd=1,nbdirs
      gt_acd(nd, :, :) = 0.D0
    END DO
    gt_ac = 0.0_R8
    wrkvd = 0.D0
    DO is=0,ns-1
      IF (.NOT.is_neutral(is)) THEN
        CALL GRADC_P_DV(ncv, nfc, nvx, 0, geo, geod, mpg, mpgd, t_a(:, &
&                 is), t_ad(:, :, is), wrkv, wrkvd, gt_ac(:, is), gt_acd&
&                 (:, :, is), nbdirs)
        DO nd=1,nbdirs
          gt_acd(nd, :, is) = geo%cvbb(:, 0)*gt_acd(nd, :, is)/geo%cvbb(&
&           :, 3)
        END DO
        gt_ac(:, is) = gt_ac(:, is)*geo%cvbb(:, 0)/geo%cvbb(:, 3)
      END IF
    END DO
!
!   ..effective temperature gradient of each isonuclear sequence in the cell center
    DO nd=1,nbdirs
      gtalcd(nd, :, :) = 0.D0
    END DO
    gtalc = 0.0_R8
    DO inucl=0,nspecies-1
      DO iz=1,znnucl_int(inucl)
        temp0 = na(:, nucl2s(inucl, iz))/nal(:, inucl)
        DO nd=1,nbdirs
          gtalcd(nd, :, inucl) = gtalcd(nd, :, inucl) + gt_ac(:, nucl2s(&
&           inucl, iz))*(nad(nd, :, nucl2s(inucl, iz))-temp0*nald(nd, :&
&           , inucl))/nal(:, inucl) + temp0*gt_acd(nd, :, nucl2s(inucl, &
&           iz))
        END DO
        gtalc(:, inucl) = gtalc(:, inucl) + temp0*gt_ac(:, nucl2s(inucl&
&         , iz))
      END DO
    END DO
!
!   ..average velocity gradient in the cell center
    CALL GRADC_P_DV(ncv, nfc, nvx, 0, geo, geod, mpg, mpgd, avm_u, &
&             avm_ud, wrkv, wrkvd, gavm_uc, gavm_ucd, nbdirs)
    DO nd=1,nbdirs
      gavm_ucd(nd, :) = geo%cvbb(:, 0)*gavm_ucd(nd, :)/geo%cvbb(:, 3)
    END DO
    gavm_uc = gavm_uc*geo%cvbb(:, 0)/geo%cvbb(:, 3)
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_TGTA_DV

!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_TGTA(ncv, nfc, nvx, ns, switch, geo, mpg, na, &
&   ua, ti)
    USE B2MOD_CONSTANTS
    USE B2US_GEO_DIFFV
    USE B2US_MAP_DIFFV
    USE B2MOD_SWITCHES_DIFFV
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!
!.declarations
!   ..input arguments
    INTEGER :: ncv, nfc, nvx, ns
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(SWITCHES), INTENT(IN) :: switch
    REAL(kind=r8) :: na(ncv, 0:ns-1), ua(ncv, 0:ns-1), ti(ncv)
!
!   ..local variables
    INTEGER :: is, iz, inucl
    REAL(kind=r8) :: rho(ncv), w_a(ncv, 0:ns-1), t_a(ncv, 0:ns-1), wrkv(&
&   nvx)
!
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TGTA calculates temperature gradients and mass-weighted
!     average flow velocities for all ions.
!
!  3. description
!
!     This routine computes:
!     rho_A_rel: relative mass density of the ion "a" to the total ion mass density
!     avm_u: mass-weighted average flow velocity of all ions
!     gtalc: effective temperature gradient of the isonuclear sequence
!           (cell-centred quantity)
!     gt_Ac: temperature gradient of each ion charge state
!           (cell-centred quantity)
!
!-----------------------------------------------------------------------
!.computation
!
!   ..relative mass density and mass-weighted velocity
    rho = 0.0_R8
    avm_u = 0.0_R8
    DO is=0,ns-1
      rho_a_rel(:, is) = is_i(is)*am(is)*na(:, is)
      rho = rho + rho_a_rel(:, is)
      avm_u = avm_u + rho_a_rel(:, is)*ua(:, is)
    END DO
    DO is=0,ns-1
      rho_a_rel(:, is) = rho_a_rel(:, is)/rho
    END DO
    avm_u = avm_u/rho
!
!   ..temperature of each ion charge state
    DO is=0,ns-1
      w_a(:, is) = ua(:, is) - avm_u
      t_a(:, is) = ti + switch%zhd_corr*switch%zhd_corrt*(am(is)*mp*w_a(&
&       :, is)**2)/3.0_R8
    END DO
!
!   ..temperature gradient of each ion charge state  in the cell center
    gt_ac = 0.0_R8
    DO is=0,ns-1
      IF (.NOT.is_neutral(is)) THEN
        CALL GRADC_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, t_a(:, is), wrkv&
&                     , gt_ac(:, is))
        gt_ac(:, is) = gt_ac(:, is)*geo%cvbb(:, 0)/geo%cvbb(:, 3)
      END IF
    END DO
!
!   ..effective temperature gradient of each isonuclear sequence in the cell center
    gtalc = 0.0_R8
    DO inucl=0,nspecies-1
      DO iz=1,znnucl_int(inucl)
        gtalc(:, inucl) = gtalc(:, inucl) + na(:, nucl2s(inucl, iz))/nal&
&         (:, inucl)*gt_ac(:, nucl2s(inucl, iz))
      END DO
    END DO
!
!   ..average velocity gradient in the cell center
    CALL GRADC_P_NODIFF(ncv, nfc, nvx, 0, geo, mpg, avm_u, wrkv, gavm_uc&
&                )
    gavm_uc = gavm_uc*geo%cvbb(:, 0)/geo%cvbb(:, 3)
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_TGTA

!  Differentiation of b2mod_zhfrtf_fr in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: smfr_p
!   with respect to varying inputs: *z2n_xy *av_ualpha *c_r_w coef
!                smfr_p
!   Plus diff mem management of: z2n_xy:in av_ualpha:in c_r_w:in
!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_FR_DV(icv, is, coef, coefd, smfr_p, smfr_pd, &
&   nbdirs)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!  Hint: nbdirsmax should be the maximum number of differentiation directions
    INTEGER :: icv, is, inucl
    REAL(kind=r8) :: coef
    REAL(kind=r8), DIMENSION(nbdirsmax) :: coefd
    REAL(kind=r8), DIMENSION(0:1) :: smfr_p
    REAL(kind=r8), DIMENSION(nbdirsmax, 0:1) :: smfr_pd
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_FR calculates the friction force.
!
!  3. description
!
!     This routine computes:
!     -smfr_p(0): smbfrial(iCv,0) part of the friction force
!     -smfr_p(1): smbfrial(iCv,1) part of the friction force
!
!-----------------------------------------------------------------------
!  ..local variabless
    REAL(kind=r8) :: temp
    REAL(kind=r8), DIMENSION(nbdirsmax) :: tempd
    INTEGER :: nd
    REAL(kind=r8) :: temp0
    REAL(kind=r8) :: temp1
    REAL(kind=r8) :: temp2
    INTEGER :: nbdirs
!
!   ..the first part of the friction force
    temp = 0
    DO nd=1,nbdirsmax
      tempd(nd) = 0.D0
    END DO
    DO inucl=0,nspecies-1
      temp0 = rmu(inucl, s2nucl(is)-1)
      temp1 = c_r_w(icv, inucl, s2nucl(is)-1)
      DO nd=1,nbdirs
        tempd(nd) = tempd(nd) + temp0*(temp1*z2n_xyd(nd, icv, inucl)+&
&         z2n_xy(icv, inucl)*c_r_wd(nd, icv, inucl, s2nucl(is)-1))
      END DO
      temp = temp + temp0*(z2n_xy(icv, inucl)*temp1)
    END DO
    DO nd=1,nbdirs
      smfr_pd(nd, 1) = -(temp*coefd(nd)+coef*tempd(nd))
    END DO
    smfr_p(1) = -(coef*temp)
!
!   ..the second part of the friction force
    temp = 0
    DO nd=1,nbdirsmax
      tempd(nd) = 0.D0
    END DO
    DO inucl=0,nspecies-1
      temp1 = rmu(inucl, s2nucl(is)-1)
      temp0 = z2n_xy(icv, inucl)*av_ualpha(icv, inucl)
      temp2 = c_r_w(icv, inucl, s2nucl(is)-1)
      DO nd=1,nbdirs
        tempd(nd) = tempd(nd) + temp1*(temp0*c_r_wd(nd, icv, inucl, &
&         s2nucl(is)-1)+temp2*(av_ualpha(icv, inucl)*z2n_xyd(nd, icv, &
&         inucl)+z2n_xy(icv, inucl)*av_ualphad(nd, icv, inucl)))
      END DO
      temp = temp + temp1*(temp2*temp0)
    END DO
    DO nd=1,nbdirs
      smfr_pd(nd, 0) = temp*coefd(nd) + coef*tempd(nd)
    END DO
    smfr_p(0) = coef*temp
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_FR_DV

!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_FR(icv, is, coef, smfr_p)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER :: icv, is, inucl
    REAL(kind=r8) :: coef
    REAL(kind=r8), DIMENSION(0:1) :: smfr_p
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_FR calculates the friction force.
!
!  3. description
!
!     This routine computes:
!     -smfr_p(0): smbfrial(iCv,0) part of the friction force
!     -smfr_p(1): smbfrial(iCv,1) part of the friction force
!
!-----------------------------------------------------------------------
!  ..local variabless
    REAL(kind=r8) :: temp
!
!   ..the first part of the friction force
    temp = 0
    DO inucl=0,nspecies-1
      temp = temp + z2n_xy(icv, inucl)*rmu(inucl, s2nucl(is)-1)*c_r_w(&
&       icv, inucl, s2nucl(is)-1)
    END DO
    smfr_p(1) = -(coef*temp)
!
!   ..the second part of the friction force
    temp = 0
    DO inucl=0,nspecies-1
      temp = temp + z2n_xy(icv, inucl)*rmu(inucl, s2nucl(is)-1)*c_r_w(&
&       icv, inucl, s2nucl(is)-1)*av_ualpha(icv, inucl)
    END DO
    smfr_p(0) = coef*temp
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_FR

!  Differentiation of b2mod_zhfrtf_tf in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: smtf
!   with respect to varying inputs: *nal *ia *gt_ac *gtalc *c_r_ta
!                *c_r_tb nais
!   Plus diff mem management of: nal:in ia:in gt_ac:in gtalc:in
!                c_r_ta:in c_r_tb:in
!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_TF_DV(icv, is, zhcscorr, nais, naisd, smtf, &
&   smtfd, smtf_nofl, nbdirs)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!  Hint: nbdirsmax should be the maximum number of differentiation directions
    INTEGER :: icv, is, zhcscorr
    REAL(kind=r8) :: smtf, smtf_nofl, nais
    REAL(kind=r8), DIMENSION(nbdirsmax) :: smtfd, naisd
    INTRINSIC SUM
    REAL(kind=r8), DIMENSION(nspecies) :: arg1
    REAL(kind=r8), DIMENSION(nbdirsmax, nspecies) :: arg1d
    INTEGER :: nd
    REAL(kind=r8), DIMENSION(nspecies) :: temp
    REAL(kind=r8), DIMENSION(nspecies) :: temp0
    REAL(kind=r8) :: temp1
    REAL(kind=r8) :: temp2
    REAL(kind=r8) :: temp3
    REAL(kind=r8) :: temp4
    REAL(kind=r8) :: temp5
    REAL(kind=r8) :: temp6
    REAL(kind=r8) :: temp7
    INTEGER :: nbdirs
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TF calculates the thermal force.
!
!  3. description
!
!     This routine computes:
!     -smtf: the thermal force
!     -smtf_nofl: the thermal force without flux limiting
!
!-----------------------------------------------------------------------
!
!   ..the thermal force before charge state correction
    temp = nal(icv, 0:nspecies-1)*gtalc(icv, 0:nspecies-1)
    temp0 = c_r_ta(icv, s2nucl(is)-1, 0:nspecies-1)
    arg1 = temp0*temp
    temp1 = SUM(arg1)
    DO nd=1,nbdirs
      arg1d(nd, :) = temp*c_r_tad(nd, icv, s2nucl(is)-1, 0:nspecies-1) +&
&       temp0*(gtalc(icv, 0:nspecies-1)*nald(nd, icv, 0:nspecies-1)+nal(&
&       icv, 0:nspecies-1)*gtalcd(nd, icv, 0:nspecies-1))
      smtfd(nd) = -(temp1*iad(nd, icv, is)+ia(icv, is)*SUM(arg1d(nd, :))&
&       )
    END DO
    smtf = -(ia(icv, is)*temp1)
!
!   ..the thermal force before charge state correction without flux limiting
    arg1 = nal(icv, 0:nspecies-1)*c_r_ta_nofl(icv, s2nucl(is)-1, 0:&
&     nspecies-1)*gtalc(icv, 0:nspecies-1)
    smtf_nofl = -(ia(icv, is)*SUM(arg1))
!
!   ..apply the charge state correction
    IF (zhcscorr .EQ. 1) THEN
      temp1 = gtalc(icv, s2nucl(is)-1)
      temp2 = nal(icv, s2nucl(is)-1)
      temp3 = nais*gt_ac(icv, is)/temp2
      temp4 = temp3 - ia(icv, is)*temp1
      temp5 = nal(icv, s2nucl(is)-1)
      temp6 = c_r_tb(icv, s2nucl(is)-1)
      temp7 = temp6*temp5
      DO nd=1,nbdirs
        smtfd(nd) = smtfd(nd) - temp4*(temp5*c_r_tbd(nd, icv, s2nucl(is)&
&         -1)+temp6*nald(nd, icv, s2nucl(is)-1)) - temp7*((gt_ac(icv, is&
&         )*naisd(nd)+nais*gt_acd(nd, icv, is)-temp3*nald(nd, icv, &
&         s2nucl(is)-1))/temp2-temp1*iad(nd, icv, is)-ia(icv, is)*gtalcd&
&         (nd, icv, s2nucl(is)-1))
      END DO
      smtf = smtf - temp7*temp4
!
      smtf_nofl = smtf_nofl - c_r_tb_nofl(icv, s2nucl(is)-1)*nal(icv, &
&       s2nucl(is)-1)*(nais/nal(icv, s2nucl(is)-1)*gt_ac(icv, is)-ia(icv&
&       , is)*gtalc(icv, s2nucl(is)-1))
    END IF
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_TF_DV

!
!...................................................................................
  SUBROUTINE B2MOD_ZHFRTF_TF(icv, is, zhcscorr, nais, smtf, smtf_nofl)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER :: icv, is, zhcscorr
    REAL(kind=r8) :: smtf, smtf_nofl, nais
    INTRINSIC SUM
    REAL(kind=r8), DIMENSION(nspecies) :: arg1
!-----------------------------------------------------------------------
!.documentation
!
!  1. purpose
!
!     B2MOD_ZHFRTF_TF calculates the thermal force.
!
!  3. description
!
!     This routine computes:
!     -smtf: the thermal force
!     -smtf_nofl: the thermal force without flux limiting
!
!-----------------------------------------------------------------------
!
!   ..the thermal force before charge state correction
    arg1 = nal(icv, 0:nspecies-1)*c_r_ta(icv, s2nucl(is)-1, 0:nspecies-1&
&     )*gtalc(icv, 0:nspecies-1)
    smtf = -(ia(icv, is)*SUM(arg1))
!
!   ..the thermal force before charge state correction without flux limiting
    arg1 = nal(icv, 0:nspecies-1)*c_r_ta_nofl(icv, s2nucl(is)-1, 0:&
&     nspecies-1)*gtalc(icv, 0:nspecies-1)
    smtf_nofl = -(ia(icv, is)*SUM(arg1))
!
!   ..apply the charge state correction
    IF (zhcscorr .EQ. 1) THEN
      smtf = smtf - c_r_tb(icv, s2nucl(is)-1)*nal(icv, s2nucl(is)-1)*(&
&       nais/nal(icv, s2nucl(is)-1)*gt_ac(icv, is)-ia(icv, is)*gtalc(icv&
&       , s2nucl(is)-1))
!
      smtf_nofl = smtf_nofl - c_r_tb_nofl(icv, s2nucl(is)-1)*nal(icv, &
&       s2nucl(is)-1)*(nais/nal(icv, s2nucl(is)-1)*gt_ac(icv, is)-ia(icv&
&       , is)*gtalc(icv, s2nucl(is)-1))
    END IF
!
    RETURN
  END SUBROUTINE B2MOD_ZHFRTF_TF
