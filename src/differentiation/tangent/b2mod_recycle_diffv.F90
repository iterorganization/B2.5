!  Differentiation of calcincidentfluxesmaxwellian in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: int4l int1l int2l int3l int0l
!                fnni feneni nni nnwwni fmomni
!   with respect to varying inputs: int4l int1l int2l int3l int0l
!                *(pl.na) *(pl.ua) tf
!   Plus diff mem management of: pl.na:in pl.ua:in
!
  SUBROUTINE CALCINCIDENTFLUXESMAXWELLIAN_DV(icv, ifc, isn, isign, istra&
&   , area, pl, pld, tf, tfd, geo, cosa, sina, fnni, fnnid, fmomni, &
&   fmomnid, nni, nnid, nnwwni, nnwwnid, feneni, fenenid, nbdirs)
!nh   08.05.2018
!     This routine calculates the incident neutral particle fluxes based on
!     a truncated Maxwellian for the incident neutrals. These particle fluxes
!     are necessary for the hybrid delta f model. It is assumed that only the
!     neutral parallel drift velocity is non-negligible (unf). This has to be
!     adapted.
    USE B2MOD_B2CMPA_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2US_GEO_DIFFV
    USE B2MOD_SUBSYS
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!     Input variables
!     isn is the neutral species index
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFFV), INTENT(IN) :: pld
    TYPE(GEOMETRY), INTENT(IN) :: geo
    INTEGER, INTENT(IN) :: icv, ifc, isn, istra
    REAL(kind=r8), INTENT(IN) :: area, tf, isign, cosa, sina
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(IN) :: tfd
!     Output variables
    REAL(kind=r8), INTENT(OUT) :: fnni, fmomni, nni, nnwwni, feneni
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(OUT) :: fnnid, fmomnid, &
&   nnid, nnwwnid, fenenid
!     Internal variables
    REAL(kind=r8) :: pit, pit2, mn, nnf, ux, uy, uz, i0l, i0r, i1l, i1r&
&   , i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r, fmox, fmoy, fmoz&
&   , pn, fmomnip, fmomnit, unf
    REAL(kind=r8), DIMENSION(nbdirsmax) :: nnfd, uxd, uyd, uzd, i0ld, &
&   i1ld, i2ld, i3ld, i4ld, fmoxd, fmoyd, fmozd, pnd, fmomnipd, fmomnitd&
&   , unfd
    INTRINSIC ABS
    INTEGER :: nd
    REAL(r8) :: temp
    REAL(kind=r8) :: temp0
    REAL(kind=r8) :: temp1
    INTEGER :: nbdirs
!
    CALL SUBINI('CalcIncidentFluxesMaxwellian')
!     Initialize/calculate some variables
    DO nd=1,nbdirs
      nnfd(nd) = pld%na(nd, icv, isn)
    END DO
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
    DO nd=1,nbdirs
!nh   neutral velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and only neu
!tral parallel
!     velocity component
      unfd(nd) = pld%ua(nd, icv, isn)
      uxd(nd) = pit2*unfd(nd)
      uyd(nd) = -(sina*isign*pit*unfd(nd))
      uzd(nd) = -(cosa*isign*pit*unfd(nd))
    END DO
    unf = pl%ua(icv, isn)
    ux = unf*pit2
    uy = -(sina*isign*unf*pit)
    uz = -(cosa*isign*unf*pit)
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN_DV(uz, uzd, tf, tfd, mn, i0l, i0ld, i0r, &
&                           i1l, i1ld, i1r, i2l, i2ld, i2r, i3l, i3ld, &
&                           i3r, i4l, i4ld, i4r, i5l, i5r, i6l, i6r, &
&                           nbdirs)
!
!     Pressure of incident neutrals
    temp = 2.0_R8*tf/mn + ux*ux + uy*uy
    nni = nnf*i0l
!
!     Incident total energy flux density of neutrals
    temp0 = tf + mn*(ux*ux+uy*uy)/2.0_R8
    temp1 = temp0*i1l + mn*i3l/2.0_R8
    DO nd=1,nbdirs
!
!     Incident particle flux density
      fnnid(nd) = -(i1l*nnfd(nd)+nnf*i1ld(nd))
!
!     Incident momentum flux densities
      fmoxd(nd) = -(mn*(i1l*(ux*nnfd(nd)+nnf*uxd(nd))+nnf*ux*i1ld(nd)))
      fmoyd(nd) = -(mn*(i1l*(uy*nnfd(nd)+nnf*uyd(nd))+nnf*uy*i1ld(nd)))
      fmozd(nd) = -(mn*(i2l*nnfd(nd)+nnf*i2ld(nd)))
      pnd(nd) = mn*((temp*i0l+i2l)*nnfd(nd)/3.0_R8+nnf*(i0l*(2.0_R8*tfd(&
&       nd)/mn+2*ux*uxd(nd)+2*uy*uyd(nd))+temp*i0ld(nd)+i2ld(nd))/3.0_R8&
&       )
!
!     Density of incident neutrals
      nnid(nd) = i0l*nnfd(nd) + nnf*i0ld(nd)
!
      nnwwnid(nd) = ux*nnid(nd) + nni*uxd(nd)
      fenenid(nd) = -(temp1*nnfd(nd)+nnf*(i1l*(tfd(nd)+mn*(2*ux*uxd(nd)+&
&       2*uy*uyd(nd))/2.0_R8)+temp0*i1ld(nd)+mn*i3ld(nd)/2.0_R8))
!
!     Convert particle flux densities in fluxes
      fnnid(nd) = area*fnnid(nd)
!
!     Calculate incident poloidal momentum flux of neutrals
      fmomnipd(nd) = isign*area*(cosa*fmozd(nd)+sina*fmoyd(nd)) + isign*&
&       area*cosa*pnd(nd)
      fmomnitd(nd) = -(area*fmoxd(nd))
      fmomnid(nd) = pit*fmomnipd(nd) + pit2*fmomnitd(nd)
!
!     Convert energy flux densities in net energy flux contributions
      fenenid(nd) = -(area*fenenid(nd))
    END DO
    fnni = -(nnf*i1l)
    fmox = -(mn*nnf*ux*i1l)
    fmoy = -(mn*nnf*uy*i1l)
    fmoz = -(mn*nnf*i2l)
    pn = mn*(nnf/3.0_R8*(temp*i0l+i2l))
    nnwwni = nni*ux
    feneni = -(nnf*temp1)
    fnni = fnni*area
    fmomnip = isign*(fmoz*cosa+fmoy*sina)*area + isign*pn*area*cosa
    fmomnit = -(fmox*area)
    fmomni = pit*fmomnip + pit2*fmomnit
    feneni = -(feneni*area)
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCINCIDENTFLUXESMAXWELLIAN_DV

!
