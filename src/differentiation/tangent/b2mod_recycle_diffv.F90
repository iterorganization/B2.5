&   area, pl, tf, geo, cosa, sina, fnni, fmomni, nni, nnwwni, feneni)
!nh   08.05.2018
!     This routine calculates the incident neutral particle fluxes based on
!     a truncated Maxwellian for the incident neutrals. These particle fluxes
!     are necessary for the hybrid delta f model. It is assumed that only the
!     neutral parallel drift velocity is non-negligible (unf). This has to be
!     adapted.
    USE B2MOD_B2CMPA_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2US_GEO_DIFFV
    USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!     Input variables
!     isn is the neutral species index
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(GEOMETRY), INTENT(IN) :: geo
    INTEGER, INTENT(IN) :: icv, ifc, isn, istra
    REAL(kind=r8), INTENT(IN) :: area, tf, isign, cosa, sina
!     Output variables
    REAL(kind=r8), INTENT(OUT) :: fnni, fmomni, nni, nnwwni, feneni
!     Internal variables
    REAL(kind=r8) :: pit, pit2, mn, nnf, ux, uy, uz, i0l, i0r, i1l, i1r&
&   , i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r, fmox, fmoy, fmoz&
&   , pn, fmomnip, fmomnit, unf
    INTRINSIC ABS
!
    CALL SUBINI('CalcIncidentFluxesMaxwellian')
!     Initialize/calculate some variables
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!nh   neutral velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and only neu
!tral parallel
!     velocity component
    unf = pl%ua(icv, isn)
    ux = unf*pit2
    uy = -(sina*isign*unf*pit)
    uz = -(cosa*isign*unf*pit)
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tf, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Incident particle flux density
    fnni = -(nnf*i1l)
!
!     Incident momentum flux densities
    fmox = -(mn*nnf*ux*i1l)
    fmoy = -(mn*nnf*uy*i1l)
    fmoz = -(mn*nnf*i2l)
!
!     Pressure of incident neutrals
    pn = mn/3.0_R8*(nnf*((2.0_R8*tf/mn+ux**2+uy**2)*i0l+i2l))
!
!     Density of incident neutrals
    nni = nnf*i0l
!
    nnwwni = nni*ux
!
!     Incident total energy flux density of neutrals
    feneni = -(nnf*((tf+mn/2.0_R8*(ux**2+uy**2))*i1l+mn/2.0_R8*i3l))
!
!     Convert particle flux densities in fluxes
    fnni = fnni*area
!
!     Calculate incident poloidal momentum flux of neutrals
    fmomnip = isign*(fmoz*cosa+fmoy*sina)*area + isign*pn*area*cosa
    fmomnit = -(fmox*area)
    fmomni = pit*fmomnip + pit2*fmomnit
!
!     Convert energy flux densities in net energy flux contributions
    feneni = -(feneni*area)
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCINCIDENTFLUXESMAXWELLIAN

!  Differentiation of calcrecycledfluxes in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: int4l int1l int2l int3l int0l
!                nnrec fnnrec fmomrec fenerec nnwwnrec
!   with respect to varying inputs: int4l int1l int2l int3l int0l
!                phi_app tif t0 recyc0 fluid_frac_hyb pof *(pl.ua)
!   Plus diff mem management of: pl.ua:in
!
  SUBROUTINE CALCRECYCLEDFLUXES_DV(icv, nci, ifc, isign, isn, iscx0, isi&
&   , istra, iwall, phi_app, phi_appd, area, recyc0, recyc0d, recycm, &
&   fluid_frac_hyb, fluid_frac_hybd, pl, pld, tif, tifd, tef, pof, pofd&
&   , nif, nef, geo, mpg, t0, t0d, cosa, sina, use_uy_uz_0, fnnrec, &
&   fnnrecd, fmomrec, fmomrecd, nnrec, nnrecd, nnwwnrec, nnwwnrecd, &
&   fenerec, fenerecd, nbdirs)
!mb   17.07.17
!     This routine calculates the recycled neutral particle, momentum and energy fluxes based
!     on the diffusion approximation (see papers of Niels Horsten).
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFFV
    USE B2MOD_NEUTRALS_NAMELIST_DIFFV, ONLY : e_fc, accel_ion
    USE B2US_GEO_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2US_MAP_DIFFV
    USE B2MOD_SUBSYS
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!     nif = plasma density on face, isn neutral species index
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFFV), INTENT(IN) :: pld
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
!!      type (B2Rates), intent (in) :: rt
    INTEGER, INTENT(IN) :: icv, nci, ifc, isn, iscx0, isi, iwall, istra&
&   , use_uy_uz_0
    REAL(kind=r8), INTENT(IN) :: phi_app, area, recyc0, recycm, pof, nif&
&   , nef, t0, isign, tif, tef, cosa, sina, fluid_frac_hyb
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(IN) :: phi_appd, recyc0d&
&   , pofd, t0d, tifd, fluid_frac_hybd
!! fluid_frac_hyb: in fluid neutral cases or standard spatially hybrid cases this multiplier is always equal to 1.
!! only in the generalized form of the spatially hybrid approach, where the fluid and kinetic treatment are mixed
!! on a single target stratum, this value can deviate from 1.
!     Output variables:
!     fmomni: momentum flux of incident neutrals.
    REAL(kind=r8), INTENT(OUT) :: fnnrec, fmomrec, nnrec, nnwwnrec, &
&   fenerec
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(OUT) :: fnnrecd, &
&   fmomrecd, nnrecd, nnwwnrecd, fenerecd
    EXTERNAL INTP_3DTABLE, INTP_3DTABLE00
    EXTERNAL INTP_3DTABLE_DV, INTP_3DTABLE00_DV
!     Internal variables
    REAL(kind=r8) :: cs, mj, shpottauj, pf, pcorf, pt, vt, pit, pit2, mn&
&   , ux, uz, i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r&
&   , i6l, i6r, rc1i, rmx1i, rmz1i, rp11i, rp21i, rp31i, re1i, fmox, &
&   fmoy, fmoz, fmomrecp, fmomrect, pnc, fenec, fdni, INTP_3DTABLE, &
&   INTP_3DTABLE00, tifev, upf, fna_mol_rec
    REAL(kind=r8), DIMENSION(nbdirsmax) :: csd, mjd, shpottaujd, pfd, &
&   pcorfd, ptd, uxd, uzd, i0ld, i1ld, i2ld, i3ld, i4ld, rc1id, rmx1id, &
&   rmz1id, rp11id, rp21id, rp31id, re1id, fmoxd, fmozd, fmomrecpd, &
&   fmomrectd, pncd, fenecd, fdnid, tifevd, upfd, fna_mol_recd
    REAL(kind=r8) :: tab(0:nm, 0:nt, 0:nshpottau), ff(0:1, 0:1, 0:1)
    INTEGER :: is, it, ilt, ilm, ilsh, ip1, ip2, ip3
    INTRINSIC ABS
    INTRINSIC SQRT
    INTRINSIC MAX
    INTRINSIC MIN
    INTRINSIC SIGN
    REAL(kind=r8) :: abs0
    REAL(kind=r8), DIMENSION(nbdirsmax) :: abs0d
    REAL(kind=r8) :: min1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: min1d
    REAL(kind=r8) :: min2
    REAL(kind=r8), DIMENSION(nbdirsmax) :: min2d
    REAL(r8) :: arg1
    REAL(r8), DIMENSION(nbdirsmax) :: arg1d
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(kind=r8) :: temp0
    REAL(kind=r8) :: temp1
    REAL(r8) :: temp2
    INTEGER :: nbdirs
!
    CALL SUBINI('CalcRecycledFluxes')
!     Initialize/calculate some variables
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
    DO nd=1,nbdirs
!mb   plasma velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and drifts
      upfd(nd) = pld%ua(nd, icv, isi)
      uxd(nd) = pit2*upfd(nd)
      uzd(nd) = -(cosa*isign*pit*upfd(nd))
!       ion particle flux density
      fdnid(nd) = t0d(nd)/area
    END DO
    upf = pl%ua(icv, isi)
    ux = upf*pit2
    uz = -(cosa*isign*upf*pit)
    fdni = t0/area
!
!     Assume poloidal and radial drifts are zero for momentum and energy
!     equations to reduce the dimensionality of the pre-evaluates integrals
!     of TRIM tables. Only toroidal drift remains!
    IF (use_uy_uz_0 .EQ. 0) THEN
      uz = 0.0_R8
      DO nd=1,nbdirsmax
        uzd(nd) = 0.D0
      END DO
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN_DV(uz, uzd, tif, tifd, mn, i0l, i0ld, i0r&
&                           , i1l, i1ld, i1r, i2l, i2ld, i2r, i3l, i3ld&
&                           , i3r, i4l, i4ld, i4r, i5l, i5r, i6l, i6r, &
&                           nbdirs)
    arg1 = 2.0_R8*tif/mn
    temp = SQRT(arg1)
    DO nd=1,nbdirs
!
!
!     Interpolate tables to find recycling values
!       Isothermal speed of sound and Mach number
      arg1d(nd) = 2.0_R8*tifd(nd)/mn
      IF (arg1 .EQ. 0.D0) THEN
        csd(nd) = 0.D0
      ELSE
        csd(nd) = arg1d(nd)/(2.0*temp)
      END IF
    END DO
    cs = temp
    IF (ux .GE. 0.) THEN
      DO nd=1,nbdirs
        abs0d(nd) = uxd(nd)
      END DO
      abs0 = ux
    ELSE
      DO nd=1,nbdirs
        abs0d(nd) = -uxd(nd)
      END DO
      abs0 = -ux
    END IF
!       Shpottau value: sheath_potential devided by tau (Ti/Te)
    temp = (pof-phi_app)/tif
    DO nd=1,nbdirs
      mjd(nd) = (abs0d(nd)-abs0*csd(nd)/cs)/cs
      tifevd(nd) = tifd(nd)/ev
      shpottaujd(nd) = accel_ion(istra)*qe*(pofd(nd)-phi_appd(nd)-temp*&
&       tifd(nd))/tif
    END DO
    mj = abs0/cs
    tifev = tif/ev
    is = recycle_index(isn)
    it = target_index(istra)
    shpottauj = accel_ion(istra)*qe*temp
    tab = rc1(:, :, :, is, it)
    CALL INTP_3DTABLE_DV(tab, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                  nshpottau, mj, mjd, tifev, tifevd, shpottauj, &
&                  shpottaujd, ilm, ilt, ilsh, ip1, ip2, ip3, rc1i, &
&                  rc1id, nbdirs)
    IF (rc1i .LT. 0.0_R8) THEN
      rc1i = 0.0_R8
      DO nd=1,nbdirsmax
        rc1id(nd) = 0.D0
      END DO
    ELSE
      rc1i = rc1i
    END IF
!
    ff = rmx1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_DV(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                    nshpottau, mj, mjd, tifev, tifevd, shpottauj, &
&                    shpottaujd, ilm, ilt, ilsh, rmx1i, rmx1id, nbdirs)
    IF (rmx1i .LT. 0.0_R8) THEN
      rmx1i = 0.0_R8
      DO nd=1,nbdirsmax
        rmx1id(nd) = 0.D0
      END DO
    ELSE
      rmx1i = rmx1i
    END IF
!
    ff = rmz1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_DV(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                    nshpottau, mj, mjd, tifev, tifevd, shpottauj, &
&                    shpottaujd, ilm, ilt, ilsh, rmz1i, rmz1id, nbdirs)
    IF (rmz1i .LT. 0.0_R8) THEN
      rmz1i = 0.0_R8
      DO nd=1,nbdirsmax
        rmz1id(nd) = 0.D0
      END DO
    ELSE
      rmz1i = rmz1i
    END IF
!
    ff = rp11(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_DV(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                    nshpottau, mj, mjd, tifev, tifevd, shpottauj, &
&                    shpottaujd, ilm, ilt, ilsh, rp11i, rp11id, nbdirs)
    IF (rp11i .LT. 0.0_R8) THEN
      rp11i = 0.0_R8
      DO nd=1,nbdirsmax
        rp11id(nd) = 0.D0
      END DO
    ELSE
      rp11i = rp11i
    END IF
!
    ff = rp21(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_DV(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                    nshpottau, mj, mjd, tifev, tifevd, shpottauj, &
&                    shpottaujd, ilm, ilt, ilsh, rp21i, rp21id, nbdirs)
    IF (rp21i .LT. 0.0_R8) THEN
      rp21i = 0.0_R8
      DO nd=1,nbdirsmax
        rp21id(nd) = 0.D0
      END DO
    ELSE
      rp21i = rp21i
    END IF
!
    ff = rp31(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_DV(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                    nshpottau, mj, mjd, tifev, tifevd, shpottauj, &
&                    shpottaujd, ilm, ilt, ilsh, rp31i, rp31id, nbdirs)
    IF (rp31i .LT. 0.0_R8) THEN
      rp31i = 0.0_R8
      DO nd=1,nbdirsmax
        rp31id(nd) = 0.D0
      END DO
    ELSE
      rp31i = rp31i
    END IF
!
    ff = re1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    CALL INTP_3DTABLE00_DV(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&                    nshpottau, mj, mjd, tifev, tifevd, shpottauj, &
&                    shpottaujd, ilm, ilt, ilsh, re1i, re1id, nbdirs)
    IF (re1i .LT. 0.0_R8) THEN
      re1i = 0.0_R8
      DO nd=1,nbdirsmax
        re1id(nd) = 0.D0
      END DO
    ELSE
      re1i = re1i
    END IF
    IF (rc1i*cs/(-i1l) .GT. recyc0) THEN
      DO nd=1,nbdirs
        min1d(nd) = recyc0d(nd)
      END DO
      min1 = recyc0
    ELSE
      temp = rc1i*cs/i1l
      DO nd=1,nbdirs
        min1d(nd) = -((cs*rc1id(nd)+rc1i*csd(nd)-temp*i1ld(nd))/i1l)
      END DO
      min1 = -temp
    END IF
!
!     Calculate fast and thermal recycled parts
    DO nd=1,nbdirs
      pfd(nd) = min1d(nd)
    END DO
    pf = min1 + 1.0e-30_R8
    IF (pf .GT. recyc0) THEN
      DO nd=1,nbdirs
        min2d(nd) = recyc0d(nd)
      END DO
      min2 = recyc0
    ELSE
      DO nd=1,nbdirs
        min2d(nd) = pfd(nd)
      END DO
      min2 = pf
    END IF
    pt = recyc0 - pf + 1.0e-30_R8
!     The reflection coefficients below already account for the probability of
!     fast particle reflection. Like in Eirene, we will only pump fast particles
!     once all thermal particles are pumped.
!
!nh 22.02.2020
!     Calculate the flux emitted as molecules. At the moment, molecules will be
!     treated kinetically.
    temp = 0.5_R8*recycm*area
    DO nd=1,nbdirs
      pcorfd(nd) = (min2d(nd)-min2*pfd(nd)/pf)/pf
      ptd(nd) = recyc0d(nd) - pfd(nd)
      fna_mol_recd(nd) = temp*(fdni*ptd(nd)+pt*fdnid(nd))
    END DO
    pcorf = min2/pf
!
    arg1 = 2.0_R8*e_fc*ev/mn
    vt = SQRT(arg1)
    fna_mol_rec = temp*(pt*fdni)
    fna_mol(ifc, isn) = fna_mol(ifc, isn) + fna_mol_rec
    IF (0.0_R8 .LT. (recyc0*t0-2.0_R8*fna_mol_rec)*fluid_frac_hyb) THEN
      temp = recyc0*t0 - 2.0_R8*fna_mol_rec
      DO nd=1,nbdirs
        fnnrecd(nd) = fluid_frac_hyb*(t0*recyc0d(nd)+recyc0*t0d(nd)-&
&         2.0_R8*fna_mol_recd(nd)) + temp*fluid_frac_hybd(nd)
      END DO
      fnnrec = temp*fluid_frac_hyb
    ELSE
      fnnrec = 0.0_R8
      DO nd=1,nbdirsmax
        fnnrecd(nd) = 0.D0
      END DO
    END IF
!
!     Recycled momentum flux densities
    temp = mn*SIGN(1.0_R8, ux)
    temp0 = pcorf*rmx1i*fdni*fluid_frac_hyb
    temp1 = cs*cs/i1l
    DO nd=1,nbdirs
      fmoxd(nd) = -(temp*(temp0*(2*cs*csd(nd)-temp1*i1ld(nd))/i1l+temp1*&
&       (fdni*fluid_frac_hyb*(rmx1i*pcorfd(nd)+pcorf*rmx1id(nd))+pcorf*&
&       rmx1i*(fluid_frac_hyb*fdnid(nd)+fdni*fluid_frac_hybd(nd)))))
      fmomrectd(nd) = area*fmoxd(nd)
    END DO
    fmox = -(temp*(temp1*temp0))
!
    fmoy = 0.0_R8
!
    temp1 = pcorf*rmz1i*fdni*fluid_frac_hyb
    temp0 = cs*cs/i1l
    DO nd=1,nbdirs
      fmozd(nd) = -(mn*(temp1*(2*cs*csd(nd)-temp0*i1ld(nd))/i1l+temp0*(&
&       fdni*fluid_frac_hyb*(rmz1i*pcorfd(nd)+pcorf*rmz1id(nd))+pcorf*&
&       rmz1i*(fluid_frac_hyb*fdnid(nd)+fdni*fluid_frac_hybd(nd)))))
    END DO
    fmoz = -(mn*(temp0*temp1))
!
    temp1 = 2.0_R8*mn*(-recycm+1)*vt
    DO nd=1,nbdirs
      fmozd(nd) = fmozd(nd) + temp1*(fdni*fluid_frac_hyb*ptd(nd)/3.0_R8+&
&       pt*(fluid_frac_hyb*fdnid(nd)+fdni*fluid_frac_hybd(nd))/3.0_R8)
    END DO
    fmoz = fmoz + temp1*(pt/3.0_R8*(fdni*fluid_frac_hyb))
!       Pressure of recycled neutrals
    temp1 = cs*cs/(3.0_R8*i1l)
    temp0 = pcorf*rp11i*fdni*fluid_frac_hyb
    DO nd=1,nbdirs
      pncd(nd) = -(mn*(temp1*(fdni*fluid_frac_hyb*(rp11i*pcorfd(nd)+&
&       pcorf*rp11id(nd))+pcorf*rp11i*(fluid_frac_hyb*fdnid(nd)+fdni*&
&       fluid_frac_hybd(nd)))+temp0*(2*cs*csd(nd)-temp1*3.0_R8*i1ld(nd))&
&       /(3.0_R8*i1l)))
    END DO
    pnc = -(mn*(temp0*temp1))
!
    temp1 = 2.0_R8*mn*(-recycm+1)*vt
    DO nd=1,nbdirs
      pncd(nd) = pncd(nd) + temp1*(fdni*fluid_frac_hyb*ptd(nd)/3.0_R8+pt&
&       *(fluid_frac_hyb*fdnid(nd)+fdni*fluid_frac_hybd(nd))/3.0_R8)
!
!     Calculate recycled poloidal momentum flux of neutrals
      fmomrecpd(nd) = isign*area*cosa*pncd(nd) - isign*area*cosa*fmozd(&
&       nd)
      fmomrecd(nd) = pit*fmomrecpd(nd) + pit2*fmomrectd(nd)
    END DO
    pnc = pnc + temp1*(pt/3.0_R8*(fdni*fluid_frac_hyb))
!       Density of recycled neutrals
    temp1 = pcorf*rp21i/i1l
    DO nd=1,nbdirs
      nnrecd(nd) = -(fdni*fluid_frac_hyb*(rp21i*pcorfd(nd)+pcorf*rp21id(&
&       nd)-temp1*i1ld(nd))/i1l+temp1*(fluid_frac_hyb*fdnid(nd)+fdni*&
&       fluid_frac_hybd(nd)))
      nnrecd(nd) = nnrecd(nd) + (1-recycm)*2.0_R8*(fluid_frac_hyb*(fdni*&
&       ptd(nd)+pt*fdnid(nd))/vt+pt*fdni*fluid_frac_hybd(nd)/vt)
    END DO
    nnrec = -(temp1*(fdni*fluid_frac_hyb))
    nnrec = nnrec + (1-recycm)*pt*fdni*2.0_R8/vt*fluid_frac_hyb
!       product of density and toroidal velocity
    temp2 = SIGN(1.0_R8, ux)
    temp1 = fluid_frac_hyb/i1l
    temp0 = pcorf*rp31i*cs*fdni
    DO nd=1,nbdirs
      nnwwnrecd(nd) = -(temp2*(temp1*(cs*fdni*(rp31i*pcorfd(nd)+pcorf*&
&       rp31id(nd))+pcorf*rp31i*(fdni*csd(nd)+cs*fdnid(nd)))+temp0*(&
&       fluid_frac_hybd(nd)-temp1*i1ld(nd))/i1l))
    END DO
    nnwwnrec = -(temp2*(temp0*temp1))
!
!     recycled total energy flux density of neutrals
    temp1 = cs*cs*cs
    temp0 = temp1*fdni*fluid_frac_hyb
    temp = pcorf*re1i/i1l
    DO nd=1,nbdirs
      fenecd(nd) = -(mn*0.5_R8*(temp0*(re1i*pcorfd(nd)+pcorf*re1id(nd)-&
&       temp*i1ld(nd))/i1l+temp*(fdni*fluid_frac_hyb*3*cs**2*csd(nd)+&
&       temp1*(fluid_frac_hyb*fdnid(nd)+fdni*fluid_frac_hybd(nd)))))
    END DO
    fenec = -(mn*0.5_R8*(temp*temp0))
    temp1 = 0.5_R8*mn*(-recycm+1)*(vt*vt)
    DO nd=1,nbdirs
      fenecd(nd) = fenecd(nd) + temp1*(fluid_frac_hyb*(fdni*ptd(nd)+pt*&
&       fdnid(nd))+pt*fdni*fluid_frac_hybd(nd))
!     Convert energy flux densities in energy fluxes
      fenerecd(nd) = area*fenecd(nd)
    END DO
    fenec = fenec + temp1*(pt*fdni*fluid_frac_hyb)
    fmomrecp = -(isign*(fmoz*cosa+fmoy*sina)*area) + isign*pnc*area*cosa
    fmomrect = fmox*area
    fmomrec = pit*fmomrecp + pit2*fmomrect
    fenerec = fenec*area
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCRECYCLEDFLUXES_DV

!
  SUBROUTINE CALCRECYCLEDFLUXES(icv, nci, ifc, isign, isn, iscx0, isi, &
&   istra, iwall, phi_app, area, recyc0, recycm, fluid_frac_hyb, pl, tif&
&   , tef, pof, nif, nef, geo, mpg, t0, cosa, sina, use_uy_uz_0, fnnrec&
&   , fmomrec, nnrec, nnwwnrec, fenerec)
!mb   17.07.17
!     This routine calculates the recycled neutral particle, momentum and energy fluxes based
!     on the diffusion approximation (see papers of Niels Horsten).
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFFV
    USE B2MOD_NEUTRALS_NAMELIST_DIFFV, ONLY : e_fc, accel_ion
    USE B2US_GEO_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2US_MAP_DIFFV
    USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!     nif = plasma density on face, isn neutral species index
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
!!      type (B2Rates), intent (in) :: rt
    INTEGER, INTENT(IN) :: icv, nci, ifc, isn, iscx0, isi, iwall, istra&
&   , use_uy_uz_0
    REAL(kind=r8), INTENT(IN) :: phi_app, area, recyc0, recycm, pof, nif&
&   , nef, t0, isign, tif, tef, cosa, sina, fluid_frac_hyb
!! fluid_frac_hyb: in fluid neutral cases or standard spatially hybrid cases this multiplier is always equal to 1.
!! only in the generalized form of the spatially hybrid approach, where the fluid and kinetic treatment are mixed
!! on a single target stratum, this value can deviate from 1.
!     Output variables:
!     fmomni: momentum flux of incident neutrals.
    REAL(kind=r8), INTENT(OUT) :: fnnrec, fmomrec, nnrec, nnwwnrec, &
&   fenerec
    EXTERNAL INTP_3DTABLE, INTP_3DTABLE00
!     Internal variables
    REAL(kind=r8) :: cs, mj, shpottauj, pf, pcorf, pt, vt, pit, pit2, mn&
&   , ux, uz, i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r&
&   , i6l, i6r, rc1i, rmx1i, rmz1i, rp11i, rp21i, rp31i, re1i, fmox, &
&   fmoy, fmoz, fmomrecp, fmomrect, pnc, fenec, fdni, INTP_3DTABLE, &
&   INTP_3DTABLE00, tifev, upf, fna_mol_rec
    REAL(kind=r8) :: tab(0:nm, 0:nt, 0:nshpottau), ff(0:1, 0:1, 0:1)
    INTEGER :: is, it, ilt, ilm, ilsh, ip1, ip2, ip3
    INTRINSIC ABS
    INTRINSIC SQRT
    INTRINSIC MAX
    INTRINSIC MIN
    INTRINSIC SIGN
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: min1
    REAL(kind=r8) :: min2
    REAL(r8) :: arg1
!
    CALL SUBINI('CalcRecycledFluxes')
!     Initialize/calculate some variables
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!mb   plasma velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and drifts
    upf = pl%ua(icv, isi)
    ux = upf*pit2
    uz = -(cosa*isign*upf*pit)
!       ion particle flux density
    fdni = t0/area
!
!     Assume poloidal and radial drifts are zero for momentum and energy
!     equations to reduce the dimensionality of the pre-evaluates integrals
!     of TRIM tables. Only toroidal drift remains!
    IF (use_uy_uz_0 .EQ. 0) uz = 0.0_R8
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!
!     Interpolate tables to find recycling values
!       Isothermal speed of sound and Mach number
    arg1 = 2.0_R8*tif/mn
    cs = SQRT(arg1)
    IF (ux .GE. 0.) THEN
      abs0 = ux
    ELSE
      abs0 = -ux
    END IF
    mj = abs0/cs
    tifev = tif/ev
    is = recycle_index(isn)
    it = target_index(istra)
!       Shpottau value: sheath_potential devided by tau (Ti/Te)
    shpottauj = accel_ion(istra)*qe*(pof-phi_app)/tif
    tab = rc1(:, :, :, is, it)
    rc1i = INTP_3DTABLE(tab, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh, ip1, ip2, ip3)
    IF (rc1i .LT. 0.0_R8) THEN
      rc1i = 0.0_R8
    ELSE
      rc1i = rc1i
    END IF
!
    ff = rmx1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rmx1i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rmx1i .LT. 0.0_R8) THEN
      rmx1i = 0.0_R8
    ELSE
      rmx1i = rmx1i
    END IF
!
    ff = rmz1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rmz1i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rmz1i .LT. 0.0_R8) THEN
      rmz1i = 0.0_R8
    ELSE
      rmz1i = rmz1i
    END IF
!
    ff = rp11(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rp11i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rp11i .LT. 0.0_R8) THEN
      rp11i = 0.0_R8
    ELSE
      rp11i = rp11i
    END IF
!
    ff = rp21(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rp21i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rp21i .LT. 0.0_R8) THEN
      rp21i = 0.0_R8
    ELSE
      rp21i = rp21i
    END IF
!
    ff = rp31(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    rp31i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (rp31i .LT. 0.0_R8) THEN
      rp31i = 0.0_R8
    ELSE
      rp31i = rp31i
    END IF
!
    ff = re1(ilm:ilm+1, ilt:ilt+1, ilsh:ilsh+1, is, it)
    re1i = INTP_3DTABLE00(ff, m, t, shpottau, dm, dt, dsh, nm, nt, &
&     nshpottau, mj, tifev, shpottauj, ilm, ilt, ilsh)
    IF (re1i .LT. 0.0_R8) THEN
      re1i = 0.0_R8
    ELSE
      re1i = re1i
    END IF
    IF (rc1i*cs/(-i1l) .GT. recyc0) THEN
      min1 = recyc0
    ELSE
      min1 = rc1i*cs/(-i1l)
    END IF
!
!     Calculate fast and thermal recycled parts
    pf = min1 + 1.0e-30_R8
    IF (pf .GT. recyc0) THEN
      min2 = recyc0
    ELSE
      min2 = pf
    END IF
    pcorf = min2/pf
    pt = recyc0 - pf + 1.0e-30_R8
!
    arg1 = 2.0_R8*e_fc*ev/mn
    vt = SQRT(arg1)
!     The reflection coefficients below already account for the probability of
!     fast particle reflection. Like in Eirene, we will only pump fast particles
!     once all thermal particles are pumped.
!
!nh 22.02.2020
!     Calculate the flux emitted as molecules. At the moment, molecules will be
!     treated kinetically.
    fna_mol_rec = recycm*pt*fdni*area*0.5_R8
    fna_mol(ifc, isn) = fna_mol(ifc, isn) + fna_mol_rec
    IF (0.0_R8 .LT. (recyc0*t0-2.0_R8*fna_mol_rec)*fluid_frac_hyb) THEN
      fnnrec = (recyc0*t0-2.0_R8*fna_mol_rec)*fluid_frac_hyb
    ELSE
      fnnrec = 0.0_R8
    END IF
!
!     Recycled momentum flux densities
    fmox = pcorf*mn*rmx1i*SIGN(1.0_R8, ux)*cs**2*fdni/(-i1l)*&
&     fluid_frac_hyb
!
    fmoy = 0.0_R8
!
    fmoz = pcorf*mn*rmz1i*cs**2*fdni/(-i1l)*fluid_frac_hyb
!
    fmoz = fmoz + 2.0_R8/3.0_R8*mn*(1-recycm)*pt*fdni*vt*fluid_frac_hyb
!       Pressure of recycled neutrals
    pnc = pcorf*mn/3.0_R8*rp11i*cs**2*fdni/(-i1l)*fluid_frac_hyb
!
    pnc = pnc + 2.0_R8/3.0_R8*mn*(1-recycm)*pt*fdni*vt*fluid_frac_hyb
!       Density of recycled neutrals
    nnrec = pcorf*rp21i*fdni/(-i1l)*fluid_frac_hyb
    nnrec = nnrec + (1-recycm)*pt*fdni*2.0_R8/vt*fluid_frac_hyb
!       product of density and toroidal velocity
    nnwwnrec = pcorf*rp31i*cs*SIGN(1.0_R8, ux)*fdni/(-i1l)*&
&     fluid_frac_hyb
!
!     recycled total energy flux density of neutrals
    fenec = 0.5_R8*pcorf*mn*re1i*cs**3*fdni/(-i1l)*fluid_frac_hyb
    fenec = fenec + 0.5_R8*mn*(1-recycm)*pt*fdni*vt**2*fluid_frac_hyb
!
!     Calculate recycled poloidal momentum flux of neutrals
    fmomrecp = -(isign*(fmoz*cosa+fmoy*sina)*area) + isign*pnc*area*cosa
    fmomrect = fmox*area
    fmomrec = pit*fmomrecp + pit2*fmomrect
!     Convert energy flux densities in energy fluxes
    fenerec = fenec*area
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCRECYCLEDFLUXES

!  Differentiation of calcreflectedfluxesdiffusion in forward (tangent) mode (with options multiDirectional context noISIZE r8):
