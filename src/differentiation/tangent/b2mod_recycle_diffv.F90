      i0r = int0r
      i1l = int1l
      i1r = int1r
      i2l = int2l
      i2r = int2r
      i3l = int3l
      i3r = int3r
      i4l = int4l
      i4r = int4r
      i5l = int5l
      i5r = int5r
      i6l = int6l
      i6r = int6r
      CALL SUBEND()
      RETURN
    ELSE
      arg1 = 2.0_R8*t/m
      temp = SQRT(arg1)
      vth = temp
      arg = -(u/vth)
      DO nd=1,nbdirs
        arg1d(nd) = 2.0_R8*td(nd)/m
        IF (arg1 .EQ. 0.D0) THEN
          vthd(nd) = 0.D0
        ELSE
          vthd(nd) = arg1d(nd)/(2.0*temp)
        END IF
        argd(nd) = -((ud(nd)-u*vthd(nd)/vth)/vth)
        arg10d(nd) = 2*arg*argd(nd)
      END DO
!     int0l = int_{-inf}^{0} M(v) dv
      CALL ERF_DV(arg, argd, result1, result1d, nbdirs)
      DO nd=1,nbdirs
        int0ld(nd) = 0.5_R8*result1d(nd)
        i0ld(nd) = int0ld(nd)
      END DO
      int0l = 0.5_R8*(1.0_R8+result1)
!
!     int0r = int_{0}^{inf} M(v) dv
      result1 = ERF(arg)
      int0r = 0.5_R8*(1.0_R8-result1)
!
!     int1l = int_{-inf}^{0} v M(v) dv
      CALL ERF_DV(arg, argd, result1, result1d, nbdirs)
      result2 = SQRT(pi)
      arg10 = arg**2
      temp = EXP(arg10)
