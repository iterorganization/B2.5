!
  SUBROUTINE CALCREFLECTEDFLUXESDIFFUSION_DV(icv, icn, nci, ifc, isign, &
&   isn, iscx0, isi, istra, iwall, phi_app, area, recyc0, recyc0d, &
&   recycm, fluid_frac_hyb, fluid_frac_hybd, pl, pld, tif, tifd, tnf, &
&   tnfd, tef, tefd, pof, nef, nefd, geo, geod, mpg, rt, rtd, dnndy, &
&   dnndyd, dnndz, dnndzd, cosa, sina, use_uy_uz_0, use_uy_uz_1, &
&   safeguard, fnnrefl, fnnrefld, fmomrefl, fmomrefld, nnrefl, nnrefld, &
&   nnwwnrefl, nnwwnrefld, fenerefl, fenerefld, fene_el, fene_eld, &
&   nbdirs)
!mb   17.07.17
!     This routine calculates the reflected neutral particle,momentum and energy fluxes with incident neutral flux based on the d
!iffusion approximation (see papers of Niels Horsten).
    USE B2MOD_B2CMPA_DIFFV
    USE B2MOD_RATES
    USE B2MOD_NEUTRALS_NAMELIST_DIFFV, ONLY : e_fc
    USE B2US_GEO_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2US_MAP_DIFFV
    USE B2MOD_SUBSYS
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFFV), INTENT(IN) :: pld
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(GEOMETRY_DIFFV), INTENT(IN) :: geod
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFFV), INTENT(IN) :: rtd
    TYPE(MAPPING), INTENT(IN) :: mpg
    INTEGER, INTENT(IN) :: icv, icn, nci, ifc, isn, iscx0, isi, iwall, &
&   istra, use_uy_uz_0, use_uy_uz_1, safeguard
    REAL(kind=r8), INTENT(IN) :: phi_app, area, recyc0, recycm, pof, nef&
&   , isign, tif, tnf, tef, sina, cosa, dnndy, dnndz, fluid_frac_hyb
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(IN) :: recyc0d, nefd, &
&   tifd, tnfd, tefd, dnndyd, dnndzd, fluid_frac_hybd
!! fluid_frac_hyb: in fluid neutral cases or standard spatially hybrid cases this multiplier is always equal to 1.
!! only in the generalized form of the spatially hybrid approach, where the fluid and kinetic treatment are mixed
!! on a single target stratum, this value can deviate from 1.
!     Output variables:
!     fmomni: momentum flux of
!     incident neutrals.
    REAL(kind=r8), INTENT(OUT) :: fnnrefl, fmomrefl, nnrefl, nnwwnrefl, &
&   fenerefl, fene_el
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(OUT) :: fnnrefld, &
&   fmomrefld, nnrefld, nnwwnrefld, fenerefld, fene_eld
    EXTERNAL INTP_2DTABLE, INTP_2DTABLE0
    EXTERNAL INTP_2DTABLE_DV, INTP_2DTABLE0_DV
!     Internal variables
    REAL(kind=r8) :: cs, mj, pf, pcorf, pt, vt, vion, vcx, vtot, pit, &
&   pit2, mn, nnf, ux, uy, uz, i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, &
&   i4l, i4r, i5l, i5r, i6l, i6r, rc1i, rmx1i, rmz1i, rp11i, rp21i, &
&   rp31i, re1i, rc2i, rmx2i, rmy2i, rmz2i, rp12i, rp22i, rp32i, re2i, &
&   fnni, fnni_nodrifts, fmox, fmoy, fmoz, fmomreflp, fmomreflt, pn, &
&   fene, INTP_2DTABLE, INTP_2DTABLE0, tifev, t_av, upf, fna_mol_refl
    REAL(kind=r8), DIMENSION(nbdirsmax) :: csd, mjd, pfd, pcorfd, ptd, &
&   viond, vcxd, vtotd, nnfd, uxd, uyd, uzd, i0ld, i1ld, i2ld, i3ld, &
&   i4ld, rc1id, rmx1id, rmz1id, rp11id, rp21id, rp31id, re1id, rc2id, &
&   rmx2id, rmy2id, rmz2id, rp12id, rp22id, rp32id, re2id, fnnid, &
&   fnni_nodriftsd, fmoxd, fmoyd, fmozd, fmomreflpd, fmomrefltd, pnd, &
&   fened, tifevd, t_avd, upfd, fna_mol_refld
    REAL(kind=r8) :: tab(0:nm, 0:nt)
    INTEGER :: ic, is, it, ispt, ilt, ilm, ip1, ip2
    INTRINSIC ABS
    INTRINSIC LOG
    INTRINSIC SQRT
    INTRINSIC MAX
    INTRINSIC MIN
    INTRINSIC SIGN
    REAL(kind=r8) :: abs0
    REAL(kind=r8), DIMENSION(nbdirsmax) :: abs0d
    REAL(kind=r8) :: min1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: min1d
    REAL(kind=r8) :: min2
    REAL(kind=r8), DIMENSION(nbdirsmax) :: min2d
    REAL(kind=r8) :: arg1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: arg1d
    REAL(r8) :: arg2
    REAL(r8), DIMENSION(nbdirsmax) :: arg2d
    REAL(kind=r8) :: result1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: result1d
    REAL(r8) :: arg10
    REAL(r8), DIMENSION(nbdirsmax) :: arg10d
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(r8) :: temp0
    REAL(kind=r8) :: temp1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: x1d
    REAL(kind=r8) :: temp2
    REAL(kind=r8) :: temp3
    REAL(kind=r8) :: temp4
    REAL(kind=r8) :: temp5
    INTEGER :: nbdirs
    REAL(kind=r8) :: x1
!
    CALL SUBINI('CalcReflectedFluxesDiffusion')
!     Initialize/calculate some variables
    DO nd=1,nbdirs
      nnfd(nd) = pld%na(nd, icv, isn)
    END DO
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
    DO nd=1,nbdirs
!mb   plasma velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and drifts
!! We use iCn instead of iCv because EIRENE doesn't see the value in the guard cell and the difference can be large.
      upfd(nd) = pld%ua(nd, icn, isi)
      uxd(nd) = pit2*upfd(nd)
      uyd(nd) = -(sina*isign*pit*upfd(nd))
      uzd(nd) = -(cosa*isign*pit*upfd(nd))
    END DO
    upf = pl%ua(icn, isi)
    ux = upf*pit2
    uy = -(sina*isign*upf*pit)
    uz = -(cosa*isign*upf*pit)
!
!     Calculate rate coefficients at boundary
    vcx = 0.0_R8
    DO nd=1,nbdirsmax
      vcxd(nd) = 0.D0
    END DO
    DO ic=0,pl%ns-1
      t_av = (tif+tnf)/2.0_R8
      arg1 = t_av/(am(isn)*ev)
      temp = LOG(arg1)
      temp0 = rt%rlcx(icv, 1, ic, iscx0)
      DO nd=1,nbdirs
        t_avd(nd) = (tifd(nd)+tnfd(nd))/2.0_R8
        arg1d(nd) = t_avd(nd)/(am(isn)*ev)
        arg2d(nd) = rtd%rlcx(nd, icv, 0, ic, iscx0) + temp*rtd%rlcx(nd, &
&         icv, 1, ic, iscx0) + temp0*arg1d(nd)/arg1
      END DO
      arg2 = rt%rlcx(icv, 0, ic, iscx0) + temp0*temp
      CALL EXPU_DV(arg2, arg2d, result1, result1d, nbdirs)
      DO nd=1,nbdirs
        vcxd(nd) = vcxd(nd) + result1*pld%na(nd, icv, ic) + pl%na(icv, &
&         ic)*result1d(nd)
      END DO
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = tef/ev
    temp = LOG(arg1)
    DO nd=1,nbdirs
      arg1d(nd) = tefd(nd)/ev
      arg2d(nd) = rtd%rlsa(nd, icv, 0, isn) + temp*rtd%rlsa(nd, icv, 1, &
&       isn) + rt%rlsa(icv, 1, isn)*arg1d(nd)/arg1
    END DO
    arg2 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*temp
    CALL EXPU_DV(arg2, arg2d, result1, result1d, nbdirs)
    DO nd=1,nbdirs
      viond(nd) = nef*result1d(nd) + result1*nefd(nd)
      vtotd(nd) = viond(nd) + vcxd(nd)
    END DO
    vion = result1*nef
    vtot = vion + vcx
!
    IF (use_uy_uz_0 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
      DO nd=1,nbdirsmax
        uyd(nd) = 0.D0
      END DO
      DO nd=1,nbdirsmax
        uzd(nd) = 0.D0
      END DO
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN_DV(uz, uzd, tif, tifd, mn, i0l, i0ld, i0r&
&                           , i1l, i1ld, i1r, i2l, i2ld, i2r, i3l, i3ld&
&                           , i3r, i4l, i4ld, i4r, i5l, i5r, i6l, i6r, &
&                           nbdirs)
!
!
!     Calculate incident particle flux density
    temp = vcx/vtot
    temp1 = (dnndz*i2l+dnndy*uy*i1l)/vtot
    DO nd=1,nbdirs
      fnnid(nd) = temp*((i2l*dnndzd(nd)+dnndz*i2ld(nd)+i1l*(uy*dnndyd(nd&
&       )+dnndy*uyd(nd))+dnndy*uy*i1ld(nd)-temp1*vtotd(nd))/vtot-nnf*&
&       i1ld(nd)-i1l*nnfd(nd)) + (temp1-i1l*nnf)*(vcxd(nd)-temp*vtotd(nd&
&       ))/vtot
    END DO
    fnni = (temp1-i1l*nnf)*temp
!
!     Assume poloidal and radial drifts are zero for momentum and energy
!     equations to reduce the dimensionality of the pre-evaluates integrals
!     of TRIM tables. Only toroidal drift remains!
    IF (use_uy_uz_1 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
      DO nd=1,nbdirsmax
        uzd(nd) = 0.D0
      END DO
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN_DV(uz, uzd, tif, tifd, mn, i0l, i0ld, i0r&
&                           , i1l, i1ld, i1r, i2l, i2ld, i2r, i3l, i3ld&
&                           , i3r, i4l, i4ld, i4r, i5l, i5r, i6l, i6r, &
&                           nbdirs)
    arg10 = 2.0_R8*tif/mn
    temp1 = SQRT(arg10)
    DO nd=1,nbdirs
!
!
!     Interpolate tables to find recycling values
!       Isothermal speed of sound and Mach number
      arg10d(nd) = 2.0_R8*tifd(nd)/mn
      IF (arg10 .EQ. 0.D0) THEN
        csd(nd) = 0.D0
      ELSE
        csd(nd) = arg10d(nd)/(2.0*temp1)
      END IF
    END DO
    cs = temp1
    IF (ux .GE. 0.) THEN
      DO nd=1,nbdirs
        abs0d(nd) = uxd(nd)
      END DO
      abs0 = ux
    ELSE
      DO nd=1,nbdirs
        abs0d(nd) = -uxd(nd)
      END DO
      abs0 = -ux
    END IF
    DO nd=1,nbdirs
      mjd(nd) = (abs0d(nd)-abs0*csd(nd)/cs)/cs
      tifevd(nd) = tifd(nd)/ev
    END DO
    mj = abs0/cs
    tifev = tif/ev
    is = recycle_index(isn)
    it = target_index(istra)
! Neutrals are not accelerated to sheath
    ispt = 0
!       Interpolate
    tab = rc1(:, :, ispt, is, it)
    CALL INTP_2DTABLE_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                  tifevd, ilm, ilt, ip1, ip2, rc1i, rc1id, nbdirs)
    IF (rc1i .LT. 0.0_R8) THEN
      rc1i = 0.0_R8
      DO nd=1,nbdirsmax
        rc1id(nd) = 0.D0
      END DO
    ELSE
      rc1i = rc1i
    END IF
    tab = rc2(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rc2i, rc2id, nbdirs)
    IF (rc2i .LT. 0.0_R8) THEN
      rc2i = 0.0_R8
      DO nd=1,nbdirsmax
        rc2id(nd) = 0.D0
      END DO
    ELSE
      rc2i = rc2i
    END IF
    tab = rmx1(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rmx1i, rmx1id, nbdirs)
    IF (rmx1i .LT. 0.0_R8) THEN
      rmx1i = 0.0_R8
      DO nd=1,nbdirsmax
        rmx1id(nd) = 0.D0
      END DO
    ELSE
      rmx1i = rmx1i
    END IF
    tab = rmx2(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rmx2i, rmx2id, nbdirs)
    IF (rmx2i .LT. 0.0_R8) THEN
      rmx2i = 0.0_R8
      DO nd=1,nbdirsmax
        rmx2id(nd) = 0.D0
      END DO
    ELSE
      rmx2i = rmx2i
    END IF
    tab = rmy2(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rmy2i, rmy2id, nbdirs)
    IF (rmy2i .LT. 0.0_R8) THEN
      rmy2i = 0.0_R8
      DO nd=1,nbdirsmax
        rmy2id(nd) = 0.D0
      END DO
    ELSE
      rmy2i = rmy2i
    END IF
    tab = rmz1(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rmz1i, rmz1id, nbdirs)
    IF (rmz1i .LT. 0.0_R8) THEN
      rmz1i = 0.0_R8
      DO nd=1,nbdirsmax
        rmz1id(nd) = 0.D0
      END DO
    ELSE
      rmz1i = rmz1i
    END IF
    tab = rmz2(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rmz2i, rmz2id, nbdirs)
    IF (rmz2i .LT. 0.0_R8) THEN
      rmz2i = 0.0_R8
      DO nd=1,nbdirsmax
        rmz2id(nd) = 0.D0
      END DO
    ELSE
      rmz2i = rmz2i
    END IF
    tab = rp11(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rp11i, rp11id, nbdirs)
    IF (rp11i .LT. 0.0_R8) THEN
      rp11i = 0.0_R8
      DO nd=1,nbdirsmax
        rp11id(nd) = 0.D0
      END DO
    ELSE
      rp11i = rp11i
    END IF
    tab = rp21(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rp21i, rp21id, nbdirs)
    IF (rp21i .LT. 0.0_R8) THEN
      rp21i = 0.0_R8
      DO nd=1,nbdirsmax
        rp21id(nd) = 0.D0
      END DO
    ELSE
      rp21i = rp21i
    END IF
    tab = rp31(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rp31i, rp31id, nbdirs)
    IF (rp31i .LT. 0.0_R8) THEN
      rp31i = 0.0_R8
      DO nd=1,nbdirsmax
        rp31id(nd) = 0.D0
      END DO
    ELSE
      rp31i = rp31i
    END IF
    tab = rp12(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rp12i, rp12id, nbdirs)
    IF (rp12i .LT. 0.0_R8) THEN
      rp12i = 0.0_R8
      DO nd=1,nbdirsmax
        rp12id(nd) = 0.D0
      END DO
    ELSE
      rp12i = rp12i
    END IF
    tab = rp22(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rp22i, rp22id, nbdirs)
    IF (rp22i .LT. 0.0_R8) THEN
      rp22i = 0.0_R8
      DO nd=1,nbdirsmax
        rp22id(nd) = 0.D0
      END DO
    ELSE
      rp22i = rp22i
    END IF
    tab = rp32(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, rp32i, rp32id, nbdirs)
    IF (rp32i .LT. 0.0_R8) THEN
      rp32i = 0.0_R8
      DO nd=1,nbdirsmax
        rp32id(nd) = 0.D0
      END DO
    ELSE
      rp32i = rp32i
    END IF
    tab = re1(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, re1i, re1id, nbdirs)
    IF (re1i .LT. 0.0_R8) THEN
      re1i = 0.0_R8
      DO nd=1,nbdirsmax
        re1id(nd) = 0.D0
      END DO
    ELSE
      re1i = re1i
    END IF
    tab = re2(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tifev, &
&                   tifevd, ilm, ilt, re2i, re2id, nbdirs)
    IF (re2i .LT. 0.0_R8) THEN
      re2i = 0.0_R8
      DO nd=1,nbdirsmax
        re2id(nd) = 0.D0
      END DO
    ELSE
      re2i = re2i
    END IF
!     Calculate incident particle flux density
    temp1 = vcx/vtot
    temp = dnndz*i2l/vtot
    DO nd=1,nbdirs
      fnni_nodriftsd(nd) = temp1*((i2l*dnndzd(nd)+dnndz*i2ld(nd)-temp*&
&       vtotd(nd))/vtot-nnf*i1ld(nd)-i1l*nnfd(nd)) + (temp-i1l*nnf)*(&
&       vcxd(nd)-temp1*vtotd(nd))/vtot
    END DO
    fnni_nodrifts = (temp-i1l*nnf)*temp1
    temp1 = vcx/(vtot*fnni_nodrifts)
    temp = cs*cs/vtot
    temp2 = nnf*rc1i*cs + dnndz*rc2i*temp
    DO nd=1,nbdirs
      x1d(nd) = temp1*(cs*(rc1i*nnfd(nd)+nnf*rc1id(nd))+nnf*rc1i*csd(nd)&
&       +temp*(rc2i*dnndzd(nd)+dnndz*rc2id(nd))+dnndz*rc2i*(2*cs*csd(nd)&
&       -temp*vtotd(nd))/vtot) + temp2*(vcxd(nd)-temp1*(fnni_nodrifts*&
&       vtotd(nd)+vtot*fnni_nodriftsd(nd)))/(vtot*fnni_nodrifts)
    END DO
    x1 = temp2*temp1
    IF (x1 .GT. recyc0) THEN
      DO nd=1,nbdirs
        min1d(nd) = recyc0d(nd)
      END DO
      min1 = recyc0
    ELSE
      DO nd=1,nbdirs
        min1d(nd) = x1d(nd)
      END DO
      min1 = x1
    END IF
!     Calculate fast and thermal reflected parts
    DO nd=1,nbdirs
      pfd(nd) = min1d(nd)
    END DO
    pf = min1 + 1.0e-30_R8
    IF (pf .GT. recyc0) THEN
      DO nd=1,nbdirs
        min2d(nd) = recyc0d(nd)
      END DO
      min2 = recyc0
    ELSE
      DO nd=1,nbdirs
        min2d(nd) = pfd(nd)
      END DO
      min2 = pf
    END IF
    DO nd=1,nbdirs
      pcorfd(nd) = (min2d(nd)-min2*pfd(nd)/pf)/pf
      ptd(nd) = recyc0d(nd) - pfd(nd)
    END DO
    pcorf = min2/pf
    pt = recyc0 - pf + 1.0e-30_R8
!
    arg10 = 2.0_R8*e_fc*ev/mn
    vt = SQRT(arg10)
!     The reflection coefficients below already account for the probability of
!     fast particle reflection. Like in Eirene, we will only pump fast particles
!     once all thermal particles are pumped.
!
!nh 22.02.2020
!     Calculate the part of the reflected flux emitted as molecules. At the moment,
!     the molecules are treated kinetically.
    IF (fnni .GE. 0.0_R8) THEN
      temp2 = 0.5_R8*recycm*area
      DO nd=1,nbdirs
        fna_mol_refld(nd) = temp2*(fnni_nodrifts*ptd(nd)+pt*&
&         fnni_nodriftsd(nd))
      END DO
      fna_mol_refl = temp2*(pt*fnni_nodrifts)
      fna_mol(ifc, isn) = fna_mol(ifc, isn) + fna_mol_refl
      IF (0.0_R8 .LT. (recyc0*fnni*area-2.0_R8*fna_mol_refl)*&
&         fluid_frac_hyb) THEN
        temp2 = area*recyc0*fnni - 2.0_R8*fna_mol_refl
        DO nd=1,nbdirs
          fnnrefld(nd) = fluid_frac_hyb*(area*(fnni*recyc0d(nd)+recyc0*&
&           fnnid(nd))-2.0_R8*fna_mol_refld(nd)) + temp2*fluid_frac_hybd&
&           (nd)
        END DO
        fnnrefl = temp2*fluid_frac_hyb
      ELSE
        fnnrefl = 0.0_R8
        DO nd=1,nbdirsmax
          fnnrefld(nd) = 0.D0
        END DO
      END IF
    ELSE
      fnnrefl = 0.0_R8
      DO nd=1,nbdirsmax
        fnnrefld(nd) = 0.D0
      END DO
    END IF
!
!     reflected momentum flux densities
    temp2 = mn*SIGN(1.0_R8, ux)
    temp1 = fluid_frac_hyb/vtot
    temp = pcorf*vcx*temp1
    temp3 = cs*cs*cs
    temp4 = dnndz*rmx2i/vtot
    temp5 = nnf*rmx1i*(cs*cs) + temp4*temp3
    DO nd=1,nbdirs
      fmoxd(nd) = temp2*(temp*(cs**2*(rmx1i*nnfd(nd)+nnf*rmx1id(nd))+(&
&       nnf*rmx1i*2*cs+temp4*3*cs**2)*csd(nd)+temp3*(rmx2i*dnndzd(nd)+&
&       dnndz*rmx2id(nd)-temp4*vtotd(nd))/vtot)+temp5*(temp1*(vcx*pcorfd&
&       (nd)+pcorf*vcxd(nd))+pcorf*vcx*(fluid_frac_hybd(nd)-temp1*vtotd(&
&       nd))/vtot))
      fmomrefltd(nd) = area*fmoxd(nd)
    END DO
    fmox = temp2*(temp5*temp)
    temp5 = fluid_frac_hyb/(vtot*vtot)
    temp4 = cs*cs*cs
    temp3 = pcorf*vcx*dnndy*rmy2i
    DO nd=1,nbdirs
      fmoyd(nd) = -(mn*(temp4*temp5*(dnndy*rmy2i*(vcx*pcorfd(nd)+pcorf*&
&       vcxd(nd))+pcorf*vcx*(rmy2i*dnndyd(nd)+dnndy*rmy2id(nd)))+temp3*(&
&       temp5*3*cs**2*csd(nd)+temp4*(fluid_frac_hybd(nd)-temp5*2*vtot*&
&       vtotd(nd))/vtot**2)))
    END DO
    fmoy = -(mn*(temp3*(temp4*temp5)))
!
    temp5 = fluid_frac_hyb/vtot
    temp4 = pcorf*vcx*temp5
    temp3 = cs*cs*cs
    temp2 = dnndz*rmz2i/vtot
    temp1 = nnf*rmz1i*(cs*cs) + temp2*temp3
    DO nd=1,nbdirs
      fmozd(nd) = mn*(temp4*(cs**2*(rmz1i*nnfd(nd)+nnf*rmz1id(nd))+(nnf*&
&       rmz1i*2*cs+temp2*3*cs**2)*csd(nd)+temp3*(rmz2i*dnndzd(nd)+dnndz*&
&       rmz2id(nd)-temp2*vtotd(nd))/vtot)+temp1*(temp5*(vcx*pcorfd(nd)+&
&       pcorf*vcxd(nd))+pcorf*vcx*(fluid_frac_hybd(nd)-temp5*vtotd(nd))/&
&       vtot))
    END DO
    fmoz = mn*(temp1*temp4)
!
    temp5 = 2.0_R8*(-recycm+1)*vt*mn
    DO nd=1,nbdirs
      fmozd(nd) = fmozd(nd) + temp5*(fnni_nodrifts*fluid_frac_hyb*ptd(nd&
&       )/3.0_R8+pt*(fluid_frac_hyb*fnni_nodriftsd(nd)+fnni_nodrifts*&
&       fluid_frac_hybd(nd))/3.0_R8)
    END DO
    fmoz = fmoz + temp5*(pt/3.0_R8*(fnni_nodrifts*fluid_frac_hyb))
!       Pressure of reflected neutrals
    temp5 = fluid_frac_hyb/(3.0_R8*vtot)
    temp4 = pcorf*vcx*temp5
    temp3 = cs*cs*cs
    temp2 = dnndz*rp12i/vtot
    temp1 = nnf*rp11i*(cs*cs) + temp2*temp3
    DO nd=1,nbdirs
      pnd(nd) = mn*(temp4*(cs**2*(rp11i*nnfd(nd)+nnf*rp11id(nd))+(nnf*&
&       rp11i*2*cs+temp2*3*cs**2)*csd(nd)+temp3*(rp12i*dnndzd(nd)+dnndz*&
&       rp12id(nd)-temp2*vtotd(nd))/vtot)+temp1*(temp5*(vcx*pcorfd(nd)+&
&       pcorf*vcxd(nd))+pcorf*vcx*(fluid_frac_hybd(nd)-temp5*3.0_R8*&
&       vtotd(nd))/(3.0_R8*vtot)))
    END DO
    pn = mn*(temp1*temp4)
!
    temp5 = 2.0_R8*(-recycm+1)*vt*mn
    DO nd=1,nbdirs
      pnd(nd) = pnd(nd) + temp5*(fnni_nodrifts*fluid_frac_hyb*ptd(nd)/&
&       3.0_R8+pt*(fluid_frac_hyb*fnni_nodriftsd(nd)+fnni_nodrifts*&
&       fluid_frac_hybd(nd))/3.0_R8)
!
!     Calculate reflected poloidal momentum flux of neutrals
      fmomreflpd(nd) = isign*area*cosa*pnd(nd) - isign*area*(cosa*fmozd(&
&       nd)+sina*fmoyd(nd))
      fmomrefld(nd) = pit*fmomreflpd(nd) + pit2*fmomrefltd(nd)
    END DO
    pn = pn + temp5*(pt/3.0_R8*(fnni_nodrifts*fluid_frac_hyb))
!       Density of reflected neutrals
    temp5 = fluid_frac_hyb/vtot
    temp4 = pcorf*vcx*temp5
    temp3 = cs/vtot
    temp2 = nnf*rp21i + dnndz*rp22i*temp3
    DO nd=1,nbdirs
      nnrefld(nd) = temp4*(rp21i*nnfd(nd)+nnf*rp21id(nd)+temp3*(rp22i*&
&       dnndzd(nd)+dnndz*rp22id(nd))+dnndz*rp22i*(csd(nd)-temp3*vtotd(nd&
&       ))/vtot) + temp2*(temp5*(vcx*pcorfd(nd)+pcorf*vcxd(nd))+pcorf*&
&       vcx*(fluid_frac_hybd(nd)-temp5*vtotd(nd))/vtot)
      nnrefld(nd) = nnrefld(nd) + (1-recycm)*2.0_R8*(fluid_frac_hyb*(&
&       fnni_nodrifts*ptd(nd)+pt*fnni_nodriftsd(nd))/vt+pt*fnni_nodrifts&
&       *fluid_frac_hybd(nd)/vt)
    END DO
    nnrefl = temp2*temp4
    nnrefl = nnrefl + (1-recycm)*pt*fnni_nodrifts*2.0_R8/vt*&
&     fluid_frac_hyb
!       product of density and toroidal velocity
    temp0 = SIGN(1.0_R8, ux)
    temp5 = fluid_frac_hyb/vtot
    temp4 = pcorf*vcx*temp5
    temp3 = cs*cs/vtot
    temp2 = nnf*rp31i*cs + dnndz*rp32i*temp3
    DO nd=1,nbdirs
      nnwwnrefld(nd) = temp0*(temp4*(cs*(rp31i*nnfd(nd)+nnf*rp31id(nd))+&
&       nnf*rp31i*csd(nd)+temp3*(rp32i*dnndzd(nd)+dnndz*rp32id(nd))+&
&       dnndz*rp32i*(2*cs*csd(nd)-temp3*vtotd(nd))/vtot)+temp2*(temp5*(&
&       vcx*pcorfd(nd)+pcorf*vcxd(nd))+pcorf*vcx*(fluid_frac_hybd(nd)-&
&       temp5*vtotd(nd))/vtot))
    END DO
    nnwwnrefl = temp0*(temp2*temp4)
!     reflected total energy flux density of neutrals
    temp5 = fluid_frac_hyb/vtot
    temp4 = pcorf*vcx*temp5
    temp3 = cs*cs*cs
    temp2 = cs**4
    temp1 = dnndz*re2i/vtot
    temp = nnf*re1i*temp3 + temp1*temp2
    DO nd=1,nbdirs
      fened(nd) = mn*0.5_R8*(temp4*(temp3*(re1i*nnfd(nd)+nnf*re1id(nd))+&
&       (nnf*re1i*3*cs**2+temp1*4*cs**3)*csd(nd)+temp2*(re2i*dnndzd(nd)+&
&       dnndz*re2id(nd)-temp1*vtotd(nd))/vtot)+temp*(temp5*(vcx*pcorfd(&
&       nd)+pcorf*vcxd(nd))+pcorf*vcx*(fluid_frac_hybd(nd)-temp5*vtotd(&
&       nd))/vtot))
    END DO
    fene = mn*0.5_R8*(temp*temp4)
    temp5 = (-recycm+1)*e_fc*ev
    DO nd=1,nbdirs
      fened(nd) = fened(nd) + temp5*(fluid_frac_hyb*(fnni_nodrifts*ptd(&
&       nd)+pt*fnni_nodriftsd(nd))+pt*fnni_nodrifts*fluid_frac_hybd(nd))
!     Convert energy flux densities in energy fluxes
      fenerefld(nd) = area*fened(nd)
    END DO
    fene = fene + temp5*(pt*fnni_nodrifts*fluid_frac_hyb)
!! the Franck-Condon energy must at least be provided by the electrons.
    temp5 = (-recycm+1)*e_fc*ev
    DO nd=1,nbdirs
      fene_eld(nd) = -(temp5*(fluid_frac_hyb*(fnni_nodrifts*ptd(nd)+pt*&
&       fnni_nodriftsd(nd))+pt*fnni_nodrifts*fluid_frac_hybd(nd)))
      fene_eld(nd) = area*fene_eld(nd)
    END DO
    fene_el = -(temp5*(pt*fnni_nodrifts*fluid_frac_hyb))
    fmomreflp = -(isign*(fmoz*cosa+fmoy*sina)*area) + isign*pn*area*cosa
    fmomreflt = fmox*area
    fmomrefl = pit*fmomreflp + pit2*fmomreflt
    fenerefl = fene*area
    fene_el = fene_el*area
!
! if incident flux is negative (this can happen if the diffusion approximation is used where it is not valid), use one-sided maxw
!ellian anyway
    IF (fnni .LT. 0.0_R8 .AND. safeguard .EQ. 1) CALL &
&     CALCREFLECTEDFLUXESMAXWELLIAN_DV(icv, nci, ifc, isign, isn, istra&
&                                , iwall, area, recyc0, recyc0d, recycm&
&                                , fluid_frac_hyb, fluid_frac_hybd, pl, &
&                                pld, tnf, tnfd, geo, mpg, cosa, sina, &
&                                fnnrefl, fnnrefld, fmomrefl, fmomrefld&
&                                , nnrefl, nnrefld, nnwwnrefl, &
&                                nnwwnrefld, fenerefl, fenerefld, &
&                                fene_el, fene_eld, nbdirs)
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCREFLECTEDFLUXESDIFFUSION_DV

!
  SUBROUTINE CALCREFLECTEDFLUXESDIFFUSION(icv, icn, nci, ifc, isign, isn&
&   , iscx0, isi, istra, iwall, phi_app, area, recyc0, recycm, &
&   fluid_frac_hyb, pl, tif, tnf, tef, pof, nef, geo, mpg, rt, dnndy, &
&   dnndz, cosa, sina, use_uy_uz_0, use_uy_uz_1, safeguard, fnnrefl, &
&   fmomrefl, nnrefl, nnwwnrefl, fenerefl, fene_el)
!mb   17.07.17
!     This routine calculates the reflected neutral particle,momentum and energy fluxes with incident neutral flux based on the d
!iffusion approximation (see papers of Niels Horsten).
    USE B2MOD_B2CMPA_DIFFV
    USE B2MOD_RATES
    USE B2MOD_NEUTRALS_NAMELIST_DIFFV, ONLY : e_fc
    USE B2US_GEO_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2US_MAP_DIFFV
    USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(MAPPING), INTENT(IN) :: mpg
    INTEGER, INTENT(IN) :: icv, icn, nci, ifc, isn, iscx0, isi, iwall, &
&   istra, use_uy_uz_0, use_uy_uz_1, safeguard
    REAL(kind=r8), INTENT(IN) :: phi_app, area, recyc0, recycm, pof, nef&
&   , isign, tif, tnf, tef, sina, cosa, dnndy, dnndz, fluid_frac_hyb
!! fluid_frac_hyb: in fluid neutral cases or standard spatially hybrid cases this multiplier is always equal to 1.
!! only in the generalized form of the spatially hybrid approach, where the fluid and kinetic treatment are mixed
!! on a single target stratum, this value can deviate from 1.
!     Output variables:
!     fmomni: momentum flux of
!     incident neutrals.
    REAL(kind=r8), INTENT(OUT) :: fnnrefl, fmomrefl, nnrefl, nnwwnrefl, &
&   fenerefl, fene_el
    EXTERNAL INTP_2DTABLE, INTP_2DTABLE0
!     Internal variables
    REAL(kind=r8) :: cs, mj, pf, pcorf, pt, vt, vion, vcx, vtot, pit, &
&   pit2, mn, nnf, ux, uy, uz, i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, &
&   i4l, i4r, i5l, i5r, i6l, i6r, rc1i, rmx1i, rmz1i, rp11i, rp21i, &
&   rp31i, re1i, rc2i, rmx2i, rmy2i, rmz2i, rp12i, rp22i, rp32i, re2i, &
&   fnni, fnni_nodrifts, fmox, fmoy, fmoz, fmomreflp, fmomreflt, pn, &
&   fene, INTP_2DTABLE, INTP_2DTABLE0, tifev, t_av, upf, fna_mol_refl
    REAL(kind=r8) :: tab(0:nm, 0:nt)
    INTEGER :: ic, is, it, ispt, ilt, ilm, ip1, ip2
    INTRINSIC ABS
    INTRINSIC LOG
    INTRINSIC SQRT
    INTRINSIC MAX
    INTRINSIC MIN
    INTRINSIC SIGN
    REAL(kind=r8) :: abs0
    REAL(kind=r8) :: min1
    REAL(kind=r8) :: min2
    REAL(kind=r8) :: arg1
    REAL(r8) :: arg2
    REAL(kind=r8) :: result1
    REAL(r8) :: arg10
    REAL(kind=r8) :: x1
!
    CALL SUBINI('CalcReflectedFluxesDiffusion')
!     Initialize/calculate some variables
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!mb   plasma velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and drifts
!! We use iCn instead of iCv because EIRENE doesn't see the value in the guard cell and the difference can be large.
    upf = pl%ua(icn, isi)
    ux = upf*pit2
    uy = -(sina*isign*upf*pit)
    uz = -(cosa*isign*upf*pit)
!
!     Calculate rate coefficients at boundary
    vcx = 0.0_R8
    DO ic=0,pl%ns-1
      t_av = (tif+tnf)/2.0_R8
      arg1 = t_av/(am(isn)*ev)
      arg2 = rt%rlcx(icv, 0, ic, iscx0) + rt%rlcx(icv, 1, ic, iscx0)*LOG&
&       (arg1)
      result1 = EXPU(arg2)
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = tef/ev
    arg2 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*LOG(arg1)
    result1 = EXPU(arg2)
    vion = result1*nef
    vtot = vion + vcx
!
    IF (use_uy_uz_0 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!
!     Calculate incident particle flux density
    fnni = vcx/vtot*(-(i1l*nnf)+(dnndz*i2l+dnndy*uy*i1l)/vtot)
!
!     Assume poloidal and radial drifts are zero for momentum and energy
!     equations to reduce the dimensionality of the pre-evaluates integrals
!     of TRIM tables. Only toroidal drift remains!
    IF (use_uy_uz_1 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!
!     Interpolate tables to find recycling values
!       Isothermal speed of sound and Mach number
    arg10 = 2.0_R8*tif/mn
    cs = SQRT(arg10)
    IF (ux .GE. 0.) THEN
      abs0 = ux
    ELSE
      abs0 = -ux
    END IF
    mj = abs0/cs
    tifev = tif/ev
    is = recycle_index(isn)
    it = target_index(istra)
! Neutrals are not accelerated to sheath
    ispt = 0
!       Interpolate
    tab = rc1(:, :, ispt, is, it)
    rc1i = INTP_2DTABLE(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt, &
&     ip1, ip2)
    IF (rc1i .LT. 0.0_R8) THEN
      rc1i = 0.0_R8
    ELSE
      rc1i = rc1i
    END IF
    tab = rc2(:, :, ispt, is, it)
    rc2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt)
    IF (rc2i .LT. 0.0_R8) THEN
      rc2i = 0.0_R8
    ELSE
      rc2i = rc2i
    END IF
    tab = rmx1(:, :, ispt, is, it)
    rmx1i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rmx1i .LT. 0.0_R8) THEN
      rmx1i = 0.0_R8
    ELSE
      rmx1i = rmx1i
    END IF
    tab = rmx2(:, :, ispt, is, it)
    rmx2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rmx2i .LT. 0.0_R8) THEN
      rmx2i = 0.0_R8
    ELSE
      rmx2i = rmx2i
    END IF
    tab = rmy2(:, :, ispt, is, it)
    rmy2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rmy2i .LT. 0.0_R8) THEN
      rmy2i = 0.0_R8
    ELSE
      rmy2i = rmy2i
    END IF
    tab = rmz1(:, :, ispt, is, it)
    rmz1i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rmz1i .LT. 0.0_R8) THEN
      rmz1i = 0.0_R8
    ELSE
      rmz1i = rmz1i
    END IF
    tab = rmz2(:, :, ispt, is, it)
    rmz2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rmz2i .LT. 0.0_R8) THEN
      rmz2i = 0.0_R8
    ELSE
      rmz2i = rmz2i
    END IF
    tab = rp11(:, :, ispt, is, it)
    rp11i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rp11i .LT. 0.0_R8) THEN
      rp11i = 0.0_R8
    ELSE
      rp11i = rp11i
    END IF
    tab = rp21(:, :, ispt, is, it)
    rp21i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rp21i .LT. 0.0_R8) THEN
      rp21i = 0.0_R8
    ELSE
      rp21i = rp21i
    END IF
    tab = rp31(:, :, ispt, is, it)
    rp31i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rp31i .LT. 0.0_R8) THEN
      rp31i = 0.0_R8
    ELSE
      rp31i = rp31i
    END IF
    tab = rp12(:, :, ispt, is, it)
    rp12i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rp12i .LT. 0.0_R8) THEN
      rp12i = 0.0_R8
    ELSE
      rp12i = rp12i
    END IF
    tab = rp22(:, :, ispt, is, it)
    rp22i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rp22i .LT. 0.0_R8) THEN
      rp22i = 0.0_R8
    ELSE
      rp22i = rp22i
    END IF
    tab = rp32(:, :, ispt, is, it)
    rp32i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt&
&     )
    IF (rp32i .LT. 0.0_R8) THEN
      rp32i = 0.0_R8
    ELSE
      rp32i = rp32i
    END IF
    tab = re1(:, :, ispt, is, it)
    re1i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt)
    IF (re1i .LT. 0.0_R8) THEN
      re1i = 0.0_R8
    ELSE
      re1i = re1i
    END IF
    tab = re2(:, :, ispt, is, it)
    re2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tifev, ilm, ilt)
    IF (re2i .LT. 0.0_R8) THEN
      re2i = 0.0_R8
    ELSE
      re2i = re2i
    END IF
!     Calculate incident particle flux density
    fnni_nodrifts = vcx/vtot*(-(i1l*nnf)+dnndz*i2l/vtot)
    x1 = vcx/vtot*(nnf*rc1i*cs+dnndz*rc2i*cs**2/vtot)/fnni_nodrifts
    IF (x1 .GT. recyc0) THEN
      min1 = recyc0
    ELSE
      min1 = x1
    END IF
!     Calculate fast and thermal reflected parts
    pf = min1 + 1.0e-30_R8
    IF (pf .GT. recyc0) THEN
      min2 = recyc0
    ELSE
      min2 = pf
    END IF
    pcorf = min2/pf
    pt = recyc0 - pf + 1.0e-30_R8
!
    arg10 = 2.0_R8*e_fc*ev/mn
    vt = SQRT(arg10)
!     The reflection coefficients below already account for the probability of
!     fast particle reflection. Like in Eirene, we will only pump fast particles
!     once all thermal particles are pumped.
!
!nh 22.02.2020
!     Calculate the part of the reflected flux emitted as molecules. At the moment,
!     the molecules are treated kinetically.
    IF (fnni .GE. 0.0_R8) THEN
      fna_mol_refl = recycm*pt*fnni_nodrifts*area*0.5_R8
      fna_mol(ifc, isn) = fna_mol(ifc, isn) + fna_mol_refl
      IF (0.0_R8 .LT. (recyc0*fnni*area-2.0_R8*fna_mol_refl)*&
&         fluid_frac_hyb) THEN
        fnnrefl = (recyc0*fnni*area-2.0_R8*fna_mol_refl)*fluid_frac_hyb
      ELSE
        fnnrefl = 0.0_R8
      END IF
    ELSE
      fnnrefl = 0.0_R8
    END IF
!
!     reflected momentum flux densities
    fmox = pcorf*mn*vcx/vtot*(nnf*rmx1i*cs**2+1.0_R8/vtot*dnndz*rmx2i*cs&
&     **3)*SIGN(1.0_R8, ux)*fluid_frac_hyb
    fmoy = -(pcorf*mn*vcx/vtot**2*dnndy*rmy2i*cs**3*fluid_frac_hyb)
!
    fmoz = pcorf*mn*vcx/vtot*(nnf*rmz1i*cs**2+1.0_R8/vtot*dnndz*rmz2i*cs&
&     **3)*fluid_frac_hyb
!
    fmoz = fmoz + (1-recycm)*pt*2.0_R8/3.0_R8*mn*fnni_nodrifts*vt*&
&     fluid_frac_hyb
!       Pressure of reflected neutrals
    pn = pcorf*mn/3.0_R8*vcx/vtot*(nnf*rp11i*cs**2+1.0_R8/vtot*dnndz*&
&     rp12i*cs**3)*fluid_frac_hyb
!
    pn = pn + (1-recycm)*pt*2.0_R8/3.0_R8*mn*fnni_nodrifts*vt*&
&     fluid_frac_hyb
!       Density of reflected neutrals
    nnrefl = pcorf*vcx/vtot*(nnf*rp21i+1.0_R8/vtot*dnndz*rp22i*cs)*&
&     fluid_frac_hyb
    nnrefl = nnrefl + (1-recycm)*pt*fnni_nodrifts*2.0_R8/vt*&
&     fluid_frac_hyb
!       product of density and toroidal velocity
    nnwwnrefl = pcorf*vcx/vtot*(nnf*rp31i*cs+1.0_R8/vtot*dnndz*rp32i*cs&
&     **2)*SIGN(1.0_R8, ux)*fluid_frac_hyb
!     reflected total energy flux density of neutrals
    fene = 0.5_R8*pcorf*mn*vcx/vtot*(nnf*re1i*cs**3+1.0_R8/vtot*dnndz*&
&     re2i*cs**4)*fluid_frac_hyb
    fene = fene + (1-recycm)*pt*fnni_nodrifts*e_fc*ev*fluid_frac_hyb
!! the Franck-Condon energy must at least be provided by the electrons.
    fene_el = -((1-recycm)*pt*fnni_nodrifts*e_fc*ev*fluid_frac_hyb)
!
!     Calculate reflected poloidal momentum flux of neutrals
    fmomreflp = -(isign*(fmoz*cosa+fmoy*sina)*area) + isign*pn*area*cosa
    fmomreflt = fmox*area
    fmomrefl = pit*fmomreflp + pit2*fmomreflt
!     Convert energy flux densities in energy fluxes
    fenerefl = fene*area
    fene_el = fene_el*area
!
! if incident flux is negative (this can happen if the diffusion approximation is used where it is not valid), use one-sided maxw
!ellian anyway
    IF (fnni .LT. 0.0_R8 .AND. safeguard .EQ. 1) CALL &
&     CALCREFLECTEDFLUXESMAXWELLIAN(icv, nci, ifc, isign, isn, istra, &
&                             iwall, area, recyc0, recycm, &
&                             fluid_frac_hyb, pl, tnf, geo, mpg, cosa, &
&                             sina, fnnrefl, fmomrefl, nnrefl, nnwwnrefl&
&                             , fenerefl, fene_el)
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCREFLECTEDFLUXESDIFFUSION

!  Differentiation of calcreflectedfluxeskn in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: int4l int1l int2l int3l int0l
!                fmomrefl nnrefl fene_el fenerefl nnwwnrefl fnnrefl
!   with respect to varying inputs: int4l int1l int2l int3l int0l
!                tef tif dnndy dnndz nef recyc0 fluid_frac_hyb
!                *(rt.rlcx) *(rt.rlsa) *(pl.na) *(pl.ua) tnf
!   Plus diff mem management of: geo.cvbb:in rt.rlcx:in rt.rlsa:in
!                pl.na:in pl.ua:in
!
  SUBROUTINE CALCREFLECTEDFLUXESKN_DV(kn_b1, kn_b2, icv, icn, nci, ifc, &
&   isign, isn, iscx0, isi, istra, iwall, phi_app, area, recyc0, recyc0d&
&   , recycm, fluid_frac_hyb, fluid_frac_hybd, pl, pld, tif, tifd, tnf, &
&   tnfd, tef, tefd, pof, nef, nefd, geo, geod, mpg, rt, rtd, dnndy, &
&   dnndyd, dnndz, dnndzd, cosa, sina, use_uy_uz_0, use_uy_uz_1, l, &
&   fnnrefl, fnnrefld0, fmomrefl, fmomrefld0, nnrefl, nnrefld0, &
&   nnwwnrefl, nnwwnrefld0, fenerefl, fenerefld0, fene_el, fene_eld0, &
&   nbdirs)
!     This routine is an interface to CalcReflectedFluxesMaxwellian and CalcReflectedFluxesDiffusion
!     It will decide which one to use based on the Knudsen number (Kn) of the neutral atoms.
    USE B2MOD_B2CMPA_DIFFV
    USE B2MOD_RATES
    USE B2US_GEO_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2US_MAP_DIFFV
    USE B2MOD_SUBSYS
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFFV), INTENT(IN) :: pld
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(GEOMETRY_DIFFV), INTENT(IN) :: geod
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFFV), INTENT(IN) :: rtd
    TYPE(MAPPING), INTENT(IN) :: mpg
    INTEGER, INTENT(IN) :: icv, icn, nci, ifc, isn, iscx0, isi, iwall, &
&   istra, use_uy_uz_0, use_uy_uz_1
    REAL(kind=r8), INTENT(IN) :: phi_app, area, recyc0, recycm, pof, nef&
&   , isign, tif, tnf, tef, l, sina, cosa, dnndy, dnndz, kn_b1, kn_b2, &
&   fluid_frac_hyb
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(IN) :: recyc0d, nefd, &
&   tifd, tnfd, tefd, dnndyd, dnndzd, fluid_frac_hybd
!     Output variables:
    REAL(kind=r8), INTENT(OUT) :: fnnrefl, fmomrefl, nnrefl, nnwwnrefl, &
&   fenerefl, fene_el
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(OUT) :: fnnrefld0, &
&   fmomrefld0, nnrefld0, nnwwnrefld0, fenerefld0, fene_eld0
!     Internal variables
    REAL(kind=r8) :: vion, vcx, vtot, mn, t_av, mfp, c_th, kn
    REAL(kind=r8), DIMENSION(nbdirsmax) :: viond, vcxd, vtotd, t_avd, &
&   mfpd, c_thd, knd
    INTEGER :: ic
    REAL(kind=r8) :: fnnreflm, fmomreflm, nnreflm, nnwwnreflm, fenereflm&
&   , fene_elm
    REAL(kind=r8), DIMENSION(nbdirsmax) :: fnnreflmd, fmomreflmd, &
&   nnreflmd, nnwwnreflmd, fenereflmd, fene_elmd
    REAL(kind=r8) :: fnnrefld, fmomrefld, nnrefld, nnwwnrefld, fenerefld&
&   , fene_eld
    REAL(kind=r8), DIMENSION(nbdirsmax) :: fnnrefldd, fmomrefldd, &
&   nnrefldd, nnwwnrefldd, fenerefldd, fene_eldd
    INTRINSIC LOG
    INTRINSIC SQRT
    REAL(kind=r8) :: arg1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: arg1d
    REAL(r8) :: arg2
    REAL(r8), DIMENSION(nbdirsmax) :: arg2d
    REAL(kind=r8) :: result1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: result1d
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(r8) :: temp0
    INTEGER :: nbdirs
!
    CALL SUBINI('CalcReflectedFluxesKn')
!
!     Calculate rate coefficients at boundary
    mn = am(isn)*mp
    vcx = 0.0_R8
    DO nd=1,nbdirsmax
      vcxd(nd) = 0.D0
    END DO
    DO ic=0,pl%ns-1
      t_av = (tif+tnf)/2.0_R8
      arg1 = t_av/(am(isn)*ev)
      temp = LOG(arg1)
      temp0 = rt%rlcx(icv, 1, ic, iscx0)
      DO nd=1,nbdirs
        t_avd(nd) = (tifd(nd)+tnfd(nd))/2.0_R8
        arg1d(nd) = t_avd(nd)/(am(isn)*ev)
        arg2d(nd) = rtd%rlcx(nd, icv, 0, ic, iscx0) + temp*rtd%rlcx(nd, &
&         icv, 1, ic, iscx0) + temp0*arg1d(nd)/arg1
      END DO
      arg2 = rt%rlcx(icv, 0, ic, iscx0) + temp0*temp
      CALL EXPU_DV(arg2, arg2d, result1, result1d, nbdirs)
      DO nd=1,nbdirs
        vcxd(nd) = vcxd(nd) + result1*pld%na(nd, icv, ic) + pl%na(icv, &
&         ic)*result1d(nd)
      END DO
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = tef/ev
    temp = LOG(arg1)
    DO nd=1,nbdirs
      arg1d(nd) = tefd(nd)/ev
      arg2d(nd) = rtd%rlsa(nd, icv, 0, isn) + temp*rtd%rlsa(nd, icv, 1, &
&       isn) + rt%rlsa(icv, 1, isn)*arg1d(nd)/arg1
      arg1d(nd) = tnfd(nd)/mn
    END DO
    arg2 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*temp
    CALL EXPU_DV(arg2, arg2d, result1, result1d, nbdirs)
    vion = result1*nef
    vtot = vion + vcx
    arg1 = tnf/mn
    temp = SQRT(arg1)
    c_th = temp
    DO nd=1,nbdirs
      viond(nd) = nef*result1d(nd) + result1*nefd(nd)
      vtotd(nd) = viond(nd) + vcxd(nd)
      IF (arg1 .EQ. 0.D0) THEN
        c_thd(nd) = 0.D0
      ELSE
        c_thd(nd) = arg1d(nd)/(2.0*temp)
      END IF
