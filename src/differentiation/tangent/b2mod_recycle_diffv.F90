  SUBROUTINE READ_RECYCLE_FNN(nsd, nbnd_local, use_coarse)
    USE B2MOD_B2CMPA_DIFFV
    USE B2MOD_ELEMENTS_DIFFV
    USE B2MOD_NEUTRALS_NAMELIST_DIFFV
    USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: nsd, nbnd_local, use_coarse
    CHARACTER(len=256) :: filename
    INTEGER :: istra, isp, is, il, it, itm, ndata
    INTEGER :: idum(0:9)
    EXTERNAL OPEN_FILE
    LOGICAL :: OPEN_FILE
!
    CHARACTER(len=10) :: fnn_version
    CHARACTER :: lblfnn*120
!nh 21.02.2020
    CHARACTER :: lblfnn_dummy*120
    CHARACTER(len=2) :: target_code(0:nbnd_local-1)
!
    CHARACTER(len=2) :: recycle_code(0:nsd-1)
    EXTERNAL CFRURE, CFRUIN
    INTRINSIC LEN_TRIM
    EXTERNAL LOWER_CASE
    EXTERNAL XERRAB
    INTEGER :: arg1
!
    IF (recycle_initialised) THEN
      RETURN
    ELSE
      CALL SUBINI('read_recycle_fnn')
      ns1 = nsd
      ALLOCATE(recycle_index(0:nsd-1))
      ALLOCATE(recycle_zn(0:nsd-1))
!! nuclear charge of recycling species
      ALLOCATE(target_index(0:nbnd_local-1))
!mb   Check how many different recycled species there are
      recycle_index = -1
      recycle_code = '  '
      isp = -1
      DO is=0,nsd-1
        DO il=0,isp
          IF (is_codes(is) .EQ. recycle_code(il)) THEN
            recycle_index(is) = il
            GOTO 5
          END IF
        END DO
        isp = isp + 1
        recycle_index(is) = isp
        recycle_code(isp) = is_codes(is)
        recycle_zn(isp) = zn(is)
 5      WRITE(*, '(1x,i3,1x,i3,1x,a)') is, recycle_index(is), &
&       recycle_code(recycle_index(is))
      END DO
!
!mb   Check how many different surface materials
!mb   Mixed materials not taken into account.
      target_index = -1
      target_code = '  '
      itm = -1
      DO istra=1,nstrai
        DO it=0,itm
          IF (surf_mat(istra) .EQ. target_code(it)) THEN
            target_index(istra) = it
            GOTO 6
          END IF
        END DO
        itm = itm + 1
        target_index(istra) = itm
        target_code(itm) = surf_mat(istra)
 6      WRITE(*, '(1x,i3,1x,i3,1x,a)') istra, target_index(istra), &
&       target_code(target_index(istra))
      END DO
!
      nm = -1
      nt = -1
      nshpottau = -1
      ALLOCATE(trimrecyc_present(0:isp, 0:itm))
      trimrecyc_present = .false.
      DO is=0,isp
        DO it=0,itm
          IF (use_coarse .EQ. 0) THEN
!c the finer tables were used for the original AFN work, but are too large to add to git for each H, D ,T x wall material combina
!tion
            filename = recycle_code(is)(1:LEN_TRIM(recycle_code(is)))//&
&             target_code(it)(1:LEN_TRIM(target_code(it)))//'.fnn'
          ELSE
!c therefore coarser tables were added for every [H D T] x [Be C Fe Mo W] combination. The difference with the finer tables shoul
!d be only a few percent
            filename = recycle_code(is)(1:LEN_TRIM(recycle_code(is)))//&
&             target_code(it)(1:LEN_TRIM(target_code(it)))//&
&             '.fnn.coarse'
          END IF
          CALL LOWER_CASE(filename)
!c first search if a new file has been made and stored locally by the user
!c in the SOLPSTOP/data.local tree structure, then in refl.data.local (not tracked by git),
!c then search in regular location (on git)
          IF (OPEN_FILE(96, filename, 'data.local/trim.data/refl.data|'&
&             //'modules/B2.5/Database/trim.data/refl.data.local|'//&
&             'modules/B2.5/Database/trim.data/refl.data')) &
&           trimrecyc_present(is, it) = .true.
          IF (trimrecyc_present(is, it)) THEN
            CALL CFVERR(96, fnn_version)
            CALL CFRUIN(96, 3, idum, 'nm,nt,nshpottau')
!           Check whether array has same dimensions as previous, otherwise give error
            IF (nm .EQ. -1 .AND. nt .EQ. -1 .AND. nshpottau .EQ. -1) &
&           THEN
!           Allocate arrays with sizes from first read data file
              nm = idum(0) - 1
              nt = idum(1) - 1
              nshpottau = idum(2) - 1
              ndata = (nm+1)*(nt+1)*(nshpottau+1)
!             ALLOCATE ARRAYS
              ALLOCATE(m(0:nm))
              ALLOCATE(t(0:nt))
              ALLOCATE(dm(0:nm-1))
              ALLOCATE(dt(0:nt-1))
              ALLOCATE(dsh(0:nshpottau-1))
              ALLOCATE(shpottau(0:nshpottau))
              ALLOCATE(rc1(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rc2(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rmx1(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rmx2(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rmy1(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rmy2(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rmz1(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rmz2(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(re1(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(re2(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rp11(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rp12(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rp21(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rp22(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rp31(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
              ALLOCATE(rp32(0:nm, 0:nt, 0:nshpottau, 0:isp, 0:itm))
!             Read label
              CALL CFRUCH(96, 120, lblfnn, 'label')
              arg1 = nm + 1
              CALL CFRURE(96, arg1, m, 'M')
              arg1 = nt + 1
              CALL CFRURE(96, arg1, t, 'T')
              arg1 = nshpottau + 1
              CALL CFRURE(96, arg1, shpottau, 'shpottau')
              dm(:) = 1.0_R8/(m(1:nm)-m(0:nm-1))
              dt(:) = 1.0_R8/(t(1:nt)-t(0:nt-1))
              dsh(:) = 1.0_R8/(shpottau(1:nshpottau)-shpottau(0:&
&               nshpottau-1))
!
            ELSE
              CALL XERTST(nm .EQ. idum(0) - 1 .AND. nt .EQ. idum(1) - 1 &
&                   .AND. nshpottau .EQ. idum(2) - 1, &
&                   'read_recycle_fnn: all .fnn files should have'//&
&                   ' same matrix dimensions')
!
!nh  After checking the dimensions of M, T and shpottau, we should skip the
!    first lines in the .fnn file. They are allocated to dummy variables.
              ALLOCATE(m_dummy(0:nm))
              ALLOCATE(t_dummy(0:nt))
              ALLOCATE(shpottau_dummy(0:nshpottau))
!
              CALL CFRUCH(96, 120, lblfnn_dummy, 'label')
              arg1 = nm + 1
              CALL CFRURE(96, arg1, m_dummy, 'M')
              arg1 = nt + 1
              CALL CFRURE(96, arg1, t_dummy, 'T')
              arg1 = nshpottau + 1
              CALL CFRURE(96, arg1, shpottau_dummy, 'shpottau')
!
              DEALLOCATE(m_dummy)
              DEALLOCATE(t_dummy)
              DEALLOCATE(shpottau_dummy)
            END IF
!
!         Read preintegrated recycling coefficient contributions
            CALL CFRURE(96, ndata, rc1(:, :, :, is, it), 'r1')
            CALL CFRURE(96, ndata, rc2(:, :, :, is, it), 'r2')
            CALL CFRURE(96, ndata, rmx1(:, :, :, is, it), 'rmx1')
            CALL CFRURE(96, ndata, rmx2(:, :, :, is, it), 'rmx2')
            CALL CFRURE(96, ndata, rmy1(:, :, :, is, it), 'rmy1')
            CALL CFRURE(96, ndata, rmy2(:, :, :, is, it), 'rmy2')
            CALL CFRURE(96, ndata, rmz1(:, :, :, is, it), 'rmz1')
            CALL CFRURE(96, ndata, rmz2(:, :, :, is, it), 'rmz2')
            CALL CFRURE(96, ndata, re1(:, :, :, is, it), 're1')
            CALL CFRURE(96, ndata, re2(:, :, :, is, it), 're2')
            CALL CFRURE(96, ndata, rp11(:, :, :, is, it), 'rp11')
            CALL CFRURE(96, ndata, rp12(:, :, :, is, it), 'rp12')
            CALL CFRURE(96, ndata, rp21(:, :, :, is, it), 'rp21')
            CALL CFRURE(96, ndata, rp22(:, :, :, is, it), 'rp22')
            CALL CFRURE(96, ndata, rp31(:, :, :, is, it), 'rp31')
            CALL CFRURE(96, ndata, rp32(:, :, :, is, it), 'rp32')
            CLOSE(96) 
          ELSE
!         Give error message or information if files are not found
            WRITE(*, *) 'No TRIM recycling data  for ', recycle_code(is)&
&           , ' onto ', target_code(it)
            IF (recycle_zn(is) .EQ. 0.0_R8) THEN
!! expected to use AFN BCs, so give error 
              IF (use_coarse .NE. 1) THEN
!! first suggest to go look for coarse files
                WRITE(*, *) 'Consider using b2stbr_afn_bcs_use_coarse'
              END IF
              CALL XERRAB('Error using advanced fluid neutral BCs')
            ELSE
!! currently, default neutral boundary conditions will automatically be used for non-hydrogenic species, so we should not give an
! error here
              WRITE(*, *) 'Default recycling model used for ', &
&             recycle_code(is), ' onto ', target_code(it)
            END IF
          END IF
        END DO
      END DO
!     Other initializations
!
      recycle_initialised = .true.
      CALL SUBEND()
      RETURN
    END IF
  END SUBROUTINE READ_RECYCLE_FNN

!  Differentiation of calcincidentfluxesdiffusion in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: int4l int1l int2l int3l int0l
!                fnni feneni nni nnwwni fmomni
!   with respect to varying inputs: int4l int1l int2l int3l int0l
!                tef tif dnndy dnndz nef *(rt.rlcx) *(rt.rlsa)
!                *(pl.na) *(pl.ua) tnf
!   Plus diff mem management of: geo.cvbb:in rt.rlcx:in rt.rlsa:in
!                pl.na:in pl.ua:in
!
!
  SUBROUTINE CALCINCIDENTFLUXESDIFFUSION_DV(icv, icn, ifc, isign, isn, &
&   iscx0, isi, istra, phi_app, area, pl, pld, tif, tifd, tnf, tnfd, tef&
&   , tefd, pof, nef, nefd, geo, geod, rt, rtd, dnndy, dnndyd, dnndz, &
&   dnndzd, cosa, sina, use_uy_uz_0, use_uy_uz_1, safeguard, fnni, fnnid&
&   , fmomni, fmomnid, nni, nnid, nnwwni, nnwwnid, feneni, fenenid, &
&   nbdirs)
!     This routine calculates the incident neutral particle,momentum and energy fluxes based
!     on the diffusion approximation (see papers of Niels Horsten)
! main reference:
!   Horsten, Niels, Giovanni Samaey, and M. Baelmans.
!  "Development and assessment of 2D fluid neutral models that include atomic databases and a microscopic reflection model."
!   Nuclear Fusion 57.11 (2017): 116043.
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFFV
    USE B2US_GEO_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2MOD_SUBSYS
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFFV), INTENT(IN) :: pld
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(GEOMETRY_DIFFV), INTENT(IN) :: geod
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFFV), INTENT(IN) :: rtd
    INTEGER, INTENT(IN) :: icv, icn, ifc, isn, iscx0, isi, istra, &
&   use_uy_uz_0, use_uy_uz_1, safeguard
    REAL(kind=r8), INTENT(IN) :: phi_app, area, tnf, tif, tef, pof, nef&
&   , isign, dnndy, dnndz, cosa, sina
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(IN) :: tnfd, tifd, tefd&
&   , nefd, dnndyd, dnndzd
!     Output variables:
!     fnni: incident neutral flux
!     fmomni: momentum flux of incident neutrals.
    REAL(kind=r8), INTENT(OUT) :: fnni, fmomni, nni, nnwwni, feneni
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(OUT) :: fnnid, fmomnid, &
&   nnid, nnwwnid, fenenid
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, pit, pit2, mn, nnf, ux, uy, uz, &
&   i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r&
&   , fmox, fmoy, fmoz, fmomnip, fmomnit, pn, t_av, upf
    REAL(kind=r8), DIMENSION(nbdirsmax) :: vcxd, viond, vtotd, nnfd, uxd&
&   , uyd, uzd, i0ld, i1ld, i2ld, i3ld, i4ld, fmoxd, fmoyd, fmozd, &
&   fmomnipd, fmomnitd, pnd, t_avd, upfd
    INTEGER :: ic
    INTRINSIC ABS
    INTRINSIC LOG
    REAL(kind=r8) :: arg1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: arg1d
    REAL(r8) :: arg2
    REAL(r8), DIMENSION(nbdirsmax) :: arg2d
    REAL(kind=r8) :: result1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: result1d
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(r8) :: temp0
    REAL(kind=r8) :: temp1
    REAL(kind=r8) :: temp2
    REAL(kind=r8) :: temp3
    REAL(r8) :: temp4
    REAL(r8) :: temp5
    REAL(r8) :: temp6
    REAL(kind=r8) :: temp7
    REAL(kind=r8) :: temp8
    INTEGER :: nbdirs
!
    CALL SUBINI('CalcIncidentFluxesDiffusion')
!     Initialize/calculate some variables
    DO nd=1,nbdirs
      nnfd(nd) = pld%na(nd, icv, isn)
    END DO
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
    DO nd=1,nbdirs
!mb   plasma velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and drifts
      upfd(nd) = pld%ua(nd, icn, isi)
      uxd(nd) = pit2*upfd(nd)
      uyd(nd) = -(sina*isign*pit*upfd(nd))
      uzd(nd) = -(cosa*isign*pit*upfd(nd))
    END DO
    upf = pl%ua(icn, isi)
    ux = upf*pit2
    uy = -(sina*isign*upf*pit)
    uz = -(cosa*isign*upf*pit)
!
!     Calculate rate coefficients at boundary
    vcx = 0.0_R8
    DO nd=1,nbdirsmax
      vcxd(nd) = 0.D0
    END DO
    DO ic=0,pl%ns-1
      t_av = (tif+tnf)/2.0_R8
      arg1 = t_av/(am(isn)*ev)
      temp = LOG(arg1)
      temp0 = rt%rlcx(icv, 1, ic, iscx0)
      DO nd=1,nbdirs
        t_avd(nd) = (tifd(nd)+tnfd(nd))/2.0_R8
        arg1d(nd) = t_avd(nd)/(am(isn)*ev)
        arg2d(nd) = rtd%rlcx(nd, icv, 0, ic, iscx0) + temp*rtd%rlcx(nd, &
&         icv, 1, ic, iscx0) + temp0*arg1d(nd)/arg1
      END DO
      arg2 = rt%rlcx(icv, 0, ic, iscx0) + temp0*temp
      CALL EXPU_DV(arg2, arg2d, result1, result1d, nbdirs)
      DO nd=1,nbdirs
        vcxd(nd) = vcxd(nd) + result1*pld%na(nd, icv, ic) + pl%na(icv, &
&         ic)*result1d(nd)
      END DO
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = tef/ev
    temp = LOG(arg1)
    DO nd=1,nbdirs
      arg1d(nd) = tefd(nd)/ev
      arg2d(nd) = rtd%rlsa(nd, icv, 0, isn) + temp*rtd%rlsa(nd, icv, 1, &
&       isn) + rt%rlsa(icv, 1, isn)*arg1d(nd)/arg1
    END DO
    arg2 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*temp
    CALL EXPU_DV(arg2, arg2d, result1, result1d, nbdirs)
    DO nd=1,nbdirs
      viond(nd) = nef*result1d(nd) + result1*nefd(nd)
      vtotd(nd) = viond(nd) + vcxd(nd)
    END DO
    vion = result1*nef
    vtot = vion + vcx
!
    IF (use_uy_uz_0 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
      DO nd=1,nbdirsmax
        uyd(nd) = 0.D0
      END DO
      DO nd=1,nbdirsmax
        uzd(nd) = 0.D0
      END DO
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN_DV(uz, uzd, tif, tifd, mn, i0l, i0ld, i0r&
&                           , i1l, i1ld, i1r, i2l, i2ld, i2r, i3l, i3ld&
&                           , i3r, i4l, i4ld, i4r, i5l, i5r, i6l, i6r, &
&                           nbdirs)
!
!     Incident particle flux density
!
    temp = vcx/(vtot*vtot)
    temp1 = dnndy*uy*i1l + dnndz*i2l
    temp2 = i1l/vtot
    DO nd=1,nbdirs
      fnnid(nd) = temp*(i1l*(uy*dnndyd(nd)+dnndy*uyd(nd))+dnndy*uy*i1ld(&
&       nd)+i2l*dnndzd(nd)+dnndz*i2ld(nd)) + temp1*(vcxd(nd)-temp*2*vtot&
&       *vtotd(nd))/vtot**2 - temp2*(vcx*nnfd(nd)+nnf*vcxd(nd)) - nnf*&
&       vcx*(i1ld(nd)-temp2*vtotd(nd))/vtot
    END DO
    fnni = temp1*temp - nnf*vcx*temp2
!
!     Assume poloidal and radial drifts are zero for momentum and energy
!     equations to reduce the dimensionality of the pre-evaluates integrals
!     of TRIM tables. Only toroidal drift remains!
    IF (use_uy_uz_1 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
      DO nd=1,nbdirsmax
        uyd(nd) = 0.D0
      END DO
      DO nd=1,nbdirsmax
        uzd(nd) = 0.D0
      END DO
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN_DV(uz, uzd, tif, tifd, mn, i0l, i0ld, i0r&
&                           , i1l, i1ld, i1r, i2l, i2ld, i2r, i3l, i3ld&
&                           , i3r, i4l, i4ld, i4r, i5l, i5r, i6l, i6r, &
&                           nbdirs)
!
!     Incident momentum flux densities
    temp2 = vcx/vtot
    temp1 = (dnndy*ux*uy*i1l+dnndz*ux*i2l)/vtot
    temp = temp1 - nnf*ux*i1l
    DO nd=1,nbdirs
      fmoxd(nd) = mn*(temp2*((uy*i1l*(ux*dnndyd(nd)+dnndy*uxd(nd))+dnndy&
&       *ux*(i1l*uyd(nd)+uy*i1ld(nd))+i2l*(ux*dnndzd(nd)+dnndz*uxd(nd))+&
&       dnndz*ux*i2ld(nd)-temp1*vtotd(nd))/vtot-i1l*(ux*nnfd(nd)+nnf*uxd&
&       (nd))-nnf*ux*i1ld(nd))+temp*(vcxd(nd)-temp2*vtotd(nd))/vtot)
!
!     Convert particle flux densities in fluxes
      fnnid(nd) = area*fnnid(nd)
      fmomnitd(nd) = -(area*fmoxd(nd))
    END DO
    fmox = mn*(temp*temp2)
!
    temp2 = vcx/vtot
    temp1 = tif/mn + uy*uy
    temp = (temp1*dnndy*i1l+dnndz*uy*i2l)/vtot
    temp3 = temp - nnf*uy*i1l
    DO nd=1,nbdirs
      fmoyd(nd) = mn*(temp2*((dnndy*i1l*(tifd(nd)/mn+2*uy*uyd(nd))+temp1&
&       *(i1l*dnndyd(nd)+dnndy*i1ld(nd))+i2l*(uy*dnndzd(nd)+dnndz*uyd(nd&
&       ))+dnndz*uy*i2ld(nd)-temp*vtotd(nd))/vtot-i1l*(uy*nnfd(nd)+nnf*&
&       uyd(nd))-nnf*uy*i1ld(nd))+temp3*(vcxd(nd)-temp2*vtotd(nd))/vtot)
    END DO
    fmoy = mn*(temp3*temp2)
!
    temp3 = vcx/vtot
    temp2 = (dnndy*uy*i2l+dnndz*i3l)/vtot
    DO nd=1,nbdirs
      fmozd(nd) = mn*(temp3*((i2l*(uy*dnndyd(nd)+dnndy*uyd(nd))+dnndy*uy&
&       *i2ld(nd)+i3l*dnndzd(nd)+dnndz*i3ld(nd)-temp2*vtotd(nd))/vtot-&
&       i2l*nnfd(nd)-nnf*i2ld(nd))+(temp2-nnf*i2l)*(vcxd(nd)-temp3*vtotd&
&       (nd))/vtot)
    END DO
    fmoz = mn*((temp2-nnf*i2l)*temp3)
!
!       Pressure of incident neutrals (total including contribution from the drift velocity)
    temp3 = vcx/(3.0_R8*vtot)
    temp0 = 2.0_R8*tif/mn + ux*ux + uy*uy
    temp4 = 4.0_R8*tif*uy/mn + ux*ux*uy + uy*uy*uy
    temp5 = temp4*i0l + uy*i2l
    temp6 = 2.0_R8*tif/mn + ux*ux + uy*uy
    temp2 = (dnndy*temp5+dnndz*(temp6*i1l+i3l))/vtot
    temp1 = nnf*(temp0*i0l+i2l) - temp2
    DO nd=1,nbdirs
      pnd(nd) = mn*(temp3*((temp0*i0l+i2l)*nnfd(nd)+nnf*(i0l*(2.0_R8*&
&       tifd(nd)/mn+2*ux*uxd(nd)+2*uy*uyd(nd))+temp0*i0ld(nd)+i2ld(nd))-&
&       (temp5*dnndyd(nd)+dnndy*(i0l*(4.0_R8*(uy*tifd(nd)+tif*uyd(nd))/&
&       mn+uy*2*ux*uxd(nd)+(ux**2+3*uy**2)*uyd(nd))+temp4*i0ld(nd)+i2l*&
&       uyd(nd)+uy*i2ld(nd))+(temp6*i1l+i3l)*dnndzd(nd)+dnndz*(i1l*(&
&       2.0_R8*tifd(nd)/mn+2*ux*uxd(nd)+2*uy*uyd(nd))+temp6*i1ld(nd)+&
&       i3ld(nd))-temp2*vtotd(nd))/vtot)+temp1*(vcxd(nd)-temp3*3.0_R8*&
&       vtotd(nd))/(3.0_R8*vtot))
!
!     Calculate incident poloidal momentum flux of neutrals
      fmomnipd(nd) = isign*area*(cosa*fmozd(nd)+sina*fmoyd(nd)) + isign*&
&       area*cosa*pnd(nd)
!     calculate parallel momentum boundary flux
      fmomnid(nd) = pit*fmomnipd(nd) + pit2*fmomnitd(nd)
    END DO
    pn = mn*(temp1*temp3)
!
!       Density of incident neutrals
    temp3 = vcx/vtot
    temp2 = (dnndy*uy*i0l+dnndz*i1l)/vtot
    nni = (nnf*i0l-temp2)*temp3
    DO nd=1,nbdirs
      nnid(nd) = temp3*(i0l*nnfd(nd)+nnf*i0ld(nd)-(i0l*(uy*dnndyd(nd)+&
&       dnndy*uyd(nd))+dnndy*uy*i0ld(nd)+i1l*dnndzd(nd)+dnndz*i1ld(nd)-&
&       temp2*vtotd(nd))/vtot) + (nnf*i0l-temp2)*(vcxd(nd)-temp3*vtotd(&
&       nd))/vtot
!
      nnwwnid(nd) = ux*nnid(nd) + nni*uxd(nd)
    END DO
    nnwwni = nni*ux
!
!     Incident total energy flux density of neutrals
    temp3 = vcx/vtot
    temp6 = 2.0_R8*tif + mn*(ux*ux+uy*uy)/2.0_R8
    temp5 = temp6*uy*i1l + mn*uy*i3l/2.0_R8
    temp2 = tif + mn*(ux*ux+uy*uy)/2.0_R8
    temp1 = temp2*i2l + mn*i4l/2.0_R8
    temp = (dnndy*temp5+dnndz*temp1)/vtot
    temp7 = tif + mn*(ux*ux+uy*uy)/2.0_R8
    temp8 = temp7*i1l + mn*i3l/2.0_R8
    DO nd=1,nbdirs
      fenenid(nd) = temp3*((temp5*dnndyd(nd)+dnndy*(uy*i1l*(2.0_R8*tifd(&
&       nd)+mn*(2*ux*uxd(nd)+2*uy*uyd(nd))/2.0_R8)+temp6*(i1l*uyd(nd)+uy&
&       *i1ld(nd))+mn*(i3l*uyd(nd)/2.0_R8+uy*i3ld(nd)/2.0_R8))+temp1*&
&       dnndzd(nd)+dnndz*(i2l*(tifd(nd)+mn*(2*ux*uxd(nd)+2*uy*uyd(nd))/&
&       2.0_R8)+temp2*i2ld(nd)+mn*i4ld(nd)/2.0_R8)-temp*vtotd(nd))/vtot-&
&       temp8*nnfd(nd)-nnf*(i1l*(tifd(nd)+mn*(2*ux*uxd(nd)+2*uy*uyd(nd))&
&       /2.0_R8)+temp7*i1ld(nd)+mn*i3ld(nd)/2.0_R8)) + (temp-nnf*temp8)*&
&       (vcxd(nd)-temp3*vtotd(nd))/vtot
!     Convert energy flux densities in net energy flux contributions
      fenenid(nd) = -(area*fenenid(nd))
    END DO
    feneni = (temp-nnf*temp8)*temp3
    fnni = fnni*area
    fmomnip = isign*(fmoz*cosa+fmoy*sina)*area + isign*pn*area*cosa
    fmomnit = -(fmox*area)
    fmomni = pit*fmomnip + pit2*fmomnit
    feneni = -(feneni*area)
!
! if incident flux is negative (this can happen if the diffusion approximation is used where it is not valid), use one-sided maxw
!ellian anyway
    IF (fnni .LT. 0.0_R8 .AND. safeguard .EQ. 1) CALL &
&     CALCINCIDENTFLUXESMAXWELLIAN_DV(icv, ifc, isn, isign, istra, area&
&                               , pl, pld, tnf, tnfd, geo, cosa, sina, &
&                               fnni, fnnid, fmomni, fmomnid, nni, nnid&
&                               , nnwwni, nnwwnid, feneni, fenenid, &
&                               nbdirs)
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCINCIDENTFLUXESDIFFUSION_DV

!
!
  SUBROUTINE CALCINCIDENTFLUXESDIFFUSION(icv, icn, ifc, isign, isn, &
&   iscx0, isi, istra, phi_app, area, pl, tif, tnf, tef, pof, nef, geo, &
&   rt, dnndy, dnndz, cosa, sina, use_uy_uz_0, use_uy_uz_1, safeguard, &
&   fnni, fmomni, nni, nnwwni, feneni)
!     This routine calculates the incident neutral particle,momentum and energy fluxes based
!     on the diffusion approximation (see papers of Niels Horsten)
! main reference:
!   Horsten, Niels, Giovanni Samaey, and M. Baelmans.
!  "Development and assessment of 2D fluid neutral models that include atomic databases and a microscopic reflection model."
!   Nuclear Fusion 57.11 (2017): 116043.
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFFV
    USE B2US_GEO_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(B2RATES), INTENT(IN) :: rt
    INTEGER, INTENT(IN) :: icv, icn, ifc, isn, iscx0, isi, istra, &
&   use_uy_uz_0, use_uy_uz_1, safeguard
    REAL(kind=r8), INTENT(IN) :: phi_app, area, tnf, tif, tef, pof, nef&
&   , isign, dnndy, dnndz, cosa, sina
!     Output variables:
!     fnni: incident neutral flux
!     fmomni: momentum flux of incident neutrals.
    REAL(kind=r8), INTENT(OUT) :: fnni, fmomni, nni, nnwwni, feneni
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, pit, pit2, mn, nnf, ux, uy, uz, &
&   i0l, i0r, i1l, i1r, i2l, i2r, i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r&
&   , fmox, fmoy, fmoz, fmomnip, fmomnit, pn, t_av, upf
    INTEGER :: ic
    INTRINSIC ABS
    INTRINSIC LOG
    REAL(kind=r8) :: arg1
    REAL(r8) :: arg2
    REAL(kind=r8) :: result1
!
    CALL SUBINI('CalcIncidentFluxesDiffusion')
!     Initialize/calculate some variables
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
!mb   plasma velocities toroidal (x),tangential (y), and normal (z) to boundary assuming absence of 9-point stencil and drifts
    upf = pl%ua(icn, isi)
    ux = upf*pit2
    uy = -(sina*isign*upf*pit)
    uz = -(cosa*isign*upf*pit)
!
!     Calculate rate coefficients at boundary
    vcx = 0.0_R8
    DO ic=0,pl%ns-1
      t_av = (tif+tnf)/2.0_R8
      arg1 = t_av/(am(isn)*ev)
      arg2 = rt%rlcx(icv, 0, ic, iscx0) + rt%rlcx(icv, 1, ic, iscx0)*LOG&
&       (arg1)
      result1 = EXPU(arg2)
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = tef/ev
    arg2 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*LOG(arg1)
    result1 = EXPU(arg2)
    vion = result1*nef
    vtot = vion + vcx
!
    IF (use_uy_uz_0 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Incident particle flux density
!
    fnni = vcx/vtot**2*(dnndy*uy*i1l+dnndz*i2l) - nnf*vcx/vtot*i1l
!
!     Assume poloidal and radial drifts are zero for momentum and energy
!     equations to reduce the dimensionality of the pre-evaluates integrals
!     of TRIM tables. Only toroidal drift remains!
    IF (use_uy_uz_1 .EQ. 0) THEN
      uy = 0.0_R8
      uz = 0.0_R8
    END IF
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN(uz, tif, mn, i0l, i0r, i1l, i1r, i2l, i2r&
&                        , i3l, i3r, i4l, i4r, i5l, i5r, i6l, i6r)
!
!     Incident momentum flux densities
    fmox = mn*vcx/vtot*(-(nnf*ux*i1l)+1.0_R8/vtot*(dnndy*ux*uy*i1l+dnndz&
&     *ux*i2l))
!
    fmoy = mn*vcx/vtot*(-(nnf*uy*i1l)+1.0_R8/vtot*(dnndy*(tif/mn+uy**2)*&
&     i1l+dnndz*uy*i2l))
!
    fmoz = mn*vcx/vtot*(-(nnf*i2l)+1.0_R8/vtot*(dnndy*uy*i2l+dnndz*i3l))
!
!       Pressure of incident neutrals (total including contribution from the drift velocity)
    pn = mn/3.0_R8*vcx/vtot*(nnf*((2.0_R8*tif/mn+ux**2+uy**2)*i0l+i2l)-&
&     1.0_R8/vtot*(dnndy*((4.0_R8*tif/mn*uy+ux**2*uy+uy**3)*i0l+uy*i2l)+&
&     dnndz*((2.0_R8*tif/mn+ux**2+uy**2)*i1l+i3l)))
!
!       Density of incident neutrals
    nni = vcx/vtot*(nnf*i0l-1.0_R8/vtot*(dnndy*uy*i0l+dnndz*i1l))
!
    nnwwni = nni*ux
!
!     Incident total energy flux density of neutrals
    feneni = vcx/vtot*(-(nnf*((tif+mn/2.0_R8*(ux**2+uy**2))*i1l+mn/&
&     2.0_R8*i3l))+1.0_R8/vtot*(dnndy*((2.0_R8*tif+mn/2.0_R8*(ux**2+uy**&
&     2))*uy*i1l+mn/2.0_R8*uy*i3l)+dnndz*((tif+mn/2.0_R8*(ux**2+uy**2))*&
&     i2l+mn/2.0_R8*i4l)))
!
!     Convert particle flux densities in fluxes
    fnni = fnni*area
!
!     Calculate incident poloidal momentum flux of neutrals
    fmomnip = isign*(fmoz*cosa+fmoy*sina)*area + isign*pn*area*cosa
    fmomnit = -(fmox*area)
!     calculate parallel momentum boundary flux
    fmomni = pit*fmomnip + pit2*fmomnit
!     Convert energy flux densities in net energy flux contributions
    feneni = -(feneni*area)
!
! if incident flux is negative (this can happen if the diffusion approximation is used where it is not valid), use one-sided maxw
!ellian anyway
    IF (fnni .LT. 0.0_R8 .AND. safeguard .EQ. 1) CALL &
&     CALCINCIDENTFLUXESMAXWELLIAN(icv, ifc, isn, isign, istra, area, pl&
&                            , tnf, geo, cosa, sina, fnni, fmomni, nni, &
&                            nnwwni, feneni)
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCINCIDENTFLUXESDIFFUSION

!  Differentiation of calcincidentfluxeskn in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: int4l int1l int2l int3l int0l
!                fnni feneni nni nnwwni fmomni
!   with respect to varying inputs: int4l int1l int2l int3l int0l
!                tef tif dnndy dnndz nef *(rt.rlcx) *(rt.rlsa)
!                *(pl.na) *(pl.ua) tnf
!   Plus diff mem management of: geo.cvbb:in rt.rlcx:in rt.rlsa:in
!                pl.na:in pl.ua:in
!
  SUBROUTINE CALCINCIDENTFLUXESKN_DV(kn_b1, kn_b2, icv, icn, ifc, isign&
&   , isn, iscx0, isi, istra, phi_app, area, pl, pld, tif, tifd, tnf, &
&   tnfd, tef, tefd, pof, nef, nefd, geo, geod, rt, rtd, dnndy, dnndyd, &
&   dnndz, dnndzd, cosa, sina, use_uy_uz_0, use_uy_uz_1, l, fnni, fnnid0&
&   , fmomni, fmomnid0, nni, nnid0, nnwwni, nnwwnid0, feneni, fenenid0, &
&   nbdirs)
!     This routine is an interface to CalcIncidentFluxesMaxwellian and CalcIncidentFluxesDiffusion
!     It will decide which one to use based on the Knudsen number (Kn) of the neutral atoms.
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFFV
    USE B2US_GEO_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2MOD_SUBSYS
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFFV), INTENT(IN) :: pld
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(GEOMETRY_DIFFV), INTENT(IN) :: geod
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFFV), INTENT(IN) :: rtd
    INTEGER, INTENT(IN) :: icv, icn, ifc, isn, iscx0, isi, istra, &
&   use_uy_uz_0, use_uy_uz_1
    REAL(kind=r8), INTENT(IN) :: phi_app, area, tnf, tif, tef, pof, nef&
&   , isign, dnndy, dnndz, cosa, sina, kn_b1, kn_b2, l
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(IN) :: tnfd, tifd, tefd&
&   , nefd, dnndyd, dnndzd
    REAL(kind=r8) :: t_av
    REAL(kind=r8), DIMENSION(nbdirsmax) :: t_avd
!     Output variables
    REAL(kind=r8), INTENT(OUT) :: fnni, fmomni, nni, nnwwni, feneni
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(OUT) :: fnnid0, fmomnid0&
&   , nnid0, nnwwnid0, fenenid0
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, mn, mfp, c_th, kn
    REAL(kind=r8), DIMENSION(nbdirsmax) :: vcxd, viond, vtotd, mfpd, &
&   c_thd, knd
    INTEGER :: ic
    REAL(kind=r8) :: fnnim, nnwwnim, nnim, fenenim, fmomnim
    REAL(kind=r8), DIMENSION(nbdirsmax) :: fnnimd, nnwwnimd, nnimd, &
&   fenenimd, fmomnimd
    REAL(kind=r8) :: fnnid, nnwwnid, nnid, fenenid, fmomnid
    REAL(kind=r8), DIMENSION(nbdirsmax) :: fnnidd, nnwwnidd, nnidd, &
&   fenenidd, fmomnidd
    INTRINSIC LOG
    INTRINSIC SQRT
    REAL(kind=r8) :: arg1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: arg1d
    REAL(r8) :: arg2
    REAL(r8), DIMENSION(nbdirsmax) :: arg2d
    REAL(kind=r8) :: result1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: result1d
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(r8) :: temp0
    INTEGER :: nbdirs
!
    CALL SUBINI('CalcIncidentFluxesKn')
!
!     Calculate rate coefficients at boundary
    mn = am(isn)*mp
    vcx = 0.0_R8
    DO nd=1,nbdirsmax
      vcxd(nd) = 0.D0
    END DO
    DO ic=0,pl%ns-1
      t_av = (tif+tnf)/2.0_R8
      arg1 = t_av/(am(isn)*ev)
      temp = LOG(arg1)
      temp0 = rt%rlcx(icv, 1, ic, iscx0)
      DO nd=1,nbdirs
        t_avd(nd) = (tifd(nd)+tnfd(nd))/2.0_R8
        arg1d(nd) = t_avd(nd)/(am(isn)*ev)
        arg2d(nd) = rtd%rlcx(nd, icv, 0, ic, iscx0) + temp*rtd%rlcx(nd, &
&         icv, 1, ic, iscx0) + temp0*arg1d(nd)/arg1
      END DO
      arg2 = rt%rlcx(icv, 0, ic, iscx0) + temp0*temp
      CALL EXPU_DV(arg2, arg2d, result1, result1d, nbdirs)
      DO nd=1,nbdirs
        vcxd(nd) = vcxd(nd) + result1*pld%na(nd, icv, ic) + pl%na(icv, &
&         ic)*result1d(nd)
      END DO
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = tef/ev
    temp = LOG(arg1)
    DO nd=1,nbdirs
      arg1d(nd) = tefd(nd)/ev
      arg2d(nd) = rtd%rlsa(nd, icv, 0, isn) + temp*rtd%rlsa(nd, icv, 1, &
&       isn) + rt%rlsa(icv, 1, isn)*arg1d(nd)/arg1
      arg1d(nd) = tnfd(nd)/mn
    END DO
    arg2 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*temp
    CALL EXPU_DV(arg2, arg2d, result1, result1d, nbdirs)
    vion = result1*nef
    vtot = vion + vcx
    arg1 = tnf/mn
    temp = SQRT(arg1)
    c_th = temp
    DO nd=1,nbdirs
      viond(nd) = nef*result1d(nd) + result1*nefd(nd)
      vtotd(nd) = viond(nd) + vcxd(nd)
      IF (arg1 .EQ. 0.D0) THEN
        c_thd(nd) = 0.D0
      ELSE
        c_thd(nd) = arg1d(nd)/(2.0*temp)
      END IF
      mfpd(nd) = (c_thd(nd)-c_th*vtotd(nd)/vtot)/vtot
!! L = macroscopic length scale
      knd(nd) = mfpd(nd)/l
    END DO
    mfp = c_th/vtot
    kn = mfp/l
!
!     if Kn is low, we use the diffusion approximation
!     if Kn is larger, we use the drifting maxwellian approximation
!     if the Kn is in between, we use a linear interpolation
    IF (kn .LT. kn_b1) THEN
!
!! <- '1': = safeguard for diffusion BCs
      CALL CALCINCIDENTFLUXESDIFFUSION_DV(icv, icn, ifc, isign, isn, &
&                                   iscx0, isi, istra, phi_app, area, pl&
&                                   , pld, tif, tifd, tnf, tnfd, tef, &
&                                   tefd, pof, nef, nefd, geo, geod, rt&
&                                   , rtd, dnndy, dnndyd, dnndz, dnndzd&
&                                   , cosa, sina, use_uy_uz_0, &
&                                   use_uy_uz_1, 1, fnni, fnnid0, fmomni&
&                                   , fmomnid0, nni, nnid0, nnwwni, &
