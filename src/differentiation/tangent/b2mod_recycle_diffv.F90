&   , rp11id, rp21id, rp31id, re1id, fnnid, fmoxd, fmozd, fmomreflpd, &
&   fmomrefltd, pnd, fened, tfevd, unfd, fna_mol_refld
    REAL(kind=r8) :: tab(0:nm, 0:nt)
    INTEGER :: is, it, ispt, ilt, ilm, ip1, ip2
    INTRINSIC ABS
    INTRINSIC SQRT
    INTRINSIC MAX
    INTRINSIC MIN
    INTRINSIC SIGN
    REAL(kind=r8) :: abs0
    REAL(kind=r8), DIMENSION(nbdirsmax) :: abs0d
    REAL(kind=r8) :: min1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: min1d
    REAL(kind=r8) :: min2
    REAL(kind=r8), DIMENSION(nbdirsmax) :: min2d
    REAL(r8) :: arg1
    REAL(r8), DIMENSION(nbdirsmax) :: arg1d
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(kind=r8), DIMENSION(nbdirsmax) :: x1d
    REAL(kind=r8) :: temp0
    REAL(r8) :: temp1
    INTEGER :: nbdirs
    REAL(kind=r8) :: x1
!
    CALL SUBINI('CalcReflectedFluxesMaxwellian')
!     Initialize/calculate some variables
    DO nd=1,nbdirs
      nnfd(nd) = pld%na(nd, icv, isn)
    END DO
    nnf = pl%na(icv, isn)
    mn = am(isn)*mp
    IF (geo%cvbb(icv, 0)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit = geo%cvbb(icv, 0)/geo%cvbb(icv, 3)
    ELSE
      pit = -(geo%cvbb(icv, 0)/geo%cvbb(icv, 3))
    END IF
    IF (geo%cvbb(icv, 2)/geo%cvbb(icv, 3) .GE. 0.) THEN
      pit2 = geo%cvbb(icv, 2)/geo%cvbb(icv, 3)
    ELSE
      pit2 = -(geo%cvbb(icv, 2)/geo%cvbb(icv, 3))
    END IF
    arg1 = 2.0_R8*tf/mn
    temp = SQRT(arg1)
    DO nd=1,nbdirs
!     Neutral velocity components: toroidal (x),tangential (y), and normal (z)
      unfd(nd) = pld%ua(nd, icv, isn)
      uxd(nd) = pit2*unfd(nd)
      uzd(nd) = -(cosa*isign*pit*unfd(nd))
!
!     Interpolate tables to find recycling values
!     Isothermal speed of sound and Mach number
      arg1d(nd) = 2.0_R8*tfd(nd)/mn
      IF (arg1 .EQ. 0.D0) THEN
        csd(nd) = 0.D0
      ELSE
        csd(nd) = arg1d(nd)/(2.0*temp)
      END IF
    END DO
    unf = pl%ua(icv, isn)
    ux = unf*pit2
    uz = -(cosa*isign*unf*pit)
!
!     Calculate the moments of a truncated drifting Maxwellian distribution
    CALL CALCMOMENTSMAXWELLIAN_DV(uz, uzd, tf, tfd, mn, i0l, i0ld, i0r, &
&                           i1l, i1ld, i1r, i2l, i2ld, i2r, i3l, i3ld, &
&                           i3r, i4l, i4ld, i4r, i5l, i5r, i6l, i6r, &
&                           nbdirs)
!
!     Calculate incident particle flux density
    DO nd=1,nbdirs
      fnnid(nd) = -(nnf*i1ld(nd)+i1l*nnfd(nd))
    END DO
    fnni = -(i1l*nnf)
    cs = temp
    IF (ux .GE. 0.) THEN
      DO nd=1,nbdirs
        abs0d(nd) = uxd(nd)
      END DO
      abs0 = ux
    ELSE
      DO nd=1,nbdirs
        abs0d(nd) = -uxd(nd)
      END DO
      abs0 = -ux
    END IF
    DO nd=1,nbdirs
      mjd(nd) = (abs0d(nd)-abs0*csd(nd)/cs)/cs
      tfevd(nd) = tfd(nd)/ev
    END DO
    mj = abs0/cs
    tfev = tf/ev
    is = recycle_index(isn)
    it = target_index(istra)
! Neutrals are not accelerated to sheath
    ispt = 0
!       Interpolate
    tab = rc1(:, :, ispt, is, it)
    CALL INTP_2DTABLE_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tfev, tfevd&
&                  , ilm, ilt, ip1, ip2, rc1i, rc1id, nbdirs)
    IF (rc1i .LT. 0.0_R8) THEN
      rc1i = 0.0_R8
      DO nd=1,nbdirsmax
        rc1id(nd) = 0.D0
      END DO
    ELSE
      rc1i = rc1i
    END IF
    tab = rc2(:, :, ispt, is, it)
    rc2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rc2i .LT. 0.0_R8) THEN
      rc2i = 0.0_R8
    ELSE
      rc2i = rc2i
    END IF
    tab = rmx1(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tfev, &
&                   tfevd, ilm, ilt, rmx1i, rmx1id, nbdirs)
    IF (rmx1i .LT. 0.0_R8) THEN
      rmx1i = 0.0_R8
      DO nd=1,nbdirsmax
        rmx1id(nd) = 0.D0
      END DO
    ELSE
      rmx1i = rmx1i
    END IF
    tab = rmx2(:, :, ispt, is, it)
    rmx2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rmx2i .LT. 0.0_R8) THEN
      rmx2i = 0.0_R8
    ELSE
      rmx2i = rmx2i
    END IF
    tab = rmy2(:, :, ispt, is, it)
    rmy2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rmy2i .LT. 0.0_R8) THEN
      rmy2i = 0.0_R8
    ELSE
      rmy2i = rmy2i
    END IF
    tab = rmz1(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tfev, &
&                   tfevd, ilm, ilt, rmz1i, rmz1id, nbdirs)
    IF (rmz1i .LT. 0.0_R8) THEN
      rmz1i = 0.0_R8
      DO nd=1,nbdirsmax
        rmz1id(nd) = 0.D0
      END DO
    ELSE
      rmz1i = rmz1i
    END IF
    tab = rmz2(:, :, ispt, is, it)
    rmz2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rmz2i .LT. 0.0_R8) THEN
      rmz2i = 0.0_R8
    ELSE
      rmz2i = rmz2i
    END IF
    tab = rp11(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tfev, &
&                   tfevd, ilm, ilt, rp11i, rp11id, nbdirs)
    IF (rp11i .LT. 0.0_R8) THEN
      rp11i = 0.0_R8
      DO nd=1,nbdirsmax
        rp11id(nd) = 0.D0
      END DO
    ELSE
      rp11i = rp11i
    END IF
    tab = rp21(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tfev, &
&                   tfevd, ilm, ilt, rp21i, rp21id, nbdirs)
    IF (rp21i .LT. 0.0_R8) THEN
      rp21i = 0.0_R8
      DO nd=1,nbdirsmax
        rp21id(nd) = 0.D0
      END DO
    ELSE
      rp21i = rp21i
    END IF
    tab = rp31(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tfev, &
&                   tfevd, ilm, ilt, rp31i, rp31id, nbdirs)
    IF (rp31i .LT. 0.0_R8) THEN
      rp31i = 0.0_R8
      DO nd=1,nbdirsmax
        rp31id(nd) = 0.D0
      END DO
    ELSE
      rp31i = rp31i
    END IF
    tab = rp12(:, :, ispt, is, it)
    rp12i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp12i .LT. 0.0_R8) THEN
      rp12i = 0.0_R8
    ELSE
      rp12i = rp12i
    END IF
    tab = rp22(:, :, ispt, is, it)
    rp22i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp22i .LT. 0.0_R8) THEN
      rp22i = 0.0_R8
    ELSE
      rp22i = rp22i
    END IF
    tab = rp32(:, :, ispt, is, it)
    rp32i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (rp32i .LT. 0.0_R8) THEN
      rp32i = 0.0_R8
    ELSE
      rp32i = rp32i
    END IF
    tab = re1(:, :, ispt, is, it)
    CALL INTP_2DTABLE0_DV(tab, m, t, dm, dt, nm, nt, mj, mjd, tfev, &
&                   tfevd, ilm, ilt, re1i, re1id, nbdirs)
    IF (re1i .LT. 0.0_R8) THEN
      re1i = 0.0_R8
      DO nd=1,nbdirsmax
        re1id(nd) = 0.D0
      END DO
    ELSE
      re1i = re1i
    END IF
    tab = re2(:, :, ispt, is, it)
    re2i = INTP_2DTABLE0(tab, m, t, dm, dt, nm, nt, mj, tfev, ilm, ilt)
    IF (re2i .LT. 0.0_R8) THEN
      re2i = 0.0_R8
    ELSE
      re2i = re2i
    END IF
    temp = cs/fnni
    DO nd=1,nbdirs
      x1d(nd) = temp*(rc1i*nnfd(nd)+nnf*rc1id(nd)) + nnf*rc1i*(csd(nd)-&
&       temp*fnnid(nd))/fnni
    END DO
    x1 = nnf*rc1i*temp
    IF (x1 .GT. recyc0) THEN
      DO nd=1,nbdirs
        min1d(nd) = recyc0d(nd)
      END DO
      min1 = recyc0
    ELSE
      DO nd=1,nbdirs
        min1d(nd) = x1d(nd)
      END DO
      min1 = x1
    END IF
!
!     Calculate fast and thermal reflected parts
    DO nd=1,nbdirs
      pfd(nd) = min1d(nd)
    END DO
    pf = min1 + 1.0e-30_R8
    IF (pf .GT. recyc0) THEN
      DO nd=1,nbdirs
        min2d(nd) = recyc0d(nd)
      END DO
      min2 = recyc0
    ELSE
      DO nd=1,nbdirs
        min2d(nd) = pfd(nd)
      END DO
      min2 = pf
    END IF
    pt = recyc0 - pf + 1.0e-30_R8
!     The reflection coefficients below already account for the probability of
!     fast particle reflection. Like in Eirene, we will only pump fast particles
!     once all thermal particles are pumped.
!
!nh 22.02.2020
!     Calculate the part of the reflected flux emitted as molecules. At the moment,
!     the molecules are treated kinetically.
    temp = 0.5_R8*recycm*area
    DO nd=1,nbdirs
      pcorfd(nd) = (min2d(nd)-min2*pfd(nd)/pf)/pf
      ptd(nd) = recyc0d(nd) - pfd(nd)
      fna_mol_refld(nd) = temp*(fnni*ptd(nd)+pt*fnnid(nd))
    END DO
    pcorf = min2/pf
    arg1 = 2.0_R8*e_fc*ev/mn
    vt = SQRT(arg1)
    fna_mol_refl = temp*(pt*fnni)
    fna_mol(ifc, isn) = fna_mol(ifc, isn) + fna_mol_refl
    IF (0.0_R8 .LT. (recyc0*fnni*area-2.0_R8*fna_mol_refl)*&
&       fluid_frac_hyb) THEN
      temp = area*recyc0*fnni - 2.0_R8*fna_mol_refl
      DO nd=1,nbdirs
        fnnrefld(nd) = fluid_frac_hyb*(area*(fnni*recyc0d(nd)+recyc0*&
&         fnnid(nd))-2.0_R8*fna_mol_refld(nd)) + temp*fluid_frac_hybd(nd&
&         )
      END DO
      fnnrefl = temp*fluid_frac_hyb
    ELSE
      fnnrefl = 0.0_R8
      DO nd=1,nbdirsmax
        fnnrefld(nd) = 0.D0
      END DO
    END IF
!
!     reflected momentum flux densities
    temp = mn*SIGN(1.0_R8, ux)
    temp0 = pcorf*nnf*(cs*cs)
    DO nd=1,nbdirs
      fmoxd(nd) = temp*(rmx1i*fluid_frac_hyb*(cs**2*(nnf*pcorfd(nd)+&
&       pcorf*nnfd(nd))+pcorf*nnf*2*cs*csd(nd))+temp0*(fluid_frac_hyb*&
&       rmx1id(nd)+rmx1i*fluid_frac_hybd(nd)))
!     Density of reflected neutrals
      nnrefld(nd) = rp21i*fluid_frac_hyb*(nnf*pcorfd(nd)+pcorf*nnfd(nd))&
&       + pcorf*nnf*(fluid_frac_hyb*rp21id(nd)+rp21i*fluid_frac_hybd(nd)&
&       )
      nnrefld(nd) = nnrefld(nd) + (1-recycm)*2.0_R8*(fluid_frac_hyb*(&
&       fnni*ptd(nd)+pt*fnnid(nd))/vt+pt*fnni*fluid_frac_hybd(nd)/vt)
      fmomrefltd(nd) = area*fmoxd(nd)
    END DO
    fmox = temp*(temp0*(rmx1i*fluid_frac_hyb))
    fmoy = 0._R8
    temp0 = pcorf*nnf*(cs*cs)
    DO nd=1,nbdirs
      fmozd(nd) = mn*(rmz1i*fluid_frac_hyb*(cs**2*(nnf*pcorfd(nd)+pcorf*&
&       nnfd(nd))+pcorf*nnf*2*cs*csd(nd))+temp0*(fluid_frac_hyb*rmz1id(&
&       nd)+rmz1i*fluid_frac_hybd(nd)))
    END DO
    fmoz = mn*(temp0*(rmz1i*fluid_frac_hyb))
    temp0 = 2.0_R8*(-recycm+1)*vt*mn
    DO nd=1,nbdirs
      fmozd(nd) = fmozd(nd) + temp0*(fnni*fluid_frac_hyb*ptd(nd)/3.0_R8+&
&       pt*(fluid_frac_hyb*fnnid(nd)+fnni*fluid_frac_hybd(nd))/3.0_R8)
    END DO
    fmoz = fmoz + temp0*(pt/3.0_R8*(fnni*fluid_frac_hyb))
!     Pressure of reflected neutrals
    temp0 = nnf*rp11i*(cs*cs)
    temp = pcorf/3.0_R8
    DO nd=1,nbdirs
      pnd(nd) = mn*(temp0*(fluid_frac_hyb*pcorfd(nd)/3.0_R8+temp*&
&       fluid_frac_hybd(nd))+temp*fluid_frac_hyb*(cs**2*(rp11i*nnfd(nd)+&
&       nnf*rp11id(nd))+nnf*rp11i*2*cs*csd(nd)))
    END DO
    pn = mn*(temp*fluid_frac_hyb*temp0)
    temp0 = 2.0_R8*(-recycm+1)*vt*mn
    DO nd=1,nbdirs
      pnd(nd) = pnd(nd) + temp0*(fnni*fluid_frac_hyb*ptd(nd)/3.0_R8+pt*(&
&       fluid_frac_hyb*fnnid(nd)+fnni*fluid_frac_hybd(nd))/3.0_R8)
!
!     Calculate reflected poloidal momentum flux of neutrals
      fmomreflpd(nd) = isign*area*cosa*pnd(nd) - isign*area*cosa*fmozd(&
&       nd)
      fmomrefld(nd) = pit*fmomreflpd(nd) + pit2*fmomrefltd(nd)
    END DO
    pn = pn + temp0*(pt/3.0_R8*(fnni*fluid_frac_hyb))
    nnrefl = pcorf*nnf*rp21i*fluid_frac_hyb
    nnrefl = nnrefl + (1-recycm)*pt*fnni*2.0_R8/vt*fluid_frac_hyb
!     Product of density and toroidal velocity
    temp1 = SIGN(1.0_R8, ux)
    temp0 = pcorf*nnf*fluid_frac_hyb
    DO nd=1,nbdirs
      nnwwnrefld(nd) = temp1*(rp31i*cs*(fluid_frac_hyb*(nnf*pcorfd(nd)+&
&       pcorf*nnfd(nd))+pcorf*nnf*fluid_frac_hybd(nd))+temp0*(cs*rp31id(&
&       nd)+rp31i*csd(nd)))
    END DO
    nnwwnrefl = temp1*(temp0*(rp31i*cs))
!     reflected total energy flux density of neutrals
    temp0 = pcorf*nnf*re1i*fluid_frac_hyb
    temp = cs*cs*cs
    DO nd=1,nbdirs
      fened(nd) = mn*0.5_R8*(temp0*3*cs**2*csd(nd)+temp*(re1i*&
&       fluid_frac_hyb*(nnf*pcorfd(nd)+pcorf*nnfd(nd))+pcorf*nnf*(&
&       fluid_frac_hyb*re1id(nd)+re1i*fluid_frac_hybd(nd))))
    END DO
    fene = mn*0.5_R8*(temp*temp0)
    temp0 = (-recycm+1)*e_fc*ev
    DO nd=1,nbdirs
      fened(nd) = fened(nd) + temp0*(fluid_frac_hyb*(fnni*ptd(nd)+pt*&
&       fnnid(nd))+pt*fnni*fluid_frac_hybd(nd))
!     Convert energy flux densities in energy fluxes
      fenerefld(nd) = area*fened(nd)
    END DO
    fene = fene + temp0*(pt*fnni*fluid_frac_hyb)
!! the Franck-Condon energy is provided by the electrons
    temp0 = (-recycm+1)*e_fc*ev
    DO nd=1,nbdirs
      fene_eld(nd) = -(temp0*(fluid_frac_hyb*(fnni*ptd(nd)+pt*fnnid(nd))&
&       +pt*fnni*fluid_frac_hybd(nd)))
      fene_eld(nd) = area*fene_eld(nd)
    END DO
    fene_el = -(temp0*(pt*fnni*fluid_frac_hyb))
    fmomreflp = -(isign*(fmoz*cosa+fmoy*sina)*area) + isign*pn*area*cosa
    fmomreflt = fmox*area
    fmomrefl = pit*fmomreflp + pit2*fmomreflt
    fenerefl = fene*area
    fene_el = fene_el*area
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCREFLECTEDFLUXESMAXWELLIAN_DV

