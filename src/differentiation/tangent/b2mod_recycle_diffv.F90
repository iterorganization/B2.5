!
!     int1r = int_{0}^{inf} v M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int1r = 0.5_R8*u*(1.0_R8-result1) + 0.5_R8/result2*vth*EXP(arg10)
!
!     int2l = int_{-inf}^{0} v^{2} M(v) dv
      CALL ERF_DV(arg, argd, result1, result1d, nbdirs)
      result2 = SQRT(pi)
      arg10 = arg**2
      temp0 = 0.5_R8*t/m + 0.5_R8*(u*u)
      temp = EXP(arg10)
      temp1 = vth*u/result2
      DO nd=1,nbdirs
        int2ld(nd) = (result1+1.0_R8)*(0.5_R8*td(nd)/m+0.5_R8*2*u*ud(nd)&
&         ) + temp0*result1d(nd) - 0.5_R8*(temp*(u*vthd(nd)+vth*ud(nd))/&
&         result2+temp1*EXP(arg10)*arg10d(nd))
        arg10d(nd) = 2*arg*argd(nd)
        i2ld(nd) = int2ld(nd)
      END DO
      int2l = temp0*(result1+1.0_R8) - 0.5_R8*(temp1*temp)
!
!     int2r = int_{0}^{inf} v^{2} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int2r = (0.5_R8*t/m+0.5_R8*u**2)*(1.0_R8-result1) + 0.5_R8/result2&
&       *vth*u*EXP(arg10)
!
! Implementation as optional arguments gave segmentation errors...
!     int3l = int_{-inf}^{0} v^{3} M(v) dv
      CALL ERF_DV(arg, argd, result1, result1d, nbdirs)
      result2 = SQRT(pi)
      arg10 = arg**2
      temp0 = 1.5_R8*t*u/m + 0.5_R8*(u*u*u)
      temp1 = EXP(arg10)/result2
      temp2 = 0.5_R8*(vth*vth*vth) + 0.5_R8*vth*(u*u)
      DO nd=1,nbdirs
        int3ld(nd) = (result1+1.0_R8)*(1.5_R8*(u*td(nd)+t*ud(nd))/m+&
&         0.5_R8*3*u**2*ud(nd)) + temp0*result1d(nd) - temp1*(0.5_R8*3*&
&         vth**2*vthd(nd)+0.5_R8*(u**2*vthd(nd)+vth*2*u*ud(nd))) - temp2&
&         *EXP(arg10)*arg10d(nd)/result2
        arg10d(nd) = 2*arg*argd(nd)
        i3ld(nd) = int3ld(nd)
      END DO
      int3l = temp0*(result1+1.0_R8) - temp2*temp1
!
!     int3r = int_{0}^{inf} v^{3} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int3r = (1.5_R8*t/m*u+0.5_R8*u**3)*(1-result1) + 1.0_R8/result2*(&
&       0.5_R8*vth**3+0.5_R8*vth*u**2)*EXP(arg10)
!
!     int4l = int_{-inf}^{0} v^{4} M(v) dv
      CALL ERF_DV(arg, argd, result1, result1d, nbdirs)
      result2 = SQRT(pi)
      arg10 = arg**2
      temp2 = 1.5_R8*(t*t)/(m*m) + 3.0_R8*(u*u)*t/m + u**4/2.0_R8
      temp1 = EXP(arg10)/result2
      temp = vth*vth*vth
      temp3 = u*u*u
      temp0 = 1.25_R8*temp*u + 0.5_R8*vth*temp3
      DO nd=1,nbdirs
        int4ld(nd) = (result1+1.0_R8)*(1.5_R8*2*t*td(nd)/m**2+3.0_R8*(t*&
&         2*u*ud(nd)/m+u**2*td(nd)/m)+4*u**3*ud(nd)/2.0_R8) + temp2*&
