      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int6r = (7.5_R8*(t/m)**3+22.5_R8*(t/m)**2*u**2+7.5_R8*t/m*u**4+&
&       0.5_R8*u**6)*(1.0_R8-result1) + 1.0_R8/result2*(4.125_R8*vth**5*&
&       u+3.5_R8*vth**3*u**3+0.5_R8*vth*u**5)*EXP(arg10)
      i0l = int0l
      i0r = int0r
      i1l = int1l
      i1r = int1r
      i2l = int2l
      i2r = int2r
      i3l = int3l
      i3r = int3r
      i4l = int4l
      i4r = int4r
      i5l = int5l
      i5r = int5r
      i6l = int6l
      i6r = int6r
      CALL SUBEND()
      RETURN
    END IF
  END SUBROUTINE CALCMOMENTSMAXWELLIAN_DV

