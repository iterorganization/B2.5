  SUBROUTINE PRECOMPUTE_KUL_QUANT(ncv, nfc, nvx, switch, geo, mpg, pl, &
&   dv, co, rt, dnn)
    USE B2MOD_TYPES
    USE B2MOD_NEUTRALS_NAMELIST_DIFFV
    USE B2US_GEO_DIFFV
    USE B2US_MAP_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2MOD_SWITCHES_DIFFV
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: ncv, nfc, nvx
    TYPE(SWITCHES), INTENT(IN) :: switch
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(MAPPING), INTENT(IN) :: mpg
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2DERIVATIVES), INTENT(INOUT) :: dv
    TYPE(B2COEFF), INTENT(INOUT) :: co
    TYPE(B2RATES), INTENT(IN) :: rt
    REAL(kind=r8), INTENT(INOUT) :: dnn(nfc, 0:1)
!     local variables
    INTEGER :: istra, i, irc1, isn, ifc
    INTEGER, SAVE :: ncall=0
    INTEGER, SAVE :: style_visc=0
    REAL(kind=r8) :: nnvx(nvx), nevx(nvx), tivx(nvx), min_kn(nstrat), &
&   mfp, mfp_out(ncv), kn, kn_ti, kn_ne, kn_out(ncv), kn_ti_out(ncv), &
&   kn_ne_out(ncv), l_ne_out(ncv), l_ti_out(ncv), dti(nfc, 0:1), dne(nfc&
&   , 0:1), l_ti, l_ne
    INTRINSIC SQRT
    REAL(kind=r8) :: arg1
    REAL(kind=r8) :: result1
!
    IF (ncall .EQ. 0) CALL IPGETI('b2sihs_style_visc', style_visc)
!! AFN_TODO generalize species index for multi-speciescases?
!! For now KUL AFN models have only been developed for single species cases.
    isn = 0
    CALL INTVERTEX_NODIFF(ncv, nvx, mpg, geo%vxvol, pl%na(:, isn), nnvx)
    CALL INTVERTEX_NODIFF(ncv, nvx, mpg, geo%vxvol, dv%ne, nevx)
    CALL INTVERTEX_NODIFF(ncv, nvx, mpg, geo%vxvol, pl%ti, tivx)
    CALL GRAD_NT_NODIFF(ncv, nfc, nvx, 1, geo, mpg, pl%na(:, isn), nnvx&
&                 , dnn)
    CALL GRAD_NODIFF(ncv, nfc, nvx, 1, geo, mpg, pl%ti, tivx, dti)
    CALL GRAD_NODIFF(ncv, nfc, nvx, 1, geo, mpg, dv%ne, nevx, dne)
!
    mfp_out = 0.0_R8
!     pre-computation of the minimal mean-free path along a boundary stratum
    min_kn(:) = 1.0_R8
    DO istra=1,nstrai
!   ..loop over boundary cells
      DO i=1,mpg%rccvp(istra, 2)
! the guard cell
        irc1 = mpg%rccv(mpg%rccvp(istra, 1)+i-1, 1)
! the guard face
        ifc = mpg%cvfc(mpg%cvfcp(irc1, 1))
        CALL CALCMINKN(irc1, isn, istra, pl, dv, rt, switch%l_macro_afn&
&                , min_kn, kn, mfp)
        mfp_out(irc1) = mfp
!
!c compute Knudsen number
!! gradient length of Ti
        arg1 = dti(ifc, 0)**2 + dti(ifc, 1)**2
        result1 = SQRT(arg1)
        l_ti = pl%ti(irc1)/result1
!! gradient length of ne
        arg1 = dne(ifc, 0)**2 + dne(ifc, 1)**2
        result1 = SQRT(arg1)
        l_ne = dv%ne(irc1)/result1
!          L = min(L_ti,L_ne) !! shortest gradient length
        kn = mfp/switch%l_macro_afn
        kn_ti = mfp/l_ti
        kn_ne = mfp/l_ne
        kn_out(irc1) = kn
        kn_ti_out(irc1) = kn_ti
        kn_ne_out(irc1) = kn_ne
        l_ti_out(irc1) = l_ti
        l_ne_out(irc1) = l_ne
!
!c compute fluid/kinetic fraction
        IF (switch%use_auto_spatial_hyb .EQ. 1 .AND. hyb_type(istra) &
&           .EQ. 'A') THEN
!! 'A' = automatic
          IF (kn .LE. switch%auto_spatial_hyb_kn_1) THEN
!! fluid treatment
            dv%fluid_frac_hyb(ifc) = 1.0_R8
            dv%kin_frac_hyb(ifc) = 0.0_R8
          ELSE IF (kn .GE. switch%auto_spatial_hyb_kn_2) THEN
!! kinetic treatment
            dv%fluid_frac_hyb(ifc) = 0.0_R8
            dv%kin_frac_hyb(ifc) = 1.0_R8
          ELSE
!! avoid discontinuities if a target face switches from fluid to kinetic. Linear interpolation in betweeen auto_Kn_1 and auto_Kn_
!2
            dv%kin_frac_hyb(ifc) = (kn-switch%auto_spatial_hyb_kn_1)/(&
&             switch%auto_spatial_hyb_kn_2-switch%auto_spatial_hyb_kn_1)
            dv%fluid_frac_hyb(ifc) = 1.0_R8 - dv%kin_frac_hyb(ifc)
          END IF
        ELSE
!! keep scaling factors both equal to 1.
          dv%fluid_frac_hyb(ifc) = 1.0_R8
          dv%kin_frac_hyb(ifc) = 1.0_R8
        END IF
      END DO
!
!c  choose the type of neutral reflection boundary condition, based on the minimum Kn along that boundary (only used if maxw(istr
!a) = 3).
      IF (min_kn(istra) .LT. switch%kn_b1) THEN
        maxw_eff(istra) = 0
      ELSE IF (min_kn(istra) .GT. switch%kn_b2) THEN
        maxw_eff(istra) = 1
      END IF
    END DO
!         do nothing to prevent oscillations if minimum Kn is just around Kn_b1
    IF (switch%afn_out .NE. 0) THEN
      CALL MY_OUT_US(70, ncv, 0, mfp_out, 'AFN_mfp')
      CALL MY_OUT_US(70, ncv, 0, kn_out, 'AFN_Kn')
      CALL MY_OUT_US(70, ncv, 0, kn_ti_out, 'AFN_Kn_ti')
      CALL MY_OUT_US(70, ncv, 0, kn_ne_out, 'AFN_Kn_ne')
      CALL MY_OUT_US(70, ncv, 0, l_ti_out, 'L_ti')
      CALL MY_OUT_US(70, ncv, 0, l_ne_out, 'L_ne')
    END IF
!
    ncall = ncall + 1
    RETURN
  END SUBROUTINE PRECOMPUTE_KUL_QUANT

