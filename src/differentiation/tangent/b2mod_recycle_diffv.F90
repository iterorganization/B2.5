!     int5l = int_{-inf}^{0} v^{5} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int5l = (7.5_R8*(t/m)**2*u+5.0_R8*t/m*u**3+u**5/2.0_R8)*(1.0_R8+&
&       result1) - 1.0_R8/result2*(vth**5+2.25_R8*vth**3*u**2+0.5_R8*vth&
&       *u**4)*EXP(arg10)
!
!     int5r = int_{0}^{inf} v^{5} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int5r = (7.5_R8*(t/m)**2*u+5.0_R8*t/m*u**3+u**5/2.0_R8)*(1.0_R8-&
