! u = drifting velocity
! T = temperature
! m = mass of a particle
    USE B2MOD_SUBSYS
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!input variables
!output variables
