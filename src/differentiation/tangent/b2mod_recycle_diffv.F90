        i0ld(nd) = int0ld(nd)
        i1ld(nd) = int1ld(nd)
        i2ld(nd) = int2ld(nd)
        i3ld(nd) = int3ld(nd)
        i4ld(nd) = int4ld(nd)
      END DO
