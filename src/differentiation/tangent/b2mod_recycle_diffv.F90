!
  SUBROUTINE CALCINCIDENTFLUXESKN(kn_b1, kn_b2, icv, icn, ifc, isign, &
&   isn, iscx0, isi, istra, phi_app, area, pl, tif, tnf, tef, pof, nef, &
&   geo, rt, dnndy, dnndz, cosa, sina, use_uy_uz_0, use_uy_uz_1, l, fnni&
&   , fmomni, nni, nnwwni, feneni)
!     This routine is an interface to CalcIncidentFluxesMaxwellian and CalcIncidentFluxesDiffusion
!     It will decide which one to use based on the Knudsen number (Kn) of the neutral atoms.
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFFV
    USE B2US_GEO_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(GEOMETRY), INTENT(IN) :: geo
    TYPE(B2RATES), INTENT(IN) :: rt
    INTEGER, INTENT(IN) :: icv, icn, ifc, isn, iscx0, isi, istra, &
&   use_uy_uz_0, use_uy_uz_1
    REAL(kind=r8), INTENT(IN) :: phi_app, area, tnf, tif, tef, pof, nef&
&   , isign, dnndy, dnndz, cosa, sina, kn_b1, kn_b2, l
    REAL(kind=r8) :: t_av
!     Output variables
    REAL(kind=r8), INTENT(OUT) :: fnni, fmomni, nni, nnwwni, feneni
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, mn, mfp, c_th, kn
    INTEGER :: ic
    REAL(kind=r8) :: fnnim, nnwwnim, nnim, fenenim, fmomnim
    REAL(kind=r8) :: fnnid, nnwwnid, nnid, fenenid, fmomnid
    INTRINSIC LOG
    INTRINSIC SQRT
    REAL(kind=r8) :: arg1
    REAL(r8) :: arg2
    REAL(kind=r8) :: result1
!
    CALL SUBINI('CalcIncidentFluxesKn')
!
!     Calculate rate coefficients at boundary
    mn = am(isn)*mp
    vcx = 0.0_R8
    DO ic=0,pl%ns-1
      t_av = (tif+tnf)/2.0_R8
      arg1 = t_av/(am(isn)*ev)
      arg2 = rt%rlcx(icv, 0, ic, iscx0) + rt%rlcx(icv, 1, ic, iscx0)*LOG&
&       (arg1)
      result1 = EXPU(arg2)
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = tef/ev
    arg2 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*LOG(arg1)
    result1 = EXPU(arg2)
    vion = result1*nef
    vtot = vion + vcx
    arg1 = tnf/mn
    c_th = SQRT(arg1)
    mfp = c_th/vtot
!! L = macroscopic length scale
    kn = mfp/l
!
!     if Kn is low, we use the diffusion approximation
!     if Kn is larger, we use the drifting maxwellian approximation
!     if the Kn is in between, we use a linear interpolation
    IF (kn .LT. kn_b1) THEN
!
!! <- '1': = safeguard for diffusion BCs
      CALL CALCINCIDENTFLUXESDIFFUSION(icv, icn, ifc, isign, isn, iscx0&
&                                , isi, istra, phi_app, area, pl, tif, &
&                                tnf, tef, pof, nef, geo, rt, dnndy, &
&                                dnndz, cosa, sina, use_uy_uz_0, &
&                                use_uy_uz_1, 1, fnni, fmomni, nni, &
&                                nnwwni, feneni)
!
    ELSE IF (kn .GT. kn_b2) THEN
!
      CALL CALCINCIDENTFLUXESMAXWELLIAN(icv, ifc, isn, isign, istra, &
&                                 area, pl, tnf, geo, cosa, sina, fnni, &
&                                 fmomni, nni, nnwwni, feneni)
!
    ELSE
      CALL CALCINCIDENTFLUXESMAXWELLIAN(icv, ifc, isn, isign, istra, &
&                                 area, pl, tnf, geo, cosa, sina, fnnim&
&                                 , fmomnim, nnim, nnwwnim, fenenim)
!! <- '1': = safeguard for diffusion BCs
      CALL CALCINCIDENTFLUXESDIFFUSION(icv, icn, ifc, isign, isn, iscx0&
&                                , isi, istra, phi_app, area, pl, tif, &
&                                tnf, tef, pof, nef, geo, rt, dnndy, &
&                                dnndz, cosa, sina, use_uy_uz_0, &
&                                use_uy_uz_1, 1, fnnid, fmomnid, nnid, &
&                                nnwwnid, fenenid)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, fnnid, fnnim, fnni)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, fmomnid, fmomnim, fmomni)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, nnid, nnim, nni)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, nnwwnid, nnwwnim, nnwwni)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, fenenid, fenenim, feneni)
    END IF
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCINCIDENTFLUXESKN

