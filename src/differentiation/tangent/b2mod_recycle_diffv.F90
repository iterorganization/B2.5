&                                 pl, tif, tnf, tef, pof, nef, geo, mpg&
&                                 , rt, dnndy, dnndz, cosa, sina, &
&                                 use_uy_uz_0, use_uy_uz_1, 1, fnnrefl, &
&                                 fmomrefl, nnrefl, nnwwnrefl, fenerefl&
&                                 , fene_el)
    ELSE IF (kn .GT. kn_b2) THEN
      CALL CALCREFLECTEDFLUXESMAXWELLIAN(icv, nci, ifc, isign, isn, &
&                                  istra, iwall, area, recyc0, recycm, &
&                                  fluid_frac_hyb, pl, tnf, geo, mpg, &
&                                  cosa, sina, fnnrefl, fmomrefl, nnrefl&
&                                  , nnwwnrefl, fenerefl, fene_el)
    ELSE
      CALL CALCREFLECTEDFLUXESMAXWELLIAN(icv, nci, ifc, isign, isn, &
&                                  istra, iwall, area, recyc0, recycm, &
&                                  fluid_frac_hyb, pl, tnf, geo, mpg, &
&                                  cosa, sina, fnnreflm, fmomreflm, &
&                                  nnreflm, nnwwnreflm, fenereflm, &
&                                  fene_elm)
!
!! <- '1': = safeguard for diffusion BCs
      CALL CALCREFLECTEDFLUXESDIFFUSION(icv, icn, nci, ifc, isign, isn, &
&                                 iscx0, isi, istra, iwall, phi_app, &
&                                 area, recyc0, recycm, fluid_frac_hyb, &
&                                 pl, tif, tnf, tef, pof, nef, geo, mpg&
&                                 , rt, dnndy, dnndz, cosa, sina, &
&                                 use_uy_uz_0, use_uy_uz_1, 1, fnnrefld&
&                                 , fmomrefld, nnrefld, nnwwnrefld, &
&                                 fenerefld, fene_eld)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, fnnrefld, fnnreflm, fnnrefl)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, fmomrefld, fmomreflm, fmomrefl)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, nnrefld, nnreflm, nnrefl)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, nnwwnrefld, nnwwnreflm, &
&                nnwwnrefl)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, fenerefld, fenereflm, fenerefl)
      CALL CALCLINCOMB(kn, kn_b1, kn_b2, fene_eld, fene_elm, fene_el)
    END IF
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCREFLECTEDFLUXESKN

