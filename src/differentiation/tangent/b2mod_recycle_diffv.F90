      int1r = 0.5_R8*u*(1.0_R8-result1) + 0.5_R8/result2*vth*EXP(arg10)
!
!     int2l = int_{-inf}^{0} v^{2} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int2l = (0.5_R8*t/m+0.5_R8*u**2)*(1.0_R8+result1) - 0.5_R8/result2&
&       *vth*u*EXP(arg10)
!
!     int2r = int_{0}^{inf} v^{2} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int2r = (0.5_R8*t/m+0.5_R8*u**2)*(1.0_R8-result1) + 0.5_R8/result2&
&       *vth*u*EXP(arg10)
!
! Implementation as optional arguments gave segmentation errors...
!     int3l = int_{-inf}^{0} v^{3} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int3l = (1.5_R8*t/m*u+0.5_R8*u**3)*(1.0_R8+result1) - 1.0_R8/&
&       result2*(0.5_R8*vth**3+0.5_R8*vth*u**2)*EXP(arg10)
!
!     int3r = int_{0}^{inf} v^{3} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int3r = (1.5_R8*t/m*u+0.5_R8*u**3)*(1-result1) + 1.0_R8/result2*(&
&       0.5_R8*vth**3+0.5_R8*vth*u**2)*EXP(arg10)
!
!     int4l = int_{-inf}^{0} v^{4} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int4l = (1.5_R8*(t/m)**2+3.0_R8*t/m*u**2+u**4/2.0_R8)*(1.0_R8+&
&       result1) - 1.0_R8/result2*(1.25_R8*vth**3*u+0.5_R8*vth*u**3)*EXP&
&       (arg10)
!
!     int4r = int_{0}^{inf} v^{4} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int4r = (1.5_R8*(t/m)**2+3.0_R8*t/m*u**2+u**4/2.0_R8)*(1.0_R8-&
&       result1) + 1.0_R8/result2*(1.25_R8*vth**3*u+0.5_R8*vth*u**3)*EXP&
&       (arg10)
!
!     int5l = int_{-inf}^{0} v^{5} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int5l = (7.5_R8*(t/m)**2*u+5.0_R8*t/m*u**3+u**5/2.0_R8)*(1.0_R8+&
&       result1) - 1.0_R8/result2*(vth**5+2.25_R8*vth**3*u**2+0.5_R8*vth&
&       *u**4)*EXP(arg10)
!
!     int5r = int_{0}^{inf} v^{5} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int5r = (7.5_R8*(t/m)**2*u+5.0_R8*t/m*u**3+u**5/2.0_R8)*(1.0_R8-&
&       result1) + 1.0_R8/result2*(vth**5+2.25_R8*vth**3*u**2+0.5_R8*vth&
&       *u**4)*EXP(arg10)
!
!     int6l = int_{-inf}^{0} v^{6} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int6l = (7.5_R8*(t/m)**3+22.5_R8*(t/m)**2*u**2+7.5_R8*t/m*u**4+&
&       0.5_R8*u**6)*(1.0_R8+result1) - 1.0_R8/result2*(4.125_R8*vth**5*&
&       u+3.5_R8*vth**3*u**3+0.5_R8*vth*u**5)*EXP(arg10)
!
!     int6r = int_{0}^{inf} v^{6} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int6r = (7.5_R8*(t/m)**3+22.5_R8*(t/m)**2*u**2+7.5_R8*t/m*u**4+&
&       0.5_R8*u**6)*(1.0_R8-result1) + 1.0_R8/result2*(4.125_R8*vth**5*&
&       u+3.5_R8*vth**3*u**3+0.5_R8*vth*u**5)*EXP(arg10)
      i0l = int0l
      i0r = int0r
      i1l = int1l
      i1r = int1r
      i2l = int2l
      i2r = int2r
      i3l = int3l
      i3r = int3r
      i4l = int4l
      i4r = int4r
      i5l = int5l
      i5r = int5r
      i6l = int6l
      i6r = int6r
      CALL SUBEND()
      RETURN
    END IF
  END SUBROUTINE CALCMOMENTSMAXWELLIAN

!  Differentiation of calclincomb in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: f
!   with respect to varying inputs: f1 f2 kn
!
  SUBROUTINE CALCLINCOMB_DV(kn, knd, kn1, kn2, f1, f1d, f2, f2d, f, fd, &
&   nbdirs)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!  Hint: nbdirsmax should be the maximum number of differentiation directions
!input variables
!output variables
    REAL(kind=r8), INTENT(IN) :: kn, kn1, kn2, f1, f2
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(IN) :: knd, f1d, f2d
    REAL(kind=r8), INTENT(OUT) :: f
    REAL(kind=r8), DIMENSION(nbdirsmax), INTENT(OUT) :: fd
    EXTERNAL XERRAB
    INTEGER :: nd
    INTEGER :: nbdirs
!                      F2
!              F       |
!       F1     |       |
!       |      |       |
!      Kn1----Kn------Kn2
    IF (kn .LT. kn1 .OR. kn .GT. kn2) CALL XERRAB(&
&                                     'Kn should be between Kn1 and Kn2'&
&                                          )
!
    DO nd=1,nbdirs
      fd(nd) = ((f2-f1)*knd(nd)+(kn-kn1)*f2d(nd)+(kn2-kn)*f1d(nd))/(kn2-&
&       kn1)
    END DO
    f = ((kn-kn1)*f2+(kn2-kn)*f1)/(kn2-kn1)
!
    RETURN
  END SUBROUTINE CALCLINCOMB_DV

!
  SUBROUTINE CALCLINCOMB(kn, kn1, kn2, f1, f2, f)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
!input variables
!output variables
    REAL(kind=r8), INTENT(IN) :: kn, kn1, kn2, f1, f2
    REAL(kind=r8), INTENT(OUT) :: f
    EXTERNAL XERRAB
!                      F2
!              F       |
!       F1     |       |
!       |      |       |
!      Kn1----Kn------Kn2
    IF (kn .LT. kn1 .OR. kn .GT. kn2) CALL XERRAB(&
&                                     'Kn should be between Kn1 and Kn2'&
&                                          )
!
    f = ((kn-kn1)*f2+(kn2-kn)*f1)/(kn2-kn1)
!
    RETURN
  END SUBROUTINE CALCLINCOMB

!  Differentiation of calcminkn in forward (tangent) mode (with options multiDirectional context noISIZE r8):
!   variations   of useful results: mfp
!   with respect to varying inputs: *(dv.ne) *(rt.rlcx) *(rt.rlsa)
!                *(pl.na) *(pl.te) *(pl.ti) *(pl.tn)
!   Plus diff mem management of: dv.ne:in rt.rlcx:in rt.rlsa:in
!                pl.na:in pl.te:in pl.ti:in pl.tn:in
!
  SUBROUTINE CALCMINKN_DV(icv, isn, istra, pl, pld, dv, dvd, rt, rtd, l&
&   , min_kn, kn, mfp, mfpd, nbdirs)
!     This routine calculates the minimum mean free path for a certain stratum
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2MOD_NEUTRALS_NAMELIST_DIFFV
    USE B2MOD_SUBSYS
!  Hint: nbdirsmax should be the maximum number of differentiation directions
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2PLASMA_DIFFV), INTENT(IN) :: pld
    TYPE(B2DERIVATIVES), INTENT(IN) :: dv
    TYPE(B2DERIVATIVES_DIFFV), INTENT(IN) :: dvd
    TYPE(B2RATES), INTENT(IN) :: rt
    TYPE(B2RATES_DIFFV), INTENT(IN) :: rtd
    INTEGER, INTENT(IN) :: icv, istra, isn
    REAL(kind=r8), INTENT(IN) :: l
!     InOut variables
    REAL(kind=r8), INTENT(INOUT) :: min_kn(nstrat)
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, mn, mfp, c_th, t_av
    REAL(kind=r8), DIMENSION(nbdirsmax) :: vcxd, viond, vtotd, mfpd, &
&   c_thd, t_avd
    REAL(kind=r8) :: tn, ti, ne, te, kn
    REAL(kind=r8), DIMENSION(nbdirsmax) :: tnd, tid, ned, ted
    INTEGER :: ic
    INTRINSIC LOG
    INTRINSIC SQRT
    INTRINSIC MIN
    REAL(kind=r8) :: arg1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: arg1d
    REAL(r8) :: arg2
    REAL(r8), DIMENSION(nbdirsmax) :: arg2d
    REAL(kind=r8) :: result1
    REAL(kind=r8), DIMENSION(nbdirsmax) :: result1d
    INTEGER :: nd
    REAL(kind=r8) :: temp
    REAL(r8) :: temp0
    INTEGER :: nbdirs
!
    CALL SUBINI('CalcMinKn')
    DO nd=1,nbdirs
!
!     Calculate rate coefficients at boundary
      tnd(nd) = pld%tn(nd, icv)
      tid(nd) = pld%ti(nd, icv)
      ned(nd) = dvd%ne(nd, icv)
      ted(nd) = pld%te(nd, icv)
    END DO
    tn = pl%tn(icv)
    ti = pl%ti(icv)
    ne = dv%ne(icv)
    te = pl%te(icv)
    mn = am(isn)*mp
    vcx = 0.0_R8
    DO nd=1,nbdirsmax
      vcxd(nd) = 0.D0
    END DO
    DO ic=0,pl%ns-1
      t_av = (ti+tn)/2.0_R8
      arg1 = t_av/(am(isn)*ev)
      temp = LOG(arg1)
      temp0 = rt%rlcx(icv, 1, ic, isn)
      DO nd=1,nbdirs
        t_avd(nd) = (tid(nd)+tnd(nd))/2.0_R8
        arg1d(nd) = t_avd(nd)/(am(isn)*ev)
        arg2d(nd) = rtd%rlcx(nd, icv, 0, ic, isn) + temp*rtd%rlcx(nd, &
&         icv, 1, ic, isn) + temp0*arg1d(nd)/arg1
      END DO
      arg2 = rt%rlcx(icv, 0, ic, isn) + temp0*temp
      CALL EXPU_DV(arg2, arg2d, result1, result1d, nbdirs)
      DO nd=1,nbdirs
        vcxd(nd) = vcxd(nd) + result1*pld%na(nd, icv, ic) + pl%na(icv, &
&         ic)*result1d(nd)
      END DO
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = te/ev
    temp = LOG(arg1)
    DO nd=1,nbdirs
      arg1d(nd) = ted(nd)/ev
      arg2d(nd) = rtd%rlsa(nd, icv, 0, isn) + temp*rtd%rlsa(nd, icv, 1, &
&       isn) + rt%rlsa(icv, 1, isn)*arg1d(nd)/arg1
      arg1d(nd) = tnd(nd)/mn
    END DO
    arg2 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*temp
    CALL EXPU_DV(arg2, arg2d, result1, result1d, nbdirs)
    vion = result1*ne
    vtot = vion + vcx
    arg1 = tn/mn
    temp = SQRT(arg1)
    c_th = temp
    DO nd=1,nbdirs
      viond(nd) = ne*result1d(nd) + result1*ned(nd)
      vtotd(nd) = viond(nd) + vcxd(nd)
      IF (arg1 .EQ. 0.D0) THEN
        c_thd(nd) = 0.D0
      ELSE
        c_thd(nd) = arg1d(nd)/(2.0*temp)
      END IF
      mfpd(nd) = (c_thd(nd)-c_th*vtotd(nd)/vtot)/vtot
    END DO
    mfp = c_th/vtot
    kn = mfp/l
    IF (kn .GT. min_kn(istra)) THEN
      min_kn(istra) = min_kn(istra)
    ELSE
      min_kn(istra) = kn
    END IF
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCMINKN_DV

!
  SUBROUTINE CALCMINKN(icv, isn, istra, pl, dv, rt, l, min_kn, kn, mfp)
!     This routine calculates the minimum mean free path for a certain stratum
    USE B2MOD_RATES
    USE B2MOD_B2CMPA_DIFFV
    USE B2US_PLASMA_DIFFV
    USE B2MOD_NEUTRALS_NAMELIST_DIFFV
    USE B2MOD_SUBSYS
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    TYPE(B2PLASMA), INTENT(IN) :: pl
    TYPE(B2DERIVATIVES), INTENT(IN) :: dv
    TYPE(B2RATES), INTENT(IN) :: rt
    INTEGER, INTENT(IN) :: icv, istra, isn
    REAL(kind=r8), INTENT(IN) :: l
!     InOut variables
    REAL(kind=r8), INTENT(INOUT) :: min_kn(nstrat)
!     Internal variables
    REAL(kind=r8) :: vcx, vion, vtot, mn, mfp, c_th, t_av
    REAL(kind=r8) :: tn, ti, ne, te, kn
    INTEGER :: ic
    INTRINSIC LOG
    INTRINSIC SQRT
    INTRINSIC MIN
    REAL(kind=r8) :: arg1
    REAL(r8) :: arg2
    REAL(kind=r8) :: result1
!
    CALL SUBINI('CalcMinKn')
!
!     Calculate rate coefficients at boundary
    tn = pl%tn(icv)
    ti = pl%ti(icv)
    ne = dv%ne(icv)
    te = pl%te(icv)
    mn = am(isn)*mp
    vcx = 0.0_R8
    DO ic=0,pl%ns-1
      t_av = (ti+tn)/2.0_R8
      arg1 = t_av/(am(isn)*ev)
      arg2 = rt%rlcx(icv, 0, ic, isn) + rt%rlcx(icv, 1, ic, isn)*LOG(&
&       arg1)
      result1 = EXPU(arg2)
      vcx = vcx + pl%na(icv, ic)*result1
    END DO
    arg1 = te/ev
    arg2 = rt%rlsa(icv, 0, isn) + rt%rlsa(icv, 1, isn)*LOG(arg1)
    result1 = EXPU(arg2)
    vion = result1*ne
    vtot = vion + vcx
    arg1 = tn/mn
    c_th = SQRT(arg1)
    mfp = c_th/vtot
    kn = mfp/l
    IF (kn .GT. min_kn(istra)) THEN
      min_kn(istra) = min_kn(istra)
    ELSE
      min_kn(istra) = kn
    END IF
!
    CALL SUBEND()
    RETURN
  END SUBROUTINE CALCMINKN

