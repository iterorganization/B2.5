    REAL(kind=r8) :: temp1
    REAL(r8) :: temp2
    REAL(kind=r8) :: temp3
    INTEGER :: nbdirs
!      external S15AEC !mb NAG erf function
    CALL SUBINI('CalcMomentsMaxwellian')
!
    IF (uold .EQ. u .AND. told .EQ. t .AND. mold .EQ. m) THEN
      DO nd=1,nbdirs
