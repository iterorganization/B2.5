&         )+0.5_R8*(temp3*vthd(nd)+vth*3*u**2*ud(nd))) - temp0*EXP(arg10&
&         )*arg10d(nd)/result2
        i4ld(nd) = int4ld(nd)
      END DO
      int4l = temp2*(result1+1.0_R8) - temp0*temp1
!
!     int4r = int_{0}^{inf} v^{4} M(v) dv
      result1 = ERF(arg)
      result2 = SQRT(pi)
      arg10 = arg**2
      int4r = (1.5_R8*(t/m)**2+3.0_R8*t/m*u**2+u**4/2.0_R8)*(1.0_R8-&
&       result1) + 1.0_R8/result2*(1.25_R8*vth**3*u+0.5_R8*vth*u**3)*EXP&
&       (arg10)
