    fmoz = fmoz + (1-recycm)*pt*2.0_R8/3.0_R8*mn*fnni*vt*fluid_frac_hyb
!     Pressure of reflected neutrals
    pn = pcorf*mn/3.0_R8*nnf*rp11i*cs**2*fluid_frac_hyb
    pn = pn + (1-recycm)*pt*2.0_R8/3.0_R8*mn*fnni*vt*fluid_frac_hyb
!     Density of reflected neutrals
    nnrefl = pcorf*nnf*rp21i*fluid_frac_hyb
    nnrefl = nnrefl + (1-recycm)*pt*fnni*2.0_R8/vt*fluid_frac_hyb
!     Product of density and toroidal velocity
    nnwwnrefl = pcorf*nnf*rp31i*cs*SIGN(1.0_R8, ux)*fluid_frac_hyb
!     reflected total energy flux density of neutrals
    fene = 0.5_R8*pcorf*mn*nnf*re1i*cs**3*fluid_frac_hyb
    fene = fene + (1-recycm)*pt*fnni*e_fc*ev*fluid_frac_hyb
!! the Franck-Condon energy is provided by the electrons
    fene_el = -((1-recycm)*pt*fnni*e_fc*ev*fluid_frac_hyb)
!
!     Calculate reflected poloidal momentum flux of neutrals
    fmomreflp = -(isign*(fmoz*cosa+fmoy*sina)*area) + isign*pn*area*cosa
    fmomreflt = fmox*area
    fmomrefl = pit*fmomreflp + pit2*fmomreflt
!     Convert energy flux densities in energy fluxes
    fenerefl = fene*area
    fene_el = fene_el*area
!
    CALL SUBEND()
    RETURN
