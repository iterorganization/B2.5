&         .OR. switch%nstg_aresco .EQ. 0.0_R8) .AND. (st%diag%aresmo(&
&         ismain)*switch%nstg_aresmo .LE. switch%nstg_aresmo*switch%&
&         nstg_aresmo .OR. switch%nstg_aresmo .EQ. 0.0_R8)
!      ..compute norms of the corrections
        CALL B2MXAC_NODIFF(ncv, ns, st%dv, st%diag)
!      ..decide what output
        domoqt = 0 .LE. switch%b2mndt_moqtlv .AND. (1 .LE. switch%&
&         b2mndt_moqtlv .OR. istg(0) .EQ. 0) .AND. (2 .LE. switch%&
&         b2mndt_moqtlv .OR. istg(1) .EQ. 0) .AND. (3 .LE. switch%&
&         b2mndt_moqtlv .OR. istg(2) .EQ. 0)
        domoit = 0 .LE. switch%b2mndt_moitlv .AND. (1 .LE. switch%&
&         b2mndt_moitlv .OR. istg(0) .EQ. 0) .AND. (2 .LE. switch%&
&         b2mndt_moitlv .OR. istg(1) .EQ. 0) .AND. (3 .LE. switch%&
&         b2mndt_moitlv .OR. istg(2) .EQ. 0)
!      ..maybe write quick trace
        IF (domoqt) THEN
          IF (MOD(ncall_b2mndt, switch%ntim_step_out) .EQ. 0) THEN
!srv 31.01.07
            CALL B2MWQT_NODIFF(nout(4), ncv, ns, itim, b2mndt_itcnt, &
&                        switch, geo, st%pl, st%dv, st%diag)
            FLUSH(nout(4)) 
          END IF
        END IF
!      ..maybe detailed monitor
        IF (domoit) THEN
          CALL B2MWIT_NODIFF(nout(5), ncv, ns, itim, b2mndt_itcnt, st%dv&
&                     )
          FLUSH(nout(5)) 
        END IF
!      ..write the tracing data
        IF (ank_tracing .NE. 0) THEN
          IF (ncall_b2mndt .EQ. 0 .AND. istg(2) .EQ. 0) THEN
            CALL ALLOC_B2MOD_USER(geo, mpg, ns, nlim, nnmoli, switch)
            CALL B2FILEI(ns, mpg%nnreg(0), switch)
          END IF
          IF (MOD(itim, ank_tracing) .EQ. 1 .OR. ank_tracing .EQ. 1) &
&           CALL B2FILEW(istg(2), mpg%nnreg(0), st%diag)
        END IF
!      ..update iteration counter
        b2mndt_itcnt = b2mndt_itcnt + 1
!      ..end inner iteration
        istg(2) = istg(2) + 1
        GOTO 3
      ELSE
        IF (ntim .EQ. 0) THEN
!iyv 06
! compute derived quantities
          IF (switch%ion_vlct_restrict .NE. 0) THEN
!iyv 06.02.14
            CALL B2XPRZ_NODIFF(ncv, ns, mp, am, st%pl%na, rz, st_ext)
            CALL B2XPPZ_NODIFF(ncv, ns, st%dv%ne, st%pl%na, st%pl%te, st&
&                        %pl%ti, st%dv%pz, st_ext)
            arg10 = st%dv%pz/rz
            result11 = SQRT(arg10)
            wrk0 = result11*geo%cvbb(:, 0)/geo%cvbb(:, 3)
            CALL INTFACE(ncv, nfc, mpg%fccv, geo%fcvol, wrk0, st%co%cssb&
&                 )
          ELSE
            st%co%cssb = 0.0_R8
          END IF
          DO is=0,ns-1
!    ..compute partial pressure
!srv 01.10.99
            CALL B2XPPB_NODIFF(ncv, st%rt%rza(:, is), st%pl%na(:, is), &
&                        st%pl%te, st%pl%ti, st%pl%tn, is, st%dv%pa(:, &
&                        is))
!    ..compute particle flux
            CALL B2TFNB_NODIFF(ncv, nfc, nvx, is, ismain, switch, geo, &
&                        mpg, st%pl, st%dv, st%co, st%rt, .true.)
!srv 01.10.99
          END DO
!      ..compute electron drift velocities
          CALL B2TFED_NODIFF(ncv, nfc, nvx, switch, geo, mpg, st%dv%&
&                      facdrift, st%dv%fac_exb, st%dv%ne, st%pl%te, st%&
&                      pl%po, st%dv%vedia, st%dv%veecrb)
!      ..compute other fluxes
          CALL B2XPEN_NODIFF(ncv, nfc, ns, mpg, switch, st%dv%fna, st%dv&
&                      %fna_53, st%dv%fne_53, st%dv%fch, st%dv%fhe, st%&
&                      dv%fhi, st%dv%fhn, st%rt%rpt, st%pl%te, st%pl%ti&
&                      , st%pl%tn, st%pl%po, st%dv%fhm, st%dv%fhp, st%dv&
&                      %fhj, st%dv%fht)
!   ..compute ue, electron parallel velocity                             !srv 05.03.11 {
          IF (switch%b2sigp_style .EQ. 2) THEN
            CALL B2XPVE_NODIFF(ncv, nfc, ns, switch, geo, mpg, qe, st%rt&
&                        %rza, st%rt%rz2, st%co%alfx_c, st%co%sigx_c, st&
&                        %dv%fch_pi_c, st%pl%po, st%dv%ne, st%pl%te, st%&
&                        pl%na, st%pl%ua, st_ext, st%dv%ue)
          ELSE
            wrkf = -(st%dv%fch_p(:, 0)/(qe*(geo%fcpbs+b2news_cutlo)))
            CALL INTCELL_NODIFF(nfc, ncv, mpg, mpg%intcellp, wrkf, st%dv&
&                         %ue)
            DO is=0,ns-1
              st%dv%ue = st%dv%ue + st%rt%rza(:, is)*st%pl%ua(:, is)*st%&
&               pl%na(:, is)
            END DO
            st%dv%ue = st%dv%ue + st_ext%ue*st_ext%ne
!srv 05.03.11
            st%dv%ue = st%dv%ue/st%dv%ne
          END IF
!   ..compute ceqp                                                       !srv 03.07.03 {
          CALL B2XPNM_NODIFF(ncv, ns, st%rt%rz2, am, st%pl%na, wrk0)
!srv 03.07.03 }
          CALL B2TREQ_NODIFF(ncv, switch, geo%cvvol, st%pl%te, st%pl%ti&
&                      , st%dv%ne, st%dv%ni, wrk0, st%dv%lnlam, st_ext, &
&                      st%co%ceqp)
!
!      ..compute energy sources
          solving = .false.
!srv 22.05.18
          CALL B2NPHT_NODIFF(ncv, nfc, nvx, ns, switch, geo, mpg, &
&                      ncall_b2news_, ismain, solving, solveee, solveei&
&                      , solveen, solveet, solvepo, solvemo, solvekt, &
&                      solvezt, switch%b2mndt_rxf, dtim, st%pl, st%dv, &
&                      st%co, st%rt, st%sr, st%srw, st%psnl, st_ext)
        END IF
!     ..end intermediate iteration
        istg(1) = istg(1) + 1
        GOTO 2
      END IF
    ELSE
!    ..end outer iteration
      istg(0) = istg(0) + 1
      GOTO 1
    END IF
  ELSE
!
!   ..mms time-dependent error analysis
    IF (switch%use_mms .EQ. 2 .AND. MOD(ncall_b2mndt + 1, switch%&
&       calc_err_step_out) .EQ. 0 .AND. switch%b2mndt_dummy .EQ. 0) CALL&
&     CALC_ERR(ncv, ns, st%pl, switch, itim, mpg)
!
    IF (switch%iav_run .GT. 0) CALL RUNNING_AVERAGE(ncv, ns, st%pl, st%&
&                                             srw, st_avg)
    IF (ibatch_av_all .GT. 0) CALL BATCH_AV_ALL(itim, ntim_batch, ncv, &
&                                         st%pl, ns)
!
! ..return
!Reset ncall_b2mndt to 0 in case of initial dummy call for Crank Nicolson
!This is currently necessary to make Crank Nicolson work
!To be assessed whether other counters ncall_* should be reset, too
    ncall_b2mndt = ncall_b2mndt + 1
!
    IF (switch%b2mndt_dummy .EQ. 1) ncall_b2mndt = 0
!
    CALL SUBEND()
    RETURN
!
!-----------------------------------------------------------------------
!.end b2mndt
!
  END IF
END SUBROUTINE B2MNDT_NODIFF

