    wrk0 = she0(:, 0) + she0(:, 1)*pl%te + she0(:, 2)*dv%ne + she0(:, 3)&
&     *pl%te*dv%ne
    CALL MY_OUT_US(70, ncv, 0, wrk0, 'b2npht_she')
    CALL MY_OUT_US(70, ncv, 0, dv%reshi, 'b2npht_reshi')
    CALL MY_OUT_US(70, ncv, 0, dv%reshn, 'b2npht_reshn')
    CALL MY_OUT_US(70, ncv, 0, dv%resht, 'b2npht_resht')
!srv 22.05.18
    CALL MY_OUT_US(70, nfc, 1, dv%fhi(1, 0), 'b2npht_fhi_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhi(1, 1), 'b2npht_fhi_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fhn(1, 0), 'b2npht_fhn_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fhn(1, 1), 'b2npht_fhn_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fkt(1, 0), 'b2npht_fkt_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fkt(1, 1), 'b2npht_fkt_r')
    CALL MY_OUT_US(70, nfc, 1, dv%fzt(1, 0), 'b2npht_fzt_th')
    CALL MY_OUT_US(70, nfc, 1, dv%fzt(1, 1), 'b2npht_fzt_r')
    CALL MY_OUT_US(70, ncv, 0, shi0(1, 0), 'b2npht_shi0')
    CALL MY_OUT_US(70, ncv, 0, shi0(1, 1), 'b2npht_shi1')
    CALL MY_OUT_US(70, ncv, 0, shi0(1, 2), 'b2npht_shi2')
    CALL MY_OUT_US(70, ncv, 0, shi0(1, 3), 'b2npht_shi3')
    wrk0 = shi0(:, 0) + shi0(:, 1)*pl%ti + shi0(:, 2)*dv%ni(:, 0) + shi0&
&     (:, 3)*pl%ti*dv%ni(:, 0)
    CALL MY_OUT_US(70, ncv, 0, wrk0, 'b2npht_shi')
    wrk0 = shn0(:, 0) + shn0(:, 1)*pl%tn + shn0(:, 2)*dv%nn + shn0(:, 3)&
&     *pl%tn*dv%nn
    CALL MY_OUT_US(70, ncv, 0, wrk0, 'b2npht_shn')
    wrk0 = skt0(:, 0) + skt0(:, 1)*pl%kt + skt0(:, 2)*dv%ni(:, 1) + skt0&
&     (:, 3)*pl%kt*dv%ni(:, 1)
    CALL MY_OUT_US(70, ncv, 0, wrk0, 'b2npht_skt')
    wrk0 = szt0(:, 0) + szt0(:, 1)*pl%zt + szt0(:, 2)*dv%ni(:, 1) + szt0&
&     (:, 3)*pl%zt*dv%ni(:, 1)
    CALL MY_OUT_US(70, ncv, 0, wrk0, 'b2npht_szt')
  END IF
!   ..compute correction
  IF (solving(4)) THEN
!srv 11.01.13
    CALL B2USHT_NODIFF(ncv, nfc, nvx, ns, switch, geo, mpg, itcnt, dv%ne&
&                , dv%ni, dv%nn, pl%te, pl%ti, pl%tn, pl%kt, pl%zt, dv%&
&                floe, dv%cone, dv%floi, dv%coni, dv%flon, dv%conn, dv%&
&                flokt, dv%conkt, dv%flozt, dv%conzt, she0, shi0, shn0, &
&                skt0, szt0, co%ceqp, dv%resht, dv%reshe, dv%reshi, dv%&
&                reshn, dv%reskt, dv%reszt, dv%cortt, dv%corte, dv%corti&
&                , dv%cortn, dv%corkt, dv%corzt, wrkf, wrkc, aa)
!   ..apply correction
!srv 22.05.18
    CALL B2UPHT_NODIFF(ncv, nfc, nvx, ns, switch, geo, mpg, solving(3), &
&                solving(1), solvereg, solvireg, solvnreg, solvtreg, &
&                solvpreg, solvmreg, solvkreg, solvzreg, rxf, dv%cortt, &
&                dv%corte, dv%corti, dv%cortn, dv%corkt, dv%corzt, dv%&
&                pccm(1:ncv, 0), pl%na, pl%ua, pl%te, pl%ti, pl%tn, pl%&
&                kt, pl%zt, pl%po, dv%ne, dv%ni)
  END IF
!   ..set error parameter and return
!     (at present all errors cause an abort through xerrab)
  ierr = 0
!
  IF (switch%tn_style .LE. 1) THEN
!! if we solve a combined ion-neutral (tn_style.eq.0)
!! or pure ion (tn_style.eq.1) energy equation,
!! tn must be exactly equal to ti.
    pl%tn = pl%ti
  END IF
!
  IF (switch%b2npht_iout .EQ. 1) THEN
    DO is=0,ns-1
      WRITE(chns, '(i3.3)') is
      arg11(:) = 'b2npht_na'//chns
      CALL MY_OUT_US(70, ncv, 0, pl%na(1, is), arg11(:))
      arg11(:) = 'b2npht_ua'//chns
      CALL MY_OUT_US(70, ncv, 0, pl%ua(1, is), arg11(:))
    END DO
    CALL MY_OUT_US(70, ncv, 0, pl%te, 'b2npht_te')
    CALL MY_OUT_US(70, ncv, 0, pl%ti, 'b2npht_ti')
    CALL MY_OUT_US(70, ncv, 0, pl%tn, 'b2npht_tn')
    CALL MY_OUT_US(70, ncv, 0, pl%po, 'b2npht_po')
    CALL MY_OUT_US(70, ncv, 0, dv%corte, 'b2npht_corte')
    CALL MY_OUT_US(70, ncv, 0, dv%corti, 'b2npht_corti')
    CALL MY_OUT_US(70, ncv, 0, dv%cortt, 'b2npht_cortt')
    CALL MY_OUT_US(70, ncv, 0, dv%cortn, 'b2npht_cortn')
  ELSE IF (switch%iout_b2wdat .EQ. 4) THEN
    CALL MY_OUT_US(70, ncv, 0, pl%te, 'b2npht_te')
    CALL MY_OUT_US(70, ncv, 0, pl%ti, 'b2npht_ti')
    CALL MY_OUT_US(70, ncv, 0, pl%tn, 'b2npht_tn')
  END IF
!
! ..return
  ncall_b2npht = ncall_b2npht + 1
  CALL SUBEND()
  RETURN
END SUBROUTINE B2NPHT_NODIFF

