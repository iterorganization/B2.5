      result1 = MAXVAL(backscattering(1:nbnd, is))
      IF (result1 .GT. 0.0_R8) THEN
        DO i=1,(nbnd-1)/nline+1
          IF (i .EQ. 1) THEN
            WRITE(line, '(a17,i3,a2)') 'BACKSCATTERING(1,', is, ')='
          ELSE
            line = ''
          END IF
          IF (i*nline .GT. nbnd) THEN
            min6 = nbnd
          ELSE
            min6 = i*nline
          END IF
          DO ibnd=(i-1)*nline+1,min6
            WRITE(numstr, '(1x,1es15.8,a1)') backscattering(ibnd, is), &
&           ','
            line = TRIM(line)//numstr
          END DO
          WRITE(nwrite, '(a)') TRIM(line)
        END DO
        DO i=1,(nbnd-1)/nline+1
          IF (i .EQ. 1) THEN
            WRITE(line, '(a24,i3,a2)') 'BACKSCATTERING_ENERGY(1,', is, &
&           ')='
          ELSE
            line = ''
          END IF
          IF (i*nline .GT. nbnd) THEN
            min7 = nbnd
          ELSE
            min7 = i*nline
          END IF
          DO ibnd=(i-1)*nline+1,min7
            WRITE(numstr, '(1x,1es15.8,a1)') backscattering_energy(ibnd&
&           , is), ','
            line = TRIM(line)//numstr
          END DO
          WRITE(nwrite, '(a)') TRIM(line)
        END DO
      END IF
    END DO
    DO ibnd=1,nbnd
      IF (.NOT.STREQL(surface_material_name(ibnd), 'C') .AND. (.NOT.&
&         STREQL(surface_material_name(ibnd), ' '))) WRITE(nwrite, &
&                                                    '(a22,i4,a4,a6,a2)'&
&                                                   ) &
&                                               'SURFACE_MATERIAL_NAME('&
&                                                    , ibnd, ')= ''', &
&                                                  surface_material_name&
&                                                    (ibnd), ''','
      IF (coating_thickness(ibnd) .GT. 0.0_R8) WRITE(nwrite, &
&                                              '(a22,i4,a4,a6,a2)') &
&                                              'COATING_MATERIAL_NAME('&
&                                              , ibnd, ')= ''', &
&                                              coating_material_name(&
&                                              ibnd), ''','
      IF (.NOT.STREQL(bulk_material_name(ibnd), 'C') .AND. (.NOT.STREQL(&
&         bulk_material_name(ibnd), ' '))) WRITE(nwrite, &
&                                          '(a19,i4,a4,a6,a2)') &
&                                          'BULK_MATERIAL_NAME(', ibnd, &
&                                          ')= ''', bulk_material_name(&
&                                          ibnd), ''','
      IF (plate_thickness(ibnd) .NE. plate_thick .AND. plate_thickness(&
&         ibnd) .NE. 0.0_R8) WRITE(nwrite, '(a16,i4,a2,1es15.8,a1)') &
&                            'PLATE_THICKNESS(', ibnd, ')=', &
&                            plate_thickness(ibnd), ','
      IF (coating_thickness(ibnd) .GT. 0.0_R8) WRITE(nwrite, &
&                                              '(a18,i4,a2,1es15.8,a1)')&
&                                              'COATING_THICKNESS(', &
&                                              ibnd, ')=', &
&                                              coating_thickness(ibnd), &
&                                              ','
      IF (backplate_temp(ibnd) .NE. plate_temp .AND. backplate_temp(ibnd&
&         ) .NE. 0.0_R8) WRITE(nwrite, '(a15,i4,a2,1es15.8,a1)') &
&                        'BACKPLATE_TEMP(', ibnd, ')=', backplate_temp(&
&                        ibnd), ','
      IF (inertial_cooling(ibnd)) WRITE(nwrite, '(a17,i4,a2,l1,a1)') &
&                                 'INERTIAL_COOLING(', ibnd, ')=', &
&                                 inertial_cooling(ibnd), ','
      IF (plate_time_factor(ibnd) .NE. 1.0_R8 .AND. plate_time_factor(&
&         ibnd) .NE. 0.0_R8) WRITE(nwrite, '(a18,i4,a2,1es15.8,a1)') &
&                            'PLATE_TIME_FACTOR(', ibnd, ')=', &
&                            plate_time_factor(ibnd), ','
    END DO
    IF (plate_model .GE. 1) THEN
!xpb  Use explicit writing of non-default elements only
      DO ix=-1,nx
        DO iy=-1,ny
          IF (xymap(ix, iy) .NE. 0) WRITE(nwrite, &
&                                   '(a6,i4,a1,i4,a2,i4,a1)') 'XYMAP(', &
&                                   ix, ',', iy, ')=', xymap(ix, iy), &
&                                   ','
        END DO
      END DO
      DO ibnd=1,nbnd
        WRITE(nwrite, '(a6,i4,a2,i4,a1,1x,a6,i4,a2,i4,a1)') 'IMAPX(', &
&       ibnd, ')=', imapx(ibnd), ',', 'IMAPY(', ibnd, ')=', imapy(ibnd)&
&       , ','
        IF (plate_time(ibnd) .NE. 0.0_R8) WRITE(nwrite, &
&                                         '(a11,i4,a2,1es15.8,a1)') &
&                                         'PLATE_TIME(', ibnd, ')=', &
&                                         plate_time(ibnd), ','
