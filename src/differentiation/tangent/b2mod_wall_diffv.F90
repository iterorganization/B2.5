        WRITE(nwrite, '(a20,i4,a2,i2,a1)') 'LAYER_NCONSTITUENTS(', ibnd&
&       , ')=', layer_nconstituents(ibnd), ','
        DO i=1,layer_nconstituents(ibnd)
          WRITE(nwrite, '(a21,i4,a1,i2,a4,i2,a1)') &
&         'LAYER_NZCONSTITUENTS(', ibnd, ',', i, ')=  ', &
&         layer_nzconstituents(ibnd, i), ','
          WRITE(nwrite, '(a23,i4,a1,i2,a2,1es15.8,a1)') &
&         'LAYER_NRELCONSTITUENTS(', ibnd, ',', i, ')=', &
&         layer_nrelconstituents(ibnd, i), ','
        END DO
      END IF
      DO itrack=1,ntargsp
        IF (deposition(ibnd, itrack) .NE. 0.0_R8) WRITE(nwrite, &
&                                         '(a11,i4,a1,i1,a2,1es15.8,a1)'&
&                                                ) 'DEPOSITION(', ibnd, &
&                                                 ',', itrack, ')=', &
&                                                 deposition(ibnd, &
&                                                 itrack), ','
        IF (monolayer_deposition(ibnd, itrack) .NE. 0.0_R8) WRITE(nwrite&
&                                                           , &
&                                              '(a,i4,a,i1,a,1es15.8,a)'&
&                                                          ) &
&                                                'MONOLAYER_DEPOSITION('&
&                                                           , ibnd, ','&
&                                                           , itrack, &
&                                                           ')=', &
&                                                   monolayer_deposition&
&                                                           (ibnd, &
&                                                           itrack), ','
        IF (erosion(ibnd, itrack) .NE. 0.0_R8) WRITE(nwrite, &
&                                          '(a8,i4,a1,i1,a2,1es15.8,a1)'&
&                                             ) 'EROSION(', ibnd, ',', &
&                                              itrack, ')=', erosion(&
&                                              ibnd, itrack), ','
        IF (monolayer_erosion(ibnd, itrack) .NE. 0.0_R8) WRITE(nwrite, &
&                                              '(a,i4,a,i1,a,1es15.8,a)'&
&                                                       ) &
&                                                   'MONOLAYER_EROSION('&
&                                                        , ibnd, ',', &
&                                                        itrack, ')=', &
&                                                      monolayer_erosion&
&                                                        (ibnd, itrack)&
&                                                        , ','
      END DO
    END DO
    IF (plate_model .GE. 1 .AND. ndepth_nml .GT. 1) THEN
      DO ibnd=1,nbnd
        DO i=1,(ndepth_nml-1)/nline+1
          IF (i .EQ. 1) THEN
            WRITE(line, '(a12,i4,a3,i4,a2)') 'TARGET_TEMP(', ibnd, ',1:'&
&           , ndepth_nml, ')='
          ELSE
            line = ''
          END IF
          IF (i*nline .GT. ndepth_nml) THEN
            min8 = ndepth_nml
          ELSE
            min8 = i*nline
          END IF
          DO is=(i-1)*nline+1,min8
            WRITE(numstr, '(1x,1es15.8,a1)') target_temp(ibnd, is), ','
            line = TRIM(line)//numstr
          END DO
          WRITE(nwrite, '(a)') TRIM(line)
        END DO
      END DO
    END IF
    WRITE(nwrite, '(a1)') '/'
    CLOSE(nwrite) 
    WRITE(*, *) 'Saved ', TRIM(filename)
    RETURN
  END SUBROUTINE WRITE_B2MOD_WALL_NAMELIST

