&                                         '(a11,i4,a2,1es15.8,a1)') &
&                                         'PLATE_AREA(', ibnd, ')=', &
&                                         plate_area(ibnd), ','
      END DO
    END IF
    DO ibnd=1,nbnd
      todo = layer_nconstituents(ibnd) .NE. matsurf_nconstituents(ibnd)
      DO i=1,layer_nconstituents(ibnd)
        found = .false.
        j = 1
        DO WHILE (.NOT.found .AND. j .LE. matsurf_nconstituents(ibnd))
          found = layer_nzconstituents(ibnd, i) .EQ. &
&           matsurf_nzconstituents(ibnd, j)
          IF (.NOT.found) j = j + 1
        END DO
        IF (found) todo = todo .OR. layer_nrelconstituents(ibnd, i) .NE.&
&           matsurf_relconstituents(ibnd, j)
      END DO
