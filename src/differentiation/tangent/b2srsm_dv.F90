!   with respect to varying inputs: ti na ne ni ua *(sr.she) *(sr.shi)
!                *(sr.smo) *(sr.sna) te
!   Plus diff mem management of: sr.she:in sr.shi:in sr.sne:in
!                sr.smo:in sr.sna:in
