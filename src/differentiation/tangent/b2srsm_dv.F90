  FUNCTION ROXA(icv, is)
  USE B2MOD_DIFFSIZES
    IMPLICIT NONE
    INTEGER, INTENT(IN) :: icv, is
    REAL(kind=r8) :: roxa
!
    roxa = am(is)*mp*na(icv, is)
    RETURN
  END FUNCTION ROXA

END SUBROUTINE B2SRSM_NODIFF

