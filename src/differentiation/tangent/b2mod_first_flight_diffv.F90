              arg1 = st%pl%tn(icv)/(mp*am(st%rt%iscx(ik)))
              result10 = SQRT(arg1)
              rnn = 2.0e-19_R8*result10
              vnn = vnn + rnn*st%pl%na(icv, st%rt%iscx(ik))
            END DO
            arg10 = eion/(am(isneut)*mp)
            result1 = SQRT(arg10)
            thru_time = dl/result1*table(ptr, 1)
            IF (flown_time + thru_time .LT. flight_time) THEN
