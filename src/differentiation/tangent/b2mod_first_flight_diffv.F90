            wrk3(icv) = wrk3(icv) - dist_deposit*sxb*st%rt%rpt(icv, &
&             isneut+1)*ev*(vion+vexc)/(vcx+vion+vexc+vnn)
            dist_end = dist_end - dist_deposit
            IF (dist_end .LE. 0.0_R8) THEN
              icvend = icv
              GOTO 100
            ELSE
              ptr = ptr + 1
            END IF
          END DO
 100      IF (dist_end .GT. 0.0_R8 .AND. icvend .NE. 0) THEN
