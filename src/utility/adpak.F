      SUBROUTINE ADBFIT(KNUCZ, PTE, PNE, PE, PA, PAA, PBT, PBFI, PBFIL)
      use b2mod_types
      use b2mod_constants
      IMPLICIT REAL (kind=R8) (A-H,O-Z)
      INTEGER KNUCZ
      REAL (kind=R8) :: PTE, PNE
      REAL (kind=R8) :: PE(KNUCZ), PA(6,KNUCZ), PAA(KNUCZ), PBT(3,KNUCZ)
CBJB  DIMENSION PBFI(1), PBFIL(1)
      REAL (kind=R8) :: PBFI(*), PBFIL(*)
C
C     EVALUATE THE BELFAST GROUP RATE COEFFICIENT FITTING FORMULAS.
C
      REAL (kind=R8) :: ZJLEV, ZX, ZLOG, ZSUM, ZIRC, ZTE
      INTEGER J, INSPC, JSPC
C
      ZJLEV = ev
C
C     CONVERT TE TO EV AND ZERO RETURN ARRAYS
C
      ZTE = 1000.0_R8 * PTE
C
      INSPC = KNUCZ + 1
      DO 10 JSPC = 1, INSPC
      PBFI(JSPC) = 0.0_R8
      PBFIL(JSPC) = 0.0_R8
   10 CONTINUE
C
C     LOOP OVER ALL SPECIES EXCEPT FULLY STRIPPED
C
      DO 100 JSPC = 1, KNUCZ
C
C     BRANCH IF HIGH TEMPERATURE FIT IS REQUIRED
C
      IF(ZTE .GT. 10.0_R8 * PE(JSPC)) GO TO 80
C
C     (IP/10)<TE<(10*IP) FITTING FORM -- EXTRAPOLATED FOR LOWER (TE)
C     BY EVALUATING IN THAT CASE THE POLYNOMIAL FACTOR AT (IP/10).
C
      ZX = PE(JSPC) / ZTE
      ZLOG = MAX(-1.0_R8,LOG10(1.0_R8/ZX))
C
      ZSUM = PA(1,JSPC)
      DO 20 J = 2, 6
      ZSUM = ZSUM + PA(J,JSPC) * (ZLOG**(J-1))
   20 CONTINUE
C
      ZIRC = EXP(-ZX) * SQRT(1.0_R8 / ZX) * ZSUM
      PBFI(JSPC) = PNE * ZIRC
      PBFIL(JSPC) = ZJLEV * PE(JSPC) * PBFI(JSPC)
      GO TO 100
C
C     HIGH TEMPERATURE FIT FOR TE > 10*IP
C
   80 ZX = PE(JSPC) / ZTE
C
      ZIRC = SQRT(ZX) * (PAA(JSPC) * LOG(1.0_R8/ZX)
     &     + PBT(1,JSPC) + PBT(2,JSPC) * ZX + PBT(3,JSPC) * ZX * ZX)
C
      PBFI(JSPC) = PNE * ZIRC
      PBFIL(JSPC) = ZJLEV * PE(JSPC) * PBFI(JSPC)
C
  100 CONTINUE
C
      RETURN
      END SUBROUTINE ADBFIT
