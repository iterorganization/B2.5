      SUBROUTINE ADECI(PTE, PNE)
      use b2mod_types
      IMPLICIT REAL (kind=R8) (A-H,O-Z)
      REAL (kind=R8) :: PTE, PNE
C
C     CALCULATE THE ELECTRON COLLISIONAL IONISATION RATE USING
C     THE FORMULAS SELECTED BY THE LECI SWITCH.  LECIOK RETURNS
C     THE RATE FORMULAS ACTUALLY USED, WHICH MAY DIFFER FROM
C     LECI IF LECI WAS SET INAPPROPRIATELY FOR THE GIVEN ELEMENT.
C
      INTEGER           NUCZ, NSPC, NVALNC(100)
      REAL (kind=R8) :: APN(100,10), AQN(100,10),
     &                  SIGMA(10,10), QN(100,10),
     &                  EN(100,10), EIN(100,5), ENM(100,5,10),
     &                  FNM(100,5,10), FNN(100), ENN(100)
      COMMON / ADSDAT / NUCZ, NSPC, APN, AQN,
     &                  NVALNC, SIGMA, QN,
     &                  EN, EIN, ENM,
     &                  FNM, FNN, ENN
C
      REAL (kind=R8) :: RCLION(100), RRAREC(100), RDIREC(100),
     &                  CIZLOS(100), RADRRC(100), RADDRC(100),
     &                  RADCLX(100), RADBRM(100)
      COMMON / ADELEC / RCLION, RRAREC, RDIREC,
     &                  CIZLOS, RADRRC, RADDRC,
     &                  RADCLX, RADBRM
C
      INTEGER           LADEN, LADTIP, LTIPOK, LECI, LECIOK
      COMMON / ADPARM / LADEN, LADTIP, LTIPOK, LECI, LECIOK

      INTEGER ILMAX, IFLAG
C
C     ILMAX = CURRENT MAXIMUM NUMBER OF RATE OPTIONS
C
      ILMAX = 3
C
C     CHECK FOR VALID LECI; RETURN LECI = 1 OPTION IF NOT VALID
C
      IF(LECI .LT. 1 .OR. LECI .GT. ILMAX) GO TO 10
C
      GO TO (10, 20, 30) LECI
C
C     LECI = 1; USE XSNQ DERIVED IONISATION RATE CALCULATION
C
   10 CALL ADXECI(PTE, PNE)
      LECIOK = 1
      RETURN
C
C     LECI = 2; USE BELFAST GROUP LIGHT ELEMENT RATE FITS
C
   20 CALL ADBFI(NUCZ, PTE, PNE, RCLION, CIZLOS, IFLAG)
      IF(IFLAG .NE. 0) GO TO 10
      LECIOK = 2
      RETURN
C
C     LECI = 3; USE RATES FROM YOUNGER FOR AVAILABLE ELEMENTS AND SHELLS,
C               XSNQ RATES OTHERWISE ON A CHARGE STATE-BY-CHARGE STATE
C               BASIS
C
   30 CALL ADXECI(PTE, PNE)
      CALL ADYGR(NUCZ, PTE, PNE, RCLION, CIZLOS, IFLAG)
      LECIOK = 3
      IF(IFLAG .NE. 0) LECIOK = 1
      RETURN
C
C
C
      END SUBROUTINE ADECI
      SUBROUTINE ADXECI(PTE, PNE)
      use b2mod_types
      use b2mod_constants
      IMPLICIT REAL (kind=R8) (A-H,O-Z)
      REAL (kind=R8) :: PTE, PNE
C
C     REV: 3/30/83 ADECI --> ADXECI TO ACCOMODATE ALTERNATIVE RATES
C     REV: 10/10/82 FOR NEW AD PACKAGE
C
C     REV: 6/15/78
C
C     COMPUTES THE XSNQ-DERIVED COLLISIONAL IONISATION RATES RCLION
C     AT THE ELECTRON TEMPERATURE PTE (KEV), WHERE JQ=(IONIC CHARGE+1).
C     THESE RATES HAVE THE UNITS (SEC-1).
C
C     NOTES:
C
C     1) ELECTRONS ARE ASSUMED TO FILL IONIC SHELLS IN SIMPLE ASCENDING
C        ORDER.  THIS IS ASSUMED BOTH EXPLICITLY (THE APN ARRAY)
C        AS WELL AS IMPLICITLY THROUGH THE USE OF NVALNC VALUES
C        IN LOOP INDICES, ETC.
C     2) THROUGH USE OF THE FUNCTION EXPUND, EXP(X) IS SET EQUAL TO
C        ZERO IN THE RATE EQUATIONS FOR VALUES OF X < -2465.0_R8
C     3) RATES FOR THE SPECIES JQ REFER TO TRANSFORMATIONS OF
C        THAT SPECIES INTO ADJACENT IONISATION STATES, NOT THE
C        RATES AT WHICH THAT SPECIES IS FORMED.
C
C
C     PRINCIPAL PHYSICS REFERENCES:
C
C     1) POST, JENSEN, TARTER, GRASBERGER, LOKKE, "STEADY STATE
C        RADIATIVE COOLING RATES FOR LOW-DENSITY HIGH-TEMPERATURE
C        PLASMAS", PPPL-1352 (SUB. TO N.F.) (WITH CORRECTIONS)
C     2) LOKKE AND GRASBERGER, "XSNQ-U A NON-LTE EMISSION AND
C        ABSORPTION COEFFICIENT SUBROUTINE", LLL REPORT UCRL-52276
C        (WITH CORRECTIONS).
C     3) XSNQ FORTRAN LISTING
C
C
C
C
      INTEGER           NUCZ, NSPC, NVALNC(100)
      REAL (kind=R8) :: APN(100,10), AQN(100,10),
     &                  SIGMA(10,10), QN(100,10),
     &                  EN(100,10), EIN(100,5), ENM(100,5,10),
     &                  FNM(100,5,10), FNN(100), ENN(100)
      COMMON / ADSDAT / NUCZ, NSPC, APN, AQN,
     &                  NVALNC, SIGMA, QN,
     &                  EN, EIN, ENM,
     &                  FNM, FNN, ENN
C
      REAL (kind=R8) :: RCLION(100), RRAREC(100), RDIREC(100),
     &                  CIZLOS(100), RADRRC(100), RADDRC(100),
     &                  RADCLX(100), RADBRM(100)
      COMMON / ADELEC / RCLION, RRAREC, RDIREC,
     &                  CIZLOS, RADRRC, RADDRC,
     &                  RADCLX, RADBRM
C
      REAL (kind=R8) :: ZJLKEV, ZTE, ZCHG, ZISUM, ZESUM, 
     &                  ZJN, ZXN, ZY, ZFY, ZGAUNT, ZITEMP 
      INTEGER JQ, JN, IVALNC
      REAL (kind=R8) :: EXPUND
      EXTERNAL EXPUND
C
      ZJLKEV = ev*1000.0_R8
C
C
      ZTE = PTE
C
      DO 100 JQ = 1 , NUCZ
C
      ZCHG = REAL(JQ - 1)
      IVALNC = NVALNC(JQ)
C
C
C
C     ******************** IONISATION RATE ********************
C
C
C     CALCULATE ELECTRON COLLISIONAL IONISATION RATES PER SHELL
C     AND SUM FOR ALL POPULATED SHELLS. ALSO COMPUTE
C     IONISATION ENERGY LOSS RATE CIZLOS.
C
      ZISUM = 0.0_R8
      ZESUM = 0.0_R8
C
      DO 20 JN = 1 , IVALNC
      ZJN = JN
      ZXN = EIN(JQ,JN)/ZTE
C
      ZY = LOG10(ZXN/4.0_R8)
      ZFY = .23_R8 + .046_R8 * ZY + .1074_R8 * ZY**2
     &          - .0459_R8 * ZY**3 - .01505_R8 * ZY**4
      ZGAUNT = 12.18_R8 * EXP(-ZJN/(ZJN+5.0_R8))*(1.0_R8+.0335_R8*ZJN)
     & * (1.0_R8 - .622_R8/(ZCHG+1.0_R8) - .0745_R8/(ZCHG+1.0_R8)**2)
     & * ABS(ZFY)
      ZITEMP = APN(JQ,JN) * EXPUND(-ZXN,-2465.0_R8) *
     & (1.0_R8-EXPUND(-ZXN,-2465.0_R8)) * ZGAUNT / EIN(JQ,JN)**2
      ZISUM = ZISUM + ZITEMP
      ZESUM = ZESUM + EIN(JQ,JN) * ZITEMP
   20 CONTINUE
C
      ZISUM = ZISUM * SQRT(ZTE)
      ZESUM = ZESUM * SQRT(ZTE)
      RCLION(JQ) = PNE * 3.44e-11_R8 * ZISUM
      CIZLOS(JQ) = PNE * ZJLKEV * 3.44e-11_R8 * ZESUM
C
C
  100 CONTINUE
C
C
      RCLION(NUCZ + 1) = 0.0_R8
      CIZLOS(NUCZ + 1) = 0.0_R8
C
C
      RETURN
      END SUBROUTINE ADXECI
