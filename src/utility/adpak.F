      CALL XERTST (LADTIP .EQ. 0 .OR. LADTIP .EQ. 1,
     . 'Invalid LADTIP value')
      LTIPOK = 0
      IF(LADTIP .EQ. 1) CALL ADTIP(LTIPOK)
