      FUNCTION ADPALI(PQ,KN)
      use b2mod_types
      use b2mod_constants
      IMPLICIT REAL (kind=R8) (A-H,O-Z)
      INTEGER KN
      REAL (kind=R8) :: PQ, ZALPHA, ZKN, rydbrg, ADPALI
#include "emissivity.constants"
C
C     REV: 10/10/82 FOR NEW AD PACKAGE
C
C     REV: 6/15/78
C
C     EVALUATE THE PAULI APPROXIMATION TO THE
C     THE DIRAC ENERGY LEVEL EQUATION, TAKING THE MEAN OF THE
C     LOWEST AND HIGHEST SUBSHELL ENERGIES TO BE THE SHELL ENERGY.
C     ADPALI(PQ,KN) IS IN KEV,  PQ IS THE EFFECTIVE NUCLEAR
C     CHARGE (IN UNITS OF ELECTRONIC CHARGE) AND KN IS THE PRINCIPAL
C     QUANTUM NUMBER.
C
C
      ZALPHA = qe**2 / (2.0_R8 * eps0 * const_h * c)
C
C
      ZKN = REAL(KN)
C
      rydbrg=(me/(2.0_R8*qe))*(qe**2/(2.0_R8*const_h*eps0))**2
      ADPALI = (rydbrg/1000.0_R8) * ( (PQ / ZKN)**2 ) *
     &     (1.0_R8 +((ZALPHA*PQ)**2)*(0.5_R8-1.0_R8/(4.0_R8*ZKN))/ZKN)
C
C
      RETURN
      END FUNCTION ADPALI
