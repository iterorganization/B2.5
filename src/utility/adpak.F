      ZNUC = REAL(NUCZ)
C
C     ******** LOOP FOR ALL SPECIES EXCEPT FULLY IONISED ********
C
      DO 100 JQ = 1 , NUCZ
C
      ZCHG = REAL(JQ - 1)
      IVALNC = NVALNC(JQ)
      IBOUND = NUCZ - JQ + 1
C
C     ***** N - N RATE *****
C
      ZZNN = 0.0_R8
      IF(FNN(JQ) .EQ. 0.0_R8) GO TO 30
C
      ZXNN = ENN(JQ) / ZTE
C
      IF(JQ .NE. 1) GO TO 20
      ZTEMP = .06_R8 * (SQRT(ZXNN) - 2.0_R8) / (1.0_R8 + ZXNN)
      GO TO 22
C
   20 IF(IBOUND .LE. 28) GO TO 21
      ZTEMP = .7_R8 * (1.0_R8 - .8_R8 / ZCHG)
      GO TO 22
C
   21 ZTEMP = ZDNN(IBOUND) * (1.0_R8 - ZGAMNN(IBOUND) / ZCHG)
C
   22 ZGNN = ZTEMP + .276_R8 * EXPE1(ZXNN)
C
      ZZNN = FNN(JQ) * EXPUND(-ZXNN , -2465.0_R8) * ZGNN
C
C     ***** SUM N - M RATES FROM ALL OCCUPIED SHELLS *****
C
   30 ZNMSUM = 0.0_R8
C
C
      DO 31 JN = 1 , IVALNC
C
      ZJN = REAL (JN)
      IIJM = IVALNC
      IF(JN .EQ. IVALNC .OR.
     &        AQN(JQ,IVALNC) .EQ. 0.0_R8) IIJM = IVALNC + 1
C
      DO 32 JM = IIJM , 10
      ZJM = REAL(JM)
C
      ZXNM = ENM(JQ,JN,JM) / ZTE
      ZTEMP = ZJM * (ZJM - ZJN) *
     1 (1.0_R8 + (1.0_R8 - 2.0_R8/ZNUC) * ZXNM) / 20.0_R8
      ZGNM = .19_R8 * (1.0_R8 + .9_R8 * (1.0_R8 + ZTEMP) * EXPE1(ZXNM))
C
C     NOTE THAT FNM HAS APN AND AQN FACTORS ALREADY INCLUDED
C
      ZZNM = FNM(JQ,JN,JM) * EXPUND(-ZXNM , -2465.0_R8) * ZGNM
      ZNMSUM = ZNMSUM + ZZNM 
   32 CONTINUE
   31 CONTINUE
C
C
C
      ZTEMP = (ZZNN + ZNMSUM) / SQRT(ZTE)
      RADCLX(JQ) = PNE * ZJLKEV * 4.99e-10_R8 * ZTEMP
C
  100 CONTINUE
C
C     ***************END OF JQ SPECIES LOOP ***************
C
      RADCLX(NSPC) = 0.0_R8
C
      RETURN
      END SUBROUTINE ADECEX
      SUBROUTINE ADBREM(PTE, PNE)
      use b2mod_types
      IMPLICIT REAL (kind=R8) (A-H,O-Z)
      REAL (KIND=R8) :: PTE, PNE
C
C     REV: 10/10/82 FOR NEW AD PACKAGE
C
C     REV: 6/15/78
C
C     INPUT : PTE, NUCZ, NSPC
C     OUTPUT : RADBRM
C
C     COMPUTE BREMSSTRAHLUNG RADIATION RADBRM(JQ) (JOULE/SEC)
C     FOR EACH IONIC SPECIES JQ = (IONIC CHARGE + 1).
C
C     REFERENCES:
C     1) POST ET AL. PPPL-1352 (CORRECTED)
C     2) KARZAS AND LATTER
C     3) TUCKER
C
C
      INTEGER           NUCZ, NSPC, NVALNC(100)
      REAL (KIND=R8) :: APN(100,10), AQN(100,10),
     &                  SIGMA(10,10), QN(100,10),
     &                  EN(100,10), EIN(100,5), ENM(100,5,10),
     &                  FNM(100,5,10), FNN(100), ENN(100)
      COMMON / ADSDAT / NUCZ, NSPC, APN, AQN,
     &                  NVALNC, SIGMA, QN,
     &                  EN, EIN, ENM,
     &                  FNM, FNN, ENN
C
      REAL (KIND=R8) :: RCLION(100), RRAREC(100), RDIREC(100),
     &                  CIZLOS(100), RADRRC(100), RADDRC(100),
     &                  RADCLX(100), RADBRM(100)
      COMMON / ADELEC / RCLION, RRAREC, RDIREC,
     &                  CIZLOS, RADRRC, RADDRC,
     &                  RADCLX, RADBRM
C
      REAL (KIND=R8) :: ZTE, ZGFF, ZCHG
      INTEGER JQ
C
      ZTE = PTE
C
C     USE FIXED FREE-FREE GAUNT FACTOR ZGFF = 1.2_R8
C
      ZGFF = 1.2_R8
C
      DO 100 JQ = 1 , NSPC
      ZCHG = REAL(JQ - 1)
      RADBRM(JQ) = PNE * 4.85e-31_R8 * ZCHG * ZCHG * SQRT(ZTE) * ZGFF
  100 CONTINUE
C
      RETURN
      END SUBROUTINE ADBREM
      FUNCTION EXPE1(PX)
      use b2mod_types
      IMPLICIT REAL (kind=R8) (A-H,O-Z)
      REAL (KIND=R8) :: EXPE1
      REAL (KIND=R8) :: PX
C
C     REV: 6/15/78
C
C     CALCULATE EXP(PX) * E1(PX), WHERE THE EXPONENTIAL INTEGRAL
C     E1(X) = INTEGRAL FROM X TO INFINITY OF (EXP(-T)/T)DT.
C
C     REFERENCE: 'HANDBOOK OF MATHEMATICAL FUNCTIONS', ABRAMOWITZ
C     AND STEGUN, NATIONAL BUREAU OF STANDARDS
C
      IF(PX.GE.1.0_R8)GO TO 2
      IF(PX.GT.1.e-4_R8)GO TO 1
C
      EXPE1 = EXP(PX) * ( - LOG(PX) - .57721566_R8 + PX )
      RETURN
C
C
    1 EXPE1 = EXP(PX) * ( - LOG(PX) - .57721566_R8 + 
     & .99999193_R8*PX - .24991055_R8*PX**2 + 
     & .05519968_R8*PX**3 - .00976004_R8*PX**4 + 
     & .00107857_R8*PX**5 )
C
      RETURN
C
C
    2 EXPE1 = (PX + 2.334733_R8 + .250621_R8/PX) /
     &         (PX**2 + 3.330657_R8*PX + 1.681534_R8)
C
      RETURN
      END FUNCTION EXPE1
