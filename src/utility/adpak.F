      SUBROUTINE ADCE
      use b2mod_types
      IMPLICIT REAL (kind=R8) (A-H,O-Z)
C
C     REV: 10/10/82 FOR NEW AD PACKAGE
C
C     CALCULATE CORONAL EQUILIBRIUM (INCLUDING CHARGE
C     EXCHANGE RECOMBINATION, IF PRESENT) USING THE IONISATION,
C     RECOMBINATION, AND RADIATION RATES
C     STORED IN THE APPROPRIATE ARRAYS BY PREVIOUS CALLS
C     TO ADSET, ADERC, AND ADBCXR.
C
      integer JSPC, NUCZ, NSPC, NVALNC(100)
      REAL (kind=R8) :: APN(100,10), AQN(100,10),
     &                  SIGMA(10,10), QN(100,10),
     &                  EN(100,10), EIN(100,5), ENM(100,5,10),
     &                  FNM(100,5,10), FNN(100), ENN(100)
      REAL (kind=R8) :: RCLION(100), RRAREC(100), RDIREC(100),
     &                  CIZLOS(100), RADRRC(100), RADDRC(100),
     &                  RADCLX(100), RADBRM(100)
      REAL (kind=R8) :: RCXREC(100), RADBCX(100)
      REAL (kind=R8) :: CEFRAC(100), CEAVGZ, CERAD
      REAL (kind=R8) :: ZFRCMX, ZPSUM
      INTEGER II, J, IPASS, ICNTR, JQ, IQFRAC 

      COMMON / ADSDAT / NUCZ, NSPC, APN, AQN,
     &                  NVALNC, SIGMA, QN,
     &                  EN, EIN, ENM,
     &                  FNM, FNN, ENN
C
      COMMON / ADELEC / RCLION, RRAREC, RDIREC,
     &                  CIZLOS, RADRRC, RADDRC,
     &                  RADCLX, RADBRM
C
      COMMON / ADNEUT / RCXREC, RADBCX
C
      COMMON / ADCEQ / CEFRAC, CEAVGZ, CERAD
C
      REAL (kind=R8) :: ZIONT(100), ZRECT(100), ZRADT(100)
      REAL (kind=R8) :: ZI(1), ZRE(1), ZRA(1)
C
C     FILL TOTAL IONISATION, RECOMBINATION, AND RADIATION ARRAYS
C
      DO 10 JSPC = 1, NSPC
C
      ZIONT(JSPC) = RCLION(JSPC)
      ZRECT(JSPC) = RRAREC(JSPC) + RDIREC(JSPC) + RCXREC(JSPC)
      ZRADT(JSPC) = RADRRC(JSPC) + RADDRC(JSPC) + RADCLX(JSPC)
     &                           + RADBRM(JSPC) + RADBCX(JSPC)
C
   10 CONTINUE
C
      GO TO 15
C
C     ENTRY FOR ALTERNATIVE EXTERNAL INPUT OF TOTAL RATE DATA
C
      ENTRY ADCE2(ZI, ZRE, ZRA)
C
      DO 12 JSPC= 1, NSPC
      ZIONT(JSPC) = ZI(JSPC)
      ZRECT(JSPC) = ZRE(JSPC)
      ZRADT(JSPC) = ZRA(JSPC)
   12 CONTINUE
C
C     FIRST GUESS AT MAXIMUM SPECIES
C
   15 DO 20 JQ = 1 , NSPC
      IF(ZRECT(JQ) .GE. ZIONT(JQ))GO TO 21
   20 CONTINUE
      JQ = NSPC
   21 ICNTR = JQ
CW    WRITE(IOUT,23)ICNTR
CW 23 FORMAT(1X,'ICNTR 1ST GUESS =',I3)
      IPASS = 1
C
C     INITIALISE CEFRAC ARRAY
C
   25 DO 26 JQ = 1,NSPC
      CEFRAC(JQ) = 0.e0_R8
   26 CONTINUE
      CEFRAC(ICNTR) = 1.e0_R8
C
C     IF ICNTR IS FULLY STRIPPED WORK DOWN IN IONIZ ONLY
C
      IF(ICNTR.EQ.NSPC)GO TO 35
C
C     WORK UP FROM ICNTR SPECIES (TOWARDS HIGHER IONIZ STATES)
C
      II = ICNTR + 1
C
      DO 30 JQ = II,NSPC
      CEFRAC(JQ) = CEFRAC(JQ-1) * ZIONT(JQ-1) / ZRECT(JQ)
   30 CONTINUE
C
C     IF ICNTR IS NEUTRAL WE ARE DONE
C
      IF(ICNTR.EQ.1)GO TO 42
C
C     WORK DOWN FROM ICNTR SPECIES
C
   35 II = ICNTR - 1
      DO 40 J = 1,II
      JQ = II - J + 1
      CEFRAC(JQ) = CEFRAC(JQ+1) * ZRECT(JQ+1) / ZIONT(JQ)
   40 CONTINUE
C
C
   42 IF(IPASS.EQ.2)GO TO 49
C
C     END OF FIRST PASS - FIND NEW MAX SPECIES AND ITERATE
C
      IPASS = 2
      ZFRCMX = 0.e0_R8
      DO 45 JQ = 1, NSPC
      IF(CEFRAC(JQ).LT.ZFRCMX)GO TO 45
      ZFRCMX = CEFRAC(JQ)
      IQFRAC = JQ
   45 CONTINUE
      ICNTR = IQFRAC
      GO TO 25
C
C
C
   49 ZPSUM = 0.e0_R8
      DO 50 JQ = 1, NSPC
      ZPSUM = ZPSUM + CEFRAC(JQ)
   50 CONTINUE
C
C     NORMALISE SPECIES FRACTIONS AND COMPUTE RADIATION RATE
C
C
      CERAD = 0.e0_R8
C
      DO 60 JQ = 1, NSPC
      CEFRAC(JQ) = CEFRAC(JQ) / ZPSUM
      IF(ZRADT(JQ) .LE. 0.e0_R8) GO TO 60
C
   60 CONTINUE
C
C     COMPUTE <Z>
C
      CEAVGZ = 0.e0_R8
      DO 70 JQ = 2,NSPC
      CEAVGZ = CEAVGZ + REAL(JQ-1) * CEFRAC(JQ)
   70 CONTINUE
C
C
      RETURN
      END SUBROUTINE ADCE
      SUBROUTINE ADSET(KNUCZ)
      use b2mod_types
      IMPLICIT REAL (kind=R8) (A-H,O-Z)
      INTEGER KNUCZ
C
C     REV: 10/10/82 FOR NEW AD PACKAGE
C
C     SETUP BASIC ATOMIC STRUCTURE DATA FOR CHOSEN ELEMENT
C     (SHELL POPULATIONS, ENERGIES, ETC.)
C
      REAL (kind=R8) :: APN(100,10), AQN(100,10),
     &                  SIGMA(10,10), QN(100,10),
     &                  EN(100,10), EIN(100,5), ENM(100,5,10),
     &                  FNM(100,5,10), FNN(100), ENN(100)
      INTEGER           LADEN, LADTIP, LTIPOK, LECI, LECIOK, 
     &                  NUCZ, NSPC, NVALNC(100)
      external subini, subend, xertst
C
      COMMON / ADSDAT / NUCZ, NSPC, APN, AQN,
     &                  NVALNC, SIGMA, QN,
     &                  EN, EIN, ENM,
     &                  FNM, FNN, ENN
C
      COMMON / ADPARM / LADEN, LADTIP, LTIPOK, LECI, LECIOK
C
      call subini ('adset')
      NUCZ = KNUCZ
C
      CALL ADAPN
C
      CALL XERTST (LADEN .EQ. 0 .OR. LADEN .EQ. 1, 
     . 'Invalid LADEN value')
      IF(LADEN .EQ. 0) CALL ADMAYR
      IF(LADEN .EQ. 1) CALL ADMORE
