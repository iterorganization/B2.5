      INTEGER IM, IN, JN, JM, JQ, IIJM, IVALNC
      REAL (kind=R8) :: ADPALI
      SAVE SIGMAY
C
      DATA ((SIGMAY(JN,JM),JN=1,10),JM=1,10)/
