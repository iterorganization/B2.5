      SUBROUTINE ADSHEN(KQ, KHOLE, KEXCIT, PSEN, PBETOT)
      use b2mod_types
      use b2mod_constants
      IMPLICIT REAL (kind=R8) (A-H,O-Z)
#include "emissivity.constants"
C
C     REV: 10/10/82 FOR NEW AD PACKAGE
C
C     REV: 1 SEPTEMBER 1981
C
C     INPUT: NUCZ, KQ, KHOLE, KEXCIT, APN
C     OUTPUT: PSEN(10), PBETOT
C
C     COMPUTE SHELL ENERGIES USING NEW SHIELDING CONSTANTS
C     AND METHOD DUE TO R. MORE (UCRL-84991).
C
C     NUCZ = ION NUCLEAR CHARGE
C     KQ = SPECIES INDEX = IONIC CHARGE + 1
C     KHOLE, KEXCIT = ELECTRON REMOVED FROM SHELL KHOLE AND/OR
C                     ADDED (EXCITED) TO SHELL KEXCIT BEFORE
C                     ENERGIES ARE COMPUTED (KHOLE = 0 NO ELECTRON
C                     REMOVED, KEXCIT = 0 NO ELECTRON ADDED)
C     APN(100,10) = ARRAY OF SHELL POPULATIONS
C     PSEN = OUTPUT ARRAY OF SHELL ENERGIES (KEV) (DEFINED POSITIVE)
C     PBETOT = OUTPUT TOTAL ION ENERGY (DEFINED POSITIVE)
C
      REAL (KIND=R8) :: PSEN(10), ZAPN(10), ZQ(10), ZEN0(10), rydbrg
C
      REAL (KIND=R8) :: APN(100,10), AQN(100,10),
     &                  SIGMA(10,10), QN(100,10),
     &                  EN(100,10), EIN(100,5), ENM(100,5,10),
     &                  FNM(100,5,10), FNN(100), ENN(100)
      INTEGER NUCZ, NSPC, NVALNC(100)
      COMMON / ADSDAT / NUCZ, NSPC, APN, AQN,
     &                  NVALNC, SIGMA, QN,
     &                  EN, EIN, ENM,
     &                  FNM, FNN, ENN
C
      REAL (kind=R8) :: SIGMOR(10,10), PBETOT
      INTEGER JN, JM, JNP1, JNM1, KQ, KHOLE, KEXCIT
      SAVE SIGMOR
C
      DATA ((SIGMOR(JN,JM),JN=1,10),JM=1,10)/
     1 0.3125_R8, 0.9380_R8, 0.9840_R8, 0.9954_R8, 0.9970_R8, 0.9970_R8,
     2 0.9990_R8, 0.9999_R8, 0.9999_R8, 0.9999_R8, 0.2345_R8, 0.6038_R8,
     3 0.9040_R8, 0.9722_R8, 0.9979_R8, 0.9880_R8, 0.9900_R8, 0.9990_R8,
     4 0.9990_R8, 0.9999_R8, 0.1093_R8, 0.4018_R8, 0.6800_R8, 0.9155_R8,
     5 0.9796_R8, 0.9820_R8, 0.9860_R8, 0.9900_R8, 0.9920_R8, 0.9999_R8,
     6 0.0622_R8, 0.2430_R8, 0.5150_R8, 0.7100_R8, 0.9200_R8, 0.9600_R8,
     7 0.9750_R8, 0.9830_R8, 0.9860_R8, 0.9900_R8, 0.0399_R8, 0.1597_R8,
     8 0.3527_R8, 0.5888_R8, 0.7320_R8, 0.8300_R8, 0.9000_R8, 0.9500_R8,
     9 0.9700_R8, 0.9800_R8, 0.0277_R8, 0.1098_R8, 0.2455_R8, 0.4267_R8,
     * 0.5764_R8, 0.7248_R8, 0.8300_R8, 0.9000_R8, 0.9500_R8, 0.9700_R8,
     1 0.0204_R8, 0.0808_R8, 0.1811_R8, 0.3184_R8, 0.4592_R8,
     2 0.6098_R8, 0.7374_R8, 0.8300_R8, 0.9000_R8, 0.9500_R8,
     3 0.0156_R8, 0.0624_R8, 0.1392_R8, 0.2457_R8, 0.3711_R8,
     4 0.5062_R8, 0.6355_R8, 0.7441_R8, 0.8300_R8, 0.9000_R8,
     5 0.0123_R8, 0.0493_R8, 0.1102_R8, 0.1948_R8, 0.2994_R8,
     6 0.4222_R8, 0.5444_R8, 0.6558_R8, 0.7553_R8, 0.8300_R8, 0.0100_R8,
     7 0.0400_R8, 0.0900_R8, 0.1584_R8, 0.2450_R8, 
     8 0.3492_R8, 0.4655_R8, 0.5760_R8, 0.6723_R8, 0.7612_R8/
C
      rydbrg=(me/(2.0_R8*qe))*(qe**2/(2.0_R8*const_h*eps0))**2
      DO 10 JN = 1, 10
      ZAPN(JN) = APN(KQ,JN)
   10 CONTINUE
C
C     REMOVE ELECTRON FROM SHELL KHOLE
C
      IF (KHOLE.GT.0) THEN
       IF (ZAPN(KHOLE).GT.0.0_R8) THEN
        ZAPN(KHOLE) = ZAPN(KHOLE) - 1.0_R8
       END IF
      END IF
C
C     ADD ELECTRON TO SHELL KEXCIT
C
      IF(KEXCIT .GT. 0) ZAPN(KEXCIT) = ZAPN(KEXCIT) + 1.0_R8
C
C     COMPUTE NUCLEAR CHARGE AS SHIELDED BY INNER ELECTRONS ONLY
C
      DO 30 JN = 1, 10
C
      ZQ(JN) = REAL(NUCZ) - 0.5_R8 * SIGMOR(JN,JN) * ZAPN(JN)
C
      IF(JN .EQ. 1) GO TO 30
      JNM1 = JN - 1
C
      DO 20 JM = 1, JNM1
      ZQ(JN) = ZQ(JN) - SIGMOR(JN,JM) * ZAPN(JM)
   20 CONTINUE
   30 CONTINUE
C
C     COMPUTE POTENTIAL DUE TO OUTER ELECTRONS
C
      DO 50 JN = 1, 10
      ZEN0(JN) = (rydbrg/1000.0_R8) * SIGMOR(JN,JN) * ZAPN(JN)
     &           * ZQ(JN) / (REAL(JN)**2)
C
      IF(JN .EQ. 10) GO TO 50
      JNP1 = JN + 1
C
      DO 40 JM = JNP1, 10
      ZEN0(JN) = ZEN0(JN) + 0.0272_R8 * ZAPN(JM) * SIGMOR(JM,JN)
     &                      * ZQ(JM) / (REAL(JM)**2)
   40 CONTINUE
C
   50 CONTINUE
C
C     COMPUTE FINAL SHELL ENERGIES AND TOTAL ION BINDING ENERGY,
C     BOTH DEFINED POSITIVE.
C
      PBETOT = 0.0_R8
C
      DO 60 JN = 1, 10
      PSEN(JN) = (rydbrg/1000.0_R8) * ((ZQ(JN)/REAL(JN))**2) - ZEN0(JN)
      PBETOT = PBETOT + 
